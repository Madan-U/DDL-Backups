-- Object: VIEW dbo.VW_Client_DPCharges
-- Server: 10.253.33.190 | DB: inhouse
--------------------------------------------------

CREATE View [dbo].[VW_Client_DPCharges]
As
SELECT PARTY_CODE = ISNULL(NISE_PARTY_CODE, ''), V.CLIENT_CODE, BILL_TRXN_ID, TRXN_TYPE, NARRATION = ISNULL(NARRATION, ''), AMOUNT = CONVERT(MONEY, AMOUNT),
BILL_NO, BILL_DATE, DPM_CHARGES = ISNULL(DPM_CHARGES, 0), SERV_TAX = ISNULL(SERV_TAX, 0)
FROM VW_BILL_TRANSACTION V WITH (NOLOCK), TBL_CLIENT_MASTER C WITH (NOLOCK)
WHERE V.CLIENT_CODE = C.CLIENT_CODE AND BILL_DATE IS NOT NULL

GO
