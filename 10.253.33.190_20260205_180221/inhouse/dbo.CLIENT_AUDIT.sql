-- Object: VIEW dbo.CLIENT_AUDIT
-- Server: 10.253.33.190 | DB: inhouse
--------------------------------------------------

CREATE VIEW [dbo].[CLIENT_AUDIT] 
AS

SELECT CLIENT_CODE AS CA_CMCD, COLUMN_NAME AS CA_FIELD, 
OLD_VALUE AS CA_OLDVALUE, NEW_VALUE AS CA_NEWVALUE, 
USER_ID AS MKRID, CONVERT(VARCHAR, DATE_TIME, 112) AS MKRDT, 
TERMINAL AS CA_COMPUTERNAME, CONVERT(VARCHAR, DATE_TIME, 108) AS MKRTM, 
MKRIDOLD='', MKRDTOLD = ''
FROM SYNERGY_CLIENT_MASTER_AUDIT WITH (NOLOCK)

GO
