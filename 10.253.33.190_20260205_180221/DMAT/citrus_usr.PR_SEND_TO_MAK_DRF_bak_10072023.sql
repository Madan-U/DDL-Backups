--SELECT * FROM DEMAT_TRAN_TRANM_DTLS_MAK WHERE DEMTTD_DEMRM_ID 
--IN (SELECT DEMRM_ID FROM DEMRM_MAK WHERE DEMRM_SLIP_SERIAL_NO = @PA_SLIP_NO)

