CREATE VIEW SYNERGY_BANK_MSTR

AS

SELECT DISTINCT  BANM_ID AS BANK_ID,
BANM_MICR AS MICR_CODE,
banm_rtgs_cd AS IFSC_CODE,
BANM_NAME AS DP_BANK_NAME,
TMPBA_DP_BANK_ADD1 AS DP_BANK_ADDR1,
TMPBA_DP_BANK_ADD2 AS DP_BANK_ADDR2,
TMPBA_DP_BANK_ADD3 AS DP_BANK_ADDR3,
TMPBA_DP_BANK_CITY AS DP_BANK_CITY,
TMPBA_DP_BANK_STATE AS DP_BANK_STATE,
TMPBA_DP_BANK_CNTRY AS DP_BANK_CNTRY,
TMPBA_DP_BANK_ZIP AS  DP_BANK_ZIP,
TMPBA_DP_BANK_PH1 AS  DP_BANK_PHONE_1,
TMPBA_DP_BANK_PH2 AS  DP_BANK_PHONE_2,
'' AS DP_BANK_FAX,
TMPBA_DP_BANK_EMAIL DP_BANK_EMAIL,
TMPBA_DP_BANK_CONNAME AS  DP_BANK_CONNAME,TMPBA_DP_BANK_CONDESG AS DP_BANK_CONDESG
FROM BANK_MSTR B WITH(NOLOCK)
LEFT OUTER JOIN 
bank_addresses_dtls D WITH(NOLOCK)
ON  BANM_ID=TMPBA_DP_BANK_FAX

GO
