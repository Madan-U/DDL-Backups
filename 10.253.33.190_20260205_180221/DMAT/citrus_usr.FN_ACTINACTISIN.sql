-- Object: FUNCTION citrus_usr.FN_ACTINACTISIN
-- Server: 10.253.33.190 | DB: DMAT
--------------------------------------------------

create FUNCTION [citrus_usr].[FN_ACTINACTISIN](@PA_INTREF VARCHAR(12))
RETURNS VARCHAR(800)
AS
BEGIN
DECLARE @L_COMMON VARCHAR(1)
SELECT @L_COMMON=CASE WHEN ISNULL(ISIN_SEC_SUB_TYPE,'0')<>'0' THEN 'Y' ELSE 'N' END FROM ISIN_MSTR WHERE ISIN_CD=@PA_INTREF AND ISIN_DELETED_IND=1
RETURN @L_COMMON
END

GO
