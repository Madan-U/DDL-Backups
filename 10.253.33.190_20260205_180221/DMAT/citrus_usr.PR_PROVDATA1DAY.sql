CREATE PROCEDURE PR_PROVDATA1DAY(@PA_FROM_DT DATETIME, @PA_TO_DT DATETIME ,@PA_LOGIN_NAME VARCHAR(100)      
)
AS
BEGIN

TRUNCATE TABLE PROVDATA1DAY_CDSL_HOLDING_DTLS
INSERT INTO PROVDATA1DAY_CDSL_HOLDING_DTLS
SELECT *  FROM CDSL_HOLDING_DTLS WHERE CDSHM_TRAS_DT BETWEEN @PA_FROM_DT AND @PA_TO_DT
AND ISNULL(CDSHM_CHARGE,'0')<>'0'

SELECT * FROM PROVDATA1DAY_CDSL_HOLDING_DTLS

END

GO
