CREATE PROC DP_CLIENT_DATA
(
@CLIENT_CODE VARCHAR(20)

)
AS BEGIN   
SELECT DPAM_SBA_NO,DPAM_SBA_NAME,DPAM_BBO_CODE,MobileNum FROM DP_ACCT_MSTR A,DPS8_PC16 B
 WHERE  A.DPAM_SBA_NO=B.BOID AND DPAM_SBA_NO=@CLIENT_CODE


END

GO
