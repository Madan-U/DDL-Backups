CREATE VIEW VW_CLIENT_SIG_DATA  
as  
SELECT DPAM_SBA_NO BOID, DPAM_BBO_CODE PARTYCODE , ACCD_BINARY_IMAGE FROM DP_ACCT_MSTR WITH (NOLOCK), ACCOUNT_DOCUMENTS WITH (NOLOCK)  
WHERE ACCD_CLISBA_ID=DPAM_ID AND ACCD_DELETED_IND=1 AND ACCD_ACCDOCM_DOC_ID=12   
AND DPAM_DELETED_IND=1

GO
