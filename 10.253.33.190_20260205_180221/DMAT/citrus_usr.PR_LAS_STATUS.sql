--------------------------------------------------

CREATE PROCEDURE PR_LAS_STATUS
(
@PA_BOID  VARCHAR(16),@PA_DTLSID VARCHAR(20),@PA_OUTPUT VARCHAR(200) OUTPUT
)
AS
BEGIN 
 IF EXISTS(SELECT DPOC_BOID FROM DPCLASS_ONL_CLIENTS WHERE DPOC_BOID = '' + @PA_BOID + '')
	  BEGIN
		
		IF EXISTS (SELECT DPTDC_ID FROM DPTDC_MAK WHERE DPTDC_DTLS_ID=@PA_DTLSID AND DPTDC_DELETED_IND=-1 AND ISNULL(DPTDC_MID_CHK,'')='')
		BEGIN
		SELECT @PA_OUTPUT = ISNULL(@PA_BOID,'') + ': IS LAS ACCOUNT INSTRUCTION , NEED DUAL CHECKER (THIS IS FIRST CHECKER)!' 
		END

		IF EXISTS (SELECT DPTDC_ID FROM DPTDC_MAK WHERE DPTDC_DTLS_ID=@PA_DTLSID AND DPTDC_DELETED_IND=0 AND ISNULL(DPTDC_MID_CHK,'')<>'')
		BEGIN
		SELECT @PA_OUTPUT = ISNULL(@PA_BOID,'') + ': IS LAS ACCOUNT INSTRUCTION , NEED DUAL CHECKER (FIRST CHECKER IS DONE)!' 
		END

		 
	  END
	  ELSE
	  BEGIN
	  SELECT @PA_OUTPUT ='0'
	  END
END

GO
