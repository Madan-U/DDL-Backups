-- Object: PROCEDURE citrus_usr.PR_RPT_FILL_EXCHANGE_AND_SEGMENT
-- Server: 10.253.33.190 | DB: DMAT
--------------------------------------------------

create PROCEDURE PR_RPT_FILL_EXCHANGE_AND_SEGMENT
(
	@PA_ACTION VARCHAR(50),
	@PA_VALUE VARCHAR(100),
	@PA_OTHERS VARCHAR(8000) OUTPUT                  
)
AS
BEGIN
	IF @PA_ACTION = 'EXCHANGE'
	BEGIN
		SELECT EXCSMD_EXCH_CD,EXCSMD_EXCH_ID FROM EXCH_SEG_MSTR_DUMMY WHERE EXCSMD_DELETED_IND=1
	END
	IF @PA_ACTION = 'SEGMENT'
	BEGIN
		SELECT SMD_SEGMENT_CD, SMD_SEGMENT_ID FROM SEG_MSTR_DUMMY WHERE SMD_DELETED_IND=1
	END
END

GO
