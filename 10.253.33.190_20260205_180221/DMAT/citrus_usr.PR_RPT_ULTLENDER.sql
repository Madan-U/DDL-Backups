create PROCEDURE [citrus_usr].[PR_RPT_ULTLENDER]
(
@PA_PLEDGEEID VARCHAR(16)
)
AS
BEGIN
DECLARE @L_YN CHAR(1), @L_RTR VARCHAR(200)
	IF EXISTS (SELECT 1 FROM ULTIMATE_LENDER_MSTR WHERE ULM_CODE=@PA_PLEDGEEID)
	BEGIN
	SELECT @L_YN='Y', @L_RTR=ULM_CODE + '|*~|' +ULM_NAME + '|*~|' + ULM_PAN  FROM ULTIMATE_LENDER_MSTR WHERE ULM_CODE=@PA_PLEDGEEID
	END
	ELSE
	BEGIN
	SELECT @L_YN='N' , @L_RTR= ''
	END

	SELECT @L_YN YNVAL,@L_RTR YSTR
END

GO
