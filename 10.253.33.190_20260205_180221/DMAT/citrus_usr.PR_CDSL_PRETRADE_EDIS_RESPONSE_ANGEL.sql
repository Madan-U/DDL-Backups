,DPTDC_BOOKING_TYPE
,DPTDC_CONVERTED_QTY
,DPTDC_REASON_CD
,'P' DPTDC_STATUS
,DPTDC_INTERNAL_TRASTM
,DPTDC_EXCM_ID
,DPTDC_CASH_TRF
,DPTDC_CM_ID
,DPTDC_CREATED_BY
,DPTDC_CREATED_DT
,DPTDC_LST_UPD_BY
,DPTDC_LST_UPD_DT
,1--DPTDC_DELETED_IND
,dptdc_rmks
,DPTDC_ERRMSG
,DPTDC_BROKERBATCH_NO
,DPTDC_BROKER_INTERNAL_REF_NO
--,dptdc_mid_chk
--,dptdc_res_cd
--,dptdc_res_desc
,''
,DPTDC_PAYMODE
,DPTDC_BANKACNO
,DPTDC_BANKACNAME
,DPTDC_BANKBRNAME
,DPTDC_TRANSFEREENAME
,DPTDC_DOI
,DPTDC_CHQ_REFNO
,isnull(DPTDC_UCC,'') DPTDC_UCC
,isnull(DPTDC_EXID,'') DPTDC_EXID
,isnull(DPTDC_SEGID,'') DPTDC_SEGID
,isnull(DPTDC_CMID,'') DPTDC_CMID
,isnull(DPTDC_EID,'') DPTDC_EID
,isnull(DPTDC_TMCMID,'') DPTDC_TMCMID,
DPTDC_CUSPA_FLG ='',DPTDC_CUSPA_CLTID='',DPTDC_CUSPA_CMBP_DPID=''
FROM EDIS_PRE_DPTDC_MAK O WITH(NOLOCK),E_DIS_PROCESS_DATA WITH(NOLOCK),DP_ACCT_MSTR WITH(NOLOCK) WHERE 
DPAM_ID=DPTDC_DPAM_ID 
AND ISNULL(LTRIM(RTRIM(DPTDC_SLIP_NO)),'')=ISNULL(LTRIM(RTRIM(SLIP_NO)),'')
AND ISIN=DPTDC_ISIN AND QTY=DPTDC_QTY*-1 AND DPAM_SBA_NO=BOID
AND DPTDC_STATUS='D' AND ISNULL(DPTDC_TRANS_NO,'')<>''
--DPTDC_SLIP_NO=SLIP_NO
AND NOT EXISTS (
SELECT 1 FROM DP_TRX_DTLS_CDSL I WITH(NOLOCK) WHERE  I.DPTDC_ID=O.DPTDC_ID AND I.DPTDC_REQUEST_DT=O.DPTDC_REQUEST_DT 
AND I.DPTDC_ISIN=O.DPTDC_ISIN
--I.DPTDC_SLIP_NO=O.DPTDC_TRANS_NO
--AND I.DPTDC_ISIN=O.DPTDC_ISIN AND I.DPTDC_QTY=O.DPTDC_QTY AND DPAM_ID=DPTDC_DPAM_ID
