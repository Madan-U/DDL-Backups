--------------------------------------------------


CREATE  VIEW [citrus_usr].[DP_BATCHDATA]
AS
SELECT DISTINCT BO_PARTYCODE,CREATED_DT,CLEA_CREATED_DT ,clea_active_from, CLEA_DP_ACCTNO
FROM ABVSCITRUS.CRMDB_A.DBO. CLIENT_MASTER, ABVSCITRUS.CRMDB_A.DBO.CLIENT_EXCHANGE
WHERE CLEA_CRN_NO=CRN_NO
AND  DP_BATCH_NO IS NOT NULL
AND STATUS=5
AND CREATED_DT > GETDATE()- 1
AND CLEA_EXCHANGE_ID=1

GO
