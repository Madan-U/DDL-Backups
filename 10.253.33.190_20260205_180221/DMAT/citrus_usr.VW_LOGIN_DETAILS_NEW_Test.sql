--SELECT * FROM VW_LOGIN_DETAILS_NEW  
CREATE   VIEW [CITRUS_USR].[VW_LOGIN_DETAILS_NEW_Test]   
AS  
SELECT  DPID = (SELECT TOP 1  DPM_DPID FROM DP_MSTR WHERE DPM_EXCSM_ID = DEFAULT_DP ) ,  LOGN_NAME USERNAME   
, ROL_DESC  USERROLE   
, LOGN_FROM_DT FROMDT  
,LOGN_TO_DT TODT  
, LOGN_CREATED_DT CREATEDDATE  
,LOGN_LST_UPD_DT DEACTVIATE_DATE
--,LOGN_PSW_EXP_ON LOGINEXPIREDON  
, CASE WHEN  LOGN_STATUS = 'A' THEN 'ACTIVE' ELSE 'DEACTIVE' END  USERSTATUS  
FROM  LOGIN_NAMES ,ENTITY_ROLES ,ROLES   
WHERE  LOGN_DELETED_IND = 1 AND LOGN_NAME = ENTRO_LOGN_NAME   
AND ENTRO_ROL_ID = ROL_ID   
AND LOGN_NAME NOT IN (SELECT RIGHT(DPAM_SBA_NO,8) FROM DP_ACCT_MSTR )  
AND ROL_DESC <> 'CLIENTLOGIN'

GO
