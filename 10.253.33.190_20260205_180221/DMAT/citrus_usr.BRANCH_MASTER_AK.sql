--------------------------------------------------

CREATE VIEW BRANCH_MASTER_AK
AS
SELECT REPLACE(BR.ENTM_SHORT_NAME ,'_BR','') BRANCH_CODE	
,BR.ENTM_NAME1 BRANCH_NAME	
,RE.ENTM_NAME1 ZONE	
,'' MOBILE_NO	
,'' REF_BRANCH_CODE	
FROM ENTITY_MSTR BR , ENTITY_MSTR RE
WHERE BR.ENTM_PARENT_ID = RE.ENTM_ID 
AND BR.ENTM_ENTTM_CD IN ('BR')

GO
