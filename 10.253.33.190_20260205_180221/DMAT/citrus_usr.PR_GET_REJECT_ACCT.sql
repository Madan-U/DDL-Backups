CREATE PROC CITRUS_USR.PR_GET_REJECT_ACCT(@PA_FROM_DT DATETIME,@PA_TO_dT DATETIME , @PA_LOGIN_NAME VARCHAR(150))
AS
BEGIN 


SELECT DPAM_ACCT_NO ACCT_NO, DPAM_SBA_NAME NAME , DPAM_BATCH_NO BATCH, tmp_date [DATE] ,  tmp_errmsg  ERROR 
FROM DP_ACCT_MSTR , tmp_client_res_MSTR WHERE tmp_formno = DPAM_ACCT_NO 
AND DPAM_ACCT_NO = DPAM_SBA_NO AND DPAM_DELETED_IND = 1 
AND tmp_date BETWEEN @PA_FROM_DT AND @PA_TO_dT AND ISNULL(tmp_errmsg,'')<>''
END

GO
