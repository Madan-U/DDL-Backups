CREATE PROC PR_SYNERGY_HOLDING (@HLD_DATE DATETIME,@PARTY_CODE VARCHAR(10))
AS 

SELECT CLIENT_CODE INTO #CLT FROM TBL_CLIENT_MASTER WHERE NISE_PARTY_CODE =@PARTY_CODE

SELECT S.* FROM SYNERGY_HOLDING S WITH(NOLOCK)  ,#CLT C WITH(NOLOCK) WHERE HLD_HOLD_DATE =@HLD_DATE AND HLD_AC_CODE=CLIENT_CODE

GO
