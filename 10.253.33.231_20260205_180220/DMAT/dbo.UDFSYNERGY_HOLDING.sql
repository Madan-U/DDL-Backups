-- Server: 10.253.33.231 | DB: DMAT
--------------------------------------------------



CREATE FUNCTION [DBO].[UDFSYNERGY_HOLDING]
(@HLD_DATE DATETIME,@DP_ID VARCHAR(10)
)
RETURNS TABLE
AS
RETURN
SELECT S.* FROM SYNERGY_HOLDING S WITH(NOLOCK)    
WHERE HLD_HOLD_DATE =@HLD_DATE AND HLD_AC_CODE=@DP_ID

GO
