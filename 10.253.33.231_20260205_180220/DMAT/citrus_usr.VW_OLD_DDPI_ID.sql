-- Object: VIEW citrus_usr.VW_OLD_DDPI_ID
-- Server: 10.253.33.231 | DB: DMAT
--------------------------------------------------




CREATE View [citrus_usr].[VW_OLD_DDPI_ID]
As

SELECT DISTINCT C.CLIENT_CODE,NISE_PARTY_CODE ,OLD_POA='N',	NEW_POA='N',	NEW_DDPI='N',	OLD_DDPI='Y' FROM(
SELECT CLIENT_CODE FROM AngelDP5.DMAT.Citrus_usr.TBL_CLIENT_POA WHERE MASTER_POA='2203320100000063'
AND POA_STATUS ='A'
GROUP BY CLIENT_CODE
HAVING COUNT(1)=2 ) A, AngelDP5.DMAT.Citrus_usr.TBL_CLIENT_MASTER C
WHERE A.CLIENT_CODE=C.CLIENT_CODE AND STATUS ='ACTIVE'
UNION ALL
SELECT DISTINCT C.CLIENT_CODE,NISE_PARTY_CODE ,OLD_POA='N',	NEW_POA='N',	NEW_DDPI='N',	OLD_DDPI='Y'  FROM (
SELECT CLIENT_CODE FROM AGMUBODPL3.DMAT.Citrus_usr.TBL_CLIENT_POA WHERE MASTER_POA='2203320000000749'
AND POA_STATUS ='A'
GROUP BY CLIENT_CODE
HAVING COUNT(1)=2 ) A, AGMUBODPL3.DMAT.Citrus_usr.TBL_CLIENT_MASTER C
WHERE A.CLIENT_CODE=C.CLIENT_CODE AND STATUS ='ACTIVE'

GO
