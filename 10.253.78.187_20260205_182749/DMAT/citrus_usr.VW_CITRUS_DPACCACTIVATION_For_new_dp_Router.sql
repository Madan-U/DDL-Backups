CREATE View  VW_CITRUS_DPACCACTIVATION_For_new_dp_Router    
As    
SELECT TMPCLI_BOID dp_id ,TMPCLI_TRADING_ID BO_PARTYCODE, UPPER(TMPCLI_SCHEME) TEMPLATE_CODE,    
(Case when ECNFLAG='Y' Then 'Yes' Else 'No' End) ECN_FLAG,DISFLAG DIS_FLAG,TMPCLI_BR BRANCH_CD,TMPCLI_SUB SUBBROKER,TMPCLI_TRA TRADER, 
TMPCLI_RE REGION,TMPCLI_AR AREA,ECSFLAG ECS_FLAG,    
CREATION_DATE =GETDATE()    
FROM TMP_CLIENT_DTLS_MSTR_CDSL_ROUTER WHERE ( TMPCLI_SCHEME <>''  AND TMPCLI_TRADING_ID <>'')

GO
