CREATE FUNCTION FN_DRF_DTLSCHK (@DEMRM_ID NUMERIC)
RETURNS CHAR(1)

AS


BEGIN
DECLARE @L CHAR(1)
IF EXISTS (SELECT 1 FROM DEMAT_REQUEST_DTLS WHERE DEMRD_DEMRM_ID=@DEMRM_ID) 
BEGIN 
SET @L='1' 
END
BEGIN
SET @L='0' 
END
RETURN @L
END

GO
