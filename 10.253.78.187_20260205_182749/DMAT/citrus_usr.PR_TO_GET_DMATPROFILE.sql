--------------------------------------------------


CREATE PROCEDURE PR_TO_GET_DMATPROFILE
(@PA_BOID VARCHAR(16))
AS
BEGIN

SELECT BROM_ID,BROM_DESC FROM CLIENT_DP_BRKG,DP_ACCT_MSTR,BROKERAGE_MSTR WHERE DPAM_ID=CLIDB_DPAM_ID AND GETDATE() BETWEEN CLIDB_EFF_FROM_DT AND 
ISNULL(CLIDB_EFF_TO_DT,'2100-01-01 00:00:00.000')
AND BROM_ID=CLIDB_BROM_ID AND CLIDB_DELETED_IND=1
AND DPAM_DELETED_IND=1 AND DPAM_SBA_NO=@PA_BOID
AND BROM_DELETED_IND=1

END

GO
