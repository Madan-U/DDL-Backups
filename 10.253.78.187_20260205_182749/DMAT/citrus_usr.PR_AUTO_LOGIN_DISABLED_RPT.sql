CREATE PROCEDURE [citrus_usr].[PR_AUTO_LOGIN_DISABLED_RPT]  
(
@PA_FROM_DT DATETIME  
,@PA_TO_DT DATETIME  
,@PA_LOGIN_NAME VARCHAR(100)  
)  
AS  
BEGIN  
  
SELECT  LOGN_NAME , LOGN_LST_UPD_BY , LOGN_LST_UPD_DT , case when LOGN_STATUS='D' then 'DISABLED' else 'ACTIVE' end LOGN_STATUS
FROM VW_DEACTIVATION_DETAILS, CITRUS_USR.LOGIN_NAMES WHERE LOGN_NAME=EMP_NO  
AND LOGN_DELETED_IND=1  
AND LOGN_STATUS='D'  AND LOGN_LST_UPD_BY='HRM' 


END

GO
