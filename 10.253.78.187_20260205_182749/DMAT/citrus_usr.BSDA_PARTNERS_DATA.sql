CREATE PROCEDURE [citrus_usr].[BSDA_PARTNERS_DATA]
( @SUB_BROKER VARCHAR(20) )

AS 
BEGIN 

SELECT CL_CODE,LONG_NAME,BRANCH_CD,SUB_BROKER,region INTO #DATA1
 FROM [AngelNseCM].MSAJAG.DBO.CLIENT_DETAILS WHERE SUB_BROKER=@SUB_BROKER



SELECT A.*,CLIENT_CODE,STATUS,ACTIVE_DATE,TEMPLATE_CODE INTO #BSDA1 FROM #DATA1 A
LEFT OUTER JOIN
TBL_CLIENT_MASTER B
ON A.CL_cODE=B.NISE_PARTY_CODE


SELECT A.*,(CASE WHEN B.Filler9='Y'THEN 'YES'ELSE 'NO'END)BSDA_FLAG FROM #BSDA1 A
LEFT OUTER JOIN
dps8_pc1 B
ON A.CLIENT_CODE=B.BOId
 
 END

GO
