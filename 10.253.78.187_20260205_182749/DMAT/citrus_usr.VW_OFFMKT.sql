CREATE VIEW VW_OFFMKT  
AS  
  
SELECT DPTDC_EXECUTION_DT,DPTDC_SLIP_NO,DPAM_SBA_NO,DPTDC_ISIN,DPTDC_QTY,  
CASE WHEN LEN(DPTDC_COUNTER_DEMAT_ACCT_NO)>=16
THEN DPTDC_COUNTER_DEMAT_ACCT_NO ELSE DPTDC_COUNTER_DP_ID +DPTDC_COUNTER_DEMAT_ACCT_NO END COUNTERDMATACCOUNT  ,DPTDC_COUNTER_DP_ID Counter_DPID
,DPTDC_CREATED_BY  ,DPAM_BBO_CODE AS  PARTY_CODE 
FROM DPTDC_MAK With(Nolock),DP_ACCT_MSTR With(Nolock) WHERE DPTDC_DELETED_IND IN (0,-1)  
AND DPAM_ID=DPTDC_DPAM_ID  
AND DPTDC_INTERNAL_TRASTM IN ('BOBO')  
--ORDER BY DPTDC_EXECUTION_DT

GO
