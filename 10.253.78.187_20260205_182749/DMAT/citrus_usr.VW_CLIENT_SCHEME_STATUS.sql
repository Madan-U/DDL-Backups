CREATE VIEW VW_CLIENT_SCHEME_STATUS
AS
 SELECT
 DPAM_SBA_NO CLIENT_CODE
,BROM_DESC    TEMPLATE_CODE
,DPAM_BBO_CODE  NISE_PARTY_CODE
,STAM_DESC  STATUS
FROM DP_ACCT_MSTR DPAM WITH(NOLOCK)
LEFT OUTER JOIN CLIENT_DP_BRKG  WITH(NOLOCK)
ON DPAM_ID = CLIDB_DPAM_ID AND GETDATE() BETWEEN CLIDB_EFF_FROM_DT  AND  ISNULL(CLIDB_EFF_TO_DT ,'DEC 31 2100')  AND CLIDB_DELETED_IND = 1
LEFT OUTER JOIN BROKERAGE_MSTR WITH(NOLOCK) ON BROM_ID = CLIDB_BROM_ID
,STATUS_MSTR  WITH(NOLOCK)
WHERE  DPAM_STAM_CD = STAM_CD
 AND ISNUMERIC(DPAM_SBA_NO)=1

GO
