CREATE FUNCTION [citrus_usr].[FN_GET_DDPI_FLAG_11Nov2022] (@PA_DPAM_SBA_NO VARCHAR(16),@PA_DPAM_CREATED_BY VARCHAR(16))
RETURNS VARCHAR(16)
AS
BEGIN
DECLARE @L_FLAG CHAR(1)
SET @L_FLAG='N'
SELECT @L_FLAG=GPABPAFLG FROM DPS8_PC5 WHERE GPABPAFLG IN ('S','P','A') AND BOID=@PA_DPAM_SBA_NO AND TYPEOFTRANS IN ('','1')
RETURN ISNULL(@L_FLAG,'') 
END

GO
