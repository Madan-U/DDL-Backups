CREATE PROCEDURE DP_NBFC_PMS_DATA
AS BEGIN 
----------NBFC PMS OUTSTANDING
SELECT NISE_PARTY_CODE,CLIENT_CODE,ACTIVE_DATE,TEMPLATE_CODE 
INTO #TEMP
FROM TBL_CLIENT_MASTER WHERE TEMPLATE_CODE IN
(
'NBC',
'NBC CORP',
'NBFC INV',
'NBFC INVW',
'NBFCCORP',
'PRENBC'

)AND STATUS='ACTIVE'


SELECT A.*,B.Actual_amount INTO #TEMP4 FROM #TEMP A
LEFT OUTER JOIN 
Vw_Acc_Curr_Bal B
ON A.CLIENT_CODE=B.CLIENT_CODE

--------98 SERIES

SELECT NISE_PARTY_CODE,CLIENT_CODE,ACTIVE_DATE,TEMPLATE_CODE 
INTO #TEMPA
FROM TBL_CLIENT_MASTER WHERE NISE_PARTY_CODE LIKE '98%'
AND STATUS='ACTIVE'


SELECT A.*,B.Actual_amount INTO #TEMP5  FROM #TEMPA A
LEFT OUTER JOIN 
Vw_Acc_Curr_Bal B
ON A.CLIENT_CODE=B.CLIENT_CODE

--10421+10421

SELECT * FROM #TEMP4 
UNION ALL
SELECT * FROM #TEMP5

END

GO
