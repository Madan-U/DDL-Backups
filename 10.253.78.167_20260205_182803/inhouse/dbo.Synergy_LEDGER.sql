-- Object: VIEW dbo.Synergy_LEDGER
-- Server: 10.253.78.167 | DB: inhouse
--------------------------------------------------


CREATE View [dbo].[Synergy_LEDGER]



as

select 
LD_CLIENTCD,
LD_DT,
(CASE WHEN LD_AMOUNT>0 THEN LD_AMOUNT ELSE LD_AMOUNT*-1 END) LD_AMOUNT,
LD_PARTICULAR,
LEFT(LD_CHEQUENO,8) LD_CHEQUENO,
LD_DEBITFLAG,
( CASE WHEN LD_DOCUMENTTYPE ='1' THEN 'P'
	   WHEN LD_DOCUMENTTYPE ='2' THEN 'R'
	   WHEN LD_DOCUMENTTYPE ='3' THEN 'J'
	   ELSE 'B' END)LD_DOCUMENTTYPE,
LD_DOCUMENTNO,
CAST(0 AS INT) LD_ENTRYNO ,
0 LD_COSTCENTER,MKRID,NULL MKRDT,convert(varchar(8),LD_ACCYEAR)LD_ACCYEAR,
LD_DPID,
CONVERT(VARCHAR(8),LD_COMMONDT)LD_COMMONDT,LD_COMMON
from dmat.citrus_usr.Synergy_LEDGER with(NOLOCK)

GO
