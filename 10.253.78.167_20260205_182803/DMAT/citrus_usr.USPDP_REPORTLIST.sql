-- Object: PROCEDURE citrus_usr.USPDP_REPORTLIST
-- Server: 10.253.78.167 | DB: DMAT
--------------------------------------------------

CREATE PROC [citrus_usr].[USPDP_REPORTLIST]
(
	@RPTGROUP VARCHAR(30)
)
AS
SELECT 
	SRNO,
	REPORTGROUP,
	PROCESSNAME,
	PROCESSTITLE,
	PROCESSSP,
	PROCESSPAGE,
	SELECTION
FROM
	DMAT_REPORTMASTER
WHERE
	REPORTGROUP = @RPTGROUP
ORDER BY
	SRNO

GO
