CREATE PROCEDURE RPT_CLIENT_DATA

(
@FROMDATE VARCHAR(20),
@TODATE VARCHAR(20)
)

AS
BEGIN
SELECT CLIENT_CODE,NISE_PARTY_CODE,FIRST_HOLD_NAME,EMAIL_ADD
 FROM TBL_CLIENT_MASTER WITH(NOLOCK) WHERE ACTIVE_dATE>=@FROMDATE and ACTIVE_DATE<=@TODATE + ' 23:59'  
 
 END

GO
