CREATE    VIEW VW_NONPOACLIENT
AS 
SELECT BOID  , SUBSTRING(BOACTDT,1,2)+'/'+SUBSTRING(BOACTDT,3,2)+'/'+SUBSTRING(BOACTDT,5,4) [ACTIVATION DATE]
,SUBCM_DESC [SUBCTGRY]
 , isnull(DPAM_BBO_CODE,'') [BBOCODE], 
 CASE WHEN  ISNULL(CITRUS_USR.[FN_CONC_VALUE](DPAM_CRN_NO,'MOBSMS'),'') = '' THEN PC1.PRIPHNUM ELSE ISNULL(CITRUS_USR.[FN_CONC_VALUE](DPAM_CRN_NO,'MOBSMS'),'')END  [SMSMOBILE]
 ,ISNULL(PC1.PRI_EMAIL,'') EMAIL
 FROM DPS8_PC1 PC1 , DP_ACCT_MSTR , SUB_CTGRY_MSTR
WHERE DPAM_SBA_NO = PC1.BOID  AND DPAM_SUBCM_CD = SUBCM_CD 
AND  DPAM_CLICM_CD IN (21,24 ) 
AND NOT EXISTS (SELECT 1 FROM DPS8_PC5 PC5 WHERE PC1.BOID = PC5.BOID AND PC5.TYPEOFTRANS IN (1,'',2) AND MASTERPOAID = '2203320000000014' )
AND BOACCTSTATUS = '1'
AND NOT EXISTS (SELECT 1 FROM DPS8_PC2 PC2 WHERE PC1.BOID = PC2.BOID AND PC2.TYPEOFTRANS IN (1,'',2))
AND NOT EXISTS (SELECT 1 FROM DPS8_PC3 PC3 WHERE PC1.BOID = PC3.BOID AND PC3.TYPEOFTRANS IN (1,'',2))
AND PC1.BOID LIKE '120%'

GO
