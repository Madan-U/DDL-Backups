CREATE PROC AUTO_TPIN_DETILS
( @PARTY_CODE VARCHAR(20),@ISIN VARCHAR(20) =''
)
AS
BEGIN 

IF @ISIN =''
BEGIN
SELECT * FROM E_DIS_TRXN_DATA
WHERE PARTYCODE =@PARTY_CODE
END 
  
SELECT * FROM E_DIS_TRXN_DATA
WHERE PARTYCODE =@PARTY_CODE
AND ISIN = @ISIN
END

GO
