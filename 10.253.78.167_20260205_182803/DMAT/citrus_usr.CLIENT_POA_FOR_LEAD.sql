CREATE VIEW CLIENT_POA_FOR_LEAD
AS
SELECT NISE_PARTY_CODE AS PARTY_CODE,ACTIVE_DATE,T.CLIENT_CODE,
POA_STATUS=(CASE WHEN ISNULL(POA_VER,'')='' THEN '' ELSE (CASE WHEN ISNULL(POA_VER,'')= 1 THEN 'P'
ELSE 'F' END) END),
POA_DATE_FROM FROM TBL_CLIENT_MASTER T WITH(NOLOCK) 
LEFT OUTER JOIN
(SELECT * FROM TBL_CLIENT_POA WITH(NOLOCK)  WHERE MASTER_POA ='2203320000000014' 
AND POA_STATUS ='A')P
ON T.CLIENT_CODE =P.CLIENT_CODE

GO
