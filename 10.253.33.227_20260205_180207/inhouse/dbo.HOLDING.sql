-- Object: VIEW dbo.HOLDING
-- Server: 10.253.33.227 | DB: inhouse
--------------------------------------------------

CREATE view HOLDING
As
select HLD_HOLD_DATE,CONVERT(vARCHAR(16),HLD_AC_CODE) HLD_AC_CODE,HLD_CAT,CONVERT(VARCHAR(12),HLD_ISIN_CODE) AS HLD_ISIN_CODE,HLD_AC_TYPE,
CAST(HLD_AC_POS AS MONEY) AS HLD_AC_POS,HLD_CCID,HLD_MARKET_TYPE,HLD_SETTLEMENT,HLD_BLC,HLD_BLF,HLD_LRD,HLD_PENDINGDT
from dmat.dbo.holdingdata with(nolock)

GO
