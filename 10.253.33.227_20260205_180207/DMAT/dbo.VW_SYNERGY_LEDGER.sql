CREATE VIEW VW_SYNERGY_LEDGER
AS 
SELECT LD_CLIENTCD,LD_DT,(CASE WHEN LD_AMOUNT <0 then LD_AMOUNT*-1 ELSE LD_AMOUNT END)LD_AMOUNT,LD_PARTICULAR,LD_CHEQUENO,LD_DEBITFLAG,LD_DOCUMENTTYPE,
CAST(LD_DOCUMENTNO AS VARCHAR)LD_DOCUMENTNO ,'' LD_ENTRYNO ,--ISNULL(LD_ENTRYNO,0)LD_ENTRYNO,
LD_COSTCENTER,MKRID,MKRDT,LD_ACCYEAR,LD_DPID,LD_COMMONDT,LD_COMMON FROM CITRUS_USR.SYNERGY_LEDGER WITH(NOLOCK)
UNION ALL
SELECT LD_CLIENTCD,LD_DT,CAST(LD_AMOUNT AS MONEY) LD_AMOUNT,LD_PARTICULAR,LD_CHEQUENO,LD_DEBITFLAG,LD_DOCUMENTTYPE,LD_DOCUMENTNO,''LD_ENTRYNO,--LD_ENTRYNO,
LD_COSTCENTER,MKRID,MKRDT,LD_ACCYEAR,LD_DPID,LD_COMMONDT,LD_COMMON
 FROM SYNERGY.DBO.SYNERGY_LEDGER WITH(NOLOCK) WHERE LD_DT <='2015-03-31 23:59'

GO
