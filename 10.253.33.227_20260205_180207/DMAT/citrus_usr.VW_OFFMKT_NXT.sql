CREATE VIEW VW_OFFMKT_NXT      
AS      
      
SELECT  DPTDC_EXECUTION_DT,DPTDC_SLIP_NO,DPAM_SBA_NO,DPTDC_ISIN,DPTDC_QTY=DPTDC_QTY*-1,      
CASE WHEN LEN(DPTDC_COUNTER_DEMAT_ACCT_NO)>=16    
THEN DPTDC_COUNTER_DEMAT_ACCT_NO ELSE DPTDC_COUNTER_DP_ID +DPTDC_COUNTER_DEMAT_ACCT_NO END COUNTERDMATACCOUNT  ,DPTDC_COUNTER_DP_ID COUNTER_DPID    
,DPTDC_CREATED_BY  ,DPAM_BBO_CODE AS  PARTY_CODE     
FROM DPTDC_MAK WITH(NOLOCK),DP_ACCT_MSTR WITH(NOLOCK) WHERE DPAM_ID=DPTDC_DPAM_ID      
AND DPTDC_INTERNAL_TRASTM IN ('BOBO') AND   DPTDC_EXECUTION_DT >=CONVERT(VARCHAR(11),GETDATE()-10,120)  
AND DPTDC_DELETED_IND ='1'

GO
