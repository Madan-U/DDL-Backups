CREATE VIEW ANG_PARTY_1  
  
AS  
  
SELECT CLIENT_CODE,NISE_PARTY_CODE AS PARTY_CODE,TEMPLATE_CODE,ACTIVE_DATE,
convert(varchar(11),isnull(REPLACE(NEXT_AMC_DATE,YEAR(NEXT_AMC_DATE),YEAR(ACTIVE_DATE)),'Jan  1 1900'),109) ACTUAL_AMC_DATE,
 NEXT_AMC_DATE,STATUS,
EMAIL_FLAG =(CASE WHEN EMAIL_FLAG ='Y' THEN 'Y' ELSE 'N' END) FROM [196.1.115.199].SYNERGY.DBO.TBL_CLIENT_MASTER WITH(NOLOCK)

GO
