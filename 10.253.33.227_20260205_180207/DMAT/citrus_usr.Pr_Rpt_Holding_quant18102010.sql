 SELECT TOP 1 @@TMPHOLDING_DT = CASE WHEN DPHMC_HOLDING_DT > @PA_FORDATE THEN @PA_FORDATE ELSE DPHMC_HOLDING_DT END FROM DP_HLDG_MSTR_CDSL WHERE DPHMC_DELETED_IND =1   



