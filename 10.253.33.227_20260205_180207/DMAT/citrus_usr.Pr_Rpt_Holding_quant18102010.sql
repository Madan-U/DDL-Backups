		AND (DPHMCD_HOLDING_DT BETWEEN EFF_FROM AND EFF_TO)                    
		AND (CONVERT(NUMERIC,DPAM_SBA_NO) BETWEEN CONVERT(NUMERIC,@PA_FROMACCID) AND  CONVERT(NUMERIC,@PA_TOACCID))   
