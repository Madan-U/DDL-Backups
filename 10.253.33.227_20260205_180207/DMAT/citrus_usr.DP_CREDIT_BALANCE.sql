CREATE PROCEDURE DP_CREDIT_BALANCE

AS BEGIN

SELECT NISE_PARTY_cODE,CLIENT_CODE,ACTIVE_DATE,FIRST_HOLD_NAME,TEMPLATE_CODE
into #CLIENT
 FROM TBL_CLIENT_MASTER WHERE STATUS='ACTIVE'
 
 SELECT A.*,B.ACTUAL_AMOUNT FROM #CLIENT A
 LEFT OUTER JOIN
 VW_ACC_CURR_BAL B
 ON A.CLIENT_cODE=B.CLIENT_CODE
 WHERE ACTUAL_AMOUNT<0
 END

GO
