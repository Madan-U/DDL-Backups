-- Object: FUNCTION citrus_usr.FORMATINTREFNO
-- Server: 10.253.33.227 | DB: DMAT
--------------------------------------------------

CREATE function [citrus_usr].[FORMATINTREFNO]
(
 @PA_VAL VARCHAR(100)
)
RETURNS VARCHAR(8000)
AS 
BEGIN 
DECLARE @L_INTREF VARCHAR(200),
@L_INTREF_LEN BIGINT
IF LEN(@PA_VAL)=0 
BEGIN 
SET @L_INTREF = @PA_VAL
END 
ELSE
BEGIN 
SET @L_INTREF_LEN = 20-LEN(@PA_VAL)
SET @L_INTREF = @PA_VAL + REPLICATE(' ',@L_INTREF_LEN)
END
RETURN @L_INTREF
END

GO
