CREATE   VIEW [citrus_usr].[UCCUNUC]
AS

SELECT 'ACTIVE' [DESC], COUNT ( DISTINCT  BOID  ) [COUNT] 
  FROM DPS8_PC1 
WHERE   BOACCTSTATUS = '1'
UNION 
SELECT  'UCC UPDATE' C , COUNT ( DISTINCT  BOID  )E 
  FROM DPS8_PC22   WHERE BOId IN (SELECT  DISTINCT  BOID   
  FROM DPS8_PC1 
WHERE   BOACCTSTATUS = '1')
UNION   
  select  'UCC NOT UPDATE' A, (SELECT  COUNT( DISTINCT  BOID  )   
  FROM DPS8_PC1 
WHERE   BOACCTSTATUS = '1') - (SELECT   COUNT ( DISTINCT  BOID  )E  
  FROM DPS8_PC22 WHERE BOId IN (SELECT  DISTINCT  BOID   
  FROM DPS8_PC1 
WHERE   BOACCTSTATUS = '1') )

GO
