-- Object: PROCEDURE citrus_usr.PR_DIS_ONLINE_DEL
-- Server: 10.253.33.227 | DB: DMAT
--------------------------------------------------

create PROCEDURE PR_DIS_ONLINE_DEL
(@PA_ID NUMERIC
,@PA_ERRMSG varchar(800) output 
)
AS 
BEGIN 

IF EXISTS (SELECT DPTDC_ID FROM DPTDC_MAK WHERE DPTDC_ID=@PA_ID AND DPTDC_DELETED_IND IN (0,-1))
BEGIN
update DPTDC_MAK set DPTDC_DELETED_IND=3 WHERE DPTDC_ID=@PA_ID AND DPTDC_DELETED_IND IN (0,-1)
END 
END

GO
