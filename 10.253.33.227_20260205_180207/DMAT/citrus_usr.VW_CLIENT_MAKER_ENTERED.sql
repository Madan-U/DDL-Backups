CREATE VIEW [citrus_usr].[VW_CLIENT_MAKER_ENTERED]
AS
SELECT DISTINCT DPAM_SBA_NO TB_CLIENT_ID,'MAKER ENTERED' AS TB_TRX_ALLOW
,DPTDC_SLIP_NO TB_INTERNAL_REFNO,DPTDC_CREATED_DT TB_TRX_DATE FROM
DPTDC_MAK,DP_ACCT_MSTR WHERE DPTDC_DPAM_ID=DPAM_ID
AND DPAM_DELETED_IND=1 AND DPTDC_DELETED_IND IN (0,-1)

GO
