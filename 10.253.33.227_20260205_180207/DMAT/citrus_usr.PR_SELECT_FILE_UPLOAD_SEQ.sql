create procedure [citrus_usr].[PR_SELECT_FILE_UPLOAD_SEQ] --484,'GROUP_CREATION',''
(@SCR_ID VARCHAR(100), 
 @OPTIONS VARCHAR(100),
 @FILE_NAME VARCHAR(100), 
 @EXTENTION VARCHAR(100) 

)
 AS
 BEGIN
 SELECT Count(FILE_NAME) CNT FROM FILE_UPLOAD_SECURITY WHERE FILE_NAME =@FILE_NAME and SCR_ID = @SCR_ID and OPTIONS = @OPTIONS and FILE_EXTENTION = @EXTENTION
 END

GO
