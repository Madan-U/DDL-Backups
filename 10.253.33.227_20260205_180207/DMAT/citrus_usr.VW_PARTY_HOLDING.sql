CREATE VIEW  [citrus_usr].[VW_PARTY_HOLDING]
AS
SELECT ISNULL(C.NISE_PARTY_CODE, '') AS CM_BLSAVINGCD, H.HLD_AC_CODE, I.COMP_NAME, H.HLD_ISIN_CODE, H.HLD_AC_POS,
HLD_AC_TYPE
FROM HOLDINGDATA H WITH(NOLOCK) , TBL_CLIENT_MASTER C WITH(NOLOCK) , VW_ISIN_MASTER  I WITH(NOLOCK)
WHERE H.HLD_AC_CODE = C.CLIENT_CODE AND H.HLD_ISIN_CODE = I.ISIN

GO
