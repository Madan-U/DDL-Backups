CREATE PROC [dbo].[TBAUDIT]
(
@FROMDATE DATETIME,
@TODATE DATETIME
)AS BEGIN

CREATE TABLE #TBL
(CL_CODE VARCHAR(15),
 CLTNAME VARCHAR(100),
 BALANCE MONEY ,
 EXCHANGE VARCHAR(15))

 CREATE NONCLUSTERED INDEX #T ON #TBL
 (CL_CODE ASC,EXCHANGE ASC)

 INSERT INTO #TBL
 SELECT CLTCODE,'' ACNAME,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END) ,'NSE'
 FROM LEDGER WITH(NOLOCK) WHERE VDT >=@FROMDATE +'00:00:00' AND  vdt <=@TODATE +'23:59:59'
 GROUP BY CLTCODE,ACNAME
 
 --SELECT * FROM #TBL ORDER BY CL_CODE 
 ---BSE
  INSERT INTO #TBL
 SELECT  CLTCODE,'' ACNAME,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END),'BSE'
 FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER WITH(NOLOCK) WHERE VDT >=@FROMDATE +'00:00:00' AND  vdt <=@TODATE +'23:59:59'
 GROUP BY CLTCODE,ACNAME 

 ---------FO
  INSERT INTO #TBL
 SELECT CLTCODE,''ACNAME,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END),'FO'
 FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WITH(NOLOCK) WHERE VDT >=@FROMDATE +'00:00:00' AND  vdt <=@TODATE +'23:59:59'
 GROUP BY CLTCODE,ACNAME

 -----NSECURFO

  INSERT INTO #TBL
 SELECT CLTCODE,''ACNAME,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END),'NSX'
 FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER WITH(NOLOCK) WHERE VDT >=@FROMDATE +'00:00:00' AND  vdt <=@TODATE +'23:59:59'
 GROUP BY CLTCODE,ACNAME

 ------MCD
  INSERT INTO #TBL
 SELECT CLTCODE,''ACNAME,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END),'MCD'
 FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER WITH(NOLOCK) WHERE VDT >=@FROMDATE +'00:00:00' AND  vdt <=@TODATE +'23:59:59'
 GROUP BY CLTCODE,ACNAME
 
CREATE TABLE #TBLMAIN
(CL_CODE VARCHAR(15),
TOTAL_BAL MONEY,
NSE_BAL MONEY,
BSE_BAL MONEY,
NSEFO_BAL MONEY,
NSX_BAL MONEY,
MCD_BAL MONEY
 )  


 CREATE NONCLUSTERED INDEX #TQ ON #TBLMAIN
 (CL_CODE ASC)


UPDATE T SET CL_CODE =S.PARENTCODE
FROM #TBL T ,MSAJAG.DBO.CLIENT_DETAILS S
WHERE T.CL_CODE=S.CL_CODE 
AND LEFT(S.CL_CODE,2) ='98'


SELECT  CL_CODE, SUM(BALANCE) BALANCE ,EXCHANGE    INTO #TBL1  FROM #TBL WHERE CL_CODE >='A001' AND CL_CODE <='ZZZZZ'
GROUP BY CL_CODE,EXCHANGE

INSERT INTO  #TBLMAIN
SELECT DISTINCT  CL_CODE,'0','0','0','0','0','0' FROM #TBL1
 
 UPDATE  #TBLMAIN SET NSE_BAL = BALANCE FROM #TBL1 WHERE EXCHANGE ='NSE'AND #TBLMAIN.CL_CODE=#TBL1.CL_CODE
 UPDATE  #TBLMAIN SET BSE_BAL = BALANCE FROM #TBL1 WHERE EXCHANGE ='BSE'AND #TBLMAIN.CL_CODE=#TBL1.CL_CODE
 UPDATE  #TBLMAIN SET NSEFO_BAL = BALANCE FROM #TBL1 WHERE EXCHANGE ='FO'AND #TBLMAIN.CL_CODE=#TBL1.CL_CODE
 UPDATE  #TBLMAIN SET NSX_BAL = BALANCE FROM #TBL1 WHERE EXCHANGE ='NSX'AND #TBLMAIN.CL_CODE=#TBL1.CL_CODE
 UPDATE  #TBLMAIN SET MCD_BAL = BALANCE FROM #TBL1 WHERE EXCHANGE ='MCD'AND #TBLMAIN.CL_CODE=#TBL1.CL_CODE
 UPDATE  #TBLMAIN SET TOTAL_BAL =  NSE_BAL+BSE_BAL+NSEFO_BAL+NSX_BAL+MCD_BAL
 
 SELECT A.CL_CODE,long_name,TOTAL_BAL,NSE_BAL,BSE_BAL,NSEFO_BAL,NSX_BAL,MCD_BAL FROM #TBLMAIN A
 LEFT OUTER JOIN 
 MSAJAG..CLIENT_DETAILS B
 ON A.CL_CODE=B.cl_code
 WHERE NSE_BAL<>'0'OR BSE_BAL<>'0'OR NSEFO_BAL<>'0'OR NSX_BAL<>'0'OR MCD_BAL<>'0'
 ORDER BY A.CL_CODE
 END

GO
