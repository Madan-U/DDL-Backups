CREATE PROCEDURE [dbo].[GET_LAST_SETTTLE_DATE_BKUP_15Jan2021]
	@REPORTDATE VARCHAR(11),
	@EXCHANGE VARCHAR(3)
AS



UPDATE D SET D.LAST_SETT_DATE = B.LAST_SETTLE_DATE FROM #DATA D,
(SELECT PARTY_CODE = CL_CODE, LAST_SETTLE_DATE = CASE WHEN MAX(PAY_B3B_PAYMENT) = 1 
AND @EXCHANGE = 'NSE' THEN 'BTB' ELSE CASE WHEN MAX(Active_Date) > DATEADD(D, -90, @REPORTDATE) THEN 'NA' ELSE '' END END 
FROM MSAJAG.DBO.CLIENT_BROK_DETAILS
GROUP BY CL_CODE) B
WHERE D.PARTY_CODE = B.PARTY_CODE


UPDATE D SET LAST_SETT_DATE = REPLACE(REPLACE(CONVERT(VARCHAR(11), SETT_DATE, 106), ' ', '-'), '-20', '-')
FROM #DATA D,
(SELECT  party_code, SETT_DATE = max(SCCS_SettDate_Last) from msajag.dbo.Sebi_Payout--, #COLL D
					WHERE SCCS_SettDate_Last >= @REPORTDATE 
					--and SCCS_SettDate_Last <= @TODATE 
					GROUP BY party_code) D1
WHERE d.PARTY_CODE = d1.Party_code

UPDATE #DATA SET LAST_SETT_DATE = 'NA' WHERE ISNULL(LAST_SETT_DATE, '') = '' 

--UPDATE #DATA SET LAST_SETT_DATE = 'ND' WHERE ISNULL(LAST_SETT_DATE, '') = ''

GO
