-- Object: PROCEDURE dbo.RPT_SFCM_DEBTORS
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


 CREATE PROC RPT_SFCM_DEBTORS (@TDATE VARCHAR(11),@FPARTY VARCHAR(10),@TPARTY VARCHAR(10))
 
 AS  --- EXEC RPT_SFCM_DEBTORS 'APR 19 2021','A','ZZZ'
 
 DECLARE @RUNTIEM VARCHAR(10)
 
 SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(RUNDATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING 
 
 SELECT REPORT_DATE,CLTCODE,CLIENT_NAME=M.LONG_NAME,PANNO=M.PAN_GIR_NO,SUM(BALANCE) AS BALANCE,RUNTIME=@RUNTIEM INTO #SFCM
 FROM ACCOUNT.DBO.SFCM_REPORTING A,MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)        
 WHERE A.CLTCODE=M.CL_CODE 
 AND CLTCODE BETWEEN @FPARTY AND @TPARTY
 AND A.REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59'
 GROUP BY CLTCODE,REPORT_DATE,M.PAN_GIR_NO,M.LONG_NAME
 
SELECT REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,BALANCE,REPORT='DR',RUNTIME
FROM #SFCM 
WHERE BALANCE < 0 
ORDER BY 5

GO
