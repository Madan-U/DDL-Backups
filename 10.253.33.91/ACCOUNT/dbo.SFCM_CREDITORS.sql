-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROC [dbo].[SFCM_CREDITORS] (@TODATE  DATETIME, @FPARTY VARCHAR(11),@TPARTY VARCHAR(11))

AS

/*

EXEC SFCM_CREDITORS 'APR 16 2021','A','ZZZ'

*/

DECLARE @FROMDATE DATETIME

SELECT @FROMDATE =SDTCUR FROM ACCOUNT.DBO.PARAMETER (NOLOCK) WHERE @TODATE BETWEEN SDTCUR AND LDTCUR         

/* Ledger Balance of All Segment */

 SELECT CLTCODE, NETAMT =SUM(NETAMT) INTO #SCFMCR FROM       
 (SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                  
  FROM ACCOUNT.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE AND EDT <=@TODATE  + ' 23:59' GROUP BY CLTCODE
  
  UNION ALL
 
  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                  
  FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE AND EDT <=@TODATE  + ' 23:59' GROUP BY CLTCODE
 
  UNION ALL
 
  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                  
  FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE AND EDT <=@TODATE  + ' 23:59' GROUP BY CLTCODE
 
  UNION ALL
 
  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                  
  FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE AND EDT <=@TODATE  + ' 23:59' GROUP BY CLTCODE

  UNION ALL

  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                
  FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE AND EDT <=@TODATE  + ' 23:59' GROUP BY CLTCODE
 
  UNION ALL
 
  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                  
  FROM MTFTRADE.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE AND EDT <=@TODATE  + ' 23:59' GROUP BY CLTCODE
 
  UNION ALL
 
  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                  
  FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE  AND EDT <=@TODATE +' 23:59' 
  AND CLTCODE BETWEEN 'A' AND 'ZZZZ9999'  GROUP BY CLTCODE               
  
  UNION ALL
  
  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                  
  FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE AND EDT <=@TODATE +' 23:59'        
  AND CLTCODE BETWEEN 'A' AND 'ZZZZ9999' GROUP BY CLTCODE
 
  UNION ALL
        
  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                  
  FROM ANGELCOMMODITY.ACCOUNTBFO.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE             
  AND EDT <=@TODATE +' 23:59' AND CLTCODE BETWEEN 'A' AND 'ZZZZ9999' GROUP BY CLTCODE          

  UNION ALL
        
  SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)                                  
  FROM ANGELCOMMODITY.ACCOUNTCURBFO.DBO.LEDGER WITH (NOLOCK) WHERE VDT >=@FROMDATE  AND EDT <=@TODATE +' 23:59'        
  AND CLTCODE BETWEEN 'A' AND 'ZZZZ9999' GROUP BY CLTCODE ) A
  GROUP BY CLTCODE
 


/* Uncleare Ledger Balance of All Segment */ 

DECLARE @RECSDATE DATETIME , @RECODATE DATETIME        
        
SET @RECSDATE =@TODATE-90        
SET @RECODATE=@TODATE+1         

INSERT INTO #SCFMCR
SELECT CLTCODE,SUM(BALANCE) AS BALANCE  FROM (        
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'NSE' AS EXCHANGE FROM LEDGER L (NOLOCK)  ,LEDGER1 L1 WITH (NOLOCK)          
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND L.VTYP ='2'  AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE         
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE         
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'BSE' AS EXCHANGE FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER L WITH (NOLOCK) ,        
AngelBSECM.ACCOUNT_AB.DBO.LEDGER1 L1 WITH (NOLOCK)             
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND  L.VTYP ='2' AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE         
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE         
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'NSEFO' AS EXCHANGE FROM ANGELFO.ACCOUNTFO.DBO.LEDGER L WITH (NOLOCK) ,        
ANGELFO.ACCOUNTFO.DBO.LEDGER1 L1 WITH (NOLOCK)         
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND  L.VTYP ='2'  AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE         
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE         
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'NSECURFO' AS EXCHANGE  FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER L WITH (NOLOCK) ,        
ANGELFO.ACCOUNTCURFO.DBO.LEDGER1 L1 WITH (NOLOCK)             
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND L.VTYP ='2'  AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE         
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE         
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'MCDXCDS' AS EXCHANGE FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER L WITH (NOLOCK),        
ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER1 L1 WITH (NOLOCK)        
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND L.VTYP ='2' AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE         
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE         
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'BSEFO' AS EXCHANGE FROM ANGELCOMMODITY.ACCOUNTBFO.DBO.LEDGER L WITH (NOLOCK) ,        
ANGELCOMMODITY.ACCOUNTBFO.DBO.LEDGER1 L1 WITH (NOLOCK)        
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND L.VTYP ='2' AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE         
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE  ,'BSECURFO' AS EXCHANGE FROM ANGELCOMMODITY.ACCOUNTCURBFO.DBO.LEDGER L WITH (NOLOCK),        
ANGELCOMMODITY.ACCOUNTCURBFO.DBO.LEDGER1 L1 WITH (NOLOCK)        
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND L.VTYP ='2' AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE           
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE         
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'MCX' AS EXCHANGE FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER L WITH (NOLOCK) ,        
ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER1 L1 WITH (NOLOCK)        
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND L.VTYP ='2'  AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE         
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE          
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'NCX' AS EXCHANGE FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER L WITH (NOLOCK) ,        
ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER1 L1 WITH (NOLOCK)        
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND L.VTYP ='2'  AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE         
AND ( RELDT ='' OR RELDT >=@RECODATE) GROUP BY CLTCODE           
UNION ALL          
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='C' THEN VAMT*-1 ELSE VAMT END) AS BALANCE ,'MTF' AS EXCHANGE FROM MTFTRADE.DBO.LEDGER L WITH (NOLOCK) ,        
MTFTRADE.DBO.LEDGER1 L1 WITH (NOLOCK)        
WHERE VDT >=@RECSDATE AND VDT <=@TODATE +' 23:59' AND L.VTYP ='2'  AND L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE =L1.BOOKTYPE         
AND ( RELDT ='' OR RELDT >=@RECODATE)         
GROUP BY CLTCODE ) A GROUP BY CLTCODE,EXCHANGE 


UPDATE #SCFMCR SET CLTCODE =PARENTCODE FROM MSAJAG.DBO.CLIENT_DETAILS WITH (NOLOCK)       
WHERE  CLTCODE LIKE '98%' AND CLTCODE=CL_CODE

DELETE FROM #SCFMCR WHERE CLTCODE LIKE '%ERROR%'

INSERT INTO TBL_SCFMCR
SELECT CLTCODE,SUM(NETAMT) AS CREDITORS,RUNDATE=GETDATE() FROM #SCFMCR A        
GROUP BY CLTCODE HAVING SUM(NETAMT) > 0 
AND CLTCODE IN ( SELECT Cl_code FROM MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK) WHERE Cl_code BETWEEN @FPARTY AND @TPARTY )
ORDER BY 2 DESC


SELECT REPORT_DATE = @TODATE, CLTCODE,SUM(NETAMT) AS CREDITORS FROM #SCFMCR A        
GROUP BY CLTCODE HAVING SUM(NETAMT) > 0 
AND CLTCODE IN ( SELECT Cl_code FROM MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK) WHERE Cl_code BETWEEN @FPARTY AND @TPARTY )
ORDER BY 3 DESC

GO
