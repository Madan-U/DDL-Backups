-- Object: PROCEDURE dbo.RPT_SEBIPAYOUT_CLTDATEINFO
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROC [DBO].[RPT_SEBIPAYOUT_CLTDATEINFO]
@FROMDATE VARCHAR(12)

AS


SELECT PARTYCODE, CONVERT(VARCHAR,PROCESS_DT,103) AS  PROCESS_DT,  CONVERT(VARCHAR,PROCESS_DT,103) AS  NEW_PROCESS_DT
FROM SEBIPAYOUT_ALL
WHERE DATEDIFF(D,PROCESS_DT,@FROMDATE  ) = 0

GO
