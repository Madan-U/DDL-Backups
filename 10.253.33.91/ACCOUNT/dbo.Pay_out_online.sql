CREATE TABLE #MARGIN
(CLTCODE VARCHAR(10),SHORT_FALL MONEY)

INSERT INTO #MARGIN
EXEC CL_MARGIN @CLTCODE


  Select b.SAUDA_DATE,b.PARTY_CODE, b.ExposureAndNet, b.Brokerage,b.Gst,b.STT,b.TurnoverCharges,b.SebiFees,b.StampDuty   
   INTO #FOCHARGES 
  from  mimansa. Clientservice.dbo.FoBillGenData b With(Nolock) WHERE B.Party_code= @CLTCODE 


     Declare @maxdate as datetime  
   select @maxdate=max(updateddate) from [CSOKYC-6].general.dbo.ncms_ld_API_UAT  with(nolock)        

 select Client_Code,Client_Name,Branch_Code,Final_Margin,Utilization,Adhoc_DrCr,Ledger_Balance,
 Stock_After_Haircut,Stock_Before_Haircut,Net_Margin,BSE_MTM,BSE_Var,NSE_MTM,NSE_Var,NSEF_Span,
 NSEF_MVM,NSEF_FUT_MTM,NSEF_OPT_MTM,NSEF_OPT_Premium,NSEF_MTM,
 MCX_Span,MCX_MVM,MCX_MTM,NCDX_Span,NCDX_MVM,NCDX_MTM,NSEC_Span,NSEC_MVM,NSEC_FUT_MTM,NSEC_OPT_MTM,
 NSEC_OPT_Premium,NSEC_MTM,MCXC_Span,MCXC_MVM,MCXC_FUT_MTM,MCXC_OPT_MTM,MCXC_OPT_Premium,MCXC_MTM,Log_Mtm  
 INTO #LD_BALANCE
   from [CSOKYC-6].general.dbo.ncms_ld_API_UAT with(nolock) where  
   updateddate = @maxdate AND Client_Code=@CLTCODE 

 
DECLARE @CLDATE DATETIME
SELECT @CLDATE =MAX(SYSDATE) FROM MSAJAG.DBO.CLOSING_MTM

SELECT PARTY_CODE,B.ISIN,B.SCRIP_CD,B.SERIES,SHROT= (DELQTY-RECQTY) * ISNULL(CL_RATE,0)  INTO #SHORT
  FROM angeldemat.[INHOUSE_NSE].[dbo].[BO_Shortage] B,MSAJAG.DBO.CLOSING_MTM C
  WHERE SYSDATE =@CLDATE AND B.ISIN= C.ISIN  AND B.SCRIP_CD=C.SCRIP_CD
  AND B.SERIES=C.SERIES 
  AND  party_code =@CLTCODE

 --SELECT * FROM   #LEDGER 
 --#UNLEDGER
 --#TDAY_UNRECO
 --#SHORT
 --#LD_BALANCE
 --#FOCHARGES
 --#MARGIN

 select PARTY_CODE,SCRIP_CD,ISIN,SUM(QTY) qty,SUM(TOTAL) amt,DP_TYPE  into #hold from (
 select  PARTY_CODE,SCRIP_CD,ISIN,QTY ,TOTAL ,(CASE WHEN SOURCE ='DP' THEN 'DP' ELSE 'POOL' END) AS DP_TYPE 
 from [CSOKYC-6].general.dbo.rms_holding WITH(NOLOCK) where PARTY_CODE =@CLTCODE
 ) a
 group by PARTY_CODE,SCRIP_CD,ISIN,DP_TYPE

 insert into  #hold
select PARTY_CODE,SCRIP_CD,ISIN,SUM(QTY),SUM(AMOUNT),'COLL' AS COLL
from [CSOKYC-6].general.dbo.CLIENT_COLLATERALS WITH(NOLOCK)  WHERE 
EFFDATE=CONVERT(VARCHAR(11),GETDATE()-1,120)  AND PARTY_CODE =@CLTCODE
group by  PARTY_CODE,SCRIP_CD,ISIN




 CREATE TABLE #FINAL
 (PARTY_CODE VARCHAR(10), TOTAL_LEDGER MONEY,UNCLEAR_BALANCE MONEY,UNRECO MONEY,TDAY_RECEIPT MONEY,TDAY_PAYMENTS MONEY, 
 SEC_SHORT MONEY,TOTAL_VAR MONEY,FO_CHRAGES MONEY,MARGIN_SHORT MONEY,
  ACCURED_CHARGES MONEY,DP_HOLDING MONEY,POOL_HOLDING MONEY,COLL_HOLD MONEY,  NET_BALANCE MONEY)

  INSERT INTO #FINAL (PARTY_CODE,TOTAL_LEDGER)
  SELECT CLTCODE,SUM(CASH_BALANCE)
  FROM #LEDGER  GROUP BY CLTCODE
  
  UPDATE #FINAL SET UNCLEAR_BALANCE =(CASH_BALANCE) FROM 
  (SELECT CLTCODE,SUM(CASH_BALANCE) AS  CASH_BALANCE FROM #UNLEDGER GROUP BY CLTCODE ) B WHERE #FINAL.PARTY_CODE=B.CLTCODE
 
  UPDATE #FINAL SET SEC_SHORT =(BALANCE) FROM 
  (SELECT party_CODE ,SUM(SHROT)*-1 AS   BALANCE FROM #SHORT GROUP BY party_CODE ) B WHERE #FINAL.PARTY_CODE=B.party_CODE

   UPDATE #FINAL SET TOTAL_VAR =(BALANCE) FROM 
  (SELECT Client_Code,SUM(Log_Mtm)*-1 AS   BALANCE FROM #LD_BALANCE GROUP BY Client_Code ) B WHERE #FINAL.PARTY_CODE=B.Client_Code

   UPDATE #FINAL SET FO_CHRAGES =(BALANCE) FROM 
  (SELECT PARTY_CODE,SUM(Brokerage+Gst+STT+TurnoverCharges+SebiFees+StampDuty)*-1 AS   BALANCE FROM #FOCHARGES GROUP BY PARTY_CODE ) 
  B WHERE #FINAL.PARTY_CODE=B.PARTY_CODE

   UPDATE #FINAL SET MARGIN_SHORT =(BALANCE) FROM 
  (SELECT CLTCODE,SUM(SHORT_FALL)*-1 AS   BALANCE FROM #MARGIN GROUP BY CLTCODE ) B WHERE #FINAL.PARTY_CODE=B.CLTCODE
   
   UPDATE #FINAL SET ACCURED_CHARGES =(BALANCE) FROM 
   (SELECT CLTCODE,SUM(SHORT_FALL)*-1 AS   BALANCE FROM #MARGIN GROUP BY CLTCODE ) B WHERE #FINAL.PARTY_CODE=B.CLTCODE

  UPDATE #FINAL SET TDAY_RECEIPT=RECEIPTS,TDAY_PAYMENTS=PAYMENT   FROM
   (SELECT CLTCODE,PAYMENT,RECEIPTS FROM #TDAY_MOVEMENT   ) B WHERE #FINAL.PARTY_CODE=B.CLTCODE

   UPDATE #FINAL SET DP_HOLDING=amt,TDAY_PAYMENTS=amt   FROM
   (SELECT * FROM #hold   ) B WHERE #FINAL.PARTY_CODE=B.PARTY_CODE AND DP_TYPE='DP'

     UPDATE #FINAL SET POOL_HOLDING=amt,TDAY_PAYMENTS=amt   FROM
   (SELECT * FROM #hold   ) B WHERE #FINAL.PARTY_CODE=B.PARTY_CODE AND DP_TYPE='POOL'

     UPDATE #FINAL SET COLL_HOLD=amt,TDAY_PAYMENTS=amt   FROM
   (SELECT * FROM #hold   ) B WHERE #FINAL.PARTY_CODE=B.PARTY_CODE AND DP_TYPE='COLL'
   
 

  SELECT PARTY_CODE,TOTAL_LEDGER ,UNCLEAR_BALANCE=ISNULL(UNCLEAR_BALANCE,0) ,UNRECO=ISNULL(UNRECO,0) ,SEC_SHORT=ISNULL(SEC_SHORT,0) ,
  TOTAL_VAR= ISNULL(TOTAL_VAR,0),FO_CHRAGES=ISNULL(FO_CHRAGES,0) ,MARGIN_SHORT=ISNULL(MARGIN_SHORT,0) ,ACCURED_CHARGES =ISNULL(ACCURED_CHARGES,0) ,
  TDAY_RECEIPT=ISNULL(TDAY_RECEIPT,0),TDAY_PAYMENTS=ISNULL(TDAY_PAYMENTS,0),
  DP_HOLDING=ISNULL(DP_HOLDING,0),POOL_HOLDING=ISNULL(POOL_HOLDING,0),COLL_HOLD=ISNULL(COLL_HOLD,0),
  NET_BALANCE=(TOTAL_LEDGER+ISNULL(UNCLEAR_BALANCE,0)+ISNULL(UNRECO,0)+ISNULL(SEC_SHORT,0)+ISNULL(TOTAL_VAR,0)+ISNULL(FO_CHRAGES,0)+
  ISNULL(MARGIN_SHORT,0)+ISNULL(ACCURED_CHARGES,0)) FROM  #FINAL 

  DROP TABLE #LEDGER 
  DROP TABLE #UNLEDGER
  DROP TABLE #TDAY_UNRECO
  DROP TABLE #SHORT
  DROP TABLE #LD_BALANCE
  DROP TABLE #FOCHARGES
  DROP TABLE #MARGIN
  DROP TABLE #FINAL
  DROP TABLE #TDAY_MOVEMENT

GO
