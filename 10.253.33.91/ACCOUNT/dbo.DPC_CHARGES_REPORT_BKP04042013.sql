CREATE PROCEDURE [dbo].[DPC_CHARGES_REPORT]      
(      
 @REPORT_LEVEL VARCHAR(10),      
 @FROM_DATE VARCHAR(11),      
 @TO_DATE VARCHAR(11),      
 @FROM_PARTY VARCHAR(10),      
 @TO_PARTY VARCHAR(10),      
 @SUMMARY_FLAG BIT,      
 @SEG_EXCH VARCHAR(200),      
 @ENTITY_TYPE VARCHAR(50),      
 @STATUSID VARCHAR(20),      
 @STATUSNAME VARCHAR(50)      
)      
AS      
    
--EXEC DPC_CHARGES_REPORT 'Party', 'Sep 1 2012', 'Sep 15 2012', 's5643','s5643',1,'BSE - CAPITAL|MCD - FUTURES|MCX - FUTURES|NCX - FUTURES|NSE - CAPITAL|NSE - FUTURES|NSX - FUTURES|','ENTITY', 'broker','broker'   
      
DECLARE       
 @PIVOT_COLS VARCHAR(8000),      
 @PIVOT_VAL VARCHAR(8000),      
 @@SQL VARCHAR(8000)      
     
SET @REPORT_LEVEL = CASE WHEN @REPORT_LEVEL = 'Client' then 'PARTY' else @REPORT_LEVEL end    
       
      
CREATE TABLE #DPC_CHARGES      
(      
 [DPC_DATE] [DATETIME] NULL,      
 [CLTCODE] [VARCHAR](10) NULL,      
 [LONG_NAME] [VARCHAR](100) NULL,      
 [BRANCH_CD] [VARCHAR](10) NULL,      
 [SUB_BROKER] [VARCHAR](25) NULL,      
 [TRADER] [VARCHAR](25) NULL,      
 [REGION] [VARCHAR](20) NULL,      
 [AREA] [VARCHAR](20) NULL,      
 [CL_TYPE] [VARCHAR](10) NULL,      
 [DPC_BALANCE] [MONEY] NULL,      
 [DPC_CHARGE] [MONEY] NULL,      
 [DPC_PERCENTE] [MONEY] NULL,      
 [AMOUNT_REV] [MONEY] NULL,      
 [EXCHANGE] [VARCHAR](10) NULL,      
 [SEGMENT] [VARCHAR](10) NULL,      
 [ENTITY] [VARCHAR](20) NULL,      
 [SEG_GROUP] VARCHAR(50) NULL,      
 [NET_BALANCE] MONEY NULL,      
 [NET_DPC_CHARGES] MONEY      
)      
      
SET @@SQL = "INSERT INTO #DPC_CHARGES "      
SET @@SQL = @@SQL + "(DPC_DATE, CLTCODE, LONG_NAME, BRANCH_CD, SUB_BROKER, TRADER, REGION, AREA, CL_TYPE, DPC_BALANCE, DPC_CHARGE, DPC_PERCENTE, AMOUNT_REV, EXCHANGE, SEGMENT, SEG_GROUP) "      
SET @@SQL = @@SQL + "SELECT "      
SET @@SQL = @@SQL + " DPC_DATE, CLTCODE = CASE WHEN '" + @REPORT_LEVEL + "' = 'PARTY' THEN CLTCODE ELSE ACCOUNT_CODE END, LONG_NAME = CASE WHEN '" + @REPORT_LEVEL + "' = 'PARTY' THEN LONG_NAME ELSE ACCOUNT_NAME END, BRANCH_CD, SUB_BROKER, TRADER, REGION, 
AREA, CL_TYPE, DPC_BALANCE, DPC_CHARGE, DPC_PERCENTE, AMOUNT_REV, EXCHANGE, SEGMENT, D." + @ENTITY_TYPE + " "      
SET @@SQL = @@SQL + "FROM "      
IF @REPORT_LEVEL = 'PARTY'      
 SET @@SQL = @@SQL + " DPC_CHARGES D LEFT OUTER JOIN MSAJAG.DBO.ENTITY_DETAILS ON ACCOUNT_CODE = CLTCODE "      
ELSE      
 SET @@SQL = @@SQL + " DPC_CHARGES D, MSAJAG.DBO.ENTITY_DETAILS "      
SET @@SQL = @@SQL + "WHERE "      
SET @@SQL = @@SQL + "DPC_DATE >= '" + @FROM_DATE + "' "      
SET @@SQL = @@SQL + "AND DPC_DATE <= '" + @TO_DATE + "' "      
IF @REPORT_LEVEL = 'PARTY'      
BEGIN      
 SET @@SQL = @@SQL + "AND CLTCODE BETWEEN '" + @FROM_PARTY + "' AND '" + @TO_PARTY + "' "      
END      
ELSE      
BEGIN      
 SET @@SQL = @@SQL + "AND " + @REPORT_LEVEL + " BETWEEN '" + @FROM_PARTY + "' AND '" + @TO_PARTY + "' "      
 SET @@SQL = @@SQL + "AND ACCOUNT_CODE = " + @REPORT_LEVEL + " "      
END      
IF @STATUSID <> 'BROKER'      
BEGIN      
 SET @@SQL = @@SQL + "AND " + @STATUSID + " = '" + @STATUSNAME + "' "      
END      
SET @@SQL = @@SQL + "AND D." + @ENTITY_TYPE + " IN ('" + REPLACE(@SEG_EXCH, '|', ''',''') + "')"      
PRINT @@SQL      
  
EXEC (@@SQL)  


  
-- OVER FUNCTION TO GET NET_BALANCE      
      
/*IF @SUMMARY_FLAG = 0      
BEGIN  */  
 UPDATE D SET       
  NET_BALANCE = D1.DPC_BALANCE,      
  NET_DPC_CHARGES = (D1.DPC_BALANCE * DPC_PERCENTE) / 100/ 365      
 FROM #DPC_CHARGES D,      
 (SELECT DPC_DATE, CLTCODE, LONG_NAME,      
    DPC_BALANCE = SUM(DPC_BALANCE) OVER (PARTITION BY DPC_DATE, CLTCODE)      
 FROM #DPC_CHARGES) D1      
 WHERE D.DPC_DATE = D1.DPC_DATE AND D.CLTCODE = D1.CLTCODE    
/*END    
ELSE    
BEGIN    
 UPDATE D SET       
  NET_BALANCE = D1.DPC_BALANCE,      
  NET_DPC_CHARGES = (D1.DPC_BALANCE * DPC_PERCENTE) / 100/ 365      
 FROM #DPC_CHARGES D,      
 (SELECT DPC_DATE, CLTCODE, LONG_NAME,      
    DPC_BALANCE = SUM(DPC_BALANCE) OVER (PARTITION BY CLTCODE)      
 FROM #DPC_CHARGES) D1      
 WHERE D.CLTCODE = D1.CLTCODE    
END  */  
    
UPDATE #DPC_CHARGES SET NET_DPC_CHARGES = D1.NET_DPC_CHARGES FROM    
(SELECT NET_DPC_CHARGES = SUM(NET_DPC_CHARGES) FROM    
(SELECT CLTCODE, NET_DPC_CHARGES FROM #DPC_CHARGES) D) D1    
WHERE #DPC_CHARGES.CLTCODE = 'ZZZZZZZZZZ' AND #DPC_CHARGES.LONG_NAME = 'TOTAL'    
    
/*UPDATE #DPC_CHARGES SET NET_DPC_CHARGES = SUM(D1.NET_DPC_CHARGES)    
FROM  (SELECT CLTCODE, LONG_NAME, NET_DPC_CHARGES FROM #DPC_CHARGES GROUP BY CLTCODE, LONG_NAME, NET_DPC_CHARGES) D1    
WHERE #DPC_CHARGES.CLTCODE = 'ZZZZZZZZZZ' AND #DPC_CHARGES.LONG_NAME = 'TOTAL'*/    
  
INSERT INTO #DPC_CHARGES (CLTCODE, LONG_NAME, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES, SEG_GROUP)    
SELECT 'ZZZZZZZZZZ', 'TOTAL', SUM(DPC_BALANCE), MAX(NET_BALANCE), SUM(CASE WHEN NET_DPC_CHARGES > 0 THEN NET_DPC_CHARGES ELSE 0 END), SEG_GROUP  
FROM #DPC_CHARGES   
GROUP BY SEG_GROUP  
  
SET @PIVOT_VAL = (SELECT      
STUFF(      
(      
SELECT ',['+ITEMS+']=ISNULL(SUM(['+ITEMS+']),0)'      
FROM SPLIT(@SEG_EXCH,'|')      
FOR XML PATH('')      
),1,1,''))      
      
SET @PIVOT_COLS = (SELECT      
STUFF(      
(      
SELECT ',['+ITEMS+']'      
FROM SPLIT(@SEG_EXCH,'|')      
FOR XML PATH('')      
),1,1,''))      
      
PRINT @PIVOT_COLS      
    
IF @SUMMARY_FLAG = 0      
BEGIN      
 SET @@SQL = "SELECT DPC_DATE, CLTCODE, LONG_NAME,"+@PIVOT_VAL+",NET_BALANCE,NET_DPC_CHARGES"      
 SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES = CASE WHEN NET_DPC_CHARGES > 0 THEN NET_DPC_CHARGES ELSE 0 END FROM #DPC_CHARGES ) AS S "      
 SET @@SQL = @@SQL + " PIVOT ( SUM(DPC_BALANCE) FOR [SEG_GROUP] IN ("+@PIVOT_COLS+")) AS P "      
 SET @@SQL = @@SQL + " GROUP BY DPC_DATE, CLTCODE, LONG_NAME, NET_BALANCE, NET_DPC_CHARGES"      
 SET @@SQL = @@SQL + " ORDER BY CLTCODE, DPC_DATE "    
END      
ELSE      
BEGIN      
 /*SET @@SQL = "SELECT CLTCODE, MAX(LONG_NAME),"+@PIVOT_VAL+",NET_BALANCE = ISNULL(NET_BALANCE, 0), NET_DPC_CHARGES = SUM(ISNULL(NET_DPC_CHARGES, 0))"      
 SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES = CASE WHEN NET_DPC_CHARGES > 0 THEN NET_DPC_CHARGES ELSE 0 END  FROM #DPC_CHARGES ) AS S "      
 SET @@SQL = @@SQL + " PIVOT (SUM(DPC_BALANCE) FOR [SEG_GROUP] IN ("+@PIVOT_COLS+")) AS P "      
 SET @@SQL = @@SQL + " GROUP BY CLTCODE, NET_BALANCE, NET_DPC_CHARGES"      
 SET @@SQL = @@SQL + " ORDER BY CLTCODE "  */  
   
 SET @@SQL = "SELECT CLTCODE, LONG_NAME,"+@PIVOT_VAL+",NET_BALANCE = MAX(NET_BALANCE),NET_DPC_CHARGES = SUM(NET_DPC_CHARGES)"      
 SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES = CASE WHEN NET_DPC_CHARGES > 0 THEN NET_DPC_CHARGES ELSE 0 END FROM #DPC_CHARGES ) AS S "      
 SET @@SQL = @@SQL + " PIVOT ( SUM(DPC_BALANCE) FOR [SEG_GROUP] IN ("+@PIVOT_COLS+")) AS P "      
 SET @@SQL = @@SQL + " GROUP BY CLTCODE, LONG_NAME"      
 SET @@SQL = @@SQL + " ORDER BY CLTCODE"    
   
 --SET @@SQL = @@SQL + " SELECT * FROM #DPC_SUMMARY "  
END      
PRINT @@SQL      
EXEC(@@SQL)

GO
