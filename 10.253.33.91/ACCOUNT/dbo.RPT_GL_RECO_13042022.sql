-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROCEDURE [dbo].[RPT_GL_RECO_13042022] (@RUNDATE VARCHAR(11))
 
 AS --- EXEC RPT_GL_RECO 'JAN 17 2022'
 
SELECT A.CLTCODE,BANK_CODE=CONVERT(VARCHAR(20),''),LEDACNAME=A.ACNAME,A.VAMT,A.DRCR,A.VTYP,A.VNO,
A.BOOKTYPE,A.VDT,A.EDT,A.CDT,B.RELDT,B.DDNO,A.NARRATION,A.ENTEREDBY,SEGMENT='NSECM',
JVCLIENT_CODE=CONVERT(VARCHAR(20),''),VSTATUS=CONVERT(VARCHAR(20),''),
MATCHING_VNO=CONVERT(VARCHAR(20),''),MATCH_VNO_VDT=GETDATE(),CLOSER_PERIOD='' INTO #NEWGL
FROM ACCOUNT.DBO.LEDGER A WITH (NOLOCK), ACCOUNT.DBO.LEDGER1 B WITH (NOLOCK)
WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND A.VAMT = B.RELAMT
AND a.CLTCODE='51000031' AND A.VTYP=2
ORDER BY A.VNO

UPDATE #NEWGL SET BANK_CODE = B.CLTCODE FROM #NEWGL A,
(SELECT A.VNO,A.VTYP,A.VAMT,A.CLTCODE,A.EDT
FROM ACCOUNT.DBO.LEDGER A WITH (NOLOCK) WHERE VTYP=2 AND CLTCODE <> '51000031') B
WHERE A.VNO=B.VNO AND A.VTYP=B.VTYP AND A.VAMT=B.VAMT AND A.EDT=B.EDT

SELECT DISTINCT VNO,VTYP INTO #VNO FROM ACCOUNT.DBO.LEDGER WITH (NOLOCK)
WHERE VTYP <> 2 AND  CLTCODE='51000031'

SELECT A.VTYP,A.VNO,EDT,ACNAME,DRCR,VAMT,VDT,BALAMT,CDT,CLTCODE,ENTEREDBY,
PDT,NARRATION,REMARK='UNMATCH' INTO #UNMATCH
FROM ACCOUNT.DBO.LEDGER A WITH (NOLOCK), #VNO B
WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP
AND A.VTYP <> 2 AND A.CLTCODE <> '51000031'

UPDATE #NEWGL 
SET JVCLIENT_CODE = B.CLTCODE,VSTATUS='MATCH',MATCHING_VNO=B.VNO,MATCH_VNO_VDT=B.VDT
FROM #NEWGL A, #UNMATCH B
WHERE A.VAMT = B.VAMT AND B.NARRATION LIKE '%'+A.VNO+'%'

UPDATE A SET REMARK ='CLOSE' FROM #UNMATCH A, #NEWGL B
WHERE A.VNO = B.MATCHING_VNO AND A.VAMT = B.VAMT

UPDATE #NEWGL 
SET JVCLIENT_CODE = B.CLTCODE,VSTATUS='MATCH',MATCHING_VNO=B.VNO,MATCH_VNO_VDT=B.VDT
FROM #NEWGL A, #UNMATCH B
WHERE A.VAMT = B.VAMT AND B.NARRATION LIKE '%'+A.DDNO+'%' AND A.VSTATUS=''
AND b.REMARK<> 'CLOSE'


UPDATE #UNMATCH SET NARRATION = NEW_NARR FROM #UNMATCH A1,
(SELECT A.VTYP,A.VNO,NEW_NARR=A.NARRATION
FROM ACCOUNT.DBO.LEDGER A WITH (NOLOCK), #VNO B
WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP
AND A.VTYP <> 2 AND A.CLTCODE = '51000031') A2
WHERE A1.VTYP = A2.VTYP AND A1.VNO = A2.VNO

UPDATE #NEWGL 
SET JVCLIENT_CODE = B.CLTCODE,VSTATUS='MATCH',MATCHING_VNO=B.VNO,MATCH_VNO_VDT=B.VDT
FROM #NEWGL A, #UNMATCH B
WHERE A.VAMT = B.VAMT AND B.NARRATION LIKE '%'+A.VNO+'%' AND VSTATUS=''

UPDATE A SET REMARK ='CLOSE' FROM #UNMATCH A, #NEWGL B
WHERE A.VNO = B.MATCHING_VNO AND A.VAMT = B.VAMT

UPDATE #NEWGL SET VSTATUS='UNMATCH' WHERE LEN(DDNO) < = 6 AND VSTATUS<>'MATCH'

UPDATE #NEWGL 
SET JVCLIENT_CODE = B.CLTCODE,VSTATUS='MATCH',MATCHING_VNO=B.VNO,MATCH_VNO_VDT=B.VDT
FROM #NEWGL A, #UNMATCH B
WHERE A.VAMT = B.VAMT AND B.NARRATION LIKE '%'+A.DDNO+'%' AND A.VSTATUS=''
AND b.REMARK<> 'CLOSE'

UPDATE A SET REMARK ='CLOSE' FROM #UNMATCH A, #NEWGL B
WHERE A.VNO = B.MATCHING_VNO AND A.VAMT = B.VAMT

UPDATE #NEWGL SET VSTATUS='UNMATCH' WHERE VSTATUS=''

TRUNCATE TABLE GLRECO_DATA

INSERT INTO GLRECO_DATA
SELECT CLTCODE,BANK_CODE,LEDACNAME,VAMT,DRCR,VTYP,VNO,BOOKTYPE,VDT,EDT,CDT,RELDT,DDNO,
NARRATION,ENTEREDBY,SEGMENT,JVCLIENT_CODE,VSTATUS,MATCHING_VNO,OPEN_CLOSE_PERIOD=DATEDIFF(DAY,VDT,MATCH_VNO_VDT) 
FROM #NEWGL 
UNION ALL
SELECT CLTCODE,BANK_CODE='',ACNAME,VAMT,DRCR,VTYP,VNO,BOOKTYPE='01',VDT,EDT,CDT,RELDT='',DDNO='',
NARRATION,ENTEREDBY,SEGMENT='NSECM',JVCLIENT_CODE='',REMARK,'',OPEN_CLOSE_PERIOD=DATEDIFF(DAY,VDT,GETDATE())
FROM #UNMATCH WHERE REMARK='UNMATCH'


DECLARE @GLRECOFILE VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'51000031_GLRECO_DATA_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'
DECLARE @GLRECO VARCHAR(MAX)                            
SET @GLRECO = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''CLTCODE'''',''''BANK_CODE'''',''''LEDACNAME'''',''''VAMT'''',''''DRCR'''',''''VTYP'''',''''VNO'''',''''BOOKTYPE'''',''''VDT'''',''''EDT'''',''''CDT'''',''''RELDT'''',''''DDNO'''',''''NARRATION'''',''''ENTEREDBY'''',''''SEGMENT'''',''''JVCLIENT_CODE'''',''''VSTATUS'''',''''MATCHING_VNO'''',''''OPEN_CLOSE_PERIOD''''' 
SET @GLRECO = @GLRECO + ' UNION ALL SELECT CLTCODE,CONVERT(VARCHAR,BANK_CODE),LEDACNAME,CONVERT(VARCHAR,VAMT),DRCR,CONVERT(VARCHAR,VTYP),CONVERT(VARCHAR,VNO),CONVERT(VARCHAR,BOOKTYPE),CONVERT(VARCHAR(11),VDT,105),CONVERT(VARCHAR(11),EDT,105),CONVERT(VARCHAR,CDT),CONVERT(VARCHAR(11),RELDT,105),CONVERT(VARCHAR,DDNO),NARRATION,ENTEREDBY,SEGMENT,CONVERT(VARCHAR,JVCLIENT_CODE),VSTATUS,CONVERT(VARCHAR,MATCHING_VNO),CONVERT(VARCHAR,OPEN_CLOSE_PERIOD) FROM ACCOUNT.DBO.GLRECO_DATA" QUERYOUT ' +@GLRECOFILE+ ' -c -t"," -c -t"," -r"\n" -T'',NO_OUTPUT'    
EXEC(@GLRECO)


/* GL DUMP DATA */

TRUNCATE TABLE GLDUMP

INSERT INTO GLDUMP
SELECT A.VDT,A.VTYP,A.VNO,A.CLTCODE,A.NARRATION,B.DDNO,A.DRCR,A.VAMT,A.EDT,A.CDT,B.RELDT,A.ENTEREDBY 
FROM ACCOUNT.DBO.LEDGER A WITH (NOLOCK), ACCOUNT.DBO.LEDGER1 B WITH (NOLOCK)
WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND A.VAMT = B.RELAMT
AND A.VNO IN (SELECT VNO FROM ACCOUNT.DBO.LEDGER A WITH (NOLOCK) WHERE CLTCODE='51000031')
AND A.CLTCODE <> '51000031'

DECLARE @GLDUMPFILE VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'51000031_GLDUMP_DATA_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'
DECLARE @GLDUMP VARCHAR(MAX)                            
SET @GLDUMP = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''VDT'''',''''VTYP'''',''''VNO'''',''''CLTCODE'''',''''NARRATION'''',''''DDNO'''',''''DRCR'''',''''VAMT'''',''''EDT'''',''''CDT'''',''''RELDT'''',''''ENTEREDBY''''' 
SET @GLDUMP = @GLDUMP + ' UNION ALL SELECT CONVERT(VARCHAR(11),VDT,105),CONVERT(VARCHAR,VTYP),CONVERT(VARCHAR,VNO),CLTCODE,NARRATION,CONVERT(VARCHAR,DDNO),DRCR,CONVERT(VARCHAR,VAMT),CONVERT(VARCHAR(11),EDT,105),CONVERT(VARCHAR,CDT),CONVERT(VARCHAR(11),RELDT,105),ENTEREDBY FROM ACCOUNT.DBO.GLDUMP" QUERYOUT ' +@GLDUMPFILE+ ' -c -t"," -c -t"," -r"\n" -T'',NO_OUTPUT'
EXEC(@GLDUMP)

GO
