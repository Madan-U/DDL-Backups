CREATE PROCEDURE [dbo].[CLS_RECON_CMS_IDFC_PROCESS] (

@UPLOADID NVARCHAR(50) 
,@UPLOAD_FLAG NVARCHAR(1)
,@BANKCODE NVARCHAR(20))

AS

BEGIN
INSERT INTO #CMS_DATA

SELECT
			RTRIM(LTRIM(U.UPLOADID))
			,RTRIM(LTRIM(@BANKCODE)) AS BANKCODE
			,REPLACE(REPLACE(RTRIM(LTRIM(.DBO.Piece(FILE_DATA,'æ',1) )), CHAR(13), ''), CHAR(10), '') AS ROWTYPE
			,RTRIM(LTRIM(CONVERT(DATETIME,RTRIM(LTRIM(.DBO.Piece(FILE_DATA,'æ',4) )),  105))) AS VALUEDATE
			,RTRIM(LTRIM(.DBO.Piece(FILE_DATA,'æ',2) )) AS DRCR
			,RTRIM(LTRIM(.DBO.Piece(FILE_DATA,'æ',5) )) AS INST_NO_CHECK
			,RTRIM(LTRIM(CAST(REPLACE(.DBO.Piece(FILE_DATA,'æ',3), '''', '') AS DECIMAL(20, 2)))) AMT
			,RTRIM(LTRIM(.DBO.Piece(FILE_DATA,'æ',7))) AS DRAWER_NAME
			,RTRIM(LTRIM(.DBO.Piece(FILE_DATA,'æ',8) ))  AS CLTCODE
			,RTRIM(LTRIM(.DBO.Piece(FILE_DATA,'æ',9) )) AS CLTACC
			,'' AS VNO
			,'' AS VTYP
			,'' AS BOOKTYPE
			,0 AS MATCHEDFLAG
			,U.UPLOADSTATUS
			,U.FILEUPLOAD_SDATE
			,GETDATE()
			,F.UPLOADBY
			FROM RECON_BANK_FILEUPLOAD_LOGS U (NOLOCK)
			INNER JOIN FILEDATA_BANKRECO F (NOLOCK)
			ON U.UPLOADID = F.PROGID
			WHERE F.PROGID = @UPLOADID
	AND RTRIM(LTRIM(.DBO.Piece(FILE_DATA,'æ',2) ))  <> ''


END
