CREATE PROC [dbo].[BANKACC_FUNDMOV_B2C] ( @FROMDATE VARCHAR(11) , @TODATE VARCHAR(11))  

---EXEC  BANKACC_FUNDMOV_B2C 'OCT 21 2024', 'OCT 22 2024'
  
AS  
--DECLARE @FROMDATE VARCHAR(11) = 'OCT 21 2024',@TODATE VARCHAR(11) = 'MAY 13 2023' -- change date  


 


  
SELECT BANKNAME,SEGMENT,VDT,VNO,VTYP,BOOKTYPE,CLTCODE,ACNAME,NARRATION,VAMT INTO #BANKCODES_DR FROM LEDGER WITH (NOLOCK) , BANKCODES  
WHERE DRCR = 'D' AND CLTCODE =  BANKCODE AND SEGMENT = 'NSE' AND  1 <> 1  
AND VDT BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'   
  
SELECT BANKNAME,SEGMENT,VDT,VNO,VTYP,BOOKTYPE,CLTCODE,ACNAME,NARRATION,VAMT INTO #BANKCODES_CR FROM LEDGER WITH (NOLOCK) , BANKCODES   
WHERE DRCR = 'C' AND CLTCODE =  BANKCODE AND SEGMENT = 'NSE'  AND  1 <> 1  
AND VDT BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'   
  
SELECT CONVERT(VARCHAR(10),D.VDT,105) AS [DATE],D.VNO,D.VTYP,D.CLTCODE,D.ACNAME,C.CLTCODE AS OCLTCODE,C.ACNAME AS OACNAME,D.NARRATION,C.NARRATION AS ONARRATION,SUM(D.VAMT) AS DRAMT,CRAMT = 0,  
CONVERT(VARCHAR(100),'') AS DDNO1,BANKNAME,SEGMENT  
INTO #BANKCODES_FINAL  
FROM #BANKCODES_CR D, LEDGER C WITH (NOLOCK)     
WHERE  D.VTYP = C.VTYP AND D.VNO = C.VNO AND D.BOOKTYPE = C.BOOKTYPE AND C.DRCR = 'D' AND C.CLTCODE IN (SELECT CLTCODE FROM ACMAST WHERE ACCAT  <> 4 )   
GROUP BY CONVERT(VARCHAR(10),D.VDT,105),D.CLTCODE,D.ACNAME,C.CLTCODE,C.ACNAME,D.NARRATION,C.NARRATION,D.VNO,D.VTYP,BANKNAME,SEGMENT  
UNION ALL  
SELECT CONVERT(VARCHAR(10),D.VDT,105) AS [DATE],D.VNO,D.VTYP,D.CLTCODE,D.ACNAME,C.CLTCODE AS OCLTCODE,C.ACNAME AS OACNAME,D.NARRATION,C.NARRATION AS ONARRATION,DRAMT=  0,SUM(C.VAMT) AS CRAMT,  
CONVERT(VARCHAR(100),'') AS DDNO1,BANKNAME,SEGMENT  
FROM #BANKCODES_DR D , LEDGER C WITH (NOLOCK)     
WHERE  D.VTYP = C.VTYP  AND D.VNO = C.VNO AND D.BOOKTYPE = C.BOOKTYPE   AND C.DRCR = 'C' AND C.CLTCODE IN (SELECT CLTCODE FROM ACMAST WHERE ACCAT <> 4 )   
GROUP BY CONVERT(VARCHAR(10),D.VDT,105),D.CLTCODE,D.ACNAME,C.CLTCODE,C.ACNAME,D.NARRATION,C.NARRATION,D.VNO,D.VTYP,BANKNAME,SEGMENT  
  
UPDATE #BANKCODES_FINAL SET DDNO1  = ISNULL(DDNO,'') FROM LEDGER1 WHERE #BANKCODES_FINAL.VNO = LEDGER1.VNO AND #BANKCODES_FINAL.VTYP = LEDGER1.VTYP  
  
SELECT * INTO #BANKCODES_FINAL1 FROM #BANKCODES_FINAL  
  
CREATE TABLE #MULTIDATABASE (SERVERNAME VARCHAR(100),DATABASENAME VARCHAR(100),SEGMENT VARCHAR(100),SRNO INT IDENTITY (1,1))  
  
INSERT INTO #MULTIDATABASE SELECT 'ANAND1','ACCOUNT','NSE'  
INSERT INTO #MULTIDATABASE SELECT 'ANAND1','ACCOUNTSLBS','SLBM'  
INSERT INTO #MULTIDATABASE SELECT 'ANAND','ACCOUNT_AB','BSE'  
INSERT INTO #MULTIDATABASE SELECT 'ANGELFO','ACCOUNTFO','NSEFO'  
INSERT INTO #MULTIDATABASE SELECT 'ANGELFO','ACCOUNTCURFO','NSX'  
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTNCDX','NCDX'  
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTMCDX','MCX'  
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTMCDXCDS','MCD'  
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTBFO','BSEFO'  
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTCURBFO','BSECURFO'  
  
  
DECLARE @SNAME VARCHAR(100),@DNAME VARCHAR(100),@SEGMENT VARCHAR(100),@SRNOMAX INT,@COUNTINT INT  
  
SET @COUNTINT = 1  
  
SELECT @SRNOMAX = MAX(SRNO) FROM #MULTIDATABASE  
  
WHILE @COUNTINT <= @SRNOMAX  
BEGIN  
  
SELECT @SNAME = SERVERNAME , @DNAME = DATABASENAME ,@SEGMENT = SEGMENT FROM #MULTIDATABASE WHERE SRNO = @COUNTINT  
  
IF @SNAME = 'ANAND1'  
BEGIN   
  
TRUNCATE TABLE #BANKCODES_DR  
TRUNCATE TABLE #BANKCODES_CR  
TRUNCATE TABLE #BANKCODES_FINAL  
  
EXEC ('INSERT INTO #BANKCODES_DR  
SELECT BANKNAME,SEGMENT,VDT,VNO,VTYP,BOOKTYPE,CLTCODE,ACNAME,NARRATION,VAMT  FROM ' + @DNAME + '.DBO.LEDGER WITH (NOLOCK) , BANKCODES  
WHERE DRCR = ''D'' AND CLTCODE =  BANKCODE AND SEGMENT = ''' + @SEGMENT + ''' AND VDT BETWEEN ''' + @FROMDATE + ''' AND ''' + @TODATE + ' 23:59:59''')   
  
EXEC ('INSERT INTO #BANKCODES_CR  
SELECT BANKNAME,SEGMENT,VDT,VNO,VTYP,BOOKTYPE,CLTCODE,ACNAME,NARRATION,VAMT  FROM ' + @DNAME + '.DBO.LEDGER WITH (NOLOCK) , BANKCODES  
WHERE DRCR = ''C'' AND CLTCODE =  BANKCODE AND SEGMENT = ''' + @SEGMENT + ''' AND VDT BETWEEN ''' + @FROMDATE + ''' AND ''' + @TODATE + ' 23:59:59''')   
  
EXEC ('INSERT INTO #BANKCODES_FINAL  
SELECT CONVERT(VARCHAR(10),D.VDT,105) AS [DATE],D.VNO,D.VTYP,D.CLTCODE,D.ACNAME,C.CLTCODE AS OCLTCODE,C.ACNAME AS OACNAME,D.NARRATION,C.NARRATION AS ONARRATION,SUM(D.VAMT) AS DRAMT,CRAMT = 0,  
CONVERT(VARCHAR(100),'''') AS DDNO1,BANKNAME,SEGMENT  
FROM #BANKCODES_CR D, ' + @DNAME + '.DBO.LEDGER C WITH (NOLOCK)     
WHERE  D.VTYP = C.VTYP AND D.VNO = C.VNO AND D.BOOKTYPE = C.BOOKTYPE AND C.DRCR = ''D''   
AND C.CLTCODE IN (SELECT CLTCODE FROM  ' + @DNAME + '.DBO.ACMAST WHERE ACCAT  <> 4 )   
GROUP BY CONVERT(VARCHAR(10),D.VDT,105),D.CLTCODE,D.ACNAME,C.CLTCODE,C.ACNAME,D.NARRATION,C.NARRATION,D.VNO,D.VTYP,BANKNAME,SEGMENT  
UNION ALL  
SELECT CONVERT(VARCHAR(10),D.VDT,105) AS [DATE],D.VNO,D.VTYP,D.CLTCODE,D.ACNAME,C.CLTCODE AS OCLTCODE,C.ACNAME AS OACNAME,D.NARRATION,C.NARRATION AS ONARRATION,DRAMT=  0,SUM(C.VAMT) AS CRAMT,  
CONVERT(VARCHAR(100),'''') AS DDNO1,BANKNAME,SEGMENT  
FROM #BANKCODES_DR D , ' + @DNAME + '.DBO.LEDGER C WITH (NOLOCK)     
WHERE  D.VTYP = C.VTYP  AND D.VNO = C.VNO AND D.BOOKTYPE = C.BOOKTYPE   AND C.DRCR = ''C''   
AND C.CLTCODE IN (SELECT CLTCODE FROM  ' + @DNAME + '.DBO.ACMAST WHERE ACCAT  <> 4 )   
GROUP BY CONVERT(VARCHAR(10),D.VDT,105),D.CLTCODE,D.ACNAME,C.CLTCODE,C.ACNAME,D.NARRATION,C.NARRATION,D.VNO,D.VTYP,BANKNAME,SEGMENT')  
  
  
EXEC ('UPDATE #BANKCODES_FINAL SET DDNO1  = ISNULL(DDNO,'''') FROM ' + @DNAME + '.DBO.LEDGER1 L WHERE #BANKCODES_FINAL.VNO = L.VNO AND #BANKCODES_FINAL.VTYP = L.VTYP')   
  
INSERT INTO #BANKCODES_FINAL1 SELECT * FROM #BANKCODES_FINAL  
  
END  
ELSE  
BEGIN  
TRUNCATE TABLE #BANKCODES_DR  
TRUNCATE TABLE #BANKCODES_CR  
TRUNCATE TABLE #BANKCODES_FINAL  
  
EXEC ('INSERT INTO #BANKCODES_DR  
SELECT BANKNAME,SEGMENT,VDT,VNO,VTYP,BOOKTYPE,CLTCODE,ACNAME,NARRATION,VAMT  FROM ' + @SNAME + '.' + @DNAME + '.DBO.LEDGER WITH (NOLOCK) , BANKCODES  
WHERE DRCR = ''D'' AND CLTCODE =  BANKCODE AND SEGMENT = ''' + @SEGMENT + ''' AND VDT BETWEEN ''' + @FROMDATE + ''' AND ''' + @TODATE + ' 23:59:59''')   
  
EXEC ('INSERT INTO #BANKCODES_CR  
SELECT BANKNAME,SEGMENT,VDT,VNO,VTYP,BOOKTYPE,CLTCODE,ACNAME,NARRATION,VAMT  FROM ' + @SNAME + '.' + @DNAME + '.DBO.LEDGER WITH (NOLOCK) , BANKCODES  
WHERE DRCR = ''C'' AND CLTCODE =  BANKCODE AND SEGMENT = ''' + @SEGMENT + ''' AND VDT BETWEEN ''' + @FROMDATE + ''' AND ''' + @TODATE + ' 23:59:59''')   
  
EXEC ('INSERT INTO #BANKCODES_FINAL  
SELECT CONVERT(VARCHAR(10),D.VDT,105) AS [DATE],D.VNO,D.VTYP,D.CLTCODE,D.ACNAME,C.CLTCODE AS OCLTCODE,C.ACNAME AS OACNAME,D.NARRATION,C.NARRATION AS ONARRATION,SUM(D.VAMT) AS DRAMT,CRAMT = 0,  
CONVERT(VARCHAR(100),'''') AS DDNO1,BANKNAME,SEGMENT  
FROM #BANKCODES_CR D, ' + @SNAME + '.' + @DNAME + '.DBO.LEDGER C WITH (NOLOCK)     
WHERE  D.VTYP = C.VTYP AND D.VNO = C.VNO AND D.BOOKTYPE = C.BOOKTYPE AND C.DRCR = ''D''   
AND C.CLTCODE IN (SELECT CLTCODE FROM  ' + @SNAME + '.' + @DNAME + '.DBO.ACMAST WHERE ACCAT  <> 4 )   
GROUP BY CONVERT(VARCHAR(10),D.VDT,105),D.CLTCODE,D.ACNAME,C.CLTCODE,C.ACNAME,D.NARRATION,C.NARRATION,D.VNO,D.VTYP,BANKNAME,SEGMENT  
UNION ALL  
SELECT CONVERT(VARCHAR(10),D.VDT,105) AS [DATE],D.VNO,D.VTYP,D.CLTCODE,D.ACNAME,C.CLTCODE AS OCLTCODE,C.ACNAME AS OACNAME,D.NARRATION,C.NARRATION AS ONARRATION,DRAMT=  0,SUM(C.VAMT) AS CRAMT,  
CONVERT(VARCHAR(100),'''') AS DDNO1,BANKNAME,SEGMENT  
FROM #BANKCODES_DR D , ' + @SNAME + '.' + @DNAME + '.DBO.LEDGER C WITH (NOLOCK)     
WHERE  D.VTYP = C.VTYP  AND D.VNO = C.VNO AND D.BOOKTYPE = C.BOOKTYPE   AND C.DRCR = ''C''   
AND C.CLTCODE IN (SELECT CLTCODE FROM  ' + @SNAME + '.' + @DNAME + '.DBO.ACMAST WHERE ACCAT  <> 4 )   
GROUP BY CONVERT(VARCHAR(10),D.VDT,105),D.CLTCODE,D.ACNAME,C.CLTCODE,C.ACNAME,D.NARRATION,C.NARRATION,D.VNO,D.VTYP,BANKNAME,SEGMENT')  
  
  
EXEC ('UPDATE #BANKCODES_FINAL SET DDNO1  = ISNULL(DDNO,'''') FROM ' + @SNAME + '.' + @DNAME + '.DBO.LEDGER1 L WHERE #BANKCODES_FINAL.VNO = L.VNO AND #BANKCODES_FINAL.VTYP = L.VTYP')   
  
INSERT INTO #BANKCODES_FINAL1 SELECT * FROM #BANKCODES_FINAL  
  
END  
  
SET @COUNTINT = @COUNTINT + 1  
  
END  
  
INSERT INTO #BANKCODES_FINAL1  
SELECT CONVERT(VARCHAR(10),B.VDT,105) AS VDT,VNO,VTYPE AS VTYP,CLTCODE,'' AS ACNAME,'' AS OCLTCODE,'' AS OACNAME,NARRATION,'' AS ONARRATION,DRAMOUNT AS DRAMT,  
CRAMOUNT AS CRAMT,DDNO1 ='',BANKNAME,A.SEGMENT   
FROM ANGELFO.BBO_FA.DBO.MFSS_LEDGER_BSE  B, BANKCODES A WHERE VDT BETWEEN @FROMDATE AND @TODATE  + ' 23:59:59'  
AND B.CLTCODE = A.BANKCODE AND (NARRATION LIKE '%C TO B%' OR NARRATION LIKE '%B TO C%' OR NARRATION LIKE '%B TO S%' OR NARRATION LIKE '%S TO B%') AND A.SEGMENT = 'BSEMFSS'  
  
UPDATE A  SET OCLTCODE = B.CLTCODE , ONARRATION = B.NARRATION  
FROM ANGELFO.BBO_FA.DBO.MFSS_LEDGER_BSE  B,#BANKCODES_FINAL1 A WHERE  A.SEGMENT = 'BSEMFSS' AND A.VNO = B.VNO  
AND A.VTYP = B.VTYPE AND A.CRAMT <> 0 AND B.DRAMOUNT <> 0  
  
UPDATE A  SET OCLTCODE = B.CLTCODE , ONARRATION = B.NARRATION  
FROM ANGELFO.BBO_FA.DBO.MFSS_LEDGER_BSE  B,#BANKCODES_FINAL1 A WHERE  A.SEGMENT = 'BSEMFSS' AND A.VNO = B.VNO  
AND A.VTYP = B.VTYPE AND A.DRAMT <> 0 AND B.CRAMOUNT <> 0  
  
UPDATE #BANKCODES_FINAL1 SET ACNAME = LONGNAME  FROM ANGELFO.BBO_FA.DBO.ACMAST A WHERE #BANKCODES_FINAL1.CLTCODE = A.CLTCODE  
AND #BANKCODES_FINAL1.SEGMENT = 'BSEMFSS'   
  
UPDATE #BANKCODES_FINAL1 SET OACNAME = LONGNAME  FROM ANGELFO.BBO_FA.DBO.ACMAST A WHERE #BANKCODES_FINAL1.CLTCODE = A.CLTCODE  
AND #BANKCODES_FINAL1.SEGMENT = 'BSEMFSS'  
  
SELECT * INTO #BANKCODES_FINAL2 FROM #BANKCODES_FINAL1  WHERE (ONARRATION LIKE '%C TO B%' OR ONARRATION LIKE '%B TO C%' OR NARRATION LIKE '%B TO S%' OR NARRATION LIKE '%S TO B%')  
ORDER BY CONVERT(DATETIME,[DATE],105),(CRAMT+DRAMT),CRAMT,DRAMT  
  
ALTER TABLE #BANKCODES_FINAL2 ADD NEW_NARRATION VARCHAR(100),TYPE_FROM_TO VARCHAR(100)  
  
UPDATE #BANKCODES_FINAL2 SET NEW_NARRATION = REPLACE(ONARRATION,' BY RTGS','')  
UPDATE #BANKCODES_FINAL2 SET NEW_NARRATION = REPLACE(NEW_NARRATION,'AMT TRF TO ','')  
UPDATE #BANKCODES_FINAL2 SET NEW_NARRATION = REPLACE(NEW_NARRATION,'AMT TRF FROM ','')  
UPDATE #BANKCODES_FINAL2 SET NEW_NARRATION = LTRIM(RTRIM(NEW_NARRATION))  
UPDATE #BANKCODES_FINAL2 SET TYPE_FROM_TO = RIGHT(NEW_NARRATION,6)  
  
SELECT DISTINCT DATE,VNO,VTYP,NEW_NARRATION,TYPE_FROM_TO,SEGMENT INTO #BANKCODES_FINAL3 FROM #BANKCODES_FINAL2  
  
ALTER TABLE #BANKCODES_FINAL3 ADD AMOUNT NUMERIC(18,2),DRCODE VARCHAR(10),CRCODE VARCHAR(10),DRCODENAME VARCHAR(150),CRCODENAME VARCHAR(150)  
  
UPDATE #BANKCODES_FINAL3 SET AMOUNT = DRAMT , DRCODE = CLTCODE,CRCODE = OCLTCODE,  
DRCODENAME = ACNAME,CRCODENAME = OACNAME  
FROM #BANKCODES_FINAL2 WHERE #BANKCODES_FINAL2.VTYP IN (5,3,2) AND   
#BANKCODES_FINAL2.VNO = #BANKCODES_FINAL3.VNO AND #BANKCODES_FINAL2.VTYP = #BANKCODES_FINAL3.VTYP  
AND #BANKCODES_FINAL2.SEGMENT = #BANKCODES_FINAL3.SEGMENT  
AND DRAMT <> 0  
  
UPDATE #BANKCODES_FINAL3 SET AMOUNT = CRAMT , CRCODE = CLTCODE,DRCODE = OCLTCODE,   
CRCODENAME = ACNAME,DRCODENAME = OACNAME  
FROM #BANKCODES_FINAL2 WHERE #BANKCODES_FINAL2.VTYP IN (5,2,3)  AND   
#BANKCODES_FINAL2.VNO = #BANKCODES_FINAL3.VNO AND #BANKCODES_FINAL2.VTYP = #BANKCODES_FINAL3.VTYP   
AND #BANKCODES_FINAL2.SEGMENT = #BANKCODES_FINAL3.SEGMENT  
AND CRAMT <> 0  
  
SELECT * FROM #BANKCODES_FINAL3  
  

DROP TABLE #BANKCODES_DR  
DROP TABLE #BANKCODES_CR  
DROP TABLE #BANKCODES_FINAL  
DROP TABLE #BANKCODES_FINAL1  
DROP TABLE #MULTIDATABASE  
DROP TABLE #BANKCODES_FINAL2  
DROP TABLE #BANKCODES_FINAL3

GO
