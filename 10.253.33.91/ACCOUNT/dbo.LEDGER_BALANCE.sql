-- Object: PROCEDURE dbo.LEDGER_BALANCE
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROC [dbo].[LEDGER_BALANCE]
AS

SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE INTO #MFSS  
FROM ANGELFO.BBO_FA.DBO.MFSS_LEDGER_BSE WHERE VDT >='2016-04-01' 
GROUP BY CLTCODE 
HAVING SUM(DRAMOUNT-CRAMOUNT) <>0

SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,BALANCE,EXCHANGE INTO #TEMP FROM(
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'NSE' AS EXCHANGE 
FROM LEDGER WHERE LTRIM(RTRIM(cltcode)) IN (Select LTRIM(RTRIM(cltcode)) FROM #MFSS)
AND VDT >='2016-04-01'
GROUP BY CLTCODE
UNION ALL
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'BSE' AS EXCHANGE 
FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER WHERE LTRIM(RTRIM(cltcode)) IN (Select LTRIM(RTRIM(cltcode)) FROM #MFSS)
AND VDT >='2016-04-01'
GROUP BY CLTCODE
UNION ALL
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'NSEFO' AS EXCHANGE 
FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WHERE LTRIM(RTRIM(cltcode)) IN (Select LTRIM(RTRIM(cltcode)) FROM #MFSS)
AND VDT >='2016-04-01'
GROUP BY CLTCODE
UNION ALL
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'MCX' AS EXCHANGE 
FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER WHERE LTRIM(RTRIM(cltcode)) IN (Select LTRIM(RTRIM(cltcode)) FROM #MFSS)
AND VDT >='2016-04-01'
GROUP BY CLTCODE
UNION ALL
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'NCDX' AS EXCHANGE 
FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER WHERE CLTCODE IN (Select LTRIM(RTRIM(cltcode)) FROM #MFSS)
AND VDT >='2016-04-01'
GROUP BY CLTCODE
UNION ALL
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'NSECURFO' AS EXCHANGE 
FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER WHERE LTRIM(RTRIM(cltcode)) IN (Select LTRIM(RTRIM(cltcode)) FROM #MFSS)
AND VDT >='2016-04-01'
GROUP BY CLTCODE
UNION ALL
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'BSEFO' AS EXCHANGE 
FROM ANGELCOMMODITY.ACCOUNTBFO.DBO.LEDGER WHERE LTRIM(RTRIM(cltcode)) IN (Select LTRIM(RTRIM(cltcode)) FROM #MFSS)
AND VDT >='2016-04-01'
GROUP BY CLTCODE
UNION ALL
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'MCXURFO' AS EXCHANGE 
FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER WHERE LTRIM(RTRIM(cltcode)) IN (Select LTRIM(RTRIM(cltcode)) FROM #MFSS)
AND VDT >='2016-04-01'
GROUP BY CLTCODE
UNION ALL 
SELECT LTRIM(RTRIM(cltcode)) AS CLTCODE ,SUM(CRAMOUNT-DRAMOUNT) BALANCE,'BSE_MFSS' AS EXCHANGE 
FROM ANGELFO.BBO_FA.DBO.MFSS_LEDGER_BSE WHERE VDT >='2016-04-01' AND LTRIM(RTRIM(cltcode)) IN (Select  LTRIM(RTRIM(cltcode)) FROM #MFSS)
GROUP BY CLTCODE 
)A 
WHERE LTRIM(RTRIM(cltcode)) >='A001' AND LTRIM(RTRIM(cltcode)) <='ZZZ'  


select LTRIM(RTRIM(cltcode)) AS CLTCODE,ISNULL(BSE_MFSS,0)BSE_MFSS,ISNULL(NSE,0)NSE,ISNULL(BSE,0)BSE,ISNULL(NSEFO,0)NSEFO,ISNULL(MCXURFO,0)MCXURFO,ISNULL(NSECURFO,0)NSECURFO,
ISNULL(MCX,0)MCX,ISNULL(NCDX,0)NCDX,ISNULL(BSEFO,0)BSEFO
from 
(
  select LTRIM(RTRIM(cltcode)) AS CLTCODE, BALANCE, EXCHANGE
  from #TEMP WHERE LTRIM(RTRIM(cltcode))  >='A001' AND LTRIM(RTRIM(cltcode))  <='ZZZ'  
) src
pivot
(
  sum(BALANCE)
  for EXCHANGE in (NCDX,NSE,BSE_MFSS,MCXURFO,NSECURFO,MCX,BSE,NSEFO,BSEFO)
) piv;

GO
