CREATE PROCEDURE [dbo].[CLS_RECON_TECHPROCESS_ICICI_PROCESS]
(
@UPLOADID NVARCHAR(50),
@BANKACCOUNT varchar(50), 
@BANKCODE NVARCHAR(50) ,
@UPLOADFILENAME varchar(500),
@UNAME varchar(50)
) 
AS
BEGIN
	BEGIN TRY
		DECLARE @VALUEDATE VARCHAR(10)

		IF OBJECT_ID('tempdb..#TEMP_TECHPROCESS') IS NOT NULL
		DROP TABLE #TEMP_TECHPROCESS

		SELECT * INTO #TEMP_TECHPROCESS FROM MSAJAG..FILEDATA (NOLOCK)
		WHERE PROGID=@UPLOADID

		CREATE NONCLUSTERED INDEX IXDPROGID ON #TEMP_TECHPROCESS(PROGID)

		SELECT TOP 1 @VALUEDATE=.DBO.PIECE(.DBO.PIECE(FILE_DATA, ':', 2),',',1) FROM #TEMP_TECHPROCESS 
		WHERE PROGID=@UPLOADID
		ORDER BY SRNO ASC


        INSERT INTO #NONCMS_DATA 
		SELECT
		  UPLOADID= @UPLOADID		
		, BANKACCOUNT = @BANKACCOUNT
		, BANKCODE = @BANKCODE
		, DESCRIPTION = REPLACE(REPLACE(.DBO.PIECE(FILE_DATA, ',', 14) ,'{itc:abc_123}{custid:','') ,'}','')
		, LEDGERBALANCE = ''
		, DRCR = 'C'
		, AMOUNT = CAST(.DBO.PIECE(FILE_DATA, ',', 7) AS DECIMAL(20,2)) 
		, VALUEDATE = CONVERT(DATETIME,RTRIM(LTRIM(@VALUEDATE)),126)  
		, REFERENCENO = .DBO.PIECE(FILE_DATA, ',', 4)		
		, CLTACCOUNT = ''
		, CUSTOMERREFERENCE = ''  
		, TRANSACTIONDETAIL = ''  
		, VNO =''
		, VTYP =''
		, BOOKTYPE= ''
		, MATCHEDFLAG= 0
		, UPLOADSTATUS = 'PENDING'
		, FILEUPLOAD_SDATE= GETDATE()
		, GETDATE()
		, UPLOADBY = @UNAME	
		--, CLOSINGBALANCE = 0	
		FROM #TEMP_TECHPROCESS
		WHERE ISNULL(.DBO.PIECE(FILE_DATA, ',', 8), '') <>''
		AND ISNULL(.DBO.PIECE(FILE_DATA, ',', 8), '') <>'Charges'	
			
		DROP TABLE #TEMP_TECHPROCESS
		DELETE MSAJAG..FILEDATA WHERE PROGID=@UPLOADID

	END TRY

	BEGIN CATCH

	DELETE FROM MSAJAG..FILEDATA  WHERE PROGID=@UPLOADID
	DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
	DELETE  FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
	SELECT MSG='WRONG FILE UPLOADED : '+ERROR_MESSAGE()
	RETURN
	END CATCH
END

GO
