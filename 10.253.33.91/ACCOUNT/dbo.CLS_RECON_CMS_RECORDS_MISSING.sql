CREATE PROCEDURE [dbo].[CLS_RECON_CMS_RECORDS_MISSING]
(		
@VALUEDATE VARCHAR(11),
@BANKCODE NVARCHAR(15)
)
AS


--EXEC CLS_RECON_CMS_RECORDS_MISSING @VALUEDATE='03/04/2018',@BANKCODE='02015'

		DECLARE @@SQL NVARCHAR(max),@@ACCOUNTSERVER  NVARCHAR(100),@@ACCOUNTDB  NVARCHAR(100),@@EXCHANGE NVARCHAR(100),@@SEGMENT NVARCHAR(100)
		
		
		CREATE TABLE #PENDING_DATA
		(
		SRNO INT IDENTITY(1,1)
		,UPLOADID NVARCHAR(50)
		,BANKCODE  NVARCHAR(50)
		,VALUEDATE DATETIME
		,DRCR  NVARCHAR(50)
		,CHECK_NUMBER  NVARCHAR(50)
		,AMOUNT DECIMAL(18,2)
		,CLTCODE  NVARCHAR(50)
		,CLTACCNO  NVARCHAR(50)
		,MATCHEDFLAG  NVARCHAR(50)
		,CROSS_NO  NVARCHAR(50)
		,BSEXCHANGE  NVARCHAR(75)
		,BSSEGMENT  NVARCHAR(75)
		)
		
		
		CREATE TABLE #LEDGER_TO_MAP(VNO VARCHAR(12),
		VTYP INT,
		BOOKTYPE VARCHAR(3),
		CLTCODE VARCHAR(10),
		DDNO VARCHAR(30),
		RELAMT MONEY,
		DRCR CHAR(1),
		EDT DATETIME,
		VDT DATETIME,
		NARRATION NVARCHAR(255),
		BBEXCHANGE  NVARCHAR(75),
		BBSEGMENT  NVARCHAR(75))
		
		DECLARE  @ALLSEGMENT TABLE(ACCOUNTDB NVARCHAR(75), ACCOUNTSERVER  NVARCHAR(75),EXCHANGE  NVARCHAR(75),SEGMENT  NVARCHAR(75))
		
			INSERT INTO @ALLSEGMENT(ACCOUNTDB,ACCOUNTSERVER,EXCHANGE,SEGMENT)
			SELECT	ACCOUNTDB,ACCOUNTSERVER,EXCHANGE,SEGMENT
			FROM PRADNYA.DBO.MULTICOMPANY
			WHERE PRIMARYSERVER = 1
			AND AccountDb NOT IN ('ACCOUNTbFO','ACCOUNTSLBS','ACCOUNTCURBFO','BBO_FA')
			--UNION ALL
			--SELECT	 ACCOUNTDB	, ACCOUNTSERVER,EXCHANGE,SEGMENT
			--FROM PRADNYA.DBO.MULTICOMPANY_ALL			
			--WHERE PRIMARYSERVER = 1
			--AND AccountDb NOT IN ('ACCOUNTbFO','ACCOUNTSLBS','ACCOUNTCURBFO')
			
			WHILE (SELECT		COUNT(*)	FROM @ALLSEGMENT)> 0 
				BEGIN				
					SELECT	TOP 1 @@ACCOUNTDB = ACCOUNTDB ,@@ACCOUNTSERVER = ACCOUNTSERVER, @@SEGMENT=SEGMENT ,@@EXCHANGE=EXCHANGE
					FROM  @ALLSEGMENT
															
					SET @@SQL="INSERT INTO #PENDING_DATA( UPLOADID,BANKCODE,VALUEDATE,DRCR,CHECK_NUMBER,AMOUNT,CLTCODE,CLTACCNO,MATCHEDFLAG,CROSS_NO,BSEXCHANGE,BSSEGMENT) 
					SELECT UPLOADID,BANKCODE,VALUEDATE,DRCR,INST_NO_CHECK,INST_AMT,CLTCODE,CLTACCNO,MATCHEDFLAG,CROSS_NO ,EXCHANGE='"+@@EXCHANGE+"',SEGMENT='"+@@SEGMENT+"'	FROM "
					+@@ACCOUNTSERVER + "." + @@ACCOUNTDB+".[DBO].RECON_CMS_DATA
					WHERE VALUEDATE = CONVERT (DATETIME,'"+@VALUEDATE+ "',103) AND MATCHEDFLAG = 0 AND ROWTYPE<>'DR' AND BANKCODE='"+@BANKCODE+"'"
					PRINT @@SQL
					EXEC(@@SQL)		
					
						SET @@SQL=""
						SET @@SQL="INSERT INTO #LEDGER_TO_MAP(VNO,VTYP,BOOKTYPE,CLTCODE,DDNO,RELAMT,DRCR,EDT,VDT,NARRATION,BBEXCHANGE,BBSEGMENT)
						SELECT	L.VNO,L.VTYP,L.BOOKTYPE,CLTCODE,DDNO,RELAMT,L1.DRCR,L.Edt,L.Vdt,L.NARRATION	,EXCHANGE='"+@@EXCHANGE+"',SEGMENT='"+@@SEGMENT+"'			
						FROM	"+@@ACCOUNTSERVER + "." + @@ACCOUNTDB+".[DBO].LEDGER L ,"+@@ACCOUNTSERVER + "." + @@ACCOUNTDB+".[DBO].LEDGER1 L1
						WHERE L.VNO = L1.VNO
						AND L.VTYP = L1.VTYP
						AND L.LNO = L1.LNO
						AND L.BOOKTYPE = L1.BOOKTYPE
						AND L1.RELDT = ''
						AND EXISTS (SELECT
						VNO
						FROM "+@@ACCOUNTSERVER + "." + @@ACCOUNTDB+".[DBO].LEDGER L11 (NOLOCK)
						WHERE L11.VNO = L1.VNO
						AND L11.VTYP = L1.VTYP
						AND L11.BOOKTYPE = L1.BOOKTYPE
						AND L11.CLTCODE = '" +@BANKCODE +"'	AND L11.VTYP IN (2, 3, 17)
						AND L11.Vdt <= CONVERT (DATETIME,'"+@VALUEDATE
						
						SET @@SQL=@@SQL+ "',103))"
						PRINT @@SQL
						EXEC(@@SQL)
						DELETE FROM @ALLSEGMENT WHERE ACCOUNTDB=@@ACCOUNTDB
				END	
				
			
			--select * from #PENDING_DATA where AMOUNT=20000.00 AND CHECK_NUMBER='000011'
			--select * from #LEDGER_TO_MAP where RELAMT=20000.00 and vno='201800163059'
			--RETURN
				SELECT VNO,VTYP,BOOKTYPE,AMOUNT,BBEXCHANGE,BBSEGMENT ,BSEXCHANGE,BSSEGMENT,BANKCODE, L.CLTCODE, L.DDNO,UPLOADID
				FROM #PENDING_DATA C
				,#LEDGER_TO_MAP L
				,CLS_MULTIBANKID_RECO_VW M
					WHERE SUBSTRING(L.DDNO, PATINDEX('%[^0]%', L.DDNO), 10) = SUBSTRING(C.CHECK_NUMBER, PATINDEX('%[^0]%', C.CHECK_NUMBER), 10)
					AND L.DDNO<>''
					AND C.DRCR = L.DRCR
					AND C.AMOUNT = L.RELAMT
					AND RIGHT(M.ACCNO, 5) = RIGHT(C.CLTACCNO, 5)
					AND M.CLTCODE = L.CLTCODE
					AND CONVERT (DATETIME,CONVERT (VARCHAR(11) ,L.VDT,109))<=C.VALUEDATE
					AND BBEXCHANGE+"_"+BBSEGMENT<>BSEXCHANGE+"_"+BSSEGMENT
					
			
			
				DROP TABLE #PENDING_DATA
				DROP TABLE #LEDGER_TO_MAP

GO
