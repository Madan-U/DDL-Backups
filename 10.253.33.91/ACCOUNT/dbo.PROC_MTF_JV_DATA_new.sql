-- Object: PROCEDURE dbo.PROC_MTF_JV_DATA_new
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------



CREATE proc [dbo].[PROC_MTF_JV_DATA_new]  
(  
@SAUDA_DATE VARCHAR(11),
@FLAG		VARCHAR(20) = ''
)  

AS  
  
SELECT * INTO #C_JVDATA  
FROM MTFTRADE.DBO.MTF_JVDATA  
WHERE SAUDA_DATE = @SAUDA_DATE  
AND FLAG = 'CASH'    

  
 ALTER TABLE #C_JVDATA  
 ADD SNO INT IDENTITY(1,1)  
   
 DECLARE @VNO  VARCHAR(12),  
   @RECCNT  INT,  
   @SDTCUR  VARCHAR(11),  
   @LDTCUR  VARCHAR(11),
   @NARRATION	VARCHAR(100)
  
 SELECT @SDTCUR = SDTCUR, @LDTCUR = LDTCUR  
 FROM PARAMETER WHERE @SAUDA_DATE BETWEEN @SDTCUR AND LDTCUR  
  
 SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #C_JVDATA  

 IF @FLAG = 'PAYIN-REV'
 BEGIN
	SET @NARRATION = 'MTF JV FROM PAYIN EXCESS REV'

	DELETE FROM LEDGER WHERE VDT = @SAUDA_DATE AND NARRATION = 'MTF JV FROM PAYIN EXCESS REV' AND CHECKEDBY = 'MTF PROCESS'
 END

 IF @FLAG = 'MTM-TDAY-REV'
 BEGIN
	SET @NARRATION = 'MTF JV FROM MTM EXCESS REV'

	DELETE FROM LEDGER WHERE VDT = @SAUDA_DATE AND NARRATION = 'MTF JV FROM MTM EXCESS REV' AND CHECKEDBY = 'MTF PROCESS'
 END

 IF @FLAG = 'CASH-TDAY-REV'
 BEGIN
	SET @NARRATION = 'MTF JV FROM CASH COLL EXCESS REV'

	DELETE FROM LEDGER WHERE VDT = @SAUDA_DATE AND NARRATION = 'MTF JV FROM CASH COLL EXCESS REV' AND CHECKEDBY = 'MTF PROCESS'
 END
   
 IF @FLAG = 'CASH-T1DAY-REV'
 BEGIN
	SET @NARRATION = 'MTF EXCESS CASH COLL REV JV FOR T+1 DAY'

	DELETE FROM LEDGER WHERE VDT = @SAUDA_DATE AND NARRATION = 'MTF EXCESS CASH COLL REV JV FOR T+1 DAY' AND CHECKEDBY = 'MTF PROCESS'
 END

 IF @RECCNT > 0   
 BEGIN  
  CREATE TABLE #C_VNO  
  (  
   VNO VARCHAR(12)  
  )  
  
  INSERT INTO #C_VNO   
  EXEC ACC_GENVNO_new @SAUDA_DATE, 6, '01', @SDTCUR, @LDTCUR, @RECCNT  
  
  SELECT @VNO = VNO FROM #C_VNO  
  
  UPDATE #C_JVDATA SET VNO = CONVERT(VARCHAR,CONVERT(BIGINT,@VNO) + SNO - 1)  
   
  UPDATE #C_JVDATA SET ACNAME = A.LONGNAME   
  FROM ACMAST A  
  WHERE #C_JVDATA.PARTY_CODE = A.CLTCODE  
  
  DECLARE @EDT	VARCHAR(11)


  SELECT @EDT = LEFT(FUNDS_PAYIN,11) FROM MSAJAG.DBO.SETT_MST SM(NOLOCK)
  WHERE	START_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59' AND SETT_TYPE = 'M'

  INSERT INTO LEDGER  
  SELECT '6', VNO, (CASE WHEN @FLAG = 'CASH-T1DAY-REV' THEN @EDT ELSE @SAUDA_DATE END), LNO=1, ACNAME=ISNULL(ACNAME,''), DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END) , AMOUNT, 
  @SAUDA_DATE, '', '', 0, 0, GETDATE(),   
  PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = @NARRATION
  FROM #C_JVDATA  
  
  INSERT INTO LEDGER  
  SELECT '6', VNO, (CASE WHEN @FLAG = 'CASH-T1DAY-REV' THEN @EDT ELSE @SAUDA_DATE END), LNO=2, ACNAME=ISNULL(A.ACNAME,''), DRCR, AMOUNT, 
  @SAUDA_DATE, '', '', 0, 0, GETDATE(),   
  GLCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = @NARRATION
  FROM #C_JVDATA J, ACMAST A  
  WHERE J.GLCODE = A.CLTCODE 
  
 END

GO
