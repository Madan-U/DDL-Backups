					SELECT @@SQL = @@SQL + "GROUP BY VTYP, BOOKTYPE"
					IF @@VNOFLAG = 1
						BEGIN
							SELECT @@SQL = @@SQL + ", CONVERT(VARCHAR(11), VDT, 109) "
						END
					ELSE IF @@VNOFLAG = 2
