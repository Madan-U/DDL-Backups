			DECLARE @@PARA AS CURSOR, @@SDTCUR AS VARCHAR(11), @@LDTCUR AS VARCHAR(11), @@VNOFLAG AS SMALLINT
			CREATE TABLE #LDVNO
			(
				VTYP SMALLINT,
				BOOKTYPE VARCHAR(2),
				VNO VARCHAR(12)
			)
		
			CREATE TABLE #LTVNO
			(
				VTYP SMALLINT,
				BOOKTYPE VARCHAR(2),
				VNO VARCHAR(12)
