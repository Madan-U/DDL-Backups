			CREATE TABLE #MISSVNOS
			(
				VTYP SMALLINT,
				BOOKTYPE VARCHAR(2),
				LDVNO VARCHAR(12),
				LTVNO VARCHAR(12),
				FINYEAR VARCHAR(30)
			)
		
