SET @CASH = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP " SELECT FILEDATA FROM ACCOUNT.DBO.PAYOUTCHECK_ANG_BEFORE " QUERYOUT ' +@CASHFILENAME+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'    
EXEC(@CASH)    
    
  
    
