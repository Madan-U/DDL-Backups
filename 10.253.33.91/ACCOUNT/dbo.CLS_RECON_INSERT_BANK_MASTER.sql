-- Object: PROCEDURE dbo.CLS_RECON_INSERT_BANK_MASTER
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------



CREATE PROCEDURE [dbo].[CLS_RECON_INSERT_BANK_MASTER] (
--DECLARE
@SEGMENT NVARCHAR(100)
,@BANKNAME NVARCHAR(100) 
,@BANKCODE NVARCHAR(30) 
,@BANKACCOUNT NVARCHAR(30) 
,@CLIENT NVARCHAR(100) 
,@FILETYPE NVARCHAR(25) 
,@ACTION VARCHAR(10)
,@BANKMASTERID nvarchar(20)
,@FILEFORMATE NVARCHAR(10)
,@ENCRYPTIONCODE NVARCHAR(50)
) 

AS
BEGIN

DECLARE @BMASTERID INT
 IF(@BANKMASTERID<>'' AND @BANKMASTERID IS NOT NULL)
SET @BMASTERID = CAST(@BANKMASTERID AS INT)

  IF @ACTION = 'ADD'
      BEGIN
			IF EXISTS (SELECT TOP 1	* FROM RECON_BANK_MASTER_VW(NOLOCK)	WHERE SEGMENT = @SEGMENT
						AND BANKCODE = @BANKCODE
						AND BANKACCOUNT = @BANKACCOUNT
						AND FILETYPE = @FILETYPE) 
	
						BEGIN
							SELECT	'ALREADY EXIST YOU HAVE INSERTED FOR ' + @SEGMENT + '-' + @BANKNAME + '-' + @BANKCODE + '-' + @BANKACCOUNT + '-' + @FILETYPE AS 'RESULT'
						END
			ELSE
			BEGIN
							INSERT INTO ACCOUNT..RECON_BANK_MASTER ([SEGMENT]
							, [BANKNAME]
							, [BANKCODE]
							, [BANKACCOUNT]
							, [CLIENT]
							, [FILETYPE]
							, [INSERTEDDATETIME]
							, [LASTUPDATE]
							, [FILEFORMATE]
							, [ENCRYPTIONCODE])
							VALUES (@SEGMENT, @BANKNAME, @BANKCODE, @BANKACCOUNT, @CLIENT, @FILETYPE, GETDATE(), GETDATE(),@FILEFORMATE,@ENCRYPTIONCODE)
							
							SELECT 'SUCCESSFULLY YOU HAVE INSERTED FOR ' + @SEGMENT + '-' + @BANKNAME + '-' + @BANKCODE + '-' + @BANKACCOUNT + '-' + @FILETYPE AS 'RESULT'
			 END
		END

IF @ACTION = 'FETCH' 
	BEGIN
		SELECT
			BANKMASTERID
			,SEGMENT
			,BANKNAME
			,BANKCODE
			,BANKACCOUNT
			,CLIENT
			,FILETYPE
			,INSERTEDDATETIME
			,LASTUPDATE
			,FILEFORMATE
			,ENCRYPTIONCODE
		FROM RECON_BANK_MASTER_VW(NOLOCK)
		WHERE SEGMENT = @SEGMENT 
		AND (BANKCODE LIKE '%' + @BANKCODE + '%'  OR @BANKCODE='')
		AND (BANKNAME = @BANKNAME OR @BANKNAME='ALL')
		AND (FILETYPE = @FILETYPE OR @FILETYPE='ALL')
	END

	IF @ACTION = 'EDIT'
	BEGIN
	UPDATE RECON_BANK_MASTER
	SET SEGMENT=@SEGMENT
		,BANKNAME=@BANKNAME
		,BANKCODE=@BANKCODE
		,BANKACCOUNT=@BANKACCOUNT
		,CLIENT=@CLIENT
		,FILETYPE=@FILETYPE
		,LASTUPDATE=GETDATE()
		,FILEFORMATE=@FILEFORMATE
		,ENCRYPTIONCODE=@ENCRYPTIONCODE
		WHERE BANKMASTERID=@BMASTERID
		SELECT 'RECORD UPDATEd SUCESSFULLY !' AS 'RESULT'
	END

	IF @ACTION = 'DELETE'
	BEGIN

	DELETE FROM RECON_BANK_MASTER
	WHERE BANKMASTERID=@BMASTERID

	SELECT 'RECORD DELETED SUCESSFULLY !' AS 'RESULT'
	END
END

GO
