/*
CLS_GET_EXCHANGE_SEGMENT_CUST 'BROKER','PARTY_LEDGER', 'BROKER', 'BROKER'
*/
CREATE PROCEDURE [dbo].[CLS_GET_EXCHANGE_SEGMENT_CUST] (
	@USERID VARCHAR(25),
	@RPTCODE VARCHAR(25),
	@STATUSID VARCHAR(10) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'
	)
AS
DECLARE @LOOPID INT,
	@DATA VARCHAR(MAX),
	@EXCHSEG VARCHAR(25)

CREATE TABLE #TEMP (EXCHSEG VARCHAR(500))

IF @STATUSID = 'FAMILY'
BEGIN
	SELECT @DATA = CASE WHEN LEN(ISNULL(FLDEXCHLIST, '')) < LEN(ISNULL(EXCHLIST, '')) THEN CASE WHEN LEN(ISNULL(FLDEXCHLIST, '')) <> '' THEN FLDEXCHLIST ELSE EXCHLIST END ELSE CASE WHEN LEN(ISNULL(EXCHLIST, '')) <> '' THEN EXCHLIST ELSE FLDEXCHLIST END END
	FROM (SELECT * FROM MSAJAG..CLS_GETRPTEXCHLIST WHERE FLDREPORTCODE = @RPTCODE
		AND FLDUSERNAME = @USERID	AND FLDEXCHLIST <> '') R FULL OUTER JOIN (SELECT CL_CODE, EXCHLIST FROM MSAJAG..CLS_GET_CLIENT_FAM_EXCH_LIST 
	WHERE FAMILY = @STATUSNAME AND LEN(EXCHLIST) = (SELECT MAX(LEN(EXCHLIST)) FROM MSAJAG..CLS_GET_CLIENT_FAM_EXCH_LIST WHERE FAMILY = @STATUSNAME)) C 
		ON CL_CODE = FLDUSERNAME 
END
ELSE
BEGIN 
print 'asd'
	SELECT @DATA = CASE WHEN LEN(ISNULL(FLDEXCHLIST, '')) < LEN(ISNULL(EXCHLIST, '')) THEN CASE WHEN ISNULL(FLDEXCHLIST, '') <> '' THEN FLDEXCHLIST ELSE EXCHLIST END ELSE CASE WHEN ISNULL(EXCHLIST, '') <> '' THEN EXCHLIST ELSE FLDEXCHLIST END END
	--SELECT @DATA = CASE WHEN LEN(ISNULL(FLDEXCHLIST, '')) < LEN(ISNULL(EXCHLIST, '')) THEN FLDEXCHLIST ELSE EXCHLIST END
	FROM (SELECT * FROM MSAJAG..CLS_GETRPTEXCHLIST WHERE FLDREPORTCODE = @RPTCODE
		AND FLDUSERNAME = @USERID	AND FLDEXCHLIST <> '') R FULL OUTER JOIN (SELECT CL_CODE, EXCHLIST FROM MSAJAG..CLS_GET_CLIENT_FAM_EXCH_LIST 
	WHERE CL_CODE = @STATUSNAME AND LEN(EXCHLIST) = (SELECT MAX(LEN(EXCHLIST)) FROM MSAJAG..CLS_GET_CLIENT_FAM_EXCH_LIST WHERE CL_CODE = @STATUSNAME)) C 
		ON CL_CODE = FLDUSERNAME 
		
END

IF LEN(@DATA) > 1
BEGIN	
		SET @LOOPID = 1
	print 'herer'
		WHILE 1 = 1
		BEGIN
			SET @EXCHSEG =.dbo.CLS_Piece(@DATA, ',', @LOOPID)
			SET @LOOPID = @LOOPID + 1

			IF @EXCHSEG IS NULL
				OR @EXCHSEG = ''
			BEGIN
				BREAK
			END

			INSERT INTO #TEMP
			VALUES (@EXCHSEG)
		END

		SELECT EXCHANGE, SEGMENT, EXCHANGE + ' ' + SEGMENT AS EXG_SEG,DEFAULTSEGMENT, DISPLAY_EXCHANGE, DISPLAY_SEGMENT, GROUPID
		FROM #TEMP T,
			CLS_COMPANY_MASTER_SETTING P
		WHERE T.EXCHSEG = P.EXCHANGE + '-' + P.SEGMENT
		ORDER BY P.EXCHANGE,
			P.SEGMENT
END
ELSE
BEGIN
		SELECT EXCHANGE, SEGMENT, EXCHANGE + ' ' + SEGMENT AS EXG_SEG,DEFAULTSEGMENT, DISPLAY_EXCHANGE, DISPLAY_SEGMENT, GROUPID
		FROM CLS_COMPANY_MASTER_SETTING P
		ORDER BY P.EXCHANGE,
			P.SEGMENT
END

GO
