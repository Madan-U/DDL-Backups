FROM (  
SELECT UCC,TDAY_CASHCOLL=CONVERT(MONEY,0),EPI_LEDGER=EPI_LEDGER FROM #CASHREPORTING_MAR  WHERE EPI_LEDGER <> 0 
UNION ALL  
SELECT PARTY_CODE,TDAY_CASHCOLL=TDAY_CASHCOLL,EPI_LEDGER=CONVERT(MONEY,0) FROM #MARGIN  WHERE EXCHANGE  = 'EPN' AND TDAY_CASHCOLL <> 0 
) A  
GROUP BY UCC  


SELECT UCC,TDAY_NONCASH=ROUND(SUM(TDAY_NONCASH),0),POOL_ACCOUNT=ROUND(SUM(POOL_ACCOUNT),0) INTO #DIFF_DATA_POOL  
FROM (  
SELECT UCC,TDAY_NONCASH=CONVERT(MONEY,0),POOL_ACCOUNT=POOL_ACCOUNT FROM #CASHREPORTING_MAR  WHERE POOL_ACCOUNT <> 0 
UNION ALL  
SELECT PARTY_CODE,TDAY_NONCASH=TDAY_NONCASH,POOL_ACCOUNT=CONVERT(MONEY,0) FROM #MARGIN  WHERE EXCHANGE  = 'EPN' AND TDAY_NONCASH <>0
) A  
GROUP BY UCC  


SELECT UCC,M_LEDGER_PEAK=ROUND(SUM(M_LEDGER_PEAK),2),PEAK_FINANCIAL_LEDGER=ROUND(SUM(PEAK_FINANCIAL_LEDGER),2) INTO #DIFF_DATA_PEAK  
FROM (  
SELECT UCC,M_LEDGER_PEAK=CONVERT(MONEY,0),PEAK_FINANCIAL_LEDGER=PEAK_FINANCIAL_LEDGER FROM #CASHREPORTING_MAR_PEAK  WHERE PEAK_FINANCIAL_LEDGER <> 0 
UNION ALL  
SELECT PARTY_CODE,M_LEDGER_PEAK=M_LEDGER_PEAK,PEAK_FINANCIAL_LEDGER=CONVERT(MONEY,0) FROM #MARGIN_FINAL_PEAK
) A  
GROUP BY UCC 

SELECT Report_Date,REPORT,SUM([Balance In TB]) AS [Balance In TB],SUM([Balance In CCE]) AS [Balance In CCE],SUM([Balance In Margin]) AS [Balance In Margin],
REC_AMT_T1 = CONVERT(NUMERIC(18,2),0),DIFF_TB_CCE = 0 ,DIFF_TB_MAR = 0 ,DIFF_CCE_MAR = 0 INTO #CHECK FROM (
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE),105),Report='EDT-Balance',[Balance In TB]=ABS(SUM(ROUND(TB_EDT,2))),[Balance In CCE] = 0,
