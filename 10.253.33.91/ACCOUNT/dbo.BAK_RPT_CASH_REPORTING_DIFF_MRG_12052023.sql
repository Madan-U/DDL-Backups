FROM #MARGIN WHERE EXCHANGE  = 'EPN' AND TDAY_CASHCOLL <> 0
UNION ALL
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE),105),Report='POOL Amount',[Balance In TB] = 0,[Balance In CCE] = ABS(SUM(ROUND(POOL_ACCOUNT,2))),
[Balance In Margin] = 0,DIFF_TB_CCE=CONVERT(MONEY,0),DIFF_TB_MAR=CONVERT(MONEY,0),DIFF_CCE_MAR=CONVERT(MONEY,0)
FROM #CASHREPORTING_MAR WHERE POOL_ACCOUNT <> 0 
UNION ALL
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE),105),Report='POOL Amount',[Balance In TB] = 0,[Balance In CCE] = 0,
[Balance In Margin]=ABS(SUM(ROUND(TDAY_NONCASH,2))),DIFF_TB_CCE=CONVERT(MONEY,0),DIFF_TB_MAR=CONVERT(MONEY,0),DIFF_CCE_MAR=CONVERT(MONEY,0)
FROM #MARGIN WHERE EXCHANGE  = 'EPN' AND TDAY_NONCASH <> 0
UNION ALL
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE),105),Report='PEAK-Balance',[Balance In TB]=0,[Balance In CCE]=ABS(SUM(ROUND(PEAK_FINANCIAL_LEDGER,2))),
[Balance In Margin] = 0,DIFF_TB_CCE=CONVERT(MONEY,0),DIFF_TB_MAR=CONVERT(MONEY,0),DIFF_CCE_MAR=CONVERT(MONEY,0)
FROM #CASHREPORTING_MAR_PEAK
UNION ALL
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE),105),Report='PEAK-Balance',[Balance In TB]=0,[Balance In CCE] = 0,
[Balance In Margin]=ABS(SUM(ROUND(M_LEDGER_PEAK,2))), DIFF_TB_CCE=CONVERT(MONEY,0),DIFF_TB_MAR=CONVERT(MONEY,0),DIFF_CCE_MAR=CONVERT(MONEY,0)
FROM #MARGIN_FINAL_PEAK) A 
GROUP BY Report_Date,REPORT


SELECT REPORT='EDT-Balance',UCC,TB_EDT_AMOUNT,M_LEDGER,REC_AMT_T1,CCE_EDT_AMOUNT INTO #CHECK1 
FROM #DIFF_DATA_MAR  WHERE (ABS(TB_EDT_AMOUNT) <> ABS(CCE_EDT_AMOUNT) OR ABS(TB_EDT_AMOUNT) <> ABS(M_LEDGER) 
OR  ABS(CCE_EDT_AMOUNT) <>  ABS(M_LEDGER))  
UNION ALL
SELECT REPORT='EPN Amount',UCC,TB_EDT_AMOUNT=0,M_LEDGER=TDAY_CASHCOLL,REC_AMT_T1=0,CCE_EDT_AMOUNT=EPI_LEDGER
FROM #DIFF_DATA_EPN  WHERE (ABS(TDAY_CASHCOLL) <> ABS(EPI_LEDGER) )  
UNION ALL
SELECT REPORT='Pool Amount',UCC,TB_EDT_AMOUNT=0,M_LEDGER=TDAY_NONCASH,REC_AMT_T1=0,CCE_EDT_AMOUNT=POOL_ACCOUNT
FROM #DIFF_DATA_POOL  WHERE (ABS(TDAY_NONCASH) <> ABS(POOL_ACCOUNT))
UNION ALL
SELECT REPORT='PEAK-Balance',UCC,TB_EDT_AMOUNT=0,M_LEDGER=M_LEDGER_PEAK,REC_AMT_T1=0,CCE_EDT_AMOUNT=PEAK_FINANCIAL_LEDGER
FROM #DIFF_DATA_PEAK  WHERE (ABS(M_LEDGER_PEAK) <> ABS(PEAK_FINANCIAL_LEDGER)) 


SELECT *,(CONVERT(NUMERIC(18,2),TB_EDT_AMOUNT)+CONVERT(NUMERIC(18,2),M_LEDGER)) AS DIFF, 
(CONVERT(NUMERIC(18,2),TB_EDT_AMOUNT)+CONVERT(NUMERIC(18,2),M_LEDGER+REC_AMT_T1)) AS DIFF1
INTO #CHECK2 FROM #CHECK1 WHERE REPORT='EDT-Balance' 
AND (CONVERT(NUMERIC(18,2),TB_EDT_AMOUNT)+CONVERT(NUMERIC(18,2),M_LEDGER)) NOT BETWEEN -1 AND 1

ALTER TABLE #CHECK ALTER COLUMN DIFF_TB_CCE NUMERIC(18,2)

ALTER TABLE #CHECK ALTER COLUMN DIFF_TB_MAR NUMERIC(18,2)

ALTER TABLE #CHECK ALTER COLUMN DIFF_CCE_MAR NUMERIC(18,2)

UPDATE #CHECK SET REC_AMT_T1 = (SELECT VAMT FROM (SELECT SUM(REC_AMT_T1) AS VAMT FROM #CHECK2 ) AS A) WHERE REPORT = 'EDT-Balance'

--UPDATE #CHECK SET DIFF_TB_CCE = [Balance In TB] - [Balance In CCE], DIFF_TB_MAR =  [Balance In TB] - ([Balance In Margin] + REC_AMT_T1), 
--DIFF_CCE_MAR =  [Balance In CCE] - ([Balance In Margin] + REC_AMT_T1) WHERE REPORT = 'EDT-Balance'

UPDATE #CHECK SET DIFF_TB_CCE = [Balance In TB] - [Balance In CCE], DIFF_TB_MAR =  [Balance In TB] - ([Balance In Margin]), 
DIFF_CCE_MAR =  [Balance In CCE] - ([Balance In Margin]) WHERE REPORT = 'EDT-Balance'

UPDATE #CHECK SET DIFF_CCE_MAR =  [Balance In CCE] - [Balance In Margin] WHERE REPORT <> 'EDT-Balance'


