INTO #DIFF_DATA_MAR  
FROM (  
SELECT UCC,CLASS_LEDGER=CONVERT(MONEY,0),M_LEDGER=CONVERT(MONEY,0),CASH_EDT_BAL=EDT_BAL FROM #CASHREPORTING_MAR  
UNION ALL  
SELECT PARTY_CODE,CLASS_LEDGER=CONVERT(MONEY,0),M_LEDGER=M_LEDGER,CASH_EDT_BAL=CONVERT(MONEY,0) FROM #MARGIN_FINAL   
UNION ALL  
SELECT CLTCODE,CLASS_LEDGER=TB_EDT,M_LEDGER=CONVERT(MONEY,0),CASH_EDT_BAL=CONVERT(MONEY,0) FROM #TBEDT_MAR ) A  
GROUP BY UCC  


UPDATE #DIFF_DATA_MAR SET REC_AMT_T1 = VAMT FROM #LEDGER_REC WHERE UCC = CLTCODE

