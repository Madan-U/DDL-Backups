[Balance In Margin] = 0,DIFF_TB_CCE=CONVERT(MONEY,0),DIFF_TB_MAR=CONVERT(MONEY,0),DIFF_CCE_MAR=CONVERT(MONEY,0)
FROM #CASHREPORTING_MAR
UNION ALL
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE),105),Report='EDT-Balance',[Balance In TB]=0,[Balance In CCE] = 0,
[Balance In Margin]=ABS(SUM(ROUND(M_LEDGER,2))), DIFF_TB_CCE=CONVERT(MONEY,0),DIFF_TB_MAR=CONVERT(MONEY,0),DIFF_CCE_MAR=CONVERT(MONEY,0)
FROM #MARGIN_FINAL
UNION ALL
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE),105),Report='EPN Amount',[Balance In TB]=0,[Balance In CCE]=ABS(SUM(ROUND(EPI_LEDGER,2))),
[Balance In Margin] = 0,DIFF_TB_CCE=CONVERT(MONEY,0),DIFF_TB_MAR=CONVERT(MONEY,0),DIFF_CCE_MAR=CONVERT(MONEY,0)
FROM #CASHREPORTING_MAR WHERE EPI_LEDGER <> 0 
UNION ALL
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE),105),Report='EPN Amount',[Balance In TB]= 0,[Balance In CCE] = 0,
[Balance In Margin]=ABS(SUM(ROUND(TDAY_CASHCOLL,2))),
