FROM #FINAL A INNER JOIN MSAJAG..CLIENT_DETAILS B ON A.CLTCODE=B.CL_CODE  
WHERE CLTCODE <>'BBBB' AND CL_CODE >='A0' AND CL_CODE <='ZZZZZZZZZZZZZ'
