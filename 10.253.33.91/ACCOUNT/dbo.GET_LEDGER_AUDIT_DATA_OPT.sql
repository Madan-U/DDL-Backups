CREATE PROCEDURE [dbo].[GET_LEDGER_AUDIT_DATA_OPT]      
 @EFFDATE VARCHAR(11),    
 @SESSIONID VARCHAR(500),    
 @PROCESS_CODE VARCHAR(10),    
 @REPORT_FORMULA VARCHAR(MAX),  
 @EXCHANGE VARCHAR(10),  
 @SEGMENT VARCHAR(50),
 @TABLE_NAME VARCHAR(20) = '' 
AS      
    
DECLARE      
 @START_DATE DATETIME
      
IF LEN(@EFFDATE) = 10      
BEGIN      
 SELECT @EFFDATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @EFFDATE, 103), 109)      
END      
 
 SET @EFFDATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @EFFDATE, 103), 120) 

SELECT @START_DATE = SDTCUR
FROM PARAMETER      
WHERE @EFFDATE BETWEEN SDTCUR AND LDTCUR      
    
    
CREATE TABLE #REPORT_DATA    
(    
 CLTCODE VARCHAR(10),    
 PARTY_NAME VARCHAR(100),       
 ENTITY_LEVEL VARCHAR(20),    
 ENTITY_CODE VARCHAR(25),      
 VDTBAL MONEY,      
 EDTBAL MONEY,      
 FDT_RECEIPT MONEY,      
 FDT_PAYMENTS MONEY,  
 MARGIN_BAL MONEY    
)    
    
DECLARE    
 @@SQL VARCHAR(MAX)  
 


 
SELECT CLTCODE,SUM(CASE WHEN VDT >= @START_DATE THEN (CASE DRCR WHEN 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END) AS VDTBAL INTO #TMP1
FROM  LEDGER L(NOLOCK) WHERE
VDT >= @START_DATE  
 ---EDT >= @START_DATE --AND CLTCODE ='RP61'
 AND CDT <= DATEADD(MI, -15, GETDATE())
 AND VDT <= @EFFDATE + ' 23:59' 
 GROUP BY       
 L.CLTCODE 
 ---HAVING SUM(CASE DRCR WHEN 'D' THEN VAMT ELSE -VAMT END) <>0 


 SELECT * INTO #EDT1 FROM (
 SELECT CLTCODE, EDT_BAL=SUM(CASE WHEN VDT >= @START_DATE AND EDT <= @EFFDATE + ' 23:59' THEN (CASE DRCR WHEN 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END       
   + CASE WHEN EDT >= @START_DATE AND VDT < @START_DATE THEN (CASE DRCR WHEN 'C' THEN VAMT ELSE -VAMT END) ELSE 0 END) 
FROM  LEDGER L(NOLOCK) WHERE
VDT >= @START_DATE-10 --- AND
-- EDT >= @START_DATE --AND CLTCODE ='RP61'
 AND CDT <= DATEADD(MI, -15, GETDATE())
 AND VDT <= @EFFDATE + ' 23:59' 
 GROUP BY       
 L.CLTCODE  ) A 
 WHERE EDT_BAL <>0
 

  SELECT * INTO #BAL1  FROM (
 SELECT CLTCODE,  FDT_RECEIPT = SUM(CASE WHEN (EDT > @EFFDATE + ' 23:59') AND L.VTYP = 2 THEN (VAMT) ELSE 0 END),      
 FDT_PAYMENTS = SUM(CASE WHEN (EDT > @EFFDATE + ' 23:59') AND L.VTYP = 3 THEN (VAMT) ELSE 0 END)   
FROM  LEDGER L(NOLOCK) WHERE
VDT >= CONVERT(DATETIME, @EFFDATE)-10 --- AND
 AND CDT <= DATEADD(MI, -15, GETDATE())
 AND VDT <= @EFFDATE + ' 23:59' 
 GROUP BY       
 L.CLTCODE  ) A 
 WHERE FDT_RECEIPT <>0 OR FDT_PAYMENTS <>0
 
 --CREATE INDEX CLTCODE ON #TMP1 (CLTCODE)
 -- CREATE INDEX CLTCODE ON #EDT1 (CLTCODE)
 --  CREATE INDEX CLTCODE ON #BAL1 (CLTCODE)

 

 SELECT DISTINCT A.* INTO #FINAL FROM (
   SELECT DISTINCT CLTCODE  FROM #TMP1
   UNION ALL
   --SELECT DISTINCT CLTCODE  FROM #EDT1
   --UNION ALL
   SELECT DISTINCT CLTCODE  FROM #BAL1 )L , FORMULA_CLIENT_MASTER A
    WHERE A.CLTCODE = L.CLTCODE AND SESSION_ID = @SESSIONID

	 CREATE INDEX CLTCODE ON #FINAL (CLTCODE)



 
 
SELECT       
 F.CLTCODE,    
 PARTY_NAME,       
 ENTITY_LEVEL,    
 ENTITY_CODE,      
 VDTBAL = ISNULL(SUM(VDTBAL),0),      
 EDTBAL = ISNULL(SUM(EDT_BAL),0),      
 FDT_RECEIPT = ISNULL(SUM(FDT_RECEIPT),0),      
 FDT_PAYMENTS = ISNULL(SUM(FDT_PAYMENTS),0),  
 MARGIN_BAL = 0  INTO   DUSTBIN.DBO.lED_bAL_TEST_SIVA    
FROM      
#FINAL F
LEFT OUTER JOIN 
#TMP1 V ON F.CLTCODE=V.CLTCODE
LEFT OUTER JOIN 
#EDT1 E ON F.CLTCODE=E.CLTCODE
LEFT OUTER JOIN 
#BAL1 B ON F.CLTCODE=B.CLTCODE
GROUP BY F.CLTCODE,    
 PARTY_NAME,       
 ENTITY_LEVEL,    
 ENTITY_CODE
 
 
  
  



    

