CREATE PROC  [dbo].[SCFM_DETAILS] 
(@FROMDATE DATETIME,@TODATE DATETIME)
AS
 
SELECT CLTCODE,SUM(BALANCE) BALANCE INTO #T1  FROM (

SELECT CLTCODE,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END)BALANCE FROM LEDGER WHERE VDT >=@FROMDATE AND EDT <=@TODATE + ' 23:59'   AND VNO <>'201700000001' AND VTYP <>'18'
GROUP  BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END)BALANCE 
FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER WHERE  VDT >=@FROMDATE AND EDT <=@TODATE   + ' 23:59'  AND VNO <>'201700000001' AND VTYP <>'18'
GROUP  BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END)BALANCE 
FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WHERE VDT >=@FROMDATE AND EDT <=@TODATE  + ' 23:59'   AND VNO <>'201700000001' AND VTYP <>'18'
GROUP  BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END)BALANCE 
FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER WHERE  VDT >=@FROMDATE AND EDT <=@TODATE + ' 23:59'    AND VNO <>'201700000001' AND VTYP <>'18'
GROUP  BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR ='D' THEN VAMT*-1 ELSE VAMT END)BALANCE 
FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER WHERE VDT >=@FROMDATE AND EDT <=@TODATE  + ' 23:59'   AND VNO <>'201700000001' AND VTYP <>'18'
GROUP  BY CLTCODE

)A
GROUP BY CLTCODE
 
SELECT CLTCODE,SUM(BALANCE) BALANCE INTO #UNCLEAR  FROM (
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='D' THEN VAMT*-1 ELSE VAMT END )BALANCE
FROM LEDGER L,LEDGER1 L1 WHERE VDT >=@FROMDATE AND VDT <=@TODATE +' 23:59'
AND L.VNO=L1.VNO AND L.VTYP=L1.VTYP AND L.BOOKTYPE=L1.BookType
AND L.DRCR ='C'
AND (RELDT >=@TODATE   + ' 23:59'  OR RELDT ='1900-01-01') AND L.VTYP =2
GROUP BY CLTCODE 
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='D' THEN VAMT*-1 ELSE VAMT END )BALANCE
FROM ANGELFO.ACCOUNTFO.DBO.LEDGER L,ANGELFO.ACCOUNTFO.DBO.LEDGER1 L1 WHERE VDT >=@FROMDATE AND VDT <=@TODATE +' 23:59'
AND L.VNO=L1.VNO AND L.VTYP=L1.VTYP AND L.BOOKTYPE=L1.BookType
AND L.DRCR ='C'
AND (RELDT >=@TODATE   + ' 23:59'  OR RELDT ='1900-01-01') AND L.VTYP =2
GROUP BY CLTCODE 
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='D' THEN VAMT*-1 ELSE VAMT END )BALANCE
FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER L,ANGELFO.ACCOUNTCURFO.DBO.LEDGER1 L1 WHERE VDT >=@FROMDATE AND VDT <=@TODATE +' 23:59'
AND L.VNO=L1.VNO AND L.VTYP=L1.VTYP AND L.BOOKTYPE=L1.BookType
AND L.DRCR ='C'
AND (RELDT >=@TODATE   + ' 23:59'  OR RELDT ='1900-01-01') AND L.VTYP =2
GROUP BY CLTCODE 
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='D' THEN VAMT*-1 ELSE VAMT END )BALANCE
FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER L,AngelBSECM.ACCOUNT_AB.DBO.LEDGER1 L1 WHERE VDT >=@FROMDATE AND VDT <=@TODATE +' 23:59'
AND L.VNO=L1.VNO AND L.VTYP=L1.VTYP AND L.BOOKTYPE=L1.BookType
AND L.DRCR ='C'
AND (RELDT >=@TODATE   + ' 23:59'  OR RELDT ='1900-01-01') AND L.VTYP =2
GROUP BY CLTCODE 
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN L.DRCR ='D' THEN VAMT*-1 ELSE VAMT END )BALANCE
FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER L,ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER1 L1 WHERE VDT >=@FROMDATE AND VDT <=@TODATE +' 23:59'
AND L.VNO=L1.VNO AND L.VTYP=L1.VTYP AND L.BOOKTYPE=L1.BookType
AND L.DRCR ='C'
AND (RELDT >=@TODATE   + ' 23:59'  OR RELDT ='1900-01-01') AND L.VTYP =2
GROUP BY CLTCODE 

)A
GROUP BY CLTCODE


UPDATE  #T1 SET CLTCODE =PARENTCODE FROM MSAJAG.DBO.CLIENT_DETAILS C
WHERE  CLTCODE LIKE '98%' AND CLTCODE=CL_cODE 

SELECT CLTCODE,SUM(BALANCE) BALANCE INTO #TEMPFINAL FROM #T1 WHERE CLTCODE >='A001' AND CLTCODE <='ZZZZZZZZ'  GROUP BY CLTCODE

INSERT INTO SCFM_SEBI
SELECT *,@TODATE AS PROCESS_DATE  FROM (
SELECT F.CLTCODE,SUM(F.BALANCE) AS BALANCE,SUM(ISNULL(U.BALANCE,0)) AS UNRECO ,SUM(F.BALANCE-ISNULL(U.BALANCE,0))NETABLANCE 
 FROM #TEMPFINAL F
LEFT OUTER JOIN 
#UNCLEAR U  ON F.CLTCODE=U.CLTCODE 
GROUP BY F.CLTCODE) A WHERE (BALANCE <> 0 OR UNRECO<>0)



INSERT INTO SCFM_MARGIN

SELECT * , @TODATE AS PROCESS_DATE FROM (
SELECT M.*,ISNULL(N.initialmargin,0)+ISNULL(N.MTMMARGIN,0)+ISNULL(N.PREMIUM_MARGIN,0)  AS NSEFOMARGIN
,ISNULL(C.initialmargin,0)+ISNULL(C.MTMMARGIN,0)+ISNULL(C.PREMIUM_MARGIN,0)  AS NSX
,ISNULL(S.initialmargin,0)+ISNULL(S.MTMMARGIN,0)+ISNULL(S.PREMIUM_MARGIN,0)  AS MCDXCDS  FROM #TEMPFINAL M
LEFT OUTER JOIN 
ANGELFO.PRADNYA.DBO.HISTORY_TBL_CLIENTMARGIN_NSEFO N ON M.CLTCODE=N.PARTY_CODE  AND N.MARGINDATE =@TODATE
LEFT OUTER JOIN 
ANGELFO.NSECURFO.DBO.TBL_CLIENTMARGIN C ON M.CLTCODE=C.PARTY_CODE  AND C.MARGINDATE =@TODATE
LEFT OUTER JOIN 
ANGELCOMMODITY.MCDXCDS.DBO.TBL_CLIENTMARGIN S ON M.CLTCODE=S.PARTY_CODE  AND S.MARGINDATE =@TODATE )A
 WHERE 
 (NSEFOMARGIN <>0 OR MCDXCDS<>0 OR NSX<>0)

GO
