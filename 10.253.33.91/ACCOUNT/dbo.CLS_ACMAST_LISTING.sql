CREATE PROC [dbo].[CLS_ACMAST_LISTING]
(
	@ACCAT VARCHAR(10),  
	@CLTCODEFR VARCHAR(20),
	@CLTCODETO VARCHAR(20),
	@BRANCHCODE VARCHAR(35),
	@GRPCODE VARCHAR(13),
	@SORTBY VARCHAR(10)
)
AS
/*
	EXEC ACMAST_LISTING '', '0', 'ZZZZZZ', '', '', 'CODE'
*/
SELECT
	A.CLTCODE,
	A.LONGNAME,
	A.GRPCODE,
	A.BRANCHCODE,
	C.CATNAME,
	AD.EXCHANGE,
	AD.SEGMENT
FROM
	ACMAST A,
	CATMAST C,
	CLS_ACMAST_DETAILS AD
WHERE
	A.CLTCODE = AD.CLTCODE
	AND A.ACCAT = C.ACCAT
	AND A.ACCAT = CASE WHEN @ACCAT = '' THEN A.ACCAT ELSE @ACCAT END
	AND A.CLTCODE >= @CLTCODEFR
	AND A.CLTCODE <= @CLTCODETO
	AND A.BRANCHCODE = CASE WHEN @BRANCHCODE = '' THEN A.BRANCHCODE ELSE @BRANCHCODE END
	AND A.GRPCODE LIKE .DBO.CLS_REPLACEGRPZEROS(@GRPCODE) + '%'
GROUP BY
	A.CLTCODE,
	A.LONGNAME,
	A.GRPCODE,
	A.BRANCHCODE,
	C.CATNAME,
	AD.EXCHANGE,
	AD.SEGMENT
ORDER BY
--A.GRPCODE,A.BRANCHCODE
CASE WHEN @SORTBY = 'CODE' THEN A.CLTCODE ELSE A.LONGNAME END

GO
