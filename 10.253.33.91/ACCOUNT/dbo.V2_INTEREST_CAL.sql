-- Object: PROCEDURE dbo.V2_INTEREST_CAL
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------



CREATE PROCEDURE V2_INTEREST_CAL 
(  
        @@BALDATE INT 
)  
  
AS  
  
/*==============================================================================================================  
        EXEC V2_INTEREST_CAL  
                @@BALDATE = 20070401 
==============================================================================================================*/  
  
        SET NOCOUNT ON

	DECLARE 
		@@CREDIT_INT MONEY, 
		@@DEBIT_INT MONEY

	SELECT 
		@@CREDIT_INT = CREDIT_INT, 
		@@DEBIT_INT = DEBIT_INT 
	FROM V2_CLIENTPROFILES_FA 
	WHERE CLTCODE = 'ALL'

	SELECT 
		A.CLTCODE, 
		A.SEGMENTCODE, 
		A.LEDTYPE, 
		VDTBAL = SUM(A.VDTBAL), 
		VDTINTEREST = (CASE WHEN SUM(A.VDTBAL) > 0 
				THEN ((SUM(A.VDTBAL) * ISNULL(CREDIT_INT,@@CREDIT_INT)) / 100) / 365
				ELSE 
				(CASE WHEN SUM(A.VDTBAL) < 0 
				THEN ((SUM(A.VDTBAL) * ISNULL(DEBIT_INT,@@DEBIT_INT)) / 100) / 365
				ELSE 0 END) END), 
		EDTBAL = SUM(A.EDTBAL), 
		EDTINTEREST = (CASE WHEN SUM(A.EDTBAL) > 0 
				THEN ((SUM(A.EDTBAL) * ISNULL(CREDIT_INT,@@CREDIT_INT)) / 100) / 365
				ELSE 
				(CASE WHEN SUM(A.EDTBAL) < 0 
				THEN ((SUM(A.EDTBAL) * ISNULL(DEBIT_INT,@@DEBIT_INT)) / 100) / 365
				ELSE 0 END) END), 
		INTDATE = @@BALDATE
	FROM 
		V2_RUNNINGBALANCES A 
		LEFT OUTER JOIN 
		V2_CLIENTPROFILES_FA F 
		ON 
		(LTRIM(RTRIM(A.CLTCODE)) = LTRIM(RTRIM(F.CLTCODE))
		AND @@BALDATE BETWEEN CONVERT(INT,CONVERT(VARCHAR,FROMDATE,112)) AND CONVERT(INT,CONVERT(VARCHAR,TODATE,112))
		), 
		(
			SELECT V.CLTCODE, V.SEGMENTCODE, V.LEDTYPE, 
			BALDATE =  
				(
					SELECT MAX(BALDATE) 
					FROM V2_RUNNINGBALANCES D 
					WHERE 
						D.CLTCODE = V.CLTCODE
						AND D.SEGMENTCODE = V.SEGMENTCODE
						AND D.LEDTYPE = V.LEDTYPE
						AND D.BALDATE <= @@BALDATE
						AND D.LEDTYPE <> 3
				)
			FROM V2_RUNNINGBALANCES V 
			WHERE V.BALDATE <= @@BALDATE 
			GROUP BY V.CLTCODE, V.SEGMENTCODE, V.LEDTYPE
		) B 
	WHERE 
		A.CLTCODE = B.CLTCODE
		AND A.SEGMENTCODE = B.SEGMENTCODE
		AND A.LEDTYPE = B.LEDTYPE
		AND A.BALDATE = B.BALDATE 
		AND A.LEDTYPE <> 3
	GROUP BY 
		A.CLTCODE, 
		A.SEGMENTCODE, 
		A.LEDTYPE, 
		CREDIT_INT, 
		DEBIT_INT
	HAVING SUM(A.VDTBAL) <> 0 OR SUM(A.EDTBAL) <> 0

	UNION ALL

	SELECT 
		A.CLTCODE, 
		A.SEGMENTCODE, 
		A.LEDTYPE, 
		VDTBAL = SUM(A.VDTBAL), 
		VDTINTEREST = (CASE WHEN SUM(A.VDTBAL) > 0 
				THEN ((SUM(A.VDTBAL) * ISNULL(CREDIT_INT,@@CREDIT_INT)) / 100) / 365
				ELSE 
				(CASE WHEN SUM(A.VDTBAL) < 0 
				THEN ((SUM(A.VDTBAL) * ISNULL(DEBIT_INT,@@DEBIT_INT)) / 100) / 365
				ELSE 0 END) END), 
		EDTBAL = SUM(A.EDTBAL), 
		EDTINTEREST = (CASE WHEN SUM(A.EDTBAL) > 0 
				THEN ((SUM(A.EDTBAL) * ISNULL(CREDIT_INT,@@CREDIT_INT)) / 100) / 365
				ELSE 
				(CASE WHEN SUM(A.EDTBAL) < 0 
				THEN ((SUM(A.EDTBAL) * ISNULL(DEBIT_INT,@@DEBIT_INT)) / 100) / 365
				ELSE 0 END) END), 
		INTDATE = @@BALDATE
	FROM 
		V2_RUNNINGBALANCES A 
		LEFT OUTER JOIN 
		V2_CLIENTPROFILES_FA F 
		ON 
		(LTRIM(RTRIM(A.CLTCODE)) = LTRIM(RTRIM(F.CLTCODE))
		AND @@BALDATE BETWEEN CONVERT(INT,CONVERT(VARCHAR,FROMDATE,112)) AND CONVERT(INT,CONVERT(VARCHAR,TODATE,112))
		) 
	WHERE 
		A.LEDTYPE = 3 
		AND A.BALDATE = @@BALDATE  
	GROUP BY 
		A.CLTCODE, 
		A.SEGMENTCODE, 
		A.LEDTYPE, 
		CREDIT_INT, 
		DEBIT_INT
	HAVING SUM(A.VDTBAL) <> 0 OR SUM(A.EDTBAL) <> 0

GO
