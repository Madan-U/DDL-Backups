GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,111),PARTY_CODE      
      
---NSECD      
INSERT INTO #ALLCHARG      
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,111),PARTY_CODE,AMOUNT=CONVERT(FLOAT(500),''),AMOUNT_DEL='0',      
AMOUNT_TRD =SUM(PBROKAMT+SBROKAMT), CHARGES='BROKERAGE', SUM(SERVICE_TAX) AS SERVICE_TAX,      
SUM(TURN_TAX) AS TURN_TAX,      
SUM(SEBI_TAX) AS SEBI_TAX,      
