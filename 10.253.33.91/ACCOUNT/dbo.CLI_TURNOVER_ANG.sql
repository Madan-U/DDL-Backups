SET @CASH = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP " SELECT DATACASH FROM [ANAND1].ACCOUNT.DBO.ALLCHARG_FINAL_ANG ORDER BY SAUDA_DATE,PARTY_CODE,SEGMENT ASC " QUERYOUT ' +@CASHFILENAME+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'          
      
EXEC(@CASH)          
      
SELECT 'File Exported at '+ @CASHFILENAME                  

DROP TABLE #ALLCHARG1    
DROP TABLE #ALLCHARG    
DROP TABLE #CLIENTDATA    
DROP TABLE [#DATA]

GO
