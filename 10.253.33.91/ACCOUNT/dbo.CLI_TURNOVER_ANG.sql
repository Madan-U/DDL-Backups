GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,111),PARTY_CODE      
      
TRUNCATE TABLE ALLCHARG_FINAL_ANG      
          
SELECT SAUDA_DATE,PARTY_CODE,SEGMENT,SUM(AMOUNT_DEL+AMOUNT_TRD) AS BROK,SUM(SERVICE_TAX) AS SERVICE_TAX,      
SUM(TURN_TAX) AS TURN_TAX,SUM(SEBI_TAX) AS SEBI_TAX, SUM(INS_CHRG) AS STT,SUM(BROKER_CHRG) AS STAMP,sum(OTHER_CHRG) as OTHER_CHRG      
INTO #ALLCHARG1
FROM #ALLCHARG      
GROUP BY SAUDA_DATE,PARTY_CODE,SEGMENT      
ORDER BY SAUDA_DATE,PARTY_CODE,SEGMENT      


INSERT INTO ALLCHARG_FINAL_ANG  SELECT 'SAUDA_DATE,PARTY_CODE,SEGMENT,BROK,SERVICE_TAX,TURN_TAX,SEBI_TAX,STT,STAMP,OTHER_CHRG'

INSERT INTO ALLCHARG_FINAL_ANG  SELECT CONVERT(VARCHAR(1000),CONVERT(VARCHAR(1000),SAUDA_DATE)+','+CONVERT(VARCHAR(1000),PARTY_CODE)+','+
CONVERT(VARCHAR(1000),SEGMENT)+','
+CONVERT(VARCHAR(1000),BROK)+','+CONVERT(VARCHAR(1000),SERVICE_TAX)+','+CONVERT(VARCHAR(1000),TURN_TAX)+','+CONVERT(VARCHAR(1000),SEBI_TAX)+','
+CONVERT(VARCHAR(1000),STT)+','+CONVERT(VARCHAR(1000),STAMP)+','+CONVERT(VARCHAR(1000),OTHER_CHRG)) FROM #ALLCHARG1
      
DECLARE @CASHFILENAME VARCHAR(1000)           
