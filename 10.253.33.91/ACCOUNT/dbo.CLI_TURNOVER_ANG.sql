SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,111),PARTY_CODE, AMOUNT=CONVERT(FLOAT(500),''),      
AMOUNT_DEL = SUM(PBROKDEL+SBROKDEL),      
