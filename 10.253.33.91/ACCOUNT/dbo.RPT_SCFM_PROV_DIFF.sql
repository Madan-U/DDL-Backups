CREATE PROCEDURE [dbo].[RPT_SCFM_PROV_DIFF] (@RUNDATE VARCHAR(11))

AS ---- EXEC RPT_SCFM_PROV_DIFF 'SEP 22 2021'

SELECT CLTCODE,SCFM_LED=SUM(BALANCE) INTO #SCFM
FROM ACCOUNT.DBO.SFCM_REPORTING A, MSAJAG.DBO.CLIENT_DETAILS B
WHERE A.CLTCODE = B.CL_CODE AND A.REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
AND CLTCODE BETWEEN 'A' AND 'ZZZZZZZZZ99999999'  
GROUP BY CLTCODE

SELECT CLTCODE,PSCFM_LED=SUM(BALANCE) INTO #PSCFM 
FROM ACCOUNT.DBO.SFCM_PROVISIONAL_DATA WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY CLTCODE

SELECT SCFM_CLTCODE=A.CLTCODE,PSCFM_CLTCODE=B.CLTCODE,
SCFM_LED=ISNULL(SCFM_LED,0),PROV_SCFM = ISNULL(PSCFM_LED,0) INTO #RPTDIFF
FROM #SCFM A FULL OUTER JOIN #PSCFM B ON A.CLTCODE = B.CLTCODE

SELECT PSCFM_CLTCODE,SCFM_CLTCODE,PROV_SCFM,SCFM_LED,DIFF_AMT=ABS(SCFM_LED)-ABS(PROV_SCFM)
FROM #RPTDIFF
WHERE ABS(SCFM_LED)-ABS(PROV_SCFM) <> 0


