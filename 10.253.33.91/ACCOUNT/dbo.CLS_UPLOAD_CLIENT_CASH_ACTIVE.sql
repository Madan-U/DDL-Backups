CREATE PROCEDURE [dbo].[CLS_UPLOAD_CLIENT_CASH_ACTIVE]
(
@TDATE VARCHAR(10), 
@FILEFOR VARCHAR(20), 
@FILETYPE VARCHAR(50), 
@PROGID VARCHAR(50), 
@UNAME VARCHAR(50),
@FILENAME VARCHAR(100) = ''
 )  
AS
BEGIN


   
     CREATE TABLE #TEMPFILEDATA  
	  (  
		 CLTCODE VARCHAR(100),
		 STATUS_FROM_DATE VARCHAR(100),
		 STATUS_FLAG VARCHAR(100)
      )  

   
	 INSERT INTO #TEMPFILEDATA(CLTCODE,STATUS_FROM_DATE,STATUS_FLAG)  
     SELECT DBO.PIECE(FILE_DATA,',',1), DBO.PIECE(FILE_DATA,',',2), DBO.PIECE(FILE_DATA,',',3)
     FROM MSAJAG..FILEDATA  
     WHERE PROGID = @PROGID AND ISNULL(FILE_DATA,'') != ''
     
     DELETE from #TEMPFILEDATA  WHERE  STATUS_FLAG LIKE 'STatus%' OR STATUS_FROM_DATE  LIKE 'Start%' OR CLTCODE LIKE 'Client%'
    
 


	 UPDATE A
	 SET STATUS_TO_DATE =  DATEADD(D,-1,CONVERT(DATETIME,B.STATUS_FROM_DATE,103))
	 FROM CLIENT_CASH_ACTIVE_LOG A
			INNER JOIN #TEMPFILEDATA B
			ON A.CLTCODE = B.CLTCODE
	WHERE B.STATUS_FLAG = 1 AND A.STATUS_TO_DATE = '12/31/2049'

	 INSERT INTO CLIENT_CASH_ACTIVE_LOG
	 SELECT CLTCODE, CONVERT(DATETIME,STATUS_FROM_DATE,103), '12/31/2049', STATUS_FLAG, GETDATE(),  'BROKER' -- @UNAME 
	 FROM #TEMPFILEDATA
	
	
	SELECT STRMSGCODE = 1, STRMSG =  ' RECORD(S) IMPORTED SUCCESSFULLY.'   

	-- SELECT * FROM #TEMPFILEDATA
         
    --DELETE #TEMPFILEDATA
	--DELETE CLIENT_CASH_ACTIVE_LOG
   
  
   DROP TABLE #TEMPFILEDATA  
   

END

GO
