-- Object: PROCEDURE dbo.GET_LAST_SETTTLE_DATE
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROCEDURE [dbo].[GET_LAST_SETTTLE_DATE]
	@REPORTDATE VARCHAR(11),
	@EXCHANGE VARCHAR(3)
AS



UPDATE D SET D.LAST_SETT_DATE = B.LAST_SETTLE_DATE FROM #DATA D,
(SELECT PARTY_CODE = CL_CODE, 
        LAST_SETTLE_DATE = CASE WHEN MAX(PAY_B3B_PAYMENT) = 1 
								     AND @EXCHANGE = 'NSE' THEN 'BTB' ELSE 
									 CASE WHEN MAX(Active_Date) > DATEADD(D, -90, @REPORTDATE) THEN 'NA' ELSE '' END END 
FROM MSAJAG.DBO.CLIENT_BROK_DETAILS
GROUP BY CL_CODE) B
WHERE D.PARTY_CODE = B.PARTY_CODE


UPDATE D SET LAST_SETT_DATE = REPLACE(REPLACE(CONVERT(VARCHAR(11), SETT_DATE, 106), ' ', '-'), '-20', '-')
FROM #DATA D,
(SELECT PARTYCODE, SETT_DATE = MAX(PROCESSDT) FROM ACCOUNT.DBO.TBL_SETTLEMENT_FUNDSNSEC 
	WHERE PROCESSDT >= DATEADD(D, -90, @REPORTDATE) AND PROCESSDT <= @REPORTDATE AND STATUS IN('A', 'F') GROUP BY PARTYCODE) D1
	WHERE PARTY_CODE = PARTYCODE 

UPDATE #DATA SET LAST_SETT_DATE = 'ND' WHERE ISNULL(LAST_SETT_DATE, '') = ''

GO
