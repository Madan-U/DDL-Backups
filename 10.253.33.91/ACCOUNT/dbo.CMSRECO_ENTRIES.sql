--EXEC CMSRECO_ENTRIES 'MAY 27 2008', '02014'
CREATE PROC CMSRECO_ENTRIES    
 @FILE_DATE VARCHAR(11),    
 @BANKCODE VARCHAR(10)    
AS    
    
DECLARE     
 @@UPD_DATE DATETIME    
    
SELECT @@UPD_DATE = MIN(UPLOAD_DATE) FROM CMSRECO WHERE FILEHEADER_DATE LIKE @FILE_DATE + '%' AND BANK_CODE = @BANKCODE AND AMOUNTMISMATCH = 'NO'    
    
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED    
SELECT L.VNO, L.VTYP, VDT = CONVERT(VARCHAR(10), VDT, 103), CLTCODE, VAMT, L.DRCR, DDNO, RELDT = CONVERT(VARCHAR(10), RELDT, 103)   
FROM LEDGER L, LEDGER1 L1,CMSRECODETAILS C   
WHERE L.VNO = L1.VNO AND L.VTYP = L1.VTYP AND L.BOOKTYPE = L1.BOOKTYPE AND L.LNO = L1.LNO AND     
L1.DDNO = C.INST_NO AND L1.RELAMT = C.INST_AMOUNT AND L1.DRCR = DEBIT_CREDIT AND RELDT = VALUE_DATE     
AND FILEHEADER_DATE LIKE @FILE_DATE + '%' AND BANK_CODE = @BANKCODE    
AND UPLOAD_DATE BETWEEN DATEADD(S, -2, @@UPD_DATE) AND DATEADD(MI,  5, @@UPD_DATE)

GO
