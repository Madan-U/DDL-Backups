CREATE PROC [dbo].[SP_CLIENT_PROFILE]  
(  
 @ACCOUNT_LEVEL VARCHAR(25),  
 @ACCOUNT_CODE  VARCHAR(10),  
 @CREDIT_INT  MONEY,  
 @DEBIT_INT  MONEY,  
 @FROMDATE  DATETIME,  
 @TODATE  DATETIME,  
 @EXEMPT_NOOFDAYS  INT,  
 @EXEMPT_NOOFDAYS_CR  INT,  
 @MIN_INTEREST  MONEY,  
 @MIN_BALANCE  MONEY,  
 @ACTIONBY  VARCHAR(30),  
 @ACTION VARCHAR(10)  
)  
/*  
