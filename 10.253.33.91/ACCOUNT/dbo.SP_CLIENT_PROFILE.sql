 select * from V2_CLIENTPROFILES_NEW  
 ROLLBACK  
 EXEC SP_CLIENT_PROFILE  '6-Client','0a141',1.00,1.00,'Aug  1 2012','Oct 31 2012 23:59',1,1,1.00,1.00, 'bhrt','ADD'  
 EXEC SP_CLIENT_PROFILE  '6-Client','0a141',1.00,1.00,'Sep  1 2012','Oct 31 2012 23:59',1,1,1.00,1.00, 'bhrt','EDIT'  
 SP_CLIENT_PROFILE '6-Client','0a141',1,1,'Jul  1 2012','Dec 31 2049 23:59',1,1,1,1,'bhrt','ADD'  
 SP_CLIENT_PROFILE 'CLIENT','0A141',10,10,'JAN 1 2012','OCT 31 2012',1,1,10,2000,'BHRT','ADD'  
 SP_CLIENT_PROFILE 'CLIENT','0A141',10,10,'FEB 1 2012','OCT 31 2012',2,1,10,2000,'BHRT','EDIT'  
 SP_CLIENT_PROFILE 'CLIENT','0A141',10,10,'MAR 1 2012','OCT 31 2012',2,1,10,2000,'BHRT','EDIT'  
 SP_CLIENT_PROFILE 'CLIENT','0A141',10,10,'APR 1 2012','OCT 31 2012',2,1,10,2000,'BHRT','EDIT'  
 SP_CLIENT_PROFILE 'CLIENT','0A141',10,10,'MAY 1 2012','JUN 30 2012',2,1,10,2000,'BHRT','EDIT'  
   
 SP_CLIENT_PROFILE 'CLIENT','0A141',10,10,'JUL 1 2012','OCT 31 2012',1,1,10,2000,'BHRT','ADD'  
 SP_CLIENT_PROFILE 'CLIENT','0A141',10,10,'AUG 1 2012','SEP 30 2012',2,1,10,2000,'BHRT','EDIT'  
 SP_CLIENT_PROFILE 'CLIENT','0A141',10,10,'SEP 1 2012','OCT 31 2012',2,1,10,2000,'BHRT','EDIT'  
   
 select * from V2_CLIENTPROFILES_NEW  
   
*/  
AS  
DECLARE   
 @UPDATEDATE DATETIME,  
 @CREATEDATE DATETIME,  
 @CHKDATEFR DATETIME,  
 @CHKDATETO DATETIME,  
 @NEWDATEFR DATETIME,  
 @NEWDATETO DATETIME,  
 @ACCOUNT_LEVEL_ VARCHAR(30),  
 @LEVEL_FLAG INT,  
 @SQL VARCHAR(MAX)  
   
 SET @CREATEDATE = GETDATE()  
 SET @UPDATEDATE = GETDATE()  
   
BEGIN TRAN  
  
SET @LEVEL_FLAG = .DBO.PIECE(@ACCOUNT_LEVEL, '-', 1)   
SET @ACCOUNT_LEVEL = UPPER(.DBO.PIECE(@ACCOUNT_LEVEL, '-', 2))  
   
IF(@ACTION = 'FETCH')  
 BEGIN  
   
 SET @SQL = " SELECT "  
 SET @SQL = @SQL + " CONVERT(VARCHAR,SRNO) SRNO, CONVERT(VARCHAR,LEVEL_FLAG) LEVEL_FLAG, ACCOUNT_LEVEL,ACCOUNT_CODE, DATEFROM = CONVERT(VARCHAR,FROMDATE,103), DATETO = CONVERT(VARCHAR,TODATE,103), "  
 SET @SQL = @SQL + " CONVERT(VARCHAR,CREDIT_INT) CREDIT_INT, CONVERT(VARCHAR,DEBIT_INT) DEBIT_INT, "  
 SET @SQL = @SQL + " FRDT = CONVERT(VARCHAR,FROMDATE,112) ,"  
 SET @SQL = @SQL + " FLAG = CONVERT(VARCHAR,CASE WHEN FROMDATE < GETDATE() THEN 0 ELSE 1 END), "  
 SET @SQL = @SQL + " CONVERT(VARCHAR,EXEMPT_NOOFDAYS) EXEMPT_NOOFDAYS,CONVERT(VARCHAR,EXEMPT_NOOFDAYS_CR) EXEMPT_NOOFDAYS_CR,CONVERT(VARCHAR,MIN_INTEREST) MIN_INTEREST,CONVERT(VARCHAR,MIN_BALANCE) MIN_BALANCE"   
 SET @SQL = @SQL + " FROM V2_CLIENTPROFILES_NEW "  
 IF @ACCOUNT_CODE <> '' OR @FROMDATE <> '' --OR @TODATE <> ''  
  BEGIN  
  SET @SQL = @SQL + " WHERE "   
  END  
   
 IF @ACCOUNT_CODE <> ''   
  BEGIN  
  SET @SQL = @SQL + " ACCOUNT_CODE = '" + @ACCOUNT_CODE + "' "  
  END   
    
 IF @FROMDATE <> '' --AND @TODATE <> ''  
  BEGIN  
  SET @SQL = @SQL + " AND CONVERT(VARCHAR,FROMDATE,109) >= '" + CONVERT(VARCHAR,@FROMDATE,109) + "' "  
  SET @SQL = @SQL + " AND CONVERT(VARCHAR,TODATE,109) <= '" + CONVERT(VARCHAR,@TODATE,109) + "' "  
  END    
  SET @SQL = @SQL + "  ORDER BY ACCOUNT_CODE, FRDT "  
  PRINT @SQL  
  EXEC(@SQL)  
  COMMIT TRAN  
 END  
ELSE  
 IF(@ACTION = 'ADD')  
 BEGIN  
   
 IF DATEDIFF(DAY,GETDATE(), @FROMDATE) < 0  
  BEGIN  
  SELECT 'PAST DATE IS NOT ALLOWED..'  
  ROLLBACK  
  RETURN  
  END  
    
   
  IF(SELECT COUNT(*) FROM V2_CLIENTPROFILES_NEW WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND (@FROMDATE BETWEEN FROMDATE AND TODATE) AND @ACCOUNT_LEVEL = ISNULL(ACCOUNT_LEVEL, '0')) = 0   
  BEGIN   
  /*print 'asadasd'  
  rollback  
  return*/  
  INSERT INTO V2_CLIENTPROFILES_NEW  
  (  
   ACCOUNT_LEVEL,  
   ACCOUNT_CODE,  
   CREDIT_INT,  
   DEBIT_INT,  
   FROMDATE,  
   TODATE,  
   EXEMPT_NOOFDAYS,  
   EXEMPT_NOOFDAYS_CR,  
   MIN_INTEREST,  
   MIN_BALANCE,  
   CREATEDATE,  
   CREATEBY,  
   UPDATEDATE,  
   UPDATEBY,  
   LEVEL_FLAG,  
   DUMMY1,  
   DUMMY2  
  )   
  VALUES  
  (  
   @ACCOUNT_LEVEL,  
   @ACCOUNT_CODE,  
   @CREDIT_INT,  
   @DEBIT_INT,  
   @FROMDATE,  
   @TODATE,  
   @EXEMPT_NOOFDAYS,  
   @EXEMPT_NOOFDAYS_CR,  
   @MIN_INTEREST,  
   @MIN_BALANCE,  
   @CREATEDATE,  
   @ACTIONBY,  
   @UPDATEDATE,  
   @ACTIONBY,  
   @LEVEL_FLAG,  
   '',  
   ''  
  )  
    
 IF @@ERROR = 0   
  BEGIN  
  SELECT 'SLAB IS ADDED SUCCESSFULLY..'  
  COMMIT TRAN  
  RETURN  
  END   
 ELSE  
  BEGIN   
  ROLLBACK  
  SELECT 'SLAB IS NOT ADDED DUE TO SOME ERROR..'   
  RETURN  
  END  
    
 END  
  ELSE  
   BEGIN  
 SELECT 'ACCOUNT ALREADY EXIST IN THE SYSTEM FOR THE SAME SLAB..'  
 COMMIT  
   END      
 END  
ELSE  
 IF @ACTION = 'EDIT'  
  BEGIN  
 SELECT   
  @CHKDATEFR = FROMDATE, @CHKDATETO = TODATE  
 FROM   
  V2_CLIENTPROFILES_NEW   
 WHERE   
  ACCOUNT_CODE = @ACCOUNT_CODE AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL   
  AND @FROMDATE BETWEEN CONVERT(VARCHAR(11),FROMDATE,109) AND CONVERT(VARCHAR(11),TODATE,109)  
     AND FROMDATE <> @FROMDATE   
  
 --print @CHKDATETO  
   
 IF DATEDIFF(DAY,@CHKDATETO , @FROMDATE) < 0  --@CHKDATETO < @FROMDATE AND @@ROWCOUNT > 0  
  BEGIN   
   SET @NEWDATETO = DATEADD(DAY,-1, @FROMDATE)  
  --PRINT @NEWDATETO  
  --PRINT @CHKDATETO  
    
  INSERT INTO V2_CLIENTPROFILES_NEW(ACCOUNT_LEVEL,ACCOUNT_CODE,CREDIT_INT,DEBIT_INT,FROMDATE,TODATE,EXEMPT_NOOFDAYS,EXEMPT_NOOFDAYS_CR,MIN_INTEREST,MIN_BALANCE,CREATEDATE,CREATEBY,UPDATEDATE,UPDATEBY,LEVEL_FLAG)  
  SELECT ACCOUNT_LEVEL,ACCOUNT_CODE,@CREDIT_INT,@DEBIT_INT,@FROMDATE,@TODATE,@EXEMPT_NOOFDAYS,@EXEMPT_NOOFDAYS_CR,@MIN_INTEREST,@MIN_BALANCE,@CREATEDATE,CREATEBY+'1',@UPDATEDATE,UPDATEBY,LEVEL_FLAG FROM V2_CLIENTPROFILES_NEW   
  WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL AND FROMDATE LIKE CONVERT(VARCHAR(11),@CHKDATEFR,109) + '%'  
    
  IF @TODATE < @CHKDATETO   
   BEGIN   
   SET @NEWDATEFR= DATEADD(DAY,+1, @TODATE)  
   SET @NEWDATETO = @CHKDATETO  
     
   INSERT INTO V2_CLIENTPROFILES_NEW(ACCOUNT_LEVEL,ACCOUNT_CODE,CREDIT_INT,DEBIT_INT,FROMDATE,TODATE,EXEMPT_NOOFDAYS,EXEMPT_NOOFDAYS_CR,MIN_INTEREST,MIN_BALANCE,CREATEDATE,CREATEBY,UPDATEDATE,UPDATEBY,LEVEL_FLAG)  
   SELECT ACCOUNT_LEVEL,ACCOUNT_CODE,@CREDIT_INT,@DEBIT_INT,@NEWDATEFR,@NEWDATETO,@EXEMPT_NOOFDAYS,@EXEMPT_NOOFDAYS_CR,@MIN_INTEREST,@MIN_BALANCE,@CREATEDATE,CREATEBY+'2',@UPDATEDATE,UPDATEBY,LEVEL_FLAG FROM V2_CLIENTPROFILES_NEW   
   WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL AND FROMDATE LIKE CONVERT(VARCHAR(11),@CHKDATEFR,109) + '%'  
   END    
     
  UPDATE V2_CLIENTPROFILES_NEW SET  
    CREDIT_INT = @CREDIT_INT,  
    DEBIT_INT = @DEBIT_INT,  
    TODATE = DATEDIFF(DAY, 1, @FROMDATE),  
    EXEMPT_NOOFDAYS = @EXEMPT_NOOFDAYS,  
    EXEMPT_NOOFDAYS_CR = @EXEMPT_NOOFDAYS_CR,  
    MIN_INTEREST = @MIN_INTEREST,  
    MIN_BALANCE = @MIN_BALANCE,  
    UPDATEDATE = @UPDATEDATE,  
    UPDATEBY = @ACTIONBY+'3'   
   WHERE   
    ACCOUNT_CODE = @ACCOUNT_CODE  
    AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL  
    AND FROMDATE LIKE CONVERT(VARCHAR(11),@CHKDATEFR,109) + '%'   
      
  IF @@ERROR = 0   
   BEGIN  
   SELECT 'APPLIED SLAB IS UPDATED SUCCESSFULLY..'  
   COMMIT TRAN  
   RETURN  
   END     
  END    
 ELSE  
  BEGIN  
  UPDATE V2_CLIENTPROFILES_NEW SET  
   CREDIT_INT = @CREDIT_INT,  
   DEBIT_INT = @DEBIT_INT,  
   --FROMDATE = @FROMDATE,  
   TODATE = @TODATE,  
   EXEMPT_NOOFDAYS = @EXEMPT_NOOFDAYS,  
   EXEMPT_NOOFDAYS_CR = @EXEMPT_NOOFDAYS_CR,  
   MIN_INTEREST = @MIN_INTEREST,  
   MIN_BALANCE = @MIN_BALANCE,  
   UPDATEDATE = @UPDATEDATE,  
   UPDATEBY = @ACTIONBY  
  WHERE   
   ACCOUNT_CODE = @ACCOUNT_CODE  
   AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL  
   AND @FROMDATE BETWEEN CONVERT(VARCHAR(11),FROMDATE,109) AND CONVERT(VARCHAR(11),TODATE,109)  
     
  IF @@ERROR = 0   
   BEGIN  
   SELECT 'APPLIED SLAB IS UPDATED SUCCESSFULLY..'  
   COMMIT TRAN  
   RETURN  
   END     
  END    
  END

GO
