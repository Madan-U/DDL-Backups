CREATE PROCEDURE [dbo].[GET_MARGIN_SHORTAGE]  
(  
 @FROM_DATE VARCHAR(11),        
 @TO_DATE VARCHAR(11),        
 @FROM_PARTY VARCHAR(10),        
 @TO_PARTY VARCHAR(10),
 @EXCHANGE VARCHAR(10),
 @SEGMENT VARCHAR(10)      
)  
AS  
  
IF LEN(@FROM_DATE) = 10  
BEGIN  
 SELECT @FROM_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @FROM_DATE, 103), 109)  
 SELECT @TO_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @TO_DATE, 103), 109)  
END  
  
  
SELECT   
 CLTCODE, DPC_DATE, WEEK_DAY = DATENAME(WEEKDAY,DPC_DATE), 
 LEDGER_BALANCE = CASE WHEN @SEGMENT = 'CAPITAL' THEN DPC_BALANCE ELSE LEDGER_BALANCE END, MARGIN_REQ, CASH_COLL = CASH_COLLATREAL, NONCASH_COLL = NONCASH_COLLATREAL,    
 SHORTAGE = CASE WHEN @SEGMENT = 'CAPITAL' THEN DPC_BALANCE ELSE LEDGER_BALANCE END + (CASE WHEN MARGIN_REQ - (CASH_COLLATREAL + NONCASH_COLLATREAL) > 0 THEN MARGIN_REQ - (CASH_COLLATREAL + NONCASH_COLLATREAL) ELSE 0 END),  
 NET_PENAL = 0  
FROM  
 DPC_CHARGES  
WHERE   
 CLTCODE BETWEEN @FROM_PARTY AND @TO_PARTY  
 AND DPC_DATE >= @FROM_DATE AND DPC_DATE <= @TO_DATE  
 AND EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT
 ORDER BY CLTCODE, DPC_DATE

GO
