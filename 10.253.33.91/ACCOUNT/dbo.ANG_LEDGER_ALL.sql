-- Object: PROCEDURE dbo.ANG_LEDGER_ALL
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROC [dbo].[ANG_LEDGER_ALL]
(@TODATE DATETIME,@CLTCODE VARCHAR(10))
AS 

DECLARE @FROMDATE DATETIME

SELECT @FROMDATE =SDTCUR FROM PARAMETER WHERE CURYEAR=1


SELECT * FROM (
SELECT CLTCODE,VDT,NARRATION,VTYP,VAMT,DRCR,'NSE' AS EXCHANGE FROM LEDGER (NOLOCK)  WHERE VDT >=@FROMDATE AND VDT <=@TODATE + ' 23:59' AND CLTCODE =@CLTCODE
UNION ALL
SELECT CLTCODE,VDT,NARRATION,VTYP,VAMT,DRCR,'BSE' AS EXCHANGE FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER WITH (NOLOCK)  WHERE VDT >=@FROMDATE AND VDT <=@TODATE + ' 23:59' AND CLTCODE =@CLTCODE
UNION ALL
SELECT CLTCODE,VDT,NARRATION,VTYP,VAMT,DRCR,'NSEFO' AS EXCHANGE FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WITH (NOLOCK)  WHERE VDT >=@FROMDATE AND VDT <=@TODATE + ' 23:59' AND CLTCODE =@CLTCODE
UNION ALL
SELECT CLTCODE,VDT,NARRATION,VTYP,VAMT,DRCR,'NSX' AS EXCHANGE FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER WITH (NOLOCK)  WHERE VDT >=@FROMDATE AND VDT <=@TODATE + ' 23:59' AND CLTCODE =@CLTCODE
UNION ALL
SELECT CLTCODE,VDT,NARRATION,VTYP,VAMT,DRCR,'MCX' AS EXCHANGE FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER WITH (NOLOCK)  WHERE VDT >=@FROMDATE AND VDT <=@TODATE + ' 23:59' AND CLTCODE =@CLTCODE
UNION ALL
SELECT CLTCODE,VDT,NARRATION,VTYP,VAMT,DRCR,'NCX' AS EXCHANGE FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER WITH (NOLOCK)  WHERE VDT >=@FROMDATE AND VDT <=@TODATE + ' 23:59' AND CLTCODE =@CLTCODE
UNION ALL
SELECT CLTCODE,VDT,NARRATION,VTYP,VAMT,DRCR,'MSX' AS EXCHANGE FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER WITH (NOLOCK)  WHERE VDT >=@FROMDATE AND VDT <=@TODATE + ' 23:59' AND CLTCODE =@CLTCODE
) A
ORDER BY CLTCODE,VDT

GO
