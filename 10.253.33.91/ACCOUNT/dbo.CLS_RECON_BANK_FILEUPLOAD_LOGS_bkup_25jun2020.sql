SET @@SQL = @@SQL + ",'" + @BANKCODE + "'"
SET @@SQL = @@SQL + ",'" + @BANKNAME + "'"
SET @@SQL = @@SQL + ",'" + @FILETYPE + "'"
SET @@SQL = @@SQL + ",'" + @BANKACCOUNT + "'"
SET @@SQL = @@SQL + ",'" + @UPLOADFILENAME + "'"
SET @@SQL = @@SQL + ",'" + @UPLOADEDDATE + "'"
