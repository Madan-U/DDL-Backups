SET  @DATE = GETDATE()

SELECT VDT,EXCHANGE,CLTCODE,SUM(VAMT)VAMT INTO #GL_BRK FROM LEDGER_ALL WITH (NOLOCK)
WHERE 
VDT =CONVERT(VARCHAR(10),GETDATE()-1,120)
AND CLTCODE IN ('520014','560011')
GROUP BY EXCHANGE,CLTCODE,VDT

 
DECLARE @COUNT INT
DECLARE @COUNT1 INT


DECLARE @BODYMSG NVARCHAR(MAX)    

DECLARE @SUBJECT NVARCHAR(MAX)    

DECLARE @TABLEHTML NVARCHAR(MAX)  


