CREATE PROC [dbo].[CLS_GETCATEGORY]
(
	@STATUSID VARCHAR(25) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'
)
AS
BEGIN
SELECT * FROM CATMAST WHERE ACCAT NOT IN(18,19,20) AND ACCAT = CASE WHEN @STATUSID IN ('TRADER', 'SUBBROKER', 'FAMILY', 'TRADER') THEN '4' ELSE ACCAT END
END

GO
