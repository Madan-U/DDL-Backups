-- Object: PROCEDURE dbo.CLS_RECON_EDTUPDATE
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------



CREATE PROCEDURE [dbo].[CLS_RECON_EDTUPDATE]
AS
BEGIN

			IF(SELECT COUNT(*) FROM LEDGER (NOLOCK)
			WHERE 	VDT>=DATEADD(DAY,-91,GETDATE())
			AND EDT LIKE 'DEC 31 2049%' AND VTYP=2)>0

			BEGIN 

				
				--SELECT * FROM LEDGER_LOG_BEFOREEDTBACKUP(NOLOCK)
			
					INSERT INTO LEDGER_LOG_BEFOREEDTBACKUP
					SELECT L.*
					,OPTCODE='',EDITDATE=GETDATE(),EDITNAME='JOB',STATUS='UNMATC'
					FROM LEDGER L(NOLOCK),LEDGER1 L1(NOLOCK) WHERE L.VNO=L1.VNO
					AND L.VTYP=L1.VTYP
					AND L.BOOKTYPE=L1.BOOKTYPE
					AND L1.RELDT<>''
					AND L.VDT>=DATEADD(DAY,-91,GETDATE())
					AND EDT LIKE 'DEC 31 2049%'
					AND L.VTYP in ('2', '3')

					--BEGIN TRAN
					UPDATE L SET EDT=RELDT FROM  LEDGER L(NOLOCK),LEDGER1 L1(NOLOCK) WHERE L.VNO=L1.VNO
					AND L.VTYP=L1.VTYP
					AND L.BOOKTYPE=L1.BOOKTYPE
					AND L1.RELDT<>''
					AND L.VDT>=DATEADD(DAY,-91,GETDATE())
					AND EDT LIKE 'DEC 31 2049%'
					AND L.VTYP in ('2', '3')
					--ROLLBACK

					


			END 

END

GO
