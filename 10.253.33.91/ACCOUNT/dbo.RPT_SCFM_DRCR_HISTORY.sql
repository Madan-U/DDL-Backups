-- Object: PROCEDURE dbo.RPT_SCFM_DRCR_HISTORY
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------



CREATE PROC [dbo].[RPT_SCFM_DRCR_HISTORY] (@FDATE VARCHAR(11),@TDATE VARCHAR(11))
 
AS  --- EXEC RPT_SCFM_DRCR 'JAN 19 2022','JAN 19 2022'
 

SELECT * INTO #DRCR FROM SCFM_REPORTING WHERE 1 = 2

IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')
BEGIN
INSERT INTO #DRCR               
SELECT REPORT_DATE,CLTCODE,BSECM,BSEFO,BSECD,NSECM,NSEFO,NSECD,MTF=ROUND(MTF,2),SLBS,MCXCD,MCDX,NCDX,NET_BALANCE=ROUND(NET_BALANCE,2),RUNDATE, NCE
FROM SCFM_REPORTING WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
END

IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING_LOG A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')
BEGIN
INSERT INTO #DRCR               
SELECT REPORT_DATE,CLTCODE,BSECM,BSEFO,BSECD,NSECM,NSEFO,NSECD,MTF=ROUND(MTF,2),SLBS,MCXCD,MCDX,NCDX,NET_BALANCE=ROUND(NET_BALANCE,2),RUNDATE, NCE
FROM SCFM_REPORTING_LOG WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
END

IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING_HISTORY A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')
BEGIN
INSERT INTO #DRCR               
SELECT REPORT_DATE,CLTCODE,BSECM,BSEFO,BSECD,NSECM,NSEFO,NSECD,MTF=ROUND(MTF,2),SLBS,MCXCD,MCDX,NCDX,NET_BALANCE=ROUND(NET_BALANCE,2),RUNDATE, NCE
FROM SCFM_REPORTING_HISTORY WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
END

SELECT [Report Date]=CONVERT(VARCHAR(11),REPORT_DATE,105),Report='MTF Creditors',
[Balance in Cr.]=SUM(MTF)/10000000,[Balance in Rs.]=SUM(MTF)
FROM #DRCR WITH (NOLOCK) WHERE MTF > 0 GROUP BY REPORT_DATE
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='MTF Debtors',
[Balance in Cr.]=SUM(MTF)/10000000,[Balance in Rs.]=SUM(MTF)
FROM #DRCR WHERE MTF < 0 GROUP BY REPORT_DATE
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Broking Creditors',
[Balance in Cr.]=SUM(NET_BALANCE-MTF)/10000000,[Balance in Rs.]=SUM(NET_BALANCE-MTF)
FROM #DRCR WHERE (NET_BALANCE-MTF) > 0 GROUP BY REPORT_DATE
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Broking Debtors',
[Balance in Cr.]=SUM(NET_BALANCE-MTF)/10000000,[Balance in Rs.]=SUM(NET_BALANCE-MTF)
FROM #DRCR WHERE (NET_BALANCE-MTF) < 0 GROUP BY REPORT_DATE
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Total Net Creditors',
[Balance in Cr.]=SUM(NET_BALANCE)/10000000,[Balance in Rs.]=SUM(NET_BALANCE)
FROM #DRCR WHERE NET_BALANCE > 0 GROUP BY REPORT_DATE
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Total Net Debtors',
[Balance in Cr.]=SUM(NET_BALANCE)/10000000,[Balance in Rs.]=SUM(NET_BALANCE)
FROM #DRCR WHERE NET_BALANCE < 0 GROUP BY REPORT_DATE

GO
