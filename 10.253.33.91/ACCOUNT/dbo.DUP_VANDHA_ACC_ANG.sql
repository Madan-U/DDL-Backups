CREATE PROC DUP_VANDHA_ACC_ANG(@FROMDATE VARCHAR(11))

AS

SET @FROMDATE = GETDATE() -30

DECLARE @TODATE VARCHAR(11)  = GETDATE()

SELECT CLTCODE = CONVERT(VARCHAR(10),''),CONVERT(VARCHAR(11),VDT,105) AS VDT,GLCODE = CLTCODE,VNO,DRCR,VAMT,EXCHANGE,VTYP,BOOKTYPE 
INTO #LEDGER
FROM LEDGER_ALL WITH (NOLOCK) WHERE VDT BETWEEN @FROMDATE AND @TODATE + ' 23:59:59' 
AND CLTCODE IN ('V59797','V63583','V59100','V27391','V1496',
'V45439','V14224','V1550','V59099','V8499',
'V48862','V59101','V7497','V39256','V5348','V36028','V35278','V37421',
'V63467','V76494','V59931','V56993','V12668','R56139','V3964','V35345',
'V10933','CHND573','V611','VZ998','V76495','VZ999','VZ997','2VZ998','2VZ997','2VZ999','VR49','2V63583',
'2V76495','2V1550','2V63467','2V59101','2V59099','2V59100','2V48862','2V27391','2V76494','2V59212')
AND VTYP= 8

UPDATE #LEDGER SET CLTCODE = L.CLTCODE FROM LEDGER_ALL L WITH (NOLOCK) WHERE
L.VNO = #LEDGER.VNO AND L.VTYP = #LEDGER.VTYP AND L.BOOKTYPE = #LEDGER.BOOKTYPE AND L.CLTCODE <> #LEDGER.GLCODE
AND #LEDGER.EXCHANGE =L.EXCHANGE

SELECT CLTCODE,VAMT,EXCHANGE,COUNT(1) AS DUP INTO #CHECK FROM #LEDGER GROUP BY CLTCODE,VAMT,EXCHANGE HAVING COUNT(1) > 1 

SELECT #LEDGER.* FROM #LEDGER , #CHECK WHERE #LEDGER.CLTCODE = #CHECK.CLTCODE AND #LEDGER.VAMT = #CHECK.VAMT
AND #LEDGER.EXCHANGE = #CHECK.EXCHANGE

DROP TABLE #LEDGER
DROP TABLE #CHECK

GO
