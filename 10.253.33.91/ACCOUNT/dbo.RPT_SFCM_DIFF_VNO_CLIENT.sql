-- Object: PROCEDURE dbo.RPT_SFCM_DIFF_VNO_CLIENT
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROC RPT_SFCM_DIFF_VNO_CLIENT (@TDATE DATETIME)
 
 AS  --- EXEC RPT_SFCM_DIFF_VNO_CLIENT 'MAY  7 2021'
 
 /* Client Wise Diff Details */

 SELECT CLTCODE,REPORT_DATE,SUM(BALANCE) AS BALANCE  INTO #SFCMT FROM SFCM_REPORTING A,MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)     
 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,REPORT_DATE
 
 SELECT CLTCODE,REPORT_DATE,SUM(BALANCE) AS BALANCE  INTO #SFCMP FROM SFCM_REPORTING_LOG A,MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)
 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,REPORT_DATE
 
 SELECT A.REPORT_DATE,A.CLTCODE,TDAYBALANCE=A.BALANCE,PDAYBALANCE=B.BALANCE,
 DIFF_AMOUNT = ABS(A.BALANCE) - ABS(B.BALANCE) INTO #DIFF
 FROM #SFCMT A WITH (NOLOCK),#SFCMP B WITH (NOLOCK)
 WHERE A.CLTCODE = B.CLTCODE AND A.REPORT_DATE = B.REPORT_DATE
 AND A.BALANCE <> B.BALANCE
 
 
 /* VNO & Client Wise Diff Details */


 SELECT CLTCODE,REPORT_DATE,VTYP,EXCHANGE,SUM(BALANCE) AS BALANCE  INTO #TDAYBALANCE FROM SFCM_REPORTING A,MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)     
 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,REPORT_DATE,VTYP,EXCHANGE
 
 SELECT CLTCODE,REPORT_DATE,VTYP,EXCHANGE,SUM(BALANCE) AS BALANCE  INTO #PDAYBALANCE FROM SFCM_REPORTING_LOG A,MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)
 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,REPORT_DATE,VTYP,EXCHANGE
 
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,VTYPE=TVTYPE,EXCHANGE=TEXCHANGE,
TBALANCE=ROUND(TBALANCE,2),PBALANCE=ROUND(PBALANCE,2),DIFF_AMOUNT=ROUND(DIFF_AMOUNT,2) FROM (
SELECT T.REPORT_DATE,T.CLTCODE,TVTYPE=ISNULL(T.VTYP,P.VTYP),TEXCHANGE=ISNULL(T.EXCHANGE,P.EXCHANGE),
TBALANCE=ISNULL(T.BALANCE,0),PVTYP=ISNULL(P.VTYP,T.VTYP),PEXCHANGE=ISNULL(P.EXCHANGE,T.EXCHANGE),PBALANCE=ISNULL(P.BALANCE,0),
DIFF_AMOUNT = ABS(ISNULL(T.BALANCE,0)) - ABS(ISNULL(P.BALANCE,0))
FROM #TDAYBALANCE T LEFT OUTER JOIN #PDAYBALANCE P 
ON (T.CLTCODE = P.CLTCODE AND T.REPORT_DATE = P.REPORT_DATE AND T.VTYP = P.VTYP AND T.EXCHANGE = P.EXCHANGE)
--WHERE T.CLTCODE='A91836' 
UNION
SELECT T.REPORT_DATE,T.CLTCODE,TVTYPE=ISNULL(T.VTYP,P.VTYP),TEXCHANGE=ISNULL(T.EXCHANGE,P.EXCHANGE),
TBALANCE=ISNULL(T.BALANCE,0),PVTYP=ISNULL(P.VTYP,T.VTYP),PEXCHANGE=ISNULL(P.EXCHANGE,T.EXCHANGE),PBALANCE=ISNULL(P.BALANCE,0),
DIFF_AMOUNT = ABS(ISNULL(T.BALANCE,0)) - ABS(ISNULL(P.BALANCE,0))
FROM #TDAYBALANCE T RIGHT OUTER JOIN #PDAYBALANCE P 
ON (T.CLTCODE = P.CLTCODE AND T.REPORT_DATE = P.REPORT_DATE AND T.VTYP = P.VTYP AND T.EXCHANGE = P.EXCHANGE)
--WHERE T.CLTCODE='A91836' 
) A WHERE DIFF_AMOUNT <> 0 AND CLTCODE IN (SELECT DISTINCT CLTCODE FROM #DIFF )
ORDER BY 2,3

GO
