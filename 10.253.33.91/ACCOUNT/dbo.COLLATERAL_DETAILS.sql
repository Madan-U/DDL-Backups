--COLLATERAL_DETAILS 'JUL 21 2015' , 'JUL 22 2015'    
    
CREATE proc COLLATERAL_DETAILS     
(    
@FROMDATE VARCHAR (11),    
@TODATE   VARCHAR (11)    
)    
AS    
BEGIN SELECT  * INTO #TEMP FROM(    
SELECT *,'NSEFO'AS EXCHANGE  FROM [AngelFO].nsefo.dbo.TBL_COLLATERAL_MARGIN  WHERE EFFDATE>=@FROMDATE    
AND EFFDATE<=@TODATE     
    
UNION ALL    
    
SELECT *,'NSECURFO' AS EXCHANGE FROM [AngelFO].NSECURFO.dbo.TBL_COLLATERAL_MARGIN  WHERE EFFDATE>=@FROMDATE    
AND EFFDATE<=@TODATE     
    
UNION ALL    
    
SELECT *,'MCDX'AS EXCHANGE FROM [AngelCommodity].MCDX.DBO.TBL_COLLATERAL_MARGIN  WHERE EFFDATE>=@FROMDATE    
AND EFFDATE<=@TODATE     
    
UNION ALL    
    
SELECT *,'MCDXCDS' AS EXCHANGE FROM [AngelCommodity].MCDXCDS.DBO.TBL_COLLATERAL_MARGIN  WHERE EFFDATE>=@FROMDATE    
AND EFFDATE<=@TODATE     
    
UNION ALL    
    
SELECT *,'NCDX' AS EXCHANGE FROM [AngelCommodity].NCDX.DBO.TBL_COLLATERAL_MARGIN  WHERE EFFDATE>=@FROMDATE    
AND EFFDATE<=@TODATE     
    
)A    
    
CREATE CLUSTERED INDEX IDX_CL ON #TEMP     
(    
EFFDATE    
)    
    
SELECT * FROM #TEMP ORDER BY EXCHANGE    
    
END

GO
