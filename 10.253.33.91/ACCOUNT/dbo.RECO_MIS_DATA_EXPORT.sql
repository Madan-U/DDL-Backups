CREATE PROCEDURE RECO_MIS_DATA_EXPORT ( @REPORTDATE VARCHAR(11))

AS --- EXEC RECO_MIS_DATA_EXPORT 'JUN  1 2021'

DECLARE @N AS VARCHAR(2) = '0'
DECLARE @path AS VARCHAR(200) =  'J:\BACKOFFICE\EXPORT\'
DECLARE @deleteSql AS VARCHAR(512)
  
SET @deleteSql = 'forfiles /P "' + @path + '" /S /M *.CSV /D -' + @N + ' /C "cmd /c del @PATH"'


DECLARE @MISII VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'RECO_DATA_MISI_MISII_' + CONVERT(VARCHAR, CONVERT(DATETIME,@REPORTDATE), 112) + '.CSV'

DECLARE @MISIISP NVARCHAR(4000) = 'EXEC [ACCOUNT].[DBO].[RPT_RECO_DATA_MISI_MISII]  ''' + @REPORTDATE + ''''

DECLARE @MISIIBCP VARCHAR(4000) = 'BCP "' + @MISIISP + '" QUERYOUT "' + @MISII + '" -c -t"," -r"\n" -T'

EXEC MASTER..XP_CMDSHELL @MISIIBCP, NO_OUTPUT


DECLARE @MISIII VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'RECO_DATA_MISIII_' + CONVERT(VARCHAR, CONVERT(DATETIME,@REPORTDATE), 112) + '.CSV'

DECLARE @MISIIISP NVARCHAR(4000) = 'EXEC [ACCOUNT].[DBO].[RPT_RECO_DATA_MISIII]  ''' + @REPORTDATE + ''''

DECLARE @MISIIIBCP VARCHAR(4000) = 'BCP "' + @MISIIISP + '" QUERYOUT "' + @MISIII + '" -c -t"," -r"\n" -T'

EXEC MASTER..XP_CMDSHELL @MISIIIBCP, NO_OUTPUT


DECLARE @MISIV VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'RECO_DATA_MISIV_' + CONVERT(VARCHAR, CONVERT(DATETIME,@REPORTDATE), 112) + '.CSV'

DECLARE @MISIVSP NVARCHAR(4000) = 'EXEC [ACCOUNT].[DBO].[RPT_RECO_DATA_MISIV]  ''' + @REPORTDATE + ''''

DECLARE @MISIVBCP VARCHAR(4000) = 'BCP "' + @MISIVSP + '" QUERYOUT "' + @MISIV + '" -c -t"," -r"\n" -T'

EXEC MASTER..XP_CMDSHELL @MISIVBCP, NO_OUTPUT

GO
