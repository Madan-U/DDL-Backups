-- Object: PROCEDURE dbo.COMB_RECO_MIS_DETAIL
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROC COMB_RECO_MIS_DETAIL (@DATEFROM DATETIME = 'OCT 12 2022')        
AS        

SELECT  VDT,DDNO,BANKCODE,SEGMENT,DRAMT,        
CRAMT,RELDT,REFNO,CLTCODE,VNO,VTYP,RECODATE INTO #REC_BANK_RECO_ANG_ALL FROM REC_BANK_RECO_ANG_ALL A WITH (NOLOCK) WHERE NOT EXISTS (        
SELECT * FROM REC_BANK_RECO_ANG_ALL B WITH (NOLOCK) WHERE  VTYP = 3      AND A.VTYP = B.VTYP  
AND RECODATE= VDT AND RELDT > VDT AND A.VNO = B.VNO ) --AND BANKCODE NOT IN ('02019')        
UNION ALL         
SELECT VDT,DDNO,BANKCODE,SEGMENT,DRAMT,CRAMT,RELDT,REFNO,CLTCODE,VNO,VTYP,RECODATE         
FROM REC_BANK_RECO_ANG_ALL B WITH (NOLOCK) WHERE  VTYP = 3 AND RECODATE= VDT AND RELDT > VDT         
        
ALTER TABLE #REC_BANK_RECO_ANG_ALL ADD EDT DATETIME         
        
UPDATE A SET A.EDT = B.EDT FROM LEDGER  B WITH (NOLOCK),#REC_BANK_RECO_ANG_ALL A WHERE A.CLTCODE = B.CLTCODE        
AND A.VNO  = B.VNO AND A.VTYP = B.VTYP AND A.VDT = B.VDT        
        
--UPDATE #REC_BANK_RECO_ANG_ALL SET  CRAMT = DRAMT,DRAMT = 0  WHERE  VTYP = 3        
        
UPDATE #REC_BANK_RECO_ANG_ALL SET  CRAMT = DRAMT,DRAMT = CRAMT       
      
UPDATE #REC_BANK_RECO_ANG_ALL SET  CRAMT = DRAMT,DRAMT = CRAMT WHERE VDT <> RELDT AND RELDT <> '1900-01-01 00:00:00.000' AND VTYP = 3      
  
UPDATE #REC_BANK_RECO_ANG_ALL SET  CRAMT = DRAMT,DRAMT = CRAMT WHERE VDT <> RELDT AND RELDT = '1900-01-01 00:00:00.000' AND VTYP = 3      
      
SELECT CONVERT(VARCHAR(11),B.VDT,105) AS VDT,        
DDNO,DETAILS,B.BANKCODE,B.SEGMENT,B.DRAMT AS [CHQ. ISSUED BUT NOT DEBITED],        
B.CRAMT AS [CHQ. DEPOSITED BUT NOT CREDITED],CONVERT(VARCHAR(11),B.RELDT,105) AS RELDT,REFNO,        
B.CLTCODE,D.longname,VNO,VTYP,CONVERT(VARCHAR(11),B.EDT,105) AS EDT         
FROM          
BANK_RECO_BALANCE_ANG  A WITH (NOLOCK), #REC_BANK_RECO_ANG_ALL B , BANK_RECO_MASTER_ANG C   WITH (NOLOCK) , ACMAST D WITH (NOLOCK)    
WHERE A.BANKCODE = B.BANKCODE AND A.SEGMENT = 'NSE'         
AND C.BANKCODE = A.BANKCODE AND C.[TYPE OF ACCOUNT] LIKE '%CLIENT%'         
AND A.SEGMENT = B.SEGMENT AND C.SEGMENT = B.SEGMENT        
AND D.cltcode =B.CLTCODE    
        
DROP TABLE #REC_BANK_RECO_ANG_ALL

GO
