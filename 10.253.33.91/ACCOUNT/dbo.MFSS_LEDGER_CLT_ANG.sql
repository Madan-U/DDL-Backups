--------------------------------------------------

CREATE PROC MFSS_LEDGER_CLT_ANG (@TODATE DATETIME,@CLTCODE VARCHAR(10))    
    
AS    
  
DECLARE @FROMDATE DATETIME,@CLINAME VARCHAR(150)  

-- EXEC MFSS_LEDGER_CLT_ANG 'SEP 21 2022','D476158' 
  
SELECT @FROMDATE = SDTCUR FROM ANGELFO.BBO_FA.DBO.PARAMETER WITH(NOLOCK) WHERE @TODATE BETWEEN SDTCUR AND LDTCUR  

SELECT VDATE=CONVERT(VARCHAR(11),VDT,103),--(CASE WHEN VTYPE <>18 THEN CONVERT(VARCHAR(11),VDT,103) ELSE '' END),  
EDATE=(CASE WHEN VTYPE <>18 THEN CONVERT(VARCHAR(11),EDT,103) ELSE '' END),VTYPE =(CASE WHEN VTYPE=15 THEN 'BILLS'    
WHEN VTYPE=8 THEN 'JOURNAL VOUCHER'    
WHEN VTYPE=2 THEN 'RECEIPT BANK'    
WHEN VTYPE=3 THEN 'PAYMENT BANK'    
WHEN VTYPE=18 THEN 'OPENING BALANCE'    
WHEN VTYPE=88 THEN 'INTJV' END )    
,VNO =(CASE WHEN VTYPE=18 THEN '' ELSE VNO END  ),NARRATION,CHQNO='',DRAMOUNT,CRAMOUNT,SUM(-DRAMOUNT+CRAMOUNT) OVER(ORDER BY VDT    
ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW)   
AS RUNNINGTOTAL  ,CLTCODE,SRNO=1  INTO #FINAL
FROM ANGELFO.BBO_FA.DBO.MFSS_LEDGER_BSE B WITH(NOLOCK) WHERE VDT BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'   
AND CLTCODE = @CLTCODE    
--AND NOT EXISTS (SELECT VNO FROM #VTYP V WHERE V.VNO=B.VNO AND V.VTYPE =B.VTYPE)    
UNION ALL    
SELECT VDATE=CONVERT(VARCHAR(10),@TODATE,103),EDATE='',VTYP='CLSBAL','','CLOSING BALANCE','',
DRAMOUNT=(CASE WHEN SUM(DRAMOUNT-CRAMOUNT)>0 THEN SUM(DRAMOUNT-CRAMOUNT) ELSE 0 END),    
CRAMOUNT =(CASE WHEN SUM(DRAMOUNT-CRAMOUNT)<0 THEN SUM(DRAMOUNT-CRAMOUNT)*-1 ELSE 0 END),SUM(-DRAMOUNT+CRAMOUNT) ,CLTCODE,SRNO=2     
FROM ANGELFO.BBO_FA.DBO.MFSS_LEDGER_BSE B WITH(NOLOCK) WHERE VDT BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'  AND CLTCODE = @CLTCODE    
--AND NOT EXISTS (SELECT VNO FROM #VTYP V WHERE V.VNO=B.VNO AND V.VTYPE =B.VTYPE)    
GROUP BY CLTCODE  


SELECT VDATE,EDATE,VTYPE,VNO,NARRATION,CHQNO,CONVERT(VARCHAR(100),
DRAMOUNT) AS DRAMOUNT,CONVERT(VARCHAR(100),CRAMOUNT) AS CRAMOUNT,CONVERT(VARCHAR(100),RUNNINGTOTAL) AS RUNNINGTOTAL
INTO #FINAL1 FROM #FINAL ORDER BY SRNO ASC,VDATE ASC,VTYPE DESC  

SELECT  @CLINAME = LONG_NAME FROM MSAJAG.DBO.CLIENT_DETAILS WITH (NOLOCK) WHERE CL_CODE = @CLTCODE

SELECT 'CLTCODE : ',@CLTCODE,'CLIENT NAME : ',@CLINAME,'STATEMENT FOR',' PERIOD :',CONVERT(VARCHAR(10),@FROMDATE,103),' TO ',CONVERT(VARCHAR(10),@TODATE,103)
UNION ALL
SELECT '','','','','','','','',''
UNION ALL
SELECT 'VDATE','EDATE','VTYPE','VNO','NARRATION','CHQNO','DRAMOUNT','CRAMOUNT','RUNNINGTOTAL'
UNION ALL
SELECT VDATE,EDATE,VTYPE,VNO,NARRATION,CHQNO,DRAMOUNT,CRAMOUNT,RUNNINGTOTAL FROM  #FINAL1


DROP TABLE #FINAL
DROP TABLE #FINAL1

GO
