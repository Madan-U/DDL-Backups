--RECEIPT_INFO 'apr  1 2015','aug 31 2015'


CREATE PROC [dbo].[RECEIPT_INFO]  ( @FROMDATE varchar(11) ,                         
@TODATE   varchar(11)  )
AS                          
 
 
SELECT CLTCODE,LONG_NAME,BRANCH_CD,(CASE WHEN SUM(BALANCE)<=0 THEN SUM(BALANCE)*-1 ELSE 0 END)DEBIT,
 (CASE WHEN SUM(BALANCE)>=0 THEN SUM(BALANCE)ELSE 0 END)CREDIT ,EXCHANGE INTO #TEMP  FROM(
SELECT CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'NSE' AS EXCHANGE 
FROM LEDGER WHERE  VDT >=@FROMDATE  AND  VDT <=@TODATE + ' 23:59'  AND VTYP =2
GROUP BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'BSE' AS EXCHANGE 
FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER WHERE 
 VDT >=@FROMDATE   AND  VDT <=@TODATE + ' 23:59'  AND VTYP =2
GROUP BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'NSEFO' AS EXCHANGE 
FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WHERE 
 VDT >=@FROMDATE   AND  VDT <=@TODATE + ' 23:59'  AND VTYP =2
GROUP BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'MCX' AS EXCHANGE 
FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER WHERE 
VDT >=@FROMDATE   AND  VDT <=@TODATE + ' 23:59'  AND VTYP =2
GROUP BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'NCDX' AS EXCHANGE 
FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER WHERE 
VDT >=@FROMDATE   AND  VDT <=@TODATE + ' 23:59'  AND VTYP =2
GROUP BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'NSECURFO' AS EXCHANGE 
FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER WHERE  VDT >=@FROMDATE 
GROUP BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'BSEFO' AS EXCHANGE 
FROM ANGELCOMMODITY.ACCOUNTBFO.DBO.LEDGER WHERE 
 VDT >=@FROMDATE   AND  VDT <=@TODATE + ' 23:59'  AND VTYP =2
GROUP BY CLTCODE
UNION ALL
SELECT CLTCODE,SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END)BALANCE,'MCXURFO' AS EXCHANGE 
FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER WHERE VDT >=@FROMDATE   AND  VDT <=@TODATE + ' 23:59'  AND VTYP =2
GROUP BY CLTCODE)A,MSAJAG.dbo.CLIENT_DETAILS
WHERE CLTCODE=cl_code
GROUP BY CLTCODE,long_name,branch_cd,EXCHANGE

SELECT CLTCODE,long_name,branch_cd,
ISNULL(NSE,0) AS NSE,
ISNULL(BSE,0) AS BSE,
ISNULL(NSEFO,0) AS NSEFO,
ISNULL(NSECURFO,0) AS NSECURFO,
ISNULL(MCX,0) AS MCX,
ISNULL(NCDX,0) AS NCDX,
ISNULL(BSEFO,0) AS BSEFO,ISNULL([MCXURFO],0) AS MCXCURFO,
(ISNULL(NSE,0)+ISNULL(BSE,0)+ISNULL(NSEFO,0)+ISNULL(NSECURFO,0) +ISNULL(MCX,0)+
ISNULL(NCDX,0)+ISNULL(BSEFO,0)+ISNULL(BSEFO,0)+ISNULL([MCXURFO],0) ) AS TOTAL
FROM (
    SELECT 
      CLTCODE,long_name,branch_cd,CREDIT,EXCHANGE
    FROM #TEMP
) as s
PIVOT
(
    SUM(CREDIT)
    FOR [EXCHANGE] IN ([NSE], [BSE], [NSEFO], [NSECURFO], 
    [MCX], [NCDX], [BSEFO], [MCXURFO])
)AS piv;

GO
