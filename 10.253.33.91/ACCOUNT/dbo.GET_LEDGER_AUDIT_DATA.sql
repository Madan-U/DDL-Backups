CREATE PROCEDURE [dbo].[GET_LEDGER_AUDIT_DATA]      
 @EFFDATE VARCHAR(11),    
 @SESSIONID VARCHAR(500),    
 @PROCESS_CODE VARCHAR(10),    
 @REPORT_FORMULA VARCHAR(MAX),  
 @EXCHANGE VARCHAR(10),  
 @SEGMENT VARCHAR(50),
 @TABLE_NAME VARCHAR(20) = '' 
AS      
    
DECLARE      
 @START_DATE VARCHAR(11)      
      
IF LEN(@EFFDATE) = 10      
BEGIN      
 SELECT @EFFDATE =  CONVERT(DATETIME, @EFFDATE, 103)     
END      
      
SELECT @START_DATE =  SDTCUR 
FROM PARAMETER      
WHERE @EFFDATE BETWEEN SDTCUR AND LDTCUR    

Declare @PAYDT Datetime
SELECT @PAYDT =  SDTCUR -10
FROM PARAMETER      
WHERE @EFFDATE BETWEEN SDTCUR AND LDTCUR 
    
    
--CREATE TABLE #REPORT_DATA    
