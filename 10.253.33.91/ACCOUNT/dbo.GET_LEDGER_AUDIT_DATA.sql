CREATE TABLE #LEDGER   
(    
 CLTCODE VARCHAR(10),    
 VDTBAL MONEY,      
 EDTBAL MONEY,      
 FDT_RECEIPT MONEY,      
 FDT_PAYMENTS MONEY  
)    

    
DECLARE    
 @@SQL VARCHAR(MAX)  
 
 INSERT INTO #LEDGER    
SELECT       
 CLTCODE = L.CLTCODE,    
 VDTBAL = SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END),---SUM(CASE WHEN VDT >= @START_DATE THEN (CASE DRCR WHEN 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END),      
 --EDTBAL = SUM(CASE WHEN EDT >= @START_DATE AND EDT <= @EFFDATE + ' 23:59' THEN (CASE DRCR WHEN 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END       
 --  + CASE WHEN EDT >= @START_DATE AND VDT < @START_DATE THEN (CASE DRCR WHEN 'C' THEN VAMT ELSE -VAMT END) ELSE 0 END ), 
 EDTBAL =0,
 FDT_RECEIPT = 0 ,      
 FDT_PAYMENTS =0
 ---INTO #LEDGER
FROM       
 LEDGER L (NOLOCK) WHERE VDT >=@START_DATE AND VDT <= @EFFDATE + ' 23:59'  
 GROUP BY CLTCODE /*    
 LEFT OUTER JOIN (SELECT RELDT = (CASE WHEN RELDT = '' THEN CONVERT(DATETIME,'DEC 31 2049 23:59') ELSE RELDT END), 
						    VTYP,VNO,LNO,BOOKTYPE FROM LEDGER1 WHERE (RELDT > @EFFDATE + ' 23:59' OR RELDT = '')) L1  
 ON L.VTYP = L1.VTYP AND L.BOOKTYPE = L1.BOOKTYPE AND L.VNO = L1.VNO AND L.LNO = L1.LNO,  */
 
 INSERT INTO  #LEDGER
 SELECT       
 CLTCODE = L.CLTCODE,    
 VDTBAL = 0,---SUM(CASE WHEN VDT >= @START_DATE THEN (CASE DRCR WHEN 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END),      
 --EDTBAL = SUM(CASE WHEN EDT >= @START_DATE AND EDT <= @EFFDATE + ' 23:59' THEN (CASE DRCR WHEN 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END       
 --  + CASE WHEN EDT >= @START_DATE AND VDT < @START_DATE THEN (CASE DRCR WHEN 'C' THEN VAMT ELSE -VAMT END) ELSE 0 END ), 
 EDTBAL =0,
 FDT_RECEIPT = SUM(CASE WHEN L.VTYP = 2 THEN (VAMT) ELSE 0 END) ,      
 FDT_PAYMENTS = SUM(CASE WHEN L.VTYP = 3 THEN (VAMT) ELSE 0 END) 

FROM       
 LEDGER L (NOLOCK) WHERE VDT >=@PAYDT  AND VDT <= @EFFDATE  + ' 23:59' AND EDT >  @EFFDATE  + ' 23:59'   AND VTYP IN(2,3)
 GROUP BY CLTCODE

 CREATE INDEX #CLTCODE ON #LEDGER(CLTCODE)
 
 --INSERT INTO #REPORT_DATA      
SELECT       
 L.CLTCODE,    
 PARTY_NAME,       
 ENTITY_LEVEL,    
 ENTITY_CODE,      
 VDTBAL = SUM(VDTBAL),      
 EDTBAL = SUM(EDTBAL),      
 FDT_RECEIPT = SUM(FDT_RECEIPT),      
 FDT_PAYMENTS = SUM(FDT_PAYMENTS),  
 MARGIN_BAL = 0  INTO #REPORT_DATA1      
FROM      
 #LEDGER L (Nolock)  ,
 FORMULA_CLIENT_MASTER A    (Nolock)  
WHERE      
 A.CLTCODE = L.CLTCODE AND SESSION_ID = @SESSIONID    
 GROUP BY       
 L.CLTCODE, PARTY_NAME, ENTITY_LEVEL, ENTITY_CODE --, L1.RELDT      
      
--HAVING SUM(VDTBAL) <> 0 AND SUM(EDTBAL) <> 0 
