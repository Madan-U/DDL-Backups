IF @TABLE_NAME <> ''
BEGIN
	SET @@SQL = " TRUNCATE TABLE " + @TABLE_NAME  
	EXEC (@@SQL)
END

IF @TABLE_NAME <> ''
   SET @@SQL = " INSERT INTO " + @TABLE_NAME + " SELECT "    
ELSE
SET @@SQL = " SELECT "   
 
SET @@SQL = @@SQL + " CLTCODE, "    
SET @@SQL = @@SQL + " PARTY_NAME, "    
SET @@SQL = @@SQL + " ENTITY_LEVEL,"    
SET @@SQL = @@SQL + " ENTITY_CODE, "    
