-- Object: PROCEDURE dbo.CLS_GETFINANCIALYEARS
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

--sp_helptext cls_getfinancialyears 

CREATE PROCEDURE [dbo].[CLS_GETFINANCIALYEARS]

AS

BEGIN

SELECT SDTCUR = CONVERT(VARCHAR(10), SDTCUR, 103)

, LDTCUR = CONVERT(VARCHAR(10), LDTCUR, 103)

, SDTCUR_11 = CONVERT(VARCHAR(11), SDTCUR, 109)

, LDTCUR_11 = CONVERT(VARCHAR(11), LDTCUR, 109)

, FLDAUTO

, CURYEAR

, VALIDLDT = CASE WHEN GETDATE() < LDTCUR THEN CONVERT(VARCHAR(10), GETDATE(), 103) ELSE CONVERT(VARCHAR(10), LDTCUR, 103) END

, REPORTDAYS

FROM .DBO.PARAMETER 

--WHERE CURYEAR=1

ORDER BY SDTCUR



END

GO
