CREATE PROC [dbo].[NRI_CONTRACT_SUMMARY_ANG_BKP_20JAN2025_SRE_33265] (@SAUDA_DATE VARCHAR(11))    
AS    
  
--EXEC NRI_CONTRACT_SUMMARY_ANG 'FEB  6 2023','A102556'  
   
SELECT DISTINCT CL_CODE,SUB_BROKER INTO #CLIENTDATA FROM (  
SELECT DISTINCT CL_CODE,SUB_BROKER FROM MSAJAG.DBO.CLIENT_DETAILS WITH (NOLOCK) WHERE PARTY_CODE BETWEEN 'ZR001' AND 'ZRR999'  
UNION ALL  
SELECT DISTINCT CL_CODE,SUB_BROKER FROM MSAJAG.DBO.CLIENT_DETAILS WITH (NOLOCK) WHERE SUB_BROKER IN ('BHKWT','BH') ) AS A  
  
SELECT DISTINCT CONVERT(VARCHAR(11),SAUDA_DATE,105) AS SAUDA_DATE,EXCHANGE='NSE',SETT_TYPE,SETT_NO,PARTY_CODE,#CLIENTDATA.SUB_BROKER,CONTRACTNO,BILLPOSTED = 'N',INHOUSE = 'N'   
INTO #CONTRACTDATA  
FROM MSAJAG.DBO.CMBILLVALAN S WITH (NOLOCK) ,#CLIENTDATA WHERE   PARTY_CODE = CL_CODE AND SAUDA_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59'  
UNION ALL  
SELECT DISTINCT CONVERT(VARCHAR(11),SAUDA_DATE,105) AS SAUDA_DATE,EXCHANGE='BSE',SETT_TYPE,SETT_NO,PARTY_CODE,#CLIENTDATA.SUB_BROKER,CONTRACTNO,BILLPOSTED = 'N',INHOUSE = 'N'   
FROM AngelBSECM.BSEDB_AB.DBO.CMBILLVALAN S WITH (NOLOCK) ,#CLIENTDATA WHERE   PARTY_CODE = CL_CODE AND SAUDA_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59'  
  
SELECT DISTINCT CLTCODE,BILLPOSTED1 = 'Y' INTO #BILLPOST FROM LEDGER WITH (NOLOCK) , #CLIENTDATA WHERE VDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59'  
AND CLTCODE = CL_CODE  
AND VTYP = 15  


SELECT  DISTINCT CONVERT(VARCHAR(11),SAUDA_DATE,105) AS SAUDA_DATE,SEGMENT,PARTY_CODE,CONTRACTNO,INHOUSE1 = 'Y' INTO #TBL_NRITRADE_REPORTS
FROM INTRANET.RISK.DBO.TBL_NRITRADE_REPORTS WITH (NOLOCK) WHERE SAUDA_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59' 
  
UPDATE #CONTRACTDATA SET BILLPOSTED = BILLPOSTED1 FROM #BILLPOST WHERE CLTCODE = PARTY_CODE  


UPDATE #CONTRACTDATA SET INHOUSE = INHOUSE1 FROM #TBL_NRITRADE_REPORTS WHERE #CONTRACTDATA.PARTY_CODE = #TBL_NRITRADE_REPORTS.PARTY_CODE AND EXCHANGE = SEGMENT AND 
#CONTRACTDATA.CONTRACTNO = #TBL_NRITRADE_REPORTS.CONTRACTNO 
  
SELECT * FROM #CONTRACTDATA WHERE CONTRACTNO <> '0'  
ORDER BY EXCHANGE,SETT_TYPE,SUB_BROKER,PARTY_CODE  
  
DROP TABLE #CLIENTDATA  
DROP TABLE #CONTRACTDATA  
DROP TABLE #BILLPOST
DROP TABLE #TBL_NRITRADE_REPORTS

GO
