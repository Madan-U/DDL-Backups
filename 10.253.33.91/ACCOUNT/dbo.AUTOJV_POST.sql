-- Object: PROCEDURE dbo.AUTOJV_POST
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROC [dbo].[AUTOJV_POST]      
AS      
      
DECLARE @REFNO VARCHAR(50),      
  @POSTCUR CURSOR      
      
SET @POSTCUR = CURSOR FOR      
 SELECT DISTINCT REFNO FROM TBL_AUTO_JV_UPLOAD  WHERE  POSTFLAG = 0      
 AND REFNO NOT IN (SELECT REFNO FROM TBL_JV_LOG      
  WHERE PROESSDATE LIKE LEFT(GETDATE(),11) + '%')       
 AND VVDT LIKE LEFT(GETDATE(),11) + '%'      
OPEN @POSTCUR      
FETCH NEXT FROM @POSTCUR INTO @REFNO      
WHILE @@FETCH_STATUS = 0       
BEGIN      
 EXEC JVENTRY_POSTING '', 'AUTO', '8', '01', 1, @REFNO      
 FETCH NEXT FROM @POSTCUR INTO @REFNO      
END

GO
