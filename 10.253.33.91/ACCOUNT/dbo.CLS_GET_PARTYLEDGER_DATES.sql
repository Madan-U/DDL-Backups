-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

--EXEC CLS_GET_PARTYLEDGER_DATES '01/04/2013', '31/03/2014', 365
CREATE PROCEDURE [dbo].[CLS_GET_PARTYLEDGER_DATES] (
	@YEAR_START_DATE VARCHAR(10) = '',
	@YEAR_END_DATE VARCHAR(10) = '',
	@REPORT_DAY INT = 15,
	@FROM_DATE VARCHAR(10) = '',
	@TO_DATE VARCHAR(10) = '',
	@LedgerFRMDATE CHAR(1)= 'N'
	)
AS
DECLARE @REPORT_DAYS INT,
	@REPORT_DAYS_PARAMETER INT,
	@REPORT_TO_DATE VARCHAR(10),
	@TO_DATE_START VARCHAR(10),
	@TO_DATE_END VARCHAR(10)
DECLARE @@TODAY_DATE DATETIME

SELECT @@TODAY_DATE = CASE 
		WHEN @TO_DATE <> ''
			THEN CONVERT(DATETIME, @TO_DATE, 103)
		ELSE GETDATE()
		END

IF @TO_DATE <> ''
BEGIN
	SELECT @REPORT_DAY = REPORTDAYS,
		@YEAR_START_DATE = CONVERT(VARCHAR(10), SDTCUR, 103),
		@YEAR_END_DATE = CONVERT(VARCHAR(10), LDTCUR, 103)
	FROM PARAMETER
	WHERE CONVERT(DATETIME, @TO_DATE, 103) BETWEEN SDTCUR AND LDTCUR
END
print @YEAR_START_DATE
IF @YEAR_START_DATE <> ''
	SELECT @REPORT_DAYS = CASE 
			WHEN REPORTDAYS < @REPORT_DAY
				THEN REPORTDAYS
			ELSE @REPORT_DAY
			END,
		@REPORT_DAYS_PARAMETER = REPORTDAYS,
		@TO_DATE_START = CONVERT(VARCHAR(10), SDTCUR, 103),
		@TO_DATE_END = CONVERT(VARCHAR(10), LDTCUR, 103),
		@REPORT_TO_DATE = CASE 
			WHEN LDTCUR < @@TODAY_DATE
				THEN CONVERT(VARCHAR(10), LDTCUR, 103)
			ELSE CASE WHEN SDTCUR < @@TODAY_DATE THEN CONVERT(VARCHAR(10), @@TODAY_DATE, 103) ELSE CONVERT(VARCHAR(10), SDTCUR, 103) END
			END
	FROM PARAMETER
	WHERE SDTCUR = CONVERT(DATETIME, @YEAR_START_DATE, 103)
ELSE
	SELECT @REPORT_DAYS = CASE 
			WHEN REPORTDAYS < @REPORT_DAY
				THEN REPORTDAYS
			ELSE @REPORT_DAY
			END,
		@REPORT_DAYS_PARAMETER = REPORTDAYS,
		@TO_DATE_START = CONVERT(VARCHAR(10), SDTCUR, 103),
		@TO_DATE_END = CONVERT(VARCHAR(10), LDTCUR, 103),
		@REPORT_TO_DATE = CASE 
			WHEN LDTCUR < @@TODAY_DATE
				THEN CONVERT(VARCHAR(10), LDTCUR, 103)
			ELSE CASE WHEN SDTCUR < @@TODAY_DATE THEN CONVERT(VARCHAR(10), @@TODAY_DATE, 103) ELSE CONVERT(VARCHAR(10), SDTCUR, 103) END
			END
	FROM PARAMETER
	WHERE CURYEAR = 1
DECLARE @REPORT_FROM_DATE VARCHAR(10)

SELECT @REPORT_FROM_DATE = CONVERT(VARCHAR(10), CASE WHEN CONVERT(DATETIME, @YEAR_START_DATE, 103) > DATEADD(D, - @REPORT_DAYS, CONVERT(DATETIME, @REPORT_TO_DATE, 103)) THEN CONVERT(DATETIME, @YEAR_START_DATE, 103) ELSE DATEADD(D, - @REPORT_DAYS, CONVERT(DATETIME, @REPORT_TO_DATE, 103)) END, 103)

DECLARE @FR_DATE_START VARCHAR(10),
	@FR_DATE_END VARCHAR(10)

SELECT @FR_DATE_START = CASE 
		WHEN CONVERT(DATETIME, @TO_DATE_START, 103) < DATEADD(D, - @REPORT_DAYS_PARAMETER, CONVERT(DATETIME, @REPORT_TO_DATE, 103))
			THEN CONVERT(VARCHAR(10), DATEADD(D, - @REPORT_DAYS_PARAMETER, CONVERT(DATETIME, @REPORT_TO_DATE, 103)), 103)
		ELSE @TO_DATE_START
		END,
	@FR_DATE_END = @REPORT_TO_DATE

SELECT @FROM_DATE = CASE 
		WHEN CONVERT(DATETIME, @REPORT_FROM_DATE, 103) > CONVERT(DATETIME, @FROM_DATE, 103)
			THEN @REPORT_FROM_DATE
		ELSE @FROM_DATE
		END

IF(@LedgerFRMDATE = 'Y' AND @REPORT_DAY = '365')
BEGIN

SELECT FROM_DATE = CASE 
		WHEN @FROM_DATE = ''
			THEN @REPORT_FROM_DATE
		ELSE @FROM_DATE
		END,
	TO_DATE = CASE 
		WHEN @TO_DATE = ''
			THEN @REPORT_TO_DATE
		ELSE @TO_DATE
		END,
	FROM_START_DATE = @FR_DATE_START,
	FROM_END_DATE = @FR_DATE_END,
	TO_START_DATE = @TO_DATE_START,
	TO_END_DATE = @TO_DATE_END,
	PERIOD_SEL = 'APR|MAY|JUN|JUL|AUG|SEP|OCT|NOV|DEC|JAN|FEB|MAR'

END
ELSE
BEGIN

SELECT FROM_DATE = CASE 
		WHEN @FROM_DATE = ''
			THEN @REPORT_FROM_DATE
		ELSE @FROM_DATE
		END,
	TO_DATE = CASE 
		WHEN @TO_DATE = ''
			THEN @REPORT_TO_DATE
		ELSE @TO_DATE
		END,
	FROM_START_DATE = @FR_DATE_START,
	FROM_END_DATE = @FR_DATE_END,
	TO_START_DATE = @TO_DATE_START,
	TO_END_DATE = @TO_DATE_END,
	PERIOD_SEL = 'APR|MAY|JUN|JUL|AUG|SEP|OCT|NOV|DEC|JAN|FEB|MAR'
END

GO
