-- Object: PROCEDURE dbo.V2_BANKPOFILEGEN
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROCEDURE [DBO].[V2_BANKPOFILEGEN]
(
	@VTYP VARCHAR(2),
	@FROMDATE VARCHAR(12),
	@TODATE VARCHAR(12),
	@VNOFROM VARCHAR(12),
	@VNOTO VARCHAR(12),
	@BANKCODE VARCHAR(12),	
	@EXCHANGE VARCHAR(3),
	@SEGMENT VARCHAR(7),
	@FILETYPE VARCHAR(4),
	@PRINTFLAG CHAR(2)
)

AS
	
	IF @PRINTFLAG = 'FP'
	BEGIN
		SELECT 
			L1.DDDT,
			L.VNO,
			ISNULL(DD,0) DD,
			ISNULL(DDNO,'') DDNO,
			VAMT = ISNULL(L.VAMT,0), 
			L.DRCR,
			L.CLTCODE, 
			L.ACNAME,
			CHEQUEINNAME = ISNULL(CHEQUEINNAME,''), 
			L.NARRATION,
			PRINTEDFLAG = ISNULL(CHQPRINTED,0), 
			L1.MICRNO,
			L.BOOKTYPE, 
			HOPAYMODE,
			M.ACCNO
		FROM 
			ACCOUNT.DBO.V2_SETTPAYOUT_ALL S (NOLOCK)
			LEFT OUTER JOIN MULTIBANKID M (NOLOCK) ON S.CLTCODE = M.CLTCODE AND M.DEFAULTBANK = 1, 
			LEDGER L (NOLOCK), 	
			LEDGER1 L1 (NOLOCK),
			(
			SELECT 
				VNO, 
				VTYP, 
				BOOKTYPE 
			FROM 
				LEDGER (NOLOCK)
			WHERE 
				CLTCODE = @BANKCODE 
				AND VTYP = @VTYP
				AND VNO BETWEEN @VNOFROM AND @VNOTO 
				AND VDT BETWEEN @FROMDATE AND @TODATE
			) B
		WHERE 
			L.VTYP = @VTYP 
			AND L.VTYP = L1.VTYP 
			AND L.BOOKTYPE = L1.BOOKTYPE 
			AND L.VNO = L1.VNO 
			AND L.LNO = L1.LNO			
			AND L.VTYP = B.VTYP 
			AND L.BOOKTYPE = B.BOOKTYPE 
			AND L.VNO = B.VNO
			AND L.VNO BETWEEN @VNOFROM AND @VNOTO 
			AND L.VDT BETWEEN @FROMDATE AND @TODATE 
			AND S.CLTCODE = L.CLTCODE
			AND L.CLTCODE <> @BANKCODE
			AND S.VNO = L.VNO 
			AND S.HOPAYMODE = @FILETYPE 
			AND CHQPRINTED = 0
			AND EXCHANGE = @EXCHANGE 
			AND SEGMENT = @SEGMENT
		ORDER BY 
			L.VNO 
	END
	
	ELSE
	BEGIN
		SELECT 
			L1.DDDT,
			L.VNO,
			ISNULL(DD,0) DD,
			ISNULL(DDNO,'') DDNO,
			VAMT = ISNULL(L.VAMT,0), 
			L.DRCR,
			L.CLTCODE, 
			L.ACNAME,
			CHEQUEINNAME = ISNULL(CHEQUEINNAME,''), 
			L.NARRATION,
			PRINTEDFLAG = ISNULL(CHQPRINTED,0), 
			L1.MICRNO,
			L.BOOKTYPE, 
			HOPAYMODE,
			M.ACCNO
		FROM 
			ACCOUNT.DBO.V2_SETTPAYOUT_ALL S (NOLOCK)
			LEFT OUTER JOIN MULTIBANKID M (NOLOCK) ON S.CLTCODE = M.CLTCODE AND M.DEFAULTBANK = 1,  
			LEDGER L (NOLOCK), 	
			LEDGER1 L1 (NOLOCK),
			(
			SELECT 
				VNO, 
				VTYP, 
				BOOKTYPE 
			FROM 
				LEDGER (NOLOCK)
			WHERE 
				CLTCODE = @BANKCODE 
				AND VTYP = @VTYP
				AND VNO BETWEEN @VNOFROM AND @VNOTO 
				AND VDT BETWEEN @FROMDATE AND @TODATE
			) B
		WHERE 
			L.VTYP = @VTYP 
			AND L.VTYP = L1.VTYP 
			AND L.BOOKTYPE = L1.BOOKTYPE 
			AND L.VNO = L1.VNO 
			AND L.LNO = L1.LNO			
			AND L.VTYP = B.VTYP 
			AND L.BOOKTYPE = B.BOOKTYPE 
			AND L.VNO = B.VNO
			AND L.VNO BETWEEN @VNOFROM AND @VNOTO 
			AND L.VDT BETWEEN @FROMDATE AND @TODATE 
			AND S.CLTCODE = L.CLTCODE
			AND L.CLTCODE <> @BANKCODE
			AND S.VNO = L.VNO 
			AND S.HOPAYMODE = @FILETYPE 
			AND CHQPRINTED > 0
			AND EXCHANGE = @EXCHANGE 
			AND SEGMENT = @SEGMENT			
		ORDER BY 
			L.VNO
	END

GO
