CREATE PROC [dbo].[CLS_PROC_CLIENTPROFILE]
(
	@ACCOUNT_LEVEL VARCHAR(25),
	@ACCOUNT_CODE  VARCHAR(1000),
	@CREDIT_INT  MONEY,
	@DEBIT_INT  MONEY,
	@FROM_DATE  VARCHAR(10),
	@TO_DATE  VARCHAR(10),
	@EXEMPT_NOOFDAYS  INT,
	@EXEMPT_NOOFDAYS_CR  INT,
	@MIN_INTEREST  MONEY,
	@MIN_BALANCE  MONEY,
	@ACTIONBY  VARCHAR(30),
	@ACTION VARCHAR(10),
	@ADDITIONAL_PARAMS VARCHAR(MAX) = ''
	
)
/*
ROLLBACK
	EXEC [CLS_PROC_CLIENTPROFILE] 'PARTY','0A149|0A141|0A147',0.0000,0.0000,'30/09/2012','01/10/2014',0,0,0.0000,0.0000,'BROKER','FETCH'


EXEC [CLS_PROC_CLIENTPROFILE] @ACCOUNT_LEVEL='AREA',@ACCOUNT_CODE='FORT',@CREDIT_INT=2.0000,@DEBIT_INT=1.0000,@FROM_DATE='01/09/2012',@TO_DATE='31/12/2012',@EXEMPT_NOOFDAYS=100,@EXEMPT_NOOFDAYS_CR=1,@MIN_INTEREST=1.0000,@MIN_BALANCE=1.0000,@ACTIONBY='Broker',@ACTION='ADD'
GO

*/
AS
DECLARE 
	@EXCHSEG VARCHAR(MAX),
	@UPDATEDATE DATETIME,
	@CREATEDATE DATETIME,
	@CHKDATEFR DATETIME,
	@CHKDATETO DATETIME,
	@NEWDATEFR DATETIME,
	@NEWDATETO DATETIME,
	@ACCOUNT_LEVEL_ VARCHAR(30),
	@LEVEL_FLAG INT,
	@FROMDATE DATETIME,
	@TODATE DATETIME,
	@SQL VARCHAR(MAX)
	
	SET @CREATEDATE = GETDATE()
	SET @UPDATEDATE = GETDATE()
	SET @FROMDATE = (SELECT CONVERT(DATETIME,@FROM_DATE,103))
	SET @TODATE = (SELECT CONVERT(DATETIME,@TO_DATE ,103)) + ' 23:59'
	SET @EXCHSEG = (SELECT .dbo.CLS_Piece(@ADDITIONAL_PARAMS,'~',2))
	SET @ADDITIONAL_PARAMS = (SELECT .dbo.CLS_Piece(@ADDITIONAL_PARAMS,'~',1))
BEGIN TRAN
	SET @LEVEL_FLAG = (SELECT LEVEL_NO FROM SELECTION_LEVELS_PARTYLEDGER WHERE LEVEL_NAME = (CASE WHEN  @ACCOUNT_LEVEL ='CLIENT' THEN 'PARTY' ELSE @ACCOUNT_LEVEL END) )

IF(@ACTION = 'FETCH')
 BEGIN
	
	SET @SQL = " SELECT EXCHANGE,SEGMENT, "
	SET @SQL = @SQL + " CONVERT(VARCHAR,SRNO) SRNO, CONVERT(VARCHAR,LEVEL_FLAG) LEVEL_FLAG, ACCOUNT_LEVEL,ACCOUNT_CODE, DATEFROM = CONVERT(VARCHAR,FROMDATE,103), DATETO = CONVERT(VARCHAR,TODATE,103), "
	SET @SQL = @SQL + " CONVERT(VARCHAR,CREDIT_INT) CREDIT_INT, CONVERT(VARCHAR,DEBIT_INT) DEBIT_INT, "
	SET @SQL = @SQL + " FRDT = CONVERT(VARCHAR,FROMDATE,112) ,"
	SET @SQL = @SQL + " FLAG = CONVERT(VARCHAR,CASE WHEN FROMDATE < GETDATE() THEN 0 ELSE 1 END), "
	SET @SQL = @SQL + " CONVERT(VARCHAR,EXEMPT_NOOFDAYS) EXEMPT_NOOFDAYS,CONVERT(VARCHAR,EXEMPT_NOOFDAYS_CR) EXEMPT_NOOFDAYS_CR,CONVERT(VARCHAR,MIN_INTEREST) MIN_INTEREST,CONVERT(VARCHAR,MIN_BALANCE) MIN_BALANCE" 
	SET @SQL = @SQL + ",MARGIN_INTEREST,WAVIER_FLAG=CASE WHEN WAVIER_FLAG=1 THEN 'YES' ELSE 'NO' END,CONVERT(VARCHAR,WAVIER_FROMDATE,103) as WAVIER_FROMDATE "
	SET @SQL = @SQL + ",CONVERT(VARCHAR,WAVIER_TODATE,103) as WAVIER_TODATE,CASHMARIGIN_FLAG=CASE WHEN CASHMARIGIN_FLAG=1 THEN 'YES' ELSE 'NO' END"
	SET @SQL = @SQL + ",INTERSEG_NET_OFF=CASE WHEN INTERSEG_NET_OFF=1 THEN 'YES' ELSE 'NO' END,ACC_HEAD,SHARING_REM_COMM,TDS,MARGIN_EXP_FLAG,COLL_FLAG,COLL_VALUE"
	SET @SQL = @SQL + ",CALENDAR_DAYS_DR,CALENDAR_DAYS_CR,REVISED_AMT,REVISED_PERC FROM CLS_CLIENTPROFILES_VW "
	IF @ACCOUNT_CODE <> '' OR @FROMDATE <> '' --OR @TODATE <> ''
	 BEGIN
		SET @SQL = @SQL + " WHERE ACCOUNT_LEVEL ='" + @ACCOUNT_LEVEL	+"'"
	 END
	IF @ACCOUNT_CODE <> '' 
	 BEGIN
		SET @SQL = @SQL + "and ACCOUNT_CODE IN (SELECT ITEMS FROM SPLIT('" + @ACCOUNT_CODE + "','|')) "
	 END 

	/* 
	IF @FROMDATE <> '' --AND @TODATE <> ''
	 BEGIN
		SET @SQL = @SQL + " CONVERT(VARCHAR,FROMDATE,109) >= '" + convert(varchar,@FROMDATE,109)+ "' "
		SET @SQL = @SQL + " AND CONVERT(VARCHAR,TODATE,109) <= '" + CONVERT(VARCHAR,@TODATE,109) + "' "
	 END  */

	 IF @FROMDATE <> '' --AND @TODATE <> ''
	 BEGIN
		/*SET @SQL = @SQL + " AND FROMDATE >= '" + CONVERT(VARCHAR,@FROMDATE)+ "' "
		SET @SQL = @SQL + " AND TODATE <= '" + CONVERT(VARCHAR,@TODATE)+ "' "*/
		SET @SQL = @SQL + " AND (FROMDATE BETWEEN '" + CONVERT(VARCHAR,@FROMDATE)+ "' AND '" + CONVERT(VARCHAR,@TODATE)+ "' "
		SET @SQL = @SQL + " OR TODATE BETWEEN '" + CONVERT(VARCHAR,@FROMDATE)+ "' AND '" + CONVERT(VARCHAR,@TODATE)+ "') "
	 END  

	 SET @SQL = @SQL + " AND EXCHANGE +'-'+SEGMENT IN(SELECT ITEMS FROM SPLIT('" + @EXCHSEG + "','|'))"

	 SET @SQL = @SQL + "  ORDER BY ACCOUNT_CODE, FRDT "
	 print @sql
	 EXEC(@SQL)

	 COMMIT TRAN
 END
ELSE
 IF(@ACTION = 'ADD')
 BEGIN
 
	
	IF DATEDIFF(DAY,GETDATE(), @FROMDATE) < 0
	 BEGIN
		SELECT 'PAST DATE IS NOT ALLOWED..'
		ROLLBACK
		RETURN
	 END
	
	
 	--IF(SELECT COUNT(*) FROM CLS_CLIENTPROFILES WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND (@FROMDATE BETWEEN FROMDATE AND TODATE) AND @ACCOUNT_LEVEL = ISNULL(ACCOUNT_LEVEL, '0')) = 0 
	IF(SELECT COUNT(*) FROM CLS_CLIENTPROFILES WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND (@FROMDATE BETWEEN FROMDATE AND TODATE) AND @ACCOUNT_LEVEL = ISNULL(ACCOUNT_LEVEL, '0') AND EXISTS(SELECT ITEMS FROM CLS_SPLIT(@EXCHSEG,'|') WHERE ITEMS = EXCHANGE + '-' + SEGMENT)) = 0  
	BEGIN 
		/*PRINT 'ASADASD'
		ROLLBACK
		RETURN*/
		INSERT INTO CLS_CLIENTPROFILES
		(
			ACCOUNT_LEVEL,
			ACCOUNT_CODE,
			CREDIT_INT,
			DEBIT_INT,
			FROMDATE,
			TODATE,
			EXEMPT_NOOFDAYS,
			EXEMPT_NOOFDAYS_CR,
			MIN_INTEREST,
			MIN_BALANCE,
			CREATEDATE,
			CREATEBY,
			UPDATEDATE,
			UPDATEBY,
			LEVEL_FLAG,
			EXCHANGE,
			SEGMENT,
			DUMMY1,
			DUMMY2
		)	
		SELECT 	
			@ACCOUNT_LEVEL,
			A.ITEMS AS ACCOUNT_CODE,
			@CREDIT_INT,
			@DEBIT_INT,
			@FROMDATE,
			@TODATE,
			@EXEMPT_NOOFDAYS,
			@EXEMPT_NOOFDAYS_CR,
			@MIN_INTEREST,
			@MIN_BALANCE,
			@CREATEDATE,
			@ACTIONBY,
			@UPDATEDATE,
			@ACTIONBY,
			@LEVEL_FLAG,
			EXCHANGE = .dbo.CLS_Piece(B.ITEMS,'-',1) ,
			SEGMENT=.dbo.CLS_Piece(B.ITEMS,'-',2) ,
			'',
			''
			FROM  SPLIT(@ACCOUNT_CODE,'|') A,SPLIT(@EXCHSEG,'|') B
			WHERE A.ITEMS <> '' AND A.ITEMS IS NOT NULL


			/**********newly added***************/
			INSERT CLS_CLIENTPROFILE_DETAILS(PROFILE_SRNO,MARGIN_INTEREST,WAVIER_FLAG,WAVIER_FROMDATE,WAVIER_TODATE,CASHMARIGIN_FLAG,INTERSEG_NET_OFF,ACC_HEAD,SHARING_REM_COMM,TDS,MARGIN_EXP_FLAG,COLL_FLAG,COLL_VALUE,CALENDAR_DAYS_DR,CALENDAR_DAYS_CR,REVISED_AMT,REVISED_PERC)
			SELECT 
				PROFILE_SRNO =  SRNO
				,MARGIN_INTEREST = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,1) 
				,WAVIER_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,2) 
				,WAVIER_FROMDATE  = CONVERT(DATETIME,.dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,3) ,103)
				,WAVIER_TODATE  = CONVERT(DATETIME,.dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,4) ,103)
				,CASHMARIGIN_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,5) 
				,INTERSEG_NET_OFF  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,6) 
				,ACC_HEAD  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,7) 
				,SHARING_REM_COMM  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,8) 
				,TDS  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,9) 
				,MARGIN_EXP_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,10) 
				,COLL_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,11) 
				,COLL_VALUE  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,12) 
				,CALENDAR_DAYS_DR  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,13) 
				,CALENDAR_DAYS_CR  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,14) 
				,REVISED_AMT  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,15) 
				,REVISED_PERC  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,16) 
			 FROM 
				CLS_CLIENTPROFILES,SPLIT(@ACCOUNT_CODE,'|') 
			WHERE 
				CONVERT(VARCHAR,FROMDATE,109) = CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE,103),109)
				AND CONVERT(VARCHAR(11),TODATE,109) =  CONVERT(VARCHAR(11),CONVERT(DATETIME,@TODATE,103),109) 
				AND ITEMS <> '' AND  ITEMS IS NOT NULL AND ITEMS = ACCOUNT_CODE
				/*************************/
		/*VALUES
		(
			@ACCOUNT_LEVEL,
			@ACCOUNT_CODE,
			@CREDIT_INT,
			@DEBIT_INT,
			@FROMDATE,
			@TODATE,
			@EXEMPT_NOOFDAYS,
			@EXEMPT_NOOFDAYS_CR,
			@MIN_INTEREST,
			@MIN_BALANCE,
			@CREATEDATE,
			@ACTIONBY,
			@UPDATEDATE,
			@ACTIONBY,
			@LEVEL_FLAG,
			'',
			''
		)*/


		
	IF @@ERROR = 0 
	 BEGIN
		SELECT 'SLAB IS ADDED SUCCESSFULLY..'
		COMMIT TRAN
		RETURN
	 END 
	ELSE
	 BEGIN 
		ROLLBACK
		SELECT 'SLAB IS NOT ADDED DUE TO SOME ERROR..'	
		RETURN
	 END
	 
	END
  ELSE
   BEGIN
	SELECT 'ACCOUNT ALREADY EXIST IN THE SYSTEM FOR THE SAME SLAB..'
	COMMIT
   END				
 END
ELSE IF @ACTION = 'EDIT'
  BEGIN
	SELECT 
		@CHKDATEFR = FROMDATE, @CHKDATETO = TODATE
	FROM 
		CLS_CLIENTPROFILES 
	WHERE 
		ACCOUNT_CODE = @ACCOUNT_CODE AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL 
		AND @FROMDATE BETWEEN CONVERT(VARCHAR(11),FROMDATE,109) AND CONVERT(VARCHAR(11),TODATE,109)
	    AND FROMDATE <> @FROMDATE 
		AND EXCHANGE = .dbo.CLS_Piece(@EXCHSEG,'-',1)
		AND SEGMENT  = .dbo.CLS_Piece(@EXCHSEG,'-',2)

	--PRINT @CHKDATETO
	
	IF DATEDIFF(DAY,@CHKDATETO , @FROMDATE) < 0  --@CHKDATETO < @FROMDATE AND @@ROWCOUNT > 0
	 BEGIN 
	 	SET @NEWDATETO = DATEADD(DAY,-1, @FROMDATE)
		--PRINT @NEWDATETO
		--PRINT @CHKDATETO
		
		INSERT INTO CLS_CLIENTPROFILES(EXCHANGE,SEGMENT,ACCOUNT_LEVEL,ACCOUNT_CODE,CREDIT_INT,DEBIT_INT,FROMDATE,TODATE,EXEMPT_NOOFDAYS,EXEMPT_NOOFDAYS_CR,MIN_INTEREST,MIN_BALANCE,CREATEDATE,CREATEBY,UPDATEDATE,UPDATEBY,LEVEL_FLAG)
		SELECT EXCHANGE,SEGMENT,ACCOUNT_LEVEL,ACCOUNT_CODE,@CREDIT_INT,@DEBIT_INT,@FROMDATE,@TODATE,@EXEMPT_NOOFDAYS,@EXEMPT_NOOFDAYS_CR,@MIN_INTEREST,@MIN_BALANCE,@CREATEDATE,CREATEBY+'1',@UPDATEDATE,UPDATEBY,LEVEL_FLAG FROM CLS_CLIENTPROFILES 
		WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL AND FROMDATE LIKE CONVERT(VARCHAR(11),@CHKDATEFR,109) + '%'
		AND EXCHANGE = .dbo.CLS_Piece(@EXCHSEG,'-',1)
		AND SEGMENT  = .dbo.CLS_Piece(@EXCHSEG,'-',2)

	
		
		/**********newly added***************/
		INSERT INTO CLS_CLIENTPROFILE_DETAILS(PROFILE_SRNO,MARGIN_INTEREST,WAVIER_FLAG,WAVIER_FROMDATE,WAVIER_TODATE,CASHMARIGIN_FLAG,INTERSEG_NET_OFF,ACC_HEAD,SHARING_REM_COMM,TDS,MARGIN_EXP_FLAG,COLL_FLAG,COLL_VALUE,CALENDAR_DAYS_DR,CALENDAR_DAYS_CR,REVISED_AMT,REVISED_PERC)
		SELECT PROFILE_SRNO =  SRNO
				,MARGIN_INTEREST = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,1) 
				,WAVIER_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,2) 
				,WAVIER_FROMDATE  = CONVERT(DATETIME,.dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,3) ,103)
				,WAVIER_TODATE  = CONVERT(DATETIME,.dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,4) ,103)
				,CASHMARIGIN_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,5) 
				,INTERSEG_NET_OFF  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,6) 
				,ACC_HEAD  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,7) 
				,SHARING_REM_COMM  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,8) 
				,TDS  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,9) 
				,MARGIN_EXP_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,10) 
				,COLL_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,11) 
				,COLL_VALUE  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,12)  
				,CALENDAR_DAYS_DR = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,13)  
				,CALENDAR_DAYS_CR = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,14) 
				,REVISED_AMT  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,15) 
				,REVISED_PERC  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,16) 
				
		FROM CLS_CLIENTPROFILES 
		WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL AND FROMDATE LIKE CONVERT(VARCHAR(11),@FROMDATE,109) + '%' 
		AND EXCHANGE = .dbo.CLS_Piece(@EXCHSEG,'-',1)
		AND SEGMENT  = .dbo.CLS_Piece(@EXCHSEG,'-',2)
		/*************************/

		IF @TODATE < @CHKDATETO 
		BEGIN 
			SET @NEWDATEFR= DATEADD(DAY,+1, @TODATE)
			SET @NEWDATETO = @CHKDATETO
			
			INSERT INTO CLS_CLIENTPROFILES(EXCHANGE,SEGMENT,ACCOUNT_LEVEL,ACCOUNT_CODE,CREDIT_INT,DEBIT_INT,FROMDATE,TODATE,EXEMPT_NOOFDAYS,EXEMPT_NOOFDAYS_CR,MIN_INTEREST,MIN_BALANCE,CREATEDATE,CREATEBY,UPDATEDATE,UPDATEBY,LEVEL_FLAG)
			SELECT EXCHANGE,SEGMENT,ACCOUNT_LEVEL,ACCOUNT_CODE,@CREDIT_INT,@DEBIT_INT,@NEWDATEFR,@NEWDATETO,@EXEMPT_NOOFDAYS,@EXEMPT_NOOFDAYS_CR,@MIN_INTEREST,@MIN_BALANCE,@CREATEDATE,CREATEBY+'2',@UPDATEDATE,UPDATEBY,LEVEL_FLAG FROM CLS_CLIENTPROFILES 
			WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL AND FROMDATE LIKE CONVERT(VARCHAR(11),@CHKDATEFR,109) + '%'
			AND EXCHANGE = .dbo.CLS_Piece(@EXCHSEG,'-',1)
			AND SEGMENT  = .dbo.CLS_Piece(@EXCHSEG,'-',2)
			/**********newly added***************/
			INSERT INTO CLS_CLIENTPROFILE_DETAILS(PROFILE_SRNO,MARGIN_INTEREST,WAVIER_FLAG,WAVIER_FROMDATE,WAVIER_TODATE,CASHMARIGIN_FLAG,INTERSEG_NET_OFF,ACC_HEAD,SHARING_REM_COMM,TDS,MARGIN_EXP_FLAG,COLL_FLAG,COLL_VALUE,CALENDAR_DAYS_DR,CALENDAR_DAYS_CR,REVISED_AMT,REVISED_PERC)
			SELECT PROFILE_SRNO =  SRNO
				,MARGIN_INTEREST = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,1) 
				,WAVIER_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,2) 
				,WAVIER_FROMDATE  = CONVERT(DATETIME,.dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,3) ,103)
				,WAVIER_TODATE  = CONVERT(DATETIME,.dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,4) ,103)
				,CASHMARIGIN_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,5) 
				,INTERSEG_NET_OFF  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,6) 
				,ACC_HEAD  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,7) 
				,SHARING_REM_COMM  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,8) 
				,TDS  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,9) 
				,MARGIN_EXP_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,10) 
				,COLL_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,11) 
				,COLL_VALUE  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,12) 
				,CALENDAR_DAYS_DR = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,13) 
				,CALENDAR_DAYS_CR  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,14) 
				,REVISED_AMT  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,15) 
				,REVISED_PERC  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,16) 
			FROM CLS_CLIENTPROFILES 
			WHERE ACCOUNT_CODE = @ACCOUNT_CODE AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL AND FROMDATE LIKE CONVERT(VARCHAR(11),@NEWDATEFR,109) + '%'
			AND EXCHANGE = .dbo.CLS_Piece(@EXCHSEG,'-',1)
			AND SEGMENT  = .dbo.CLS_Piece(@EXCHSEG,'-',2)
			/*************************/
		END  
		
			UPDATE CLS_CLIENTPROFILES SET
					CREDIT_INT = @CREDIT_INT,
					DEBIT_INT = @DEBIT_INT,
					TODATE = DATEDIFF(DAY, 1, @FROMDATE),
					EXEMPT_NOOFDAYS = @EXEMPT_NOOFDAYS,
					EXEMPT_NOOFDAYS_CR = @EXEMPT_NOOFDAYS_CR,
					MIN_INTEREST = @MIN_INTEREST,
					MIN_BALANCE = @MIN_BALANCE,
					UPDATEDATE = @UPDATEDATE,
					UPDATEBY = @ACTIONBY+'3' 
				WHERE 
					ACCOUNT_CODE = @ACCOUNT_CODE
					AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL
					AND FROMDATE LIKE CONVERT(VARCHAR(11),@CHKDATEFR,109) + '%'	
					AND EXCHANGE = .dbo.CLS_Piece(@EXCHSEG,'-',1)
					AND SEGMENT  = .dbo.CLS_Piece(@EXCHSEG,'-',2)

			IF @@ERROR = 0 
			 BEGIN
				SELECT 'APPLIED SLAB IS UPDATED SUCCESSFULLY..'
				COMMIT TRAN
				RETURN
			 END 		
		 END  
	ELSE
		BEGIN


		UPDATE CLS_CLIENTPROFILES SET
				CREDIT_INT = @CREDIT_INT,
				DEBIT_INT = @DEBIT_INT,
				--FROMDATE = @FROMDATE,
				TODATE = @TODATE,
				EXEMPT_NOOFDAYS = @EXEMPT_NOOFDAYS,
				EXEMPT_NOOFDAYS_CR = @EXEMPT_NOOFDAYS_CR,
				MIN_INTEREST = @MIN_INTEREST,
				MIN_BALANCE = @MIN_BALANCE,
				UPDATEDATE = @UPDATEDATE,
				UPDATEBY = @ACTIONBY
			WHERE 
				ACCOUNT_CODE = @ACCOUNT_CODE
				AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL
				AND @FROMDATE BETWEEN CONVERT(VARCHAR(11),FROMDATE,109) AND CONVERT(VARCHAR(11),TODATE,109)
				AND EXCHANGE = .dbo.CLS_Piece(@EXCHSEG,'-',1)
				AND SEGMENT  = .dbo.CLS_Piece(@EXCHSEG,'-',2)


		/**********newly added***************/
		UPDATE CLS_CLIENTPROFILE_DETAILS
				SET MARGIN_INTEREST = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,1) 
				,WAVIER_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,2) 
				,WAVIER_FROMDATE  = CONVERT(DATETIME,.dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,3) ,103)
				,WAVIER_TODATE  = CONVERT(DATETIME,.dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,4) ,103)
				,CASHMARIGIN_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,5) 
				,INTERSEG_NET_OFF  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,6) 
				,ACC_HEAD  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,7) 
				,SHARING_REM_COMM  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,8) 
				,TDS  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,9) 
				,MARGIN_EXP_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,10) 
				,COLL_FLAG  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,11) 
				,COLL_VALUE  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,12)  
				,CALENDAR_DAYS_DR = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,13) 
				,CALENDAR_DAYS_CR = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,14) 
				,REVISED_AMT  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,15) 
				,REVISED_PERC  = .dbo.CLS_Piece(@ADDITIONAL_PARAMS ,'|' ,16) 
			WHERE 
			PROFILE_SRNO = (SELECT SRNO FROM CLS_CLIENTPROFILES 
							WHERE 
							ACCOUNT_CODE = @ACCOUNT_CODE
							AND ACCOUNT_LEVEL = @ACCOUNT_LEVEL
							AND @FROMDATE BETWEEN CONVERT(VARCHAR(11),FROMDATE,109) AND CONVERT(VARCHAR(11),TODATE,109) 
							AND EXCHANGE = .dbo.CLS_Piece(@EXCHSEG,'-',1)
							AND SEGMENT  = .dbo.CLS_Piece(@EXCHSEG,'-',2))
		/*************************/	
		IF @@ERROR = 0 
			BEGIN
			SELECT 'APPLIED SLAB IS UPDATED SUCCESSFULLY..'
			COMMIT TRAN
			RETURN
			END 		
		END		
  END

GO
