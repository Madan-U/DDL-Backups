--SELECT @FDATE, @TDATE, @FCLTCODE, @TCLTCODE, @TRANON, '', @VTYPE, '', @STATUSID, @STATUSNAME, @EXCHANGE, @SEGMENT,'','','','','',''

END    
IF @FLAG = 2    
BEGIN    
 IF CHARINDEX('/',@FDATE) > 0    
 BEGIN    
  SET @FDATE = CONVERT(VARCHAR(11),CONVERT(DATETIME,@FDATE,103),109)    
 END    
     
 IF CHARINDEX('/',@TDATE) > 0    
 BEGIN    
  SET @TDATE = CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE,103),109)    
 END    
-- EXEC VOUCHERPRINTINGSP @VTYPE,@BANKFRM,@BANKTO,@FDATE, @TDATE,@FVNO,@TVNO,1,0    
   --EXEC RPT_VOUCHERPRINTING @VTYPE,@BANKFRM,@FDATE,@TDATE,@FCLTCODE,@TCLTCODE  
  --SELECT @VTYPE,@BANKFRM,@BANKTO,'','',@FVNO,@TVNO
  return
   IF @VTYPE = 3 OR @VTYPE = 2 OR @VTYPE= 19 OR @VTYPE = 20 
		EXEC CLS_VOUCHERPRINTINGSP @VTYPE,@BANKFRM,@BANKTO,@FDATE,@TDATE,@FVNO,@TVNO,1,0,'',@VNOSTR,@FCLTCODE,@TCLTCODE     
   ELSE IF  
   @VTYPE = 5   
 EXEC CLS_VOUCHERPRINTINGSP @VTYPE,@BANKFRM,@BANKTO,'','',@FVNO,@TVNO,1,3,'',@VNOSTR,@FCLTCODE,@TCLTCODE       
   ELSE  
 EXEC CLS_VOUCHERPRINTINGSP @VTYPE,@BANKFRM,@BANKTO,'','',@FVNO,@TVNO,1,1,'',@VNOSTR,@FCLTCODE,@TCLTCODE       
    
     
END

