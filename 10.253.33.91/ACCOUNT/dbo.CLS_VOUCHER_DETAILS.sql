AS   


IF @FLAG =1    
BEGIN    
  
 DECLARE @SHARESERVER VARCHAR(50),
@ACCOUNTDB VARCHAR(50),@SQL VARCHAR(4000)

SELECT @SHARESERVER=SHARESERVER ,@ACCOUNTDB =ACCOUNTDB FROM ACCOUNT..CLS_COMPANY_MASTER_SETTING WHERE EXCHANGE=@EXCHANGE AND SEGMENT=@SEGMENT
--SELECT @FDATE, @TDATE, @FCLTCODE, @TCLTCODE, @TRANON, '', @VTYPE, '', @STATUSID, @STATUSNAME, @EXCHANGE, @SEGMENT,'','','','','',''    

SET @SQL=''
SET @SQL=@SQL +
"SELECT  EXCHANGE,    
 SEGMENT,     
 VNO,     
 VTYPE,     
 BOOKTYPE,     
 VDT = CONVERT(VARCHAR(11),VDT,109),     
 EDT = CONVERT(VARCHAR(11),EDT,109),     
 DRAMOUNT,    
 CRAMOUNT,     
 CLTCODE,    
 ACNAME,    
 NARRATION,    
 BRANCHCODE,    
 --OPPACCOUNT,    
 BNKNAME,    
 BRNNAME,    
 CHQDD,    
 INSTNO,    
 INSTDATE= CONVERT(VARCHAR(11),INSTDATE,109),    
 RELDT= CONVERT(VARCHAR(11),RELDT,109),    
 REFNO,    
 MICR,    
 CHQINNAME,    
 CHQPRINTED,    
 CLEARMODE,    
 VDESC/*,    
 SHORTDESC */    
FROM  "   
SET @SQL = @SQL + @ACCOUNTDB+".DBO.CLS_TRANSACTIONS_CLIENTS( "
SET @SQL = @SQL +"'"+ @FDATE +"' , '" +@TDATE +"' , '"+ @FCLTCODE +"','"+ @TCLTCODE+"','"+ @TRANON+"','',"
SET @SQL = @SQL +  CAST(@VTYPE AS NVARCHAR(10))
SET @SQL = @SQL +",''"

SET @SQL = @SQL +",'" + @STATUSID+"','"+ @STATUSNAME+"','"+ @EXCHANGE+"','"+ @SEGMENT+"','','','','','','') A"  
 --WHERE  CASE WHEN CHQPRINTED = 0 THEN 0 ELSE 1 END = @PRINTFLAG
--AND 
IF(@STATUSNAME <> 'BROKER')
BEGIN
 SET @SQL = @SQL + " INNER JOIN msajag.dbo.CLIENT_DETAILS C ON A.CLTCODE = C.CL_CODE "
END 
SET @SQL = @SQL + " WHERE VNO BETWEEN '"+ @FVNO +"' AND '"+ @TVNO +"'"

IF(@STATUSNAME <> 'BROKER')
BEGIN
SET @SQL = @SQL + " AND '"+ @STATUSNAME+"' =(
              CASE
                     WHEN '" + @STATUSID+"' = 'BRANCH' THEN C.BRANCH_CD
                     WHEN '" + @STATUSID+"' = 'SUBBROKER'THEN C.SUB_BROKER
                     WHEN '" + @STATUSID+"' = 'TRADER' THEN C.TRADER
                     WHEN '" + @STATUSID+"' = 'FAMILY' THEN C.FAMILY
                     WHEN '" + @STATUSID+"' = 'AREA'   THEN C.AREA
                     WHEN '" + @STATUSID+"' = 'REGION' THEN C.REGION
                     WHEN '" + @STATUSID+"' = 'CLIENT' THEN C.PARTY_CODE
                     ELSE 'BROKER'
              END) "
END 
SET @SQL = @SQL + " AND ISNULL(INSTNO,'') = case when isnull('"+@DDNO+"','') = ''then ISNULL(INSTNO,'')  else '" +@DDNO + "' END ORDER BY VNO  "

PRINT @SQL
EXEC(@SQL)

