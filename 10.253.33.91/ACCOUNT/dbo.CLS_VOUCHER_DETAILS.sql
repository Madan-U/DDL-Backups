 @FINYR VARCHAR(25),    
 @FDATE VARCHAR(11),    
 @TDATE VARCHAR(11),    
 @BRANCH VARCHAR(20),    
 @TRANON VARCHAR(1), --FOR EDT & VDT    
 @FCLTCODE VARCHAR(10),      
 @TCLTCODE VARCHAR(10),    
 @VTYPE SMALLINT,    
 @FVNO VARCHAR(12),      
 @TVNO VARCHAR(12),    
 @BANKFRM VARCHAR(35),    
 @BANKTO VARCHAR(35),    
 @STATUSID VARCHAR(25),    
 @STATUSNAME VARCHAR(25),    
 @EXCHANGE VARCHAR(10),    
 @SEGMENT VARCHAR(10),    
 @VNOSTR VARCHAR(MAX),  
 @FLAG INT ,
@PRINTFLAG INT,
@DDNO VARchar(50)

)    
