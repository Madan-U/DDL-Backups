CREATE PROCEDURE [dbo].[GET_AGEING_MTF_VERIFICATION_NEW_ALL_SEGMENT]  
 @CLTCODE VARCHAR(10),  
 @REPORT_DATE VARCHAR(11)
AS

CREATE TABLE #AGEING_DAYS  
(  
 VDT DATETIME,  
 NODAYS INT,  
 SNO INT  
)    
   
INSERT INTO #AGEING_DAYS   
EXEC GET_CALENDER_DAYS @REPORT_DATE

DECLARE
	@VDT_AMT MONEY,@OPDT DATETIME

	SELECT @OPDT=sdtcur FROM parameter WHERE @REPORT_DATE BETWEEN sdtcur AND ldtcur


SELECT @VDT_AMT=SUM(CASE DRCR WHEN 'D' THEN VAMT ELSE -VAMT END)
 FROM VW_LEDGER_TEMP_NEW_ALL_SEGMENT WHERE VDT BETWEEN  @OPDT 
 AND @REPORT_DATE + ' 23:59' AND CLTCODE = @CLTCODE
	 
 


CREATE TABLE [DBO].[#LEDGERTRAN]
(
  [CLTCODE] [VARCHAR] (10) ,
  [VTYP] [SMALLINT] NOT NULL ,
  [VNO] [VARCHAR]  (12) ,
  [EVDT] [DATETIME] NULL ,
  [VAMT] [MONEY] NULL,
  [SRNO] INT NULL,
  [NET_AMOUNT] [MONEY] NULL,
  [NARRATION] [VARCHAR] (500) NULL 
) ON [PRIMARY]

INSERT INTO #LEDGERTRAN
SELECT L.CLTCODE, VTYP, VNO, VDT, VAMT = CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END, 1, CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END, NARRATION
FROM VW_LEDGER_TEMP_NEW_ALL_SEGMENT L 
WHERE VDT <= @REPORT_DATE  + ' 23:59:59'
--     AND VDT > DATEADD(MONTH,-12,@@OPENBALDT)
AND CLTCODE = @CLTCODE
AND VTYP not in(15, 35, 18)

/* NEW QUERY TO HANDLE THE MTF TRADE */
INSERT INTO #LEDGERTRAN
SELECT L.CLTCODE, VTYP = 15, VNO, VDT,
VAMT = CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END, 2, CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END, NARRATION
FROM VW_LEDGER_TEMP_NEW_ALL_SEGMENT L 
WHERE VDT <= @REPORT_DATE  + ' 23:59:59'
--     AND VDT > DATEADD(MONTH,-12,@@OPENBALDT)
AND 1 = (CASE WHEN VTYP = 35 THEN 1 
WHEN VTYP = 15 AND NARRATION LIKE '%NSECMNBILL%' THEN 1 
WHEN VTYP = 15 AND NARRATION LIKE '%BSECMDBILL%' THEN 1 
ELSE 2
END)
AND VTYP IN (15,35)
AND CLTCODE = @CLTCODE
--GROUP BY L.CLTCODE, CONVERT(VARCHAR(11), VDT, 109)

INSERT INTO #LEDGERTRAN
SELECT L.CLTCODE, VTYP = 15, VNO = VNO, VDT = CONVERT(VARCHAR(11), VDT, 109), 
VAMT = (CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END), 3, (CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END), NARRATION
FROM VW_LEDGER_TEMP_NEW_ALL_SEGMENT L 
WHERE VDT <= @REPORT_DATE  + ' 23:59:59'
--     AND VDT > DATEADD(MONTH,-12,@@OPENBALDT)
AND 1 = (CASE WHEN VTYP = 35 THEN 2 
WHEN VTYP = 15 AND NARRATION LIKE '%NSECMNBILL%' THEN 2
WHEN VTYP = 15 AND NARRATION LIKE '%BSECMDBILL%' THEN 2 
ELSE 1
END)
AND VTYP IN (15,35)
AND CLTCODE = @CLTCODE

UPDATE L SET NET_AMOUNT = (SELECT SUM(NET_AMOUNT) FROM #LEDGERTRAN L1 WHERE L.SRNO = L1.SRNO AND L.CLTCODE = L1.CLTCODE AND CONVERT(VARCHAR(11), L.EVDT, 109) = CONVERT(VARCHAR(11), L1.EVDT, 109) GROUP BY CLTCODE, CONVERT(VARCHAR(11), L1.EVDT, 109))
FROM #LEDGERTRAN L WHERE SRNO = 2



SELECT LEDBAL = @VDT_AMT, VDESC, CLTCODE, VTYP, VNO, EVDT, VAMT, NET_AMOUNT, NARRATION = G.NARRATION, DESCTIPTION = CASE WHEN SRNO = 2 THEN 'MTF NET BILL' ELSE CASE WHEN SRNO = 3 THEN 'BILLS' ELSE 'OTHER TR' END END + ' OF ' + CONVERT(VARCHAR(11), EVDT, 109),
 AGEING_AMOUNT = CASE WHEN @VDT_AMT > 0 AND NET_AMOUNT > 0 THEN NET_AMOUNT ELSE CASE WHEN @VDT_AMT <= 0 AND NET_AMOUNT <= 0 THEN ABS(NET_AMOUNT) ELSE 0 END END,    NODAYS = NODAYS FROM #LEDGERTRAN G, #AGEING_DAYS A, VMAST V WHERE G.VTYP = V.Vtype AND 

CONVERT(VARCHAR(11), EVDT, 109) = CONVERT(VARCHAR(11), VDT, 109)  
ORDER BY VDT DESC

GO
