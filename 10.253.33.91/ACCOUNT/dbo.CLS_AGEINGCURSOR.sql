DECLARE @@PREVIOUS_DAYS INT

SET @@PREVIOUS_DAYS = 0

DECLARE @RCOUNT INT
DECLARE @@COUNTER INT
DECLARE @COLFIELD VARCHAR(1000)
DECLARE @SUMFIELD VARCHAR(1000)
DECLARE @@BALANCE VARCHAR(1000)

SET @COLFIELD = ''
SET @SUMFIELD = ''
SET @@BALANCE = ''
SET @@COUNTER = 1
SET @RCOUNT = (
		SELECT DISTINCT COUNT(1)
		FROM #AGEDAYS
		)

DECLARE WRAPPER CURSOR
FOR
SELECT AGEING_DAYS
FROM #AGEDAYS
ORDER BY AGEING_DAYS

DECLARE @@DAYS INT

OPEN WRAPPER

FETCH NEXT
FROM WRAPPER
INTO @@DAYS

WHILE @@FETCH_STATUS = 0
BEGIN
	IF (@@COUNTER < @RCOUNT)
	BEGIN
		PRINT @@COUNTER

		SET @COLFIELD = @COLFIELD + '[' + LTRIM(RTRIM(CONVERT(CHAR, CASE 
							WHEN @@PREVIOUS_DAYS = 0
								THEN @@PREVIOUS_DAYS
							ELSE @@PREVIOUS_DAYS + 1
							END))) + '-' + LTRIM(RTRIM(CONVERT(CHAR, @@DAYS))) + '] = ISNULL([' + LTRIM(RTRIM(CONVERT(CHAR, @@DAYS))) + '],0),'
		SET @SUMFIELD = @SUMFIELD + '[' + LTRIM(RTRIM(@@DAYS)) + '],'
		SET @@BALANCE = @@BALANCE + 'ISNULL([' + LTRIM(RTRIM(CONVERT(CHAR, @@DAYS))) + '],0)+'

		INSERT INTO #AGEINGLIST
		SELECT CLTCODE = UPPER(CLTCODE)
			,BALANCE = SUM(BALAMT)
			,AGEDAYS = @@DAYS
			,DRCR
			,SUB_BROKER
			,SUB_BRNAME
			,BRANCHCODE
			,BRANCHNAME
		FROM #FINTEMPAGEING
		WHERE AGEDAYS >= CASE 
				WHEN @@PREVIOUS_DAYS = 0
					THEN @@PREVIOUS_DAYS
				ELSE @@PREVIOUS_DAYS + 1
				END
			AND AGEDAYS <= @@DAYS
		GROUP BY UPPER(CLTCODE)
			,DRCR
			,SUB_BROKER
			,SUB_BRNAME
			,BRANCHCODE
			,BRANCHNAME
		ORDER BY UPPER(CLTCODE)
			,DRCR
	END

	SET @@PREVIOUS_DAYS = @@DAYS
	SET @@COUNTER = @@COUNTER + 1

	FETCH NEXT
	FROM WRAPPER
	INTO @@DAYS
END

CLOSE WRAPPER

SET @COLFIELD = @COLFIELD + '[ > ' + LTRIM(RTRIM(CONVERT(CHAR, @@DAYS))) + ']=ISNULL([' + LTRIM(RTRIM(CONVERT(CHAR, @@DAYS))) + '],0)'
SET @SUMFIELD = @SUMFIELD + '[' + LTRIM(RTRIM(@@DAYS)) + ']'
SET @@BALANCE = @@BALANCE + 'ISNULL([' + LTRIM(RTRIM(CONVERT(CHAR, @@DAYS))) + '],0)'

DEALLOCATE WRAPPER

INSERT INTO #AGEINGLIST
SELECT CLTCODE = UPPER(CLTCODE)
	,BALANCE = SUM(BALAMT)
	,AGEDAYS = @@DAYS
	,DRCR
	,SUB_BROKER
	,SUB_BRNAME
	,BRANCHCODE
	,BRANCHNAME
FROM #FINTEMPAGEING
WHERE AGEDAYS >= @@DAYS
GROUP BY UPPER(CLTCODE)
	,DRCR
	,SUB_BROKER
	,SUB_BRNAME
	,BRANCHCODE
	,BRANCHNAME
ORDER BY UPPER(CLTCODE)
	,DRCR

