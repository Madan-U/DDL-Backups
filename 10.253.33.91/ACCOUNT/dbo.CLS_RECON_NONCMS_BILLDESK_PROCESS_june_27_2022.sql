-- Object: PROCEDURE dbo.CLS_RECON_NONCMS_BILLDESK_PROCESS_june_27_2022
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------






create PROCEDURE [dbo].[CLS_RECON_NONCMS_BILLDESK_PROCESS_june_27_2022]
( @UPLOADID NVARCHAR(50) 
,@BANKCODE NVARCHAR(50)
,@UPLOADFILENAME NVARCHAR(200)
,@UNAME NVARCHAR(50)) 
AS

BEGIN

		

		IF CHARINDEX('.XLS',UPPER(@UPLOADFILENAME))>1
		BEGIN
			INSERT INTO #NONCMS_DATA (UPLOADID
			, BANKACCOUNT
			, BANKCODE
			, DESCRIPTION
			, LEDGERBALANCE
			, DRCR
			, AMT
			, VALUEDATE
			, REFERENCENO
			, CLTACCOUNT
			, CUSTOMERREFERENCE
			, TRANSACTIONDETAIL
			, VNO
			, VTYP
			, BOOKTYPE
			, MATCHEDFLAG
			, UPLOADSTATUS
			, UPLOADEDDATE
			, MODIFIEDDATE
			, UPLOADBY)
			SELECT
			UPLOADID
			,BANKACCOUNT
			, BANKCODE
			,LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,'Æ',6)))+'_'+LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,'Æ',8)))  AS 'DESCRIPTION'
			,'' AS 'LEDGERBALANCE'
			,'C' AS 'DRCR'
			,CAST(DBO.PIECE(FILE_DATA,'Æ',13) AS DECIMAL(18,2)) AS 'AMT'
			,CONVERT( DATETIME,.DBO.PIECE(FILE_DATA,'Æ',12),103) AS 'VALUEDATE'
			,LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,'Æ',11))) AS 'REFERENCENO'
			,' '
			,' '
			,' '
			,' '
			,' '
			,' '
			,0 AS 'MATCHEDFLAG'
			,UPLOADSTATUS
			,GETDATE()
			,MODIFIEDDATE
			,U.UPLOADBY
			FROM RECON_BANK_FILEUPLOAD_LOGS U (NOLOCK)
			INNER JOIN MSAJAG..FILEDATA F (NOLOCK)
			ON U.UPLOADID = F.PROGID
			WHERE F.PROGID = @UPLOADID
			AND ISNULL(DBO.PIECE(FILE_DATA,'Æ',15) , '')<>''
			AND ISNULL(DBO.PIECE(FILE_DATA,'Æ',11) , '')<>'Ref 4'
		END

		IF CHARINDEX('.CSV',UPPER(@UPLOADFILENAME))>1
		BEGIN 
			BEGIN TRY

				DECLARE @D VARCHAR(50),@VALUEDATE VARCHAR(11)
				SELECT  @D=.DBO.PIECE(@UPLOADFILENAME,'_',4)


				SELECT @VALUEDATE= STUFF(STUFF( @D, 5, 0, '/'), 3, 0, '/')-- DD/MM/YYYY
				IF CHARINDEX('.CSV',UPPER(@UPLOADFILENAME))>1
				SELECT @VALUEDATE=REPLACE(@VALUEDATE,'.','')

				IF(@VALUEDATE='' OR @VALUEDATE IS NULL)
				BEGIN
				DELETE FROM MSAJAG..FILEDATA	WHERE PROGID = @UPLOADID
				SELECT		MSG = 'PLEASE ADD DATE IN THE FILE NAME'
				DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
				DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
				RETURN
				END

				SELECT * INTO #TEMP_BILLDESK FROM  MSAJAG..FILEDATA (NOLOCK) WHERE PROGID=@UPLOADID
				AND ISNULL(.DBO.PIECE(FILE_DATA,',',14),'')<>''
				AND ISNULL(.DBO.PIECE(FILE_DATA,',',14),'') NOT LIKE '%Gross Amount%'

				INSERT INTO #NONCMS_DATA (UPLOADID
				, BANKACCOUNT
				, BANKCODE
				, DESCRIPTION
				, LEDGERBALANCE
				, DRCR
				, AMT
				, VALUEDATE
				, REFERENCENO
				, CLTACCOUNT
				, CUSTOMERREFERENCE
				, TRANSACTIONDETAIL
				, VNO
				, VTYP
				, BOOKTYPE
				, MATCHEDFLAG
				, UPLOADSTATUS
				, UPLOADEDDATE
				, MODIFIEDDATE
				, UPLOADBY)
				SELECT
					UPLOADID
					,BANKACCOUNT = BANKACCOUNT
					,BANKCODE = BANKCODE
					,DESCRIPTION = .DBO.PIECE(FILE_DATA,',',9)+.DBO.PIECE(FILE_DATA,',',10)+.DBO.PIECE(FILE_DATA,',',11)+.DBO.PIECE(FILE_DATA,',',12)
					,LEDGERBALANCE = ''
					,DRCR ='C'
					,AMT = CAST (.DBO.PIECE(FILE_DATA,',',14) AS MONEY)
					,VALUEDATE = CONVERT(DATETIME,@VALUEDATE,103)
					,REFERENCENO = .DBO.PIECE(FILE_DATA,',',12)
					,' '
					,' '
					,' '
					,' '
					,' '
					,' '
					,MATCHEDFLAG = 0
					,UPLOADSTATUS
					,GETDATE()
					,MODIFIEDDATE=GETDATE()
					,U.UPLOADBY
				FROM RECON_BANK_FILEUPLOAD_LOGS U (NOLOCK)
				INNER JOIN #TEMP_BILLDESK F (NOLOCK)
					ON U.UPLOADID = F.PROGID
				WHERE F.PROGID = @UPLOADID

				DROP TABLE #TEMP_BILLDESK
				DELETE FROM  MSAJAG..FILEDATA WHERE PROGID=@UPLOADID
				

			END TRY

			BEGIN CATCH

				DROP TABLE #TEMP_BILLDESK
				DELETE FROM  MSAJAG..FILEDATA WHERE PROGID=@UPLOADID
				SELECT 'NOT CORRECT FILE FORMAT '
				DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
				DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
				RETURN

			END CATCH

		END

END

GO
