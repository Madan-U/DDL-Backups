-- Object: PROCEDURE dbo.GET_TODAYS
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------



CREATE PROC [dbo].[GET_TODAYS]

AS

DECLARE @@LDTCUR AS DATETIME,

		    @@SDTCUR AS DATETIME,

				@DAYS INT



SELECT @@LDTCUR = CASE WHEN LDTCUR > GETDATE() THEN GETDATE() ELSE LDTCUR END, @DAYS = REPORTDAYS FROM PARAMETER WHERE CURYEAR = 1



SELECT @@SDTCUR = CASE WHEN SDTCUR > DATEADD(d,-@DAYS,@@LDTCUR) THEN SDTCUR ELSE DATEADD(D,-@DAYS,@@LDTCUR) END FROM PARAMETER WHERE CURYEAR = 1



SELECT convert(varchar,@@LDTCUR,103), convert(varchar,@@SDTCUR,103), convert(varchar(10), getdate(),103)

GO
