CREATE PROCEDURE [dbo].[CLS_RECON_NONCMS_RAZORPAY_PROCESS]
( @UPLOADID NVARCHAR(50) 
,@BANKCODE NVARCHAR(50)
,@FILE_PATH NVARCHAR(150)
,@UNAME NVARCHAR(50) 
,@UPLOADFILENAME VARCHAR(100)
)
AS
BEGIN
BEGIN TRY

if CHARINDEX('XLS',@UPLOADFILENAME)>0
BEGIN
DECLARE @SQL NVARCHAR(800)
SET @SQL = ''
SET @SQL = @SQL + ' SELECT * INTO ##CLSRECONTABLE FROM CLSRECONTABLE_'+@UPLOADID
SET @SQL = @SQL + ' DROP TABLE CLSRECONTABLE_'+@UPLOADID

PRINT @SQL

EXEC(@SQL)

INSERT INTO #NONCMS_DATA (
		UPLOADID
		, BANKACCOUNT
		, BANKCODE
		, DESCRIPTION
		, LEDGERBALANCE
		, DRCR
		, AMT
		, VALUEDATE
		, REFERENCENO
		, CLTACCOUNT
		, CUSTOMERREFERENCE
		, TRANSACTIONDETAIL
		, VNO
		, VTYP
		, BOOKTYPE
		, MATCHEDFLAG
		, UPLOADSTATUS
		, UPLOADEDDATE
		, MODIFIEDDATE
		, UPLOADBY)

	SELECT
	(SELECT UPLOADID FROM RECON_BANK_FILEUPLOAD_LOGS(NOLOCK) WHERE UPLOADID=@UPLOADID) AS 'UPLOADID'
	,SEGMENT_NAME AS 'BANKACCOUNT'
	,@BANKCODE AS 'BANKCODE'
	,REF_NO+'_'+CLIENT_CODE +'_'+ SETTLEMENT_UTR+'_'+SEGMENT_NAME AS 'DESCRIPTION'
	,'' AS 'LEDGERBALANCE'	
	,'C' AS 'DRCR'	
	,CAST (RTRIM(LTRIM(AMOUNT)) AS DECIMAL(20,2)) AS 'AMOUNT'
	,CONVERT(DATETIME, RTRIM(LTRIM(SETTLEMENT_INITIATED_ON)), 103) AS 'VALUEDATE'
	, REF_NO AS 'REFERENCENO'
	,'' AS 'CLTACCOUNT'
	,'' AS 'CUSTOMERREFERENCE'
	,'' AS 'TRANSACTIONDETAIL'
	,'' AS 'VNO'
	,'' AS 'VTYP'
	,0  AS 'BOOKTYPE'
	,0
	,'PENDING' AS 'UPLOADSTATUS'
	,(SELECT FILEUPLOAD_SDATE FROM RECON_BANK_FILEUPLOAD_LOGS(NOLOCK) WHERE UPLOADID=@UPLOADID) AS 'FILEUPLOAD_SDATE'
	,GETDATE()
	,@UNAME	
	FROM ##CLSRECONTABLE
	WHERE ISNULL(AMOUNT , '')<>''
	
	DROP TABLE ##CLSRECONTABLE
	--DELETE FROM #NONCMS_DATA WHERE BANKACCOUNT<>'NSE'
	--SELECT * FROM #NONCMS_DATA WHERE BANKACCOUNT='NSE'
	--return
END

if CHARINDEX('CSV',UPPER(@UPLOADFILENAME))>0
BEGIN


SELECT REFERENCENO=.DBO.PIECE(FILE_DATA,',',6)
,[DESCRIPTION]=.DBO.PIECE(FILE_DATA,',',6)+'_'+.DBO.PIECE(FILE_DATA,',',7)+'_'+.DBO.PIECE(FILE_DATA,',',8)+'_'+.DBO.PIECE(FILE_DATA,',',10)
,AMOUNT=CAST(.DBO.PIECE(FILE_DATA,',',13) AS DECIMAL(20,2))
,bankaccount=''
into #tempdata
 FROM MSAJAG..FILEDATA (NOLOCK) WHERE PROGID=@UPLOADID
 AND UPPER(.DBO.PIECE(FILE_DATA,',',6))<>'SOURCE'
 AND ISNULL(.DBO.PIECE(FILE_DATA,',',6),'')<>''



--select 11, CHARINDEX('CSV',UPPER(@UPLOADFILENAME)),.dbo.PIECE(.dbo.PIECE(@UPLOADFILENAME,'_',3),'.',1)
--return
declare @d varchar(20),@valuedate varchar(15)

select @d =LTRIM(rtrim(.dbo.PIECE(.dbo.PIECE(@UPLOADFILENAME,'_',3),'.',1)))
select @valuedate=stuff(stuff(@d,5,0,'/'),3,0,'/')
--select @valuedate
--return

							INSERT INTO #NONCMS_DATA (
							UPLOADID
							, BANKACCOUNT
							, BANKCODE
							, DESCRIPTION
							, LEDGERBALANCE
							, DRCR
							, AMT
							, VALUEDATE
							, REFERENCENO
							, CLTACCOUNT
							, CUSTOMERREFERENCE
							, TRANSACTIONDETAIL
							, VNO
							, VTYP
							, BOOKTYPE
							, MATCHEDFLAG
							, UPLOADSTATUS
							, UPLOADEDDATE
							, MODIFIEDDATE
							, UPLOADBY
							)

							SELECT
							@UPLOADID AS 'UPLOADID'
							,(SELECT BANKACCOUNT FROM RECON_BANK_FILEUPLOAD_LOGS(NOLOCK) WHERE UPLOADID=@UPLOADID)  AS 'BANKACCOUNT'
							,@BANKCODE AS 'BANKCODE'
							,[DESCRIPTION] AS 'DESCRIPTION'
							,'' AS 'LEDGERBALANCE'	
							,'C' AS 'DRCR'	
							,CAST(AMOUNT AS DECIMAL(20,2)) AS 'AMOUNT'
							,CONVERT(DATETIME, CONVERT(VARCHAR(11),@valuedate,109),103) AS 'VALUEDATE'
							, REFERENCENO AS 'REFERENCENO'
							,'' AS 'CLTACCOUNT'
							,'' AS 'CUSTOMERREFERENCE'
							,'' AS 'TRANSACTIONDETAIL'
							,'' AS 'VNO'
							,'' AS 'VTYP'
							,0  AS 'BOOKTYPE'
							,0
							,'PENDING' AS 'UPLOADSTATUS'
							,GETDATE() AS 'FILEUPLOAD_SDATE'
							,GETDATE()
							,@UNAME	
							FROM #tempdata
							
							--MSAJAG..FILEDATA f,RECON_BANK_FILEUPLOAD_LOGS r
							-- WHERE PROGID=@UPLOADID
							-- and f.PROGID=r.UPLOADID
							-- AND UPPER(.DBO.PIECE(FILE_DATA,',',6))<>'SOURCE'
							-- AND ISNULL(.DBO.PIECE(FILE_DATA,',',6),'')<>''

							 --select * from #NONCMS_DATA
							 --return
END
END TRY
BEGIN CATCH  
  
 SELECT MSG=ERROR_MESSAGE()  
 DELETE D FROM RECON_BANK_UPLOAD_PROCESS_INFO D(NOLOCK) WHERE UPLOADID=@UPLOADID  
 DELETE FROM MSAJAG..FILEDATA WHERE PROGID=@UPLOADID  
 DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID  
 RETURN  
 END CATCH
	END

GO
