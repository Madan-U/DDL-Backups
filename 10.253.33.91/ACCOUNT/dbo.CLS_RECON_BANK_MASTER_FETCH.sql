-- Object: PROCEDURE dbo.CLS_RECON_BANK_MASTER_FETCH
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

Create PROCEDURE [dbo].[CLS_RECON_BANK_MASTER_FETCH]
(
	@FileType varchar(20)
)

AS
BEGIN
	IF @FileType =''
	BEGIN 
		SELECT BANKMASTERID,SEGMENT=UPPER(SEGMENT),BANKNAME=UPPER(BANKNAME),
		BANKCODE=UPPER(BANKCODE),BANKACCOUNT=UPPER(BANKACCOUNT),CLIENT=UPPER(CLIENT),FILETYPE=UPPER(FILETYPE),INSERTEDDATETIME,LASTUPDATE,FILEFORMATE,ENCRYPTIONCODE FROM RECON_BANK_MASTER_VW
	END
	ELSE IF @FileType ='BANKNAME'
	BEGIN 
		SELECT DISTINCT BankName FROM RECON_BANK_MASTER_VW ORDER BY BankName
	END
	ELSE
	BEGIN
		SELECT DISTINCT FILETYPE FROM RECON_BANK_MASTER_VW ORDER BY FILETYPE
	END
END

GO
