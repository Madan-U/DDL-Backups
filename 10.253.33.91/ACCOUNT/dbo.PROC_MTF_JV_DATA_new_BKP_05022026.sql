CREATE PROC [dbo].[PROC_MTF_JV_DATA_new_BKP_05022026]  
(  
 @SAUDA_DATE VARCHAR(11)  
)  
AS  
  
SELECT * INTO #C_JVDATA  
 FROM MTFTRADE.DBO.MTF_JVDATA_test  
WHERE SAUDA_DATE = @SAUDA_DATE  
AND FLAG = 'CASH'  
  
 ALTER TABLE #C_JVDATA  
 ADD SNO INT IDENTITY(1,1)  
   
 DECLARE @VNO  VARCHAR(12),  
   @RECCNT  INT,  
   @SDTCUR  VARCHAR(11),  
   @LDTCUR  VARCHAR(11)  
  
 SELECT @SDTCUR = SDTCUR, @LDTCUR = LDTCUR  
 FROM PARAMETER WHERE @SAUDA_DATE BETWEEN @SDTCUR AND LDTCUR  
  
 SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #C_JVDATA  
   
 IF @RECCNT > 0   
 BEGIN  
  CREATE TABLE #C_VNO  
  (  
   VNO VARCHAR(12)  
  )  
  
  INSERT INTO #C_VNO   
  EXEC ACC_GENVNO_new @SAUDA_DATE, 6, '01', @SDTCUR, @LDTCUR, @RECCNT  
  
  SELECT @VNO = VNO FROM #C_VNO  
  
  UPDATE #C_JVDATA SET VNO = CONVERT(VARCHAR,CONVERT(BIGINT,@VNO) + SNO - 1)  
   
  UPDATE #C_JVDATA SET ACNAME = A.LONGNAME   
  FROM ACMAST A  
  WHERE #C_JVDATA.PARTY_CODE = A.CLTCODE  
  
  INSERT INTO LEDGER  
  SELECT '6', VNO, @SAUDA_DATE, LNO=1, ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END) , AMOUNT, @SAUDA_DATE, '', '', 0, 0, GETDATE(),   
  PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'MTF JV FROM ' + FLAG + ' SEGMENT'
  FROM #C_JVDATA  
  
  INSERT INTO LEDGER  
  SELECT '6', VNO, @SAUDA_DATE, LNO=2, A.ACNAME, DRCR, AMOUNT, @SAUDA_DATE, '', '', 0, 0, GETDATE(),   
  GLCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'MTF JV FROM ' + FLAG + ' SEGMENT'
  FROM #C_JVDATA J, ACMAST A  
  WHERE J.GLCODE = A.CLTCODE  
 END

GO
