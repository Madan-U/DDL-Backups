CREATE PROC UNCLEARED_REC_ANG (@FROMDATE VARCHAR(11))  
  
AS  
  
-- EXEC UNCLEARED_REC_ANG 'APR 13 2023'  
  
DECLARE @START_DATE VARCHAR(11)  
  
SELECT @START_DATE = SDTCUR FROM PARAMETER WITH (NOLOCK) WHERE @FROMDATE BETWEEN SDTCUR AND LDTCUR  
  
SELECT @FROMDATE AS FDATE,CLTCODE,SUM(VAMT) AS VAMT FROM (  
SELECT CLTCODE,SUM(VAMT) AS VAMT FROM LEDGER L WITH (NOLOCK)   
WHERE VDT BETWEEN @FROMDATE AND @FROMDATE + ' 23:59:59' AND L.VTYP  =2  
AND CONVERT(DATETIME,CONVERT(VARCHAR(11),VDT)) <> CONVERT(DATETIME,CONVERT(VARCHAR(11),EDT))  
AND CLTCODE BETWEEN 'A' AND 'ZZZZZZZZZZ' GROUP BY CLTCODE  
UNION ALL  
SELECT CLTCODE,SUM(VAMT) AS VAMT FROM LEDGER L WITH (NOLOCK)   
WHERE VDT >= @START_DATE  
AND CONVERT(DATETIME,CONVERT(VARCHAR(11),VDT)) < CONVERT(DATETIME,@FROMDATE)  
AND CONVERT(DATETIME,CONVERT(VARCHAR(11),EDT)) > CONVERT(DATETIME,@FROMDATE)  
AND CONVERT(DATETIME,CONVERT(VARCHAR(11),VDT)) <> CONVERT(DATETIME,@FROMDATE)  
AND VTYP = 2 AND CLTCODE BETWEEN 'A' AND 'ZZZZZZZZZZ' GROUP BY CLTCODE) AS A GROUP BY CLTCODE

GO
