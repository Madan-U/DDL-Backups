CLTCODE IN ('03015') AND  VDT >=@FROMDATE AND VDT <=@TODATE + ' 23:59'   ) LR 
WHERE L.VNO=LR.VNO AND L.VTYP=LR.VTYP AND L.BOOKTYPE = LR.BOOKTYPE 
AND L.CLTCODE <>LR.CLTCODE AND L.CLTCODE IN (SELECT CLTCODE FROM ACMAST WHERE ACTYP IN ('INCOME','EXPENSES'))
