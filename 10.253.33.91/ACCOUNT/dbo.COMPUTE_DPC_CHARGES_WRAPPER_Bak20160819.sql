CREATE  PROCEDURE [dbo].[COMPUTE_DPC_CHARGES_WRAPPER]                    
(                    
 @FROM_DATE VARCHAR(11),                    
 @TO_DATE VARCHAR(11),                    
 @FROM_PARTY VARCHAR(10),                    
 @TO_PARTY VARCHAR(10)                    
)                    
AS                    
/*SELECT * INTO DPC_CHARGES_VERIFIED_CASH FROM DPC_CHARGES              
SELECT * FROM DPC_CHARGES WHERE CLTCODE = 's5643' AND entity = 'nse - FUTURES' ORDER BY DPC_DATE              
DELETE FROM DPC_CHARGES WHERE CLTCODE = 's5643'              
--EXEC COMPUTE_DPC_CHARGES_WRAPPER 'SEP  1 2012', 'SEP 15 2012', 'S5643', 'S5643'              
s5643              
*/         
      
TRUNCATE TABLE DEBIT_DEPC_BILLS      
      
IF  EXISTS (SELECT * FROM sys.objects WHERE object_id = OBJECT_ID(N'[dbo].[#DPC_CHARGES]') AND type in (N'U'))
DROP TABLE [dbo].[#DPC_CHARGES]
      
SELECT * INTO #DPC_CHARGES FROM DPC_CHARGES WHERE 1 = 2      

          
DECLARE                    
 @ACCOUNTSERVER VARCHAR(20),                    
 @ACCOUNTDB VARCHAR(20),                    
 @EXCHANGE VARCHAR(10),                    
 @SEGMENT VARCHAR(10),                    
 @GROUPID VARCHAR(10),                    
 @@SQL VARCHAR(1000),                    
 @@CUR AS CURSOR                    
         

 SET @@CUR = CURSOR FOR                    

 SELECT ACCOUNTSERVER, ACCOUNTDB, EXCHANGE, SEGMENT, GROUPID FROM DPC_MASTER_SETTING  ORDER BY EXCHANGE              

 OPEN @@CUR                    
 FETCH NEXT FROM @@CUR INTO @ACCOUNTSERVER, @ACCOUNTDB, @EXCHANGE, @SEGMENT, @GROUPID                    
 WHILE @@FETCH_STATUS = 0                    
 BEGIN                    
  TRUNCATE TABLE DPC_CHARGES_FINAL      
              
  --SET @@SQL = " EXEC " + @ACCOUNTSERVER + "." + @ACCOUNTDB + ".DBO.COMPUTE_DPC_CHARGES '" + @FROM_DATE + "','" + @TO_DATE + "','" + @FROM_PARTY + "','" + @TO_PARTY + "'"                    

	SET @@SQL = " INSERT INTO DPC_CHARGES_FINAL "                    
    SET @@SQL = @@SQL + " EXEC " + @ACCOUNTSERVER + "." + @ACCOUNTDB + ".DBO.COMPUTE_DPC_CHARGES '" + @FROM_DATE + "','" + @TO_DATE + "','" + @FROM_PARTY + "','" + @TO_PARTY + "'" 
        
  EXEC (@@SQL)           
  PRINT @@SQL          
  /*INSERT INTO DPC_CHARGES (DPC_DATE,CLTCODE,LONG_NAME,BRANCH_CD,SUB_BROKER,TRADER,REGION,AREA,CL_TYPE,DPC_BALANCE,LEDGER_BALANCE,CASH_COLLATREAL,NONCASH_COLLATREAL,MARGIN_REQ,DPC_CHARGE,DPC_PERCENTE,AMOUNT_REV,EXCHANGE,SEGMENT,ENTITY,SEG_GROUP,NET_TYPE_
  
    
BALANCE,NET_TYPE_INT,NET_BALANCE,NET_INT)                    
  SELECT                     
   DPC_DATE,CLTCODE,LONG_NAME,BRANCH_CD,SUB_BROKER,TRADER,REGION,AREA,CL_TYPE,DPC_BALANCE,LEDGER_BALANCE,CASH_COLLATREAL,NONCASH_COLLATREAL,MARGIN_REQ,DPC_CHARGE,DPC_PERCENTE,AMOUNT_REV,                     
   EXCHANGE = @EXCHANGE,                     
   SEGMENT = @SEGMENT,                     
   ENTIRY = @EXCHANGE + ' - ' + @SEGMENT,                    
   ENTITY_TYPE = @GROUPID,                    
   DPC_BALANCE, DPC_CHARGE, DPC_BALANCE, DPC_CHARGE             
  FROM DPC_CHARGES_FINAL        */      
        
  INSERT INTO #DPC_CHARGES (DPC_DATE,CLTCODE,LONG_NAME,BRANCH_CD,SUB_BROKER,TRADER,REGION,AREA,CL_TYPE,DPC_BALANCE,LEDGER_BALANCE,CASH_COLLATREAL,NONCASH_COLLATREAL,MARGIN_REQ,DPC_CHARGE,DPC_PERCENTE,AMOUNT_REV,EXCHANGE,SEGMENT,ENTITY,SEG_GROUP,NET_TYPE_BALANCE,NET_TYPE_INT,NET_BALANCE,NET_INT)
  SELECT                     
   DPC_DATE,CLTCODE,LONG_NAME,BRANCH_CD,SUB_BROKER,TRADER,REGION,AREA,CL_TYPE,DPC_BALANCE,LEDGER_BALANCE,CASH_COLLATREAL,NONCASH_COLLATREAL,MARGIN_REQ,DPC_CHARGE,DPC_PERCENTE,AMOUNT_REV,                     
   EXCHANGE = @EXCHANGE,                     
   SEGMENT = @SEGMENT,                     
   ENTIRY = @EXCHANGE + ' - ' + @SEGMENT,                    
   ENTITY_TYPE = @GROUPID,                    
   DPC_BALANCE, DPC_CHARGE, DPC_BALANCE, DPC_CHARGE             
  FROM DPC_CHARGES_FINAL(nolock)       
              
                     
  FETCH NEXT FROM @@CUR INTO @ACCOUNTSERVER, @ACCOUNTDB, @EXCHANGE, @SEGMENT, @GROUPID                     
 END      
    
 DECLARE                    
      @DAYS INT                    
      SELECT @DAYS = DATEDIFF(DD,@FROM_DATE,@TO_DATE) +1                    
                    
SELECT                     
 DPC_DATE = DT.DPC_DATE, CLTCODE, LONG_NAME, BRANCH_CD, SUB_BROKER, TRADER, REGION, AREA, CL_TYPE, DPC_PERCENTE,EXCHANGE, SEGMENT  INTO #ALL_DATES_EXCHSEG      
FROM                     
 #DPC_CHARGES D,                     
 (SELECT                    
           DPC_DATE = DATEADD(DD,NUMBER-1,@FROM_DATE)                     
      FROM                    
            DBO.F_TABLE_NUMBER_RANGE( 1, @DAYS )                    
  ) DT       
       
UPDATE D SET VDT = CASE WHEN DPC_BALANCE + BILL_AMOUNT > 0 THEN T_2_DATE ELSE T_4_DATE END                    
FROM                    
 DEBIT_DEPC_BILLS D(nolock), (SELECT CLTCODE, DPC_DATE, EXCHANGE, SEGMENT,       
 DPC_BALANCE = SUM(DPC_BALANCE) OVER(PARTITION BY CLTCODE, DPC_DATE) FROM #DPC_CHARGES       
 WHERE DPC_DATE BETWEEN @FROM_DATE AND @TO_DATE) C                    
WHERE                     
 CONVERT(DATETIME, CONVERT(VARCHAR(11), DPC_DATE, 109)) = CONVERT(DATETIME, CONVERT(VARCHAR(11), T_4_DATE, 109))                    
 AND D.CLTCODE = C.CLTCODE      
 AND D.EXCHANGE = C.EXCHANGE      
 AND D.SEGMENT = C.SEGMENT      
       
INSERT INTO DEBIT_DEPC_BILLS(CLTCODE, BILL_AMOUNT, VDT, EXCHANGE, SEGMENT)              
SELECT DISTINCT CLTCODE, 0, DPC_DATE, EXCHANGE, SEGMENT              
FROM #ALL_DATES_EXCHSEG A WHERE NOT EXISTS (SELECT VDT FROM DEBIT_DEPC_BILLS TEMP(nolock) WHERE A.CLTCODE = TEMP.CLTCODE AND A.DPC_DATE = TEMP.VDT AND A.EXCHANGE = TEMP.EXCHANGE AND A.SEGMENT = TEMP.SEGMENT)              
      
UPDATE D SET BILL_AMOUNT = (SELECT SUM(BILL_AMOUNT) FROM DEBIT_DEPC_BILLS D1(nolock) WHERE D.CLTCODE = D1.CLTCODE AND D1.VDT <= D.VDT AND D1.VDT >= @FROM_DATE AND D.EXCHANGE = D1.EXCHANGE AND D.SEGMENT = D1.SEGMENT)                    
FROM DEBIT_DEPC_BILLS D               
       
      
UPDATE C       
 SET DPC_BALANCE = DPC_BALANCE + BILL_AMOUNT,      
 LEDGER_BALANCE = LEDGER_BALANCE + BILL_AMOUNT,      
 DPC_CHARGE = (DPC_BALANCE + BILL_AMOUNT * DPC_PERCENTE)/ 100/ 365                    
FROM       
 #DPC_CHARGES C, DEBIT_DEPC_BILLS D(nolock)      
WHERE       
 CONVERT(DATETIME, CONVERT(VARCHAR(11), DPC_DATE, 109)) = CONVERT(DATETIME, CONVERT(VARCHAR(11), VDT, 109))                    
 AND D.CLTCODE = C.CLTCODE      
 AND D.EXCHANGE = C.EXCHANGE      
 AND D.SEGMENT = C.SEGMENT      
      
                     
UPDATE D SET NET_TYPE_BALANCE = D1.NET_TYPE_BALANCE, NET_TYPE_INT = CASE WHEN D1.NET_TYPE_INT > 0 THEN D1.NET_TYPE_INT ELSE 0 END                    
FROM #DPC_CHARGES D, (                    
 SELECT                     
 CLTCODE, SEG_GROUP, DPC_DATE,                     
 NET_TYPE_BALANCE = SUM(DPC_BALANCE),                    
 NET_TYPE_INT = SUM((DPC_BALANCE * DPC_PERCENTE) / 365)                     
 FROM DPC_CHARGES (nolock)                    
 GROUP BY CLTCODE, SEG_GROUP, DPC_DATE      
) D1                    
WHERE D.CLTCODE = D1.CLTCODE AND D.SEG_GROUP = D1.SEG_GROUP             
             
                    
 UPDATE D SET NET_BALANCE = D1.NET_BALANCE, NET_INT = CASE WHEN D1.NET_INT > 0 THEN D1.NET_INT ELSE 0 END                    
 FROM #DPC_CHARGES D, (                    
 SELECT                     
  CLTCODE, SEG_GROUP, DPC_DATE,                    
  NET_BALANCE = SUM(DPC_BALANCE),                    
  NET_INT = SUM((DPC_BALANCE * DPC_PERCENTE) / 365)                     
 FROM DPC_CHARGES(nolock)                    
 GROUP BY CLTCODE, SEG_GROUP, DPC_DATE) D1                    
 WHERE D.CLTCODE = D1.CLTCODE AND D.SEG_GROUP = D1.SEG_GROUP       
       
 INSERT INTO DPC_CHARGES      
 SELECT * FROM #DPC_CHARGES

GO
