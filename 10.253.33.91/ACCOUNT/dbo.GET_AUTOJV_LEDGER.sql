CREATE PROCEDURE GET_AUTOJV_LEDGER
	@START_DATE VARCHAR(11),
	@START_DATENEW VARCHAR(11),
	@CLTCODE_FR VARCHAR(10),
	@CLTCODE_TO VARCHAR(10)
AS


SELECT CLTCODE, VDT, EDT, VAMT, DRCR, VNO, VTYP, BOOKTYPE, LNO 
FROM   LEDGER (NOLOCK)
WHERE EDT >= @START_DATE
AND VDT <= @START_DATENEW + ' 23:59' 
AND CLTCODE BETWEEN  @CLTCODE_FR AND @CLTCODE_TO

GO
