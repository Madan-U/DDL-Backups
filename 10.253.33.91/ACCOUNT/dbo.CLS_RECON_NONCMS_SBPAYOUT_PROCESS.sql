-- Object: PROCEDURE dbo.CLS_RECON_NONCMS_SBPAYOUT_PROCESS
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------



CREATE PROCEDURE [dbo].[CLS_RECON_NONCMS_SBPAYOUT_PROCESS]
( 
 @UPLOADID NVARCHAR(50) 
,@BANKCODE NVARCHAR(50)
,@UPLOADFILENAME NVARCHAR(50)
,@UNAME VARCHAR(50)
) 
AS

BEGIN
		
		DECLARE @D VARCHAR(50),@VALUEDATE VARCHAR(11)
		SELECT  @D=.DBO.PIECE(@UPLOADFILENAME,'_',2)


		SELECT @VALUEDATE= STUFF(STUFF( @D, 5, 0, '/'), 3, 0, '/')-- DD/MM/YYYY
		IF CHARINDEX('.CSV',UPPER(@UPLOADFILENAME))>1
		SELECT @VALUEDATE=REPLACE(@VALUEDATE,'.','')

		IF(@VALUEDATE='' OR @VALUEDATE IS NULL)
		BEGIN
			DELETE FROM MSAJAG..FILEDATA	WHERE PROGID = @UPLOADID
			SELECT		MSG = 'PLEASE ADD DATE IN THE FILE NAME'
			DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
			DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
			RETURN
		END


	IF CHARINDEX('.CSV',@UPLOADFILENAME) > 0
	BEGIN
		BEGIN TRY

			SELECT * 
			INTO #TEMP_SBPAYOUT
			FROM MSAJAG..FILEDATA (NOLOCK) WHERE PROGID=@UPLOADID
			ORDER BY SRNO DESC

			DELETE FROM #TEMP_SBPAYOUT WHERE ISNULL(FILE_DATA,'')='' --and 
			DELETE FROM #TEMP_SBPAYOUT WHERE FILE_DATA LIKE '%FORT%'

			
				UPDATE #TEMP_SBPAYOUT SET  FILE_DATA=ISNULL(DBO.PIECE(FILE_DATA,'"',1),'')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',2),'')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',3),'')
				+ REPLACE(ISNULL(DBO.PIECE(FILE_DATA,'"',4),''),',','')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',5),'')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',6),'')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',7),'')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',8),'')
				WHERE 
				ISNULL(DBO.PIECE(FILE_DATA,'"',4),'')<>''
				AND ISNULL(DBO.PIECE(FILE_DATA,'"',4),'') NOT LIKE '%Jehangir%'
				AND ISNULL(DBO.PIECE(FILE_DATA,'"',4),'') NOT LIKE '%RTGS%'

				UPDATE #TEMP_SBPAYOUT SET  FILE_DATA=ISNULL(DBO.PIECE(FILE_DATA,'"',1),'')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',2),'')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',3),'')
				+ REPLACE(ISNULL(DBO.PIECE(FILE_DATA,'"',4),''),',','')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',5),'')
				+ REPLACE(ISNULL(DBO.PIECE(FILE_DATA,'"',6),''),',','')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',7),'')
				+ ISNULL(DBO.PIECE(FILE_DATA,'"',8),'')
				WHERE 
				ISNULL(DBO.PIECE(FILE_DATA,'"',4),'')<>''
				AND ISNULL(DBO.PIECE(FILE_DATA,'"',4),'') NOT LIKE '%Jehangir%'
				AND ISNULL(DBO.PIECE(FILE_DATA,'"',4),'') LIKE '%RTGS%'

				--update #TEMP_SBPAYOUT set file_data  =replace (file_data,'FORT,NEFT','FORT,,NEFT')  where FILE_DATA like '%FORT,NEFT%'

			INSERT INTO #NONCMS_DATA (UPLOADID
			, BANKACCOUNT
			, BANKCODE
			, DESCRIPTION
			, LEDGERBALANCE
			, DRCR
			, AMT
			, VALUEDATE
			, REFERENCENO
			, CLTACCOUNT
			, CUSTOMERREFERENCE
			, TRANSACTIONDETAIL
			, VNO
			, VTYP
			, BOOKTYPE
			, MATCHEDFLAG
			, UPLOADSTATUS
			, UPLOADEDDATE
			, MODIFIEDDATE
			, UPLOADBY)

			SELECT 
			PROGID
			,ACCOUNTNUMBER=RL.BANKACCOUNT
			,BANKCODE = @BANKCODE
			,DESCRIPTION =left(.DBO.PIECE(FILE_DATA,',',5),50)
			,LEDGERBALANCE=''
			,DRCR =  CASE WHEN .DBO.PIECE(FILE_DATA,',',8)='0.00' THEN 'D' ELSE 'C' END
			,AMOUNT = CASE WHEN .DBO.PIECE(FILE_DATA,',',8)='0.00' THEN CAST(.DBO.PIECE(FILE_DATA,',',7) AS MONEY) ELSE CAST(.DBO.PIECE(FILE_DATA,',',8) AS MONEY) END
			,VALUEDATE = CONVERT(DATETIME, @VALUEDATE,103)
			,REFERENCENO = LEFT(.DBO.PIECE(FILE_DATA,',',4),30)
			,CLTACCOUNT =''
			,CUSTOMERREFERENCE =''
			, TRANSACTIONDETAIL=''
			, VNO = ''
			, VTYP = ''
			, BOOKTYPE = ''
			, MATCHEDFLAG = 0
			, UPLOADSTATUS = 'PENDING'
			, UPLOADEDDATE = GETDATE()
			, MODIFIEDDATE = GETDATE()
			, UPLOADBY=RL.UPLOADBY
			FROM #TEMP_SBPAYOUT T , RECON_BANK_FILEUPLOAD_LOGS (NOLOCK) RL 
			WHERE T.PROGID=RL.UPLOADID
			AND .DBO.PIECE(FILE_DATA,',',3)<>''
			AND DBO.PIECE(FILE_DATA,',',3) NOT IN ('DESCRIPTION','G-1 Akruti Trade Centre','"Hdfc Bank Ltd.')
			
			DROP TABLE #TEMP_SBPAYOUT
			DELETE FROM MSAJAG..FILEDATA WHERE PROGID=@UPLOADID
			
		END TRY

		BEGIN CATCH

			DROP TABLE #TEMP_SBPAYOUT
			DELETE FROM MSAJAG..FILEDATA WHERE PROGID=@UPLOADID
			SELECT MSG=ERROR_MESSAGE()
			DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
			DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
			

		END CATCH

	END

IF((SELECT COUNT(1) FROM #NONCMS_DATA) <=0)
BEGIN
	SELECT MSG='NOT CORRECT FILE FORMAT '
	DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
	DELETE FROM MSAJAG..FILEDATA WHERE PROGID=@UPLOADID
	DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
	RETURN
END


END

GO
