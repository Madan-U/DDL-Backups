CREATE PROCEDURE [dbo].[RPT_TOP10_DEBTORS_02052022] (@FDATE VARCHAR(11),@TDATE VARCHAR(11))

AS ---- EXEC RPT_TOP10_DEBTORS 'NOV  1 2021','NOV 10 2021'

SELECT REPORT_DATE,CLTCODE,BALANCE INTO #TOPDRCR
FROM SFCM_REPORTING_EXCHANGE WITH (NOLOCK)
WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
AND BALANCE < 0

INSERT INTO #TOPDRCR
SELECT C.REPORT_DATE,C.CLTCODE,BALANCE=SUM(C.BALANCE)
FROM SFCM_REPORTING C WITH (NOLOCK) 
WHERE C.REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
AND C.CLTCODE IN (SELECT CL_CODE FROM MSAJAG.DBO.CLIENT_DETAILS WITH (NOLOCK))
GROUP BY C.REPORT_DATE,C.CLTCODE
HAVING SUM(C.BALANCE) < 0

INSERT INTO #TOPDRCR
SELECT C.REPORT_DATE,C.CLTCODE,BALANCE=SUM(C.BALANCE)
FROM SFCM_REPORTING_LOG C WITH (NOLOCK) 
WHERE C.REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
AND C.CLTCODE IN (SELECT CL_CODE FROM MSAJAG.DBO.CLIENT_DETAILS WITH (NOLOCK))
GROUP BY C.REPORT_DATE,C.CLTCODE
HAVING SUM(C.BALANCE) < 0

INSERT INTO #TOPDRCR
SELECT C.REPORT_DATE,C.CLTCODE,BALANCE=SUM(C.BALANCE)
FROM SFCM_REPORTING_HISTORY C WITH (NOLOCK) 
WHERE C.REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
AND C.CLTCODE IN (SELECT CL_CODE FROM MSAJAG.DBO.CLIENT_DETAILS WITH (NOLOCK))
GROUP BY C.REPORT_DATE,C.CLTCODE
HAVING SUM(C.BALANCE) < 0

DELETE FROM #TOPDRCR WHERE CLTCODE='ERROR' ---- AS PER CONFIRMATION OF RICHA COMPLIANCE TEAM

--SELECT TOP 10 * INTO #DRCRDATA FROM #TOPDRCR A WHERE BALANCE IN (SELECT MAX(BALANCE) FROM #TOPDRCR B WHERE A.CLTCODE = B.CLTCODE)
--AND A.REPORT_DATE IN (SELECT MAX(REPORT_DATE) FROM #TOPDRCR C WHERE A.CLTCODE = C.CLTCODE)
--ORDER BY ABS(A.BALANCE) DESC  

SELECT TOP 10 A.REPORT_DATE,A.CLTCODE,CLIENT_NAME=CONVERT(VARCHAR(500),''),PANNO=CONVERT(VARCHAR(20),''),A.BALANCE,REPORT='DR' INTO #DRCRDATA 
FROM #TOPDRCR A WHERE BALANCE IN (SELECT MAX(BALANCE) FROM #TOPDRCR B WHERE A.CLTCODE = B.CLTCODE)
AND A.REPORT_DATE IN (SELECT MAX(REPORT_DATE) FROM #TOPDRCR C WHERE A.CLTCODE = C.CLTCODE)
ORDER BY ABS(A.BALANCE) DESC 

UPDATE #DRCRDATA SET CLIENT_NAME = LONG_NAME, PANNO = PAN_GIR_NO FROM #DRCRDATA A , MSAJAG.DBO.CLIENT_DETAILS B WITH (NOLOCK)
WHERE A.CLTCODE = B.CL_CODE

SELECT * FROM #DRCRDATA ORDER BY ABS(BALANCE) DESC

GO
