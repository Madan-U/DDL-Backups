-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROC [dbo].[RPT_SCFM_REPORTING_PROCESS_04052022] (@TODATE  DATETIME)

AS --- EXEC RPT_SCFM_REPORTING_PROCESS 'JAN 24 2022'

DECLARE @FROMDATE DATETIME
SELECT @FROMDATE =SDTCUR FROM PARAMETER WHERE @TODATE BETWEEN SDTCUR AND LDTCUR

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='NSECM'                
INTO #NSECM                  
FROM LEDGER (NOLOCK) WHERE VDT > = @FROMDATE        
AND EDT <= @TODATE  + ' 23:59'        
GROUP BY CLTCODE

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='BSECM'            
INTO #BSECM                  
FROM ANAND.ACCOUNT_AB.DBO.LEDGER  WHERE VDT >= @FROMDATE         
AND EDT <= @TODATE  + ' 23:59'        
GROUP BY CLTCODE

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='NSEFO'                  
INTO #NSEFO                  
FROM ANGELFO.ACCOUNTFO.DBO.LEDGER  WHERE VDT >= @FROMDATE        
AND EDT <= @TODATE  + ' 23:59'        
GROUP BY CLTCODE

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='NSECD'                  
INTO #NSECD
FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER  WHERE VDT >= @FROMDATE        
AND EDT <= @TODATE  + ' 23:59'        
GROUP BY CLTCODE

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='MCXCD'                  
INTO #MCXCD
FROM ANGELCOMMODITY.ACCOUNTMCDXCDS.DBO.LEDGER  WHERE VDT >= @FROMDATE
AND EDT <= @TODATE  + ' 23:59'        
GROUP BY CLTCODE

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='MTF'                  
INTO #MTF                  
FROM MTFTRADE.DBO.LEDGER  WHERE VDT >= @FROMDATE            
AND EDT <= @TODATE  + ' 23:59'        
GROUP BY CLTCODE

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='SLBS'                  
INTO #SLBS                  
FROM ACCOUNTSLBS.DBO.LEDGER  WHERE VDT >= @FROMDATE            
AND EDT <= @TODATE  + ' 23:59'        
GROUP BY CLTCODE        

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='MCDX'                 
INTO #MCDX        
FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER WHERE VDT >= @FROMDATE             
AND EDT <= @TODATE +' 23:59' GROUP BY CLTCODE         

SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='NCDX'                  
INTO #NCDX                  
FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER  WHERE VDT >= @FROMDATE             
AND EDT <= @TODATE +' 23:59' GROUP BY CLTCODE          
        
SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='BSEFO'                  
INTO #BSEFO                  
FROM ANGELCOMMODITY.ACCOUNTBFO.DBO.LEDGER  WHERE VDT >= @FROMDATE             
AND EDT <= @TODATE +' 23:59' GROUP BY CLTCODE          
        
SELECT CLTCODE,NETAMT=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),EXCHANGE='BSECD'                 
INTO #BSECD                 
FROM ANGELCOMMODITY.ACCOUNTCURBFO.DBO.LEDGER  WHERE VDT >= @FROMDATE             
AND EDT <=@TODATE +' 23:59' GROUP BY CLTCODE


SELECT * INTO #AB FROM (                  
SELECT * FROM #NSECM  WHERE NETAMT <> 0                
UNION ALL                  
SELECT * FROM #BSECM WHERE NETAMT <> 0                 
UNION ALL                  
SELECT * FROM #NSEFO WHERE NETAMT <> 0                 
UNION ALL                  
SELECT * FROM #NSECD WHERE NETAMT <> 0                  
UNION ALL                  
SELECT * FROM #MCXCD WHERE NETAMT <> 0         
UNION ALL                  
SELECT * FROM #MTF WHERE NETAMT <> 0       
UNION ALL
SELECT * FROM #SLBS WHERE NETAMT <> 0
UNION ALL        
SELECT * FROM #MCDX WHERE NETAMT <> 0                 
UNION ALL                  
SELECT * FROM #NCDX WHERE NETAMT <> 0          
UNION ALL                  
SELECT * FROM #BSEFO WHERE NETAMT <> 0         
UNION ALL                  
SELECT * FROM #BSECD WHERE NETAMT <> 0         
)X

DELETE FROM #AB WHERE CLTCODE NOT IN (SELECT PARTY_CODE FROM SCFM_CLIENT)
DELETE FROM #AB WHERE CLTCODE='BBBB'

IF (SELECT COUNT(*) FROM SCFM_REPORTING WHERE REPORT_DATE = @TODATE) <> 0
BEGIN
TRUNCATE TABLE SCFM_REPORTING
END

IF (SELECT COUNT(*) FROM SCFM_REPORTING_LOG WHERE REPORT_DATE = @TODATE ) <> 0
BEGIN
DELETE FROM SCFM_REPORTING_LOG WHERE REPORT_DATE = @TODATE
END

INSERT INTO SCFM_REPORTING_LOG
SELECT *,GETDATE() FROM SCFM_REPORTING

TRUNCATE TABLE SCFM_REPORTING

INSERT INTO SCFM_REPORTING
SELECT @TODATE,CLTCODE,[BSECM]=ISNULL([BSECM],0),[BSEFO]=ISNULL([BSEFO],0),[BSECD]=ISNULL([BSECD],0),
[NSECM]=ISNULL([NSECM],0),[NSEFO]=ISNULL([NSEFO],0),[NSECD]=ISNULL([NSECD],0),
[MTF]=ISNULL([MTF],0),[SLBS]=ISNULL([SLBS],0),[MCXCD]=ISNULL([MCXCD],0),
[MCDX]=ISNULL([MCDX],0),[NCDX]=ISNULL([NCDX],0),
NET_BALANCE=ISNULL([NSECM],0)+ISNULL([BSECM],0)+ISNULL([NSEFO],0)+ISNULL([BSEFO],0)+ISNULL([MTF],0)
+ISNULL([SLBS],0)+ISNULL([NSECD],0)+ISNULL([BSECD],0)+ISNULL([MCXCD],0)+ISNULL([MCDX],0)+ISNULL([NCDX],0),GETDATE()
FROM (SELECT CLTCODE,LEDBAL=SUM(NETAMT),EXCHANGE FROM #AB GROUP BY CLTCODE,EXCHANGE ) AS SOURCETABLE
PIVOT
(SUM(LEDBAL) FOR EXCHANGE IN ([NSECM],[BSECM],[NSEFO],[BSEFO],[MTF],[SLBS],[NSECD],[BSECD],[MCXCD],[MCDX],[NCDX])) AS PIVOTTABLE

SELECT 'SCFM REPORTING PROCESS RUN SUCESSFULLY !!'

GO
