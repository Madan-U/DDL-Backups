CREATE PROCEDURE [dbo].[GST_REVESAL_POSTING]
	@INV_DATE VARCHAR(25),
	@PARTY_CODE VARCHAR(10),
	@INV_NO VARCHAR(20),
	@RECORDFOR VARCHAR(20),
	@INVOICE_TYPE VARCHAR(20),
	@REV_AMOUNT MONEY,
	@VDT VARCHAR(10),
	@GL_CODE VARCHAR(10),
	@NARRATION VARCHAR(500),
	@UNAME VARCHAR(20),
	@ReversalType VARCHAR(20),
	@BranchCode  VARCHAR(20),
	@SrNO INT,
	@ACTIONBY VARCHAR(20),
	@Remarks VARCHAR(300)
AS

IF LEN(@INV_DATE) = 10
	AND CHARINDEX('/', @INV_DATE) > 0
BEGIN
	SET @INV_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @INV_DATE, 103), 109)
END


DECLARE
	@FROMDATE VARCHAR(11),
	@TODATE VARCHAR(11)

SET @FROMDATE = LEFT(CONVERT(DATETIME,@INV_DATE),11)

SET @TODATE = LEFT(DATEADD(M,1,CONVERT(DATETIME,@INV_DATE))-1,11)


DECLARE
	@SESSIONID VARCHAR(20)

CREATE TABLE #GST_SETTING
(
	STATE_CODE VARCHAR(10),
	IGST_RATE MONEY,
	CGST_RATE MONEY,
	SGST_RATE MONEY
)



INSERT INTO #GST_SETTING
SELECT TOP 1 STATE_CODE, IGST_RATE, CGST_RATE, SGST_RATE FROM MSAJAG.DBO.TBL_GST_INVOICE--_TMP
WHERE INVOICE_DATE BETWEEN @FROMDATE AND @TODATE
AND INV_NO = @INV_NO
AND RECORDFOR = @RECORDFOR
AND INVOICE_TYPE = @INVOICE_TYPE

CREATE TABLE #GST_CODES
(
	GST_CODE VARCHAR(10),
	GTS_RATE MONEY,
	SNO INT IDENTITY(1, 1)
)


INSERT INTO #GST_CODES
SELECT ACCODE, CASE WHEN IGST_RATE <> 0 THEN IGST_RATE ELSE CASE WHEN CGST_RATE <> 0 THEN CGST_RATE ELSE CASE WHEN SGST_RATE <> 0 THEN SGST_RATE END END END FROM MSAJAG.DBO.Valanaccount V, #GST_SETTING G 
WHERE 
	LEFT(ACNAME, 4) = STATE_CODE + '_' + CASE WHEN IGST_RATE <> 0 THEN 'I' END 
	OR LEFT(ACNAME, 4) = STATE_CODE + '_' + CASE WHEN CGST_RATE <> 0 THEN 'C' END
	OR LEFT(ACNAME, 4) = STATE_CODE + '_' + CASE WHEN SGST_RATE <> 0 THEN 'S' END
	

SELECT @SESSIONID = REPLACE(CONVERT(VARCHAR, GETDATE(),111),'/','') + REPLACE(CONVERT(VARCHAR, GETDATE(),114),':','')


/* Start here for Exclusive & Inclusive Amount Effect*/

DECLARE @GLAmount Money
DECLARE @GSTAmount Money
DECLARE @CltCodeAmount Money

DECLARE @GSTDivided DECIMAL(18,2)
DECLARE @SGST INT
DECLARE @CGST INT
DECLARE @IGST INT
DECLARE @SIGST INT
DECLARE @CIGST INT

SELECT @GSTDivided = 100 + CGST_RATE + IGST_RATE + SGST_RATE, @CIGST = CGST_RATE + IGST_RATE, @SIGST = SGST_RATE  + IGST_RATE, @SGST= SGST_RATE , @CGST= CGST_RATE, @IGST = IGST_RATE FROM #GST_SETTING

DECLARE @CGSRValue DECIMAL(18,2)
SET @CGSRValue = (@REV_AMOUNT - @REV_AMOUNT * 100/@GSTDivided) *@CIGST/(@CIGST+@SGST)
-- SELECT  (236 - 236 * 100/236) * 9 /18
DECLARE @SGSRValue DECIMAL(18,2)
SET @SGSRValue = (@REV_AMOUNT - @REV_AMOUNT * 100/@GSTDivided) *@SIGST/(@SIGST+@SGST)

DECLARE @IGSTValue DECIMAL(18,2)
SET @IGSTValue = CASE WHEN @IGST <> 0 THEN  (@REV_AMOUNT - @REV_AMOUNT * 100/@GSTDivided) *@IGST/(@IGST) ELSE 0 END 




INSERT INTO JV_CN_DN_DETAILS
SELECT 
	VTYPE = CASE WHEN @INVOICE_TYPE = 'DEBIT' THEN 7 ELSE 6 END,
	BOOKTYPE = '01',
	SRNO = 1, VVDT = @VDT, EEDT = @VDT,
	CLTCODE = @PARTY_CODE, 
	DRCR = CASE WHEN @INVOICE_TYPE = 'DEBIT' THEN 'C' ELSE 'D' END,
	--AMOUNT = @REV_AMOUNT,
	AMOUNT = CASE WHEN (UPPER(LEFT(@ReversalType,1)) = 'E') THEN @REV_AMOUNT + @REV_AMOUNT * @CGST /100 + @REV_AMOUNT * @SGST /100  + @REV_AMOUNT * @IGST /100 
					WHEN (UPPER(LEFT(@ReversalType,1)) = 'I') THEN @REV_AMOUNT END, 
	NARRATION = @NARRATION,
	BRANCHCODE = 'ALL',
	L1_SRNO = 1,
	SESSIONID = @SESSIONID

INSERT INTO JV_CN_DN_DETAILS
SELECT 
	VTYPE = CASE WHEN @INVOICE_TYPE = 'DEBIT' THEN 7 ELSE 6 END,
	BOOKTYPE = '01',
	SRNO = 1, VVDT = @VDT, EEDT = @VDT,
	CLTCODE = GST_CODE, 
	DRCR = CASE WHEN @INVOICE_TYPE = 'DEBIT' THEN 'D' ELSE 'C' END,
	--AMOUNT = (@REV_AMOUNT * GTS_RATE) / 100,
	AMOUNT = CASE WHEN (UPPER(LEFT(@ReversalType,1)) = 'E') THEN  @REV_AMOUNT * GTS_RATE /100 WHEN (UPPER(LEFT(@ReversalType,1)) = 'I') 
				THEN   (@REV_AMOUNT - @REV_AMOUNT * 100/@GSTDivided) *@CIGST/(@CIGST+@SGST) END ,
	NARRATION = @NARRATION,
	BRANCHCODE = 'ALL',
	L1_SRNO = SNO + 2,
	SESSIONID = @SESSIONID
FROM #GST_CODES


PRINT 'CGS' + CONVERT(VARCHAR(20),@CGSRValue)
PRINT 'SGS' + CONVERT(VARCHAR(20),@SGSRValue)

INSERT INTO JV_CN_DN_DETAILS
SELECT 
	VTYPE = CASE WHEN @INVOICE_TYPE = 'DEBIT' THEN 7 ELSE 6 END,
	BOOKTYPE = '01',
	SRNO = 1, VVDT = @VDT, EEDT = @VDT,
	CLTCODE = @GL_CODE, 
	DRCR = CASE WHEN @INVOICE_TYPE = 'DEBIT' THEN 'D' ELSE 'C' END,
	--AMOUNT = (SELECT ABS(SUM(CASE WHEN DRCR = 'D' THEN AMOUNT ELSE -AMOUNT END)) FROM JV_CN_DN_DETAILS WHERE SESSIONID = @SESSIONID),
	AMOUNT = CASE WHEN (UPPER(LEFT(@ReversalType,1)) = 'I') THEN  @REV_AMOUNT  - CASE WHEN @IGST = 0 THEN  (@CGSRValue + @SGSRValue) ELSE @IGSTValue END 
				  WHEN (UPPER(LEFT(@ReversalType,1)) = 'E') THEN @REV_AMOUNT END ,	
				
	NARRATION = @NARRATION,
	BRANCHCODE = 'ALL',
	L1_SRNO = 2,
	SESSIONID = @SESSIONID


CREATE TABLE #POSTING_DATA
(
	CLTCODE VARCHAR(100),
	AMOUNT MONEY,
	VNO VARCHAR(12),
	VTYP INT,
	BOOKTYPE VARCHAR(3)
)


PRINT @SGST 
PRINT @CGST 
PRINT @IGST 

--EXEC JV_DN_CN_POSTING @SESSIONID, @UNAME
EXEC JV_DN_CN_POSTING_WithOutput @SESSIONID, @UNAME 
DECLARE
	@VNO VARCHAR(12),
	@VTYP INT,
	@BOOKTYPE VARCHAR(3)

--INSERT INTO #OutputData
--SELECT rrr = CLTCODE + ', ' + CONVERT(VARCHAR, AMOUNT) + ', ' + VNO FROM #POSTING_DATA


SELECT TOP 1 @VNO = VNO, @VTYP = VTYP, @BOOKTYPE = BOOKTYPE FROM #POSTING_DATA
IF(@VNO = '' OR @VNO IS NULL)  
RETURN;


INSERT INTO GST_REVERSAL_ENTRY_DETAILS
SELECT
	@INV_DATE,
	@PARTY_CODE,
	@INV_NO,
	@RECORDFOR,
	@INVOICE_TYPE,
	--@REV_AMOUNT,
	CASE WHEN (UPPER(LEFT(@ReversalType,1)) = 'E') THEN @REV_AMOUNT + @REV_AMOUNT * @CGST /100 + @REV_AMOUNT * @SGST /100  + @REV_AMOUNT * @IGST /100 
					WHEN (UPPER(LEFT(@ReversalType,1)) = 'I') THEN @REV_AMOUNT END,
	@VDT,
	@GL_CODE,
	@NARRATION,
	@VNO,
	@VTYP,
	@BOOKTYPE,
	@UNAME,
	GETDATE(),
	@BranchCode

INSERT INTO GST_REVERSAL_OutputData
SELECT RESULT = 'Data Uploaded Successfully'          
UNION ALL          
SELECT RESULT = CLTCODE + ', ' + CONVERT(VARCHAR, AMOUNT) + ', ' + VNO FROM #POSTING_DATA


UPDATE MSAJAG.DBO.GSTReversal_Preprocess
SET [Status] = 'A', ActionBy = @ACTIONBY, ActionOn = GETDATE(), Remarks = @Remarks
WHERE Srno = @SrNO

--SELECT * FROM delete GST_REVERSAL_ENTRY_DETAILS where INV_DATE = 'JUN 2017'

GO
