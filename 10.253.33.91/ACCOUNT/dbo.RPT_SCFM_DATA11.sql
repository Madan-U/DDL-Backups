--------------------------------------------------



CREATE PROCEDURE [dbo].[RPT_SCFM_DATA11] (@FDATE VARCHAR(11),@RUNDATE VARCHAR(11))
 
AS --- EXEC RPT_SCFM_DATA11 'JUN 24 2022','JUN 24 2022'

SELECT TRANSDATE,EXCHANGE,SEGMENT,PARTY_CODE,CASH_AMT,GSEC_AMT,NONCASH_AMT,TOTAL_AMT,MARGIN_AMT,
MARGIN_AVAILABLE_AMT=CONVERT(MONEY,0),FREE_CASH_AMT=CONVERT(MONEY,0) INTO #SCFM11
FROM MSAJAG.DBO.TBL_CC02_DATA_ABL WITH (NOLOCK) WHERE TRANSDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59:59'

DELETE FROM #SCFM11 WHERE PARTY_CODE IN ('12798','PRO_12798')

UPDATE #SCFM11 SET MARGIN_AVAILABLE_AMT = CASE WHEN (MARGIN_AMT-GSEC_AMT-NONCASH_AMT) > 0 THEN (MARGIN_AMT-GSEC_AMT-NONCASH_AMT) ELSE 0 END

UPDATE #SCFM11 SET FREE_CASH_AMT = CASE WHEN (CASH_AMT - MARGIN_AVAILABLE_AMT) > 0 THEN (CASH_AMT - MARGIN_AVAILABLE_AMT) ELSE 0 END

SELECT [Transaction Date]=CONVERT(VARCHAR(11),TRANSDATE,105),Exchange,Segment,
[Margin Available]=SUM(MARGIN_AVAILABLE_AMT),[Free Cash Amount]=SUM(FREE_CASH_AMT)
FROM #SCFM11 
GROUP BY TRANSDATE,EXCHANGE,SEGMENT
UNION ALL 
SELECT '-','TOTAL','-',SUM(MARGIN_AVAILABLE_AMT),SUM(FREE_CASH_AMT) FROM #SCFM11

GO
