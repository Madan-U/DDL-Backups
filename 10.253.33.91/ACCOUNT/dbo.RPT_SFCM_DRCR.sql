-- Object: PROCEDURE dbo.RPT_SFCM_DRCR
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


 CREATE PROC [dbo].[RPT_SFCM_DRCR] (@FDATE VARCHAR(11),@TDATE VARCHAR(11))
 
 AS  --- EXEC RPT_SFCM_DRCR 'DEC  3 2021','DEC  3 2021'
 
  DECLARE @RUNTIEM VARCHAR(10)
  
 CREATE TABLE #DRCR (REPORT_DATE VARCHAR(11),CLTCODE VARCHAR(10),BALANCE MONEY)

IF EXISTS(SELECT 1 FROM ANAND1.ACCOUNT.DBO.SFCM_REPORTING A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')
BEGIN

	 SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(RUNDATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING

     INSERT INTO #DRCR               
	 SELECT CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,SUM(BALANCE) AS BALANCE  FROM SFCM_REPORTING A,MSAJAG.DBO.CLIENT_DETAILS M        
	 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59' 
	 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,CONVERT(VARCHAR(11),REPORT_DATE,105)
END

IF EXISTS(SELECT 1 FROM ANAND1.ACCOUNT.DBO.SFCM_REPORTING_LOG A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')
BEGIN

	 SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(LOG_DATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING_LOG

     INSERT INTO #DRCR               
	 SELECT CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,SUM(BALANCE) AS BALANCE  FROM SFCM_REPORTING_LOG A,MSAJAG.DBO.CLIENT_DETAILS M        
	 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59' 
	 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,CONVERT(VARCHAR(11),REPORT_DATE,105)
END

IF EXISTS(SELECT 1 FROM ANAND1.ACCOUNT.DBO.SFCM_REPORTING_HISTORY A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')
BEGIN

	 SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(HIS_DATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING_HISTORY 
	 WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59' 

     INSERT INTO #DRCR               
	 SELECT CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,SUM(BALANCE) AS BALANCE  FROM SFCM_REPORTING_HISTORY A,MSAJAG.DBO.CLIENT_DETAILS M        
	 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59' 
	 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,CONVERT(VARCHAR(11),REPORT_DATE,105)
END

SELECT Report='Creditors',Report_Date,RunTime=@RUNTIEM,CONVERT(VARCHAR,(SUM(BALANCE)/10000000)) AS [Amount in Cr],[Amount in Rs] = SUM(BALANCE) 
FROM #DRCR WHERE BALANCE >0 GROUP BY REPORT_DATE          
 UNION ALL          
SELECT Report='Debtors',Report_Date,RunTime=@RUNTIEM,CONVERT(VARCHAR,(SUM(BALANCE)/10000000)) AS [Amount in Cr],[Amount in Rs] = SUM(BALANCE) 
FROM #DRCR WHERE BALANCE <0 GROUP BY REPORT_DATE

GO
