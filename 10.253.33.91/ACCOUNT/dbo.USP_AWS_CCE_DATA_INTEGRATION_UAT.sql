CREATE PROC [dbo].[USP_AWS_CCE_DATA_INTEGRATION_UAT]
@date VARCHAR(11) 
AS
BEGIN

 declare @REPORT_DATE Varchar(11)  
   set @REPORT_DATE= replace(CONVERT(VARCHAR(12),convert(datetime,@date,103),100), '/', ' ')

	DECLARE @CURFLAG INT
	SET @CURFLAG = 0
	
	IF @REPORT_DATE <= LEFT(GETDATE()-1,11) 
	begin 
	print'aaa'
	  SET @CURFLAG = 0
	  end
	ELSE 
	BEGIN
		SET @CURFLAG = 1
	    SELECT STATUS='KINDLY ENTER T-1 DAY DATE'  

	END
	--print @CURFLAG

IF (@CURFLAG=0 )
BEGIN	
		insert into tbl_cceaws_staginglog_uat
		select 'USP_AWS_CCE_DATA_INTEGRATION',getdate(),''

		DECLARE @PANNO VARCHAR(20),@BILLDATE DATETIME
		SELECT	@PANNO = ISNULL(LTRIM(RTRIM(PANNO)),'') FROM MSAJAG.dbo.OWNER WITH (NOLOCK) 

		DECLARE	@@PARENT_FLAG BIT
		SELECT @@PARENT_FLAG = PARENT_FLAG FROM MSAJAG..WEEKLY_CASH_BALANCES_SETTING WITH (NOLOCK)


		CREATE TABLE #CL_LIST    
		(    
		 PARTY_CODE VARCHAR(10),    
		 PARTY_NAME VARCHAR(100),    
		 CLIENT_PAN VARCHAR(50),
		 CL_TYPE VARCHAR(10),
		 PARENT_CODE VARCHAR(10)   
		)    
    
		INSERT INTO #CL_LIST    
		SELECT CL_CODE, LONG_NAME, PAN_GIR_NO, CL_TYPE, 
		PARENTCODE = CASE WHEN @@PARENT_FLAG = 1 THEN PARENTCODE ELSE CL_CODE END
		FROM MSAJAG..VW_CLIENT_DETAILS_DATA  WITH (NOLOCK)  
		WHERE CL_CODE BETWEEN '00000' AND 'ZZZZZ'   

		IF @@PARENT_FLAG = 1
		BEGIN
			UPDATE C SET C.PARTY_NAME =C1.PARTY_NAME, C.CLIENT_PAN = C1.CLIENT_PAN
			FROM #CL_LIST C, (SELECT * FROM #CL_LIST WHERE PARTY_CODE = PARENT_CODE) C1
			WHERE C.PARTY_CODE = C1.PARENT_CODE
		END

		--IF EXISTS(SELECT TOP 1 * FROM CLIENT_CASH_DETAILS WITH(Nolock) WHERE REPORT_DATE = @REPORT_DATE )
		--BEGIN		
		--	DELETE A 
		--	FROM ACCOUNT.DBO.CLIENT_CASH_DETAILS A WITH(NOLOCK)  WHERE REPORT_DATE = @REPORT_DATE

		--	DELETE B FROM CLIENT_CASH_LED_BALANCES B WITH(Nolock)
		--	WHERE EXISTS(SELECT SNO FROM CLIENT_CASH_DETAILS C WITH (Nolock) 
		--	WHERE C.SNO = B.SNO AND REPORT_DATE = @REPORT_DATE)
		--END

	
		INSERT INTO CLIENT_CASH_DETAILS_UAT
		SELECT TRADING_PAN = @PANNO, PARTY_CODE = ISNULL(PARTY_CODE, ''), 
		PARENT_CODE = ISNULL(PARENT_CODE, ''), CLIENT_PAN = ISNULL(CLIENT_PAN, '6666666666'), 
		PARTY_NAME = ISNULL(PARTY_NAME, ''), CL_TYPE = ISNULL(CL_TYPE, ''),
		@REPORT_DATE, LAST_SETT_DATE = 'NA', GETDATE() 
		FROM #CL_LIST C

		SELECT SNO, UCC INTO #CL_CASH_DETAILS FROM CLIENT_CASH_DETAILS_UAT WITH (NOLOCK)
		WHERE REPORT_DATE = @REPORT_DATE

		CREATE CLUSTERED INDEX IDX_SNO ON #CL_CASH_DETAILS(SNO)
    
		INSERT INTO  CLIENT_CASH_LED_BALANCES_UAT 
		SELECT 	SNO,
					A.CL_CODE,
					EXCHANGE,
					SEGMENT,
					VDT_BAL = SUM(ISNULL(VDT_BAL, 0)),
					EDT_BAL = SUM(ISNULL(EDT_BAL, 0)),
					OPEN_BILL1 = SUM(ISNULL(OPEN_BILL_1, 0)),
					OPEN_BILL2 = SUM(ISNULL(OPEN_BILL_2, 0)),
					CHQ_DEPOSITS = SUM(ISNULL(CHQ_DEPOSITS, 0)),
					CHQ_ISSUE = SUM(ISNULL(CHQ_ISSUE, 0)),
					MARGIN_REQ = SUM(ISNULL(MARGIN_REQ, 0)),
					PEAK_FINANCIAL_LEDGER = SUM(ISNULL(PEAK_FINANCIAL_LEDGER, 0)),
					BG = SUM(ISNULL(BG, 0)),
					FDR = SUM(ISNULL(FDR, 0)),
					GOV_IND_SEC = SUM(ISNULL(GOV_IND_SEC, 0)),
					GILT_FUNDS = SUM(ISNULL(GILT_FUNDS, 0)),
					EPI_LEDGER = SUM(ISNULL(EPI_LEDGER, 0)),
					POOL_ACCOUNT = SUM(ISNULL(POOL_ACCOUNT, 0)),
					LEDACCOUNT =  SUM(ISNULL(LEDACCOUNT, 0)),
					COMM_VALUE = SUM(ISNULL(COMM_VALUE, 0)) ,
					CASH_COLL_MTF_POSITIONS=SUM(ISNULL(CASH_COLL_MTF_POSITIONS,0))
					--CASE WHEN SEGMENT='MTF' THEN max(ISNULL (NETAMT,0)) ELSE 0 END
				FROM  MSAJAG.DBO.TBL_FINAL_AUDIT_DATA_CCE_AWS A WITH (NOLOCK),#CL_CASH_DETAILS C WITH (NOLOCK)
				WHERE A.CL_CODE=C.UCC
				GROUP BY SNO,
					A.CL_CODE,
					EXCHANGE,
					SEGMENT

		 UPDATE tbl_cceaws_staginglog_uat SET END_TIME=GETDATE() 
		 WHERE ID=(SELECT MAX(ID) FROM tbl_cceaws_staginglog_uat)

		 DECLARE @TOTALAWSROWSCOUNT INT--,@REPORT_DATE VARCHAR(11) 

		 SET @TOTALAWSROWSCOUNT=(SELECT COUNT(1) FROM MSAJAG.DBO.TBL_FINAL_AUDIT_DATA_CCE_AWS  WITH (NOLOCK) 
								WHERE CAST(REPORT_DATE AS date)=@REPORT_DATE)

	     SELECT STATUS='AWS DATA INTEGRATION PROCESS COMPLETED SUCESSFULLY FOR '+@REPORT_DATE,TOTAL_RECORDS=@TOTALAWSROWSCOUNT  

	END

END

GO
