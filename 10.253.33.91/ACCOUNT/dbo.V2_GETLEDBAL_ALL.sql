			A.CLTCODE,
			A.BRANCHCODE,
			SUB_BROKER = '',
			LONGNAME,
			ACCAT,
			BTOBPAYMENT,
			PAYMODE,
			POBANKCODE,			
			CLIENTTYPE = '',					
			FAMILYCODE = '',					
			RUNDATE = GETDATE()
		FROM 
			ACMAST A WITH(NOLOCK), 
			V2_SETTPAYOUT_ALL P WITH(NOLOCK)
		WHERE 
			A.CLTCODE = P.CLTCODE AND A.CLTCODE BETWEEN @FPARTY AND @TPARTY
			AND A.ACCAT IN (4, 104)
			AND STATUS IN ('P', 'I')
