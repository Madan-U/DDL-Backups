CREATE PROCEDURE [dbo].[V2_GETLEDBAL_ALL]
(
	@SDATE VARCHAR(11),
	@FPARTY VARCHAR(10),
	@TPARTY VARCHAR(10),
	@GENFLAG VARCHAR(1),
	@USER VARCHAR(25),	
	@EFFDATE VARCHAR(11),
	@COMP_FOR VARCHAR(6) = 'BRANCH', -- COMPUTATION FOR BRANCH REQUESTING
	@NCCPERCENT INT = 0 -- NON CASH COLLATERAL PERCENTAGE
)

AS
/*===========================================================================
		---------------------------------------------------------------------
		FO CREDIT BALANCES AFTER ADJUSTING
		MARGIN REQUIREMENT & COLLATERAL
		TO BE CONSIDERED WHILE COMPUTING NET BALANCE
		---------------------------------------------------------------------
		
		EXEC V2_GETLEDBAL_ALL
			@SDATE = 'APR  1 2011',
			@FPARTY = '',
			@TPARTY = '',			
			@GENFLAG = 'A',
			@USER = 'DEMO',
			@EFFDATE = 'OCT 10 2011',
			@COMP_FOR = 'BRANCH'

		SELECT * FROM V2_LEDGERBALANCE_ALL

		SELECT PARTYCODE FROM V2_LEDGERBALANCE_ALL GROUP BY PARTYCODE HAVING COUNT(*) > 1

	===========================================================================*/
	
	SET NOCOUNT ON
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

	IF @FPARTY = '' SET @FPARTY = '0000000000'

	IF @TPARTY = '' SET @TPARTY = 'ZZZZZZZZZZ'

	DECLARE @@RECCOUNT INT

	SET @@RECCOUNT = 0

	SELECT @@RECCOUNT = COUNT(1) FROM V2_SETTPAYOUT_ALL WHERE LEFT(REQUESTDT,11) = LEFT(GETDATE(),11) AND STATUS IN ('P', 'I')

	IF @COMP_FOR = 'BRANCH'
	BEGIN
		IF @@RECCOUNT <> 0
		BEGIN
			SELECT ':: SORRY!! CANNOT PROCEED. THERE ARE SOME REQUESTS PENDING. PLEASE REJECT/APPROVE TO RECALCULATE ::'
			RETURN
		END
	END

	DELETE
		V2_FOLEDBAL
	WHERE
		CLTCODE BETWEEN @FPARTY AND @TPARTY	
	
	/*===========================================================================
	CREATE # TABLES FOR CANNED LEDGER BALANCES AND BILL AMOUNTS
	===========================================================================*/
	CREATE TABLE #LEDGERBALANCE
	(
		PARTYCODE VARCHAR(10),
		PARTYNAME VARCHAR(100),
		CHEQUENAME VARCHAR(100),
		BRANCHCODE VARCHAR(10),
		SUB_BROKER VARCHAR(10),
		ACCAT VARCHAR(10),
		BTOBPAYMENT INT,
		EXCHANGE VARCHAR(3),
		SEGMENT VARCHAR(7),
		LEDBAL MONEY,
		AMTTOPAY MONEY,
		PAYMODE VARCHAR(1),
		BANKCODE VARCHAR(10),
		GENFLAG VARCHAR(1),
		CLIENTTYPE VARCHAR(3),		
		FAMILYCODE VARCHAR(10),
		RUNDATE DATETIME,
		UNCLEARED_AMT MONEY
	)

	CREATE INDEX [PARTYCODE] ON [DBO].[#LEDGERBALANCE]([PARTYCODE]) ON [PRIMARY]

	CREATE TABLE [#LEDGERBILLTMP]
	(
		[CLTCODE] [VARCHAR] (10) NULL,
		[BRANCHCODE] [VARCHAR] (10) NULL,
		[SUB_BROKER] [VARCHAR](10) NULL,
		[LONGNAME] [VARCHAR] (100) NULL,
		[ACCAT] [CHAR] (10) NULL,
		[BTOBPAYMENT] [INT] NULL,
		[PAYMODE] [CHAR] (1) NULL,
		[POBANKCODE] [VARCHAR] (10) NULL,		
		[CLIENTTYPE] [VARCHAR] (3),		
		[FAMILYCODE] [VARCHAR] (10),
		[RUNDATE] [DATETIME]
	) ON [PRIMARY]

	CREATE INDEX [CLTCODE] ON [DBO].[#LEDGERBILLTMP]([CLTCODE]) ON [PRIMARY]

	IF @COMP_FOR = 'BRANCH'
	BEGIN
		IF @GENFLAG = 'A' -- ADHOC PAYOUT
		BEGIN
			INSERT INTO
				#LEDGERBILLTMP
			SELECT
				A.CLTCODE,
				A.BRANCHCODE,
				SUB_BROKER = '',
				LONGNAME,
				ACCAT,
				BTOBPAYMENT,
				PAYMODE,
				POBANKCODE,				
				CLIENTTYPE = '',					
				FAMILYCODE = '',					
				RUNDATE = GETDATE()
			FROM 
				ACMAST A WITH(NOLOCK)
			WHERE 
				A.CLTCODE BETWEEN @FPARTY AND @TPARTY
				AND A.ACCAT IN (4, 104)
				AND A.POBANKCODE IN
				(
					SELECT
					CLTCODE
					FROM ACMAST
					WITH(NOLOCK)
					WHERE ACCAT = 2
				)
				AND BTOBPAYMENT = 0
			GROUP BY 
				A.CLTCODE,
				A.BRANCHCODE,
				LONGNAME,
				ACCAT,
				BTOBPAYMENT,
				PAYMODE,
				POBANKCODE
				
			DELETE
				L
			FROM
				#LEDGERBILLTMP L,
				MSAJAG.DBO.CLIENT5 C5
			WHERE
				CLTCODE = CL_CODE
				AND INACTIVEFROM < CONVERT(VARCHAR(11), GETDATE())

			UPDATE
				#LEDGERBILLTMP
			SET
				SUB_BROKER = C1.SUB_BROKER,
				CLIENTTYPE = C1.CL_TYPE,
				FAMILYCODE = C1.FAMILY
			FROM 
				MSAJAG.DBO.CLIENT1 C1,				
				MSAJAG.DBO.CLIENTTYPE C3
			WHERE 
				C1.CL_TYPE = C3.CL_TYPE				
				AND CLTCODE = C1.CL_CODE

			DELETE 
				#LEDGERBILLTMP
			WHERE 
				CLIENTTYPE IN (SELECT CL_TYPE FROM MSAJAG.DBO.CLIENTTYPE WHERE FUNDS_ADHOC_PAYOUT <> '1')
		END 
	END	

