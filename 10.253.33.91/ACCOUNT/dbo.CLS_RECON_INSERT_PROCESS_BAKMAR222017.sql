	BEGIN
		SELECT	@BANKCODE = BANKCODE
		FROM RECON_BANK_MASTER_VW 
		WHERE SEGMENT=@SEGMENT AND BANKNAME=@BANKNAME AND FILETYPE=@FILETYPE

		UPDATE RECON_BANK_FILEUPLOAD_LOGS
		SET BANKCODE=@BANKCODE
		WHERE SEGMENT=@SEGMENT AND BANKNAME=@BANKNAME AND FILETYPE=@FILETYPE
	END
END
