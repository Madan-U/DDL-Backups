CREATE  PROCEDURE [dbo].[CLS_RECON_INSERT_PROCESS] (@UPLOADID   NVARCHAR(50)
                                                      , @FILETYPE NVARCHAR(50)
                                                      , @BANKNAME NVARCHAR(50)
													  , @UPLOAD_FLAG CHAR(1)
													  , @REPROCESS_FLAG CHAR(1)
													  , @UPLOADDATE NVARCHAR(25)
													  , @BANKCODE NVARCHAR(20)
													  , @SEGMENT NVARCHAR(20))
