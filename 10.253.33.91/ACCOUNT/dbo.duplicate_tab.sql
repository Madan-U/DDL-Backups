CREATE PROC DUPLICATE_TAB(@VDT VARCHAR(11))
AS 
BEGIN

SELECT L.CLTCODE,L2.CLTCODE AS BANK_CODE, L1.DDNO,L.VDT,L.VAMT INTO #TEMP  FROM LEDGER L ,LEDGER1 L1,
(SELECT * FROM LEDGER WHERE VTYP=2 AND VDT >=@VDT AND DRCR='D' ) L2
WHERE L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE=L1.BOOKTYPE
AND L.VTYP=L2.VTYP AND L.VNO=L2.VNO AND L.BOOKTYPE=L2.BOOKTYPE
AND L.VDT >=@VDT AND L.DRCR='C' AND L.CLTCODE <>L2.CLTCODE  --AND L1.DDNO='110618112769'
GROUP BY L.CLTCODE,L2.CLTCODE, L1.DDNO,L.VDT ,L.VAMT
HAVING COUNT(*)>1


SELECT L.CLTCODE,L2.CLTCODE AS BANK_CODE, L1.DDNO,L.VDT,L.VAMT  ,L.VNO,L.ENTEREDBY ,L.CDT FROM LEDGER L ,LEDGER1 L1,
(SELECT * FROM LEDGER WHERE VTYP=2 AND VDT >=@VDT AND DRCR='D' ) L2
WHERE L.VTYP=L1.VTYP AND L.VNO=L1.VNO AND L.BOOKTYPE=L1.BOOKTYPE
AND L.VTYP=L2.VTYP AND L.VNO=L2.VNO AND L.BOOKTYPE=L2.BOOKTYPE
AND L.VDT >=@VDT AND L.DRCR='C' AND L.CLTCODE <>L2.CLTCODE AND EXISTS ( SELECT CLTCODE FROM #TEMP T
WHERE T.CLTCODE=L.CLTCODE AND T.VAMT=L.VAMT AND T.DDNO=L1.DDNO)
ORDER BY DDNO

END

GO
