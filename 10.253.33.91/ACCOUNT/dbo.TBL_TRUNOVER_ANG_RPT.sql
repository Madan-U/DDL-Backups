--------------------------------------------------



CREATE PROC [dbo].[TBL_TRUNOVER_ANG_RPT]  
  
AS  
  
DECLARE @FROMDATE VARCHAR(11) = CONVERT(VARCHAR(11),GETDATE()-10)  
DECLARE @TODATE VARCHAR(11) = CONVERT(VARCHAR(11),GETDATE())  
  
DELETE TBL_TRUNOVER_ANG WHERE SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'  
  
INSERT INTO TBL_TRUNOVER_ANG  
SELECT * FROM (  
SELECT EXCHANGE,SEGMENT,SAUDA_DATE,BUY_TURNOVER = SUM(CASE WHEN SELL_BUY = '1' THEN MARKETAMT ELSE 0 END)  
,SELL_TURNOVER = SUM(CASE WHEN SELL_BUY = '2' THEN MARKETAMT ELSE 0 END)   
,PAYIN_PAYOUT = SUM(CASE WHEN SELL_BUY = '1' THEN MARKETAMT ELSE 0 END) - SUM(CASE WHEN SELL_BUY = '2' THEN MARKETAMT ELSE 0 END)  
FROM MSAJAG.DBO.COMMON_CONTRACT_DATA WITH (NOLOCK)   
WHERE SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'  AND QTY <> 0  AND ORDER_NO <> '' AND SETT_TYPE IN ('N','W','D','C','M','Z','RC','RD')
AND SEGMENT = 'CAPITAL'  
GROUP BY EXCHANGE,SEGMENT,SAUDA_DATE   
UNION ALL  
SELECT EXCHANGE = 'NSE',SEGMENT = 'FUTURES',LEFT(SAUDA_DATE,11),  
(SUM(CASE WHEN ( INST_TYPE LIKE 'FUT%' AND AUCTIONPART <> 'CA')   
   THEN ISNULL(PRATE * PQTY , 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART <> 'EA')   
   THEN ISNULL(PRATE * PQTY , 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART = 'EA')   
   THEN ISNULL(STRIKE_PRICE * PQTY , 0) ELSE 0 END)) AS BUYING_TURNOVER,  
(SUM(CASE WHEN ( INST_TYPE LIKE 'FUT%' AND AUCTIONPART <> 'CA')   
   THEN ISNULL(SRATE * SQTY, 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART <> 'EA')   
   THEN ISNULL(SRATE * SQTY, 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART = 'EA')   
   THEN ISNULL(STRIKE_PRICE * SQTY, 0) ELSE 0 END)) AS SELLING_TURNOVER,0 AS PAYIN_PAYOUT  
FROM ANGELFO.NSEFO.DBO.FOBILLVALAN WITH (NOLOCK)  
WHERE  TRADETYPE = 'BT'  
AND PARTY_CODE BETWEEN 'A'AND 'ZZZZZZZZZZ'  
AND SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'   
GROUP BY SAUDA_DATE  
UNION ALL  
SELECT EXCHANGE = 'NSX',SEGMENT = 'FUTURES',LEFT(SAUDA_DATE,11),  
(SUM(CASE WHEN ( INST_TYPE LIKE 'FUT%' AND AUCTIONPART <> 'CA')   
   THEN ISNULL(PRATE * PQTY , 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART <> 'EA')   
   THEN ISNULL(PRATE * PQTY , 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART = 'EA')   
   THEN ISNULL(STRIKE_PRICE * PQTY , 0) ELSE 0 END)) AS BUYING_TURNOVER,  
(SUM(CASE WHEN ( INST_TYPE LIKE 'FUT%' AND AUCTIONPART <> 'CA')   
   THEN ISNULL(SRATE * SQTY, 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART <> 'EA')   
   THEN ISNULL(SRATE * SQTY, 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART = 'EA')   
   THEN ISNULL(STRIKE_PRICE * SQTY, 0) ELSE 0 END)) AS SELLING_TURNOVER,0 AS PAYIN_PAYOUT  
FROM ANGELFO.NSECURFO.DBO.FOBILLVALAN WITH (NOLOCK)  
WHERE  TRADETYPE = 'BT'  
AND PARTY_CODE BETWEEN 'A'AND 'ZZZZZZZZZZ'  
AND SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'   
GROUP BY SAUDA_DATE  
UNION ALL  
SELECT EXCHANGE = 'MCX',SEGMENT = 'FUTURES',LEFT(SAUDA_DATE,11),  
(SUM(CASE WHEN ( INST_TYPE LIKE 'FUT%' AND AUCTIONPART <> 'CA')   
 THEN ISNULL(PRATE * PQTY * NUMERATOR / DENOMINATOR , 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART <> 'EA')   
 THEN ISNULL( PRATE * PQTY * NUMERATOR / DENOMINATOR , 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%'  AND AUCTIONPART <> 'CA' AND AUCTIONPART = 'EA')   
 THEN ISNULL( STRIKE_PRICE * PQTY * NUMERATOR / DENOMINATOR, 0) ELSE 0 END) ) AS BUYING_TURNOVER,  
(SUM(CASE WHEN ( INST_TYPE LIKE 'FUT%' AND AUCTIONPART <> 'CA')   
 THEN ISNULL( SRATE * SQTY * NUMERATOR / DENOMINATOR, 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART <> 'EA')   
 THEN ISNULL(  SRATE * SQTY * NUMERATOR / DENOMINATOR, 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%'  AND AUCTIONPART <> 'CA' AND AUCTIONPART = 'EA')   
 THEN ISNULL(  STRIKE_PRICE * SQTY * NUMERATOR / DENOMINATOR, 0) ELSE 0 END) ) AS SELLING_TURNOVER,0 AS PAYIN_PAYOUT  
FROM ANGELCOMMODITY.MCDX.DBO.FOBILLVALAN WITH (NOLOCK)  
WHERE  TRADETYPE = 'BT'  
AND PARTY_CODE BETWEEN 'A'AND 'ZZZZZZZZZZ'  
       AND SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'   
GROUP  BY SAUDA_DATE   
UNION ALL  
SELECT EXCHANGE = 'NCX',SEGMENT = 'FUTURES',LEFT(SAUDA_DATE,11),  
(SUM(CASE WHEN ( INST_TYPE LIKE 'FUT%' AND AUCTIONPART <> 'CA')   
 THEN ISNULL(PRATE * PQTY * NUMERATOR / DENOMINATOR , 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART <> 'EA')   
 THEN ISNULL( PRATE * PQTY * NUMERATOR / DENOMINATOR , 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%'  AND AUCTIONPART <> 'CA' AND AUCTIONPART = 'EA')   
 THEN ISNULL( STRIKE_PRICE * PQTY * NUMERATOR / DENOMINATOR, 0) ELSE 0 END) ) AS BUYING_TURNOVER,  
(SUM(CASE WHEN ( INST_TYPE LIKE 'FUT%' AND AUCTIONPART <> 'CA')   
 THEN ISNULL( SRATE * SQTY * NUMERATOR / DENOMINATOR, 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%' AND AUCTIONPART <> 'CA' AND AUCTIONPART <> 'EA')   
 THEN ISNULL(  SRATE * SQTY * NUMERATOR / DENOMINATOR, 0) ELSE 0 END)) +   
(SUM(CASE WHEN ( INST_TYPE LIKE 'OPT%'  AND AUCTIONPART <> 'CA' AND AUCTIONPART = 'EA')   
 THEN ISNULL(  STRIKE_PRICE * SQTY * NUMERATOR / DENOMINATOR, 0) ELSE 0 END) ) AS SELLING_TURNOVER,0 AS PAYIN_PAYOUT  
FROM ANGELCOMMODITY.NCDX.DBO.FOBILLVALAN WITH (NOLOCK)  
WHERE  TRADETYPE = 'BT'  
AND PARTY_CODE BETWEEN 'A'AND 'ZZZZZZZZZZ'  
       AND SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'   
GROUP  BY SAUDA_DATE  ) AS A ORDER BY SAUDA_DATE  
  
  
  
--REPORT STARTS  
  
IF ( CONVERT(VARCHAR(11),ACCOUNT.DBO.FN_NEXTWEEKDAY(GETDATE(), 'MON'),109) = CONVERT(VARCHAR(11),GETDATE()))  
BEGIN   
RETURN  
END  
  
IF ( CONVERT(VARCHAR(11),ACCOUNT.DBO.FN_NEXTWEEKDAY(GETDATE(), 'SUN'),109) = CONVERT(VARCHAR(11),GETDATE()))  
BEGIN   
RETURN  
END  
  
IF ( SELECT COUNT(1) FROM MSAJAG.DBO.SETT_MST WHERE CONVERT(VARCHAR(11),START_DATE) = CONVERT(VARCHAR(11),GETDATE()-1) AND SETT_TYPE = 'M') = ''  
BEGIN   
RETURN  
END  
  
SELECT SAUDA_DATE = 'FY 2021-22',SUM(CASH_BUY_TURNOVER) AS CASH_BUY_TURNOVER ,  
SUM(CASH_SELL_TURNOVER) AS CASH_SELL_TURNOVER,  
SUM(CASH_PAYIN_PAYOUT) AS CASH_PAYIN_PAYOUT,SUM(FO_BUY_TURNOVER) AS FO_BUY_TURNOVER,  
SUM(FO_SELL_TURNOVER) AS FO_SELL_TURNOVER,SUM(FO_PAYIN_PAYOUT) AS FO_PAYIN_PAYOUT,SUM(CUR_BUY_TURNOVER) AS CUR_BUY_TURNOVER  
,SUM(CUR_SELL_TURNOVER) AS CUR_SELL_TURNOVER,  
SUM(CUR_PAYIN_PAYOUT) AS CUR_PAYIN_PAYOUT,SUM(COM_BUY_TURNOVER) AS COM_BUY_TURNOVER,  
SUM(COM_SELL_TURNOVER) AS COM_SELL_TURNOVER,SUM(COM_PAYIN_PAYOUT) AS COM_PAYIN_PAYOUT INTO #TURNOVER_COMBINED  
FROM  
(  
SELECT SAUDA_DATE, CASH_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'CAPITAL' THEN BUY_TURNOVER ELSE '0' END)    
, CASH_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'CAPITAL' THEN SELL_TURNOVER ELSE '0' END ),  
CASH_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN PAYIN_PAYOUT ELSE '0' END),   
FO_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN BUY_TURNOVER ELSE '0' END)    
,FO_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN SELL_TURNOVER ELSE '0' END ),  
FO_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES' AND EXCHANGE = 'NSE' THEN PAYIN_PAYOUT ELSE '0' END),  
CUR_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSX'  THEN BUY_TURNOVER ELSE '0' END)    
,CUR_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSX'  THEN SELL_TURNOVER ELSE '0' END ),  
CUR_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES' AND EXCHANGE = 'NSX' THEN PAYIN_PAYOUT ELSE '0' END),  
COM_BUY_TURNOVER =  SUM(CASE WHEN EXCHANGE IN ('MCX','NCX')  THEN BUY_TURNOVER ELSE '0' END)    
,COM_SELL_TURNOVER =  SUM(CASE WHEN EXCHANGE IN ('MCX','NCX')  THEN SELL_TURNOVER ELSE '0' END ),  
COM_PAYIN_PAYOUT = SUM(CASE WHEN EXCHANGE IN ('MCX','NCX') THEN PAYIN_PAYOUT ELSE '0' END)  
FROM TBL_TRUNOVER_ANG  WHERE SAUDA_DATE BETWEEN 'APR  1 2021' AND 'MAR 31 2022 23:59' GROUP BY  SAUDA_DATE  
) AS A  
  
  
ALTER TABLE #TURNOVER_COMBINED ADD SRNO  int NOT NULL IDENTITY(1, 1)  
  
INSERT INTO #TURNOVER_COMBINED 
SELECT SAUDA_DATE = 'FY 2022-23',SUM(CASH_BUY_TURNOVER) AS CASH_BUY_TURNOVER ,  
SUM(CASH_SELL_TURNOVER) AS CASH_SELL_TURNOVER,  
SUM(CASH_PAYIN_PAYOUT) AS CASH_PAYIN_PAYOUT,SUM(FO_BUY_TURNOVER) AS FO_BUY_TURNOVER,  
SUM(FO_SELL_TURNOVER) AS FO_SELL_TURNOVER,SUM(FO_PAYIN_PAYOUT) AS FO_PAYIN_PAYOUT,SUM(CUR_BUY_TURNOVER) AS CUR_BUY_TURNOVER  
,SUM(CUR_SELL_TURNOVER) AS CUR_SELL_TURNOVER,  
SUM(CUR_PAYIN_PAYOUT) AS CUR_PAYIN_PAYOUT,SUM(COM_BUY_TURNOVER) AS COM_BUY_TURNOVER,  
SUM(COM_SELL_TURNOVER) AS COM_SELL_TURNOVER,SUM(COM_PAYIN_PAYOUT) AS COM_PAYIN_PAYOUT  
FROM  
(  
SELECT SAUDA_DATE, CASH_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'CAPITAL' THEN BUY_TURNOVER ELSE '0' END)    
, CASH_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'CAPITAL' THEN SELL_TURNOVER ELSE '0' END ),  
CASH_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN PAYIN_PAYOUT ELSE '0' END),   
FO_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN BUY_TURNOVER ELSE '0' END)    
,FO_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN SELL_TURNOVER ELSE '0' END ),  
FO_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES' AND EXCHANGE = 'NSE' THEN PAYIN_PAYOUT ELSE '0' END),  
CUR_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSX'  THEN BUY_TURNOVER ELSE '0' END)    
,CUR_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSX'  THEN SELL_TURNOVER ELSE '0' END ),  
CUR_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES' AND EXCHANGE = 'NSX' THEN PAYIN_PAYOUT ELSE '0' END),  
COM_BUY_TURNOVER =  SUM(CASE WHEN EXCHANGE IN ('MCX','NCX')  THEN BUY_TURNOVER ELSE '0' END)    
,COM_SELL_TURNOVER =  SUM(CASE WHEN EXCHANGE IN ('MCX','NCX')  THEN SELL_TURNOVER ELSE '0' END ),  
COM_PAYIN_PAYOUT = SUM(CASE WHEN EXCHANGE IN ('MCX','NCX') THEN PAYIN_PAYOUT ELSE '0' END)  
FROM TBL_TRUNOVER_ANG  WHERE SAUDA_DATE BETWEEN 'APR  1 2022' AND 'MAR 31 2023 23:59' GROUP BY  SAUDA_DATE  
) AS A  
  
  
INSERT INTO  #TURNOVER_COMBINED  
SELECT SAUDA_DATE  ,SUM(CASH_BUY_TURNOVER) AS CASH_BUY_TURNOVER ,  
SUM(CASH_SELL_TURNOVER) AS CASH_SELL_TURNOVER,  
SUM(CASH_PAYIN_PAYOUT) AS CASH_PAYIN_PAYOUT,SUM(FO_BUY_TURNOVER) AS FO_BUY_TURNOVER,  
SUM(FO_SELL_TURNOVER) AS FO_SELL_TURNOVER,SUM(FO_PAYIN_PAYOUT) AS FO_PAYIN_PAYOUT,SUM(CUR_BUY_TURNOVER) AS CUR_BUY_TURNOVER  
,SUM(CUR_SELL_TURNOVER) AS CUR_SELL_TURNOVER,  
SUM(CUR_PAYIN_PAYOUT) AS CUR_PAYIN_PAYOUT,SUM(COM_BUY_TURNOVER) AS COM_BUY_TURNOVER,  
SUM(COM_SELL_TURNOVER) AS COM_SELL_TURNOVER,SUM(COM_PAYIN_PAYOUT) AS COM_PAYIN_PAYOUT  
FROM  
(  
SELECT REPLACE(RIGHT(CONVERT(VARCHAR(9), CONVERT(DATE,SAUDA_DATE), 6), 6), ' ', '-') AS SAUDA_DATE,   
CASH_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'CAPITAL' THEN BUY_TURNOVER ELSE '0' END)    
, CASH_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'CAPITAL' THEN SELL_TURNOVER ELSE '0' END ),  
CASH_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN PAYIN_PAYOUT ELSE '0' END),   
FO_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN BUY_TURNOVER ELSE '0' END)    
,FO_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN SELL_TURNOVER ELSE '0' END ),  
FO_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES' AND EXCHANGE = 'NSE' THEN PAYIN_PAYOUT ELSE '0' END),  
CUR_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSX'  THEN BUY_TURNOVER ELSE '0' END)    
,CUR_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSX'  THEN SELL_TURNOVER ELSE '0' END ),  
CUR_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES' AND EXCHANGE = 'NSX' THEN PAYIN_PAYOUT ELSE '0' END),  
COM_BUY_TURNOVER =  SUM(CASE WHEN EXCHANGE IN ('MCX','NCX')  THEN BUY_TURNOVER ELSE '0' END)    
,COM_SELL_TURNOVER =  SUM(CASE WHEN EXCHANGE IN ('MCX','NCX')  THEN SELL_TURNOVER ELSE '0' END ),  
COM_PAYIN_PAYOUT = SUM(CASE WHEN EXCHANGE IN ('MCX','NCX') THEN PAYIN_PAYOUT ELSE '0' END)  
FROM TBL_TRUNOVER_ANG  WHERE SAUDA_DATE BETWEEN 'APR  1 2022' AND 'MAR 31 2023 23:59' 
GROUP BY REPLACE(RIGHT(CONVERT(VARCHAR(9), CONVERT(DATE,SAUDA_DATE), 6), 6), ' ', '-')  
) AS A GROUP BY SAUDA_DATE ORDER BY CONVERT(DATE,'01'+'-'+SAUDA_DATE) ASC  
  
  
  
INSERT INTO  #TURNOVER_COMBINED  
SELECT FORMAT(SAUDA_DATE,'dd/MM/yyyy') AS  SAUDA_DATE,SUM(CASH_BUY_TURNOVER) AS CASH_BUY_TURNOVER ,  
SUM(CASH_SELL_TURNOVER) AS CASH_SELL_TURNOVER,  
SUM(CASH_PAYIN_PAYOUT) AS CASH_PAYIN_PAYOUT,SUM(FO_BUY_TURNOVER) AS FO_BUY_TURNOVER,  
SUM(FO_SELL_TURNOVER) AS FO_SELL_TURNOVER,SUM(FO_PAYIN_PAYOUT) AS FO_PAYIN_PAYOUT,SUM(CUR_BUY_TURNOVER) AS CUR_BUY_TURNOVER  
,SUM(CUR_SELL_TURNOVER) AS CUR_SELL_TURNOVER,  
SUM(CUR_PAYIN_PAYOUT) AS CUR_PAYIN_PAYOUT,SUM(COM_BUY_TURNOVER) AS COM_BUY_TURNOVER,  
SUM(COM_SELL_TURNOVER) AS COM_SELL_TURNOVER,SUM(COM_PAYIN_PAYOUT) AS COM_PAYIN_PAYOUT   
FROM  
(  
SELECT TOP  5 SAUDA_DATE, CASH_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'CAPITAL' THEN BUY_TURNOVER ELSE '0' END)    
, CASH_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'CAPITAL' THEN SELL_TURNOVER ELSE '0' END ),  
CASH_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN PAYIN_PAYOUT ELSE '0' END),   
FO_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN BUY_TURNOVER ELSE '0' END)    
,FO_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSE'  THEN SELL_TURNOVER ELSE '0' END ),  
FO_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES' AND EXCHANGE = 'NSE' THEN PAYIN_PAYOUT ELSE '0' END),  
CUR_BUY_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSX'  THEN BUY_TURNOVER ELSE '0' END)    
,CUR_SELL_TURNOVER =  SUM(CASE WHEN SEGMENT = 'FUTURES'  AND EXCHANGE = 'NSX'  THEN SELL_TURNOVER ELSE '0' END ),  
CUR_PAYIN_PAYOUT = SUM(CASE WHEN SEGMENT = 'FUTURES' AND EXCHANGE = 'NSX' THEN PAYIN_PAYOUT ELSE '0' END),  
COM_BUY_TURNOVER =  SUM(CASE WHEN EXCHANGE IN ('MCX','NCX')  THEN BUY_TURNOVER ELSE '0' END)    
,COM_SELL_TURNOVER =  SUM(CASE WHEN EXCHANGE IN ('MCX','NCX')  THEN SELL_TURNOVER ELSE '0' END ),  
COM_PAYIN_PAYOUT = SUM(CASE WHEN EXCHANGE IN ('MCX','NCX') THEN PAYIN_PAYOUT ELSE '0' END)  
FROM TBL_TRUNOVER_ANG WHERE CONVERT(DATE,SAUDA_DATE) BETWEEN @FROMDATE AND @TODATE + ' 23:59' GROUP BY  SAUDA_DATE ORDER BY  SAUDA_DATE DESC  
) AS A    
GROUP BY SAUDA_DATE ORDER BY CONVERT(DATE,SAUDA_DATE) ASC  
  
  
SELECT DISTINCT SRNO,SAUDA_DATE,  
CONVERT(NUMERIC(18,0),SUM(CASH_BUY_TURNOVER)/10000000) AS CASH_BUY_TURNOVER ,  
CONVERT(NUMERIC(18,0),SUM(CASH_SELL_TURNOVER)/10000000) AS CASH_SELL_TURNOVER,  
CONVERT(NUMERIC(18,0),SUM(CASH_PAYIN_PAYOUT)/10000000) AS CASH_PAYIN_PAYOUT,  
CONVERT(NUMERIC(18,0),SUM(FO_BUY_TURNOVER)/10000000) AS FO_BUY_TURNOVER,  
CONVERT(NUMERIC(18,0),SUM(FO_SELL_TURNOVER)/10000000) AS FO_SELL_TURNOVER,  
CONVERT(NUMERIC(18,0),SUM(FO_PAYIN_PAYOUT)/10000000) AS FO_PAYIN_PAYOUT,  
CONVERT(NUMERIC(18,0),SUM(CUR_BUY_TURNOVER)/10000000) AS CUR_BUY_TURNOVER,  
CONVERT(NUMERIC(18,0),SUM(CUR_SELL_TURNOVER)/10000000) AS CUR_SELL_TURNOVER,  
CONVERT(NUMERIC(18,0),SUM(CUR_PAYIN_PAYOUT)/10000000) AS CUR_PAYIN_PAYOUT,  
CONVERT(NUMERIC(18,0),SUM(COM_BUY_TURNOVER)/10000000) AS COM_BUY_TURNOVER,  
CONVERT(NUMERIC(18,0),SUM(COM_SELL_TURNOVER)/10000000) AS COM_SELL_TURNOVER,  
CONVERT(NUMERIC(18,0),SUM(COM_PAYIN_PAYOUT)/10000000) AS COM_PAYIN_PAYOUT    
INTO #TURNOVER_COMBINED_FINAL  
FROM #TURNOVER_COMBINED GROUP BY SRNO,SAUDA_DATE  
  
UPDATE #TURNOVER_COMBINED_FINAL SET CASH_PAYIN_PAYOUT = CASH_SELL_TURNOVER + CASH_BUY_TURNOVER,  
FO_PAYIN_PAYOUT = FO_SELL_TURNOVER + FO_BUY_TURNOVER,  
CUR_PAYIN_PAYOUT = CUR_SELL_TURNOVER + CUR_BUY_TURNOVER,  
COM_PAYIN_PAYOUT = COM_SELL_TURNOVER + COM_BUY_TURNOVER  
  
ALTER TABLE #TURNOVER_COMBINED_FINAL ADD TOTAL_TURNOVER NUMERIC(18,0)  
  
UPDATE #TURNOVER_COMBINED_FINAL SET TOTAL_TURNOVER = CASH_PAYIN_PAYOUT + FO_PAYIN_PAYOUT + CUR_PAYIN_PAYOUT + COM_PAYIN_PAYOUT  
  
DECLARE @EMAILS VARCHAR(4000),@BODYCONTENT VARCHAR(MAX),@PEOPLE VARCHAR(4000),@SNAME VARCHAR(4000),@SUB VARCHAR(4000),@XML VARCHAR(MAX),@TBLE VARCHAR(MAX)  
  
SET @SUB = 'TURNOVER REPORT'  
  
SET @XML =  CAST((SELECT [td/@align]='center',td = SAUDA_DATE,'',  
    [td/@align]='right',td = CASH_BUY_TURNOVER,'',  
    [td/@align]='right',td = CASH_SELL_TURNOVER,'',  
    [td/@align]='right',td = CASH_PAYIN_PAYOUT,'',  
    [td/@align]='right',td = FO_BUY_TURNOVER,'',  
    [td/@align]='right',td = FO_SELL_TURNOVER,'',  
    [td/@align]='right',td = FO_PAYIN_PAYOUT,'',  
    [td/@align]='right',td = CUR_BUY_TURNOVER,'',  
    [td/@align]='right',td = CUR_SELL_TURNOVER,'',  
    [td/@align]='right',td = CUR_PAYIN_PAYOUT,'',  
    [td/@align]='right',td = COM_BUY_TURNOVER,'',  
    [td/@align]='right',td = COM_SELL_TURNOVER,'',  
    [td/@align]='right',td = COM_PAYIN_PAYOUT,'',  
    [td/@align]='right',td = TOTAL_TURNOVER,''  
