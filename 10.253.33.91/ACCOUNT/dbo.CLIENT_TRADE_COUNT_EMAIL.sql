CREATE PROC [dbo].[CLIENT_TRADE_COUNT_EMAIL]
AS


DECLARE @TODATE VARCHAR(11) = GETDATE()-1

SELECT DISTINCT PARTY_CODE,CONVERT(VARCHAR(11),SAUDA_DATE) AS SAUDA_DATE INTO #COMMON_CONTRACT_DATA FROM MSAJAG.DBO.COMMON_CONTRACT_DATA WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN  CONVERT(DATETIME,@TODATE) - 30 AND @TODATE + ' 23:59:59'
UNION ALL
SELECT DISTINCT PARTY_CODE,CONVERT(VARCHAR(11),SAUDA_DATE) AS SAUDA_DATE FROM ANGELCOMMODITY.MCDX.DBO.COMMON_CONTRACT_DATA  WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN  CONVERT(DATETIME,@TODATE) - 30 AND @TODATE + ' 23:59:59'

SELECT COUNT(DISTINCT PARTY_CODE) AS PARTY_CNT,CONVERT(VARCHAR(11),CONVERT(DATETIME,SAUDA_DATE),105) AS SAUDA_DATE,
CONVERT(DATETIME,SAUDA_DATE) AS SAUDA_DATE1 INTO #COMMON_CONTRACT_DATA_FINAL
FROM #COMMON_CONTRACT_DATA
GROUP BY SAUDA_DATE
ORDER BY CONVERT(DATETIME,SAUDA_DATE) DESC

DECLARE @FROMDATE DATETIME

SET @FROMDATE = CONVERT(DATETIME,@TODATE) - 30

DECLARE @EMAILS VARCHAR(4000),@BODYCONTENT VARCHAR(MAX),@PEOPLE VARCHAR(4000),@SNAME VARCHAR(4000),@SUB VARCHAR(4000),@XML VARCHAR(MAX),@TBLE VARCHAR(MAX),@SUB1 VARCHAR(4000)              
      
SET @SUB = 'Unique Client Trade Count for the period : ' + CONVERT(VARCHAR(11),@FROMDATE,105)+ ' to ' + CONVERT(VARCHAR(11),CONVERT(DATETIME,@TODATE),105) + '.'        
      
SET @SUB1 = 'Unique Client Trade Count for the period : ' + CONVERT(VARCHAR(11),@FROMDATE,105)+ ' to ' + CONVERT(VARCHAR(11),CONVERT(DATETIME,@TODATE),105) + '.'              
        
SET @XML =  CAST((SELECT [td/@align]='center',[td/@width]='50%',td = SAUDA_DATE,'',              
    [td/@align]='center',[td/@width]='50%',td = PARTY_CNT ,''
FROM #COMMON_CONTRACT_DATA_FINAL ORDER BY SAUDA_DATE1 DESC FOR XML PATH('TR'), ELEMENTS ) AS VARCHAR(MAX))              
        
SET @TBLE ='<HTML><HEAD><STYLE>BODY>TABLE>TBODY>TR>TD:NTH-CHILD(1)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(2)BODY>TABLE>TBODY > TR > TD:NTH-CHILD(3)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(4)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(5)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(6)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(7)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(10) BODY > TABLE > TBODY > TR > TD:NTH-CHILD(11)BODY > TABLE > TBODY > TR > TD:NTH-CHILD(12) BODY > TABLE > TBODY > TR > TD:NTH-CHILD(13) </STYLE> </HEAD> <TABLE BORDER = 1;> <THEAD> <TR STYLE="BACKGROUND-COLOR: #4F6E9C; COLOR: WHITE; FONT-SIZE: 13PX"><TH> SAUDA_DATE </TH> <TH> PARTY_CNT </TH> </TR> </THEAD><TBODY STYLE="FONT-SIZE: 13PX;">'                  
              
SET @TBLE = @TBLE + @XML +'</TBODY></TABLE></BODY></HTML>'              
              
SELECT @BODYCONTENT = '<P><SPAN STYLE="COLOR: RGB(0, 0, 128); FONT-FAMILY: VERDANA; FONT-SIZE: SMALL; FONT-STYLE: NORMAL; FONT-VARIANT-LIGATURES: NORMAL; FONT-VARIANT-CAPS: NORMAL; FONT-WEIGHT: 400; LETTER-SPACING: NORMAL; ORPHANS: 2; TEXT-ALIGN: START; TEXT-INDENT: 0PX; TEXT-TRANSFORM: NONE; WHITE-SPACE: NORMAL; WIDOWS: 2; WORD-SPACING: 0PX; -WEBKIT-TEXT-STROKE-WIDTH: 0PX; BACKGROUND-COLOR: RGB(255, 255, 255); TEXT-DECORATION-THICKNESS: INITIAL; TEXT-DECORATION-STYLE: INITIAL; TEXT-DECORATION-COLOR: INITIAL; DISPLAY: INLINE !IMPORTANT; FLOAT: NONE;">Dear All,</SPAN></P><P><SPAN STYLE="COLOR: RGB(0, 0, 128); FONT-FAMILY: VERDANA; FONT-SIZE: SMALL; FONT-STYLE: NORMAL; FONT-VARIANT-LIGATURES: NORMAL; FONT-VARIANT-CAPS: NORMAL; FONT-WEIGHT: 400; LETTER-SPACING: NORMAL; ORPHANS: 2; TEXT-ALIGN: START; TEXT-INDENT: 0PX; TEXT-TRANSFORM: NONE; WHITE-SPACE: NORMAL; WIDOWS: 2; WORD-SPACING: 0PX; -WEBKIT-TEXT-STROKE-WIDTH: 0PX; BACKGROUND-COLOR: RGB(255, 255, 255); TEXT-DECORATION-THICKNESS: INITIAL; TEXT-DECORATION-STYLE: INITIAL; TEXT-DECORATION-COLOR: INITIAL; DISPLAY: INLINE !IMPORTANT; FLOAT: NONE;">'+ @SUB1 + '</P>'+@TBLE+''     
         
