CREATE PROCEDURE [dbo].[RPT_EPN_EXCH_BOFILE] (@RUNDATE DATETIME)

AS ---- EXEC RPT_EPN_EXCH_BOFILE 'JUL 27 2021'

--DECLARE @LASTSETT INT
DECLARE @LASTSETT DATETIME

--SELECT @LASTSETT = SETT_NO FROM SETT_MST WHERE SETT_TYPE='N' 
--AND START_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'

SELECT @LASTSETT = FUNDS_PAYIN FROM SETT_MST WHERE SETT_TYPE='N' 
AND START_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'

SELECT SETT_NO,PARTY_CODE,CERTNO,SCRIP_CD,SUM(QTY) AS QTY,REASON,BRANCHCD,BCLTDPID,SERIES,SETT_TYPE INTO #EPN 
FROM DELTRANS WITH (NOLOCK)
WHERE SETT_NO IN (SELECT SETT_NO FROM SETT_MST WHERE SETT_TYPE='N' AND FUNDS_PAYIN BETWEEN @LASTSETT-1 AND @LASTSETT)
AND BCLTDPID IN ('10184021','1203320006951435')
AND PARTY_CODE NOT IN ('NSE','BROKER','EXE')
AND DRCR='C'
GROUP BY SETT_NO,PARTY_CODE,CERTNO,SCRIP_CD,REASON,BRANCHCD,BCLTDPID,SERIES,SETT_TYPE

SELECT * INTO #DELCLT FROM DELIVERYCLT WITH (NOLOCK) WHERE INOUT ='I' 
AND SETT_NO IN (SELECT SETT_NO FROM SETT_MST WHERE SETT_TYPE='N' AND FUNDS_PAYIN BETWEEN @LASTSETT-1 AND @LASTSETT)

SELECT A.*,B.PARTY_CODE AS PARTYCODE,B.I_ISIN,B.QTY AS OBLIGATION INTO #EPNFINAL
FROM #EPN A,#DELCLT B
WHERE A.PARTY_CODE=B.PARTY_CODE
AND A.CERTNO=B.I_ISIN
AND A.SETT_NO=B.SETT_NO

--DELETE FROM #EPNFINAL WHERE BRANCHCD='HO' AND REASON='DEMAT'

SELECT RECTYPE=CONVERT(VARCHAR,20)+','+A.SETT_TYPE+','+CONVERT(VARCHAR,A.SETT_NO)+','+
A.SCRIP_CD+','+A.SERIES+','+LTRIM(RTRIM(A.PARTY_CODE))+','+CONVERT(VARCHAR,A.OBLIGATION)+','+CONVERT(VARCHAR,A.QTY) INTO #FILE
FROM #EPNFINAL A
UNION ALL 
SELECT DISTINCT CONVERT(VARCHAR,30)+','+A.SETT_TYPE+','+CONVERT(VARCHAR,A.SETT_NO)+','+A.SCRIP_CD+','+A.SERIES+','+CONVERT(VARCHAR,'0')
FROM #EPNFINAL A

DECLARE @FCOUNT VARCHAR(10)
SELECT @FCOUNT = COUNT(*)  FROM #FILE

TRUNCATE TABLE EPN_EXCH_BOFILE

INSERT INTO EPN_EXCH_BOFILE
SELECT RECORD='10,ACEP,'+REPLACE(CONVERT(VARCHAR(11),@RUNDATE,105),'-','')+',M,12798,0'+ @FCOUNT 
UNION ALL
SELECT * FROM #FILE

DECLARE @DIRCOLLNAMEAM VARCHAR(100) = 
'D:\BACKOFFICE\EXPORT\' +'12798_CEP' + CONVERT(VARCHAR(8), @RUNDATE, 112) + '.A' + CONVERT(VARCHAR(2),@RUNDATE,105)

DECLARE @S VARCHAR(MAX)                            

SET @S = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT RECORD FROM MSAJAG.DBO.EPN_EXCH_BOFILE ORDER BY 1" QUERYOUT ' +@DIRCOLLNAMEAM+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'

EXEC(@S)

GO
