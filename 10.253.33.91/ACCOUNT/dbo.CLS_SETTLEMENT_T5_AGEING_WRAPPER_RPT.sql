CREATE PROC [dbo].[CLS_SETTLEMENT_T5_AGEING_WRAPPER_RPT]
	@TODATE		VARCHAR(11),
	@FROMPARTY	VARCHAR(20),
	@TOPARTY	VARCHAR(20),
	@STATUSID	VARCHAR(20),
	@STATUSNAME	VARCHAR(30),
	@DAYS_PARAM varchar(10) = '6'
AS

--DECLARE @TODATE VARCHAR(11)
--SET @TODATE = '05/10/2019'

SET @TODATE = CONVERT(datetime,@TODATE,103)


--DECLARE	@FROMPARTY VARCHAR(10) 
--DECLARE @TOPARTY VARCHAR(10) 
DECLARE @OPENDATE VARCHAR(11)
DECLARE @DTTYPE VARCHAR(3)
DECLARE @DAYS VARCHAR(500)
DECLARE @EXCSEGMENT VARCHAR(1000)


SELECT @OPENDATE = SDTCUR FROM PARAMETER WHERE CURYEAR = 1 --'01/04/2019'
--SET	@FROMPARTY = '0'
--SET @TOPARTY  ='ZZZ'
SET @DTTYPE = 'VDT'
SET @EXCSEGMENT = 'BSE-CAPITAL|MTF-TRADE|NSE-CAPITAL|NSE-FUTURES'


SELECT ROW_NUMBER() OVER (ORDER BY  START_DATE DESC ) AS SRNO ,START_DATE 
INTO #TEMP_SETT_MST
FROM MSAJAG..SETT_MST
WHERE SETT_TYPE = 'N' 
	AND START_DATE <= @TODATE
ORDER BY START_DATE DESC

SELECT @DAYS = DATEDIFF(DAY,START_DATE,@TODATE)
FROM #TEMP_SETT_MST
WHERE SRNO = @DAYS_PARAM



CREATE TABLE #TEMP_AGEING_FILE
(
	CLTCODE VARCHAR(25),
	CLIENT_NAME VARCHAR(300),
	BRANCH VARCHAR(25),
	BRANCHNAME VARCHAR(100),
	BALANCE VARCHAR(300),
	DAYS_DATA VARCHAR(300),
	DAYA_DATA_TOTAL VARCHAR(300),
	NET_TOTAL VARCHAR(300)
)

EXEC [CLS_AGEINGCURSOR] @FROMPARTY,@TOPARTY,@OPENDATE,@TODATE,'PARTY','',0,'D',@DTTYPE,@STATUSID,@STATUSNAME,@DAYS,'0','ZZZZ','N',@EXCSEGMENT,'CLIENT','','Y'

--SELECT CLTCODE, CLIENT_NAME, SEGMENT = 'EQ',BALANCE, DAYS_DATA, DAYA_DATA_TOTAL, NET_TOTAL, NOOFDAYS = @DAYS_PARAM
SELECT [Client Code] = CLTCODE, [Client Name] = CLIENT_NAME, Segment = 'EQ',[Ageing Debit Amount] = DAYA_DATA_TOTAL, [Number of Ageing days] = @DAYS_PARAM + 1
FROM #TEMP_AGEING_FILE
WHERE DAYA_DATA_TOTAL <> '0.00'


DROP TABLE #TEMP_SETT_MST
DROP TABLE #TEMP_AGEING_FILE

GO
