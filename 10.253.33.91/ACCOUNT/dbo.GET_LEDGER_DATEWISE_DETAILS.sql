-- Object: PROCEDURE dbo.GET_LEDGER_DATEWISE_DETAILS
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROCEDURE GET_LEDGER_DATEWISE_DETAILS
	@FROM_DATE VARCHAR(11), -- REPORT FROM DATE
	@TO_DATE VARCHAR(11), -- REPORT TO DATE
	@FROM_PARTY VARCHAR(10), -- FROM PARTY
	@TO_PARTY VARCHAR(10), -- TO PARTY
	@VEDT_FLAG CHAR(3) -- 'VDT' OR 'EDT'
AS

CREATE TABLE #LEDGER_DETAILS
(
	CLTCODE VARCHAR(10),
	VDT DATETIME,
	VTYPE INT,
	DRAMOUNT MONEY,
	CRAMOUNT MONEY,
	NARRATION	VARCHAR(500),
	RUNNING_BAL MONEY,
	EXCHANGE VARCHAR(20),
	ENTRY_FLAG VARCHAR(10)
)



CREATE TABLE #LEDGER_DATA
(
	CLTCODE VARCHAR(10),
	VDT DATETIME,
	VTYPE INT,
	DRAMOUNT MONEY,
	CRAMOUNT MONEY,
	NARRATION	VARCHAR(500),
	RUNNING_BAL MONEY,
	EXCHANGE VARCHAR(20),
	SNO INT IDENTITY(1, 1)
)

DECLARE @@ACCOUNTDB CURSOR
DECLARE @ACCOUNTDB VARCHAR(100),
@ACCOUNTSERVER varchar(30),
@EXCHANGE VARCHAR(10),
@SEGMENT VARCHAR(10),
@@SQL VARCHAR(MAX),
@@OPEN_DATE VARCHAR(11)

SELECT @@OPEN_DATE = CONVERT(VARCHAR(11), SDTCUR, 109) FROM PARAMETER WHERE @FROM_DATE BETWEEN SDTCUR AND LDTCUR

SET @@ACCOUNTDB = CURSOR FOR SELECT ACCOUNTDB, ACCOUNTSERVER, EXCHANGE, SEGMENT FROM   PRADNYA.DBO.MULTICOMPANY WHERE PRIMARYSERVER = 1 AND ACCOUNTDB IN('ACCOUNT', 'ACCOUNTBSE', 'ACCOUNTFO')

OPEN @@ACCOUNTDB  

FETCH NEXT FROM @@ACCOUNTDB INTO @ACCOUNTDB, @ACCOUNTSERVER, @EXCHANGE,  @SEGMENT
WHILE @@FETCH_STATUS = 0  

BEGIN
	SET @@SQL =  "INSERT INTO #LEDGER_DETAILS SELECT L.CLTCODE, '" + @FROM_DATE + "', VTYPE = 18, "
	SET @@SQL =  @@SQL + "DRAMOUNT = CASE WHEN SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END) < 0 THEN  ABS(SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END)) ELSE 0 END, "
	SET @@SQL =  @@SQL + "CRAMOUNT = CASE WHEN SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END) >= 0 THEN  SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END) ELSE 0 END, "
	SET @@SQL =  @@SQL + " NARRATION = 'OPENING BALANCE',RUNNING_BAL = 0, EXCHANGE = '" + @EXCHANGE + ' - ' + @SEGMENT + "', ENTRY_FLAG = 'O' "
	SET @@SQL =  @@SQL + " FROM " + @ACCOUNTSERVER + "." + @ACCOUNTDB + ".DBO.LEDGER L, " + @ACCOUNTSERVER + "." + @ACCOUNTDB + ".DBO.ACMAST A "
	SET @@SQL =  @@SQL + " WHERE A.CLTCODE = L.CLTCODE AND " + @VEDT_FLAG + " >= '" + @@OPEN_DATE + "' AND " + @VEDT_FLAG + " < '" + @FROM_DATE + "' AND L.CLTCODE BETWEEN '" + @FROM_PARTY + "' AND '" + @TO_PARTY + "' AND ACCAT = 4"
	SET @@SQL =  @@SQL + " GROUP BY L.CLTCODE "

	EXEC (@@SQL)

	IF @VEDT_FLAG = 'EDT'
	BEGIN
		SET @@SQL =  "INSERT INTO #LEDGER_DETAILS SELECT L.CLTCODE, '" + @FROM_DATE + "', VTYPE = 18, "
		SET @@SQL =  @@SQL + "DRAMOUNT = CASE WHEN SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END) < 0 THEN  ABS(SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END)) ELSE 0 END, "
		SET @@SQL =  @@SQL + "CRAMOUNT = CASE WHEN SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END) >= 0 THEN  SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END, "
		SET @@SQL =  @@SQL + " NARRATION = 'OPENING BALANCE', RUNNING_BAL = 0, EXCHANGE = '" + @EXCHANGE + ' - ' + @SEGMENT + "', ENTRY_FLAG = 'O' "
		SET @@SQL =  @@SQL + " FROM " + @ACCOUNTSERVER + "." + @ACCOUNTDB + ".DBO.LEDGER L, " + @ACCOUNTSERVER + "." + @ACCOUNTDB + ".DBO.ACMAST A "
		SET @@SQL =  @@SQL + " WHERE A.CLTCODE = L.CLTCODE AND EDT >= '" + @@OPEN_DATE + "' AND VDT < '" + @@OPEN_DATE + "' AND L.CLTCODE BETWEEN '" + @FROM_PARTY + "' AND '" + @TO_PARTY + "' AND ACCAT = 4"
		SET @@SQL =  @@SQL + " GROUP BY L.CLTCODE "

	EXEC (@@SQL)
	END

	SET @@SQL =  "INSERT INTO #LEDGER_DETAILS SELECT L.CLTCODE, CONVERT(DATETIME, CONVERT(VARCHAR(11), VDT, 109)), VTYP, "
	SET @@SQL =  @@SQL + "DRAMOUNT = CASE WHEN DRCR = 'D' THEN VAMT ELSE 0 END, "
	SET @@SQL =  @@SQL + "CRAMOUNT = CASE WHEN DRCR = 'C' THEN VAMT ELSE 0 END, "
	SET @@SQL =  @@SQL + " NARRATION, RUNNING_BAL = 0, EXCHANGE = '" + @EXCHANGE + ' - ' + @SEGMENT + "', ENTRY_FLAG = 'R' "
	SET @@SQL =  @@SQL + " FROM " + @ACCOUNTSERVER + "." + @ACCOUNTDB + ".DBO.LEDGER L, " + @ACCOUNTSERVER + "." + @ACCOUNTDB + ".DBO.ACMAST A "
	SET @@SQL =  @@SQL + " WHERE A.CLTCODE = L.CLTCODE AND VDT >= '" + @FROM_DATE + "' AND VDT <= '" + @TO_DATE + " 23:59' AND L.CLTCODE BETWEEN '" + @FROM_PARTY + "' AND '" + @TO_PARTY + "' AND ACCAT = 4"

	EXEC (@@SQL)
FETCH NEXT FROM @@ACCOUNTDB INTO @ACCOUNTDB, @ACCOUNTSERVER, @EXCHANGE,  @SEGMENT 
END


CLOSE @@ACCOUNTDB
DEALLOCATE @@ACCOUNTDB


INSERT INTO #LEDGER_DATA
SELECT CLTCODE, VDT, 18, SUM(DRAMOUNT), SUM(CRAMOUNT), NARRATION, 0, EXCHANGE = 'OPENING'
FROM #LEDGER_DETAILS
WHERE ENTRY_FLAG = 'O'
GROUP BY CLTCODE, VDT, NARRATION
ORDER BY CLTCODE

INSERT INTO #LEDGER_DATA
SELECT CLTCODE, VDT, VTYPE, DRAMOUNT, CRAMOUNT, NARRATION, 0, EXCHANGE
FROM #LEDGER_DETAILS
WHERE ENTRY_FLAG = 'R'
ORDER BY CLTCODE, VDT

UPDATE #LEDGER_DATA SET RUNNING_BAL = (SELECT SUM(CRAMOUNT - DRAMOUNT) FROM #LEDGER_DATA L WHERE #LEDGER_DATA.SNO >= L.SNO AND #LEDGER_DATA.CLTCODE = L.CLTCODE)


SELECT CLTCODE, VDT, VTYPE, DRAMOUNT, CRAMOUNT, NARRATION, RUNNING_BAL, EXCHANGE FROM #LEDGER_DATA ORDER BY CLTCODE, VDT, SNO

GO
