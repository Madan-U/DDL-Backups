CREATE PROCEDURE [dbo].[CLS_RECON_NONCMS_IDFC_PROCESS]
( @UPLOADID NVARCHAR(50) 
,@BANKCODE NVARCHAR(50)
,@UPLOADFILENAME NVARCHAR(200)
,@UNAME NVARCHAR(50)) 
AS

BEGIN
INSERT INTO #NONCMS_DATA (UPLOADID
, BANKACCOUNT
, BANKCODE
, DESCRIPTION
, LEDGERBALANCE
, DRCR
, AMT
, VALUEDATE
, REFERENCENO
, CLTACCOUNT
, CUSTOMERREFERENCE
, TRANSACTIONDETAIL
, VNO
, VTYP
, BOOKTYPE
, MATCHEDFLAG
, UPLOADSTATUS
, UPLOADEDDATE
, MODIFIEDDATE
, UPLOADBY)
	SELECT
		UPLOADID
		,BANKACCOUNT
		, BANKCODE
		,CASE WHEN .DBO.PIECE(FILE_DATA,'Æ',3) LIKE '%MUMBAI|IDLCC|%' THEN .DBO.PIECE(.DBO.PIECE(FILE_DATA,'Æ',3),'|',4) ELSE .DBO.PIECE(FILE_DATA,'Æ',3)END  AS 'DESCRIPTION'
		,'' AS 'LEDGERBALANCE'
		, CASE WHEN CONVERT(NUMERIC, .DBO.PIECE(REPLACE(FILE_DATA, ',', ''),'Æ',5)) <> 0.00 THEN 'D'
			   WHEN CONVERT(NUMERIC, .DBO.PIECE(REPLACE(FILE_DATA, ',', ''),'Æ',6))<> 0.00 THEN 'C' ELSE '' END AS 'DRCR'
		,CASE  WHEN CONVERT(NUMERIC, .DBO.PIECE(REPLACE(FILE_DATA, ',', ''),'Æ',5)) <> 0.00 THEN CONVERT(NUMERIC, .DBO.PIECE(REPLACE(FILE_DATA, ',', ''),'Æ',5))
			   WHEN CONVERT(NUMERIC, .DBO.PIECE(REPLACE(FILE_DATA, ',', ''),'Æ',6))<> 0.00 THEN CONVERT(NUMERIC, .DBO.PIECE(REPLACE(FILE_DATA, ',', ''),'Æ',6)) ELSE '0.00' END AS 'AMT'
		, CONVERT( DATETIME,.DBO.PIECE(FILE_DATA,'Æ',2),103) AS 'VALUEDATE'
		,.DBO.PIECE(FILE_DATA,'Æ',4) AS 'REFERENCENO'
		,' '
		,' '
		,' '
		,' '
		,' '
		,' '
		,0 AS 'MATCHEDFLAG'
		,UPLOADSTATUS
		,GETDATE()
		,MODIFIEDDATE
		,U.UPLOADBY
	FROM RECON_BANK_FILEUPLOAD_LOGS U (NOLOCK)
	INNER JOIN MSAJAG..FILEDATA F (NOLOCK)
		ON U.UPLOADID = F.PROGID
	WHERE F.PROGID = @UPLOADID
	AND ISNULL(DBO.PIECE(FILE_DATA,'Æ',2) , '')<>''
AND ISNULL(DBO.PIECE(FILE_DATA,'Æ',2) , '')<>'Value Date'

--SELECT * FROM #NONCMS_DATA RETURN

END

