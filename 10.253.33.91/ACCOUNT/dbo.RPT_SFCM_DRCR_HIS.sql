-- Object: PROCEDURE dbo.RPT_SFCM_DRCR_HIS
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


 CREATE PROC [dbo].[RPT_SFCM_DRCR_HIS] (@TDATE VARCHAR(11))
 
 AS  --- EXEC RPT_SFCM_DRCR_HIS 'MAY 21 2021'
 
  DECLARE @RUNTIEM VARCHAR(10)
 
 SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(HIS_DATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING_HISTORY WHERE REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59'
 
 CREATE TABLE #DRCR (REPORT_DATE VARCHAR(11),CLTCODE VARCHAR(10),BALANCE MONEY)
            
 INSERT INTO #DRCR               
 SELECT CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,SUM(BALANCE) AS BALANCE  FROM SFCM_REPORTING_HISTORY A,MSAJAG.DBO.CLIENT_DETAILS M        
 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,CONVERT(VARCHAR(11),REPORT_DATE,105)
 
SELECT Report='Creditors',Report_Date,RunTime=@RUNTIEM,CONVERT(VARCHAR,(SUM(BALANCE)/10000000)) AS [Amount in Cr],[Amount in Rs] = SUM(BALANCE) FROM #DRCR WHERE BALANCE >0  AND CLTCODE NOT LIKE '%ERROR%' GROUP BY REPORT_DATE          
 UNION ALL          
SELECT Report='Debtors',Report_Date,RunTime=@RUNTIEM,CONVERT(VARCHAR,(SUM(BALANCE)/10000000)) AS [Amount in Cr],[Amount in Rs] = SUM(BALANCE) FROM #DRCR WHERE BALANCE <0   AND CLTCODE NOT LIKE '%ERROR%' GROUP BY REPORT_DATE

GO
