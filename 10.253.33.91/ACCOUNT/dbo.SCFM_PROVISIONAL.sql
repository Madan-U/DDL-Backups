BEGIN
DELETE FROM SCFM_PROVISIONAL_DATA_LOG WHERE REPORT_DATE = @TODATE
END

INSERT INTO SCFM_PROVISIONAL_DATA_LOG SELECT * FROM SCFM_PROVISIONAL_DATA

TRUNCATE TABLE SCFM_PROVISIONAL_DATA

INSERT INTO SCFM_PROVISIONAL_DATA
SELECT REPORT_DATE,CLTCODE,BROKING,MTF,TOTAL_AMT,RUNTIME=GETDATE() FROM #PROSCFM ORDER BY CLTCODE


------/* Provisional SCFM Report */--------

SELECT [Report Date]=CONVERT(VARCHAR(11),REPORT_DATE,105),Report='Prov MTF Creditors',
[Balance in Cr.]=CAST(SUM(MTF)/10000000 AS NUMERIC(18,2)),[Balance in Rs.]=SUM(MTF)
FROM #PROSCFM WITH (NOLOCK) WHERE MTF > 0 GROUP BY REPORT_DATE
union ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Prov MTF Debtors',
[Balance in Cr.]=CAST(SUM(MTF)/10000000 AS NUMERIC(18,2)),[Balance in Rs.]=SUM(MTF)
from #PROSCFM WHERE MTF < 0 group BY REPORT_DATE
union ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Prov Broking Creditors',
[Balance in Cr.]=CAST(SUM(BROKING)/10000000 AS NUMERIC(18,2)),[Balance in Rs.]=SUM(BROKING)
from #PROSCFM WHERE (BROKING) > 0 GROUP BY REPORT_DATE
union ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Prov Broking Debtors',
[Balance in Cr.]=CAST(SUM(BROKING)/10000000 AS NUMERIC(18,2)),[Balance in Rs.]=SUM(BROKING)
from #PROSCFM WHERE (BROKING) < 0 group BY REPORT_DATE
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Prov Total Gross Creditors',
CAST(((SELECT ISNULL(SUM(MTF),0) FROM #PROSCFM WITH (NOLOCK) WHERE MTF > 0 ) +
(SELECT SUM(BROKING) FROM #PROSCFM WHERE (BROKING) > 0 ))/10000000 AS NUMERIC(18,2)) AS [Balance in Cr.],
(SELECT ISNULL(SUM(MTF),0) FROM #PROSCFM WITH (NOLOCK) WHERE MTF > 0 ) +
(SELECT SUM(BROKING) FROM #PROSCFM WHERE (BROKING) > 0 ) AS [Balance in Rs.]
from #PROSCFM GROUP BY REPORT_DATE
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Prov Total Gross Debtors',
CAST(((SELECT SUM(MTF) FROM #PROSCFM WITH (NOLOCK) WHERE MTF < 0 ) +
(SELECT SUM(BROKING) FROM #PROSCFM WHERE (BROKING) < 0 ))/10000000  AS NUMERIC(18,2)) AS [Balance in Cr.],
(SELECT SUM(MTF) FROM #PROSCFM WITH (NOLOCK) WHERE MTF < 0 ) +
(SELECT SUM(BROKING) FROM #PROSCFM WHERE (BROKING) < 0 ) AS [Balance in Rs.]
from #PROSCFM GROUP BY REPORT_DATE
union ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Prov Total Net Creditors',
[Balance in Cr.]=CAST(SUM(TOTAL_AMT)/10000000 AS NUMERIC(18,2)),[Balance in Rs.]=SUM(TOTAL_AMT)
from #PROSCFM WHERE TOTAL_AMT > 0 GROUP BY REPORT_DATE
union ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Prov Total Net Debtors',
[Balance in Cr.]=CAST(SUM(TOTAL_AMT)/10000000 AS NUMERIC(18,2)),[Balance in Rs.]=SUM(TOTAL_AMT)
from #PROSCFM WHERE TOTAL_AMT < 0 GROUP BY REPORT_DATE

