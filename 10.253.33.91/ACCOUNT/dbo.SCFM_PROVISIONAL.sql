TOTAL_AMT=ISNULL([BROKING],0) + ISNULL([MTF],0) INTO #PROSCFM
FROM (
SELECT * FROM #EDTLED) AS SourceTable
PIVOT
(SUM(NETAMT) FOR EXCHANGE IN ([BROKING],[MTF])) AS PivotTable


IF (SELECT COUNT(*) FROM SCFM_PROVISIONAL_DATA WHERE REPORT_DATE = @TODATE) <> 0
BEGIN
TRUNCATE TABLE SCFM_PROVISIONAL_DATA
END

