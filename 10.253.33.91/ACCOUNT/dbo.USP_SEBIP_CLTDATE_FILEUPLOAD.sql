CREATE PROCEDURE [dbo].[USP_SEBIP_CLTDATE_FILEUPLOAD]
@PROGID VARCHAR(50),
@USERCAT VARCHAR(20),
@STATUSID VARCHAR(50),
@STATUSNAME VARCHAR(50),
@UNAME VARCHAR(50),
@EXCHSEG VARCHAR(8000)
AS
BEGIN
    BEGIN TRY
	BEGIN TRAN

		CREATE TABLE #TEMPFILEDATA
		(
		   CODE VARCHAR(50), P_DATE DATETIME, NEW_P_DATE DATETIME	 	 
		)
   
		INSERT INTO #TEMPFILEDATA
		SELECT DBO.PIECE(FILE_DATA,',',1), CONVERT(DATETIME,DBO.PIECE(FILE_DATA,',',2),103), CONVERT(DATETIME,DBO.PIECE(FILE_DATA,',',3),103)
		FROM MSAJAG..FILEDATA  
		WHERE PROGID = @PROGID AND ISNULL(FILE_DATA,'') != ''
     
	
	   INSERT INTO SEBIPAYOUT_PROCESSDATE
	   SELECT *,@UNAME,GETDATE()
	   FROM #TEMPFILEDATA


/* NEW PATCH ADDED TO UPDATE THE EFFECTIVE DATE IN LEDGER */	   
DECLARE @EDT_UPDATION INT
 
SELECT @EDT_UPDATION = ISNULL(EDT_UPDATION,0) 
FROM QS_NONTRADED_SETTING 

IF(@EDT_UPDATION = 1)
BEGIN
	
	SELECT A.PARTYCODE, A.VNO , B.NEW_P_DATE, P_DATE = B.P_DATE
	INTO #TMP_SEBIPAYOUT
	FROM SEBIPAYOUT_ALL A
			INNER JOIN #TEMPFILEDATA B
			ON A.PARTYCODE= B.CODE 
					AND DATEDIFF(D,A.PROCESS_DT,B.P_DATE) = 0 

	UPDATE A
	SET EDT =  NEW_P_DATE
	FROM LEDGER A
			INNER JOIN #TMP_SEBIPAYOUT B	
			ON A.VNO = B.VNO 
	WHERE A.VTYP = 3
	AND DATEDIFF(D, EDT, P_DATE) = 0  

END
/* NEW PATCH ADDED TO UPDATE THE EFFECTIVE DATE IN LEDGER */


		   
		SELECT STRMSGCODE = 1, STRMSG =  ' RECORD(S) IMPORTED SUCCESSFULLY.' 
	COMMIT TRAN 
   END TRY
   BEGIN CATCH
   ROLLBACK TRAN 
       SELECT ERROR_NUMBER() AS ERRORNUMBER  
        ,ERROR_MESSAGE() AS ERRORMESSAGE;  
   END CATCH

END

GO
