--------------------------------------------------



--EXEC RPT_WEEKLY_LEDGER_DETAILS 'APR  1 2020', 'JAN 15 2021', 'BROKER', 'BROKER','B000000001', 'B000000001'
CREATE PROCEDURE [dbo].[RPT_WEEKLY_LEDGER_DETAILS_BKUP_04MAR2021]
	@FROMDATE VARCHAR(11),
	@TODATE VARCHAR(11),
	@STATUSID VARCHAR(30) = 'BROKER',
	@STATUSNAME VARCHAR(50) = 'BROKER',
	@FROMCODE VARCHAR(10) = '000',
	@TOCODE VARCHAR(10) = 'zzzz',
	@FROM_BRANCH VARCHAR(10) = '0000',
	@TO_BRANCH VARCHAR(10) = 'ZZZZZ',
	@FROM_SUBBROKER VARCHAR(30) = '00000',
	@TO_SUBBROKER VARCHAR(30) = 'ZZZZZ',
	@BOUNCE_FLAG INT = 1
AS

IF LEN(@FROMDATE) = 10
BEGIN
	SELECT @FROMDATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @FROMDATE, 103), 109)
	SELECT @TODATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @TODATE, 103), 109)
END

DECLARE
	@CLEARINGCODE VARCHAR(20)

SELECT @CLEARINGCODE = CLEARINGCODE FROM MSAJAG..TBL_INTEROP_SETTING



CREATE TABLE #WEEKLY_REPORT_DETAILS
(
	SNO INT IDENTITY(1, 1),
	SRNO INT,
	UCC VARCHAR(10),
	PARTY_CODE VARCHAR(10),
	PARTY_NAME VARCHAR(100),
	CLIENT_PAN VARCHAR(50),
	CL_TYPE VARCHAR(30),
	VDT DATETIME,
	EDT DATETIME,
	EXCHSEG VARCHAR(30),
	SETT_TYPE VARCHAR(10),
	SETT_NO VARCHAR(10),
	CHQNO VARCHAR(50),
	VDESC VARCHAR(50),
	NARRATION VARCHAR(2000),
	VNO VARCHAR(12),
	DEBIT MONEY,
	CREDIT MONEY,
	BALANCE MONEY,
	TABLE_CODE VARCHAR(10),
	ADD1 VARCHAR(100),
	ADD2 VARCHAR(100),
	ADD3 VARCHAR(100),
	CITY VARCHAR(100),
	[STATE] VARCHAR(100),
	ZIP VARCHAR(100),
	PHONE1 VARCHAR(100),
	PHONE2 VARCHAR(100),
	FAIMLIYCODE VARCHAR(100)
)
--SELECT @FROMDATE, @TODATE

CREATE TABLE #CL_REPORT_LIST
(
	CL_CODE VARCHAR(10) NOT NULL,
	ADD1 VARCHAR(100),
	ADD2 VARCHAR(100),
	ADD3 VARCHAR(100),
	CITY VARCHAR(100),
	[STATE] VARCHAR(100),
	ZIP VARCHAR(100),
	PHONE1 VARCHAR(100),
	PHONE2 VARCHAR(100),
	FAIMLIYCODE VARCHAR(100)
)


ALTER TABLE #CL_REPORT_LIST ADD  CONSTRAINT [PK_CL_REPORT_LIST] PRIMARY KEY CLUSTERED 
(
	[CL_CODE] ASC
)

INSERT INTO #CL_REPORT_LIST
SELECT CL_CODE, L_ADDRESS1, L_ADDRESS2, L_ADDRESS3, L_CITY, L_STATE, L_ZIP, RES_PHONE1, MOBILE_PAGER, FAMILY  FROM MSAJAG..CLIENT_DETAILS (NOLOCK)
WHERE CL_CODE BETWEEN @FROMCODE AND @TOCODE
AND BRANCH_CD = CASE WHEN @STATUSID = 'BRANCH' THEN @STATUSNAME ELSE BRANCH_CD END
AND SUB_BROKER = CASE WHEN @STATUSID = 'SUBBROKER' THEN @STATUSNAME ELSE SUB_BROKER END
AND BRANCH_CD BETWEEN @FROM_BRANCH AND @TO_BRANCH
AND SUB_BROKER BETWEEN @FROM_SUBBROKER AND @TO_SUBBROKER

INSERT INTO #WEEKLY_REPORT_DETAILS
SELECT SRNO, UCC, PARTY_CODE, PARTY_NAME, CLIENT_PAN, CL_TYPE, VDT, EDT, EXCHSEG = '', SETT_TYPE = '', SETT_NO = '', 
CHQNO = '', VDESC = '', NARRATION, VNO = '', DEBIT = CASE WHEN SUM(DEBIT - CREDIT) > 0 THEN SUM(DEBIT - CREDIT) ELSE 0 END, 
CREDIT = CASE WHEN SUM(CREDIT - DEBIT) > 0 THEN SUM(CREDIT - DEBIT) ELSE 0 END, BALANCE = 0, TABLE_CODE = 'TB1', ADD1, ADD2, ADD3, CITY,[STATE], ZIP, PHONE1, PHONE2, FAIMLIYCODE
FROM WEEKLY_REPORT_DETAILS, #CL_REPORT_LIST
WHERE VDT BETWEEN @FROMDATE AND @TODATE + ' 23:59' 
AND PARTY_CODE = CL_CODE AND NARRATION = 'OPENING BALANCE'
GROUP BY SRNO, UCC, PARTY_CODE, PARTY_NAME, CLIENT_PAN, CL_TYPE, VDT, EDT, NARRATION, ADD1, ADD2, ADD3, CITY,[STATE], ZIP, PHONE1, PHONE2, FAIMLIYCODE


INSERT INTO #WEEKLY_REPORT_DETAILS
SELECT SRNO, UCC, PARTY_CODE, PARTY_NAME, CLIENT_PAN, CL_TYPE, VDT, EDT, EXCHSEG, SETT_TYPE, SETT_NO, CHQNO, VDESC, NARRATION, VNO, DEBIT, CREDIT, BALANCE, TABLE_CODE, ADD1, ADD2, ADD3, CITY,[STATE], ZIP, PHONE1, PHONE2, FAIMLIYCODE
FROM WEEKLY_REPORT_DETAILS, #CL_REPORT_LIST
WHERE VDT BETWEEN @FROMDATE AND @TODATE + ' 23:59' 
AND PARTY_CODE = CL_CODE AND NARRATION <> 'OPENING BALANCE'
ORDER BY PARTY_CODE, TABLE_CODE, VDT

CREATE TABLE #WEEKLY_RUNNING_BALANCE
(
	SSNO INT IDENTITY(1, 1),
	SNO INT,
	SRNO INT,
	TABLE_CODE VARCHAR(10),
	DEBIT MONEY,
	CREDIT MONEY,
	RUNNING MONEY
)

INSERT INTO #WEEKLY_RUNNING_BALANCE
SELECT SNO, SRNO, TABLE_CODE, DEBIT, CREDIT, RUNNING = 0
FROM #WEEKLY_REPORT_DETAILS
ORDER BY SRNO, VDT

UPDATE #WEEKLY_RUNNING_BALANCE SET RUNNING = (SELECT SUM(CREDIT - DEBIT) FROM #WEEKLY_RUNNING_BALANCE W WHERE W.SSNO <= #WEEKLY_RUNNING_BALANCE.SSNO AND W.SRNO = #WEEKLY_RUNNING_BALANCE.SRNO AND W.TABLE_CODE = #WEEKLY_RUNNING_BALANCE.TABLE_CODE)


UPDATE #WEEKLY_REPORT_DETAILS SET BALANCE = RUNNING FROM #WEEKLY_RUNNING_BALANCE R WHERE R.SNO = #WEEKLY_REPORT_DETAILS.SNO

SELECT SNO, SRNO, UCC, PARTY_CODE, Left(PARTY_NAME,20) as PARTY_NAME, CLIENT_PAN, CL_TYPE, VDT = CONVERT(VARCHAR(10), VDT, 103), EDT = CONVERT(VARCHAR(10), EDT, 103), 
EXCHSEG, SETT_TYPE, SETT_NO, CHQNO, VDESC, NARRATION, VNO, DEBIT, CREDIT, BALANCE, TABLE_CODE, ADD1, ADD2, ADD3, CITY,[STATE], ZIP, PHONE1, PHONE2, FAIMLIYCODE, CLEARINGCODE = @CLEARINGCODE
FROM #WEEKLY_REPORT_DETAILS
order by UCC, SRNO

DROP TABLE #WEEKLY_REPORT_DETAILS
DROP TABLE #WEEKLY_RUNNING_BALANCE
DROP TABLE #CL_REPORT_LIST

GO
