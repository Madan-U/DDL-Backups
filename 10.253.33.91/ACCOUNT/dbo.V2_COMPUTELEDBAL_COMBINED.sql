-- Object: PROCEDURE dbo.V2_COMPUTELEDBAL_COMBINED
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROCEDURE [DBO].[V2_COMPUTELEDBAL_COMBINED]
(
	@SDATE VARCHAR(11),
	@FPARTY VARCHAR(10),
	@TPARTY VARCHAR(10),
	@GENFLAG VARCHAR(1),
	@USER VARCHAR(25),	
	@EFFDATE VARCHAR(11),	
	@COMP_FOR VARCHAR(6) = 'BRANCH', -- COMPUTATION FOR BRANCH REQUESTING
	@FAMLEDBAL CHAR(1) = 'Y',
	@REPORTOPT CHAR(1) = 'N',
	@NCCPERCENT INT = 0
)

AS
	/*===========================================================================
	---------------------------------------------------------------------
	FO CREDIT BALANCES AFTER ADJUSTING
	MARGIN REQUIREMENT & COLLATERAL
	TO BE CONSIDERED WHILE COMPUTING NET BALANCE
	---------------------------------------------------------------------	

	EXEC V2_COMPUTELEDBAL_COMBINED
		@SDATE = 'APR  1 2011',
		@FPARTY = '',
		@TPARTY = '',			
		@GENFLAG = 'A',
		@USER = 'DEMO',
		@EFFDATE = 'OCT 10 2011',
		@COMP_FOR = 'BRANCH',
		@FAMLEDBAL = 'Y'

	SELECT * FROM V2_LEDGERBALANCE_ALL

	SELECT PARTYCODE FROM V2_LEDGERBALANCE_ALL GROUP BY PARTYCODE HAVING COUNT(*) > 1

	SELECT * FROM MSAJAG..ACCBILL WHERE SETT_NO = '2005079'
	===========================================================================*/
	
	SET NOCOUNT ON
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

	IF @REPORTOPT = 'Y'
	BEGIN
		SELECT '.:: UPDATION COMPLETED SUCCESSFULLY ON ' + CONVERT(VARCHAR, MAX(RUNDATE)) + ' ::.' FROM V2_LEDGERBALANCE_ALL
		RETURN
	END

	IF @FPARTY = '' SET @FPARTY = '0000000000'

	IF @TPARTY = '' SET @TPARTY = 'ZZZZZZZZZZ'

	UPDATE
		V2_SETTPAYOUT_ALL
	SET
		STATUS = 'R', APPROVEDDT = GETDATE()
	WHERE
		CONVERT(VARCHAR(11), REQUESTDT, 112) < CONVERT(VARCHAR(11), GETDATE() - 1, 112)
		AND STATUS IN ('P', 'I')

	DECLARE @@RECCOUNT INT

	SET @@RECCOUNT = 0

	SELECT @@RECCOUNT = COUNT(1) FROM V2_SETTPAYOUT_ALL WHERE LEFT(REQUESTDT,11) = LEFT(GETDATE(),11) AND STATUS IN ('P', 'I')

	IF @COMP_FOR = 'BRANCH'
	BEGIN
		IF @@RECCOUNT <> 0
		BEGIN
			SELECT ':: SORRY!! CANNOT PROCEED. THERE ARE SOME REQUESTS PENDING. PLEASE REJECT/APPROVE TO RECALCULATE ::'
			RETURN
		END
	END 

	BEGIN TRAN

	DELETE
		V2_LEDGERBALANCE_ALL
	WHERE 
		PARTYCODE BETWEEN @FPARTY AND @TPARTY

	DELETE
		V2_LEDGERBALANCE_NET
	WHERE 
		PARTY_CODE BETWEEN @FPARTY AND @TPARTY
		
	CREATE TABLE [DBO].[#V2_LEDGERBALANCE_ALL]
	(
		[SRNO] [INT] IDENTITY(1,1) NOT NULL,
		[PARTYCODE] [VARCHAR](10) NULL,
		[PARTYNAME] [VARCHAR](100) NULL,
		[CHEQUENAME] [VARCHAR](100) NULL,
		[BRANCHCODE] [VARCHAR](10) NULL,
		[SUB_BROKER] [VARCHAR](10) NULL,
		[ACCAT] [VARCHAR](10) NULL,
		[BTOBPAYMENT] [INT] NULL,
		[EXCHANGE] [VARCHAR](3) NULL,
		[SEGMENT] [VARCHAR](7) NULL,
		[LEDBAL] [MONEY] NULL,
		[FAMLEDBAL] [MONEY] NULL,
		[AMTTOPAY] [MONEY] NULL,
		[PAYMODE] [VARCHAR](1) NULL,
		[BANKCODE] [VARCHAR](10) NULL,
		[GENFLAG] [VARCHAR](1) NULL,
		[CLIENTTYPE] [VARCHAR](3) NULL,
		[FAMILYCODE] [VARCHAR](10) NULL,
		[RUNDATE] [DATETIME] NULL,
		[UNCLEARED_AMT] [MONEY] NULL,
		[PAYAMT_TODAY] [MONEY] NULL,
		[CLIENT] [VARCHAR](10) NULL
	) ON [PRIMARY]
	
	/*===========================================================================
	INSERT INTO TEMP TABLE AFTER GROUPING AS REQUIRED
	===========================================================================*/
	INSERT INTO 
		#V2_LEDGERBALANCE_ALL
	EXEC V2_GETLEDBAL_ALL
		@SDATE,
		@FPARTY,
		@TPARTY,
		@GENFLAG,
		@USER,		
		@EFFDATE,
		@COMP_FOR,
		@NCCPERCENT
		
	/*CASH SEGMENT SHORTAGE CALCULATION*/
	CREATE TABLE #SHORTAGE_RMS
	(
		EXCHANGE CHAR(3),
		SETT_NO VARCHAR(7),
		SETT_TYPE VARCHAR(2),
		PARTY_CODE VARCHAR(10),
		SCRIP_NAME VARCHAR(50),
		SELLSHORTAGE INT,
		SHORTAGETYPE VARCHAR(5),
		CL_RATE NUMERIC(18, 4),
		SEC_PAYIN DATETIME
	)
		
	DECLARE
		@@SHORTAGE_DATE VARCHAR(11),
		@@COUNT INT

	SET @@SHORTAGE_DATE = CONVERT(VARCHAR(11), GETDATE())

	SELECT
		@@COUNT = COUNT(1)
	FROM
		BILLPOSTED
	WHERE
		CONVERT(VARCHAR(11), BILLDATE) = @@SHORTAGE_DATE
		AND SETT_TYPE IN ('A', 'AC', 'AD')

	IF @@COUNT = 0
	BEGIN
		INSERT INTO
			#SHORTAGE_RMS
			(EXCHANGE,SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_NAME,SELLSHORTAGE,SHORTAGETYPE,CL_RATE,SEC_PAYIN)
		EXEC
			MSAJAG.DBO.GET_SHORTAGE_PO @@SHORTAGE_DATE
	END

	SELECT
		PARTY_CODE,
		SHORTAGE_AMT = CASE WHEN CONVERT(VARCHAR(11), MAX(SEC_PAYIN), 103) = CONVERT(VARCHAR(11), GETDATE(), 103) 
							THEN
								SUM(SELLSHORTAGE * CL_RATE) * (1 + 0.20)
							ELSE
								SUM(SELLSHORTAGE * CL_RATE) * 0.20
							END
	INTO
		#SHORTAGE_RMS_FINAL
	FROM
		#SHORTAGE_RMS
	GROUP BY
		PARTY_CODE
		
	INSERT INTO 
		V2_LEDGERBALANCE_ALL
	SELECT
		PARTYCODE,
		PARTYNAME,
		CHEQUENAME,
		BRANCHCODE,
		SUB_BROKER,
		ACCAT,
		BTOBPAYMENT,
		EXCHANGE,
		SEGMENT,
		LEDBAL = SUM(LEDBAL) - SUM(ISNULL(SHORTAGE_AMT, 0)),
		FAMLEDBAL = SUM(FAMLEDBAL) - SUM(ISNULL(SHORTAGE_AMT, 0)),
		AMTTOPAY = CASE WHEN SUM(LEDBAL) - SUM(ISNULL(SHORTAGE_AMT, 0)) > 0 
						THEN SUM(LEDBAL) - SUM(ISNULL(SHORTAGE_AMT, 0)) 
						ELSE 
							0
						END,
		PAYMODE,
		BANKCODE,
		GENFLAG,
		CLIENTTYPE,
		FAMILYCODE, 
		RUNDATE,
		UNCLEARED_AMT = SUM(UNCLEARED_AMT),
		0,
		''		
	FROM
		#V2_LEDGERBALANCE_ALL
		LEFT OUTER JOIN
		#SHORTAGE_RMS_FINAL
		ON
		PARTYCODE = PARTY_CODE
	GROUP BY
		PARTYCODE,
		PARTYNAME,
		CHEQUENAME,
		BRANCHCODE,
		SUB_BROKER,
		ACCAT,
		BTOBPAYMENT,
		EXCHANGE,
		SEGMENT,		
		PAYMODE,
		BANKCODE,
		GENFLAG,
		CLIENTTYPE,
		FAMILYCODE, 
		RUNDATE

	INSERT INTO
		V2_LEDGERBALANCE_NET
	SELECT
		PARTYCODE,
		NET_LEDBAL = SUM(LEDBAL),
		FAM_LEDBAL = FAMLEDBAL,
		TOT_LEDBAL = SUM(LEDBAL),
		TOT_AMTTOPAY = CASE WHEN SUM(AMTTOPAY) <= SUM(LEDBAL) THEN SUM(AMTTOPAY) ELSE SUM(LEDBAL) END,
		CLIENT = ''
	FROM
		V2_LEDGERBALANCE_ALL
	WHERE 
		PARTYCODE BETWEEN @FPARTY AND @TPARTY
	GROUP BY
		PARTYCODE,
		FAMLEDBAL

	IF @FAMLEDBAL = 'Y'
	BEGIN
		UPDATE
			V2_LEDGERBALANCE_NET
		SET
			TOT_LEDBAL = CASE WHEN FAM_LEDBAL < 0 THEN TOT_LEDBAL - FAM_LEDBAL ELSE TOT_LEDBAL END,
			TOT_AMTTOPAY = CASE WHEN FAM_LEDBAL < 0 THEN TOT_AMTTOPAY - FAM_LEDBAL ELSE TOT_AMTTOPAY END
	END

	DELETE
		V2_LEDGERBALANCE_NET
	WHERE
		TOT_AMTTOPAY <= 0
	
	COMMIT TRAN

	SELECT '.:: UPDATION COMPLETED SUCCESSFULLY ON ' + CONVERT(VARCHAR, MAX(RUNDATE)) + ' ::.' FROM V2_LEDGERBALANCE_ALL

GO
