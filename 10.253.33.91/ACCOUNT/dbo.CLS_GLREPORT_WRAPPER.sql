CREATE PROCEDURE [dbo].[CLS_GLREPORT_WRAPPER]
 @FDATE VARCHAR(11),            /* AS MMM DD YYYY */  
 @TDATE VARCHAR(11),            /* AS MMM DD YYYY */   
 @SDATE VARCHAR(11),            /* AS MMM DD YYYY */  
 @FCODE VARCHAR(10),  
 @TCODE VARCHAR(10),  
 @STATUSID VARCHAR(30),  
 @STATUSNAME VARCHAR(30),  
 @BRANCH VARCHAR(10),  
 @GROUPBY VARCHAR(10),  
 @SELECTIONBY VARCHAR(3),
 @REPORT_TYPE VARCHAR(5),
 @EXCSEGMENT VARCHAR(2000),
 @RUNBAL VARCHAR(1)   = 'N' 
AS  
/*
exec cls_glreport_wrapper @FDATE='01/04/2019',@TDATE='13/06/2019',@SDATE='01/04/2019',@FCODE='A020202001',@TCODE='A020202001',@STATUSID='Broker',@STATUSNAME='Broker',@BRANCH='',@GROUPBY='N',@SELECTIONBY='vdt',@REPORT_TYPE='BANK',@EXCSEGMENT='BSE~CAPITAL|NSE~CAPITAL|NSE~FUTURES',@RUNBAL='Y'
*/
BEGIN   
     
CREATE TABLE [DBO].[#TMPLEDGERNEW] (    
 [SRNO] [INT] IDENTITY (1, 1) NOT NULL ,                    
 [BOOKTYPE] [CHAR] (2) NULL ,                    
 [VOUDT] [VARCHAR] (10) NULL ,        
 [EFFDT] [VARCHAR] (10) NULL ,                    
 [SHORTDESC] [CHAR] (6) NOT NULL DEFAULT(' ') ,                    
 [DRAMT] [MONEY] NULL ,                    
 [CRAMT] [MONEY] NULL ,                    
 [VNO] [VARCHAR] (12) NULL ,                    
 [NARRATION] [VARCHAR] (500) NULL ,                    
 [DDNO] [VARCHAR] (20) NULL ,                    
 [CLTCODE] [VARCHAR] (10) NOT NULL ,                    
 [LONGNAME] [VARCHAR] (100) NULL ,                    
 [VTYP] [SMALLINT] NOT NULL ,                    
 [ACCAT] [CHAR] (2) NULL ,                    
 [OPBAL] [MONEY] NOT NULL ,                    
 [CROSAC] [VARCHAR] (10) NOT NULL ,                    
 [ACNAME] [VARCHAR] (100) NULL ,                    
 [BRANCHCODE] [VARCHAR] (35) NULL ,                    
 [VCHDT] [DATETIME] NULL,    
 [EXCHANGE] [VARCHAR] (15) NULL,                    
 [SEGMENT] [VARCHAR] (10) NULL,    
 [ENTITY] [VARCHAR] (50) NULL,    
 [ORDERSEG][BIGINT] NULL,  
 [EDIFF] INT NULL,
 [BALANCE] [MONEY] NULL  	
 ) ON [PRIMARY]  

 CREATE NONCLUSTERED INDEX [PK_GL_TEMP] ON [dbo].[#TMPLEDGERNEW]
(
	SRNO ASC,
	CLTCODE ASC,
	EXCHANGE ASC,
	SEGMENT ASC
)

 CREATE TABLE [DBO].[#DBNAME]
 (
	ACCOUNTDB VARCHAR(20), 
	EXCHANGE VARCHAR(6), 
    SEGMENT VARCHAR(10),
	ACCOUNTSERVER VARCHAR(20)
 ) ON [PRIMARY] 
   
DECLARE        
 @@ACCOUNTDB VARCHAR(20),
 @@ACCOUNTSVR VARCHAR(20),        
 @@EXCHANGE VARCHAR(15),        
 @@SEGMENT VARCHAR(15),        
 @@SQL VARCHAR(1000),
 @@REPORTSQL VARCHAR(8000),
 @@DB VARCHAR(1000),            
 @@SEGORD TINYINT,        
 @@MAINREC AS CURSOR
 --@@CNT_REC INT           
 SET @EXCSEGMENT = REPLACE(@EXCSEGMENT,'-','~')

 --IF @REPORT_TYPE ='BANK' AND @BRANCH <> '' AND @BRANCH <> '%'
--	SELECT @BRANCH=BRANCHCODE FROM Acmast WHERE Cltcode=@FCODE

 IF CHARINDEX('/',@FDATE) > 0
 BEGIN
	SET @FDATE = CONVERT(VARCHAR(11),CONVERT(DATETIME,@FDATE,103),109)
 END
  IF CHARINDEX('/',@TDATE) > 0
 BEGIN
	SET @TDATE = CONVERT(VARCHAR(11),CONVERT(DATETIME,@TDATE,103),109)
 END
  IF CHARINDEX('/',@SDATE) > 0
 BEGIN
	SET @SDATE = CONVERT(VARCHAR(11),CONVERT(DATETIME,@SDATE,103),109)
 END
  
SELECT @@DB = " INSERT INTO #DBNAME SELECT ACCOUNTDB, EXCHANGE, SEGMENT, ACCOUNTSERVER FROM PRADNYA.DBO.MULTICOMPANY WHERE PRIMARYSERVER = 1 AND EXCHANGE + '~' + SEGMENT IN ('" + REPLACE(@EXCSEGMENT,'|',''',''') + "') ORDER BY EXCHANGE "
EXEC(@@DB) 
          
SET @@MAINREC = CURSOR FOR                      
                
SELECT ACCOUNTDB, EXCHANGE, SEGMENT, ACCOUNTSERVER FROM #DBNAME                
       
SET @@SEGORD = 1        
                    
OPEN @@MAINREC                      
 FETCH NEXT FROM @@MAINREC INTO @@ACCOUNTDB, @@EXCHANGE, @@SEGMENT, @@ACCOUNTSVR                  
WHILE @@FETCH_STATUS = 0                      
BEGIN                
	--SET @@CNT_REC = 0 
    IF @@ACCOUNTSVR = @@SERVERNAME
	 BEGIN 
		SET @@SQL = "INSERT INTO #TMPLEDGERNEW(BOOKTYPE,VOUDT,EFFDT,SHORTDESC,DRAMT,CRAMT,VNO,NARRATION,DDNO,CLTCODE, "
  		SET @@SQL = @@SQL + "LONGNAME,VTYP,ACCAT,OPBAL,CROSAC,ACNAME,BRANCHCODE,VCHDT,EDIFF)      "
		SET @@SQL = @@SQL + "EXEC " + @@ACCOUNTDB + ".DBO.CLS_GL_REPORT '" + @FDATE + "','" + @TDATE + "','" + @SDATE + "','" + @FCODE + "','" + @TCODE + "','" + @STATUSID + "','" + @STATUSNAME + "','" + @BRANCH + "','" + @SELECTIONBY + "','" + @REPORT_TYPE + "'"
	 END
	ELSE
	 BEGIN
		SET @@SQL = "INSERT INTO #TMPLEDGERNEW(BOOKTYPE,VOUDT,EFFDT,SHORTDESC,DRAMT,CRAMT,VNO,NARRATION,DDNO,CLTCODE, "
  		SET @@SQL = @@SQL + "LONGNAME,VTYP,ACCAT,OPBAL,CROSAC,ACNAME,BRANCHCODE,VCHDT,EDIFF)      "
		SET @@SQL = @@SQL + "EXEC " + ".DBO.CLS_GL_REPORT '" + @FDATE + "','" + @TDATE + "','" + @SDATE + "','" + @FCODE + "','" + @TCODE + "','" + @STATUSID + "','" + @STATUSNAME + "','" + @BRANCH + "','" + @SELECTIONBY + "','" + @REPORT_TYPE + "' "
		--SET @@CNT_REC = @@ROWCOUNT
	END	
	PRINT @@SQL
	EXEC (@@SQL) 
	
	IF @GROUPBY ='Y' 
		SET @GROUPBY ='1'
	ELSE 
		SET @GROUPBY ='0'

    
	--IF @GROUPBY = '0' 
	-- BEGIN	
	 
	  UPDATE #TMPLEDGERNEW SET EXCHANGE = @@EXCHANGE, SEGMENT= @@SEGMENT, ENTITY =  @@EXCHANGE + ' - ' + @@SEGMENT WHERE SEGMENT IS NULL  
	  
	-- IF @@ROWCOUNT > 0
	-- BEGIN
	--  SET @@SQL = "INSERT INTO #TMPLEDGERNEW(BOOKTYPE,VOUDT,EFFDT,SHORTDESC,DRAMT,CRAMT,VNO,NARRATION,DDNO,CLTCODE, "
 -- 	  SET @@SQL = @@SQL + "LONGNAME,VTYP,ACCAT,OPBAL,CROSAC,ACNAME,BRANCHCODE,VCHDT,EDIFF,BALANCE)      "
 -- 	  SET @@SQL = @@SQL + "SELECT '','SUBTOT','','',SUM(ISNULL(DRAMT,0)),SUM(ISNULL(CRAMT,0)),'','','','YYYYY','','','',MAX(ISNULL(OPBAL,0)),'','','','','', SUM(ISNULL(CRAMT,0)) - SUM(ISNULL(DRAMT,0)) FROM #TMPLEDGERNEW WHERE EXCHANGE = '" + @@EXCHANGE + "' AND SEGMENT = '" + @@SEGMENT + "'"
  	  
 -- 	  PRINT @@SQL
	--  EXEC (@@SQL)
	-- END 
	  
  UPDATE #TMPLEDGERNEW SET EXCHANGE = @@EXCHANGE, SEGMENT= @@SEGMENT, ENTITY =  @@EXCHANGE + ' - ' + @@SEGMENT, ORDERSEG = @@SEGORD WHERE ORDERSEG IS NULL    

	-- END
	--ELSE IF @GROUPBY = '1' 
	-- BEGIN
	--  UPDATE #TMPLEDGERNEW SET EXCHANGE = @@EXCHANGE, SEGMENT= @@SEGMENT, ENTITY =  'GENERAL LEDGER' WHERE SEGMENT IS NULL    
	  
	--  SET @@SQL = "INSERT INTO #TMPLEDGERNEW(BOOKTYPE,VOUDT,EFFDT,SHORTDESC,DRAMT,CRAMT,VNO,NARRATION,DDNO,CLTCODE, "
 -- 	  SET @@SQL = @@SQL + "LONGNAME,VTYP,ACCAT,OPBAL,CROSAC,ACNAME,BRANCHCODE,VCHDT,EDIFF,BALANCE)      "
 -- 	  SET @@SQL = @@SQL + "SELECT '','SUBTOT','','',SUM(ISNULL(DRAMT,0)),SUM(ISNULL(CRAMT,0)),'','','','YYYYY','','','',MAX(ISNULL(OPBAL,0)),'','','','','', SUM(ISNULL(CRAMT,0)) - SUM(ISNULL(DRAMT,0)) FROM #TMPLEDGERNEW WHERE EXCHANGE = '" + @@EXCHANGE + "' AND SEGMENT = '" + @@SEGMENT + "' "
  	  
 -- 	  PRINT @@SQL
	--  EXEC (@@SQL)
	  
	--  UPDATE #TMPLEDGERNEW SET ORDERSEG = 1 WHERE ORDERSEG IS NULL    
	--END
	
	
	

	SET @@SEGORD = @@SEGORD + 1         
 	      
	FETCH NEXT FROM @@MAINREC INTO @@ACCOUNTDB, @@EXCHANGE, @@SEGMENT, @@ACCOUNTSVR        
END 
 

UPDATE 
	#TMPLEDGERNEW
SET 
	DRAMT  = CASE WHEN OPBAL < 0 THEN ABS(OPBAL) ELSE 0 END,   
	CRAMT  = CASE WHEN OPBAL > 0 THEN OPBAL ELSE 0 END  
WHERE 
	SHORTDESC = 'OPBAL'



	SELECT A.* INTO #TEMPDATA
	FROM
	(
		SELECT 
		        SRNO,--ROW_NUMBER() OVER (ORDER BY (SELECT NULL))AS ID,
			 	BOOKTYPE,
			    VOUDT AS VDT,
				EFFDT AS EDT,
				SHORTDESC,
				DRAMT DEBIT,
				CRAMT CREDIT,
				VNO,
				NARRATION,
				DDNO,
				CLTCODE,    
				LONGNAME,
				VTYP,
				ACCAT,
				OPBAL,
				CROSAC,
				ACNAME,
				BRANCHCODE,
				SEGMENT,
				EXCHANGE,     
				ENTITY,    
				ORDERSEG,
				VCHDT,
				--BALANCE = ISNULL(BALANCE, 0)
				--BALANCE = CASE WHEN @RUNBAL = 'Y' 
				--THEN 
				--	(
				--	ISNULL((
				--		SELECT 
				--		SUM(CASE WHEN DRAMT = 0 THEN CRAMT ELSE -DRAMT END) 
				--		FROM  #TMPLEDGERNEW B
				--		WHERE /*SHORTDESC <> 'OPBAL'
				--		AND*/ B.SRNO <= T.SRNO
				--		AND  B.CLTCODE = T.CLTCODE AND B.EXCHANGE = T.EXCHANGE  AND B.SEGMENT =T.SEGMENT
				--	),''))
				--ELSE ISNULL(BALANCE,0) END
				BALANCE = SUM(CASE WHEN DRAMT = 0 THEN CRAMT ELSE -DRAMT END) OVER (  ORDER BY SRNO ) 
		FROM #TMPLEDGERNEW T
		--ORDER BY T.SRNO,T.CLTCODE	
)A

		DELETE FROM #TEMPDATA WHERE VTYP = 18

		DELETE T FROM #TEMPDATA T,#TEMPDATA T2 WHERE T.CROSAC=T2.CROSAC
		AND T.CROSAC BETWEEN @FCODE AND @TCODE
		AND @REPORT_TYPE='CASH'
		
		SELECT	
				
				SRNO,
				BOOKTYPE ,--= CONVERT(INT,BOOKTYPE) ,
				VDT,
				EDT,
				VNO,
				CLTCODE ,  
				CROSAC,
				SHORTDESC = ltrim(rtrim(SHORTDESC)),
				NARRATION,
				DDNO,
				ENTITY,    
				DEBIT,  --= CASE WHEN OPBAL < 0 THEN OPBAL ELSE 0 END,   
				CREDIT,  --= CASE WHEN OPBAL > 0 THEN OPBAL ELSE 0 END,   
				BALANCE ,
				LONGNAME,
				VTYP,
				ACCAT,
				OPBAL,
				ACNAME,
				BRANCHCODE,
				SEGMENT,
				EXCHANGE,     
				ORDERSEG,
				VCHDT
--INTO TMP_BANKDATA
FROM #TEMPDATA	
UNION ALL
SELECT			
				ISNULL(9999,''),
				BOOKTYPE =	'998',
				'SUBTOT',
				'',
				'',
				CLTCODE ,  
				'',
				'',
				'',
				'',
				'',    
				SUM(DEBIT),
				SUM(CREDIT),
				SUM(ISNULL(CREDIT,0)) - SUM(ISNULL(DEBIT,0)) BALANCE,
				
				'',
				'',
				'',
				'',
				'',
				'',
				SEGMENT,
				EXCHANGE,     
				ORDERSEG,
				''
FROM #TEMPDATA	
WHERE CLTCODE <> ''
GROUP BY CLTCODE,EXCHANGE,SEGMENT,ORDERSEG
UNION 
SELECT			
				ISNULL(99999,''),
				BOOKTYPE =	'',
				'TOTAL',
				'',
				'',
				CLTCODE ,  
				'',
				'',
				'',
				'',
				'',    
				SUM(DEBIT),
				SUM(CREDIT),
				SUM(ISNULL(CREDIT,0)) - SUM(ISNULL(DEBIT,0)) BALANCE,

				'',
				'',
				'',
				'',
				'',
				'',
				SEGMENT='ZZZZ',     
				EXCHANGE ='ZZZZ',
				ORDERSEG = MAX(ORDERSEG) + 1,
				''
FROM #TEMPDATA	
WHERE CLTCODE <> ''
GROUP BY CLTCODE
UNION 
SELECT			
				ISNULL(0,''),
				BOOKTYPE =	'-1',
				ENTITY,
				'',
				'',
				CLTCODE ,  
				'',
				'HEADER',
				'',
				'',
				ENTITY,    
				'',
				'',
				'',
				'',
				'',
				'',
				'',
				'',
				'',
				'',     
				'',
				ORDERSEG ,
				''
FROM #TEMPDATA	
WHERE CLTCODE <> ''

GROUP BY CLTCODE,ENTITY,ORDERSEG
ORDER BY CLTCODE,ORDERSEG,SRNO
--CONVERT(INT,BOOKTYPE)--,ORDERSEG

DROP TABLE #TMPLEDGERNEW




END

