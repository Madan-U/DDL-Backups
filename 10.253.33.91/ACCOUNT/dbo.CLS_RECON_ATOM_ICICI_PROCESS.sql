-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROCEDURE [dbo].[CLS_RECON_ATOM_ICICI_PROCESS]
( 
 @UPLOADID NVARCHAR(50) 
,@BANKCODE NVARCHAR(50)
,@UPLOADFILENAME NVARCHAR(50)
,@UNAME VARCHAR(50)
) 
AS

BEGIN
DECLARE @CLOSINGBALANCE DECIMAL(18,2)

	IF CHARINDEX('.CSV',@UPLOADFILENAME) > 0
	BEGIN
		BEGIN TRY

			DECLARE @D VARCHAR(20),@VALUEDATE DATETIME,@FILDATEDATE VARCHAR(20)
			SELECT @D=.DBO.PIECE (@UPLOADFILENAME,'_',3)
			SELECT @FILDATEDATE=STUFF(STUFF( @D, 5, 0, '/'), 3, 0, '/')-- DD/MM/YYYY
			
			SELECT @VALUEDATE=CONVERT(DATETIME,CONVERT(VARCHAR(10),@FILDATEDATE,109),103)
			
			
			SELECT 
			PROGID,
			REFERENCENO = .DBO.PIECE(FILE_DATA,',',3),
			CLIENTCODE = .DBO.PIECE(FILE_DATA,',',6),
			AMOUNT = CAST(.DBO.PIECE(FILE_DATA,',',22) AS MONEY),
			DESCRIPTION = .DBO.PIECE(FILE_DATA,',',3)+'|'+.DBO.PIECE(FILE_DATA,',',6),
			--VALUEDATE = CONVERT(DATETIME,CONVERT(VARCHAR(11),.DBO.PIECE(FILE_DATA,',',24),109),103),
			UPLOADDATETIME,
			UPLOADBY INTO
			#TEMPDATA_ATOM 
			FROM MSAJAG..FILEDATA (NOLOCK) WHERE PROGID=@UPLOADID
			AND ISNULL(DBO.PIECE(FILE_DATA,',',24),'') <>''
			AND UPPER(ISNULL(DBO.PIECE(FILE_DATA,',',24),''))<>'SETTLEMENT DATE'
			
		 

			INSERT INTO #NONCMS_DATA (UPLOADID
			, BANKACCOUNT
			, BANKCODE
			, DESCRIPTION
			, LEDGERBALANCE
			, DRCR
			, AMT
			, VALUEDATE
			, REFERENCENO
			, CLTACCOUNT
			, CUSTOMERREFERENCE
			, TRANSACTIONDETAIL
			, VNO
			, VTYP
			, BOOKTYPE
			, MATCHEDFLAG
			, UPLOADSTATUS
			, UPLOADEDDATE
			, MODIFIEDDATE
			, UPLOADBY)

			SELECT 
			PROGID
			,ACCOUNTNUMBER=RL.BANKACCOUNT
			,BANKCODE
			,DESCRIPTION = T.DESCRIPTION
			,LEDGERBALANCE=''
			,DRCR =  'C'
			,AMOUNT = T.AMOUNT
			,VALUEDATE = @VALUEDATE --T.VALUEDATE
			,REFERENCENO = T.REFERENCENO
			,CLTACCOUNT = T.CLIENTCODE
			, '' AS 'CUSTOMERREFERENCE'
			, TRANSACTIONDETAIL=''
			,'' AS  VNO
			,'' AS  VTYP
			,'' AS  BOOKTYPE
			, MATCHEDFLAG = 0
			, UPLOADSTATUS = 'PENDING'
			, UPLOADEDDATE = GETDATE()
			, MODIFIEDDATE = GETDATE()
			, UPLOADBY=RL.UPLOADBY
			FROM #TEMPDATA_ATOM T, RECON_BANK_FILEUPLOAD_LOGS (NOLOCK) RL WHERE T.PROGID=RL.UPLOADID
			--RETURN
			DROP TABLE #TEMPDATA_ATOM

			--DELETE FROM MSAJAG..FILEDATA WHERE PROGID=@UPLOADID

		END TRY

		BEGIN CATCH

			DROP TABLE #TEMPDATA_ATOM
			DELETE FROM MSAJAG..FILEDATA WHERE PROGID=@UPLOADID
			SELECT MSG='NOT CORRECT FILE FORMAT '
			DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
			DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID

		END CATCH

	END

IF((SELECT COUNT(1) FROM #NONCMS_DATA) <0)
BEGIN
	SELECT MSG='NOT CORRECT FILE FORMAT '
	DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
	DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
END


END

GO
