-- Object: PROCEDURE dbo.GL_ACCOUNT_TPT_REPORT
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROCEDURE [dbo].[GL_ACCOUNT_TPT_REPORT] (@TODATE VARCHAR(11))      
      
AS       
      
--EXEC GL_ACCOUNT_TPT_REPORT 'JAN 31 2022'      --RPT_RECO_STATS
      
DECLARE @GL_CODE VARCHAR(10) = '5100000017'      
      
SELECT EXCHNAGE = 'NSE',VDT,LEDGER.VNO,LEDGER.VTYP,SHORTDESC,LEDGER.NARRATION,CLTCODE,DRAMT= CASE WHEN DRCR= 'D' THEN VAMT ELSE '0' END      
,CRAMT= CASE WHEN DRCR= 'C' THEN VAMT ELSE '0' END,LEDGER.BOOKTYPE      
INTO #LEDGERDATA      
FROM LEDGER WITH (NOLOCK), VMAST      WITH (NOLOCK)
WHERE LEDGER.VTYP = VMAST.VTYPE          AND VTYP <> '18'
AND CLTCODE  = @GL_CODE AND VDT BETWEEN 'APR  1 2019' AND @TODATE + ' 23:59'       
      
INSERT INTO #LEDGERDATA      
SELECT EXCHNAGE = 'BSE',VDT,LEDGER.VNO,LEDGER.VTYP,SHORTDESC,LEDGER.NARRATION,CLTCODE,DRAMT= CASE WHEN DRCR= 'D' THEN VAMT ELSE '0' END      
,CRAMT= CASE WHEN DRCR= 'C' THEN VAMT ELSE '0' END,LEDGER.BOOKTYPE      
FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER WITH (NOLOCK) , AngelBSECM.ACCOUNT_AB.DBO.VMAST      WITH (NOLOCK)
WHERE LEDGER.VTYP = VMAST.VTYPE          AND VTYP <> '18'
AND CLTCODE  = @GL_CODE AND VDT BETWEEN 'APR  1 2019' AND @TODATE + ' 23:59'       
      
      
ALTER TABLE #LEDGERDATA ADD DDNO VARCHAR(50)       
      
UPDATE #LEDGERDATA SET #LEDGERDATA.DDNO  = A.DDNO       
FROM LEDGER1 A WHERE EXCHNAGE = 'NSE' AND A.VNO  = #LEDGERDATA.VNO AND A.VTYP  = #LEDGERDATA.VTYP       
AND A.BOOKTYPE  = #LEDGERDATA.BOOKTYPE      
      
UPDATE #LEDGERDATA SET #LEDGERDATA.DDNO  = A.DDNO       
FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER1 A WHERE EXCHNAGE = 'BSE' AND A.VNO  = #LEDGERDATA.VNO AND A.VTYP  = #LEDGERDATA.VTYP       
AND A.BOOKTYPE  = #LEDGERDATA.BOOKTYPE      
      
SELECT EXCHNAGE,CONVERT(CHAR(10), VDT, 103) AS VOUCHERDT,SHORTDESC,VNO,NARRATION,DDNO = ISNULL(DDNO,''),DRAMT,CRAMT      
FROM #LEDGERDATA WHERE VTYP <> '18'  AND CLTCODE  = @GL_CODE ORDER BY VDT,EXCHNAGE ASC      
      
      
DROP TABLE #LEDGERDATA

GO
