CREATE PROCEDURE [dbo].[EXPCETIONAL_POST_REPORT_29042022] (@RPTDATE DATETIME)

AS ---- EXEC EXPCETIONAL_POST_REPORT 'APR 19 2022'

SELECT A.CLTCODE,BANKCODE=CONVERT(VARCHAR(20),''),BANK_NAME=CONVERT(VARCHAR(MAX),''),
A.VDT,A.EDT,RELDATE=CONVERT(DATETIME,''),A.VNO,A.VTYP,DDNO=CONVERT(VARCHAR(500),''),
A.DRCR,A.VAMT,A.CDT,A.ENTEREDBY,A.CHECKEDBY,A.NARRATION INTO #EXPRPT
FROM LEDGER A WITH (NOLOCK), MSAJAG.DBO.CLIENT_DETAILS B WITH (NOLOCK)
WHERE A.CLTCODE = B.CL_CODE
AND VDT < @RPTDATE AND CDT > @RPTDATE + ' 04:01:00' AND A.VTYP IN (3,2,8,17,88)

INSERT INTO #EXPRPT
SELECT A.CLTCODE,BANKCODE=CONVERT(VARCHAR(20),''),BANK_NAME=CONVERT(VARCHAR(MAX),''),
A.VDT,A.EDT,RELDATE=CONVERT(DATETIME,''),A.VNO,A.VTYP,DDNO=CONVERT(VARCHAR(500),''),
A.DRCR,A.VAMT,A.CDT,A.ENTEREDBY,A.CHECKEDBY,A.NARRATION 
FROM LEDGER A WITH (NOLOCK), MSAJAG.DBO.CLIENT_DETAILS B WITH (NOLOCK)
WHERE A.CLTCODE = B.CL_CODE
AND VDT > @RPTDATE-7 AND VDT < @RPTDATE + ' 23:59:59' AND LEFT(EDT,11) < LEFT(VDT,11)
AND A.VTYP IN (3,2,8,17,88)

UPDATE #EXPRPT SET BANKCODE = B.CLTCODE, BANK_NAME = B.ACNAME FROM #EXPRPT A, LEDGER B WITH (NOLOCK)
WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND A.VAMT = B.VAMT AND A.CLTCODE <>  B.CLTCODE

UPDATE #EXPRPT SET RELDATE = B.reldt, DDNO= B.ddno FROM #EXPRPT A, LEDGER1 B WITH (NOLOCK)
WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND A.VAMT = B.relamt

SELECT * FROM #EXPRPT ORDER BY VNO

GO
