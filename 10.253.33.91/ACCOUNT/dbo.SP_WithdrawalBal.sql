-- Object: PROCEDURE dbo.SP_WithdrawalBal
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE proc [dbo].[SP_WithdrawalBal](@CLTCODE VARCHAR(10))
as
BEGIN
DECLARE @FROMDATE VARCHAR(20)  --@CLTCODE VARCHAR(10)

--SET @CLTCODE = 'M219423' -- CLIENT CODE CHANGE

SET @FROMDATE = CONVERT(VARCHAR(11),GETDATE(),109)

DECLARE @FINSTDT DATETIME 

SELECT @FINSTDT = SDTCUR FROM ACCOUNT.DBO.PARAMETER WITH (NOLOCK) WHERE @FROMDATE BETWEEN SDTCUR AND LDTCUR

SELECT CLTCODE,SUM(VAMT)AS VAMT,CONVERT(VARCHAR(10),'') AS SEGMENT  INTO #VDTLED FROM (    
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   ACCOUNT.DBO.LEDGER WITH(NOLOCK)  
WHERE    VDT>=@FINSTDT   AND  VDT<=@FROMDATE  + ' 23:59' AND CLTCODE = @CLTCODE AND 1 <> 1 GROUP BY CLTCODE)A    
 GROUP BY CLTCODE    
 HAVING SUM(VAMT) <>0

 SELECT * INTO #VDTLED_FINAL FROM #VDTLED WHERE 1 <> 1

CREATE TABLE #MULTIDATABASE (SERVERNAME VARCHAR(100),DATABASENAME VARCHAR(100),SEGMENT VARCHAR(100),SRNO INT IDENTITY (1,1))

INSERT INTO #MULTIDATABASE SELECT 'ANAND1','ACCOUNT','NSE'
INSERT INTO #MULTIDATABASE SELECT 'ANAND1','ACCOUNTSLBS','SLBM'
INSERT INTO #MULTIDATABASE SELECT 'ANAND1','MTFTRADE','MTF'
INSERT INTO #MULTIDATABASE SELECT 'ANAND','ACCOUNT_AB','BSE'
INSERT INTO #MULTIDATABASE SELECT 'ANGELFO','ACCOUNTFO','NSEFO'
INSERT INTO #MULTIDATABASE SELECT 'ANGELFO','ACCOUNTCURFO','NSX'
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTNCDX','NCDX'
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTMCDX','MCX'
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTMCDXCDS','MCD'
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTBFO','BSEFO'
INSERT INTO #MULTIDATABASE SELECT 'ANGELCOMMODITY','ACCOUNTCURBFO','BSECURFO'


DECLARE @SNAME VARCHAR(100),@DNAME VARCHAR(100),@SEGMENT VARCHAR(100),@SRNOMAX INT,@COUNTINT INT

SET @COUNTINT = 1

SELECT @SRNOMAX = MAX(SRNO) FROM #MULTIDATABASE

WHILE @COUNTINT <= @SRNOMAX
BEGIN

SELECT @SNAME = SERVERNAME , @DNAME = DATABASENAME ,@SEGMENT = SEGMENT FROM #MULTIDATABASE WHERE SRNO = @COUNTINT

IF @SNAME = 'ANAND1'
BEGIN 

TRUNCATE TABLE #VDTLED

EXEC ('INSERT INTO #VDTLED
SELECT CLTCODE,SUM(CASE WHEN DRCR= ''D'' THEN -VAMT ELSE VAMT END) AS VAMT,''' + @SEGMENT + '''  FROM ' + @DNAME + '.DBO.LEDGER WITH (NOLOCK) 
WHERE  VDT BETWEEN ''' + @FINSTDT + ''' AND ''' + @FROMDATE + ' 23:59:59'' AND CLTCODE = ''' + @CLTCODE + ''' GROUP BY CLTCODE HAVING SUM(VAMT) <> 0 ') 

INSERT INTO #VDTLED_FINAL SELECT * FROM #VDTLED

END
ELSE
BEGIN
TRUNCATE TABLE #VDTLED

EXEC ('INSERT INTO #VDTLED
SELECT CLTCODE,SUM(CASE WHEN DRCR= ''D'' THEN -VAMT ELSE VAMT END) AS VAMT,''' + @SEGMENT + '''  FROM ' + @SNAME + '.' + @DNAME + '.DBO.LEDGER WITH (NOLOCK) 
WHERE  VDT BETWEEN ''' + @FINSTDT + ''' AND ''' + @FROMDATE + ' 23:59:59'' AND CLTCODE = ''' + @CLTCODE  +  ''' GROUP BY CLTCODE HAVING SUM(VAMT) <> 0 ') 

INSERT INTO #VDTLED_FINAL SELECT * FROM #VDTLED

END

SET @COUNTINT = @COUNTINT + 1

END

SELECT CLTCODE,ISNULL([BSE],0) AS [BSE],ISNULL([BSECURFO],0) AS [BSECURFO],ISNULL([BSEFO],0) AS [BSEFO],
ISNULL([MCD],0) AS [MCD],ISNULL([MCX],0) AS [MCX],ISNULL([MTF] ,0) AS [MTF],
ISNULL([NCDX],0) AS [NCDX] ,ISNULL([NSE],0) AS [NSE],ISNULL([NSEFO],0) AS [NSEFO],ISNULL([NSX],0) AS [NSX],ISNULL([SLBM],0) AS [SLBM]
,ISNULL([BSE],0)+ISNULL([BSECURFO],0)+ISNULL([BSEFO],0)+ISNULL([MCD],0)+ISNULL([MCX],0)+ISNULL([MTF] ,0)+
ISNULL([NCDX],0)+ISNULL([NSE],0)+ISNULL([NSEFO],0)+ISNULL([NSX],0)+ISNULL([SLBM],0) AS TOTAL INTO #TEMP_VDT
FROM
(SELECT CLTCODE,VAMT,SEGMENT FROM #VDTLED_FINAL) AS A
PIVOT
(SUM(VAMT) FOR SEGMENT IN ([BSE],[BSECURFO],[BSEFO],[MCD],[MCX],[MTF],[NCDX],[NSE],[NSEFO],[NSX],[SLBM] )) AS B


  SELECT  CLTCODE,NSE AS NSE,BSE AS BSE,[NSEFO] AS FO,NSX AS NSX,    
  MCD AS MCD ,MCX AS MCX,[NCDX] AS NCX,BSEFO AS BSEFO,[BSECURFO] AS BSX,MTF AS MTF,[SLBM] AS SLBS,    
      TOTAL AS TOTAL,CONVERT(NUMERIC(18,2),0) AS UNRECOAMT,CONVERT(NUMERIC(18,2),0) AS CREDITBILLS
	  ,CONVERT(NUMERIC(18,2),0) AS ACCCHARGES
	  ,CONVERT(NUMERIC(18,2),0) AS NONCASHCOLL
	  ,CONVERT(NUMERIC(18,2),0) AS ACCRUEDCHARGES
	  	  ,CONVERT(NUMERIC(18,2),0) AS SHORTSELLVALUE
	  INTO #TEMP_VDT1
	  FROM #TEMP_VDT 
 	  
SELECT CLTCODE,SUM(VAMT) AS VAMT  INTO #UNCLEARED_REC_ANG FROM ACCOUNT.DBO.LEDGER L WITH (NOLOCK)   
WHERE VDT BETWEEN @FINSTDT AND @FROMDATE + ' 23:59:59' AND L.VTYP  =2     AND CLTCODE = @CLTCODE
AND EDT LIKE '%2049%' GROUP BY CLTCODE


SELECT CLTCODE,SUM(VAMT) AS VAMT  INTO #CREDITBILLS FROM ACCOUNT.DBO.LEDGER_ALL L WITH (NOLOCK)   
WHERE VDT <= @FROMDATE  AND EDT > @FROMDATE AND L.VTYP  = 15 AND CLTCODE = @CLTCODE
 GROUP BY CLTCODE


SELECT  PARTY_CODE,ACCRUEDCHARGES INTO #CUSA_PO FROM [196.1.115.132].CMS.DBO.CUSA_PO A WITH (NOLOCK) 
WHERE A.PARTY_CODE = @CLTCODE

SELECT PARTY_CODE,SUM(FINALAMOUNT) FINALAMOUNT INTO #COLLATERALDETAILS FROM MSAJAG.DBO.COLLATERALDETAILS WITH (NOLOCK)
WHERE EFFDATE = (SELECT TOP 1 EFFDATE FROM MSAJAG.DBO.COLLATERALDETAILS WITH (NOLOCK) ORDER BY EFFDATE DESC)
AND PARTY_CODE = @CLTCODE
GROUP BY PARTY_CODE

UPDATE #TEMP_VDT1 SET UNRECOAMT = VAMT FROM #UNCLEARED_REC_ANG WHERE #UNCLEARED_REC_ANG.CLTCODE = #TEMP_VDT1.CLTCODE

UPDATE #TEMP_VDT1 SET CREDITBILLS = VAMT FROM #CREDITBILLS WHERE #CREDITBILLS.CLTCODE = #TEMP_VDT1.CLTCODE

UPDATE #TEMP_VDT1 SET CREDITBILLS = FINALAMOUNT FROM #COLLATERALDETAILS WHERE PARTY_CODE = #TEMP_VDT1.CLTCODE

CREATE TABLE #MRG (PARTY_CODE VARCHAR(11),MARGIN NUMERIC(18,2),SEGMENT VARCHAR(5))        

INSERT INTO #MRG        
SELECT PARTY_CODE, SUM(TOTAL_MARGIN_VAR) AS MARGIN,SEGMENT='CM'         
FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK)   
WHERE MARGINDATE  = (SELECT TOP 1 MARGINDATE FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) ORDER BY MARGINDATE DESC)  
AND PARTY_CODE = @CLTCODE
GROUP BY PARTY_CODE

INSERT INTO #MRG        
SELECT PARTY_CODE,SUM(TOTALMARGIN) AS MARGIN, SEGMENT='FO'        
FROM ANGELFO.NSEFO.DBO.FOMARGINNEW WITH (NOLOCK)        
WHERE MDATE  = (SELECT TOP 1 MDATE FROM ANGELFO.NSEFO.DBO.FOMARGINNEW WITH (NOLOCK) ORDER BY MDATE DESC ) 
AND PARTY_CODE = @CLTCODE
GROUP BY PARTY_CODE


INSERT INTO #MRG        
SELECT PARTY_CODE,SUM(TOTALMARGIN) AS MARGIN, SEGMENT='CD'        
FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW WITH (NOLOCK)        
WHERE MDATE  = (SELECT TOP 1 MDATE FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW WITH (NOLOCK) ORDER BY MDATE DESC ) 
AND PARTY_CODE = @CLTCODE
GROUP BY PARTY_CODE
        
INSERT INTO #MRG        
SELECT PARTY_CODE,SUM(TOTALMARGIN) AS MARGIN, SEGMENT='MX'        
FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW WITH (NOLOCK)        
WHERE MDATE  = (SELECT TOP 1 MDATE FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW WITH (NOLOCK) ORDER BY MDATE DESC ) 
AND PARTY_CODE = @CLTCODE
GROUP BY PARTY_CODE
 
INSERT INTO #MRG        
SELECT PARTY_CODE,SUM(INITIALMARGIN)+SUM(CASE WHEN MTMMARGIN < 0 THEN 0 ELSE MTMMARGIN END ) + SUM(ADDMARGIN) AS MARGIN,SEGMENT='NX' 
FROM ANGELCOMMODITY.NCDX.DBO.TBL_CLIENTMARGIN WITH (NOLOCK)        
WHERE MARGINDATE  = (SELECT TOP 1 MARGINDATE FROM ANGELCOMMODITY.NCDX.DBO.TBL_CLIENTMARGIN WITH (NOLOCK) ORDER BY MARGINDATE DESC ) 
AND PARTY_CODE = @CLTCODE
GROUP BY PARTY_CODE
 

SELECT PARTY_CODE,CM_MARGIN=ISNULL([CM],0),FO_MARGIN=ISNULL([FO],0),        
CD_MARGIN=ISNULL([CD],0),MX_MARGIN=ISNULL([MX],0),NX_MARGIN=ISNULL([NX],0),        
TOTAL_MARIN=ISNULL([CM],0)+ISNULL([FO],0)+ISNULL([CD],0)+ISNULL([MX],0)+ISNULL([NX],0)  INTO #ALLMRG        
FROM (        
SELECT PARTY_CODE,MARGIN,SEGMENT FROM #MRG) AS SourceTable        
PIVOT        
(SUM(MARGIN) FOR SEGMENT IN ([CM],[FO],[CD],[MX],[NX])) AS PivotTable         


SELECT *,CONVERT(money,0.00) as ExpoUtilised,CONVERT(money,0.00) as NetPayable into #ncms FROM #TEMP_VDT1 LEFT OUTER JOIN #ALLMRG ON (PARTY_CODE = CLTCODE)
update #ncms set TOTAL_MARIN=isnull(TOTAL_MARIN,0)*-1 ,UNRECOAMT=isnull(UNRECOAMT,0)*-1,CREDITBILLS=isnull(CREDITBILLS,0)*-1
declare @NonCashConsideration as int=100

update #ncms set ExpoUtilised=ExpoUtilised+      
 (case         
 when  NONCASHCOLL=0 then   isnull(TOTAL_MARIN,0)         
 when NONCASHCOLL>((isnull(TOTAL_MARIN,0)*-1)*(@NonCashConsideration/100)) and NONCASHCOLL>0             
 then 0               
 when NONCASHCOLL<((isnull(TOTAL_MARIN,0)*-1)*(@NonCashConsideration/100)) and NONCASHCOLL>0  then   (isnull(TOTAL_MARIN,0)*(@NonCashConsideration/100)) +  NONCASHCOLL       
 end)

 update #ncms set NetPayable=TOTAL+ExpoUtilised+CREDITBILLS+UnrecoAmt+AccruedCharges     

 select CLTCODE,TOTAL as BALANCE,CREDITBILLS,UNRECOAMT,ACCCHARGES,isnull(TOTAL_MARIN,0)TOTAL_MARIN,isnull(SHORTSELLVALUE,0) as SHORTSELLVALUE,NONCASHCOLL,isnull(NETPAYABLE,0) NETPAYABLE from #ncms

DROP TABLE #CUSA_PO
DROP TABLE #TEMP_VDT1
DROP TABLE #TEMP_VDT
DROP TABLE #VDTLED_FINAL
DROP TABLE #VDTLED
DROP TABLE #MULTIDATABASE
DROP TABLE #UNCLEARED_REC_ANG
DROP TABLE #ALLMRG
DROP TABLE #CREDITBILLS
DROP TABLE #COLLATERALDETAILS
DROP TABLE #MRG 

END

GO
