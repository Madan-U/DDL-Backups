-- Object: PROCEDURE dbo.CLS_RECON_NONCMS_ICICIPAY_PROCESS
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROCEDURE [dbo].[CLS_RECON_NONCMS_ICICIPAY_PROCESS]
( @UPLOADID NVARCHAR(50) 
,@BANKCODE NVARCHAR(50)
,@BANKACCOUNT VARCHAR(50)
,@UPLOADFILENAME VARCHAR(199)
,@UNAME VARCHAR(50)
) 
AS

BEGIN

BEGIN TRY 
SELECT * INTO #TEMPDATAICICI
FROM MSAJAG.DBO.FILEDATA (NOLOCK)
WHERE  PROGID=@UPLOADID
AND ISNULL(.DBO.PIECE(FILE_DATA,',',1),'')<>''
AND .DBO.PIECE(FILE_DATA,',',2) NOT IN('TRAN DATE')
AND .DBO.PIECE(FILE_DATA,',',3) NOT IN ('B/F')
AND .DBO.PIECE(FILE_DATA,',',5) NOT IN ('INST NUM')

INSERT INTO #NONCMS_DATA (UPLOADID
, BANKACCOUNT
, BANKCODE
, DESCRIPTION
, LEDGERBALANCE
, DRCR
, AMT
, VALUEDATE
, REFERENCENO
, CLTACCOUNT
, CUSTOMERREFERENCE
, TRANSACTIONDETAIL
, VNO
, VTYP
, BOOKTYPE
, MATCHEDFLAG
, UPLOADSTATUS
, UPLOADEDDATE
, MODIFIEDDATE
, UPLOADBY
)

SELECT 
UPLOADID=PROGID
, BANKACCOUNT =@BANKACCOUNT
, BANKCODE = @BANKCODE
, DESCRIPTION = .DBO.PIECE(FILE_DATA,',',3)+'/'+.DBO.PIECE(FILE_DATA,',',4)
, LEDGERBALANCE=''
, DRCR=CASE WHEN .DBO.PIECE(FILE_DATA,',',8)<>'' THEN 'D' ELSE 'C' END
, AMT=  CAST (CASE WHEN .DBO.PIECE(FILE_DATA,',',8)<>'' THEN .DBO.PIECE(FILE_DATA,',',8) ELSE .DBO.PIECE(FILE_DATA,',',9)END AS MONEY)
, VALUEDATE=CONVERT(DATETIME,.DBO.PIECE(FILE_DATA,',',2),103)
, REFERENCENO = .DBO.PIECE(FILE_DATA,',',5)
, CLTACCOUNT=''
, CUSTOMERREFERENCE =''
, TRANSACTIONDETAIL=''
, VNO=''
, VTYP=''
, BOOKTYPE=''
, MATCHEDFLAG=0
, UPLOADSTATUS='PENDING'
, UPLOADEDDATE=GETDATE()
, MODIFIEDDATE=GETDATE()
, UPLOADBY=@UNAME
 FROM #TEMPDATAICICI

 DROP TABLE #TEMPDATAICICI
 END TRY
 BEGIN CATCH
		SELECT MSG=ERROR_MESSAGE()		
		DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID	
		DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
		DELETE FROM MSAJAG..FILEDATA WHERE PROGID=@UPLOADID
 END CATCH
 END

GO
