-- Object: PROCEDURE dbo.RPT_ACC_TRIALBALANCE_N
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROCEDURE RPT_ACC_TRIALBALANCE_N        

    

@VDT VARCHAR(11),               /* AS ON DATE ENTERED BY USER */        

@FLAG VARCHAR(15),              /* SORT ORDER FOR REPORT - CODEWISE OR NAMEWISE */        

@VIEWOPTION VARCHAR(10),        /* OPTIONS FOR ACCOUNTS SELECTION - ALL, GL, PARTY */        

@BALANCE VARCHAR(10),           /* NORMAL OR WITHOPBAL */        

@STDATE VARCHAR(11),            /* START DATE ENTERED BY USER */        

@CURRYRSTDATE VARCHAR(11), /* START DATE OF FINANCIAL YEAR */        

@OPENENTRYFLAG INT,        

@OPENINGENTRYDATE VARCHAR(11),  /* O/P ENTRY DATE ( VTYP = 18 ) FROUND FROM LEDGER FOR VDT <= LAST DATE ENTERED BY USER */        

@STATUSID VARCHAR(20),          /* AS BROKER/BRANCH/CLIENT ETC. */        

@STATUSNAME VARCHAR(20),        /* IN CASE OF BRANCH LOGIN BRANCHCODE */        

@SORTBYDATE VARCHAR(3)          /* WHETHER REPORT IS BASED ON VDT OR EDT */        

        

AS        

    

DECLARE      

@@COSTCODE AS VARCHAR(3)    

    

        

SELECT * INTO  #ACMAST FROM ACMAST WHERE 1 = 2        

      

SELECT CLTCODE,VAMT DRTOT,VAMT CRTOT, VAMT AMOUNT INTO #TEMPLEDGER FROM LEDGER WHERE 1=2        

      

SELECT CLTCODE,VAMT DRTOT,VAMT CRTOT, VAMT AMOUNT,ACNAME,VNO BRANCHCODE, LNO ACCAT  INTO #TEMPLEDGERFINAL FROM LEDGER WHERE 1=2        

      

      

IF UPPER(@STATUSID) = 'BROKER'        

BEGIN    

  IF @BALANCE='NORMAL'        

    IF @SORTBYDATE = 'VDT'        

     BEGIN           

      INSERT INTO #TEMPLEDGER SELECT L.CLTCODE , 0,0,      

      AMOUNT = SUM(CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END)        

      FROM N_LEDGER L WHERE VDT >= @CURRYRSTDATE + ' 00:00:00' AND VDT <=  @VDT + ' 23:59:59'          

      GROUP BY L.CLTCODE        

     END        

    ELSE       

      BEGIN        

       INSERT INTO #TEMPLEDGER        

       SELECT CLTCODE, AMOUNT=SUM(AMOUNT) ,0,0         

       FROM          

        (           

         SELECT L.CLTCODE, AMOUNT = ISNULL(SUM( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END),0)      

         FROM N_LEDGER L WHERE EDT LIKE @CURRYRSTDATE + '%' AND VTYP = 18          

         GROUP BY L.CLTCODE          

      

         UNION ALL          

      

         SELECT L.CLTCODE, AMOUNT = SUM( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END)       

         FROM N_LEDGER  L  WHERE EDT >= @CURRYRSTDATE + ' 00:00:00' AND EDT < @VDT AND VTYP <> 18          

         GROUP BY L.CLTCODE    

      

         UNION ALL          

      

         SELECT L.CLTCODE,AMOUNT = ISNULL(SUM( CASE WHEN UPPER(L.DRCR) = 'C' THEN L.VAMT ELSE -L.VAMT END),0)      

         FROM N_LEDGER L  WHERE L.VDT >=  @CURRYRSTDATE + ' 00:00:00'        

         AND EDT < @CURRYRSTDATE          

         GROUP BY L.CLTCODE           

        ) T          

        GROUP BY CLTCODE           

      END        

   ELSE      

      IF @SORTBYDATE = 'VDT'        

       BEGIN        

        INSERT INTO #TEMPLEDGER        

        SELECT L.CLTCODE ,        

        DRTOT = SUM(CASE WHEN VTYP <> 18 AND VDT >= @STDATE  THEN ( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE 0 END) ELSE 0 END),         

        CRTOT = SUM(CASE WHEN VTYP <> 18 AND VDT >= @STDATE  THEN ( CASE WHEN UPPER(L.DRCR) = 'C' THEN VAMT ELSE 0 END) ELSE 0 END),         

        OPBAL = SUM(CASE WHEN VDT < @STDATE THEN ( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END)        

        FROM N_LEDGER L WHERE  VDT >= @CURRYRSTDATE + ' 00:00:00' AND VDT <= @VDT + ' 23:59:59'         

        GROUP BY L.CLTCODE        

       END        

      ELSE        

       BEGIN        

        INSERT INTO #TEMPLEDGER        

        SELECT L.CLTCODE ,        

        DRTOT = SUM(CASE WHEN VTYP <> 18 AND VDT >= @STDATE    THEN ( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE 0 END) ELSE 0 END),         

        CRTOT = SUM(CASE WHEN VTYP <> 18 AND VDT >= @STDATE   THEN ( CASE WHEN UPPER(L.DRCR) = 'C' THEN VAMT ELSE 0 END) ELSE 0 END),         

        OPBAL = SUM(CASE WHEN VDT < @STDATE THEN ( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END)        

    FROM N_LEDGER L WHERE EDT >= @CURRYRSTDATE + ' 00:00:00' AND EDT <= @VDT + ' 23:59:59'         

        GROUP BY L.CLTCODE        

      END         

      

IF  @VIEWOPTION = 'PARTYWISE'         

 BEGIN        

   INSERT INTO #TEMPLEDGERFINAL SELECT L.*,LEFT(ACNAME,45) ACNAME, BRANCHCODE,ACCAT       

    FROM #TEMPLEDGER L, ACMAST A WHERE L.CLTCODE =A.CLTCODE   AND ACCAT= 4 AND ( DRTOT <> 0 OR CRTOT <> 0 OR  AMOUNT <> 0)      

 END        

ELSE        

 IF @VIEWOPTION = 'GL'        

  BEGIN        

    INSERT INTO #TEMPLEDGERFINAL SELECT L.*,LEFT(ACNAME,45) ACNAME, BRANCHCODE,ACCAT       

     FROM #TEMPLEDGER L, ACMAST A WHERE L.CLTCODE =A.CLTCODE   AND ACCAT <> 4 AND ( DRTOT <> 0  OR CRTOT <> 0 OR  AMOUNT <> 0)      

  END        

 ELSE        

  BEGIN        

    INSERT INTO #TEMPLEDGERFINAL SELECT L.*,LEFT(ACNAME,45) ACNAME, BRANCHCODE,ACCAT       

     FROM #TEMPLEDGER L, ACMAST A WHERE L.CLTCODE =A.CLTCODE  AND ( DRTOT <> 0  OR CRTOT <> 0 OR  AMOUNT <> 0)      

  END       

      

IF @VIEWOPTION = 'GL'        

 BEGIN      

   INSERT INTO #TEMPLEDGERFINAL      

   SELECT 'ZZZZZZZZZZ',SUM(DRTOT),SUM(CRTOT),SUM(AMOUNT),'CLIENT BALANCES',' ',4  FROM #TEMPLEDGER L, ACMAST A WHERE L.CLTCODE =A.CLTCODE  AND ACCAT = 4       

 END      

END     

     

    

/*  -- FOR BRANCH SELECTION --  */      

IF UPPER(@STATUSID) = 'BRANCH'        

BEGIN    

 SELECT @@COSTCODE = (SELECT COSTCODE FROM COSTMAST C, CATEGORY C2 WHERE C2.CATEGORY = 'BRANCH' AND C.CATCODE = C2.CATCODE AND COSTNAME = @STATUSNAME )      

 IF @BALANCE='NORMAL'        

    IF @SORTBYDATE = 'VDT'        

     BEGIN           

      INSERT INTO #TEMPLEDGER SELECT L.CLTCODE , 0,0,      

      AMOUNT = SUM(CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END)        

      FROM N_LEDGER L, LEDGER2 L2 WHERE VDT >= @CURRYRSTDATE + ' 00:00:00' AND VDT <=  @VDT + ' 23:59:59'          

   AND L.VNO = L2.VNO AND L.VTYP = L2.VTYPE AND L.BOOKTYPE = L2.BOOKTYPE AND L.LNO = L2.LNO    

   AND COSTCODE = @@COSTCODE    

      GROUP BY L.CLTCODE        

     END        

    ELSE       

      BEGIN        

       INSERT INTO #TEMPLEDGER        

       SELECT CLTCODE, AMOUNT=SUM(AMOUNT) ,0,0         

       FROM          

        (           

         SELECT L.CLTCODE, AMOUNT = ISNULL(SUM( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END),0)      

         FROM N_LEDGER L, LEDGER2 L2 WHERE EDT LIKE @CURRYRSTDATE + '%' AND VTYP = 18          

      AND L.VNO = L2.VNO AND L.VTYP = L2.VTYPE AND L.BOOKTYPE = L2.BOOKTYPE AND L.LNO = L2.LNO    

         AND COSTCODE = @@COSTCODE    

     GROUP BY L.CLTCODE          

      

         UNION ALL          

      

         SELECT L.CLTCODE, AMOUNT = SUM( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END)       

         FROM N_LEDGER L, LEDGER2 L2 WHERE EDT >= @CURRYRSTDATE + ' 00:00:00' AND EDT < @VDT AND VTYP <> 18          

     AND L.VNO = L2.VNO AND L.VTYP = L2.VTYPE AND L.BOOKTYPE = L2.BOOKTYPE AND L.LNO = L2.LNO    

         AND COSTCODE = @@COSTCODE    

         GROUP BY L.CLTCODE           

      

         UNION ALL          

      

         SELECT L.CLTCODE,AMOUNT = ISNULL(SUM( CASE WHEN UPPER(L.DRCR) = 'C' THEN L.VAMT ELSE -L.VAMT END),0)      

         FROM N_LEDGER L, LEDGER2 L2 WHERE L.VDT >=  @CURRYRSTDATE + ' 00:00:00' AND EDT < @CURRYRSTDATE    

     AND L.VNO = L2.VNO AND L.VTYP = L2.VTYPE AND L.BOOKTYPE = L2.BOOKTYPE AND L.LNO = L2.LNO    

         AND COSTCODE = @@COSTCODE    

         GROUP BY L.CLTCODE           

        ) T          

        GROUP BY CLTCODE           

      END        

   ELSE      

      IF @SORTBYDATE = 'VDT'        

       BEGIN        

        INSERT INTO #TEMPLEDGER        

        SELECT L2.CLTCODE ,        

        DRTOT = SUM(CASE WHEN VTYP <> 18 AND VDT >= @STDATE  THEN ( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE 0 END) ELSE 0 END),         

        CRTOT = SUM(CASE WHEN VTYP <> 18 AND VDT >= @STDATE  THEN ( CASE WHEN UPPER(L.DRCR) = 'C' THEN VAMT ELSE 0 END) ELSE 0 END),         

        OPBAL = SUM(CASE WHEN VDT < @STDATE THEN ( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END)        

        FROM N_LEDGER L, LEDGER2 L2 WHERE  VDT >= @CURRYRSTDATE + ' 00:00:00' AND VDT <= @VDT + ' 23:59:59'    

    AND L.VNO = L2.VNO AND L.VTYP = L2.VTYPE AND L.BOOKTYPE = L2.BOOKTYPE AND L.LNO = L2.LNO    

        AND COSTCODE = @@COSTCODE    

        GROUP BY L2.CLTCODE        

       END        

     ELSE        

       BEGIN        

        INSERT INTO #TEMPLEDGER        

        SELECT L2.CLTCODE ,        

        DRTOT = SUM(CASE WHEN VTYP <> 18 AND VDT >= @STDATE    THEN ( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE 0 END) ELSE 0 END),         

        CRTOT = SUM(CASE WHEN VTYP <> 18 AND VDT >= @STDATE   THEN ( CASE WHEN UPPER(L.DRCR) = 'C' THEN VAMT ELSE 0 END) ELSE 0 END),         

        OPBAL = SUM(CASE WHEN VDT < @STDATE THEN ( CASE WHEN UPPER(L.DRCR) = 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END)        

        FROM N_LEDGER L, LEDGER2 L2 WHERE EDT >= @CURRYRSTDATE + ' 00:00:00' AND EDT <= @VDT + ' 23:59:59'         

    AND L.VNO = L2.VNO AND L.VTYP = L2.VTYPE AND L.BOOKTYPE = L2.BOOKTYPE AND L.LNO = L2.LNO    

        AND COSTCODE = @@COSTCODE    

        GROUP BY L2.CLTCODE        

      END         

      

IF  @VIEWOPTION = 'PARTYWISE'         

 BEGIN        

   INSERT INTO #TEMPLEDGERFINAL SELECT L.*,LEFT(ACNAME,45) ACNAME, BRANCHCODE,ACCAT       

    FROM #TEMPLEDGER L, ACMAST A WHERE L.CLTCODE =A.CLTCODE   AND ACCAT= 4 AND ( DRTOT <> 0 OR CRTOT <> 0 OR  AMOUNT <> 0)      

 END        

ELSE        

 IF @VIEWOPTION = 'GL'        

  BEGIN        

    INSERT INTO #TEMPLEDGERFINAL SELECT L.*,LEFT(ACNAME,45) ACNAME, BRANCHCODE,ACCAT       

     FROM #TEMPLEDGER L, ACMAST A WHERE L.CLTCODE =A.CLTCODE   AND ACCAT <> 4 AND ( DRTOT <> 0  OR CRTOT <> 0 OR  AMOUNT <> 0)      

  END        

 ELSE        

  BEGIN        

    INSERT INTO #TEMPLEDGERFINAL SELECT L.*,LEFT(ACNAME,45) ACNAME, BRANCHCODE,ACCAT       

     FROM #TEMPLEDGER L, ACMAST A WHERE L.CLTCODE =A.CLTCODE  AND ( DRTOT <> 0  OR CRTOT <> 0 OR  AMOUNT <> 0)      

  END       

      

IF @VIEWOPTION = 'GL'        

 BEGIN      

   INSERT INTO #TEMPLEDGERFINAL      

   SELECT 'ZZZZZZZZZZ',SUM(DRTOT),SUM(CRTOT),SUM(AMOUNT),'CLIENT BALANCES',' ',4  FROM #TEMPLEDGER L, ACMAST A WHERE L.CLTCODE =A.CLTCODE  AND ACCAT = 4       

 END      

END     

/* ---------------------------- */      
-------- ADDED FOR 
 SELECT DISTINCT CLTCODE,sum( DRTOT ) AS DRTOT,
 SUM(CRTOT) AS CRTOT,SUM(amount ) AS AMOUNT,ACNAME,BRANCHCODE,ACCAT
 INTO #TEMPLEDGERFINAL1
 FROM 
 (
 SELECT ISNULL(NEWGRP,CLTCODE) CLTCODE ,(DRTOT)DRTOT ,(CRTOT) CRTOT,(AMOUNT) AMOUNT,ISNULL(NEWGRPNAME,ACNAME) ACNAME, 
 ISNULL(BRANCH,BRANCHCODE) AS BRANCHCODE,ISNULL(AC_CAT,ACCAT) AS ACCAT
 FROM 
 (
 SELECT CLTCODE,DRTOT,CRTOT,AMOUNT,ACNAME,BRANCHCODE,ACCAT,
 (CASE WHEN ACCAT <> 4 AND CLTCODE >='05A001' AND CLTCODE<='05ZZZZZZ' THEN LEFT(CLTCODE,2) +' SERIES'
       WHEN ACCAT <> 4 AND CLTCODE >='30A001' AND CLTCODE<='30ZZZZZZ' THEN LEFT(CLTCODE,2) +' SERIES'
        WHEN ACCAT <> 4 AND CLTCODE LIKE '7%' THEN LEFT(CLTCODE,1) +' SERIES'
		WHEN ACCAT <> 4 AND  CLTCODE NOT LIKE '05%' AND CLTCODE NOT LIKE '30%' THEN LEFT(CLTCODE,2) +' SERIES'
		ELSE CLTCODE END ) SERIES_TYPE
 FROM #TEMPLEDGERFINAL )A
 LEFT OUTER JOIN 
 (select *,'HO' as branch,'3' AS AC_CAT FROM Acmapping WHERE Segment ='NSECM')B
  ON (A.SERIES_TYPE=SERIES OR OLDCODE=CLTCODE)
  )A
GROUP BY  CLTCODE, ACNAME,BRANCHCODE,ACCAT  

-----------------


IF @FLAG = 'CODEWISE'        

 BEGIN      

  SELECT * FROM #TEMPLEDGERFINAL1 ORDER BY  CLTCODE, ACNAME, BRANCHCODE      

 END      

ELSE      

 BEGIN      

  SELECT * FROM #TEMPLEDGERFINAL1  ORDER BY  ACNAME,CLTCODE, BRANCHCODE      

 END

GO
