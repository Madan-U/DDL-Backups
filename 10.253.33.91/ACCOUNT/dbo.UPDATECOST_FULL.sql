-- Object: PROCEDURE dbo.UPDATECOST_FULL
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROC UPDATECOST_FULL  
AS  
DECLARE  
	@@BRANCH_CODE AS VARCHAR(10),  
	@@COSTCODE AS VARCHAR(10),  
	@@BRCNT AS INT,  
	@@BRCURS AS CURSOR  
------------------------------------------UPDATING NSE SEGMENT --------------------------------------------------------
SET @@BRCURS = CURSOR FOR  
	SELECT DISTINCT 
		BRANCH_CODE 
	FROM MSAJAG.DBO.BRANCH 
	WHERE BRANCH_CODE NOT IN 
		(
		SELECT DISTINCT 
			COSTNAME 
		FROM ACCOUNT.DBO.COSTMAST
		)
OPEN @@BRCURS  
	FETCH NEXT 
	FROM @@BRCURS 
	INTO @@BRANCH_CODE
WHILE @@FETCH_STATUS = 0  
	BEGIN  
		SELECT 
			@@COSTCODE = CONVERT(VARCHAR(10), ISNULL(MAX(COSTCODE)+1, 1)) 
		FROM ACCOUNT.DBO.COSTMAST 
		INSERT 
		INTO ACCOUNT.DBO.COSTMAST VALUES 
			(
				@@BRANCH_CODE, 
				@@COSTCODE, 
				1, 
				'10000000000'
			) 
			FETCH NEXT 
		FROM @@BRCURS 
		INTO @@BRANCH_CODE
	END  
CLOSE @@BRCURS  
SET @@BRCURS = CURSOR FOR  
	SELECT DISTINCT 
		COSTNAME 
	FROM ACCOUNT.DBO.COSTMAST 
	WHERE COSTNAME NOT IN 
		(
		SELECT DISTINCT 
			BRANCHNAME 
		FROM ACCOUNT.DBO.BRANCHACCOUNTS
		)
OPEN @@BRCURS  
	FETCH NEXT
	FROM @@BRCURS
	INTO @@BRANCH_CODE  
WHILE @@FETCH_STATUS = 0  
	BEGIN  
		INSERT 
		INTO ACCOUNT.DBO.BRANCHACCOUNTS VALUES 
			(
				@@BRANCH_CODE, 
				@@BRANCH_CODE, 
				'HOCTRL', 
				0
			) 
		FETCH NEXT 
		FROM @@BRCURS 
		INTO @@BRANCH_CODE
	END  
CLOSE @@BRCURS  
	INSERT 
	INTO ACCOUNT.DBO.ACMAST 
	SELECT 
		LTRIM(RTRIM(BRANCHNAME)) + ' CONTROL A/C', 
		LTRIM(RTRIM(BRANCHNAME)) + ' CONTROL A/C', 
		'ASSET', 
		'4', 
		'', 
		LTRIM(RTRIM(BRCONTROLAC)), 
		'', 
		'A0000000000', 
		'', 
		'0', 
		LTRIM(RTRIM(BRANCHNAME)), 
		'0', 
		'C', 
		'', 
		'', 
		'' 
	FROM ACCOUNT.DBO.BRANCHACCOUNTS 
	WHERE BRCONTROLAC NOT IN 
		(
		SELECT DISTINCT 
			CLTCODE 
		FROM ACCOUNT.DBO.ACMAST
		)

GO
