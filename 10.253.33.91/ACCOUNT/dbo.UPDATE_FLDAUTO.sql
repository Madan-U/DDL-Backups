--------------------------------------------------

CREATE PROCEDURE UPDATE_FLDAUTO

      @VDT VARCHAR(11)

AS

 

UPDATE V2 SET FINYEAR = P.FLDAUTO FROM V2_LASTVNO V2, PARAMETER P

WHERE VTYP IN(15, 21) AND @VDT BETWEEN SDTCUR AND LDTCUR

GO
