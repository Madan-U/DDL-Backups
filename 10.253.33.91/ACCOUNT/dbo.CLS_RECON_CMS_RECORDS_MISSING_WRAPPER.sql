CREATE PROCEDURE [dbo].[CLS_RECON_CMS_RECORDS_MISSING_WRAPPER]
(
	@VALUEDATE VARCHAR(11)
)

AS
	BEGIN
		CREATE TABLE #REPORTS(VNO NVARCHAR(50)
		,VTYP NVARCHAR(10)
		,BOOKTYPE NVARCHAR(10)
		,AMOUNT DECIMAL(18,2)
		,BBEXCHANGE NVARCHAR(50)
		,BBSEGMENT NVARCHAR(50)
		,BSEXCHANGE NVARCHAR(50)
		,BSSEGMENT NVARCHAR(50)
		,BANKCODE NVARCHAR(50)
		,CLTCODE NVARCHAR(50)
		,DDNO NVARCHAR(50)
		,UPLOADID NVARCHAR(50))

		--SET @VALUEDATE='MAY 29 2018'
		DECLARE  @ALLBANKCODE TABLE(BANKCODE NVARCHAR(70))
		DECLARE @SEGMENTBANKCODE NVARCHAR(50)
		INSERT INTO @ALLBANKCODE
		SELECT DISTINCT BANKCODE FROM RECON_BANK_MASTER_VW   ORDER BY BANKCODE 

		WHILE (SELECT COUNT(*) FROM @ALLBANKCODE)> 0 
		BEGIN	
			SELECT 	TOP 1 @SEGMENTBANKCODE=BANKCODE FROM @ALLBANKCODE
			DECLARE @@SQL NVARCHAR(MAX)
			
			SET @@SQL=""
			SET @@SQL ="INSERT INTO #REPORTS "
			SET @@SQL= @@SQL + "EXEC CLS_RECON_CMS_RECORDS_MISSING '"+@VALUEDATE+"','"+@SEGMENTBANKCODE+"'"
			
			PRINT @@SQL
			EXEC(@@SQL)
			DELETE FROM @ALLBANKCODE WHERE BANKCODE=@SEGMENTBANKCODE

		END
		SELECT * FROM #REPORTS
	 END

GO
