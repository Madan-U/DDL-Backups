CREATE PROC [dbo].[CLS_SP_GET_GRPMAIN_GRPCODE]
(
	@GRPMAIN VARCHAR(13)
)

AS
BEGIN
--SELECT * FROM .DBO.[CLS_GET_GRPMAIN_GRPCODE](@GRPMAIN)

SELECT GROUP_NAME = GRPNAME,MAIN_GROUP = GRPMAIN, GROUP_CODE = GRPCODE INTO #TEMP FROM GRPMAST WHERE GRPMAIN LIKE @GRPMAIN + '%'
--SELECT GROUP_NAME = GRPNAME,MAIN_GROUP = GRPMAIN, GROUP_CODE = GRPCODE FROM GRPMAST WHERE GRPMAIN LIKE @GRPMAIN + '%'
UPDATE #TEMP SET MAIN_GROUP = '' WHERE GROUP_NAME IN ('INCOME','EXPENSES','ASSETS','LIABILITIES')
SELECT * FROM #TEMP 

ORDER BY 2 
END

GO
