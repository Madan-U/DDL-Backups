/* EXEC BENTOCOLL 'FEB 15 2015','FEB 15 2015'*/    
    
CREATE PROCEDURE [dbo].[BENTOCOLL]      
  @FROMDATE VARCHAR(11),          
  @TODATE VARCHAR(11),    
  @STATUS VARCHAR(11)    
AS    
IF @STATUS = 'D'     
BEGIN    
SELECT PARTY_CODE,SCRIP_CD,SERIES,ISIN,QTY,DPID,CLTDPID,BDPID,BCLTDPID,PROCESS_STATUS,FILEDATE,INSTTYPE,PROCESS_DATE,PROCESSEDQTY,(QTY - PROCESSEDQTY) AS FINALQTY,'BSE' AS EXCHANGE   
FROM [AngelDemat].BSEDB.DBO.TBL_BENTOCOLL_FINAL     
GROUP BY PARTY_CODE,SCRIP_CD,SERIES,ISIN,QTY,DPID,CLTDPID,BDPID,BCLTDPID,PROCESS_STATUS,FILEDATE,INSTTYPE,PROCESS_DATE,PROCESSEDQTY  
HAVING PROCESS_DATE >= @FROMDATE AND PROCESS_DATE < =@TODATE + ' 23:59:59'   
      
UNION       
SELECT PARTY_CODE,SCRIP_CD,SERIES,ISIN,QTY,DPID,CLTDPID,BDPID,BCLTDPID,PROCESS_STATUS,FILEDATE,INSTTYPE,PROCESS_DATE,PROCESSEDQTY,(QTY - PROCESSEDQTY) AS FINALQTY, 'NSE' AS EXCHANGE   
FROM [AngelDemat].MSAJAG.DBO.TBL_BENTOCOLL_FINAL     
GROUP BY PARTY_CODE,SCRIP_CD,SERIES,ISIN,QTY,DPID,CLTDPID,BDPID,BCLTDPID,PROCESS_STATUS,FILEDATE,INSTTYPE,PROCESS_DATE,PROCESSEDQTY  
HAVING PROCESS_DATE >= @FROMDATE AND PROCESS_DATE < =@TODATE + ' 23:59:59'   
ORDER BY  EXCHANGE    
END    
IF @STATUS = 'P'    
BEGIN    
SELECT SNO,PARTY_CODE,SCRIP_CD,SERIES,ISIN,QTY,DPID,CLTDPID,BDPID,BCLTDPID,PROCESS_STATUS,PROCESS_DATE,INSTTYPE,'BSE' AS EXCHANGE   
FROM [AngelDemat].BSEDB.DBO.TBL_BENTOCOLL    
WHERE PROCESS_DATE >= @FROMDATE AND PROCESS_DATE < =@TODATE + ' 23:59:59'   
UNION       
SELECT SNO,PARTY_CODE,SCRIP_CD,SERIES,ISIN,QTY,DPID,CLTDPID,BDPID,BCLTDPID,PROCESS_STATUS,PROCESS_DATE,INSTTYPE,'NSE' AS EXCHANGE   
FROM [AngelDemat].MSAJAG.DBO.TBL_BENTOCOLL    
WHERE PROCESS_DATE >= @FROMDATE AND PROCESS_DATE < =@TODATE + ' 23:59:59'   
ORDER BY  EXCHANGE    
END

GO
