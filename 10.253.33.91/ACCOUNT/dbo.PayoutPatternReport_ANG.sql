-- Object: PROCEDURE dbo.PayoutPatternReport_ANG
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

  
CREATE PROC [dbo].[PayoutPatternReport_ANG] (@PAYOUTDATE VARCHAR(11))  
  
AS  
  
IF (SELECT COUNT(1) FROM  MSAJAG.DBO.TBL_COMBINE_REPORTING_DETAIL WITH (NOLOCK)   WHERE EXCHANGE = 'MCX' AND MARGINDATE = @PAYOUTDATE) = 0  
BEGIN   
 SELECT 'NO MARGIN DATA '  
 RETURN  
END  
  
  
SELECT A.CLTCODE,SUM(VAMT) AS VAMT,VTYP,VDT   
INTO #PAYOUT  
FROM LEDGER A WITH (NOLOCK), ACMAST B WITH (NOLOCK) WHERE  VDT BETWEEN @PAYOUTDATE AND @PAYOUTDATE + ' 23:59'   
AND A.CLTCODE = B.CLTCODE AND A.CLTCODE BETWEEN 'A000000000' AND 'ZZZZZZZZZZ' AND VTYP = 3 AND ACCAT = 4   
GROUP BY A.CLTCODE,VTYP,VDT   

CREATE INDEX CLIIDX ON #PAYOUT (CLTCODE)
  
SELECT PARTY_CODE,SUM(TDAY_MARGIN) AS INI_MARGIN INTO #MARGIN   
FROM MSAJAG.DBO.TBL_COMBINE_REPORTING_DETAIL WITH (NOLOCK)  WHERE  MARGINDATE = @PAYOUTDATE --AND EXCHANGE = 'NSX'  
GROUP BY PARTY_CODE  
HAVING SUM(TDAY_MARGIN) <> 0   

DELETE #MARGIN WHERE PARTY_CODE NOT IN (SELECT DISTINCT CLTCODE  FROM #PAYOUT )
  
  
SELECT A.CLTCODE,A.VAMT,A.VTYP,A.VDT   
INTO #PAYOUT_180  
FROM LEDGER A WITH (NOLOCK), #PAYOUT B WITH (NOLOCK) WHERE  A.VDT BETWEEN CONVERT(DATETIME,@PAYOUTDATE) - 180 AND @PAYOUTDATE + ' 23:59'  
AND A.CLTCODE = B.CLTCODE  AND  A.VTYP = B.VTYP   
  
SELECT CLTCODE,SUM(VAMT) AS VAMT,COUNT(VDT) AS COUNT_PAY INTO #PAYOUT_180_FINAL  FROM #PAYOUT_180 GROUP BY CLTCODE  
  
  
SELECT B.PARTY_CODE,B.SUB_BROKER,A.B2C,C.VDT,C.CLTCODE,C.VAMT AS PAYOUT_AMT  
,D.INI_MARGIN,E.VAMT AS PAYOUT_AMT_6MON,E.COUNT_PAY,PERC =  CONVERT(NUMERIC(18,2),(C.VAMT / D.INI_MARGIN) * 100)  
INTO #FINAL_OUT  
FROM INTRANET.RISK.DBO.CLIENT_DETAILS A WITH (NOLOCK), MSAJAG.DBO.CLIENT_DETAILS B WITH (NOLOCK),  
#PAYOUT C , #MARGIN D , #PAYOUT_180_FINAL E  
WHERE A.B2C='N' AND A.PARTY_CODE = B.PARTY_CODE AND A.PARTY_CODE = C.CLTCODE AND A.PARTY_CODE = D.PARTY_CODE  
AND A.PARTY_CODE = E.CLTCODE  
  
DELETE FROM #FINAL_OUT WHERE PAYOUT_AMT < 3000    
  
DELETE FROM #FINAL_OUT WHERE PERC < 1    
  
DELETE FROM #FINAL_OUT WHERE PERC > 18    
  
DELETE FROM #FINAL_OUT WHERE LEFT(SUB_BROKER,2) = 'CB'  
  
SELECT   
SUB_BROKER AS [AP Tag],CLTCODE AS [Client Code],format(VDT,'dd/MM/yyyy') AS [Date],  
INI_MARGIN AS [Initial Margin],  
PAYOUT_AMT AS [PayOut Amount],  
COUNT_PAY AS [Number of payouts in last 6 months],  
PAYOUT_AMT_6MON AS [Total payout Amount in last 6 months],  
PERC AS [Percent = Payout Amount / Initial Margin],  
CASE WHEN PERC BETWEEN 1 AND 3 THEN '1% - 3%'   
            WHEN PERC BETWEEN 3.01 AND 6 THEN '3% - 6%'  
            WHEN PERC BETWEEN 6.01 AND 18 THEN '6% - 18%' END  
AS Category  FROM #FINAL_OUT ORDER BY PERC  
  
DROP TABLE #PAYOUT  
DROP TABLE #PAYOUT_180  
DROP TABLE #PAYOUT_180_FINAL  
DROP TABLE #MARGIN  
DROP TABLE #FINAL_OUT

GO
