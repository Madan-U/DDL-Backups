-- Object: PROCEDURE dbo.RPT_SFCM_DRCR_DIFF
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

 CREATE PROC RPT_SFCM_DRCR_DIFF (@TDATE VARCHAR(11))
 
 AS  --- EXEC RPT_SFCM_DRCR_DIFF 'APR 27 2021'
 
 DECLARE @RUNTIEMT VARCHAR(10),@RUNTIEMP VARCHAR(10)
 
 SELECT @RUNTIEMT = CONVERT(VARCHAR(10), CAST(MAX(RUNDATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING
 
 SELECT @RUNTIEMP = CONVERT(VARCHAR(10), CAST(MAX(LOG_DATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING_LOG
              
 SELECT CLTCODE,REPORT_DATE,SUM(BALANCE) AS BALANCE  INTO #SFCMT FROM SFCM_REPORTING A,MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)     
 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,REPORT_DATE
 
 SELECT CLTCODE,REPORT_DATE,SUM(BALANCE) AS BALANCE  INTO #SFCMP FROM SFCM_REPORTING_LOG A,MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)
 WHERE CLTCODE BETWEEN 'A' AND 'ZZZZ9999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE GROUP BY CLTCODE,REPORT_DATE
 
SELECT REPORT='Creditors for the Day',REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),RUNTIME=@RUNTIEMT,SUM(BALANCE) as BALANCE 
FROM #SFCMT WHERE BALANCE >0  AND CLTCODE NOT LIKE '%ERROR%' GROUP BY CONVERT(VARCHAR(11),REPORT_DATE,105)    
 UNION ALL          
SELECT REPORT='Debtors for the Day',REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),RUNTIME=@RUNTIEMT,SUM(BALANCE) as BALANCE 
FROM #SFCMT WHERE BALANCE <0   AND CLTCODE NOT LIKE '%ERROR%' GROUP BY CONVERT(VARCHAR(11),REPORT_DATE,105)
 UNION ALL
SELECT REPORT='Creditors as on Previous Run',REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),RUNTIME=@RUNTIEMP,SUM(BALANCE) as BALANCE 
FROM #SFCMP WHERE BALANCE >0  AND CLTCODE NOT LIKE '%ERROR%'  GROUP BY CONVERT(VARCHAR(11),REPORT_DATE,105)        
 UNION ALL          
SELECT REPORT='Debtors as on Previous Run',REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),RUNTIME=@RUNTIEMP,SUM(BALANCE) as BALANCE 
FROM #SFCMP WHERE BALANCE <0   AND CLTCODE NOT LIKE '%ERROR%' GROUP BY CONVERT(VARCHAR(11),REPORT_DATE,105)
 UNION ALL
SELECT 'Difference in Creditors','','',
abs((SELECT SUM(BALANCE) AS BALANCE FROM #SFCMT  WHERE BALANCE > 0) - (SELECT SUM(BALANCE) AS BALANCE FROM #SFCMP  WHERE BALANCE > 0))
UNION ALL
SELECT 'Difference in Debtors','','',
abs((SELECT SUM(BALANCE) AS BALANCE FROM #SFCMT  WHERE BALANCE < 0) - (SELECT SUM(BALANCE) AS BALANCE FROM #SFCMP  WHERE BALANCE < 0))

GO
