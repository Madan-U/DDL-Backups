CREATE PROCEDURE [dbo].[GET_CALENDER_DAYS]  
(  
 @VDT VARCHAR(11)  
)  
AS  
  
CREATE TABLE #SETT_CALENDER  
(  
 VDT DATETIME,  
 NODAYS INT,  
 SNO INT IDENTITY(1, 1)  
)  
  
IF LEN(@VDT) = 11  
BEGIN  
 SELECT @VDT = CONVERT(VARCHAR(10), CONVERT(DATETIME, @VDT, 109), 103)   
END  
  
INSERT INTO #SETT_CALENDER(VDT)  
SELECT START_DATE FROM MSAJAG.DBO.SETT_MST WHERE SETT_TYPE = 'N' AND START_DATE <= CONVERT(DATETIME, @VDT, 103) ORDER BY START_DATE DESC  
--SELECT VDT FROM BillPosted WHERE Sett_Type = 'N' AND VDT <= CONVERT(DATETIME, @VDT, 103) ORDER BY vdt DESC  
UPDATE #SETT_CALENDER SET NODAYS = SNO  
  
  
CREATE TABLE #MISSING_DATES  
(  
 VDT DATETIME,  
 NODAYS INT,  
 SNO INT IDENTITY(1, 1)  
);  
  
  
WITH Dates AS (  
        SELECT CONVERT(DATETIME, dateadd(yy, -1, CONVERT(DATETIME, @VDT, 103))) as [Date]  
        UNION ALL   
        SELECT DATEADD(DAY, 1, [Date])  
        FROM Dates  
        where Date < CONVERT(DATETIME, @VDT, 103)  
  
)   
  
INSERT INTO #MISSING_DATES(VDT)  
SELECT [Date]  
FROM Dates  
ORDER BY [Date] DESC  
OPTION (MAXRECURSION 400)  
  
  
  
INSERT INTO #SETT_CALENDER(VDT)  
SELECT VDT FROM #MISSING_DATES M  
WHERE NOT EXISTS(SELECT VDT FROM #SETT_CALENDER S WHERE S.VDT = M.VDT) order by vdt desc  
TRUNCATE TABLE #MISSING_DATES  
  
INSERT INTO #MISSING_DATES  
SELECT VDT, NODAYS FROM #SETT_CALENDER  
ORDER BY VDT DESC  
  
  
/*UPDATE M SET M.NODAYS = S.NODAYS FROM #MISSING_DATES M, #SETT_CALENDER S  
WHERE M.VDT = S.VDT*/  
  
  
  
  
UPDATE S SET   
NODAYS = (SELECT MAX(NODAYS) FROM #MISSING_DATES S1 WHERE S.SNO > S1.SNO)  
FROM #MISSING_DATES S WHERE S.NODAYS IS NULL  
  
SELECT * FROM #MISSING_DATES WHERE ISNULL(NODAYS, 0) > 0 ORDER BY VDT DESC

GO
