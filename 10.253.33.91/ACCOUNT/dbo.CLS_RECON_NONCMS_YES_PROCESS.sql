CREATE PROCEDURE [dbo].[CLS_RECON_NONCMS_YES_PROCESS]
(
	@UPLOADID NVARCHAR(50),
	@BANKACCOUNT varchar(50), 
	@BANKCODE NVARCHAR(50) ,
	@UPLOADFILENAME varchar(500),
	@UNAME varchar(50)
) 
AS
BEGIN
	BEGIN TRY
		
		DECLARE @D VARCHAR(50),@VALUEDATE VARCHAR(11)
		SELECT  @D=.DBO.PIECE(@UPLOADFILENAME,'_',2)


		SELECT @VALUEDATE= STUFF(STUFF( @D, 5, 0, '/'), 3, 0, '/')-- DD/MM/YYYY
		IF CHARINDEX('.CSV',UPPER(@UPLOADFILENAME))>1
		SELECT @VALUEDATE=REPLACE(@VALUEDATE,'.','')

		IF OBJECT_ID('tempdb..#TEMP_YESBANK') IS NOT NULL
		DROP TABLE #TEMP_YESBANK

		SELECT * INTO #TEMP_YESBANK FROM MSAJAG..FILEDATA (NOLOCK)
		WHERE PROGID=@UPLOADID

		CREATE NONCLUSTERED INDEX IXDPROGID ON #TEMP_YESBANK(PROGID)	


        INSERT INTO #NONCMS_DATA 
		SELECT
		  UPLOADID= @UPLOADID		
		, BANKACCOUNT = @BANKACCOUNT
		, BANKCODE = @BANKCODE
		, DESCRIPTION = .DBO.PIECE(FILE_DATA,',',4)
		, LEDGERBALANCE = ''
		, DRCR = .DBO.PIECE(FILE_DATA,',',7)
		, AMOUNT = ABS(CAST(.DBO.PIECE(FILE_DATA,',',8) AS MONEY))
		, VALUEDATE = CONVERT(DATETIME,@VALUEDATE,103)  
		, REFERENCENO = .DBO.PIECE(FILE_DATA,',',6)		
		, CLTACCOUNT = ''
		, CUSTOMERREFERENCE = ''  
		, TRANSACTIONDETAIL = ''  
		, VNO =''
		, VTYP =''
		, BOOKTYPE= ''
		, MATCHEDFLAG= 0
		, UPLOADSTATUS = 'PENDING'
		, FILEUPLOAD_SDATE= GETDATE()
		, GETDATE()
		, UPLOADBY = @UNAME				
		FROM #TEMP_YESBANK
		WHERE  ISNULL(.DBO.PIECE(FILE_DATA,',',5),'') <> ''
		AND ISNULL(.DBO.PIECE(FILE_DATA,',',5),'') <> 'VALUE_DATE'
			
		DROP TABLE #TEMP_YESBANK
		DELETE MSAJAG..FILEDATA WHERE PROGID=@UPLOADID

	END TRY

	BEGIN CATCH

		DELETE FROM MSAJAG..FILEDATA  WHERE PROGID=@UPLOADID
		DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
		DELETE  FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
		SELECT MSG='WRONG FILE UPLOADED : '+ERROR_MESSAGE()
		RETURN

	END CATCH
END

GO
