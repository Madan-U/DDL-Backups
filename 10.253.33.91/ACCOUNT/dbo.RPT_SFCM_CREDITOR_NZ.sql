-- Object: PROCEDURE dbo.RPT_SFCM_CREDITOR_NZ
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------




 CREATE PROC [dbo].[RPT_SFCM_CREDITOR_NZ] (@TDATE DATETIME)
 
 AS  --- EXEC RPT_SFCM_CREDITOR_NZ 'APR 28 2021'
 
 DECLARE @RUNTIEM VARCHAR(10)
 
TRUNCATE TABLE SFCMCR

IF EXISTS(SELECT 1 FROM ANAND1.ACCOUNT.DBO.SFCM_REPORTING A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59')
BEGIN 

SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(RUNDATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING

 INSERT INTO SFCMCR
 SELECT REPORT_DATE,CLTCODE,CLIENT_NAME=M.LONG_NAME,PANNO=M.PAN_GIR_NO,SUM(BALANCE) AS BALANCE,RUNTIME=@RUNTIEM  
 FROM SFCM_REPORTING A WITH (NOLOCK),MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)        
 WHERE CLTCODE BETWEEN 'N' AND 'ZZZZZZ9999999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE 
 GROUP BY CLTCODE,REPORT_DATE,M.LONG_NAME,M.PAN_GIR_NO
END

IF EXISTS(SELECT 1 FROM ANAND1.ACCOUNT.DBO.SFCM_REPORTING_LOG A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59')
BEGIN

SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(LOG_DATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING_LOG

 INSERT INTO SFCMCR
 SELECT REPORT_DATE,CLTCODE,CLIENT_NAME=M.LONG_NAME,PANNO=M.PAN_GIR_NO,SUM(BALANCE) AS BALANCE,RUNTIME=@RUNTIEM  
 FROM ANAND1.ACCOUNT.DBO.SFCM_REPORTING_LOG A WITH (NOLOCK),MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)        
 WHERE CLTCODE BETWEEN 'N' AND 'ZZZZZZ9999999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE 
 GROUP BY CLTCODE,REPORT_DATE,M.LONG_NAME,M.PAN_GIR_NO
END

IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SFCM_REPORTING_HISTORY A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59')
BEGIN
SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(HIS_DATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING_HISTORY 
WHERE REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59'

 INSERT INTO SFCMCR
 SELECT REPORT_DATE,CLTCODE,CLIENT_NAME=M.LONG_NAME,PANNO=M.PAN_GIR_NO,SUM(BALANCE) AS BALANCE,RUNTIME=@RUNTIEM  
 FROM ACCOUNT.DBO.SFCM_REPORTING_HISTORY A WITH (NOLOCK),MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)        
 WHERE CLTCODE BETWEEN 'N' AND 'ZZZZZZ9999999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE 
 GROUP BY CLTCODE,REPORT_DATE,M.LONG_NAME,M.PAN_GIR_NO
END
               
SELECT REPORT_DATE='REPORT_DATE',CLTCODE='CLTCODE',CLIENT_NAME='CLIENT_NAME',PANNO='PANNO',BALANCE='BALANCE',REPORT='REPORT',RUNTIME='RUNTIME'
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE=LTRIM(RTRIM(CLTCODE)),CLIENT_NAME=REPLACE(CLIENT_NAME,',',''),PANNO=REPLACE(PANNO,',',''),
CONVERT(VARCHAR,SUM(BALANCE)) AS BALANCE,REPORT_DATE='CR',RUNTIME
FROM SFCMCR WHERE BALANCE >0  
GROUP BY REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,RUNTIME
ORDER BY BALANCE DESC

GO
