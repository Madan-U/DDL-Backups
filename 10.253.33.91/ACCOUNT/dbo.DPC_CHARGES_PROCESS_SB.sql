-- Object: PROCEDURE dbo.DPC_CHARGES_PROCESS_SB
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

CREATE PROCEDURE [dbo].[DPC_CHARGES_PROCESS_SB]      
(      
 @FROMDATE VARCHAR(11),      
 @TODATE VARCHAR(11),      
 @FROMSB VARCHAR(25)='0',      
 @TOSB VARCHAR(25)='ZZZZ',      
 @FROM_BR VARCHAR(25)='0',      
 @TO_BR VARCHAR(25)='ZZZZ',      
 --@BALTYPE VARCHAR(1),      
 --@BALON VARCHAR(1),      
 --@ONLYTRAN VARCHAR(1),      
 --@ACCAT VARCHAR(10),      
 @STATUSID VARCHAR(25),      
 @STATUSNAME VARCHAR(25),      
 @EXCHANGE VARCHAR(10),      
 @SEGMENT VARCHAR(10)      
)      
      
AS      
/*      
 EXEC DPC_CHARGES_PROCESS_SB 'MAR  1 2013','mar  8 2013','ALC','ALC','XS','XS','BROKER','BROKER','NSE','CAPITAL'      
*/      
BEGIN      
CREATE TABLE #DPC_CHARGES_SB      
(      
SUBBROKER_CODE VARCHAR(15),      
BRANCH_CD VARCHAR(30),    
BALANCE_DATE   DATETIME,      
BROKERAGE_LEDGER MONEY DEFAULT  0,      
CASH_DEPOSIT MONEY DEFAULT  0,      
CL_NET_DR_WO_PENAL MONEY DEFAULT 0,      
CL_NET_DR_WO_PENAL_EXCEPTIONAL MONEY DEFAULT 0,      
NET_SB_CR MONEY DEFAULT 0,      
CL_NET_DR_WITH_PENAL MONEY DEFAULT 0,      
INTEREST_AMT MONEY DEFAULT 0      
)      
      
      
CREATE TABLE #LEDGERTEMP      
(      
 CLTCODE VARCHAR(30),    
 BRANCH_CD VARCHAR(30),      
 BALANCE MONEY DEFAULT 0,      
 RUNNING_BALANCE MONEY DEFAULT 0,      
 VDT DATETIME,      
 EXCHANGE VARCHAR(15),      
 SEGMENT VARCHAR(15),      
)      
    
 /****ENTRIES FOR DPC_CHARGES***/     
INSERT INTO #DPC_CHARGES_SB(SUBBROKER_CODE,BRANCH_CD,BALANCE_DATE ,CL_NET_DR_WO_PENAL,CL_NET_DR_WITH_PENAL)      
SELECT       
 SUB_BROKER,     
 BRANCH_CD,     
 DPC_DATE,      
 CASE WHEN DPC_BALANCE < 1000 THEN DPC_BALANCE ELSE 0 END,      
 CASE WHEN DPC_BALANCE > 1000 THEN DPC_BALANCE ELSE 0 END      
FROM        
 DPC_CHARGES D    
WHERE      
 DPC_DATE >= @FROMDATE       
 AND DPC_DATE  <= @TODATE +' 23:59'       
 AND BRANCH_CD >= @FROM_BR  
 AND BRANCH_CD <=  @TO_BR  
 AND SUB_BROKER >= @FROMSB  
 AND SUB_BROKER <= @TOSB  
   
       
 /***************************/    
/*********ENTRIES FROM LEDGER************/    
 INSERT INTO #LEDGERTEMP      
 SELECT S.SUB_BROKER,BRANCH,BALANCE,RUNNING_BALANCE=0,BALANSON_D,EXCHANGE,SEGMENT FROM .dbo.BBO_SETT_DAILYBALANCE_NONCLIENTS(@FROMDATE, @TODATE, @FROMSB, @TOSB, 'C', 'V', '', '', @STATUSID, @STATUSNAME,@EXCHANGE,@SEGMENT) A,  MSAJAG..SUBBROKERS S       
 WHERE BRANCH >=@FROM_BR AND BRANCH <=@TO_BR AND CLTCODE LIKE '05%' AND A.CLTCODE = '05'+S.SUB_BROKER      
 AND EXISTS (SELECT SUB_BROKER FROM #DPC_CHARGES_SB D WHERE '05'+SUB_BROKER = A.CLTCODE)    
 /***************************/    
     
 /**********MISSING ENTRIES INSERTION*****************/     
 INSERT INTO  #LEDGERTEMP      
 SELECT       
  A.CLTCODE,BRANCH_CD,BALANCE=0,RUNNING_BALANCE=0,VDT = DATES ,EXCHANGE,SEGMENT       
 FROM       
  DATESERIES(@FROMDATE,@TODATE) D ,#LEDGERTEMP A      
 WHERE NOT EXISTS(SELECT CLTCODE FROM #LEDGERTEMP  L      
         WHERE  L.CLTCODE = A.CLTCODE       
         AND L.VDT = CONVERT(VARCHAR(11),D.DATES,109)      
         )      
       
 GROUP BY A.CLTCODE,BRANCH_CD,DATES,EXCHANGE,SEGMENT       
UPDATE #LEDGERTEMP      
SET RUNNING_BALANCE = (SELECT SUM(BALANCE) FROM  #LEDGERTEMP  B      
WHERE A.CLTCODE = B.CLTCODE       
         AND B.[VDT] <= A.[VDT])      
         FROM  #LEDGERTEMP A      
      
    
INSERT INTO #DPC_CHARGES_SB(SUBBROKER_CODE,BRANCH_CD,BALANCE_DATE,CASH_DEPOSIT)      
SELECT CLTCODE,BRANCH_CD,VDT,RUNNING_BALANCE FROM   #LEDGERTEMP  ORDER BY CLTCODE,VDT       
/*****************************************/      
/*********ENTERIES FOR BROKERAGE LEDGER****************/    
      
INSERT INTO #DPC_CHARGES_SB(SUBBROKER_CODE,BRANCH_CD,BROKERAGE_LEDGER,BALANCE_DATE)    
SELECT     
 SUB_BROKER,BRANCH_CD,SB_LEDGER = SUM(SB_LEDGER),  BALON = CONVERT(DATETIME,CONVERT(VARCHAR(11),UPDATED_ON))    
FROM     
 [CSOKYC-6].HISTORY.DBO.MKT_SB_LEDBAL M    
WHERE    
 UPDATED_ON > = @FROMDATE AND UPDATED_ON <= @TODATE +' 23:59'   
 AND BRANCH_CD > = @FROM_BR AND BRANCH_CD <= @TO_BR  
 AND SUB_BROKER >= @FROMSB  
 AND SUB_BROKER <= @TOSB  
 AND EXISTS(SELECT SUB_BROKER FROM #DPC_CHARGES_SB D WHERE M.SUB_BROKER = D.SUBBROKER_CODE)    
GROUP BY     
 BRANCH_CD,SUB_BROKER,CONVERT(DATETIME,CONVERT(VARCHAR(11),UPDATED_ON)) ORDER BY SUB_BROKER,CONVERT(DATETIME,CONVERT(VARCHAR(11),UPDATED_ON)) ASC    
/************************************************/    
      
/*********************INTEREST CALCULATION********************************/      
  truncate table DPC_CHARGES_SB
  
INSERT INTO DPC_CHARGES_SB  
SELECT       
 SUBBROKER_CODE,    
 BRANCH_CD,    
 BALANCE_DATE,      
 BROKERAGE_LEDGER = SUM(BROKERAGE_LEDGER),      
 CASH_DEPOSIT = SUM(CASH_DEPOSIT),      
 CL_NET_DR_WO_PENAL=SUM(CL_NET_DR_WO_PENAL),      
 CL_NET_DR_WO_PENAL_EXCEPTIONAL =SUM(CL_NET_DR_WO_PENAL_EXCEPTIONAL),      
 NET_SB_CR =CASE WHEN SUM((BROKERAGE_LEDGER+CASH_DEPOSIT) - (CL_NET_DR_WO_PENAL+CL_NET_DR_WO_PENAL_EXCEPTIONAL))<SUM(CL_NET_DR_WITH_PENAL) then       
    SUM((BROKERAGE_LEDGER+CASH_DEPOSIT) - (CL_NET_DR_WO_PENAL+CL_NET_DR_WO_PENAL_EXCEPTIONAL)) else      
     SUM(CL_NET_DR_WITH_PENAL) end,      
 CL_NET_DR_WITH_PENAL = SUM(CL_NET_DR_WITH_PENAL),      
 INTEREST_AMT =CASE WHEN SUM((BROKERAGE_LEDGER+CASH_DEPOSIT) - (CL_NET_DR_WO_PENAL+CL_NET_DR_WO_PENAL_EXCEPTIONAL))<SUM(CL_NET_DR_WITH_PENAL) then       
    (SUM((BROKERAGE_LEDGER+CASH_DEPOSIT) - (CL_NET_DR_WO_PENAL+CL_NET_DR_WO_PENAL_EXCEPTIONAL))*0.12)/365 else      
     (SUM(CL_NET_DR_WITH_PENAL)*0.12)/365 end      
FROM       
 #DPC_CHARGES_SB      
GROUP BY      
 BRANCH_CD,SUBBROKER_CODE,BALANCE_DATE      
ORDER BY      
 BRANCH_CD,SUBBROKER_CODE,BALANCE_DATE     
   
  SELECT * FROM DPC_CHARGES_SB  
    
    
/**********************************************/       
    
END

GO
