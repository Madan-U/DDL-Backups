CREATE PROC  [dbo].[CLS_GENMULTI_VNO]
(
	@SESSION_ID VARCHAR(500)
)
AS
/*
ALTER TABLE CLS_TBL_VNOGENDATA
ALTER COLUMN SESSIONID VARCHAR(500)


UPDATE CLS_TBL_VNOGENDATA
SET 
GROUP_NO = 1
,set_sno = 7
WHERE 
SRNO = '551'
SELECT * FROM CLS_TBL_VNOGENDATA WHERE SESSIONID = '72FAF0E6-174B-4563-BCA4-46C4F89ACBBA'
BEGIN TRAN
EXEC CLS_GENMULTI_VNO @SESSION_ID = '72FAF0E6-174B-4563-BCA4-46C4F89ACBBA'
ROLLBACK



BEGIN TRAN
EXEC CLS_ACC_GENVNO  'MAR 11 2015','3','01','APR 01 2014','MAR 31 2015',1
ROLLBACK
*/
BEGIN
	
	SELECT * INTO #TEMP_GENVNO_DATA 
	FROM CLS_TBL_VNOGENDATA 
	WHERE SESSIONID = @SESSION_ID
	ORDER BY VDT,VTYPE,BOOKTYPE,SET_SNO
	 
	UPDATE T
	SET
		VNOFLAG = P.VNOFLAG
		,SDTCUR = P.SDTCUR
		,LDTCUR = P.LDTCUR
	FROM
	#TEMP_GENVNO_DATA T , PARAMETER P
	WHERE SESSIONID= @SESSION_ID AND CONVERT(DATETIME,T.VDT,103) BETWEEN P.SDTCUR AND P.LDTCUR

	/*********** SETTING UP LINE NO.  *******************/
	
	UPDATE C SET 
	SET_SNO = D.SET_SNO
	FROM #TEMP_GENVNO_DATA C,(SELECT SRNO,SET_SNO = 
										DENSE_RANK() OVER(PARTITION BY 
										CASE WHEN VNOFLAG = 0 OR  VNOFLAG = 1 OR   VNOFLAG = 2 THEN SDTCUR ELSE '' END,
										CASE WHEN VNOFLAG = 1 THEN DATEPART(DD,CONVERT(DATETIME,VDT,103)) ELSE '' END,
										CASE WHEN VNOFLAG = 2 OR  VNOFLAG = 1 THEN DATEPART(MM,CONVERT(DATETIME,VDT,103)) ELSE '' END,
										VTYPE,BOOKTYPE,GROUP_NO ORDER BY 
										SET_SNO)
										
	FROM #TEMP_GENVNO_DATA 
	)D WHERE C.SET_SNO IS NOT NULL AND C.SRNO = D.SRNO
	
		
	UPDATE C SET 
	SET_SNO = D.SET_SNO,
	GROUP_NO = D.GROUP_NO /*Setting group no using dense rank*/
	FROM #TEMP_GENVNO_DATA C,(SELECT SRNO,SET_SNO = 
										ROW_NUMBER() OVER(PARTITION BY 
										CASE WHEN VNOFLAG = 0 OR  VNOFLAG = 1 OR   VNOFLAG = 2 THEN SDTCUR ELSE '' END,
										CASE WHEN VNOFLAG = 1 THEN DATEPART(DD,CONVERT(DATETIME,VDT,103)) ELSE '' END,
										CASE WHEN VNOFLAG = 2 OR  VNOFLAG = 1 THEN DATEPART(MM,CONVERT(DATETIME,VDT,103)) ELSE '' END,
										VTYPE,BOOKTYPE,GROUP_NO ORDER BY 
										CASE WHEN VNOFLAG = 0 OR  VNOFLAG = 1 OR   VNOFLAG = 2 THEN SDTCUR ELSE '' END,
										CASE WHEN VNOFLAG = 1  THEN DATEPART(DD,CONVERT(DATETIME,VDT,103)) ELSE '' END,
										CASE WHEN VNOFLAG = 2 OR  VNOFLAG = 1 THEN DATEPART(MM,CONVERT(DATETIME,VDT,103)) ELSE '' END,
										VTYPE,BOOKTYPE,GROUP_NO),
									GROUP_NO = DENSE_RANK() OVER (ORDER BY 
										CASE WHEN VNOFLAG = 0 OR  VNOFLAG = 1 OR   VNOFLAG = 2 THEN SDTCUR ELSE '' END,
										CASE WHEN VNOFLAG = 1  THEN DATEPART(DD,CONVERT(DATETIME,VDT,103)) ELSE '' END,
										CASE WHEN VNOFLAG = 2 OR  VNOFLAG = 1 THEN DATEPART(MM,CONVERT(DATETIME,VDT,103)) ELSE '' END,
										VTYPE,BOOKTYPE)
										
	FROM #TEMP_GENVNO_DATA 
	)D WHERE C.SET_SNO IS NULL AND C.SRNO = D.SRNO
	/*******************************************/

DECLARE MULTIVNO_CURSOR CURSOR FOR
	SELECT  
		MAXCOUNT = MAX(SET_SNO)
		,SDTCUR,LDTCUR
		,CASE WHEN VNOFLAG = 1 THEN DATEPART(DD,CONVERT(DATETIME,VDT,103)) ELSE '' END AS [DATE]
		,CASE WHEN VNOFLAG = 2 OR  VNOFLAG = 1 THEN DATEPART(MM,CONVERT(DATETIME,VDT,103)) ELSE '' END as [MONTH]
		,CASE WHEN VNOFLAG = 2 OR  VNOFLAG = 1 THEN DATEPART(YY,CONVERT(DATETIME,VDT,103)) ELSE DATEPART(YY,CONVERT(DATETIME,SDTCUR,103)) END AS [YEAR]
		,VDT = MAX(VDT)
		,VTYPE
		,BOOKTYPE 
		,VNOFLAG
		,GROUP_NO
	FROM 
		#TEMP_GENVNO_DATA 
	GROUP BY 
		SDTCUR,LDTCUR
		,CASE WHEN VNOFLAG = 1 THEN DATEPART(DD,CONVERT(DATETIME,VDT,103)) ELSE '' END
		,CASE WHEN VNOFLAG = 2 OR  VNOFLAG = 1 THEN DATEPART(MM,CONVERT(DATETIME,VDT,103)) ELSE '' END
		,CASE WHEN VNOFLAG = 2 OR  VNOFLAG = 1 THEN DATEPART(YY,CONVERT(DATETIME,VDT,103)) ELSE DATEPART(YY,CONVERT(DATETIME,SDTCUR,103)) END 
		, VTYPE,BOOKTYPE,VNOFLAG,GROUP_NO 
	
	DECLARE 
	 @@RECORD_CNT INT
	,@@SDTCUR DATETIME
	,@@LDTCUR DATETIME
	,@@DATE INT
	,@@MONTH INT
	,@@YEAR INT 
	,@@VTYPE INT
	,@@BOOKTYPE CHAR(2)
	,@@VNOFLAG INT
	,@@VDT VARCHAR(11)
	,@@VNO VARCHAR(12)
	,@@GROUP_NO VARCHAR(20)
	CREATE TABLE #TEMP_RETVNO
	(
		VNO VARCHAR(12)
	)
	
	BEGIN 
	OPEN MULTIVNO_CURSOR 	
	FETCH NEXT FROM MULTIVNO_CURSOR  INTO @@RECORD_CNT,@@SDTCUR,@@LDTCUR,@@DATE,@@MONTH,@@YEAR,@@VDT,@@VTYPE,@@BOOKTYPE,@@VNOFLAG,@@GROUP_NO
	WHILE @@FETCH_STATUS = 0
		BEGIN
		SET  @@VDT = CONVERT(VARCHAR(11),CONVERT(DATETIME,@@VDT,103),109)

		TRUNCATE TABLE #TEMP_RETVNO
		INSERT INTO #TEMP_RETVNO 
		EXEC [CLS_ACC_GENVNO] @@VDT,@@VTYPE,@@BOOKTYPE, @@SDTCUR,@@LDTCUR,@@RECORD_CNT
		
		SET @@VNO = (SELECT VNO FROM #TEMP_RETVNO) 
		
		/*****UPDATING VNO*******/
		UPDATE  T 
			SET VNO = CONVERT(BIGINT, @@VNO) + SET_SNO - 1 FROM #TEMP_GENVNO_DATA T
		WHERE 
		--((@@VNOFLAG = 0 AND DATEPART(YY,CONVERT(DATETIME,SDTCUR,103)) = @@YEAR)
		--OR (@@VNOFLAG = 2 AND DATEPART(MM,CONVERT(DATETIME,VDT,103)) = @@MONTH AND DATEPART(YY,CONVERT(DATETIME,VDT,103)) = @@YEAR)
		--OR (@@VNOFLAG = 1 AND CONVERT(VARCHAR(11),CONVERT(DATETIME,VDT,103),109) = @@VDT 
		--AND VTYPE = @@VTYPE AND BOOKTYPE = @@BOOKTYPE ))
		--AND 
		T.GROUP_NO = @@GROUP_NO
		/************************/
		
		FETCH NEXT FROM MULTIVNO_CURSOR  INTO @@RECORD_CNT,@@SDTCUR,@@LDTCUR,@@DATE,@@MONTH,@@YEAR,@@VDT,@@VTYPE,@@BOOKTYPE,@@VNOFLAG,@@GROUP_NO
		END
	CLOSE MULTIVNO_CURSOR 	
	END
	DEALLOCATE MULTIVNO_CURSOR
	
	UPDATE G
	SET
		G.VNO = T.VNO
	FROM #TEMP_GENVNO_DATA T , CLS_TBL_VNOGENDATA G
	WHERE T.SRNO = G.SRNO
	
	--SELECT * FROM #TEMP_GENVNO_DATA order by vno
	--SELECT * FROM CLS_TBL_VNOGENDATA WHERE SESSIONID= @SESSION_ID
			
	
END

GO
