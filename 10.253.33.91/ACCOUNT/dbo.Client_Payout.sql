CREATE Proc [dbo].[Client_Payout] (@party_code varchar(10))
As

DECLARE @FROMDATE DATETIME 
 
Select @FROMDATE=sdtcur from parameter where GETDATE() BETWEEN sdtcur AND ldtcur

SELECT CLTCODE,BALANCE=SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END ),EXCHANGE INTO #LEDBAL
FROM LEDGER_ALL WHERE CLTCODE =@party_code AND VDT >=CONVERT(VARCHAR(11),@FROMDATE,120)
GROUP BY CLTCODE ,EXCHANGE


SELECT CLTCODE,BALANCE=SUM(CASE WHEN DRCR='C' THEN 0 ELSE -VAMT END ),EXCHANGE INTO #UNRELASIZED_AMOUNT 
FROM LEDGER_ALL WHERE CLTCODE =@party_code AND EDT > CONVERT(VARCHAR(11),GETDATE(),120)
GROUP BY CLTCODE,EXCHANGE
 
CREATE TABLE #LD_BAL (
client_code VARCHAR(10),BSECM MONEY,NSECM MONEY,NSEFO MONEY,MCX MONEY,NCDEX MONEY,NSX MONEY,MCD	MONEY, BSX MONEY,TOTAL_BAL MONEY)
 
INSERT INTO #LD_BAL
EXEC [CSOKYC-6].GENERAL.DBO.cli_Ld_Balance @party_code

SELECT CLTCODE,UNRECO_AMOUNT,EXCHANGE INTO #UNREO 
FROM LEDGER1_UNCLEAR WHERE CLTCODE =@party_code AND VDT >=CONVERT(VARCHAR(11),@FROMDATE,120)




CREATE TABLE #FOBROK (
client_code VARCHAR(10),ExposureAndNet MONEY,Brokerage MONEY,Gst MONEY,STT MONEY,TurnoverCharges MONEY,SebiFees MONEY,StampDuty	MONEY )

  --INSERT INTO  #FOBROK 
  --EXEC MIMANSA.CLIENTSERVICE.DBO.GETCLIENTFOTRADESBILL 'K78026','FO','S'   					

 --SELECT * FROM #LEDBAL
 --SELECT * FROM #LD_BAL
 --SELECT * FROM #UNREO
 --SELECT * FROM #UNRELASIZED_AMOUNT
 --SELECT * FROM #FOBROK


 CREATE TABLE #FINAL_BAL
 (CLTCODE VARCHAR(10),LEDGER_BAL MONEY,UNRECO_AMOUNT MONEY , UNREL MONEY,LD_BAL MONEY,OTHER_CHARGES MONEY,MARGIN_SHORTAGE MONEY,NET_AVAILABLE MONEY)

INSERT INTO #FINAL_BAL (CLTCODE,LEDGER_BAL)
SELECT CLTCODE,SUM(BALANCE) FROM #LEDBAL
GROUP BY CLTCODE

UPDATE #FINAL_BAL SET  LD_BAL =TOTAL_BAL 
FROM #LD_BAL WHERE CLTCODE=client_code

UPDATE #FINAL_BAL  SET  UNRECO_AMOUNT =#UNREO.UNRECO_AMOUNT 
FROM #UNREO WHERE #FINAL_BAL.CLTCODE=#UNREO.CLTCODE


UPDATE #FINAL_BAL  SET  UNREL =BALANCE 
FROM #UNRELASIZED_AMOUNT WHERE #FINAL_BAL.CLTCODE=#UNRELASIZED_AMOUNT.CLTCODE

UPDATE #FINAL_BAL  SET  OTHER_CHARGES =(Brokerage+Gst+STT +TurnoverCharges+SebiFees +StampDuty )
FROM #FOBROK WHERE #FINAL_BAL.CLTCODE=#FOBROK.client_code


UPDATE #FINAL_BAL SET NET_AVAILABLE =ISNULL( LEDGER_BAL,0) -ISNULL( UNRECO_AMOUNT,0)-
ISNULL( UNREL,0)-ISNULL( LD_BAL,0)-ISNULL( OTHER_CHARGES,0)-ISNULL( MARGIN_SHORTAGE,0)
 
 SELECT CLTCODE,NET_AVAILABLE=ISNULL(NET_AVAILABLE,0),LEDGER_BAL=ISNULL( LEDGER_BAL,0),UNRECO_AMOUNT=ISNULL( UNRECO_AMOUNT,0),
UNREL=ISNULL( UNREL,0),LD_BAL=ISNULL( LD_BAL,0),OTHER_CHARGES=ISNULL( OTHER_CHARGES,0),MARGIN_SHORTAGE=ISNULL( MARGIN_SHORTAGE,0) FROM #FINAL_BAL

GO
