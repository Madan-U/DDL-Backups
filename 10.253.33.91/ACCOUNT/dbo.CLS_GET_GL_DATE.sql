-- Object: PROCEDURE dbo.CLS_GET_GL_DATE
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROC [dbo].[CLS_GET_GL_DATE]
(
	@SDT VARCHAR(11),
	@EDT VARCHAR(11),
	@DAYS INT
)
AS
BEGIN
		DECLARE @@END_DT VARCHAR(10),
		@@TODAY_DATE DATETIME,
		@@NEW_DATE VARCHAR(11)
		SET @@TODAY_DATE = GETDATE()
		SELECT @@END_DT =
		CASE 
		WHEN LDTCUR < @@TODAY_DATE
			THEN CONVERT(VARCHAR(10), LDTCUR, 103)
		ELSE CONVERT(VARCHAR(10), @@TODAY_DATE, 103)
		END
		FROM PARAMETER
		WHERE SDTCUR = CONVERT(DATETIME, @SDT, 103)
		--SELECT @@END_DT
		
		SET @EDT  = CONVERT(DATETIME,@@END_DT,103)
		SET @@NEW_DATE = CONVERT(VARCHAR(11),DATEADD(D,- @DAYS,@EDT), 109) 
		IF @@NEW_DATE <  CONVERT(DATETIME,@SDT,103)
		BEGIN
			SET @@NEW_DATE = CONVERT(DATETIME,@SDT,103)
		END
		SELECT  CONVERT(VARCHAR(10),CONVERT(DATETIME ,@@NEW_DATE,109),103) AS FDATE ,@@END_DT AS TDATE
END

GO
