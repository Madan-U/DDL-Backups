CLTYPE=B.CL_TYPE,BILLTYPE=B2B_B2C FROM #NLEDGER A,
(SELECT PARTY_CODE,LONG_NAME,BRANCH_CD,SB=SUB_BROKER,CL_TYPE,R=REGION,
