WHERE VDT >= @TODATE + ' 23:59:59' AND DRCR='D' AND VTYP='3' AND BOOKTYPE='E8' AND ACCOUNTTYPE='POA' AND INACTIVEDATE > GETDATE()
GROUP BY CLTCODE

