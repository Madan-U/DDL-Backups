FROM #LEDGER A LEFT OUTER JOIN MSAJAG.DBO.CLIENT_DETAILS B WITH (NOLOCK) 
ON A.CLTCODE = B.cl_code
GROUP BY  A.CLTCODE,B.LONG_NAME,B.BRANCH_CD,B.SUB_BROKER,B.REGION,B.CL_TYPE
HAVING ROUND(SUM(NETAMT),2)  < -50
ORDER BY 1


