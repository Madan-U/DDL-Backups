-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------





create PROCEDURE [dbo].[CLS_RECON_NONCMS_HDFC_PROCESS_bkp_21092021]
( @UPLOADID NVARCHAR(50) 
,@BANKCODE NVARCHAR(50)
,@UPLOADFILENAME NVARCHAR(200)
,@UNAME NVARCHAR(50)) 
AS

BEGIN


DECLARE @D VARCHAR(50),@VALUEDATE VARCHAR(11)
SELECT  @D=.DBO.PIECE(@UPLOADFILENAME,'_',2)


SELECT @VALUEDATE= STUFF(STUFF( @D, 5, 0, '/'), 3, 0, '/')-- DD/MM/YYYY
IF CHARINDEX('.CSV',UPPER(@UPLOADFILENAME))>1
SELECT @VALUEDATE=REPLACE(@VALUEDATE,'.','')

IF(@VALUEDATE='' OR @VALUEDATE IS NULL)
BEGIN
	DELETE FROM MSAJAG..FILEDATA	WHERE PROGID = @UPLOADID
	SELECT		'PLEASE ADD DATE IN THE FILE NAME'
	DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
	RETURN
END
IF CHARINDEX('.CSV',UPPER(@UPLOADFILENAME))>1
BEGIN
BEGIN TRY
	SELECT PROGID,UPLOADBY, .DBO.PIECE(FILE_DATA,'"',1)+
	REPLACE(.DBO.PIECE(FILE_DATA,'"',2),',','') +
	.DBO.PIECE(FILE_DATA,'"',3)+
	REPLACE(.DBO.PIECE(FILE_DATA,'"',4),',','')+
	.DBO.PIECE(FILE_DATA,'"',5)+
	REPLACE(.DBO.PIECE(FILE_DATA,'"',6),',','')+
	.DBO.PIECE(FILE_DATA,'"',7) AS 'FILE_DATA'
	INTO #TEMP
	FROM MSAJAG..FILEDATA WHERE FILEDATA.PROGID=@UPLOADID
	AND FILE_DATA <>'' 
	AND (LEN(FILE_DATA) - LEN(REPLACE(FILE_DATA, '"', '')))=6	
	
	UNION
	
	SELECT  PROGID,UPLOADBY,.DBO.PIECE(FILE_DATA,'"',1)+
	REPLACE(.DBO.PIECE(FILE_DATA,'"',2),',','')+
	.DBO.PIECE(FILE_DATA,'"',3) +
	REPLACE(.DBO.PIECE(FILE_DATA,'"',4),',','') AS 'FILE_DATA'
	FROM MSAJAG..FILEDATA WHERE FILEDATA.PROGID=@UPLOADID
	AND FILE_DATA<>'' 
	AND (LEN(FILE_DATA) - LEN(REPLACE(FILE_DATA, '"', '')))=4

	UNION

	SELECT  PROGID,UPLOADBY,FILE_DATA AS 'FILE_DATA'
	FROM MSAJAG..FILEDATA WHERE FILEDATA.PROGID=@UPLOADID
	AND FILE_DATA<>'' 
	AND (LEN(FILE_DATA) - LEN(REPLACE(FILE_DATA, '"', '')))=2
	AND UPPER(FILE_DATA)  NOT LIKE ('%TOTAL DEBITS%')
	AND UPPER(FILE_DATA)  NOT LIKE ('%TOTAL CREDITS%')
	AND .DBO.PIECE(FILE_DATA,',',3) LIKE '%ATOM %'

INSERT INTO #NONCMS_DATA (UPLOADID
	, BANKACCOUNT
	, BANKCODE	
	, DESCRIPTION
	, LEDGERBALANCE	
	, DRCR
	, AMT	
	, VALUEDATE	
	, REFERENCENO	
	, CLTACCOUNT	
	, CUSTOMERREFERENCE
	, TRANSACTIONDETAIL	
	, VNO	
	, VTYP	
	, BOOKTYPE	
	, MATCHEDFLAG	
	, UPLOADSTATUS
	, UPLOADEDDATE	
	, MODIFIEDDATE	
	, UPLOADBY)
	
	SELECT @UPLOADID
	,U.BANKACCOUNT
	,@BANKCODE
	,.DBO.PIECE(FILE_DATA,',',3)DESCRIPTION
	,'' LEDGERBALANCE
	,CASE WHEN CAST (RTRIM(LTRIM(DBO.PIECE(FILE_DATA,',',6))) AS DECIMAL(20,2)) ='0.00' THEN 'C' ELSE'D' END DRCR
	,CASE WHEN CAST (RTRIM(LTRIM(DBO.PIECE(FILE_DATA,',',6))) AS DECIMAL(20,2)) ='0.00' THEN CAST (RTRIM(LTRIM(DBO.PIECE(FILE_DATA,',',7))) AS DECIMAL(20,2)) ELSE CAST (RTRIM(LTRIM(DBO.PIECE(FILE_DATA,',',6))) AS DECIMAL(20,2)) END AMT
	,CONVERT(DATETIME,(CONVERT(VARCHAR(10),REPLACE(@VALUEDATE, '/', '-'),109)),103)
	,.DBO.PIECE(FILE_DATA,',',4)REFERENCENO
	,'' CLTACCOUNT
	,'' CUSTOMERREFERENCE
	,'' TRANSACTIONDETAIL
	,'' VNO
	,'' VTYP
	,'' BOOKTYPE
	,0 MATCHEDFLAG
	,'PENDING' UPLOADSTATUS
	, UPLOADEDDATE=GETDATE()
	, MODIFIEDDATE=GETDATE()
	, UPLOADBY=F.UPLOADBY
	FROM #TEMP F
	INNER JOIN RECON_BANK_FILEUPLOAD_LOGS U (NOLOCK)
	ON U.UPLOADID = F.PROGID
	WHERE PROGID=@UPLOADID
	AND .DBO.PIECE(FILE_DATA,',',5)<>'FORT MUMBAI'

--select * from #NONCMS_DATA where description like'%atom %'
--return

	DROP TABLE #TEMP
	END TRY
	BEGIN CATCH
		SELECT 'NOT CORRECT FILE FORMAT '
		DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
		DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
		RETURN
	END CATCH
END
ELSE
BEGIN
BEGIN TRY
CREATE TABLE #NONCMS_HDFC (
	UPLOADID NVARCHAR(50),
	BANKACCOUNT NVARCHAR(50),
	BANKCODE NVARCHAR(50),	
	BOOKDATE NVARCHAR(50),
	DESCRIPTION NVARCHAR(500),
	LEDGERBALANCE NVARCHAR (500) ,
	CREDITAMT NVARCHAR (50),
	DEBITAMT NVARCHAR(50),
	VALUEDATE NVARCHAR(50),
	REFERENCENO NVARCHAR (50),
	TRANSACTIONBRANCH NVARCHAR(500),
	MATCHEDFLAG BIT,
	UPLOADSTATUS NVARCHAR(50),
	UPLOADEDDATE DATETIME,
	MODIFIEDDATE DATETIME,
	UPLOADBY NVARCHAR(50))

INSERT INTO #NONCMS_HDFC (UPLOADID,
BANKACCOUNT,
BANKCODE,
BOOKDATE,
DESCRIPTION,
LEDGERBALANCE,
CREDITAMT,
DEBITAMT,
VALUEDATE,
REFERENCENO,
TRANSACTIONBRANCH,
MATCHEDFLAG,
UPLOADSTATUS,
UPLOADEDDATE,
MODIFIEDDATE,
UPLOADBY)
	SELECT
		U.UPLOADID
		,U.BANKACCOUNT
		--,'' AS BANKACCOUNT
		, @BANKCODE 
		,.DBO.PIECE(REPLACE(F.FILE_DATA, '''', ''), '	', 1) BookType
		,.DBO.PIECE(FILE_DATA, '	', 2) Description
		,.DBO.PIECE(FILE_DATA, '	', 3) LedgerBalance
		,.DBO.PIECE(FILE_DATA, '	', 4) Credit
		,.DBO.PIECE(FILE_DATA, '	', 5) Debit
		--,.DBO.PIECE(FILE_DATA, '	', 6) ValueDate
		,@VALUEDATE AS VALUEDATE
		,.DBO.PIECE(FILE_DATA, '	', 7) ReferenceNo
		,.DBO.PIECE(FILE_DATA, '	', 8) TransactionBranch
		,0
		,U.UPLOADSTATUS
		,U.FILEUPLOAD_SDATE
		,GETDATE()
		,U.UPLOADBY
	FROM RECON_BANK_FILEUPLOAD_LOGS U (NOLOCK)
	INNER JOIN MSAJAG.DBO.FILEDATA F (NOLOCK)
		ON U.UPLOADID = F.PROGID
	WHERE F.PROGID = @UPLOADID
	AND.DBO.PIECE(FILE_DATA, '	', 2) <> ''

INSERT INTO #NONCMS_DATA (UPLOADID
, BANKACCOUNT
, BANKCODE
, DESCRIPTION
, LEDGERBALANCE
, DRCR
, AMT
, VALUEDATE
, REFERENCENO
, CLTACCOUNT
, CustomerReference
, TransaCtionDetail
, VNO
, VTYP
, BOOKTYPE
, MATCHEDFLAG
, UPLOADSTATUS
, UPLOADEDDATE
, MODIFIEDDATE
, UPLOADBY)
	SELECT
		UPLOADID
		,BANKACCOUNT
		, BANKCODE
		,DESCRIPTION
		,LEDGERBALANCE
		,CASE
			WHEN CREDITAMT <> '' THEN 'C'
			WHEN DEBITAMT <> '' THEN 'D'
			ELSE ' '
		END AS DRCR
		,CASE
			WHEN CREDITAMT <> '' THEN REPLACE(REPLACE(REPLACE(CREDITAMT, '(', ''), ')', ''), ',', '')
			WHEN DEBITAMT <> '' THEN REPLACE(REPLACE(REPLACE(DEBITAMT, '(', ''), ')', ''), ',', '')
			ELSE '0'
		END AS AMT
		,REPLACE(VALUEDATE, '/', '-')
		,REFERENCENO
		,' '
		,' '
		,' '
		,' '
		,' '
		,' '
		,MATCHEDFLAG
		,UPLOADSTATUS
		,UPLOADEDDATE
		,MODIFIEDDATE
		,UPLOADBY
	FROM #NONCMS_HDFC
	WHERE LEDGERBALANCE = ''

	DROP TABLE #NONCMS_HDFC
	END TRY
	BEGIN CATCH
		SELECT 'NOT CORRECT FILE FORMAT '
		DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
		DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
		RETURN
	END CATCH

END

IF((SELECT COUNT(1) FROM #NONCMS_DATA) <=0)
BEGIN
	SELECT 'NOT CORRECT FILE FORMAT '
	DELETE FROM RECON_BANK_UPLOAD_PROCESS_INFO WHERE UPLOADID=@UPLOADID
	DELETE FROM RECON_BANK_FILEUPLOAD_LOGS WHERE UPLOADID=@UPLOADID
	RETURN
END

END

GO
