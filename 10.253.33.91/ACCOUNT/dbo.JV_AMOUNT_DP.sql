CREATE PROCEDURE [dbo].[JV_AMOUNT_DP]
(
@DATE VARCHAR(20)
)
AS
BEGIN 


SELECT 'NSE'AS EXCHANGE,VDT, EDT, DRCR, SUM(vamt)AS AMOUNT,NARRATION FROM LEDGER WITH (NOLOCK)
 WHERE VTYP=8 AND NARRATION LIKE '%DEMAT CHARGES FOR SELL TRANSACTION FROM HOLDING%'AND VDT=@DATE
 group by vdt, edt, drcr,narration

END

GO
