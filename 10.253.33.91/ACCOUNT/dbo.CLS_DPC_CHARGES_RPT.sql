DECLARE @SELECT_COLS_MARGIN VARCHAR(8000)
DECLARE @SELECT_COLS_COL  VARCHAR(8000)
DECLARE @ALTERCOLS_MARGIN VARCHAR(8000)
DECLARE @ALTERCOLS_COL  VARCHAR(8000)
DECLARE @UPDATECOLS_MARGIN VARCHAR(8000)
DECLARE @UPDATECOLS_COL  VARCHAR(8000)


SET @ALTERCOLS_MARGIN =  (SELECT      
STUFF((SELECT ',['+ITEMS+'_MARGIN] MONEY'      
		FROM ACCOUNT..CLS_SPLIT(@SEG_EXCH,'|') WHERE  ITEMS <> 'NSE-CAPITAL'     
		FOR XML PATH('')      
	),1,1,''))

SET @ALTERCOLS_COL =  (SELECT      
STUFF((SELECT ',['+ITEMS+'_COL] MONEY'      
		FROM ACCOUNT..CLS_SPLIT(@SEG_EXCH,'|')      
		FOR XML PATH('')      
	),1,1,''))

IF @ALTERCOLS_MARGIN <> '' AND @ALTERCOLS_MARGIN IS NOT NULL
BEGIN
	EXEC ('ALTER TABLE #DPC_CHARGES ADD '+ @ALTERCOLS_MARGIN )
END

EXEC ('ALTER TABLE #DPC_CHARGES ADD ' + @ALTERCOLS_COL)


SET @SELECT_COLS_MARGIN =  (SELECT      
STUFF((SELECT ',['+ITEMS+'_MARGIN] = ISNULL(SUM(['+ITEMS+'_MARGIN]),0) '      
		FROM ACCOUNT..CLS_SPLIT(@SEG_EXCH,'|') WHERE  ITEMS <> 'NSE-CAPITAL'     
		FOR XML PATH('')      
	  ),1,1,''))



SET @SELECT_COLS_COL =  (SELECT      
STUFF((SELECT ',['+ITEMS+'_COL]= ISNULL(SUM(['+ITEMS+'_COL]),0) '      
		FROM ACCOUNT..CLS_SPLIT(@SEG_EXCH,'|')      
		FOR XML PATH('')      
		),1,1,''))

SET @ALTERCOLS_MARGIN =  (SELECT      
STUFF((SELECT ',['+ITEMS+'_MARGIN] '      
		FROM ACCOUNT..CLS_SPLIT(@SEG_EXCH,'|') WHERE  ITEMS <> 'NSE-CAPITAL'     
		FOR XML PATH('')      
	  ),1,1,''))

SET @ALTERCOLS_COL =  (SELECT      
STUFF((SELECT ',['+ITEMS+'_COL] '      
		FROM ACCOUNT..CLS_SPLIT(@SEG_EXCH,'|')      
		FOR XML PATH('')      
		),1,1,''))



SET @UPDATECOLS_MARGIN = (SELECT      
STUFF(      
(      
SELECT ',['+ITEMS+'_MARGIN] = CASE WHEN SEG_GROUP = '''+ITEMS+''' THEN MARGIN_REQ ELSE 0 END '       
FROM ACCOUNT..CLS_SPLIT(@SEG_EXCH,'|')  WHERE  ITEMS <> 'NSE-CAPITAL'       
FOR XML PATH('')      
),1,1,''))

SET @UPDATECOLS_COL = (SELECT      
STUFF(      
(      
SELECT ',['+ITEMS+'_COL] = CASE WHEN SEG_GROUP = '''+ITEMS+''' THEN (CASH_COLLATREAL + NONCASH_COLLATREAL) ELSE 0 END '       
FROM ACCOUNT..CLS_SPLIT(@SEG_EXCH,'|')      
FOR XML PATH('')      
),1,1,''))

IF @UPDATECOLS_MARGIN <> '' AND @UPDATECOLS_MARGIN IS NOT NULL
BEGIN	
	EXEC ("UPDATE #DPC_CHARGES SET " + @UPDATECOLS_MARGIN)
END


EXEC ("UPDATE #DPC_CHARGES SET " + @UPDATECOLS_COL)
	

--select * from #DPC_CHARGES

IF @SHOWDIFF = 'Y'  
 BEGIN  
  IF @SUMMARY_FLAG = 0      
 BEGIN      
  SET @@SQL = "SELECT DPC_DATE=CONVERT(VARCHAR,DPC_DATE,103), CLTCODE, LONG_NAME,"+@PIVOT_VAL+","+@SELECT_COLS_COL+","+@SELECT_COLS_MARGIN+",DPC_BALANCE = NET_BALANCE,CASE WHEN CLTCODE='ZZZZZZZZZZ' THEN MAX(NET_DPC_CHARGES) ELSE SUM(NET_DPC_CHARGES) END NET_DPC_CHARGES, NET_DPC_CHARGES_GLOBAL AS GLOBAL_CHRG"      
  SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP,LEDGER_BALANCE, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES, NET_DPC_CHARGES_GLOBAL,"+@ALTERCOLS_COL+","+@ALTERCOLS_MARGIN+"  FROM #DPC_CHARGES WHERE NET_DPC_CHARGES <> NET_DPC_CHARGES_GLOBAL) AS S "      
  SET @@SQL = @@SQL + " PIVOT ( SUM(LEDGER_BALANCE) FOR [SEG_GROUP] IN ("+@PIVOT_COLS+")) AS P "      
  SET @@SQL = @@SQL + " GROUP BY DPC_DATE, CLTCODE, LONG_NAME, NET_BALANCE, NET_DPC_CHARGES_GLOBAL "-- ,"+@ALTERCOLS_MARGIN+","+@ALTERCOLS_COL      
  SET @@SQL = @@SQL + " ORDER BY CLTCODE, CONVERT(DATETIME,DPC_DATE) "    
 END      
 ELSE      
 BEGIN   
  SET @@SQL = "SELECT CLTCODE, MAX(LONG_NAME),"+@PIVOT_VAL+","+@ALTERCOLS_COL+","+@ALTERCOLS_MARGIN+",DPC_BALANCE = ISNULL(NET_BALANCE, 0), CASE WHEN CLTCODE='ZZZZZZZZZZ' THEN MAX(NET_DPC_CHARGES) ELSE SUM(NET_DPC_CHARGES) END NET_DPC_CHARGES, GLOBAL_CHRG = ISNULL(NET_DPC_CHARGES_GLOBAL,0)"      
  SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP,LEDGER_BALANCE, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES_GLOBAL,"+@ALTERCOLS_COL+","+@ALTERCOLS_MARGIN+"  FROM #DPC_CHARGES "  
  SET @@SQL = @@SQL + " WHERE NET_DPC_CHARGES <> NET_DPC_CHARGES_GLOBAL) AS S "      
  SET @@SQL = @@SQL + " PIVOT (SUM(LEDGER_BALANCE) FOR [SEG_GROUP] IN ("+@PIVOT_COLS+")) AS P "      
  SET @@SQL = @@SQL + " GROUP BY CLTCODE, NET_BALANCE, NET_DPC_CHARGES, NET_DPC_CHARGES_GLOBAL "
  SET @@SQL = @@SQL + " ORDER BY CLTCODE "    
 END      
 END  
ELSE  
 BEGIN  
 IF @SUMMARY_FLAG = 0      
 BEGIN  
  IF @SELECT_COLS_MARGIN IS NULL
	BEGIN
	SET @@SQL = "SELECT DPC_DATE=CONVERT(VARCHAR,DPC_DATE,103), CLTCODE, LONG_NAME,"+@PIVOT_VAL+","+@SELECT_COLS_COL+",DPC_BALANCE = NET_BALANCE,CASE WHEN CLTCODE='ZZZZZZZZZZ' THEN MAX(NET_DPC_CHARGES) ELSE SUM(NET_DPC_CHARGES) END NET_DPC_CHARGES"      
    SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP,LEDGER_BALANCE, DPC_BALANCE, NET_BALANCE,NET_DPC_CHARGES ,"+@ALTERCOLS_COL+" FROM #DPC_CHARGES ) AS S "      
	END
  ELSE
  BEGIN
	SET @@SQL = "SELECT DPC_DATE=CONVERT(VARCHAR,DPC_DATE,103), CLTCODE, LONG_NAME,"+@PIVOT_VAL+","+@SELECT_COLS_COL+","+@SELECT_COLS_MARGIN+",DPC_BALANCE = NET_BALANCE,CASE WHEN CLTCODE='ZZZZZZZZZZ' THEN MAX(NET_DPC_CHARGES) ELSE SUM(NET_DPC_CHARGES) END NET_DPC_CHARGES"      
	SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP,LEDGER_BALANCE, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES,"+@ALTERCOLS_COL+","+@ALTERCOLS_MARGIN+" FROM #DPC_CHARGES ) AS S "      
  END
  SET @@SQL = @@SQL + " PIVOT ( SUM(LEDGER_BALANCE) FOR [SEG_GROUP] IN ("+@PIVOT_COLS+")) AS P "      
  SET @@SQL = @@SQL + " GROUP BY DPC_DATE, CLTCODE, LONG_NAME, NET_BALANCE "--,"+@ALTERCOLS_MARGIN+","+@ALTERCOLS_COL      
  SET @@SQL = @@SQL + " ORDER BY CLTCODE, CONVERT(DATETIME,DPC_DATE) "    

  --SET @@SQL = @@SQL + " UNION SELECT DPC_DATE=CONVERT(VARCHAR,DPC_DATE,103), CLTCODE, LONG_NAME,"+@PIVOT_VAL_DUMMY+","+@PIVOT_VAL_COLLATERAL+",NET_BALANCE,NET_DPC_CHARGES"      
  ---SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES,CASH_COLLATREAL FROM #DPC_CHARGES ) AS S "      
  --SET @@SQL = @@SQL + " PIVOT ( SUM(CASH_COLLATREAL) FOR [SEG_GROUP] IN ("+@PIVOT_VAL_COLLATERAL+")) AS P "      
  --SET @@SQL = @@SQL + " GROUP BY DPC_DATE, CLTCODE, LONG_NAME, NET_BALANCE, NET_DPC_CHARGES"      
  --SET @@SQL = @@SQL + " ORDER BY CLTCODE, CONVERT(DATETIME,DPC_DATE) "    

  --SET @@SQL = "UNION SELECT DPC_DATE=CONVERT(VARCHAR,DPC_DATE,103), CLTCODE, LONG_NAME,"+@PIVOT_VAL+",NET_BALANCE,NET_DPC_CHARGES"      
  --SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES,CASH_COLLATREAL FROM #DPC_CHARGES ) AS S "      
  --SET @@SQL = @@SQL + " PIVOT ( SUM(CASH_COLLATREAL) FOR [SEG_GROUP] IN ("+@PIVOT_COLS+")) AS P "      
  --SET @@SQL = @@SQL + " GROUP BY DPC_DATE, CLTCODE, LONG_NAME, NET_BALANCE, NET_DPC_CHARGES"      
  --SET @@SQL = @@SQL + " ORDER BY CLTCODE, CONVERT(DATETIME,DPC_DATE) "    

 END      
 ELSE      
 BEGIN   
IF @SELECT_COLS_MARGIN IS NULL
  BEGIN
	SET @@SQL = "SELECT CLTCODE, MAX(LONG_NAME),"+@PIVOT_VAL+","+@SELECT_COLS_COL+",DPC_BALANCE = ISNULL(NET_BALANCE, 0),CASE WHEN CLTCODE='ZZZZZZZZZZ' THEN MAX(NET_DPC_CHARGES) ELSE SUM(NET_DPC_CHARGES) END NET_DPC_CHARGES"      	
 SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP,LEDGER_BALANCE, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES,"+@ALTERCOLS_COL+" FROM #DPC_CHARGES) AS S "  
  END	
ELSE 
BEGIN
  SET @@SQL = "SELECT CLTCODE, MAX(LONG_NAME),"+@PIVOT_VAL+","+@SELECT_COLS_COL+","+@SELECT_COLS_MARGIN+",DPC_BALANCE = ISNULL(NET_BALANCE, 0), CASE WHEN CLTCODE='ZZZZZZZZZZ' THEN MAX(NET_DPC_CHARGES) ELSE SUM(NET_DPC_CHARGES) END NET_DPC_CHARGES"      
  SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP,LEDGER_BALANCE, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES,"+@ALTERCOLS_COL+","+@ALTERCOLS_MARGIN+" FROM #DPC_CHARGES) AS S "  
END
--SET @@SQL = "SELECT CLTCODE, MAX(LONG_NAME),"+@PIVOT_VAL+","+@SELECT_COLS_COL+","+@SELECT_COLS_MARGIN+",DPC_BALANCE = ISNULL(NET_BALANCE, 0), NET_DPC_CHARGES = ISNULL(NET_DPC_CHARGES, 0)"      
  --SET @@SQL = @@SQL + " FROM (SELECT DPC_DATE, CLTCODE, LONG_NAME, SEG_GROUP,LEDGER_BALANCE, DPC_BALANCE, NET_BALANCE, NET_DPC_CHARGES,"+@ALTERCOLS_COL+","+@ALTERCOLS_MARGIN+" FROM #DPC_CHARGES) AS S "  
  SET @@SQL = @@SQL + " PIVOT (SUM(LEDGER_BALANCE) FOR [SEG_GROUP] IN ("+@PIVOT_COLS+")) AS P "      
  SET @@SQL = @@SQL + " WHERE ISNULL(NET_DPC_CHARGES, 0) < 0 GROUP BY CLTCODE, NET_BALANCE"
  SET @@SQL = @@SQL + " ORDER BY CLTCODE "    
 END      
 END  
  
PRINT @@SQL      
