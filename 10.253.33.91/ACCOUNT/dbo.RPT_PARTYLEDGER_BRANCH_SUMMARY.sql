/*	*****************************************************************************************************
	THIS PROCEDURE WILL DISPLAY PARTY LEDGER SUMMARY FOR BRANCH
	IN BROKER, AREA, REGION, BRANCH, SUBBROKER, TRADER AND PARTY LOGINS.
	THIS REPORT CAN BE VIEWED ON VOUCHER DATE OR EFFECTIVE DATE.
	CREATED BY	:=	YATIN DESAI
	CREATED DATE	:=	DECEMBER 02 2005
	DATABASE		:=	ACCOUNT AND ACCOUNTBSE AND ACCOUNTFO AND ACCOUNTMCDX AND ACCOUNTNCDX
	SYNTAX AS FOLLOWS -----
	EXEC RPT_PARTYLEDGER_BRANCH_SUMMARY 'APR  1 2005', 'DEC 31 2005', 'APR  1 2005', 'MAR 31 2006', 'BROKER', 'BROKER', 'EDT', 'A', 'BZZZZ'
	EXEC RPT_PARTYLEDGER_BRANCH_SUMMARY 'APR  1 2005', 'DEC 31 2005', 'APR  1 2005', 'MAR 31 2006', 'REGION', 'AHM', 'VDT', 'APS', 'ZPS'
	*****************************************************************************************************
*/
CREATE PROC RPT_PARTYLEDGER_BRANCH_SUMMARY
	@FDATE VARCHAR(11),
	@TDATE VARCHAR(11),
	@STDDATE VARCHAR(11),
	@LSTDATE VARCHAR(11),
	@STATUSID VARCHAR(20),
	@STATUSNAME VARCHAR(20),
	@DISPLAYRPT VARCHAR(3),
	@FPARTY VARCHAR(10),
	@TPARTY VARCHAR(10)

AS

DECLARE
@@OBJID AS INT
SELECT @@OBJID = ISNULL(OBJECT_ID('NEWPARTYLEDGER'), 0)
IF @@OBJID = 0
	BEGIN
		EXEC CREATEPARTYLEDGER
	END

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

DELETE FROM NEWPARTYLEDGER WHERE SPID = @@SPID


IF @STATUSID = 'BROKER'
	BEGIN
		IF @DISPLAYRPT = 'VDT'
			BEGIN
					INSERT INTO NEWPARTYLEDGER (BRANCH_CODE, VDT, EDT, VAMT, SPID)
					SELECT A.BRANCHCODE, CONVERT(VARCHAR(11), VDT, 109), CONVERT(VARCHAR(11), EDT, 109), CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END, @@SPID
						FROM ACMAST A (NOLOCK) LEFT JOIN LEDGER L (NOLOCK)
							ON A.CLTCODE = L.CLTCODE
						WHERE
							A.BRANCHCODE BETWEEN @FPARTY AND @TPARTY
							AND L.VDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
							AND A.ACCAT IN (4, 104)
					INSERT INTO NEWPARTYLEDGER (BRANCH_CODE, MAMT, SPID)
					SELECT A.BRANCHCODE, CASE WHEN DRCR = 'C' THEN AMOUNT ELSE -AMOUNT END, @@SPID
						FROM ACMAST A (NOLOCK) LEFT JOIN MARGINLEDGER M (NOLOCK)
							ON A.CLTCODE = M.PARTY_CODE
						WHERE 
							A.BRANCHCODE BETWEEN @FPARTY AND @TPARTY
							AND M.VDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
							AND A.ACCAT IN (4, 104)
					UPDATE NEWPARTYLEDGER SET BRANCH = B.BRANCH FROM MSAJAG.DBO.BRANCH B
						WHERE NEWPARTYLEDGER.BRANCH_CODE = B.BRANCH_CODE AND SPID = @@SPID

				SELECT BRANCHCODE, BRANCH,
						'$$' + CONVERT(VARCHAR, SUM(VAMT), 1) AS VAMT,
						'$$' + CONVERT(VARCHAR, SUM(MAMT), 1) AS MAMT,
						'$$' + CONVERT(VARCHAR, SUM(UNREALISED), 1) AS UNREALISED
					FROM
						(
							SELECT BRANCH_CODE AS BRANCHCODE, BRANCH, SUM(VAMT) AS VAMT, SUM(MAMT) AS MAMT,
								SUM(CASE WHEN VDT <= @TDATE + ' 23:59:59' AND EDT > @TDATE + ' 23:59:59' THEN VAMT ELSE 0 END) AS UNREALISED
							FROM
								NEWPARTYLEDGER (NOLOCK)
							WHERE SPID = @@SPID
							GROUP BY BRANCH_CODE, BRANCH

						UNION ALL

							SELECT BRANCHCODE = 'ZZZZZZZZZZZ', BRANCH = '', SUM(VAMT) AS VAMT, SUM(MAMT) AS MAMT,
								SUM(CASE WHEN VDT <= @TDATE + ' 23:59:59' AND EDT > @TDATE + ' 23:59:59' THEN VAMT ELSE 0 END) AS UNREALISED
							FROM
								NEWPARTYLEDGER (NOLOCK)
							WHERE SPID = @@SPID

						) A
				GROUP BY BRANCHCODE, BRANCH
				ORDER BY BRANCHCODE, BRANCH
				DELETE FROM NEWPARTYLEDGER WHERE SPID = @@SPID
			END
		ELSE
			BEGIN
				-- INSERTING NORMAL EDT RECORDS
				INSERT INTO NEWPARTYLEDGER (BRANCH_CODE, VDT, EDT, VAMT, SPID)
				SELECT A.BRANCHCODE, CONVERT(VARCHAR(11), VDT, 109), CONVERT(VARCHAR(11), EDT, 109), CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END, @@SPID
					FROM ACMAST A (NOLOCK) LEFT JOIN LEDGER L (NOLOCK)
						ON A.CLTCODE = L.CLTCODE
					WHERE
						A.BRANCHCODE BETWEEN @FPARTY AND @TPARTY
						AND L.EDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
						AND A.ACCAT IN (4, 104)
				-- INSERTING MARGIN EDT RECORDS
				INSERT INTO NEWPARTYLEDGER (BRANCH_CODE, MAMT, SPID)
				SELECT A.BRANCHCODE, CASE WHEN M.DRCR = 'C' THEN AMOUNT ELSE -AMOUNT END, @@SPID
					FROM ACMAST A (NOLOCK) LEFT JOIN MARGINLEDGER M (NOLOCK)
						ON A.CLTCODE = M.PARTY_CODE,
						LEDGER L
					WHERE 
						M.VNO = L.VNO AND M.VTYP = L.VTYP AND M.BOOKTYPE = L.BOOKTYPE AND M.LNO = L.LNO
						AND A.BRANCHCODE BETWEEN @FPARTY AND @TPARTY
						AND L.EDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
						AND A.ACCAT IN (4, 104)

				UPDATE NEWPARTYLEDGER SET BRANCH = B.BRANCH FROM MSAJAG.DBO.BRANCH B
					WHERE NEWPARTYLEDGER.BRANCH_CODE = B.BRANCH_CODE AND SPID = @@SPID

				SELECT BRANCHCODE, BRANCH,
						'$$' + CONVERT(VARCHAR, SUM(VAMT), 1) AS VAMT,
						'$$' + CONVERT(VARCHAR, SUM(MAMT), 1) AS MAMT
				FROM
					(
						SELECT BRANCH_CODE AS BRANCHCODE, BRANCH,
							SUM(VAMT) AS VAMT, SUM(MAMT) AS MAMT
						FROM
							NEWPARTYLEDGER (NOLOCK)
						WHERE
							SPID = @@SPID
						GROUP BY BRANCH_CODE, BRANCH

					UNION ALL

						SELECT BRANCH_CODE AS BRANCHCODE, BRANCH,
							SUM(CASE WHEN VAMT >= 0 THEN -VAMT ELSE ABS(VAMT) END) AS VAMT,
							SUM(CASE WHEN MAMT >= 0 THEN -MAMT ELSE ABS(MAMT) END) AS MAMT
						FROM
							NEWPARTYLEDGER (NOLOCK)
						WHERE
							VDT < @STDDATE
							AND EDT >= @STDDATE
							AND SPID = @@SPID
						GROUP BY BRANCH_CODE, BRANCH

					UNION ALL

						SELECT BRANCHCODE = 'ZZZZZZZZZZZ', BRANCH = '',
							SUM(VAMT) AS VAMT, SUM(MAMT) AS MAMT
						FROM
							NEWPARTYLEDGER (NOLOCK)
						WHERE SPID = @@SPID

					UNION ALL

						SELECT BRANCH_CODE AS BRANCHCODE, BRANCH,
							SUM(CASE WHEN VAMT >= 0 THEN -VAMT ELSE ABS(VAMT) END) AS VAMT,
							SUM(CASE WHEN MAMT >= 0 THEN -MAMT ELSE ABS(MAMT) END) AS MAMT
						FROM
							NEWPARTYLEDGER (NOLOCK)
						WHERE
							VDT < @STDDATE
							AND EDT >= @STDDATE
							AND SPID = @@SPID
						GROUP BY BRANCH_CODE, BRANCH
					) A
				GROUP BY BRANCHCODE, BRANCH
				ORDER BY BRANCHCODE, BRANCH
				DELETE FROM NEWPARTYLEDGER WHERE SPID = @@SPID
			END
	END
/* --------------------------------------------- NEED TO MODIFY -----------------------------------------------------------*/
ELSE IF @STATUSID = 'AREA'
	BEGIN
		IF @DISPLAYRPT = 'VDT'
			BEGIN
				SELECT BRANCH_CODE, BRANCH, CONVERT(VARCHAR, SUM(VAMT), 1) AS VAMT, CONVERT(VARCHAR, SUM(MAMT), 1) AS MAMT, CONVERT(VARCHAR, SUM(UNREALISED), 1) AS UNREALISED
				FROM
					(
						SELECT B.BRANCH_CODE, B.BRANCH, SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END) VAMT, MAMT = 0,
						UNREALISED = SUM(CASE WHEN VDT <= @TDATE + ' 23:59:59' AND EDT > @TDATE + ' 23:59:59' THEN 
										(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END)
						FROM MSAJAG.DBO.CLIENT1 C LEFT JOIN LEDGER L
								ON C.CL_CODE = L.CLTCODE,
							MSAJAG.DBO.BRANCH B
						WHERE
							C.BRANCH_CD = B.BRANCH_CODE
							AND C.AREA = @STATUSNAME
							AND C.BRANCH_CD BETWEEN @FPARTY AND @TPARTY
							AND L.VDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
						GROUP BY B.BRANCH_CODE, B.BRANCH
					UNION ALL
						SELECT B.BRANCH_CODE, B.BRANCH, VAMT = 0, MAMT = SUM(CASE WHEN DRCR = 'D' THEN AMOUNT ELSE -AMOUNT END),
						UNREALISED = 0
						FROM MSAJAG.DBO.CLIENT1 C LEFT JOIN MARGINLEDGER L
								ON C.CL_CODE = L.PARTY_CODE,
							MSAJAG.DBO.BRANCH B
						WHERE
							C.BRANCH_CD = B.BRANCH_CODE
							AND C.AREA = @STATUSNAME
							AND C.BRANCH_CD BETWEEN @FPARTY AND @TPARTY
							AND L.VDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
						GROUP BY B.BRANCH_CODE, B.BRANCH
					) A
				GROUP BY BRANCH_CODE, BRANCH
				HAVING SUM(VAMT) <> 0 AND SUM(MAMT) <> 0 OR SUM(UNREALISED) <> 0
			END
		ELSE IF @DISPLAYRPT = 'EDT'
			BEGIN
				PRINT 'EDT'
				SELECT BRANCH_CODE, BRANCH, CONVERT(VARCHAR, SUM(VAMT), 1) VAMT, CONVERT(VARCHAR, SUM(MAMT), 1) MAMT, CONVERT(VARCHAR, SUM(UNREALISED), 1) AS UNREALISED
				FROM
					(
						SELECT B.BRANCH_CODE, B.BRANCH, SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END) VAMT, MAMT = 0,
						UNREALISED = 0
						FROM MSAJAG.DBO.CLIENT1 C LEFT JOIN LEDGER L
								ON C.CL_CODE = L.CLTCODE,
							MSAJAG.DBO.BRANCH B
						WHERE
							C.BRANCH_CD = B.BRANCH_CODE
							AND C.AREA = @STATUSNAME
							AND C.BRANCH_CD BETWEEN @FPARTY AND @TPARTY
							AND L.EDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
						GROUP BY B.BRANCH_CODE, B.BRANCH
					UNION ALL
						SELECT B.BRANCH_CODE, B.BRANCH, VAMT = 0, MAMT = SUM(CASE WHEN M.DRCR = 'D' THEN M.AMOUNT ELSE -M.AMOUNT END),
						UNREALISED = 0
						FROM MSAJAG.DBO.CLIENT1 C LEFT JOIN MARGINLEDGER M
								ON C.CL_CODE = M.PARTY_CODE,
							MSAJAG.DBO.BRANCH B,
							LEDGER L
						WHERE
							C.BRANCH_CD = B.BRANCH_CODE
							AND M.VNO = L.VNO AND M.VTYP = L.VTYP AND M.BOOKTYPE = L.BOOKTYPE AND M.LNO = L.LNO
							AND C.AREA = @STATUSNAME
							AND C.BRANCH_CD BETWEEN @FPARTY AND @TPARTY
							AND L.EDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
						GROUP BY B.BRANCH_CODE, B.BRANCH
					UNION ALL
						SELECT B.BRANCH_CODE, B.BRANCH, SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END) VAMT, MAMT = 0,
						UNREALISED = 0
						FROM MSAJAG.DBO.CLIENT1 C LEFT JOIN LEDGER L
								ON C.CL_CODE = L.CLTCODE,
							MSAJAG.DBO.BRANCH B
						WHERE
							C.BRANCH_CD = B.BRANCH_CODE
							AND C.AREA = @STATUSNAME
							AND C.BRANCH_CD BETWEEN @FPARTY AND @TPARTY
							AND L.VDT < @STDDATE
							AND L.EDT >= @STDDATE
						GROUP BY B.BRANCH_CODE, B.BRANCH
					UNION ALL
						SELECT B.BRANCH_CODE, B.BRANCH, VAMT = 0, MAMT = SUM(CASE WHEN M.DRCR = 'C' THEN M.AMOUNT ELSE -M.AMOUNT END),
						UNREALISED = 0
						FROM MSAJAG.DBO.CLIENT1 C LEFT JOIN MARGINLEDGER M
								ON C.CL_CODE = M.PARTY_CODE,
							MSAJAG.DBO.BRANCH B,
							LEDGER L
						WHERE
							C.BRANCH_CD = B.BRANCH_CODE
							AND M.VNO = L.VNO AND M.VTYP = L.VTYP AND M.BOOKTYPE = L.BOOKTYPE AND M.LNO = L.LNO
							AND C.AREA = @STATUSNAME
							AND C.BRANCH_CD BETWEEN @FPARTY AND @TPARTY
							AND L.VDT < @STDDATE
							AND L.EDT >= @STDDATE
						GROUP BY B.BRANCH_CODE, B.BRANCH
					) A
				GROUP BY BRANCH_CODE, BRANCH
				HAVING SUM(VAMT) <> 0 AND SUM(MAMT) <> 0 OR SUM(UNREALISED) <> 0
			END
	END
ELSE IF @STATUSID = 'REGION'
	BEGIN
		IF @DISPLAYRPT = 'VDT'
			BEGIN
				SELECT BRANCH_CODE, BRANCH, CONVERT(VARCHAR, SUM(VAMT), 1) AS VAMT, CONVERT(VARCHAR, SUM(MAMT), 1) AS MAMT, CONVERT(VARCHAR, SUM(UNREALISED), 1) AS UNREALISED
				FROM
					(
						SELECT B.BRANCH_CODE, B.BRANCH, SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END) VAMT, MAMT = 0,
						UNREALISED = SUM(CASE WHEN VDT <= @TDATE + ' 23:59:59' AND EDT > @TDATE + ' 23:59:59' THEN 
										(CASE WHEN DRCR = 'D' THEN VAMT ELSE -VAMT END) ELSE 0 END)
						FROM MSAJAG.DBO.CLIENT1 C LEFT JOIN LEDGER L
								ON C.CL_CODE = L.CLTCODE,
							MSAJAG.DBO.BRANCH B
						WHERE
							C.BRANCH_CD = B.BRANCH_CODE
							AND C.REGION = @STATUSNAME
							AND C.BRANCH_CD BETWEEN @FPARTY AND @TPARTY
							AND L.VDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
						GROUP BY B.BRANCH_CODE, B.BRANCH
					UNION ALL
						SELECT B.BRANCH_CODE, B.BRANCH, VAMT = 0, MAMT = SUM(CASE WHEN DRCR = 'D' THEN AMOUNT ELSE -AMOUNT END),
						UNREALISED = 0
						FROM MSAJAG.DBO.CLIENT1 C LEFT JOIN MARGINLEDGER L
								ON C.CL_CODE = L.PARTY_CODE,
							MSAJAG.DBO.BRANCH B
						WHERE
							C.BRANCH_CD = B.BRANCH_CODE
							AND C.REGION = @STATUSNAME
							AND C.BRANCH_CD BETWEEN @FPARTY AND @TPARTY
							AND L.VDT BETWEEN @STDDATE AND @TDATE + ' 23:59:59'
							AND C.REGION = @STATUSNAME
						GROUP BY B.BRANCH_CODE, B.BRANCH
					) A
				GROUP BY BRANCH_CODE, BRANCH
				HAVING SUM(VAMT) > 0 OR SUM(MAMT) > 0
			END
		ELSE IF @DISPLAYRPT = 'EDT'
			BEGIN
				PRINT 'START HERE'
			END
	END


