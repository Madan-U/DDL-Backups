CREATE PROC [dbo].[RECO_TIM_REPORT](@FROMDATE VARCHAR(11),@TODATE VARCHAR(11))              
AS              
              
--DECLARE @VDT VARCHAR(11) = 'APR 27 2022'              
            
--EXEC RECO_TIM_REPORT 'DEC  9 2022'  ,'DEC  9 2022'        
      
SELECT DISTINCT L.CLTCODE,L.VNO,L.VTYP,L.BOOKTYPE,L.EDT,L.VDT,MAX(L.CDT) AS CDT,L.ENTEREDBY,L1.RELDT,L1.DDNO,MAX(L1.UPDDATE) AS  UPDDATE,MAX(L.UPDDATE) AS  EUPDDATE              
INTO #LEDGER_TRIG              
FROM LEDGER_TRIG L WITH (NOLOCK), LEDGER1_TRIG L1 WITH (NOLOCK),ACMAST A WITH (NOLOCK)              
WHERE L.VTYP IN (2,3)              
AND ACCAT = 4 AND A.CLTCODE =L.CLTCODE AND L.VNO = L1.VNO AND L.VTYP = L1.VTYP AND L.BOOKTYPE = L1.BOOKTYPE              
AND RELDT  LIKE '%1900%' AND EDT  LIKE '%2049%' AND L.DBACTION = L1.DBACTION AND L.DBACTION = 'MODIFIED'              
AND (VDT BETWEEN @FROMDATE AND  @TODATE + ' 23:59'  OR L1.UPDDATE  BETWEEN @FROMDATE AND  @TODATE + ' 23:59'  OR L.UPDDATE 
BETWEEN @FROMDATE AND  @TODATE + ' 23:59')       
GROUP BY L.CLTCODE,L.VNO,L.VTYP,L.BOOKTYPE,L.EDT,L.VDT,L.ENTEREDBY,L1.RELDT,L1.DDNO 

CREATE CLUSTERED INDEX CLILED ON #LEDGER_TRIG (VNO,VTYP,BOOKTYPE)

