SELECT EXCHANGE = 'NSECM',              
L.CLTCODE,L.VNO,L.VTYP,L.EDT AS NEWEDT,LT.EDT AS OLDEDT,L.VDT,L.CDT,L.ENTEREDBY AS MODIFIEDBY,L1.RELDT AS NEWRELDT,LT.RELDT AS OLDRELDT,        
L1.DDNO,L.NARRATION,L.VAMT,LT.UPDDATE,LT.EUPDDATE,LT.ENTEREDBY,              
LT.BOOKTYPE,CONVERT(TIME,LT.UPDDATE) AS UPDTIME    ,CONVERT(TIME,LT.EUPDDATE) AS EUPDTIME            
INTO #LEDGER_TEMP              
FROM LEDGER L WITH (NOLOCK), LEDGER1 L1 WITH (NOLOCK),ACMAST A WITH (NOLOCK) ,#LEDGER_TRIG LT              
WHERE ACCAT = 4 AND A.CLTCODE =L.CLTCODE AND L.VTYP = L1.VTYP  AND L.VNO = L1.VNO  AND L.BOOKTYPE = L1.BOOKTYPE              
AND LT.VNO = L1.VNO AND LT.VTYP = L1.VTYP AND LT.BOOKTYPE = L1.BOOKTYPE AND  CONVERT(DATE,LT.UPDDATE) >=  CONVERT(DATE,L1.RELDT+1)              
AND (CONVERT(TIME,LT.EUPDDATE) > '03:00:00' OR CONVERT(TIME,LT.UPDDATE) > '03:00:00' )             
AND L1.RELDT NOT LIKE '%1900%' AND L.EDT NOT LIKE '%2049%'              
              
              
SELECT EXCHANGE,L1.CLTCODE,L1.VNO,L1.VTYP,FORMAT(L1.NEWEDT,'dd/MM/yyyy') AS NEWEDT,FORMAT(L1.OLDEDT,'dd/MM/yyyy') AS OLDEDT,        
FORMAT(L1.VDT,'dd/MM/yyyy') AS VDT,FORMAT(L1.CDT,'dd/MM/yyyy') AS CDT,            
MODIFIEDBY,FORMAT(NEWRELDT,'dd/MM/yyyy') AS NEWRELDT,FORMAT(OLDRELDT,'dd/MM/yyyy') AS OLDRELDT,L1.DDNO,L1.NARRATION,L1.VAMT,            
FORMAT(L1.UPDDATE,'dd/MM/yyyy') AS RELUPDDATE,L1.ENTEREDBY,FORMAT(L1.EUPDDATE,'dd/MM/yyyy') AS EDTUPDDATE,L.CLTCODE               
AS BANKCODE ,FORMAT(L1.UPDDATE,'HH:mm:ss') AS UPDTIME ,FORMAT(CONVERT(DATETIME,EUPDTIME),'HH:mm:ss') AS EUPDTIME ,L1.NEWEDT AS NEWEDT1,L1.VDT AS VDT1               
INTO #LEDGER_FINAL              
FROM LEDGER L WITH (NOLOCK), #LEDGER_TEMP L1,ACMAST A   WITH (NOLOCK)            
WHERE ACCAT = 2 AND A.CLTCODE = L.CLTCODE AND L.VNO = L1.VNO AND L.VTYP = L1.VTYP AND L.BOOKTYPE = L1.BOOKTYPE              
AND L1.CLTCODE <> L.CLTCODE AND (CONVERT(TIME,L1.EUPDDATE) > '03:00:00' OR CONVERT(TIME,L1.UPDDATE) > '03:00:00' )         
              
SELECT DISTINCT EXCHANGE,CLTCODE,VNO,VTYP,NEWEDT,OLDEDT,VDT,CDT,MODIFIEDBY,NEWRELDT,OLDRELDT,DDNO,NARRATION,VAMT,RELUPDDATE,ENTEREDBY,EDTUPDDATE,BANKCODE,UPDTIME,EUPDTIME  
FROM #LEDGER_FINAL     
WHERE  (CONVERT(TIME,UPDTIME) > '03:00:00' OR (CONVERT(TIME,EUPDTIME) > '03:00:00' AND CONVERT(DATE,NEWEDT1) > CONVERT(DATE,VDT1)) )       
ORDER BY VDT,CLTCODE ASC            
              
DROP TABLE #LEDGER_TRIG              
DROP TABLE #LEDGER_TEMP              
DROP TABLE #LEDGER_FINAL
--DROP TABLE #LEDGER1_TRIG

GO
