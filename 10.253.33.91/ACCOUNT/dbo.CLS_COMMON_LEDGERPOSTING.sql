--------------------------------------------------



CREATE PROCEDURE [dbo].[CLS_COMMON_LEDGERPOSTING]
(
	@PROGID VARCHAR(200) =''
)
AS
/*

*/
BEGIN

/******LEDGER POSTING PART********/
	/************VNO GENERATION***************/
	DECLARE @SESSIONID VARCHAR(500)
	SET @SESSIONID = (SELECT NEWID())
	SELECT * INTO #V2_OFFLINE FROM V2_OFFLINE_LEDGER_ENTRIES WHERE (((RESERVED3 = @PROGID or VOUCHERNO = @PROGID)
	AND ROWSTATE = 0 AND APPROVALFLAG = 1) OR (@PROGID  = '' AND ROWSTATE = 0 AND APPROVALFLAG = 1))
	
	INSERT INTO CLS_TBL_VNOGENDATA(VDT,VTYPE,BOOKTYPE,SET_SNO,GROUP_NO,MAPPING_SNO,SESSIONID) 
	SELECT CONVERT(VARCHAR(11),VDATE,103),VOUCHERTYPE,BOOKTYPE,SNO,VOUCHERNO,FLDAUTO,@SESSIONID FROM #V2_OFFLINE
	
	EXEC CLS_GENMULTI_VNO @SESSIONID
	/****************************************/

	--SELECT VNO,VDT,VTYPE,MAPPING_SNO,SESSIONID,V.* FROM 
	--CLS_TBL_VNOGENDATA C, #V2_OFFLINE V
	--WHERE SESSIONID= @SESSIONID AND C.MAPPING_SNO = V.FLDAUTO ORDER BY VTYPE,VNO

	INSERT INTO LEDGER
	(VTYP,VNO,EDT,LNO,ACNAME,DRCR,VAMT,VDT,VNO1,REFNO,BALAMT,NODAYS,CDT,CLTCODE,BOOKTYPE,ENTEREDBY,PDT,CHECKEDBY,ACTNODAYS,NARRATION)
	SELECT 
		VOUCHERTYPE
		,VNO
		,EDATE 
		,LNO = ROW_NUMBER() OVER(PARTITION BY VNO,VTYPE,C.BOOKTYPE ORDER BY VNO,convert(int,RESERVED2)) +  
			  (CASE WHEN (VOUCHERTYPE = 5 OR VOUCHERTYPE = 6 OR VOUCHERTYPE = 7 OR VOUCHERTYPE = 8 OR VOUCHERTYPE = 24) THEN 0 ELSE   1 END)
		,ACNAME = LONGNAME
		,DRCR = CASE WHEN CREDITAMT= 0 THEN 'D' ELSE 'C' END 
		,VAMT = ROUND((CASE WHEN CREDITAMT= 0 THEN DEBITAMT ELSE CREDITAMT END),2) 
		,VDT = VDATE
		,VNO1 = VNO
		,REFNO =''
		,BALAMT = ROUND((CASE WHEN CREDITAMT= 0 THEN DEBITAMT ELSE CREDITAMT END),2) 
		,NODAYS = ''
		,CDT = GETDATE()
		,CLTCODE = CASE WHEN ISNULL(V.MARGINCODE,'') <> '' THEN MARGINCODE ELSE V.CLTCODE  END
		,V.BOOKTYPE
		,ENTEREDBY = ADDBY
		,PDT = ''
		,CHECKEDBY = ''
		,ACTNODAYS = ''
		,NARRATION 
	FROM
		#V2_OFFLINE V , CLS_TBL_VNOGENDATA C,ACMAST A
	WHERE
		SESSIONID= @SESSIONID AND C.MAPPING_SNO = V.FLDAUTO AND A.CLTCODE = V.CLTCODE
		


	INSERT INTO LEDGER
	(VTYP,VNO,EDT,LNO,ACNAME,DRCR,VAMT,VDT,VNO1,REFNO,BALAMT,NODAYS,CDT,CLTCODE,BOOKTYPE,ENTEREDBY,PDT,CHECKEDBY,ACTNODAYS,NARRATION)
	SELECT 
		VOUCHERTYPE
		,VNO
		,EDATE 
		,LNO = '1'
		,ACNAME = Longname
		,DRCR =  CASE WHEN VOUCHERTYPE = 5 THEN  (CASE WHEN CREDITAMT= 0 THEN 'D' ELSE 'C' END) 
					ELSE (CASE WHEN CREDITAMT= 0 THEN 'C' ELSE 'D' END) END
		,VAMT = ROUND((CASE WHEN CREDITAMT= 0 THEN DEBITAMT ELSE CREDITAMT END),2) 
		,VDT = VDATE
		,VNO1 = VNO
		,REFNO =''
		,BALAMT = ROUND((CASE WHEN CREDITAMT= 0 THEN DEBITAMT ELSE CREDITAMT END),2) 
		,NODAYS = ''
		,CDT = GETDATE()
		,CLTCODE = CASE WHEN OPPCODE = '' THEN 'BLANK' ELSE OPPCODE END
		,V.BOOKTYPE
		,ENTEREDBY = ADDBY
		,PDT = ''
		,CHECKEDBY = ''
		,ACTNODAYS = ''
		,NARRATION 
	FROM
		#V2_OFFLINE V , CLS_TBL_VNOGENDATA C ,ACMAST A
	WHERE
		SESSIONID= @SESSIONID AND C.MAPPING_SNO = V.FLDAUTO  AND A.CLTCODE = V.OPPCODE 
		AND VOUCHERTYPE NOT IN (5,6,7,8,24)
	
	INSERT INTO LEDGER1(BNKNAME,BRNNAME,DD,DDNO,DDDT,RELDT,RELAMT,REFNO,RECEIPTNO,VTYP,VNO,LNO,DRCR,BOOKTYPE,MICRNO,SLIPNO,SLIPDATE,CHEQUEINNAME,CHQPRINTED,CLEAR_MODE)
	SELECT 
		 BNKNAME = BANKNAME
		,BRNNAME = ''
		,DD = ''
		,DDNO
		,DDDT = VDATE
		,RELDT = ISNULL(REL_DATE,'')
		,RELAMT = ABS(SUM(ROUND((CREDITAMT-DEBITAMT),2)))
		,REFNO = ISNULL(V.RESERVED4,'0')
		,RECEIPTNO = ''
		,VTYPE 
		,VNO
		,LNO =  case when VTYPE = 5 THEN CONVERT(INT,V.RESERVED2)  ELSE 2 END
		,DRCR = CASE WHEN SUM(ROUND((CREDITAMT-DEBITAMT),2)) < 0 THEN 'D' ELSE 'C' END 
		,V.BOOKTYPE
		,MICR
		,SLIPNO = ''
		,SLIPDATE = ''
		,CHEQUENAME
		,CHQPRINTED = ''
		,CLEAR_MODE
	FROM
		#V2_OFFLINE V , CLS_TBL_VNOGENDATA C 
	WHERE
		SESSIONID= @SESSIONID 
		AND VTYPE NOT IN(1,4,6,7,8,22,23,24)
		AND C.MAPPING_SNO = V.FLDAUTO 	
	GROUP BY  
		BANKNAME
		,DDNO
		,VDATE
		,ISNULL(REL_DATE,'')
		,ISNULL(V.RESERVED4,'0')
		,VTYPE 
		,VNO
		,V.BOOKTYPE
		,MICR
		,CHEQUENAME
		,CLEAR_MODE
		,CONVERT(INT,V.RESERVED2) 
		
	
	INSERT INTO MARGINLEDGER(VTYP,VNO,LNO,DRCR,VDT,AMOUNT,PARTY_CODE,EXCHANGE,SEGMENT,SETT_NO,SETT_TYPE,BOOKTYPE,MCLTCODE)
	SELECT 
	VTYPE
	,VNO
	,LNO='1'
	,DRCR = CASE WHEN CREDITAMT= 0 THEN 'D' ELSE 'C' END 
	,VDT =VDATE
	,AMOUNT = ROUND((CASE WHEN CREDITAMT= 0 THEN DEBITAMT ELSE CREDITAMT END),2) 
	,PARTY_CODE = V.CLTCODE
	,EXCHANGE
	,SEGMENT
	,SETT_NO
	,ISNULL(SETT_TYPE,'')
	,V.BOOKTYPE
	,MCLTCODE = MARGINCODE 
	FROM
		#V2_OFFLINE V , CLS_TBL_VNOGENDATA C 
	WHERE 
	SESSIONID= @SESSIONID 
	AND C.MAPPING_SNO = V.FLDAUTO 	
	AND MARGINCODE <> '' AND VTYPE IN('19','20','22','23','24')
	
	UPDATE 	V2
	SET 
		LEDGERVNO = VNO,
		ROWSTATE = 1
	FROM 
		V2_OFFLINE_LEDGER_ENTRIES V2 ,CLS_TBL_VNOGENDATA C
	WHERE 
	SESSIONID= @SESSIONID AND C.MAPPING_SNO = V2.FLDAUTO


DELETE FROM CLS_TBL_VNOGENDATA WHERE 	SESSIONID = @SESSIONID
DROP TABLE #V2_OFFLINE	


/********************************/
END

GO
