 CREATE PROC [dbo].[RPT_SFCM_DEBTORS_AM] (@TDATE VARCHAR(11))
 
 AS  --- EXEC RPT_SFCM_DEBTORS_AM 'APR 28 2021'
 
 DECLARE @RUNTIEM VARCHAR(10)
 
 SELECT @RUNTIEM = CONVERT(VARCHAR(10), CAST(MAX(RUNDATE) AS TIME), 0) FROM ACCOUNT.DBO.SFCM_REPORTING
 
 TRUNCATE TABLE SFCMDR
 
 INSERT INTO SFCMDR
               
 SELECT REPORT_DATE,CLTCODE,CLIENT_NAME=M.LONG_NAME,PANNO=M.PAN_GIR_NO,SUM(BALANCE) AS BALANCE,RUNTIME=@RUNTIEM
 FROM SFCM_REPORTING A WITH (NOLOCK),MSAJAG.DBO.CLIENT_DETAILS M WITH (NOLOCK)        
 WHERE CLTCODE BETWEEN 'A' AND 'MZZZZ9999' AND REPORT_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
 AND A.CLTCODE=M.CL_CODE 
 GROUP BY CLTCODE,REPORT_DATE,M.LONG_NAME,M.PAN_GIR_NO

SELECT REPORT_DATE='REPORT_DATE',CLTCODE='CLTCODE',CLIENT_NAME='CLIENT_NAME',PANNO='PANNO',BALANCE='BALANCE',REPORT='REPORT',RUNTIME='RUNTIME'
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE=LTRIM(RTRIM(CLTCODE)),CLIENT_NAME=REPLACE(CLIENT_NAME,',',''),PANNO=REPLACE(PANNO,',',''),
CONVERT(VARCHAR,SUM(BALANCE)) AS BALANCE,REPORT='DR',RUNTIME
FROM SFCMDR WHERE BALANCE < 0 
GROUP BY REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,RUNTIME
ORDER BY BALANCE DESC

GO
