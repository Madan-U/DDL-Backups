-- Object: PROCEDURE dbo.RPT_SCFM_DRCR
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

  
  
CREATE PROC [dbo].[RPT_SCFM_DRCR] (@FDATE VARCHAR(11),@TDATE VARCHAR(11))  
   
AS  --- EXEC RPT_SCFM_DRCR 'APR  8 2022','APR  8 2022'  
   
  
CREATE TABLE #DRCR (REPORT_DATE VARCHAR(11),CLTCODE VARCHAR(10),BROKING_BAL MONEY,MTF_BAL MONEY,NET_BAL MONEY)  
  
IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')  
BEGIN  
INSERT INTO #DRCR  
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,  
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+  
ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS BROKING_BAL,  
ROUND(MTF,2) AS MTF_BAL,  
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+  
ROUND(MTF,2)+ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS NET_BALANCE  
FROM ACCOUNT.DBO.SCFM_REPORTING A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'  
END  
  
IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING_LOG A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')  
BEGIN  
INSERT INTO #DRCR                 
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,  
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+  
ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS BROKING_BAL,  
ROUND(MTF,2) AS MTF_BAL,  
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+  
ROUND(MTF,2)+ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS NET_BALANCE  
FROM ACCOUNT.DBO.SCFM_REPORTING_LOG A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'  
END  
  
IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING_HISTORY A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59')  
BEGIN  
INSERT INTO #DRCR                 
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,  
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+  
ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS BROKING_BAL,  
ROUND(MTF,2) AS MTF_BAL,  
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+  
ROUND(MTF,2)+ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS NET_BALANCE  
FROM ACCOUNT.DBO.SCFM_REPORTING_HISTORY A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'  
END  
  
SELECT [Report Date]=CONVERT(VARCHAR(11),REPORT_DATE,105),Report='MTF Creditors',  
[Balance in Cr.]=ISNULL(SUM(MTF_BAL),0)/10000000,[Balance in Rs.]=SUM(MTF_BAL)  
FROM #DRCR WITH (NOLOCK) WHERE MTF_BAL < 0 GROUP BY REPORT_DATE  
union ALL  
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='MTF Debtors',  
[Balance in Cr.]=SUM(MTF_BAL)/10000000,[Balance in Rs.]=SUM(MTF_BAL)  
from #DRCR WHERE MTF_BAL > 0 group BY REPORT_DATE  
union ALL  
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Broking Creditors',  
[Balance in Cr.]=SUM(BROKING_BAL)/10000000,[Balance in Rs.]=SUM(BROKING_BAL)  
from #DRCR WHERE BROKING_BAL < 0 GROUP BY REPORT_DATE  
union ALL  
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Broking Debtors',  
[Balance in Cr.]=SUM(BROKING_BAL)/10000000,[Balance in Rs.]=SUM(BROKING_BAL)  
from #DRCR WHERE BROKING_BAL > 0 group BY REPORT_DATE  
UNION ALL  
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Total Gross Creditors',  
CAST(((SELECT ISNULL(SUM(MTF_BAL),0) FROM #DRCR WITH (NOLOCK) WHERE MTF_BAL < 0 ) +  
(SELECT SUM(BROKING_BAL) FROM #DRCR WHERE (BROKING_BAL) < 0 ))/10000000 AS NUMERIC(18,2)) AS [Balance in Cr.],  
(SELECT ISNULL(SUM(MTF_BAL),0) FROM #DRCR WITH (NOLOCK) WHERE MTF_BAL < 0 ) +  
(SELECT SUM(BROKING_BAL) FROM #DRCR WHERE (BROKING_BAL) < 0 ) AS [Balance in Rs.]  
from #DRCR GROUP BY REPORT_DATE  
UNION ALL  
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Total Gross Debtors',  
CAST(((SELECT SUM(MTF_BAL) FROM #DRCR WITH (NOLOCK) WHERE MTF_BAL > 0 ) +  
(SELECT SUM(BROKING_BAL) FROM #DRCR WHERE (BROKING_BAL) > 0 ))/10000000  AS NUMERIC(18,2)) AS [Balance in Cr.],  
(SELECT SUM(MTF_BAL) FROM #DRCR WITH (NOLOCK) WHERE MTF_BAL > 0 ) +  
(SELECT SUM(BROKING_BAL) FROM #DRCR WHERE (BROKING_BAL) > 0 ) AS [Balance in Rs.]  
from #DRCR GROUP BY REPORT_DATE  
union ALL  
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Total Net Creditors',  
[Balance in Cr.]=SUM(NET_BAL)/10000000,[Balance in Rs.]=SUM(NET_BAL)  
from #DRCR WHERE NET_BAL < 0 GROUP BY REPORT_DATE  
union ALL  
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT='Total Net Debtors',  
[Balance in Cr.]=SUM(NET_BAL)/10000000,[Balance in Rs.]=SUM(NET_BAL)  
from #DRCR WHERE NET_BAL > 0 GROUP BY REPORT_DATE  
  
  
DROP TABLE #DRCR

GO
