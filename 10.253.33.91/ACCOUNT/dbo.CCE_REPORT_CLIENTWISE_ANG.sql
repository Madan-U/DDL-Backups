SELECT [TRADING MEMBER PAN],[DATE],[UNIQUE CLIENT CODE],[CLIENT PAN],[CLIENT NAME],[MTF/ NON MTF INDICATOR],[FINANCIAL LEDGER BALANCE-A],      
[FINANCIAL LEDGER BALANCE (CLEAR)-B],[Peak Financial Ledger Balance (clear)-C],[FINANCIAL LEDGER BALANCE-MCX],[FINANCIAL LEDGER BALANCE-NCDEX],      
[FINANCIAL LEDGER BALANCE-ICEX],[BANK GUARANTEE (BG)],[FIXED DEPOSIT RECEIPT (FDR)],[GOVERNMENT OF INDIA SECURITIES (GSEC)],[GILT FUNDS],      
[Credit entry in ledger in lieu of EPI],[Pool Account],[Uncleared Cheques],[Value of Commodities],      
[Cash Collateral for MTF Positions] = CASE WHEN [MTF/ NON MTF INDICATOR] = 'MTF' THEN CASE WHEN ROUND([FINANCIAL LEDGER BALANCE-A], 2) + ROUND(ISNULL([Cash Collateral for MTF Positions],0), 2) > 0 THEN CONVERT(VARCHAR, ROUND([FINANCIAL LEDGER BALANCE-A], 2) + ROUND(ISNULL([Cash Collateral for MTF Positions],0), 2)) ELSE CONVERT(VARCHAR, 0) END ELSE 'NA' END      
