AS        
      
--EXEC CCE_REPORT_CLIENTWISE_ANG 'NOV  1 2022','A86891'      
         
SELECT SNO,TRADING_PAN,REPORT_DATE,CLIENT_NAME,CLIENT_PAN INTO #CCE_VDT FROM ACCOUNT.DBO.CLIENT_CASH_DETAILS(NOLOCK)                 
WHERE REPORT_DATE BETWEEN @REPORTDATE AND @REPORTDATE + ' 23:59' AND UCC = @CLTCODE        
      
      
      
SELECT [TRADING MEMBER PAN] = TRADING_PAN,[DATE] = CONVERT(VARCHAR(11),REPORT_DATE,105),      
[UNIQUE CLIENT CODE] = L.PARTY_CODE,[CLIENT PAN] = CLIENT_PAN,[CLIENT NAME] = CLIENT_NAME,      
[MTF/ NON MTF INDICATOR]=CASE WHEN L.EXCHANGE='MTF' THEN 'MTF' ELSE 'NON-MTF' END,      
ROUND(SUM(VDT_BAL),2) AS [FINANCIAL LEDGER BALANCE-A],        
ROUND(SUM(EDT_BAL),2) AS [FINANCIAL LEDGER BALANCE (CLEAR)-B],      
--SUM(OPEN_BILL1) AS OPEN_BILL1 ,SUM(OPEN_BILL2) AS OPEN_BILL2,        
--SUM(CHQ_DEPOSITS) AS CHQ_DEPOSITS,SUM(CHQ_ISSUE) AS CHQ_ISSUE,SUM(MARGIN_REQ) AS MARGIN_REQ,        
ROUND(SUM(PEAK_FINANCIAL_LEDGER),2) AS [Peak Financial Ledger Balance (clear)-C],        
ROUND(SUM(CASE WHEN EXCHANGE = 'MCX' THEN EDT_BAL ELSE 0 END),2) AS [FINANCIAL LEDGER BALANCE-MCX],      
ROUND(SUM(CASE WHEN EXCHANGE = 'NCX' THEN EDT_BAL ELSE 0 END),2) AS [FINANCIAL LEDGER BALANCE-NCDEX],      
ROUND(SUM(CASE WHEN EXCHANGE = 'ICX' THEN EDT_BAL ELSE 0 END),2) AS [FINANCIAL LEDGER BALANCE-ICEX],      
SUM(BG) AS [BANK GUARANTEE (BG)],SUM(FDR) AS [FIXED DEPOSIT RECEIPT (FDR)],SUM(GOV_IND_SEC) AS [GOVERNMENT OF INDIA SECURITIES (GSEC)],      
SUM(GILT_FUNDS) AS [GILT FUNDS],        
SUM(EPI_LEDGER) AS [Credit entry in ledger in lieu of EPI],SUM(POOL_ACCOUNT) AS [Pool Account],      
SUM(CHQ_DEPOSITS) AS [Uncleared Cheques],      
--SUM(LEDACCOUNT) AS LEDACCOUNT,      
SUM(COMM_VALUE) AS [Value of Commodities],        
ROUND(SUM(CASH_COLL_MTF_POSITIONS),2) AS [Cash Collateral for MTF Positions]  INTO #CCE_VDT_FINAL      
FROM #CCE_VDT C, ACCOUNT.DBO.CLIENT_CASH_LED_BALANCES L WITH (NOLOCK) WHERE C.SNO = L.SNO              
GROUP BY TRADING_PAN,PARTY_CODE,CLIENT_PAN,CLIENT_NAME,CONVERT(VARCHAR(11),REPORT_DATE,105),CASE WHEN L.EXCHANGE='MTF' THEN 'MTF' ELSE 'NON-MTF' END        
      
