CREATE PROCEDURE [dbo].[RPT_SCFM_DRCR_EXPORT] 
	@RUNDATE VARCHAR(11)
AS

BEGIN

CREATE TABLE #DRCR (REPORT_DATE VARCHAR(11),CLTCODE VARCHAR(10),BROKING_BAL MONEY,MTF_BAL MONEY,NET_BAL MONEY)

IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59')
BEGIN
INSERT INTO #DRCR
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+
ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS BROKING_BAL,
ROUND(MTF,2) AS MTF_BAL,
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+
ROUND(MTF,2)+ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS NET_BALANCE
FROM ACCOUNT.DBO.SCFM_REPORTING A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
END

IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING_LOG A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59')
BEGIN
INSERT INTO #DRCR
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+
ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS BROKING_BAL,
ROUND(MTF,2) AS MTF_BAL,
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+
ROUND(MTF,2)+ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS NET_BALANCE
FROM ACCOUNT.DBO.SCFM_REPORTING_LOG A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
END

IF EXISTS(SELECT 1 FROM ACCOUNT.DBO.SCFM_REPORTING_HISTORY A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59')
BEGIN
INSERT INTO #DRCR
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),CLTCODE,
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+
ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS BROKING_BAL,
ROUND(MTF,2) AS MTF_BAL,
ROUND(BSECM,2)+ROUND(BSEFO,2)+ROUND(BSECD,2)+ROUND(NSECM,2)+ROUND(NSEFO,2)+ROUND(NSECD,2)+
ROUND(MTF,2)+ROUND(SLBS,2)+ROUND(MCXCD,2)+ROUND(MCDX,2)+ROUND(NCDX,2)+ROUND(NCE,2) AS NET_BALANCE
FROM ACCOUNT.DBO.SCFM_REPORTING_HISTORY A WITH (NOLOCK) WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
end


/* MTF Creditors Exports */

TRUNCATE TABLE SCFMDRCR_EXPORT_NEW
INSERT INTO SCFMDRCR_EXPORT_NEW
SELECT ROW_NUMBER() OVER(ORDER BY (SELECT 1)) AS SNO,REPORT_DATE,CLTCODE,CLIENT_NAME=CONVERT(VARCHAR(1000),''),
PANNO=CONVERT(VARCHAR(20),''),REPORT='MTF Creditors',MTF_BAL=SUM(MTF_BAL)
FROM #DRCR WITH (NOLOCK) WHERE MTF_BAL < 0 GROUP BY REPORT_DATE,CLTCODE

UPDATE A SET CLIENT_NAME = REPLACE(LONG_NAME,',','') , PANNO = REPLACE(PAN_GIR_NO,',','')  
FROM SCFMDRCR_EXPORT_NEW A, MSAJAG.DBO.Client_Details  B WITH (NOLOCK) WHERE A.CLTCODE = B.CL_CODE

DECLARE @MTFCRFILE VARCHAR(100) = 'J:\BACKOFFICE\DRCRFILES\MTF_CREDITORS_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'
DECLARE @MTFCR VARCHAR(MAX)                            
SET @MTFCR = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT_DATE'''',''''CLTCODE'''',''''CLIENT_NAME'''',''''PANNO'''',''''REPORT'''',''''BALANCE''''' 
SET @MTFCR = @MTFCR + ' UNION ALL SELECT REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,REPORT,CONVERT(VARCHAR,BALANCE) from ACCOUNT.DBO.SCFMDRCR_EXPORT_NEW" QUERYOUT ' +@MTFCRFILE+ ' -c -t"," -c -t"," -r"\n" -T'',NO_OUTPUT'
EXEC(@MTFCR)


/* MTF Debtors Exports */

TRUNCATE TABLE SCFMDRCR_EXPORT_NEW
INSERT INTO SCFMDRCR_EXPORT_NEW
SELECT ROW_NUMBER() OVER(ORDER BY (SELECT 1)) AS SNO,REPORT_DATE,CLTCODE,CLIENT_NAME=CONVERT(VARCHAR(1000),''),
PANNO=CONVERT(VARCHAR(20),''),REPORT='MTF Debtors',MTF_BAL=SUM(MTF_BAL)
FROM #DRCR WITH (NOLOCK) WHERE MTF_BAL > 0 GROUP BY REPORT_DATE,CLTCODE

UPDATE A SET CLIENT_NAME = REPLACE(LONG_NAME,',','') , PANNO = REPLACE(PAN_GIR_NO,',','')  
FROM SCFMDRCR_EXPORT_NEW A, MSAJAG.DBO.Client_Details  B WITH (NOLOCK) WHERE A.CLTCODE = B.CL_CODE

DECLARE @MTFDRFILE VARCHAR(100) = 'J:\BACKOFFICE\DRCRFILES\MTF_DEBTORS_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'
DECLARE @MTFDR VARCHAR(MAX)                            
SET @MTFDR = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT_DATE'''',''''CLTCODE'''',''''CLIENT_NAME'''',''''PANNO'''',''''REPORT'''',''''BALANCE''''' 
SET @MTFDR = @MTFDR + ' UNION ALL SELECT REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,REPORT,CONVERT(VARCHAR,BALANCE) from ACCOUNT.DBO.SCFMDRCR_EXPORT_NEW" QUERYOUT ' +@MTFDRFILE+ ' -c -t"," -c -t"," -r"\n" -T'',NO_OUTPUT'
EXEC(@MTFDR)



/* Broking Creditors Exports */

TRUNCATE TABLE SCFMDRCR_EXPORT_NEW
INSERT INTO SCFMDRCR_EXPORT_NEW
SELECT ROW_NUMBER() OVER(ORDER BY (SELECT 1)) AS SNO,REPORT_DATE,CLTCODE,CLIENT_NAME=CONVERT(VARCHAR(1000),''),
PANNO=CONVERT(VARCHAR(20),''),REPORT='Broking Creditors',BROKING_BAL=SUM(BROKING_BAL)
FROM #DRCR WITH (NOLOCK) WHERE BROKING_BAL < 0 GROUP BY REPORT_DATE,CLTCODE

UPDATE A SET CLIENT_NAME = REPLACE(LONG_NAME,',','') , PANNO = REPLACE(PAN_GIR_NO,',','')  
FROM SCFMDRCR_EXPORT_NEW A, MSAJAG.DBO.Client_Details  B WITH (NOLOCK) WHERE A.CLTCODE = B.CL_CODE

DECLARE @BROKCRAMFILE VARCHAR(100) = 'J:\BACKOFFICE\DRCRFILES\BROKING_CREDITORS_01_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'
DECLARE @BROKAMCR VARCHAR(MAX)                            
SET @BROKAMCR = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT_DATE'''',''''CLTCODE'''',''''CLIENT_NAME'''',''''PANNO'''',''''REPORT'''',''''BALANCE''''' 
SET @BROKAMCR = @BROKAMCR + ' UNION ALL SELECT REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,REPORT,CONVERT(VARCHAR,BALANCE) from ACCOUNT.DBO.SCFMDRCR_EXPORT_NEW WHERE SRNO < = 900000 " QUERYOUT ' +@BROKCRAMFILE+ ' -c -t"," -c -t"," -r"\n" -T'',NO_OUTPUT'
EXEC(@BROKAMCR)

DECLARE @BROKCRNZFILE VARCHAR(100) = 'J:\BACKOFFICE\DRCRFILES\BROKING_CREDITORS_02_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'
DECLARE @BROKNZCR VARCHAR(MAX)                            
SET @BROKNZCR = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT_DATE'''',''''CLTCODE'''',''''CLIENT_NAME'''',''''PANNO'''',''''REPORT'''',''''BALANCE''''' 
SET @BROKNZCR = @BROKNZCR + ' UNION ALL SELECT REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,REPORT,CONVERT(VARCHAR,BALANCE) from ACCOUNT.DBO.SCFMDRCR_EXPORT_NEW WHERE SRNO > 900000 " QUERYOUT ' +@BROKCRNZFILE+ ' -c -t"," -c -t"," -r"\n" -T'',NO_OUTPUT'
EXEC(@BROKNZCR)


/* Broking Debtors Exports */

TRUNCATE TABLE SCFMDRCR_EXPORT_NEW
INSERT INTO SCFMDRCR_EXPORT_NEW
SELECT ROW_NUMBER() OVER(ORDER BY (SELECT 1)) AS SNO,REPORT_DATE,CLTCODE,CLIENT_NAME=CONVERT(VARCHAR(1000),''),
PANNO=CONVERT(VARCHAR(20),''),REPORT='Broking Debtors',BROKING_BAL=SUM(BROKING_BAL)
FROM #DRCR WITH (NOLOCK) WHERE BROKING_BAL > 0 GROUP BY REPORT_DATE,CLTCODE

UPDATE A SET CLIENT_NAME = REPLACE(LONG_NAME,',','') , PANNO = REPLACE(PAN_GIR_NO,',','')  
FROM SCFMDRCR_EXPORT_NEW A, MSAJAG.DBO.Client_Details  B WITH (NOLOCK) WHERE A.CLTCODE = B.CL_CODE

DECLARE @BROKDRAMFILE VARCHAR(100) = 'J:\BACKOFFICE\DRCRFILES\BROKING_DEBTORS_01_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'
DECLARE @BROKAMDR VARCHAR(MAX)                            
SET @BROKAMDR = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT_DATE'''',''''CLTCODE'''',''''CLIENT_NAME'''',''''PANNO'''',''''REPORT'''',''''BALANCE''''' 
SET @BROKAMDR = @BROKAMDR + ' UNION ALL SELECT REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,REPORT,CONVERT(VARCHAR,BALANCE) from ACCOUNT.DBO.SCFMDRCR_EXPORT_NEW WHERE SRNO < = 600000 " QUERYOUT ' +@BROKDRAMFILE+ ' -c -t"," -c -t"," -r"\n" -T'',NO_OUTPUT'
EXEC(@BROKAMDR)

DECLARE @BROKDRNZFILE VARCHAR(100) = 'J:\BACKOFFICE\DRCRFILES\BROKING_DEBTORS_02_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'
DECLARE @BROKNZDR VARCHAR(MAX)                            
SET @BROKNZDR = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT_DATE'''',''''CLTCODE'''',''''CLIENT_NAME'''',''''PANNO'''',''''REPORT'''',''''BALANCE''''' 
SET @BROKNZDR = @BROKNZDR + ' UNION ALL SELECT REPORT_DATE,CLTCODE,CLIENT_NAME,PANNO,REPORT,CONVERT(VARCHAR,BALANCE) from ACCOUNT.DBO.SCFMDRCR_EXPORT_NEW WHERE SRNO > 600000 " QUERYOUT ' +@BROKDRNZFILE+ ' -c -t"," -c -t"," -r"\n" -T'',NO_OUTPUT'
EXEC(@BROKNZDR)

SELECT 'Debtors and Creditors Client Data Exported for SCFM Reporting - ' + @RUNDATE

END

GO
