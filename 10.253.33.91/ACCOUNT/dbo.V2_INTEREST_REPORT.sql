--------------------------------------------------



CREATE PROCEDURE V2_INTEREST_REPORT  
(  
    @FILTER VARCHAR(11), 
    @INTERESTON VARCHAR(3), 
    @FROMDATE VARCHAR(11), 
	@TODATE VARCHAR(11), 
	@FROMPARTY VARCHAR(10), 
	@TOPARTY VARCHAR(10), 
	@STATUSID VARCHAR(20), 
	@STATUSNAME VARCHAR(20) 
)  
  
AS  
  
/*==============================================================================================================  
        EXEC V2_INTEREST_REPORT  
	    @FILTER = 'BRANCH', 
	    @INTERESTON = 'VDT', 
	    @FROMDATE = 'JUN  1 2007', 
		@TODATE = 'JUN  7 2007', 
		@FROMPARTY = '0000000000', 
		@TOPARTY = 'ZZZZZZZZZZ', 
		@STATUSID = 'BROKER', 
		@STATUSNAME = 'BROKER' 
==============================================================================================================*/  
  
        SET NOCOUNT ON

	DECLARE 
		@@FDATE INT, 
		@@TDATE INT 

	SET @@FDATE = CONVERT(INT,CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE),112))
	SET @@TDATE = CONVERT(INT,CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE),112))

SELECT PARTY_CODE, LONG_NAME INTO #CLIENT FROM MSAJAG.DBO.CLIENT_DETAILS C WHERE 1 = 2

INSERT INTO #CLIENT 
EXEC V2_ACCOUNT_LISTING  
	    @FILTER, 
		@FROMPARTY, 
		@TOPARTY, 
		@STATUSID, 
		@STATUSNAME 

	SELECT 
		CLTCODE = C1.PARTY_CODE, 
		NSECM = SUM(CASE WHEN SEGMENTCODE = 1 THEN (CASE WHEN @INTERESTON = 'VDT' THEN VDTINTEREST ELSE EDTINTEREST END) ELSE 0 END), 
		BSECM = SUM(CASE WHEN SEGMENTCODE = 2 THEN (CASE WHEN @INTERESTON = 'VDT' THEN VDTINTEREST ELSE EDTINTEREST END) ELSE 0 END), 
		NSEFO = SUM(CASE WHEN SEGMENTCODE = 3 THEN (CASE WHEN @INTERESTON = 'VDT' THEN VDTINTEREST ELSE EDTINTEREST END) ELSE 0 END), 
		BSEFO = SUM(CASE WHEN SEGMENTCODE = 4 THEN (CASE WHEN @INTERESTON = 'VDT' THEN VDTINTEREST ELSE EDTINTEREST END) ELSE 0 END), 
		INTEREST = SUM(CASE WHEN @INTERESTON = 'VDT' THEN VDTINTEREST ELSE EDTINTEREST END), 
		C1.LONG_NAME 
	FROM V2_INTEREST V, 
		MSAJAG.DBO.CLIENT_DETAILS C, 
		#CLIENT C1  
	WHERE 
		C.PARTY_CODE = V.CLTCODE 
		AND V.INTDATE BETWEEN @@FDATE AND @@TDATE 
		AND C1.PARTY_CODE = ( 
			CASE 
				WHEN @FILTER = 'BRANCH' 
				THEN C.BRANCH_CD 
				WHEN @FILTER = 'CLIENT' 
				THEN C.PARTY_CODE 
				WHEN @FILTER = 'FAMILY' 
				THEN C.FAMILY 
			END) 
		AND C.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
	GROUP BY C1.PARTY_CODE, C1.LONG_NAME

GO
