WHERE FORMAT(A.SAUDA_DATE,'dd/MM/yyyy') = B.SAUDA_DATE
AND A.FLAG = 1 AND EXCHANGE = 'NSEFO'

UPDATE #TEMPPAYIN_FINAL SET CURRENCY = CONVERT(VARCHAR(11),'*'+CURRENCY) FROM #TEMPPAYIN A,#TEMPPAYIN_FINAL B
WHERE FORMAT(A.SAUDA_DATE,'dd/MM/yyyy') = B.SAUDA_DATE
AND A.FLAG = 1 AND EXCHANGE = 'NSECD'

UPDATE #TEMPPAYIN_FINAL SET CASH = CONVERT(VARCHAR(11),'*'+CASH) FROM #TEMPPAYIN A,#TEMPPAYIN_FINAL B
WHERE FORMAT(A.SAUDA_DATE,'dd/MM/yyyy') = B.SAUDA_DATE
AND A.FLAG = 1 AND EXCHANGE = 'NSECM'
    
