CREATE PROC TBL_PAYINPAYOUTREPORT_PROV
AS
DECLARE @FM_DATE VARCHAR(11) ,@LM_DATE VARCHAR(11) ,@START_DATE VARCHAR(11) = 'APR  1 2021'

SET @FM_DATE = CONVERT(VARCHAR(11),ACCOUNT.DBO.FN_NEXTWEEKDAY(GETDATE()-14, 'MON'),109)    
SET @LM_DATE = CONVERT(VARCHAR(11),ACCOUNT.DBO.FN_NEXTWEEKDAY(GETDATE(), 'FRI'),109)    

--SELECT @FM_DATE, @LM_DATE

IF ( CONVERT(VARCHAR(11),ACCOUNT.DBO.FN_NEXTWEEKDAY(GETDATE(), 'SAT'),109) = CONVERT(VARCHAR(11),GETDATE()))
BEGIN 
RETURN
END

IF ( CONVERT(VARCHAR(11),ACCOUNT.DBO.FN_NEXTWEEKDAY(GETDATE(), 'SUN'),109) = CONVERT(VARCHAR(11),GETDATE()))
BEGIN 
RETURN
END

IF ( SELECT COUNT(1) FROM MSAJAG.DBO.SETT_MST WHERE CONVERT(VARCHAR(11),START_DATE) = CONVERT(VARCHAR(11),GETDATE()) AND SETT_TYPE = 'N') = ''
BEGIN 
RETURN
END

--SELECT @FM_DATE = LEFT(GETDATE()-DAY(GETDATE())+1,11), @LM_DATE = LEFT(CONVERT(DATETIME,EOMONTH(GETDATE())),11)


--DELETE CASH_PAYINPAYOUT_ANG WHERE  SAUDA_DATE BETWEEN @FM_DATE AND @LM_DATE
--DELETE FNO_PAYINPAYOUT_ANG WHERE  SAUDA_DATE BETWEEN @FM_DATE AND @LM_DATE

 
SELECT EXCHANGE='NSECM',SAUDA_DATE=OLB_DATE,SETT_NO,SETT_TYPE,PAMT=SUM(C_PAMT),SAMT=SUM(C_SAMT),NET_PAYIN_PAYOUT=SUM(C_SAMT)-SUM(C_PAMT) INTO #CASH_PAYINPAYOUT_ANG
FROM MSAJAG.DBO.TBL_NSE_OBL WHERE OLB_DATE >= @START_DATE
GROUP BY SETT_NO,SETT_TYPE,OLB_DATE
ORDER BY OLB_DATE,SETT_NO

UPDATE #CASH_PAYINPAYOUT_ANG SET SAUDA_DATE = START_DATE FROM MSAJAG.DBO.SETT_MST A WITH (NOLOCK) WHERE  #CASH_PAYINPAYOUT_ANG.SETT_NO = A.SETT_NO
AND #CASH_PAYINPAYOUT_ANG.SETT_TYPE = A.SETT_TYPE AND START_DATE >= @START_DATE


SELECT EXCHANGE='NSEFO',SAUDA_DATE=FOEA_POSITION_DATE,PREMIUM_ACCOUNT=SUM(FOEA_NETPREMIUM),
CLEARING_HOUSE = SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE),
OPTION_EX_ALLOCATION=SUM(FOEA_EXERCISED_ASSIGNED_VALUE),
NET_PAYIN_PAYOUT=SUM(FOEA_NETPREMIUM)+SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE)+SUM(FOEA_EXERCISED_ASSIGNED_VALUE) INTO #FNO_PAYINPAYOUT_ANG
FROM ANGELFO.NSEFO.DBO.FOEXERCISEALLOCATION WITH (NOLOCK)
WHERE FOEA_POSITION_DATE >= @START_DATE

GROUP BY FOEA_POSITION_DATE
UNION ALL    
    
    
SELECT EXCHANGE='NSEFO',SAUDA_DATE=FOEA_POSITION_DATE,PREMIUM_ACCOUNT=SUM(FOEA_NETPREMIUM),    
CLEARING_HOUSE = SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE),    
OPTION_EX_ALLOCATION=SUM(FOEA_EXERCISED_ASSIGNED_VALUE),    
NET_PAYIN_PAYOUT=SUM(FOEA_NETPREMIUM)+SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE)+SUM(FOEA_EXERCISED_ASSIGNED_VALUE)     
FROM ANGELFO.PRADNYA.DBO.HISTORY_FOEXERCISEALLOCATION_NSEFO WITH (NOLOCK)    
WHERE FOEA_POSITION_DATE >= @START_DATE    
GROUP BY FOEA_POSITION_DATE    

UNION ALL
SELECT EXCHANGE='NSECD',SAUDA_DATE=FOEA_POSITION_DATE,PREMIUM_ACCOUNT=SUM(FOEA_NETPREMIUM),
CLEARING_HOUSE = SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE),
OPTION_EX_ALLOCATION=SUM(FOEA_EXERCISED_ASSIGNED_VALUE),
NET_PAYIN_PAYOUT=SUM(FOEA_NETPREMIUM)+SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE)+SUM(FOEA_EXERCISED_ASSIGNED_VALUE)
FROM ANGELFO.NSECURFO.DBO.FOEXERCISEALLOCATION WITH (NOLOCK)
WHERE FOEA_POSITION_DATE >= @START_DATE
GROUP BY FOEA_POSITION_DATE
UNION ALL
SELECT EXCHANGE='MCDX',SAUDA_DATE=FOEA_POSITION_DATE,PREMIUM_ACCOUNT=SUM(FOEA_NETPREMIUM),
CLEARING_HOUSE = SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE),
OPTION_EX_ALLOCATION=SUM(FOEA_EXERCISED_ASSIGNED_VALUE),
NET_PAYIN_PAYOUT=SUM(FOEA_NETPREMIUM)+SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE)+SUM(FOEA_EXERCISED_ASSIGNED_VALUE)
FROM ANGELCOMMODITY.MCDX.DBO.FOEXERCISEALLOCATION WITH (NOLOCK)
WHERE FOEA_POSITION_DATE >= @START_DATE
GROUP BY FOEA_POSITION_DATE
UNION ALL
SELECT EXCHANGE='NCDX',SAUDA_DATE=FOEA_POSITION_DATE,PREMIUM_ACCOUNT=SUM(FOEA_NETPREMIUM),
CLEARING_HOUSE = SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE),
OPTION_EX_ALLOCATION=SUM(FOEA_EXERCISED_ASSIGNED_VALUE),
NET_PAYIN_PAYOUT=SUM(FOEA_NETPREMIUM)+SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) + SUM(FOEA_FUTURES_FINAL_SETTLEMENT_VALUE)+SUM(FOEA_EXERCISED_ASSIGNED_VALUE)
FROM ANGELCOMMODITY.NCDX.DBO.FOEXERCISEALLOCATION WITH (NOLOCK)
WHERE FOEA_POSITION_DATE >= @START_DATE
GROUP BY FOEA_POSITION_DATE
ORDER BY EXCHANGE,SAUDA_DATE

DECLARE @FNODATE DATETIME

SELECT @FNODATE = CONVERT(VARCHAR(11),START_DATE) FROM MSAJAG.DBO.SETT_MST WHERE  SETT_TYPE = 'N' AND SETT_NO IN 
(SELECT SETT_NO + 1  FROM MSAJAG.DBO.SETT_MST WHERE  SETT_TYPE = 'N' AND CONVERT(VARCHAR(11),START_DATE) = CONVERT(VARCHAR(11),GETDATE()))

-- REPORT STARTS

SELECT EXCHANGE,SAUDA_DATE,NET_PAYIN_PAYOUT,SETT_TYPE INTO #TEMPPAYINTEMP FROM (    
SELECT EXCHANGE,SAUDA_DATE,SETT_TYPE--,SETT_NO,SETT_TYPE,PAMT,SAMT    
,NET_PAYIN_PAYOUT FROM #CASH_PAYINPAYOUT_ANG     
UNION ALL    
SELECT EXCHANGE,SAUDA_DATE,SETT_TYPE=''--,PREMIUM_ACCOUNT,CLEARING_HOUSE,OPTION_EX_ALLOCATION    
,NET_PAYIN_PAYOUT     
FROM #FNO_PAYINPAYOUT_ANG    
UNION ALL    
SELECT 'NSEFO',VDT,SETT_TYPE='F',AMT = SUM(CASE WHEN DRCR = 'D' THEN VAMT ELSE - VAMT END)    
FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WITH (NOLOCK) WHERE VTYP = 79 AND CLTCODE = '99885' AND VDT >= @START_DATE    
GROUP BY VDT) AS A    
    
    
ALTER TABLE #TEMPPAYINTEMP ADD PAYINPAYOUT DATETIME     
    
UPDATE #TEMPPAYINTEMP SET PAYINPAYOUT  = EdtCr    
FROM ACCOUNT.DBO.BillPosted A WHERE A.BillDate = #TEMPPAYINTEMP.SAUDA_DATE AND A.Sett_Type = #TEMPPAYINTEMP.SETT_TYPE    
AND EXCHANGE = 'NSECM'    
    
UPDATE #TEMPPAYINTEMP SET PAYINPAYOUT  = EdtCr    
FROM ANGELFO.ACCOUNTFO.DBO.BillPosted A WHERE A.BillDate = #TEMPPAYINTEMP.SAUDA_DATE AND A.Sett_Type = #TEMPPAYINTEMP.SETT_TYPE    
AND EXCHANGE = 'NSEFO'    
    
UPDATE #TEMPPAYINTEMP SET PAYINPAYOUT  = EdtCr    
FROM ANGELFO.ACCOUNTCURFO.DBO.BillPosted A WHERE A.BillDate = #TEMPPAYINTEMP.SAUDA_DATE AND A.Sett_Type = #TEMPPAYINTEMP.SETT_TYPE    
AND EXCHANGE = 'NSECD'    
    
UPDATE #TEMPPAYINTEMP SET PAYINPAYOUT  = EdtCr    
FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.BillPosted A WHERE A.BillDate = #TEMPPAYINTEMP.SAUDA_DATE AND A.Sett_Type = #TEMPPAYINTEMP.SETT_TYPE    
AND EXCHANGE = 'MCDX'    
    
UPDATE #TEMPPAYINTEMP SET PAYINPAYOUT  = EdtCr    
FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.BillPosted A WHERE A.BillDate = #TEMPPAYINTEMP.SAUDA_DATE AND A.Sett_Type = #TEMPPAYINTEMP.SETT_TYPE    
AND EXCHANGE = 'NCDX'    

UPDATE #TEMPPAYINTEMP SET PAYINPAYOUT  = @FNODATE WHERE CONVERT(VARCHAR(11),SAUDA_DATE) = CONVERT(VARCHAR(11),GETDATE())
AND EXCHANGE = 'NSEFO'  AND SETT_TYPE = '' AND PAYINPAYOUT IS NULL    
    
DELETE FROM #TEMPPAYINTEMP WHERE PAYINPAYOUT IS NULL    
    
SELECT EXCHANGE,PAYINPAYOUT AS SAUDA_DATE, SUM(NET_PAYIN_PAYOUT) AS NET_PAYIN_PAYOUT INTO #TEMPPAYIN  FROM #TEMPPAYINTEMP    
GROUP BY EXCHANGE,PAYINPAYOUT    


