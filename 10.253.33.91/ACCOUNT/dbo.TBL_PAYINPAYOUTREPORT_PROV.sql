SELECT @TDAY AS SAUDA_DATE,EXCHANGE = 'NSECM' 
UNION ALL
SELECT @TDAY AS SAUDA_DATE,EXCHANGE = 'NSEFO' 
UNION ALL
SELECT @TDAY AS SAUDA_DATE,EXCHANGE = 'NSECD'
UNION ALL
SELECT @TDAY AS SAUDA_DATE,EXCHANGE = 'MCDX'
UNION ALL
SELECT @TDAY AS SAUDA_DATE,EXCHANGE = 'NCDX'
) AS A


SELECT @TDAY = MAX(SAUDA_DATE-1)  FROM  #TEMPPAYIN

