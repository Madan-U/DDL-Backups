-- Object: PROCEDURE dbo.SEBI_CREDITOR_DEBTOR
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------


CREATE PROC [dbo].[SEBI_CREDITOR_DEBTOR]        
(        
 @REPORTDATE VARCHAR(11)        
)        
AS        
     --EXEC SEBI_CREDITOR_DEBTOR 'JUN 12 2017'   
      
DECLARE @NSEMEMBERCODE VARCHAR(10),        
  @BSEMEMBERCODE VARCHAR(10)        
        
SELECT @NSEMEMBERCODE = MEMBERCODE FROM MSAJAG.DBO.OWNER        
SELECT @BSEMEMBERCODE = MEMBERCODE FROM [AngelBSECM].BSEDB_AB.DBO.OWNER        
        
SELECT PARTY_CODE, PARTY_NAME, CL_TYPE,         
TOTAL_VDTBAL = SUM(ISNULL( TOTAL_VDTBAL ,0)),        
TOTAL_OPENBILL = SUM(ISNULL( TOTAL_OPENBILL ,0)),        
TOTAL_DEPOSITS = SUM(ISNULL( TOTAL_DEPOSITS ,0)),        
TOTAL_ISSUE     = SUM(ISNULL( TOTAL_ISSUE ,0)),        
TOTAL_CASH_COLL = CONVERT(NUMERIC(18,4),0),        
TOTAL_MARGIN_REQ= SUM(ISNULL( TOTAL_MARGIN ,0)),        
TOTAL_LED_DATA = SUM(ISNULL(TOTAL_VDTBAL,0))        
        - SUM(ISNULL(TOTAL_OPENBILL,0)      )        
        - SUM(ISNULL(TOTAL_DEPOSITS,0)    )        
        + SUM(ISNULL(TOTAL_ISSUE,0)     ),        
CREDITOR_BALANCE = CONVERT(NUMERIC(18,4),0),        
DEBTOR_BALANCE = CONVERT(NUMERIC(18,4),0),        
CM_VDTBAL = SUM(ISNULL( CM_VDTBAL ,0)),
BSECM_VDTBAL = SUM(ISNULL( BSECM_VDTBAL ,0)),        
FO_VDTBAL = SUM(ISNULL( FO_VDTBAL ,0)),        
NSX_VDTBAL = SUM(ISNULL( NSX_VDTBAL ,0)),        
BFO_VDTBAL = SUM(ISNULL( BFO_VDTBAL ,0)),        
BSX_VDTBAL = SUM(ISNULL( BSX_VDTBAL ,0)),        
MFO_VDTBAL = SUM(ISNULL( MFO_VDTBAL ,0)),        
MSX_VDTBAL = SUM(ISNULL( MSX_VDTBAL ,0)),        
NSEMF_VDTBAL = SUM(ISNULL( NSEMF_VDTBAL ,0)),        
BSEMF_VDTBAL = SUM(ISNULL( BSEMF_VDTBAL ,0)),
NSESLBS_VDTBAL = SUM(ISNULL( NSESLBS_VDTBAL ,0)),    
BSESLBS_VDTBAL = SUM(ISNULL( BSESLBS_VDTBAL ,0)),        
NSECM_OPENBILL_1 = SUM(ISNULL( NSECM_OPENBILL_1 ,0)),        
BSECM_OPENBILL_1 = SUM(ISNULL( BSECM_OPENBILL_1 ,0)),        
NSEMF_OPENBILL_1 = SUM(ISNULL( NSEMF_OPENBILL_1 ,0)),        
BSEMF_OPENBILL_1 = SUM(ISNULL( BSEMF_OPENBILL_1 ,0)),        
NSECM_OPENBILL_2 = SUM(ISNULL( NSECM_OPENBILL_2 ,0)),        
BSECM_OPENBILL_2 = SUM(ISNULL( BSECM_OPENBILL_2 ,0)),        
NSEMF_OPENBILL_2 = SUM(ISNULL( NSEMF_OPENBILL_2 ,0)),        
BSEMF_OPENBILL_2 = SUM(ISNULL( BSEMF_OPENBILL_2 ,0)),        
NFO_OPENBILL_1 = SUM(ISNULL( NFO_OPENBILL_1 ,0)),        
NSX_OPENBILL_1 = SUM(ISNULL( NSX_OPENBILL_1 ,0)),        
BFO_OPENBILL_1 = SUM(ISNULL( BFO_OPENBILL_1 ,0)),        
BSX_OPENBILL_1 = SUM(ISNULL( BSX_OPENBILL_1 ,0)),        
MFO_OPENBILL_1 = SUM(ISNULL( MFO_OPENBILL_1 ,0)),        
MSX_OPENBILL_1 = SUM(ISNULL( MSX_OPENBILL_1 ,0)),    
NSESLBS_OPENBILL_1 = SUM(ISNULL( NSESLBS_OPENBILL_1 ,0)),    
BSESLBS_OPENBILL_1 = SUM(ISNULL( BSESLBS_OPENBILL_1 ,0)),      
CM_DEPOSITS = SUM(ISNULL( CM_DEPOSITS ,0)),    
BSECM_DEPOSITS = SUM(ISNULL( BSECM_DEPOSITS ,0)),        
FO_DEPOSITS = SUM(ISNULL( FO_DEPOSITS ,0)),        
NSX_DEPOSITS = SUM(ISNULL( NSX_DEPOSITS ,0)),        
BFO_DEPOSITS = SUM(ISNULL( BFO_DEPOSITS ,0)),        
BSX_DEPOSITS = SUM(ISNULL( BSX_DEPOSITS ,0)),        
MFO_DEPOSITS = SUM(ISNULL( MFO_DEPOSITS ,0)),        
MSX_DEPOSITS = SUM(ISNULL( MSX_DEPOSITS ,0)),        
NSEMF_DEPOSITS = SUM(ISNULL( NSEMF_DEPOSITS ,0)),        
BSEMF_DEPOSITS = SUM(ISNULL( BSEMF_DEPOSITS ,0)), 
NSESLBS_DEPOSITS = SUM(ISNULL( NSESLBS_DEPOSITS ,0)), 
BSESLBS_DEPOSITS = SUM(ISNULL( BSESLBS_DEPOSITS ,0)),     
CM_ISSUE = SUM(ISNULL( CM_ISSUE ,0)),    
BSECM_ISSUE = SUM(ISNULL( BSECM_ISSUE ,0)),        
FO_ISSUE = SUM(ISNULL( FO_ISSUE ,0)),        
NSX_ISSUE = SUM(ISNULL( NSX_ISSUE ,0)),        
BFO_ISSUE = SUM(ISNULL( BFO_ISSUE ,0)),        
BSX_ISSUE = SUM(ISNULL( BSX_ISSUE ,0)),        
MFO_ISSUE = SUM(ISNULL( MFO_ISSUE ,0)),        
MSX_ISSUE = SUM(ISNULL( MSX_ISSUE ,0)),        
NSEMF_ISSUE = SUM(ISNULL( NSEMF_ISSUE ,0)),        
BSEMF_ISSUE = SUM(ISNULL( BSEMF_ISSUE ,0)),    
NSESLBS_ISSUE = SUM(ISNULL( NSESLBS_ISSUE ,0)),    
BSESLBS_ISSUE = SUM(ISNULL( BSESLBS_ISSUE ,0)),    
NET_BALANCE = SUM(ISNULL( NET_BALANCE ,0)),        
NSECM_MARGIN = SUM(ISNULL( NSECM_MARGIN ,0)),        
BSECM_MARGIN = SUM(ISNULL( BSECM_MARGIN ,0)),        
FO_MARGIN = SUM(ISNULL( FO_MARGIN ,0)),        
NSX_MARGIN = SUM(ISNULL( NSX_MARGIN ,0)),        
BFO_MARGIN = SUM(ISNULL( BFO_MARGIN ,0)),        
BSX_MARGIN = SUM(ISNULL( BSX_MARGIN ,0)),        
MFO_MARGIN = SUM(ISNULL( MFO_MARGIN ,0)),        
MSX_MARGIN = SUM(ISNULL( MSX_MARGIN ,0)),
NSESLBS_MARGIN = SUM(ISNULL( NSESLBS_MARGIN ,0))   ,
BSESLBS_MARGIN = SUM(ISNULL( BSESLBS_MARGIN ,0))          
INTO #DATA FROM ACCOUNT.DBO.FINAL_AUDIT_DATA D with(nolock)          
WHERE EFFDATE = @REPORTDATE       
AND CL_TYPE <> 'PRO'        
AND PARTY_CODE NOT IN (@NSEMEMBERCODE, @BSEMEMBERCODE)        
AND NOT EXISTS (SELECT PARTY_CODE FROM SEBI_EXCEP_PARTY S     
       WHERE S.PARTY_CODE = D.PARTY_CODE)  
GROUP BY PARTY_CODE, PARTY_NAME, CL_TYPE        
    
CREATE CLUSTERED INDEX [PARTYIDX] ON [DBO].[#DATA]     
(    
 [PARTY_CODE] ASC    
)    
        
SELECT C.PARTY_CODE, AMOUNT = SUM(FINALAMOUNT), D.CL_TYPE            
INTO #LEDDATA            
FROM MSAJAG.DBO.COLLATERALDETAILS C (NOLOCK), MSAJAG.DBO.CLIENT_DETAILS D (NOLOCK)            
WHERE EFFDATE = @REPORTDATE       
AND C.PARTY_CODE = D.CL_CODE            
AND COLL_TYPE = 'MARGIN'            
AND D.CL_TYPE <> 'PRO'        
AND C.PARTY_CODE NOT IN (@NSEMEMBERCODE, @BSEMEMBERCODE)        
AND NOT EXISTS (SELECT PARTY_CODE FROM SEBI_EXCEP_PARTY S     
       WHERE S.PARTY_CODE = C.PARTY_CODE)   
GROUP BY C.PARTY_CODE, CL_TYPE            
            
INSERT INTO #LEDDATA            
SELECT C.PARTY_CODE, AMOUNT = SUM(FINALAMOUNT), D.CL_TYPE            
FROM MSAJAG.DBO.COLLATERALDETAILS C (NOLOCK), MSAJAG.DBO.CLIENT1 D (NOLOCK)            
WHERE EFFDATE = @REPORTDATE       
AND C.PARTY_CODE = D.CL_CODE            
AND COLL_TYPE = 'MARGIN'            
AND D.CL_TYPE <> 'PRO'        
AND C.PARTY_CODE NOT IN (@NSEMEMBERCODE, @BSEMEMBERCODE)        
AND NOT EXISTS (SELECT PARTY_CODE FROM SEBI_EXCEP_PARTY S     
       WHERE S.PARTY_CODE = D.CL_CODE)        
AND NOT EXISTS (SELECT CL_CODE FROM MSAJAG.DBO.CLIENT_DETAILS D1 (NOLOCK) WHERE D.CL_CODE = D1.CL_CODE)  
GROUP BY C.PARTY_CODE, CL_TYPE           
        
UPDATE #DATA SET TOTAL_CASH_COLL = L.AMOUNT        
FROM #LEDDATA L        
WHERE L.PARTY_CODE = #DATA.PARTY_CODE        
        
UPDATE #DATA SET         
CREDITOR_BALANCE = (CASE WHEN (TOTAL_CASH_COLL+TOTAL_LED_DATA) >= 0 THEN (TOTAL_CASH_COLL+TOTAL_LED_DATA) ELSE 0 END),            
DEBTOR_BALANCE = (CASE WHEN (TOTAL_CASH_COLL+TOTAL_LED_DATA) < 0 THEN ABS((TOTAL_CASH_COLL+TOTAL_LED_DATA)) ELSE 0 END)           
  
--SELECT * INTO BAK_SEBIDATA FROM #DATA  
       
SELECT * FROM #DATA with(nolock)     
WHERE ( ABS(ISNULL(TOTAL_VDTBAL,0))    
 + ABS(ISNULL(TOTAL_OPENBILL,0))    
 + ABS(ISNULL(TOTAL_DEPOSITS,0))    
 + ABS(ISNULL(TOTAL_ISSUE,0))    
 + ABS(ISNULL(TOTAL_MARGIN_REQ,0))     
 + ABS(ISNULL(TOTAL_CASH_COLL,0))   
 +ABS(ISNULL(CREDITOR_BALANCE,0))  
+ABS(ISNULL(CREDITOR_BALANCE,0))  
+ABS(ISNULL(DEBTOR_BALANCE,0))  
+ABS(ISNULL(CM_VDTBAL,0))
+ABS(ISNULL(BSECM_VDTBAL,0))  
+ABS(ISNULL(FO_VDTBAL,0))  
+ABS(ISNULL(NSX_VDTBAL,0))  
+ABS(ISNULL(BFO_VDTBAL,0))  
+ABS(ISNULL(BSX_VDTBAL,0))  
+ABS(ISNULL(MFO_VDTBAL,0))  
+ABS(ISNULL(MSX_VDTBAL,0))  
+ABS(ISNULL(NSEMF_VDTBAL,0))  
+ABS(ISNULL(BSEMF_VDTBAL,0)) 
+ABS(ISNULL(NSESLBS_VDTBAL,0))   
+ABS(ISNULL(BSESLBS_VDTBAL,0)) 
+ABS(ISNULL(NSECM_OPENBILL_1,0))  
+ABS(ISNULL(BSECM_OPENBILL_1,0))  
+ABS(ISNULL(NSEMF_OPENBILL_1,0))  
+ABS(ISNULL(BSEMF_OPENBILL_1,0))  
+ABS(ISNULL(NSECM_OPENBILL_2,0))  
+ABS(ISNULL(BSECM_OPENBILL_2,0))  
+ABS(ISNULL(NSEMF_OPENBILL_2,0))  
+ABS(ISNULL(BSEMF_OPENBILL_2,0))  
+ABS(ISNULL(NFO_OPENBILL_1,0))  
+ABS(ISNULL(NSX_OPENBILL_1,0))  
+ABS(ISNULL(BFO_OPENBILL_1,0))  
+ABS(ISNULL(BSX_OPENBILL_1,0))  
+ABS(ISNULL(MFO_OPENBILL_1,0))  
+ABS(ISNULL(MSX_OPENBILL_1,0))  
+ABS(ISNULL(NSESLBS_OPENBILL_1,0))  
+ABS(ISNULL(BSESLBS_OPENBILL_1,0))  
+ABS(ISNULL(CM_DEPOSITS,0)) 
+ABS(ISNULL(BSECM_DEPOSITS,0))   
+ABS(ISNULL(FO_DEPOSITS,0))  
+ABS(ISNULL(NSX_DEPOSITS,0))  
+ABS(ISNULL(BFO_DEPOSITS,0))  
+ABS(ISNULL(BSX_DEPOSITS,0))  
+ABS(ISNULL(MFO_DEPOSITS,0))  
+ABS(ISNULL(MSX_DEPOSITS,0))  
+ABS(ISNULL(NSEMF_DEPOSITS,0))  
+ABS(ISNULL(BSEMF_DEPOSITS,0))  
+ABS(ISNULL(NSESLBS_DEPOSITS,0))  
+ABS(ISNULL(BSESLBS_DEPOSITS,0))  
+ABS(ISNULL(CM_ISSUE,0))  
+ABS(ISNULL(BSECM_ISSUE,0))  
+ABS(ISNULL(FO_ISSUE,0))  
+ABS(ISNULL(NSX_ISSUE,0))  
+ABS(ISNULL(BFO_ISSUE,0))  
+ABS(ISNULL(BSX_ISSUE,0))  
+ABS(ISNULL(MFO_ISSUE,0))  
+ABS(ISNULL(MSX_ISSUE,0))  
+ABS(ISNULL(NSEMF_ISSUE,0))  
+ABS(ISNULL(BSEMF_ISSUE,0))  
+ABS(ISNULL(NSESLBS_ISSUE,0)) 
+ABS(ISNULL(BSESLBS_ISSUE,0))  
+ABS(ISNULL(NET_BALANCE,0))  
+ABS(ISNULL(NSECM_MARGIN,0))  
+ABS(ISNULL(BSECM_MARGIN,0))  
+ABS(ISNULL(FO_MARGIN,0))  
+ABS(ISNULL(NSX_MARGIN,0))  
+ABS(ISNULL(BFO_MARGIN,0))  
+ABS(ISNULL(BSX_MARGIN,0))  
+ABS(ISNULL(MFO_MARGIN,0))  
+ABS(ISNULL(MSX_MARGIN,0))  
+ABS(ISNULL(NSESLBS_MARGIN,0))  
+ABS(ISNULL(BSESLBS_MARGIN,0))  
) > 0     
ORDER BY PARTY_CODE    
        
DROP TABLE #DATA        
DROP TABLE #LEDDATA

GO
