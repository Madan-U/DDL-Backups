-- Object: PROCEDURE dbo.CL_MARGIN
-- Server: 10.253.33.91 | DB: ACCOUNT
--------------------------------------------------

		     
			
			CREATE PROC CL_MARGIN (@PARTY_CODE VARCHAR(10)) AS
			
			 DECLARE @FROMDATE DATETIME 
			 SET @FROMDATE = (SELECT TOP 1 (margindate) FROM MSAJAG.DBO.TBL_COMBINE_REPORTING  ORDER BY MARGINDATE DESC)
			 
			  SELECT * INTO #TBL_COMBINE_REPORTING  
			  FROM  MSAJAG.DBO.TBL_COMBINE_REPORTING  WHERE  margindate = @FROMDATE and PARTY_CODE =@party_code 

			   SELECT PARTY_CODE,IM= TOTAL_MARGIN-MTOM_LOSS-ADDITIONAL_MARGIN ,MTOM =MTOM_LOSS+ADDITIONAL_MARGIN INTO #CASH
			   FROM MSAJAG.DBO.TBL_CMMARGIN WHERE MARGINDATE  = @FROMDATE  and PARTY_CODE = @party_code


			   UPDATE  T SET TDAY_MARGIN_SHORT = IM+TDAY_MARGIN_SHORT ,TDAY_MTM_SHORT=TDAY_MTM_SHORT+MTOM   
			   FROM #CASH C,#TBL_COMBINE_REPORTING T
				WHERE C.PARTY_CODE=T.PARTY_CODE 

				SELECT * FROM (
				SELECT PARTY_CODE = @PARTY_CODE, SHORTATGE=ROUND(Sum((CASE WHEN TDAY_MARGIN_SHORT <0 THEN TDAY_MARGIN_SHORT ELSE 0 END)+
				(CASE WHEN  TDAY_MTM_SHORT<0 THEN TDAY_MTM_SHORT ELSE 0 END)),2)
				FROM #TBL_COMBINE_REPORTING)A
				WHERE SHORTATGE<0

GO
