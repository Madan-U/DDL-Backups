TB_EDT_AMOUNT=ROUND(SUM(CLASS_LEDGER),2),WER_EDT_AMOUNT=SUM(SCFM_BALANCE),CCE_EDT_AMOUNT=SUM(CASH_EDT_BAL) INTO #DIFF_DATA  
FROM (  
SELECT UCC,CLASS_LEDGER=CONVERT(MONEY,0),SCFM_BALANCE=CONVERT(MONEY,0),CASH_EDT_BAL=EDT_BAL FROM #CASHREPORTING  
UNION ALL  
SELECT CLTCODE,CLASS_LEDGER=CONVERT(MONEY,0),SCFM_BALANCE=BALANCE,CASH_EDT_BAL=CONVERT(MONEY,0) FROM #SCFM   
UNION ALL  
SELECT CLTCODE,CLASS_LEDGER=TB_EDT,SCFM_BALANCE=CONVERT(MONEY,0),CASH_EDT_BAL=CONVERT(MONEY,0) FROM #TBEDT ) A  
GROUP BY UCC  
  
TRUNCATE TABLE SFCM_DIFF_DATA_NEW  
  
  
INSERT INTO SFCM_DIFF_DATA_NEW  
SELECT * FROM #DIFF_DATA  
WHERE (ABS(TB_EDT_AMOUNT) <> ABS(CCE_EDT_AMOUNT) OR ABS(TB_EDT_AMOUNT) <> ABS(WER_EDT_AMOUNT) OR  ABS(CCE_EDT_AMOUNT) <>  ABS(WER_EDT_AMOUNT))  

