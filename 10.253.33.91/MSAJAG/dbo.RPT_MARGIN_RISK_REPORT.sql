SELECT REPORT_DATE,UCC,EDT_BAL=SUM(EDT_BAL),MARGIN=SUM(MARGIN),MTF_AVALABLECASH=SUM(MTF_AVALABLECASH),EPN=SUM(EPN),
NONCASH_COLL=SUM(NONCASH_COLL),REPLEDGE_CM=SUM(REPLEDGE_CM),REPLEDGE_CC=SUM(REPLEDGE_CC),
BROKERAGE=SUM(BROKERAGE) INTO #COMBILE_ALLP
FROM #REPORTINGP GROUP BY REPORT_DATE,UCC


SELECT REPORT_DATE,UCC,EDT_BAL,MARGIN,TOTAL_CASH=EDT_BAL,
NONCASH_COLL,REPLEDGE_CM,REPLEDGE_CC,FREE_HOLDING=CONVERT(MONEY,0),NONCASH_TOTAL=CONVERT(MONEY,0),
CASE WHEN ((EDT_BAL+MTF_AVALABLECASH)/MARGIN)*100 > 100 THEN 100
	 WHEN ((EDT_BAL+MTF_AVALABLECASH)/MARGIN)*100 BETWEEN 0 AND 100 THEN ((EDT_BAL+MTF_AVALABLECASH)/MARGIN)*100
     WHEN ((EDT_BAL+MTF_AVALABLECASH)/MARGIN)*100 < 0 THEN 0 END AS AVALABLECASH_PER,AVALABLECASH_PER_RATIO=CONVERT(VARCHAR(10),''),
ISNULL(CASE WHEN  (((NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC)/MARGIN)*100) > 100 THEN 100
	 WHEN  (((NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC)/MARGIN)*100) < 100 THEN (((NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC)/MARGIN)*100)
     WHEN (((NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC)/MARGIN)*100) < 0 THEN 0 END,0)  AS NONCASH_PER,
	 NONCASH_PER_RATIO=CONVERT(VARCHAR(10),''),RATIO =CONVERT(VARCHAR(10),'') ,
	 CASH_SHORTAGE=CONVERT(MONEY,0),CASH_SURPLACE=CONVERT(MONEY,0),
	 NONCASH_SHORTAGE=CONVERT(MONEY,0),NONCASH_SURPLUSE=CONVERT(MONEY,0),
	 NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST=CONVERT(MONEY,0),EPN,MARGIN_SHORTAGE=CONVERT(MONEY,0),
	 CASH_SHORTAGE_WITH_EPN=CONVERT(MONEY,0),CASH_SURPLUS_WITH_EPN=CONVERT(MONEY,0),
	 NONCASH_SHORTAGE_WITH_EPN=CONVERT(MONEY,0),NONCASH_SHORTAGE_FREE_STOCK=CONVERT(MONEY,0),
	 TOTAL_SHORTAGE=CONVERT(MONEY,0),TOTAL_SHORTAGE_FREE_STOCK=CONVERT(MONEY,0),
	 MARGIN_BUCKET=CONVERT(VARCHAR(100),''),BROKERAGE
	 INTO #FINAL_REPORTP
