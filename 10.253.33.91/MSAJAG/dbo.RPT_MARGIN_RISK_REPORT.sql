-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[RPT_MARGIN_RISK_REPORT] (@RUNDATE VARCHAR(11),@REPORT VARCHAR(5))

AS --- EXEC RPT_MARGIN_RISK_REPORT 'FEB 23 2022','EOD'

IF @REPORT = 'EOD'
BEGIN

SELECT REPORT_DATE,SNO INTO #CCE FROM ACCOUNT.DBO.CLIENT_CASH_DETAILS (NOLOCK) 
WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'

SELECT REPORT_DATE,UCC=LTRIM(RTRIM(ISNULL(PARTY_CODE, ''))),EDT_BAL = SUM(EDT_BAL), 
MARGIN = CONVERT(MONEY,0),MTF_AVALABLECASH = CONVERT(MONEY,0),
NONCASH_COLL= CONVERT(MONEY,0),REPLEDGE_CM= CONVERT(MONEY,0),
REPLEDGE_CC= CONVERT(MONEY,0),EPN = CONVERT(MONEY,0),
BROKERAGE = CONVERT(MONEY,0) into #REPORTING
FROM #CCE C , ACCOUNT.DBO.CLIENT_CASH_LED_BALANCES L WITH (NOLOCK)
WHERE C.SNO = L.SNO AND PARTY_CODE BETWEEN 'A' AND 'ZZZZZZZZZZZZZZZZZ999999'
AND L.EXCHANGE<>'MTF'
GROUP BY REPORT_DATE,PARTY_CODE

----MTF AVALABLE CASH
SELECT PARTY_CODE,AVALABLECASH=CASE WHEN SUM(NETAMT)<0 THEN 0 ELSE  SUM(NETAMT) END
INTO #MTF_AVALABLECASH 
FROM (
SELECT  PARTY_CODE,
NETAMT = SUM(NETAMT)
FROM MTFTRADE.DBO.TBL_MTF_DATA WITH (NOLOCK) WHERE SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' 
GROUP BY PARTY_CODE
UNION ALL
SELECT  DISTINCT PARTY_CODE,MTFLEDBAL
FROM MTFTRADE.DBO.TBL_MTF_DATA WITH (NOLOCK) WHERE SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' ) A
GROUP BY A.PARTY_CODE

UPDATE A SET EDT_BAL = EDT_BAL + AVALABLECASH FROM #REPORTING A, #MTF_AVALABLECASH B WHERE A.UCC = B.PARTY_CODE

UPDATE #REPORTING SET MTF_AVALABLECASH = AVALABLECASH FROM #REPORTING A , #MTF_AVALABLECASH B WHERE A.UCC = B.PARTY_CODE


/* MARGIN UPDATE */  

SELECT PARTY_CODE,SUM(EXTREME_LOSS_MARGIN) AS ALL_MARGIN,SEGMENT='CM'  INTO #ALLMARGIN
FROM TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' GROUP BY PARTY_CODE

INSERT INTO #ALLMARGIN
SELECT PARTY_CODE,SUM(TOTALMARGIN-MTOMLOSS) AS FNO_MARGIN,SEGMENT='FO' 
FROM ANGELFO.NSEFO.DBO.FOMARGINNEW WITH (NOLOCK) 
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' group BY PARTY_CODE

INSERT INTO #ALLMARGIN
SELECT PARTY_CODE,SUM(TOTALMARGIN-MTOMLOSS) AS CDS_MARGIN,SEGMENT='CD'
FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW WITH (NOLOCK) 
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' GROUP BY PARTY_CODE

INSERT INTO #ALLMARGIN
SELECT PARTY_CODE,MCX=sum(INITIALMARGIN),SEGMENT='CO'
from ANGELCOMMODITY.MCDX.DBO.TBL_CLIENTMARGIN WITH (NOLOCK)
WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' GROUP BY PARTY_CODE

INSERT INTO #ALLMARGIN
select PARTY_CODE,NCX=sum(INITIALMARGIN),SEGMENT='CO'
from ANGELCOMMODITY.NCDX.DBO.TBL_CLIENTMARGIN WITH (NOLOCK)
WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' GROUP BY PARTY_CODE

DELETE FROM #ALLMARGIN WHERE PARTY_CODE='12798'

UPDATE A SET MARGIN = ALL_MARGIN FROM #REPORTING A,
(SELECT PARTY_CODE,ALL_MARGIN=SUM(ALL_MARGIN) FROM #ALLMARGIN GROUP BY PARTY_CODE)B
WHERE A.UCC = B.PARTY_CODE


/* NONCASH UPDATE */

UPDATE A SET NONCASH_COLL = NONCASH_POST_HAIRCUT FROM #REPORTING A,
(SELECT MARGINDATE,PARTY_CODE,NONCASH_POST_HAIRCUT=SUM(TDAY_NONCASH) 
FROM MSAJAG.DBO.TBL_COMBINE_REPORTING_DETAIL WITH (NOLOCK)
WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY MARGINDATE,PARTY_CODE,EXCHANGE,SEGMENT
HAVING SUM(TDAY_NONCASH) <> 0) B WHERE A.UCC = B.PARTY_CODE

/* EPN UPDATE */

UPDATE A SET A.EPN = MARGEPN FROM #REPORTING A,
(SELECT MARGINDATE,PARTY_CODE,MARGEPN=SUM(TDAY_CASHCOLL)  
FROM MSAJAG.DBO.TBL_COMBINE_REPORTING_DETAIL WITH (NOLOCK)
WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' AND EXCHANGE='EPN'
GROUP BY MARGINDATE,PARTY_CODE) B WHERE A.UCC = B.PARTY_CODE 


/* BROKERAGE INCOME */

---NSECM
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,
BROKERAGE = SUM(PBROKTRD+SBROKTRD+PBROKDEL+SBROKDEL) INTO #BROKERAGE
FROM MSAJAG.DBO.CMBILLVALAN S WITH (NOLOCK) where S.SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

---BSECM
INSERT INTO #BROKERAGE
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,BROKERAGE = SUM(PBROKTRD+SBROKTRD+PBROKDEL+SBROKDEL) 
FROM AngelBSECM.BSEDB_AB.DBO.CMBILLVALAN S WITH (NOLOCK) WHERE S.SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

---NSEFO
INSERT INTO #BROKERAGE
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,BROKERAGE =SUM(PBROKAMT+SBROKAMT)
FROM ANGELFO.NSEFO.DBO.FOBILLVALAN WITH (NOLOCK) WHERE TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' 
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

---NSECD
INSERT INTO #BROKERAGE
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,BROKERAGE =SUM(PBROKAMT+SBROKAMT)
FROM ANGELFO.NSECURFO.DBO.FOBILLVALAN WITH (NOLOCK) where  TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

---MCDX
INSERT INTO #BROKERAGE
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,BROKERAGE =SUM(PBROKAMT+SBROKAMT)
FROM ANGELCOMMODITY.MCDX.DBO.FOBILLVALAN WITH (NOLOCK) WHERE  TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

---NCDX
INSERT INTO #BROKERAGE
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,BROKERAGE =SUM(PBROKAMT+SBROKAMT)
FROM ANGELCOMMODITY.NCDX.DBO.FOBILLVALAN WITH (NOLOCK) where  TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

UPDATE A SET A.BROKERAGE = BROK FROM #REPORTING A,
(SELECT PARTY_CODE,BROK=SUM(BROKERAGE) FROM #BROKERAGE GROUP BY PARTY_CODE) B
where A.UCC = B.PARTY_CODE


/* FINAL REPORT */

SELECT REPORT_DATE,UCC,EDT_BAL=SUM(EDT_BAL),MARGIN=SUM(MARGIN),MTF_AVALABLECASH=SUM(MTF_AVALABLECASH),EPN=SUM(EPN),
NONCASH_COLL=SUM(NONCASH_COLL),REPLEDGE_CM=SUM(REPLEDGE_CM),REPLEDGE_CC=SUM(REPLEDGE_CC),
BROKERAGE=SUM(BROKERAGE) INTO #COMBILE_ALL
FROM #REPORTING GROUP BY REPORT_DATE,UCC

SELECT REPORT_DATE,UCC,EDT_BAL,MARGIN,TOTAL_CASH=EDT_BAL,
NONCASH_COLL,REPLEDGE_CM,REPLEDGE_CC,FREE_HOLDING=CONVERT(MONEY,0),NONCASH_TOTAL=CONVERT(MONEY,0),
CASE WHEN ((EDT_BAL+MTF_AVALABLECASH)/MARGIN)*100 > 100 THEN 100
	 WHEN ((EDT_BAL+MTF_AVALABLECASH)/MARGIN)*100 BETWEEN 0 AND 100 THEN ((EDT_BAL+MTF_AVALABLECASH)/MARGIN)*100
     WHEN ((EDT_BAL+MTF_AVALABLECASH)/MARGIN)*100 < 0 THEN 0 END AS AVALABLECASH_PER,AVALABLECASH_PER_RATIO=CONVERT(VARCHAR(10),''),
ISNULL(CASE WHEN  (((NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC)/MARGIN)*100) > 100 THEN 100
	 WHEN  (((NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC)/MARGIN)*100) < 100 THEN (((NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC)/MARGIN)*100)
     WHEN (((NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC)/MARGIN)*100) < 0 THEN 0 END,0)  AS NONCASH_PER,
	 NONCASH_PER_RATIO=CONVERT(VARCHAR(10),''),RATIO =CONVERT(VARCHAR(10),'') ,
	 CASH_SHORTAGE=CONVERT(MONEY,0),CASH_SURPLACE=CONVERT(MONEY,0),
	 NONCASH_SHORTAGE=CONVERT(MONEY,0),NONCASH_SURPLUSE=CONVERT(MONEY,0),
	 NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST=CONVERT(MONEY,0),EPN,MARGIN_SHORTAGE=CONVERT(MONEY,0),
	 CASH_SHORTAGE_WITH_EPN=CONVERT(MONEY,0),CASH_SURPLUS_WITH_EPN=CONVERT(MONEY,0),
	 NONCASH_SHORTAGE_WITH_EPN=CONVERT(MONEY,0),NONCASH_SHORTAGE_FREE_STOCK=CONVERT(MONEY,0),
	 TOTAL_SHORTAGE=CONVERT(MONEY,0),TOTAL_SHORTAGE_FREE_STOCK=CONVERT(MONEY,0),
	 MARGIN_BUCKET=CONVERT(VARCHAR(100),''),BROKERAGE
	 INTO #FINAL_REPORT
FROM #COMBILE_ALL WHERE MARGIN > 0


UPDATE A SET AVALABLECASH_PER_RATIO = APR FROM #FINAL_REPORT A,
(SELECT REPORT_DATE,UCC,
CASE WHEN AVALABLECASH_PER BETWEEN 0 AND 10 THEN 10
	 WHEN AVALABLECASH_PER BETWEEN 10.01 AND 20 THEN 20
	 WHEN AVALABLECASH_PER BETWEEN 20.01 AND 30 THEN 30
	 WHEN AVALABLECASH_PER BETWEEN 30.01 AND 40 THEN 40
	 WHEN AVALABLECASH_PER BETWEEN 40.01 AND 50 THEN 50
	 WHEN AVALABLECASH_PER BETWEEN 50.01 AND 60 THEN 60
	 WHEN AVALABLECASH_PER BETWEEN 60.01 AND 70 THEN 70
	 WHEN AVALABLECASH_PER BETWEEN 70.01 AND 80 THEN 80
	 WHEN AVALABLECASH_PER BETWEEN 80.01 AND 90 THEN 90
	 WHEN AVALABLECASH_PER BETWEEN 90.01 AND 100 THEN 100 END AS APR
FROM #FINAL_REPORT ) B WHERE A.UCC = B.UCC


 UPDATE A SET NONCASH_PER_RATIO = NCR FROM #FINAL_REPORT A,
(SELECT REPORT_DATE,UCC,
CASE WHEN NONCASH_PER BETWEEN 0 AND 10 THEN 10
	 WHEN NONCASH_PER BETWEEN 10.01 AND 20 THEN 20
	 WHEN NONCASH_PER BETWEEN 20.01 AND 30 THEN 30
	 WHEN NONCASH_PER BETWEEN 30.01 AND 40 THEN 40
	 WHEN NONCASH_PER BETWEEN 40.01 AND 50 THEN 50
	 WHEN NONCASH_PER BETWEEN 50.01 AND 60 THEN 60
	 WHEN NONCASH_PER BETWEEN 60.01 AND 70 THEN 70
	 WHEN NONCASH_PER BETWEEN 70.01 AND 80 THEN 80
	 WHEN NONCASH_PER BETWEEN 80.01 AND 90 THEN 90
	 WHEN NONCASH_PER BETWEEN 90.01 AND 100 THEN 100 END AS NCR
FROM #FINAL_REPORT ) B WHERE A.UCC = B.UCC

UPDATE #FINAL_REPORT SET RATIO = AVALABLECASH_PER_RATIO + ':' + NONCASH_PER_RATIO

UPDATE #FINAL_REPORT SET NONCASH_TOTAL = NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC ;

UPDATE #FINAL_REPORT SET CASH_SHORTAGE = CAST(CASE WHEN ((MARGIN*0.5)-TOTAL_CASH) < = 0 THEN 0 
												  ELSE  ((MARGIN*0.5)-TOTAL_CASH) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORT SET CASH_SURPLACE = CAST(CASE WHEN ((MARGIN*0.5)-TOTAL_CASH) >  0 THEN 0 
												  ELSE  ((MARGIN*0.5)-TOTAL_CASH) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORT SET NONCASH_SHORTAGE = CAST(CASE WHEN ((MARGIN*0.5)-NONCASH_TOTAL) < = 0 THEN 0 
												  ELSE  ((MARGIN*0.5)-NONCASH_TOTAL) END AS NUMERIC(18,0)) 

UPDATE #FINAL_REPORT SET NONCASH_SURPLUSE = CAST(CASE WHEN ((MARGIN*0.5)-NONCASH_TOTAL) > 0 THEN 0 
												  ELSE  ((MARGIN*0.5)-NONCASH_TOTAL) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORT SET NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST = CAST(CASE WHEN (CASH_SURPLACE + NONCASH_SHORTAGE) < 0 THEN 0
																	  ELSE (CASH_SURPLACE + NONCASH_SHORTAGE) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORT SET MARGIN_SHORTAGE = CAST(CASE WHEN (MARGIN -(TOTAL_CASH+NONCASH_TOTAL+EPN)) < 0 THEN 0
													 ELSE (MARGIN -(TOTAL_CASH+NONCASH_TOTAL+EPN)) END AS NUMERIC(18,0)) 

UPDATE #FINAL_REPORT SET CASH_SHORTAGE_WITH_EPN = CAST(CASE WHEN ((MARGIN*0.5)-(TOTAL_CASH+EPN)) < = 0 THEN 0 
												  ELSE  ((MARGIN*0.5)-(TOTAL_CASH+EPN)) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORT SET CASH_SURPLUS_WITH_EPN = CAST(CASE WHEN ((MARGIN*0.5)-(TOTAL_CASH+EPN)) > 0 THEN 0 
												           ELSE ((MARGIN*0.5)-(TOTAL_CASH+EPN)) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORT SET NONCASH_SHORTAGE_WITH_EPN = CAST(CASE WHEN (CASH_SURPLUS_WITH_EPN + NONCASH_SHORTAGE) < 0 THEN 0
																	  ELSE (CASH_SURPLUS_WITH_EPN + NONCASH_SHORTAGE) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORT SET NONCASH_SHORTAGE_FREE_STOCK = CASE WHEN (NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST-FREE_HOLDING) < 0 THEN 0 ELSE (NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST-FREE_HOLDING) END

UPDATE #FINAL_REPORT SET TOTAL_SHORTAGE = CASH_SHORTAGE + NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST

UPDATE #FINAL_REPORT SET TOTAL_SHORTAGE_FREE_STOCK = CASH_SHORTAGE + NONCASH_SHORTAGE_FREE_STOCK

UPDATE A SET A.MARGIN_BUCKET = CASE 
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '0' AND '200000' THEN 'UP TO 2 LACS'
when (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '200001' AND '500000' THEN 'UP TO 5 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '500001' AND '1000000' THEN 'UP TO 10 LACS'
when (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '1000001' AND '1500000' THEN 'UP TO 15 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '1500001' AND '2500000' THEN 'UP TO 25 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '2500001' AND '5000000' THEN 'UP TO 50 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '5000001' AND '7500000' THEN 'UP TO 75 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '7500001' AND '10000000' THEN 'UP TO 1 CR'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '10000001' AND '30000000' THEN 'UP TO 3 CR'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORT B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '30000001' AND '990000000' THEN 'UP TO 3 CR PLUS'
ELSE '-' END
FROM #FINAL_REPORT A

SELECT [Report Date]=CONVERT(VARCHAR(11),REPORT_DATE,105),[Report Name]='EOD-Margin',Margin_Bucket,
Margin=cast(sum(MARGIN)/10000000 as numeric(18,0)),
[Total Shortage]=cast(sum(TOTAL_SHORTAGE)/10000000 as numeric(18,0)),
[Shortage %] = cast(sum(TOTAL_SHORTAGE)*100/(select SUM(TOTAL_SHORTAGE) FROM #FINAL_REPORT) as numeric(18,0)),
UCC=COUNT(UCC),[Brokerage In Lacs]=ROUND(SUM(BROKERAGE)/100000,2) INTO #FREPORT
FROM #FINAL_REPORT 
GROUP BY REPORT_DATE,MARGIN_BUCKET
UNION ALL
SELECT '	','	',MARGIN_BUCKET='Total',MARGIN=cast(sum(MARGIN)/10000000 as numeric(18,0)),
TOTAL_SHORTAGE=cast(sum(TOTAL_SHORTAGE)/10000000 as numeric(18,0)),
[SHORTAGE %] = cast(sum(TOTAL_SHORTAGE)*100/(select SUM(TOTAL_SHORTAGE) FROM #FINAL_REPORT) as numeric(18,0)),
UCC=COUNT(UCC),BROKERAGE=ROUND(SUM(BROKERAGE)/100000,2)
FROM #FINAL_REPORT

SELECT 	* FROM #FREPORT
ORDER BY CASE WHEN MARGIN_BUCKET='UP TO 2 LACS' THEN 1 WHEN MARGIN_BUCKET='UP TO 5 LACS' THEN 2 WHEN MARGIN_BUCKET='UP TO 10 LACS' THEN 3
WHEN MARGIN_BUCKET='UP TO 15 LACS' THEN 4 WHEN MARGIN_BUCKET='UP TO 25 LACS' THEN 5 WHEN MARGIN_BUCKET='UP TO 50 LACS' THEN 6
WHEN MARGIN_BUCKET='UP TO 75 LACS' THEN 7 WHEN MARGIN_BUCKET='UP TO 1 CR' THEN 8 WHEN MARGIN_BUCKET='UP TO 3 CR' THEN 9
WHEN MARGIN_BUCKET='UP TO 3 CR PLUS' THEN 10 WHEN MARGIN_BUCKET='Total' THEN 11 END
END

IF @REPORT='PEAK'
BEGIN
SELECT REPORT_DATE,SNO INTO #CCEP FROM ACCOUNT.DBO.CLIENT_CASH_DETAILS (NOLOCK) 
WHERE REPORT_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'

SELECT REPORT_DATE,UCC=LTRIM(RTRIM(ISNULL(PARTY_CODE, ''))),EDT_BAL = SUM(EDT_BAL), 
MARGIN = CONVERT(MONEY,0),MTF_AVALABLECASH = CONVERT(MONEY,0),
NONCASH_COLL= CONVERT(MONEY,0),REPLEDGE_CM= CONVERT(MONEY,0),
REPLEDGE_CC= CONVERT(MONEY,0),EPN = CONVERT(MONEY,0),
BROKERAGE = CONVERT(MONEY,0) into #REPORTINGP
FROM #CCEP C , ACCOUNT.DBO.CLIENT_CASH_LED_BALANCES L WITH (NOLOCK)
WHERE C.SNO = L.SNO AND PARTY_CODE BETWEEN 'A' AND 'ZZZZZZZZZZZZZZZZZ999999'
AND L.EXCHANGE<>'MTF'
GROUP BY REPORT_DATE,PARTY_CODE


----MTF AVALABLE CASH
SELECT PARTY_CODE,AVALABLECASH=CASE WHEN SUM(NETAMT)<0 THEN 0 ELSE  SUM(NETAMT) END
INTO #MTF_AVALABLECASHP 
FROM (
SELECT  PARTY_CODE,
NETAMT = SUM(NETAMT)
FROM MTFTRADE.DBO.TBL_MTF_DATA WITH (NOLOCK) WHERE SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' 
GROUP BY PARTY_CODE
UNION ALL
SELECT  DISTINCT PARTY_CODE,MTFLEDBAL
FROM MTFTRADE.DBO.TBL_MTF_DATA WITH (NOLOCK) WHERE SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' ) A
GROUP BY A.PARTY_CODE

UPDATE A SET EDT_BAL = EDT_BAL + AVALABLECASH 
FROM #REPORTINGP A, #MTF_AVALABLECASHP B WHERE A.UCC = B.PARTY_CODE

UPDATE #REPORTINGP SET MTF_AVALABLECASH = AVALABLECASH FROM #REPORTINGP A , #MTF_AVALABLECASHP B 
WHERE A.UCC = B.PARTY_CODE


/* MARGIN UPDATE */

SELECT PARTY_CODE,
(SELECT MAX(NSECM) FROM ( VALUES (EXTREME_LOSS_MARGIN),(PEAKMARGIN)) AS NSECM (NSECM)) AS ALL_MARGIN,
EXCHANGE='CM' INTO #ALLMARGINP
FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'

INSERT INTO #ALLMARGINP
SELECT PARTY_CODE,
(SELECT MAX(NSEFNO) FROM ( VALUES (TOTALMARGIN-MTOMLOSS),(PEAKMARGIN)) AS NSEFNO (NSEFNO)) AS FNO_MARGIN,
EXCHANGE='FO'
FROM ANGELFO.NSEFO.DBO.FOMARGINNEW WITH (NOLOCK) 
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'


INSERT INTO #ALLMARGINP
SELECT PARTY_CODE,
(SELECT CAST(MAX(NSECD) AS NUMERIC(18,2)) FROM ( VALUES (TOTALMARGIN-MTOMLOSS),(PEAKMARGIN)) AS NSECD (NSECD)) AS CDS_MARGIN,
EXCHANGE='CD'
FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW WITH (NOLOCK) 
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'

INSERT INTO #ALLMARGINP
SELECT PARTY_CODE,
(SELECT CAST(MAX(COM) AS NUMERIC(18,2)) FROM ( VALUES (SUM(COM_MARGIN)),(SUM(PEAKMARGIN))) AS COM (COM)) AS COM_MARGIN,
EXCHANGE='CO'
FROM (SELECT PARTY_CODE,COM_MARGIN=REGULARMARGIN,PEAKMARGIN
FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW_DATA WITH (NOLOCK) 
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
UNION ALL
SELECT PARTY_CODE,COM_MARGIN=INITIALMARGIN,PEAKMARGIN
FROM ANGELCOMMODITY.NCDX.DBO.FOMARGINNEW_DATA WITH (NOLOCK)
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59')B 
GROUP BY B.PARTY_CODE

DELETE FROM #ALLMARGINP WHERE PARTY_CODE='12798'

UPDATE A SET MARGIN = ALL_MARGIN FROM #REPORTINGP A,
(SELECT PARTY_CODE,ALL_MARGIN=SUM(ALL_MARGIN) FROM #ALLMARGINP GROUP BY PARTY_CODE)B
WHERE A.UCC = B.PARTY_CODE


/* NONCASH UPDATE */

UPDATE A SET NONCASH_COLL = NONCASH_POST_HAIRCUT FROM #REPORTINGP A,
(SELECT MARGINDATE,PARTY_CODE,NONCASH_POST_HAIRCUT=SUM(TDAY_NONCASH) 
FROM MSAJAG.DBO.TBL_COMBINE_REPORTING_DETAIL WITH (NOLOCK)
WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY MARGINDATE,PARTY_CODE,EXCHANGE,SEGMENT
HAVING SUM(TDAY_NONCASH) <> 0) B WHERE A.UCC = B.PARTY_CODE

/* EPN UPDATE */

UPDATE A SET A.EPN = MARGEPN FROM #REPORTINGP A,
(SELECT MARGINDATE,PARTY_CODE,MARGEPN=SUM(TDAY_CASHCOLL)  
FROM MSAJAG.DBO.TBL_COMBINE_REPORTING_DETAIL WITH (NOLOCK)
WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' AND EXCHANGE='EPN'
GROUP BY MARGINDATE,PARTY_CODE) B WHERE A.UCC = B.PARTY_CODE 


/* BROKERAGE INCOME */ -- DROP TABLE #BROKERAGE

---NSECM
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,
BROKERAGE = SUM(PBROKTRD+SBROKTRD+PBROKDEL+SBROKDEL) INTO #BROKERAGEP
FROM MSAJAG.DBO.CMBILLVALAN S WITH (NOLOCK) where S.SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

---BSECM
INSERT INTO #BROKERAGEP
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,BROKERAGE = SUM(PBROKTRD+SBROKTRD+PBROKDEL+SBROKDEL) 
FROM AngelBSECM.BSEDB_AB.DBO.CMBILLVALAN S WITH (NOLOCK) WHERE S.SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

---NSEFO
INSERT INTO #BROKERAGEP
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE,BROKERAGE =SUM(PBROKAMT+SBROKAMT)
FROM ANGELFO.NSEFO.DBO.FOBILLVALAN WITH (NOLOCK) WHERE TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' 
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105),PARTY_CODE

---NSECD
