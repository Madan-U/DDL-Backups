UPDATE A SET AVALABLECASH_PER_RATIO = APR FROM #FINAL_REPORTP A,
(SELECT REPORT_DATE,UCC,
CASE WHEN AVALABLECASH_PER BETWEEN 0 AND 10 THEN 10
	 WHEN AVALABLECASH_PER BETWEEN 10.01 AND 20 THEN 20
	 WHEN AVALABLECASH_PER BETWEEN 20.01 AND 30 THEN 30
	 WHEN AVALABLECASH_PER BETWEEN 30.01 AND 40 THEN 40
	 WHEN AVALABLECASH_PER BETWEEN 40.01 AND 50 THEN 50
	 WHEN AVALABLECASH_PER BETWEEN 50.01 AND 60 THEN 60
	 WHEN AVALABLECASH_PER BETWEEN 60.01 AND 70 THEN 70
	 WHEN AVALABLECASH_PER BETWEEN 70.01 AND 80 THEN 80
	 WHEN AVALABLECASH_PER BETWEEN 80.01 AND 90 THEN 90
	 WHEN AVALABLECASH_PER BETWEEN 90.01 AND 100 THEN 100 END AS APR
FROM #FINAL_REPORTP ) B WHERE A.UCC = B.UCC


 UPDATE A SET NONCASH_PER_RATIO = NCR FROM #FINAL_REPORTP A,
(SELECT REPORT_DATE,UCC,
CASE WHEN NONCASH_PER BETWEEN 0 AND 10 THEN 10
	 WHEN NONCASH_PER BETWEEN 10.01 AND 20 THEN 20
	 WHEN NONCASH_PER BETWEEN 20.01 AND 30 THEN 30
	 WHEN NONCASH_PER BETWEEN 30.01 AND 40 THEN 40
	 WHEN NONCASH_PER BETWEEN 40.01 AND 50 THEN 50
	 WHEN NONCASH_PER BETWEEN 50.01 AND 60 THEN 60
	 WHEN NONCASH_PER BETWEEN 60.01 AND 70 THEN 70
	 WHEN NONCASH_PER BETWEEN 70.01 AND 80 THEN 80
	 WHEN NONCASH_PER BETWEEN 80.01 AND 90 THEN 90
	 WHEN NONCASH_PER BETWEEN 90.01 AND 100 THEN 100 END AS NCR
FROM #FINAL_REPORTP ) B WHERE A.UCC = B.UCC

UPDATE #FINAL_REPORTP SET RATIO = AVALABLECASH_PER_RATIO + ':' + NONCASH_PER_RATIO

UPDATE #FINAL_REPORTP SET NONCASH_TOTAL = NONCASH_COLL+REPLEDGE_CM+REPLEDGE_CC ;

UPDATE #FINAL_REPORTP SET CASH_SHORTAGE = CAST(CASE WHEN ((MARGIN*0.5)-TOTAL_CASH) < = 0 THEN 0 
												  ELSE  ((MARGIN*0.5)-TOTAL_CASH) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORTP SET CASH_SURPLACE = CAST(CASE WHEN ((MARGIN*0.5)-TOTAL_CASH) >  0 THEN 0 
												  ELSE  ((MARGIN*0.5)-TOTAL_CASH) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORTP SET NONCASH_SHORTAGE = CAST(CASE WHEN ((MARGIN*0.5)-NONCASH_TOTAL) < = 0 THEN 0 
												  ELSE  ((MARGIN*0.5)-NONCASH_TOTAL) END AS NUMERIC(18,0)) 

UPDATE #FINAL_REPORTP SET NONCASH_SURPLUSE = CAST(CASE WHEN ((MARGIN*0.5)-NONCASH_TOTAL) > 0 THEN 0 
												  ELSE  ((MARGIN*0.5)-NONCASH_TOTAL) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORTP SET NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST = CAST(CASE WHEN (CASH_SURPLACE + NONCASH_SHORTAGE) < 0 THEN 0
																	  ELSE (CASH_SURPLACE + NONCASH_SHORTAGE) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORTP SET MARGIN_SHORTAGE = CAST(CASE WHEN (MARGIN -(TOTAL_CASH+NONCASH_TOTAL+EPN)) < 0 THEN 0
													 ELSE (MARGIN -(TOTAL_CASH+NONCASH_TOTAL+EPN)) END AS NUMERIC(18,0)) 

UPDATE #FINAL_REPORTP SET CASH_SHORTAGE_WITH_EPN = CAST(CASE WHEN ((MARGIN*0.5)-(TOTAL_CASH+EPN)) < = 0 THEN 0 
												  ELSE  ((MARGIN*0.5)-(TOTAL_CASH+EPN)) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORTP SET CASH_SURPLUS_WITH_EPN = CAST(CASE WHEN ((MARGIN*0.5)-(TOTAL_CASH+EPN)) > 0 THEN 0 
												           ELSE ((MARGIN*0.5)-(TOTAL_CASH+EPN)) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORTP SET NONCASH_SHORTAGE_WITH_EPN = CAST(CASE WHEN (CASH_SURPLUS_WITH_EPN + NONCASH_SHORTAGE) < 0 THEN 0
																	  ELSE (CASH_SURPLUS_WITH_EPN + NONCASH_SHORTAGE) END AS NUMERIC(18,0))

UPDATE #FINAL_REPORTP SET NONCASH_SHORTAGE_FREE_STOCK = CASE WHEN (NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST-FREE_HOLDING) < 0 THEN 0 ELSE (NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST-FREE_HOLDING) END

UPDATE #FINAL_REPORTP SET TOTAL_SHORTAGE = CASH_SHORTAGE + NONCASH_SHORTAGE_CASH_SURPLUSE_ADJUST

UPDATE #FINAL_REPORTP SET TOTAL_SHORTAGE_FREE_STOCK = CASH_SHORTAGE + NONCASH_SHORTAGE_FREE_STOCK

UPDATE A SET A.MARGIN_BUCKET = CASE 
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '0' AND '200000' THEN 'UP TO 2 LACS'
when (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '200001' AND '500000' THEN 'UP TO 5 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '500001' AND '1000000' THEN 'UP TO 10 LACS'
when (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '1000001' AND '1500000' THEN 'UP TO 15 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '1500001' AND '2500000' THEN 'UP TO 25 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '2500001' AND '5000000' THEN 'UP TO 50 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '5000001' AND '7500000' THEN 'UP TO 75 LACS'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '7500001' AND '10000000' THEN 'UP TO 1 CR'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '10000001' AND '30000000' THEN 'UP TO 3 CR'
WHEN (SELECT SUM(MARGIN) FROM #FINAL_REPORTP B WHERE A.UCC = B.UCC GROUP BY UCC ) 
BETWEEN '30000001' AND '990000000' THEN 'UP TO 3 CR PLUS'
ELSE '-' END
FROM #FINAL_REPORTP A

SELECT [Report Date]=CONVERT(VARCHAR(11),REPORT_DATE,105),[Report Name]='PEAK-Margin',Margin_Bucket,
Margin=cast(sum(MARGIN)/10000000 as numeric(18,0)),
[Total Shortage]=cast(sum(TOTAL_SHORTAGE)/10000000 as numeric(18,0)),
[Shortage %] = cast(sum(TOTAL_SHORTAGE)*100/(select SUM(TOTAL_SHORTAGE) FROM #FINAL_REPORTP) as numeric(18,0)),
UCC=COUNT(UCC),[Brokerage In Lacs]=ROUND(SUM(BROKERAGE)/100000,2) INTO #FREPORTP
FROM #FINAL_REPORTP 
GROUP BY REPORT_DATE,MARGIN_BUCKET
UNION ALL
SELECT '	','		',MARGIN_BUCKET='Total',MARGIN=cast(sum(MARGIN)/10000000 as numeric(18,0)),
TOTAL_SHORTAGE=cast(sum(TOTAL_SHORTAGE)/10000000 as numeric(18,0)),
[SHORTAGE %] = cast(sum(TOTAL_SHORTAGE)*100/(select SUM(TOTAL_SHORTAGE) FROM #FINAL_REPORTP) as numeric(18,0)),
UCC=COUNT(UCC),BROKERAGE=ROUND(SUM(BROKERAGE)/100000,2)
FROM #FINAL_REPORTP

SELECT 	* FROM #FREPORTP
ORDER BY CASE WHEN MARGIN_BUCKET='UP TO 2 LACS' THEN 1 WHEN MARGIN_BUCKET='UP TO 5 LACS' THEN 2 WHEN MARGIN_BUCKET='UP TO 10 LACS' THEN 3
WHEN MARGIN_BUCKET='UP TO 15 LACS' THEN 4 WHEN MARGIN_BUCKET='UP TO 25 LACS' THEN 5 WHEN MARGIN_BUCKET='UP TO 50 LACS' THEN 6
WHEN MARGIN_BUCKET='UP TO 75 LACS' THEN 7 WHEN MARGIN_BUCKET='UP TO 1 CR' THEN 8 WHEN MARGIN_BUCKET='UP TO 3 CR' THEN 9
WHEN MARGIN_BUCKET='UP TO 3 CR PLUS' THEN 10 WHEN MARGIN_BUCKET='Total' THEN 11 END
END

