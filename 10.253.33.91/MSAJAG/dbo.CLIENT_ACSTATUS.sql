SELECT PARTY_CODE,(CASE WHEN MAX(INACTIVE_FROM) > GETDATE() THEN 'YES' ELSE 'NO' END) AS INACTIVE INTO #MF FROM ( 
SELECT PARTY_CODE,INACTIVE_FROM FROM ANGELFO.NSEMFSS.DBO.MFSS_CLIENT WHERE PARTY_CODE=@CL_CODE
UNION
SELECT PARTY_CODE,INACTIVE_FROM FROM ANGELFO.BSEMFSS.DBO.MFSS_CLIENT WHERE PARTY_CODE=@CL_CODE)A
GROUP BY party_code


SELECT NISE_PARTY_CODE,(CASE WHEN STATUS ='ACTIVE' THEN  'YES' ELSE 'NO' END) AS CLOSE_DATE INTO #DP FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER B2,
AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_POA C
WHERE B2.CLIENT_CODE=c.CLIENT_CODE
AND NISE_PARTY_CODE=@CL_CODE AND MASTER_POA ='2203320000000014'
AND POA_VER='2'
 
 
UPDATE A SET MUTUAL_FUNDS=INACTIVE
FROM #MF D,#ACTIVE A
WHERE D.PARTY_CODE=A.CL_CODE

UPDATE A SET IPO=CLOSE_DATE
FROM #DP D,#ACTIVE A
WHERE D.NISE_PARTY_CODE=A.CL_CODE


UPDATE D SET NSE_CASH =INACTIVE
FROM #ACTIVE D,#NSE A
WHERE D.CL_CODE=A.CL_CODE

UPDATE D SET BSE_CASH =INACTIVE
FROM #ACTIVE D,#BSE A
WHERE D.CL_CODE=A.CL_CODE

UPDATE D SET NSE_FUTURES=INACTIVE
FROM #ACTIVE D,#NFO A
WHERE D.CL_CODE=A.CL_CODE

UPDATE D SET BSE_FUTURES=INACTIVE
FROM #ACTIVE D,#BFO A
WHERE D.CL_CODE=A.CL_CODE


UPDATE D SET NSX_FUTURES=INACTIVE
FROM #ACTIVE D,#NSXCURR A
WHERE D.CL_CODE=A.CL_CODE


UPDATE D SET MCD_FUTURES=INACTIVE
FROM #ACTIVE D,#MCDCURR A
WHERE D.CL_CODE=A.CL_CODE

UPDATE D SET MCX_FUTURES=INACTIVE
FROM #ACTIVE D,#MCXCOMM A
WHERE D.CL_CODE=A.CL_CODE

UPDATE D SET NCX_FUTURES=INACTIVE
FROM #ACTIVE D,#NCXCOMM A
WHERE D.CL_CODE=A.CL_CODE


SELECT UPPER(CL_CODE)CL_CODE,(CASE WHEN NSE_CASH ='' THEN 'N/A' ELSE NSE_CASH END ) NSE_CASH,
(CASE WHEN BSE_CASH ='' THEN 'N/A' ELSE BSE_CASH END ) BSE_CASH,
