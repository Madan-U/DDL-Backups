(CASE WHEN MCX_FUTURES ='' THEN 'N/A' ELSE MCX_FUTURES END ) MCX_COMM,
(CASE WHEN NCX_FUTURES ='' THEN 'N/A' ELSE NCX_FUTURES END )NCX_COMM,
