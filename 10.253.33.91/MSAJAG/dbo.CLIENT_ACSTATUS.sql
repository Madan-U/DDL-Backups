SELECT  B.CL_CODE,(CASE WHEN MAX(INACTIVE_FROM) > GETDATE() THEN 'YES' ELSE 'NO' END) AS INACTIVE INTO #NSE  FROM 
CLIENT_BROK_DETAILS B,#ACTIVE A
WHERE A.cl_code=B.Cl_Code AND Segment='CAPITAL' AND EXCHANGE ='NSE' AND ISNULL(Deactive_value ,'') <>'P'
AND A.cl_code=@CL_CODE
GROUP BY B.CL_CODE

SELECT  B.CL_CODE,(CASE WHEN MAX(INACTIVE_FROM) > GETDATE() THEN 'YES' ELSE 'NO' END) AS INACTIVE INTO #BSE  FROM 
CLIENT_BROK_DETAILS B,#ACTIVE A
WHERE A.cl_code=B.Cl_Code AND Segment='CAPITAL' AND EXCHANGE ='BSE' AND ISNULL(Deactive_value ,'') <>'P'
AND A.cl_code=@CL_CODE
