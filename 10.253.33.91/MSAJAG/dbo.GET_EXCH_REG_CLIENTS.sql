CREATE PROCEDURE [dbo].[GET_EXCH_REG_CLIENTS]
(
	@EXCHANGE VARCHAR(10),
	@FROMCODE VARCHAR(10),
	@TOCODE VARCHAR(10),
	@FROMDATE VARCHAR(11) = ''
)
AS

SELECT CL_CODE FROM CLIENT_DETAILS C WITH (NOLOCK)
WHERE EXISTS(SELECT CL_CODE FROM CLIENT_BROK_DETAILS CB WITH (NOLOCK)
WHERE C.CL_CODE = CB.CL_CODE AND EXCHANGE = CASE WHEN @EXCHANGE <> '' THEN @EXCHANGE ELSE EXCHANGE END AND ACTIVE_DATE <= CASE WHEN @FROMDATE <> '' THEN @FROMDATE + ' 23:59' ELSE ACTIVE_DATE END)
and C.CL_CODE BETWEEN @FROMCODE AND @TOCODE

GO
