CREATE PROC [dbo].[ADDITIONAL_BROEKRAGE_BLANK_REPORT]

AS 

DECLARE @SNO nvarchar(max)
DECLARE @EXCHANGE nvarchar(max)
DECLARE @BODYMSG NVARCHAR(MAX)    
DECLARE @SUBJECT NVARCHAR(MAX)  
DECLARE @TABLEHTML NVARCHAR(MAX)  
DECLARE @FINALCOUNT NVARCHAR(MAX)


SELECT @SNO= SNO,@EXCHANGE= EXCHANGE FROM (
SELECT SNO , EXCHANGE ='NSE' FROM TBL_ADDITIONAL_BROEKRAGE
where TODATE > GETDATE()
AND BRANCH_CD = '' AND SUB_BROKER = '' AND PARTY_CODE = ''
UNION 
SELECT SNO, EXCHANGE ='BSE' FROM AngelBSECM.BSEDB_AB.DBO.TBL_ADDITIONAL_BROEKRAGE
where TODATE > GETDATE()
AND BRANCH_CD = '' AND SUB_BROKER = '' AND PARTY_CODE = ''
UNION 
SELECT SNO , EXCHANGE ='NSEFO' FROM ANGELFO.NSEFO.DBO.TBL_ADDITIONAL_BROEKRAGE
where TODATE > GETDATE()
AND BRANCH_CD = '' AND SUB_BROKER = '' AND PARTY_CODE = ''
UNION 
SELECT SNO , EXCHANGE ='NSX'FROM ANGELFO.NSECURFO.DBO.TBL_ADDITIONAL_BROEKRAGE
where TODATE > GETDATE()
AND BRANCH_CD = '' AND SUB_BROKER = '' AND PARTY_CODE = ''
UNION 
SELECT SNO , EXCHANGE ='MCX'FROM ANGELCOMMODITY.MCDX.DBO.TBL_ADDITIONAL_BROEKRAGE
where TODATE > GETDATE()
AND BRANCH_CD = '' AND SUB_BROKER = '' AND PARTY_CODE = ''
UNION 
SELECT SNO , EXCHANGE ='NCX'FROM ANGELCOMMODITY.NCDX.DBO.TBL_ADDITIONAL_BROEKRAGE
where TODATE > GETDATE()
AND BRANCH_CD = '' AND SUB_BROKER = '' AND PARTY_CODE = '')A

SELECT ISNULL (@SNO,'')
SELECT len(ISNULL (@SNO,''))

IF 
(SELECT len(ISNULL (@SNO,''))) = 0 

RETURN

IF 
(SELECT len(ISNULL (@SNO,''))) <> 0 

 BEGIN

  SET @FINALCOUNT= '<big>' + '<FONT COLOR="RED">'+ @SNO +' IS BLANK FOR EXCHANGE ' + @EXCHANGE + '</FONT>' + '</big> '

 
 END


 SET @SUBJECT = 'ADDITIONAL_BROEKRAGE IS BLANK'   


EXEC MSDB.DBO.SP_SEND_DBMAIL    
@PROFILE_NAME ='DBA',
@RECIPIENTS='bo.support@angelbroking.com' ,    
@COPY_RECIPIENTS='',    
@SUBJECT = @SUBJECT,    
@BODY = @FINALCOUNT,     
@BODY_FORMAT = 'HTML' ;

GO
