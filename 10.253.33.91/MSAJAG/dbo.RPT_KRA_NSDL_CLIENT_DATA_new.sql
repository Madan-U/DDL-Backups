		GROSS_ANNUAL_INCOME		= ISNULL(CU.GROSS_INCOME,'')	+ SPACE(2-LEN(ISNULL(CU.GROSS_INCOME,''))),
		NETWORTH				= CONVERT(VARCHAR,CONVERT(NUMERIC(18,3),ROUND(NET_WORTH,3))) + SPACE(18-LEN(CONVERT(VARCHAR,CONVERT(NUMERIC(18,3),ROUND(NET_WORTH,3))))),
