		NATIONALITY_OTHER					VARCHAR(50),
		PROOFOFIDENTITY						VARCHAR(2),
		PROOFOFIDENTITY_OTHER				VARCHAR(75),
		CORRESPONDENCE_ADDRESS1				VARCHAR(36),
		CORRESPONDENCE_ADDRESS2				VARCHAR(36),
