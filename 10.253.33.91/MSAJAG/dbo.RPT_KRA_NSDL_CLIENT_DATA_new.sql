		MOBILENO				= LEFT(ISNULL(MOBILE_PAGER,''),15) + SPACE(15-LEN(LEFT(ISNULL(MOBILE_PAGER,''),15))),
		FAXNO					= LEFT(ISNULL(FAX,''),24)	+ SPACE(24-LEN(LEFT(ISNULL(FAX,''),24))),
