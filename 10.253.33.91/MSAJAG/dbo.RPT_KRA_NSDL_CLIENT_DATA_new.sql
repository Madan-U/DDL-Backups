		FILLER_OPTIONAL3		= SPACE(50),
		PERMANENT_ADDRESS_FLAG	= (
		CASE
			WHEN ISNULL(P_ADDRESS1,'') = '' THEN 'Y'
			ELSE 'N'
		END),
		PERMANENT_ADDRESS1		= (
		CASE
			WHEN ISNULL(P_ADDRESS1,'') = '' THEN LEFT(ISNULL(L_ADDRESS1,'') + SPACE(36-LEN(ISNULL(L_ADDRESS1,''))),36)
