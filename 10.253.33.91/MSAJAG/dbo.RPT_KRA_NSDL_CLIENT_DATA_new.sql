			THEN (CASE WHEN ISNULL(CU.NATIONALITY_OTHER,'') = '99' THEN ISNULL(CU.NATIONALITY_OTHER,'') + SPACE(50-LEN(ISNULL(CU.NATIONALITY_OTHER,''))) ELSE SPACE(50) END)
			ELSE ''
		END),
		PROOFOFIDENTITY		= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN ISNULL(CU.PROOF_OF_ID,'') + SPACE(2-LEN(ISNULL(CU.PROOF_OF_ID,'')))
			ELSE ''
		END),
		PROOFOFIDENTITY_OTHER	= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN SPACE(75)
			ELSE ''
		END),
		CORRESPONDENCE_ADDRESS1	= LEFT(ISNULL(L_ADDRESS1,'')	+ SPACE(36-LEN(ISNULL(L_ADDRESS1,''))),36),
		CORRESPONDENCE_ADDRESS2	= LEFT(ISNULL(L_ADDRESS2,'')	+ SPACE(36-LEN(ISNULL(L_ADDRESS2,''))),36),
		CORRESPONDENCE_ADDRESS3	= LEFT(ISNULL(L_ADDRESS3,'')	+ SPACE(36-LEN(ISNULL(L_ADDRESS3,''))),36),
		CORRESPONDENCE_CITY		= LEFT(ISNULL(L_CITY,'')		+ SPACE(36-LEN(ISNULL(L_CITY,''))),36),
		CORRESPONDENCE_PINCODE	= LEFT(ISNULL(L_ZIP,'')			+ SPACE(10-LEN(ISNULL(L_ZIP,''))),10),
		CORRESPONDENCE_STATE	= ISNULL(L_STATE,''),
		CORRESPONDENCE_STATE_OTHER		= SPACE(75),
		CORRESPONDENCE_COUNTRY			= ISNULL(L_NATION,''),
		CORRESPONDENCE_PROOFOFADDRESS	= ISNULL(CU.PROOF_OF_ADDRESS,'') + SPACE(2-LEN(ISNULL(CU.PROOF_OF_ADDRESS,''))),
		CORRESPONDENCE_PROOFOFADDRESS_OTHER	= SPACE(75),
		TEL_OFF					= LEFT(ISNULL(OFF_PHONE1,''),24) + SPACE(24-LEN(LEFT(ISNULL(OFF_PHONE1,''),24))),
