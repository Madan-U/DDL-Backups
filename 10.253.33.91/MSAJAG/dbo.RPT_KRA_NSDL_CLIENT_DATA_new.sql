CREATE PROC [dbo].[RPT_KRA_NSDL_CLIENT_DATA]
	(
	@FROMDATE	VARCHAR(11),
	@TODATE		VARCHAR(11),
	@FROMPARTY	VARCHAR(15),
	@TOPARTY	VARCHAR(15),
	@OPT_TYPE	VARCHAR(2),
	@FROMBRANCH VARCHAR(15),
	@TOBRANCH	VARCHAR(15),
	@STATUSID	VARCHAR(15),
	@STATUSNAME	VARCHAR(25),
	@BATCHDATE	VARCHAR(20) = '',
	@CL_TYPE	VARCHAR(1)	= 'I',
	@USERNAME	VARCHAR(20) = ''
	)

	AS

	/*
	EXEC RPT_KRA_NSDL_CLIENT_DATA 'JAN  1 2000','JAN 20 2012','','ZZZZZZZ','A','','ZZZZZZZZ','BROKER','BROKER','2012011701','N'
	EXEC RPT_KRA_NSDL_CLIENT_DATA 'JAN 28 2000','JAN 28 2012','','ZZZZZZZ','A','','ZZZZZZZZ','BROKER','BROKER','2012011701','I','SWAPNIL'
	*/

	CREATE TABLE #CLIENT
		(
		PARTY_CODE	VARCHAR(15),
		PAN_GIR_NO	VARCHAR(20)
		)

	DECLARE @BATCH_DATE		VARCHAR(11)
	DECLARE @BATCH_NO		VARCHAR(10)
	DECLARE @STARTKRA_DATE	VARCHAR(11)

	SET @BATCH_DATE		= LEFT(@BATCHDATE,8)
	SET @BATCH_NO		= RIGHT(@BATCHDATE,(LEN(@BATCHDATE)-LEN(@BATCH_DATE)))
	SET @STARTKRA_DATE	= 'JAN  1 2012'

	IF @TOPARTY = ''
	BEGIN
		SET @TOPARTY = 'zzzzzzzzz'
	END

	IF @TOBRANCH = ''
	BEGIN
		SET @TOBRANCH = 'zzzzzzzzz'
	END

	IF (@OPT_TYPE = 'M')
	BEGIN
		INSERT INTO #CLIENT
		SELECT
			DISTINCT
			PARTY_CODE, PAN_GIR_NO
		FROM
			CLIENT_DETAILS C (NOLOCK),
			(
			SELECT	
				CL_CODE,
				INACTIVE_FROM	= MAX(INACTIVE_FROM),
				ACTIVE_DATE		= MIN(ACTIVE_DATE),
				SYSTEMDATE		= MIN(SYSTEMDATE),
				MODIFIEDON		= MAX(MODIFIEDON)
			FROM
				CLIENT_BROK_DETAILS (NOLOCK)
			GROUP BY
				CL_CODE
			) CB
		WHERE
			C.CL_CODE = CB.CL_CODE
			AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			AND BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
			AND C.MODIFIDEDON BETWEEN @FROMDATE AND @TODATE + ' 23:59'  
			AND LEFT(SYSTEMDATE,11) <> LEFT(C.MODIFIDEDON,11)
			AND SYSTEMDATE >= @STARTKRA_DATE
	END
	IF (@OPT_TYPE = 'S')
	BEGIN
		INSERT INTO #CLIENT
		SELECT
			DISTINCT
			PARTY_CODE, PAN_GIR_NO
		FROM
			CLIENT_DETAILS C (NOLOCK),
			(
			SELECT	
				CL_CODE,
				INACTIVE_FROM	= MAX(INACTIVE_FROM),
				ACTIVE_DATE		= MIN(ACTIVE_DATE),
				SYSTEMDATE		= MIN(SYSTEMDATE),
				MODIFIEDON		= MAX(MODIFIEDON)
			FROM
				CLIENT_BROK_DETAILS (NOLOCK)
			GROUP BY
				CL_CODE
			) CB
		WHERE
			C.CL_CODE = CB.CL_CODE
			AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			AND BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
			AND ACTIVE_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'  
			AND INACTIVE_FROM NOT BETWEEN @FROMDATE AND @TODATE + ' 23:59'  
			AND SYSTEMDATE >= @STARTKRA_DATE
	END
	IF (@OPT_TYPE = 'I')
	BEGIN
		INSERT INTO #CLIENT
		SELECT
			DISTINCT
			PARTY_CODE, PAN_GIR_NO
		FROM
			CLIENT_DETAILS C (NOLOCK),
			(
			SELECT	
				CL_CODE,
				INACTIVE_FROM	= MAX(INACTIVE_FROM),
				ACTIVE_DATE		= MIN(ACTIVE_DATE),
				SYSTEMDATE		= MIN(SYSTEMDATE),
				MODIFIEDON		= MAX(MODIFIEDON)
			FROM
				CLIENT_BROK_DETAILS (NOLOCK)
			GROUP BY
				CL_CODE
			) CB
		WHERE
			C.CL_CODE = CB.CL_CODE
			AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			AND BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
			AND INACTIVE_FROM BETWEEN @FROMDATE AND @TODATE + ' 23:59'  
			AND SYSTEMDATE >= @STARTKRA_DATE
	END
	IF (@OPT_TYPE = 'A')
	BEGIN
		INSERT INTO #CLIENT
		SELECT
			DISTINCT
			PARTY_CODE, PAN_GIR_NO
		FROM
			CLIENT_DETAILS C (NOLOCK),
			(
			SELECT	
				CL_CODE,
				INACTIVE_FROM	= MAX(INACTIVE_FROM),
				ACTIVE_DATE		= MIN(ACTIVE_DATE),
				SYSTEMDATE		= MIN(SYSTEMDATE),
				MODIFIEDON		= MAX(MODIFIEDON)
			FROM
				CLIENT_BROK_DETAILS (NOLOCK)
			GROUP BY
				CL_CODE
			) CB
		WHERE
			C.CL_CODE = CB.CL_CODE
			AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			AND BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
			AND SYSTEMDATE BETWEEN @FROMDATE AND @TODATE + ' 23:59' 
			AND SYSTEMDATE >= @STARTKRA_DATE
	END


	/* ---  insert distinct party code --- */

	SELECT 
		DISTINCT 
		PAN_GIR_NO, 
		PARTY_CODE, 
		FAMILY 
	INTO 
		#CLIENT_NEW
	FROM 
		CLIENT_DETAILS
	WHERE 
		PAN_GIR_NO <> ''
		AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY  
		AND PAN_GIR_NO NOT IN 
			(
			SELECT 
				DISTINCT
				PAN_NO
			FROM 
				TBL_KRA_LOG T
			WHERE 
				SRNO IN (SELECT MAX(SRNO) FROM TBL_KRA_LOG T1 WHERE T.PAN_NO = T1.PAN_NO)
				AND ISNULL(I_STATUS,1) = 0
			)
	SELECT 
		DISTINCT 
		PAN_GIR_NO, 
		FAMILY 
	INTO 
		#CLIENT1
	FROM 
		#CLIENT_NEW
	WHERE 
		PAN_GIR_NO IN ( SELECT PAN_GIR_NO FROM #CLIENT_NEW GROUP BY PAN_GIR_NO HAVING COUNT(1) > 1 )

	SELECT 
		PAN_GIR_NO,
		PARTY_CODE 
	INTO 
		#FINAL_CLIENT
	FROM 
		#CLIENT_NEW
	WHERE 
		PAN_GIR_NO NOT IN (SELECT PAN_GIR_NO FROM #CLIENT_NEW GROUP BY PAN_GIR_NO HAVING COUNT(1) > 1 )

	INSERT INTO #FINAL_CLIENT
	SELECT 
		PAN_GIR_NO, 
		FAMILY 
	FROM 
		#CLIENT1
	WHERE 
		PAN_GIR_NO NOT IN (SELECT PAN_GIR_NO FROM #CLIENT1 GROUP BY PAN_GIR_NO HAVING COUNT(1) > 1 )

	INSERT INTO #FINAL_CLIENT
	SELECT 
		PAN_GIR_NO, 
		FAMILY=MIN(FAMILY) 
	FROM 
		#CLIENT1
	WHERE 
		PAN_GIR_NO IN (SELECT PAN_GIR_NO FROM #CLIENT1 GROUP BY PAN_GIR_NO HAVING COUNT(1) > 1 )
	GROUP BY PAN_GIR_NO, FAMILY

	/* ---  insert distinct party code --- */


	CREATE TABLE #KRADATA
