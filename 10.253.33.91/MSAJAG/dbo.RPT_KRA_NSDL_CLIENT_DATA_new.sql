		END),
		PERMANENT_ADDRESS3		= (
		CASE
			WHEN ISNULL(P_ADDRESS1,'') = '' THEN LEFT(ISNULL(L_ADDRESS3,'') + SPACE(36-LEN(ISNULL(L_ADDRESS3,''))),36)
			ELSE LEFT(ISNULL(P_ADDRESS3,'') + SPACE(36-LEN(ISNULL(P_ADDRESS3,''))),36)
		END),

		PERMANENT_CITY		= (
		CASE
			WHEN ISNULL(P_ADDRESS1,'') = '' THEN LEFT(ISNULL(L_CITY,'')		+ SPACE(36-LEN(ISNULL(L_CITY,''))),36)
			ELSE LEFT(ISNULL(P_CITY,'')		+ SPACE(36-LEN(ISNULL(P_CITY,''))),36)
		END),
		PERMANENT_PINCODE	= (
		CASE
			WHEN ISNULL(P_ADDRESS1,'') = '' THEN LEFT(ISNULL(L_ZIP,'')		+ SPACE(10-LEN(ISNULL(L_ZIP,''))),10)
			ELSE LEFT(ISNULL(P_ZIP,'')		+ SPACE(10-LEN(ISNULL(P_ZIP,''))),10)
		END),
		PERMANENT_STATE		= (
		CASE
			WHEN ISNULL(P_ADDRESS1,'') = '' THEN ISNULL(L_STATE,'')
			ELSE ISNULL(P_STATE,'')
		END),
		PERMANENT_STATE_OTHER = SPACE(75),
		PERMANENT_COUNTRY	= (
		CASE
			WHEN ISNULL(P_ADDRESS1,'') = '' THEN ISNULL(L_NATION,'')
