		MOBILENO							VARCHAR(15),
		FAXNO								VARCHAR(24),
		EMAILID								VARCHAR(50),
		FILLER_OPTIONAL3					VARCHAR(50),
		PERMANENT_ADDRESS_FLAG				VARCHAR(1),
		PERMANENT_ADDRESS1					VARCHAR(36),
		PERMANENT_ADDRESS2					VARCHAR(36),
		PERMANENT_ADDRESS3					VARCHAR(36),
		PERMANENT_CITY						VARCHAR(36),
		PERMANENT_PINCODE					VARCHAR(10),
		PERMANENT_STATE						VARCHAR(30),
		PERMANENT_STATE_OTHER				VARCHAR(75),
		PERMANENT_COUNTRY					VARCHAR(30),
		PERMANENT_PROOFOFADDRESS			VARCHAR(2),
		PERMANENT_PROOFOFADDRESS_OTHER		VARCHAR(75),
		GROSS_ANNUAL_INCOME					VARCHAR(2),
		NETWORTH							VARCHAR(18),
		NETWORTH_DATE						VARCHAR(8),
		FILLER_OPTIONAL4					VARCHAR(3),
		PEP									VARCHAR(2),
		OCCUPATION							VARCHAR(2),
		OCCUPATION_OTHER					VARCHAR(75),
		FILLER_OPTIONAL5					VARCHAR(77),
		OTHER_INFORMATION					VARCHAR(100),
		DATE_DECLARATION					VARCHAR(8),
		TRUE_DOCUMENTS_RECEIVED				VARCHAR(1),
		SELF_CERTIFIED_DOCUMENT_RECEIVED	VARCHAR(1),
		DOCUMENT_RECEIVED_DATE				VARCHAR(8),
		NOOFDOCUMENTS_SUBMITTED				VARCHAR(2),
		SENDER_REFERENCE_NUMBER1			VARCHAR(35),
		SENDER_REFERENCE_NUMBER2			VARCHAR(35),
		FILLER_OPTIONAL6					VARCHAR(65),
		/* --- FOR NON INDIVIDUAL --- */
		RECORD_TYPE_F						VARCHAR(2),
		LINE_NUMBER_F						VARCHAR(7),
		NAME_PROMOTER						VARCHAR(250),
		RELATIONSHIP						VARCHAR(2),
		RELATIONSHIP_OTHER					VARCHAR(75),
		PAN_PROMOTER						VARCHAR(10),
		PAN_EXEMPTION_PROMOTER				VARCHAR(75),
		PROMOTER_ADDRESS1					VARCHAR(36),
		PROMOTER_ADDRESS2					VARCHAR(36),
		PROMOTER_ADDRESS3					VARCHAR(36),
		PROMOTER_CITY						VARCHAR(36),
		PROMOTER_PINNO						VARCHAR(10),
		PROMOTER_STATE						VARCHAR(30),
		PROMOTER_STATE_OTHER				VARCHAR(75),
		PROMOTER_COUNTRY					VARCHAR(30),
		PROMOTER_UID						VARCHAR(16),
		PAN_NONINDIVIDUAL					VARCHAR(10),
		FILLER_F							VARCHAR(69),
		/* --- FOR NON INDIVIDUAL --- */
		PARTY_CODE							VARCHAR(20),
		CATEGORY							VARCHAR(2),
		)

	INSERT INTO #KRADATA
	SELECT
		BATCH_NO			= '',
		RECORD_TYPE_H		= (CASE WHEN @CL_TYPE = 'I' THEN '02' ELSE '04' END),
		BRANCH_CD			= '',
		INTERMEDIARY_ID		= '',
		INTERMEDIARY_ROLE	= '',
		TOTALNOOFRECORDS	= '',
		SEND_DATE			= '',	
		[USER_ID]			= '',
		FILE_FORMAT_VERSION	= '',
		FILLER_H			= '',
		RECORD_TYPE_D		= (CASE WHEN @CL_TYPE = 'I' THEN '02' ELSE '04' END),
		LINE_NUMBER			= '0000001',  
		TRANSACTION_STATUS	= 'A',
		NAME_APPLICANT		= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN LEFT(LONG_NAME,45) + SPACE(45-LEN(LEFT(LONG_NAME,45)))
			ELSE LEFT(LONG_NAME,250) + SPACE(250-LEN(LEFT(LONG_NAME,250)))
		END),
		FILLER				= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN SPACE(205)
			ELSE SPACE(47)
		END),
		FATHER_NAME			= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN LEFT(ISNULL(FATHERNAME,''),45) + SPACE(45-LEN(LEFT(ISNULL(FATHERNAME,''),45)))
			ELSE ''
		END),
		GENDER				= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN SEX + SPACE(1-LEN(SEX))
			ELSE ''
		END),
		MARITAL_STATUS		= (
		CASE
			WHEN CU.CATEGORY = 'I' 
			THEN (CASE 
						WHEN ISNULL(CU.MARITALSTATUS,'') = '01' THEN 'M'
						WHEN ISNULL(CU.MARITALSTATUS,'') = '02' THEN 'S'
						ELSE ISNULL(CU.MARITALSTATUS,'') + SPACE(1-LEN(ISNULL(CU.MARITALSTATUS,'')))
					END)
			ELSE ''
		END),
		DOB					= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN CONVERT(VARCHAR(8),DOB,112) + SPACE(8-LEN(CONVERT(VARCHAR(8),DOB,112)))
			ELSE ''
		END),
		FILLER_OPTIONAL1	= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN SPACE(83) 
			ELSE '' 
		END),

		/* --- FOR NONINDIVIDUAL --- */
		DATE_INCORPORATION	= (
		CASE
			WHEN CU.CATEGORY = 'N' THEN CONVERT(VARCHAR(8),CLIENT_AGREEMENT_ON,112) + SPACE(8-LEN(CONVERT(VARCHAR(8),CLIENT_AGREEMENT_ON,112)))
			ELSE ''
		END),
		PLACE_INCORPORATION	= (
		CASE
			WHEN CU.CATEGORY = 'N' THEN ISNULL(CU.PLACEOFINCORPRATION,'') + SPACE(75-LEN(ISNULL(CU.PLACEOFINCORPRATION,'')))
			ELSE ''
		END),
		DATE_COMMENCEMENT	= (
		CASE
			WHEN CU.CATEGORY = 'N' THEN ISNULL(CONVERT(VARCHAR(8),CU.DATEOFCOMMENCMENT,112),'') + SPACE(8-LEN(ISNULL(CONVERT(VARCHAR(8),CU.DATEOFCOMMENCMENT,112),'')))
			ELSE ''
		END),
		/* --- FOR NONINDIVIDUAL --- */

		PAN					= C.PAN_GIR_NO + SPACE(10-LEN(C.PAN_GIR_NO)),
		PAN_EXEMPTION_DOC	= SPACE(75),
		[UID]					= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN ISNULL(CU.AADHAR_UID,'') + SPACE(16-LEN(ISNULL(CU.AADHAR_UID,'')))
			ELSE SPACE(16)
		END),

		[STATUS]				= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN ISNULL(CU.RES_STATUS_IND,'') + SPACE(2-LEN(ISNULL(CU.RES_STATUS_IND,'')))
			ELSE ISNULL(CU.STATUS,'') + SPACE(2-LEN(ISNULL(CU.STATUS,'')))
		END),
		STATUS_OTHER		= (
		CASE
			WHEN CU.CATEGORY = 'N' 
			THEN (CASE WHEN ISNULL(CU.STATUS,'') = '99' THEN ISNULL(CU.STATUS_OTHER,'') + SPACE(75-LEN(ISNULL(CU.STATUS_OTHER,''))) ELSE SPACE(75) END)
			ELSE ''
		END),

		FILLER_OPTIONAL2	= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN SPACE(75)
			ELSE SPACE(129)
		END),

		NATIONALITY			= (
		CASE
			WHEN CU.CATEGORY = 'I' THEN ISNULL(CU.NATIONALITY,'') + SPACE(2-LEN(ISNULL(CU.NATIONALITY,'')))
			ELSE ''
		END),
