-- Object: PROCEDURE dbo.V2_CLASS_SAUDASUMMARY
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC V2_CLASS_SAUDASUMMARY    
(    
 @FROMPARTY VARCHAR(10),    
 @TOPARTY VARCHAR(10),    
 @FROMDATE VARCHAR(11),    
 @TODATE VARCHAR(11)    
)    
    
AS    
/*    
 EXEC V2_CLASS_SAUDASUMMARY '000000000', 'ZZZZZZZZZZ', 'jul 25 2008', 'Aug  7 2008'    
*/    
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED    
SELECT     
 SAUDA_DATE = LEFT(C.SAUDA_DATE,11),  
 C.PARTY_CODE,     
 C.SETT_NO,     
 C.SCRIP_CD,    
 C.SERIES,    
 C.SCRIP_NAME,    
 PQTY = SUM(C.PQTYTRD + C.PQTYDEL),    
 PAVG = ROUND(ABS(CASE WHEN SUM(C.PQTYTRD + C.PQTYDEL) <> 0 THEN SUM(C.PAMTTRD+C.PAMTDEL)/SUM(C.PQTYTRD + C.PQTYDEL) ELSE SUM(C.PAMTTRD + C.PAMTDEL) END),2),    
 PAMT = SUM(C.PAMTTRD + C.PAMTDEL),    
 SQTY = SUM(C.SQTYTRD + C.SQTYDEL),    
 SAVG = ROUND(ABS(CASE WHEN SUM(C.SQTYTRD + C.SQTYDEL) <> 0 THEN SUM(C.SAMTTRD + C.SAMTDEL)/SUM(C.SQTYTRD + C.SQTYDEL) ELSE SUM(C.SAMTTRD + C.SAMTDEL) END),2),    
 SAMT = SUM(C.SAMTTRD + C.SAMTDEL),     
 OPENQTY = SUM(C.PQTYDEL - C.SQTYDEL),    
 OPENAVG = ROUND(ABS(CASE WHEN SUM(C.PQTYDEL - C.SQTYDEL) <> 0 THEN SUM(C.SAMTDEL - C.PAMTDEL) / SUM(C.PQTYDEL - C.SQTYDEL) ELSE SUM(C.SAMTDEL - C.PAMTDEL) END),2),    
 OPENAMT = SUM(C.SAMTDEL - C.PAMTDEL),    
 PNL = ROUND(SUM(C.SAMTTRD - C.PAMTTRD),2)    
FROM 
	CMBILLVALAN C (NOLOCK), 
	CLIENT2 C2 (NOLOCK),
	CLIENT_PRINT_SETTINGS P (NOLOCK) 
WHERE C.PARTY_CODE >= @FROMPARTY    
AND C.PARTY_CODE <= @TOPARTY    
AND C.PARTY_CODE = C2.PARTY_CODE
AND C.SAUDA_DATE >= @FROMDATE    
AND C.SAUDA_DATE <= @TODATE + ' 23:59:59'    
AND P.PRINT_FLAG = C2.PRINTF 
AND P.PRINT_FLAG IN (2,4)
GROUP BY    
 LEFT(C.SAUDA_DATE,11),  
 C.PARTY_CODE,    
 C.SETT_NO,    
 C.SCRIP_CD,    
 C.SERIES,    
 C.SCRIP_NAME    
ORDER BY    
 C.PARTY_CODE,    
 C.SETT_NO,    
 C.SCRIP_CD,    
 C.SERIES,    
 C.SCRIP_NAME

GO
