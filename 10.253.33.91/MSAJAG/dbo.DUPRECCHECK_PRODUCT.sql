-- Object: PROCEDURE dbo.DUPRECCHECK_PRODUCT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [DBO].[DUPRECCHECK_PRODUCT]
AS
DECLARE @SAUDA_DATE VARCHAR(11)

SELECT TOP 1 @SAUDA_DATE = LEFT(SAUDA_DATE,11) FROM TRADE_PRODUCT_TMP

INSERT INTO TRADE_PRODUCT
SELECT DISTINCT SAUDA_DATE=LEFT(SAUDA_DATE,11),ORDER_NO,SCRIP_CD,SERIES,PRODUCT_CODE,GETDATE() FROM TRADE_PRODUCT_TMP T1
WHERE NOT EXISTS (SELECT ORDER_NO FROM TRADE_PRODUCT T 
				  WHERE T.SAUDA_DATE LIKE @SAUDA_DATE + '%'
				  AND LEFT(T.SAUDA_DATE,11) = LEFT(T1.SAUDA_DATE,11)
				  AND T.SCRIP_CD = T1.SCRIP_CD
				  AND T.SERIES = T1.SERIES
				  AND T.ORDER_NO = T1.ORDER_NO)
AND ( LEN(PRODUCT_CODE) >= 2 AND LEN(PRODUCT_CODE) <= 5		
	 OR LEN(PRODUCT_CODE) > 13)
		  
TRUNCATE TABLE TRADE_PRODUCT_TMP

GO
