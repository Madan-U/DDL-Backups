CREATE PROC [dbo].[BROKERAGE_MASTER_ISSUE_TRADE_IMPORT]
AS 
BEGIN

SELECT *   INTO #MASTER_ISSUE FROM (
SELECT   DISTINCT CL_CODE,EXCHANGE,CONVERT(VARCHAR(11),ACTIVE_DATE,120) AS AD,TRD_BROK,0 DEL_BROK,INACTIVE_FROM FROM CLIENT_BROK_DETAILS (NOLOCK)
WHERE TRD_BROK IN (SELECT TABLE_NO FROM BROKTABLE WHERE TRD_DEL='D') AND SEGMENT='CAPITAL' AND EXCHANGE='NSE' AND DEACTIVE_VALUE NOT IN ('T','C')
 UNION ALL
 SELECT   DISTINCT CL_CODE,EXCHANGE,CONVERT(VARCHAR(11),ACTIVE_DATE,120) AS AD ,0 TRD_BROK,DEL_BROK ,INACTIVE_FROM  FROM CLIENT_BROK_DETAILS (NOLOCK)
 WHERE DEL_BROK IN (SELECT TABLE_NO FROM BROKTABLE WHERE TRD_DEL<>'D') AND SEGMENT='CAPITAL' AND EXCHANGE='NSE' AND DEACTIVE_VALUE NOT IN ('T','C')
 UNION ALL
 SELECT   DISTINCT CL_CODE ,EXCHANGE,CONVERT(VARCHAR(11),ACTIVE_DATE,120) AS AD,TRD_BROK,0 DEL_BROK ,INACTIVE_FROM  FROM CLIENT_BROK_DETAILS (NOLOCK)
 WHERE TRD_BROK IN (SELECT TABLE_NO FROM AngelBSECM.BSEDB_AB.DBO.BROKTABLE WHERE TRD_DEL='D') AND SEGMENT='CAPITAL' AND EXCHANGE='BSE'AND DEACTIVE_VALUE NOT IN ('T','C')
 UNION ALL
 SELECT   DISTINCT CL_CODE,EXCHANGE,CONVERT(VARCHAR(11),ACTIVE_DATE,120) AS AD,0 TRD_BROK,DEL_BROK ,INACTIVE_FROM   FROM CLIENT_BROK_DETAILS (NOLOCK)
 WHERE DEL_BROK IN (SELECT TABLE_NO FROM  AngelBSECM.BSEDB_AB.DBO.BROKTABLE WHERE TRD_DEL<>'D') AND SEGMENT='CAPITAL' AND EXCHANGE='BSE' AND DEACTIVE_VALUE NOT IN ('T','C') )A
ORDER BY INACTIVE_FROM DESC  
 
 DECLARE @P_COUNT NVARCHAR(100) 
 SELECT @P_COUNT =COUNT(DISTINCT CL_CODE ) FROM #MASTER_ISSUE
      
DECLARE @BODYMSG NVARCHAR(MAX)          
      
DECLARE @SUBJECT NVARCHAR(MAX)          
      
DECLARE @TABLEHTML NVARCHAR(MAX)        

DECLARE @DATE VARCHAR(20)      
SET  @DATE = GETDATE()      
            
SET @SUBJECT = 'BROKERAGE TABLE ISSUE IN TRADE IMPORT' + ' '+ @DATE         
      
SET @TABLEHTML =           
      
N'<STYLE TYPE="TEXT/CSS">         
#BOX-TABLE          
{          
FONT-FAMILY: "LUCIDA SANS UNICODE", "LUCIDA GRANDE", SANS-SERIF;          
FONT-SIZE: 12PX;          
TEXT-ALIGN: CENTER;          
BORDER-COLLAPSE: COLLAPSE;          
BORDER-TOP: 7PX SOLID #9BAFF1;          
BORDER-BOTTOM: 7PX SOLID #9BAFF1;          
}          
#BOX-TABLE TH          
{          
FONT-SIZE: 13PX;          
FONT-WEIGHT: NORMAL;          
BACKGROUND: #B9C9FE;          
BORDER-RIGHT: 2PX SOLID #9BAFF1;          
BORDER-LEFT: 2PX SOLID #9BAFF1;          
BORDER-BOTTOM: 2PX SOLID #9BAFF1;          
COLOR: #039;          
}          
#BOX-TABLE TD          
{          
BORDER-RIGHT: 1PX SOLID #AABCFE;          
BORDER-LEFT: 1PX SOLID #AABCFE;          
BORDER-BOTTOM: 1PX SOLID #AABCFE;          
COLOR: #669;          
}          
TR:NTH-CHILD(ODD) { BACKGROUND-COLOR:#EEE; }          
TR:NTH-CHILD(EVEN) { BACKGROUND-COLOR:#FFF; }           
</STYLE>'+           
N'<H3><FONT COLOR="BLUE">BELOW ARE THE LIST OF CLIENTS '+'('+@P_COUNT+')'+' WITH MISMATCHED BROKERAGE TABLE NO.</H3>' +          
N'<TABLE BORDER=1 >' +          
N'<TR><TH>INACTIVE_FROM</TH>          
<TH>EXCHANGE</TH>          
<TH>CL_CODE</TH>          
<TH>AD</TH>      
<TH>TRD_BROK</TH>          
<TH>DEL_BROK</TH>               
</TR>' +          
      
      
CAST ( (         
         
select INACTIVE_FROM=(CONVERT(VARCHAR(20),INACTIVE_FROM,120)),TD = [EXCHANGE],'', TD = [CL_CODE],'', TD = [AD] ,'',TD = [TRD_BROK],'',TD = [DEL_BROK],''      
     
 from #MASTER_ISSUE       
       
 FOR XML PATH('TR'), TYPE           
      
) AS NVARCHAR(MAX) ) +        
N'</TABLE>'           
      
      
EXEC MSDB.DBO.SP_SEND_DBMAIL
@PROFILE_NAME ='DBA', 
--@COPY_RECIPIENTS='punit.verma@angelbroking.com',       
@RECIPIENTS='brokerage.modification@angelbroking.com;deepak.redekar@angelbroking.com;suresh.raut@angelbroking.com;suresh.k@angelbroking.com' ,          
@COPY_RECIPIENTS='updationteam@angelbroking.com;nilesh.gokral@angelbroking.com',          
@SUBJECT = @SUBJECT,          
@BODY = @TABLEHTML,           
@BODY_FORMAT = 'HTML' ;          
      
      END

GO
