-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------




CREATE PROC [dbo].[FETCH_GST_INVOICE] 
(
	@FORTHEDAY	VARCHAR(11),
	@EXCHANGE	VARCHAR(3),
	@SEGMENT	VARCHAR(7)
)
AS

SELECT INVOICE_DATE = CONVERT(DATETIME, LEFT(SAUDA_DATE,11)),EXCHANGE=@EXCHANGE, SEGMENT=@SEGMENT, RECORDFOR='SETTLEMENT',
SUBRECORDFOR=CONVERT(VARCHAR(20),'CONTRACT'),INV_DESC = CONVERT(VARCHAR(500),'TOWORDS BROKERAGE AND CHARGES AGAINST THE TRADES EXECUTION'),
C.SETT_NO, C.SETT_TYPE, PARTY_CODE, CONTRACTNO, INT_REF_NO = CONVERT(VARCHAR(50),@EXCHANGE+'_'+@SEGMENT+'_' + CONTRACTNO),
TAXABLE_AMT = SUM(PBROKTRD+SBROKTRD+PBROKDEL+SBROKDEL)
			+ SUM(CASE WHEN Turnover_Ac = 1 THEN TURN_TAX ELSE 0 END)
			+ SUM(CASE WHEN Sebi_Turn_Ac = 1 THEN SEBI_TAX ELSE 0 END)
			+ SUM(CASE WHEN Broker_Note_Ac = 1 THEN BROKER_CHRG ELSE 0 END)
			+ SUM(CASE WHEN Other_Chrg_Ac = 1 THEN OTHER_CHRG ELSE 0 END)
			+ SUM(CASE WHEN STT_TAX_AC = 1 THEN INS_CHRG ELSE 0 END),
TAX_AMT = SUM(C.SERVICE_TAX),
INVOICE_TYPE = CONVERT(VARCHAR(20),'DEBIT'),
INVOICE_SUBTYPE = CONVERT(VARCHAR(20),''),
INVOICE_CATEGORY = CONVERT(VARCHAR(20),''),
REV_INT_REF_NO = CONVERT(VARCHAR(50),''),
LOCATION_CODE = CONVERT(VARCHAR(50),''),
SOURCESTATE=CONVERT(VARCHAR(50),''),
TARGETSTATE=CONVERT(VARCHAR(50),''),
STATE_CODE=CONVERT(VARCHAR(2),''),
INV_NO=CONVERT(VARCHAR(50),''),
SACCODE=CONVERT(VARCHAR(50),''),
SAC_DESC=CONVERT(VARCHAR(500),''),
BRANCH_CODE = CONVERT(VARCHAR(10),''),
IGST_RATE = CONVERT(NUMERIC(18,4),0),
IGST_AMOUNT = CONVERT(NUMERIC(18,4),0),
CGST_RATE = CONVERT(NUMERIC(18,4),0),
CGST_AMOUNT = CONVERT(NUMERIC(18,4),0),
SGST_RATE = CONVERT(NUMERIC(18,4),0),
SGST_AMOUNT = CONVERT(NUMERIC(18,4),0),
TOTCHRG = SUM(PBROKTRD+SBROKTRD+PBROKDEL+SBROKDEL)
		+ SUM(TURN_TAX)
		+ SUM(SEBI_TAX)
		+ SUM(BROKER_CHRG)
		+ SUM(OTHER_CHRG)
		+ SUM(INS_CHRG)  INTO #GST_DATA
FROM CMBILLVALAN C, GLOBALS G, OWNER O , SETT_MST S
WHERE  S.START_DATE BETWEEN @FORTHEDAY AND @FORTHEDAY + ' 23:59:59'
AND SAUDA_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT
AND C.SETT_NO=S.SETT_NO AND C.SETT_TYPE=S.SETT_TYPE
AND C.SERVICE_TAX > 0 
GROUP BY C.SETT_NO, C.SETT_TYPE, PARTY_CODE, CONTRACTNO, LEFT(SAUDA_DATE,11)

CREATE INDEX #C ON #GST_DATA(INVOICE_DATE,PARTY_CODE)


SELECT PARTY_CODE,IPFT_CHRG=SUM(IPFT_CHRG) INTO #IPFT
FROM TBL_ADDITIONAL_TAX_DATA  WHERE SAUDA_DATE BETWEEN @FORTHEDAY AND @FORTHEDAY + ' 23:59:59' GROUP BY PARTY_CODE

CREATE INDEX #C ON #IPFT(PARTY_CODE)


UPDATE G SET TAXABLE_AMT=TAXABLE_AMT+IPFT_CHRG,TOTCHRG=TOTCHRG+IPFT_CHRG FROM #GST_DATA G,#IPFT I WHERE G.PARTY_CODE =I.PARTY_CODE 
AND TAX_AMT >0

SELECT * FROM #GST_DATA 

DROP TABLE #GST_DATA

DROP TABLE #IPFT

GO
