CREATE	PROC [dbo].[RPT_SEC_REG_UPLOAD_BAK_17122018]
		(
		@FROMDATE	VARCHAR(11),
		@TODATE		VARCHAR(11)
		)
	
		AS

		/*
		EXEC	RPT_SEC_REG_UPLOAD 'DEC  1 2018', 'DEC 11 2018'
		*/		
		
		DECLARE @FILENAME   VARCHAR(50)
		DECLARE @BATCHNO	INT  
		DECLARE @MEMBERCODE VARCHAR(20)
		DECLARE @MEMBERNAME	VARCHAR(500)
		DECLARE @NEWFROMDATE VARCHAR(11)

		SELECT @MEMBERCODE = MEMBERCODE, @MEMBERNAME = ISNULL(LTRIM(RTRIM(COMPANY)),'') FROM OWNER (NOLOCK)
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
		WHERE FLDFILENO = 'SR'  
		AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		
		  
		IF @BATCHNO = 1  
		BEGIN  
			INSERT INTO ASIANFILEBATCHNO VALUES ('SR', @BATCHNO, GETDATE(), GETDATE())  
		END  
		ELSE  
		BEGIN  
			UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'SR' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		END  
		  
		SET @FILENAME = @MEMBERCODE + '_SR_' + REPLACE(CONVERT(VARCHAR,GETDATE(),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) 

		SET  @NEWFROMDATE = 'APR  1 1950'
		
		SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED                      
		SELECT  * INTO #DEL_TRANSSTAT FROM ANGELDEMAT.MSAJAG.DBO.DELTRANS_REPORT WITH(NOLOCK)
		WHERE TRANSDATE >= @NEWFROMDATE AND TRANSDATE <= @TODATE + ' 23:59:59'
		--AND PARTY_CODE='A80914'

		CREATE INDEX [DELHOLD] ON [DBO].[#DEL_TRANSSTAT] ([TRANSDATE], [PARTY_CODE], [SCRIP_CD], [HOLDERNAME], [DRCR], [BDPTYPE], [BDPID], [BCLTDPID])
		

		SELECT * INTO #DEL_TRANSSTAT_BSE FROM ANGELDEMAT.BSEDB.DBO.DELTRANS_REPORT WITH(NOLOCK)
		WHERE TRANSDATE >= @NEWFROMDATE AND TRANSDATE <= @TODATE + ' 23:59:59'
		--AND PARTY_CODE='A80914'

		CREATE INDEX [DELHOLDBSE] ON [DBO].[#DEL_TRANSSTAT_BSE] ([TRANSDATE], [PARTY_CODE], [SCRIP_CD], [HOLDERNAME], [DRCR], [BDPTYPE], [BDPID], [BCLTDPID])


		SELECT	TRANS_DATE = CONVERT(VARCHAR,TRANSDATE,105),
				EXE_DATE = CONVERT(VARCHAR,TRANSDATE,105),
				EXCHANGE = ISNULL(EXCHANGE,''),
				SEGMENT = (
				CASE 
					WHEN ISNULL(SEGMENT,'') = 'CAPITAL' THEN 'CM'
					ELSE 'FO'
				END),
				UCC_CODE = D.PARTY_CODE,
				PARTY_CODE = CONVERT(VARCHAR(20),''),
				CLIENT_NAME = C1.LONG_NAME,
				CLIENT_PAN = C1.PAN_GIR_NO,
				MEMBER_DEMAT_ACC = (
				CASE 
					WHEN DP.DPID = D.BDPID AND DP.DPCLTNO = D.BCLTDPID THEN 
					CASE 
						WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
						ELSE D.BCLTDPID 
					END
					ELSE ''
				END),
				COUNTERPARTY_DEMAT_ACC = (
				CASE 
					WHEN DP.DPID <> D.BDPID AND DP.DPCLTNO <> D.BCLTDPID THEN 
					CASE 
						WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
						ELSE D.BCLTDPID 
					END
					ELSE ''
				END),
				SETT_NO = SETT_NO,
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				QTY_DELIVERED = SUM(
				CASE  
					WHEN DRCR = 'D' AND TRTYPE <> 909 THEN QTY 
					ELSE 0
					END),
				QTY_RECEIVED = SUM(
				CASE  
					WHEN DRCR = 'C' AND TRTYPE <> 909 THEN QTY 
					ELSE 0
					END), 
				BALANCE_QTY = SUM(
				CASE  
					WHEN DRCR = 'D' AND TRTYPE <> 909 THEN QTY 
					ELSE -QTY
					END), 
				PLEDGE_UNPLEDGE_QTY = SUM(
				CASE 
					WHEN TRTYPE = 909 OR DESCRIPTION LIKE '%PLEDGE%' THEN QTY 
					ELSE 0 
					END),
				TRF_REF_NO = (
				CASE 
					WHEN DRCR = 'C' THEN FROMNO
					ELSE SLIPNO
				END),
				POA_FLAG = CONVERT(VARCHAR(3),'NO')/*(
				CASE
					WHEN (CD.POA1 = 1 OR CD.POA2 = 1 OR CD.POA3 = 1) THEN 'YES'
					ELSE 'NO'
				END)*/,
				TRANSACTION_TYPE = (
				CASE 
					WHEN DRCR = 'C' AND ISNULL(ACCOUNTTYPE,'') = 'POOL' THEN 'MKT' 
					WHEN DRCR = 'D' AND ISNULL(ACCOUNTTYPE,'') = 'BEN' AND TRTYPE = 1000 THEN 'MKT'  
					WHEN DRCR = 'D' AND ISNULL(ACCOUNTTYPE,'') = 'MAR' AND TRTYPE = 1000 THEN 'MKT'  
					WHEN DRCR = 'D' AND ISNULL(ACCOUNTTYPE,'') = 'POOL' AND TRTYPE = 907 THEN 'MKT' 
					ELSE 'OFF_MKT'
				END),
				PURPOSE = (
				CASE 
					WHEN DRCR = 'C' THEN 'Payin'
					ELSE 'Payout'
				END),
				ORDDATE = CONVERT(VARCHAR,TRANSDATE,112),
				DPID = D.DPID,
				FLAG_NEW = 1,
				BDPID = D.BDPID,
				BCLTDPID = D.BCLTDPID
		INTO	#SECREG
		FROM	#DEL_TRANSSTAT D (NOLOCK),
				ANGELDEMAT.MSAJAG.DBO.DELIVERYDP DP WITH (NOLOCK),
				--ON ( DP.DPID = D.BDPID AND DP.DPCLTNO = D.BCLTDPID ),
				CLIENT1 C1 (NOLOCK)
		WHERE	--D.TRANSDATE BETWEEN @FROMDATE AND @TODATE +' 23:59:59'
				D.TRANSDATE <= @TODATE
				AND FILLER2 = 1       
				AND TRTYPE <> 906      
				AND D.PARTY_CODE <> 'BROKER'      
				AND CERTNO LIKE 'IN%'
				AND DP.DPID = D.BDPID AND DP.DPCLTNO = D.BCLTDPID
				AND D.PARTY_CODE = C1.CL_CODE
				AND DP.EXCHANGE IN ('NSE','BSE')
		GROUP  BY
				CONVERT(VARCHAR,TRANSDATE,105),
				CONVERT(VARCHAR,TRANSDATE,112),
				ISNULL(EXCHANGE,''),
				ISNULL(SEGMENT,''),
				D.PARTY_CODE,
				--C1.UCC_CODE,
				C1.LONG_NAME,
				C1.PAN_GIR_NO,
				CASE 
					WHEN DP.DPID = D.BDPID AND DP.DPCLTNO = D.BCLTDPID THEN 
					CASE 
						WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
						ELSE D.BCLTDPID 
					END
					ELSE ''
				END,
				CASE 
					WHEN DP.DPID <> D.BDPID AND DP.DPCLTNO <> D.BCLTDPID THEN 
					CASE 
						WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
						ELSE D.BCLTDPID 
					END
					ELSE ''
				END,				
				SETT_NO,
				CERTNO,
				SCRIP_CD,
				REFNO,
				DRCR,
				ISNULL(ACCOUNTTYPE,''),
				DELIVERED,
				TRTYPE,
				ISett_No,
				ISett_Type,
				REASON,
				/*CD.POA1,
				CD.POA2,
				CD.POA3,*/
				FROMNO,
				SLIPNO,
				D.DPID,
				D.BDPID,
				D.BCLTDPID


		UPDATE	#SECREG
		SET		POA_FLAG = 'YES'
		FROM	ANGELDEMAT.MSAJAG.DBO.MULTICLTID M WITH (NOLOCK)
		WHERE	M.CLTDPNO = #SECREG.MEMBER_DEMAT_ACC
				AND M.DPID = #SECREG.DPID
				AND M.DEF = 1
				AND FLAG_NEW = 1


		UPDATE	#SECREG
		SET		PARTY_CODE = (CASE WHEN #SECREG.UCC_CODE <> ISNULL(UC.UCC_CODE,'') THEN ISNULL(UC.UCC_CODE,'') ELSE '' END)
		FROM	UCC_CLIENT UC (NOLOCK)
		WHERE	UC.PARTY_CODE = #SECREG.UCC_CODE		
		

		INSERT	INTO #SECREG
		SELECT	TRANS_DATE = CONVERT(VARCHAR,TRANSDATE,105),
				EXE_DATE = CONVERT(VARCHAR,TRANSDATE,105),
				EXCHANGE = ISNULL(EXCHANGE,''),
				SEGMENT = (
				CASE 
					WHEN ISNULL(SEGMENT,'') = 'CAPITAL' THEN 'CM'
					ELSE 'FO'
				END),
				UCC_CODE = D.PARTY_CODE,
				PARTY_CODE = CONVERT(VARCHAR(20),''),
				CLIENT_NAME = C1.LONG_NAME,
				CLIENT_PAN = C1.PAN_GIR_NO,
				MEMBER_DEMAT_ACC = (
				CASE 
					WHEN DP.DPID = D.BDPID AND DP.DPCLTNO = D.BCLTDPID THEN 
					CASE 
						WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
						ELSE D.BCLTDPID 
					END
					ELSE ''
				END),
				COUNTERPARTY_DEMAT_ACC = (
				CASE 
					WHEN DP.DPID <> D.BDPID AND DP.DPCLTNO <> D.BCLTDPID THEN 
					CASE 
						WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
						ELSE D.BCLTDPID 
					END
					ELSE ''
				END),
				SETT_NO = SETT_NO,
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				QTY_DELIVERED = SUM(
				CASE  
					WHEN DRCR = 'D' AND TRTYPE <> 909 THEN QTY 
					ELSE 0
					END),
				QTY_RECEIVED = SUM(
				CASE  
					WHEN DRCR = 'C' AND TRTYPE <> 909 THEN QTY 
					ELSE 0
					END), 
				BALANCE_QTY = SUM(
				CASE  
					WHEN DRCR = 'D' AND TRTYPE <> 909 THEN QTY 
					ELSE -QTY
					END), 
				PLEDGE_UNPLEDGE_QTY = SUM(
				CASE 
					WHEN TRTYPE = 909 OR DESCRIPTION LIKE '%PLEDGE%' THEN QTY 
					ELSE 0 
					END),
				TRF_REF_NO = (
				CASE 
					WHEN DRCR = 'C' THEN FROMNO
					ELSE SLIPNO
				END),
				POA_FLAG = CONVERT(VARCHAR(3),'NO')/*(
				CASE
					WHEN (CD.POA1 = 1 OR CD.POA2 = 1 OR CD.POA3 = 1) THEN 'YES'
					ELSE 'NO'
				END)*/,
				TRANSACTION_TYPE = (
				CASE 
					WHEN DRCR = 'C' AND ISNULL(ACCOUNTTYPE,'') = 'POOL' THEN 'MKT' 
					WHEN DRCR = 'D' AND ISNULL(ACCOUNTTYPE,'') = 'BEN' AND TRTYPE = 1000 THEN 'MKT'  
					WHEN DRCR = 'D' AND ISNULL(ACCOUNTTYPE,'') = 'MAR' AND TRTYPE = 1000 THEN 'MKT'  
					WHEN DRCR = 'D' AND ISNULL(ACCOUNTTYPE,'') = 'POOL' AND TRTYPE = 907 THEN 'MKT' 
					ELSE 'OFF_MKT'
				END),
				PURPOSE = (
				CASE 
					WHEN DRCR = 'C' THEN 'Payin'
					ELSE 'Payout'
				END),
				ORDDATE = CONVERT(VARCHAR,TRANSDATE,112),
				DPID = D.DPID,
				FLAG_NEW = 2,
				BDPID = D.BDPID,
				BCLTDPID = D.BCLTDPID
		FROM	#DEL_TRANSSTAT_BSE D (NOLOCK),
				ANGELDEMAT.BSEDB.DBO.DELIVERYDP DP WITH (NOLOCK),
				--ON ( DP.DPID = D.BDPID AND DP.DPCLTNO = D.BCLTDPID ),
				ANAND.BSEDB_AB.DBO.CLIENT1 C1 WITH (NOLOCK)
		WHERE	--D.TRANSDATE BETWEEN @FROMDATE AND @TODATE +' 23:59:59'
				D.TRANSDATE <= @TODATE
				AND FILLER2 = 1       
				AND TRTYPE <> 906      
				AND D.PARTY_CODE <> 'BROKER'      
				AND CERTNO LIKE 'IN%' 
				AND DP.DPID = D.BDPID AND DP.DPCLTNO = D.BCLTDPID
				AND D.PARTY_CODE = C1.CL_CODE     
				AND DP.EXCHANGE IN ('NSE','BSE')
		GROUP  BY
				CONVERT(VARCHAR,TRANSDATE,105),
				CONVERT(VARCHAR,TRANSDATE,112),
				ISNULL(EXCHANGE,''),
				ISNULL(SEGMENT,''),
				D.PARTY_CODE,
				C1.LONG_NAME,
				C1.PAN_GIR_NO,
				CASE 
					WHEN DP.DPID = D.BDPID AND DP.DPCLTNO = D.BCLTDPID THEN 
					CASE 
						WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
						ELSE D.BCLTDPID 
					END
					ELSE ''
				END,
				CASE 
					WHEN DP.DPID <> D.BDPID AND DP.DPCLTNO <> D.BCLTDPID THEN 
					CASE 
						WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
						ELSE D.BCLTDPID 
					END
					ELSE ''
				END,				
				SETT_NO,
				CERTNO,
				SCRIP_CD,
				REFNO,
				DRCR,
				ISNULL(ACCOUNTTYPE,''),
				DELIVERED,
				TRTYPE,
				ISett_No,
				ISett_Type,
				REASON,
				/*CD.POA1,
				CD.POA2,
				CD.POA3,*/
				FROMNO,
				SLIPNO,
				D.DPID,
				D.BDPID,
				D.BCLTDPID


		UPDATE	#SECREG
		SET		POA_FLAG = 'YES'
		FROM	ANGELDEMAT.BSEDB.DBO.MULTICLTID M WITH (NOLOCK)
		WHERE	M.CLTDPNO = #SECREG.MEMBER_DEMAT_ACC
				AND M.DPID = #SECREG.DPID
				AND M.DEF = 1
				AND	FLAG_NEW = 2


		UPDATE	#SECREG
		SET		PARTY_CODE = (CASE WHEN #SECREG.UCC_CODE <> ISNULL(UC.UCC_CODE,'') THEN ISNULL(UC.UCC_CODE,'') ELSE '' END)
		FROM	ANAND.BSEDB_AB.DBO.UCC_CLIENT UC WITH (NOLOCK)
		WHERE	UC.PARTY_CODE = #SECREG.UCC_CODE		
				AND	FLAG_NEW = 2

		
		SELECT	TRANS_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE),105),
				EXE_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE),105),
				EXCHANGE,
				SEGMENT,
				UCC_CODE,
				PARTY_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				MEMBER_DEMAT_ACC =  CONVERT(VARCHAR(20),''),
				COUNTERPARTY_DEMAT_ACC =  CONVERT(VARCHAR(20),''),
				SETT_NO =  CONVERT(VARCHAR(15),''),
				ISIN,
				SCRIP_NAME,
				QTY_DELIVERED = 0,
				QTY_RECEIVED = 0, 
				BALANCE_QTY = ABS(SUM(QTY_DELIVERED-QTY_RECEIVED)), 
				PLEDGE_UNPLEDGE_QTY = 0,
				TRF_REF_NO =  CONVERT(VARCHAR(20),''),
				POA_FLAG =  CONVERT(VARCHAR(5),''),
				TRANSACTION_TYPE = CONVERT(VARCHAR(20),''),
				PURPOSE = 'Opening-Balance',
				ORDDATE = CONVERT(VARCHAR(10),''),
				ORDFLAG = 0				
		INTO	#SECREG_NEW
		FROM	#SECREG
		WHERE	CONVERT(DATETIME,TRANS_DATE,105) < @FROMDATE
				AND EXISTS (SELECT PARTY_CODE FROM #SECREG D2     
				WHERE #SECREG.UCC_CODE = D2.UCC_CODE AND #SECREG.ISIN = D2.ISIN     
				AND CONVERT(DATETIME,TRANS_DATE,105) >= @FROMDATE AND CONVERT(DATETIME,TRANS_DATE,105) <= @TODATE + ' 23:59:59' )
		GROUP BY 
				EXCHANGE,
				SEGMENT,
				UCC_CODE,
				PARTY_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				ISIN,
				SCRIP_NAME
		HAVING SUM(QTY_DELIVERED-QTY_RECEIVED) <> 0 

		UNION ALL

		SELECT	TRANS_DATE,
				EXE_DATE,
				EXCHANGE,
				SEGMENT,
				UCC_CODE,
				PARTY_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				MEMBER_DEMAT_ACC,
				COUNTERPARTY_DEMAT_ACC,
				SETT_NO,
				ISIN,
				SCRIP_NAME,
				QTY_DELIVERED,
				QTY_RECEIVED, 
				BALANCE_QTY = ABS(BALANCE_QTY),
				PLEDGE_UNPLEDGE_QTY,
				TRF_REF_NO = CONVERT(VARCHAR,TRF_REF_NO),
				POA_FLAG,
				TRANSACTION_TYPE,
				PURPOSE,
				ORDDATE,
				ORDFLAG = 1
		FROM	#SECREG
		WHERE	CONVERT(DATETIME,TRANS_DATE,105) BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'

		UNION ALL
		
		SELECT	TRANS_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE),105),
				EXE_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE),105),
				EXCHANGE,
				SEGMENT,
				UCC_CODE,
				PARTY_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				MEMBER_DEMAT_ACC = '',
				COUNTERPARTY_DEMAT_ACC = '',
				SETT_NO = '',
				ISIN,
				SCRIP_NAME,
				QTY_DELIVERED = 0,
				QTY_RECEIVED = 0, 
				BALANCE_QTY = ABS(SUM(QTY_DELIVERED-QTY_RECEIVED)),
				PLEDGE_UNPLEDGE_QTY = 0,
				TRF_REF_NO = '',
				POA_FLAG = '',
				TRANSACTION_TYPE = '',
				PURPOSE = 'Closing-Balance',
				ORDDATE = '',
				ORDFLAG = 2				
		FROM	#SECREG
		WHERE	CONVERT(DATETIME,TRANS_DATE,105) <= @TODATE
				AND EXISTS (SELECT PARTY_CODE FROM #SECREG D2     
				WHERE #SECREG.UCC_CODE = D2.UCC_CODE AND #SECREG.ISIN = D2.ISIN     
				AND CONVERT(DATETIME,TRANS_DATE,105) >= @FROMDATE AND CONVERT(DATETIME,TRANS_DATE,105) <= @TODATE + ' 23:59:59' )    
		GROUP BY 
				EXCHANGE,
				SEGMENT,
				UCC_CODE,
				PARTY_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				ISIN,
				SCRIP_NAME
		

	
		SELECT	* FROM #SECREG_NEW
		ORDER BY
				UCC_CODE,
				EXCHANGE,
				SEGMENT,
				ISIN,
				ORDFLAG,
				SCRIP_NAME,
				ORDDATE
		RETURN



		SELECT	TOP 1 FLAG = 1, DATA = @MEMBERNAME +','+ REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE,109),103),'/','') +' to ' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE,109),103),'/',''),
				EXCHANGE = '',
				SEGMENT = '',
				UCC_CODE = '',
				ORDDATE = '',
				SCRIP_NAME = '',
				ISIN = '',
				ORDFLAG = -1
		INTO	#REPORT
		FROM	#SECREG_NEW
		
		UNION		

		SELECT	FLAG = 2,
				DATA =	TRANS_DATE +','+ EXE_DATE +','+ EXCHANGE +','
						+ SEGMENT +','+ UCC_CODE +','+ PARTY_CODE +','
						+ CLIENT_NAME +','+ CLIENT_PAN +','+ MEMBER_DEMAT_ACC +','
						+ COUNTERPARTY_DEMAT_ACC +','+ SETT_NO +','+ ISIN +','
						+ SCRIP_NAME +','+ CONVERT(VARCHAR,QTY_DELIVERED) +','
						+ CONVERT(VARCHAR,QTY_RECEIVED) +','+ CONVERT(VARCHAR,BALANCE_QTY) +','
						+ CONVERT(VARCHAR,PLEDGE_UNPLEDGE_QTY) +','+ CONVERT(VARCHAR,TRF_REF_NO) +','
						+ POA_FLAG +','+ TRANSACTION_TYPE +','+ PURPOSE,						
				EXCHANGE,
				SEGMENT,
				UCC_CODE,
				ORDDATE,
				SCRIP_NAME,
				ISIN,
				ORDFLAG
		FROM	#SECREG_NEW
		ORDER BY
				UCC_CODE,
				EXCHANGE,
				SEGMENT,
				ISIN,
				ORDFLAG,
				SCRIP_NAME,
				ORDDATE

		SELECT	FILENAME = @FILENAME, DATA FROM #REPORT

						
		DROP TABLE #SECREG
		DROP TABLE #REPORT
		DROP TABLE #SECREG_NEW
		DROP TABLE #DEL_TRANSSTAT
		DROP TABLE #DEL_TRANSSTAT_BSE

GO
