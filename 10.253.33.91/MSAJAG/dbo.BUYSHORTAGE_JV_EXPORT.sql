CREATE PROCEDURE  [dbo].[BUYSHORTAGE_JV_EXPORT] (@TDATE DATETIME)

AS --- EXEC BUYSHORTAGE_JV_EXPORT 'JUN  4 2021'

SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,MARKETRATE,AMOUNT INTO #BUYSHORT
FROM MSAJAG.DBO.SETTLEMENT WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
AND SETT_TYPE='A' AND AUCTIONPART='FC' AND SELL_BUY='2'
UNION ALL 
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,MARKETRATE,AMOUNT 
FROM AngelBSECM.BSEDB_AB.DBO.SETTLEMENT WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN @TDATE AND @TDATE + ' 23:59' 
AND SETT_TYPE='AD' AND AUCTIONPART='N' AND SELL_BUY='2'
ORDER BY PARTY_CODE

CREATE TABLE #JV_TABLE_TMP_CR
(SRNO INT IDENTITY(1,1),VDT VARCHAR(11),EDT VARCHAR(11),CLTCODE VARCHAR(20),GLCODE VARCHAR(20),AMUNT MONEY,NARRATION VARCHAR(500),BRANCH  VARCHAR(12))

CREATE TABLE #JV_TABLE_TMP_DR
(SRNO INT IDENTITY(1,1),VDT VARCHAR(11),EDT VARCHAR(11),CLTCODE VARCHAR(20),GLCODE VARCHAR(20),AMUNT MONEY,NARRATION VARCHAR(500),BRANCH  VARCHAR(12))


INSERT INTO #JV_TABLE_TMP_CR
SELECT SAUDA_DATE,CONVERT(VARCHAR(11),GETDATE()+1,105),PARTY_CODE,'46000063',Amount,
'Being Prov Buy Shortage Credit for Sett No '+CONVERT(VARCHAR,sett_no),'ALL' FROM #BUYSHORT

INSERT INTO #JV_TABLE_TMP_DR
SELECT CONVERT(VARCHAR(11),GETDATE()+1,105),CONVERT(VARCHAR(11),GETDATE()+1,105),PARTY_CODE,'46000063',Amount,
'Being Reversal of Prov Buy Shortage Credit for Sett No '+CONVERT(VARCHAR,sett_no),'ALL' FROM #BUYSHORT

TRUNCATE TABLE MSAJAG.DBO.CRJV

INSERT INTO MSAJAG.DBO.CRJV VALUES ('Sno','VoucherDate','EffectiveDate','Party_Code','DrCr','Amount','Narration','Branch_Code')

INSERT INTO MSAJAG.DBO.CRJV
SELECT SRNO,VDT, EDT,CLTCODE=LTRIM(RTRIM(A.CLTCODE)),DRCR = 'C',A.AMUNT,A.NARRATION,A.BRANCH
FROM #JV_TABLE_TMP_CR A
UNION ALL
SELECT SRNO,VDT, EDT,CLTCODE=LTRIM(RTRIM(A.GLCODE)),DRCR = 'D',A.AMUNT,A.NARRATION,A.BRANCH
FROM #JV_TABLE_TMP_CR A
ORDER BY SRNO

TRUNCATE TABLE MSAJAG.DBO.DRJV

INSERT INTO MSAJAG.DBO.DRJV VALUES ('Sno','VoucherDate','EffectiveDate','Party_Code','DrCr','Amount','Narration','Branch_Code')

INSERT INTO MSAJAG.DBO.DRJV
SELECT SRNO,VDT, EDT,CLTCODE=LTRIM(RTRIM(A.CLTCODE)),DRCR = 'D',A.AMUNT,A.NARRATION,A.BRANCH 
FROM #JV_TABLE_TMP_DR A
UNION ALL
SELECT SRNO,VDT, EDT,CLTCODE=LTRIM(RTRIM(A.GLCODE)),DRCR = 'C',A.AMUNT,A.NARRATION,A.BRANCH
FROM #JV_TABLE_TMP_DR A
ORDER BY SRNO

DECLARE @N AS VARCHAR(2) = '2'
DECLARE @path AS VARCHAR(200) =  'J:\BACKOFFICE\EXPORT'
DECLARE @deleteSql AS VARCHAR(512)
SET @deleteSql = 'forfiles /P "' + @path + '" /S /M *.CSV /D -' + @N + ' /C "cmd /c del @PATH"'
EXEC master.dbo.xp_cmdshell @deleteSql, NO_OUTPUT

DECLARE @CRNAME VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'BUY_SHORTAGE_CRJV_' + CONVERT(VARCHAR, @TDATE, 112) + '.CSV'
DECLARE @CRBCP VARCHAR(4000) = 'BCP "SELECT * FROM MSAJAG.DBO.CRJV" QUERYOUT "' + @CRNAME + '" -c -t"," -r"\n" -T'
EXEC MASTER..XP_CMDSHELL @CRBCP, NO_OUTPUT

DECLARE @DRNAME VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'BUY_SHORTAGE_DRJV_' + CONVERT(VARCHAR, @TDATE, 112) + '.CSV'
DECLARE @DRBCP VARCHAR(4000) = 'BCP "SELECT * FROM MSAJAG.DBO.DRJV" QUERYOUT "' + @DRNAME + '" -c -t"," -r"\n" -T'
EXEC MASTER..XP_CMDSHELL @DRBCP, NO_OUTPUT

GO
