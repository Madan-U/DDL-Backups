CREATE proc [dbo].[CLI_ACC_OPEN]  ( @frdate  AS DATETIME,@todate  AS DATETIME,@TYPE AS VARCHAR(12))  
   
AS  
  
IF @TYPE='TRADING'    
  
BEGIN   
  
SELECT DISTINCT C.CL_CODE,SEGMENT,EXCHANGE,LONG_NAME,PAN_GIR_NO,ACTIVE_DATE,DEACTIVE_REMARKS,BRANCH_CD,REGION,CltDpId1 AS CLIENT_CODE INTO #TEMP1  
FROM CLIENT_BROK_DETAILS AS C WITH(NOLOCK) ,CLIENT_DETAILS AS C1 WITH (NOLOCK) WHERE ACTIVE_DATE >=@frdate AND ACTIVE_DATE <=@todate +' 23:59'  
AND C.CL_CODE >='A001' AND C.CL_CODE <='ZZZZZ' AND INACTIVE_FROM LIKE 'DEC 31 2049%' AND DEACTIVE_VALUE <>'N'  
AND C.CL_CODE=C1.CL_CODE  
  
  
  
--SELECT A.*,CLIENT_CODE INTO #TEMP1 FROM  
--(SELECT * FROM #TEMP )A  
  
--LEFT OUTER JOIN (  
  
--SELECT KIT_NO,CLIENT_CODE FROM [ABCSOORACLEMDLW].KYC_CI.DBO.CDSLAPI_REJECTION WITH(NOLOCK)   
  
--WHERE ENTITY='CDSL' AND STATUS ='A') B  
  
--ON A.CL_CODE=KIT_NO   
  
--ORDER BY ACTIVE_DATE,CL_CODE  
  
  
SELECT ROW_NUMBER() OVER(ORDER BY CL_CODE) AS SR_NO,* FROM(
SELECT DISTINCT CL_CODE,  
  
(CASE WHEN SEGMENT ='FUTURES' AND EXCHANGE IN('MCX','NCX') THEN 'COMMODITY'  
  
      WHEN SEGMENT ='FUTURES' AND EXCHANGE IN('MCD','NSX') THEN 'CURRENCY'  
  
   ELSE 'CASH' END) AS SEGMENT  
  
,LONG_NAME,PAN_GIR_NO,CONVERT(VARCHAR(11),ACTIVE_DATE,120)ACTIVE_DATE,DEACTIVE_REMARKS,BRANCH_CD,REGION,CLIENT_CODE AS DPIID  
  
 FROM #TEMP1  )A 
   
  END  
  
IF @TYPE='DP'  
  
 BEGIN   
   
 SELECT ROW_NUMBER()OVER(ORDER BY CLIENT_CODE) AS SRNO ,CLIENT_CODE as DPD,NISE_PARTY_CODE AS TRADING_CODE,SUB_TYPE,FIRST_HOLD_NAME,ITPAN,SECOND_HOLD_NAME,SECOND_HOLD_ITPAN,  
 THIRD_HOLD_NAME,THIRD_HOLD_ITPAN,NOMI_GUARD_NAME AS Nominee_Guardian,CONVERT(VARCHAR(11),ACTIVE_DATE,120) AS ACCOUNT_OPEN,
TEMPLATE_CODE AS DPTYPE  
 FROM AGMUBODPL3.DMAT.DBO.TBL_CLIENT_MASTER WITH(NOLOCK)   
 WHERE ACTIVE_DATE >=@frdate AND ACTIVE_DATE <=@todate +' 23:59'  
  
END

GO
