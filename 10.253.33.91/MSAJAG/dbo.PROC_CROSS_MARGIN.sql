-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC PROC_CROSS_MARGIN 
(@REPORT_DATE VARCHAR(11))

AS

SELECT DISTINCT RECTYPE = (CASE WHEN LEN(PARTICIPANT_CODE) > 2 
					   THEN 30
					   ELSE 20
				  END),
CL_CODE = LEFT((CASE WHEN LEN(PARTICIPANT_CODE) > 2 
					   THEN PARTICIPANT_CODE
					   ELSE C.CL_CODE
				  END),12)
INTO #CLDET FROM CLIENT_DETAILS C, CLIENT_BROK_DETAILS D, CLIENT_ADDITIONAL_DETAILS A
WHERE C.CL_CODE = D.CL_CODE
AND D.EXCHANGE = 'NSE' AND D.SEGMENT = 'FUTURES'
AND CL_TYPE <> 'PRO'
AND A.CL_CODE = C.CL_CODE
AND CROSS_MARGIN_FLAG = 1
UNION
SELECT RECTYPE = 10, CL_CODE = 'PRO_'+MEMBERCODE FROM OWNER

SELECT FILEREC='01,CMP,'+MEMBERCODE+','+CONVERT(VARCHAR,CONVERT(DATETIME,@REPORT_DATE),112)+','+RIGHT('0000000'+CONVERT(VARCHAR,COUNT(1)),7)
FROM #CLDET, OWNER
GROUP BY MEMBERCODE
UNION 
SELECT FILEREC = CONVERT(VARCHAR,RECTYPE) + ',' + CL_CODE FROM #CLDET
ORDER BY 1

TRUNCATE TABLE #CLDET
DROP TABLE #CLDET

GO
