-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

    
CREATE PROC ANGBROK_hist ( @DATE DATETIME)    
AS     
SELECT CONVERT(VARCHAR(10),SAUDA_DATE,103) AS SDATE,'NSE' AS EXCHANGE,    
PARTY_CODE,SCRIP_CD,TRADE_NO,ORDER_NO,USER_ID,SUM(TRADEQTY*MARKETRATE)AS TURNOVER,    
SUM(TRADEQTY*NBROKAPP)AS BROKERAGE,SUM(INS_CHRG+TURN_TAX+SEBI_TAX+BROKER_CHRG+NSERTAX) AS TAXES,    
(CASE WHEN BILLFLAG IN(2,3) THEN 'INTRADAY' ELSE 'DELIVERY' END ) AS TYPE    
FROM HISTORY WHERE SAUDA_DATE >=@date AND SAUDA_DATE <= @DATE +'23:59:00.000'    
AND SETT_TYPE IN ('N','W')    
GROUP BY CONVERT(VARCHAR(10),SAUDA_DATE,103),PARTY_CODE,SCRIP_CD,USER_ID,TRADE_NO,ORDER_NO,BILLFLAG    
UNION ALL    
SELECT CONVERT(VARCHAR(10),CD_SAUDA_DATE,103) AS SDATE,'NSE' AS EXCHANGE,CD_PARTY_CODE,    
CD_SCRIP_CD,CD_TRADE_NO,CD_ORDER_NO,'0' AS USER_ID,'0' AS TURNOVER,    
CD_TOTALBROKERAGE,(CD_TOTALSERTAX) AS TAXES,'MIN_BROKERAGE' AS TYPE    
FROM CHARGES_DETAIL    
WHERE CD_SAUDA_DATE >=@date AND CD_SAUDA_DATE <= @DATE +'23:59:00.000'    
AND CD_SETT_TYPE IN ('N','W')

GO
