CREATE  PROCEDURE REARRANGEAFTERHISTBILLFLAG (@SETT_NO VARCHAR(7),@SETT_TYPE VARCHAR(2),@PARTY VARCHAR(10),@SCRIP VARCHAR(12),@SERIES VARCHAR(2),@MEMCODE VARCHAR(15),@TMARK VARCHAR(1)) AS 
DECLARE 
	@@TRADE_NO VARCHAR(14),
 	@@PQTY NUMERIC(9), 
        	@@SQTY NUMERIC(9),
        	@@LTRADE_NO VARCHAR(14),
        	@@LQTY NUMERIC(9), 
 	@@PDIFF NUMERIC(9),
	@@TTRADE_NO VARCHAR(14),
	@@TFLAG INT,
	@@TRD CURSOR,
 	@@FLAG CURSOR,
 	@@LOOP CURSOR
	
	UPDATE HISTORY SET BILLFLAG = (case when s.sell_buy = 1 then 2
				       else 3   end )
	FROM HISTORY  S, CURSORBILLSALEHIST B 
	WHERE B.SETT_NO = S.SETT_NO
	AND B.SETT_TYPE = S.SETT_TYPE 
	AND B.PARTY_CODE = S.PARTY_CODE	
 	AND B.SCRIP_CD = S.SCRIP_CD           
	AND B.SERIES = S.SERIES          
              	AND  B.SQTY<> 0 AND B.PQTY <>  0 
              	AND S.TMARK = B.TMARK
	AND S.PARTIPANTCODE = B.PARTIPANTCODE 
	AND  S.PARTY_CODE = @PARTY AND S.SCRIP_CD = @SCRIP
             	AND S.SERIES = @SERIES    
	AND S.SETT_NO = @SETT_NO AND S.SETT_TYPE = @SETT_TYPE
	AND S.PARTIPANTCODE = @MEMCODE
	AND S.TMARK = @TMARK
	


	UPDATE HISTORY  SET BILLFLAG = 4 FROM HISTORY  S, CURSORBILLSALEHIST B 
	WHERE B.SETT_NO = S.SETT_NO
	AND B.SETT_TYPE = S.SETT_TYPE 
	AND B.PARTY_CODE = S.PARTY_CODE	
 	AND B.SCRIP_CD = S.SCRIP_CD           
	AND B.SERIES = S.SERIES          
                AND  B.SQTY = 0 AND B.PQTY >  0 
                AND S.SELL_BUY = 1
                AND S.TMARK = B.TMARK
	AND S.PARTIPANTCODE = B.PARTIPANTCODE 
	AND  S.PARTY_CODE = @PARTY AND S.SCRIP_CD = @SCRIP
                AND S.SERIES = @SERIES    
	AND S.SETT_NO = @SETT_NO AND S.SETT_TYPE = @SETT_TYPE
	AND S.PARTIPANTCODE = @MEMCODE
	AND S.TMARK = @TMARK

	UPDATE HISTORY  SET BILLFLAG = 5 FROM HISTORY  S, CURSORBILLSALEHIST B 
	WHERE B.SETT_NO = S.SETT_NO
	AND B.SETT_TYPE = S.SETT_TYPE 
	AND B.PARTY_CODE = S.PARTY_CODE	
 	AND B.SCRIP_CD = S.SCRIP_CD           
	AND B.SERIES = S.SERIES          
                AND  B.SQTY > 0 AND B.PQTY =  0 
                AND S.SELL_BUY = 2
                AND S.TMARK = B.TMARK
	AND S.PARTIPANTCODE = B.PARTIPANTCODE 
	AND  S.PARTY_CODE = @PARTY AND S.SCRIP_CD = @SCRIP
                AND S.SERIES = @SERIES    
	AND S.SETT_NO = @SETT_NO AND S.SETT_TYPE = @SETT_TYPE
	AND S.PARTIPANTCODE = @MEMCODE
	AND S.TMARK = @TMARK
 

	/*  APPLY SETT_FLAG =4 IF SELL_ BUY = 'B' ELSE 5  TO ALL TRADES WHERE TMARK  = 'D' */
	UPDATE HISTORY  SET BILLFLAG = 1 
	WHERE  PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES    
	 AND SETT_TYPE = @SETT_TYPE  AND SETT_NO = @SETT_NO
	AND PARTIPANTCODE = @MEMCODE AND TMARK = 'D'
	AND  TRADEQTY > 0 

	/*  APPLY SETT_FLAG = 1 TO ALL TRADES WHERE CLIENT2. TRAN_CAT   = 'DEL' */
	UPDATE HISTORY   SET BILLFLAG = 1  FROM HISTORY  T , CLIENT2 
	WHERE  CLIENT2.PARTY_CODE = T.PARTY_CODE 
	AND CLIENT2.TRAN_CAT = 'DEL'  AND
	T.PARTY_CODE = @PARTY AND T.SCRIP_CD = @SCRIP AND T.SERIES = @SERIES    
	AND  T.SETT_TYPE = @SETT_TYPE  
	AND T.PARTIPANTCODE = @MEMCODE AND T.TMARK = @TMARK
	AND  T.TRADEQTY > 0 	

	
	SET @@FLAG = CURSOR FOR
	SELECT PQTY,SQTY FROM CURSORBILLUNEQUALSALEPURHIST
 	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE
	AND PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP
 	AND SERIES = @SERIES 
	AND PARTIPANTCODE = @MEMCODE
	AND TMARK = @TMARK
	AND PQTY <> 0 AND  SQTY <> 0 AND ISNULL(PQTY,0) <> ISNULL(SQTY,0)
	GROUP BY PQTY,SQTY
	
	OPEN @@FLAG
	FETCH NEXT FROM @@FLAG INTO @@PQTY,@@SQTY
	WHILE @@FETCH_STATUS = 0 
	BEGIN
		IF @@PQTY > @@SQTY 
        		BEGIN 
  			SELECT @@PDIFF = @@PQTY - @@SQTY 
		/*
  			SET @@LOOP  = CURSOR FOR 
   			SELECT TRADE_NO,TRADEQTY FROM HISTORY 
   			WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 1 
   			AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE AND TRADEQTY = @@PDIFF  
  			
			OPEN @@LOOP
  			FETCH NEXT FROM @@LOOP INTO @@LTRADE_NO,@@LQTY     
  			IF @@FETCH_STATUS = 0 
  			BEGIN
   				UPDATE HISTORY SET BILLFLAG = 4   
   				WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 1 
   				AND TRADE_NO = @@LTRADE_NO AND TRADEQTY = @@LQTY  
   				AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
   			
				CLOSE @@LOOP
   				DEALLOCATE @@LOOP 
			END
  			ELSE IF @@FETCH_STATUS <> 0  
 	 		BEGIN 
   				CLOSE @@LOOP
   				DEALLOCATE @@LOOP  
   			*/
				SET @@LOOP  = CURSOR FOR 
    				SELECT TRADE_NO,TRADEQTY FROM HISTORY 
    				WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 1 
    				AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  and SettFlag = 4	
				/* order by marketrate asc, Tradeqty Desc  */
--				order by marketrate Desc
			        Order by Sauda_date
				OPEN @@LOOP
   				FETCH NEXT FROM @@LOOP INTO @@LTRADE_NO,@@LQTY     
   				WHILE @@PDIFF > 0       
   				BEGIN
    					IF @@PDIFF >= @@LQTY 
    					BEGIN
     						UPDATE HISTORY SET BILLFLAG = 4   
     						WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 1 
     						AND TRADE_NO = @@LTRADE_NO AND TRADEQTY = @@LQTY  
     						AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
     
						SELECT @@PDIFF = @@PDIFF - @@LQTY
    					END    
    					ELSE IF @@PDIFF < @@LQTY 
    					BEGIN
						SELECT @@TTRADE_NO = @@ltrade_no
						SELECT @@TFLAG = 1 
						WHILE @@TFLAG = 1 
						BEGIN
							SET @@TRD = CURSOR FOR
							SELECT TRADE_NO FROM HISTORY WHERE TRADE_NO = 'B' + @@TTRADE_NO
							AND party_code = @Party and scrip_cd = @scrip and series = @series and sell_buy = 1 
     							AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
							OPEN @@TRD
							FETCH NEXT FROM @@TRD INTO @@TTRADE_NO
							IF @@FETCH_STATUS = 0 
								SELECT @@TFLAG = 1
							ELSE
								SELECT @@TFLAG = 0
							CLOSE @@TRD
							DEALLOCATE @@TRD			
						END	

     						INSERT INTO HISTORY SELECT CONTRACTNO,BILLNO,'B'+TRADE_NO,PARTY_CODE,SCRIP_CD,USER_ID,@@PDiff,AUCTIONPART,MARKETTYPE,SERIES,ORDER_NO,MARKETRATE,SAUDA_DATE,TABLE_NO,LINE_NO,VAL_PERC,NORMAL,DAY_PUC,DAY_SALES,SETT_PURCH,SETT_SALES,SELL_BUY,SETTFLAG,BROKAPPLIED,NETRATE,AMOUNT,INS_CHRG,TURN_TAX,OTHER_CHRG,SEBI_TAX,BROKER_CHRG,SERVICE_TAX,TRADE_AMOUNT,4,SETT_NO,NBROKAPP,NSERTAX,N_NETRATE,SETT_TYPE,PARTIPANTCODE,STATUS,PRO_CLI,CPID,INSTRUMENT,BOOKTYPE,BRANCH_ID,TMARK,SCHEME,DUMMY1,DUMMY2
     						FROM HISTORY WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 1 
     						AND TRADE_NO = @@LTRADE_NO AND TRADEQTY = @@LQTY  
     						AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
 
						UPDATE HISTORY SET BILLFLAG = 2,TRADEQTY = @@LQTY - @@PDIFF,N_NETRATE = NETRATE,NSERTAX = SERVICE_TAX,NBROKAPP = BROKAPPLIED    
    						WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 1 
     						AND TRADE_NO = @@LTRADE_NO AND TRADEQTY = @@LQTY
     						AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
     		
						SELECT @@PDIFF = 0   
    					END 
    					FETCH NEXT FROM @@LOOP INTO @@LTRADE_NO,@@LQTY
   				END
   				CLOSE @@LOOP
  			END    
 		/*END*/
 		ELSE IF @@PQTY < @@SQTY
 		BEGIN 
  			SELECT @@PDIFF = @@SQTY - @@PQTY
  			
		/*	SET @@LOOP  = CURSOR FOR 
   			SELECT TRADE_NO,TRADEQTY FROM HISTORY 
   			WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 2 
   			AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE AND TRADEQTY = @@PDIFF  
  			
			OPEN @@LOOP
  			FETCH NEXT FROM @@LOOP INTO @@LTRADE_NO,@@LQTY     
  			
			IF @@FETCH_STATUS = 0 
  			BEGIN
   				UPDATE HISTORY SET BILLFLAG = 5  
   				WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 2 
   				AND TRADE_NO = @@LTRADE_NO AND TRADEQTY = @@LQTY  
   				AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
   				
				CLOSE @@LOOP
   				DEALLOCATE @@LOOP 
  			END
  			ELSE IF @@FETCH_STATUS <> 0  
  			BEGIN 
   				CLOSE @@LOOP
   				DEALLOCATE @@LOOP  
   			*/
				SET @@LOOP  = CURSOR FOR 
    				SELECT TRADE_NO,TRADEQTY FROM HISTORY 
    				WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 2 
    				AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  and SettFlag = 5
				/* order by marketrate asc, Tradeqty Desc  */
--				order by marketrate Desc
			        Order by Sauda_date
   
				OPEN @@LOOP
   				FETCH NEXT FROM @@LOOP INTO @@LTRADE_NO,@@LQTY     
   				
				WHILE @@PDIFF > 0       
   				BEGIN
    					IF @@PDIFF >= @@LQTY 
    					BEGIN
     						UPDATE HISTORY SET BILLFLAG = 5   
     						WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 2 
     						AND TRADE_NO = @@LTRADE_NO AND TRADEQTY = @@LQTY  
     						AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
     	
						SELECT @@PDIFF = @@PDIFF - @@LQTY
    					END    
    					ELSE IF @@PDIFF < @@LQTY 
    					BEGIN
						SELECT @@TTRADE_NO = @@ltrade_no
						SELECT @@TFLAG = 1 
						WHILE @@TFLAG = 1 
						BEGIN
							SET @@TRD = CURSOR FOR
							SELECT TRADE_NO FROM HISTORY WHERE TRADE_NO = 'B' + @@TTRADE_NO
							AND party_code = @Party and scrip_cd = @scrip and series = @series and sell_buy = 2 
     							AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
							OPEN @@TRD
							FETCH NEXT FROM @@TRD INTO @@TTRADE_NO
							IF @@FETCH_STATUS = 0 
								SELECT @@TFLAG = 1
							ELSE
								SELECT @@TFLAG = 0
							CLOSE @@TRD
							DEALLOCATE @@TRD			
						END	
    						INSERT INTO HISTORY SELECT CONTRACTNO,BILLNO,'B'+TRADE_NO,PARTY_CODE,SCRIP_CD,USER_ID,@@PDiff,AUCTIONPART,MARKETTYPE,SERIES,ORDER_NO,MARKETRATE,SAUDA_DATE,TABLE_NO,LINE_NO,VAL_PERC,NORMAL,DAY_PUC,DAY_SALES,SETT_PURCH,SETT_SALES,SELL_BUY,SETTFLAG,BROKAPPLIED,NETRATE,AMOUNT,INS_CHRG,TURN_TAX,OTHER_CHRG,SEBI_TAX,BROKER_CHRG,SERVICE_TAX,TRADE_AMOUNT,5,SETT_NO,NBROKAPP,NSERTAX,N_NETRATE,SETT_TYPE,PARTIPANTCODE,STATUS,PRO_CLI,CPID,INSTRUMENT,BOOKTYPE,BRANCH_ID,TMARK,SCHEME,DUMMY1,DUMMY2
     						FROM HISTORY WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 2 
     						AND TRADE_NO = @@LTRADE_NO AND TRADEQTY = @@LQTY  
     						AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
     
     						UPDATE HISTORY SET BILLFLAG = 3,TRADEQTY = @@LQTY - @@PDIFF,N_NETRATE = NETRATE,NSERTAX = SERVICE_TAX,NBROKAPP = BROKAPPLIED     
    						 WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES AND SELL_BUY = 2 
     						AND TRADE_NO = @@LTRADE_NO AND TRADEQTY = @@LQTY
     						AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE   
     					
						SELECT @@PDIFF = 0   
    					END 
    					FETCH NEXT FROM @@LOOP INTO @@LTRADE_NO,@@LQTY
   				END
   				CLOSE @@LOOP
  			END
 		/*END */
 		FETCH NEXT FROM @@FLAG INTO @@PQTY,@@SQTY
	END 
	CLOSE @@FLAG
	DEALLOCATE @@FLAG

/*ELSE
 UPDATE HISTORY SET BILLFLAG = 1 
 WHERE PARTY_CODE = @PARTY AND SCRIP_CD = @SCRIP AND SERIES = @SERIES  
 AND SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
 AND TRADEQTY > 0*/

GO
