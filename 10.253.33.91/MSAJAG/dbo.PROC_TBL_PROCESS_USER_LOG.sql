-- Object: PROCEDURE dbo.PROC_TBL_PROCESS_USER_LOG
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE  PROC [dbo].[PROC_TBL_PROCESS_USER_LOG]
(
	@TRADEDATE VARCHAR(11)
)
AS

-- EXEC PROC_TBL_PROCESS_USER_LOG 'MAR 24 2017'
--DECLARE @TRADEDATE VARCHAR(11)

DELETE FROM TBL_PROCESS_USER_LOG
WHERE PROCESSDATE = @TRADEDATE

INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE='NSE', SEGMENT = 'CAPITAL', 
TYPEOFTRANS = 'TRADE PROCESS', SETT_NO, SETT_TYPE, 
NOOFREC=COUNT(1), 
PROCESSED_BY = (CASE WHEN SETT_TYPE IN ('C', 'D', 'N', 'W') THEN 'BY AUTOMATION' 
		     WHEN SETT_TYPE IN ('A', 'X', 'AD', 'AC') THEN 'BY AUCTION TEAM'  
ELSE 'BY TRADE PROCESSING TEAM'END)
FROM SETTLEMENT (NOLOCK)
WHERE SAUDA_DATE LIKE @TRADEDATE + '%'
AND TRADEQTY > 0 
GROUP BY SETT_NO, SETT_TYPE

INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE = 'NSE', SEGMENT = 'CAPITAL', 
TYPEOFTRANS = 'CASH MARGIN DATA', SETT_NO = '', SETT_TYPE = '', 
NOOFREC=COUNT(1), 
PROCESSED_BY = 'BY AUTOMATION'
FROM TBL_MG02 (NOLOCK)
WHERE MARGIN_DATE = @TRADEDATE  

INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE = 'NSE', SEGMENT = 'CAPITAL', 
TYPEOFTRANS = 'LEDGER - ' + CONVERT(VARCHAR,VDESC), SETT_NO='', SETT_TYPE='', 
NOOFREC=COUNT(1), 
PROCESSED_BY = (CASE WHEN ENTEREDBY IN ('AUTO', 'ANIMESH') THEN 'BY AUTOMATION' ELSE ENTEREDBY END)
FROM ACCOUNT.DBO.LEDGER (NOLOCK), ACCOUNT.DBO.VMAST (NOLOCK)
WHERE VDT >= @TRADEDATE AND VDT <= @TRADEDATE + ' 23:59'
AND VAMT > 0 
AND VTYP = VTYPE
GROUP BY VTYP, VDESC, ENTEREDBY



INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE, SEGMENT, 
TYPEOFTRANS = 'CLIENT ADDITION', SETT_NO = '', SETT_TYPE = '', 
NOOFREC=COUNT(1), 
PROCESSED_BY = MODIFIDEDBY
FROM CLIENT_DETAILS C, CLIENT_BROK_DETAILS B 
WHERE B.ACTIVE_DATE = @TRADEDATE 
AND C.CL_CODE=B.CL_CODE
GROUP BY EXCHANGE, SEGMENT,MODIFIDEDBY


INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE='NSE', SEGMENT='CAPITAL', 
TYPEOFTRANS = 'CLIENT MODIFICATION', SETT_NO = '', SETT_TYPE = '', 
NOOFREC=COUNT(1), 
PROCESSED_BY = MODIFIDEDBY
FROM CLIENT_DETAILS C(NOLOCK), CLIENT_BROK_DETAILS B (NOLOCK)
WHERE B.MODIFIEDON = @TRADEDATE 
AND C.CL_CODE=B.CL_CODE
GROUP BY MODIFIDEDBY


INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE='', SEGMENT='', 
TYPEOFTRANS = 'CORPORATE ACTION PROCESS', SETT_NO, SETT_TYPE , 
NOOFREC=COUNT(1), 
PROCESSED_BY = 'BY AUTOMATION'
FROM TBL_NSE_CORPACT (NOLOCK)
WHERE UPLOADDATE = @TRADEDATE 
GROUP BY SETT_NO, SETT_TYPE


INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE='NSE', SEGMENT='CAPITAL', 
TYPEOFTRANS = (CASE WHEN UPLOADSTATUS ='PENDING' THEN 'CMS PENDING RECO' + BANKCODE
                   WHEN UPLOADSTATUS ='AUTORECO' THEN 'CMS AUTORECO RECO' + BANKCODE
                   WHEN UPLOADSTATUS ='MANUAL RECO' THEN 'CMS MANUAL RECO' + BANKCODE
                   WHEN UPLOADSTATUS ='REPROCESS' THEN 'CMS REPROCESS'  + BANKCODE ELSE '' END ),
SETT_NO = '', SETT_TYPE = '', 
NOOFREC=COUNT(1), 
PROCESSED_BY = UPLOADBY
FROM  ACCOUNT..RECON_CMS_DATA (NOLOCK)
WHERE MODIFIEDDATE = @TRADEDATE 
GROUP BY UPLOADBY,UPLOADSTATUS,BANKCODE


SELECT @TRADEDATE, EXCHANGE='NSE', SEGMENT='CAPITAL', 
TYPEOFTRANS = (CASE WHEN UPLOADSTATUS ='PENDING' THEN 'NONCMS PENDING RECO'+ BANKCODE
                   WHEN UPLOADSTATUS ='AUTORECO' THEN 'NONCMS AUTORECO RECO'+ BANKCODE
                   WHEN UPLOADSTATUS ='MANUAL RECO' THEN 'NONCMS MANUAL RECO'+ BANKCODE
                   WHEN UPLOADSTATUS ='REPROCESS' THEN 'NONCMS REPROCESS'+ BANKCODE ELSE '' END) ,
SETT_NO = '', SETT_TYPE = '', 
NOOFREC=COUNT(1), 
PROCESSED_BY = UPLOADBY
FROM  ACCOUNT..RECON_NONCMS_DATA (NOLOCK)
WHERE MODIFIEDDATE = @TRADEDATE 
GROUP BY UPLOADBY,UPLOADSTATUS , BANKCODE

INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE, SEGMENT, 
TYPEOFTRANS = 'COLLATERAL PROCESS - CALCULATION', SETT_NO = '', SETT_TYPE = '', 
NOOFREC=COUNT(1), 
PROCESSED_BY = 'BY AUTOMATION'
FROM COLLATERALDETAILS (NOLOCK)
WHERE EFFDATE = @TRADEDATE 
AND PARTY_CODE <> 'BROKER'
GROUP BY EXCHANGE, SEGMENT



INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE, SEGMENT, 
TYPEOFTRANS = 'COMMON CONTRACT PROCESS', SETT_NO, SETT_TYPE, 
NOOFREC=COUNT(DISTINCT CONTRACTNO), 
PROCESSED_BY = 'BY AUTOMATION'
FROM COMMON_CONTRACT_DATA
WHERE SAUDA_DATE = @TRADEDATE 
GROUP BY EXCHANGE, SEGMENT, SETT_NO, SETT_TYPE


INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE, SEGMENT, 
TYPEOFTRANS = 'CORPORATE ACTION PROCESS', SETT_NO='', SETT_TYPE='', 
NOOFREC=COUNT(1), 
PROCESSED_BY = 'BY CORPORATE ACTION TEAM'
FROM ANGELDEMAT.MSAJAG.DBO.TBL_CORPACT_DATA
WHERE RECORD_DATE = @TRADEDATE 
GROUP BY EXCHANGE, SEGMENT


INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE='NBF', SEGMENT='NBFC', 
TYPEOFTRANS = 'NBFC TRANS PROCESS', SETT_NO='', SETT_TYPE='', 
NOOFREC=COUNT(1), 
PROCESSED_BY = 'BY NBFC TEAM'
FROM ABVSCITRUS.NBFC.DBO.NBFC_TRANS
WHERE MARGINDATE = @TRADEDATE 
--GROUP BY EXCHANGE


INSERT INTO TBL_PROCESS_USER_LOG
SELECT @TRADEDATE, EXCHANGE = 'NBF', SEGMENT = 'NBFC', 
TYPEOFTRANS = 'LEDGER - ' + CONVERT(VARCHAR,VDESC), SETT_NO='', SETT_TYPE='', 
NOOFREC=COUNT(1), 
PROCESSED_BY = (CASE WHEN ENTEREDBY IN ('AUTO', 'ANIMESH') THEN 'BY AUTOMATION' ELSE ENTEREDBY END)
FROM  ABVSCITRUS.ACCOUNTNBFC.DBO.LEDGER , ABVSCITRUS.ACCOUNTNBFC.DBO.VMAST 
WHERE VDT >= @TRADEDATE AND VDT <= @TRADEDATE + ' 23:59'
AND VAMT > 0 
AND VTYP = VTYPE
GROUP BY VTYP, VDESC, ENTEREDBY




INSERT INTO TBL_PROCESS_USER_LOG
EXEC ANGELDEMAT.MSAJAG.DBO.PROC_TBL_PROCESS_USER_LOG @TRADEDATE


INSERT INTO TBL_PROCESS_USER_LOG
EXEC AngelBSECM.BSEDB_AB.DBO.PROC_TBL_PROCESS_USER_LOG @TRADEDATE


INSERT INTO TBL_PROCESS_USER_LOG
EXEC ANGELFO.NSEFO.DBO.PROC_TBL_PROCESS_USER_LOG @TRADEDATE


INSERT INTO TBL_PROCESS_USER_LOG
EXEC ANGELCOMMODITY.MCDX.DBO.PROC_TBL_PROCESS_USER_LOG @TRADEDATE


SELECT PROCESSDATE,EXCHANGE,SEGMENT,TYPEOFTRANS,SETT_NO,SETT_TYPE,NOOFREC=SUM(NOOFREC),PROCESSED_BY 
FROM TBL_PROCESS_USER_LOG
WHERE PROCESSDATE = @TRADEDATE
GROUP BY PROCESSDATE,EXCHANGE,SEGMENT,TYPEOFTRANS,SETT_NO,SETT_TYPE,PROCESSED_BY 
ORDER BY EXCHANGE,SEGMENT

GO
