-- Object: PROCEDURE dbo.PROC_ALL_BATCH_GENERATION_24102022
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROCEDURE [dbo].[PROC_ALL_BATCH_GENERATION_24102022] (@RUNDTE DATETIME,@BATCH INT,@REPORT VARCHAR(2))

AS --- EXEC PROC_ALL_BATCH_GENERATION 'SEP 26 2022','145','I'

/* DELETE OLD DATA FROM BATCH FOLDER */

DECLARE @N AS VARCHAR(2) = '0'
DECLARE @PATH AS VARCHAR(200) =  'J:\BACKOFFICE\ALLOCATION\BATCH_FILES'
DECLARE @DELETESQL AS VARCHAR(512)
SET @DELETESQL = 'FORFILES /P "' + @PATH + '" /S /M *.* /D -' + @N + ' /C "CMD /C DEL @PATH"'
EXEC MASTER.DBO.XP_CMDSHELL @DELETESQL, NO_OUTPUT 

IF @REPORT = 'F'
BEGIN
IF OBJECT_ID(N'tempdb..#NSEFO') IS NOT NULL
BEGIN 
DROP TABLE #NSEFO
END

SELECT RPTDATE=REPLACE(LEFT(CONVERT(varchar,GETDATE(),13),11),' ','-'),SEGMENT='FO',CM_CODE='12798',TM_CODE='12798',FILER1='',CLIENT_CODE,
ATYPE='C',ALLCATION_AMT=ROUND((FO_ALLOCATED_TDAY+NOTIONAL_2),2),FILER2='',FILER3='',FILER4='',FILER5='',FILER6='',FILER7='',UPLOAD='U' INTO #NSEFO
FROM MSAJAG.DBO.TBL_ALLOCATION_REPORTING WITH (NOLOCK) 
WHERE REMARKS='NCL UPLOAD' AND FO_TDAY_MAG > 0 AND SHORATGE1 > 0 AND RPT_DATE = @RUNDTE

IF OBJECT_ID(N'tempdb..#NSECD') IS NOT NULL
BEGIN 
DROP TABLE #NSECD
END

SELECT RPTDATE=REPLACE(LEFT(CONVERT(varchar,GETDATE(),13),11),' ','-'),SEGMENT='CD',CM_CODE='12798',TM_CODE='12798',FILER1='',CLIENT_CODE,
ATYPE='C',ALLCATION_AMT=ROUND((CD_ALLOCATED_TDAY+NOTIONAL_2),2),FILER2='',FILER3='',FILER4='',FILER5='',FILER6='',FILER7='',UPLOAD='U' INTO #NSECD
FROM MSAJAG.DBO.TBL_ALLOCATION_REPORTING WITH (NOLOCK) 
WHERE REMARKS='NCL UPLOAD' AND CD_TDAY_MAG > 0 AND SHORATGE1 > 0 AND CLIENT_CODE NOT IN (SELECT CLIENT_CODE FROM #NSEFO) AND RPT_DATE = @RUNDTE

IF OBJECT_ID(N'tempdb..#NSECM') IS NOT NULL
BEGIN 
DROP TABLE #NSECM
END

SELECT RPTDATE=REPLACE(LEFT(CONVERT(varchar,GETDATE(),13),11),' ','-'),SEGMENT='CM',CM_CODE='12798',TM_CODE='12798',FILER1='',CLIENT_CODE,
ATYPE='C',ALLCATION_AMT=ROUND(NCL_UPLOAD,2),FILER2='',FILER3='',FILER4='',FILER5='',FILER6='',FILER7='',UPLOAD='U' INTO #NSECM
FROM MSAJAG.DBO.TBL_ALLOCATION_REPORTING WITH (NOLOCK) 
WHERE REMARKS='NCL UPLOAD' AND CLIENT_CODE NOT IN (SELECT CLIENT_CODE FROM #NSEFO UNION SELECT CLIENT_CODE FROM #NSECD) AND RPT_DATE = @RUNDTE


IF OBJECT_ID(N'tempdb..#ALL_DATA') IS NOT NULL
BEGIN 
DROP TABLE #ALL_DATA
END

SELECT ROW_NUMBER() OVER (ORDER BY CLIENT_CODE ASC) AS SRNO, RPTDATE,SEGMENT,CM_CODE,TM_CODE,FILER1,
CLIENT_CODE,ATYPE,ALLCATION_AMT,FILER2,FILER3,FILER4,FILER5,FILER6,FILER7,UPLOAD INTO #ALL_DATA FROM (
SELECT * FROM #NSECM
UNION ALL
SELECT * FROM #NSEFO
UNION ALL
SELECT * FROM #NSECD) A



/* GENERATE BATCH FILES */

DECLARE @ID_CONTROL INT
DECLARE @BATCHSIZE INT
DECLARE @RESULTS INT
DECLARE @DELETEFILE AS VARCHAR(1000)

SET @RESULTS = 1
SET @BATCHSIZE = 10000
SET @ID_CONTROL = 0


WHILE (@RESULTS > 0) 
BEGIN

TRUNCATE TABLE MSAJAG.DBO.ALLSPLIT

INSERT INTO MSAJAG.DBO.ALLSPLIT
SELECT RPTDATE+','+SEGMENT+','+CM_CODE+','+TM_CODE+',,'+CLIENT_CODE+','+ATYPE+','+CONVERT(VARCHAR,ALLCATION_AMT)+',,,,,,,'+UPLOAD 
FROM #ALL_DATA 
WHERE  SRNO > @ID_CONTROL AND SRNO <= @ID_CONTROL + @BATCHSIZE ORDER BY SRNO

DECLARE @DIRCOLLNAMEAM VARCHAR(100) = 
'J:\BACKOFFICE\ALLOCATION\BATCH_FILES\' +'12798_ALLOC_' + REPLACE(CONVERT(VARCHAR(11),@RUNDTE,105),'-','') +
CASE WHEN LEN(@BATCH)=1 THEN '.T000' WHEN LEN(@BATCH)=2 THEN '.T00' WHEN LEN(@BATCH)=3 THEN '.T0' ELSE '.T' END + CONVERT(VARCHAR,@BATCH) 

DECLARE @DIRCOLLSTMTAM NVARCHAR(4000) = 'SELECT ALL_DATA FROM MSAJAG.DBO.ALLSPLIT'

DECLARE @DIRCOLLBCPAM VARCHAR(4000) = 'BCP "' + @DIRCOLLSTMTAM + '" QUERYOUT "' + @DIRCOLLNAMEAM + '" -c -t"," -r"\n" -T'

EXEC MASTER..XP_CMDSHELL @DIRCOLLBCPAM, NO_OUTPUT

SELECT @RESULTS = COUNT (*) FROM #ALL_DATA WHERE  SRNO > @ID_CONTROL AND SRNO <= @ID_CONTROL + @BATCHSIZE

SET @ID_CONTROL = @ID_CONTROL + @BATCHSIZE

SET @BATCH = @BATCH + 1

IF(@RESULTS=0)
BEGIN
SET @DELETEFILE = 'DEL '+@DIRCOLLNAMEAM+''

EXEC XP_CMDSHELL @DELETEFILE, NO_OUTPUT
END
END
END


/* INCREMENAL BATCH GENERATION */

IF @REPORT = 'I'
BEGIN
IF OBJECT_ID(N'tempdb..#ALL_DATA_INC') IS NOT NULL
BEGIN 
DROP TABLE #ALL_DATA_INC
END

---INCREMENNTAL BATCH GENERATION 
SELECT ROW_NUMBER() OVER (ORDER BY A.CLIENT_CODE ASC) AS SRNO,RPTDATE=REPLACE(LEFT(CONVERT(varchar,GETDATE(),13),11),' ','-'),
SEGMENT='CM',CM_CODE='12798',TM_CODE='12798',FILER1='',A.CLIENT_CODE,
ATYPE='C',ALLCATION_AMT=ROUND(A.NCL_UPLOAD,2),FILER2='',FILER3='',FILER4='',FILER5='',FILER6='',FILER7='',UPLOAD='U' INTO #ALL_DATA_INC
FROM TBL_ALLOCATION_REPORTING A LEFT OUTER JOIN TBL_ALLOCATION_REPORTING_LOG B ON (A.CLIENT_CODE = B.CLIENT_CODE)
WHERE A.REMARKS='NCL UPLOAD' AND A.NCL_UPLOAD > B.NCL_UPLOAD


/* GENERATE BATCH FILES */

DECLARE @ID_CONTROL_INC INT
DECLARE @BATCHSIZE_INC INT
DECLARE @RESULTS_INC INT
DECLARE @DELETEFILE_INC AS VARCHAR(1000)

SET @RESULTS_INC = 1
SET @BATCHSIZE_INC = 10000
SET @ID_CONTROL_INC = 0


WHILE (@RESULTS_INC > 0) 
BEGIN

TRUNCATE TABLE MSAJAG.DBO.ALLSPLIT

INSERT INTO MSAJAG.DBO.ALLSPLIT
SELECT RPTDATE+','+SEGMENT+','+CM_CODE+','+TM_CODE+',,'+CLIENT_CODE+','+ATYPE+','+CONVERT(VARCHAR,ALLCATION_AMT)+',,,,,,,'+UPLOAD 
FROM #ALL_DATA_INC 
WHERE  SRNO > @ID_CONTROL_INC AND SRNO <= @ID_CONTROL_INC + @BATCHSIZE_INC ORDER BY SRNO

DECLARE @DIRCOLLNAMEAM_INC VARCHAR(100) = 
'J:\BACKOFFICE\ALLOCATION\BATCH_FILES\' +'12798_ALLOC_' + REPLACE(CONVERT(VARCHAR(11),@RUNDTE,105),'-','') +
CASE WHEN LEN(@BATCH)=1 THEN '.T000' WHEN LEN(@BATCH)=2 THEN '.T00' WHEN LEN(@BATCH)=3 THEN '.T0' ELSE '.T' END + CONVERT(VARCHAR,@BATCH) 

DECLARE @DIRCOLLSTMTAM_INC NVARCHAR(4000) = 'SELECT ALL_DATA FROM MSAJAG.DBO.ALLSPLIT'

DECLARE @DIRCOLLBCPAM_INC VARCHAR(4000) = 'BCP "' + @DIRCOLLSTMTAM_INC + '" QUERYOUT "' + @DIRCOLLNAMEAM_INC + '" -c -t"," -r"\n" -T'

EXEC MASTER..XP_CMDSHELL @DIRCOLLBCPAM_INC, NO_OUTPUT

SELECT @RESULTS_INC = COUNT (*) FROM #ALL_DATA_INC WHERE  SRNO > @ID_CONTROL_INC AND SRNO <= @ID_CONTROL_INC + @BATCHSIZE_INC

SET @ID_CONTROL_INC = @ID_CONTROL_INC + @BATCHSIZE_INC

SET @BATCH = @BATCH + 1

IF(@RESULTS_INC=0)
BEGIN
SET @DELETEFILE_INC = 'DEL '+@DIRCOLLNAMEAM_INC+''

EXEC XP_CMDSHELL @DELETEFILE_INC, NO_OUTPUT
END
END
END

GO
