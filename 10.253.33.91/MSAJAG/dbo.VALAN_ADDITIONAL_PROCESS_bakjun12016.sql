CREATE PROC VALAN_ADDITIONAL_PROCESS
(
	@SETT_NO	VARCHAR(7),
	@SETT_TYPE	VARCHAR(2)
)
AS

DECLARE @SBCCODE VARCHAR(10) 
DECLARE @SBCCODEPAY VARCHAR(10) 

SET @SBCCODE = ''
SET @SBCCODEPAY = ''

SELECT @SBCCODE = ACCODE FROM VALANACCOUNT 
WHERE ACNAME = 'SWACHH BHARAT CESS'

SELECT @SBCCODEPAY = ACCODE FROM VALANACCOUNT 
WHERE ACNAME = 'SWACHH BHARAT CESS PAYABLE'

IF ISNULL(@SBCCODE,'') = ''
BEGIN
	RETURN
END

SELECT A.* INTO #SBC
FROM ACCBILL A, VALANACCOUNT V, GLOBALS
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE  = @SETT_TYPE
AND PARTY_CODE = ACCODE
AND V.ACNAME = 'SERVICE TAX'
AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
AND ISNULL(SBCCHARGE,0) > 0

IF (SELECT ISNULL(COUNT(1),0) FROM #SBC) > 0 
BEGIN
	UPDATE #SBC SET PARTY_CODE = @SBCCODE, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(SBCCHARGE,0) / 100,2)
	FROM GLOBALS
	WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
	AND ISNULL(SBCCHARGE,0) > 0

	SELECT A.* INTO #SERTAX
	FROM ACCBILL A, VALANACCOUNT V, GLOBALS
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE  = @SETT_TYPE
	AND PARTY_CODE = ACCODE
	AND V.ACNAME = 'SERVICE TAX'
	AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
	AND ISNULL(SBCCHARGE,0) > 0

	UPDATE #SERTAX SET AMOUNT = #SERTAX.AMOUNT - #SBC.AMOUNT
	FROM #SBC 
	WHERE #SERTAX.SETT_NO = #SBC.SETT_NO
	AND #SERTAX.SETT_TYPE = #SBC.SETT_TYPE
	AND #SERTAX.BRANCHCD = #SBC.BRANCHCD

	DELETE FROM ACCBILL
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE  = @SETT_TYPE
	AND EXISTS (SELECT PARTY_CODE FROM #SERTAX WHERE ACCBILL.PARTY_CODE = #SERTAX.PARTY_CODE)

	INSERT INTO ACCBILL
	SELECT * FROM #SBC

	INSERT INTO ACCBILL
	SELECT * FROM #SERTAX

	DROP TABLE #SERTAX
END
DROP TABLE #SBC

SELECT A.* INTO #INST_SBC
FROM IACCBILL A, VALANACCOUNT V, GLOBALS
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE  = @SETT_TYPE
AND PARTY_CODE = ACCODE
AND V.ACNAME = 'SERVICE TAX'
AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
AND ISNULL(SBCCHARGE,0) > 0

IF (SELECT ISNULL(COUNT(1),0) FROM #INST_SBC) > 0 
BEGIN
	UPDATE #INST_SBC SET PARTY_CODE = @SBCCODE, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(SBCCHARGE,0) / 100,2)
	FROM GLOBALS
	WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
	AND ISNULL(SBCCHARGE,0) > 0

	SELECT A.* INTO #INST_SERTAX
	FROM IACCBILL A, VALANACCOUNT V, GLOBALS
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE  = @SETT_TYPE
	AND PARTY_CODE = ACCODE
	AND V.ACNAME = 'SERVICE TAX'
	AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
	AND ISNULL(SBCCHARGE,0) > 0

	UPDATE #INST_SERTAX SET AMOUNT = #INST_SERTAX.AMOUNT - #INST_SBC.AMOUNT
	FROM #INST_SBC 
	WHERE #INST_SERTAX.SETT_NO = #INST_SBC.SETT_NO
	AND #INST_SERTAX.SETT_TYPE = #INST_SBC.SETT_TYPE
	AND #INST_SERTAX.BRANCHCD = #INST_SBC.BRANCHCD

	DELETE FROM IACCBILL
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE  = @SETT_TYPE
	AND EXISTS (SELECT PARTY_CODE FROM #INST_SERTAX WHERE IACCBILL.PARTY_CODE = #INST_SERTAX.PARTY_CODE)

	INSERT INTO IACCBILL
	SELECT * FROM #INST_SBC

	INSERT INTO IACCBILL
	SELECT * FROM #INST_SERTAX

	DROP TABLE #INST_SERTAX
END
DROP TABLE #INST_SBC

SELECT A.* INTO #PAYSBC
FROM ACCBILL A, VALANACCOUNT V, GLOBALS
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE  = @SETT_TYPE
AND PARTY_CODE = ACCODE
AND V.ACNAME = 'SERVICE TAX PAYABLE'
AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
AND ISNULL(SBCCHARGE,0) > 0

IF (SELECT ISNULL(COUNT(1),0) FROM #PAYSBC) > 0 
BEGIN
	UPDATE #PAYSBC SET PARTY_CODE = @SBCCODEPAY, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(SBCCHARGE,0) / 100,2)
	FROM GLOBALS
	WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
	AND ISNULL(SBCCHARGE,0) > 0

	SELECT A.* INTO #PAYSERTAX
	FROM ACCBILL A, VALANACCOUNT V, GLOBALS
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE  = @SETT_TYPE
	AND PARTY_CODE = ACCODE
	AND V.ACNAME = 'SERVICE TAX PAYABLE'
	AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
	AND ISNULL(SBCCHARGE,0) > 0

	UPDATE #PAYSERTAX SET AMOUNT = #PAYSERTAX.AMOUNT - #PAYSBC.AMOUNT
	FROM #PAYSBC 
	WHERE #PAYSERTAX.SETT_NO = #PAYSBC.SETT_NO
	AND #PAYSERTAX.SETT_TYPE = #PAYSBC.SETT_TYPE
	AND #PAYSERTAX.BRANCHCD = #PAYSBC.BRANCHCD

	DELETE FROM ACCBILL
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE  = @SETT_TYPE
	AND EXISTS (SELECT PARTY_CODE FROM #PAYSERTAX WHERE ACCBILL.PARTY_CODE = #PAYSERTAX.PARTY_CODE)

	INSERT INTO ACCBILL
	SELECT * FROM #PAYSBC

	INSERT INTO ACCBILL
	SELECT * FROM #PAYSERTAX

	DROP TABLE #PAYSERTAX
END
DROP TABLE #PAYSBC

SELECT A.* INTO #PAYINST_SBC
FROM IACCBILL A, VALANACCOUNT V, GLOBALS
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE  = @SETT_TYPE
AND PARTY_CODE = ACCODE
AND V.ACNAME = 'SERVICE TAX PAYABLE'
AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
AND ISNULL(SBCCHARGE,0) > 0

IF (SELECT ISNULL(COUNT(1),0) FROM #PAYINST_SBC) > 0 
BEGIN
	UPDATE #PAYINST_SBC SET PARTY_CODE = @SBCCODEPAY, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(SBCCHARGE,0) / 100,2)
	FROM GLOBALS
	WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
	AND ISNULL(SBCCHARGE,0) > 0

	SELECT A.* INTO #PAYINST_SERTAX
	FROM IACCBILL A, VALANACCOUNT V, GLOBALS
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE  = @SETT_TYPE
	AND PARTY_CODE = ACCODE
	AND V.ACNAME = 'SERVICE TAX PAYABLE'
	AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT 
	AND ISNULL(SBCCHARGE,0) > 0

	UPDATE #PAYINST_SERTAX SET AMOUNT = #PAYINST_SERTAX.AMOUNT - #PAYINST_SBC.AMOUNT
	FROM #PAYINST_SBC 
	WHERE #PAYINST_SERTAX.SETT_NO = #PAYINST_SBC.SETT_NO
	AND #PAYINST_SERTAX.SETT_TYPE = #PAYINST_SBC.SETT_TYPE
	AND #PAYINST_SERTAX.BRANCHCD = #PAYINST_SBC.BRANCHCD

	DELETE FROM IACCBILL
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE  = @SETT_TYPE
	AND EXISTS (SELECT PARTY_CODE FROM #PAYINST_SERTAX WHERE IACCBILL.PARTY_CODE = #PAYINST_SERTAX.PARTY_CODE)

	INSERT INTO IACCBILL
	SELECT * FROM #PAYINST_SBC

	INSERT INTO IACCBILL
	SELECT * FROM #PAYINST_SERTAX

	DROP TABLE #PAYINST_SERTAX
END
DROP TABLE #PAYINST_SBC

GO
