CREATE PROC PROC_OBL
(
	@TRADEDATE	VARCHAR(11)
)
AS

SELECT A.PARTY_CODE, EXCHANGE = 'NSE', SELL_BUY, AMOUNT FROM ACCBILL A, SETT_MST S--, TBLCLIENTMARGIN T
WHERE A.SETT_NO = S.SETT_NO AND A.SETT_TYPE = S.SETT_TYPE
AND LEFT(S.START_DATE,11) = @TRADEDATE
AND A.Party_Code NOT IN (SELECT ACCODE FROM ValanAccount)

GO
