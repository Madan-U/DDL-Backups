CREATE PROC BOLOGIN_01102015_OLD    
(@FROMDATE DATETIME,@TODATE DATETIME,@EXCH VARCHAR(10),@RPT VARCHAR(10))    
AS    
--- LOGIN_DETAILS    
IF @RPT ='U'    
 BEGIN     
  IF @EXCH ='NBFC'    
    BEGIN     
    SELECT UPPER(FLDUSERNAME) AS LOGIN_ID,UPPER(FLDFIRSTNAME) AS USER_NAME, CONVERT(VARCHAR(11),ACTION_MODIFIEDDATE,120) AS CREATION_DATE    
     FROM ABVSCITRUS.NBFC.DBO.TBLPRADNYAUSERS_AUDIT WHERE     
     ACTION_MODIFIEDDATE >=@FROMDATE AND ACTION_MODIFIEDDATE <=@TODATE + ' 23:59'    
 END       
 END    
-- DEACTIVATE LOGIN DETAILS    
IF @RPT ='D'    
 BEGIN     
  IF @EXCH ='NBFC'    
    BEGIN     
    SELECT UPPER(FLDUSERNAME) AS LOGIN_ID,UPPER(FLDFIRSTNAME) AS USER_NAME,FLDSTATUS,    
     CONVERT(VARCHAR(11),UPDDATE,120) AS UPDDATE,CONVERT(VARCHAR(11),SEPARATIONDATE,120) SEPARATIONDATE     
     FROM  DEACTIVELOGIN D, ABVSCITRUS.NBFC.DBO.TBLUSERCONTROLMASTER U, ABVSCITRUS.NBFC.DBO.TBLPRADNYAUSERS P WHERE     
      UPDDATE  >=@FROMDATE AND UPDDATE <=@TODATE + ' 23:59' AND SEGMENT ='NBFC'    
      AND BO_LOGINID= FLDUSERNAME  AND U.FLDUSERID=P.FLDAUTO AND TYPE ='QUIT'    
 END       
    
 END    
 ------ LAST LOGIN DETAILS     
    
 IF @RPT ='L'    
 BEGIN     
  IF @EXCH ='NBFC'    
    BEGIN     
    SELECT UPPER(FLDUSERNAME) AS LOGIN_ID,UPPER(FLDFIRSTNAME) AS USER_NAME,    
     MAX(ADDDT) AS LAST_LOGIN    
     FROM   ABVSCITRUS.NBFC.DBO.V2_REPORT_ACCESS_LOG U, ABVSCITRUS.NBFC.DBO.TBLPRADNYAUSERS P WHERE     
      ADDDT  >=@FROMDATE AND ADDDT <=@TODATE + ' 23:59'    
   AND  USERNAME=FLDUSERNAME       
   GROUP BY FLDUSERNAME,FLDFIRSTNAME    
 END       
 END    
--------- CATEGORY CHANGE    
    
 IF @RPT ='C'    
 BEGIN     
  IF @EXCH ='NBFC'    
    BEGIN     
    SELECT UPPER(FLDUSERNAME) AS LOGIN_ID,FLDCATEGORY_OLD AS OLD_CATEGORY,FLDCATEGORY_NEW AS NEW_CATEOGRY,    
    ACTION_MODIFIEDDATE AS MODIFIED_DATE    
     FROM   ABVSCITRUS.NBFC.DBO.TBLCATMUENU_CHANGE_AUDIT U WHERE     
      ACTION_MODIFIEDDATE  >=@FROMDATE AND ACTION_MODIFIEDDATE <=@TODATE + ' 23:59'    
 END       
    
 END

GO
