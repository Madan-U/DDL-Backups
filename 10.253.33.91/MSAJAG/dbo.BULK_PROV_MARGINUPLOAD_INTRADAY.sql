-- Object: PROCEDURE dbo.BULK_PROV_MARGINUPLOAD_INTRADAY
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[BULK_PROV_MARGINUPLOAD_INTRADAY]  
(
	@TDATE VARCHAR(10), 
	@FILEFOR VARCHAR(20), 
	@FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), 
	@UNAME VARCHAR(50),
	@FILENAME VARCHAR(100) = ''
)
AS         
      
DECLARE @STRSQL VARCHAR(500), 
		@FILECOUNTER INT,
		@FILEDT VARCHAR(8),
		@FILEDATE	DATETIME,
		@RECCNT	INT,
		@SAUDA_DATE	VARCHAR(11)
		  
SET @SAUDA_DATE = CONVERT(DATETIME,@TDATE,103)

SET @FILECOUNTER = LEFT(RIGHT(@FILENAME,6),2)

SET @FILEDT = LEFT(RIGHT(@FILENAME,16),8)

SET @FILEDATE = CONVERT(DATETIME,LEFT(@FILEDT,2)+'/'+RIGHT(LEFT(@FILEDT,4),2)+'/'+RIGHT(@FILEDT,4),103)

IF CONVERT(DATETIME,@SAUDA_DATE) <> @FILEDATE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'SELECTED DATE IS NOT MATCHING WITH THE FILE DATE.'
	RETURN
END

SELECT MARGINDATE,PARTY_CODE,VARMARGIN,MINIMUM_MARGIN=VARMARGIN,ADDITIONAL_MARGIN,MTOM_LOSS,TOTAL_MARGIN_VAR,TOTAL_MARGIN,CL_FLAG 
INTO #TBL_CMMARGIN_PROV_INTRADAY FROM TBL_CMMARGIN_PROV_INTRADAY
WHERE 1 = 2 

IF @FILETYPE = 'BLK'
BEGIN
	SET @STRSQL = LOWER('BULK INSERT #TBL_CMMARGIN_PROV_INTRADAY FROM ''' + @FILENAME  + '''  WITH (FIELDTERMINATOR = '','',ROWTERMINATOR = ''\n'')')          
	EXEC(@STRSQL)
END
ELSE
BEGIN
	INSERT INTO #TBL_CMMARGIN_PROV_INTRADAY
	SELECT 
		MARGINDATE = DBO.PIECE(FILE_DATA,',',1),
		PARTY_CODE = DBO.PIECE(FILE_DATA,',',2),
		VARMARGIN = DBO.PIECE(FILE_DATA,',',3),
		MINIMUM_MARGIN = DBO.PIECE(FILE_DATA,',',4),
		ADDITIONAL_MARGIN = DBO.PIECE(FILE_DATA,',',5),
		MTOM_LOSS = DBO.PIECE(FILE_DATA,',',6),
		TOTAL_MARGIN_VAR = DBO.PIECE(FILE_DATA,',',7),
		TOTAL_MARGIN = DBO.PIECE(FILE_DATA,',',8),
		CL_FLAG  = DBO.PIECE(FILE_DATA,',',9)
	FROM FILEDATA  (NOLOCK)
	WHERE PROGID= @PROGID
	AND ISNULL(DBO.PIECE(FILE_DATA,',',1),0) <> ''
END

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #TBL_CMMARGIN_PROV_INTRADAY
IF @RECCNT = 0 
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'NO DATA FOUND IN THE FILE, PLEASE CHECK AND IMPORT CORRECT FILE'
END

DELETE FROM TBL_CMMARGIN_PROV_INTRADAY
WHERE MARGINDATE = @FILEDATE AND FILECOUNTER = @FILECOUNTER

INSERT INTO TBL_CMMARGIN_PROV_INTRADAY
SELECT FILECOUNTER=@FILECOUNTER,MARGINDATE,PARTY_CODE,VARMARGIN,MINIMUM_MARGIN,
ADDITIONAL_MARGIN,MTOM_LOSS,TOTAL_MARGIN_VAR,TOTAL_MARGIN,CL_FLAG,
UPLOAD_ON=GETDATE(),UPLOAD_BY=''
FROM #TBL_CMMARGIN_PROV_INTRADAY

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END

DROP TABLE #TBL_CMMARGIN_PROV_INTRADAY

GO
