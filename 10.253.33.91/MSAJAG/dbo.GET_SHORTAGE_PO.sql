-- Object: PROCEDURE dbo.GET_SHORTAGE_PO
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[GET_SHORTAGE_PO]
(
	@SHRTDATE VARCHAR(11)
)

AS
	DECLARE
		@SETT_NO VARCHAR(7),
		@SETT_TYPE VARCHAR(2),
		@SETTCUR CURSOR

	CREATE TABLE #SHORTAGE
	(
		SETT_NO VARCHAR(7),
		SETT_TYPE VARCHAR(2),
		PARTY_CODE VARCHAR(10),
		PARTY_NAME VARCHAR(150),
		SCRIP_CD  VARCHAR(12),
		SERIES VARCHAR(3),
		BUYTRADEQTY INT,
		BUYREDQTY INT,
		SELLTRADEQTY INT,
		SELLRECQTY INT,
		BUYSHORTAGE INT,
		SELLSHORTAGE INT,
		CL_RATE NUMERIC(18, 4),
		CL_TYPE VARCHAR(3),
		FLAG INT
	)

	CREATE TABLE #SHORTAGE_FINAL
	(
		SETT_NO VARCHAR(7),
		SETT_TYPE VARCHAR(2),
		PARTY_CODE VARCHAR(10),
		PARTY_NAME VARCHAR(150),
		SCRIP_CD  VARCHAR(12),
		SERIES VARCHAR(3),
		SCRIP_NAME VARCHAR(50),
		BUYTRADEQTY INT,
		BUYREDQTY INT,
		SELLTRADEQTY INT,
		SELLRECQTY INT,
		BUYSHORTAGE INT,
		SELLSHORTAGE INT,
		CL_RATE NUMERIC(18, 4),
		CL_TYPE VARCHAR(3),
		FLAG INT,
		SEC_PAYIN DATETIME
	)

	SET @SETTCUR = CURSOR FOR
	SELECT
		SETT_NO,
		SETT_TYPE
	FROM
		SETT_MST
	WHERE
		SEC_PAYIN LIKE @SHRTDATE + '%'
		AND SETT_TYPE IN ('N', 'W')

	OPEN @SETTCUR
	FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE
	WHILE @@FETCH_STATUS = 0
	BEGIN
		IF
		(
		SELECT
			ISNULL(COUNT(1),0)
		FROM
			MSAJAG.DBO.ACCBILL
		WHERE
			SETT_NO = @SETT_NO
			AND SETT_TYPE = (CASE WHEN @SETT_TYPE = 'N' THEN 'A' ELSE 'X' END)
		) <= 0
		BEGIN
			INSERT INTO #SHORTAGE
			EXEC MSAJAG.DBO.RPT_SHORTAGEPAYIN 'BROKER', 'BROKER', @SETT_NO, @SETT_NO, @SETT_TYPE, '%'
		END
		FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE
	END
	CLOSE @SETTCUR
	DEALLOCATE @SETTCUR

	INSERT INTO
		#SHORTAGE_FINAL
	SELECT
		S.SETT_NO,
		S.SETT_TYPE,
		PARTY_CODE,
		PARTY_NAME,
		SCRIP_CD,
		S.SERIES,
		SCRIP_NAME = SCRIP_CD,
		BUYTRADEQTY,
		BUYREDQTY,
		SELLTRADEQTY,
		SELLRECQTY,
		BUYSHORTAGE,
		SELLSHORTAGE,
		CL_RATE,
		CL_TYPE,
		FLAG,
		SEC_PAYIN
	FROM
		#SHORTAGE S,
		SETT_MST M
	WHERE
		S.SETT_TYPE = M.SETT_TYPE
		AND S.SETT_NO = M.SETT_NO

	SET @SETTCUR = CURSOR FOR
	SELECT
		SETT_NO,
		SETT_TYPE
	FROM
		AngelBSECM.BSEDB_AB.DBO.SETT_MST
	WHERE
		SEC_PAYIN LIKE @SHRTDATE + '%'
		AND SETT_TYPE IN ('D', 'C')

	OPEN @SETTCUR
	FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE
	WHILE @@FETCH_STATUS = 0
	BEGIN
		IF
		(
		SELECT
			ISNULL(COUNT(1),0)
		FROM
			AngelBSECM.BSEDB_AB.DBO.ACCBILL
		WHERE
			SETT_NO = @SETT_NO
			AND SETT_TYPE = (CASE WHEN @SETT_TYPE = 'C' THEN 'AC' ELSE 'AD' END)
		) <= 0
		BEGIN
			INSERT INTO #SHORTAGE_FINAL
			EXEC AngelBSECM.BSEDB_AB.DBO.RPT_SHORTAGEPAYIN 'BROKER', 'BROKER', @SETT_NO, @SETT_NO, @SETT_TYPE, '%'
		END
		FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE
	END
	CLOSE @SETTCUR
	DEALLOCATE @SETTCUR

	UPDATE
		#SHORTAGE_FINAL
	SET
		CL_RATE = C.CL_RATE
	FROM
		MSAJAG.DBO.CLOSING C
	WHERE
		LEFT(C.SYSDATE,11) = (SELECT LEFT(MAX(SYSDATE),11) FROM MSAJAG.DBO.CLOSING C1
									WHERE SYSDATE <= @SHRTDATE + ' 23:59:59'
										AND C.SCRIP_CD = C1.SCRIP_CD
										AND C.SERIES IN ('EQ', 'BE')
										AND C1.SERIES IN ('EQ', 'BE'))
		AND C.SCRIP_CD = #SHORTAGE_FINAL.SCRIP_CD
		AND C.SERIES IN ('EQ', 'BE')
		AND #SHORTAGE_FINAL.SERIES IN ('EQ', 'BE')

	UPDATE
		#SHORTAGE_FINAL
	SET
		CL_RATE = C.CL_RATE
	FROM
		MSAJAG.DBO.CLOSING C
	WHERE
		LEFT(C.SYSDATE,11) = (SELECT LEFT(MAX(SYSDATE),11) FROM MSAJAG.DBO.CLOSING C1
									WHERE SYSDATE <= @SHRTDATE + ' 23:59:59'
										AND C.SCRIP_CD = C1.SCRIP_CD
										AND C.SERIES  = C1.SERIES
										AND C1.SERIES NOT IN ('EQ', 'BE'))
		AND C.SCRIP_CD = #SHORTAGE_FINAL.SCRIP_CD
		AND C.SERIES = #SHORTAGE_FINAL.SERIES
		AND C.SERIES NOT IN ('EQ', 'BE')

	UPDATE
		#SHORTAGE_FINAL
	SET
		CL_RATE = C.CL_RATE
	FROM
		AngelBSECM.BSEDB_AB.DBO.CLOSING C
	WHERE
		LEFT(C.SYSDATE,11) = (SELECT LEFT(MAX(SYSDATE),11) FROM AngelBSECM.BSEDB_AB.DBO.CLOSING C1
									WHERE SYSDATE <= @SHRTDATE + ' 23:59:59'
										AND C.SCRIP_CD = C1.SCRIP_CD
										AND C.SERIES = C1.SERIES)
		AND C.SCRIP_CD = #SHORTAGE_FINAL.SCRIP_CD
		AND C.SERIES = #SHORTAGE_FINAL.SERIES

	SELECT
		EXCHANGE = (CASE WHEN SETT_TYPE IN ('N', 'W') THEN 'NSE' ELSE 'BSE' END),
		SETT_NO,
		SETT_TYPE,
		PARTY_CODE,
		SCRIP_NAME,
		SELLSHORTAGE,
		SHORTAGETYPE = 'PAYIN',
		CL_RATE,
		SEC_PAYIN
	FROM
		#SHORTAGE_FINAL
	ORDER BY
		PARTY_CODE,
		SCRIP_NAME,
		SETT_NO,
		SETT_TYPE

	DROP TABLE
		#SHORTAGE
	DROP TABLE
		#SHORTAGE_FINAL

GO
