-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[PROC_ALL_AGGREGATOR_CALCULATION] (@RPT_DATE VARCHAR(11),@REPORT VARCHAR(10))

AS ---- PROC_ALL_AGGREGATOR_CALCULATION 'SEP 12 2022',''

IF ( SELECT COUNT (*) FROM TBL_CLIENT_COLL_ALLOCATE WITH (NOLOCK) WHERE PROCESS_DT = @RPT_DATE) <> 0
BEGIN
UPDATE A SET PRO_CASH = PROCASH, EPN_AMT = EPN FROM TBL_ALL_AGGREGATOR_TRANS A,
(SELECT PROCESS_DT,PARTY_CODE,PROCASH=SUM(PROCASH),EPN=SUM(MARGINRATIO)
FROM TBL_CLIENT_COLL_ALLOCATE WITH (NOLOCK) 
WHERE PROCESS_DT = CONVERT(DATETIME,@RPT_DATE,103) GROUP BY PROCESS_DT,PARTY_CODE) B 
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.REPORT_DATE = CONVERT(DATETIME,@RPT_DATE,103)
END
ELSE
BEGIN
SELECT 'BO Allocation Process is Pending'
RETURN
END


UPDATE TBL_ALL_AGGREGATOR_TRANS SET ELIGIABLE_AMT = RECEIPT_AMT WHERE PRO_CASH = 0

UPDATE TBL_ALL_AGGREGATOR_TRANS SET ELIGIABLE_AMT = CASE WHEN PRO_CASH > 0 THEN CASE WHEN (EPN_AMT-RECEIPT_AMT) > PRO_CASH 
														 THEN RECEIPT_AMT ELSE (EPN_AMT-PRO_CASH) END ELSE RECEIPT_AMT END

UPDATE TBL_ALL_AGGREGATOR_TRANS SET ELIGIABLE_AMT = ELIGIABLE_AMT + CASE WHEN RECEIPT_AMT > EPN_AMT 
																		 THEN (RECEIPT_AMT-EPN_AMT) ELSE 0 END WHERE ELIGIABLE_AMT=0

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=16 AND REPORT_DATE = @RPT_DATE

SELECT 'Aggregator Transaction Calculation Process Completed Successfully'

GO
