CREATE PROC [dbo].[PR_UPD_ADDITIONAL_TBL]  AS  

return
/*----------------------- NSE CM ---------------*/  

UPDATE V  
SET V.CLIENTTYPE = D.CL_TYPE  
FROM MSAJAG.DBO.CMBILLVALAN V , MSAJAG.DBO.CLIENT1 D  
WHERE V.PARTY_CODE = D.CL_CODE   
AND V.CLIENTTYPE <> D.CL_TYPE  
  

UPDATE V  
SET V.CLIENTTYPE = D.CL_TYPE  
FROM MSAJAG.DBO.NSEBSEVALAN V , MSAJAG.DBO.CLIENT1 D  
WHERE V.PARTY_CODE = D.CL_CODE   
AND V.CLIENTTYPE <> D.CL_TYPE  

  

/*----------------------- BSE CM ---------------*/  

UPDATE V  
SET V.CLIENTTYPE = D.CL_TYPE  
FROM BSEDB_ab.DBO.CMBILLVALAN V , BSEDB.DBO.CLIENT1 D  
WHERE V.PARTY_CODE = D.CL_CODE   
AND V.CLIENTTYPE <> D.CL_TYPE  

/*----------------------- NSE F&O ---------------*/  

UPDATE V  
SET V.CLIENT_TYPE = D.CL_TYPE  
FROM NSEFO.DBO.FOBILLVALAN V , NSEFO.DBO.CLIENT1 D  
WHERE V.PARTY_CODE = D.CL_CODE   
AND V.CLIENT_TYPE <> D.CL_TYPE

GO
