-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[CLS_PROC_FA_SETTIGS]
(
	@REPORTCODE VARCHAR(20)
	,@FIELD_ID VARCHAR(50)	= ''
)
AS
BEGIN
	SELECT * FROM CLS_FA_SETTINGS WHERE REPORTCODE=@REPORTCODE AND FIELD_ID= CASE WHEN @FIELD_ID ='' THEN FIELD_ID ELSE @FIELD_ID END
END

GO
