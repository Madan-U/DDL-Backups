-- Object: PROCEDURE dbo.COMBINE_MARGIN_REPORTING_AUTO
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[COMBINE_MARGIN_REPORTING_AUTO]
(
	@FLAG VARCHAR(20),

	@SAUDA VARCHAR(11)
)

AS

	SET @SAUDA=CONVERT(VARCHAR(11),@SAUDA,100)
	
IF @FLAG = 'AFTER_APPORVAL'

	BEGIN 
	
		INSERT INTO  CMR_AUTO_PROCESS_LOG
		SELECT @SAUDA,GETDATE(),'AFTER_APPORVAL','START'

		EXEC msdb.DBO.SP_START_JOB 'COMBINE_MARGIN_REPORTING_AUTO_JOB' 
		WAITFOR DELAY '00:00:10'; 

		INSERT INTO  CMR_AUTO_PROCESS_LOG
		SELECT @SAUDA,GETDATE(),'AFTER_APPORVAL','END'

		SELECT 'COMBINE_MARGIN_REPORTING PROCESS STARTED SUCCESSFULLY FOR ' + @SAUDA  AS [STATUS]

	END

IF (@SAUDA IS NULL OR @FLAG IS NULL)

	BEGIN
		SELECT ' PROCESS TESTED COMPLETED SUCCESSFULLY.....!!!!' AS [STATUS] 
	END

GO
