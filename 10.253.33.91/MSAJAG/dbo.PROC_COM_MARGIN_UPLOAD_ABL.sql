CREATE PROC [dbo].[PROC_COM_MARGIN_UPLOAD_ABL] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @RECCNT	INT

TRUNCATE TABLE TDAY_MARGIN_DATA_COM


INSERT INTO TDAY_MARGIN_DATA_COM
SELECT CONVERT(DATETIME,@TDATE,103) CD_MARGIN_DATE,
.DBO.PIECE(FILE_DATA,',',1),.DBO.PIECE(FILE_DATA,',',2),.DBO.PIECE(FILE_DATA,',',3)
FROM FILEDATA WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) <> 'Client Code'

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=13 AND REPORT_DATE = CONVERT(DATETIME,@TDATE,103)

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA WHERE PROGID = @PROGID

DELETE FROM FILEDATA WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
