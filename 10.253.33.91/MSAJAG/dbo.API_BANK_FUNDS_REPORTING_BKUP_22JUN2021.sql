CREATE PROCEDURE [dbo].[API_BANK_FUNDS_REPORTING_BKUP_22JUN2021]
	@FR_DATE VARCHAR(11),
	@TO_DATE VARCHAR(11),
	@BANK_CODE VARCHAR(10) = '',
	@EXCSEGMENT VARCHAR(500) = ''
AS
 
CREATE TABLE [DBO].[#BBDBNAME_LIST] (
	ACCOUNTDB VARCHAR(20)
	,EXCHANGE VARCHAR(6)
	,SEGMENT VARCHAR(10)
	,ACCOUNTSERVER VARCHAR(50)
	,SHAREDB VARCHAR(20)
	) ON [PRIMARY]

DECLARE
	@DB1 VARCHAR(1000)
	,@SQL VARCHAR(5000)
	,@SEGORD1 SMALLINT
	,@MAINREC1 AS CURSOR
	,@ACCOUNTDB1 VARCHAR(20)
	,@SHAREDB1 VARCHAR(20)
	,@ACCOUNTSVR1 VARCHAR(50)
	,@EXCHANGE1 VARCHAR(15)
	,@SEGMENT1 VARCHAR(15)

IF @EXCSEGMENT <> ''
	SELECT @DB1 = " INSERT INTO #BBDBNAME_LIST SELECT ACCOUNTDB, EXCHANGE, SEGMENT, ACCOUNTSERVER, SHAREDB FROM PRADNYA.DBO.VW_REPORTING_MULTICOMPANY (NOLOCK) WHERE EXCHANGE+'~'+SEGMENT IN ('" + REPLACE(@EXCSEGMENT, ',', ''',''') + "') ORDER BY EXCHANGE "
ELSE
	SELECT @DB1 = " INSERT INTO #BBDBNAME_LIST SELECT ACCOUNTDB, EXCHANGE, SEGMENT, ACCOUNTSERVER, SHAREDB FROM PRADNYA.DBO.VW_REPORTING_MULTICOMPANY (NOLOCK) ORDER BY EXCHANGE "

--PRINT @DB1
EXEC (@DB1)



DECLARE @FILENAME   VARCHAR(50)
DECLARE @BATCHNO	INT  
DECLARE @MEMBERCODE VARCHAR(20)
DECLARE @MEMBERNAME	VARCHAR(500)

SELECT @MEMBERCODE = MEMBERCODE, @MEMBERNAME = ISNULL(LTRIM(RTRIM(COMPANY)),'') FROM OWNER (NOLOCK)
SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
WHERE FLDFILENO = 'BB'  
AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
  
IF @BATCHNO = 1  
BEGIN  
	INSERT INTO ASIANFILEBATCHNO VALUES ('BB', @BATCHNO, GETDATE(), GETDATE())  
END  
ELSE  
BEGIN  
	UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'BB' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
END  
  
SET	@FILENAME = @MEMBERCODE + '_BB_' + REPLACE(CONVERT(VARCHAR,GETDATE(),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) 




CREATE TABLE #BBLEDGER_PL
(
	VDT DATETIME,
	EDT DATETIME,
	LONG_NAME VARCHAR(150),
	BANK_ACC_NO VARCHAR(30),
	UCC_CODE VARCHAR(30),
	CLTCODE VARCHAR(10),
	CLIENT_PAN VARCHAR(50),
	NARRATION VARCHAR(500),
	VNO VARCHAR(12),
	VTYP INT,
	SETT_NO VARCHAR(10),
	CHQ_NO VARCHAR(30),
	DRAMOUNT MONEY,
	CRAMOUNT MONEY,
	RUNNING_BALANCE MONEY,
	OPBAL MONEY,
	PDT DATETIME,
	BANK_CODE VARCHAR(10),
	EXCHANGE VARCHAR(10),
	SEGMENT VARCHAR(10)
)

SET @MAINREC1 = CURSOR
FOR

SELECT ACCOUNTDB
	,EXCHANGE
	,SEGMENT
	,ACCOUNTSERVER
	,SHAREDB
FROM #BBDBNAME_LIST

SET @SEGORD1 = 1

OPEN @MAINREC1

FETCH NEXT
FROM @MAINREC1
INTO @ACCOUNTDB1
	,@EXCHANGE1
	,@SEGMENT1
	,@ACCOUNTSVR1
	,@SHAREDB1

WHILE @@FETCH_STATUS = 0
BEGIN

	SET @SQL = "INSERT INTO #BBLEDGER_PL EXEC " + @ACCOUNTSVR1 + "." + @ACCOUNTDB1 + ".DBO.BANK_FUNDS_REPORTING '" + @FR_DATE + "','" + @TO_DATE + "','"  + @EXCHANGE1 + "','"  + @SEGMENT1 + "','"  + @BANK_CODE + "'"
	PRINT @SQL
	EXEC (@SQL)

	
FETCH NEXT
	FROM @MAINREC1
	INTO @ACCOUNTDB1
		,@EXCHANGE1
		,@SEGMENT1
		,@ACCOUNTSVR1
		,@SHAREDB1
END

CLOSE @MAINREC1

CREATE TABLE #BBLEDGER_PL_DETAILS_FINAL
(
	VDT DATETIME,
	EDT DATETIME,
	LONG_NAME VARCHAR(100),
	BANK_ACC_NO VARCHAR(30),
	UCC_CODE VARCHAR(12),
	CLTCODE VARCHAR(10),
	CLIENT_PAN VARCHAR(30),
	NARRATION VARCHAR(500),
	VNO VARCHAR(12),
	VTYP INT,
	SETT_NO VARCHAR(10),
	CHQ_NO VARCHAR(30),
	DRAMOUNT MONEY,
	CRAMOUNT MONEY,
	RUNNING_BALANCE MONEY,
	OPBAL MONEY,
	PDT DATETIME,
	BANK_CODE VARCHAR(10),
	EXCHANGE VARCHAR(10),
	SEGMENT VARCHAR(10),
	PURPOSE VARCHAR(500),
	SNO INT IDENTITY(1, 1)
)

INSERT INTO #BBLEDGER_PL_DETAILS_FINAL
SELECT B.*, PURPOSE = PURPOSE_REMARKS FROM #BBLEDGER_PL B, PURPOSE_MASTER P WHERE B.VTYP = P.VTYP ORDER BY BANK_CODE, EXCHANGE, SEGMENT, VDT, PDT

UPDATE L SET RUNNING_BALANCE = OPBAL + (SELECT SUM(CRAMOUNT - DRAMOUNT) FROM #BBLEDGER_PL_DETAILS_FINAL L1 WHERE L1.BANK_CODE = L.BANK_CODE AND L1.EXCHANGE = L.EXCHANGE AND L1.SEGMENT = L.SEGMENT AND L1.SNO <= L.SNO) FROM #BBLEDGER_PL_DETAILS_FINAL L

DECLARE
	@MEMBER_CODE VARCHAR(200),
	@BANK_NAME VARCHAR(100),
	@ACCCODE VARCHAR(25)

SELECT @MEMBER_CODE = COMPANY FROM OWNER

