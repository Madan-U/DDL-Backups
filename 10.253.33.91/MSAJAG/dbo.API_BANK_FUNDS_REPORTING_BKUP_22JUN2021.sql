INSERT INTO #REPORT_DATA
SELECT ISNULL(BANK_CODE,''), ISNULL(EXCHANGE,''), ISNULL(SEGMENT,''), REPORTDATA = ISNULL(REPLACE(CONVERT(VARCHAR(10), VDT, 103), '/', '-') ,'')+ ',' + ISNULL(REPLACE(CONVERT(VARCHAR(10), EDT, 103), '/', '-'),'') + ',' + ISNULL(LONG_NAME,'') + ',' + ISNULL(BANK_ACC_NO,'') + ',' + ISNULL(UCC_CODE,'') + ',' + ISNULL(CLTCODE,'') + ',' + ISNULL(CLIENT_PAN,'') + ',' + ISNULL(NARRATION,'') + ',' + ISNULL(VNO,'') + ',' + ISNULL(SETT_NO, '') + ',' + ISNULL(CHQ_NO, '') + ',' + ISNULL(CONVERT(VARCHAR, DRAMOUNT),'') + ',' + ISNULL(CONVERT(VARCHAR, CRAMOUNT),'') + ',' + ISNULL(CONVERT(VARCHAR, RUNNING_BALANCE),'') + ',' + ISNULL(PURPOSE,'') + ',' + ISNULL(EXCHANGE,'')+ ','+ISNULL(SEGMENT,'') FROM #BBLEDGER_PL_DETAILS_FINAL ORDER BY  EXCHANGE, SEGMENT

SELECT FILENAME = @FILENAME, REPORTDATA FROM #REPORT_DATA ORDER BY BANK_CODE, SNO

GO
