 SET   TDAY_MTM_SHORT = TDAY_MTM_SHORT + PRADNYA.DBO.FNMAX(/*(CASE WHEN (TDAY_MARGIN_SHORT - TDAY_MARGIN) > 0 THEN (TDAY_MARGIN_SHORT - TDAY_MARGIN) ELSE 0 END) */       
     + PRADNYA.DBO.FNMAX(((T1DAY_CASH+T1DAY_NONCASH+TDAY_MTM)-(TDAY_CASH + TDAY_NONCASH))  
     +(CASE WHEN (TDAY_CASH + TDAY_NONCASH)<0 THEN (TDAY_CASH + TDAY_NONCASH) ELSE 0 END)  
     ,0)+ PRADNYA.DBO.FNMIN((CASE WHEN (TDAY_CASH + TDAY_NONCASH)>0 THEN   
       (TDAY_MARGIN_SHORT) ELSE 0 END ), PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN,0)),0)     
 WHERE MARGINDATE = @MARGINDATE    
   
 IF CONVERT(DATETIME,@MARGINDATE) <= CONVERT(DATETIME,'SEP 15 2020')
 BEGIN 
	 UPDATE TBL_COMBINE_REPORTING_TDAY SET TDAY_MARGIN_SHORT = TDAY_MARGIN_SHORT -   
			 (CASE WHEN (TDAY_MARGIN_SHORT - TDAY_MARGIN + ISNULL(TDAY_MARGIN_CASH,0)) > 0 THEN PRADNYA.DBO.FNMIN(TDAY_MTM, (TDAY_MARGIN_SHORT - TDAY_MARGIN + ISNULL(TDAY_MARGIN_CASH,0)))  
			 ELSE 0 END),  
	 TDAY_MTM_SHORT = TDAY_MTM_SHORT + (CASE WHEN (TDAY_MARGIN_SHORT - TDAY_MARGIN + ISNULL(TDAY_MARGIN_CASH,0)) > 0 THEN PRADNYA.DBO.FNMIN(TDAY_MTM, (TDAY_MARGIN_SHORT - TDAY_MARGIN + ISNULL(TDAY_MARGIN_CASH,0)))  
			 ELSE 0 END)  
	 FROM (SELECT CL_CODE=PARTY_CODE, TDAY_MARGIN_CASH = SUM(TDAY_MARGIN), TDAY_MTM_CASH = SUM(TDAY_MTM)  
	 FROM #COMBINE_REPORTING_DETAIL   
	 WHERE MARGINDATE  = @MARGINDATE  
	 AND SEGMENT = 'CAPITAL'-- AND PARTY_CODE ='K90758'   
	 GROUP BY PARTY_CODE) D  
	 WHERE TBL_COMBINE_REPORTING_TDAY.MARGINDATE  = @MARGINDATE  
	 AND TBL_COMBINE_REPORTING_TDAY.PARTY_CODE = D.CL_CODE   
	 AND (TDAY_MARGIN_SHORT - TDAY_MARGIN + ISNULL(TDAY_MARGIN_CASH,0)) > 0   
	 AND TDAY_MTM > TDAY_MTM_SHORT 
END

