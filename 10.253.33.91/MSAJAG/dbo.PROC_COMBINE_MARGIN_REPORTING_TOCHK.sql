 SELECT * FROM TBL_COMBINE_REPORTING_DETAIL
 WHERE MARGINDATE = @MARGINDATE AND PARTY_CODE = 'G73513' 
 INSERT INTO TBL_COMBINE_REPORTING_TDAY
 SELECT * FROM TBL_COMBINE_REPORTING
 WHERE MARGINDATE = @MARGINDATE AND PARTY_CODE = 'G73513' 
 
 
 SELECT * INTO #COMBINE_REPORTING_DETAIL FROM TBL_COMBINE_REPORTING_DETAIL
 WHERE MARGINDATE = @MARGINDATE AND PARTY_CODE = 'G73513' 
  
	SELECT TRANSDATE, PARTY_CODE, MARGIN_BENEFIT = SUM(ROUND(MARGIN_BENEFIT,2)), CASHBENEFIT = SUM(CASHBENEFIT)
	INTO #EPNPOOL
	FROM TBL_EPN_BENEFIT
	WHERE TRANSDATE = @MARGINDATE AND PARTY_CODE = 'G73513'
	AND MARGIN_BENEFIT + CASHBENEFIT > 0 
	GROUP BY TRANSDATE, PARTY_CODE

UPDATE TBL_COMBINE_REPORTING_TDAY SET TDAY_MARGIN = TDAY_MARGIN - #EPNPOOL.MARGIN_BENEFIT, TDAY_NONCASH = TDAY_NONCASH-MARGIN_BENEFIT
FROM #EPNPOOL
WHERE MARGINDATE = @MARGINDATE AND TBL_COMBINE_REPORTING_TDAY.PARTY_CODE = #EPNPOOL.PARTY_CODE

UPDATE #COMBINE_REPORTING_DETAIL SET TDAY_MARGIN = TDAY_MARGIN - #EPNPOOL.MARGIN_BENEFIT 
FROM #EPNPOOL
WHERE MARGINDATE = @MARGINDATE AND #COMBINE_REPORTING_DETAIL.PARTY_CODE = #EPNPOOL.PARTY_CODE
AND EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL' AND ISNULL(TDAY_MARGIN,0) > 0 

UPDATE TBL_COMBINE_REPORTING_TDAY_DETAIL SET TDAY_MARGIN = TDAY_MARGIN - #EPNPOOL.MARGIN_BENEFIT 
FROM #EPNPOOL
WHERE MARGINDATE = @MARGINDATE AND TBL_COMBINE_REPORTING_TDAY_DETAIL.PARTY_CODE = #EPNPOOL.PARTY_CODE
AND EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL'

UPDATE TBL_COMBINE_REPORTING_TDAY_DETAIL SET TDAY_NONCASH = TDAY_NONCASH-MARGIN_BENEFIT
FROM #EPNPOOL
WHERE MARGINDATE = @MARGINDATE AND TBL_COMBINE_REPORTING_TDAY_DETAIL.PARTY_CODE = #EPNPOOL.PARTY_CODE
AND EXCHANGE = 'EPN' AND SEGMENT = 'EPN'

UPDATE TBL_COMBINE_REPORTING_TDAY   
SET   
TDAY_MARGIN_SHORT = TDAY_CASH + TDAY_NONCASH    
WHERE MARGINDATE = @MARGINDATE   


IF (SELECT ISNULL(SUM(T1DAY_MARGIN+T1DAY_MTM),0) FROM  TBL_COMBINE_REPORTING_TDAY WHERE MARGINDATE = @MARGINDATE) > 0   
BEGIN     
	SET @DAY = 1 
 UPDATE TBL_COMBINE_REPORTING_TDAY SET --TDAY_MARGIN = TDAY_MARGIN - ISNULL(TDAY_MARGIN_CASH,0),  
 TDAY_MTM = TDAY_MTM - ISNULL(TDAY_MTM_CASH,0)   
 FROM (SELECT CL_CODE=PARTY_CODE, TDAY_MARGIN_CASH = SUM(TDAY_MARGIN), TDAY_MTM_CASH = SUM(TDAY_MTM)  
 FROM #COMBINE_REPORTING_DETAIL   
 WHERE MARGINDATE  = @MARGINDATE  
 AND SEGMENT = 'CAPITAL'-- AND PARTY_CODE ='K90758'   
 GROUP BY PARTY_CODE) D  
 WHERE TBL_COMBINE_REPORTING_TDAY.MARGINDATE  = @MARGINDATE  
 AND TBL_COMBINE_REPORTING_TDAY.PARTY_CODE = D.CL_CODE   
 
