 	UPDATE TBL_COMBINE_REPORTING_TDAY SET TDAY_MTM = TDAY_MTM + ISNULL(OTHERMARGIN,0) 
	FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW_DATA M
	WHERE MDATE = (SELECT MAX(MDATE) FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW_DATA
	WHERE MDATE <= @MARGINDATE )
	AND TBL_COMBINE_REPORTING_TDAY.PARTY_CODE = M.PARTY_CODE 
	AND TBL_COMBINE_REPORTING_TDAY.MARGINDATE  = @MARGINDATE  

 	UPDATE TBL_COMBINE_REPORTING_TDAY SET TDAY_MTM = TDAY_MTM + ISNULL(OTHERMARGIN,0) 
	FROM ANGELCOMMODITY.NCDX.DBO.FOMARGINNEW_DATA M
