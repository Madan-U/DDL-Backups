 BEGIN    
	UPDATE TBL_COMBINE_REPORTING_TDAY SET TDAY_MARGIN = TDAY_MARGIN + ISNULL(TDAY_MARGIN_CASH,0), TDAY_MTM = TDAY_MTM + ISNULL(TDAY_MTM_CASH,0),  
