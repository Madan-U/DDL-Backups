BEGIN     
  
 UPDATE TBL_COMBINE_REPORTING_TDAY_DETAIL   
  
 SET TDAY_MARGIN_SHORT = T1.TDAY_MARGIN_SHORT,  
  
  TDAY_MTM_SHORT = T1.TDAY_MTM_SHORT,  
  
  T1DAY_MARGIN_SHORT = T1.T1DAY_MARGIN_SHORT,  
  
  T1DAY_MTM_SHORT = T1.T1DAY_MTM_SHORT,  
  
  T2DAY_MARGIN_SHORT = T1.T2DAY_MARGIN_SHORT,  
  
  T2DAY_MTM_SHORT = T1.T2DAY_MTM_SHORT,  
  
  --T_MARGINAVL = (CASE WHEN T1.TDAY_MARGIN_SHORT >= 0 THEN T1.TDAY_MARGIN ELSE T1.TDAY_MARGIN_SHORT END),  
  
  --T_MTMAVL = (CASE WHEN T1.TDAY_MTM_SHORT >= 0 THEN T1.TDAY_MTM ELSE T1.TDAY_MTM_SHORT END)   
  
  T_MARGINAVL = (CASE WHEN T1.TDAY_MARGIN_SHORT >= 0 THEN T1.TDAY_MARGIN   
  
  ELSE (CASE WHEN T1.TDAY_MARGIN  > ABS(T1.TDAY_MARGIN_SHORT) THEN T1.TDAY_MARGIN_SHORT ELSE -T1.TDAY_MARGIN END) END),  
  
  T_MTMAVL = (CASE WHEN T1.TDAY_MTM_SHORT >= 0 THEN T1.TDAY_MTM   
  
  ELSE (CASE WHEN T1.TDAY_MTM  > ABS(T1.TDAY_MTM_SHORT) THEN T1.TDAY_MTM_SHORT ELSE -T1.TDAY_MTM END) END)   
  
 FROM TBL_COMBINE_REPORTING_TDAY T1  
  
 WHERE TBL_COMBINE_REPORTING_TDAY_DETAIL.MARGINDATE = @MARGINDATE   
  
 AND T1.MARGINDATE = @MARGINDATE  
  
 AND TBL_COMBINE_REPORTING_TDAY_DETAIL.PARTY_CODE = T1.PARTY_CODE  
  
 AND TBL_COMBINE_REPORTING_TDAY_DETAIL.EXCHANGE = T1.EXCHANGE  
  
 AND TBL_COMBINE_REPORTING_TDAY_DETAIL.SEGMENT = T1.SEGMENT  
  
END  
  
ELSE  
  
BEGIN  
   
  
 UPDATE TBL_COMBINE_REPORTING_TDAY_DETAIL  
  
 SET T_MARGINAVL = TDAY_MARGIN   
  
 WHERE MARGINDATE = @MARGINDATE   
  
 AND EXISTS (SELECT PARTY_CODE FROM TBL_COMBINE_REPORTING_TDAY  
  
    WHERE MARGINDATE = @MARGINDATE   
  
    AND TBL_COMBINE_REPORTING_TDAY_DETAIL.PARTY_CODE = TBL_COMBINE_REPORTING_TDAY.PARTY_CODE  
  
    AND TDAY_MARGIN_SHORT >= 0)  
  
  
  
 UPDATE TBL_COMBINE_REPORTING_TDAY_DETAIL  
  
 SET T_MTMAVL = TDAY_MTM  
  
 WHERE MARGINDATE = @MARGINDATE   
  
 AND EXISTS (SELECT PARTY_CODE FROM TBL_COMBINE_REPORTING_TDAY  
  
    WHERE MARGINDATE = @MARGINDATE   
  
    AND TBL_COMBINE_REPORTING_TDAY_DETAIL.PARTY_CODE = TBL_COMBINE_REPORTING_TDAY.PARTY_CODE  
  
    AND TDAY_MTM_SHORT >= 0)  
  
 SELECT distinct PARTY_CODE INTO #PARTYCNT FROM TBL_COMBINE_REPORTING_TDAY_DETAIL  
 WHERE MARGINDATE = @MARGINDATE and EXCHANGE <> 'POA'  
 AND TDAY_MARGIN + TDAY_MTM > 0   
 GROUP BY PARTY_CODE   
 HAVING COUNT(1) = 1  
    
  CREATE CLUSTERED INDEX PARTIDX ON #PARTYCNT  
  (  
  PARTY_CODE  
  )  
  
 UPDATE TBL_COMBINE_REPORTING_TDAY_DETAIL  
 SET T_MARGINAVL = T1.TDAY_MARGIN_SHORT, T_MTMAVL = T1.TDAY_MTM_SHORT  
 FROM TBL_COMBINE_REPORTING_TDAY T1  
 WHERE TBL_COMBINE_REPORTING_TDAY_DETAIL.MARGINDATE = @MARGINDATE   
 AND T1.MARGINDATE = @MARGINDATE AND TBL_COMBINE_REPORTING_TDAY_DETAIL.TDAY_MARGIN + TBL_COMBINE_REPORTING_TDAY_DETAIL.TDAY_MTM > 0   
 AND TBL_COMBINE_REPORTING_TDAY_DETAIL.PARTY_CODE = T1.PARTY_CODE  
 AND EXISTS (SELECT PARTY_CODE FROM #PARTYCNT  
    WHERE #PARTYCNT.PARTY_CODE = TBL_COMBINE_REPORTING_TDAY_DETAIL.PARTY_CODE)  
	
SELECT * INTO #V2_TBL_EXCHANGE_MARGIN_REPORT FROM V2_TBL_EXCHANGE_MARGIN_REPORT

 IF CONVERT(DATETIME,@MARGINDATE) > CONVERT(DATETIME,'SEP 15 2020')
 BEGIN
	UPDATE #V2_TBL_EXCHANGE_MARGIN_REPORT SET EXCHANGE_PRIORITY = 0 WHERE SEGMENT = 'CAPITAL'
 end
       
 SET @MRGCUR = CURSOR FOR  
 SELECT PARTY_CODE, AVAL_MARGIN=TDAY_MARGIN_SHORT FROM TBL_COMBINE_REPORTING_TDAY  
 WHERE MARGINDATE = @MARGINDATE  AND TDAY_MARGIN_SHORT < 0   
 AND NOT EXISTS (SELECT PARTY_CODE FROM #PARTYCNT  
    WHERE #PARTYCNT.PARTY_CODE = TBL_COMBINE_REPORTING_TDAY.PARTY_CODE)  
   ORDER BY PARTY_CODE   
  
 OPEN @MRGCUR  
 FETCH NEXT FROM @MRGCUR INTO @PARTY_CODE, @SHRT_AMT  
 WHILE @@FETCH_STATUS = 0  
 BEGIN  
  SET @EXCHANGEWISE_CURSOR = CURSOR FOR  
  SELECT PARTY_CODE, T.EXCHANGE, T.SEGMENT, TDAY_MARGIN, EXCHANGE_PRIORITY   
  FROM TBL_COMBINE_REPORTING_TDAY_DETAIL T, #V2_TBL_EXCHANGE_MARGIN_REPORT T1  
  WHERE MARGINDATE = @MARGINDATE  
  AND PARTY_CODE = @PARTY_CODE  
  AND T.EXCHANGE = T1.EXCHANGE   
  AND T.SEGMENT = T1.SEGMENT   
  AND TDAY_MARGIN > 0  
  ORDER BY EXCHANGE_PRIORITY DESC  
  
  OPEN @EXCHANGEWISE_CURSOR  
  
  FETCH NEXT FROM @EXCHANGEWISE_CURSOR INTO @PARTY_CODE, @EXCHANGE_NEW, @SEGMENT_NEW, @MARG_AMT, @EXCHANGE_PRIORITY  
  WHILE @@FETCH_STATUS = 0  
  
  BEGIN  
  
   IF @SHRT_AMT < 0   
  
   BEGIN  
  
    IF @MARG_AMT >= ABS(@SHRT_AMT)  
  
