WHERE MARGINDATE  = @MARGINDATE   
AND TDAY_MTM > 0 AND TDAY_MTM_SHORT - TDAY_MTM < 0   

IF (SELECT ISNULL(SUM(TDAY_MARGIN+TDAY_MTM),0) FROM TBL_COMBINE_REPORTING_TDAY 
				WHERE MARGINDATE = @MARGINDATE) >= 0 
BEGIN  
	UPDATE TBL_COMBINE_REPORTING_TDAY SET TDAY_MARGIN_SHORT = 0,TDAY_MTM_SHORT=0
	WHERE MARGINDATE = @MARGINDATE AND T2DAY_LEDGER >= 0 AND TDAY_MARGIN_SHORT < 0 
