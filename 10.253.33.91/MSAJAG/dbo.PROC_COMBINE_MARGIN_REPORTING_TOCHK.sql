	UPDATE TBL_COMBINE_REPORTING_TDAY SET TDAY_MARGIN_SHORT =0 ---PRADNYA.DBO.FNMIN(ABS(TDAY_MARGIN_SHORT),ABS(T2DAY_LEDGER))
