CREATE PROC [dbo].[CLS_GET_GLOBALNINFO] AS

SELECT 
	FLDENCRYPTION,FLDPWDMAXLENGTH,FLDPWDMINLENGTH,FLDSPCLCHAR,FLDBLOCKPWD,
	FLDOLDPASSWORD,FLDPWDALPHANUMONLY 
FROM TBLGLOBALPARAMS (NOLOCK)

GO
