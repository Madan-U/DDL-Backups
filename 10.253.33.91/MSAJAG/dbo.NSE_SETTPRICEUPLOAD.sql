--------------------------------------------------

CREATE  PROC [dbo].[NSE_SETTPRICEUPLOAD]   
(  
 @CLDATE VARCHAR(11),  
 @FILENAME VARCHAR(200)  
)  
AS  
  
-- EXEC NSE_SETTPRICEUPLOAD 'MAY 16 2019','D:\BACKOFFICE\CM_MTM_PRICES_16052019.CSV'  
DECLARE @STRSQL  VARCHAR(500)  
  
SELECT SCRIP_CD,SERIES,ISIN,CL_RATE=CONVERT(FLOAT,0) INTO #CLOSING_MTM   
FROM CLOSING_MTM  
WHERE 1 = 2   
  
SET @STRSQL = LOWER('BULK INSERT #CLOSING_MTM FROM ''' + @FILENAME  + '''  WITH ( FIELDTERMINATOR = '','', ROWTERMINATOR = ''\N'',FIRSTROW=2 )')        
EXEC(@STRSQL)    
  
DELETE FROM CLOSING_MTM WHERE SYSDATE = @CLDATE  
  
INSERT INTO CLOSING_MTM  
SELECT @CLDATE, SCRIP_CD, SERIES, ISIN, CL_RATE=CONVERT(NUMERIC(18,4),CL_RATE), GETDATE(), 'ANI'  
FROM #CLOSING_MTM

GO
