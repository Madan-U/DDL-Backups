CREATE PROC [dbo].[PROC_ALL_CC01_RESPONSE_UPLOAD_ABL] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @RECCNT	INT

TRUNCATE TABLE TBL_CLIENT_RESPONSE

INSERT INTO TBL_CLIENT_RESPONSE
SELECT DBO.PIECE(FILE_DATA,',',1) AS REPORT_DATE,
DBO.PIECE(FILE_DATA,',',2) SEGMENT,
DBO.PIECE(FILE_DATA,',',3) TM_CODE,
DBO.PIECE(FILE_DATA,',',4) CM_CODE,
DBO.PIECE(FILE_DATA,',',5) CP_CODE,
DBO.PIECE(FILE_DATA,',',6) PARTY_CODE,
DBO.PIECE(FILE_DATA,',',7) ACCOUNT_TYPE,
CONVERT(MONEY,DBO.PIECE(FILE_DATA,',',8)) AMOUNT,
DBO.PIECE(FILE_DATA,',',9) FILLER1,
DBO.PIECE(FILE_DATA,',',10) FILLER2,
DBO.PIECE(FILE_DATA,',',11) FILLER3,
DBO.PIECE(FILE_DATA,',',12) FILLER4,
DBO.PIECE(FILE_DATA,',',13) FILLER5,
DBO.PIECE(FILE_DATA,',',14) FILLER6,
DBO.PIECE(FILE_DATA,',',15) ACTION_TYPE,
DBO.PIECE(FILE_DATA,',',16) RESPONSE_CODE
FROM FILEDATA WHERE PROGID = @PROGID 

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA WHERE PROGID = @PROGID

DELETE FROM FILEDATA WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'RESPONSE UPLOADED WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
