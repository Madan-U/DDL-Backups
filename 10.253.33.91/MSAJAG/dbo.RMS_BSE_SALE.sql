-- Object: PROCEDURE dbo.RMS_BSE_SALE
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC RMS_BSE_SALE      
(        
 @TRADEDATE VARCHAR(11)        
)        
AS        
-- EXEC RMS_BSE_SALE 'FEB 26 2013'        
      
DECLARE @SNO INT,      
  @RECCNT INT,      
  @BATCHNO INT      
        
SELECT FILENAME=CONVERT(VARCHAR(50),''),      
SELL_BUY = 'S',      
QTY = TOSELLQTY,      
DIS_QTY = CEILING(CASE WHEN TOSELLQTY > 15 THEN TOSELLQTY / 10 ELSE TOSELLQTY END),      
SCRIP_CD = SERIES,      
CL_RATE = CONVERT(NUMERIC(18,2),CL_RATE - CL_RATE*2.00/100.00),      
PARTY_CODE=LTRIM(RTRIM(PARTY_CODE)),      
FILLER1='EOSESS',      
FILLER2='CLIENT',      
FILLER3='L'      
INTO #RMS        
FROM TBL_RMS_SALE        
WHERE PROCESS_DATE = @TRADEDATE AND EXCHANGE = 'BSE'        
AND PARTY_CODE IN (SELECT PCODE FROM V_USERLIST      
         WHERE CATEGORY IN (2,4))  
  
UPDATE #RMS SET CL_RATE = ((FLOOR(( CL_RATE * POWER(10,2)+ 5 + -2.5)/(5 + 0 )) * (5 + 0))/POWER(10,2))  
WHERE CL_RATE > 10
  
UPDATE #RMS SET CL_RATE = CL_RATE * 100  
               
ALTER TABLE #RMS      
ADD SNO INT IDENTITY(1,1)      
      
SELECT @SNO = MAX(SNO) FROM #RMS      
      
SET @RECCNT = 1      
      
SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0) FROM ASIANFILEBATCHNO      
WHERE FLDFILENO = @TRADEDATE       
AND FLDFILENO = 998      
      
WHILE @RECCNT <= @SNO      
BEGIN       
 SET @BATCHNO = @BATCHNO + 1       
 UPDATE #RMS SET FILENAME = 'OUTPUT_BSE_T6T7'       
              + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@TRADEDATE),103),'/','')      
              + convert(varchar,@BATCHNO) + '.dot'      
 WHERE SNO >= @RECCNT AND SNO < @RECCNT + 500      
       
 SET @RECCNT = @RECCNT + 500      
 INSERT INTO ASIANFILEBATCHNO VALUES (998, @BATCHNO, @TRADEDATE, GETDATE())      
END      
      
SELECT FILENAME,SELL_BUY,QTY,DIS_QTY,SCRIP_CD,  
CL_RATE=CONVERT(VARCHAR,CONVERT(NUMERIC(18,0),CL_RATE)),  
PARTY_CODE,FILLER1,FILLER2,FILLER3 FROM #RMS      
ORDER BY 1, PARTY_CODE      
      
DROP TABLE #RMS

GO
