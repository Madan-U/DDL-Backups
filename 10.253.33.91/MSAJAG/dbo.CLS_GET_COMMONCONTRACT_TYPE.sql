-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[CLS_GET_COMMONCONTRACT_TYPE]  

AS

SELECT DISTINCT CONTRACT_TYPE, FNO_CONTRACT_BILL,CONTRACTGROUP=0 
FROM AngelNseCM.MSAJAG.dbo.TBL_COMMONCONTRACT_MASTER
WHERE GETDATE() BETWEEN EFFECTIVE_FROM AND EFFECTIVE_TO

GO
