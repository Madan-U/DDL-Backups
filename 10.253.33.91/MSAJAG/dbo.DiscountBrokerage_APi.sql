Create Proc DiscountBrokerage_APi (@CL_CODE VARCHAR(10))
AS
DECLARE @CNT INT
 

SELECT @CNT=COUNT(1) FROM TBL_ADDITIONAL_BROEKRAGE WHERE PARTY_CODE =@CL_CODE AND TODATE > GETDATE()
IF @CNT =1  
 BEGIN 
   Print 'Client Already Registered in Nse Segment'
 END  

Else 
BEGIN 
INSERT INTO TBL_ADDITIONAL_BROEKRAGE
SELECT
'' BRANCH_CD,'' SUB_BROKER,CD.CL_CODE PARTY_CODE,20.00 TRD_BROK_VAL,20.00 DEL_BROK_VAL,CONVERT(DATETIME,(CONVERT(vARCHAR(11),GETDATE(),120) )) FROMDATE,
'2049-12-31 23:59:59:000' TODATE,'AUTO' ADDED_BY,
GETDATE() ADDED_ON,'' MODIFIED_BY,GETDATE() MODIFIED_ON 
FROM CLIENT1 CD WITH(NOLOCK) ,CLIENT5 AS C5 
WHERE CD.CL_CODE=C5.CL_CODE 
AND CD.CL_CODE =@CL_CODE 

Print @CL_CODE +' : Client Added in NSE Segment'
END

GO
