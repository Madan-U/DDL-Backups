CREATE PROCEDURE PROC_ALL_CC01_RESPOSE_EXPORT_ABL (@RPT_DATE VARCHAR(11),@REPORT VARCHAR(10))

AS --- EXEC PROC_ALL_CC01_RESPOSE_EXPORT_ABL 'SEP 20 2022',''

DECLARE @PRV_DATE VARCHAR(11),@EPN_SETT_NO VARCHAR(11)

SELECT @EPN_SETT_NO = SETT_NO-1 FROM SETT_MST WHERE SETT_TYPE='N' AND START_DATE  BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59:59'
SELECT @PRV_DATE = LEFT(START_DATE,11) FROM SETT_MST WHERE SETT_TYPE='N' AND SETT_NO = @EPN_SETT_NO


/* DELETE OLD DATA FROM FOLDER */

DECLARE @N AS VARCHAR(2) = '0'
DECLARE @PATH AS VARCHAR(200) =  'J:\BACKOFFICE\ALLOCATION'
DECLARE @DELETESQL AS VARCHAR(512)
SET @DELETESQL = 'FORFILES /P "' + @PATH + '" /S /M *.* /D -' + @N + ' /C "CMD /C DEL @PATH"'
EXEC MASTER.DBO.XP_CMDSHELL @DELETESQL, NO_OUTPUT 


/* RESPONSE FILE DATA */

SELECT PARTY_CODE,AMOUNT=CASE WHEN A.RESPONSE_CODE='01050100' THEN A.AMOUNT ELSE 0 END,G_SEC=CONVERT(MONEY,0),REPLEDGE=CONVERT(MONEY,0),
TOTAL_AMT=CASE WHEN A.RESPONSE_CODE='01050100' THEN A.AMOUNT ELSE 0 END,MARGIN_AMT=CONVERT(MONEY,0),RESPONSE_CODE INTO #CC01_CHANGE_CM
FROM TBL_CLIENT_RESPONSE A WITH (NOLOCK) 
WHERE REPORT_DATE = @RPT_DATE AND SEGMENT='CM' AND RESPONSE_CODE='01050100'

SELECT PARTY_CODE,AMOUNT=CASE WHEN A.RESPONSE_CODE='01050100' THEN A.AMOUNT ELSE 0 END,G_SEC=CONVERT(MONEY,0),REPLEDGE=CONVERT(MONEY,0),
TOTAL_AMT=CASE WHEN A.RESPONSE_CODE='01050100' THEN A.AMOUNT ELSE 0 END,MARGIN_AMT=CONVERT(MONEY,0),RESPONSE_CODE INTO #CC01_CHANGE_FO
FROM TBL_CLIENT_RESPONSE A WITH (NOLOCK) 
WHERE REPORT_DATE = @RPT_DATE AND SEGMENT='FO' AND RESPONSE_CODE='01050100'

SELECT PARTY_CODE,AMOUNT=CASE WHEN A.RESPONSE_CODE='01050100' THEN A.AMOUNT ELSE 0 END,G_SEC=CONVERT(MONEY,0),REPLEDGE=CONVERT(MONEY,0),
TOTAL_AMT=CASE WHEN A.RESPONSE_CODE='01050100' THEN A.AMOUNT ELSE 0 END,MARGIN_AMT=CONVERT(MONEY,0),RESPONSE_CODE INTO #CC01_CHANGE_CD
FROM TBL_CLIENT_RESPONSE A WITH (NOLOCK) 
WHERE REPORT_DATE = @RPT_DATE AND SEGMENT='CD' AND RESPONSE_CODE='01050100'


----NSECM
INSERT INTO #CC01_CHANGE_CM
SELECT PARTY_CODE,ALLOCATION_AMT,CONVERT(MONEY,0),CONVERT(MONEY,0),ALLOCATION_AMT,CONVERT(MONEY,0),CONVERT(MONEY,0)
FROM TBL_CC01_DATA_ABL WITH (NOLOCK) WHERE TRANSDATE=@PRV_DATE AND EXCHANGE='NSE' AND SEGMENT='CAPITAL' AND PARTY_CODE IN 
(SELECT PARTY_CODE FROM TBL_CLIENT_RESPONSE WHERE REPORT_DATE = @RPT_DATE AND SEGMENT='CM' AND RESPONSE_CODE!='01050100')

INSERT INTO #CC01_CHANGE_CM
SELECT PARTY_CODE,ALLOCATION_AMT,CONVERT(MONEY,0),CONVERT(MONEY,0),ALLOCATION_AMT,CONVERT(MONEY,0),CONVERT(MONEY,0)
FROM TBL_CC01_DATA_ABL WITH (NOLOCK) 
WHERE TRANSDATE=@PRV_DATE AND EXCHANGE='NSE' AND SEGMENT='CAPITAL'
AND PARTY_CODE NOT IN (SELECT PARTY_CODE FROM #CC01_CHANGE_CM)

TRUNCATE TABLE CC01_TEMP_EXPORT

INSERT INTO CC01_TEMP_EXPORT
SELECT PARTY_CODE,AMOUNT=AMOUNT,G_SEC,REPLEDGE,TOTAL_AMT=TOTAL_AMT,MARGIN_AMT
FROM #CC01_CHANGE_CM 

DECLARE @NSECM VARCHAR(100) = 'J:\BACKOFFICE\ALLOCATION\' +'CC01_CM_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RPT_DATE), 112) + '.CSV'
DECLARE @NSE VARCHAR(MAX)                            
SET @NSE = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT PARTY_CODE,CONVERT(VARCHAR,CAST(AMOUNT AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(G_SEC AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(REPLEDGE AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(TOTAL_AMT AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(MARGIN_AMT AS NUMERIC(18,2)))'
SET @NSE = @NSE + ' FROM MSAJAG.DBO.CC01_TEMP_EXPORT WITH (NOLOCK)" QUERYOUT ' +@NSECM+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'
EXEC(@NSE)



----NSEFO
INSERT INTO #CC01_CHANGE_FO
SELECT PARTY_CODE,ALLOCATION_AMT,0,0,ALLOCATION_AMT,0,0
FROM TBL_CC01_DATA_ABL WITH (NOLOCK) WHERE TRANSDATE=@PRV_DATE AND EXCHANGE='NSE' AND SEGMENT='FUTURES' AND PARTY_CODE IN 
(SELECT PARTY_CODE FROM TBL_CLIENT_RESPONSE WHERE REPORT_DATE = @RPT_DATE AND SEGMENT='FO' AND RESPONSE_CODE!='01050100')

INSERT INTO #CC01_CHANGE_FO
SELECT PARTY_CODE,ALLOCATION_AMT,0,0,ALLOCATION_AMT,0,0 FROM TBL_CC01_DATA_ABL WITH (NOLOCK) 
WHERE TRANSDATE=@PRV_DATE AND EXCHANGE='NSE' AND SEGMENT='FUTURES'
AND PARTY_CODE NOT IN (SELECT PARTY_CODE FROM #CC01_CHANGE_FO)

TRUNCATE TABLE CC01_TEMP_EXPORT

INSERT INTO CC01_TEMP_EXPORT
SELECT PARTY_CODE,AMOUNT=AMOUNT,G_SEC,REPLEDGE,TOTAL_AMT=TOTAL_AMT,MARGIN_AMT
FROM #CC01_CHANGE_FO

DECLARE @NSEFO VARCHAR(100) = 'J:\BACKOFFICE\ALLOCATION\' +'CC01_FO_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RPT_DATE), 112) + '.CSV'
DECLARE @NSEF VARCHAR(MAX)                            
SET @NSEF = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT PARTY_CODE,CONVERT(VARCHAR,CAST(AMOUNT AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(G_SEC AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(REPLEDGE AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(TOTAL_AMT AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(MARGIN_AMT AS NUMERIC(18,2)))'
SET @NSEF = @NSEF + ' FROM MSAJAG.DBO.CC01_TEMP_EXPORT WITH (NOLOCK)" QUERYOUT ' +@NSEFO+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'
EXEC(@NSEF)



----NSECD
INSERT INTO #CC01_CHANGE_CD
SELECT PARTY_CODE,ALLOCATION_AMT,0,0,ALLOCATION_AMT,0,0
FROM TBL_CC01_DATA_ABL WITH (NOLOCK) WHERE TRANSDATE=@PRV_DATE AND EXCHANGE='NSX' AND SEGMENT='FUTURES' AND PARTY_CODE IN 
(SELECT PARTY_CODE FROM TBL_CLIENT_RESPONSE WHERE REPORT_DATE = @RPT_DATE AND SEGMENT='CD' AND RESPONSE_CODE!='01050100')

INSERT INTO #CC01_CHANGE_CD
SELECT PARTY_CODE,ALLOCATION_AMT,0,0,ALLOCATION_AMT,0,0 FROM TBL_CC01_DATA_ABL WITH (NOLOCK) 
WHERE TRANSDATE=@PRV_DATE AND EXCHANGE='NSX' AND SEGMENT='FUTURES'
AND PARTY_CODE NOT IN (SELECT PARTY_CODE FROM #CC01_CHANGE_CD)

TRUNCATE TABLE CC01_TEMP_EXPORT

INSERT INTO CC01_TEMP_EXPORT
SELECT PARTY_CODE,AMOUNT=AMOUNT,G_SEC,REPLEDGE,TOTAL_AMT=TOTAL_AMT,MARGIN_AMT
FROM #CC01_CHANGE_CD

DECLARE @NSECD VARCHAR(100) = 'J:\BACKOFFICE\ALLOCATION\' +'CC01_CD_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RPT_DATE), 112) + '.CSV'
DECLARE @NSEC VARCHAR(MAX)                            
SET @NSEC = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT PARTY_CODE,CONVERT(VARCHAR,CAST(AMOUNT AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(G_SEC AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(REPLEDGE AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(TOTAL_AMT AS NUMERIC(18,2))),CONVERT(VARCHAR,CAST(MARGIN_AMT AS NUMERIC(18,2)))'
SET @NSEC = @NSEC + ' FROM MSAJAG.DBO.CC01_TEMP_EXPORT WITH (NOLOCK) ORDER BY 1" QUERYOUT ' +@NSECD+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'
EXEC(@NSEC)


SELECT 'CCO1 Files Successfully Exported to - J:\BACKOFFICE\ALLOCATION\'

GO
