CREATE PROC V2_OFFLINE_CLIENTMASTER_Optimised
(
	@StatusId  Varchar(15),
	@StatusName    Varchar(25),
	@ModifyDate    VARCHAR(11),
	@ToDate        VARCHAR(11),
	@ModifyType    VARCHAR(1),
	@FromPartyCode VARCHAR(10),
	@ToPartyCode   VARCHAR(10),
	@BranchCd      VARCHAR(10),
	@SubBroker     VARCHAR(10)
)

AS

-- EXEC V2_OFFLINE_CLIENTMASTER_Optimised 'BROKER','BROKER','20120826','20120827','I','','','All','All'

BEGIN
	SELECT @ModifyDate = LEFT(CONVERT(DATETIME,REPLACE(REPLACE(@ModifyDate,'/',''),'-',''),112),11),
			@ToDate = LEFT(CONVERT(DATETIME,REPLACE(REPLACE(@ToDate,'/',''),'-',''),112),11)

	IF ISNULL(@FromPartyCode,'') = '' OR @FromPartyCode = '%'
	BEGIN
	  SELECT @FromPartyCode = '0000000000'
	END

	IF ISNULL(@ToPartyCode,'') = '' OR @ToPartyCode = '%'
	BEGIN        
	  SELECT @ToPartyCode = 'ZZZZZZZZZZ'        
	END        

	IF ISNULL(@BranchCd,'ALL') = 'ALL'        
	BEGIN        
	  SELECT @BranchCd = '%'        
	END        

	IF ISNULL(@SubBroker,'ALL') = 'ALL'        
	BEGIN        
	  SELECT @SubBroker = '%'        
	END


	SELECT CL_CODE,PARTY_CODE,LONG_NAME,BRANCH_CD,SUB_BROKER,TRADER,IMP_STATUS = 'NEW',
	NSE=CONVERT(VARCHAR(3),'NO'),BSE=CONVERT(VARCHAR(3),'NO'),
	FO=CONVERT(VARCHAR(3),'NO'),NCDX=CONVERT(VARCHAR(3),'NO'),MCX=CONVERT(VARCHAR(3),'NO')
	INTO #CLIENT_DETAILS
	FROM  CLIENT_DETAILS WITH(NOLOCK)
	WHERE ModifidedOn >= @MODIFYDATE AND ModifidedOn <= @TODATE + ' 23:59:59'


	SELECT DISTINCT CL_CODE
	INTO #CL
	FROM  CLIENT_BROK_DETAILS WITH(NOLOCK)         
	WHERE SYSTEMDATE >= @MODIFYDATE AND SYSTEMDATE <= @TODATE + ' 23:59:59'

	DELETE FROM #CL WHERE Cl_Code IN (SELECT Cl_Code FROM #CLIENT_DETAILS)

	INSERT INTO #CLIENT_DETAILS
	SELECT CD.cl_code,PARTY_CODE,LONG_NAME,BRANCH_CD,SUB_BROKER,TRADER,
	IMP_STATUS='NEW',NSE='NO',BSE='NO',FO='NO',NCDX='NO',MCX='NO'
	FROM  CLIENT_DETAILS CD WITH(NOLOCK),#CL
	WHERE CD.cl_code = #cl.Cl_Code

	SELECT CBD.Cl_Code,EXCHANGE,SEGMENT,SYSTEMDATE,Remarks='YES'
	INTO #CLIENT_BROK_DETAILS
	FROM CLIENT_BROK_DETAILS CBD WITH (NOLOCK)
	INNER JOIN #CLIENT_DETAILS CD ON CBD.Cl_Code = CD.CL_CODE

	UPDATE #CLIENT_BROK_DETAILS SET Remarks = 'NEW'
	WHERE SYSTEMDATE >= @ModifyDate AND SYSTEMDATE <= @TODATE + ' 23:59:59'

	UPDATE CD SET NSE = Remarks
	FROM #CLIENT_BROK_DETAILS CBD, #CLIENT_DETAILS CD
	WHERE CBD.Cl_Code = CD.CL_CODE AND EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL'

	UPDATE CD SET BSE = Remarks
	FROM #CLIENT_BROK_DETAILS CBD, #CLIENT_DETAILS CD
	WHERE CBD.Cl_Code = CD.CL_CODE AND EXCHANGE = 'BSE' AND SEGMENT = 'CAPITAL'

	UPDATE CD SET FO = Remarks
	FROM #CLIENT_BROK_DETAILS CBD, #CLIENT_DETAILS CD
	WHERE CBD.Cl_Code = CD.CL_CODE AND EXCHANGE = 'NSE' AND SEGMENT = 'FUTURES'

	UPDATE CD SET NCDX = Remarks
	FROM #CLIENT_BROK_DETAILS CBD, #CLIENT_DETAILS CD
	WHERE CBD.Cl_Code = CD.CL_CODE AND EXCHANGE = 'NCX' AND SEGMENT = 'FUTURES'

	UPDATE CD SET MCX = Remarks
	FROM #CLIENT_BROK_DETAILS CBD, #CLIENT_DETAILS CD
	WHERE CBD.Cl_Code = CD.CL_CODE AND EXCHANGE = 'MCX' AND SEGMENT = 'FUTURES'
	
	SELECT * FROM #CLIENT_DETAILS
END

GO
