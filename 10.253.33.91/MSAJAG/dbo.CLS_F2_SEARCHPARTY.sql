  --EXEC CLS_F2_SEARCHPARTY 'A','Z','NSE','CAPITAL','BROKER','BROKER','PARTY','%'  
CREATE PROCEDURE [dbo].[CLS_F2_SEARCHPARTY]          
(          
 @FROMCODE VARCHAR(20) = '0',      
 @TOCODE VARCHAR(20) = 'ZZZZZZZZZZ',            
 @EXCHANGE VARCHAR(3) = '',      
 @SEGMENT VARCHAR(7) = '',      
 @STATUSID VARCHAR(25),      
 @STATUSNAME VARCHAR(25),      
 @SEARCHWHAT VARCHAR(20) = 'PARTY',      
 @CLTYPE VARCHAR(3) = ''       
)          
AS       
SET NOCOUNT ON;          
IF @TOCODE = ''        
SET @TOCODE = 'ZZZZZ'        
         
SELECT TOP 10 PARTY_CODE AS CODE, LONG_NAME FROM CLIENT1 (NOLOCK), CLIENT2 (NOLOCK)  
WHERE CLIENT1.CL_CODE = CLIENT2.CL_CODE  
AND PARTY_CODE BETWEEN @FROMCODE AND @TOCODE  
AND @STATUSNAME = (  
    CASE  
  WHEN @STATUSID = 'BRANCH'  
  THEN BRANCH_CD  
  WHEN @STATUSID = 'SUBBROKER'  
  THEN SUB_BROKER  
  WHEN @STATUSID = 'TRADER'  
  THEN TRADER  
  WHEN @STATUSID = 'FAMILY'  
  THEN FAMILY  
  WHEN @STATUSID = 'AREA'  
  THEN AREA  
  WHEN @STATUSID = 'REGION'  
  THEN REGION  
  WHEN @STATUSID = 'CLIENT'  
  THEN PARTY_CODE  
 ELSE 'BROKER'  
    END)

GO
