CREATE PROC [dbo].[PR_RESTRICTED_SCRIP_DATA]     
(    
  @FILEPATH VARCHAR(1000),@FROM_DATE VARCHAR(11) ,@TO_DATE VARCHAR(11)     
 )    
 AS    
 BEGIN    
    
 /*    
 EXEC PR_RESTRICTED_SCRIP_DATA 'J:\Backoffice\RESTRICTED_SCRIP\RESTRICTED_SCRIP.csv','2021-07-07','2021-08-07'    
    
 CREATE TABLE RESTRICTED_SCRIP_DATA    
(SRNO INT,SYMBOL VARCHAR(12),SCRIP_CD INT,ISIN VARCHAR(20))    
    
INSERT INTO RESTRICTED_SCRIP_DATA VALUES ('1','FCSSOFT','532666','INE512B01022')    
= '2021-07-27'    
*/    
TRUNCATE TABLE RESTRICTED_SCRIP_DATA    
    
--DECLARE @filePath varchar(500)='J:\Backoffice\RESTRICTED_SCRIP\RESTRICTED_SCRIP.csv'    
  DECLARE @sql NVARCHAR(4000) = 'BULK INSERT RESTRICTED_SCRIP_DATA FROM ''' + @filePath + ''' WITH ( FIELDTERMINATOR ='','', ROWTERMINATOR =''\n'',FirstRow=2 )';          
  EXEC(@sql)     
    
IF OBJECT_ID ('TEMPDB.DBO.#SCRIP_CD_NSE') IS NOT NULL    
BEGIN    
DROP TABLE #SCRIP_CD_NSE    
END    
    
IF OBJECT_ID ('TEMPDB.DBO.#SCRIP_CD_BSE') IS NOT NULL    
BEGIN    
DROP TABLE #SCRIP_CD_BSE    
END    
    
    
    
/* NSE FETCHING SCRIP_CD BASED ON ISIN   */    
    
SELECT DISTINCT SCRIP_CD INTO #SCRIP_CD_NSE FROM MULTIISIN WITH (NOLOCK)    
WHERE ISIN  IN (SELECT ISIN FROM RESTRICTED_SCRIP_DATA)    
    
/* BSE FETCHING SCRIP_CD BASED ON ISIN   */    
    
SELECT DISTINCT SCRIP_CD INTO #SCRIP_CD_BSE FROM [AngelBSECM].BSEDB_AB.DBO.MULTIISIN WITH (NOLOCK)    
WHERE ISIN  IN (SELECT ISIN FROM RESTRICTED_SCRIP_DATA)    
    
IF OBJECT_ID ('TEMPDB.DBO.#COMB') IS NOT NULL    
BEGIN    
DROP TABLE #COMB    
END    
/* INDEX  ON TEMP TABLE */    
    
CREATE INDEX IX_SCRIP_CD ON #SCRIP_CD_NSE ( SCRIP_CD)    
    
CREATE INDEX IX_SCRIP_CD_BSE ON #SCRIP_CD_BSE ( SCRIP_CD)    
/* FETCHING THE DATA   */    
    
SELECT PARTY_CODE, LONG_NAME=PARTY_NAME,SCRIP_CD,ISIN,MEMBERTYPE, COMPANYNAME,    
PTRADEDQTY = SUM(PQTYTRD + PQTYDEL) ,PTRADEDAMT = SUM(PAMTTRD + PAMTDEL) ,STRADEDQTY = SUM(SQTYTRD + SQTYDEL), STRADEDAMT = SUM(SAMTTRD + SAMTDEL), BUYBROKERAGE = SUM(PBROKTRD) , SELBROKERAGE= SUM(SBROKTRD) , BUYDELIVERYCHRG = SUM(PBROKDEL), SELLDELIVERYCHRG = SUM(SBROKDEL),     
CLIENTTYPE, BILLPAMT = SUM(PAMT) , BILLSAMT = SUM(SAMT) , PMARKETRATE = ( SUM(PRATE) / CASE WHEN SUM(PQTYTRD + PQTYDEL) > 0 THEN SUM(PQTYTRD + PQTYDEL) ELSE 1 END) , SMARKETRATE = ( SUM(SRATE) / (CASE WHEN SUM(SQTYTRD + SQTYDEL) > 0 THEN SUM(SQTYTRD + SQTYDEL) ELSE 1 END ) ),  PNETRATE = ( SUM(PAMTTRD + PAMTDEL) / CASE WHEN SUM(PQTYTRD + PQTYDEL) > 0 THEN SUM(PQTYTRD + PQTYDEL) ELSE 1 END) , SNETRATE = ( SUM(SAMTTRD+SAMTDEL) / (CASE WHEN SUM(SQTYTRD + SQTYDEL) > 0 THEN SUM(SQTYTRD + SQTYDEL) ELSE 1 END ) ), TRDAMT= SUM(TRDAMT) ,DELAMT=SUM(DELAMT), SERINEX=SUM(SERINEX),SERVICE_TAX= SUM(SERVICE_TAX) ,EXSERVICE_TAX= SUM(EXSERVICE_TAX),TURN_TAX=SUM(TURN_TAX),SEBI_TAX=SUM(SEBI_TAX),INS_CHRG=SUM(INS_CHRG),BROKER_CHRG=SUM(BROKER_CHRG),OTHER_CHRG=SUM(OTHER_CHRG),
  
PNL = SUM(SAMTTRD-PAMTTRD)       
INTO #COMB    
FROM CMBILLVALAN S WITH (NOLOCK)    
WHERE SAUDA_DATE >= @FROM_DATE AND SAUDA_DATE<= @TO_DATE +' 23:59:59'    
AND   TRADETYPE NOT IN ( 'SBF','SCF','IBF','ICF','IR' )    
AND SCRIP_CD  IN (SELECT SCRIP_CD FROM #SCRIP_CD_NSE)    
GROUP BY PARTY_CODE, PARTY_NAME,CLIENTTYPE,MEMBERTYPE, COMPANYNAME,SCRIP_CD,ISIN    
    
UNION ALL    
    
SELECT PARTY_CODE, LONG_NAME=PARTY_NAME,SCRIP_CD,ISIN,MEMBERTYPE, COMPANYNAME,    
PTRADEDQTY = SUM(PQTYTRD + PQTYDEL) ,PTRADEDAMT = SUM(PAMTTRD + PAMTDEL) ,STRADEDQTY = SUM(SQTYTRD + SQTYDEL), STRADEDAMT = SUM(SAMTTRD + SAMTDEL), BUYBROKERAGE = SUM(PBROKTRD) , SELBROKERAGE= SUM(SBROKTRD) , BUYDELIVERYCHRG = SUM(PBROKDEL), SELLDELIVERYCHRG = SUM(SBROKDEL),     
CLIENTTYPE, BILLPAMT = SUM(PAMT) , BILLSAMT = SUM(SAMT) , PMARKETRATE = ( SUM(PRATE) / CASE WHEN SUM(PQTYTRD + PQTYDEL) > 0 THEN SUM(PQTYTRD + PQTYDEL) ELSE 1 END) , SMARKETRATE = ( SUM(SRATE) / (CASE WHEN SUM(SQTYTRD + SQTYDEL) > 0 THEN SUM(SQTYTRD + SQTYDEL) ELSE 1 END ) ),  PNETRATE = ( SUM(PAMTTRD + PAMTDEL) / CASE WHEN SUM(PQTYTRD + PQTYDEL) > 0 THEN SUM(PQTYTRD + PQTYDEL) ELSE 1 END) , SNETRATE = ( SUM(SAMTTRD+SAMTDEL) / (CASE WHEN SUM(SQTYTRD + SQTYDEL) > 0 THEN SUM(SQTYTRD + SQTYDEL) ELSE 1 END ) 
  
), TRDAMT= SUM(TRDAMT) ,DELAMT=SUM(DELAMT), SERINEX=SUM(SERINEX),SERVICE_TAX= SUM(SERVICE_TAX) ,EXSERVICE_TAX= SUM(EXSERVICE_TAX),TURN_TAX=SUM(TURN_TAX),SEBI_TAX=SUM(SEBI_TAX),INS_CHRG=SUM(INS_CHRG),BROKER_CHRG=SUM(BROKER_CHRG),OTHER_CHRG=SUM(OTHER_CHRG),
  
PNL = SUM(SAMTTRD-PAMTTRD)       
FROM AngelBSECM.BSEDB_AB.DBO.CMBILLVALAN S WITH (NOLOCK)    
WHERE SAUDA_DATE >= @FROM_DATE AND SAUDA_DATE<= @TO_DATE +' 23:59:59'    
AND   TRADETYPE NOT IN ( 'SBF','SCF','IBF','ICF','IR' )    
AND SCRIP_CD  IN (SELECT SCRIP_CD FROM #SCRIP_CD_BSE)    
GROUP BY PARTY_CODE, PARTY_NAME,CLIENTTYPE,MEMBERTYPE, COMPANYNAME,SCRIP_CD,ISIN    
ORDER BY PARTY_CODE    
    
/* FORMATTING  */    
    
SELECT PARTY_CODE, LONG_NAME, CLIENT_TYPE=''    
,SCRIP_CD,ISIN,    
MEMBERTYPE= (CASE WHEN MEMBERTYPE ='612' THEN 'BSE' ELSE 'NSE' END), COMPANYNAME,    
PTRADEDQTY  AS BUY_QTY,PNETRATE AS BUY_RATE,PTRADEDAMT AS BUY_VALUE,STRADEDQTY AS SELL_QTY,SNETRATE AS SELL_RATE,STRADEDAMT AS SELL_VALUE    
INTO #COMB_F FROM #COMB --WHERE     
    
/* B2B OR B2C  */    
    
SELECT PARTY_CODE,B2C INTO #B2C FROM INTRANET.RISK.DBO.CLIENT_DETAILS WITH (NOLOCK) WHERE PARTY_CODE IN ( SELECT DISTINCT PARTY_CODE FROM #COMB_F)    
      
/* FINAL OUTPUT*/    
    
 SELECT B.PARTY_CODE, LONG_NAME, CLIENT_TYPE= (CASE WHEN B2C ='Y' THEN 'B2C' ELSE 'B2B' END),SCRIP_CD,ISIN,MEMBERTYPE, COMPANYNAME,BUY_QTY,BUY_RATE,BUY_VALUE,SELL_QTY,SELL_RATE, SELL_VALUE ,@FROM_DATE  AS FROM_DATE,@TO_DATE  AS TO_DATE    
INTO RESTRICTED_SCRIP_OUTPUT FROM #B2C A , #COMB_F B    
WHERE A.PARTY_CODE =B.PARTY_CODE    
    
