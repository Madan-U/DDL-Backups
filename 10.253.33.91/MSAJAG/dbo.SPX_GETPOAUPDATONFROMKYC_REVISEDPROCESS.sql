SELECT DISTINCT NISE_PARTY_CODE,CLIENT_CODE,DP,INTRODUCER,DEPOSIT AS DEPOSIT,DEF FROM    
(SELECT DISTINCT NISE_PARTY_CODE,CLIENT_CODE,'12033200' AS DP,'ANGEL SECURITIES LTD' AS INTRODUCER,'CDSL' AS DEPOSIT,'1' AS DEF,PARTY_CODE,DPTYPE ,      
(SELECT COUNT(*) FROM MSAJAG.DBO.MULTICLTID B1 WHERE B1.PARTY_CODE=NISE_PARTY_CODE AND     
'12033200'=B1.DpId AND CLIENT_CODE=B1.CltDpNo AND 'CDSL'=B1.DPTYPE AND B1.DEF='1') AS COUNT1    
