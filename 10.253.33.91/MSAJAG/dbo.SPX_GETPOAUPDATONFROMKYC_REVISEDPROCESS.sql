(              
  SELECT LTRIM(RTRIM(NISE_PARTY_CODE))+'_'+LTRIM(RTRIM(CLIENT_CODE)) FROM  TBL_CLIENT_MASTER_CLOSEDP--[MIS].dbo.kyc.TBL_CLIENT_MASTER_CLOSEDP               
)        
          
-- DELETE PREVOIUS DATA            
