ON A.NISE_PARTY_CODE=B.PARTY_CODE AND MSAJAG_CLIENT4_196='N' and NSESEG='Y' and BOEXIST='Y'   
) C WHERE ISNULL(C.DEPOSIT,'')<>ISNULL(C.DEPOSITORY,'')    
