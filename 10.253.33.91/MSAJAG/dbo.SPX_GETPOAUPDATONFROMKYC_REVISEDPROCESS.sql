WHERE A.CM_BLSAVINGCD=LTRIM(RTRIM(PARTY_CODE)) AND CM_CD=LTRIM(RTRIM(CLTDPNO))          
AND BOEXIST='Y' AND NSESEG='Y' AND MSAJAG_MULTICLTID_196='N' AND DPID='12033200'           
           
          
