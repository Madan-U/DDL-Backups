AND PARTY_CODE IN           
(          
  SELECT CM_BLSAVINGCD FROM  #SYNERGY_DATA_final WHERE MSAJAG_CLIENT4_196='N' and NSESEG='Y' and BOEXIST='Y'          
)AND DEFDP=0             
          
