CREATE PROCEDURE
	[dbo].[BBGSETTBROKRECAL_ONLINE_TEST]
	(
		@SETT_TYPE VARCHAR(2), @SAUDA_DATE VARCHAR(11), @FROMPARTY VARCHAR(10), @TOPARTY VARCHAR(10)
	)

AS
IF LEN(LTRIM(RTRIM(@SAUDA_DATE))) = 10 AND CHARINDEX('/', @SAUDA_DATE) > 0
	BEGIN
		SET @SAUDA_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @SAUDA_DATE, 103), 109)
	END

DECLARE
@PARTY_CODE AS VARCHAR(12),
@SCRIP_CD AS VARCHAR(12),
@SERIES AS VARCHAR(3),
@PARTIPANTCODE VARCHAR(15),
@SELL_BUY AS INT,
@PQTY AS INT,
@SQTY AS INT,
@PRATE AS MONEY,
@SRATE AS MONEY,
@TPQTY AS INT,
@TSQTY AS INT,
@TPRATE AS MONEY,
@TSRATE AS MONEY,
@TMARK VARCHAR(3),
@SETT_NO  AS VARCHAR(10),
@GETPOS AS CURSOR,
@GETPARTY AS CURSOR,
@TRD_VAL NUMERIC(18,4)

SELECT DISTINCT PARTY_CODE, TRD_VAL = CONVERT(NUMERIC(18,4),-1), DEL_VAL = CONVERT(NUMERIC(18,4),-1)      
INTO #BRK FROM SETTLEMENT       
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'      
AND SETT_TYPE = @SETT_TYPE      
AND AUCTIONPART NOT LIKE 'A%'      
AND AUCTIONPART NOT LIKE 'F%'      
AND TRADE_NO NOT LIKE '%C%'   
     
UPDATE #BRK SET TRD_VAL = TRD_BROK_VAL, DEL_VAL = DEL_BROK_VAL      
FROM TBL_ADDITIONAL_BROEKRAGE T      
WHERE T.PARTY_CODE = #BRK.PARTY_CODE      
AND @SAUDA_DATE BETWEEN FROMDATE AND TODATE      
AND TRD_VAL = -1      
      
UPDATE #BRK SET TRD_VAL = TRD_BROK_VAL, DEL_VAL = DEL_BROK_VAL      
FROM TBL_ADDITIONAL_BROEKRAGE T, SUBBROKERS S, CLIENT1 C1      
WHERE #BRK.PARTY_CODE = C1.CL_CODE      
AND C1.SUB_BROKER = T.SUB_BROKER      
AND @SAUDA_DATE BETWEEN FROMDATE AND TODATE      
AND TRD_VAL = -1      
      
UPDATE #BRK SET TRD_VAL = TRD_BROK_VAL, DEL_VAL = DEL_BROK_VAL      
FROM TBL_ADDITIONAL_BROEKRAGE T, BRANCH B, CLIENT1 C1      
WHERE #BRK.PARTY_CODE = C1.CL_CODE      
AND C1.BRANCH_CD = T.BRANCH_CD      
AND @SAUDA_DATE BETWEEN FROMDATE AND TODATE      
AND TRD_VAL = -1       
  
UPDATE #BRK SET TRD_VAL = TRD_BROK_VAL, DEL_VAL = DEL_BROK_VAL    
FROM TBL_ADDITIONAL_BROEKRAGE T      
WHERE T.PARTY_CODE = ''  
AND T.SUB_BROKER = ''  
AND T.BRANCH_CD = ''    
AND @SAUDA_DATE BETWEEN FROMDATE AND TODATE      
AND TRD_VAL = -1   

DELETE FROM #BRK
WHERE TRD_VAL = -1 AND DEL_VAL = -1

UPDATE #BRK SET TRD_VAL = 0
WHERE TRD_VAL = -1 

UPDATE #BRK SET DEL_VAL = 0
WHERE DEL_VAL = -1 

SELECT PARTY_CODE, SYMBOL, ORDER_NO, SELL_BUY, TRADE_NO 
INTO #T_DATA 
FROM TBL_TRADE_ONLINE T, OWNER 
WHERE T.SAUDA_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59'  
AND ORDER_INIT_FROM in ('DIET', 'NETNET', 'IWIN', 'LBS', 'ANGELEYE', 'iWinTouch','AEMobile') 
AND T.MEMBERCODE = OWNER.MEMBERCODE 
AND SEGMENT = 'NSE' AND INST_TYPE = ''

CREATE CLUSTERED INDEX IDX_CL ON #BRK
(
	PARTY_CODE
)

CREATE CLUSTERED INDEX IDX_party ON #T_DATA
(
	PARTY_CODE,
	SYMBOL
)

TRUNCATE TABLE BBGSETTLEMENT  
  
INSERT INTO BBGSETTLEMENT  
SELECT CONTRACTNO,BILLNO,TRADE_NO,SETTLEMENT.PARTY_CODE,SCRIP_CD,USER_ID,TRADEQTY,AUCTIONPART,MARKETTYPE,SERIES,
	   ORDER_NO,MARKETRATE,SAUDA_DATE,TABLE_NO,LINE_NO,VAL_PERC,NORMAL,DAY_PUC,DAY_SALES,SETT_PURCH,
	   SETT_SALES,SELL_BUY,SETTFLAG,BROKAPPLIED,NETRATE,AMOUNT,INS_CHRG,TURN_TAX,OTHER_CHRG,SEBI_TAX,
	   BROKER_CHRG,SERVICE_TAX,TRADE_AMOUNT,BILLFLAG,SETT_NO,NBROKAPP,NSERTAX,N_NETRATE,SETT_TYPE,
	   PARTIPANTCODE,STATUS,PRO_CLI,CPID,INSTRUMENT,BOOKTYPE,BRANCH_ID,TMARK,SCHEME,DUMMY1,DUMMY2,'' 
FROM SETTLEMENT, #BRK    
WHERE SAUDA_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59'  
AND SETT_TYPE = @SETT_TYPE  
AND EXISTS (SELECT PARTY_CODE FROM #T_DATA T 
			WHERE T.PARTY_CODE = SETTLEMENT.PARTY_CODE	
			AND T.SYMBOL = SETTLEMENT.SCRIP_CD)
AND SETTLEMENT.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
AND SETTLEMENT.PARTY_CODE = #BRK.PARTY_CODE

IF (SELECT ISNULL(COUNT(1),0) FROM BBGSETTLEMENT) > 0  
BEGIN  

 UPDATE BBGSETTLEMENT SET STATUS = 'X'   
 WHERE SAUDA_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59'  
 AND EXISTS (SELECT PARTY_CODE FROM #T_DATA T
			WHERE T.PARTY_CODE = BBGSETTLEMENT.PARTY_CODE	
			AND T.SYMBOL = BBGSETTLEMENT.SCRIP_CD
			AND T.ORDER_NO = BBGSETTLEMENT.ORDER_NO  
			AND T.SELL_BUY = (CASE WHEN BBGSETTLEMENT.SELL_BUY = '1' THEN 'B' ELSE 'S' END)  
			AND T.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(BBGSETTLEMENT.TRADE_NO))   
 AND STATUS <> 'E'  

SELECT * INTO #CLIENT1
FROM CLIENT1 
WHERE CL_CODE IN (SELECT PARTY_CODE FROM #BRK)

CREATE CLUSTERED INDEX IDXCL ON #CLIENT1 (CL_CODE)

SELECT * INTO #CLIENT2
FROM CLIENT2
WHERE CL_CODE IN (SELECT PARTY_CODE FROM #BRK)

CREATE CLUSTERED INDEX IDXCL ON #CLIENT2 (CL_CODE)

SELECT * INTO #CLIENTTAXES_NEW
FROM CLIENTTAXES_NEW 
WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #BRK)
AND @SAUDA_DATE BETWEEN FROMDATE AND TODATE

CREATE CLUSTERED INDEX IDXCL ON #CLIENTTAXES_NEW (PARTY_CODE)

SELECT * INTO #CLIENTBROK_SCHEME
FROM CLIENTBROK_SCHEME 
WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #BRK)
AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE

CREATE CLUSTERED INDEX IDXCL ON #CLIENTBROK_SCHEME (PARTY_CODE)

SELECT * INTO #BROKTABLE
FROM BROKTABLE
WHERE TABLE_NO IN (SELECT TABLE_NO FROM #CLIENTBROK_SCHEME)

CREATE CLUSTERED INDEX IDXCL ON #BROKTABLE (TABLE_NO)

SELECT SETT_NO, SETT_TYPE, B.PARTY_CODE, B.SCRIP_CD, SERIES, PARTIPANTCODE, TRD_VAL,
PQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END ),
PRATE = (Case WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END ) > 0 THEN
			SUM(CASE WHEN SELL_BUY = 1 THEN (TRADEQTY*MARKETRATE) ELSE 0 END)/
			SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END ) 
		 ELSE 0 END),
SQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END ),
SRATE = (Case WHEN SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END ) > 0 THEN
			SUM(CASE WHEN SELL_BUY = 2 THEN (TRADEQTY*MARKETRATE) ELSE 0 END)/
			SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END ) 
		 ELSE 0 END),
PMORE = 'N',
SMORE = 'N',
SRATEMORE = 'N',
PRATEMORE = 'N',
EQ= 'N'		
into #BROKSET 
FROM BBGSETTLEMENT B, #BRK K
WHERE B.PARTY_CODE = K.PARTY_CODE AND TRD_VAL > 0 
GROUP BY SETT_NO, SETT_TYPE, B.PARTY_CODE, B.SCRIP_CD, SERIES, PARTIPANTCODE, TRD_VAL

UPDATE #BROKSET SET PMORE = (CASE WHEN PQTY >= SQTY THEN 'Y' 
								  ELSE 'N' END),
				    SMORE = (CASE WHEN SQTY >= PQTY THEN 'Y' 
								  ELSE 'N' END), 				  
					EQ = (CASE WHEN PQTY = SQTY THEN 'Y' 
								  ELSE 'N' END),
					PRATEMORE = (CASE WHEN PRATE > Srate THEN 'Y' 
								  ELSE 'N' END),
					sRATEMORE = (CASE WHEN sRATE > prate THEN 'Y' 
								  ELSE 'N' END)


CREATE CLUSTERED INDEX IDXCL ON #BROKSET (SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, PARTIPANTCODE)
 
UPDATE BBGSETTLEMENT SET
    BROKAPPLIED =(CASE
		WHEN BBGSETTLEMENT.STATUS <> 'X' THEN BROKAPPLIED
	       ELSE
               (  CASE
                         WHEN ( BBGSETTLEMENT.SETTFLAG = 1 AND BROKTABLE.VAL_PERC ='V' AND SELL_BUY = 1)
                              THEN  /* BROKTABLE.NORMAL */
		((FLOOR(( BROKTABLE.NORMAL * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                         WHEN ( BBGSETTLEMENT.SETTFLAG = 1 AND BROKTABLE.VAL_PERC ='V' AND SELL_BUY = 2)
                              THEN /* BROKTABLE.NORMAL  */
		((FLOOR(( BROKTABLE.NORMAL * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                         WHEN ( BBGSETTLEMENT.SETTFLAG = 1 AND BROKTABLE.VAL_PERC ='P' AND SELL_BUY = 1)
                               THEN
                                          ((FLOOR ( (((BROKTABLE.NORMAL * BBGSETTLEMENT.MARKETRATE/100 ) )  * POWER(10,CT.ROUND_TO) + CT.ROFIG + CT.ERRNUM ) /  (CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  / POWER(10,CT.ROUND_TO))
                        WHEN ( BBGSETTLEMENT.SETTFLAG = 1 AND BROKTABLE.VAL_PERC ='P' AND SELL_BUY = 2)
                             THEN /* ROUND((BROKTABLE.NORMAL /100 )* BBGSETTLEMENT.MARKETRATE,CT.ROUND_TO)         */
		((FLOOR(( ((BROKTABLE.NORMAL * BBGSETTLEMENT.MARKETRATE/100 )) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                      WHEN (BBGSETTLEMENT.SETTFLAG = 2  AND BROKTABLE.VAL_PERC ='V' )
                            THEN /* ((BROKTABLE.DAY_PUC)) */
		((FLOOR(( (((CASE WHEN STATUS = 'X' THEN BROKTABLE.DAY_PUC-ROUND((BROKTABLE.DAY_PUC*TRD_VAL/100),4) ELSE BROKTABLE.DAY_PUC END)))  * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                      WHEN (BBGSETTLEMENT.SETTFLAG = 2  AND BROKTABLE.VAL_PERC ='P' )
                             THEN /* ROUND((BROKTABLE.DAY_PUC/100) * BBGSETTLEMENT.MARKETRATE,CT.ROUND_TO)  */
		((FLOOR(( (((CASE WHEN STATUS = 'X' THEN BROKTABLE.DAY_PUC-ROUND((BROKTABLE.DAY_PUC*TRD_VAL/100),4) ELSE BROKTABLE.DAY_PUC END) 
				* BBGSETTLEMENT.MARKETRATE/100)) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                   WHEN (BBGSETTLEMENT.SETTFLAG = 3  AND BROKTABLE.VAL_PERC ='V' )
                             THEN /* BROKTABLE.DAY_SALES */

		((FLOOR(( (CASE WHEN STATUS = 'X' THEN BROKTABLE.DAY_SALES-ROUND((BROKTABLE.DAY_SALES*TRD_VAL/100),4) ELSE BROKTABLE.DAY_SALES END) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                  WHEN (BBGSETTLEMENT.SETTFLAG = 3  AND BROKTABLE.VAL_PERC ='P' )
                             THEN /*ROUND((BROKTABLE.DAY_SALES/ 100) * BBGSETTLEMENT.MARKETRATE ,CT.ROUND_TO) */
		((FLOOR(( (((CASE WHEN STATUS = 'X' THEN BROKTABLE.DAY_SALES-ROUND((BROKTABLE.DAY_SALES*TRD_VAL/100),4) ELSE BROKTABLE.DAY_SALES END) * BBGSETTLEMENT.MARKETRATE/ 100)) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                WHEN ( BBGSETTLEMENT.SETTFLAG = 4  AND BROKTABLE.VAL_PERC ='V' )
                             THEN /* BROKTABLE.SETT_PURCH  */
		((FLOOR(( (CASE WHEN STATUS = 'X' THEN BROKTABLE.SETT_PURCH-ROUND((BROKTABLE.SETT_PURCH*TRD_VAL/100),4) ELSE BROKTABLE.SETT_PURCH END) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                         WHEN ( BBGSETTLEMENT.SETTFLAG = 4  AND BROKTABLE.VAL_PERC ='P' )
                            THEN /* ROUND((BROKTABLE.SETT_PURCH/100) * BBGSETTLEMENT.MARKETRATE ,CT.ROUND_TO) */
		((FLOOR(( (((CASE WHEN STATUS = 'X' THEN BROKTABLE.SETT_PURCH-ROUND((BROKTABLE.SETT_PURCH*TRD_VAL/100),4) ELSE BROKTABLE.SETT_PURCH END) * BBGSETTLEMENT.MARKETRATE/100)) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
             WHEN ( BBGSETTLEMENT.SETTFLAG = 5  AND BROKTABLE.VAL_PERC ='V' )
                             THEN /* BROKTABLE.SETT_SALES */
		((FLOOR(((CASE WHEN STATUS = 'X' THEN BROKTABLE.SETT_SALES-ROUND((BROKTABLE.SETT_SALES*TRD_VAL/100),4) ELSE BROKTABLE.SETT_SALES END) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
                         WHEN ( BBGSETTLEMENT.SETTFLAG = 5  AND BROKTABLE.VAL_PERC ='P' )
                             THEN /* ROUND((BROKTABLE.SETT_SALES/100) * BBGSETTLEMENT.MARKETRATE ,CT.ROUND_TO)*/
		((FLOOR(( (((CASE WHEN STATUS = 'X' THEN BROKTABLE.SETT_SALES-ROUND((BROKTABLE.SETT_SALES*TRD_VAL/100),4) ELSE BROKTABLE.SETT_SALES END) * BBGSETTLEMENT.MARKETRATE/100)) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /

		(CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /

		POWER(10,CT.ROUND_TO))
   ELSE  0
                        END)
                       END)

   FROM #BROKTABLE BROKTABLE, #CLIENT2 CLIENT2, BBGSETTLEMENT, GLOBALS, #CLIENT1 CLIENT1, #CLIENTBROK_SCHEME S, #CLIENTTAXES_NEW CT, OWNER, #BROKSET 
   WHERE BBGSETTLEMENT.PARTY_CODE = CLIENT2.CL_CODE
   AND CLIENT1.CL_CODE=CLIENT2.CL_CODE
   AND CLIENT2.CL_CODE = CT.PARTY_CODE
   AND CLIENT2.TRAN_CAT = CT.TRANS_CAT
   AND S.TRADE_TYPE = (CASE WHEN BBGSETTLEMENT.PARTIPANTCODE = MEMBERCODE THEN 'NRM' ELSE 'INS' END)
   AND BBGSETTLEMENT.SAUDA_DATE BETWEEN FROMDATE AND TODATE
   AND S.TABLE_NO = BROKTABLE.TABLE_NO
   AND S.SCHEME_TYPE = CLIENT2.TRAN_CAT
   AND S.SCRIP_CD = 'ALL'
   AND S.PARTY_CODE = BBGSETTLEMENT.PARTY_CODE
   AND BBGSETTLEMENT.SAUDA_DATE BETWEEN S.FROM_DATE AND S.TO_DATE
   AND BROKTABLE.LINE_NO = ( CASE WHEN S.BROKSCHEME = 2 THEN
							    (SELECT MIN(BROKTABLE.LINE_NO) FROM BROKTABLE WHERE
                                               		    S.TABLE_NO = BROKTABLE.TABLE_NO
				                	    AND TRD_DEL =
							    ( CASE WHEN EQ = 'Y' THEN
								  ( CASE WHEN PRATEMORE = 'Y'
									 THEN ( CASE WHEN ( BBGSETTLEMENT.SELL_BUY = 1 )
							    		             THEN 'F'
							    		             ELSE 'S'
									        END )
									 ELSE
									      ( CASE WHEN ( BBGSETTLEMENT.SELL_BUY = 2 )
							    		             THEN 'F'
							    			     ELSE 'S'
									        END )
									 END )
								   ELSE
									( CASE WHEN PMORE = 'Y'
									       THEN ( CASE WHEN ( BBGSETTLEMENT.SELL_BUY = 1 )
							    			           THEN 'F'

							    			           ELSE 'S'
										      END )
									       ELSE
									           ( CASE WHEN ( BBGSETTLEMENT.SELL_BUY = 2 )
							    		                  THEN
                                                                                              'F'
                                                                                          ELSE
                                                                                              'S'
									             END )
									  END )
								   END )
							    AND BBGSETTLEMENT.PARTY_CODE =  CLIENT2.PARTY_CODE
                                               		    AND BBGSETTLEMENT.MARKETRATE <= BROKTABLE.UPPER_LIM)
						ELSE ( CASE WHEN S.BROKSCHEME = 1 THEN
					       		   (SELECT MIN(BROKTABLE.LINE_NO) FROM BROKTABLE WHERE
                                               		    S.TABLE_NO = BROKTABLE.TABLE_NO
				                	    AND TRD_DEL = ( CASE WHEN PMORE = 'Y'
										 THEN ( CASE WHEN ( BBGSETTLEMENT.SELL_BUY = 1 )
							    			 	    THEN 'F'
							    			 	    ELSE 'S'
										       END )
									     	 ELSE
										      ( CASE WHEN ( BBGSETTLEMENT.SELL_BUY = 2 )
							    			 	    THEN 'F'
							    			 	    ELSE 'S'
										       END )
									    END )
                                                	    AND BBGSETTLEMENT.PARTY_CODE =  CLIENT2.PARTY_CODE
                                               		    AND BBGSETTLEMENT.MARKETRATE <= BROKTABLE.UPPER_LIM)
							ELSE ( CASE WHEN S.BROKSCHEME = 3 THEN
					       		   	   (SELECT MIN(BROKTABLE.LINE_NO) FROM BROKTABLE WHERE
                                               		            S.TABLE_NO = BROKTABLE.TABLE_NO
				                	            AND TRD_DEL = ( CASE WHEN SMORE ='Y'
									        	 THEN ( CASE WHEN ( BBGSETTLEMENT.SELL_BUY = 2 )
							    			           	     THEN 'F'
							    			 	             ELSE 'S'
										         END )
									     	    ELSE ( CASE WHEN ( BBGSETTLEMENT.SELL_BUY = 1 )
							    			 	        THEN 'F'
							    			 	        ELSE 'S'
										           END )
									            END )
        	                                        	    AND BBGSETTLEMENT.PARTY_CODE =  CLIENT2.PARTY_CODE
	                                               		    AND BBGSETTLEMENT.MARKETRATE <= BROKTABLE.UPPER_LIM)
								ELSE
							    	   (SELECT MIN(BROKTABLE.LINE_NO) FROM BROKTABLE
							    	    WHERE S.TABLE_NO = BROKTABLE.TABLE_NO
							     	    AND TRD_DEL = 'T'
   		                   				    AND BBGSETTLEMENT.PARTY_CODE =  CLIENT2.PARTY_CODE
				           			    AND BBGSETTLEMENT.MARKETRATE <= BROKTABLE.UPPER_LIM )
								END )
							END )
						END )
  AND BBGSETTLEMENT.STATUS <> 'E'
  AND CT.TRANS_CAT = CLIENT2.TRAN_CAT
  AND BBGSETTLEMENT.SETT_NO = #BROKSET.SETT_NO
  AND BBGSETTLEMENT.SETT_TYPE = #BROKSET.SETT_TYPE
  AND BBGSETTLEMENT.PARTY_CODE = #BROKSET.PARTY_CODE
  AND BBGSETTLEMENT.SCRIP_CD = #BROKSET.SCRIP_CD
  AND BBGSETTLEMENT.Series = #BROKSET.Series
  AND BBGSETTLEMENT.PARTIPANTCODE = #BROKSET.PARTIPANTCODE
  AND BBGSETTLEMENT.TRADEQTY > 0
  AND SAUDA_DATE > GLOBALS.YEAR_START_DT
  AND SAUDA_DATE < GLOBALS.YEAR_END_DT

UPDATE BBGSETTLEMENT SET BROKAPPLIED = 0
WHERE BROKAPPLIED < 0   


UPDATE BBGSETTLEMENT SET   
NETRATE = MARKETRATE + (CASE WHEN SELL_BUY = 1 THEN BROKAPPLIED ELSE -BROKAPPLIED END),  
N_NETRATE = MARKETRATE + (CASE WHEN SELL_BUY = 1 THEN BROKAPPLIED ELSE -BROKAPPLIED END),  
NBROKAPP = BROKAPPLIED  
WHERE STATUS <> 'E' AND STATUS = 'X'

UPDATE BBGSETTLEMENT SET         
      NBROKAPP = (  CASE        
    WHEN (  BROKTABLE.VAL_PERC ='V' )      
                             THEN       
  ((FLOOR(((CASE WHEN BBGSETTLEMENT.STATUS = 'X' THEN BROKTABLE.NORMAL - ROUND((BROKTABLE.NORMAL*DEL_VAL/100),4) ELSE BROKTABLE.NORMAL END) * POWER(10,CT.ROUND_TO)+CT.ROFIG + CT.ERRNUM ) /        
      
  (CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  /       
      
  POWER(10,CT.ROUND_TO))      
      
                        WHEN (  BROKTABLE.VAL_PERC ='P' )      
                             THEN      ((FLOOR ( ((((CASE WHEN BBGSETTLEMENT.STATUS = 'X' THEN BROKTABLE.NORMAL - ROUND((BROKTABLE.NORMAL*DEL_VAL/100),4) ELSE BROKTABLE.NORMAL END) /100 ) * BBGSETTLEMENT.MARKETRATE)  * POWER(10,CT.ROUND_TO) + CT.ROFIG + 



CT.ERRNUM ) /  (CT.ROFIG + CT.NOZERO )) * (CT.ROFIG +CT.NOZERO ) )  / POWER(10,CT.ROUND_TO))      
   ELSE        
          BROKAPPLIED       
                        END       
                         )
FROM #BROKTABLE BROKTABLE, #CLIENT2 CLIENT2, GLOBALS, BBGSETTLEMENT,  #CLIENTBROK_SCHEME S, #CLIENTTAXES_NEW CT, OWNER, #BRK K      
      
WHERE BBGSETTLEMENT.PARTY_CODE = CLIENT2.CL_CODE  
      AND CLIENT2.CL_CODE = CT.PARTY_CODE      
      AND CT.TRANS_CAT = 'DEL'       
      AND S.TRADE_TYPE = 'NRM'
      AND BBGSETTLEMENT.SAUDA_DATE BETWEEN FROMDATE AND TODATE       
   AND S.TABLE_NO = BROKTABLE.TABLE_NO      
      AND S.SCHEME_TYPE = 'DEL'   
      --AND S.SCRIP_CD = 'ALL'      
      AND S.PARTY_CODE = BBGSETTLEMENT.PARTY_CODE      
      AND BBGSETTLEMENT.SAUDA_DATE BETWEEN S.FROM_DATE AND S.TO_DATE   
   AND BBGSETTLEMENT.MARKETRATE > BROKTABLE.LOWER_LIM            
   AND BBGSETTLEMENT.MARKETRATE <= BROKTABLE.UPPER_LIM            
      AND TRD_DEL = 'D'    
      AND GLOBALS.EXCHANGE = 'NSE'  
      AND BBGSETTLEMENT.BILLFLAG IN(4,5)       
      AND BBGSETTLEMENT.STATUS <> 'E'      
      AND SAUDA_DATE > GLOBALS.YEAR_START_DT      
      AND SAUDA_DATE < GLOBALS.YEAR_END_DT      
      AND AUCTIONPART NOT LIKE 'A%'  
      AND AUCTIONPART NOT LIKE 'F%'       
	  AND K.PARTY_CODE = CLIENT2.PARTY_CODE
	  AND DEL_VAL > 0 

 UPDATE BBGSETTLEMENT SET STATUS = '11',  
 N_NETRATE = MARKETRATE + (CASE WHEN SELL_BUY = 1 THEN NBROKAPP ELSE -NBROKAPP END),
 BROKAPPLIED = (CASE WHEN BROKAPPLIED > NBROKAPP THEN NBROKAPP ELSE BROKAPPLIED END),
 NETRATE = MARKETRATE + (CASE WHEN BROKAPPLIED > NBROKAPP THEN (CASE WHEN SELL_BUY = 1 THEN NBROKAPP ELSE -NBROKAPP END) ELSE (CASE WHEN SELL_BUY = 1 THEN BROKAPPLIED ELSE -BROKAPPLIED END) END)
 WHERE STATUS <> 'E' AND STATUS = 'X'  

DELETE FROM SETTLEMENT   
WHERE SAUDA_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59'  
