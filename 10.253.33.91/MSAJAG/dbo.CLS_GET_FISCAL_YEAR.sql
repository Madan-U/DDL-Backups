-- Object: PROCEDURE dbo.CLS_GET_FISCAL_YEAR
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[CLS_GET_FISCAL_YEAR]
(
	@DATE VARCHAR(11)
)
AS
SELECT 
	STARTDATE = CONVERT(VARCHAR(10), SDTCUR, 103),
	ENDDATE = CONVERT(VARCHAR(10), LDTCUR, 103)
FROM 
	ACCOUNT.dbo.PARAMETER
WHERE CONVERT(DATETIME, @DATE, 103) BETWEEN SDTCUR AND LDTCUR

GO
