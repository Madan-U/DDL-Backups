CREATE PROC [dbo].[PROC_TRADE_ONLINE_BAKMAY22016]
(
	@TDATE		VARCHAR(10), 
	@FILEFOR	VARCHAR(20), 
	@FILETYPE	VARCHAR(50), 
	@PROGID		VARCHAR(50), 
	@UNAME		VARCHAR(50),
	@FILENAME	VARCHAR(100) = ''
)
AS

DECLARE @RECCNT NUMERIC(18,0),
	    @FILEDATE	DATETIME

INSERT INTO FILEDATA_HISTORY SELECT PROGID,FILE_DATA,UploadDateTime,UploadBy FROM FILEDATA
DELETE FROM FILEDATA WHERE PROGID <> @PROGID
	    
	    

SELECT @FILEDATE = CONVERT(DATETIME,ISNULL(LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',1))),'01/01/1900'),103) FROM FILEDATA 
WHERE PROGID = @PROGID AND ISNUMERIC(.DBO.PIECE(FILE_DATA,',',18)) = 1


CREATE TABLE #CLIENT
	(
	PARTY_CODE VARCHAR(10)
	)
	
    INSERT INTO #CLIENT 
    select * from (
	SELECT CL_CODE  FROM CLIENT1 WHERE BRANCH_CD IN (SELECT BRANCH_CD FROM TBL_ADDITIONAL_BROEKRAGE WHERE PARTY_CODE='')
    UNION ALL
    SELECT CL_CODE  FROM CLIENT1 WHERE SUB_BROKER IN (SELECT SUB_BROKER FROM TBL_ADDITIONAL_BROEKRAGE WHERE PARTY_CODE='')
    UNION ALL
    SELECT CL_CODE  FROM CLIENT1 WHERE CL_CODE  IN (SELECT PARTY_CODE FROM TBL_ADDITIONAL_BROEKRAGE )
  )A
    






IF CONVERT(VARCHAR,@FILEDATE,103) = @TDATE
BEGIN
	DELETE FROM TBL_TRADE_ONLINE
	WHERE SAUDA_DATE = @FILEDATE
	
	INSERT INTO TBL_TRADE_ONLINE 
	SELECT 
		SAUDA_DATE = @FILEDATE, 
		PARTY_CODE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',23))),
		MEMBERCODE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',5))),
		SYMBOL=(CASE WHEN LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',4))) = 'BSE' AND LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',8))) = ''
					 THEN LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',9))) 
					 ELSE LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',10)))
				END),
		EXPIRYDATE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',12))),
		STRIKE_PRICE=(CASE WHEN LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',13))) = '' THEN '0' ELSE LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',13))) END),
		OPTION_TYPE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',14))),
		QTY=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',18))),
		PRICE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',19))),
		INTIATED_BY=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',2))),
		MODIFIED_BY=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',3))),
		PRODUCT_TYPE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',32))),
		ORDER_NO=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',25))),
		TRADE_NO=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',26))),
		ORDER_INIT_FROM=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',35))),
		ORDER_MODY_FROM=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',36))),
		SEGMENT=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',4))),
		INST_TYPE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',8))),
		SELL_BUY=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',17))),
		UPLOADED_BY = @UNAME,
		UPLOADED_ON = GETDATE()
	FROM FILEDATA 
	WHERE PROGID = @PROGID AND ISNUMERIC(.DBO.PIECE(FILE_DATA,',',18)) = 1
	AND LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',1))) = @TDATE
	AND LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',23))) in(SELECT DISTINCT PARTY_CODE FROM #CLIENT)


	

	SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA 
	WHERE PROGID = @PROGID AND ISNUMERIC(.DBO.PIECE(FILE_DATA,',',18)) = 1
	AND LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',1))) = @TDATE

	SELECT STRMSGCODE = 1, STRMSG = CONVERT(VARCHAR,@RECCNT) + ' RECORD(S) IMPORTED SUCCESSFULLY.' 
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'INVALID FILE FORMAT OR DATE IS CORRECT.' 
END

GO
