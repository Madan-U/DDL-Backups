--------------------------------------------------

CREATE PROCEDURE GET_MEMBER_LIST_NEW          
AS          
BEGIN          
	SELECT SNO=1,MEMBERCODE,MEMBERCODEEXC = MEMBERCODE + ' - NSE',LOGINID,
	PASSWORDKEY,TOKENID,PANNO,
	TOKEN_EXP = CONVERT(VARCHAR,DATEADD(SS, EXPIRES_IN, LOGINLOGOUT),109) 
	FROM OWNER,TBL_HOLDING_USER_DT   
	WHERE DATEADD(SS, EXPIRES_IN, LOGINLOGOUT) > GETDATE()
	UNION
	SELECT SNO=2,MEMBERCODE,MEMBERCODEEXC = MEMBERCODE + ' - NSE',LOGINID,
	PASSWORDKEY,TOKENID='',PANNO,
	TOKEN_EXP = CONVERT(VARCHAR,DATEADD(SS, EXPIRES_IN, LOGINLOGOUT),109) 
	FROM OWNER,TBL_HOLDING_USER_DT  
END

GO
