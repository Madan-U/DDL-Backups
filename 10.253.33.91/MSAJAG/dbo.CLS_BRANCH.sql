-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[CLS_BRANCH]
(
	@BRANCH VARCHAR(20)
)
AS
BEGIN 
	SELECT BRANCH_CODE,BRANCH 
	FROM BRANCH
	WHERE  BRANCH_CODE LIKE (CASE WHEN @BRANCH <> '' THEN @BRANCH + '%' ELSE BRANCH_CODE END) OR 
	  BRANCH LIKE (CASE WHEN @BRANCH <> '' THEN @BRANCH + '%' ELSE BRANCH END)  
	ORDER BY BRANCH
END

GO
