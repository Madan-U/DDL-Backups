CREATE PROCEDURE [dbo].[BROK_DETAIL_MODIFCATION_VBB_NEW] ( @PARTY_CODE VARCHAR(10) , @BROK_SCHEME VARCHAR(50))     
     
AS     
      
DECLARE @CNT INT ,@BROKCNT INT   
SELECT DISTINCT TABLE_NO INTO #TABLE_NO FROM BROKTABLE WHERE TABLE_NAME LIKE @BROK_SCHEME+'%_VBB'     
    
INSERT INTO #TABLE_NO  
SELECT DISTINCT TABLE_NO  FROM BROKTABLE WHERE TABLE_NAME LIKE @BROK_SCHEME+'%'

SET @BROKCNT=(SELECT COUNT(1) FROM #TABLE_NO )
     
IF (@BROKCNT=0)     
      
   BEGIN      
     SELECT 'PLEASE ENTER VALID BROKERAGE PLAN'     
     
   RETURN     
 END     
     
     
     
SELECT @CNT =COUNT(1)  FROM  CLIENT_DETAILS WHERE CL_CODE =@PARTY_CODE      
SELECT @CNT     
      
 IF (@CNT =0 )     
   BEGIN      
     SELECT 'CODE NOT EXIST'     
     
   RETURN     
   END     
     
SET @CNT=0     
 
     
SELECT DISTINCT CL_CODE,   SCHEME_NAME  AS SCHEME_NAME 
 INTO #PLANNAME   
FROM      
( SELECT DISTINCT SP_PARTY_CODE AS CL_CODE,SCHEME_NAME  FROM SCHEME_MAPPING ,MAPPING_TABLE M      
WHERE SP_PARTY_CODE =@PARTY_CODE AND SP_COMPUTATION_LEVEL ='O' AND SP_DATE_TO > GETDATE()    AND SP_SCHEME_ID=TRD_SCHEME_ID 
 UNION  ALL     
 SELECT DISTINCT SP_PARTY_CODE AS CL_CODE,SCHEME_NAME  FROM  ANGELFO.NSEFO.DBO.SCHEME_MAPPING ,MAPPING_TABLE M      
 WHERE SP_PARTY_CODE =@PARTY_CODE AND SP_COMPUTATION_LEVEL ='O' AND SP_DATE_TO > GETDATE()   AND SP_SCHEME_ID=TRD_SCHEME_ID   
 UNION  ALL     
 SELECT DISTINCT SP_PARTY_CODE AS CL_CODE,SCHEME_NAME  FROM  ANGELCOMMODITY.MCDX.DBO.SCHEME_MAPPING    ,MAPPING_TABLE M  
 WHERE SP_PARTY_CODE =@PARTY_CODE AND SP_COMPUTATION_LEVEL ='O' AND SP_DATE_TO > GETDATE()    AND SP_SCHEME_ID=TRD_SCHEME_ID  
)A     
      

IF (@BROK_SCHEME=(SELECT SCHEME_NAME FROM #PLANNAME))
  BEGIN         
       
   SELECT @BROK_SCHEME+' ALREADY MAPPED FOR CLIENT : '+ @PARTY_CODE AS REMARK     
     
   RETURN     
      
 END     
     
EXEC MSAJAG.DBO.VBB_CI_NEW_CLIENT   @PARTY_CODE ,@BROK_SCHEME   
EXEC ANGELBSECM.BSEDB_AB.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME   
EXEC ANGELFO.NSEFO.DBO.VBB_CI_NEW_CLIENT   @PARTY_CODE ,@BROK_SCHEME   
EXEC ANGELFO.NSECURFO.DBO.VBB_CI_NEW_CLIENT   @PARTY_CODE ,@BROK_SCHEME   
EXEC ANGELCOMMODITY.BSECURFO.DBO.VBB_CI_NEW_CLIENT   @PARTY_CODE ,@BROK_SCHEME   
EXEC ANGELCOMMODITY.MCDX.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME    
EXEC ANGELCOMMODITY.NCDX.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME   
EXEC ANGELCOMMODITY.BSEFO.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME   
EXEC ANGELCOMMODITY.NCE.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME       
     
SELECT @BROK_SCHEME+' MAPPED FOR CLIENT : '+ @PARTY_CODE AS REMARK

GO
