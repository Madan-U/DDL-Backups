-- Object: PROCEDURE dbo.PROC_SETTFLAG_START_BAK_21AUG2020
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE  PROC [dbo].[PROC_SETTFLAG_START_BAK_21AUG2020]
(
	@SETT_NO	VARCHAR(7),
	@SETT_TYPE	VARCHAR(2),
	@SAUDA_DATE	VARCHAR(11),
	@FROMPARTY	VARCHAR(10),
	@TOPARTY	VARCHAR(10)
)
AS

DECLARE 
@CLEARINGCODE		VARCHAR(10),  
@OTHER_SETT_TYPE	VARCHAR(2),
@OTHER_SETT_NO		VARCHAR(7) 

SET @CLEARINGCODE = ''		    
SET @OTHER_SETT_TYPE = '' 
SET @OTHER_SETT_NO = ''

IF @TOPARTY = ''
BEGIN
	SET @TOPARTY = 'ZZZZZZZZZZ'
END

IF @SAUDA_DATE = ''
BEGIN
	SELECT @SAUDA_DATE = START_DATE FROM SETT_MST 
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
END

IF @SETT_NO = ''
BEGIN
	SELECT @SETT_NO = SETT_NO FROM SETT_MST 
	WHERE START_DATE LIKE @SAUDA_DATE + '%' AND SETT_TYPE = @SETT_TYPE  
END

TRUNCATE TABLE COMBINE_SETTLEMENT

SELECT @CLEARINGCODE = ISNULL(CLEARINGCODE,'') FROM TBL_INTEROP_SETTING
WHERE  @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE
 
IF (SELECT ISNULL(COUNT(1),0) FROM TBL_INTEROP_SETT_TYPE 
	WHERE NSE_SETT_TYPE = @SETT_TYPE 
	  AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE) > 0
BEGIN 
	SELECT @OTHER_SETT_TYPE = BSE_SETT_TYPE FROM TBL_INTEROP_SETT_TYPE 
	WHERE NSE_SETT_TYPE = @SETT_TYPE 
	  AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE
	
	SELECT @OTHER_SETT_NO = SETT_NO FROM ANAND.BSEDB_AB.DBO.SETT_MST 
	WHERE SETT_TYPE = @OTHER_SETT_TYPE 
	 AND @SAUDA_DATE BETWEEN START_DATE AND END_DATE
END
 
IF @OTHER_SETT_TYPE = ''
BEGIN
	SET @CLEARINGCODE = ''
END                        
                      
IF @CLEARINGCODE = 'NSCCL' OR @CLEARINGCODE = ''
BEGIN
	INSERT INTO COMBINE_SETTLEMENT   
	SELECT SrNo,Contractno,Billno,Trade_No,S.PARTY_CODE,Scrip_Cd,User_Id,   
	Tradeqty,Auctionpart=(CASE WHEN Auctionpart = 'MF' THEN 'N' ELSE Auctionpart END),
	Markettype,Series,Order_No,Marketrate,Sauda_Date,   
	S.Table_No,Line_No,Val_Perc,Normal,Day_Puc,Day_Sales,Sett_Purch,Sett_Sales,Sell_Buy,  
	Settflag,Brokapplied,Netrate,Amount,Ins_Chrg,Turn_Tax,S.Other_Chrg,Sebi_Tax,Broker_Chrg,  
	Service_Tax,Trade_Amount,Billflag,Sett_No,Nbrokapp,Nsertax,N_Netrate,Sett_Type,Partipantcode,  
	Status,Pro_Cli=0,Cpid,Instrument,Booktype,Branch_Id,Tmark,Scheme,S.Dummy1,S.Dummy2, 
	EXCHG = 'NSE', org_contractno=Contractno, org_sett_no = sett_no, org_sett_type = sett_Type, 
	ORG_SCRIP_CD = SCRIP_CD, ORG_SERIES = SERIES,ISIN=''
	FROM SETTLEMENT S--, CLIENT2 C2  
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE = @SETT_TYPE  
	AND SAUDA_DATE LIKE @SAUDA_DATE + '%'   
	AND S.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
	--AND S.PARTY_CODE = C2.PARTY_CODE   
	AND AUCTIONPART NOT IN ('AP','AR','FP','FL','FA','FC') 
	AND TRADE_NO NOT LIKE '%C%'  
END

IF @CLEARINGCODE = 'NSCCL'
BEGIN
	IF @OTHER_SETT_TYPE <> ''
	BEGIN
		INSERT INTO COMBINE_SETTLEMENT
		SELECT SrNo,Contractno,Billno,Trade_No,S.PARTY_CODE,Scrip_Cd,User_Id,   
		Tradeqty,Auctionpart=(CASE WHEN Auctionpart = 'MF' THEN 'N' ELSE Auctionpart END),
		Markettype,Series,Order_No,Marketrate,Sauda_Date,
		S.Table_No,Line_No,Val_Perc,Normal,Day_Puc,Day_Sales,Sett_Purch,Sett_Sales,Sell_Buy,   
		Settflag,Brokapplied,Netrate,Amount,Ins_Chrg,Turn_Tax,S.Other_Chrg,Sebi_Tax,Broker_Chrg,  
		Service_Tax,Trade_Amount,Billflag,Sett_No=@SETT_NO,Nbrokapp,Nsertax,N_Netrate,
		Sett_Type=@SETT_TYPE,Participantcode,  
		Status,Pro_Cli=0,Cpid,Instrument,Booktype,Branch_Id,Tmark,Scheme,DUMMY1 = S.Dummy2,DUMMY2 = S.Dummy1, 
		EXCHG = 'BSE', org_contractno=Contractno, org_sett_no = sett_no, org_sett_type = sett_Type, 
		ORG_SCRIP_CD = SCRIP_CD, ORG_SERIES = SERIES,ISIN=''
		FROM anand.bsedb_ab.DBO.SETTLEMENT S--, anand.bsedb_ab.DBO.CLIENT2 C2  
		WHERE SETT_NO = @OTHER_SETT_NO
		AND SETT_TYPE = @OTHER_SETT_TYPE  
		AND SAUDA_DATE LIKE @SAUDA_DATE + '%'   
		AND S.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		--AND S.PARTY_CODE = C2.PARTY_CODE   
		AND AUCTIONPART NOT IN ('AP','AR','FP','FL','FA','FC') 
		AND TRADE_NO NOT LIKE '%C%'  
	END

	SELECT PARTY_CODE, CONTRACTNO = MAX(CONTRACTNO) 
	INTO #STT_CONT FROM COMBINE_SETTLEMENT
	WHERE EXCHG = 'NSE'
	GROUP BY PARTY_CODE

	UPDATE COMBINE_SETTLEMENT SET Contractno = #STT_CONT.CONTRACTNO 
	FROM #STT_CONT
	WHERE #STT_CONT.Party_Code = COMBINE_SETTLEMENT.Party_Code
	AND #STT_CONT.CONTRACTNO <> COMBINE_SETTLEMENT.Contractno 

	UPDATE COMBINE_SETTLEMENT SET PartiPantCode = MEMBERCODE FROM OWNER 

	UPDATE COMBINE_SETTLEMENT SET ISIN = M.ISIN FROM TBL_EXCHG_SCRIP_MAP M
	WHERE M.BSECODE = SCRIP_CD AND SERIES = 'BSE' AND @Sauda_Date BETWEEN FROM_DATE AND TO_DATE 
	/*	
	UPDATE COMBINE_SETTLEMENT SET ISIN = M.ISIN FROM TBL_EXCHG_SCRIP_MAP M
	WHERE M.NSE_SCRIP_CD = SCRIP_CD AND NSE_SERIES  = SERIES  AND @Sauda_Date BETWEEN FROM_DATE AND TO_DATE 
	*/	
	UPDATE COMBINE_SETTLEMENT SET SCRIP_CD = NSE_SCRIP_CD, SERIES = NSE_SERIES FROM TBL_EXCHG_SCRIP_MAP M
	WHERE M.ISIN = COMBINE_SETTLEMENT.ISIN AND @Sauda_Date BETWEEN FROM_DATE AND TO_DATE 
	AND COMBINE_SETTLEMENT.SCRIP_CD = BSECODE
		
	UPDATE COMBINE_SETTLEMENT SET SERIES=LEFT(SERIES,2) WHERE LEN(SERIES)<>2
END

GO
