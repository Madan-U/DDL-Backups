CREATE PROCEDURE [dbo].[CLS_COMMON_ADVANCEFILTER]
(
@SESSIONID NVARCHAR(50),
@REPORTCODE VARCHAR(50),  
@UNAME NVARCHAR(50)
)
AS

/*

Exec CLS_COMMON_ADVANCEFILTER 'omrbjuoujtl4m1gf2y3x0jox','CLIENT_LEDGER','CLASSUSER'

*/
BEGIN 
IF(@SESSIONID IS NULL AND @SESSIONID<>'')
	BEGIN
	SELECT * FROM CLS_ADVANCEFILTER WHERE FLAG=1 AND FLDREPORTCODE = @REPORTCODE
	ORDER BY ID,SRNO
	END
ELSE
	BEGIN
	
	SELECT * INTO #CLS_ADVANCEFILTERTABLE FROM CLS_ADVANCEFILTER WHERE FLAG=1 AND FLDREPORTCODE = @REPORTCODE ORDER BY ID,SRNO

	UPDATE AD SET DEF_VALUE =PVALUE FROM #CLS_ADVANCEFILTERTABLE AD,CLS_ADVANCE_FILTER_PARAMETERS P
	WHERE P.SESSIONID=@SESSIONID
	AND CNTRL_ID=P.PKEY

	SELECT * FROM #CLS_ADVANCEFILTERTABLE  WHERE FLAG=1
	ORDER BY ID,SRNO 
	END
END

GO
