CREATE PROCEDURE [dbo].[PROC_DEL_MASTER_POAID]
(
	@DPID			VARCHAR(8),
	@CLTDPID		VARCHAR(16),
	@POA_TYPE		VARCHAR(10), -- PAY-IN, MARGIN, ALL
	@MASTER_POA_ID	VARCHAR(16),
	@FROM_DATE		VARCHAR(11),
	@TO_DATE		VARCHAR(11),
	@USERNAME		VARCHAR(50),
	@TYPE			VARCHAR(10),
	@SRNO			INT
)AS
--EXEC PROC_DEL_MASTER_POAID '','123456778999','','BBBBBBB','','Sep 19 2014','broker','UPDATE',2
BEGIN
	IF @TYPE='ADD'
	BEGIN
		INSERT INTO TBL_DEL_MASTER_POAID 
		VALUES(@DPID,@CLTDPID,@POA_TYPE,@MASTER_POA_ID,@FROM_DATE,@TO_DATE,@USERNAME,GETDATE(),'',GETDATE())
	END
	IF @TYPE='UPDATE'
	BEGIN
		UPDATE TBL_DEL_MASTER_POAID 
		SET CLTDPID = @CLTDPID,
		MASTER_POA_ID = @MASTER_POA_ID,
		TO_DATE = @TO_DATE,
		EDITED_BY = @USERNAME,
		EDITED_ON = GETDATE()
		WHERE SNO=@SRNO		
	END
END

GO
