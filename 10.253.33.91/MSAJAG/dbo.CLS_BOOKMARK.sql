CREATE PROC [dbo].[CLS_BOOKMARK]
(
	@RPTCODE VARCHAR(20),
	@STATUSNAME VARCHAR(25),
	@BOOKMARK CHAR(1)
)
/*
SELECT * FROM CLS_TBLREPORT_SETTING where FLDREPORTCODE = 'PARTY_LEDGER'

update CLS_TBLREPORT_SETTING set FLDOPTIONLIST = '' where  FLDREPORTCODE = 'PNL_NEW'

EXEC CLS_BOOKMARK 'PARTY_LEDGER','BROKER', ''
EXEC CLS_BOOKMARK 'CASH_BOOK','BROKER', 'N'
SELECT * FROM CLS_TBLUSERREPORT_SETTING WHERE FLDREPORTCODE = 'CASH_BOOK' AND FLDUSERNAME = 'BROKER'

BSE-CAPITAL,NSE-CAPITAL,NSE-FUTURES,NCX-FUTURES,NSX-FUTURES,MCX-FUTURES,MCD-FUTURES,TRP-PRODUCT
TRP-PRODUCT,NSE-FUTURES,NSX-FUTURES,BSE-FUTURES,BSX-FUTURES,MFO-FUTURES,MCX-FUTURES,NCX-FUTURES
*/
AS

DECLARE 
	@RESULT VARCHAR(500)
	SET @RESULT = ''
	
IF @BOOKMARK = 'Y'
 BEGIN
	IF (SELECT COUNT(*) FROM CLS_TBLUSERREPORT_SETTING WHERE FLDUSERNAME = @STATUSNAME AND FLDREPORTCODE = @RPTCODE) = 1
	 BEGIN
		UPDATE CLS_TBLUSERREPORT_SETTING SET BOOKMARK = 1 WHERE FLDUSERNAME = @STATUSNAME AND FLDREPORTCODE = @RPTCODE
		SET @RESULT = 'CREATED'
	 END 
    ELSE

	BEGIN
		IF EXISTS (SELECT @STATUSNAME, FLDREPORTCODE, FLDEXCHLIST,FLDOPTIONLIST, 1 FROM CLS_TBLREPORT_SETTING WHERE FLDREPORTCODE = @RPTCODE)
			BEGIN 
				INSERT INTO CLS_TBLUSERREPORT_SETTING (FLDUSERNAME, FLDREPORTCODE,FLDEXCHLIST, FLDOPTIONLIST, BOOKMARK) 
				SELECT @STATUSNAME, FLDREPORTCODE, FLDEXCHLIST,FLDOPTIONLIST, 1 FROM CLS_TBLREPORT_SETTING WHERE FLDREPORTCODE = @RPTCODE
				SET @RESULT = 'CREATED'
			END
		ELSE
			BEGIN 
				SET @RESULT = '0'
			END
	 END
	 SELECT RESULT = UPPER(@RESULT), FLDREPORTCODE = '', FLDREPORTPATH= '', CHILDMAP=''
 END
ELSE IF @BOOKMARK = 'N'
 BEGIN
	UPDATE CLS_TBLUSERREPORT_SETTING SET BOOKMARK = 0 WHERE FLDUSERNAME = @STATUSNAME AND FLDREPORTCODE = @RPTCODE
	SET @RESULT = 'REMOVED'
	SELECT RESULT = UPPER(@RESULT), FLDREPORTCODE = '', FLDREPORTPATH= '', CHILDMAP=''
 END
ELSE 
 BEGIN
	SELECT RESULT = UPPER(@RESULT), A.FLDREPORTCODE, FLDREPORTPATH, CHILDMAP  FROM CLS_TBLUSERREPORT_SETTING A LEFT OUTER JOIN CLS_TBLREPORT_SETTING B ON A.FLDREPORTCODE = B.FLDREPORTCODE, CLS_TBLREPORTS C WHERE FLDUSERNAME = @STATUSNAME AND BOOKMARK = 1 AND B.FLDREPORTCODE = C.FLDREPORTCODE
 END

GO
