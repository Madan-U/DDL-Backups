-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[PROC_CM_MARGIN_DWLOAD_BKUP_18May20]

(

	@EFFDATE	VARCHAR(11),

	@COMBINEMARGIN INT = 0 

)

AS



SET NOCOUNT ON 

-- EXEC PROC_CM_MARGIN_DWLOAD 'DEC 23 2019'



DECLARE @CL_DATE VARCHAR(11)

DECLARE @VARDATE VARCHAR(11)

DECLARE @T2DAY	VARCHAR(11)

DECLARE @FILENAME VARCHAR(50)

DECLARE @BATCHNO INT

DECLARE @MEMBERCODE VARCHAR(10) 



SET @FILENAME = ''



SELECT @MEMBERCODE = MEMBERCODE FROM OWNER



SELECT @BATCHNO = ISNULL(MAX(fldBatchNo),0) FROM ASIANFILEBATCHNO

WHERE fldFileDate = LEFT(CONVERT(DATETIME, @EFFDATE,103),11) AND FldFileNo = 'CM'



SELECT @BATCHNO = @BATCHNO + 1



SET @FILENAME = 'C_MRG_' + 'TM' + '_' + REPLACE(@EFFDATE,'/','') + '_' + RIGHT('00' + CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'



INSERT INTO ASIANFILEBATCHNO

SELECT 'CM', @BATCHNO, LEFT(CONVERT(DATETIME, @EFFDATE,103),11), GETDATE() 



CREATE TABLE #CASHBAL 

(

	PARTY_CODE VARCHAR(10),

	LEDBAL NUMERIC(18,2)

)



SET @EFFDATE = LEFT(CONVERT(DATETIME, @EFFDATE,103),11)



IF @COMBINEMARGIN = 0 

BEGIN

	IF @EFFDATE = LEFT(CONVERT(DATETIME,GETDATE(),109),11)

	BEGIN

		EXEC MSAJAG.DBO.PR_GET_CASH_MARGIN @EFFDATE

	END



	SELECT @T2DAY = MIN(SEC_PAYIN) FROM MSAJAG.DBO.SETT_MST 

	WHERE SEC_PAYIN >= @EFFDATE AND SETT_TYPE = 'N'



	SELECT @CL_DATE = LEFT(MAX(SYSDATE),11) FROM CLOSING WHERE SYSDATE <= @EFFDATE + '  23:59'

	SELECT @VARDATE = LEFT(MAX(RECDATE),11) FROM VARCONTROL WHERE RECDATE <= @EFFDATE + '  23:59'

 

	EXEC ACCOUNT.DBO.PR_GET_LEDBAL_CMMARGIN @EFFDATE, @T2DAY

	EXEC ANAND.ACCOUNT_AB.DBO.PR_GET_LEDBAL_CMMARGIN @EFFDATE, @T2DAY



	SELECT EXCHANGE, PARTY_CODE, SCRIP_CD, SERIES, ISIN, QTY, EFFDATE, HAIRCUT = CONVERT(NUMERIC(18,4),100),

	CL_RATE =  CONVERT(NUMERIC(18,4),100)

	INTO #HOLDDATA

	FROM COLLATERALDETAILS 

	WHERE EFFDATE = @EFFDATE

	AND SEGMENT = 'CAPITAL'



	INSERT INTO #HOLDDATA

	SELECT EXCHANGE, PARTY_CODE, SCRIP_CD, SERIES, ISIN, QTY, TRANSDATE, HAIRCUT = CONVERT(NUMERIC(18,4),100),

	CL_RATE =  CONVERT(NUMERIC(18,4),100)

	FROM DELHOLD_MARGIN

	WHERE TRANSDATE = @EFFDATE



	INSERT INTO #CASHBAL 

	SELECT PARTY_CODE, LEDBAL=SUM(LEDBAL) 

	FROM ACCOUNT.DBO.FOMARGINNEW_LEDGERBAL

	WHERE MARGINDATE = @EFFDATE

	GROUP BY PARTY_CODE



	INSERT INTO #CASHBAL 

	SELECT PARTY_CODE, LEDBAL=SUM(LEDBAL) FROM ANAND.ACCOUNT_AB.DBO.FOMARGINNEW_LEDGERBAL

	WHERE MARGINDATE = @EFFDATE

	GROUP BY PARTY_CODE



	INSERT INTO #CASHBAL 

	SELECT PARTY_CODE, LEDBAL=SUM(CASH) FROM MSAJAG.DBO.COLLATERAL

	WHERE TRANS_DATE = @EFFDATE

	GROUP BY PARTY_CODE



	UPDATE #HOLDDATA SET SCRIP_CD = M.SCRIP_CD, SERIES = M.SERIES

	FROM MSAJAG.DBO.MULTIISIN M

	WHERE M.ISIN =  #HOLDDATA.ISIN

	AND VALID = 1



	UPDATE #HOLDDATA SET CL_RATE = C.CL_RATE FROM MSAJAG.DBO.CLOSING C

	WHERE C.SCRIP_CD = #HOLDDATA.SCRIP_CD AND C.SERIES = #HOLDDATA.SERIES

	AND C.SYSDATE BETWEEN @CL_DATE AND @CL_DATE + ' 23:59'



	UPDATE #HOLDDATA SET CL_RATE = C.CL_RATE FROM MSAJAG.DBO.CLOSING C

	WHERE C.SCRIP_CD = #HOLDDATA.SCRIP_CD AND C.SERIES IN ('EQ','BE','BZ')

	AND #HOLDDATA.SERIES IN ('EQ','BE','BZ')

	AND C.SYSDATE BETWEEN @CL_DATE AND @CL_DATE + ' 23:59'

	AND #HOLDDATA.CL_RATE = 0



	UPDATE #HOLDDATA SET HAIRCUT = V.Varmarginrate

	FROM MSAJAG.DBO.VARDETAIL V, MSAJAG.DBO.VARCONTROL C

	WHERE C.RECDATE BETWEEN @VARDATE AND @VARDATE+' 23:59'

	AND C.DETAILKEY = V.DETAILKEY

	AND V.ISIN = #HOLDDATA.ISIN

	AND HAIRCUT = 100



	UPDATE #HOLDDATA SET SCRIP_CD = M.SCRIP_CD, SERIES = M.SERIES

	FROM [196.1.115.197].BSEDB.DBO.MULTIISIN M

	WHERE M.ISIN =  #HOLDDATA.ISIN

	AND VALID = 1

	AND CL_RATE = 0



	UPDATE #HOLDDATA SET CL_RATE = C.CL_RATE FROM [196.1.115.197].BSEDB.DBO.CLOSING C

	WHERE C.SCRIP_CD = #HOLDDATA.SCRIP_CD AND C.SERIES = #HOLDDATA.SERIES

	AND C.SYSDATE BETWEEN @CL_DATE AND @CL_DATE + ' 23:59'

	AND #HOLDDATA.CL_RATE = 0



	UPDATE #HOLDDATA SET HAIRCUT = V.MARGIN+ELM

	FROM [196.1.115.197].BSEDB.DBO.VARDETAIL V

	WHERE V.FDATE BETWEEN @VARDATE AND @VARDATE+' 23:59' 

	AND V.ISIN = #HOLDDATA.ISIN

	AND HAIRCUT = 100



	INSERT INTO #CASHBAL 

	SELECT PARTY_CODE, LEDBAL=SUM(CONVERT(NUMERIC(18,2),CL_RATE*QTY-CL_RATE*QTY*HAIRCUT/100))  

	FROM #HOLDDATA

	GROUP BY PARTY_CODE

END

ELSE

BEGIN

	INSERT INTO #CASHBAL

	SELECT PARTY_CODE, LEDBAL = (CASE WHEN T_MARGINAVL > 0 THEN TDAY_MARGIN ELSE TDAY_MARGIN+T_MARGINAVL END)  

		+(CASE WHEN T_MTMAVL > 0 THEN TDAY_MTM ELSE TDAY_MTM+T_MTMAVL END) 

	FROM MSAJAG.DBO.TBL_COMBINE_REPORTING_DETAIL

	WHERE MARGINDATE = @EFFDATE 

	AND EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL'

	AND TDAY_MARGIN + TDAY_MTM > 0 

END



SELECT MARGINDATE=REPLACE(CONVERT(VARCHAR,MARGINDATE,106),' ','-'),

PARTY_CODE,VARMARGIN,EXTREME_LOSS_MARGIN,ADDITIONAL_MARGIN,MTOM_LOSS,TOTAL_MARGIN,CL_FLAG,

COLLAMT=CONVERT(NUMERIC(18,2),0) 

INTO #MAR_RETURN

FROM TBL_CMMARGIN

WHERE MARGINDATE = @EFFDATE



UPDATE #MAR_RETURN SET COLLAMT = LEDBAL

FROM (SELECT PARTY_CODE, LEDBAL = SUM(LEDBAL)

	FROM #CASHBAL 

GROUP BY PARTY_CODE) C

WHERE C.PARTY_CODE = #MAR_RETURN.PARTY_CODE





UPDATE #MAR_RETURN SET COLLAMT = 0

WHERE COLLAMT < 0 



UPDATE #MAR_RETURN SET COLLAMT = TOTAL_MARGIN

WHERE COLLAMT > TOTAL_MARGIN 





SELECT PARTY_CODE, 

DATA = UPPER(MARGINDATE 

+ ',' +  LTRIM(RTRIM(PARTY_CODE)) 

+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),VARMARGIN)) 

+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EXTREME_LOSS_MARGIN))

+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ADDITIONAL_MARGIN))

+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),MTOM_LOSS))

+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_MARGIN))

+ ',' + CL_FLAG

+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),COLLAMT))),

FILENAME = @FILENAME

FROM #MAR_RETURN

ORDER BY PARTY_CODE

GO
