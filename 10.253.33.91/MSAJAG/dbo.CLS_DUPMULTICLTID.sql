CREATE PROC [dbo].[CLS_DUPMULTICLTID]
(
@FROMCLIENT VARCHAR(10),
@TOCLIENT VARCHAR(10)
)
AS
--EXEC CLS_DUPMULTICLTID '0','zzzz'

BEGIN
--Exec Rpt_DelMultiDup
SELECT PARTY_CODE, CLTDPNO, DPID, INTRODUCER, DPTYPE 
FROM   MULTICLTID 
WHERE PARTY_CODE >=@FROMCLIENT
AND PARTY_CODE<=@TOCLIENT
GROUP BY PARTY_CODE, CLTDPNO, DPID, INTRODUCER, DPTYPE 
HAVING COUNT(PARTY_CODE) > 1 
ORDER BY PARTY_CODE, CLTDPNO, DPID 

END

GO
