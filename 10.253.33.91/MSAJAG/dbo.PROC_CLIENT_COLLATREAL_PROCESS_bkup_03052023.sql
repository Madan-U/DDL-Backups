DECLARE @CCCMDATE	VARCHAR(11)

SELECT @CCCMDATE = ISNULL(MAX(RECORDDATE),'APR  1 2022') FROM TBL_CCCM_SCRIP_DATA
WHERE RECORDDATE <= @RUNDATE AND FILETYPE = 'CC'

SELECT * INTO #TBL_CCCM_SCRIP_DATA FROM TBL_CCCM_SCRIP_DATA WHERE RECORDDATE=@CCCMDATE 

CREATE INDEX #PARTY ON #TBL_CCCM_SCRIP_DATA(CLIENT_CODE,ISIN,EXCHANGE,SEGMENT)


iF @RUNDATE <> @CCCMDATE 
BEGIN
	SELECT *,HAIRCUT=CONVERT(NUMERIC(18,3),0),CL_RATE=CONVERT(NUMERIC(18,3),0),FINAL_EFFECTIVE_VALUE=CONVERT(NUMERIC(18,3),0)
	INTO #STOCK
	FROM TBL_CCCM_SCRIP_DATA_INTRADAY
	WHERE TRANSDATE = @RUNDATE
	
	iF (sELECT iSNULL(COUNT(1),0) fROM #STOCK) > 0 
	bEGIN
		SELECT DISTINCT EXCHANGE, SEGMENT INTO #STCOK_SEG FROM #STOCK

		SELECT DISTINCT ISIN, CL_RATE,HAIRCUT INTO #HAIRCUT FROM TBL_NONCASH_REPORT
			WHERE PROCESS_DATE=@RUNDATE

			CREATE CLUSTERED INDEX IDXHAIR ON #HAIRCUT(ISIN)

		UPDATE #STOCK SET HAIRCUT = C.HAIRCUT, CL_RATE = C.CL_RATE, 
		FINAL_EFFECTIVE_VALUE = NET_EFF_QTY *  C.CL_RATE - NET_EFF_QTY *  C.CL_RATE * C.HAIRCUT / 100
		FROM #HAIRCUT C
		WHERE C.ISIN = #STOCK.ISIN
		
		UPDATE #STOCK SET HAIRCUT = C.HAIRCUT,		
		FINAL_EFFECTIVE_VALUE = NET_EFF_QTY *  CL_RATE - NET_EFF_QTY *  CL_RATE * C.HAIRCUT / 100
		FROM TBL_CC_HAIRCUT C
		WHERE SYSDATE = @RUNDATE
		AND C.ISIN = #STOCK.ISIN 

		SELECT * INTO #SUMMARY FROM TBL_NONCASH_SUMMARY T
		WHERE PROCESS_DATE = @RUNDATE
		AND EXCHANGE IN (SELECT EXCHANGE FROM #STOCK_SEG S WHERE T.EXCHANGE = S.EXCHANGE AND T.SEGMENT = S.SEGMENT)

		UPDATE #SUMMARY SET APPROVED_SEC_CASH_WNCL = 0, APPROVED_SEC_NONCASH_WNCL = 0
			WHERE PROCESS_DATE = @RUNDATE 
			AND ISIN NOT IN (SELECT ISIN FROM #STOCK WHERE #STOCK.CLIENT_CODE = #SUMMARY.PARTY_CODE AND 
			#STOCK.ISIN = #SUMMARY.ISIN 
			AND #STOCK.EXCHANGE = #SUMMARY.EXCHANGE 
			AND #STOCK.SEGMENT = #SUMMARY.SEGMENT)
			
			UPDATE #SUMMARY SET  
			APPROVED_SEC_CASH_REC_TM = APPROVED_SEC_CASH_REC_TM  
									 - (CASE WHEN (CASE WHEN APPROVED_SEC_CASH_WNCL > 0 
													  THEN APPROVED_SEC_CASH_WNCL -FINAL_EFFECTIVE_VALUE/C.QTY*#SUMMARY.REPLDQTY 
													  ELSE 0 
													END) < 0 
										   THEN (CASE WHEN APPROVED_SEC_CASH_WNCL > 0 
													  THEN APPROVED_SEC_CASH_WNCL -FINAL_EFFECTIVE_VALUE/C.QTY*#SUMMARY.REPLDQTY 
													  ELSE 0 
													END)
											ELSE 0 END),
		APPROVED_SEC_NONCASH_REC_TM = APPROVED_SEC_NONCASH_REC_TM  
								 - (CASE WHEN (CASE WHEN APPROVED_SEC_NONCASH_WNCL > 0 
													  THEN APPROVED_SEC_NONCASH_WNCL -FINAL_EFFECTIVE_VALUE/C.QTY*#SUMMARY.REPLDQTY 
													  ELSE 0 
													END) < 0 
										   THEN (CASE WHEN APPROVED_SEC_NONCASH_WNCL > 0 
													  THEN APPROVED_SEC_NONCASH_WNCL -FINAL_EFFECTIVE_VALUE/C.QTY*#SUMMARY.REPLDQTY 
													  ELSE 0 
													END)
											ELSE 0 END),
		APPROVED_SEC_CASH_RET_TM= APPROVED_SEC_CASH_RET_TM  
								 + (CASE WHEN (CASE WHEN APPROVED_SEC_CASH_WNCL > 0 
													  THEN APPROVED_SEC_CASH_WNCL -FINAL_EFFECTIVE_VALUE/C.QTY*#SUMMARY.REPLDQTY 
													  ELSE 0 
													END) > 0 
										   THEN (CASE WHEN APPROVED_SEC_CASH_WNCL > 0 
													  THEN APPROVED_SEC_CASH_WNCL -FINAL_EFFECTIVE_VALUE/C.QTY*#SUMMARY.REPLDQTY 
													  ELSE 0 
													END)
											ELSE 0 END),
		APPROVED_SEC_NONCASH_RET_TM = APPROVED_SEC_NONCASH_RET_TM  
								 + (CASE WHEN (CASE WHEN APPROVED_SEC_NONCASH_WNCL > 0 
