CREATE PROCEDURE PROC_ALL_REPORT_FREEZE_DATA (@RPTDATE VARCHAR(11),@REPORT VARCHAR(10))

AS ---- EXEC PROC_ALL_REPORT_FREEZE_DATA 'OCT 10 2022','COM'

IF @REPORT ='EQY'
BEGIN
	DELETE FROM TBL_ALLOCATION_REPORTING_HISTORY_EQUITY WHERE RPT_DATE = @RPTDATE

	INSERT INTO TBL_ALLOCATION_REPORTING_HISTORY_EQUITY
	SELECT *, GETDATE() FROM TBL_ALLOCATION_REPORTING WHERE RPT_DATE = @RPTDATE
END
  ELSE
	BEGIN
	DELETE FROM TBL_ALLOCATION_REPORTING_HISTORY_COMM WHERE RPT_DATE = @RPTDATE

	INSERT INTO TBL_ALLOCATION_REPORTING_HISTORY_COMM
	SELECT *, GETDATE() FROM TBL_ALLOCATION_REPORTING WHERE RPT_DATE = @RPTDATE
END

SELECT  CASE WHEN @REPORT='EQY' THEN 'Equity' else 'Commodity' end +'-'+'Allocation reporting data freeze for date ' + @RPTDATE

GO
