 SET @MRGCUR = CURSOR FOR  
 SELECT FAMILY, PARTY_CODE, AVAL_MARGIN=TDAY_MARGIN_SHORT FROM TBL_COMBINE_REPORTING  
 WHERE MARGINDATE = @MARGINDATE 
 AND TDAY_MARGIN_SHORT > 0 AND ISNULL(FAMILY,'') <> ''
 ORDER BY FAMILY   
 OPEN @MRGCUR  
 FETCH NEXT FROM @MRGCUR INTO @FAMILY, @F_PARTY_CODE, @SHRT_AMT  
 WHILE @@FETCH_STATUS = 0  
 BEGIN  
  SET @EXCHANGEWISE_CURSOR = CURSOR FOR  
  SELECT PARTY_CODE,  TDAY_MARGIN_SHORT 
  FROM TBL_COMBINE_REPORTING 
  WHERE MARGINDATE = @MARGINDATE  
  AND FAMILY = @FAMILY   
  AND TDAY_MARGIN > 0 AND TDAY_MARGIN_SHORT < 0 
  ORDER BY TDAY_MARGIN_SHORT DESC  
  OPEN @EXCHANGEWISE_CURSOR  
  FETCH NEXT FROM @EXCHANGEWISE_CURSOR INTO @PARTY_CODE, @MARG_AMT  
  WHILE @@FETCH_STATUS = 0  
  BEGIN  
   IF @SHRT_AMT > 0   
   BEGIN  
    IF ABS(@MARG_AMT) >= @SHRT_AMT  
    BEGIN  
     UPDATE TBL_COMBINE_REPORTING SET TDAY_MARGIN_SHORT = TDAY_MARGIN_SHORT + @SHRT_AMT, 
     FAMILY_AMOUNT = FAMILY_AMOUNT + (CASE WHEN FAMILY_AMOUNT <> '' THEN ',' ELSE '' END) + CONVERT(VARCHAR,@SHRT_AMT),
     FAMILY_CODE = FAMILY_CODE + (CASE WHEN FAMILY_CODE <> '' THEN ',' ELSE '' END) + @F_PARTY_CODE 
     WHERE MARGINDATE = @MARGINDATE  
     AND PARTY_CODE = @PARTY_CODE  
     
