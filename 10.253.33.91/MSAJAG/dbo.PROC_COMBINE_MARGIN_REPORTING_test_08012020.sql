CREATE PROCEDURE [dbo].[PROC_COMBINE_MARGIN_REPORTING_test_08012020]   
(  
 @MARGINDATE VARCHAR(11),  
 @COMBINEFLAG INT = 1,  
 @FORREPORTING VARCHAR(1)='Y',
 @INTERNALFLAG	INT = 0  
)      
AS      
  
-- EXEC PROC_COMBINE_MARGIN_REPORTING_test_08012020 'JAN  1 2020',1,'Y',1  

SET NOCOUNT ON   
DECLARE @EXCHANGE_NEW VARCHAR(3)  
DECLARE @SEGMENT_NEW VARCHAR(10)  
DECLARE @SHAREDB VARCHAR(35)  
DECLARE @SHARESERVER VARCHAR(35)  
DECLARE @ACCOUNTDB VARCHAR(35)  
DECLARE @ACCOUNTSERVER VARCHAR(35)  
  
DECLARE @EXCHANGEWISE_CURSOR CURSOR   
DECLARE @STRSQL VARCHAR(8000)   
DECLARE @MARGINDATENEW VARCHAR(11)  
DECLARE @MARGINDATECURSOR CURSOR  
DECLARE @MRGCUR CURSOR  
DECLARE @TDAY1FLAG	INT
  
DECLARE @CNT INT  
DECLARE @DAY INT  
DECLARE @PARTY_CODE VARCHAR(10)  
DECLARE @F_PARTY_CODE VARCHAR(10) 
DECLARE @FAMILY VARCHAR(10) 
DECLARE @SHRT_AMT NUMERIC(18,4)  
DECLARE @MARG_AMT NUMERIC(18,4)  
  
DECLARE @EXCHANGE_PRIORITY INT  

SET @COMBINEFLAG = 1 
--SET @FORREPORTING = 'Y'
SET @TDAY1FLAG = 0 

IF @INTERNALFLAG = 0 
BEGIN
	DELETE FROM TBL_COMBINE_REPORTING_DETAIL  
	WHERE MARGINDATE = @MARGINDATE   
  
IF @MARGINDATE = LEFT(GETDATE(),11)  

BEGIN  

		EXEC PROC_RMS_CALCULATION @MARGINDATE  

		--EXEC EDP_DP_BALANCE_UPDATE_NEW

		--EXEC PROC_COMBINE_HOLDING

		
END  
EXEC V2_PR_COLLATERAL_MARGIN_COLLATERAL @MARGINDATE  
  
	SET @CNT = 1  
	SET @DAY = 1   
  
	SET @MARGINDATECURSOR = CURSOR FOR  
	SELECT DISTINCT TOP 3 SYSDATE FROM MSAJAG.DBO.CLOSING   
	WHERE SYSDATE >= @MARGINDATE   
	ORDER BY SYSDATE 
	--SELECT DISTINCT TOP 3 TRADE_DATE FROM SAN2.MCDX.DBO.FOCLOSING 
	--WHERE TRADE_DATE >= @MARGINDATE 
	--ORDER BY TRADE_DATE   
	OPEN  @MARGINDATECURSOR  
	FETCH NEXT FROM @MARGINDATECURSOR INTO @MARGINDATENEW  
	WHILE @@FETCH_STATUS = 0  
	BEGIN  
	 SET @EXCHANGEWISE_CURSOR = CURSOR  
	 FOR  
	 SELECT DISTINCT   
	  EXCHANGE = MAX(M.EXCHANGE),  
	  SEGMENT = MAX(M.SEGMENT),  
	  SHARESERVER,  
	  ACCOUNTDB,  
	  ACCOUNTSERVER  
	 FROM PRADNYA..MULTICOMPANY M(NOLOCK)   
	 WHERE  M.SEGMENT NOT IN ('SLBS')  
	  AND SEGMENT_DESCRIPTION NOT LIKE '%PCM%'   
	  AND PRIMARYSERVER = 1   
	  AND EXISTS (SELECT T.EXCHANGE FROM V2_TBL_EXCHANGE_MARGIN_REPORT T   
		 WHERE T.EXCHANGE = M.EXCHANGE  
		 AND T.SEGMENT = M.SEGMENT) 
	 GROUP BY SHARESERVER, ACCOUNTDB, ACCOUNTSERVER   
	 OPEN @EXCHANGEWISE_CURSOR  
	 FETCH NEXT  
	 FROM @EXCHANGEWISE_CURSOR  
	 INTO  
	  @EXCHANGE_NEW,  
	  @SEGMENT_NEW,  
	  @SHARESERVER,  
	  @ACCOUNTDB,  
	  @ACCOUNTSERVER  
  
	 WHILE @@FETCH_STATUS = 0  
	 BEGIN  
	  SET @STRSQL = ''  
	  IF @ACCOUNTSERVER = @@SERVERNAME   
	  BEGIN  
	   SET @ACCOUNTSERVER =''  
	  END  
	  ELSE  
	  BEGIN  
	   SET @ACCOUNTSERVER = @ACCOUNTSERVER + '.'  
	  END   
     
	  SET @STRSQL = 'EXEC ' + @ACCOUNTSERVER + @ACCOUNTDB + '.DBO.PR_GET_LEDBAL_COMBINE ''' + @MARGINDATENEW + ''' '   
	  EXEC (@STRSQL)   
  
	  IF @CNT = 1  
	  BEGIN  
	   SET @STRSQL = ' INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, TDAY_LEDGER )'  
	   SET @STRSQL = @STRSQL + ' SELECT ''' + @MARGINDATE + ''', ''' + @EXCHANGE_NEW + ''', ''' + @SEGMENT_NEW + ''', PARTY_CODE, LEDBAL '  
	   SET @STRSQL = @STRSQL + ' FROM ' + @ACCOUNTSERVER + @ACCOUNTDB + '.DBO.FOMARGINNEW_LEDGERBAL WHERE MARGINDATE = ''' + @MARGINDATENEW + ''' '   
	   EXEC (@STRSQL)  
     
	  END  
  
	  IF @CNT = 2  
	  BEGIN  
	   SET @STRSQL = ' INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, T1DAY_LEDGER )'  
	   SET @STRSQL = @STRSQL + ' SELECT ''' + @MARGINDATE + ''', ''' + @EXCHANGE_NEW + ''', ''' + @SEGMENT_NEW + ''', PARTY_CODE, LEDBAL '  
	   SET @STRSQL = @STRSQL + ' FROM ' + @ACCOUNTSERVER + @ACCOUNTDB + '.DBO.FOMARGINNEW_LEDGERBAL WHERE MARGINDATE = ''' + @MARGINDATENEW + ''' '   
	   EXEC (@STRSQL)  
  
	  END  
  
	  IF @CNT = 3  
	  BEGIN  
	   SET @STRSQL = ' INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, T2DAY_LEDGER )'  
	   SET @STRSQL = @STRSQL + ' SELECT ''' + @MARGINDATE + ''', ''' + @EXCHANGE_NEW + ''', ''' + @SEGMENT_NEW + ''', PARTY_CODE, LEDBAL '  
	   SET @STRSQL = @STRSQL + ' FROM ' + @ACCOUNTSERVER + @ACCOUNTDB + '.DBO.FOMARGINNEW_LEDGERBAL WHERE MARGINDATE = ''' + @MARGINDATENEW + ''' '   
	   EXEC (@STRSQL)  
	  END  
	  FETCH NEXT  
	  FROM @EXCHANGEWISE_CURSOR  
	  INTO   
	   @EXCHANGE_NEW,  
	   @SEGMENT_NEW,  
	   @SHARESERVER,  
	   @ACCOUNTDB,  
	   @ACCOUNTSERVER  
	 END  
  
	 CLOSE @EXCHANGEWISE_CURSOR  
  
	 IF @DAY = 1   
	 BEGIN  
	  INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, TDAY_CASHCOLL, TDAY_FDBG, TDAY_NONCASH)  
	  SELECT MARGINDATE=@MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE,   
	  TDAY_CASHCOLL = ISNULL(SUM(CASE WHEN COLL_TYPE = 'MARGIN' THEN ISNULL(MRG_FINALAMOUNT,0) ELSE 0 END),0),  
	  TDAY_FDBG = ISNULL(SUM(CASE WHEN COLL_TYPE = 'BG' OR COLL_TYPE = 'FD' THEN ISNULL(MRG_FINALAMOUNT,0) ELSE 0 END),0),  
	  TDAY_NONCASH = ISNULL(SUM(CASE WHEN COLL_TYPE = 'SEC' THEN ISNULL(MRG_FINALAMOUNT,0) ELSE 0 END),0)  
	  FROM  V2_TBL_COLLATERAL_MARGIN_COMBINE M  
	  WHERE EFFDATE = @MARGINDATENEW  
	  AND EXISTS (SELECT T.EXCHANGE FROM V2_TBL_EXCHANGE_MARGIN_REPORT T   
	   WHERE T.EXCHANGE = M.EXCHANGE  
	   AND T.SEGMENT = M.SEGMENT)  
	  GROUP BY EXCHANGE, SEGMENT, PARTY_CODE  
    
	  SET @MRGCUR = CURSOR FOR             
	   SELECT             
	   MARGSQL = ' INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, TDAY_MARGIN, TDAY_MTM )'  
		  + ' SELECT ''' + @MARGINDATE + ''',''' + T.EXCHANGE + ''', ''' + T.SEGMENT + ''',PARTY_CODE,MARGIN_CALL=SUM(ISNULL(' + MARGINCOLUMNSNONCASH  + ',0)),MTM_CALL=SUM(ISNULL(' + MARGINCOLUMNSCASH  + ',0)) FROM ' +            
	   SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE +             
	   ' WHERE ' + MARGINDATEFIELD + ' = (SELECT MAX(' + MARGINDATEFIELD + ') FROM ' + SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE + ' WHERE ' + MARGINDATEFIELD + ' <= ''' +  @MARGINDATENEW +' 23:59'') GROUP BY PARTY_CODE'            
	   FROM V2_TBL_EXCHANGE_MARGIN_REPORT T, PRADNYA..MULTICOMPANY M            
	   WHERE T.EXCHANGE = M.EXCHANGE            
	   AND T.SEGMENT = M.SEGMENT            
	   --AND T.SEGMENT <> 'CAPITAL'   
	   AND T.SEGMENT <> 'POA'          
	  OPEN @MRGCUR            
                  
	  FETCH NEXT FROM @MRGCUR INTO @STRSQL            
	  WHILE @@FETCH_STATUS = 0                   
	  BEGIN                
	   EXEC (@STRSQL)        
	   FETCH NEXT FROM @MRGCUR INTO @STRSQL            
	  END             
	  CLOSE @MRGCUR            
	  DEALLOCATE @MRGCUR    
  
	 END  
  
	 IF @DAY = 2   
	 BEGIN  
	  INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, T1DAY_CASHCOLL, T1DAY_FDBG, T1DAY_NONCASH)  
	  SELECT MARGINDATE=@MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE,   
	  T1DAY_CASHCOLL = SUM(CASE WHEN COLL_TYPE = 'MARGIN' THEN MRG_FINALAMOUNT ELSE 0 END),  
	  T1DAY_FDBG = SUM(CASE WHEN COLL_TYPE = 'BG' OR COLL_TYPE = 'FD' THEN MRG_FINALAMOUNT ELSE 0 END),  
	  T1DAY_NONCASH = SUM(CASE WHEN COLL_TYPE = 'SEC' THEN MRG_FINALAMOUNT ELSE 0 END)   
	  FROM  V2_TBL_COLLATERAL_MARGIN_COMBINE M  
	  WHERE EFFDATE = @MARGINDATENEW  
	  AND EXISTS (SELECT T.EXCHANGE FROM V2_TBL_EXCHANGE_MARGIN_REPORT T   
	   WHERE T.EXCHANGE = M.EXCHANGE  
	   AND T.SEGMENT = M.SEGMENT)  
	  GROUP BY EXCHANGE, SEGMENT, PARTY_CODE  
  
    
	  SET @MRGCUR = CURSOR FOR             
	   SELECT             
	   MARGSQL = ' INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, T1DAY_MARGIN, T1DAY_MTM )'  
		  + ' SELECT ''' + @MARGINDATE + ''',''' + T.EXCHANGE + ''', ''' + T.SEGMENT + ''',PARTY_CODE,MARGIN_CALL=SUM(' + MARGINCOLUMNSNONCASH  + '),MTM_CALL=SUM(' + MARGINCOLUMNSCASH  + ') FROM ' +            
	   SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE +             
	   ' WHERE ' + MARGINDATEFIELD + ' = (SELECT MAX(' + MARGINDATEFIELD + ') FROM ' + SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE + ' WHERE ' + MARGINDATEFIELD + ' <= ''' +  @MARGINDATENEW +' 23:59'') GROUP BY PARTY_CODE'            
	   FROM V2_TBL_EXCHANGE_MARGIN_REPORT T, PRADNYA..MULTICOMPANY M            
	   WHERE T.EXCHANGE = M.EXCHANGE            
	   AND T.SEGMENT = M.SEGMENT            
	  -- AND T.SEGMENT <> 'CAPITAL'   
	   AND T.SEGMENT <> 'POA'               
	  OPEN @MRGCUR            
                  
	  FETCH NEXT FROM @MRGCUR INTO @STRSQL            
	  WHILE @@FETCH_STATUS = 0                   
	  BEGIN                
	   EXEC (@STRSQL)     
	   FETCH NEXT FROM @MRGCUR INTO @STRSQL            
	  END             
	  CLOSE @MRGCUR            
	  DEALLOCATE @MRGCUR    
  
	 END  
  
	 IF @DAY = 3   
	 BEGIN  
	  INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, T2DAY_CASHCOLL, T2DAY_FDBG, T2DAY_NONCASH)  
	  SELECT MARGINDATE=@MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE,   
	  T2DAY_CASHCOLL = SUM(CASE WHEN COLL_TYPE = 'MARGIN' THEN MRG_FINALAMOUNT ELSE 0 END),  
	  T2DAY_FDBG = SUM(CASE WHEN COLL_TYPE = 'BG' OR COLL_TYPE = 'FD' THEN MRG_FINALAMOUNT ELSE 0 END),  
	  T2DAY_NONCASH = SUM(CASE WHEN COLL_TYPE = 'SEC' THEN MRG_FINALAMOUNT ELSE 0 END)   
	  FROM  V2_TBL_COLLATERAL_MARGIN_COMBINE M  
	  WHERE EFFDATE = @MARGINDATENEW  
	  AND EXISTS (SELECT T.EXCHANGE FROM V2_TBL_EXCHANGE_MARGIN_REPORT T   
	   WHERE T.EXCHANGE = M.EXCHANGE  
	   AND T.SEGMENT = M.SEGMENT)  
	  GROUP BY EXCHANGE, SEGMENT, PARTY_CODE  
     
	  SET @MRGCUR = CURSOR FOR             
	   SELECT             
	   MARGSQL = ' INSERT INTO TBL_COMBINE_REPORTING_DETAIL (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, T2DAY_MARGIN, T2DAY_MTM )'  
		  + ' SELECT ''' + @MARGINDATE + ''',''' + T.EXCHANGE + ''', ''' + T.SEGMENT + ''',PARTY_CODE,MARGIN_CALL=SUM(' + MARGINCOLUMNSNONCASH  + '),MTM_CALL=SUM(' + MARGINCOLUMNSCASH  + ') FROM ' +            
	   SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE +             
	   ' WHERE ' + MARGINDATEFIELD + ' = (SELECT MAX(' + MARGINDATEFIELD + ') FROM ' + SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE + ' WHERE ' + MARGINDATEFIELD + ' <= ''' +  @MARGINDATENEW +' 23:59'') GROUP BY PARTY_CODE'            
	   FROM V2_TBL_EXCHANGE_MARGIN_REPORT T, PRADNYA..MULTICOMPANY M            
	   WHERE T.EXCHANGE = M.EXCHANGE            
	   AND T.SEGMENT = M.SEGMENT            
	  -- AND T.SEGMENT <> 'CAPITAL'      
	   AND T.SEGMENT <> 'POA'            
	  OPEN @MRGCUR            
                  
	  FETCH NEXT FROM @MRGCUR INTO @STRSQL            
	  WHILE @@FETCH_STATUS = 0                   
	  BEGIN                
	   EXEC (@STRSQL)   
      
	   FETCH NEXT FROM @MRGCUR INTO @STRSQL            
	  END             
	  CLOSE @MRGCUR            
	  DEALLOCATE @MRGCUR    
  
	 END  
	 SET @DAY = @DAY + 1  
	 SET @CNT = @CNT + 1   
  
	 DEALLOCATE @EXCHANGEWISE_CURSOR  
	 FETCH NEXT FROM @MARGINDATECURSOR INTO @MARGINDATENEW  
	END  
	CLOSE @MARGINDATECURSOR  
	DEALLOCATE @MARGINDATECURSOR  
  
	IF @FORREPORTING <> 'Y'   
	BEGIN  
	 DELETE FROM TBL_COMBINE_REPORTING_DETAIL  
	 WHERE MARGINDATE = @MARGINDATE  
	 AND EXCHANGE = 'POA' AND SEGMENT = 'POA'  
	END  
END
 

SELECT MARGINDATE,EXCHANGE,SEGMENT,PARTY_CODE,  
    TDAY_LEDGER=SUM(ISNULL(TDAY_LEDGER,0)),  
    TDAY_MARGIN=SUM(ISNULL(TDAY_MARGIN,0)),  
    TDAY_MTM=SUM(ISNULL(TDAY_MTM,0)),  
    TDAY_CASHCOLL=SUM(ISNULL(TDAY_CASHCOLL,0)),  
    TDAY_FDBG=SUM(ISNULL(TDAY_FDBG,0)),  
    TDAY_NONCASH=SUM(ISNULL(TDAY_NONCASH,0)),  
    TDAY_MARGIN_SHORT = CONVERT(NUMERIC(18,2),0),   
       TDAY_MTM_SHORT = CONVERT(NUMERIC(18,2),0),  
    T1DAY_LEDGER=SUM(ISNULL(T1DAY_LEDGER,0)),  
    T1DAY_MARGIN=SUM(ISNULL(T1DAY_MARGIN,0)),  
    T1DAY_MTM=SUM(ISNULL(T1DAY_MTM,0)),  
    T1DAY_CASHCOLL=SUM(ISNULL(T1DAY_CASHCOLL,0)),  
    T1DAY_FDBG=SUM(ISNULL(T1DAY_FDBG,0)),  
    T1DAY_NONCASH=SUM(ISNULL(T1DAY_NONCASH,0)),  
    T1DAY_MARGIN_SHORT = CONVERT(NUMERIC(18,2),0),   
       T1DAY_MTM_SHORT = CONVERT(NUMERIC(18,2),0),  
    T2DAY_LEDGER=SUM(ISNULL(T2DAY_LEDGER,0)),  
    T2DAY_MARGIN=SUM(ISNULL(T2DAY_MARGIN,0)),  
    T2DAY_MTM=SUM(ISNULL(T2DAY_MTM,0)),  
    T2DAY_CASHCOLL=SUM(ISNULL(T2DAY_CASHCOLL,0)),  
    T2DAY_FDBG=SUM(ISNULL(T2DAY_FDBG,0)),  
    T2DAY_NONCASH =SUM(ISNULL(T2DAY_NONCASH,0)),  
    T2DAY_MARGIN_SHORT = CONVERT(NUMERIC(18,2),0),   
       T2DAY_MTM_SHORT = CONVERT(NUMERIC(18,2),0),  
    T_MARGINAVL = CONVERT(NUMERIC(18,2),0),  
    T_MTMAVL = CONVERT(NUMERIC(18,2),0),
    FAMILY_AMOUNT = MAX(FAMILY_AMOUNT),
    FAMILY_CODE =  MAX(FAMILY_CODE),
    FAMILY =  MAX(FAMILY)  
INTO #COMBINE_REPORTING_DETAIL  
FROM TBL_COMBINE_REPORTING_DETAIL  
WHERE MARGINDATE = @MARGINDATE  
GROUP BY MARGINDATE,EXCHANGE,SEGMENT,PARTY_CODE

UPDATE #COMBINE_REPORTING_DETAIL SET FAMILY = '', FAMILY_AMOUNT = '', FAMILY_CODE = ''

UPDATE #COMBINE_REPORTING_DETAIL SET FAMILY = T.FAMILY 
FROM TBL_FAMILY_ADJUSTMENT T
WHERE @MARGINDATE BETWEEN FROM_DATE AND TO_DATE 
AND T.PARTY_CODE = #COMBINE_REPORTING_DETAIL.PARTY_CODE 
 
DELETE FROM TBL_COMBINE_REPORTING_DETAIL  
WHERE MARGINDATE = @MARGINDATE  
  
DELETE FROM TBL_COMBINE_REPORTING  
WHERE MARGINDATE = @MARGINDATE  
  
INSERT INTO TBL_COMBINE_REPORTING  
SELECT MARGINDATE,  
    EXCHANGE = (CASE WHEN @COMBINEFLAG = 1 THEN '' ELSE EXCHANGE END),  
    SEGMENT = (CASE WHEN @COMBINEFLAG = 1 THEN '' ELSE SEGMENT END),  
    PARTY_CODE,  
    TDAY_LEDGER=SUM(ISNULL(TDAY_LEDGER,0)),  
    TDAY_MARGIN=SUM(ISNULL(TDAY_MARGIN,0)),  
    TDAY_MTM=SUM(ISNULL(TDAY_MTM,0)),  
    TDAY_CASHCOLL=SUM(ISNULL(TDAY_CASHCOLL,0)),  
    TDAY_FDBG=SUM(ISNULL(TDAY_FDBG,0)),  
    TDAY_CASH=SUM(ISNULL(TDAY_LEDGER,0))+SUM(ISNULL(TDAY_CASHCOLL,0)),  
    TDAY_NONCASH=SUM(ISNULL(TDAY_NONCASH,0))+SUM(ISNULL(TDAY_FDBG,0)),  
    TDAY_MARGIN_SHORT = 0,   
       TDAY_MTM_SHORT = 0,  
    T1DAY_LEDGER=SUM(ISNULL(T1DAY_LEDGER,0)),  
    T1DAY_MARGIN=SUM(ISNULL(T1DAY_MARGIN,0)),  
    T1DAY_MTM=SUM(ISNULL(T1DAY_MTM,0)),  
    T1DAY_CASHCOLL=SUM(ISNULL(T1DAY_CASHCOLL,0)),  
    T1DAY_FDBG=SUM(ISNULL(T1DAY_FDBG,0)),  
    T1DAY_CASH=SUM(ISNULL(T1DAY_LEDGER,0))+SUM(ISNULL(T1DAY_CASHCOLL,0)),  
    T1DAY_NONCASH=SUM(ISNULL(T1DAY_NONCASH,0))+SUM(ISNULL(T1DAY_FDBG,0)),  
    T1DAY_MARGIN_SHORT = 0,   
       T1DAY_MTM_SHORT = 0,  
    T2DAY_LEDGER=SUM(ISNULL(T2DAY_LEDGER,0)),  
    T2DAY_MARGIN=SUM(ISNULL(T2DAY_MARGIN,0)),  
    T2DAY_MTM=SUM(ISNULL(T2DAY_MTM,0)),  
    T2DAY_CASHCOLL=SUM(ISNULL(T2DAY_CASHCOLL,0)),  
    T2DAY_FDBG=SUM(ISNULL(T2DAY_FDBG,0)),  
    T2DAY_CASH=SUM(ISNULL(T2DAY_LEDGER,0))+SUM(ISNULL(T2DAY_CASHCOLL,0)),  
    T2DAY_NONCASH =SUM(ISNULL(T2DAY_NONCASH,0))+SUM(ISNULL(T2DAY_FDBG,0)),  
    T2DAY_MARGIN_SHORT = 0,   
       T2DAY_MTM_SHORT = 0,
    FAMILY_AMOUNT = '',
    FAMILY_CODE = '',
    FAMILY 
FROM #COMBINE_REPORTING_DETAIL  
WHERE MARGINDATE = @MARGINDATE  
GROUP BY MARGINDATE, (CASE WHEN @COMBINEFLAG = 1 THEN '' ELSE EXCHANGE END), 
		             (CASE WHEN @COMBINEFLAG = 1 THEN '' ELSE SEGMENT END), PARTY_CODE, FAMILY 
--HAVING SUM(TDAY_MARGIN+TDAY_MTM) > 0   

UPDATE TBL_COMBINE_REPORTING SET TDAY_MARGIN = TDAY_MARGIN - ISNULL(TDAY_MARGIN_CASH,0), 
TDAY_MTM = TDAY_MTM - ISNULL(TDAY_MTM_CASH,0)
FROM (SELECT CL_CODE=PARTY_CODE, TDAY_MARGIN_CASH=SUM(TDAY_MARGIN), TDAY_MTM_CASH = SUM(TDAY_MTM) 
FROM #COMBINE_REPORTING_DETAIL 
WHERE MARGINDATE  = @MARGINDATE
AND SEGMENT='CAPITAL'-- AND PARTY_CODE ='K90758'
GROUP BY PARTY_CODE) D
WHERE TBL_COMBINE_REPORTING.MARGINDATE  = @MARGINDATE
AND TBL_COMBINE_REPORTING.PARTY_CODE = D.CL_CODE

INSERT INTO TBL_COMBINE_REPORTING_DETAIL  
SELECT * FROM #COMBINE_REPORTING_DETAIL  
WHERE EXISTS (SELECT PARTY_CODE FROM TBL_COMBINE_REPORTING  
     WHERE TBL_COMBINE_REPORTING.MARGINDATE = @MARGINDATE  
     AND TBL_COMBINE_REPORTING.PARTY_CODE = #COMBINE_REPORTING_DETAIL.PARTY_CODE) 

UPDATE TBL_COMBINE_REPORTING   
SET   
TDAY_MARGIN_SHORT = TDAY_CASH + TDAY_NONCASH - TDAY_MARGIN,  
T1DAY_MARGIN_SHORT = T1DAY_CASH + T1DAY_NONCASH - T1DAY_MARGIN,  
T2DAY_MARGIN_SHORT = T2DAY_CASH + T2DAY_NONCASH - T2DAY_MARGIN  
WHERE MARGINDATE = @MARGINDATE   

