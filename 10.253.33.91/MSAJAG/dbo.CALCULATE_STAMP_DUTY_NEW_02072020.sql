CREATE PROC [dbo].[CALCULATE_STAMP_DUTY_NEW]
(                    
	@SETT_TYPE VARCHAR(2),              
	@SAUDA_DATE VARCHAR(11),              
	@FROMPARTY VARCHAR(10),                     
	@TOPARTY VARCHAR(10)                    
)
AS     
-- EXEC CALCULATE_STAMP_DUTY_NEW 'N', 'JAN  8 2020', '0','ZZZ'

SET NOCOUNT ON
DECLARE @CLEARINGCODE	VARCHAR(10),
		@BROK_SQ_OFF	INT,
		@STAMP_SQ_OFF	INT,
		@SETTCUR		CURSOR,
		@PARTY_CODE		VARCHAR(10),
		@FLAG			INT,
		@SNO			NUMERIC(18,0),
		@TOTALSTAMP		NUMERIC(18,4),
		@DIFF			NUMERIC(18,4),
		@CONTRACTNO		VARCHAR(11)

DECLARE @SETT_NO VARCHAR(7),
		@STAMPCUR CURSOR,
		@DIFFCUR	CURSOR      

SET @BROK_SQ_OFF = 0
SET @STAMP_SQ_OFF = 0 

SELECT @CLEARINGCODE = ISNULL(CLEARINGCODE,''), 
	   @BROK_SQ_OFF = ISNULL(BROK_SQ_OFF,0), 
	   @STAMP_SQ_OFF = ISNULL(STAMP_SQ_OFF,0) 
FROM TBL_INTEROP_SETTING
WHERE  @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE

SELECT DISTINCT PARTY_CODE, L_STATE INTO #TBL_STAMP_DATA  
FROM TBL_STAMP_DATA
WHERE SAUDA_DATE = @SAUDA_DATE

IF @SAUDA_DATE = LEFT(GETDATE(),11) 
BEGIN
	TRUNCATE TABLE #TBL_STAMP_DATA
END

	SET @SETTCUR = CURSOR FOR 
	SELECT DISTINCT SETT_NO FROM SETT_MST WHERE SETT_TYPE = @SETT_TYPE              
	AND @SAUDA_DATE BETWEEN START_DATE AND END_DATE  
	ORDER BY 1
	OPEN @SETTCUR
	FETCH NEXT FROM @SETTCUR INTO @SETT_NO
	WHILE @@FETCH_STATUS = 0 
	BEGIN
	---	EXEC PROC_SETTFLAG_START @SETT_NO, @SETT_TYPE, @SAUDA_DATE, @FROMPARTY, @TOPARTY

	---	IF @BROK_SQ_OFF = 0 AND @STAMP_SQ_OFF = 1
	--	BEGIN
	--		EXEC PROC_SETTFLAG_SET
	--	END
		
		UPDATE COMBINE_SETTLEMENT SET BROKER_CHRG = 0 
		WHERE SETT_NO = @SETT_NO
		AND SETT_TYPE = @SETT_TYPE
		AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY

		UPDATE ISETTLEMENT SET BROKER_CHRG = 0 
		WHERE SETT_NO = @SETT_NO
		AND SETT_TYPE = @SETT_TYPE
		AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY

		SELECT SETT_NO, SETT_TYPE, CONTRACTNO = CONVERT(VARCHAR(11),''), TRADE_TYPE = 'S', 
			   SAUDA_DATE=CONVERT(DATETIME,LEFT(SAUDA_DATE,11)),PARTY_CODE, SCRIP_CD, SERIES, 
			   BUYSQQTY=SUM(CASE WHEN SETTFLAG = 2 AND SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
			   SELLSQQTY=SUM(CASE WHEN SETTFLAG = 3 AND SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
			   BUYDELQTY=SUM(CASE WHEN SETTFLAG <> 2 AND SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
			   SELLDELQTY=SUM(CASE WHEN SETTFLAG <> 3 AND SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
			   BUYAVGRATE = (CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END) > 0 THEN
								(CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END) > 0 
									  THEN CONVERT(NUMERIC(18,2),(SUM(CASE WHEN SELL_BUY = 1 THEN MARKETRATE * TRADEQTY ELSE 0 END)
									       / SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END))) ELSE 0 END) ELSE 0 END),
			   SELLAVGRATE = (CASE WHEN SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END) > 0 THEN
								(CASE WHEN SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END) > 0 
									  THEN CONVERT(NUMERIC(18,2),(SUM(CASE WHEN SELL_BUY = 2 THEN MARKETRATE * TRADEQTY ELSE 0 END)
									       / SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END))) ELSE 0 END) ELSE 0 END),
			   BUYSQAMT = CONVERT(NUMERIC(18,2),SUM(CASE WHEN SETTFLAG = 2 AND SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END)),
			   BUYDELAMT = CONVERT(NUMERIC(18,2),SUM(CASE WHEN SETTFLAG <> 2 AND SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END)),
			   SELLSQAMT = CONVERT(NUMERIC(18,2),SUM(CASE WHEN SETTFLAG = 3 AND SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)),
			   SELLDELAMT = CONVERT(NUMERIC(18,2),SUM(CASE WHEN SETTFLAG <> 3 AND SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)),
			   BUYSQSTAMP = CONVERT(NUMERIC(18,2),0),
			   SELLSQSTAMP = CONVERT(NUMERIC(18,2),0),
			   BUYDELSTAMP = CONVERT(NUMERIC(18,2),0),
			   SELLDELSTAMP = CONVERT(NUMERIC(18,2),0),
			   TOTALSTAMP =  CONVERT(NUMERIC(18,2),0),
			   SRNO = 0, TRADEQTY = MAX(TRADEQTY),
			   L_STATE = CONVERT(VARCHAR(100),''),
			   SELL_BUY, SETTFLAG
			   INTO #STAMP_DATA
			   FROM COMBINE_SETTLEMENT
			   WHERE SETT_NO = @SETT_NO
			   AND SETT_TYPE = @SETT_TYPE
			   AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			   AND TRADEQTY > 0 
			   AND MARKETRATE > 0 
			   AND AUCTIONPART NOT LIKE 'A%'
			   AND AUCTIONPART NOT IN ('FP','FA','FL','FC')
		GROUP BY SETT_NO, SETT_TYPE, CONVERT(DATETIME,LEFT(SAUDA_DATE,11)), PARTY_CODE, SCRIP_CD, SERIES, SELL_BUY, SETTFLAG
	
		INSERT INTO #STAMP_DATA
		SELECT SETT_NO, SETT_TYPE, CONTRACTNO, TRADE_TYPE = 'I', 
			   SAUDA_DATE=CONVERT(DATETIME,LEFT(SAUDA_DATE,11)),PARTY_CODE, SCRIP_CD, SERIES, 
			   BUYSQQTY=0,
			   SELLSQQTY=0,
			   BUYDELQTY=SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
			   SELLDELQTY=SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
			   BUYAVGRATE = (CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END) > 0 THEN
								(CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END) > 0 
									  THEN CONVERT(NUMERIC(18,2),(SUM(CASE WHEN SELL_BUY = 1 THEN MARKETRATE * TRADEQTY ELSE 0 END)
									       / SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END))) ELSE 0 END) ELSE 0 END),
			   SELLAVGRATE = (CASE WHEN SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END) > 0 THEN
								(CASE WHEN SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END) > 0 
									  THEN CONVERT(NUMERIC(18,2),(SUM(CASE WHEN SELL_BUY = 2 THEN MARKETRATE * TRADEQTY ELSE 0 END)
									       / SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END))) ELSE 0 END) ELSE 0 END),
			   BUYSQAMT = CONVERT(NUMERIC(18,2),0),
			   BUYDELAMT = CONVERT(NUMERIC(18,2),SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END)),
			   SELLSQAMT = CONVERT(NUMERIC(18,2),0),
			   SELLDELAMT = CONVERT(NUMERIC(18,2),SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)),
			   BUYSQSTAMP = CONVERT(NUMERIC(18,2),0),
			   SELLSQSTAMP = CONVERT(NUMERIC(18,2),0),
			   BUYDELSTAMP = CONVERT(NUMERIC(18,2),0),
			   SELLDELSTAMP = CONVERT(NUMERIC(18,2),0),
			   TOTALSTAMP =  CONVERT(NUMERIC(18,2),0),
			   SRNO = 0, TRADEQTY = MAX(TRADEQTY),
			   L_STATE = CONVERT(VARCHAR(100),''),
			   SELL_BUY, SETTFLAG
			   FROM ISETTLEMENT
			   WHERE SETT_NO = @SETT_NO
			   AND SETT_TYPE = @SETT_TYPE
			   AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			   AND TRADEQTY > 0 
			   AND MARKETRATE > 0 
			   AND AUCTIONPART NOT LIKE 'A%'
			   AND AUCTIONPART NOT LIKE 'F%' 
		GROUP BY SETT_NO, SETT_TYPE, CONTRACTNO, CONVERT(DATETIME,LEFT(SAUDA_DATE,11)), PARTY_CODE, 
		SCRIP_CD, SERIES, SELL_BUY, SETTFLAG

		ALTER TABLE #STAMP_DATA
		ADD SNO INT IDENTITY(1,1)

		UPDATE #STAMP_DATA SET L_STATE = C.L_STATE
		FROM #TBL_STAMP_DATA C
		WHERE C.PARTY_CODE = #STAMP_DATA.PARTY_CODE

		UPDATE #STAMP_DATA SET L_STATE = C1.L_STATE
		FROM CLIENT1 C1
		WHERE C1.CL_CODE = #STAMP_DATA.PARTY_CODE
		AND #STAMP_DATA.L_STATE = ''

		UPDATE #STAMP_DATA SET SRNO = C.SRNO
		FROM COMBINE_SETTLEMENT C
		WHERE 
		#STAMP_DATA.SETT_NO = C.SETT_NO
		AND #STAMP_DATA.SETT_TYPE = C.SETT_TYPE
		AND #STAMP_DATA.PARTY_CODE = C.PARTY_CODE 
		AND #STAMP_DATA.SCRIP_CD = C.SCRIP_CD
		AND #STAMP_DATA.SERIES = C.SERIES  
		AND #STAMP_DATA.SELL_BUY = C.SELL_BUY  
		AND #STAMP_DATA.SETTFLAG = C.SETTFLAG 
		AND #STAMP_DATA.TRADEQTY = C.TRADEQTY
		AND C.TRADEQTY > 0 
		AND MARKETRATE > 0 
		AND AUCTIONPART NOT LIKE 'A%'
		AND AUCTIONPART NOT LIKE 'F%' 
		AND TRADE_TYPE = 'S'

		UPDATE #STAMP_DATA SET SRNO = C.SRNO
		FROM ISETTLEMENT C
		WHERE 
		#STAMP_DATA.SETT_NO = C.SETT_NO
		AND #STAMP_DATA.SETT_TYPE = C.SETT_TYPE
		AND #STAMP_DATA.PARTY_CODE = C.PARTY_CODE 
		AND #STAMP_DATA.SCRIP_CD = C.SCRIP_CD
		AND #STAMP_DATA.SERIES = C.SERIES  
		AND #STAMP_DATA.SELL_BUY = C.SELL_BUY  
		AND #STAMP_DATA.SETTFLAG = C.SETTFLAG 
		AND #STAMP_DATA.CONTRACTNO = C.CONTRACTNO 
		AND #STAMP_DATA.TRADEQTY = C.TRADEQTY
		AND C.TRADEQTY > 0 
		AND MARKETRATE > 0 
		AND AUCTIONPART NOT LIKE 'A%'
		AND AUCTIONPART NOT LIKE 'F%' 
		AND TRADE_TYPE = 'I'

		UPDATE #STAMP_DATA SET 
		BUYSQSTAMP = (BUYSQAMT * BUYSQTAX) / 100,
		SELLSQSTAMP = (SELLSQAMT * SELLSQTAX) / 100,
		BUYDELSTAMP = (BUYDELAMT * BUYDELTAX) / 100,
		SELLDELSTAMP = (SELLDELAMT * SELLDELTAX) / 100
		FROM TBL_STAMP_TAX
		WHERE TBL_STAMP_TAX.SETT_TYPE = @SETT_TYPE
		AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE
		
		UPDATE #STAMP_DATA SET 
		BUYSQSTAMP = CONVERT(NUMERIC(18,2),BUYSQSTAMP),
		SELLSQSTAMP = CONVERT(NUMERIC(18,2),SELLSQSTAMP),
		BUYDELSTAMP = CONVERT(NUMERIC(18,2),BUYDELSTAMP),
		SELLDELSTAMP = CONVERT(NUMERIC(18,2),SELLDELSTAMP)
		
		UPDATE #STAMP_DATA SET TOTALSTAMP = BUYSQSTAMP + SELLSQSTAMP + BUYDELSTAMP + SELLDELSTAMP 

		DELETE FROM TBL_STAMP_DATA
		WHERE SAUDA_DATE = @SAUDA_DATE
		AND SETT_NO  IN (SELECT DISTINCT ORG_SETT_NO  FROM COMBINE_SETTLEMENT WHERE ORG_SETT_TYPE = TBL_STAMP_DATA.SETT_TYPE
		AND ORG_SETT_NO = TBL_STAMP_DATA.SETT_NO)
		AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
		
		INSERT INTO TBL_STAMP_DATA
		SELECT SETT_NO,SETT_TYPE,CONTRACTNO,TRADE_TYPE,SAUDA_DATE,PARTY_CODE,SCRIP_CD,SERIES,
			   BUYSQQTY=SUM(BUYSQQTY),SELLSQQTY=SUM(SELLSQQTY),BUYDELQTY=SUM(BUYDELQTY),SELLDELQTY=SUM(SELLDELQTY),
			   BUYAVGRATE=SUM(BUYAVGRATE),SELLAVGRATE=SUM(SELLAVGRATE),
			   BUYSQSTAMP=SUM(BUYSQSTAMP),SELLSQSTAMP=SUM(SELLSQSTAMP),BUYDELSTAMP=SUM(BUYDELSTAMP),SELLDELSTAMP=SUM(SELLDELSTAMP),			   
			   TOTALSTAMP = CONVERT(NUMERIC(18,0),SUM(TOTALSTAMP)),
			   L_STATE,LASTRUNDATE=GETDATE() 
		FROM #STAMP_DATA 
		WHERE TRADE_TYPE = 'I'
		GROUP BY SETT_NO,SETT_TYPE,CONTRACTNO,TRADE_TYPE,SAUDA_DATE,PARTY_CODE,SCRIP_CD,SERIES,L_STATE
		 
		SELECT 
		PARTY_CODE, CONTRACTNO, 
		BROKER_CHRG = SUM(TOTALSTAMP), 
		ACT_BROKER_CHRG = CONVERT(NUMERIC(18,0),SUM(TOTALSTAMP)), 
		TRADE_TYPE
		INTO #DIFF
		FROM #STAMP_DATA
		GROUP BY PARTY_CODE, CONTRACTNO, TRADE_TYPE
		HAVING SUM(TOTALSTAMP) <> CONVERT(NUMERIC(18,0),SUM(TOTALSTAMP))
		
		SET @STAMPCUR = CURSOR FOR 
			SELECT PARTY_CODE, CONTRACTNO, DIFF = ACT_BROKER_CHRG - BROKER_CHRG, 
			FLAG = (CASE WHEN ACT_BROKER_CHRG - BROKER_CHRG > 0 THEN 0 ELSE 1 END)
			FROM #DIFF
			WHERE BROKER_CHRG <> ACT_BROKER_CHRG
		OPEN @STAMPCUR
		FETCH NEXT FROM @STAMPCUR INTO @PARTY_CODE, @CONTRACTNO, @DIFF, @FLAG
		WHILE @@FETCH_STATUS = 0 
		BEGIN
			SET @DIFFCUR = CURSOR FOR
				SELECT SNO, TOTALSTAMP
				FROM #STAMP_DATA
				WHERE PARTY_CODE = @PARTY_CODE
				AND CONTRACTNO = (CASE WHEN @CONTRACTNO = '' THEN CONTRACTNO ELSE @CONTRACTNO END)
				ORDER BY TOTALSTAMP DESC
			OPEN @DIFFCUR
			FETCH NEXT FROM @DIFFCUR INTO @SNO, @TOTALSTAMP
			WHILE @@FETCH_STATUS = 0 
			BEGIN
				IF @TOTALSTAMP > ABS(@DIFF) 
				BEGIN
					UPDATE #STAMP_DATA SET TOTALSTAMP = TOTALSTAMP + @DIFF
					WHERE SNO = @SNO
					SET @DIFF = 0 
				END
				ELSE
				BEGIN
					IF @FLAG = 0
					BEGIN
						UPDATE #STAMP_DATA SET TOTALSTAMP = TOTALSTAMP + @DIFF
						WHERE SNO = @SNO
						SET @DIFF = 0 
					END 
					ELSE
					BEGIN
						UPDATE #STAMP_DATA SET TOTALSTAMP = 0
						WHERE SNO = @SNO
						SET @DIFF = @DIFF + @TOTALSTAMP
					END
				END
				FETCH NEXT FROM @DIFFCUR INTO @SNO, @TOTALSTAMP
			END
			CLOSE @DIFFCUR
			DEALLOCATE @DIFFCUR
		
			FETCH NEXT FROM @STAMPCUR INTO @PARTY_CODE, @CONTRACTNO, @DIFF, @FLAG
		END
		CLOSE @STAMPCUR
		DEALLOCATE @STAMPCUR 
		 
		UPDATE COMBINE_SETTLEMENT SET BROKER_CHRG = TOTALSTAMP
		FROM  #STAMP_DATA
		WHERE #STAMP_DATA.SRNO = COMBINE_SETTLEMENT.SRNO
		AND TRADE_TYPE = 'S'
				 
		SELECT ORG_SETT_NO, ORG_SETT_TYPE, ORG_CONTRACTNO, TRADE_TYPE = 'S', 
			   SAUDA_DATE=CONVERT(DATETIME,LEFT(SAUDA_DATE,11)),PARTY_CODE, ORG_SCRIP_CD, ORG_SERIES, 
			   BUYSQQTY=SUM(CASE WHEN SETTFLAG = 2 AND SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
			   SELLSQQTY=SUM(CASE WHEN SETTFLAG = 3 AND SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
			   BUYDELQTY=SUM(CASE WHEN SETTFLAG <> 2 AND SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
			   SELLDELQTY=SUM(CASE WHEN SETTFLAG <> 3 AND SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
			   BUYAVGRATE = (CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END) > 0 THEN
								(CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END) > 0 
									  THEN CONVERT(NUMERIC(18,2),(SUM(CASE WHEN SELL_BUY = 1 THEN MARKETRATE * TRADEQTY ELSE 0 END)
									       / SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END))) ELSE 0 END) ELSE 0 END),
			   SELLAVGRATE = (CASE WHEN SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END) > 0 THEN
								(CASE WHEN SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END) > 0 
									  THEN CONVERT(NUMERIC(18,2),(SUM(CASE WHEN SELL_BUY = 2 THEN MARKETRATE * TRADEQTY ELSE 0 END)
									       / SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END))) ELSE 0 END) ELSE 0 END),
			   BUYSQSTAMP = SUM(CASE WHEN SETTFLAG = 2 AND SELL_BUY = 1 THEN BROKER_CHRG ELSE 0 END),
			   SELLSQSTAMP = SUM(CASE WHEN SETTFLAG = 3 AND SELL_BUY = 2 THEN BROKER_CHRG ELSE 0 END),
			   BUYDELSTAMP = SUM(CASE WHEN SETTFLAG <> 2 AND SELL_BUY = 1 THEN BROKER_CHRG ELSE 0 END),
			   SELLDELSTAMP = SUM(CASE WHEN SETTFLAG <> 3 AND SELL_BUY = 2 THEN BROKER_CHRG ELSE 0 END),
			   TOTALSTAMP =  SUM(BROKER_CHRG),
			   L_STATE = CONVERT(VARCHAR(100),''),LASTRUNDATE=GETDATE() 
			   INTO #STAMP_DATA_NEW
			   FROM COMBINE_SETTLEMENT
			   WHERE SETT_NO = @SETT_NO
			   AND SETT_TYPE = @SETT_TYPE
			   AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			   AND TRADEQTY > 0 
			   AND MARKETRATE > 0 
			   AND AUCTIONPART NOT LIKE 'A%'
			   AND AUCTIONPART NOT IN ('FP','FA','FL','FC')
		GROUP BY ORG_SETT_NO, ORG_SETT_TYPE, ORG_CONTRACTNO, CONVERT(DATETIME,LEFT(SAUDA_DATE,11)), PARTY_CODE, ORG_SCRIP_CD, ORG_SERIES

		UPDATE #STAMP_DATA_NEW SET L_STATE = C.L_STATE
		FROM #TBL_STAMP_DATA C
		WHERE C.PARTY_CODE = #STAMP_DATA_NEW.PARTY_CODE

		UPDATE #STAMP_DATA_NEW SET L_STATE = C1.L_STATE
		FROM CLIENT1 C1
		WHERE C1.CL_CODE = #STAMP_DATA_NEW.PARTY_CODE
		AND #STAMP_DATA_NEW.L_STATE = ''

		INSERT INTO TBL_STAMP_DATA
		SELECT * FROM #STAMP_DATA_NEW
		
		UPDATE #STAMP_DATA SET BUYSQSTAMP = 0, SELLSQSTAMP = 0, BUYDELSTAMP = 0, SELLDELSTAMP = 0, TOTALSTAMP = 0 
		FROM CLIENTTAXES_NEW
		WHERE @SAUDA_DATE BETWEEN Fromdate AND Todate
		AND CLIENTTAXES_NEW.PARTY_CODE = #STAMP_DATA.PARTY_CODE
		AND Broker_Note = 0 AND Trans_Cat = 'TRD'

		UPDATE #STAMP_DATA SET BUYSQSTAMP = 0, SELLSQSTAMP = 0, BUYDELSTAMP = 0, SELLDELSTAMP = 0, TOTALSTAMP = 0 
		FROM CLIENTTAXES_NEW
		WHERE @SAUDA_DATE BETWEEN Fromdate AND Todate
		AND CLIENTTAXES_NEW.PARTY_CODE = #STAMP_DATA.PARTY_CODE
		AND Broker_Note = 0 AND Trans_Cat = 'DEL'

		UPDATE #STAMP_DATA SET BUYSQSTAMP = 0, SELLSQSTAMP = 0, BUYDELSTAMP = 0, SELLDELSTAMP = 0, TOTALSTAMP = 0 
		FROM CLIENT2
		WHERE CLIENT2.PARTY_CODE = #STAMP_DATA.PARTY_CODE
		AND BrokerNOTE = 0  
		
		UPDATE COMBINE_SETTLEMENT SET BROKER_CHRG = TOTALSTAMP
		FROM  #STAMP_DATA
		WHERE #STAMP_DATA.SRNO = COMBINE_SETTLEMENT.SRNO
		AND TRADE_TYPE = 'S'
				
		UPDATE ISETTLEMENT SET BROKER_CHRG = BUYSQSTAMP + SELLSQSTAMP + BUYDELSTAMP + SELLDELSTAMP
		FROM  #STAMP_DATA
		WHERE #STAMP_DATA.SRNO = ISETTLEMENT.SRNO
		AND TRADE_TYPE = 'I'
		 
	--	EXEC PROC_SETTFLAG_END @SETT_NO, @SETT_TYPE, @SAUDA_DATE, @FROMPARTY, @TOPARTY

		FETCH NEXT FROM @SETTCUR INTO @SETT_NO
	END
	CLOSE @SETTCUR
	DEALLOCATE @SETTCUR

GO
