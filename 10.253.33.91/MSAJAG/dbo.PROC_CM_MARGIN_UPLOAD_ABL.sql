CREATE PROC [dbo].[PROC_CM_MARGIN_UPLOAD_ABL] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @RECCNT	INT

TRUNCATE TABLE TDAY_MARGIN_DATA_CM

INSERT INTO TDAY_MARGIN_DATA_CM
SELECT CONVERT(DATETIME,@TDATE,103),
DBO.PIECE(FILE_DATA,',',1) CM_CODE,
DBO.PIECE(FILE_DATA,',',2) CM_NAME,
DBO.PIECE(FILE_DATA,',',3) TM_CP_CODE,
DBO.PIECE(FILE_DATA,',',4) TM_CP_NAME,
DBO.PIECE(FILE_DATA,',',5) CLIENT_CODE,
DBO.PIECE(FILE_DATA,',',6)  CASH,
DBO.PIECE(FILE_DATA,',',7)  NON_CASH,
DBO.PIECE(FILE_DATA,',',8)  MTM_LOSS,
DBO.PIECE(FILE_DATA,',',9)  TRADE_MARGINS ,
DBO.PIECE(FILE_DATA,',',10)  ICMTM,
DBO.PIECE(FILE_DATA,',',11)  TOTAL_MRG,
DBO.PIECE(FILE_DATA,',',12)  CLT_MRG_EXCESS_90,
DBO.PIECE(FILE_DATA,',',13)  CLT_NON_CASH_EXCESS_90,
DBO.PIECE(FILE_DATA,',',14)  ELIGIBLE_NON_CASH 
FROM FILEDATA WHERE PROGID = @PROGID AND DBO.PIECE(FILE_DATA,',',5) NOT IN ('','Client Code')

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=10 AND REPORT_DATE = CONVERT(DATETIME,@TDATE,103)

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA WHERE PROGID = @PROGID

DELETE FROM FILEDATA WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'CM MARGIN UPLOADED WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
