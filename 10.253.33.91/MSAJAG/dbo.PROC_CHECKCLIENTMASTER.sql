--------------------------------------------------

CREATE PROC PROC_CHECKCLIENTMASTER @PARTY_CODE VARCHAR(11)  
AS  
  
DECLARE @RETURN_VALUE INT  
SET @RETURN_VALUE = 0  
  
IF (SELECT COUNT(1) FROM (SELECT PARTY_CODE FROM CLIENTMASTER (NOLOCK) WHERE PARTY_CODE = @PARTY_CODE) CLMST ) >0  
BEGIN  
 SET @RETURN_VALUE = 1  
END  
  
SELECT @RETURN_VALUE

GO
