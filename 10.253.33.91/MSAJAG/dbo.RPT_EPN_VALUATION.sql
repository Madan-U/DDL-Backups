CREATE PROCEDURE RPT_EPN_VALUATION (@MARGINDATE VARCHAR(11))

AS ---- EXEC RPT_EPN_VALUATION 'MAR 30 2022'

DECLARE @PrevDate Varchar(11),@EPNDATE Varchar(11)
		
SELECT @EPNDATE = ISNULL(MAX(Start_Date),'APR  1 2020') FROM SETT_MST
WHERE Start_Date <= @MARGINDATE AND Sett_Type = 'N'

SELECT @PREVDATE = ISNULL(MAX(Start_Date),'APR  1 2020') FROM SETT_MST
WHERE Start_Date < @EPNDATE AND Sett_Type = 'N'

TRUNCATE TABLE EPN_VALUATION

INSERT INTO EPN_VALUATION
SELECT EPN_DATE=CONVERT(DATETIME,@MARGINDATE),SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SELL_AMOUNT=SUM(SELLAMOUNT),
EPN_QTY=SUM(EPNQTY_CC),EPN_AMOUNT = SUM(EPIACCOUNT)
FROM
(
SELECT SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SELLAMOUNT=SUM(SELLAMOUNT),EPNQTY_CC=SUM(EPNQTY_CC),EPIACCOUNT = SUM(CASHBENEFIT)
FROM TBL_EPN_BENEFIT
WHERE TRANSDATE = @EPNDATE AND CASHBENEFIT > 0 GROUP BY TRANSDATE,PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE
UNION ALL
SELECT SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SELLAMOUNT=SUM(SELLAMOUNT),EPNQTY_CC=SUM(EPNQTY_CC),EPIACCOUNT=-SUM(CASHBENEFIT)*20/100 
FROM (
SELECT SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SELLAMOUNT=SUM(SELLAMOUNT),EPNQTY_CC=SUM(EPNQTY_CC),CASHBENEFIT=-SUM(CASHBENEFIT) FROM TBL_EPN_BENEFIT T 
WHERE TRANSDATE = @PREVDATE
AND SETT_NO IN (SELECT SETT_NO FROM TBL_EPN_BENEFIT T1
WHERE TRANSDATE = @EPNDATE AND T.SETT_NO = T1.SETT_NO AND T.SETT_TYPE = T1.SETT_TYPE)
AND SETT_NO NOT IN (SELECT SETT_NO FROM SETT_MST S 
WHERE SEC_PAYIN = @EPNDATE AND T.SETT_NO = S.SETT_NO AND T.SETT_TYPE = S.SETT_TYPE) GROUP BY PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE
UNION ALL
SELECT SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SELLAMOUNT=SUM(SELLAMOUNT),EPNQTY_CC=SUM(EPNQTY_CC),CASHBENEFIT=SUM(CASHBENEFIT) FROM TBL_EPN_BENEFIT T WHERE TRANSDATE = @EPNDATE 
GROUP BY PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE ) A 
GROUP BY PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE	
HAVING SUM(CASHBENEFIT) > 0 ) A
GROUP BY  PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE

declare @DIRCOLLNAMEAM VARCHAR(100) = 
'D:\BACKOFFICE\EXPORT\' +'EPN_VALUATION_' + REPLACE(CONVERT(VARCHAR(11),@MARGINDATE,105),'-','') + '.CSV'

DECLARE @EDTFILENAME1 VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'EPN_VALUATION_' + CONVERT(VARCHAR, CONVERT(DATETIME,@MARGINDATE), 112) + '.CSV'
DECLARE @EDT1 VARCHAR(MAX)                            
SET @EDT1 = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT DATE'''',''''SETT_NO'''',''''SETT_TYPE'''',''''PARTY_CODE'''',''''SCRIP_CD'''',''''SELL_AMOUNT'''',''''EPN_QTY'''',''''EPN_AMOUNT''''' 
SET @EDT1 = @EDT1 + ' UNION ALL SELECT CONVERT(VARCHAR(11),EPN_DATE,105),SETT_NO,SETT_TYPE,LTRIM(RTRIM(PARTY_CODE)),SCRIP_CD,CONVERT(VARCHAR,SELL_AMOUNT),CONVERT(VARCHAR,EPN_QTY),CONVERT(VARCHAR,EPN_AMOUNT) FROM MSAJAG.DBO.EPN_VALUATION" QUERYOUT ' +@EDTFILENAME1+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'    
EXEC(@EDT1)

SELECT 'EPN Valuation data Exported to ' + 'D:\BACKOFFICE\EXPORT\' +'EPN_VALUATION_' + REPLACE(CONVERT(VARCHAR(11),@MARGINDATE,105),'-','') + '.CSV'

GO
