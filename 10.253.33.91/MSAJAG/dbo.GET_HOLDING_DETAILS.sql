CREATE  PROCEDURE [dbo].[GET_HOLDING_DETAILS]
(
 @FDATE VARCHAR(11),
 @TDATE VARCHAR(11),
 @FPARTY VARCHAR(10),
 @TPARTY VARCHAR(10),
 @EXCHANGE VARCHAR(10),
 @FOR VARCHAR(1),
 @TYPE VARCHAR(1),
 @RESETFLAG VARCHAR(1)
)
AS
BEGIN 
--EXEC GET_HOLDING_DETAILS @FDATE='23/09/2019',@TDATE='27/12/2019',@FPARTY='',@TPARTY='' ,@EXCHANGE='',@FOR='H',@TYPE='2'
 
 DECLARE @COUNTER INT
     
	 SELECT   
	 @FDATE = CASE WHEN LEN(@FDATE) = 10 AND CHARINDEX('/', @FDATE) > 0          
     THEN CONVERT(VARCHAR(11), CONVERT(DATETIME, @FDATE, 103), 109)          
     ELSE @FDATE END,
	 @TDATE = CASE WHEN LEN(@TDATE) = 10 AND CHARINDEX('/', @TDATE) > 0          
     THEN CONVERT(VARCHAR(11), CONVERT(DATETIME, @TDATE, 103), 109)          
     ELSE @TDATE END   

	 IF LEN(@FPARTY)=0
	 BEGIN
		SET @FPARTY='00' 
	 END

	 IF LEN(@TPARTY)=0
	 BEGIN 
		SET @TPARTY='ZZZZZZZZZZ'
	 END

IF @TYPE='1'
BEGIN
	IF @FOR='H'
	 BEGIN 

		DECLARE
		@P_DATE VARCHAR(11),
		@SEQUENCE VARCHAR(10)

		SET @P_DATE = (SELECT CONVERT(VARCHAR(11),SEQDATE) FROM TBL_HOLDING_FILE_SEQ) 

		IF @P_DATE<>CONVERT(VARCHAR(11),GETDATE())
		BEGIN
			DELETE FROM TBL_HOLDING_FILE_SEQ
			INSERT TBL_HOLDING_FILE_SEQ VALUES(0,CONVERT(VARCHAR(11),GETDATE()))
		END 

		IF(SELECT COUNT(1) FROM TBL_HOLDING_FILE_SEQ)=0
		BEGIN
			INSERT TBL_HOLDING_FILE_SEQ VALUES(0,CONVERT(VARCHAR(11),GETDATE()))
		END

		SET @SEQUENCE = (SELECT	 SEQNO FROM TBL_HOLDING_FILE_SEQ)

		SELECT DISTINCT WEEKDAY=CONVERT(VARCHAR,WEEKDAY,103),BATCH_COUNTER,
		SEQUENCENO = @SEQUENCE
		FROM TBL_API_NSE_HOLDING 
		WHERE ACDID=''
		AND UCC>=@FPARTY
		AND UCC<=@TPARTY
		AND LASTWEEKDAY LIKE  @FDATE + '%'
		AND ACDID=''
		ORDER BY WEEKDAY
	 END 
END
IF @TYPE='2'
BEGIN
 IF @FOR='H'
	 BEGIN
	  
		SELECT * INTO #HOLDINGDATA FROM TBL_API_NSE_HOLDING 
		WHERE ACDID='' 
		AND WEEKDAY BETWEEN @FDATE AND @TDATE  

		CREATE INDEX IND_HOLDINGDATA ON #HOLDINGDATA(WEEKDAY)  

		SELECT  LCOUNTER=1,
		MEMBERPAN AS MEMBERPAN, 
		LASTWEEKDAY = CONVERT(VARCHAR,LASTWEEKDAY,105), 
		WEEKDAY = CONVERT(VARCHAR,WEEKDAY,105),
		VERSIONNO = '1.0',
		UID AS RECID,
		DMAT AS DMAT,
		ACCOUNTTYPE AS ACCOUNTTYPE,
		UCC AS UCC,
		CLIENTNAME AS CLIENTNAME,
		PAN AS PAN,
		ISIN AS ISIN,
		SECURITYTYPE AS SECURITYTYPE ,					
		TOTALPLDGQTY AS TOTALPLDGQTY,
		FREEBALQTY AS FREEBALQTY,
		TOTALQTY AS TOTALQTY,
		ACTION='A',
		UNITTYPE,
		NAMEOFSECORCOMMODITY
		FROM #HOLDINGDATA  		
		ORDER BY WEEKDAY,UCC ASC 

		DROP TABLE #HOLDINGDATA
	END
	 
END
END

GO
