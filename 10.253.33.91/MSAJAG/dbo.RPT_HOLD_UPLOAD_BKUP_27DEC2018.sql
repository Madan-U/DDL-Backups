Create	PROC [dbo].[RPT_HOLD_UPLOAD_BKUP_27DEC2018]
		(
		@FROMDATE	VARCHAR(11) 
		)

		AS

		/*
		EXEC	RPT_HOLD_UPLOAD 'DEC 17 2018'
		*/
		
		/*
		IF (SELECT CASE WHEN @FROMDATE = LEFT(GETDATE(),11) THEN 1 ELSE 0 END) = 0 
		BEGIN
			SELECT * FROM OWNER WHERE 1 = 2 
			RETURN
		END
		*/

		DECLARE @FILENAME   VARCHAR(50)
		DECLARE @BATCHNO	INT  
		DECLARE @MEMBERCODE VARCHAR(20)
		DECLARE @MEMBERNAME	VARCHAR(500)

		SELECT @MEMBERCODE = MEMBERCODE, @MEMBERNAME = ISNULL(LTRIM(RTRIM(COMPANY)),'') FROM OWNER (NOLOCK)
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
		WHERE FLDFILENO = 'HS'  
		AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)

		IF @BATCHNO = 1  
		BEGIN  
			INSERT INTO ASIANFILEBATCHNO VALUES ('HS', @BATCHNO, GETDATE(), GETDATE())  
		END  
		ELSE  
		BEGIN  
			UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'HS' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		END  
		  
		SET	@FILENAME = @MEMBERCODE + '_HS_' + REPLACE(CONVERT(VARCHAR,GETDATE(),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) 
		
		
		CREATE TABLE #HOLD
		(
		MEMBER_DEMAT_ACC	VARCHAR(50),
		MEMBER_ACC_TYPE		VARCHAR(50),
		UCC_CODE			VARCHAR(20),
		CLIENT_NAME			VARCHAR(500),
		CLIENT_PAN			VARCHAR(50),
		ISIN				VARCHAR(100),
		SCRIP_NAME			VARCHAR(500),
		SECURITY_TYPE		VARCHAR(10),
		PLEDGED_BAL_QTY		NUMERIC(18,3),
		FREE_BAL_QTY		NUMERIC(18,3),
		TOTAL_QTY			NUMERIC(18,3)
		)
		
		CREATE CLUSTERED INDEX [DELHOLD] ON [DBO].[#HOLD] (
			[UCC_CODE] ASC,
			[SCRIP_NAME],
			[ISIN],
			[SECURITY_TYPE]			
			)
		
		INSERT INTO #HOLD
		EXEC ANGELDEMAT.MSAJAG.DBO.RPT_DELHOLDINGASONDATE_UPD 'BROKER', 'BROKER',@FROMDATE, '', '', '', 'zzzzzz', '', 'zzzzzz', 1, 'S'  


		INSERT INTO #HOLD
		EXEC ANGELDEMAT.BSEDB.DBO.RPT_DELHOLDINGASONDATE_UPD 'BROKER', 'BROKER',@FROMDATE, '', '', '', 'zzzzzz', '', 'zzzzzz', 1, 'S'  


		SELECT	FILENAME = @FILENAME,
				[Members Demat Account No.] = MEMBER_DEMAT_ACC,
				[Member Account Type] = MEMBER_ACC_TYPE,
				[Unique Client Code (UCC)] = UCC_CODE,
				[Client Name] = CLIENT_NAME,
				[Client PAN] = CLIENT_PAN,
				[ISIN Code] = ISIN,
				[Scrip Name] = SCRIP_NAME,
				[Security Type] = SECURITY_TYPE,
				[Pledged Balance (Qty.)] = SUM(PLEDGED_BAL_QTY),
				[Free Balance (Qty.)] = SUM(FREE_BAL_QTY),
				[Total (Qty.)] = SUM(TOTAL_QTY)
		FROM	#HOLD				
		GROUP BY
				MEMBER_DEMAT_ACC,
				MEMBER_ACC_TYPE,
				UCC_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				ISIN,
				SCRIP_NAME,
				SECURITY_TYPE
		ORDER BY
				UCC_CODE,
				SCRIP_NAME,
				ISIN
				
		DROP TABLE #HOLD

GO
