CREATE PROCEDURE [dbo].[ADDRESS_DATA_RPT]
(
@DATE VARCHAR(20)
)
AS 
BEGIN

 
 EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'ADDRESS_DETAILS'   
 WAITFOR DELAY '00:0:45';  

SELECT CL_cODE,LONG_NAME,L_ADDRESS1,L_ADDRESS2,L_ADDRESS3,L_CITY,L_STATE,
L_ZIP,L_NATION,BRANCH_CD,SUB_BROKER,REGION FROM CLIENT_DETAILS 
WHERE CL_CODE IN 
(SELECT * FROM SSIS_FOCLIENT)
END

GO
