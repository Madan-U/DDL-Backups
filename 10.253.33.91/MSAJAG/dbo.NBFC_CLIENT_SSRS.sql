--------------------------------------------------



CREATE PROCEDURE [dbo].[NBFC_CLIENT_SSRS]
(
@DATE VARCHAR(20)
)
AS
BEGIN
 EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'NBFC_CLIENT_SSIS'   
 WAITFOR DELAY '00:0:45';  

SELECT CL_CODE,LONG_NAME,DOB,SEX,PAN_GIR_NO,RES_PHONE1,RES_PHONE2,MOBILE_PAGER,EMAIL,LTRIM(RTRIM(l_address1))AS L_ADDRESS1,LTRIM(RTRIM(l_address2))AS L_ADDRESS2,
LTRIM(RTRIM(l_address3))AS L_ADDRESS3
,LTRIM(RTRIM(l_city))AS L_CITY,LTRIM(RTRIM(l_state))AS L_STATE,LTRIM(RTRIM(l_zip))AS L_ZIP,LTRIM(RTRIM(l_nation))AS L_NATION

 FROM  CLIENT_DETAILS WHERE CL_CODE IN
(
select * from SSIS_NBFC_DATA
)


END

GO
