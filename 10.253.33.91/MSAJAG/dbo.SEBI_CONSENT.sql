CREATE Proc  SEBI_CONSENT
(@PARTY_CODE VARCHAR(10),@CONSENT_DATE DATETIME,@IP_ADDRESS VARCHAR(25),@MODE VARCHAR(50),@INACTIVE_DATE DATETIME,@ACTIVE_FLAG VARCHAR(1))
as


IF @ACTIVE_FLAG ='A'

BEGIN 

	INSERT INTO  TBL_SEBI_HOLD_DATA (PARTY_CODE,CONSENT_DATE,IP_ADDRESS,MODE,INACTIVE_DATE,ACTIVE_FLAG,DUMMY1,DUMMY2)
	VALUES (@PARTY_CODE,@CONSENT_DATE,@IP_ADDRESS,@MODE,@INACTIVE_DATE,@ACTIVE_FLAG,'','')

	Select @PARTY_CODE +' Record Added Successfully'

END 

IF @ACTIVE_FLAG ='D'

BEGIN 

	 UPDATE TBL_SEBI_HOLD_DATA SET INACTIVE_DATE =@INACTIVE_DATE,ACTIVE_FLAG=@ACTIVE_FLAG,IP_ADDRESS = IP_ADDRESS + @IP_ADDRESS
	 WHERE PARTY_CODE=@PARTY_CODE AND INACTIVE_DATE > GETDATE()

	 Select @PARTY_CODE +' Record Deactivated Successfully'


END

GO
