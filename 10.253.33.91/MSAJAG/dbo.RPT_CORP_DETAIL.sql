CREATE PROC [dbo].[RPT_CORP_DETAIL]
(  
 @STATUSID VARCHAR(50),  
 @STATUSNAME VARCHAR(50),  
 @FROMDATE VARCHAR(11),  
 @TODATE  VARCHAR(11),  
 @FROMPARTY VARCHAR(10),  
 @TOPARTY VARCHAR(10),  
 @FROMSCRIP VARCHAR(12)='',  
 @TOSCRIP VARCHAR(12)='',  
 @OPT  VARCHAR(20)=''  
)  
  
AS  

SET @TOSCRIP = (CASE WHEN @TOSCRIP = '' THEN 'ZZZZZ' ELSE @TOSCRIP END)

SELECT RECORD_DATE = LEFT(RECORD_DATE,11), EFF_DATE = LEFT(EFF_DATE,11), 
SCRIP_NAME, ISIN, SETT_NO, SETT_TYPE, PARTY_CODE, QTY = SUM(QTY), NEWQTY = SUM(NEWQTY),   
RATE, AMOUNT = SUM(AMOUNT), TYPE_CP_ACTION, RATIO, DPID, CLTDPID = LEFT(CLTDPID,8) + ' ' + RIGHT(CLTDPID,8),   
EXCHANGE, SEGMENT, SERVERNAME, MAKER_ON = LEFT(MAKER_ON,11) + ' ' + CONVERT(VARCHAR, MAKER_ON,108),
RECORD_STATUS = (CASE WHEN PROCESS_STATUS = 1 THEN 'CREATED' 
					  WHEN PROCESS_STATUS < 0 THEN 'REJECTED' 	
					  WHEN PROCESS_STATUS = 2 THEN 'MAKER' 
					  WHEN PROCESS_STATUS = 3 THEN 'CHECKER' 
					  WHEN PROCESS_STATUS = 4 THEN 'PROCESSED' 
					  ELSE 'N/A'
				 END)
FROM ANGELDEMAT.MSAJAG.DBO.TBL_CORPACT_DATA  
WHERE RECORD_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'  
AND SCRIP_NAME >= @FROMSCRIP AND SCRIP_NAME <= @TOSCRIP  
GROUP BY RECORD_DATE, LEFT(EFF_DATE,11), SCRIP_NAME, ISIN, SETT_NO, SETT_TYPE, PARTY_CODE, RATE, TYPE_CP_ACTION,  
RATIO, DPID, CLTDPID, EXCHANGE, SEGMENT, SERVERNAME, MAKER_ON ,PROCESS_STATUS 
ORDER BY SCRIP_NAME, ISIN, PARTY_CODE, SETT_NO, SETT_TYPE, EXCHANGE, SEGMENT

GO
