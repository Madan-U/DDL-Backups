-- Object: PROCEDURE dbo.PROC_BGDETAIL_UPLOAD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[PROC_BGDETAIL_UPLOAD]    
(    
 @TDATE  VARCHAR(10),     
 @FILEFOR VARCHAR(20),     
 @FILETYPE VARCHAR(50),     
 @PROGID  VARCHAR(50),     
 @UNAME  VARCHAR(50),    
 @FILENAME VARCHAR(500) = ''    
)    
AS    

DECLARE @RECCNT NUMERIC(18,0),      
	@CNT NUMERIC(18,0),      
  @FROMDATE DATETIME      

SET @CNT = 1
      
SELECT @CNT=ISNULL(COUNT(1),1)  
FROM FILEDATA      
WHERE PROGID = @PROGID       
AND .DBO.PIECE(FILE_DATA,',',1) <> @TDATE
AND LEN(FILE_DATA) > 0

IF @CNT > 0 
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'INVALID DATE PRESENT IN THE FILE OR IT IS NOT MATCHING WITH THE SELECTED DATE.'  
	RETURN
END
      
 UPDATE ACCOUNT.DBO.TBL_BGDATA SET TODATE = CONVERT(DATETIME, @TDATE,103) - 1 + ' 23:59:59'
 WHERE TODATE >= CONVERT(DATETIME, @TDATE,103)

 DELETE FROM ACCOUNT.DBO.TBL_BGDATA 
 WHERE TODATE < FROMDATE

 INSERT INTO ACCOUNT.DBO.TBL_BGDATA
 SELECT CONVERT(DATETIME, @TDATE,103),
		'DEC 31 2049 23:59:59',
        LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',2))),       
		LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',3))),
		CONVERT(DATETIME, LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',4))),103),
		CONVERT(DATETIME, LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',5))),103),
		ENTERED_BY=@UNAME,
		ENTERED_DATE=GETDATE() 
 FROM FILEDATA      
 WHERE PROGID = @PROGID       
 AND .DBO.PIECE(FILE_DATA,',',1) = @TDATE
        
 SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA      
 WHERE PROGID = @PROGID       
 AND .DBO.PIECE(FILE_DATA,',',1) = @TDATE
      
 SELECT STRMSGCODE = 1, STRMSG = CONVERT(VARCHAR,@RECCNT) + ' RECORD(S) IMPORTED SUCCESSFULLY.'

GO
