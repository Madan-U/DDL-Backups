-- Object: PROCEDURE dbo.CNTJV_EXPORT_NSECM
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------






CREATE PROC [dbo].[CNTJV_EXPORT_NSECM] ( @REPORTDATE VARCHAR(11))

AS --- EXEC CNTJV_EXPORT_NSECM 'DEC  2 2021'

TRUNCATE TABLE CALLNTRADE
TRUNCATE TABLE JV_TABLE_TMP


INSERT INTO CALLNTRADE
SELECT PARTY_CODE,STATE=UPPER(STATE),PRODUCT_TYPE=CASE WHEN PRODUCT_TYPE IN ('ADMIN','RISK-SQ-OFF') THEN 'SQOFF' ELSE 'CNT' END,ORDER_NO,EXCHANGE,SEGMENT
FROM ORDER_TRANS_JV_CALC (NOLOCK) WHERE CONVERT(DATETIME,TRADE_DATE) BETWEEN @REPORTDATE AND @REPORTDATE + ' 23:59'
AND ORDER_TYPE='OFFLINE' AND EXCHANGE IN ('NSE','BSE') AND SEGMENT='CAPITAL'
AND PARTY_CODE NOT IN (SELECT PARTY_CODE FROM MSAJAG.DBO.CNT_EXCEPTION_MASTER)

IF(SELECT COUNT(DISTINCT PARTY_CODE) from CALLNTRADE )>20000
BEGIN 
Truncate table CALLNTRADE
Return
END
Else 
Begin

INSERT INTO JV_TABLE_TMP
SELECT PARTY_CODE,'61411', '99190',COUNT(1)*20 ,0,CONVERT(VARCHAR(500),''), 'ALL',PRODUCT_TYPE
FROM CALLNTRADE GROUP BY PARTY_CODE,PRODUCT_TYPE

UPDATE JV_TABLE_TMP SET NARRATION ='Call & Trade charges for '+ REPLACE(CONVERT(VARCHAR(11),CONVERT(DATETIME,@REPORTDATE),106),' ','-') WHERE PRODUCT='CNT'

UPDATE JV_TABLE_TMP SET NARRATION ='Square off charges for '+ REPLACE(CONVERT(VARCHAR(11),CONVERT(DATETIME,@REPORTDATE),106),' ','-') WHERE PRODUCT='SQOFF'

UPDATE JV_TABLE_TMP SET GST_CHARGES = CHARGAMT*18/100

DELETE FROM TBL_CNTDATA_LOG WHERE CNT_DATE = CONVERT(DATETIME,@REPORTDATE) AND EXCHANGE='NSECM'

INSERT INTO TBL_CNTDATA_LOG
SELECT CNT_DATE=CONVERT(DATETIME,@REPORTDATE),CLTCODE,GLCODE,GSTCODE,CHARGAMT,GST_CHARGES,NARRATION,BRANCH,PRODUCT,EXCHANGE='NSECM',RUNTIME=GETDATE() 
FROM JV_TABLE_TMP

TRUNCATE TABLE NSECM_CNT

INSERT INTO NSECM_CNT VALUES ('Sno','VoucherDate','EffectiveDate','Party_Code','DrCr','Amount','Narration','Branch_Code')

INSERT INTO NSECM_CNT
SELECT SRNO,
VDT = CASE WHEN DATENAME(DW,GETDATE()) = 'SATURDAY' THEN CONVERT(VARCHAR(10),GETDATE(),103) ELSE CONVERT(VARCHAR(10),GETDATE(),103) END, 
EDT =  CASE WHEN DATENAME(DW,GETDATE()) = 'SATURDAY' THEN CONVERT(VARCHAR(10),GETDATE(),103) ELSE CONVERT(VARCHAR(10),GETDATE(),103) END,
CLTCODE=LTRIM(RTRIM(CLTCODE)),DRCR = 'D',VAMT = CHARGAMT+GST_CHARGES,NARRATION,BRANCH 
FROM JV_TABLE_TMP 
UNION ALL
SELECT SRNO,
VDT =  CASE WHEN DATENAME(DW,GETDATE()) = 'SATURDAY' THEN CONVERT(VARCHAR(10),GETDATE(),103) ELSE CONVERT(VARCHAR(10),GETDATE(),103) END,
EDT =  CASE WHEN DATENAME(DW,GETDATE()) = 'SATURDAY' THEN CONVERT(VARCHAR(10),GETDATE(),103) ELSE CONVERT(VARCHAR(10),GETDATE(),103) END,
CLTCODE=LTRIM(RTRIM(GLCODE)),DRCR = 'C',VAMT = CHARGAMT,NARRATION,BRANCH 
FROM JV_TABLE_TMP 
UNION ALL
SELECT SRNO,
VDT =  CASE WHEN DATENAME(DW,GETDATE()) = 'SATURDAY' THEN CONVERT(VARCHAR(10),GETDATE(),103) ELSE CONVERT(VARCHAR(10),GETDATE(),103) END, 
EDT =  CASE WHEN DATENAME(DW,GETDATE()) = 'SATURDAY' THEN CONVERT(VARCHAR(10),GETDATE(),103) ELSE CONVERT(VARCHAR(10),GETDATE(),103) END,
CLTCODE=LTRIM(RTRIM(GSTCODE)),DRCR = 'C',VAMT = GST_CHARGES,'GST ' + NARRATION,BRANCH 
FROM JV_TABLE_TMP
ORDER BY SRNO

END

SELECT * FROM NSECM_CNT

GO
