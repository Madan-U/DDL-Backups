CREATE PROC [dbo].[PROC_ALL_EPN_TRANS_UPLOAD_ABL] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @RECCNT	INT

TRUNCATE TABLE TBL_ALL_EPN_DATA

INSERT INTO TBL_ALL_EPN_DATA
SELECT CONVERT(DATETIME,@TDATE,103),
DBO.PIECE(FILE_DATA,',',1) PARTY_CODE,
LTRIM(RTRIM(DBO.PIECE(FILE_DATA,',',2))) SYMBOL_NSE,
LTRIM(RTRIM(DBO.PIECE(FILE_DATA,',',6))) SYMBOL_BSE,
DBO.PIECE(FILE_DATA,',',3) SERIES,
ABS(DBO.PIECE(FILE_DATA,',',4)) NET_SELL_QTY,
DBO.PIECE(FILE_DATA,',',5) SELL_AVG_PRICE,0,'',0,0
FROM FILEDATA WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',2) <> 'Symbol'

DELETE FROM TBL_ALL_EPN_DATA WHERE CLTCODE=''

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=1 AND REPORT_DATE = CONVERT(DATETIME,@TDATE,103)

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA WHERE PROGID = @PROGID

DELETE FROM FILEDATA WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
