--EXEC RPT_AGTS_COMMONDETAILS 'COMPANY_DET'


CREATE PROC [dbo].[RPT_AGTS_COMMONDETAILS_BKP_01APR2025_SRE_35529]
		(
		@DETAIL_FLAG	VARCHAR(20),
		@DEALADDFOR		VARCHAR(20)='BRANCH',
		@BRANCH_CODE	VARCHAR(20)='',
		@SUBBROKER_CODE	VARCHAR(20)='',
		@TRADER_CODE	VARCHAR(20)=''
		)
		
		AS

		/*
		EXEC RPT_AGTS_COMMONDETAILS 'COMPANY_DET'
		EXEC RPT_AGTS_COMMONDETAILS 'DEALADD_DET'
		EXEC RPT_AGTS_COMMONDETAILS 'AUTHOSIGN_DET'
		*/

		DECLARE	@PRINTDEALINGADD VARCHAR(20)
		SET	@PRINTDEALINGADD = @DEALADDFOR
		
		
		CREATE	TABLE #AGTS_COMMDETAILS_TMP 
				(
				COMPANYHEADER1 VARCHAR(MAX),
				COMPANYHEADER2 VARCHAR(MAX),
				COMPANYHEADER3 VARCHAR(MAX),
				COMPANYHEADER4 VARCHAR(MAX),
				COMPANYHEADER5 VARCHAR(MAX),
				COMPANYHEADER6 VARCHAR(MAX),
				COMPANYHEADER7 VARCHAR(MAX),
				COMPANYHEADER8 VARCHAR(MAX),
				COMPANYHEADER9 VARCHAR(MAX),
				COMPANYHEADER10 VARCHAR(MAX),
				COMPANYHEADER11 VARCHAR(MAX),
				COMPANYHEADER12 VARCHAR(MAX),
				COMPANYHEADER13 VARCHAR(MAX),
				COMPANYHEADER14 VARCHAR(MAX),
				COMPANYHEADER15 VARCHAR(MAX),
				AUTHORISEDSIGNETORIES1 VARCHAR(MAX),
				AUTHORISEDSIGNETORIES2 VARCHAR(MAX),
				COMPANY			VARCHAR(200),
				PANNO			VARCHAR(20),
				SERVICES_TAXNO	VARCHAR(20),
				CITY			VARCHAR(50),
				COMPANYFOOTER1	VARCHAR(MAX),
				COMPANYFOOTER2	VARCHAR(MAX),
				COMPANYFOOTER3	VARCHAR(MAX),
				COMPANYFOOTER4	VARCHAR(MAX),
				COMPANYFOOTER5	VARCHAR(MAX),
				COMPANYFOOTER6	VARCHAR(MAX),
				COMPANYFOOTER7	VARCHAR(MAX),
				COMPANYFOOTER8	VARCHAR(MAX),
				COMPANYFOOTER9	VARCHAR(MAX),
				COMPANYFOOTER10	VARCHAR(MAX)
				)

		/*
		COMPANYHEADER1,COMPANYHEADER2,COMPANYHEADER3,COMPANYHEADER4,COMPANYHEADER5,COMPANYHEADER6,COMPANYHEADER7,COMPANYHEADER8,COMPANYHEADER9,COMPANYHEADER10,COMPANYHEADER11,COMPANYHEADER12,COMPANYHEADER13,COMPANYHEADER14,COMPANYHEADER15,AUTHORISEDSIGNETORIES1,AUTHORISEDSIGNETORIES2,COMPANY,PANNO,SERVICES_TAXNO,CITY,COMPANYFOOTER1,COMPANYFOOTER2,COMPANYFOOTER3,COMPANYFOOTER4,COMPANYFOOTER5,COMPANYFOOTER6,COMPANYFOOTER7,COMPANYFOOTER8,COMPANYFOOTER9,COMPANYFOOTER10
		*/
		
		IF @DETAIL_FLAG = 'COMPANY_DET'
		BEGIN
		
			INSERT	INTO #AGTS_COMMDETAILS_TMP (COMPANYHEADER1,COMPANYHEADER2,COMPANYHEADER3,COMPANYHEADER4,COMPANYHEADER5,COMPANYHEADER6,COMPANYHEADER7,COMPANYHEADER8,COMPANYHEADER9,COMPANYHEADER10,COMPANYHEADER11,COMPANYHEADER12,COMPANYHEADER13,COMPANY,PANNO,SERVICES_TAXNO,CITY,COMPANYFOOTER1,COMPANYFOOTER2,COMPANYFOOTER3,COMPANYFOOTER4,COMPANYFOOTER5,COMPANYFOOTER6,COMPANYFOOTER7,COMPANYFOOTER8,COMPANYFOOTER9,COMPANYFOOTER10)
			SELECT	COMPANYHEADER1 = COMPANY,
					COMPANYHEADER2 = 'Corporate Office: '+ Addr1 +' '+ Addr2 +' '+ City +' - '+ Zip,
					COMPANYHEADER3 = 'Cin: '+ CIN,
					COMPANYHEADER4 = 'SEBI Regn.No.: '+ COMMONSEBINO,
					COMPANYHEADER5 = 'Member Code: '+ COMMONMEMBERCODE,
					COMPANYHEADER6 = 'Email: '+ EMAIL +'  Visit us at: '+ WEBSITE_ID +'  Email ID For Investor Complaints: '+ IGRIV_EMAIL,
					COMPANYHEADER7 = 'Compliance Officer: '+ COMPLIANCE_NAME +' (Email id: '+ COMPLIANCE_EMAIL +', Tel: '+ COMPLIANCE_PHONE +') ',
					COMPANYHEADER8 = '',
					COMPANYHEADER9 = '',
					COMPANYHEADER10 = '',
					COMPANYHEADER11 = '',
					COMPANYHEADER12 = '',
					COMPANYHEADER13 = '',
					COMPANY,
					PANNO,
					SERVICES_TAXNO,
					CITY,
					COMPANYFOOTER1	= 'If you require any further information/ details regarding the AGTS, you may contact us on '+ PHONE +' or write us at '+ EMAIL +'.',
					COMPANYFOOTER2	= '',
					COMPANYFOOTER3	= '',
					COMPANYFOOTER4	= '',
					COMPANYFOOTER5	= '',
					COMPANYFOOTER6	= '',
					COMPANYFOOTER7	= '',
					COMPANYFOOTER8	= '',
					COMPANYFOOTER9	= '',
					COMPANYFOOTER10	= ''
			FROM	OWNER (NOLOCK)
	
		END	


		IF @DETAIL_FLAG = 'DEALADD_DET'
		BEGIN
				
			IF (SELECT COUNT(1) FROM DEALINGADDRESS (NOLOCK)) > 0
			BEGIN
				INSERT	INTO #AGTS_COMMDETAILS_TMP (COMPANYHEADER14,COMPANYHEADER15)
				SELECT	COMPANYHEADER14 = 'Dealing Office Address: '+ BRANCHNAME +' '+ ADDR1 +' '+ ADDR2 +' '+ ADDR3 +', Tel.: ' + PHONE1 +', Fax: '+ FAX,
						COMPANYHEADER15 = ''
				FROM	DEALINGADDRESS (NOLOCK)

			END
			ELSE
			BEGIN

				IF @DEALADDFOR = ''
				BEGIN
					SELECT	@PRINTDEALINGADD = ISNULL(PRINT_DEALING,'BRANCH') FROM TBL_COMMONCONTRACT_MASTER (NOLOCK) WHERE GETDATE() BETWEEN EFFECTIVE_FROM AND EFFECTIVE_TO
				END
				
				IF (@PRINTDEALINGADD = 'BRANCH')
				BEGIN
					INSERT	INTO #AGTS_COMMDETAILS_TMP (COMPANYHEADER14,COMPANYHEADER15,AUTHORISEDSIGNETORIES1,AUTHORISEDSIGNETORIES2)
					SELECT	COMPANYHEADER14 = 'Dealing Office Address: '+ LTRIM(RTRIM(LONG_NAME)) +' '+ LTRIM(RTRIM(ADDRESS1)) +' '+ LTRIM(RTRIM(ADDRESS2)) +' '+ LTRIM(RTRIM(CITY)) +', Tel.: ' + LTRIM(RTRIM(PHONE1)) +', Fax: '+ LTRIM(RTRIM(FAX)),
							COMPANYHEADER15 = '',
							AUTHORISEDSIGNETORIES1 = LTRIM(RTRIM(CONTACT_PERSON)),
							AUTHORISEDSIGNETORIES2 = ''
					FROM	BRANCH (NOLOCK) 
					WHERE	BRANCH_CODE = @BRANCH_CODE				
					
				END
				ELSE IF (@PRINTDEALINGADD = 'BRANCHES')
				BEGIN
					INSERT	INTO #AGTS_COMMDETAILS_TMP (COMPANYHEADER14,COMPANYHEADER15,AUTHORISEDSIGNETORIES1,AUTHORISEDSIGNETORIES2)
					SELECT	COMPANYHEADER14 = 'Dealing Office Address: '+ LTRIM(RTRIM(LONG_NAME)) +' '+ LTRIM(RTRIM(ADDRESS1)) +' '+ LTRIM(RTRIM(ADDRESS2)) +' '+ LTRIM(RTRIM(CITY)) +', Tel.: ' + LTRIM(RTRIM(PHONE1)) +', Fax: '+ LTRIM(RTRIM(FAX)),
							COMPANYHEADER15 = '',
							AUTHORISEDSIGNETORIES1 = LTRIM(RTRIM(CONTACT_PERSON)),
							AUTHORISEDSIGNETORIES2 = ''
					FROM	BRANCHES (NOLOCK) 
					WHERE	SHORT_NAME = @TRADER_CODE
				END
				ELSE IF (@PRINTDEALINGADD = 'SUBBROKERS')
				BEGIN
					INSERT	INTO #AGTS_COMMDETAILS_TMP (COMPANYHEADER14,COMPANYHEADER15,AUTHORISEDSIGNETORIES1,AUTHORISEDSIGNETORIES2)
					SELECT	COMPANYHEADER14 = 'Dealing Office Address: '+ LTRIM(RTRIM(NAME)) +' '+ LTRIM(RTRIM(ADDRESS1)) +' '+ LTRIM(RTRIM(ADDRESS2)) +' '+ LTRIM(RTRIM(CITY)) +', Tel.: ' + LTRIM(RTRIM(PHONE1)) +', Fax: '+ LTRIM(RTRIM(FAX)),
							COMPANYHEADER15 = '',
							AUTHORISEDSIGNETORIES1 = LTRIM(RTRIM(CONTACT_PERSON)),
							AUTHORISEDSIGNETORIES2 = ''
					FROM	SUBBROKERS (NOLOCK) 
					WHERE	SUB_BROKER = @SUBBROKER_CODE
				END
				
			END

		END	

		IF @DETAIL_FLAG = 'AUTHOSIGN_DET'
		BEGIN

			IF (SELECT COUNT(1) FROM AUTHORISEDSIGNETORIES (NOLOCK)) > 0
			BEGIN
					
				INSERT	INTO #AGTS_COMMDETAILS_TMP (AUTHORISEDSIGNETORIES1,AUTHORISEDSIGNETORIES2)
				SELECT	AUTHORISEDSIGNETORIES1 = LTRIM(RTRIM(AUTHORISEDSIGNETORY1)) + (
						CASE
							WHEN AUTHORISEDSIGNETORY2 <> '' THEN ', '+ LTRIM(RTRIM(AUTHORISEDSIGNETORY2))
							ELSE ''
						END) + (
						CASE
							WHEN AUTHORISEDSIGNETORY3 <> '' THEN ', '+ LTRIM(RTRIM(AUTHORISEDSIGNETORY3))
							ELSE ''
						END),						
						AUTHORISEDSIGNETORIES2 = LTRIM(RTRIM(AUTHORISEDSIGNETORY4)) + (
						CASE
							WHEN AUTHORISEDSIGNETORY5 <> '' THEN ', '+ LTRIM(RTRIM(AUTHORISEDSIGNETORY5))
							ELSE ''
						END) + (
						CASE
							WHEN AUTHORISEDSIGNETORY6 <> '' THEN ', '+ LTRIM(RTRIM(AUTHORISEDSIGNETORY6))
							ELSE ''
						END) + (
						CASE
							WHEN AUTHORISEDSIGNETORY7 <> '' THEN ', '+ LTRIM(RTRIM(AUTHORISEDSIGNETORY7))
							ELSE ''
						END)
				FROM	AUTHORISEDSIGNETORIES (NOLOCK) 
					
			END
		
		END	
				
		SELECT * FROM #AGTS_COMMDETAILS_TMP
		
		DROP TABLE #AGTS_COMMDETAILS_TMP

GO
