CREATE PROCEDURE [dbo].[PROC_SHORT_MARGIN_UPEOD_MCX]
	@MARGINDATE VARCHAR(11),
	@FLDFILENAME VARCHAR (300),
	@UPLOAD_BY VARCHAR(30) = ''
AS

TRUNCATE TABLE TBL_MCX_SHORT_ALLOC_TMP

SELECT MARGINDATE=CONVERT(VARCHAR(10),''),CMID,TMID,CLIENT_CODE,EOD_SHORT_ALLOC,
EOD_SHORT_REP=CONVERT(VARCHAR(10),''),
EXCESS_NCL=CONVERT(VARCHAR(10),''),
EXCESS_ICCL=CONVERT(VARCHAR(10),''),
EXCESS_NCCL=CONVERT(VARCHAR(10),'') 
INTO #TBL_MCX_SHORT_ALLOC_TMP
FROM TBL_MCX_SHORT_ALLOC_TMP
WHERE 1 = 2

DECLARE
	@STRSQL VARCHAR(1000)
		
DECLARE
	@FILENAME VARCHAR(50)
SELECT @FILENAME = RIGHT(@FLDFILENAME, CHARINDEX('\', REVERSE(@FLDFILENAME),0) - 1)

	Set @strSql = 'Bulk insert #TBL_MCX_SHORT_ALLOC_TMP from ''' + @FLDFILENAME  + '''  with ( FIELDTERMINATOR = '','', ROWTERMINATOR = ''\n'' )'
	Exec(@strSql)
	  
	INSERT INTO TBL_MCX_SHORT_ALLOC_TMP
	SELECT MARGINDATE=CONVERT(DATETIME,MARGINDATE,103),CMID,TMID,CLIENT_CODE,EOD_SHORT_ALLOC,EOD_SHORT_REP=0,
	EXCESS_NCL=0,EXCESS_ICCL=0,EXCESS_NCCL=0,EXCESS_MCCIL=0,@FILENAME
	FROM #TBL_MCX_SHORT_ALLOC_TMP WHERE CONVERT(DATETIME,MARGINDATE,103) = @MARGINDATE  

	DELETE FROM TBL_MCX_SHORT_ALLOC WHERE MARGINDATE = @MARGINDATE 
	
INSERT INTO TBL_MCX_SHORT_ALLOC 
SELECT MARGINDATE,CMID,TMID,CLIENT_CODE,EOD_SHORT_ALLOC,EOD_SHORT_REP=0, EXCESS_NCL=0,EXCESS_ICCL=0,EXCESS_NCCL=0,EXCESS_MCCIL=0,
@FILENAME,@UPLOAD_BY,UPLOAD_ON=GETDATE()
FROM TBL_MCX_SHORT_ALLOC_TMP WHERE MARGINDATE = @MARGINDATE

GO
