---DECLARE @TDATE DATETIME   
  
  
---COMB_MARGIN_CALC_REPORT '2018-08-01','2018-08-13','',''  
  
---EXEC COMB_MARGIN_CALC_REPORT'Aug  1 2018', 'Aug  8 2018', 'A0001', 'ZZ999','NSEFO'  
  
---EXEC COMB_MARGIN_CALC_REPORT'Aug  1 2018', 'Aug  8 2018', 'A0001', 'ZZ999','NSX'  
  
CREATE PROCEDURE [dbo].[CLIENT_DATA]  
(  
  
@FDATE DATETIME,  
@TDATE DATETIME,  
@CONSOL VARCHAR(10)  ---10  CONSOL INDICATES THAT WHETHER IS "NSx","NSEFO"  
  
)  
  
AS   
   
DECLARE @FromDate DATETIME=CAST(@Fdate  AS DATETIME)  
  
DECLARE @ToDate DATETIME=dateadd(ms, -3, (dateadd(day, +1, convert(varchar, @Tdate, 101))))--DATEADD(DD, -1, DATEADD(D, 1, CONVERT(DATETIME2, @Tdate)))    
  
  
BEGIN   
  
 SELECT A.CL_CODE,LONG_NAME,pan_gir_no,email,mobile_pager,EXCHANGE,Segment,Active_Date,InActive_From,  
(CASE WHEN InActive_From LIKE '%2049%'THEN 'ACTIVE'ELSE 'INACTIVE'END)AS STATUS  
,l_address1,l_address2,l_address3,L_CITY,L_STATE,L_ZIP,P_address1,P_address2,P_address3,P_CITY,P_STATE,P_ZIP,  
DOB  
INTO #NSE  
 FROM CLIENT_DETAILS A,CLIENT_BROK_DETAILS B  
WHERE ACTIVE_DATE>=@FromDate  
AND ACTIVE_DATE<=@ToDate  
AND A.CL_cODE=B.Cl_Code  
---AND EXCHANGE='BSX'  
AND SEGMENT='FUTURES' OR SEGMENT='CAPITAL'  
  
SELECT A.*,B.BankID,B.Cltdpid,B.Depository,B.DefDp INTO #BANK FROM #NSE A  
LEFT OUTER JOIN  
[AngelCommodity].BSECURFO.DBO.CLIENT4 B  
ON A.CL_CODE=B.CL_CODE  
AND B.Depository IN ('CURRENT','SAVING')  
AND A.CL_cODE BETWEEN 'A00'AND 'ZZ999'  
  
---SELECT TOP 2 * FROM POBANK  
  
  
SELECT A.*,B.Bank_Name INTO #BANKFINAL FROM #BANK A  
LEFT OUTER JOIN  
[AngelCommodity].BSECURFO.DBO.POBank B  
ON A.BankID=B.BankId  
  
  
SELECT A.*,B.BankID AS DP_BANKID,B.Cltdpid AS DPID,B.Depository AS DP_DEPOSITORY,B.DefDp AS DP_DEFDP INTO #DP  
 FROM #BANKFINAL A  
LEFT OUTER JOIN  
[AngelCommodity].BSECURFO.DBO.CLIENT4 B  
ON A.CL_CODE=B.CL_CODE  
AND B.Depository NOT IN ('CURRENT','SAVING')  
AND A.CL_cODE BETWEEN 'A00'AND 'ZZ999'  
  
  
SELECT A.*,B.BankName AS DPNAME INTO #DPFINAL  
 FROM #DP A  
LEFT OUTER JOIN  
[AngelCommodity].BSECURFO.DBO.BANK B  
ON A.DP_BANKID=B.BankId  
  
---------------INCOME  
  
SELECT A.*,B.GROSS_INCOME INTO #INCOME FROM #DPFINAL A  
LEFT OUTER JOIN  
CLIENT_MASTER_UCC_DATA B  
ON A.CL_cODE=B.PARTY_CODE  
  
---SELECT * FROM CLIENT_STATIC_CODES  
  
SELECT  A.*,DESCRIPTION AS INCOME INTO #INCFINAL FROM #INCOME A  
LEFT OUTER JOIN  
CLIENT_STATIC_CODES B  
ON A.GROSS_INCOME=B.CODE  
AND CATEGORY='INCOMESLAB_NONIND'AND KRA_TYPE='CDSL'  
  
  
----OCCUPATION  
SELECT A.*,B.OCCUPATION INTO #OCC12 FROM #INCFINAL A  
LEFT OUTER JOIN  
CLIENT_MASTER_UCC_DATA B  
ON A.CL_cODE=B.PARTY_CODE  
  
---SELECT * FROM CLIENT_STATIC_CODES WHERE CATEGORY='OCCUPATION'  
  
SELECT  A.*,DESCRIPTION AS OCCUPATION FROM #OCC12 A  
LEFT OUTER JOIN  
CLIENT_STATIC_CODES B  
ON A.OCCUPATION=B.CODE  
AND CATEGORY='OCCUPATION'AND KRA_TYPE='CDSL'  
  
   
 END

GO
