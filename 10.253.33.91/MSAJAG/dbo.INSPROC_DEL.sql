CREATE PROC [DBO].[INSPROC_DEL]
(
	@SETT_NO	VARCHAR(7),
	@SETT_TYPE	VARCHAR(2)
)
AS

DECLARE @CM_SETT_NO		VARCHAR(7),
		@CM_SETT_TYPE	VARCHAR(2),
		@START_DATE		VARCHAR(11)

SELECT @START_DATE = START_DATE FROM SETT_MST WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE
SELECT @CM_SETT_NO = @SETT_NO, @CM_SETT_TYPE = @SETT_TYPE FROM SETT_MST 
WHERE START_DATE = @START_DATE AND SETT_TYPE = 'M'

EXEC INSPROC @CM_SETT_NO, @CM_SETT_TYPE 
EXEC INSPROC @SETT_NO, @SETT_TYPE

GO
