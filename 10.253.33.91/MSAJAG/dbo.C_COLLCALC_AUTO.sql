ELSE 'PROCESS NOT DONE CORRECTLY.' END) FROM C_AUTO_COLL_LOG WHERE SAUDA_DATE_ORG = @SAUDA_DATE_ORG AND SAUDA_DATE = @SAUDA_DATE ) A 

