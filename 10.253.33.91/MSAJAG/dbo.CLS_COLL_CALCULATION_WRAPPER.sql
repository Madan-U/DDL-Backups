--  CLS_COLL_CALCULATION_WRAPPER 'NSE', 'CAPITAL', '00', 'ZZZZ', '25/04/2018' ,'Badrish'

CREATE PROC [dbo].[CLS_COLL_CALCULATION_WRAPPER]
@EXCHANGE VARCHAR(3),
@SEGMENT VARCHAR(20),
@FROMPARTY VARCHAR(10),
@TOPARTY VARCHAR(10),
@CALC_DATE VARCHAR(12),
@USERNAME VARCHAR(20)
AS

/*
DECLARE @EXCHANGE VARCHAR(3)
DECLARE @SEGMENT VARCHAR(20)
DECLARE @FROMPARTY VARCHAR(10)
DECLARE @TOPARTY VARCHAR(10)
DECLARE @CALC_DATE VARCHAR(12)
DECLARE @USERNAME VARCHAR(20)

SET @EXCHANGE  = 'NSE'
SET @SEGMENT   = 'CAPITAL'
SET @FROMPARTY  = '000'
SET @TOPARTY    = 'ZZZZ'
SET @CALC_DATE  = '25/04/2018'
SET @USERNAME = 'BADRISH'
*/

BEGIN

DECLARE @MSG VARCHAR(100)
DECLARE @TODAYCALDATE DATETIME
DECLARE @SDTCUR DATETIME
DECLARE @LDTCUR DATETIME
DECLARE @ACCOUNTDB VARCHAR(50)
DECLARE @ACCOUNTDBSERVER VARCHAR(50)
DECLARE @SHAREDB VARCHAR(50)
DECLARE @SHAREDBSERVER VARCHAR(50)
DECLARE @OPENENTRYDATE DATETIME
DECLARE @SQL VARCHAR(8000)

SELECT @ACCOUNTDB =ACCOUNTDB , @ACCOUNTDBSERVER = ACCOUNTSERVER, @SHAREDB = SHAREDB, @SHAREDBSERVER = SHARESERVER
FROM PRADNYA..MULTICOMPANY
WHERE EXCHANGE=  @EXCHANGE AND SEGMENT = @SEGMENT



SELECT @TODAYCALDATE = LEFT(GETDATE(),11)

--START CHECKING FOR CLOSING PRICE WITH EXCHANGE SEGMENT AND DATE

SET @CALC_DATE = CONVERT(DATETIME,@CALC_DATE ,103)  

IF NOT EXISTS (SELECT TOP 1 SCRIP_CD FROM C_VALUATION WHERE DATEDIFF(DAY,SYSDATE,@CALC_DATE) = 0 AND EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT)
BEGIN
		
		SET @MSG =  'NO CLOSING PRICES FOUND IN THE MASTER FOR THE SELECTED DATE.PLEASE RUN VALUATION PROCESS.'
		
		SELECT  @MSG AS MSG
		RETURN 
END
	
-- START CHECKING FOR INSTRUMENT TYPE MASTER 
IF NOT EXISTS (SELECT * FROM INSTRUTYPEMST WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND ACTIVE = 1)
BEGIN
		SET @MSG =  'INSTRUMENT TYPE MASTER IS NOT COMPLETE.PLEASE  CHECK IT.'
		
		SELECT @MSG AS MSG
		RETURN 
END
	
--BEGIN TRY
--BEGIN TRAN

IF @SEGMENT='CAPITAL' OR @SEGMENT='FUTURES'
BEGIN
	              INSERT INTO COLLATERALCALCULATION_LOG (EXCHANGE,SEGMENT,FROMPARTY,TOPARTY,EVALUATIONDATE,COLLATERALDATE,USERID,EXECUTIONDATE) 
	              VALUES(@EXCHANGE,@SEGMENT,@FROMPARTY,@TOPARTY,@CALC_DATE,@CALC_DATE,@USERNAME,@CALC_DATE)
				  
	              PRINT 'DATA SUCCESSFULLY INSERT IN THE  TABLE COLLATERALCALCULATION_LOG "'
	              
END

CREATE TABLE #CURRFYEAR (SDTCUR DATETIME, LDTCUR DATETIME)

INSERT INTO #CURRFYEAR
EXEC C_SELFINYEARSPMS  @TODAYCALDATE, @ACCOUNTDB, @ACCOUNTDBSERVER

SELECT @SDTCUR = SDTCUR , @LDTCUR = LDTCUR  FROM #CURRFYEAR

CREATE TABLE #OPENENTRY (OPENENTRYDATE DATETIME)

INSERT INTO #OPENENTRY
EXEC C_CHECKOPENINGENTRYMS @SDTCUR,@LDTCUR, @ACCOUNTDB, @ACCOUNTDBSERVER
--SELECT @OPENENTRYDATE = OPENENTRYDATE FROM #OPENENTRY

--TRUNCATE TABLE #OPENENTRY

INSERT INTO #OPENENTRY
EXEC C_LASTOPENINGENTRYMS @SDTCUR , @ACCOUNTDB, @ACCOUNTDBSERVER
--SELECT @OPENENTRYDATE = OPENENTRYDATE FROM #OPENENTRY

DELETE FROM C_MARGINLEDGER 

--IF(@OPENENTRYDATE <> '')
IF EXISTS( SELECT * FROM #OPENENTRY)
BEGIN
	
		SET @SQL = ""
		SET @SQL = @SQL + " INSERT INTO C_MARGINLEDGER "
		SET @SQL = @SQL + " SELECT M.EXCHANGE,LEFT(M.SEGMENT,8),M.PARTY_CODE, CAMT = ISNULL((CASE WHEN DRCR = 'C' THEN SUM(AMOUNT) ELSE 0 END),0), "
		SET @SQL = @SQL + " DAMT = ISNULL((CASE WHEN DRCR = 'D' THEN SUM(AMOUNT) ELSE 0 END),0), DRCR "
		SET @SQL = @SQL + " FROM " + @ACCOUNTDBSERVER + "."  + @ACCOUNTDB +".DBO.MARGINLEDGER M "
		SET @SQL = @SQL + " LEFT OUTER JOIN C_ACCOUNTMAPPING A "
		SET @SQL = @SQL + " ON ( MCLTCODE = ACCOUNTCODE AND A.PARTY_CODE = M.PARTY_CODE AND A.EXCHANGE = M.EXCHANGE AND A.SEGMENT = LEFT(M.SEGMENT,7) AND ACTIVE = 1 AND EFFDATE <= '" +  CONVERT(VARCHAR(12),@TODAYCALDATE) +" 23:59:59' )"
		SET @SQL = @SQL + " WHERE VDT >= '" + CONVERT(VARCHAR(12),@SDTCUR) + "' AND VDT <= '" + CONVERT(VARCHAR(12),@TODAYCALDATE)  + " 23:59:59' AND M.EXCHANGE = '" + @EXCHANGE + "' AND LEFT(M.SEGMENT,7) = '" + @SEGMENT + "' "
		SET @SQL = @SQL + "  GROUP BY DRCR, M.PARTY_CODE,M.EXCHANGE,LEFT(M.SEGMENT,8) "
					
END
ELSE
BEGIN
		SET @SQL = ""
		SET @SQL = @SQL + " INSERT INTO C_MARGINLEDGER "
		SET @SQL = @SQL + " SELECT M.EXCHANGE,LEFT(M.SEGMENT,8),M.PARTY_CODE, CAMT = ISNULL((CASE WHEN DRCR = 'C' THEN SUM(AMOUNT) ELSE 0 END),0),"
		SET @SQL = @SQL + " DAMT = ISNULL((CASE WHEN DRCR = 'D' THEN SUM(AMOUNT) ELSE 0 END),0), DRCR"
		SET @SQL = @SQL +  " FROM " +  + @ACCOUNTDBSERVER + "."  + @ACCOUNTDB + ".DBO.MARGINLEDGER M LEFT OUTER JOIN C_ACCOUNTMAPPING A"
		SET @SQL = @SQL + " ON ( MCLTCODE = ACCOUNTCODE AND A.PARTY_CODE = M.PARTY_CODE AND A.EXCHANGE = M.EXCHANGE"
		SET @SQL = @SQL +  " AND A.SEGMENT = LEFT(M.SEGMENT,7) AND ACTIVE = 1 AND EFFDATE <= '" + CONVERT(VARCHAR(12),@TODAYCALDATE) + " 23:59:59' )"
		SET @SQL = @SQL + " WHERE VDT <= '" + CONVERT(VARCHAR(12),@TODAYCALDATE) + " 23:59:59' AND M.EXCHANGE = '" + RTRIM(LTRIM(@EXCHANGE)) + "' AND LEFT(M.SEGMENT,7) = '" + LTRIM(RTRIM(@SEGMENT)) + "' GROUP BY DRCR, M.PARTY_CODE,M.EXCHANGE,LEFT(M.SEGMENT,8) "

END

PRINT @SQL
EXEC(@SQL)


	EXEC C_COLLATERALCALCULATIONSPMS_NEW @EXCHANGE,@SEGMENT,@FROMPARTY,@TOPARTY,@TODAYCALDATE , @CALC_DATE, @SHAREDB, @SHAREDBSERVER, 1
		
	SELECT ISNULL(SUM(CASH),'') CASH, ISNULL(SUM(NONCASH),'') NONCASH FROM COLLATERAL 
	WHERE TRANS_DATE LIKE CONVERT(VARCHAR(12),@TODAYCALDATE) +'%'


	
DROP TABLE #CURRFYEAR
DROP TABLE #OPENENTRY

--	COMMIT TRAN
--END TRY

