	-- EXEC [CLS_CONTRACTSUMMARY] @STATUSID='BROKER',@STATUSNAME='BROKER',@FROMDATE='25/04/2011',@TODATE='25/04/2013',@FROMPARTY='',@TOPARTY='ZZZZZZZZ',@FROMBRANCH='',@TOBRANCH='ZZZZZZZZ',@CLIENTTYPE='ALL'  
