			AMOUNT = AMOUNT + CD_TRDBUYBROKERAGE - CD_TRDSELLBROKERAGE + CD_DELBUYBROKERAGE - CD_DELSELLBROKERAGE +	(
			CASE WHEN SERVICE_CHRG <> 0
				  THEN (CD_TRDBUYSERTAX + CD_DELBUYSERTAX)-(CD_TRDSELLSERTAX + CD_DELSELLSERTAX)
				  ELSE 0
			END),
			PURCHASEBROKERAGE = PURCHASEBROKERAGE + CD_TRDBUYBROKERAGE + CD_DELBUYBROKERAGE +
								(CASE WHEN SERVICE_CHRG <> 0 THEN CD_TRDBUYSERTAX + CD_DELBUYSERTAX ELSE 0 END),
			SELLBROKERAGE = SELLBROKERAGE + CD_TRDSELLBROKERAGE + CD_DELSELLBROKERAGE +
