		#SETTLEMENT
	FROM
		SETTLEMENT S (NOLOCK)
	WHERE
		SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'	
		AND S.TRADEQTY > 0
		AND EXISTS (
			SELECT CL_CODE FROM CLIENT1 C1 (NOLOCK)
			WHERE C1.CL_CODE = S.PARTY_CODE
			AND (
			CASE
				WHEN @CONSOL = 'PARTY_CODE' THEN PARTY_CODE
				WHEN @CONSOL = 'FAMILY' THEN FAMILY
				WHEN @CONSOL = 'TRADER' THEN TRADER
				WHEN @CONSOL = 'SUB_BROKER' THEN SUB_BROKER
				WHEN @CONSOL = 'BRANCH_CD' THEN BRANCH_CD
				WHEN @CONSOL = 'REGION' THEN REGION
				WHEN @CONSOL = 'AREA' THEN AREA
				ELSE PARTY_CODE 
			END)  BETWEEN @FROMCODE AND @TOCODE
