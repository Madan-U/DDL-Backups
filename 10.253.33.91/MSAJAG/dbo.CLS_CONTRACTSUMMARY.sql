CREATE PROC [dbo].[CLS_CONTRACTSUMMARY](
	@STATUSID VARCHAR(15),
	@STATUSNAME VARCHAR(25),
	@FROMDATE VARCHAR(11),
	@TODATE VARCHAR(11),
	@FROMCODE VARCHAR(20),
	@TOCODE VARCHAR (20),
	@CONSOL VARCHAR(10),
	@CLIENTTYPE VARCHAR(5)
	)

