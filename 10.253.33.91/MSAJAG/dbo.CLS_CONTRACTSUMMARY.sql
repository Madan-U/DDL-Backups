			SERVICE_TAX = SERVICE_TAX + (
			CASE WHEN SERVICE_CHRG = 0
				THEN CD_TOTALSERTAX
				ELSE 0
			END)
	FROM	(
			SELECT CD_SETT_NO, CD_SETT_TYPE, CD_PARTY_CODE, CD_SAUDA_DATE,
			CD_TRDBUYBROKERAGE = SUM(CD_TRDBUYBROKERAGE),
			CD_TRDSELLBROKERAGE = SUM(CD_TRDSELLBROKERAGE),
