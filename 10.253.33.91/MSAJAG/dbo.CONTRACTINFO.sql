CREATE PROCEDURE CONTRACTINFO
(
@FROMSAUDADATE VARCHAR(11),
@TOSAUDADATE   VARCHAR(11),
@FROMPARTY     VARCHAR(20),
@TOPARTY       VARCHAR(20)
)
AS
--EXECUTE CONTRACTINFO 'JUL 12 2006','JUL 12 2011','00','ZZZ' 
BEGIN
	SELECT DISTINCT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),SETT_TYPE,SETT_NO,PARTY_CODE,CONTRACTNO,YY=YEAR(SAUDA_DATE),MM=MONTH(SAUDA_DATE),DD=DAY(SAUDA_DATE) 
	FROM   SETTLEMENT
	WHERE
		 SAUDA_DATE BETWEEN @FROMSAUDADATE AND @TOSAUDADATE + ' 23:59:59'	
	     AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
   -- ORDER BY YY DESC,MM DESC,DD DESC,PARTY_CODE
	UNION 
	SELECT DISTINCT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),SETT_TYPE,SETT_NO,PARTY_CODE,CONTRACTNO,YY=YEAR(SAUDA_DATE),MM=MONTH(SAUDA_DATE),DD=DAY(SAUDA_DATE) 
	FROM   ISETTLEMENT
	WHERE
		 SAUDA_DATE BETWEEN @FROMSAUDADATE AND @TOSAUDADATE + ' 23:59:59'	
	     AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
	UNION 
	SELECT DISTINCT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),SETT_TYPE,SETT_NO,PARTY_CODE,CONTRACTNO,YY=YEAR(SAUDA_DATE),MM=MONTH(SAUDA_DATE),DD=DAY(SAUDA_DATE) 
	FROM   HISTORY
	WHERE
		 SAUDA_DATE BETWEEN @FROMSAUDADATE AND @TOSAUDADATE + ' 23:59:59'	
	     AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
    ORDER BY YY DESC,MM DESC,DD DESC,PARTY_CODE
END

GO
