				SELL_BUY		= DBO.PIECE(FILEDATA,',',11),
				TRADEQTY		= DBO.PIECE(FILEDATA,',',12),
				MARKETRATE		= DBO.PIECE(FILEDATA,',',13),
				PRO_CLI			= DBO.PIECE(FILEDATA,',',14),
				PARTY_CODE		= DBO.PIECE(FILEDATA,',',15),
				PARTIPANTCODE	= DBO.PIECE(FILEDATA,',',16),
				AUCTIONPART		= DBO.PIECE(FILEDATA,',',17),
				AUCTIONNO		= DBO.PIECE(FILEDATA,',',18),
				SETTNO			= DBO.PIECE(FILEDATA,',',19),
				SAUDA_DATE		= DBO.PIECE(FILEDATA,',',20),
				TRADEMODIFIED	= DBO.PIECE(FILEDATA,',',21),
				ORDER_NO		= DBO.PIECE(FILEDATA,',',22),
				CPID			= DBO.PIECE(FILEDATA,',',23),
				ORDERTIME		= DBO.PIECE(FILEDATA,',',24)
		FROM CLASSFILEUPD_BULK
			WHERE
				SPID= @SESSIONID
				AND  DBO.PIECE(FILEDATA,',',1) <> ''
		END	
		
		EXEC UPDATETRADE
	END	

	 EXEC DUPRECCHECK
	 
	 EXEC DUPRECSETTCHECK
	 
	 EXEC DUPRECCHECK_CTCLID

	 EXEC DUPRECCHECK_PRODUCT
	
	
	IF (SELECT COUNT(1) FROM TRADE) > 0
	BEGIN

		UPDATE TRADE SET DUMMY1 = MARKETRATE
		
		EXEC TRD_CLIENTMASTER

		SET @SAUDA_DATE = LEFT(CONVERT(DATETIME,@SAUDA_DATE,103),11)
		INSERT INTO STAT_TRD
		SELECT ACTIONCODE	 = 'I10',SYSDATE = @SAUDA_DATE,FILEDATE = @SAUDA_DATE,COM_DATE = LEFT(GETDATE(),11)
	END
	
	/*---------------------------------------------------------------- END OF THE PROC --------------------------------------*/

GO
