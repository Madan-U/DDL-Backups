				AUCTIONPART		= DBO.PIECE(FILE_DATA,',',17),
				AUCTIONNO		= DBO.PIECE(FILE_DATA,',',18),
				SETTNO			= DBO.PIECE(FILE_DATA,',',19),
				SAUDA_DATE		= DBO.PIECE(FILE_DATA,',',20),
				TRADEMODIFIED	= DBO.PIECE(FILE_DATA,',',21),
				ORDER_NO		= DBO.PIECE(FILE_DATA,',',22),
				CPID			= DBO.PIECE(FILE_DATA,',',23),
				ORDERTIME		= DBO.PIECE(FILE_DATA,',',24)
			FROM CLASSFILEUPD
			WHERE
				SESSION_ID= @SESSIONID
				AND  DBO.PIECE(FILE_DATA,',',1) <> ''
		END
		ELSE
		BEGIN
			INSERT INTO TRADE_IMP
			SELECT
				TRADE_NO		= DBO.PIECE(FILEDATA,',',1),
				STATUS			= DBO.PIECE(FILEDATA,',',2),
				SCRIP_CD		= DBO.PIECE(FILEDATA,',',3),
