				CPID			= DBO.PIECE(FILEDATA,',',23),
				ORDERTIME		= DBO.PIECE(FILEDATA,',',24),
				RESERVE			= DBO.PIECE(FILEDATA,',',25)
		FROM CLASSFILEUPD_BULK
			WHERE
				SPID= @SESSIONID
				AND  DBO.PIECE(FILEDATA,',',1) <> ''
		END	
		
		EXEC UPDATETRADE_NOTIS
	END
	--------------------
	
