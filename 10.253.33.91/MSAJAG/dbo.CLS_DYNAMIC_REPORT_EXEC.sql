CREATE proc [dbo].[CLS_DYNAMIC_REPORT_EXEC](

	@CONTROLCODE VARCHAR(25),
	@PARAMS VARCHAR(8000),
	@SPNAME VARCHAR(100),
	@LEVEL varchar(5),
	@STATUSID VARCHAR(15),        
	@STATUSNAME VARCHAR(25)

	--@USERNAME VARCHAR(30)
)
AS
/*
select * from CLS_DYANAMIC_REPORT_SETTINGS

2,5
*/

DECLARE @@SP_NAME AS VARCHAR(100),
		@@SQL AS VARCHAR(8000),
		@@SP_IN_PARAMS VARCHAR(1000),
		@@PARAMS_DATA VARCHAR(1000),
		@@PARAMS_INDEX VARCHAR(100),
		@@PARAMS_DUMMY VARCHAR(1000)


DECLARE @MIN INT, @MAX INT,@MAX_DB INT, @COUNTCHAR VARCHAR(20), @LOOP INT, @INDEX INT
SET @MIN = 1

SET @@SP_NAME = ''
SET @@SP_IN_PARAMS = ''


IF(@LEVEL = '1')
 BEGIN
	SELECT @@SP_NAME = PROC_NAME_LVL1, @@SP_IN_PARAMS = PROC_PARAMS_LVL1 FROM  CLS_DYANAMIC_REPORT_SETTINGS WHERE CTRL_CODE = @CONTROLCODE AND CTRL_TYPE = 'B' AND PROC_NAME_LVL1 <> ''  
 END
ELSE
 BEGIN
	SELECT @@SP_NAME = PROC_NAME_LVL2, @@SP_IN_PARAMS = PROC_PARAMS_LVL2, @@PARAMS_INDEX = RPT_REDIRECT_PARAM FROM  CLS_DYANAMIC_REPORT_SETTINGS WHERE CTRL_CODE = @CONTROLCODE AND CTRL_TYPE = 'B' 
 END

IF @@SP_NAME = '' 
 SET @@SP_NAME = @@SP_NAME

IF(LEN(@@PARAMS_INDEX) > 0)
 BEGIN
	SET @LOOP = 1
	SET @@PARAMS_DUMMY = ''
	WHILE(1=1)
	 BEGIN
		SET @INDEX = .DBO.PIECE(@@PARAMS_INDEX,',',  @LOOP)
		IF ISNULL(@INDEX,'') = ''
		 BREAK;

		SET @@PARAMS_DUMMY = @@PARAMS_DUMMY + .DBO.PIECE(@PARAMS,'±',  @INDEX) + '±'
		SET @LOOP = @LOOP + 1
	  END
	  SET @PARAMS = LEFT(@@PARAMS_DUMMY, LEN(@@PARAMS_DUMMY)-1)
 END

SELECT @MAX_DB = COUNT(*) FROM  CLS_DYANAMIC_REPORT_SETTINGS WHERE CTRL_CODE = @CONTROLCODE AND CTRL_TYPE <> 'B' 
SELECT @MAX = LEN(@PARAMS ) - LEN(REPLACE(@PARAMS ,'±','')) + 1

--IF (@MAX != @MAX_DB)
-- BEGIN
--	SELECT 'CONFIGURATION MISMATCH OF CONTROL ' + @CONTROLCODE
--	RETURN
-- END

SET @@SQL = ""

SET @@SQL = @@SQL + " EXEC " + @@SP_NAME + " "


IF(LEN(@PARAMS) > 0)
BEGIN
	WHILE(@MIN <= @MAX )
	BEGIN	
		SET @@PARAMS_DATA = .DBO.PIECE(@PARAMS, '±',@MIN) 
		SET @@SQL = @@SQL + '''' + .DBO.PIECE(@PARAMS, '±',@MIN) + '''' + ','
		SET @MIN = @MIN + 1
	END	
 
	SET @@SQL = LEFT(@@SQL, LEN(@@SQL)-1) 
	--SET @@SQL = @@SQL + '''' + @USERNAME + ''''
END 
print @@SQL
EXEC(@@SQL)

GO
