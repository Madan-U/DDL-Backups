-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[Rpt_DelNSEEarlyFile_PHY] (          
@TRANSDATE VARCHAR(11),            
@FROMBATCHNO INT,          
@TOBATCHNO INT,          
@FROMSLIPNO VARCHAR(10),          
@TOSLIPNO VARCHAR(10) ) AS          

DECLARE @MEMBERCODE VARCHAR(5)
DECLARE @SETTCUR	CURSOR,
		@SETT_NO	VARCHAR(7),
		@SETT_TYPE	VARCHAR(2)

SELECT @MEMBERCODE = MEMBERCODE FROM OWNER 

SELECT DISTINCT SETT_NO, SETT_TYPE INTO #DEL 
FROM SETT_MST WHERE SETT_TYPE IN ('F')
AND START_DATE >= @TRANSDATE AND CONVERT(DATETIME,LEFT(SEC_PAYIN,11)) < @TRANSDATE

SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,
	   ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,
	   BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5
INTO #DELEPI
FROM DELTRANS (NOLOCK)
WHERE 1 = 2 

SET @SETTCUR = CURSOR FOR
SELECT SETT_NO, SETT_TYPE FROM #DEL
ORDER BY 1, 2
OPEN @SETTCUR
FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE
WHILE @@FETCH_STATUS = 0  
BEGIN 
	INSERT INTO #DELEPI
	SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,
	ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,
	BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5
	FROM DELTRANS
	WHERE SETT_NO = @SETT_NO
	AND SETT_TYPE = @SETT_TYPE
	AND FILLER2 = 1 
	AND PARTY_CODE <> 'BROKER'

	INSERT INTO #DELEPI
	SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,
	ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,
	BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5
	FROM DELTRANS
	WHERE ISETT_NO = @SETT_NO
	AND ISETT_TYPE = @SETT_TYPE
	AND FILLER2 = 1 AND DRCR = 'D'
	AND PARTY_CODE <> 'BROKER'

	FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE
END
CLOSE @SETTCUR
DEALLOCATE @SETTCUR

SELECT RECTYPE = 20, SETT_NO, SETT_TYPE, SCRIP_CD = UPPER(SCRIP_CD),     
SERIES = UPPER(SERIES), PARTY_CODE, QTY = SUM(QTY)      
INTO #DELTRANSEPN   
FROM #DELEPI D        
WHERE FILLER2 = 1 AND DRCR = 'C' AND CERTNO LIKE 'IN%'        
AND TCODE IN (SELECT TCODE FROM #DELEPI D1        
WHERE D.SETT_NO = D1.SETT_NO
AND D.SETT_TYPE = D1.SETT_TYPE
AND FILLER2 = 1 AND DRCR = 'D' AND TRTYPE = 906 AND DELIVERED <> '0'        
AND ( REASON LIKE 'EARLY%' OR FILLER1 LIKE 'EARLY%' ) AND D.CERTNO = D1.CERTNO        
AND CONVERT(INT,BATCHNO) >= @FROMBATCHNO        
AND CONVERT(INT,BATCHNO) <= @TOBATCHNO        
AND SLIPNO >= @FROMSLIPNO        
AND SLIPNO <= @TOSLIPNO       
AND TRANSDATE LIKE @TRANSDATE + '%')
GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE

INSERT INTO #DELTRANSEPN
SELECT RECTYPE = 20, ISETT_NO, ISETT_TYPE, SCRIP_CD = UPPER(SCRIP_CD),     
SERIES = UPPER(SERIES), PARTY_CODE, QTY = SUM(QTY) 
FROM #DELEPI 
WHERE FILLER2 = 1 AND DRCR = 'D' AND TRTYPE = 1000 
AND DELIVERED <> '0'        
AND ( REASON LIKE 'EARLY%' OR FILLER1 LIKE 'EARLY%' OR HOLDERNAME = 'EARLY PAYIN')
GROUP BY ISETT_NO, ISETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE

SELECT RECTYPE, SCRIP_CD, SERIES='AF', MEMBERCODE = @MEMBERCODE, PARTY_CODE, SETT_TYPE, SETT_NO, QTY = SUM(QTY)   
FROM #DELTRANSEPN  
GROUP BY RECTYPE, SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE  
ORDER BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE

GO
