-- Object: PROCEDURE dbo.DELIVERY_CLIENT_REPORT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[DELIVERY_CLIENT_REPORT]  

AS

  DECLARE @PARTYCODE NVARCHAR(MAX) ,
		  @ORDER_NO NVARCHAR(MAX) ,
		  @BELOW_50K NVARCHAR(MAX), 
		  @ABOVE_50K NVARCHAR(MAX) ,
		  @ACTIVECL NVARCHAR(MAX),
		  @TRADECL NVARCHAR(MAX)

  DECLARE @BODYMSG NVARCHAR(MAX)    
  DECLARE @SUBJECT NVARCHAR(MAX)  
  DECLARE @TABLEHTML NVARCHAR(MAX)  
  DECLARE @FINALCOUNT NVARCHAR(MAX)

SELECT DISTINCT PARTY_CODE,ORDER_NO,TURN_OVER =SUM(Tradeqty*MarketRate),SUM(TRADEQTY*NBrokApp) BROKERAGE INTO #ORD  FROM (
SELECT DISTINCT TMark,Billflag,PARTY_CODE,ORDER_NO,TRADE_NO,NBrokApp,Tradeqty,MarketRate
 FROM SETTLEMENT WITH(NOLOCK) WHERE SAUDA_DATE >= CONVERT(VARCHAR(10),GETDATE(),120) AND Billflag NOT IN ('2','3')
UNION ALL
SELECT DISTINCT TMark,Billflag,PARTY_CODE,ORDER_NO,TRADE_NO,NBrokApp,Tradeqty,MarketRate
 FROM AngelBSECM.BSEDB_AB.DBO.SETTLEMENT WITH(NOLOCK) WHERE SAUDA_DATE >= CONVERT(VARCHAR(10),GETDATE(),120) AND Billflag NOT IN ('2','3') )A
WHERE EXISTS (SELECT PARTY_CODE FROM Del_Zero_Brok_CLIENT D WHERE
CONVERT(VARCHAR(10),GETDATE(),120) BETWEEN FROM_DATE AND TO_DATE AND D.PARTY_CODE =A.Party_Code )
--AND PARTY_CODE IN (SELECT CL_CODE FROM #CL)
GROUP BY PARTY_CODE,ORDER_NO
 
 
 --SELECT * FROM #ORD WHERE TURN_OVER <50000 ORDER BY TURN_OVER
 --SELECT * FROM #ORD WHERE TURN_OVER >50000 ORDER BY TURN_OVER

SELECT DISTINCT CL_CODE INTO #CL  FROM CLIENT_BROK_DETAILS WHERE ACTIVE_DATE >=CONVERT(VARCHAR(10),GETDATE(),120)

 
 SELECT @PARTYCODE=COUNT(DISTINCT PARTY_CODE) FROM #ORD

 SELECT @ORDER_NO=COUNT(DISTINCT Order_no) FROM #ORD

 SELECT @BELOW_50K=SUM(CASE WHEN TURN_OVER <50000 THEN 1 ELSE 0 END) --AS BELOW_50K
 ,
 @ABOVE_50K=SUM(CASE WHEN TURN_OVER >50000 THEN 1 ELSE 0 END) --AS ABOVE_50K
  FROM #ORD

  SELECT @ACTIVECL=COUNT(1) FROM #CL

  SELECT @TRADECL = COUNT(DISTINCT PARTY_CODE ) FROM #ORD
  WHERE PARTY_CODE IN ( SELECT  CL_CODE FROM #CL )
  
  TRUNCATE TABLE AUTO_DEL_REPORT

  INSERT INTO AUTO_DEL_REPORT
  SELECT @PARTYCODE AS P ,@ORDER_NO AS O ,@BELOW_50K AS B50K,@ABOVE_50K AS A50K ,@ACTIVECL AS AC,@TRADECL AS TC

DECLARE @DATE VARCHAR(11)
SET  @DATE = GETDATE()

  
SET @SUBJECT = 'DELIVERY REPORT FOR' + ' '+ @DATE   

SET @TABLEHTML =     

--' Dear Sir, <BR/><BR/> 

--<b> KINDLY FIND THE DELIVERY BELOW STATUS </b> <BR/><BR/> ' 
 
-- +  


N'<STYLE TYPE="TEXT/CSS">   
#BOX-TABLE    
{    
FONT-FAMILY: "LUCIDA SANS UNICODE", "LUCIDA GRANDE", SANS-SERIF;    
FONT-SIZE: 12PX;    
TEXT-ALIGN: CENTER;    
BORDER-COLLAPSE: COLLAPSE;    
BORDER-TOP: 7PX SOLID #9BAFF1;    
BORDER-BOTTOM: 7PX SOLID #9BAFF1;    
}    
#BOX-TABLE TH    
{    
FONT-SIZE: 13PX;    
FONT-WEIGHT: NORMAL;    
BACKGROUND: #4286f4;    
BORDER-RIGHT: 2PX SOLID #9BAFF1;    
BORDER-LEFT: 2PX SOLID #9BAFF1;    
BORDER-BOTTOM: 2PX SOLID #9BAFF1;    
COLOR: #039;    
}    
#BOX-TABLE TD    
{    
BORDER-RIGHT: 1PX SOLID #AABCFE;    
BORDER-LEFT: 1PX SOLID #AABCFE;    
BORDER-BOTTOM: 1PX SOLID #AABCFE;    
COLOR: #669;    
}    
TR:NTH-CHILD(ODD) { BACKGROUND-COLOR:#EEE; }    
TR:NTH-CHILD(EVEN) { BACKGROUND-COLOR:#FFF; }     
</STYLE>'+    

N'<H3><FONT COLOR="BLUE"><b>DELIVERY CLIENT COUNT STATUS FOR  ' + @DATE  + ' </b> </H3>' +  
N'<TABLE BORDER=1 >' +    
N'<TR><TH BGCOLOR = "#00bfff">PARTY_COUNT </TH>
<TH BGCOLOR = "#00bfff"> ORDER_COUNT </TH>   
<TH BGCOLOR = "#00bfff"> BELOW_50K_COUNT </TH>   
<TH BGCOLOR = "#00bfff"> ABOVE_50K_COUNT </TH>
<TH BGCOLOR = "#00bfff"> ACTIVE_PARTY_COUNT</TH>    
<TH BGCOLOR = "#00bfff">TRADED_PARTY_COUNT</TH>
<FONT COLOR="BLUE">         
</TR>' +    


CAST ( (   
   


select TD = [PARTY_COUNT],'', TD = [ORDER_COUNT],'', TD = [BELOW_50K_COUNT] ,'',TD = [ABOVE_50K_COUNT],'',TD = [ACTIVE_PARTY_COUNT],'',TD = [TRADED_PARTY_COUNT],''

 from AUTO_DEL_REPORT 
 
 FOR XML PATH('TR'), TYPE     

) AS NVARCHAR(MAX) ) +  
N'</TABLE>'     



EXEC MSDB.DBO.SP_SEND_DBMAIL    
@PROFILE_NAME ='DBA',
@RECIPIENTS='' ,    
@COPY_RECIPIENTS='',    
@SUBJECT = @SUBJECT,    
@BODY = @TABLEHTML,     
@BODY_FORMAT = 'HTML' ;    


DROP TABLE #CL

DROP TABLE #ORD

GO
