CREATE PROC [dbo].[CNT_Omnesys_UPDATE_BackDate_Processing]
(
@TO_Date DATETIME 
)
AS
BEGIN
 
SET @TO_Date= CONVERT(VARCHAR(10),@TO_Date,120)

IF OBJECT_ID('tempdb..#TRADENXT', 'U') IS NOT NULL
DROP TABLE #TRADENXT
IF OBJECT_ID('tempdb..#TradeNXT_Dealer', 'U') IS NOT NULL
DROP TABLE #TradeNXT_Dealer  

UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE REMARKS IS NOT NULL)
and ORDER_TYPE <> 'ONLINE'


UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE = @TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE REMARKS IS NOT NULL)
and ORDER_TYPE <> 'ONLINE'


UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE REMARKS ='SmartApi')
and ORDER_TYPE <> 'ONLINE'

UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE REMARKS <> '')
and ORDER_TYPE <> 'ONLINE'
 
UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE  USERID IN ('RISKAD','OMSYS1')  AND EXCHG IN ('NFO' ,'NSE','BSE')
AND TIME BETWEEN '15:15' AND '15:24:59'
 )
and ORDER_TYPE <> 'ONLINE'
AND  EXCHANGE IN ('BSE','NSE')

UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE USERID IN ('RISKAD','OMSYS1') AND EXCHG in ('CDS')
AND TIME BETWEEN '16:45' AND '17:00'
 )
and ORDER_TYPE <> 'ONLINE'
AND EXCHANGE ='NSECDS'

UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE USERID IN ('RISKAD','OMSYS1') AND EXCHG in ('MCX' ,'NCDEX')
AND TIME BETWEEN '20:45' AND '21:00'
 )
and ORDER_TYPE <> 'ONLINE'
AND EXCHANGE IN ('MCX' ,'NCDEX')

UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE USERID IN ('RISKAD','OMSYS1') AND EXCHG in ('MCX' ,'NCDEX')
AND TIME BETWEEN '23:15' AND '23:30'
 )
and ORDER_TYPE <> 'ONLINE'
AND EXCHANGE IN ('MCX' ,'NCDEX')

SELECT EXCHG_ORDER_NO INTO #TRADENXT FROM CNT_Omnesys WHERE  REMARKS ='TRADENXT'

SELECT EXCHG_ORDER_NO INTO #TradeNXT_Dealer FROM CNT_Omnesys WHERE  REMARKS ='TradeNXT_Dealer' 

	 CREATE INDEX #T1 ON #TradeNXT_Dealer(EXCHG_ORDER_NO)
 
UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='OFFLINE',PRODUCT_TYPE='TradeNXT_Dealer'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM #TradeNXT_Dealer )
and ORDER_TYPE <>'OFFLINE'

UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM #TRADENXT )
and ORDER_TYPE ='OFFLINE'

UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='OFFLINE',PRODUCT_TYPE='TradeNXT_Dealer'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM #TradeNXT_Dealer )
and ORDER_TYPE <>'OFFLINE'

UPDATE ORDER_TRANS_JV_CALC SET ORDER_TYPE ='ONLINE'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE  USERID IN ('CHIEF','CHIEF1')
 )
and ORDER_TYPE <> 'ONLINE'

UPDATE ORDER_TRANS_JV_CALC SET PRODUCT_TYPE ='RISK-SQ-OFF'
WHERE TRADE_DATE =@TO_Date
AND ORDER_NO IN (SELECT EXCHG_ORDER_NO FROM CNT_Omnesys WHERE  USERID IN ('OMSYS1','MRO14')
 )
and ORDER_TYPE = 'OFFLINE'
AND PRODUCT_TYPE IS NULL

