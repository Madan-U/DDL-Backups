-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE	PROC [dbo].[RPT_HOLD_CLEARING_UPLOAD]
		(
		@FROMDATE	VARCHAR(11),
		@EXCHANGE	VARCHAR(3)  = 'NSE',
		@FROMPARTY	VARCHAR(20) = '',
		@TOPARTY	VARCHAR(20) = 'zzzzzzzzzz'
		)

		AS

		/*
		EXEC	RPT_HOLD_CLEARING_UPLOAD 'JUN 24 2019','NSE','0','ZZZZZ'
		*/
		
		IF @TOPARTY = ''
		BEGIN
			SET @TOPARTY = 'zzzzzzzzzz'
		END

		DECLARE @FILENAME   VARCHAR(50)
		DECLARE @BATCHNO	INT  
		DECLARE @MEMBERCODE VARCHAR(20)
		DECLARE @MEMBERNAME	VARCHAR(500)
		DECLARE @PANNO		VARCHAR(20)

		/*
		DECLARE	@FROMDATE	VARCHAR(11),
				@FROMPARTY	VARCHAR(20),
				@TOPARTY	VARCHAR(20)
				
		SELECT	@FROMDATE = 'JUL  1 2019', @FROMPARTY = '', @TOPARTY = 'zzzzzzzzzz'
		*/

		CREATE	TABLE #HOLD
				(
				CLEARING_MEMBER_ID		VARCHAR(20),
				CLEARING_MEMBER_PAN		VARCHAR(20),
				HOLDING_DATE			VARCHAR(11),
				CLEARING_MEMBER_DEMAT_ACCNO VARCHAR(20),
				CLEARING_MEMBER_ACC_TYPE	VARCHAR(10),
				TRADING_MEMBER_NAME		VARCHAR(100),
				TRADING_MEMBER_PAN		VARCHAR(20),
				UCC_CODE				VARCHAR(20),
				CLIENT_PAN				VARCHAR(20),
				ISIN					VARCHAR(20),
				SECURITY_TYPE			VARCHAR(10),
				TOTAL_QTY				NUMERIC(18,3),
				ACTION					VARCHAR(2)
				)

		INSERT	INTO #HOLD
		SELECT 	CLEARING_MEMBER_ID,
				CLEARING_MEMBER_PAN,
				HOLDING_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE,109),105),
				CLEARING_MEMBER_DEMAT_ACCNO = CLEARING_MEMBER_ACCNO,
				CLEARING_MEMBER_ACC_TYPE = 'CM COLL',
				TRADING_MEMBER_NAME,
				TRADING_MEMBER_PAN,
				UCC_CODE = PARTY_CODE,
				CLIENT_PAN = ISNULL(C1.PAN_GIR_NO,''),
				ISIN = CERTNO,
				SECURITY_TYPE = SERIES,
				TOTAL_QTY = SUM(CRQTY-DRQTY),
				ACTION = 'A'
		FROM	angeldemat.msajag.dbo.TBL_DEL_HOLD_SEC D (NOLOCK),
				angeldemat.msajag.dbo.DELIVERYDP DP (NOLOCK),
				angeldemat.msajag.dbo.CLIENT1 C1 (NOLOCK),
				angeldemat.msajag.dbo.TBL_CLEARING_MEMBER_DETAIL T(NOLOCK)
		WHERE	TRANSDATE <= @FROMDATE
				AND D.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
				AND D.BDPID = DP.DPID
				AND D.BCLTDPID = DP.DPCLTNO
				AND D.PARTY_CODE = C1.CL_CODE
				AND T.CLEARING_MEMBER_ACCNO = (CASE
					WHEN LEN(D.BCLTDPID) >= 16 THEN D.BCLTDPID
					ELSE D.BDPID + D.BCLTDPID
				END)
				--AND SETT_TYPE NOT IN('OS','TS','BX','OB','TK','DL','DX','TX')
				--AND DP.DESCRIPTION LIKE '%COLL%'
				--AND D.EXCHANGE+D.SEGMENT = 'NSEFUTURES'
		GROUP BY
				CLEARING_MEMBER_ID,
				CLEARING_MEMBER_PAN,
				CLEARING_MEMBER_ACCNO,
				TRADING_MEMBER_NAME,
				TRADING_MEMBER_PAN,
				PARTY_CODE,
				ISNULL(C1.PAN_GIR_NO,''),
				CERTNO,
				SERIES
		HAVING	SUM(CRQTY-DRQTY) > 0


		INSERT	INTO #HOLD
		SELECT 	CLEARING_MEMBER_ID,
				CLEARING_MEMBER_PAN,
				HOLDING_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE,109),105),
				CLEARING_MEMBER_DEMAT_ACCNO = CLEARING_MEMBER_ACCNO,
				CLEARING_MEMBER_ACC_TYPE = 'CM COLL',
				TRADING_MEMBER_NAME,
				TRADING_MEMBER_PAN,
				UCC_CODE = PARTY_CODE,
				CLIENT_PAN = ISNULL(C1.PAN_GIR_NO,''),
				ISIN = CERTNO,
				SECURITY_TYPE = SERIES,
				TOTAL_QTY = SUM(CRQTY-DRQTY),
				ACTION = 'A'
		FROM	angeldemat.bsedb.dbo.TBL_DEL_HOLD_SEC D (NOLOCK),
				angeldemat.bsedb.dbo.DELIVERYDP DP (NOLOCK),
				angeldemat.bsedb.dbo.CLIENT1 C1 (NOLOCK),
				angeldemat.msajag.dbo.TBL_CLEARING_MEMBER_DETAIL T(NOLOCK)
		WHERE	TRANSDATE <= @FROMDATE
				AND D.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
				AND D.BDPID = DP.DPID
				AND D.BCLTDPID = DP.DPCLTNO
				AND D.PARTY_CODE = C1.CL_CODE
				AND T.CLEARING_MEMBER_ACCNO = (CASE
					WHEN LEN(D.BCLTDPID) >= 16 THEN D.BCLTDPID
					ELSE D.BDPID + D.BCLTDPID
				END)
				AND SETT_NO <> '' 
				AND SETT_TYPE <> '' 
				--AND SETT_TYPE NOT IN('OS','TS','BX','OB','TK','DL','DX','TX') 
				--AND DP.DESCRIPTION LIKE '%COLL%'
				--AND D.EXCHANGE+D.SEGMENT = 'NSEFUTURES'
		GROUP BY
				CLEARING_MEMBER_ID,
				CLEARING_MEMBER_PAN,
				CLEARING_MEMBER_ACCNO,
				TRADING_MEMBER_NAME,
				TRADING_MEMBER_PAN,
				PARTY_CODE,
				ISNULL(C1.PAN_GIR_NO,''),
				CERTNO,
				SERIES
		HAVING	SUM(CRQTY-DRQTY) > 0


		UPDATE	#HOLD
		SET		SECURITY_TYPE = M.SERIES
		FROM	MULTIISIN M (NOLOCK)
		WHERE	M.ISIN = #HOLD.ISIN
				AND VALID = 1


		UPDATE	#HOLD
		SET		SECURITY_TYPE = (
				CASE
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),2) = 'MF' OR LEFT(ISNULL(ISIN,''),3) = 'INF' THEN 'MF'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'N' AND LEFT(ISNULL(SECURITY_TYPE,''),2) = 'NA' THEN 'NA'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'N' AND LEFT(ISNULL(SECURITY_TYPE,''),2) <> 'NA' THEN 'BOND'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'G' THEN 'DEBT'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'P' THEN 'PREF'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'W' THEN 'WT'
					---WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'I' THEN 'IDR'
					ELSE 'EQ'
				END)


		SELECT	CLEARING_MEMBER_ID		= CONVERT(VARCHAR(50),CLEARING_MEMBER_ID),
				CLEARING_MEMBER_PAN		= CONVERT(VARCHAR(50),CLEARING_MEMBER_PAN),
				HOLDING_DATE			= CONVERT(VARCHAR(50),HOLDING_DATE),
				CLEARING_MEMBER_DEMAT_ACCNO = CONVERT(VARCHAR(50),CLEARING_MEMBER_DEMAT_ACCNO),
				CLEARING_MEMBER_ACC_TYPE	= CONVERT(VARCHAR(50),CLEARING_MEMBER_ACC_TYPE),
				TRADING_MEMBER_NAME		= CONVERT(VARCHAR(50),TRADING_MEMBER_NAME),
				TRADING_MEMBER_PAN		= CONVERT(VARCHAR(50),TRADING_MEMBER_PAN),
				UCC_CODE				= CONVERT(VARCHAR(50),UCC_CODE),
				CLIENT_PAN				= CONVERT(VARCHAR(50),CLIENT_PAN),
				ISIN					= CONVERT(VARCHAR(50),ISIN),
				SECURITY_TYPE			= CONVERT(VARCHAR(50),SECURITY_TYPE),
				TOTAL_QTY				= CONVERT(VARCHAR(50),ROUND(ABS(SUM(TOTAL_QTY)),3)),
				ACTION					= CONVERT(VARCHAR(50),ACTION),
				ORDFLAG					= 2
		FROM	#HOLD
		GROUP BY
				CLEARING_MEMBER_ID,
				CLEARING_MEMBER_PAN,
				HOLDING_DATE,
				CLEARING_MEMBER_DEMAT_ACCNO,
				CLEARING_MEMBER_ACC_TYPE,
				TRADING_MEMBER_NAME,
				TRADING_MEMBER_PAN,
				UCC_CODE,
				CLIENT_PAN,
				ISIN,
				SECURITY_TYPE,
				ACTION
		ORDER BY
				UCC_CODE,
				ISIN				

				
		/*
		INSERT INTO #HOLD_NEW
		SELECT	'Clearing Member ID (Assigned by CC)',
				'Clearing Member PAN',
				'Holding Date',
				'Clearing Member''s Demat Account No.',
				'Clearing Member Account Type',
				'Trading member Name',
				'Trading member PAN',
				'Client UCC',
				'Client PAN',
				'ISIN',
				'Security Type',
				'Total Quantity',
				'Action',
				ORDFLAG=1

		SELECT * FROM #HOLD
		ORDER BY UCC_CODE, ISIN
		*/

		DROP TABLE #HOLD

GO
