-- Object: PROCEDURE dbo.CLS_GET_RPT_EXCHLIST
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE  PROCEDURE [dbo].[CLS_GET_RPT_EXCHLIST]   
(  
 @USERID VARCHAR(25),  
 @RPTCODE VARCHAR(25)  
)  
AS  
DECLARE @LOOPID INT  
 ,@DATA VARCHAR(MAX)  
 ,@EXCHSEG VARCHAR(25)  
  
CREATE TABLE #TEMP (EXCHSEG VARCHAR(500))  
  
 SET @DATA ='0'  
  
 SELECT @DATA = FLDEXCHLIST  
 FROM CLS_TBLUSERREPORT_SETTING WITH (NOLOCK)  
 WHERE FLDREPORTCODE = @RPTCODE     
 AND FLDUSERNAME = @USERID  
 AND ISNULL(FLDUSERNAME, '') <> ''  
 AND ISNULL(FLDEXCHLIST, '') <> ''  
  
 IF @DATA = '0'  
 BEGIN  
  SELECT  @DATA = FLDEXCHLIST  
  FROM CLS_TBLREPORT_SETTING  
  WHERE FLDREPORTCODE = @RPTCODE  
 END  
  
 SET @LOOPID = 1  
  
 WHILE 1 = 1  
 BEGIN  
  SET @EXCHSEG =.DBO.CLS_PIECE(@DATA, ',', @LOOPID)  
  SET @LOOPID = @LOOPID + 1  
  
  IF @EXCHSEG IS NULL  
   OR @EXCHSEG = ''  
  BEGIN  
   BREAK  
  END  

  INSERT INTO #TEMP  
  VALUES (@EXCHSEG)  
 END  
  
 SELECT EXCHSEG = T.EXCHSEG  
  ,P.SEGMENT_DESCRIPTION  
  ,CATEGORY  
 FROM #TEMP T  
  ,PRADNYA..CLS_MULTICOMPANY P  
 WHERE T.EXCHSEG = P.EXCHANGE + '-' + P.SEGMENT  
 AND PRIMARYSERVER = 1  
 ORDER BY P.EXCHANGE  
  ,P.SEGMENT  
  
  
/*------------------------------------- END OF THE PROC --------------------------*/

GO
