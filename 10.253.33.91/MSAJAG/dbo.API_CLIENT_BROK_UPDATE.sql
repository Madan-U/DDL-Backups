-- Object: PROCEDURE dbo.API_CLIENT_BROK_UPDATE
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------




CREATE PROC [dbo].[API_CLIENT_BROK_UPDATE]



(	@CL_CODE	VARCHAR(10),

	@PAYOUTFLAG	INT	

)



--exec API_CLIENT_BROK_UPDATE 'A72999',0



AS

BEGIN
	DECLARE @STATUSFLAG	INT,
		@Msg		VARCHAR(100) 
	SELECT @CL_CODE = LTRIM(RTRIM(@CL_CODE))
	IF (SELECT ISNULL(COUNT(1),0) FROM CLIENT_BROK_DETAILS WITH (NOLOCK)
			WHERE CL_CODE = @CL_CODE AND SEGMENT = 'CAPITAL'
	   ) > 0
	BEGIN   
		IF @PAYOUTFLAG >= 0 AND @PAYOUTFLAG <= 2
		BEGIN 

		 

		   INSERT INTO CL_PAYOUT_FLAG_AUDIT  
		   SELECT CL_CODE,DEBIT_BALANCE,@PAYOUTFLAG,GETDATE(),EXCHANGE,SEGMENT FROM CLIENT_BROK_DETAILS 
			WHERE CL_CODE = @CL_CODE AND SEGMENT = 'CAPITAL'
		    
			INSERT INTO CLIENT_BROK_DETAILS_LOG
			SELECT TOP 10 CL_CODE,EXCHANGE,SEGMENT,BROK_SCHEME,TRD_BROK,DEL_BROK,
				   SER_TAX,SER_TAX_METHOD,CREDIT_LIMIT,INACTIVE_FROM,PRINT_OPTIONS,
				   NO_OF_COPIES,PARTICIPANT_CODE,CUSTODIAN_CODE,INST_CONTRACT,ROUND_STYLE,
				   STP_PROVIDER,STP_RP_STYLE,MARKET_TYPE,MULTIPLIER,CHARGED,MAINTENANCE,
				   REQD_BY_EXCH,REQD_BY_BROKER,CLIENT_RATING,DEBIT_BALANCE,INTER_SETT,
				   TRD_STT,TRD_TRAN_CHRGS,TRD_SEBI_FEES,TRD_STAMP_DUTY,TRD_OTHER_CHRGS,
				   TRD_EFF_DT,DEL_STT,DEL_TRAN_CHRGS,DEL_SEBI_FEES,DEL_STAMP_DUTY,DEL_OTHER_CHRGS,
				   DEL_EFF_DT,ROUNDING_METHOD,ROUND_TO_DIGIT,ROUND_TO_PAISE,FUT_BROK,FUT_OPT_BROK,
				   FUT_FUT_FIN_BROK,FUT_OPT_EXC,FUT_BROK_APPLICABLE,FUT_STT,FUT_TRAN_CHRGS,FUT_SEBI_FEES,
				   FUT_STAMP_DUTY,FUT_OTHER_CHRGS,STATUS,MODIFIEDON,MODIFIEDBY,IMP_STATUS,PAY_B3B_PAYMENT,
				   PAY_BANK_NAME,PAY_BRANCH_NAME,PAY_AC_NO,PAY_PAYMENT_MODE,BROK_EFF_DATE,INST_TRD_BROK,
				   INST_DEL_BROK,EDIT_BY='API',EDIT_ON=GETDATE(),SYSTEMDATE,ACTIVE_DATE,CHECKACTIVECLIENT,DEACTIVE_REMARKS,
				   DEACTIVE_VALUE
			FROM CLIENT_BROK_DETAILS 
			WHERE CL_CODE = @CL_CODE AND SEGMENT = 'CAPITAL'

			UPDATE CLIENT_BROK_DETAILS 
			SET DEBIT_BALANCE = @PAYOUTFLAG, IMP_STATUS = 0, MODIFIEDON = GETDATE(), MODIFIEDBY = 'API'
			WHERE CL_CODE = @CL_CODE AND SEGMENT = 'CAPITAL'

			SET @STATUSFLAG = 1
			SET @Msg = 'Record Updated Successfully'

		END
		ELSE
		BEGIN
			SET @STATUSFLAG = -1
			SET @Msg = 'Invalid Parameter'
		END
	END
	ELSE
	BEGIN
		SET @STATUSFLAG = -1
		SET @Msg = 'Client does not exist'
	END
		
	SELECT @STATUSFLAG, @Msg

END

GO
