CREATE PROCEDURE [dbo].[CLS_DELAUCTIONPROV] (
	@STATUSID VARCHAR(25),
	@STATUSNAME VARCHAR(25),
	@SETT_NO VARCHAR(25),
	@SETT_TYPE VARCHAR(2),
	@FROMPARTY VARCHAR(25),
	@TOPARTY VARCHAR(25)
	)
AS
--EXEC [CLS_DELAUCTIONPROV] 'BROKER','BROKER','2014105','A','0','ZZZZZ'
--EXEC CLS_DELAUCTIONPROV 'BROKER','BROKER','2006030','A','0','ZZZZZ'
DECLARE @REMARK VARCHAR(15)

SELECT @REMARK = 'PROVISIONAL'

SELECT S.SETT_NO,
	S.SETT_TYPE,
	PAYIN = CONVERT(VARCHAR, START_DATE, 103),
	SAUDA_DATE = CONVERT(VARCHAR, START_DATE, 103),
	PAYOUT = CONVERT(VARCHAR, END_DATE, 103),
	C2.PARTY_CODE,
	C1.LONG_NAME,
	C1.L_ADDRESS1,
	C1.L_ADDRESS2,
	C1.L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		Email,
	BILLNO,
	SCRIPNAME = A.SCRIP_CD,
	QTY = SUM(TRADEQTY),
	AMOUNT = SUM(TRADEQTY * N_NETRATE) + SUM(CASE WHEN SERVICE_CHRG = 1 THEN NSERTAX ELSE 0 END) + SUM(CASE WHEN AUCTIONPART LIKE 'F%' THEN A.OTHER_CHRG ELSE 0 END),
	MARKETRATE = ROUND((SUM(TRADEQTY * N_NETRATE) + SUM(CASE WHEN SERVICE_CHRG = 1 THEN NSERTAX ELSE 0 END) + SUM(CASE WHEN AUCTIONPART LIKE 'F%' THEN A.OTHER_CHRG ELSE 0 END)) / SUM(TRADEQTY), 4),
	SELL_BUY,
	SERVICE_TAX = SUM(CASE WHEN SERVICE_CHRG = 0 THEN NSERTAX ELSE 0 END),
	INS_CHRG = SUM(CASE WHEN C2.INSURANCE_CHRG = 1 THEN INS_CHRG ELSE 0 END),
	TURN_TAX = SUM(CASE WHEN TURNOVER_TAX = 1 THEN TURN_TAX ELSE 0 END),
	OTHER_CHRG = SUM(CASE WHEN C2.OTHER_CHRG = 1 THEN (CASE WHEN AUCTIONPART NOT LIKE 'F%' THEN A.OTHER_CHRG ELSE 0 END) ELSE 0 END),
	SABI_TAX = SUM(CASE WHEN SEBI_TURN_TAX = 1 THEN SEBI_TAX ELSE 0 END),
	BROKER_CHRG = CONVERT(NUMERIC(18,2),0),
	REMARK = @REMARK, /*SUM(CASE WHEN BROKERNOTE = 1 THEN BROKER_CHRG ELSE 0 END)*/
	PENALTY = SUM(CASE WHEN AUCTIONPART LIKE 'AP%' THEN A.OTHER_CHRG ELSE 0 END),
	AUCTIONPART,
	PENALTYPER = AUCTIONCHRG,
	NETAMOUNT = CONVERT(NUMERIC(18,2),0)
INTO #AUCTION
FROM CLIENT2 C2,
	CLIENT1 C1,
	SETTLEMENT A,
	SETT_MST S,
	DELSEGMENT
WHERE S.SETT_NO = A.SETT_NO
	AND S.SETT_TYPE = A.SETT_TYPE
	AND C1.CL_CODE = C2.CL_CODE
	AND C2.PARTY_CODE = A.PARTY_CODE
	--AND MARKETRATE > 0 AND TRADEQTY > 0           
	AND A.SETT_NO = @SETT_NO
	AND A.SETT_TYPE = @SETT_TYPE
	AND A.PARTY_CODE >= @FROMPARTY
	AND A.PARTY_CODE <= @TOPARTY
	AND AUCTIONPART LIKE 'AP'
	AND @STATUSNAME = (CASE WHEN @STATUSID = 'BRANCH' THEN C1.BRANCH_CD WHEN @STATUSID = 'SUBBROKER' THEN C1.SUB_BROKER WHEN @STATUSID = 'TRADER' THEN C1.TRADER WHEN @STATUSID = 'FAMILY' THEN C1.FAMILY WHEN @STATUSID = 'AREA' THEN C1.AREA WHEN @STATUSID = 'REGION' THEN C1.REGION WHEN @STATUSID = 'CLIENT' THEN C2.PARTY_CODE ELSE 'BROKER' END)
GROUP BY S.SETT_NO,
	S.SETT_TYPE,
	START_DATE,
	END_DATE,
	C2.PARTY_CODE,
	C1.LONG_NAME,
	C1.L_ADDRESS1,
	C1.L_ADDRESS2,
	C1.L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		Email,
	BILLNO,
	A.SCRIP_CD,
	SELL_BUY,
	AUCTIONPART,
	AUCTIONCHRG

INSERT INTO #AUCTION
SELECT S.SETT_NO,
	S.SETT_TYPE,
	PAYIN = CONVERT(VARCHAR, START_DATE, 103),
	SAUDA_DATE = CONVERT(VARCHAR, START_DATE, 103),
	PAYOUT = CONVERT(VARCHAR, END_DATE, 103),
	C2.PARTY_CODE,
	C1.LONG_NAME,
	C1.L_ADDRESS1,
	C1.L_ADDRESS2,
	C1.L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		Email,
	BILLNO,
	SCRIPNAME = A.SCRIP_CD,
	QTY = SUM(TRADEQTY),
	AMOUNT = SUM(TRADEQTY * N_NETRATE) + SUM(CASE WHEN SERVICE_CHRG = 1 THEN NSERTAX ELSE 0 END) + SUM(CASE WHEN AUCTIONPART LIKE 'F%' THEN A.OTHER_CHRG ELSE 0 END),
	MARKETRATE = ROUND((SUM(TRADEQTY * N_NETRATE) + SUM(CASE WHEN SERVICE_CHRG = 1 THEN NSERTAX ELSE 0 END) + SUM(CASE WHEN AUCTIONPART LIKE 'F%' THEN A.OTHER_CHRG ELSE 0 END)) / SUM(TRADEQTY), 4),
	SELL_BUY,
	SERVICE_TAX = SUM(CASE WHEN SERVICE_CHRG = 0 THEN NSERTAX ELSE 0 END),
	INS_CHRG = SUM(CASE WHEN C2.INSURANCE_CHRG = 1 THEN INS_CHRG ELSE 0 END),
	TURN_TAX = SUM(CASE WHEN TURNOVER_TAX = 1 THEN TURN_TAX ELSE 0 END),
	OTHER_CHRG = SUM(CASE WHEN C2.OTHER_CHRG = 1 THEN (CASE WHEN AUCTIONPART NOT LIKE 'F%' THEN A.OTHER_CHRG ELSE 0 END) ELSE 0 END),
	SABI_TAX = SUM(CASE WHEN SEBI_TURN_TAX = 1 THEN SEBI_TAX ELSE 0 END),
	BROKER_CHRG = 0,
	REMARK = @REMARK, /*SUM(CASE WHEN BROKERNOTE = 1 THEN BROKER_CHRG ELSE 0 END)*/
	PENALTY = SUM(CASE WHEN AUCTIONPART LIKE 'AP%' THEN A.OTHER_CHRG ELSE 0 END),
	AUCTIONPART,
	PENALTYPER = AUCTIONCHRG,NETAMOUNT=0
FROM CLIENT2 C2,
	CLIENT1 C1,
	HISTORY A,
	SETT_MST S,
	DELSEGMENT
WHERE S.SETT_NO = A.SETT_NO
	AND S.SETT_TYPE = A.SETT_TYPE
	AND C1.CL_CODE = C2.CL_CODE
	AND C2.PARTY_CODE = A.PARTY_CODE
	AND MARKETRATE > 0
	AND TRADEQTY > 0
	AND A.SETT_NO = @SETT_NO
	AND A.SETT_TYPE = @SETT_TYPE
	AND A.PARTY_CODE >= @FROMPARTY
	AND A.PARTY_CODE <= @TOPARTY
	AND AUCTIONPART LIKE 'AP'
	AND @STATUSNAME = (CASE WHEN @STATUSID = 'BRANCH' THEN C1.BRANCH_CD WHEN @STATUSID = 'SUBBROKER' THEN C1.SUB_BROKER WHEN @STATUSID = 'TRADER' THEN C1.TRADER WHEN @STATUSID = 'FAMILY' THEN C1.FAMILY WHEN @STATUSID = 'AREA' THEN C1.AREA WHEN @STATUSID = 'REGION' THEN C1.REGION WHEN @STATUSID = 'CLIENT' THEN C2.PARTY_CODE ELSE 'BROKER' END)
GROUP BY S.SETT_NO,
	S.SETT_TYPE,
	START_DATE,
	END_DATE,
	C2.PARTY_CODE,
	C1.LONG_NAME,
	C1.L_ADDRESS1,
	C1.L_ADDRESS2,
	C1.L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		Email,
	BILLNO,
	A.SCRIP_CD,
	SELL_BUY,
	AUCTIONPART,
	AUCTIONCHRG

ALTER TABLE #AUCTION ADD DEBITAMOUNT MONEY
ALTER TABLE #AUCTION ADD CREDITAMOUNT MONEY
--ALTER TABLE #AUCTION ADD NETAMOUNT MONEY



SELECT [SETT_NO] = SETT_NO,
	SETT_TYPE,
	PAYIN,
	SAUDA_DATE,
	PAYOUT,
	PARTY_CODE,
	LONG_NAME,
	L_ADDRESS1,
	L_ADDRESS2,
	L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		Email,
	BILLNO,
	SCRIPNAME,
	QTY,
	AMOUNT,
	MARKETRATE,
	SELL_BUY,
	SERVICE_TAX,
	INS_CHRG,
	TURN_TAX,
	OTHER_CHRG,
	SABI_TAX,
	BROKER_CHRG,
	REMARK,
	PENALTY,
	AUCTIONPART,
	PENALTYPER,
	DEBITAMOUNT = (CASE WHEN SELL_BUY = 1 THEN AMOUNT ELSE 0 END),
	CREDITAMOUNT = (CASE WHEN SELL_BUY = 2 THEN -AMOUNT ELSE 0 END),
	NETAMOUNT,
	REC_TYPE = 1
INTO #AUCTION1
FROM #AUCTION


SELECT [SETT_NO] = SETT_NO,
	SETT_TYPE,
	PAYIN,
	SAUDA_DATE,
	PAYOUT,
	PARTY_CODE,
	LONG_NAME,
	L_ADDRESS1,
	L_ADDRESS2,
	L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		Email,
	BILLNO,
	SCRIPNAME,
	QTY,
	AMOUNT,
	MARKETRATE=CONVERT(NUMERIC(18,4),ROUND(MARKETRATE,4)),
	SELL_BUY,
	SERVICE_TAX,
	INS_CHRG,
	TURN_TAX,
	OTHER_CHRG,
	SABI_TAX,
	BROKER_CHRG,
	REMARK,
	PENALTY,
	AUCTIONPART,
	PENALTYPER,
	DEBITAMOUNT=CONVERT(NUMERIC(18,2),ROUND(DEBITAMOUNT,2)),
	CREDITAMOUNT=CONVERT(NUMERIC(18,2),ROUND(CREDITAMOUNT,2)),
	NETAMOUNT = CONVERT(NUMERIC(18,2),0),
	REC_TYPE = 1
INTO #AUCTION2
FROM #AUCTION1

INSERT INTO 
#AUCTION2
SELECT SETT_NO,
	SETT_TYPE = '',
	PAYIN = '',
	SAUDA_DATE = '',
	PAYOUT = '',
	PARTY_CODE,
	LONG_NAME,
	L_ADDRESS1,
	L_ADDRESS2,
	L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		Email,
	[BILLNO] = '',
	[SCRIPNAME] = 'TOTAL',
	QTY = SUM(QTY),
	AMOUNT = SUM(AMOUNT),
	[MARKETRATE] =0,
	[SELL_BUY] = '',
	SERVICE_TAX = SUM(SERVICE_TAX),
	INS_CHRG = SUM(INS_CHRG),
	TURN_TAX = SUM(TURN_TAX),
	OTHER_CHRG = SUM(OTHER_CHRG),
	SABI_TAX = SUM(SABI_TAX),
	BROKER_CHRG = 0,
	REMARK = '',
	PENALTY = 0,
	AUCTIONPART = '',
	PENALTYPER = 0,
	DEBITAMOUNT = CONVERT(NUMERIC(18,2),ROUND(SUM(DEBITAMOUNT),2)), 
	CREDITAMOUNT =CONVERT(NUMERIC(18,2),ROUND(SUM(CREDITAMOUNT),2)), 
	NETAMOUNT=0,
	REC_TYPE = 2	
FROM #AUCTION2
GROUP BY PARTY_CODE,
	LONG_NAME,
	L_ADDRESS1,
	L_ADDRESS2,
	L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		Email,
	SETT_NO
ORDER BY PARTY_CODE,
	REC_TYPE
	
UPDATE	#AUCTION2
SET		NETAMOUNT   = A.NETAMOUNT,
		SERVICE_TAX = A.SERVICE_TAX,
		INS_CHRG    = A.INS_CHRG,
		TURN_TAX	= A.TURN_TAX,
		OTHER_CHRG	= A.OTHER_CHRG,
		SABI_TAX	= A.SABI_TAX
FROM	
(
SELECT 
		NETAMOUNT = (SUM(DEBITAMOUNT) + SUM(CREDITAMOUNT)) + (SUM(SERVICE_TAX) + SUM(INS_CHRG) + SUM(TURN_TAX) + SUM(SABI_TAX) + SUM(OTHER_CHRG)),
		SERVICE_TAX = SUM(SERVICE_TAX),
		INS_CHRG = SUM(INS_CHRG),
		TURN_TAX = SUM(TURN_TAX),
		OTHER_CHRG = SUM(OTHER_CHRG),
		SABI_TAX = SUM(SABI_TAX),
		PARTY_CODE
FROM    #AUCTION2
GROUP BY PARTY_CODE,
		LONG_NAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_STATE,
		L_ZIP,
			
		SETT_NO,
		REC_TYPE
) A
WHERE	A.PARTY_CODE = #AUCTION2.PARTY_CODE



SELECT SETT_NO,
	SETT_TYPE,
	PAYIN,
	SAUDA_DATE,
	PAYOUT,
	PARTY_CODE,
	LONG_NAME,
	L_ADDRESS1,
	L_ADDRESS2,
	L_ADDRESS3,
	L_CITY,
	L_STATE,
	L_ZIP,
		Res_Phone1,
		Res_Phone2,
		Off_Phone1,
		Off_Phone2,
		a.Email,
	BILLNO,
	SCRIPNAME,
	QTY,
	AMOUNT,
	MARKETRATE,
	SELL_BUY,
	SERVICE_TAX,
	INS_CHRG,
	TURN_TAX,
	OTHER_CHRG,
	SABI_TAX,
	BROKER_CHRG,
	REMARK,
	PENALTY,
	AUCTIONPART,
	PENALTYPER,
	DEBITAMOUNT,
	CREDITAMOUNT,
	NETAMOUNT,
	REC_TYPE,
	COMPANY,ADDR1,ADDR2,PHONE,ZIP,CITY,
	COMPLIANCE_DETAILS = ' Compliance Officer: '+ ISNULL(COMPLIANCE_NAME,'') +'(Email id: '+ ISNULL(COMPLIANCE_EMAIL,'') +', Tel No.: '+ ISNULL(COMPLIANCE_PHONE,'') +')',
	PdfFileName = 'Auction_' + ltrim(rtrim(PARTY_CODE))+ '_' +CONVERT(VARCHAR(11),CONVERT(DATETIME, SAUDA_DATE, 103), 111)
FROM #AUCTION2 as a,
	OWNER
WHERE DEBITAMOUNT+CREDITAMOUNT<>0
ORDER BY PARTY_CODE,
	REC_TYPE

GO
