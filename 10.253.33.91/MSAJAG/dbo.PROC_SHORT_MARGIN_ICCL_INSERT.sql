CREATE PROC [dbo].[PROC_SHORT_MARGIN_ICCL_INSERT]
(
	@MARGINDATE	VARCHAR(11),
	@FILENAME	VARCHAR(500),
	@UNAME		VARCHAR(50) = 'AUTO'
)
AS
DECLARE @EXCHANGE	VARCHAR(3),
		@SEGMENT	VARCHAR(7)
		 
DELETE FROM TBL_SHORT_ALLOC_ICCL 
WHERE MARGINDATE = @MARGINDATE 

INSERT INTO TBL_SHORT_ALLOC_ICCL
SELECT MARGINDATE=LEFT(MARGINDATE,11),CMCODE,TMCODE,CLIENT_CODE,CPCODE,TOTAL_COLL,MARGIN_REQ,EOD_SHORT_ALLOC,
	   INDICATOR,UPLOAD_BY=@UNAME,UPLOAD_ON=GETDATE()
FROM TBL_SHORT_ALLOC_ICCL_TMP

GO
