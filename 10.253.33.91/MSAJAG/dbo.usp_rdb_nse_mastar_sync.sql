FROM TBL_ADDITIONAL_TAX_DATA_RTB  WHERE SAUDA_DATE=@DATE   AND SETT_TYPE  IN ('N','W','M','Z')
DELETE TBL_ADDITIONAL_TAX_DATA WHERE  SAUDA_DATE= @DATE
INSERT INTO TBL_ADDITIONAL_TAX_DATA
SELECT PARTY_CODE,SAUDA_DATE,SETT_NO,SETT_TYPE,CONTRACTNO,ORDER_NO,TRADE_NO,SCRIP_CD,SERIES,MARKETRATE,TRADEQTY,AMOUNT,SELL_BUY,TRDTYPE,IPFT_CHRG,
ADD_CHRG_1,ADD_CHRG_2,ADD_CHRG_3,ADD_CHRG_4,ADD_CHRG_5 FROM #IPFT
