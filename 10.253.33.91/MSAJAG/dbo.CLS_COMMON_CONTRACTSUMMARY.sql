CREATE PROC [dbo].[CLS_COMMON_CONTRACTSUMMARY] (
	@STATUSID VARCHAR(15),
	@STATUSNAME VARCHAR(25),
	@FROMDATE VARCHAR(11),
	@TODATE VARCHAR(11),
	@FROMPARTY VARCHAR(20),
	@TOPARTY VARCHAR (20),
	@FROMBRANCH VARCHAR(20),
	@TOBRANCH VARCHAR (20),
	@FROMSUBBROKER VARCHAR(20),
	@TOSUBBROKER VARCHAR (20),
	@FROMCONTRACT VARCHAR(10),
	@TOCONTRACT VARCHAR(10),
	@CONTFLAG VARCHAR(10),
	@EXCHANGE VARCHAR(15),
	@BOUNCEDFLAG INT = 0
	)

	AS
	
	/*
	EXEC CLS_COMMON_CONTRACTSUMMARY 'BROKER','BROKER','07/02/2018','07/02/2018','','ZZZZZZ','','ZZZZZZZZZZZ','','ZZZZZZZZZZ','','99999999999999','','NSEFUTURES',0
	exec msajag.dbo.[cls_common_contractsummary] @STATUSID='Broker',@STATUSNAME='Broker',@FROMDATE='25/06/2000',@TODATE='25/06/2015',@FROMPARTY='0a141',@TOPARTY='0a141',@FROMBRANCH='',@TOBRANCH='',@FROMSUBBROKER='',@TOSUBBROKER='',@FROMCONTRACT='0',@TOCONTRACT='99999999999',@CONTFLAG='',@EXCHANGE='',@BOUNCEDFLAG='0'
	*/

	IF LEN(@FROMDATE) = 10 AND CHARINDEX('/', @FROMDATE) > 0
	BEGIN
		SET @FROMDATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @FROMDATE, 103), 109)
	END

	IF LEN(@TODATE) = 10 AND CHARINDEX('/', @TODATE) > 0
	BEGIN
		SET @TODATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @TODATE, 103), 109)
	END


	IF @TOPARTY = ''
	BEGIN
		SET @TOPARTY = 'ZZZZZZZZZZZ'
	END

	IF @TOBRANCH = ''
	BEGIN
		SET @TOBRANCH = 'ZZZZZZZZZZZ'
	END

	IF @TOSUBBROKER = ''
	BEGIN
		SET @TOSUBBROKER = 'ZZZZZZZZZZZ'
	END

	IF @TOCONTRACT = ''
	BEGIN
		SET @TOCONTRACT = '999999999999'
	END
	

	DECLARE @CONTRACT_TYPE		INT
	DECLARE @FNO_CONTRACT_BILL	INT,
			@STTFLAG			INT,
			@SERFLAG			INT


	SET @CONTRACT_TYPE = 0
	SET @FNO_CONTRACT_BILL = 0
	SET @STTFLAG = 0
	SET @SERFLAG = 0




	SELECT 
		@CONTRACT_TYPE = CONTRACT_TYPE,
		@FNO_CONTRACT_BILL = FNO_CONTRACT_BILL,
		@STTFLAG = 0

	FROM TBL_COMMONCONTRACT_MASTER(NOLOCK)
	WHERE @FROMDATE BETWEEN EFFECTIVE_FROM AND EFFECTIVE_TO

	CREATE TABLE #PARTY (PARTYCODE VARCHAR(15), SAUDA_DATE DATETIME)

	IF @BOUNCEDFLAG = 0
	BEGIN
		INSERT INTO #PARTY
		SELECT DISTINCT PARTY_CODE, SAUDA_DATE=LEFT(SAUDA_DATE,11)
		FROM COMMON_CONTRACT_DATA (NOLOCK)
		WHERE SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'
			AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
	END
	ELSE
	BEGIN
		IF (@EXCHANGE = '') 
		BEGIN
			INSERT INTO #PARTY
			SELECT DISTINCT PARTY_CODE, Sdate=LEFT(Sdate,11)
			FROM TBL_ECNBOUNCED (NOLOCK)
			WHERE Sdate BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'
			--AND EXCHANGE = 'ALLEQUITY'
		END	
		ELSE
		BEGIN
			INSERT INTO #PARTY
			SELECT DISTINCT PARTY_CODE, Sdate=LEFT(Sdate,11)
			FROM TBL_ECNBOUNCED (NOLOCK)
			WHERE Sdate BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'
			/*AND EXCHANGE = (
			CASE
				WHEN @EXCHANGE = 'NSECAPITAL' THEN 'NSECM'
				WHEN @EXCHANGE = 'BSECAPITAL' THEN 'BSECM'
				WHEN @EXCHANGE = 'NSEFUTURES' THEN 'NSEFO'
				ELSE ''
			END)
			*/
		END	
	END

	


	SET NOCOUNT ON
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	SELECT 
		SAUDA_DATE = CONVERT(VARCHAR,C.SAUDA_DATE,103),
		PARTY_CODE,
		PARTYNAME,
		EXCHANGE,
		SEGMENT,
		CONTRACTNO = CONTRACTNO_NEW,
		SETT_TYPE,
		SETT_NO,
		NETAMOUNT = (
		CASE
			WHEN SEGMENT = 'FUTURES' THEN MAX(NETOBLIGATION)-SUM(SERVICE_TAX+INS_CHRG+SEBI_TAX+TURN_TAX+BROKER_CHRG+OTHER_CHRG)
			ELSE SUM(NETAMOUNTALL) 
		END)
	INTO 
		#CONTRACT
	FROM 
		COMMON_CONTRACT_DATA C (NOLOCK),
		CLIENT_PRINT_SETTINGS CP (NOLOCK),
		#PARTY P
	WHERE
		C.SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'
		AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		AND BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
		AND SUB_BROKER BETWEEN @FROMSUBBROKER AND @TOSUBBROKER
		AND CONTRACTNO_NEW BETWEEN @FROMCONTRACT AND @TOCONTRACT
		AND C.PARTY_CODE = P.PARTYCODE
		AND LEFT(C.SAUDA_DATE,11) = LEFT(P.SAUDA_DATE,11)
		AND C.PRINTF = CP.PRINT_FLAG
		AND CP.VALID_REPORT LIKE (CASE WHEN @BOUNCEDFLAG = 0 THEN '%' + @CONTFLAG + '%' ELSE CP.VALID_REPORT END)
		AND EXCHANGE + SEGMENT = (CASE WHEN @EXCHANGE <> '' THEN @EXCHANGE ELSE EXCHANGE + SEGMENT END)
		AND @STATUSNAME = (
		CASE 
			WHEN @STATUSID = 'BRANCH' THEN BRANCH_CD 
			WHEN @STATUSID = 'SUBBROKER' THEN SUB_BROKER 
			WHEN @STATUSID = 'TRADER'	THEN TRADER 
			WHEN @STATUSID = 'FAMILY' THEN FAMILY 
			WHEN @STATUSID = 'AREA' THEN AREA 
			WHEN @STATUSID = 'REGION' THEN REGION 
			WHEN @STATUSID = 'CLIENT' THEN PARTY_CODE 
			ELSE 'BROKER' 
		END)		
	GROUP BY
		CONVERT(VARCHAR,C.SAUDA_DATE,103),
		PARTY_CODE,
		PARTYNAME,
		EXCHANGE,
		SEGMENT,
		CONTRACTNO_NEW,
		SETT_TYPE,
		SETT_NO

	
	IF (@EXCHANGE <> '')
	BEGIN
		UPDATE	#CONTRACT
		SET		CONTRACTNO = ISNULL(A.CONTRACTNO,'0')
		FROM	(	SELECT	SAUDA_DATE,
							PARTY_CODE,
							EXCHANGE,
							SEGMENT,
							CONTRACTNO,
							SETT_TYPE,
							SETT_NO
					FROM	#CONTRACT	
					WHERE	CONTRACTNO <> 0
					GROUP BY
							SAUDA_DATE,
							PARTY_CODE,
							EXCHANGE,
							SEGMENT,
							CONTRACTNO,
							SETT_TYPE,
							SETT_NO
				) A
		WHERE	A.SAUDA_DATE = #CONTRACT.SAUDA_DATE
				AND A.PARTY_CODE = #CONTRACT.PARTY_CODE
				AND A.EXCHANGE = #CONTRACT.EXCHANGE
				AND A.SEGMENT = #CONTRACT.SEGMENT
				AND A.SETT_TYPE = #CONTRACT.SETT_TYPE
				AND A.SETT_NO = #CONTRACT.SETT_NO
				AND #CONTRACT.CONTRACTNO = 0
	END

	/*
	DECLARE @COLS AS VARCHAR(1000),
			@SQL  AS NVARCHAR(1000)
			
	/*
	SET @COLS = STUFF((SELECT DISTINCT ',' + C.EXCHANGE+C.SEGMENT 
				FROM #CONTRACT C
				FOR XML PATH(''), TYPE
				).VALUE('.', 'NVARCHAR(MAX)'),1,1,'')
	*/

	SET @COLS = STUFF((
					SELECT DISTINCT ',' + EXCHANGE + SEGMENT
					FROM #CONTRACT C
					ORDER BY 1
					FOR XML PATH('')
					), 1, 1, '')

				
	SET @SQL = 'SELECT SAUDA_DATE,CLIENT_CODE,PARTYNAME,CONTRACTNO,'+ ISNULL(@COLS,'-') + ' FROM '
	SET @SQL = @SQL + ' (SELECT SAUDA_DATE,CLIENT_CODE=PARTY_CODE,PARTYNAME,CONTRACTNO,[EXCHANGE]=EXCHANGE+SEGMENT,'
	SET @SQL = @SQL + ' NETAMOUNT=(CASE WHEN NETAMOUNT IS NOT NULL  THEN CONVERT(NUMERIC(20,2),ROUND(ISNULL(NETAMOUNT,0),2)) ELSE 0 END) FROM #CONTRACT) AS S '
	SET @SQL = @SQL + ' PIVOT '
	SET @SQL = @SQL + ' ( '
	SET @SQL = @SQL + '     SUM(NETAMOUNT) '
	SET @SQL = @SQL + '     FOR [EXCHANGE] IN ('+ @COLS + ') '
	SET @SQL = @SQL + ' )AS PVT '
	SET @SQL = @SQL + ' ORDER BY '
	SET @SQL = @SQL + ' 	CLIENT_CODE,'
	SET @SQL = @SQL + ' 	CONTRACTNO '
	EXEC (@SQL)
	*/
	
	SELECT
		DISTINCT
		SAUDA_DATE,
		CLIENT_CODE = PARTY_CODE,
		CLIENT_NAME = PARTYNAME,
		CONTRACTNO
	FROM
		#CONTRACT
	ORDER BY
		CLIENT_CODE,
		CONTRACTNO

	DROP TABLE #CONTRACT

GO
