-- Object: PROCEDURE dbo.RPT_EXCHANGE_PEAK_MARGIN
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[RPT_EXCHANGE_PEAK_MARGIN] (@RUNDATE VARCHAR(11),@PARTY VARCHAR(11),@RPT VARCHAR(11))

AS ---- EXEC RPT_EXCHANGE_PEAK_MARGIN 'AUG 27 2021','GRMB131','NSECM'

IF @RPT = 'NSECM'
BEGIN
SELECT [Trade Date]=CONVERT(VARCHAR(11),A.MARGINDATE,105),[Client Code]=PARTY_CODE,[ VAR + Extreme Loss Margin]=A.VARMARGIN,
[Minimum Margin]=A.MINIMUM_MARGIN,[ Additional Margin]=A.ADDITIONAL_MARGIN,
[MTM Loss]=A.MTOM_LOSS,[Total Actual Margin]=ISNULL(A.TOTAL_MARGIN_VAR, 0),[Margin To Be Collected]=ISNULL(A.TOTAL_MARGIN, 0),
[Client Flag]=CL_FLAG,[File No]=FILECOUNTER,Exchange='NSECM'
FROM MSAJAG.DBO.TBL_CMMARGIN_INTRADAY A WITH (NOLOCK)
WHERE MARGINDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
AND A.PARTY_CODE = @PARTY
ORDER BY PARTY_CODE,A.FILECOUNTER DESC
END

IF @RPT = 'NSEFO'
BEGIN
SELECT [Trade Date]=CONVERT(VARCHAR(11),MDATE,105),[Client Code]=PARTY_CODE,
[Span Margin]=SPANMARGIN,Filler,[Extreme Loss Margin]=EXTREAMELOSSMARGIN,[Delivery Margin]=DEL_MARGIN,
[Crystallized Obligation]=MTOMLOSS,[Total Margin]=TOTALMARGIN,[Client Flag]=CL_TYPE,[File No]=FILECOUNTER,Exchange='NSEFO'
FROM ANGELFO.NSEFO.DBO.FOMARGINNEW_INTRADAY A WITH (NOLOCK)
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' 
AND A.PARTY_CODE = @PARTY
ORDER BY PARTY_CODE,FILECOUNTER DESC
END

IF @RPT = 'NSECD'
BEGIN
SELECT [Trade Date]=CONVERT(VARCHAR(11),MDATE,105),[Client Code]=PARTY_CODE,
[Span Margin]=SPANMARGIN,Filler,[Extreme Loss Margin]=EXTREAMELOSSMARGIN,[Crystallized Obligation]=MTOMLOSS,
[Margin To Be Collected]=TOTALMARGIN,[Client Flag]=CL_TYPE,[File No]=FILECOUNTER,Exchange='NSECD'
FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW_INTRADAY A WITH (NOLOCK)
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' 
AND A.PARTY_CODE = @PARTY
ORDER BY PARTY_CODE,FILECOUNTER DESC
END

IF @RPT = 'MCDX'
BEGIN
SELECT [Trade Date]=CONVERT(VARCHAR(11),MDATE,105),[Snapshot Interval]=A.FILETIME,[Client Code]=PARTY_CODE,
[Initial margin ]=A.INITIALMARGIN,ELM=A.EXTREAMELOSSMARGIN,[Net Buy Premium]=A.NETBUYPREMIUM,
[Total Upfront Margin]=A.TOTALMARGIN,[File No]=FILECOUNTER,Exchange='MCDX'
FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW_INTRADAY A WITH (NOLOCK)
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
AND A.PARTY_CODE = @PARTY
ORDER BY PARTY_CODE,FILECOUNTER DESC
END

IF @RPT = 'NCE'
BEGIN
SELECT [Trade Date]=CONVERT(VARCHAR(11),MDATE,105),[Client Code]=PARTY_CODE,
[Span Margin]=SPANMARGIN,Filler,[Extreme Loss Margin]=EXTREAMELOSSMARGIN,[Delivery Margin]=DEL_MARGIN,
[Crystallized Obligation]=MTOMLOSS,[Total Margin]=TOTALMARGIN,[Client Flag]=CL_TYPE,[File No]=FILECOUNTER,Exchange='NCE'
FROM ANGELCOMMODITY.NCE.DBO.FOMARGINNEW_INTRADAY A WITH (NOLOCK)
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59' 
AND A.PARTY_CODE = @PARTY
ORDER BY PARTY_CODE,FILECOUNTER DESC
END


IF @RPT = 'NCDX'
BEGIN
SELECT [Trade Date]=CONVERT(VARCHAR(11),MDATE,105),[Client Code]=PARTY_CODE,
[Portfolio Based Margin]=A.PORTFOLIO_BASED_MARGIN,[Net Buy Premium]=A.NET_BUY_PREMIUM,
[Initial Margin]=A.INITIAL_MARGIN,[ELM Margin]=A.ELM_MARGIN,[Pre-Expiry Margin]=A.PRE_EXPIRY_MARGIN,
[Delivery Margin]=A.DELIVERY_MARGIN,[Unidirectional Margin]=A.UNIDIRECTIONAL_MARGIN,
[Concentration Margin]=A.CONCENTRATION_MARGIN,[Adhoc Margin]=A.ADHOC_MARGIN,
[Cash Margin]=	A.CASH_MARGIN,[Crystallized Loss Margin]=A.CRYSTALLIZED_LOSS_MARGIN,
[MTM Profit/Loss]=A.MTOM_LOSS,[Client Flag]=CL_TYPE,[File No]=FILECOUNTER,Exchange='NCDX'
FROM ANGELCOMMODITY.NCDX.DBO.FOMARGINNEW_INTRADAY A WITH (NOLOCK)
WHERE MDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
AND A.PARTY_CODE = @PARTY
ORDER BY PARTY_CODE,FILECOUNTER DESC
END

GO
