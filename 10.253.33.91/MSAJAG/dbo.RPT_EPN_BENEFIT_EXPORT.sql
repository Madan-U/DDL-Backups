CREATE PROCEDURE RPT_EPN_BENEFIT_EXPORT (@RUNDATE VARCHAR(11),@RPTTYPE VARCHAR(10))

AS ---- EXEC RPT_EPN_BENEFIT_EXPORT 'JUN 03 2022',''

TRUNCATE TABLE EPN_BENEFIT_EXPORT

INSERT INTO EPN_BENEFIT_EXPORT
SELECT SETT_NO,SETT_TYPE,TRANSDATE,PARTY_CODE,SCRIP_CD,SERIES,SELLQTY,EPNQTY_CC,
NONEPNQTY,MARGIN_REQUIREMENT,SELLAMOUNT,SQOFFAMT,CASHBENEFIT,MARGIN_BENEFIT
FROM TBL_EPN_BENEFIT WITH (NOLOCK)
WHERE TRANSDATE BETWEEN @RUNDATE AND @RUNDATE + ' 23:59:59'

--SELECT TOP 1 * FROM EPN_BENEFIT_EXPORT

DECLARE @DIRCOLLNAMEAM VARCHAR(100) = 
'J:\BACKOFFICE\EXPORT\' +'EPN_DATA_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'

DECLARE @S VARCHAR(MAX)                            
SET @S = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''SETT_NO'''',''''SETT_TYPE'''',''''TRANSDATE'''',''''PARTY_CODE'''',''''SCRIP_CD'''',''''SERIES'''',''''SELLQTY'''',''''EPNQTY_CC'''',''''NONEPNQTY'''',''''MARGIN_REQUIREMENT'''',''''SELLAMOUNT'''',''''SQOFFAMT'''',''''CASHBENEFIT'''',''''MARGIN_BENEFIT''''' 
SET @S = @S + ' UNION ALL SELECT SETT_NO,SETT_TYPE,CONVERT(VARCHAR(11),TRANSDATE,105),PARTY_CODE,SCRIP_CD,SERIES,CONVERT(VARCHAR,SELLQTY),CONVERT(VARCHAR,EPNQTY_CC),CONVERT(VARCHAR,NONEPNQTY),CONVERT(VARCHAR,MARGIN_REQUIREMENT),CONVERT(VARCHAR,SELLAMOUNT),CONVERT(VARCHAR,SQOFFAMT),CONVERT(VARCHAR,CASHBENEFIT),CONVERT(VARCHAR,MARGIN_BENEFIT) FROM MSAJAG.DBO.EPN_BENEFIT_EXPORT" QUERYOUT ' +@DIRCOLLNAMEAM+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'
EXEC(@S)

SELECT 'EPN data Exported to ' + 'J:\BACKOFFICE\EXPORT\' +'EPN_DATA_' + CONVERT(VARCHAR, CONVERT(DATETIME,@RUNDATE), 112) + '.CSV'

GO
