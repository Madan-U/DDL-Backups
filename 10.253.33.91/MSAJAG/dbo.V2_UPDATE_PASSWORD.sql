-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[V2_UPDATE_PASSWORD]    
(    
 @PASSWORD VARCHAR(512),    
 @USERID VARCHAR(25),    
 @DAYCOUNT INT/*,    
 @OLDPASSWORD  VARCHAR(512)   */  
)    
/*    
V2_UPDATE_PASSWORD 'SA@123','demo',0    
    
SELECT *FROM TBLPRADNYAUSERS   where fldusername = 'vin'  
  
select * from tblusercontrolmaster where flduserid = 346  
  
V2_UPDATE_PASSWORD 'sa@123','vin', 30    
    
    
     
*/    
AS    
SET NOCOUNT ON    
    
DECLARE @@FLDOLDPASSWORD TINYINT    
DECLARE @@FLDAUTO INT    
DECLARE @@PASSAUTO BIGINT    
DECLARE @@OLDPASSSTRING VARCHAR(2000)    
DECLARE @@OLDPASSCOUNT INT    
DECLARE @@NEWPASSSTRING VARCHAR(2000)    
DECLARE @@OVERRIDE_PWD TINYINT    
DECLARE @@SQL VARCHAR(MAX)      
    
CREATE TABLE [DBO].[#DBNAME]          
(          
  EXCHANGE VARCHAR(6),           
  SEGMENT VARCHAR(10),          
  SHARESERVER VARCHAR(20),        
  SHAREDB VARCHAR(20)    
) ON [PRIMARY]        
CREATE TABLE #TEMP    
(    
 FLDOLDPASSWORD TINYINT     
)      
CREATE TABLE #TBLAUTO    
(    
 FLDAUTO INT     
)      
CREATE TABLE #TBLUSERPASSHIST    
(    
 FLDOLDPASSLISTING VARCHAR(2000),    
 FLDAUTO INT    
)    
CREATE TABLE #RESULT    
(    
 UNAME VARCHAR(100),    
 EXCHANGE VARCHAR(6),           
 SEGMENT VARCHAR(10),       
 MSG VARCHAR(500)    
)    
DECLARE @@MAINREC AS CURSOR,      
  @@ACCOUNTDB VARCHAR(20),        
  @@SHAREDB VARCHAR(20),           
  @@SHARESERVER VARCHAR(20),          
  @@EXCHANGE VARCHAR(15),          
  @@SEGMENT VARCHAR(15),      
  @@ORDERSECOUNT INT     
    
DECLARE @@TBLPRADNYAUSERS AS VARCHAR(200),    
  @@TBLUSERCONTROLMASTER AS VARCHAR(200),    
  @@TBLGLOBALPARAMS AS VARCHAR(200),    
  @@TBLUSERPASSHIST AS VARCHAR(200)   
    
     
      
SELECT @@OVERRIDE_PWD = OVERRIDE_PWD FROM MSAJAG..TBLGLOBALPARAMS      
    
IF @@OVERRIDE_PWD = 1    
 BEGIN     
    INSERT INTO [#DBNAME]     
 SELECT EXCHANGE, SEGMENT, SHARESERVER, SHAREDB FROM PRADNYA..MULTICOMPANY     
 END      
ELSE    
 BEGIN     
 INSERT INTO [#DBNAME]     
 SELECT EXCHANGE, SEGMENT, SHARESERVER, SHAREDB FROM PRADNYA..MULTICOMPANY WHERE SHAREDB = DB_NAME() and ShareServer='AngelNSECM'      
 END    
    
  
    
SET @@MAINREC = CURSOR FOR          
     SELECT EXCHANGE, SEGMENT, SHARESERVER, SHAREDB FROM #DBNAME         
           
OPEN @@MAINREC          
 FETCH NEXT FROM @@MAINREC INTO @@EXCHANGE, @@SEGMENT, @@SHARESERVER, @@SHAREDB    
  WHILE @@FETCH_STATUS = 0          
  BEGIN     
      
 --  IF @@SERVERNAME <> REPLACE(REPLACE(@@SHARESERVER,'[',''),']','')      
 --   BEGIN    
 --    SET @@TBLPRADNYAUSERS = @@SHARESERVER +"."+@@SHAREDB+".TBLPRADNYAUSERS "      
 -- SET @@TBLGLOBALPARAMS = @@SHARESERVER +"."+@@SHAREDB+".TBLGLOBALPARAMS "      
 -- SET @@TBLUSERPASSHIST = @@SHARESERVER +"."+@@SHAREDB+".TBLUSERPASSHIST "      
 -- END     
 --ELSE    
 -- BEGIN      
 -- SET @@TBLPRADNYAUSERS = @@SHAREDB+"..TBLPRADNYAUSERS "      
 -- SET @@TBLGLOBALPARAMS = @@SHAREDB+"..TBLGLOBALPARAMS "     
 -- SET @@TBLUSERPASSHIST  = @@SHAREDB+"..TBLUSERPASSHIST "     
      
 -- END     
 
  IF @@SERVERNAME <> REPLACE(REPLACE(@@SHARESERVER,'[',''),']','')      
    BEGIN    
     SET @@TBLPRADNYAUSERS = "TBLPRADNYAUSERS "      
  SET @@TBLGLOBALPARAMS = "TBLGLOBALPARAMS "      
  SET @@TBLUSERPASSHIST = "TBLUSERPASSHIST "      
  END     
 ELSE    
  BEGIN      
  SET @@TBLPRADNYAUSERS = "TBLPRADNYAUSERS "      
  SET @@TBLGLOBALPARAMS = "TBLGLOBALPARAMS "     
  SET @@TBLUSERPASSHIST  = "TBLUSERPASSHIST "     
      
  END    
    
   SET @@SQL = " INSERT INTO #TEMP SELECT CONVERT(VARCHAR,ISNULL(FLDOLDPASSWORD,1)) AS FLDOLDPASSWORD  FROM "+@@TBLGLOBALPARAMS+" "    
   EXEC(@@SQL)    
 SELECT @@FLDOLDPASSWORD = FLDOLDPASSWORD FROM #TEMP    
     
 IF ISNULL(@@FLDOLDPASSWORD,0) = 0    
  SET @@FLDOLDPASSWORD = 1    
     
 SET @@SQL = " INSERT INTO #TBLAUTO SELECT ISNULL(FLDAUTO,0) FROM " + @@TBLPRADNYAUSERS + " WHERE FLDUSERNAME = '"+ @USERID + "' "    
 EXEC(@@SQL)    
 SELECT @@FLDAUTO = FLDAUTO FROM #TBLAUTO    
     
 IF ISNULL(@@FLDAUTO,0) = 0    
  BEGIN    
  INSERT INTO #RESULT VALUES (@USERID,@@EXCHANGE, @@SEGMENT,'USER NOT FOUND')    
  END    
      
 SET @@SQL = " INSERT INTO #TBLUSERPASSHIST SELECT ISNULL(FLDOLDPASSLISTING,''), CONVERT(VARCHAR,ISNULL(FLDAUTO,0))  FROM "+@@TBLUSERPASSHIST+" WHERE CONVERT(VARCHAR,FLDUSERID) = '"+ CONVERT(VARCHAR,@@FLDAUTO) + "' "    
 EXEC(@@SQL)    
 SELECT @@OLDPASSSTRING = FLDOLDPASSLISTING , @@PASSAUTO = FLDAUTO FROM #TBLUSERPASSHIST    
     
 IF ISNULL(@@OLDPASSSTRING,'') = ''    
 BEGIN    
  SET @@OLDPASSSTRING = ''    
 END    
    
 IF ISNULL(@@PASSAUTO,0) = 0    
 BEGIN    
  SET @@PASSAUTO = 0    
 END    
     
 SELECT     
  @@OLDPASSCOUNT = COUNT(1)    
 FROM     
  FUN_SPLITSTRING (@@OLDPASSSTRING,'±')    
 WHERE     
  SPLITTED_VALUE = @PASSWORD    
  AND SNO > (    
       SELECT     
         ISNULL(ISNULL(MAX(SNO),0) - @@FLDOLDPASSWORD, 0)    
       FROM     
        FUN_SPLITSTRING (@@OLDPASSSTRING,'±')    
      )    
 IF ISNULL(@@OLDPASSCOUNT,0) > 0    
  BEGIN    
  INSERT INTO #RESULT VALUES (@USERID,@@EXCHANGE, @@SEGMENT,'PASSWORD ALREADY USED DURING LAST ' + CAST(@@FLDOLDPASSWORD AS VARCHAR) + ' ATTEMPTS.  PLEASE CHANGE')    
  END    
     
 SET @@SQL = " UPDATE TBLPRADNYAUSERS "    
 SET @@SQL = @@SQL + " SET "    
 SET @@SQL = @@SQL + "  FLDPASSWORD = '" + @PASSWORD +"', "    
 SET @@SQL = @@SQL + "  PWD_EXPIRY_DATE = (GETDATE()+ " + CONVERT(VARCHAR,@DAYCOUNT) + ") "    
 SET @@SQL = @@SQL + " WHERE "    
 SET @@SQL = @@SQL + "  CONVERT(VARCHAR,FLDAUTO) = '" + CONVERT(VARCHAR,@@FLDAUTO) +"' "    
             
 SET @@SQL = @@SQL + " UPDATE TBLUSERCONTROLMASTER "    
 SET @@SQL = @@SQL + "SET "    
 SET @@SQL = @@SQL + " FLDATTEMPTCNT = 0,"    
 SET @@SQL = @@SQL + " FLDFIRSTLOGIN = 'N',"    
 SET @@SQL = @@SQL + " FLDSTATUS = 0" 
 SET @@SQL = @@SQL + " WHERE "    
 SET @@SQL = @@SQL + " CONVERT(VARCHAR,TBLUSERCONTROLMASTER.FLDUSERID) = '" + CONVERT(VARCHAR,@@FLDAUTO) +"' "    
SET @@SQL = @@SQL + " AND UPPER(FLDACCESSLVL) <> UPPER('L') "
 EXEC(@@SQL)    
     
 SET @@NEWPASSSTRING = ''    
 DECLARE @@STRING VARCHAR(100)    
    
 DECLARE VENDOR_CURSOR CURSOR FOR     
 SELECT SPLITTED_VALUE    
 FROM     
  FUN_SPLITSTRING (@@OLDPASSSTRING,'±')    
 WHERE  SNO > (    
       SELECT     
         ISNULL(ISNULL(MAX(SNO),0) - @@FLDOLDPASSWORD, 0)    
       FROM     
        FUN_SPLITSTRING (@@OLDPASSSTRING,'±')    
      )    
 OPEN VENDOR_CURSOR    
 FETCH NEXT FROM vendor_cursor     
 INTO @@STRING    
    
 WHILE @@FETCH_STATUS = 0    
 BEGIN    
  SET @@NEWPASSSTRING = @@NEWPASSSTRING + @@STRING + '±'    
 FETCH NEXT FROM VENDOR_CURSOR     
 INTO @@STRING    
 END     
    
 CLOSE VENDOR_CURSOR    
 DEALLOCATE VENDOR_CURSOR     
     
 SET @@NEWPASSSTRING = @@NEWPASSSTRING + @PASSWORD     
 IF(SELECT COUNT(*) FROM #RESULT WHERE EXCHANGE = @@EXCHANGE AND SEGMENT = @@SEGMENT) = 0     
  BEGIN    
   IF ISNULL(@@PASSAUTO,0) = 0    
    SET @@SQL = " INSERT INTO TBLUSERPASSHIST (FLDUSERID, FLDOLDPASSLISTING) VALUES('" + CONVERT(VARCHAR,@@FLDAUTO) + "', '"+ @@NEWPASSSTRING +"') "    
   ELSE    
       SET @@SQL = " UPDATE TBLUSERPASSHIST SET FLDOLDPASSLISTING = '"+@@NEWPASSSTRING+"' WHERE CONVERT(VARCHAR,FLDAUTO) = '"+CONVERT(VARCHAR,@@PASSAUTO)+"' AND CONVERT(VARCHAR,FLDUSERID) = '"+CONVERT(VARCHAR,@@FLDAUTO)+"' "     
           
   EXEC(@@SQL)    
   INSERT INTO #RESULT VALUES (@USERID,@@EXCHANGE, @@SEGMENT,'PASSWORD UPDATED SUCCESSFULLY')        
  END      
      
     
  FETCH NEXT FROM @@MAINREC INTO @@EXCHANGE, @@SEGMENT, @@SHARESERVER, @@SHAREDB    
END      
CLOSE @@MAINREC      
DEALLOCATE @@MAINREC          
    
SELECT * FROM #RESULT    
DROP TABLE #RESULT    
RETURN

GO
