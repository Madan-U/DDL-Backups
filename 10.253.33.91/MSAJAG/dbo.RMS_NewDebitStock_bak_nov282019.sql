DECLARE   
@SEC_PAYIN DATETIME,  
@SNO  NUMERIC(18, 0),  
@SHRTSNO NUMERIC(18, 0),  
@PARTY_CODE VARCHAR(10),  
@CERTNO  VARCHAR(12),  
@PAYQTY     INT,  
@SHRTQTY    INT,  
@DELCUR  CURSOR,  
@SHRTCUR CURSOR,  
@DATECUR CURSOR,  
@DPID  VARCHAR(8),  
@CLTDPID VARCHAR(16)  
  
SET @DATECUR = CURSOR FOR  
SELECT DISTINCT TRANSDATE FROM #DELBTST  
ORDER BY TRANSDATE  
OPEN @DATECUR  
FETCH NEXT FROM @DATECUR INTO @SEC_PAYIN  
WHILE @@FETCH_STATUS = 0  
BEGIN  
  
 SET @DELCUR = CURSOR FOR  
  SELECT SNO, PARTY_CODE, CERTNO, PAYQTY FROM #DELBTST D1  
  WHERE TRANSDATE = @SEC_PAYIN  
  AND HOLDFLAG = 'PAY'   
  AND PAYQTY > 0   
  AND PARTY_CODE IN (SELECT PARTY_CODE FROM #DELBTST D2  
         WHERE D1.PARTY_CODE = D2.PARTY_CODE  
         AND D1.CERTNO = D2.CERTNO  
         AND HOLDFLAG = 'SHRT'   
         AND SHRTQTY > 0   
         AND TRANSDATE > @SEC_PAYIN)  
  ORDER BY PARTY_CODE, CERTNO, TRANSDATE  
 OPEN @DELCUR  
 FETCH NEXT FROM @DELCUR INTO @SNO, @PARTY_CODE, @CERTNO, @PAYQTY  
 WHILE @@FETCH_STATUS = 0  
 BEGIN  
  SET @SHRTCUR = CURSOR FOR  
  SELECT SNO, SHRTQTY FROM #DELBTST  
  WHERE PARTY_CODE = @PARTY_CODE  
  AND CERTNO = @CERTNO  
  AND HOLDFLAG = 'SHRT'  
  AND TRANSDATE > @SEC_PAYIN  
  OPEN @SHRTCUR  
  FETCH NEXT FROM @SHRTCUR INTO @SHRTSNO, @SHRTQTY  
  WHILE @@FETCH_STATUS = 0 AND @PAYQTY > 0  
  BEGIN  
   IF @PAYQTY >= @SHRTQTY  
   BEGIN  
    UPDATE #DELBTST SET SHRTQTY = 0  
    WHERE SNO = @SHRTSNO  
      
    UPDATE #DELBTST SET PAYQTY = PAYQTY - @SHRTQTY  
    WHERE SNO = @SNO  
  
    SET @PAYQTY = @PAYQTY - @SHRTQTY  
   END  
   ELSE  
   BEGIN  
    UPDATE #DELBTST SET SHRTQTY = SHRTQTY - @PAYQTY  
    WHERE SNO = @SHRTSNO  
      
    UPDATE #DELBTST SET PAYQTY = 0  
    WHERE SNO = @SNO  
  
    SET @PAYQTY = 0  
   END  
   FETCH NEXT FROM @SHRTCUR INTO @SHRTSNO, @SHRTQTY  
  END  
  CLOSE @SHRTCUR  
  DEALLOCATE @SHRTCUR  
  FETCH NEXT FROM @DELCUR INTO @SNO, @PARTY_CODE, @CERTNO, @PAYQTY  
 END  
 CLOSE @DELCUR  
 DEALLOCATE @DELCUR  
 FETCH NEXT FROM @DATECUR INTO @SEC_PAYIN  
END  
CLOSE @DATECUR  
DEALLOCATE @DATECUR  
  
SET @SHRTCUR = CURSOR FOR  
 SELECT SNO, PARTY_CODE, CERTNO, SHRTQTY FROM #DELBTST D1  
 WHERE HOLDFLAG = 'SHRT'   
 AND SHRTQTY > 0   
 AND PARTY_CODE IN (SELECT PARTY_CODE FROM #DELBTST D2  
        WHERE D1.PARTY_CODE = D2.PARTY_CODE  
        AND D1.CERTNO = D2.CERTNO  
        AND HOLDFLAG IN ('HOLD', 'POOL')  
        AND PAYQTY > 0)  
 ORDER BY PARTY_CODE, CERTNO, TRANSDATE  
