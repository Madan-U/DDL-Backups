-- Object: PROCEDURE dbo.PMS_PEP
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

 CREATE PROC [dbo].[PMS_PEP]   
 (    
  
 @KYC VARCHAR(10),  
 @DATE_FROM VARCHAR(11),   
 @DATE_TO   VARCHAR(11)  
 )  
 AS  
BEGIN  
 CREATE TABLE #PMS_PEP_REPORT  
 (  
  PARTY_CODE VARCHAR(20) NULL,  
  DP_CODE   VARCHAR(20)NULL,  
  PARTY_NAME VARCHAR(100)NULL,  
  PAN_NO   VARCHAR(20)NULL,  
  ACTIVE_DATE VARCHAR(11),  
  INACTIVE_DATE VARCHAR(11),  
  CL_STATUS VARCHAR(20)NULL,  
  CL_TYPE VARCHAR(20)NULL,  
  CATEGORY VARCHAR(20) NULL,  
  EXCHANGE VARCHAR(20) NULL,  
  SEGMENT VARCHAR(20) NULL  
 )  
  
IF @KYC = 'TRADING'  
  BEGIN  
  
 SELECT DISTINCT A.cl_code,A.cl_status,A.cl_type,A.pan_gir_no,A.long_name,MAX(CONVERT(VARCHAR(10),ACTIVE_DATE,120)) AS ACTIVE_DATE,  
 MAX(CONVERT(VARCHAR(10),InActive_From,120)) AS InActive_DATE ,Exchange,Segment  
 INTO #TEMP3  
 FROM Client_Details A WITH (NOLOCK), CLIENT_BROK_DETAILS B WITH (NOLOCK)  
 WHERE A.cl_code =B.Cl_Code  
 AND Active_Date >= @DATE_FROM AND Active_Date <= @DATE_TO + ' 23:59:59'  
 GROUP BY A.cl_code,A.cl_status,A.cl_type,A.pan_gir_no,A.long_name ,Exchange,Segment  
  
 SELECT DISTINCT A.PARTY_CODE,PEP,PEP_COMM,pan_gir_no,long_name,ACTIVE_DATE,Exchange,Segment,  
  InActive_DATE,cl_status,CL_TYPE  INTO #TEMP6  
 FROM CLIENT_MASTER_UCC_DATA A  WITH (NOLOCK) ,#TEMP3 B WITH (NOLOCK)   
 WHERE A.PARTY_CODE=B.Cl_code AND A.PEP='PEP'  
 ORDER BY A.PARTY_CODE  
  
 INSERT INTO #PMS_PEP_REPORT(PARTY_CODE,PARTY_NAME,PAN_NO,ACTIVE_DATE,INACTIVE_DATE,CL_STATUS,CL_TYPE,CATEGORY,Exchange,Segment)  
 SELECT  DISTINCT A.PARTY_CODE,long_name,pan_gir_no,ACTIVE_DATE,  
  InActive_DATE,cl_status,CL_TYPE ,DESCRIPTION  AS RECORDS ,Exchange,Segment  
 FROM #TEMP6 A,CLIENT_STATIC_CODES B WHERE A.PEP_COMM=B.CODE AND CATEGORY='COMMPEP'  
 AND DESCRIPTION LIKE 'PEP%'  
 UNION ALL  
 SELECT DISTINCT cl_code,long_name,pan_gir_no,ACTIVE_DATE,  
  InActive_DATE,cl_status,CL_TYPE  ,'NRI' AS RECORDS ,Exchange,Segment  
 FROM #TEMP3 WHERE cl_TYPE='NRI'  
 UNION ALL  
 SELECT DISTINCT Cl_code,long_name,pan_gir_no,ACTIVE_DATE,  
  InActive_DATE,cl_status,CL_TYPE ,'___T' AS RECORDS ,Exchange,Segment  
 FROM #TEMP3 WHERE pan_gir_no like '___T%'  
 ORDER BY PARTY_CODE  
  
  END  
  
  
  
 --/*  
  
IF @KYC = 'DP'  
  
  BEGIN  
  
 SELECT DISTINCT NISE_PARTY_CODE,CLIENT_CODE,FIRST_HOLD_NAME,FIRST_HOLD_PAN,MAX(CONVERT(VARCHAR(10),ACTIVE_DATE,120)) AS ACTIVE_DATE ,  
 MAX(CONVERT(VARCHAR(10),CLOSURE_DATE,120)) AS CLOSURE_DATE  
 INTO #TEMP1   
 FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER WITH (NOLOCK)  
 WHERE  Active_Date >= @DATE_FROM AND Active_Date <=  @DATE_TO  + ' 23:59:59'  
 GROUP BY NISE_PARTY_CODE,CLIENT_CODE,FIRST_HOLD_NAME,FIRST_HOLD_PAN  
  
 SELECT DISTINCT A.*,B.long_name,B.pan_gir_no,B.CL_CODE,cl_status,CL_TYPE INTO #DP  
 FROM #TEMP1 a  LEFT OUTER JOIN client_DETAILS B WITH (NOLOCK)   
 ON A.NISE_PARTY_CODE=B.CL_CODE  
  
 SELECT DISTINCT A.PARTY_CODE,PEP,PEP_COMM,B.* INTO #DP_F1  
 FROM CLIENT_MASTER_UCC_DATA A  WITH (NOLOCK) ,#DP B WITH (NOLOCK)   
 WHERE A.PARTY_CODE=B.NISE_PARTY_CODE AND A.PEP='PEP'  
  
 INSERT INTO #PMS_PEP_REPORT(PARTY_CODE,DP_CODE,PARTY_NAME,PAN_NO,ACTIVE_DATE,INACTIVE_DATE,CL_STATUS,CL_TYPE,CATEGORY,Exchange,Segment)  
 SELECT DISTINCT NISE_PARTY_CODE,CLIENT_CODE,FIRST_HOLD_NAME,  
 FIRST_HOLD_PAN,ACTIVE_DATE,CLOSURE_DATE,cl_status,CL_TYPE ,'NRI' AS RECORDS ,Exchange = '',Segment =''  
 FROM #DP WHERE cl_TYPE='NRI'  
  
 UNION ALL  
 SELECT DISTINCT  NISE_PARTY_CODE,CLIENT_CODE,FIRST_HOLD_NAME,  
 FIRST_HOLD_PAN,ACTIVE_DATE,CLOSURE_DATE,cl_status,CL_TYPE , '___T' AS RECORDS ,Exchange = '',Segment =''  
 FROM #DP WHERE FIRST_HOLD_PAN like '___T%'  
 UNION ALL  
 SELECT DISTINCT NISE_PARTY_CODE,CLIENT_CODE,FIRST_HOLD_NAME,   
 FIRST_HOLD_PAN,ACTIVE_DATE,CLOSURE_DATE,cl_status,CL_TYPE ,DESCRIPTION  AS RECORDS ,Exchange = '',Segment =''  
 FROM #DP_F1 A,CLIENT_STATIC_CODES B WHERE A.PEP_COMM=B.CODE AND CATEGORY='COMMPEP'  
 AND DESCRIPTION LIKE 'PEP%'  
   
  END   
  
 -- */   
  
 SELECT * FROM #PMS_PEP_REPORT  
  
  
 DROP TABLE #PMS_PEP_REPORT  
  
END

GO
