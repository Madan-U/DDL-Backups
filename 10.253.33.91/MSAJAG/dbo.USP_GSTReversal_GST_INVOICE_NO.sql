-- USP_GSTReversal_GST_INVOICE_NO 'JUN 2017', '','','HO'
CREATE PROCEDURE [dbo].[USP_GSTReversal_GST_INVOICE_NO]
	@INV_DATE VARCHAR(25),
	@FromClient VARCHAR(10),
	@ToClient VARCHAR(10),
	@Branch VARCHAR(10)
AS

BEGIN
	DECLARE
		@FROMDATE VARCHAR(11),
		@TODATE VARCHAR(11)




	IF LEN(@INV_DATE) = 10
		AND CHARINDEX('/', @INV_DATE) > 0
	BEGIN
		SET @INV_DATE = CONVERT(VARCHAR(25), CONVERT(DATETIME, @INV_DATE, 103), 109)
	END
	/*
	SET @FROMDATE = LEFT(CONVERT(DATETIME,@INV_DATE),11)
	SET @TODATE = LEFT(DATEADD(M,1,CONVERT(DATETIME,@INV_DATE))-1,11)
	*/

	SET @FROMDATE = DATEADD(MONTH, DATEDIFF(MONTH, 0, @INV_DATE), 0)
	SET @TODATE =  DATEADD(s,-1,DATEADD(mm, DATEDIFF(m,0,@INV_DATE)+1,0))


	SELECT DISTINCT RECORDFOR 
	FROM TBL_GST_INVOICE--_TMP 
	WHERE INVOICE_DATE BETWEEN @FROMDATE AND @TODATE
	AND (party_code BETWEEN @FromClient and @ToClient
				OR BRANCH_CODE = CASE WHEN @Branch = '' THEN NULL ELSE @Branch END )


	SELECT DISTINCT INVOICE_TYPE 
	FROM TBL_GST_INVOICE--_TMP 
	WHERE INVOICE_DATE BETWEEN @FROMDATE AND @TODATE
	AND (party_code BETWEEN @FromClient and @ToClient
				OR BRANCH_CODE = CASE WHEN @Branch = '' THEN NULL ELSE @Branch END )

/*
	SELECT ReversalType  
	FROM TBL_GST_Reversal_Type ORDER BY 1
*/

END

GO
