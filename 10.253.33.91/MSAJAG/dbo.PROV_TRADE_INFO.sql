CREATE PROC [dbo].[PROV_TRADE_INFO] 
AS 
DECLARE @SDATE DATETIME
SET @SDATE =CONVERT(VARCHAR(11),GETDATE(),120)
 
SELECT PARTY_CODE,SUM(TURNOVER) AS NET_VALUE FROM ( 
SELECT PARTY_CODE,TURNOVER=SUM(CASE WHEN SELL_BUY =1 THEN TRADEQTY*MARKETRATE ELSE TRADEQTY*MARKETRATE*-1 END)
 FROM SETTLEMENT WITH(NOLOCK) WHERE SAUDA_DATE >=@SDATE AND SAUDA_DATE <=@SDATE +' 23:59'
 GROUP BY PARTY_CODE
UNION ALL
SELECT  PARTY_CODE,TURNOVER=SUM(CASE WHEN SELL_BUY =1 THEN TRADEQTY*MARKETRATE ELSE TRADEQTY*MARKETRATE*-1 END) 
 FROM AngelBSECM.BSEDB_AB.DBO.SETTLEMENT WITH(NOLOCK) WHERE SAUDA_DATE >=@SDATE AND SAUDA_DATE <=@SDATE +' 23:59'
 GROUP BY PARTY_CODE
  )A 
  GROUP BY PARTY_CODE
  HAVING SUM(TURNOVER) >0

GO
