-- Object: PROCEDURE dbo.EPN_ADJ_FILE_EXPORT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROCEDURE EPN_ADJ_FILE_EXPORT (@TDAY DATETIME )

AS
---- PROVISONAL M13 EXPORT

TRUNCATE TABLE TBL_CMMARGIN_PROV_EXPORT

INSERT INTO TBL_CMMARGIN_PROV_EXPORT
SELECT 'MARGINDATE','PARTY_CODE','VARMARGIN','EXTREME_LOSS_MARGIN','ADDITIONAL_MARGIN',
'MTOM_LOSS','TOTAL_MARGIN','CL_FLAG','UPLOAD_ON','UPLOAD_BY','TOTAL_MARGIN_VAR','PEAKMARGIN'

INSERT INTO TBL_CMMARGIN_PROV_EXPORT
SELECT MARGINDATE=CONVERT(VARCHAR(11),MARGINDATE,105),PARTY_CODE,VARMARGIN,EXTREME_LOSS_MARGIN,
ADDITIONAL_MARGIN,MTOM_LOSS,TOTAL_MARGIN,CL_FLAG,UPLOAD_ON,UPLOAD_BY,TOTAL_MARGIN_VAR,PEAKMARGIN 
FROM MSAJAG.DBO.TBL_CMMARGIN_LASTDAY ---WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #EPNMAST)

DECLARE @PNAME VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'PROVISONAL_MG13_' + CONVERT(VARCHAR(11), @TDAY, 105) + '.CSV'
DECLARE @PBCP VARCHAR(4000) = 'BCP "SELECT * FROM MSAJAG.DBO.TBL_CMMARGIN_PROV_EXPORT ORDER BY 1 DESC" QUERYOUT "' + @PNAME + '" -c -t"," -r"\n" -T'
EXEC MASTER..XP_CMDSHELL @PBCP, NO_OUTPUT

---- EPN ADJUSTED M13 EXPORT

TRUNCATE TABLE TBL_CMMARGIN_ADJ_EXPORT

INSERT INTO TBL_CMMARGIN_ADJ_EXPORT
SELECT 'MARGINDATE','PARTY_CODE','VARMARGIN','EXTREME_LOSS_MARGIN','ADDITIONAL_MARGIN',
'MTOM_LOSS','TOTAL_MARGIN','CL_FLAG','UPLOAD_ON','UPLOAD_BY','TOTAL_MARGIN_VAR','PEAKMARGIN'

INSERT INTO TBL_CMMARGIN_ADJ_EXPORT
SELECT MARGINDATE=CONVERT(VARCHAR(11),MARGINDATE,105),PARTY_CODE,VARMARGIN,EXTREME_LOSS_MARGIN,
ADDITIONAL_MARGIN,MTOM_LOSS,TOTAL_MARGIN,CL_FLAG,UPLOAD_ON,UPLOAD_BY,TOTAL_MARGIN_VAR,PEAKMARGIN 
FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) 
WHERE MARGINDATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59' --AND PARTY_CODE IN (SELECT PARTY_CODE FROM #EPNMAST)

DECLARE @CNAME VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'EPN_ADJUSTED_MG13_' + CONVERT(VARCHAR(11), @TDAY, 105) + '.CSV'
DECLARE @CBCP VARCHAR(4000) = 'BCP "SELECT * FROM MSAJAG.DBO.TBL_CMMARGIN_ADJ_EXPORT ORDER BY 1 DESC" QUERYOUT "' + @CNAME + '" -c -t"," -r"\n" -T'
EXEC MASTER..XP_CMDSHELL @CBCP, NO_OUTPUT

GO
