CREATE PROC [dbo].[PROC_POOL_MARGIN_BENEFIT]
(
	@SAUDA_DATE VARCHAR(11),
	@FROMPARTY	VARCHAR(10)= '',
	@TOPARTY	VARCHAR(10) = 'ZZZZZZZZZZ'
)
AS

-- EXEC PROC_POOL_MARGIN_BENEFIT 'MAY 28 2021'

DELETE FROM TBL_EPN_BENEFIT
WHERE TRANSDATE = @SAUDA_DATE

INSERT INTO TBL_EPN_BENEFIT
SELECT SETT_NO,SETT_TYPE,TRANSDATE,PARTY_CODE,SCRIP_CD,SERIES,
SELLQTY=SUM(SELLQTY),EPNQTY_CC=SUM(EPNQTY_CC),NONEPNQTY=SUM(NONEPNQTY),
MARGIN_REQUIREMENT=SUM(MARGIN_AMT),
SELLAMOUNT=SUM(NETAMT),
SQOFFAMT = SUM(SQOFFAMT),
CASHBENEFIT = SUM(CASE WHEN EPNQTY_CC > 0 AND NETAMT < 0 AND SELLQTY > 0 
			  THEN (CASE WHEN SELLQTY = EPNQTY_CC THEN ABS(NETAMT) 
			  ELSE CONVERT(NUMERIC(18,2),(ABS(NETAMT)/SELLQTY)*( CASE WHEN EPNQTY_CC>SELLQTY THEN SELLQTY ELSE EPNQTY_CC END)) END) ELSE 0 END),
MARGIN_BENEFIT = SUM(CASE WHEN SELLQTY > 0 AND (SELLQTY - EPNQTY_CC) > 0 
THEN (case when (SELLQTY - EPNQTY_CC) = NONEPNQTY then MARGIN_AMT else CONVERT(NUMERIC(18,2),(MARGIN_AMT/(SELLQTY - EPNQTY_CC))*NONEPNQTY) end) ELSE 0 END)
FROM (
		SELECT SETT_NO, SETT_TYPE, TRANSDATE, PARTY_CODE, SCRIP_CD, SERIES, 
		SELLQTY = SUM(SELLQTY), EPNQTY = SUM(EPNQTY), EPNQTY_CC = SUM(EPNQTY_CC), 
		NONEPNQTY = (CASE WHEN SUM(NONEPNQTY)-SUM(EPNQTY_CC) > 0 THEN SUM(NONEPNQTY)-SUM(EPNQTY_CC) ELSE 0 END),
		SQOFFAMT=SUM(SQOFFAMT),
		NETAMT = SUM(NETAMT), 
		MARGIN_AMT = SUM(MARGIN_AMT)
		FROM (
				SELECT SETT_NO, SETT_TYPE, TRANSDATE, PARTY_CODE, SCRIP_CD, SERIES, 
				SELLQTY = SUM(CASE WHEN RECORD_TYPE = 'MARGIN' AND OPENQTY < 0 THEN ABS(OPENQTY) ELSE 0 END),
				EPNQTY = SUM(CASE WHEN RECORD_TYPE = 'PAYIN' THEN EPNQTY ELSE 0 END),
				EPNQTY_CC = SUM(CASE WHEN RECORD_TYPE = 'EPN' THEN EPNQTY ELSE 0 END),
								NONEPNQTY = (CASE WHEN SUM(CASE WHEN RECORD_TYPE = 'MARGIN' AND OPENQTY < 0 THEN ABS(OPENQTY) ELSE 0 END) > 
									(CASE WHEN SUM(NONEPNQTY) - SUM(CASE WHEN RECORD_TYPE = 'EPN' THEN EPNQTY ELSE 0 END) > 0 THEN
									 SUM(NONEPNQTY) - SUM(CASE WHEN RECORD_TYPE = 'EPN' THEN EPNQTY ELSE 0 END)
									 ELSE 0 END) THEN (CASE WHEN SUM(NONEPNQTY) - SUM(CASE WHEN RECORD_TYPE = 'EPN' THEN EPNQTY ELSE 0 END) > 0 THEN
									 SUM(NONEPNQTY) - SUM(CASE WHEN RECORD_TYPE = 'EPN' THEN EPNQTY ELSE 0 END)
									 ELSE 0 END) ELSE SUM(CASE WHEN RECORD_TYPE = 'MARGIN' AND OPENQTY < 0 THEN ABS(OPENQTY) ELSE 0 END) END),
				SQOFFAMT=SUM(SQOFFAMT),
				NETAMT = SUM(NETAMT), 
				MARGIN_AMT = SUM(MARGIN_AMT)
				FROM TBL_PAYIN_DATA (NOLOCK)
				WHERE TRANSDATE = @SAUDA_DATE AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
				AND RECORD_TYPE <> 'EPN'
				GROUP BY TRANSDATE,SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES 

				UNION ALL
				SELECT D.SETT_NO, D.SETT_TYPE, TRANSDATE = @SAUDA_DATE, PARTY_CODE, SCRIP_CD, D.SERIES, 
				SELLQTY= 0,EPNQTY=0,
				EPNQTY_CC = MAX(EPNQTY),
				NONEPNQTY=0,
				SQOFFAMT=CONVERT(NUMERIC(18,4),0),
				NETAMT=CONVERT(NUMERIC(18,4),0),
				MARGIN_AMT=CONVERT(NUMERIC(18,4),0)
				FROM TBL_PAYIN_DATA D(NOLOCK), SETT_MST S(NOLOCK)
				WHERE D.SETT_NO = S.SETT_NO AND D.SETT_TYPE = S.SETT_TYPE 
				AND RECORD_TYPE='EPN' AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
				AND TRANSDATE >= START_DATE AND TRANSDATE < SEC_PAYIN
				AND @SAUDA_DATE >= START_DATE AND @SAUDA_DATE < SEC_PAYIN
				AND TRANSDATE <= @SAUDA_DATE
				GROUP BY D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES
			) A GROUP BY SETT_NO, SETT_TYPE, TRANSDATE, PARTY_CODE, SCRIP_CD, SERIES
	) A
GROUP BY SETT_NO,SETT_TYPE, TRANSDATE, PARTY_CODE, SCRIP_CD, SERIES 

EXEC PROC_EPI_LEDGER_INSERT @SAUDA_DATE

EXEC PEAK_EPN_BENEFIT @SAUDA_DATE

GO
