CREATE PROC [dbo].[CL_ALLSEGMENT_STATUS](@CL_CODE VARCHAR(10))  
AS  
  
 
 -- return 0
 
 
 SET NOCOUNT ON   
  
  
CREATE TABLE #ACTIVE  
(CL_CODE VARCHAR(10),  
EQUITY VARCHAR(15),  
FUTURES VARCHAR(15),  
CURRENCY VARCHAR(15),  
COMMODITY VARCHAR(15),  
MUTUAL_FUNDS VARCHAR(15),  
IPO VARCHAR(15))  
  
   
  
  
-- set @CL_CODE='rp61'  
INSERT INTO #ACTIVE VALUES( @CL_CODE,'','','','','','')  
  
SELECT  B.CL_CODE,(CASE WHEN MAX(INACTIVE_FROM) > GETDATE() THEN 'YES' ELSE 'NO' END) AS INACTIVE INTO #EQ  FROM   
CLIENT_BROK_DETAILS B,#ACTIVE A  
WHERE A.cl_code=B.Cl_Code AND Segment='CAPITAL'  
AND A.cl_code=@CL_CODE AND ISNULL(DEACTIVE_VALUE,'') NOT IN ('P')  
GROUP BY B.CL_CODE  
  
SELECT  B.CL_CODE,(CASE WHEN MAX(INACTIVE_FROM) > GETDATE() THEN 'YES' ELSE 'NO' END) AS INACTIVE INTO #FO FROM   
CLIENT_BROK_DETAILS B,#ACTIVE A  
WHERE A.cl_code=B.Cl_Code AND Segment='FUTURES'AND Exchange IN ('NSE','BSE')  
AND A.cl_code=@CL_CODE AND ISNULL(DEACTIVE_VALUE,'') NOT IN ('P')  
GROUP BY B.CL_CODE  
  
SELECT  B.CL_CODE,(CASE WHEN MAX(INACTIVE_FROM) > GETDATE() THEN 'YES' ELSE 'NO' END) AS INACTIVE INTO #CURRENCY  FROM   
CLIENT_BROK_DETAILS B,#ACTIVE A  
WHERE A.cl_code=B.Cl_Code AND Segment='FUTURES'AND Exchange IN ('NSX','MCD') AND ISNULL(DEACTIVE_VALUE,'') NOT IN ('P')  
AND A.cl_code=@CL_CODE GROUP BY B.CL_CODE   
  
SELECT  B.CL_CODE,(CASE WHEN MAX(INACTIVE_FROM) > GETDATE() THEN 'YES' ELSE 'NO' END) AS INACTIVE INTO #COMM FROM   
CLIENT_BROK_DETAILS B,#ACTIVE A  
WHERE A.cl_code=B.Cl_Code AND Segment='FUTURES'AND Exchange IN ('MCX','NCX') AND ISNULL(DEACTIVE_VALUE,'') NOT IN ('P')  
AND A.cl_code=@CL_CODE GROUP BY B.CL_CODE  
  
   
  
SELECT PARTY_CODE,(CASE WHEN MAX(INACTIVE_FROM) > GETDATE() THEN 'YES' ELSE 'NO' END) AS INACTIVE INTO #MF FROM (   
SELECT PARTY_CODE,INACTIVE_FROM FROM ANGELFO.NSEMFSS.DBO.MFSS_CLIENT WHERE PARTY_CODE=@CL_CODE  
UNION  
SELECT PARTY_CODE,INACTIVE_FROM FROM ANGELFO.BSEMFSS.DBO.MFSS_CLIENT WHERE PARTY_CODE=@CL_CODE)A  
GROUP BY party_code  
  
  
SELECT NISE_PARTY_CODE=@CL_CODE,'' AS CLOSE_DATE INTO #DP 
--FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER B2,  
--AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_POA C  
--WHERE B2.CLIENT_CODE=c.CLIENT_CODE  
--AND NISE_PARTY_CODE=@CL_CODE AND MASTER_POA ='2203320000000014'  
--AND POA_VER='2' 
/*
SELECT top 1 Party_code NISE_PARTY_CODE,(CASE WHEN def ='1' THEN  'YES' ELSE 'NO' END) AS CLOSE_DATE INTO #DP 
from MultiCltId where party_Code =@CL_CODE   */
   
UPDATE A SET MUTUAL_FUNDS=INACTIVE  
FROM #MF D,#ACTIVE A  
WHERE D.PARTY_CODE=A.CL_CODE  
  
UPDATE A SET IPO=CLOSE_DATE  
FROM #DP D,#ACTIVE A  
WHERE D.NISE_PARTY_CODE=A.CL_CODE  
  
  
UPDATE D SET CURRENCY=INACTIVE  
FROM #ACTIVE D,#CURRENCY A  
WHERE D.CL_CODE=A.CL_CODE  
  
  
UPDATE D SET FUTURES=INACTIVE  
FROM #ACTIVE D,#FO A  
WHERE D.CL_CODE=A.CL_CODE  
  
  
UPDATE D SET COMMODITY=INACTIVE  
FROM #ACTIVE D,#COMM A  
WHERE D.CL_CODE=A.CL_CODE  
  
UPDATE D SET EQUITY=INACTIVE  
FROM #ACTIVE D,#EQ A  
WHERE D.CL_CODE=A.CL_CODE  
  
  
  
SELECT UPPER(CL_CODE)CL_CODE,(CASE WHEN EQUITY ='' THEN 'N/A' ELSE EQUITY END ) EQUITY,  
(CASE WHEN FUTURES ='' THEN 'N/A' ELSE FUTURES END ) FUTURES,(CASE WHEN CURRENCY ='' THEN 'N/A' ELSE CURRENCY END ) CURRENCY,  
(CASE WHEN COMMODITY ='' THEN 'N/A' ELSE COMMODITY END )COMMODITY,  
(CASE WHEN MUTUAL_FUNDS ='' THEN 'N/A' ELSE MUTUAL_FUNDS END ) MUTUAL_FUNDS,(CASE WHEN IPO ='' THEN 'N/A' ELSE IPO END )IPO  
 FROM #ACTIVE

GO
