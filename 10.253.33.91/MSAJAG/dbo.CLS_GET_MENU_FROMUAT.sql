CREATE PROCEDURE [dbo].[CLS_GET_MENU] 
(
	@STRCATEGORY VARCHAR(20),
	@STRAPPNAME VARCHAR(256),
	@TYPE CHAR(1) = 'H',
	@USERID VARCHAR(20)
)

AS

 --SELECT * FROM STATICMENUSTRING
 
 --SELECT * FROM STATICREPORTS

IF @TYPE = 'V'
	EXEC CLS_GET_MENU_CLIENT @STRCATEGORY, @STRAPPNAME,@USERID
ELSE
	EXEC CLS_GET_MENU_MEGA @STRCATEGORY, @STRAPPNAME,@USERID

GO
