-- Object: PROCEDURE dbo.PROC_STATMP_UPLOAD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[PROC_STATMP_UPLOAD] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @SETT_DATE DATETIME,
		@MEMBERCODE	VARCHAR(10),
		@MEMBER_TYPE VARCHAR(2),
		@FILEDATE	DATETIME,
		@RECCNT		INT

SELECT  
	@FILEDATE = .DBO.PIECE(FILE_DATA,',',2),
	@SETT_DATE = .DBO.PIECE(FILE_DATA,',',3),
	@MEMBER_TYPE = .DBO.PIECE(FILE_DATA,',',4),
	@MEMBERCODE = .DBO.PIECE(FILE_DATA,',',5) 
FROM FILEDATA
WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) = '10'

IF @FILEDATE <> CONVERT(DATETIME,@TDATE,103)
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'SELECTED DATE IS NOT MATCHING WITH THE FILE DATE.'
	RETURN
END

SELECT 
	RECTYPE,SAUDA_DATE,SETT_DATE,MEMBER_TYPE,MEMBERCODE,TM_CODE,PARTY_CODE,STATE_NAME,
	SETT_TYPE,SETT_NO,SCRIP_CD,SERIES,BUYQTY,SELLQTY,DELBUYQTY,DELBUYVAL,TRDBUYQTY,
	TRDBUYVAL,DELSTAMP,TRDSTAMP,TOTALSTAMP
INTO #TBL_STAMP_EXCHG
FROM TBL_STAMP_EXCHG
WHERE 1 = 2

INSERT INTO #TBL_STAMP_EXCHG
SELECT 
	RECTYPE = .DBO.PIECE(FILE_DATA,',',1), 
	SAUDA_DATE = .DBO.PIECE(FILE_DATA,',',2),
	SETT_DATE = .DBO.PIECE(FILE_DATA,',',3),
	MEMBER_TYPE = .DBO.PIECE(FILE_DATA,',',4),
	MEMBERCODE = .DBO.PIECE(FILE_DATA,',',5),
	TM_CODE = '',
	PARTY_CODE = '',
	STATE_NAME = '',
	SETT_TYPE = '',
	SETT_NO = '',
	SCRIP_CD = '',
	SERIES = '',
	BUYQTY = 0,
	SELLQTY = 0,
	DELBUYQTY = 0,
	DELBUYVAL = 0,
	TRDBUYQTY = 0,
	TRDBUYVAL = 0,
	DELSTAMP = 0,
	TRDSTAMP = 0,
	TOTALSTAMP = .DBO.PIECE(FILE_DATA,',',6) 
FROM FILEDATA
WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) = '10'

INSERT INTO #TBL_STAMP_EXCHG
SELECT 
	RECTYPE = .DBO.PIECE(FILE_DATA,',',1), 
	SAUDA_DATE = .DBO.PIECE(FILE_DATA,',',2),
	SETT_DATE = @SETT_DATE,
	MEMBER_TYPE = @MEMBER_TYPE,
	MEMBERCODE = @MEMBERCODE,
	TM_CODE = .DBO.PIECE(FILE_DATA,',',3),
	PARTY_CODE = .DBO.PIECE(FILE_DATA,',',4),
	STATE_NAME = .DBO.PIECE(FILE_DATA,',',6),
	SETT_TYPE = '',
	SETT_NO = '',
	SCRIP_CD = '',
	SERIES = '',
	BUYQTY = 0,
	SELLQTY = 0,
	DELBUYQTY = 0,
	DELBUYVAL = 0,
	TRDBUYQTY = 0,
	TRDBUYVAL = 0,
	DELSTAMP = 0,
	TRDSTAMP = 0,
	TOTALSTAMP = .DBO.PIECE(FILE_DATA,',',5) 
FROM FILEDATA
WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) = '20'

INSERT INTO #TBL_STAMP_EXCHG
SELECT 
	.DBO.PIECE(FILE_DATA,',',1), 
	.DBO.PIECE(FILE_DATA,',',2),
	SETT_DATE = @SETT_DATE,
	MEMBER_TYPE = @MEMBER_TYPE,
	MEMBERCODE = @MEMBERCODE,
	TM_CODE = .DBO.PIECE(FILE_DATA,',',3),
	PARTY_CODE = .DBO.PIECE(FILE_DATA,',',4),
	STATE_NAME = '',
	SETT_TYPE = .DBO.PIECE(FILE_DATA,',',5),
	SETT_NO = .DBO.PIECE(FILE_DATA,',',6),
	SCRIP_CD = .DBO.PIECE(FILE_DATA,',',7),
	SERIES = .DBO.PIECE(FILE_DATA,',',8),
	BUYQTY = .DBO.PIECE(FILE_DATA,',',9),
	SELLQTY = .DBO.PIECE(FILE_DATA,',',10),
	DELBUYQTY = .DBO.PIECE(FILE_DATA,',',11),
	DELBUYVAL = .DBO.PIECE(FILE_DATA,',',12),
	TRDBUYQTY = .DBO.PIECE(FILE_DATA,',',13),
	TRDBUYVAL = .DBO.PIECE(FILE_DATA,',',14),
	DELSTAMP = .DBO.PIECE(FILE_DATA,',',15),
	TRDSTAMP = .DBO.PIECE(FILE_DATA,',',16),
	TOTALSTAMP = .DBO.PIECE(FILE_DATA,',',17) 
FROM FILEDATA
WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) = '30'

UPDATE #TBL_STAMP_EXCHG SET STATE_NAME = T.STATE_NAME
FROM (
		SELECT DISTINCT RECTYPE, PARTY_CODE, STATE_NAME FROM #TBL_STAMP_EXCHG 
		WHERE RECTYPE = 20
	 ) T
WHERE #TBL_STAMP_EXCHG.PARTY_CODE = T.PARTY_CODE
AND #TBL_STAMP_EXCHG.RECTYPE = 30 AND T.RECTYPE = 20 

DELETE FROM TBL_STAMP_EXCHG 
WHERE SAUDA_DATE = @FILEDATE

INSERT INTO TBL_STAMP_EXCHG 
SELECT *, UPLOAD_BY = @UNAME, UPLOAD_ON = GETDATE() 
FROM #TBL_STAMP_EXCHG

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #TBL_STAMP_EXCHG

DROP TABLE #TBL_STAMP_EXCHG

DELETE FROM FILEDATA
WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
