  GROSS_VALUE = TAXABLE_AMT,  
  TAXABLE_VALUE =TAXABLE_AMT,  
  IGST_RATE,  
  IGST_AMOUNT,  
  CGST_RATE,  
  CGST_AMOUNT,  
  SGST_RATE,  
  SGST_AMOUNT,  
  UGST_RATE = SGST_RATE,  
  UGST_AMOUNT = SGST_AMOUNT,  
  TOTAL_VALUE = ROUND(TAXABLE_AMT,2) + ROUND(TAX_AMT,2),  
  FT_TOTAL_VALUE = 0.00,  
  FT_AMT_REC_ADV = 0.00,  
  FT_NETAMOUNT = 0.00,  
  FT_TAXPAYABLE = 0.00,  
  FT_IGST = 0.00,  
  FT_CGST = 0.00,  
  FT_SGST = 0.00,  
  FT_GRAND_TOTAL = 0.00,  
  FT_AMT_INWORDS = '',  
  FT_BANK_NAME = '',  
  FT_BANK_BRANCH = '',  
  FT_BANK_ACCOUNTNO = '',  
  FT_BANK_IFSCCODE = '',  
  FT_AUTHO_SIGNATORY = 'Harikrishna Negi',  
  INVOICEDRCR = LEFT(INVOICE_TYPE,1),  
  INOICE_TYPE = INVOICE_TYPE   
