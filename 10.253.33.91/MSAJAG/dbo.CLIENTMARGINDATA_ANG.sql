CREATE PROC CLIENTMARGINDATA_ANG(@DATE VARCHAR(11)='',@FROMPARTY VARCHAR(10)='A000000000',@TOPARTY VARCHAR(10)='ZZZZZZZZZZ')    
AS    
CREATE TABLE #CLIENTMARGINDATA (MDATE DATETIME,CLIENTCODE VARCHAR(10),MINDI VARCHAR(1))    
    
CREATE TABLE [DBO].[#DATA]([FILEDATA] [VARCHAR](MAX))          
          
BULK INSERT [DBO].[#DATA] FROM 'J:\BACKOFFICE\EXPORT\MARGIN_DATA.CSV' WITH ( FIRSTROW = 0);          
          
INSERT INTO #CLIENTMARGINDATA    
SELECT CONVERT(VARCHAR(11), CONVERT(DATE, DBO.PIECE(FILEDATA,',',1), 103), 109), DBO.PIECE(FILEDATA,',',2), DBO.PIECE(FILEDATA,',',3)    
FROM #DATA          

SELECT DISTINCT * INTO #CLIENTMARGINDATA_FINAL FROM #CLIENTMARGINDATA
        
SELECT EXCHANGE,SEGMENT,PARTY_CODE,MARGINDATE,TDAY_LEDGER = SUM(TDAY_LEDGER),TDAY_MARGIN = SUM(TDAY_MARGIN),TDAY_CASHCOLL = SUM(TDAY_CASHCOLL),    
TDAY_NONCASH = SUM(TDAY_NONCASH),TDAY_MTM= SUM(TDAY_MTM),T_MARGINAVL = SUM(T_MARGINAVL),T_MTMAVL = SUM(T_MTMAVL),MINDI AS MARGINTYPE     
FROM MSAJAG.DBO.TBL_COMBINE_REPORTING_DETAIL , #CLIENTMARGINDATA_FINAL WHERE  MARGINDATE = MDATE    
AND PARTY_CODE = CLIENTCODE AND MINDI = 'E'    
GROUP BY EXCHANGE,SEGMENT,PARTY_CODE,MARGINDATE,MINDI     
UNION ALL    
SELECT EXCHANGE,SEGMENT,PARTY_CODE,MARGINDATE,TDAY_LEDGER = SUM(TDAY_LEDGER),TDAY_MARGIN = SUM(TDAY_MARGIN),TDAY_CASHCOLL = SUM(TDAY_CASHCOLL),    
TDAY_NONCASH = SUM(TDAY_NONCASH),TDAY_MTM= SUM(TDAY_MTM),T_MARGINAVL = SUM(T_MARGINAVL),T_MTMAVL = SUM(T_MTMAVL),MINDI AS MARGINTYPE     
FROM MSAJAG.DBO.TBL_COMBINE_REPORTING_PEAK_DETAIL , #CLIENTMARGINDATA_FINAL    
WHERE  MARGINDATE = MDATE     
AND PARTY_CODE =CLIENTCODE  AND MINDI = 'P'    
GROUP BY EXCHANGE,SEGMENT,PARTY_CODE,MARGINDATE,MINDI ORDER BY MINDI,MARGINDATE ASC    
    
DROP TABLE #CLIENTMARGINDATA    
DROP TABLE #CLIENTMARGINDATA_FINAL
DROP TABLE [#DATA]

GO
