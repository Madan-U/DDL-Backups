CREATE PROCEDURE [dbo].[CLS_GET_RPT_OPTION] 
(
	@USERID VARCHAR(25)	,
	@RPTCODE VARCHAR(25)
)
AS
DECLARE @LOOPID INT
	,@OPT_LIST VARCHAR(MAX)
	,@RPT_DAYS VARCHAR(MAX)


SET @OPT_LIST ='0'

	SELECT @OPT_LIST = FLDOPTIONLIST
	FROM CLS_TBLUSERREPORT_SETTING WITH (NOLOCK)
	WHERE FLDREPORTCODE = @RPTCODE			
	AND	FLDUSERNAME = @USERID
	AND ISNULL(FLDUSERNAME, '') <> ''
	AND ISNULL(FLDOPTIONLIST, '') <> ''

	IF @OPT_LIST = '0'
	BEGIN
		SELECT  @OPT_LIST = FLDOPTIONLIST
		FROM CLS_TBLREPORT_SETTING WITH (NOLOCK)
		WHERE FLDREPORTCODE = @RPTCODE
	END

	SELECT  @RPT_DAYS = NO_OF_DAYS
	FROM CLS_TBLREPORT_SETTING
	WHERE FLDREPORTCODE = @RPTCODE

		
SELECT OPTION_LIST = @OPT_LIST ,NODAYS = @RPT_DAYS

/*------------------------------------------- END OF THE PROC ----------------------------*/

GO
