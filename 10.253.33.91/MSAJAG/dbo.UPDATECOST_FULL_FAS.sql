-- Object: PROCEDURE dbo.UPDATECOST_FULL_FAS
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[UPDATECOST_FULL_FAS]  
AS  
DECLARE  
	@@BRANCH_CODE AS VARCHAR(10),  
	@@COSTCODE AS VARCHAR(10),  
	@@BRCNT AS INT,  
	@@BRCURS AS CURSOR  
------------------------------------------UPDATING NSE SEGMENT --------------------------------------------------------
SET @@BRCURS = CURSOR FOR  
	SELECT DISTINCT 
		BRANCH_CODE 
	FROM FAS.DBO.BRANCH 
	WHERE BRANCH_CODE NOT IN 
		(
		SELECT DISTINCT 
			COSTNAME 
		FROM FAS.DBO.COSTMAST
		)
OPEN @@BRCURS  
	FETCH NEXT 
	FROM @@BRCURS 
	INTO @@BRANCH_CODE
WHILE @@FETCH_STATUS = 0  
	BEGIN  
		SELECT 
			@@COSTCODE = CONVERT(VARCHAR(10), ISNULL(MAX(COSTCODE)+1, 1)) 
		FROM FAS.DBO.COSTMAST 
		INSERT 
		INTO FAS.DBO.COSTMAST VALUES 
			(
				@@BRANCH_CODE, 
				@@COSTCODE, 
				1, 
				'10000000000'
			) 
			FETCH NEXT 
		FROM @@BRCURS 
		INTO @@BRANCH_CODE
	END  
CLOSE @@BRCURS  
SET @@BRCURS = CURSOR FOR  
	SELECT DISTINCT 
		COSTNAME 
	FROM FAS.DBO.COSTMAST 
	WHERE COSTNAME NOT IN 
		(
		SELECT DISTINCT 
			BRANCHNAME 
		FROM FAS.DBO.BRANCHACCOUNTS
		)
OPEN @@BRCURS  
	FETCH NEXT
	FROM @@BRCURS
	INTO @@BRANCH_CODE  
WHILE @@FETCH_STATUS = 0  
	BEGIN  
		INSERT 
		INTO FAS.DBO.BRANCHACCOUNTS VALUES 
			(
				@@BRANCH_CODE, 
				@@BRANCH_CODE, 
				'HOCTRL', 
				0
			) 
		FETCH NEXT 
		FROM @@BRCURS 
		INTO @@BRANCH_CODE
	END  
CLOSE @@BRCURS  
	INSERT 
	INTO FAS.DBO.ACMAST 
	SELECT 
		LTRIM(RTRIM(BRANCHNAME)) + ' CONTROL A/C', 
		LTRIM(RTRIM(BRANCHNAME)) + ' CONTROL A/C', 
		'ASSET', 
		'3', 
		'', 
		LTRIM(RTRIM(BRCONTROLAC)), 
		'', 
		'A0000000000', 
		'', 
		'0', 
		LTRIM(RTRIM(BRANCHNAME)), 
		'0', 
		'C', 
		'', 
		'', 
		'' 
	FROM FAS.DBO.BRANCHACCOUNTS 
	WHERE BRCONTROLAC NOT IN 
		(
		SELECT DISTINCT 
			CLTCODE 
		FROM FAS.DBO.ACMAST
		)
  
SELECT * FROM BRANCH

GO
