CREATE PROCEDURE [dbo].[PAYIN_PAYOUT_DETAILS] --'S194675'  
(@PARTY_cODE VARCHAR(15))  
AS   
  
BEGIN  
  
--RETURN 0  
   
SELECT PARTY_CODE,BankID,Cltdpid,(CASE WHEN DEFDP='1'THEN 'POA'ELSE 'NONPOA'END)AS POS_STATUS,Depository AS DPTYPE,'PAYOUT'AS [PAYIN/PAYOUT] into #dpdetails  
 FROM CLIENT4 WHERE Cl_code=@PARTY_cODE AND Depository IN ('CDSL','NSDL')  
UNION   
SELECT PARTY_CODE,BankID,Cltdpid,(CASE WHEN DEFDP='1'THEN 'POA'ELSE 'NONPOA'END)AS POS_STATUS,Depository AS DPTYPE,'PAYOUT'AS [PAYIN/PAYOUT]  
FROM [AngelBSECM].BSEDB_AB.DBO.CLIENT4  WHERE Cl_code=@PARTY_cODE AND Depository IN ('CDSL','NSDL')  
  
UNION  
  
SELECT PARTY_CODE,DPID AS BANKID,CLTDPNO AS Cltdpid,(CASE WHEN Def='1'THEN 'POA'ELSE 'NONPOA'END)AS POS_STATUS,DPTYPE,'PAYIN'AS [PAYIN/PAYOUT]  
 FROM MultiCltId WHERE PARTY_CODE=@PARTY_cODE   
 UNION   
 SELECT PARTY_CODE,DPID AS BANKID,CLTDPNO AS Cltdpid,(CASE WHEN Def='1'THEN 'POA'ELSE 'NONPOA'END)AS POS_STATUS,DPTYPE,'PAYIN'AS [PAYIN/PAYOUT]  
 FROM [AngelBSECM].BSEDB_AB.DBO.MultiCltId WHERE PARTY_CODE=@PARTY_cODE   
  
  
  
 select D.*,BANKNAME  AS DP_NAME FROM #dpdetails  D  
 LEFT OUTER JOIN  
 BANK  B ON D.BankID=B.BankID  
   
  
  
END

GO
