--PR_MIG_CITRUS_MULTIDP_UPD 'MLDH099'

CREATE PROC  [dbo].[PR_MIG_CITRUS_MULTIDP_UPD_21062016] (@PARTY_CODE VARCHAR(12)) AS



CREATE TABLE [#MIG_CITRUS_MULTICLTID]
(
	[PARTY_CODE] [VARCHAR](10) NOT NULL,
	[CLTDPNO] [VARCHAR](16) NOT NULL,
	[DPID] [VARCHAR](16) NOT NULL,
	[INTRODUCER] [VARCHAR](100) NULL,
	[DPTYPE] [VARCHAR](4) NULL,
	[DEF] [INT] NULL
) ON [PRIMARY]

CREATE CLUSTERED INDEX [PK_MULTICLTID] ON [#MIG_CITRUS_MULTICLTID] 
(
	[PARTY_CODE] ASC,
	[CLTDPNO] ASC,
	[DPID] ASC
)

INSERT INTO #MIG_CITRUS_MULTICLTID
SELECT 	* FROM  [172.31.16.57].CRMDB_a.DBO.VIEW_CLASS_CLIENT_MULTIDP_ALL A
WHERE BO_PARTYCODE = @PARTY_CODE

--UPDATING AT NSE CM
INSERT INTO MULTICLTID      
SELECT PARTY_CODE, CLTDPNO, DPID, INTRODUCER, DPTYPE, DEF       
FROM #MIG_CITRUS_MULTICLTID C     
WHERE NOT EXISTS (SELECT PARTY_CODE FROM MULTICLTID M WHERE M.PARTY_CODE = C.PARTY_CODE 
				AND M.DPID = C.DPID AND M.CLTDPNO = C.CLTDPNO)
				
UPDATE MULTICLTID SET DEF = 0 WHERE PARTY_CODE = @PARTY_CODE
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #MIG_CITRUS_MULTICLTID WHERE DEF=1)
				          
UPDATE M SET DEF = 1
FROM 
#MIG_CITRUS_MULTICLTID C,
MULTICLTID M
WHERE M.PARTY_CODE = C.PARTY_CODE 
AND M.DPID = C.DPID AND M.CLTDPNO = C.CLTDPNO 
AND C.DEF=1



INSERT INTO DelLog
SELECT party_code,CLTDPID,BANKID,DEFDP,CLTDPID,BANKID,DEFDP,'CITRUS',GETDATE(),'CITRUS'
FROM Client4 WHERE  Depository IN ('CDSL','NSDL') 
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #MIG_CITRUS_MULTICLTID WHERE DEF=1)

DELETE FROM client4 WHERE  Depository IN ('CDSL','NSDL') 
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #MIG_CITRUS_MULTICLTID WHERE DEF=1)


INSERT INTO client4      
SELECT PARTY_CODE, PARTY_CODE,InstruTypeMst=1, DPID, CLTDPNO , DPTYPE, DEF       
FROM #MIG_CITRUS_MULTICLTID C WHERE  DEF=1    
AND  NOT EXISTS (SELECT PARTY_CODE FROM client4 M WHERE M.PARTY_CODE = C.PARTY_CODE 
				AND M.BankID = C.DPID AND M.Cltdpid = C.CLTDPNO)
				


--UPDATING AT BSE CM
INSERT INTO [196.1.115.201].BSEDB_ab.DBO.MULTICLTID      
SELECT PARTY_CODE, CLTDPNO, DPID, INTRODUCER, DPTYPE, DEF       
FROM #MIG_CITRUS_MULTICLTID C     
WHERE NOT EXISTS (SELECT PARTY_CODE FROM [196.1.115.201].BSEDB_ab.DBO.MULTICLTID M WHERE M.PARTY_CODE = C.PARTY_CODE 
				AND M.DPID = C.DPID AND M.CLTDPNO = C.CLTDPNO)

UPDATE [196.1.115.201].BSEDB_ab.DBO.MULTICLTID SET DEF = 0 WHERE PARTY_CODE = @PARTY_CODE
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #MIG_CITRUS_MULTICLTID WHERE DEF=1)
				          
UPDATE M SET DEF = 1
FROM 
#MIG_CITRUS_MULTICLTID C,
[196.1.115.201].BSEDB_ab.DBO.MULTICLTID M
WHERE M.PARTY_CODE = C.PARTY_CODE 
AND M.DPID = C.DPID AND M.CLTDPNO = C.CLTDPNO 
AND C.DEF=1



INSERT INTO ANAND.BSEDB_AB.DBO.DelLog
SELECT party_code,CLTDPID,BANKID,DEFDP,CLTDPID,BANKID,DEFDP,'CITRUS',GETDATE(),'CITRUS'
FROM Client4 WHERE  Depository IN ('CDSL','NSDL') 
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #MIG_CITRUS_MULTICLTID WHERE DEF=1)

DELETE FROM ANAND.BSEDB_AB.DBO.client4 WHERE  Depository IN ('CDSL','NSDL') 
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #MIG_CITRUS_MULTICLTID WHERE DEF=1)


INSERT INTO ANAND.BSEDB_AB.DBO.client4      
SELECT PARTY_CODE, PARTY_CODE,InstruTypeMst=1, DPID, CLTDPNO , DPTYPE, DEF       
FROM #MIG_CITRUS_MULTICLTID C WHERE  DEF=1    
AND  NOT EXISTS (SELECT PARTY_CODE FROM ANAND.BSEDB_AB.DBO.client4 M WHERE M.PARTY_CODE = C.PARTY_CODE 
				AND M.BankID = C.DPID AND M.Cltdpid = C.CLTDPNO)
				
DROP TABLE #MIG_CITRUS_MULTICLTID

/*------------------------- END OF THE PROC -----------------------*/

GO
