CREATE PROC PROC_UPDATE_BATCHNO (@REPORT_DATE VARCHAR(11), @FILENAME VARCHAR(50), @GENERATEBY VARCHAR(50), @BATCHNO INT)
AS

BEGIN
	INSERT INTO CLASS_BATCHNO VALUES (@REPORT_DATE, @BATCHNO, @FILENAME, GETDATE(), @GENERATEBY)
END

GO
