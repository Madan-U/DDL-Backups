CREATE PROCEDURE [dbo].[SETT_CLIENT_DATA]
(
@DATE VARCHAR(20)
)
AS
BEGIN 
 EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'SETT_CLIENT_DATA'   
 WAITFOR DELAY '00:0:45';  





SELECT CL_cODE,LONG_NAME,EMAIL,MOBILE_PAGER,BRANCH_CD,SUB_BROKER,REGION,PAN_GIR_NO
 FROM CLIENT_DETAILS WITH(NOLOCK)
WHERE CL_CODE IN 
(SELECT * FROM SSIS_FOCLIENT)

END

GO
