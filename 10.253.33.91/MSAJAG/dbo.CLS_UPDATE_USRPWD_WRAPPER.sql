CREATE PROC [dbo].[CLS_UPDATE_USRPWD_WRAPPER]   
(  
  @USENAME VARCHAR(30),  
  @NEWPWD VARCHAR(30),  
  @NEWPWD_ORG VARCHAR(30),  
  @EMAILID VARCHAR(100),
  @PANNO VARCHAR(10),
  @DOB VARCHAR(10),
  @SRC VARCHAR(10) = 'CP' -- CP/API
) AS  
  
DECLARE @ERRCODE  INT,  
  @ERRMSG VARCHAR(MAX),  
  @EMAILFROM VARCHAR(200),  
  @EMAILTO VARCHAR(200),  
  @EMAILSUBJECT VARCHAR(300),  
  @EMAILMESSAGE VARCHAR(MAX),  
  @SMTP_SRV_NAME VARCHAR(60),  
  @COMPANYNAME VARCHAR(200),
  @PartyCode varchar(30),
  @DOBNEW DATETIME
   
DECLARE @ADMIN_AUTO INT,
		@KEYVALUE VARCHAR(10)

SET @DOBNEW = convert(datetime,@DOB,105)

SELECT 
	@ADMIN_AUTO = FLDADMINAUTO,@PartyCode=isnull(fldstname,0) FROM TBLPRADNYAUSERS (NOLOCK)  
WHERE 
	FLDUSERNAME = @USENAME

SELECT 
	@KEYVALUE = KEYVALUE 
FROM 
	CLS_WEBCONFIG_SETTING (NOLOCK) 
WHERE
	KEYNAME = 'FORGETPASSOPTION'

IF (SELECT COUNT(1) FROM TBLADMIN (NOLOCK) WHERE FLDAUTO_ADMIN = @ADMIN_AUTO AND FLDSTNAME = 'CLIENT') > 0  
 BEGIN 

   --IF ISNULL(@PANNO,'') = '' AND ISNULL(@DOBNEW,'') = ''
   -- BEGIN
   --   SET @ERRCODE = -1  
   --   SET @ERRMSG = 'PLEASE ENTER PAN NO OR DATE OF BIRTH'     
   --   SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT,   
   --          EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME  
   --   RETURN   
   --END

  IF(CHARINDEX('E',@KEYVALUE) >= 1)
   BEGIN
    IF (SELECT COUNT(1) FROM MSAJAG.DBO.CLIENT_DETAILS  WHERE CL_CODE = @PartyCode AND EMAIL like '%' + @EMAILID + '%')= 0
     BEGIN   
      SET @ERRCODE = -1  
      SET @ERRMSG = 'INVALID EMAIL ID'     
      SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT,   
             EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME  
      RETURN   
     END 
   END

  IF(CHARINDEX('P',@KEYVALUE) >= 1)
   BEGIN
	IF (SELECT COUNT(1) FROM MSAJAG.DBO.CLIENT_DETAILS  WHERE CL_CODE = @PartyCode AND PAN_GIR_NO = @PANNO)= 0
     BEGIN   
      SET @ERRCODE = -1  
      SET @ERRMSG = 'INVALID PAN NUMBER'     
      SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT,   
             EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME  
      RETURN   
     END  
   END

  IF(CHARINDEX('D',@KEYVALUE) >= 1)
   BEGIN
	IF (SELECT COUNT(1) FROM MSAJAG.DBO.CLIENT_DETAILS  WHERE CL_CODE = @PartyCode AND DOB = @DOBNEW)= 0  
     BEGIN 
      SET @ERRCODE = -1  
      SET @ERRMSG = 'INVALID DATE OF BIRTH'     
      SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT,   
             EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME  
      RETURN   
     END 
   END

 -- Else IF(CHARINDEX('P',@KEYVALUE) >= 1) AND ISNULL(@PANNO,'') != ''
 --  BEGIN
	--IF (SELECT COUNT(1) FROM MSAJAG.DBO.CLIENT_DETAILS  WHERE CL_CODE = @PartyCode AND PAN_GIR_NO = @PANNO)= 0
 --    BEGIN   
 --     SET @ERRCODE = -1  
 --     SET @ERRMSG = 'INVALID PAN NUMBER'     
 --     SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT,   
 --            EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME  
 --     RETURN   
 --    END  
 --  END

 -- Else IF(CHARINDEX('D',@KEYVALUE) >= 1) AND ISNULL(@DOBNEW,'') != ''
 --  BEGIN
	--IF (SELECT COUNT(1) FROM MSAJAG.DBO.CLIENT_DETAILS  WHERE CL_CODE = @PartyCode AND DOB = @DOBNEW)= 0  
 --    BEGIN 
 --     SET @ERRCODE = -1  
 --     SET @ERRMSG = 'INVALID DATE OF BIRTH'     
 --     SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT,   
 --            EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME  
 --     RETURN   
 --    END 
 --  END

  

  ELSE 
   BEGIN
	IF (SELECT COUNT(1) FROM MSAJAG.DBO.CLIENT_DETAILS  WHERE CL_CODE = @PartyCode AND EMAIL like '%' + @EMAILID + '%')= 0
     BEGIN   
      SET @ERRCODE = -1  
      SET @ERRMSG = 'INVALID EMAIL ID'     
      SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT,   
             EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME  
      RETURN   
     END 
   END
 END 
 
ELSE  

 BEGIN  
  IF (SELECT COUNT(1) FROM TBLPRADNYAUSERS (NOLOCK) WHERE FLDUSERNAME = @USENAME  
      AND (FLDADDRESS1 = @EMAILID OR FLDADDRESS2 = @EMAILID )) = 0   
   BEGIN
    SET @ERRCODE = -1  
    SET @ERRMSG = 'INVALID EMAIL ID'     
    SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT,   
           EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME  
    RETURN  
   END  
 END  

EXEC CLS_UPDATE_USRPWD @USENAME,@NEWPWD,@NEWPWD_ORG,@EMAILID

UPDATE A SET  FLDFIRSTLOGIN = CASE WHEN @SRC = 'API' THEN 'N' ELSE 'Y' END, FLDFORCELOGOUT = 0,FLDSTATUS=0   
FROM TBLUSERCONTROLMASTER A,  
TBLPRADNYAUSERS B  
WHERE A.FLDUSERID = B.FLDAUTO AND B.FLDUSERNAME =   @USENAME
  
SELECT ERRCODE = @ERRCODE, ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = REPLACE( @EMAILSUBJECT, '&','~' ),   
EMAILMESSAGE = Replace (REPLACE(REPLACE( @EMAILMESSAGE, '<', '$' ),'>','@'),'&','~') , SMTP_SRV_NAME = @SMTP_SRV_NAME

GO
