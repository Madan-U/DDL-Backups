CREATE PROCEDURE Client_Printflag_Mismatch
AS
SELECT CL_CODE,EXCHANGE,PRINT_OPTIONS  FROM CLIENT_BROK_DETAILS(NOLOCK) WHERE CL_CODE IN (
SELECT CL_CODE  FROM CLIENT_BROK_DETAILS (NOLOCK) WHERE  EXCHANGE NOT IN ('MCX','NCX')
AND INACTIVE_FROM > GETDATE ()
GROUP BY CL_CODE 
HAVING COUNT(DISTINCT PRINT_OPTIONS) > 1  
) ORDER BY CL_CODE

GO
