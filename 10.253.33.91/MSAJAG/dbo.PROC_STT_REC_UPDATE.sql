-- Object: PROCEDURE dbo.PROC_STT_REC_UPDATE
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

  
CREATE PROC [dbo].[PROC_STT_REC_UPDATE]  
AS  
  
IF (SELECT ISNULL(COUNT(1),0) FROM TBL_STT_SCRIP_TMP  
 WHERE CHARGE_FLAG = 0) > 0  
BEGIN  
 UPDATE TBL_STT_SCRIP SET DATETO = LEFT(GETDATE()-1,11) + ' 23:59:59'  
 WHERE NOT EXISTS (SELECT SCRIP_CD FROM TBL_STT_SCRIP_TMP T  
       WHERE TBL_STT_SCRIP.SCRIP_CD = T.SCRIP_CD  
       AND TBL_STT_SCRIP.SERIES = T.SERIES  
       AND T.CHARGE_FLAG = 0)  
 AND TBL_STT_SCRIP.CHARGE_FLAG = 0  
 AND DATETO = LEFT(GETDATE(),11) + ' 23:59:59'  
  
 UPDATE TBL_STT_SCRIP SET DATETO = LEFT(GETDATE()-1,11) + ' 23:59:59'  
 WHERE EXISTS (SELECT SCRIP_CD FROM TBL_STT_SCRIP_TMP T  
       WHERE TBL_STT_SCRIP.SCRIP_CD = T.SCRIP_CD  
       AND TBL_STT_SCRIP.SERIES = T.SERIES  
       AND T.CHARGE_FLAG = 0)  
 AND TBL_STT_SCRIP.CHARGE_FLAG = 1  
 AND DATETO = LEFT(GETDATE(),11) + ' 23:59:59'  
END  
  
IF (SELECT ISNULL(COUNT(1),0) FROM TBL_STT_SCRIP_TMP  
 WHERE CHARGE_FLAG = 0) > 0  
BEGIN  
 UPDATE TBL_STT_SCRIP SET DATETO = LEFT(GETDATE()-1,11) + ' 23:59:59'  
 WHERE NOT EXISTS (SELECT SCRIP_CD FROM TBL_STT_SCRIP_TMP T  
       WHERE TBL_STT_SCRIP.SCRIP_CD = T.SCRIP_CD  
       AND TBL_STT_SCRIP.SERIES = T.SERIES  
       AND T.CHARGE_FLAG = 1)  
 AND TBL_STT_SCRIP.CHARGE_FLAG = 1  
 AND DATETO = LEFT(GETDATE(),11) + ' 23:59:59'  
  
 UPDATE TBL_STT_SCRIP SET DATETO = LEFT(GETDATE()-1,11) + ' 23:59:59'  
 WHERE EXISTS (SELECT SCRIP_CD FROM TBL_STT_SCRIP_TMP T  
       WHERE TBL_STT_SCRIP.SCRIP_CD = T.SCRIP_CD  
       AND TBL_STT_SCRIP.SERIES = T.SERIES  
       AND T.CHARGE_FLAG = 1)  
 AND TBL_STT_SCRIP.CHARGE_FLAG = 0  
 AND DATETO = LEFT(GETDATE(),11) + ' 23:59:59'  
END  
  
INSERT INTO TBL_STT_SCRIP  
SELECT SCRIP_CD,DATEFROM,DATETO,ADDEDBY,ADDEDON,MODIFIEDBY,MODIFIEDON,SERIES,CHARGE_FLAG FROM TBL_STT_SCRIP_TMP  
WHERE NOT EXISTS (SELECT SCRIP_CD FROM TBL_STT_SCRIP T  
      WHERE TBL_STT_SCRIP_TMP.SCRIP_CD = T.SCRIP_CD  
      AND TBL_STT_SCRIP_TMP.SERIES = T.SERIES  
      AND LEFT(GETDATE(),11) BETWEEN DATEFROM AND DATETO)  
  
DELETE FROM TBL_STT_SCRIP WHERE DATEFROM > DATETO

GO
