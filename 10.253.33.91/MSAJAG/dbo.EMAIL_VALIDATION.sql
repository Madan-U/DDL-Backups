CREATE PROC [dbo].[EMAIL_VALIDATION]    
AS     

SELECT * INTO #EMAIL FROM (
SELECT CL_CODE,EMAIL,'GMAIL' AS DOMAIN  FROM CLIENT_DETAILS 
WHERE SOUNDEX(RIGHT(EMAIL,LEN(EMAIL)-CHARINDEX('@',EMAIL)))=SOUNDEX('GMAIL.COM')
AND RIGHT(EMAIL,LEN(EMAIL)-CHARINDEX('@',EMAIL))  <> 'GMAIL.COM'
UNION ALL
SELECT CL_CODE,EMAIL,'HOTMAIL' AS DOMAIN FROM CLIENT_DETAILS 
WHERE SOUNDEX(RIGHT(EMAIL,LEN(EMAIL)-CHARINDEX('@',EMAIL)))=SOUNDEX('HOTMAIL.COM')
AND RIGHT(EMAIL,LEN(EMAIL)-CHARINDEX('@',EMAIL))  not in ('HOTMAIL.COM','HOTMAIL.CO.UK')
UNION ALL
SELECT CL_CODE,EMAIL,'yahoo' AS DOMAIN FROM CLIENT_DETAILS 
WHERE SOUNDEX(RIGHT(EMAIL,LEN(EMAIL)-CHARINDEX('@',EMAIL)))=SOUNDEX('YAHOO.COM')
AND RIGHT(EMAIL,LEN(EMAIL)-CHARINDEX('@',EMAIL))  NOT IN ( 'yahoo.com', 'yahoo.co.uk','yahoo.in','yahoo.com.au','yahoo.co.jp', 'yahoo.co.kr', 
'yahoo.co.id', 'yahoo.co.in', 'yahoo.com.sg', 'yahoo.com.ph','yahoomail.co.in','yahoomail.coM','yahoo.ca')
UNION ALL
SELECT CL_CODE,EMAIL,'REDIFFMAIL' AS DOMAIN FROM CLIENT_DETAILS 
WHERE SOUNDEX(RIGHT(EMAIL,LEN(EMAIL)-CHARINDEX('@',EMAIL)))=SOUNDEX('REDIFFMAIL.COM')
