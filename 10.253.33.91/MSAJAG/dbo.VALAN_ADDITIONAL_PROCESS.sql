--- PROCEDURE FOR CASH, MFSS AND SLBS SEGMENT AND COMM DELIVERY

CREATE PROC [dbo].[VALAN_ADDITIONAL_PROCESS]    
(    
 @SETT_NO VARCHAR(7),    
 @SETT_TYPE VARCHAR(2)    
)    
AS    


-----------------------------
DECLARE  @SETT_NO_L VARCHAR(7)
DECLARE  @SETT_TYPE_L VARCHAR(2)    

SET @SETT_NO_L = @SETT_NO
SET @SETT_TYPE_L= @SETT_TYPE    


-----------------------------

    
DECLARE @SBCCODE VARCHAR(10)     
DECLARE @SBCCODEPAY VARCHAR(10)     
DECLARE @KKCCODE VARCHAR(10)     
DECLARE @KKCCODEPAY VARCHAR(10)     
    
SET @SBCCODE = ''    
SET @SBCCODEPAY = ''    
SET @KKCCODE = ''    
SET @KKCCODEPAY = ''    
    
SELECT @SBCCODE = ACCODE FROM VALANACCOUNT     
WHERE ACNAME = 'SWACHH BHARAT CESS'    
    
SELECT @SBCCODEPAY = ACCODE FROM VALANACCOUNT     
WHERE ACNAME = 'SWACHH BHARAT CESS PAYABLE'    
    
SELECT @KKCCODE = ACCODE FROM VALANACCOUNT     
WHERE ACNAME = 'KRISHI KALYAN CESS'    
    
SELECT @KKCCODEPAY = ACCODE FROM VALANACCOUNT     
WHERE ACNAME = 'KRISHI KALYAN CESS PAYABLE'    
    
IF ISNULL(@SBCCODE,'')='' AND ISNULL(@KKCCODE,'')=''
BEGIN    
 RETURN    
END    
    
SELECT A.* INTO #SBC    
FROM ACCBILL A, VALANACCOUNT V, GLOBALS    
WHERE SETT_NO = @SETT_NO_L    
AND SETT_TYPE  = @SETT_TYPE_L    
AND PARTY_CODE = ACCODE    
AND V.ACNAME = 'SERVICE TAX'    
AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
AND ISNULL(SBCCHARGE,0) > 0    
    
IF (SELECT ISNULL(COUNT(1),0) FROM #SBC) > 0     
BEGIN    
 UPDATE #SBC SET PARTY_CODE = @SBCCODE, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(SBCCHARGE,0) / 100,2)    
 FROM GLOBALS    
 WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
 AND ISNULL(SBCCHARGE,0) > 0    
    
 SELECT A.* INTO #SERTAX    
 FROM ACCBILL A, VALANACCOUNT V, GLOBALS    
 WHERE SETT_NO = @SETT_NO_L    
 AND SETT_TYPE  = @SETT_TYPE_L    
 AND PARTY_CODE = ACCODE    
 AND V.ACNAME = 'SERVICE TAX'    
 AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
 AND ISNULL(SBCCHARGE,0) > 0    
    
 UPDATE #SERTAX SET AMOUNT = #SERTAX.AMOUNT - #SBC.AMOUNT    
 FROM #SBC     
 WHERE #SERTAX.SETT_NO = #SBC.SETT_NO    
 AND #SERTAX.SETT_TYPE = #SBC.SETT_TYPE    
 AND #SERTAX.BRANCHCD = #SBC.BRANCHCD    
 
    
 INSERT INTO ACCBILL    
 SELECT * FROM #SBC    
	 
	SELECT A.* INTO #KKC    
	FROM ACCBILL A, VALANACCOUNT V, GLOBALS    
	WHERE SETT_NO = @SETT_NO_L    
	AND SETT_TYPE  = @SETT_TYPE_L    
	AND PARTY_CODE = ACCODE    
	AND V.ACNAME = 'SERVICE TAX'    
	AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
	AND ISNULL(KRISHICHARGE,0) > 0    

	IF (SELECT ISNULL(COUNT(1),0) FROM #KKC) > 0     
	BEGIN    
	 UPDATE #KKC SET PARTY_CODE = @KKCCODE, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(KRISHICHARGE,0) / 100,2)    
	 FROM GLOBALS    
	 WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
	 AND ISNULL(KRISHICHARGE,0) > 0    
	 
	 UPDATE #SERTAX SET AMOUNT = #SERTAX.AMOUNT - #KKC.AMOUNT    
	 FROM #KKC     
	 WHERE #SERTAX.SETT_NO = #KKC.SETT_NO    
	 AND #SERTAX.SETT_TYPE = #KKC.SETT_TYPE    
	 AND #SERTAX.BRANCHCD = #KKC.BRANCHCD    
	 
	 INSERT INTO ACCBILL    
	 SELECT * FROM #KKC    
	  
	END    
	DROP TABLE #KKC    
   
 DELETE FROM ACCBILL    
 WHERE SETT_NO = @SETT_NO_L    
 AND SETT_TYPE  = @SETT_TYPE_L    
 AND EXISTS (SELECT PARTY_CODE FROM #SERTAX WHERE ACCBILL.PARTY_CODE = #SERTAX.PARTY_CODE)    	
 
 INSERT INTO ACCBILL    
 SELECT * FROM #SERTAX    
    
 DROP TABLE #SERTAX    
END    
DROP TABLE #SBC    
    
SELECT A.* INTO #INST_SBC    
FROM IACCBILL A, VALANACCOUNT V, GLOBALS    
WHERE SETT_NO = @SETT_NO_L    
AND SETT_TYPE  = @SETT_TYPE_L    
AND PARTY_CODE = ACCODE    
AND V.ACNAME = 'SERVICE TAX'    
AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
AND ISNULL(SBCCHARGE,0) > 0    
    
IF (SELECT ISNULL(COUNT(1),0) FROM #INST_SBC) > 0     
BEGIN    
 UPDATE #INST_SBC SET PARTY_CODE = @SBCCODE, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(SBCCHARGE,0) / 100,2)    
 FROM GLOBALS    
 WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
 AND ISNULL(SBCCHARGE,0) > 0    
    
 SELECT A.* INTO #INST_SERTAX    
 FROM IACCBILL A, VALANACCOUNT V, GLOBALS    
 WHERE SETT_NO = @SETT_NO_L    
 AND SETT_TYPE  = @SETT_TYPE_L    
 AND PARTY_CODE = ACCODE    
 AND V.ACNAME = 'SERVICE TAX'    
 AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
 AND ISNULL(SBCCHARGE,0) > 0    
    
 UPDATE #INST_SERTAX SET AMOUNT = #INST_SERTAX.AMOUNT - #INST_SBC.AMOUNT    
 FROM #INST_SBC     
 WHERE #INST_SERTAX.SETT_NO = #INST_SBC.SETT_NO    
 AND #INST_SERTAX.SETT_TYPE = #INST_SBC.SETT_TYPE    
 AND #INST_SERTAX.BRANCHCD = #INST_SBC.BRANCHCD    
     
 
 INSERT INTO IACCBILL    
 SELECT * FROM #INST_SBC    


	 
	SELECT A.* INTO #INST_KKC    
	FROM IACCBILL A, VALANACCOUNT V, GLOBALS    
	WHERE SETT_NO = @SETT_NO_L    
	AND SETT_TYPE  = @SETT_TYPE_L    
	AND PARTY_CODE = ACCODE    
	AND V.ACNAME = 'SERVICE TAX'    
	AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
	AND ISNULL(KRISHICHARGE,0) > 0    
	    
	IF (SELECT ISNULL(COUNT(1),0) FROM #INST_KKC) > 0     
	BEGIN    
	 UPDATE #INST_KKC SET PARTY_CODE = @KKCCODE, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(KRISHICHARGE,0) / 100,2)    
	 FROM GLOBALS    
	 WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
	 AND ISNULL(KRISHICHARGE,0) > 0    
	 
	 UPDATE #INST_SERTAX SET AMOUNT = #INST_SERTAX.AMOUNT - #INST_KKC.AMOUNT    
	 FROM #INST_KKC     
	 WHERE #INST_SERTAX.SETT_NO = #INST_KKC.SETT_NO    
	 AND #INST_SERTAX.SETT_TYPE = #INST_KKC.SETT_TYPE    
	 AND #INST_SERTAX.BRANCHCD = #INST_KKC.BRANCHCD    
	 
	 INSERT INTO IACCBILL    
	 SELECT * FROM #INST_KKC    
	   
	END    
	DROP TABLE #INST_KKC    
	
  
 DELETE FROM IACCBILL    
 WHERE SETT_NO = @SETT_NO_L    
 AND SETT_TYPE  = @SETT_TYPE_L    
 AND EXISTS (SELECT PARTY_CODE FROM #INST_SERTAX WHERE IACCBILL.PARTY_CODE = #INST_SERTAX.PARTY_CODE)   	
     
 INSERT INTO IACCBILL    
 SELECT * FROM #INST_SERTAX    
    
 DROP TABLE #INST_SERTAX   
END    
DROP TABLE #INST_SBC    
    
SELECT A.* INTO #PAYSBC    
FROM ACCBILL A, VALANACCOUNT V, GLOBALS    
WHERE SETT_NO = @SETT_NO_L    
AND SETT_TYPE  = @SETT_TYPE_L    
AND PARTY_CODE = ACCODE    
AND V.ACNAME = 'SERVICE TAX PAYABLE'    
AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
AND ISNULL(SBCCHARGE,0) > 0    
    
IF (SELECT ISNULL(COUNT(1),0) FROM #PAYSBC) > 0     
BEGIN    
 UPDATE #PAYSBC SET PARTY_CODE = @SBCCODEPAY, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(SBCCHARGE,0) / 100,2)    
 FROM GLOBALS    
 WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
 AND ISNULL(SBCCHARGE,0) > 0    
    
 SELECT A.* INTO #PAYSERTAX    
 FROM ACCBILL A, VALANACCOUNT V, GLOBALS    
 WHERE SETT_NO = @SETT_NO_L    
 AND SETT_TYPE  = @SETT_TYPE_L    
 AND PARTY_CODE = ACCODE    
 AND V.ACNAME = 'SERVICE TAX PAYABLE'    
 AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
 AND ISNULL(SBCCHARGE,0) > 0    
    
 UPDATE #PAYSERTAX SET AMOUNT = #PAYSERTAX.AMOUNT - #PAYSBC.AMOUNT    
 FROM #PAYSBC     
 WHERE #PAYSERTAX.SETT_NO = #PAYSBC.SETT_NO    
 AND #PAYSERTAX.SETT_TYPE = #PAYSBC.SETT_TYPE    
 AND #PAYSERTAX.BRANCHCD = #PAYSBC.BRANCHCD    
    
    
 INSERT INTO ACCBILL    
 SELECT * FROM #PAYSBC    
 
	 
	SELECT A.* INTO #PAYKKC    
	FROM ACCBILL A, VALANACCOUNT V, GLOBALS    
	WHERE SETT_NO = @SETT_NO_L    
	AND SETT_TYPE  = @SETT_TYPE_L    
	AND PARTY_CODE = ACCODE    
	AND V.ACNAME = 'SERVICE TAX PAYABLE'    
	AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
	AND ISNULL(KRISHICHARGE,0) > 0    
	    
	IF (SELECT ISNULL(COUNT(1),0) FROM #PAYKKC) > 0     
	BEGIN    
	 UPDATE #PAYKKC SET PARTY_CODE = @KKCCODEPAY, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(KRISHICHARGE,0) / 100,2)    
	 FROM GLOBALS    
	 WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
	 AND ISNULL(KRISHICHARGE,0) > 0    
	 
	 UPDATE #PAYSERTAX SET AMOUNT = #PAYSERTAX.AMOUNT - #PAYKKC.AMOUNT    
	 FROM #PAYKKC     
	 WHERE #PAYSERTAX.SETT_NO = #PAYKKC.SETT_NO    
	 AND #PAYSERTAX.SETT_TYPE = #PAYKKC.SETT_TYPE    
	 AND #PAYSERTAX.BRANCHCD = #PAYKKC.BRANCHCD    
	  
	 INSERT INTO ACCBILL    
	 SELECT * FROM #PAYKKC    

	END    
	DROP TABLE #PAYKKC  
	
 
 DELETE FROM ACCBILL    
 WHERE SETT_NO = @SETT_NO_L    
 AND SETT_TYPE  = @SETT_TYPE_L    
 AND EXISTS (SELECT PARTY_CODE FROM #PAYSERTAX WHERE ACCBILL.PARTY_CODE = #PAYSERTAX.PARTY_CODE)    	
 
 INSERT INTO ACCBILL    
 SELECT * FROM #PAYSERTAX    
    
 DROP TABLE #PAYSERTAX    
END    
DROP TABLE #PAYSBC    
    
SELECT A.* INTO #PAYINST_SBC    
FROM IACCBILL A, VALANACCOUNT V, GLOBALS    
WHERE SETT_NO = @SETT_NO_L    
AND SETT_TYPE  = @SETT_TYPE_L    
AND PARTY_CODE = ACCODE    
AND V.ACNAME = 'SERVICE TAX PAYABLE'    
AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
AND ISNULL(SBCCHARGE,0) > 0    
    
IF (SELECT ISNULL(COUNT(1),0) FROM #PAYINST_SBC) > 0     
BEGIN    
 UPDATE #PAYINST_SBC SET PARTY_CODE = @SBCCODEPAY, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(SBCCHARGE,0) / 100,2)    
 FROM GLOBALS    
 WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
 AND ISNULL(SBCCHARGE,0) > 0    
    
 SELECT A.* INTO #PAYINST_SERTAX    
 FROM IACCBILL A, VALANACCOUNT V, GLOBALS    
 WHERE SETT_NO = @SETT_NO_L    
 AND SETT_TYPE  = @SETT_TYPE_L    
 AND PARTY_CODE = ACCODE    
 AND V.ACNAME = 'SERVICE TAX PAYABLE'    
 AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
 AND ISNULL(SBCCHARGE,0) > 0    
    
 UPDATE #PAYINST_SERTAX SET AMOUNT = #PAYINST_SERTAX.AMOUNT - #PAYINST_SBC.AMOUNT    
 FROM #PAYINST_SBC     
 WHERE #PAYINST_SERTAX.SETT_NO = #PAYINST_SBC.SETT_NO    
 AND #PAYINST_SERTAX.SETT_TYPE = #PAYINST_SBC.SETT_TYPE    
 AND #PAYINST_SERTAX.BRANCHCD = #PAYINST_SBC.BRANCHCD    
    
    
 INSERT INTO IACCBILL    
 SELECT * FROM #PAYINST_SBC    
 
	 
	SELECT A.* INTO #PAYINST_KKC    
	FROM IACCBILL A, VALANACCOUNT V, GLOBALS    
	WHERE SETT_NO = @SETT_NO_L    
	AND SETT_TYPE  = @SETT_TYPE_L    
	AND PARTY_CODE = ACCODE    
	AND V.ACNAME = 'SERVICE TAX PAYABLE'    
	AND START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
	AND ISNULL(KRISHICHARGE,0) > 0    
	    
	IF (SELECT ISNULL(COUNT(1),0) FROM #PAYINST_KKC) > 0     
	BEGIN    
	 UPDATE #PAYINST_KKC SET PARTY_CODE = @KKCCODEPAY, AMOUNT = ROUND((AMOUNT * 100 / SERVICE_TAX) * ISNULL(KRISHICHARGE,0) / 100,2)    
	 FROM GLOBALS    
	 WHERE START_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT     
	 AND ISNULL(KRISHICHARGE,0) > 0    

	 UPDATE #PAYINST_SERTAX SET AMOUNT = #PAYINST_SERTAX.AMOUNT - #PAYINST_KKC.AMOUNT    
	 FROM #PAYINST_KKC     
	 WHERE #PAYINST_SERTAX.SETT_NO = #PAYINST_KKC.SETT_NO    
	 AND #PAYINST_SERTAX.SETT_TYPE = #PAYINST_KKC.SETT_TYPE    
	 AND #PAYINST_SERTAX.BRANCHCD = #PAYINST_KKC.BRANCHCD    
	 
	 
	 DELETE FROM IACCBILL    
	 WHERE SETT_NO = @SETT_NO_L    
	 AND SETT_TYPE  = @SETT_TYPE_L    
	 AND EXISTS (SELECT PARTY_CODE FROM #PAYINST_SERTAX WHERE IACCBILL.PARTY_CODE = #PAYINST_SERTAX.PARTY_CODE)    

	 INSERT INTO IACCBILL    
	 SELECT * FROM #PAYINST_KKC    
	    
	END    
	DROP TABLE #PAYINST_KKC 
 
  INSERT INTO IACCBILL    
 SELECT * FROM #PAYINST_SERTAX    
    
 DROP TABLE #PAYINST_SERTAX    
 
END    

DROP TABLE #PAYINST_SBC

GO
