-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[CLS_GET_ADVANCE_FILTER_VALUE]
(
	@SESSIONID VARCHAR(50),
	@KEY VARCHAR(500)
)

AS

SELECT PVALUE FROM CLS_ADVANCE_FILTER_PARAMETERS WHERE SESSIONID = @SESSIONID AND PKEY = @KEY

GO
