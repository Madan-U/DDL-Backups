CREATE PROCEDURE [dbo].[SSRS_CLIENT_DATA_DP]
AS BEGIN

 EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'VISHAL_KYC_DATA'   
 WAITFOR DELAY '00:0:45';  


SELECT CLIENT_CODE,NISE_PARTY_cODE,FIRST_HOLD_NAME,ACTIVE_DATE,STATUS,TEMPLATE_CODE,FIRST_HOLD_STATE,EMAIL_ADD,FIRST_HOLD_MOBILE
INTO #TEMP1
 FROM AGMUBODPL3.dmat.citrus_usr.TBL_CLIENT_MASTER WHERE CLIENT_CODE IN (SELECT * FROM SSRS_CLIENTID)
 
 
 SELECT A.*,ACTUAL_AMOUNT FROM #TEMP1 A
 LEFT OUTER JOIN
 AGMUBODPL3.dmat.citrus_usr.VW_ACC_CURR_BAL B WITH(NOLOCK)
 ON A.CLIENT_CODE=B.CLIENT_cODE
 
 
 
 END

GO
