CREATE PROCEDURE [dbo].[PROC_SHORT_MARGIN_UPINTRA_MCX]
	@MARGINDATE VARCHAR(11),
	@FLDFILENAME VARCHAR (300),
	@UPLOAD_BY VARCHAR(30) = ''
AS

TRUNCATE TABLE TBL_MCX_SHORT_ALLOC_INTRA_TMP
 
DECLARE
	@STRSQL VARCHAR(1000)
		
DECLARE
	@FILENAME VARCHAR(50)
SELECT @FILENAME = RIGHT(@FLDFILENAME, CHARINDEX('\', REVERSE(@FLDFILENAME),0) - 1)

	Set @strSql = 'Bulk insert TBL_MCX_SHORT_ALLOC_INTRA_TMP from ''' + @FLDFILENAME  + '''  with ( FIELDTERMINATOR = '','', ROWTERMINATOR = ''\n'' )'
	Exec(@strSql)
	   
	DELETE FROM TBL_MCX_SHORT_ALLOC_INTRA WHERE MARGINDATE = @MARGINDATE 
	
INSERT INTO TBL_MCX_SHORT_ALLOC_INTRA 
SELECT MARGINDATE,CMID,TMID,CLIENT_CODE,EOD_SHORT_ALLOC, 
@FILENAME,@UPLOAD_BY,UPLOAD_ON=GETDATE()
FROM TBL_MCX_SHORT_ALLOC_INTRA_TMP WHERE MARGINDATE = @MARGINDATE

GO
