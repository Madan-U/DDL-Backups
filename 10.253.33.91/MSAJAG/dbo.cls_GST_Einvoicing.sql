CREATE PROC [dbo].[cls_GST_Einvoicing]
@FROMDATE varchar(12), 
@TODATE varchar(12)
AS
BEGIN

seT @FROMDATE = CONVERT(datetime,@FROMDATE,103)
seT @TODATE = CONVERT(datetime,@TODATE,103)




SELECT  [SUPPLY TYPE CODE] = 'B2B',[DOCUMENT TYPE] = 'TAX INVOICE',[DOCUMENT NUMBER] = INV_NO, [DOCUMENT DATE]=CONVERT(VARCHAR(11),@TODATE,103), [BUYER GSTIN]=B.GST_NO,
         [BUYER LEGAL NAME] = B.LONG_NAME, [BUYER POS] = A.SOURCESTATE, [BUYER ADDR1]= B.L_ADDRESS1,[BUYER ADDR2]= L_ADDRESS2,	[BUYER LOCATION]=L_CITY ,[BUYER PIN CODE]=L_ZIP,[BUYER STATE]= A.TARGETSTATE,
		 [SL.NO.] = 1 , [PRODUCT DESCRIPTION] ='N/A',	[IS_SERVICE] = 'YES',[HSN CODE]='997212',	[BAR CODE]='N/A', 	[QUANTITY]='0',	[FREE QUANTITY]='N/A',	[UNIT]='OTHERS',[UNIT PRICE]=SUM(TAXABLE_AMT),	[GROSS AMOUNT]=SUM(TAXABLE_AMT),	[DISCOUNT]='N/A',	
		 [PRE TAX VALUE]='N/A',	[TAXABLE VALUE]=SUM(TAXABLE_AMT), [GST RATE (%)] ='18',	[SGST AMT(RS)] =SUM(SGST_AMOUNT),[CGST AMT (RS)] =SUM(CGST_AMOUNT),	[IGST AMT (RS)] =SUM(IGST_AMOUNT),	
		 [CESS RATE (%)] ='N/A',[CESS AMT ADVAL (RS)] ='N/A'	,[CESS NON ADVAL AMT (RS)] ='N/A', [STATE CESS RATE (%)] ='N/A',[STATE CESS ADVAL AMT (RS)] ='N/A'	,[STATE CESS NON-ADVAL AMT (RS)] ='N/A'	,	[OTHER CHARGES] ='N/A'	,
		 [ITEM TOTAL] =SUM(round(TAXABLE_AMT,2)) + sum(round(TAX_AMT,2)), [BATCH NAME] ='N/A'	,[BATCH EXPIRY DT] ='N/A', [WARRANTY DT] ='N/A'	, [TOTAL TAXABLE VALUE] =SUM(TAXABLE_AMT)	,	[SGST AMT] =SUM(SGST_AMOUNT),	[CGST AMT] =SUM(CGST_AMOUNT),	[IGST AMT] =SUM(IGST_AMOUNT)	,
		 [CESS AMT] ='N/A', [STATE CESS AMT] ='N/A', [DISCOUNT] ='N/A',[OTHER CHARGES] ='N/A',[ROUND OFF] ='N/A', [TOTAL INVOICE VALUE] =SUM(TAXABLE_AMT) + SUM(TAX_AMT), [SHIPPING BILL NO] ='N/A', [SHIPPING BILL DT] ='N/A', [PORT] ='N/A',	[REFUND CLAIM] ='N/A',	[FOREIGN CURRENCY] ='N/A', 	[COUNTRY CODE] ='N/A', 	
		 [EXPORT DUTY AMOUNT] ='N/A',	[TRANS ID] ='N/A',	[TRANS NAME] ='N/A',	[TRANS MODE] ='N/A', 	[DISTANCE] ='N/A', 	[TRANS DOC NO.] ='N/A',	[TRANS DOC DATE] ='N/A',	[VEHICLE NO.] ='N/A',	[VEHICLE TYPE] ='N/A',	[ERROR LIST] ='N/A'
FROM TBL_GST_INVOICE A  WITH (INDEX(PARTY)), CLIENT_DETAILS  B
WHERE A.PARTY_CODE = B.PARTY_CODE
AND INVOICE_DATE BETWEEN @FROMDATE AND @TODATE 
AND B.GST_NO <> ''
--AND B.GST_NO IN ('27AAACA8324Q1ZA','27ADOPK3701D1ZT',
--'27AABFD9775C1ZC',
--'27ACAPW0615K1ZJ',
--'27ACPPN8252N1ZQ',
--'27AAACG3691A1ZX',
--'27ASPPM2152A1ZY',
--'27AQSPS4429B1ZJ',
--'27ACHPB9314P1Z9',
--'27CATPK4679L2ZN')
GROUP BY A.INV_NO, B.GST_NO, B.long_name, A.SOURCESTATE, B.l_address1, l_address2, l_city, l_zip, A.TARGETSTATE





--SELECT  TOP 10 * 
--FROM TBL_GST_INVOICE 
--WHERE INVOICE_DATE BETWEEN @FROMDATE AND @TODATE





END

GO
