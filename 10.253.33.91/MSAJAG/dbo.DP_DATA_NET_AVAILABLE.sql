CREATE PROC [dbo].[DP_DATA_NET_AVAILABLE]     
    
AS    
    


SELECT CLIENT_CODE,NISE_PARTY_CODE,FIRST_HOLD_NAME,ACTIVE_DATE,TEMPLATE_CODE  INTO #TEMP FROM   AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER 
WHERE status='ACTIVE'

SELECT A.*,B.ACTUAL_AMOUNT INTO #OS FROM #TEMP A
LEFT OUTER JOIN
 AGMUBODPL3.DMAT.CITRUS_USR.VW_ACC_CURR_BAL B
ON A.CLIENT_CODE=B.CLIENT_CODE


SELECT * INTO #OS1 FROM #OS WHERE ACTUAL_AMOUNT>0

SELECT A.*,sum(Net_Ledger) Net_Ledger,SUM(NET_AVAILABLE)AS NET_AVAILABLE FROM #OS1 A
LEFT OUTER JOIN
[CSOKYC-6].GENERAL.DBO.tbl_RMS_Collection_Cli B
ON A.NISE_PARTY_CODE=B.PARTY_cODE
GROUP BY CLIENT_CODE,NISE_PARTY_CODE,FIRST_HOLD_NAME,ACTIVE_DATE,TEMPLATE_CODE,ACTUAL_AMOUNT

GO
