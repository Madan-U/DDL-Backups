-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROCEDURE [dbo].[RPT_ACTIVE_CLIENT_VDT] (@RUNDATE DATETIME)

AS --- EXEC RPT_ACTIVE_CLIENT_VDT 'JUN 28 2021'

SELECT EXCHNAGE='NSECM',A.CLTCODE,A.VAMT,A.DRCR,A.VTYP,A.VNO,A.VDT,A.EDT,
A.CDT,B.RELDT,B.DDNO,A.NARRATION,A.ENTEREDBY,ACTIVE_DATE=CONVERT(DATETIME,'') INTO #DATA
FROM ACCOUNT.DBO.LEDGER A WITH (NOLOCK) ,ACCOUNT.DBO.LEDGER1 B WITH (NOLOCK)
WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND A.VAMT = B.RELAMT 
AND A.VTYP='2' AND A.DRCR='C' AND A.VDT >= @RUNDATE-5  AND A.VDT <= @RUNDATE + ' 23:59'

INSERT INTO #DATA
SELECT EXCHNAGE='BSECM',A.CLTCODE,A.VAMT,A.DRCR,A.VTYP,A.VNO,A.VDT,A.EDT,
A.CDT,B.RELDT,B.DDNO,A.NARRATION,A.ENTEREDBY,ACTIVE_DATE=CONVERT(DATETIME,'') 
FROM AngelBSECM.ACCOUNT_AB.DBO.LEDGER A WITH (NOLOCK) ,AngelBSECM.ACCOUNT_AB.DBO.LEDGER1 B WITH (NOLOCK)
WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND A.VAMT = B.RELAMT 
AND A.VTYP='2' AND A.DRCR='C' AND A.VDT >= @RUNDATE-5   AND A.VDT <= @RUNDATE + ' 23:59'

UPDATE #DATA SET ACTIVE_DATE = ADATE FROM #DATA A,
(SELECT CL_CODE,ADATE=LEFT(MIN(Active_Date),11) FROM CLIENT_BROK_DETAILS WITH (NOLOCK)
WHERE CL_CODE IN (SELECT DISTINCT CLTCODE FROM #DATA) GROUP BY CL_CODE) B
WHERE A.CLTCODE = B.Cl_Code

SELECT EXCHNAGE='EXCHNAGE',CLTCODE='CLTCODE',VAMT='VAMT',DRCR='DRCR',VTYP='VTYP',
VNO='VNO',VDT='VDT',EDT='EDT',CDT='CDT',RELDT='RELDT',DDNO='DDNO',NARRATION='NARRATION',
ENTEREDBY='ENTEREDBY',ACTIVE_DATE='ACTIVE_DATE'
UNION ALL
SELECT EXCHNAGE,CLTCODE,CONVERT(VARCHAR,VAMT),DRCR,VTYP=CONVERT(VARCHAR,VTYP),VNO=CONVERT(VARCHAR,VNO),
VDT=CONVERT(VARCHAR(11),VDT,105),EDT=CONVERT(VARCHAR(11),EDT,105),CDT=CONVERT(VARCHAR(11),CDT,105),RELDT=CONVERT(VARCHAR(11),RELDT,105),
DDNO,NARRATION,ENTEREDBY,ACTIVE_DATE=CONVERT(VARCHAR(11),ACTIVE_DATE,105) 
FROM #DATA WHERE ACTIVE_DATE > VDT

GO
