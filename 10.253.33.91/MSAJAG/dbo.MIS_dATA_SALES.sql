CREATE PROCEDURE [dbo].[MIS_dATA_SALES]
(@date date)
AS
BEGIN 

 EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'MIS_DATA_SACHIN' 
 WAITFOR DELAY '00:0:45';




SELECT CL_CODE,LONG_NAME,EMAIL,MOBILE_PAGER,BRANCH_cD,SUB_BROKER,REGION,AREA,(CASE WHEN B2C='Y' THEN 'B2C'ELSE 'B2B'END)AS B2B_B2C
FROM INTRANET.RISK.DBO.CLIENT_DETAILS
WHERE CL_CODE IN 
(SELECT * FROM SSIS_FOCLIENT)

END

GO
