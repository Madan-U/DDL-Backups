		INVOICE_CATEGORY,INV_NO=0,RECORDFOR,INVOICE_TYPE,
		INVOICE_SERIES=STATE_CODE+RIGHT(LOCATION_CODE,2)+'/'+LEFT(RECORDFOR,1)+LEFT(INVOICE_CATEGORY,1)+'/'+LEFT(INVOICE_TYPE,1)
FROM TBL_GST_INVOICE_TMP */

UPDATE TBL_GST_INVOICE_TMP SET SACCODE = T.SACCODE, SAC_DESC = [DESC]
FROM ACCOUNT.DBO.TBL_SAC_CODE_UPDATE T
WHERE CLTCODE = SUBRECORDFOR
AND INVOICE_DATE BETWEEN EFF_FROM_DATE AND EFF_TO_DATE 

DELETE FROM TBL_GST_INVOICE
WHERE INVOICE_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'

INSERT INTO TBL_GST_INVOICE 
SELECT * FROM TBL_GST_INVOICE_TMP

GO
