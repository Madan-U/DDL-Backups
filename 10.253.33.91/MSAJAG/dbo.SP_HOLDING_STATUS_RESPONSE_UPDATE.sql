CREATE  PROCEDURE [dbo].[SP_HOLDING_STATUS_RESPONSE_UPDATE]
(  
	@HTYPE VARCHAR(1) 
)
AS
 
BEGIN
	DELETE FROM HOLDING_FILE_UPLOAD WHERE FILE_DATA IS NULL

	INSERT INTO HOLDING_UPLOAD_STATUS
	SELECT 
		.DBO.PIECE(FILE_DATA,'~',1),
		.DBO.PIECE(FILE_DATA,'~',2),
		.DBO.PIECE(FILE_DATA,'~',3),
		.DBO.PIECE(FILE_DATA,'~',4),			 
	REPLACE(.DBO.PIECE(FILE_DATA,'~',5), CHAR(13), '')
	FROM HOLDING_FILE_UPLOAD 
	
	IF @HTYPE = 'H'
	BEGIN
		UPDATE B SET 
		CODE= A.STATUS,
		HDRSTATUS= A.HDRSTATUS,
		HDRCODE= A.CODE,
		ACDID= A.ACDID
		FROM HOLDING_UPLOAD_STATUS A , TBL_API_NSE_HOLDING B
		WHERE A.UID = B.UID 
	END 
	SELECT ERRCODE='1',MESSAGETEXT='DONE'
	----DROP TABLE #PROCESSDATA
END

GO
