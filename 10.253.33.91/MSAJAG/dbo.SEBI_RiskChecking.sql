--SEBI_RiskChecking '06/10/2023'
CREATE PROCEDURE [dbo].[SEBI_RiskChecking](@MDATE varchar(11) )
--DECLARE @MDATE DATETIME = 'Dec  23 2023'
as
BEGIN

DECLARE @MDATE1 DATETIME

SET @MDATE1 = (SELECT TOP 1 MARGINDATE FROM TBL_COMBINE_REPORTING_DETAIL WITH (NOLOCK) WHERE  MARGINDATE <= CONVERT(VARCHAR(11),CONVERT(DATETIME, @MDATE,103)) ORDER BY MARGINDATE DESC)

DECLARE @MDATECM DATETIME

SET @MDATECM = (SELECT TOP 1 MARGINDATE FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) WHERE  MARGINDATE <= CONVERT(VARCHAR(11),CONVERT(DATETIME, @MDATE,103)) ORDER BY MARGINDATE DESC)

IF @MDATE1 <> @MDATECM
BEGIN
PRINT 'Combined Margin Process is Pending'
RETURN
END


SELECT EXCHANGE,SEGMENT,PARTY_CODE,MARGINDATE,TDAY_LEDGER = SUM(TDAY_LEDGER),TDAY_MARGIN = SUM(TDAY_MARGIN),TDAY_CASHCOLL = SUM(TDAY_CASHCOLL),
TDAY_NONCASH = SUM(TDAY_NONCASH),TDAY_MTM= SUM(TDAY_MTM),T_MARGINAVL = SUM(T_MARGINAVL),T_MTMAVL = SUM(T_MTMAVL), MTYPE = 'E'
INTO #MARGIN
FROM TBL_COMBINE_REPORTING_DETAIL WITH (NOLOCK) WHERE  MARGINDATE = @MDATE1
GROUP BY EXCHANGE,SEGMENT,PARTY_CODE,MARGINDATE ORDER BY MARGINDATE ASC

--print @MDATE
--select replace(CONVERT(VARCHAR(12),convert(datetime, @MDATE,103),109), '/', ' ')


SELECT MARGINDATE,PARTY_CODE,MTYPE,SUM(TDAY_LEDGER) AS TDAY_LEDGER,SUM(TDAY_MARGIN) AS TDAY_MARGIN,
SUM(TDAY_CASHCOLL) AS TDAY_CASHCOLL,SUM(TDAY_NONCASH) AS TDAY_NONCASH,SUM(TDAY_MTM) AS TDAY_MTM,
SUM(EPN) AS EPN,SUM(MTF) AS MTF,PAYOUTAMT = CONVERT(NUMERIC(18,2),0) 
INTO #MARGIN_FINAL FROM (
SELECT MARGINDATE,PARTY_CODE,MTYPE,TDAY_LEDGER = SUM(TDAY_LEDGER),TDAY_MARGIN = SUM(TDAY_MARGIN),TDAY_CASHCOLL = SUM(TDAY_CASHCOLL),
TDAY_NONCASH = SUM(TDAY_NONCASH),TDAY_MTM= SUM(TDAY_MTM) ,0 AS EPN,0 AS MTF
FROM #MARGIN 
WHERE EXCHANGE NOT IN ('MTF','EPN')
GROUP BY MARGINDATE,PARTY_cODE,MTYPE
UNION ALL
SELECT MARGINDATE,PARTY_CODE,MTYPE,TDAY_LEDGER = SUM(TDAY_LEDGER),TDAY_MARGIN = SUM(TDAY_MARGIN),TDAY_CASHCOLL = 0,
TDAY_NONCASH = 0,TDAY_MTM= SUM(TDAY_MTM) ,SUM(CASE WHEN EXCHANGE = 'EPN' THEN TDAY_CASHCOLL ELSE 0 END) AS EPN,
SUM(CASE WHEN EXCHANGE = 'MTF' THEN TDAY_NONCASH ELSE 0 END) AS MTF 
FROM #MARGIN 
WHERE EXCHANGE IN ('MTF','EPN')
GROUP BY MARGINDATE,PARTY_cODE,MTYPE) AS A
GROUP BY MARGINDATE,PARTY_CODE,MTYPE

SELECT L.CLTCODE,SUM(VAMT) AS VAMT INTO #PAYOUTDATA
FROM ACCOUNT.DBO.LEDGER L WITH (NOLOCK) , #MARGIN_FINAL M 
WHERE VDT BETWEEN CONVERT(VARCHAR(11),CONVERT(DATETIME, @MDATE,103)) AND CONVERT(VARCHAR(11),CONVERT(DATETIME, @MDATE,103)) + ' 23:59:59'
AND L.CLTCODE = M.PARTY_CODE AND VTYP = 3 --AND NARRATION LIKE '%Amt Transfer Quarterly Settlement%'
GROUP BY L.CLTCODE 

UPDATE #MARGIN_FINAL SET PAYOUTAMT = VAMT FROM #PAYOUTDATA WHERE #PAYOUTDATA.CLTCODE = #MARGIN_FINAL.PARTY_CODE

DELETE #MARGIN_FINAL WHERE PAYOUTAMT = 0


SELECT CONVERT(VARCHAR(10),MARGINDATE,105) AS MARGINDATE,M.PARTY_CODE,MTYPE,TDAY_LEDGER,TDAY_MARGIN,TDAY_CASHCOLL,TDAY_NONCASH,TDAY_MTM,EPN,MTF,
PAYOUTAMT,MARGINR = SUM (TDAY_MARGIN+TDAY_MTM),LEDGCOLL = SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF),
NETAFTERPAY = CONVERT(NUMERIC(18,2),SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF) -  SUM (TDAY_MARGIN+TDAY_MTM)) INTO #MARGIN_FINAL1
FROM #MARGIN_FINAL M  WHERE  1 <> 1
GROUP BY CONVERT(VARCHAR(10),MARGINDATE,105),M.PARTY_CODE,MTYPE,TDAY_LEDGER,TDAY_MARGIN,TDAY_CASHCOLL,TDAY_NONCASH,TDAY_MTM,EPN,MTF,PAYOUTAMT
HAVING CONVERT(NUMERIC(18,2),SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF) -  SUM (TDAY_MARGIN+TDAY_MTM)) < 0 

IF CONVERT(DATETIME, @MDATE,103) = CONVERT(DATETIME, @MDATE1,103)

BEGIN 
INSERT INTO #MARGIN_FINAL1
SELECT CONVERT(VARCHAR(10),MARGINDATE,105) AS MARGINDATE,M.PARTY_CODE,MTYPE,TDAY_LEDGER,TDAY_MARGIN,TDAY_CASHCOLL,TDAY_NONCASH,TDAY_MTM,EPN,MTF,
PAYOUTAMT,MARGINR = SUM (TDAY_MARGIN+TDAY_MTM),LEDGCOLL = SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF),
NETAFTERPAY = CONVERT(NUMERIC(18,2),SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF) -  SUM (TDAY_MARGIN+TDAY_MTM)) 
FROM #MARGIN_FINAL M  
GROUP BY CONVERT(VARCHAR(10),MARGINDATE,105),M.PARTY_CODE,MTYPE,TDAY_LEDGER,TDAY_MARGIN,TDAY_CASHCOLL,TDAY_NONCASH,TDAY_MTM,EPN,MTF,PAYOUTAMT
HAVING CONVERT(NUMERIC(18,2),SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF) -  SUM (TDAY_MARGIN+TDAY_MTM)) < 0 
END
ELSE
BEGIN
INSERT INTO #MARGIN_FINAL1
SELECT CONVERT(VARCHAR(10),MARGINDATE,105) AS MARGINDATE,M.PARTY_CODE,MTYPE,TDAY_LEDGER,TDAY_MARGIN,TDAY_CASHCOLL,TDAY_NONCASH,TDAY_MTM,EPN,MTF,
PAYOUTAMT,MARGINR = SUM (TDAY_MARGIN+TDAY_MTM),LEDGCOLL = SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF),
NETAFTERPAY = CONVERT(NUMERIC(18,2),SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF) -  SUM (TDAY_MARGIN+TDAY_MTM) - PAYOUTAMT)  
FROM #MARGIN_FINAL M  
GROUP BY CONVERT(VARCHAR(10),MARGINDATE,105),M.PARTY_CODE,MTYPE,TDAY_LEDGER,TDAY_MARGIN,TDAY_CASHCOLL,TDAY_NONCASH,TDAY_MTM,EPN,MTF,PAYOUTAMT
HAVING CONVERT(NUMERIC(18,2),SUM(TDAY_LEDGER+TDAY_CASHCOLL+TDAY_NONCASH+EPN+MTF) -  SUM (TDAY_MARGIN+TDAY_MTM) - PAYOUTAMT) < 0 
END




SELECT  MARGINDATE,M.PARTY_CODE,LONG_NAME,MTYPE,TDAY_LEDGER,TDAY_MARGIN,TDAY_CASHCOLL,TDAY_NONCASH,TDAY_MTM,EPN,MTF,
PAYOUTAMT,MARGINR,LEDGCOLL,NETAFTERPAY ,B2B_B2C,SUB_BROKER,BRANCH_CD,REGION 
FROM #MARGIN_FINAL1 M  , CLIENT_DETAILS C WITH (NOLOCK), VW_BTB_BTC VW WITH (NOLOCK)
WHERE  M.PARTY_CODE = C.CL_CODE AND VW.CL_CODE = M.PARTY_CODE
ORDER BY NETAFTERPAY 

DROP TABLE #PAYOUTDATA
DROP TABLE #MARGIN_FINAL
DROP TABLE #MARGIN
DROP TABLE #MARGIN_FINAL1

end

GO
