		UPDATE	D
		SET		LAST_SETT_DATE = --CONVERT(VARCHAR,SETT_DATE, 105)
		case when SETT_DATE <= CONVERT(VARCHAR(11), CONVERT(datetime,REPORT_DATE, 105), 109) + ' 23:59' then CONVERT(VARCHAR,SETT_DATE, 105) else 'NA' end
		FROM	#REPORT_DATA D,
				(
					SELECT  party_code, SETT_DATE = max(SCCS_SettDate_Last) from Sebi_Payout--, #COLL D
