BEGIN 

      INSERT INTO ASIANFILEBATCHNO VALUES ('CE', @BATCHNO, GETDATE(), GETDATE()) 

END 

ELSE 

BEGIN 

      UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'CE' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)

END 

 

SET   @FILENAMEHS = @PANNO + '_CE_' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE,109),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'

 

 

 

SELECT TRADING_PAN, REPORT_DATE,UCC=PARENTCODE,CLIENT_PAN,CLIENT_NAME,SEGMENT_INDICATOR = CASE WHEN EXCHANGE = 'MTF' THEN 'MTF' ELSE 'NON MTF' END, EXCHANGE, SEGMENT,

VDT_BAL, EDT_BAL,PEAK_FINANCIAL_LEDGER, BG, FDR, GOV_IND_SEC, GILT_FUNDS, EPI_LEDGER,POOL_ACCOUNT,CHQ_DEPOSITS,CHQ_ISSUE,

COMM_VALUE,LAST_SETT_DATE,OPEN_BILL1, OPEN_BILL2, MARGIN_REQ

INTO #REPORT_DATA

FROM ACCOUNT..CLIENT_CASH_DETAILS C, ACCOUNT..CLIENT_CASH_LED_BALANCES L

WHERE C.SNO = L.SNO

AND REPORT_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'

AND UCC BETWEEN @FROMPARTY AND @TOPARTY

 

 

declare

      @TRADING_PAN varchar(10)

 

SELECT TOP 1 @TRADING_PAN = TRADING_PAN FROM #REPORT_DATA

 

 

 

delete R from #REPORT_DATA R WHERE VDT_BAL = 0 and EDT_BAL = 0 and PEAK_FINANCIAL_LEDGER = 0 and BG = 0 and FDR = 0 and GOV_IND_SEC = 0and GILT_FUNDS = 0

and EPI_LEDGER  = 0 and POOL_ACCOUNT = 0 and CHQ_DEPOSITS = 0 and CHQ_ISSUE = 0 and COMM_VALUE = 0

 

CREATE TABLE #CAL_DATES

(

      CAL_DATES DATETIME

)

 

INSERT INTO #CAL_DATES

EXEC ACCOUNT..GET_CAL_DATES @FROMDATE, @TODATE

 

INSERT INTO #REPORT_DATA

SELECT @TRADING_PAN, CAL_DATES, CL_CODE, PAN_GIR_NO, LONG_NAME, 'NON MTF', 'NSE', 'CAPITAL',  VDT_BAL = 0, EDT_BAL = 0,PEAK_FINANCIAL_LEDGER = 0, BG = 0, FDR = 0, GOV_IND_SEC = 0, GILT_FUNDS = 0, EPI_LEDGER = 0,POOL_ACCOUNT = 0,CHQ_DEPOSITS = 0,CHQ_ISSUE = 0,COMM_VALUE = 0,LAST_SETT_DATE = 'ND',OPEN_BILL1=0, OPEN_BILL2=0, MARGIN_REQ = 0

FROM CLIENT_DETAILS C, #CLIENT_LIST CL, #CAL_DATES CD

WHERE CL_CODE = CLTCODE

AND NOT EXISTS(SELECT UCC FROM #REPORT_DATA WHERE UCC = CLTCODE AND REPORT_DATE = CAL_DATES)

AND CL_CODE BETWEEN @FROMPARTY AND @TOPARTY

 

