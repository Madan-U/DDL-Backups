-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROCEDURE [dbo].[CLS_GET_REDIRECTION_DETAILS](
	@TOKEN VARCHAR(50)

	)
AS
BEGIN
declare @CLIENT_TRADE_TYPE varchar(5)
SET @CLIENT_TRADE_TYPE  = 'N'

	--  select * from CLASSAPP.DBO.PASSPORT_LOGIN_MAP
	select @CLIENT_TRADE_TYPE = CLIENT_TRADE_TYPE from CLIENT_MASTER_CKYC WHERE PARTY_CODE =(SELECT Fldstname FROM MSAJAG..TBLPRADNYAUSERS A
    inner join CLS_LOGIN_REDIRECT B on b.LoginID = a.Fldusername
    WHERE   TOKEN = @TOKEN )


	SELECT  FLDUSERNAME + '§' + FLDPASSWORD + '§' +@CLIENT_TRADE_TYPE AS Data FROM MSAJAG..TBLPRADNYAUSERS A
    inner join CLS_LOGIN_REDIRECT B on b.LoginID = a.Fldusername
    WHERE   TOKEN = @TOKEN 


END

GO
