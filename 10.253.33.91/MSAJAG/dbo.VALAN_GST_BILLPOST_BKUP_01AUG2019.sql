	WHERE SETT_NO = @SETT_NO_L AND SETT_TYPE = @SETT_TYPE_L

	DELETE FROM IACCBILL 
	WHERE SETT_NO = @SETT_NO_L AND SETT_TYPE = @SETT_TYPE_L
	AND Party_Code IN (SELECT ACCODE FROM Valanaccount
	WHERE ACNAME = 'SERVICE TAX')

