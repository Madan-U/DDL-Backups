	WHERE SETT_NO = @SETT_NO_L AND SETT_TYPE = @SETT_TYPE_L
	AND PARTY_CODE IN (SELECT ACCODE FROM VALANACCOUNT WHERE ACNAME = 'ROUNDING OFF')

