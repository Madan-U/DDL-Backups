CREATE PROC [dbo].[RPT_IPFT_INST]
(
@SAUDA_DATE VARCHAR(11),
@FPARTY VARCHAR(20),
@TPARTY VARCHAR(20),
@DFLAG  VARCHAR(2)='D'

)

/*
EXEC RPT_IPFT_INST 'FEB  9 2022','0','zzzzz','D'
EXEC RPT_IPFT_INST 'MAR  8 2019','0','zzzzz','S'
EXEC RPT_IPFT_INST 'MAR  8 2019','00','zzz','D'
EXEC RPT_IPFT_INST 'MAR  8 2019','0A149','0A149',''
*/

AS
IF LEN(@SAUDA_DATE) = 10 AND CHARINDEX('/', @SAUDA_DATE) > 0
BEGIN
	SET @SAUDA_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @SAUDA_DATE, 103), 109)
END

CREATE TABLE #IPFTDATA
   (
    [SAUDA_DATE] [VARCHAR](12) NULL,
	[PARTY_CODE] [varchar](25) NULL,
	[CONTRACTNO_NEW] [varchar](10) NULL,
	EXCHANGE VARCHAR(3),
	SEGMENT VARCHAR(15),
	[SETT_NO] [varchar](10) NULL,
	[SETT_TYPE] [varchar](3) NULL,
	[SCRIPNAME] [varchar](100) NULL,
	[TRADEQTY] [bigint] NULL,
	[TURNOVER] [numeric](18, 4) NULL,
	[SELL_BUY] [VARCHAR](5) NULL,
	[IPFT_CHRG] [numeric](18, 4) NULL,
	[TRDTYPE] [VARCHAR](10) NULL,
	ORDFLAG [int] NULL
) 


BEGIN

IF @DFLAG='S'
	BEGIN
				INSERT INTO #IPFTDATA (SAUDA_DATE,PARTY_CODE,CONTRACTNO_NEW,TURNOVER,IPFT_CHRG,ORDFLAG)
				SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO_NEW=CONTRACTNO,
					   --EXCHANGE='NSE',SEGMENT='CAPITAL',
					   --SETT_NO,SETT_TYPE,
					   --SCRIPNAME=S1.LONG_NAME,TRADEQTY=SUM(TRADEQTY),
					   TURNOVER=SUM(AMOUNT), 
					   --SELL_BUY=(CASE WHEN SELL_BUY='1' THEN 'BUY' ELSE 'SELL' END),
					   IPFT_CHRG=SUM(IPFT_CHRG),
					   --TRDTYPE='INST',
					   ORDFLAG=1 
				from  TBL_ADDITIONAL_TAX_DATA I(NOLOCK)
						--, MSAJAG..SCRIP1 S1(NOLOCK), MSAJAG..SCRIP2 S2(NOLOCK) 
				WHERE SAUDA_DATE >=@SAUDA_DATE
					  AND SAUDA_DATE<=@SAUDA_DATE+ ' 23:59:59'
					  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
					  AND I.TRDTYPE = 'I'
					  --AND S1.CO_CODE = S2.CO_CODE AND S1.SERIES = S2.SERIES AND I.SCRIP_CD = S2.SCRIP_CD AND S2.SERIES = I.SERIES 
				GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO
				--,SETT_NO,SETT_TYPE,S1.LONG_NAME,SELL_BUY  
				--HAVING SUM(IPFT_CHRG)>0
				ORDER BY CONVERT(VARCHAR,SAUDA_DATE,112),CONTRACTNO_NEW,PARTY_CODE
				--,EXCHANGE,SEGMENT,SCRIPNAME

				INSERT INTO #IPFTDATA (SAUDA_DATE,PARTY_CODE,CONTRACTNO_NEW,TURNOVER,IPFT_CHRG,ORDFLAG)
				SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO_NEW=CONTRACTNO,
					   --EXCHANGE='BSE',SEGMENT='CAPITAL',SETT_NO,SETT_TYPE,
					   --SCRIPNAME=S1.LONG_NAME,TRADEQTY=SUM(TRADEQTY),
					   TURNOVER=SUM(AMOUNT), 
					   --SELL_BUY=(CASE WHEN SELL_BUY='1' THEN 'BUY' ELSE 'SELL' END),
					   IPFT_CHRG=SUM(IPFT_CHRG),
					   --CTYPE='I',
					   ORDFLAG=1 
				 from  [ANGELBSECM].BSEDB_AB.DBO.TBL_ADDITIONAL_TAX_DATA I(NOLOCK), [ANGELBSECM].BSEDB_AB.DBO.SCRIP1 S1(NOLOCK), [ANGELBSECM].BSEDB_AB.DBO.SCRIP2 S2(NOLOCK) 
				WHERE SAUDA_DATE >=@SAUDA_DATE
					  AND SAUDA_DATE<=@SAUDA_DATE+ ' 23:59:59'
					  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
					  AND I.TRDTYPE = 'I'
					  --AND S1.CO_CODE = S2.CO_CODE AND S1.SERIES = S2.SERIES AND I.SCRIP_CD = S2.BSECODE
				GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO  
				--HAVING SUM(IPFT_CHRG)>0
				ORDER BY CONVERT(VARCHAR,SAUDA_DATE,112),CONTRACTNO_NEW,PARTY_CODE --,EXCHANGE,SEGMENT,SCRIPNAME


				INSERT INTO #IPFTDATA (SAUDA_DATE,PARTY_CODE,CONTRACTNO_NEW,TURNOVER,IPFT_CHRG,ORDFLAG)
				SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO_NEW=CONTRACTNO,
					   --EXCHANGE='NSE',SEGMENT='FUTURES',SETT_NO,SETT_TYPE,
					   --SCRIPNAME=(CASE WHEN LEFT(I.INST_TYPE,3) = 'FUT' THEN RTRIM(I.INST_TYPE) + ' ' + RTRIM(I.SYMBOL) + ' ' + UPPER(REPLACE(CONVERT(VARCHAR,I.EXPIRYDATE,6),' ',''))
					   --	ELSE RTRIM(I.INST_TYPE) + ' ' + RTRIM(I.SYMBOL) + ' ' + UPPER(REPLACE(CONVERT(VARCHAR,I.EXPIRYDATE,6),' ','')) + ' ' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(I.STRIKE_PRICE,2))) + ' ' + RTRIM(I.OPTION_TYPE)
						--END),
						--TRADEQTY=SUM(TRADEQTY),
						TURNOVER=SUM(AMOUNT), 
						--SELL_BUY=(CASE WHEN SELL_BUY='1' THEN 'BUY' ELSE 'SELL' END),
						IPFT_CHRG=SUM(IPFT_CHRG),
					   --CTYPE='I',
					   ORDFLAG=1 
				from  [ANGELFO].NSEFO.DBO.TBL_ADDITIONAL_TAX_DATA I(NOLOCK)
				WHERE SAUDA_DATE >=@SAUDA_DATE
					  AND SAUDA_DATE <=@SAUDA_DATE+ ' 23:59:59'
					  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
					  AND I.TRDTYPE = 'I'
				GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO--,SETT_NO,SETT_TYPE,SELL_BUY,
						/*(CASE WHEN LEFT(I.INST_TYPE,3) = 'FUT' THEN RTRIM(I.INST_TYPE) + ' ' + RTRIM(I.SYMBOL) + ' ' + UPPER(REPLACE(CONVERT(VARCHAR,I.EXPIRYDATE,6),' ',''))
						ELSE RTRIM(I.INST_TYPE) + ' ' + RTRIM(I.SYMBOL) + ' ' + UPPER(REPLACE(CONVERT(VARCHAR,I.EXPIRYDATE,6),' ','')) + ' ' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(I.STRIKE_PRICE,2))) + ' ' + RTRIM(I.OPTION_TYPE)
						END) 
						*/
				--HAVING SUM(IPFT_CHRG)>0
				ORDER BY CONVERT(VARCHAR,SAUDA_DATE,112),CONTRACTNO_NEW,PARTY_CODE

				SELECT 
				SAUDA_DATE,PARTY_CODE,CONTRACTNO_NEW,
				--EXCHANGE,SEGMENT,
				TURNOVER=SUM(TURNOVER),
				IPFT_CHRG=SUM(IPFT_CHRG)
				FROM #IPFTDATA
				GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO_NEW
				ORDER BY CONVERT(VARCHAR,SAUDA_DATE,112),PARTY_CODE,CONTRACTNO_NEW
				

	END
ELSE IF @DFLAG='D'
	BEGIN

		INSERT INTO #IPFTDATA
		SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO_NEW=CONTRACTNO,
			   EXCHANGE='NSE',SEGMENT='CAPITAL',SETT_NO,SETT_TYPE,
			   SCRIPNAME=S1.LONG_NAME,TRADEQTY=SUM(TRADEQTY),TURNOVER=SUM(AMOUNT), SELL_BUY=(CASE WHEN SELL_BUY='1' THEN 'BUY' ELSE 'SELL' END),IPFT_CHRG=SUM(IPFT_CHRG),
			   TRDTYPE,
			   ORDFLAG=1 
		from  TBL_ADDITIONAL_TAX_DATA I(NOLOCK), SCRIP1 S1(NOLOCK), SCRIP2 S2(NOLOCK) 
		WHERE SAUDA_DATE >=@SAUDA_DATE
		      AND SAUDA_DATE<=@SAUDA_DATE+ ' 23:59:59'
			  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
			  AND I.TRDTYPE = 'I'
			  AND S1.CO_CODE = S2.CO_CODE AND S1.SERIES = S2.SERIES AND I.SCRIP_CD = S2.SCRIP_CD AND S2.SERIES = I.SERIES 
		GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO,SETT_NO,SETT_TYPE,S1.LONG_NAME,SELL_BUY,TRDTYPE  
		--HAVING SUM(IPFT_CHRG)>0
		ORDER BY CONVERT(VARCHAR,SAUDA_DATE,112),CONTRACTNO_NEW,PARTY_CODE,EXCHANGE,SEGMENT,SCRIPNAME

		INSERT INTO #IPFTDATA
		SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO_NEW=CONTRACTNO,
			   EXCHANGE='BSE',SEGMENT='CAPITAL',SETT_NO,SETT_TYPE,
			   SCRIPNAME=S1.LONG_NAME,TRADEQTY=SUM(TRADEQTY),TURNOVER=SUM(AMOUNT), SELL_BUY=(CASE WHEN SELL_BUY='1' THEN 'BUY' ELSE 'SELL' END),IPFT_CHRG=SUM(IPFT_CHRG),
			   TRDTYPE,
			   ORDFLAG=1 
		from  [ANGELBSECM].BSEDB_AB.DBO.TBL_ADDITIONAL_TAX_DATA I(NOLOCK), [ANGELBSECM].BSEDB_AB.DBO.SCRIP1 S1(NOLOCK), [ANGELBSECM].BSEDB_AB.DBO.SCRIP2 S2(NOLOCK) 
		WHERE SAUDA_DATE >=@SAUDA_DATE
		      AND SAUDA_DATE<=@SAUDA_DATE+ ' 23:59:59'
			  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
			  AND I.TRDTYPE = 'I'
			  AND S1.CO_CODE = S2.CO_CODE AND S1.SERIES = S2.SERIES AND I.SCRIP_CD = S2.BSECODE
		GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO,SETT_NO,SETT_TYPE,S1.LONG_NAME,SELL_BUY,TRDTYPE  
		--HAVING SUM(IPFT_CHRG)>0
		ORDER BY CONVERT(VARCHAR,SAUDA_DATE,112),CONTRACTNO_NEW,PARTY_CODE,EXCHANGE,SEGMENT,SCRIPNAME

		INSERT INTO #IPFTDATA
		SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO_NEW=CONTRACTNO,
			   EXCHANGE='NSE',SEGMENT='FUTURES',SETT_NO,SETT_TYPE,
			   SCRIPNAME=(CASE WHEN LEFT(I.INST_TYPE,3) = 'FUT' THEN RTRIM(I.INST_TYPE) + ' ' + RTRIM(I.SYMBOL) + ' ' + UPPER(REPLACE(CONVERT(VARCHAR,I.EXPIRYDATE,6),' ',''))
				ELSE RTRIM(I.INST_TYPE) + ' ' + RTRIM(I.SYMBOL) + ' ' + UPPER(REPLACE(CONVERT(VARCHAR,I.EXPIRYDATE,6),' ','')) + ' ' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(I.STRIKE_PRICE,2))) + ' ' + RTRIM(I.OPTION_TYPE)
				END),TRADEQTY=SUM(TRADEQTY),TURNOVER=SUM(AMOUNT), SELL_BUY=(CASE WHEN SELL_BUY='1' THEN 'BUY' ELSE 'SELL' END),IPFT_CHRG=SUM(IPFT_CHRG),
			   TRDTYPE,
			   ORDFLAG=1 
		from  [ANGELFO].NSEFO.DBO.TBL_ADDITIONAL_TAX_DATA I(NOLOCK)
		WHERE SAUDA_DATE >=@SAUDA_DATE
		      AND SAUDA_DATE <=@SAUDA_DATE+ ' 23:59:59'
			  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
			  AND I.TRDTYPE = 'I'
		GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO,SETT_NO,SETT_TYPE,SELL_BUY,TRDTYPE,
				(CASE WHEN LEFT(I.INST_TYPE,3) = 'FUT' THEN RTRIM(I.INST_TYPE) + ' ' + RTRIM(I.SYMBOL) + ' ' + UPPER(REPLACE(CONVERT(VARCHAR,I.EXPIRYDATE,6),' ',''))
				ELSE RTRIM(I.INST_TYPE) + ' ' + RTRIM(I.SYMBOL) + ' ' + UPPER(REPLACE(CONVERT(VARCHAR,I.EXPIRYDATE,6),' ','')) + ' ' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(I.STRIKE_PRICE,2))) + ' ' + RTRIM(I.OPTION_TYPE)
				END) 
		--HAVING SUM(IPFT_CHRG)>0
		ORDER BY CONVERT(VARCHAR,SAUDA_DATE,112),CONTRACTNO_NEW,PARTY_CODE,EXCHANGE,SEGMENT,SCRIPNAME


	    INSERT INTO #IPFTDATA
		SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO_NEW,
			   EXCHANGE,SEGMENT,SETT_NO='',SETT_TYPE='',
		       SCRIPNAME='TOTAL',TRADEQTY='',TURNOVER=SUM(TURNOVER),SELL_BUY='',IPFT_CHRG=SUM(IPFT_CHRG),TRDTYPE='',
			   ORDFLAG =2
		from #IPFTDATA(NOLOCK)
		WHERE CONVERT(VARCHAR(11), CONVERT(DATETIME, SAUDA_DATE, 103), 109) >=@SAUDA_DATE
		      AND CONVERT(VARCHAR(11), CONVERT(DATETIME, SAUDA_DATE, 103), 109) <=@SAUDA_DATE+ ' 23:59:59'
			  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
			 -- AND CTYPE='I'
		GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO_NEW,EXCHANGE,SEGMENT,SCRIPNAME

	--RETURN
		
		
		SELECT SAUDA_DATE,PARTY_CODE,CONTRACTNO_NEW,EXCHANGE,SEGMENT,SETT_NO,SETT_TYPE,SCRIPNAME,
		TRADEQTY=(CASE WHEN TRADEQTY=0 THEN null ELSE TRADEQTY END),TURNOVER,SELL_BUY,IPFT_CHRG--,TRDTYPE,ORDFLAG
		FROM #IPFTDATA 
		ORDER BY CONVERT(VARCHAR,SAUDA_DATE,112),PARTY_CODE,CONTRACTNO_NEW,EXCHANGE,SEGMENT,ORDFLAG
		  
		DROP TABLE #IPFTDATA
	END
ELSE IF @DFLAG=''
	BEGIN
		
		CREATE TABLE #PARTY
		(
		PARTY_CODE VARCHAR(25)
		)
		
		INSERT #PARTY
		SELECT DISTINCT PARTY_CODE=UPPER(PARTY_CODE)
		from TBL_ADDITIONAL_TAX_DATA(NOLOCK)
		WHERE  SAUDA_DATE >=@SAUDA_DATE
		      AND SAUDA_DATE<=@SAUDA_DATE+ ' 23:59:59'
			  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
			  AND TRDTYPE = 'I'

        INSERT #PARTY
		SELECT DISTINCT PARTY_CODE=UPPER(PARTY_CODE)
		--EXCHANGE,SEGMENT,
		from [ANGELBSECM].BSEDB_AB.DBO.TBL_ADDITIONAL_TAX_DATA with (NOLOCK)
		WHERE  SAUDA_DATE >=@SAUDA_DATE
		      AND SAUDA_DATE<=@SAUDA_DATE+ ' 23:59:59'
			  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
			  AND TRDTYPE = 'I'
        INSERT #PARTY
		SELECT DISTINCT PARTY_CODE=UPPER(PARTY_CODE)
		--EXCHANGE,SEGMENT,
		from [ANGELFO].NSEFO.DBO.TBL_ADDITIONAL_TAX_DATA with (NOLOCK)
		WHERE  SAUDA_DATE >=@SAUDA_DATE
		      AND SAUDA_DATE<=@SAUDA_DATE+ ' 23:59:59'
			  AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'
			  AND TRDTYPE = 'I'
	     
		SELECT DISTINCT PARTY_CODE FROM #PARTY
		WHERE PARTY_CODE BETWEEN @FPARTY AND @TPARTY + '%'

		DROP TABLE #PARTY

	END

END

GO
