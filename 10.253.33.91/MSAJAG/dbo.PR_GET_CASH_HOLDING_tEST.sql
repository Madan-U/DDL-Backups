CREATE PROC [dbo].[PR_GET_CASH_HOLDING_tEST]      
(    
@EFFDATE Varchar(11),    
@INTMODE INT = 0      
) AS      

-- EXEC PR_GET_CASH_HOLDING 'MAY 29 2018'      
/*      
 @INTMODE = 0 - BEN HOLDING (TRTYPE = 904)      
 @INTMODE = 1 - PLEDGE HOLDING (TRTYPE = 909)      
*/      
DECLARE @CASH INT      
DECLARE @NCASH INT      
      
CREATE TABLE #DELHOLD      
(      
 EXCHANGE VARCHAR(3),      
 PARTY_CODE VARCHAR(10),      
 SCRIP_CD VARCHAR(12),      
 SERIES  VARCHAR(3),      
 ISIN  VARCHAR(16),      
 QTY   INT,      
)      
      
    
CREATE CLUSTERED INDEX IDSCRIP ON #DELHOLD (ISIN)      
    
IF LEFT(GETDATE(),11) = @EFFDATE  
--IF 1 = 1 
BEGIN   
  
INSERT INTO #DELHOLD     
SELECT       
 EXCHANGE = 'NSE',       
 PARTY_CODE,       
 D.SCRIP_CD,       
 D.SERIES,       
 ISIN = CERTNO,      
 QTY = SUM(QTY)      
FROM    
ANGELDEMAT.MSAJAG.DBO.DELTRANS D, ANGELDEMAT.MSAJAG.DBO.DELIVERYDP DP    
WHERE       
 D.BDPID = DP.DPID      
 AND DP.DESCRIPTION NOT LIKE '%POOL%'      
 AND DP.DESCRIPTION NOT LIKE '%PRINC%'        
AND D.BCLTDPID = DPCLTNO      
 AND D.DRCR = 'D'      
 AND FILLER2 = 1       
 AND DELIVERED = 'G'      
 AND TRTYPE = 1000      
 AND PARTY_CODE <> 'BROKER'      
 AND TRANSDATE >= LEFT(GETDATE(),11) 
 AND SEGMENT = 'CAPITAL' AND ACCOUNTTYPE = 'BEN'   
GROUP BY       
 PARTY_CODE, D.SCRIP_CD, D.SERIES, CERTNO      


INSERT INTO #DELHOLD     
SELECT       
 EXCHANGE = 'NSE',       
 PARTY_CODE,       
 D.SCRIP_CD,       
 D.SERIES,       
 ISIN = CERTNO,      
 QTY = SUM(QTY)      
FROM    
ANGELDEMAT.MSAJAG.DBO.DELTRANS D, ANGELDEMAT.MSAJAG.DBO.DELIVERYDP DP    
WHERE       
 D.BDPID = DP.DPID          
AND D.BCLTDPID = DPCLTNO      
 AND D.DRCR = 'D'      
 AND FILLER2 = 1       
 AND DELIVERED = '0'      
 AND TRTYPE IN ('904','909')    
 AND SHARETYPE = 'DEMAT'  
 AND PARTY_CODE <> 'BROKER'      
 AND SEGMENT = 'CAPITAL' 
GROUP BY       
 PARTY_CODE, D.SCRIP_CD, D.SERIES, CERTNO   
       
INSERT INTO #DELHOLD     
    
SELECT       
 EXCHANGE = 'NSE',       
 PARTY_CODE,       
 D.SCRIP_CD ,       
 D.SERIES,       
 ISIN = CERTNO,      
 QTY = SUM(QTY)      
FROM       
ANGELDEMAT.BSEDB.DBO.DELTRANS D , ANGELDEMAT.BSEDB.DBO.DELIVERYDP DP       
WHERE       
 D.BDPID = DP.DPID      
 AND DP.DESCRIPTION NOT LIKE '%POOL%'      
 AND DP.DESCRIPTION NOT LIKE '%PRINC%'      
 AND D.BCLTDPID = DPCLTNO      
 AND D.DRCR = 'D'      
 AND FILLER2 = 1       
 AND DELIVERED = 'G'      
 AND TRTYPE = 1000      
 AND PARTY_CODE <> 'BROKER'      
 AND TRANSDATE >= LEFT(GETDATE(),11)     
 AND SEGMENT = 'CAPITAL' 
GROUP BY       
 PARTY_CODE, D.SCRIP_CD, D.SERIES, CERTNO      

INSERT INTO #DELHOLD     
SELECT       
 EXCHANGE = 'BSE',       
 PARTY_CODE,       
 D.SCRIP_CD,       
 D.SERIES,       
 ISIN = CERTNO,      
 QTY = SUM(QTY)      
FROM    
ANGELDEMAT.BSEDB.DBO.DELTRANS D, ANGELDEMAT.BSEDB.DBO.DELIVERYDP DP    
WHERE       
 D.BDPID = DP.DPID          
AND D.BCLTDPID = DPCLTNO      
 AND D.DRCR = 'D'      
 AND FILLER2 = 1       
 AND DELIVERED = '0'      
 AND TRTYPE IN ('904','909') 
 AND SHARETYPE = 'DEMAT'  
 AND PARTY_CODE <> 'BROKER'      
 AND SEGMENT = 'CAPITAL' 
GROUP BY       
 PARTY_CODE, D.SCRIP_CD, D.SERIES, CERTNO       
    
INSERT INTO #DELHOLD     
    
SELECT       
 EXCHANGE = 'NSE',       
 PARTY_CODE,       
 D.SCRIP_CD,       
 D.SERIES,       
 ISIN = CERTNO,      
 QTY = SUM(QTY)      
FROM ANGELDEMAT.MSAJAG.DBO.DELTRANS D , --MSAJAG.DBO.DELIVERYDP DP  (NOLOCK),     
(SELECT SETT_NO, SETT_TYPE, SEC_PAYIN FROM ANGELDEMAT.MSAJAG.DBO.SETT_MST      
WHERE LEFT(GETDATE(),11) BETWEEN START_DATE AND SEC_PAYIN) S    
WHERE       
D.SETT_NO = S.SETT_NO     
AND D.SETT_TYPE = S.SETT_TYPE        
AND D.DRCR = 'C'      
AND FILLER2 = 1       
AND SHARETYPE <> 'AUCTION'     
AND PARTY_CODE <> 'BROKER'      
AND TRANSDATE < SEC_PAYIN      
AND TRTYPE <> 906    
AND LEFT(GETDATE(),11) <> LEFT(SEC_PAYIN,11)    
AND TRANSDATE<=@EFFDATE
AND CLTDPID NOT IN (SELECT DPCLTNO FROM ANGELDEMAT.MSAJAG.DBO.DELIVERYDP WHERE SEGMENT = 'MTF' AND ACCOUNTTYPE = 'BEN'
UNION ALL SELECT DPCLTNO FROM ANGELDEMAT.BSEDB.DBO.DELIVERYDP WHERE SEGMENT = 'MTF' AND ACCOUNTTYPE = 'BEN')
and tcode not in (select tcode from MSAJAG.DBO.DELTRANS d1    
 where d1.sett_no = d.sett_no and d1.sett_type = d.sett_type    
 and d1.certno = d.certno and d1.drcr <> d.drcr and d1.trtype = 906 and d1.filler1 = 'early payin')    
GROUP BY       
 PARTY_CODE, D.SCRIP_CD, D.SERIES, CERTNO      
      
INSERT INTO #DELHOLD     
    
SELECT       
 EXCHANGE = 'NSE',      PARTY_CODE,       
 D.SCRIP_CD ,       
 D.SERIES,       
 ISIN = CERTNO,      
 QTY = SUM(QTY)      
FROM       
ANGELDEMAT.BSEDB.DBO.DELTRANS D , --MSAJAG.DBO.DELIVERYDP DP  (NOLOCK),     
(SELECT SETT_NO, SETT_TYPE, SEC_PAYIN FROM ANGELDEMAT.BSEDB.DBO.SETT_MST  
WHERE LEFT(GETDATE(),11) BETWEEN START_DATE AND SEC_PAYIN) S    
WHERE       
D.SETT_NO = S.SETT_NO     
AND D.SETT_TYPE = S.SETT_TYPE      
AND D.DRCR = 'C'      
AND FILLER2 = 1       
AND SHARETYPE <> 'AUCTION'     
AND PARTY_CODE <> 'BROKER'      
AND TRANSDATE < SEC_PAYIN    
--AND TRANSDATE >= START_DATE  
AND TRANSDATE<=@EFFDATE  
AND TRTYPE <> 906    
AND LEFT(GETDATE(),11) <> LEFT(SEC_PAYIN,11) 
AND CLTDPID NOT IN (SELECT DPCLTNO FROM ANGELDEMAT.MSAJAG.DBO.DELIVERYDP WHERE SEGMENT = 'MTF' AND ACCOUNTTYPE = 'BEN'
UNION ALL SELECT DPCLTNO FROM ANGELDEMAT.BSEDB.DBO.DELIVERYDP WHERE SEGMENT = 'MTF' AND ACCOUNTTYPE = 'BEN')   
and tcode not in (select tcode from MSAJAG.DBO.DELTRANS d1    
 where d1.sett_no = d.sett_no and d1.sett_type = d.sett_type    
 and d1.certno = d.certno and d1.drcr <> d.drcr and d1.trtype = 906 and d1.filler1 = 'early payin')    
GROUP BY       
 PARTY_CODE, D.SCRIP_CD, D.SERIES, CERTNO     
       
UPDATE #DELHOLD SET SCRIP_CD = M.SCRIP_CD, SERIES = M.SERIES      
FROM ANGELDEMAT.MSAJAG.DBO.MULTIISIN M     
WHERE M.ISIN = #DELHOLD.ISIN      
AND VALID = 1     
    
UPDATE #DELHOLD SET SCRIP_CD = M.SCRIP_CD, SERIES = M.SERIES      
FROM ANGELDEMAT.BSEDB.DBO.MULTIISIN M     
WHERE M.ISIN = #DELHOLD.ISIN      
AND VALID = 1 AND (#DELHOLD.SERIES IS NULL OR #DELHOLD.SCRIP_CD IS NULL)    
    
DELETE FROM DELHOLD_COLL_TEST WHERE TRANSDATE = @EFFDATE    
    
INSERT INTO DELHOLD_COLL_TEST     
SELECT     
 EXCHANGE, PARTY_CODE, SCRIP_CD, SERIES, ISIN, QTY=SUM(QTY), TRANSDATE = @EFFDATE    
FROM #DELHOLD    
GROUP BY EXCHANGE, PARTY_CODE, SCRIP_CD, SERIES, ISIN    
END    
 
      
DROP TABLE #DELHOLD      
/*-------------------------------  END OF THE PROC -----------------------------*/

GO
