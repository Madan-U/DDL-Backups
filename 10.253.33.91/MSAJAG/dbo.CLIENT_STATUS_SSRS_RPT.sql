 CREATE PROCEDURE [dbo].[CLIENT_STATUS_SSRS_RPT]
 (
 @DATE VARCHAR(20)
 )
 AS
 BEGIN
 
  EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'CLIENT_STATUS_DATA'   
 WAITFOR DELAY '00:0:45';  


SELECT A.CL_CODE,LONG_NAME,BRANCH_CD,SUB_BROKER,REGION,PAN_GIR_NO,EMAIL,MOBILE_PAGER,EXCHANGE,SEGMENT,ACTIVE_DATE,INACTIVE_FROM,DEACTIVE_REMARKS,DEACTIVE_VALUE
 FROM CLIENT_BROK_DETAILS A,CLIENT_DETAILS B WHERE A.Cl_Code=B.cl_code AND A.CL_CODE IN 
(
SELECT * FROM SSRS_CL_cODE
)
END

GO
