DECLARE @CMD VARCHAR(MAX)
SET @CMD = LOWER( ' BULK INSERT #temp FROM ''' + @filename + ''' WITH  (ROWTERMINATOR = ''\N'',FIELDTERMINATOR = '','') ')
EXEC (@CMD)


CREATE TABLE #CLIENT
	(
	PARTY_CODE VARCHAR(10)
	)
	
    INSERT INTO #CLIENT 
    select * from (
	SELECT CL_CODE  FROM CLIENT1 WHERE BRANCH_CD IN (SELECT BRANCH_CD FROM TBL_ADDITIONAL_BROEKRAGE where todate>getdate()and party_code='')
    UNION ALL
    SELECT CL_CODE  FROM CLIENT1 WHERE SUB_BROKER IN (SELECT SUB_BROKER FROM TBL_ADDITIONAL_BROEKRAGE where todate>getdate() and party_code='')
    UNION ALL
    SELECT CL_CODE  FROM CLIENT1 WHERE CL_CODE  IN (SELECT PARTY_CODE FROM TBL_ADDITIONAL_BROEKRAGE where todate>getdate())
  )A


	DELETE FROM TBL_TRADE_ONLINE
	WHERE SAUDA_DATE = @tradedate
	
	INSERT INTO TBL_TRADE_ONLINE
	SELECT 
		SAUDA_DATE = @tradedate, 
		PARTY_CODE=LTRIM(RTRIM(FILLER23)),
		MEMBERCODE=LTRIM(RTRIM(FILLER5)),
		SYMBOL=(CASE WHEN LTRIM(RTRIM(FILLER4)) = 'BSE' AND LTRIM(RTRIM(FILLER8)) = ''
					 THEN LTRIM(RTRIM(FILLER9)) 
					 ELSE LTRIM(RTRIM(FILLER10))
				END),
		EXPIRYDATE=LTRIM(RTRIM(FILLER12)),
		STRIKE_PRICE=(CASE WHEN LTRIM(RTRIM(FILLER13)) = '' THEN '0' ELSE LTRIM(RTRIM(FILLER13)) END),
		OPTION_TYPE=LTRIM(RTRIM(FILLER14)),
		QTY=LTRIM(RTRIM(FILLER18)),
		PRICE=LTRIM(RTRIM(FILLER19)),
		INTIATED_BY=LTRIM(RTRIM(FILLER2)),
		MODIFIED_BY=LTRIM(RTRIM(FILLER3)),
		PRODUCT_TYPE=LTRIM(RTRIM(FILLER32)),
		ORDER_NO=LTRIM(RTRIM(FILLER25)),
		TRADE_NO=LTRIM(RTRIM(FILLER26)),
		ORDER_INIT_FROM=LTRIM(RTRIM(FILLER35)),
		ORDER_MODY_FROM=LTRIM(RTRIM(FILLER36)),
		SEGMENT=LTRIM(RTRIM(FILLER4)),
		INST_TYPE=LTRIM(RTRIM(FILLER8)),
		SELL_BUY=LTRIM(RTRIM(FILLER17)),
		UPLOADED_BY = @UNAME,
		UPLOADED_ON = GETDATE()
	FROM #TEMP 
	WHERE ISNUMERIC(FILLER18) = 1
	AND LTRIM(RTRIM(FILLER1)) = @TDATE
	AND LTRIM(RTRIM(FILLER23)) in(SELECT PARTY_CODE FROM #CLIENT)
	
DELETE FROM TBL_CONTRACT_TERMINAL_MAPPING     
WHERE REMARK_DESC ='MARGIN SQUARE-OFF'    


INSERT INTO TBL_CONTRACT_TERMINAL_MAPPING    
SELECT     
'',    
ORDER_NO=LTRIM(RTRIM(FILLER25)),    
'$',    
'MARGIN SQUARE-OFF',    
@tradedate,    
'2049-12-31 00:00:00.000',    
INSERTEDON = GETDATE(),    
INSERTEDBY = @UNAME    
    FROM #TEMP     
     WHERE ISNUMERIC(FILLER18) = 1  
      AND LTRIM(RTRIM(FILLER1)) = @TDATE    
     AND LTRIM(RTRIM(FILLER35))='RISKADMIN'    
     AND  LTRIM(RTRIM(FILLER4)) = 'NSE'

