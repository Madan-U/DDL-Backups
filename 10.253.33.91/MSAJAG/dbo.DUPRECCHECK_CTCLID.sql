-- Object: PROCEDURE dbo.DUPRECCHECK_CTCLID
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[DUPRECCHECK_CTCLID]
AS
DECLARE @SAUDA_DATE VARCHAR(11)

SELECT TOP 1 @SAUDA_DATE = LEFT(SAUDA_DATE,11) FROM TRADE_CTCLID_TMP

INSERT INTO TRADE_CTCLID
SELECT DISTINCT * FROM TRADE_CTCLID_TMP T1
WHERE NOT EXISTS (SELECT ORDER_NO FROM TRADE_CTCLID T 
				  WHERE T.SAUDA_DATE LIKE @SAUDA_DATE + '%'
				  AND T.SAUDA_DATE = T1.SAUDA_DATE 
				  AND T.SCRIP_CD = T1.SCRIP_CD
				  AND T.SERIES = T1.SERIES
				  AND T.ORDER_NO = T1.ORDER_NO
				  )
				  
TRUNCATE TABLE TRADE_CTCLID_TMP

GO
