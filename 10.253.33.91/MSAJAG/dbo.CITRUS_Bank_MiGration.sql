CREATE Proc [dbo].[CITRUS_Bank_MiGration]
As 


TRuncate table MIG_CITRUS_MULTIBANK_TMP

INSERT INTO MIG_CITRUS_MULTIBANK_TMP         
SELECT BO_PARTYCODE,BANM_NAME,BANM_BRANCH,BANM_MICR,BANM_IFSC_CD,CLIBA_AC_NO,        
AC_TYPE= CASE WHEN CLIBA_AC_TYPE in ( 'SAVINGS','SAVING') THEN 'SAVING'         
   WHEN CLIBA_AC_TYPE = 'CURRENT' THEN 'CURRENT'            
   ELSE '' END,CLIBA_AC_NAME,CLIBA_DEF_FLG, BANM_ID = CONVERT(VARCHAR(20), BANM_ID)        
FROM ABVSCITRUS.CRMDB_A.DBO.VIEW_CLASS_CLIENT_MULTIBANK_ALL A  WITH(NOLOCK)      
WHERE  CLIBA_AC_NO <> ''     -- and BO_PARTYCODE not in ('NULL')  
AND BO_PARTYCODE IN   
(SELECT PARTY_CODE FROM ABVSCITRUS.CRMDB_A.DBO.CLIENT_DP_BANK_MIG WITH (NOLOCK)   
WHERE UPD_FLAG = 'N' AND MOD3 LIKE '%BANK%' and party_code is not null and party_code <>'')  


 SELECT M.* INTO #MIG_CITRUS_MULTIBANK   
 FROM  DBO.CLIENT5 ,MIG_CITRUS_MULTIBANK_TMP M WITH (NOLOCK) 
 WHERE M.PARTY_CODE=CL_CODE  
 
 INSERT INTO POBANK (BANK_NAME,BRANCH_NAME) 
 SELECT DISTINCT BANK_NAME=LEFT(ISNULL(BANK_NAME, ''''),50), BRANCH_NAME=LEFT(ISNULL(BRANCH_NAME, ''''),40) 
 FROM #MIG_CITRUS_MULTIBANK M WITH (NOLOCK) 
 WHERE NOT EXISTS (SELECT BANK_NAME, BRANCH_NAME FROM DBO.POBANK WITH (NOLOCK) WHERE M.BANK_NAME = BANK_NAME AND M.BRANCH_NAME = BRANCH_NAME) 
   
 SELECT PARTY_CODE,BANKID = MAX(P.BANKID),AC_NO,AC_TYPE,AC_NAME, DEF       
 INTO #MULTIBANKID_ACC        
 FROM #MIG_CITRUS_MULTIBANK M (NOLOCK) , DBO.POBANK P (NOLOCK)       
 WHERE P.BANK_NAME = M.BANK_NAME AND P.BRANCH_NAME = M.BRANCH_NAME       
 AND NOT EXISTS (SELECT CLTCODE FROM Account.DBO.MULTIBANKID A WHERE A.CLTCODE = PARTY_CODE        
   AND A.ACCNO = M.AC_NO)       
 GROUP BY PARTY_CODE,AC_NO,AC_TYPE,AC_NAME, DEF        
        
	 
       
 INSERT INTO  Account.dbo.MULTIBANKID(CLTCODE,BANKID, ACCNO, ACCTYPE, CHEQUENAME, DEFAULTBANK)       
 SELECT * FROM #MULTIBANKID_ACC        
         
 
 UPDATE Account.DBO.MULTIBANKID SET DEFAULTBANK = 0    
 FROM MIG_CITRUS_MULTIBANK_TMP T, Account.DBO.MULTIBANKID  WHERE cltcode =t.PARTY_CODE  
 AND EXISTS (SELECT PARTY_CODE FROM #MULTIBANKID_ACC T1 WHERE T.PARTY_CODE = T1.PARTY_CODE AND DEF <>1 and AC_NO=Accno)  
 and DEFAULTBANK=1
        
         
          
 UPDATE M       
 SET DEFAULTBANK = 1   
 FROM MIG_CITRUS_MULTIBANK_TMP A,       
 Account.DBO.MULTIBANKID M,       
 POBANK P (NOLOCK)       
 WHERE P.BANK_NAME = A.BANK_NAME AND P.BRANCH_NAME = A.BRANCH_NAME       
 AND DEF = 1 AND M.CLTCODE = A.PARTY_CODE AND A.AC_NO = M.ACCNO  and DEFAULTBANK <> 1  /*AND M.BANKID=P.BANKID*/  
  
 DROP TABLE #MULTIBANKID_ACC

GO
