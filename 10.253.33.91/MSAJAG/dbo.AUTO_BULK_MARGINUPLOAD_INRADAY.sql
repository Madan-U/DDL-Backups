CREATE PROC [dbo].[AUTO_BULK_MARGINUPLOAD_INRADAY]   
(
	@FILENAME VARCHAR(100)='',
	@MARGINDATE VARCHAR(11)
)  
AS  
/*
	PROC TO IMPORT CASH MARGINUPLOAD_INRADAY FILE
*/
DECLARE @STRSQL VARCHAR(MAX),
@RECCNT	INT,
@FILECOUNTER INT

SET @FILECOUNTER = LEFT(RIGHT(@FILENAME,6),2)



SELECT MARGINDATE,PARTY_CODE,VARMARGIN,MINIMUM_MARGIN=VARMARGIN,ADDITIONAL_MARGIN,MTOM_LOSS,TOTAL_MARGIN_VAR,TOTAL_MARGIN,CL_FLAG 
INTO #TBL_CMMARGIN_INTRADAY FROM TBL_CMMARGIN_INTRADAY
WHERE 1 = 2 



SET @STRSQL = LOWER('BULK INSERT #TBL_CMMARGIN_INTRADAY FROM ''' + @FILENAME  + '''  WITH (FIELDTERMINATOR = '','',ROWTERMINATOR = ''\n'')')          
EXEC(@STRSQL)

DELETE FROM TBL_CMMARGIN_INTRADAY
WHERE MARGINDATE = @MARGINDATE AND FILECOUNTER = @FILECOUNTER

INSERT INTO TBL_CMMARGIN_INTRADAY
SELECT FILECOUNTER=@FILECOUNTER,MARGINDATE,PARTY_CODE,VARMARGIN,MINIMUM_MARGIN,
ADDITIONAL_MARGIN,MTOM_LOSS,TOTAL_MARGIN_VAR,TOTAL_MARGIN,CL_FLAG,
UPLOAD_ON=GETDATE(),UPLOAD_BY=''
FROM #TBL_CMMARGIN_INTRADAY

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #TBL_CMMARGIN_INTRADAY
IF @RECCNT = 0 
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'NO DATA FOUND IN THE FILE, PLEASE CHECK AND IMPORT CORRECT FILE'
END

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END

DROP TABLE #TBL_CMMARGIN_INTRADAY


--EXEC V2_PROCESS_STATUS_LOG_UPD @MARGINDATE,'MRGINTRADAYIMP','','AUTO_PROCESS',''

/*------------------------------------- END OF THE PROC ------------------------*/



