CREATE PROCEDURE [dbo].[CLS_PR_VALIDATEFILE] 
(
	@FILETYPE VARCHAR(15),
	@FILEPATHNAME VARCHAR(400),
	@SAUDA_DATE VARCHAR(11)
) AS

DECLARE @FILESINGLE VARCHAR(1000),			@LOOPID INT,				@DEFPATH VARCHAR(500),			@IMPFILE VARCHAR(500),
		@VALIDATEFILE VARCHAR(500),			@MEMBERCODE VARCHAR(15),	@SQL VARCHAR(8000),				@FILENAME VARCHAR(100),
		@INTSTAT INT,						@STRRET VARCHAR(200),		@EXCHANGECODE VARCHAR(3),		@INTDATEVAL INT,
		@ORGSAUDA_DATE VARCHAR(11),			@FILEDATE_V VARCHAR(12),	@FILETYPE_DESC VARCHAR(100)

SET @ORGSAUDA_DATE = @SAUDA_DATE

select @MEMBERCODE =MemberCode from owner(nolock)

IF  CHARINDEX('\',@FILEPATHNAME) > 0
BEGIN
	SET @FILENAME = REVERSE(LEFT(REVERSE(@FILEPATHNAME),CHARINDEX('\',REVERSE(@FILEPATHNAME))-1))
END
ELSE
BEGIN
	SET @FILENAME = @FILEPATHNAME
END

SET @INTSTAT = 0
SET @STRRET  = ''
SET @INTDATEVAL = 0
 

IF @FILETYPE ='CLOSING'
BEGIN
	IF RIGHT(@FILENAME,3) <> '.MD' AND LEFT(@FILENAME,4) <> LEFT(REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@SAUDA_DATE),103),'/',''),4)
	BEGIN
		SET @INTSTAT = 1
		SET @STRRET  = 'INVALID CLOSING FILE SELECTED'
	END
END


IF @FILETYPE ='MG02'
BEGIN
	IF LEFT(@FILENAME,6) <> 'C_MG02' 
	BEGIN
		SET @INTSTAT = 1
		SET @STRRET  = 'INVALID MARGIN FILE SELECTED'
	END
	SET @INTDATEVAL = 1
END

IF @FILETYPE ='VAR'
BEGIN
	IF LEFT(@FILENAME,15) <> 'C_VAR1_'+  REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@SAUDA_DATE),101),'/','')
	BEGIN
		SET @INTSTAT = 1
		SET @STRRET  = 'INVALID VAR MARGIN FILE SELECTED'
	END
END

IF @FILETYPE ='SCRIP'
BEGIN
	IF LEFT(@FILENAME,6) <> 'C_SEC_'
	BEGIN
		SET @INTSTAT = 1
		SET @STRRET  = 'INVALID SCRIP MASTER FILE SELECTED'
	END
	SET @INTDATEVAL = 1
END

IF @FILETYPE ='SETTMST'
BEGIN
	IF LEFT(@FILENAME,6) <> 'C_STC_'
	BEGIN
		SET @INTSTAT = 1
		SET @STRRET  = 'INVALID SETT MASTER FILE SELECTED'
	END
END

IF @FILETYPE ='STT'
BEGIN
	IF LEFT(@FILENAME,5) <> 'MWST_'
	BEGIN
		SET @INTSTAT = 1
		SET @STRRET  = 'INVALID STT  FILE SELECTED'
	END
	SET @INTDATEVAL = 1
END


IF @FILETYPE ='C_VAR_BOD'
	BEGIN
		IF LEFT(@FILENAME,6) <> 'C_VAR1'--+  REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@SAUDA_DATE),101),'/','')
		BEGIN
			SET @INTSTAT = 1
			SET @STRRET  = 'INVALID VAR MARGIN FILE SELECTED'
		END
		SET @FILENAME = REVERSE(SUBSTRING(REVERSE(@FILENAME), CHARINDEX('.', REVERSE(@FILENAME)) + 1, 999))
		SET @FILEDATE_V = SUBSTRING(@FILENAME,LEN(@FILENAME)-9,8)
		SET @SAUDA_DATE = LEFT(REPLACE(@SAUDA_DATE,'/',''),8)
		SET @INTDATEVAL = 1
		SELECT @FILENAME,@FILEDATE_V,@SAUDA_DATE,LEFT(@FILENAME,6)
	END


  --IF @FILETYPE IN ('MRG_UDIFFEOD','MRG_UDIFFPEAK')
		--BEGIN
		--   SELECT @VALIDATEFILE,@MEMBERCODE
		--   IF (SELECT CHARINDEX(UPPER('Margin_NCL_CM'),@FILENAME)) >  0 AND (SELECT CHARINDEX(UPPER(@MEMBERCODE),@FILENAME)) > 0
		--   BEGIN
		--     select 'dddd'
		--	 SET @VALIDATEFILE = REPLACE(@VALIDATEFILE,'MEMBERCODE',@MEMBERCODE)
		--	 SET @FILENAME = @VALIDATEFILE
		--   END
		--END


IF @INTDATEVAL = 1 AND @INTSTAT = 0
BEGIN
	--DECLARE @FILEDATE_V VARCHAR(12)
	DECLARE @FILEDATE VARCHAR(12)

	
	SET @FILEDATE_V = SUBSTRING(@FILENAME,LEN(@FILENAME)-11,8)
	
	
	IF LEN(@FILEDATE_V) <> 8
	BEGIN
		SET @INTSTAT = 1
		SET @STRRET = 'WRONG FILE NAME'		
	END	 
	ELSE
	BEGIN
		IF @FILEDATE_V <> REPLACE(@SAUDA_DATE,'/','')
		BEGIN
			SET @INTSTAT = 1	
			SET @STRRET = 'FILE DATE AND ENTER SELECT DATE IS DIFFERENT'			
		END
	END
END	

SELECT @INTSTAT AS INTSTAT,@STRRET AS STRRET

/*-------------------------------------- END OF THE PROC ---------------------------------*/

GO
