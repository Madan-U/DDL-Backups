WHERE RECDATE >= @MARGINDATE AND RECDATE <= @MARGINDATE + ' 23:59'  
    
SELECT SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY=NETQTY,PAYQTY=0,  
MTOM,NETAMT,  
ADNL_MARGIN=ADNL_MRG,  
ADNL_PER=CONVERT(NUMERIC(18,4),0),  
LEDAMT = CONVERT(NUMERIC(18,4),0)  
INTO #PAYIN  
FROM TBL_MG02 D   
WHERE D.MARGIN_DATE = @MARGINDATE  
AND Party_Code IN (SELECT PARTY_CODE FROM TBL_COMBINE_REPORTING_TDAY)

--AND MTOM <> 0   
/*  
