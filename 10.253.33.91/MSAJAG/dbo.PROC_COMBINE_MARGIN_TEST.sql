FROM VARDETAIL D WHERE DETAILKEY =@VARKEY   
AND D.SCRIP_CD = #PAYIN.SCRIP_CD AND D.SERIES = #PAYIN.SERIES  
  
UPDATE #PAYIN SET ADNL_MARGIN = ABS(NETAMT*ADNL_PER) / 100  
--WHERE QTY < 0   
*/  
  
SELECT DISTINCT SETT_NO, SETT_TYPE INTO #SETT FROM #PAYIN   
  
UPDATE #PAYIN SET PAYQTY = D.QTY   
FROM (SELECT D.SETT_NO, D.SETT_TYPE, PARTY_CODE,SCRIP_CD,D.SERIES,QTY = SUM(QTY)  
   FROM ANGELDEMAT.MSAJAG.DBO.DELTRANS_REPORT D, #SETT S  
   WHERE D.SETT_NO = S.SETT_NO   
   AND D.SETT_TYPE = S.SETT_TYPE  
   AND FILLER2 = 1 AND DRCR = 'C' AND SHARETYPE <> 'AUCTION'  
   AND Party_Code IN (SELECT PARTY_CODE FROM TBL_COMBINE_REPORTING_TDAY)
   GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE,SCRIP_CD,D.SERIES ) D  
WHERE D.SETT_NO = #PAYIN.SETT_NO   
   AND D.SETT_TYPE = #PAYIN.SETT_TYPE  
   AND D.PARTY_CODE = #PAYIN.PARTY_CODE   
   AND D.SCRIP_CD = #PAYIN.SCRIP_CD   
   AND D.SERIES = #PAYIN.SERIES   
   AND #PAYIN.QTY < 0   

IF CONVERT(DATETIME,@MARGINDATE) <= CONVERT(DATETIME,'MAR  1 2023')
BEGIN
	IF (SELECT ISNULL(SUM(T2DAY_MARGIN+T2DAY_MTM),0) FROM TBL_COMBINE_REPORTING_TDAY   
		WHERE MARGINDATE = @MARGINDATE) > 0   
	BEGIN    
	 UPDATE #PAYIN SET LEDAMT = T.T2DAY_CASH  
	 FROM TBL_COMBINE_REPORTING_TDAY T  
	 WHERE MARGINDATE =@MARGINDATE   
	 AND #PAYIN.PARTY_CODE = T.PARTY_CODE   
	END  
	ELSE  
	BEGIN    
	 UPDATE #PAYIN SET LEDAMT = -1  
	 FROM TBL_COMBINE_REPORTING_TDAY T  
	 WHERE MARGINDATE =@MARGINDATE   
	 AND #PAYIN.PARTY_CODE = T.PARTY_CODE   
	END  
END
ELSE
BEGIN
	IF (SELECT ISNULL(SUM(T1DAY_MARGIN+T1DAY_MTM),0) FROM TBL_COMBINE_REPORTING_TDAY   
		WHERE MARGINDATE = @MARGINDATE) > 0   
	BEGIN    
	 UPDATE #PAYIN SET LEDAMT = T.T1DAY_CASH  
	 FROM TBL_COMBINE_REPORTING_TDAY T  
	 WHERE MARGINDATE =@MARGINDATE   
	 AND #PAYIN.PARTY_CODE = T.PARTY_CODE   
	END  
	ELSE  
	BEGIN    
