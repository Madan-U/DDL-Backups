     +(CASE WHEN (TDAY_CASH + TDAY_NONCASH)<0 THEN (TDAY_CASH + TDAY_NONCASH) ELSE 0 END)    
     ,0)+ PRADNYA.DBO.FNMIN((CASE WHEN (TDAY_CASH + TDAY_NONCASH)>0 THEN     
       (TDAY_CASH + TDAY_NONCASH-TDAY_MARGIN) ELSE 0 END ), PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN,0)),0) 
 WHERE MARGINDATE = @MARGINDATE     
 
/*  
 UPDATE TBL_COMBINE_REPORTING_TDAY set TDAY_MARGIN_SHORT = (CASE WHEN TDAY_MARGIN_SHORT - PRADNYA.DBO.fnMax(TDAY_MTM_SHORT,0) >= 0 THEN TDAY_MARGIN_SHORT ELSE 0 END)
