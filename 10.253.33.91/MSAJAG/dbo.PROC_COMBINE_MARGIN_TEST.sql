	 FROM TBL_COMBINE_REPORTING_TDAY T  
	 WHERE MARGINDATE =@MARGINDATE   
	 AND #PAYIN.PARTY_CODE = T.PARTY_CODE   
	END  
END

UPDATE #PAYIN SET PAYQTY = ABS(QTY)  
WHERE QTY < 0 AND PAYQTY > ABS(QTY)   
   
SELECT PARTY_CODE, COLLECT=SUM(PRADNYA.DBO.FNMIN(MTOM,COLLECT))+CONVERT(NUMERIC(18,2),SUM(ADNL_MARGIN))   
INTO #MTMCOLLECT  
FROM (  
SELECT SETT_NO,SETT_TYPE,PARTY_CODE,MTOM=(CASE WHEN SUM(MTOM) < 0 THEN ABS(SUM(MTOM)) ELSE 0 END),  
COLLECT=SUM(CASE WHEN QTY < 0 AND MTOM < 0 THEN ABS(PAYQTY*(MTOM/ABS(QTY)))   
        WHEN QTY > 0 AND MTOM < 0 AND LEDAMT >= 0 THEN ABS(MTOM)
   ELSE 0 END),  
ADNL_MARGIN = SUM(CASE WHEN QTY < 0 AND ADNL_MARGIN > 0 THEN ABS(PAYQTY*(ADNL_MARGIN/ABS(QTY)))   
        WHEN QTY > 0 AND ADNL_MARGIN > 0 AND LEDAMT >= 0     
        THEN ABS(ADNL_MARGIN)   
      ELSE 0 END)  
FROM #PAYIN    
GROUP BY SETT_NO,SETT_TYPE,PARTY_CODE ) A  
GROUP BY PARTY_CODE  
  
