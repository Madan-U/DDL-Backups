*/
CREATE PROCEDURE [dbo].[CLS_GET_USER_VALIDATE] (
	@USERNAME VARCHAR(30),
	@PASSWORD VARCHAR(60),
	@IPADDRESS VARCHAR(100),
	@MACID VARCHAR(100),
    @TOG CHAR(1)='N'
)
AS
DECLARE 
	@FIRSTLOGIN INT,
	@ERRCODE INT,
	@ERRMSG VARCHAR(8000)
DECLARE 
	@CLIENT_LOGO INT,
	@COMP_LOGO INT,
	@COMP_NAME INT,
	@SCROLLER INT,
	@FOOTER INT,
	@LOGIN_REDIRECTION INT
DECLARE 
	@FLDUSERNAME VARCHAR(30),
	@FLDCATEGORY VARCHAR(30),
	@FLDSTNAME VARCHAR(30),
	@FLDPWDEXPIRY INT,
	@FLDPWDEXPIRYDATE DATETIME,
	@FLDATTEMPTCNT INT,
	@FLDSTATUS INT,
	@FLDLOGINFLAG INT,
	@FLDACCESSLVL CHAR(1),
	@FLDIPADD VARCHAR(100),
	@FLDFIRSTLOGIN CHAR(1),
	@FLDFORCELOGOUT INT,
	@LOOPI INT,
	@SPLITIP VARCHAR(20),
	@CSSFOLDER VARCHAR(100),
	@FLDFIRSTNAME VARCHAR(25),
	@FLDCATEGORYNAME VARCHAR(50),
	@SQLSCHEMA VARCHAR(50),
	@IPLOGIN BIT,
	@USERID INT,
	@PWD VARCHAR(50),
	@LASTLOGIN_LOG VARCHAR(30),
	@FLDADMIN VARCHAR(30),
	@PWD_EXPIRY_DATE VARCHAR(100),
	@FLD_MAC_ID VARCHAR(100),
	@TODAY VARCHAR(10),
	@GROUPID VARCHAR(20),
	@EXPDATE VARCHAR(20),
	@USER_SESSION VARCHAR(100),
	@LOGINMSG VARCHAR(6)

	SET @ERRCODE = 0
	SET @ERRMSG = ''
	SET @FIRSTLOGIN = 0
	SET @FLDUSERNAME = ''
	SET @FLDCATEGORYNAME = ''
	SET @FLDCATEGORY = ''
	SET @FLDSTNAME = ''
	SET @LASTLOGIN_LOG = ''
	SET @FLDADMIN = ''
	SET @PWD_EXPIRY_DATE = ''
	SET @USERID = 0
	SET @GROUPID = 0
	SET @TODAY = CONVERT(VARCHAR(10),GETDATE(),103)
	SET @SQLSCHEMA = ''
	SET @CSSFOLDER = ''
	SET @USER_SESSION = NEWID()
	SET @LOGINMSG = 'LOG_SC'

CREATE TABLE #CLIENT_DATA
(
	FLDUSERNAME VARCHAR(30),
	MOBILE VARCHAR(40),
	EMAIL VARCHAR(100),
	PAN VARCHAR(50),
	DOB DATETIME
)

IF (SELECT COUNT(1) FROM CLS_TBLPRADNYAUSERS_VW(NOLOCK) WHERE FLDUSERNAME = @USERNAME) = 0
BEGIN
	SET @ERRCODE = - 1
	--SET @ERRMSG = 'USER ID DOEST NOT EXISTS'
	SET @ERRMSG = 'INVALID USER ID / PASSWORD'
	SET @LOGINMSG = 'LOG_FL'
END

SELECT @CLIENT_LOGO =ISNULL(CLIENT_LOGO,0),	@COMP_LOGO=ISNULL(COMP_LOGO,0),	@COMP_NAME=ISNULL(COMP_NAME,0),	@SCROLLER=ISNULL(SCROLLER,0),@FOOTER =ISNULL(FOOTER,0), @LOGIN_REDIRECTION  =ISNULL(LOGIN_REDIRECTION,0) FROM CLS_GLOBAL_SETTING

SELECT 
	@USERID = A.FLDAUTO,
	@PWD = A.FLDPASSWORD,
	@FLDUSERNAME = FLDUSERNAME,
	@FLDCATEGORY = C.FLDCATEGORYCODE,
	@FLDCATEGORYNAME = FLDCATEGORYNAME, 
	@FLDSTNAME = A.FLDSTNAME,
	@FLDPWDEXPIRYDATE = A.PWD_EXPIRY_DATE,
	@FLDPWDEXPIRY = CASE WHEN A.PWD_EXPIRY_DATE < GETDATE()	THEN 1	ELSE 0	END,
	@FLDATTEMPTCNT = CASE WHEN B.FLDATTEMPTCNT >= B.FLDMAXATTEMPT THEN 1 ELSE 0	END,
	@FLDSTATUS = B.FLDSTATUS,
	@FLDLOGINFLAG = B.FLDLOGINFLAG,
	@FLDACCESSLVL = B.FLDACCESSLVL,
	@FLDIPADD = B.FLDIPADD,
	@FLD_MAC_ID = LTRIM(RTRIM(B.FLD_MAC_ID)),
	@FLDFIRSTLOGIN = B.FLDFIRSTLOGIN,
	@FLDFORCELOGOUT = B.FLDFORCELOGOUT,
	@FLDADMIN = D.FLDSTATUS,
	@GROUPID =  A.GROUP_ID,
	@EXPDATE=A.PWD_EXPIRY_DATE,
	@FLDFIRSTNAME =REPLACE(A.FLDFIRSTNAME, A.FLDSTNAME + ' ', '') + ' (' + A.FLDSTNAME +')'
FROM 
	CLS_TBLPRADNYAUSERS_VW A(NOLOCK) LEFT JOIN TBLUSERCONTROLMASTER B(NOLOCK) ON (A.FLDAUTO = B.FLDUSERID),
	TBLCATEGORY C(NOLOCK),
	TBLADMIN D(NOLOCK)
WHERE 
	C.FLDCATEGORYCODE = A.FLDCATEGORY
	AND C.FLDADMINAUTO = A.FLDADMINAUTO
	AND D.FLDAUTO_ADMIN = A.FLDADMINAUTO
	AND FLDUSERNAME = @USERNAME

SELECT @FLDCATEGORY = CLS_FLDCATEGORY FROM CLASSPLUS_CATMAPPING WHERE FLDCATEGORY = @FLDCATEGORY
SET @FIRSTLOGIN = CASE WHEN @FLDFIRSTLOGIN = 'N' THEN 0	ELSE 1 END

IF @FLDFORCELOGOUT > 0 AND @ERRCODE = 0
BEGIN
	SET @ERRCODE = - 1
	SET @ERRMSG = 'ACCESS HAS BEEN TEMPORARILY RESTRICTED DUE TO DATABASE MAINTAINANCE AT CSO KINDLY BEAR WITH US FOR SOME TIME SORRY FOR THE INCONVENIENCE.'
	SET @LOGINMSG = 'LOG_FL'
END
PRINT @FLDSTATUS

IF @FLDSTATUS > 0 AND @ERRCODE = 0
BEGIN
	SET @ERRCODE = - 1
	SET @ERRMSG = 'ACCESS DENIED. LOGIN ID DISABLED'
	SET @LOGINMSG = 'LOG_FL'
END

IF @FLDATTEMPTCNT > 0 AND @ERRCODE = 0
BEGIN
	SET @ERRCODE = - 1
	SET @ERRMSG = 'YOU HAVE EXCEEDED THE MAXIMUM NUMBER OF EXTENT FOR LOGIN'
	SET @LOGINMSG = 'LOG_FL'
	UPDATE TBLUSERCONTROLMASTER SET FLDSTATUS = (CASE WHEN (ISNULL(FLDATTEMPTCNT,0)) >= ISNULL(FLDMAXATTEMPT,0) THEN 1 ELSE ISNULL(FLDSTATUS,0) END) WHERE FLDUSERID = @USERID
END

IF @ERRCODE = 0
BEGIN
	IF (SELECT COUNT(1) FROM CLS_TBLPRADNYAUSERS_VW(NOLOCK) WHERE FLDUSERNAME = @USERNAME AND REPLACE( FLDPASSWORD,'''','`') = @PASSWORD) = 0
	BEGIN
		SET @ERRCODE = - 1
		SET @ERRMSG = 'INVALID USER ID / PASSWORD'
		SET @LOGINMSG = 'LOG_FL'
		UPDATE B SET FLDATTEMPTCNT = ISNULL(FLDATTEMPTCNT,0) + 1 FROM TBLPRADNYAUSERS A, TBLUSERCONTROLMASTER B WHERE A.FLDAUTO = B.FLDUSERID AND FLDUSERNAME = @USERNAME
		SELECT @FLDATTEMPTCNT = CASE WHEN B.FLDATTEMPTCNT >= B.FLDMAXATTEMPT THEN 1 ELSE 0	END FROM TBLPRADNYAUSERS A, TBLUSERCONTROLMASTER B WHERE A.FLDAUTO = B.FLDUSERID AND FLDUSERNAME = @USERNAME
		IF @FLDATTEMPTCNT > 0
		 SET @ERRMSG = 'YOU HAVE EXCEEDED THE MAXIMUM NUMBER OF EXTENT FOR LOGIN'

		UPDATE TBLUSERCONTROLMASTER SET FLDSTATUS = (CASE WHEN (ISNULL(FLDATTEMPTCNT,0)) >= ISNULL(FLDMAXATTEMPT,0) THEN 1 ELSE ISNULL(FLDSTATUS,0) END) WHERE FLDUSERID = @USERID
	END
END	

IF @FLDPWDEXPIRY > 0 AND @ERRCODE = 0
BEGIN
	SET @ERRCODE = - 1
	SET @ERRMSG = 'ACCESS DENIED. YOUR PASSWORD IS EXPIRED'
	SET @LOGINMSG = 'LOG_FL'
END

IF @FLDACCESSLVL = 'L' AND @ERRCODE = 0
BEGIN
	SET @IPLOGIN = 0
	IF LEN(@FLDIPADD) > 0
	BEGIN
		SET @LOOPI = 1
		WHILE 1 = 1
		BEGIN
			SET @SPLITIP =.DBO.CLS_PIECE(@FLDIPADD, '|', @LOOPI)
			IF @SPLITIP IS NULL OR @SPLITIP = ''
			BEGIN
				BREAK
			END
			IF LEFT(LTRIM(RTRIM(@IPADDRESS)), LEN(LTRIM(RTRIM(@SPLITIP)))) = LTRIM(RTRIM(@SPLITIP))
			BEGIN
				SET @IPLOGIN = 1
				BREAK
			END
			SET @LOOPI = @LOOPI + 1
		END
	END
	
	IF @IPLOGIN = 0
	BEGIN 
		IF @FLD_MAC_ID <> '' AND UPPER(@FLD_MAC_ID) = UPPER(@MACID)
		BEGIN
			SET @IPLOGIN = 1
		END
	END
	
	IF @IPLOGIN = 0
	BEGIN
		SET @ERRCODE = - 1
		SET @ERRMSG = 'ACCESS DENIED. YOUR IP ADDRESS / MAC ID IS NOT REGISTERED IN THE SYSTEM'
		SET @LOGINMSG = 'LOG_FL'
	END
END

IF @FLDADMIN = 'CLIENT'
 BEGIN
	--Added By Bhrt for MObile Number and Email Address
	INSERT INTO #CLIENT_DATA 
	SELECT TOP 1 CL_CODE,MOBILE_PAGER,EMAIL,PAN_GIR_NO,DOB FROM CLIENT_DETAILS(NOLOCK) WHERE CL_CODE = @USERNAME 

	IF @TOG = 'Y'
		BEGIN 
			IF (SELECT COUNT(1) FROM SYS.TABLES WHERE NAME = 'CLIENT_MASTER_CKYC') > 0
				BEGIN
					DECLARE @OBJID INT
					SELECT @OBJID = OBJECT_ID FROM SYS.TABLES WHERE NAME = 'CLIENT_MASTER_CKYC'
					IF (SELECT COUNT(1) FROM SYS.COLUMNS WHERE OBJECT_ID = @OBJID AND NAME = 'CLIENT_TRADE_TYPE') > 0
						BEGIN
							SELECT @CSSFOLDER = CASE WHEN CLIENT_TRADE_TYPE = 'T' THEN 'ALL_CM12' ELSE '' END,
							@ERRCODE = CASE WHEN CLIENT_TRADE_TYPE = 'T' THEN '' ELSE '-1' END,
							@ERRMSG = CASE WHEN CLIENT_TRADE_TYPE = 'T' THEN '' ELSE 'INVALID USER ID / PASSWORD' END 
							FROM CLIENT_MASTER_CKYC WHERE PARTY_CODE = @FLDSTNAME
							IF @@ROWCOUNT <= 0
								BEGIN
									SET @CSSFOLDER = ''
									SET @ERRCODE = - 1
									SET @ERRMSG = 'INVALID USER ID / PASSWORD'
									SET @LOGINMSG = 'LOG_FL'
								END
						END
					ELSE
						BEGIN
							SET @CSSFOLDER = ''
							SET @ERRCODE = - 1
							SET @ERRMSG = 'INVALID USER ID / PASSWORD'
							SET @LOGINMSG = 'LOG_FL'
						END
				END
		END
END
ELSE
BEGIN
	IF @TOG = 'Y'
		BEGIN
			IF (SELECT COUNT(1) FROM SYS.TABLES WHERE NAME = 'CLIENT_MASTER_CKYC') > 0
				BEGIN
					IF (SELECT COUNT(1) FROM CLIENT_MASTER_CKYC WHERE PARTY_CODE = @FLDUSERNAME) <= 0
						BEGIN
							SET @CSSFOLDER = ''
							SET @ERRCODE = - 1
							SET @ERRMSG = 'INVALID USER ID / PASSWORD'
							SET @LOGINMSG = 'LOG_FL'
						END
				END
		END
		
	 INSERT INTO #CLIENT_DATA 
	 SELECT CL_CODE = FLDUSERNAME ,MOBILE_PAGER = FLDPHONE1,EMAIL = FLDADDRESS2,PAN_GIR_NO = '',DOB = 'DEC 31 2049' FROM TBLPRADNYAUSERS WHERE FLDUSERNAME = @USERNAME 
END

IF @ERRCODE = 0
BEGIN
	UPDATE B SET FLDATTEMPTCNT = 0 FROM TBLPRADNYAUSERS A, TBLUSERCONTROLMASTER B WHERE A.FLDAUTO = B.FLDUSERID AND FLDUSERNAME = @USERNAME

	SELECT 
		@PWD_EXPIRY_DATE = 'PASSWORD EXPIRES ON - ' + 
		CASE WHEN DATEDIFF(DD, GETDATE(), @FLDPWDEXPIRYDATE) = 2 THEN 'THE DAY AFTER TOMORROW'
		ELSE 
			CASE WHEN DATEDIFF(DD, GETDATE(), @FLDPWDEXPIRYDATE) = 1 THEN 'TOMORROW'
		ELSE 
			CASE WHEN DATEDIFF(DD, GETDATE(), @FLDPWDEXPIRYDATE) = 0 THEN '<U>TODAY</U>'
		ELSE 
			LEFT(CONVERT(VARCHAR, @FLDPWDEXPIRYDATE, 109), 11)
		END END END

	SELECT @LASTLOGIN_LOG = CONVERT(VARCHAR(20), ADDDT, 113) FROM V2_LOGIN_LOG(NOLOCK) WHERE SNO = (SELECT MAX(SNO)	FROM V2_LOGIN_LOG(NOLOCK) WHERE USERID = @USERID)
END
print 'sdf'
SELECT FLDUSERNAME	= @USERNAME,
	FLDCATEGORY		= @FLDCATEGORY,
	FLDSTNAME		= @FLDSTNAME,		-- STATUSNAME
	FIRSTLOGIN		= @FIRSTLOGIN,
	ERRCODE			= @ERRCODE,
	ERRMSG			= @ERRMSG,
	LASTLOGIN_LOG	= @LASTLOGIN_LOG,
	FLDADMIN		= @FLDADMIN,		-- STATUSID
	PWD_EXPIRY_DATE = @PWD_EXPIRY_DATE,
	FLDUSERID		= @USERID,
	FLDPASSWORD		= @PWD,
	SQLSCHEMA		= @SQLSCHEMA,
	FLDCATEGORYNAME	= @FLDCATEGORYNAME, TODAY = @TODAY, GROUPID = @GROUPID,
	CSSFOLDER		= @CSSFOLDER,
	FLDFIRSTNAME	= @FLDFIRSTNAME,
	CLIENT_LOGO		= ISNULL(@CLIENT_LOGO ,0),
	COMP_LOGO		= ISNULL(@COMP_LOGO ,0),
	COMP_NAME		= ISNULL(@COMP_NAME  ,0),
	SCROLLER		= ISNULL(@SCROLLER ,0), 
	FOOTER			= ISNULL(@FOOTER ,0) ,
	LOGIN_REDIRECTION= ISNULL(@LOGIN_REDIRECTION ,0), 
	USER_SESSION	= @USER_SESSION,
	MOBILE			= ISNULL(MOBILE,''),
	EMAIL			= ISNULL(EMAIL,''),
	PAN				= ISNULL(PAN,''),
	DOB				= ISNULL(DOB,'31 DEC 2049')
FROM 
	#CLIENT_DATA

--INSERT INTO V2_LOGIN_LOG (USERID, USERNAME, CATEGORY, STATUSNAME, STATUSID, IPADD, ACTION, ADDDT) VALUES (@USERID, @FLDUSERNAME + '(CP)', @FLDCATEGORY, @FLDADMIN, @FLDSTNAME, @IPADDRESS, 'LOG_FL', GETDATE())
INSERT INTO V2_LOGIN_LOG (USERID, USERNAME, CATEGORY, STATUSNAME, STATUSID, IPADD, ACTION, ADDDT) VALUES (@USERID, @FLDUSERNAME, @FLDCATEGORY, @FLDADMIN, @FLDSTNAME, @IPADDRESS, @LOGINMSG + '(CP)', GETDATE())

/*- this is added by bharat for 2 factor authentication-*/
IF(SELECT KEYVALUE FROM CLS_WEBCONFIG_SETTING WHERE KEYNAME = 'OTP') = 'Y' AND 1=2
BEGIN
	SELECT * FROM CLS_2FA_SETTING
END
RETURN
/*------------------------------ END OF THE PROC ---------------------------------*/

GO
