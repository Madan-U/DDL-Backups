CREATE PROCEDURE [dbo].[RPT_DAILY_MG02] (@RUNDATE VARCHAR(11),@PARTY VARCHAR(11),@RPT VARCHAR(11))
AS ---- EXEC RPT_DAILY_MG02 'AUG 30 2021','GRMB131', ''

SELECT MARGINDATE=CONVERT(VARCHAR(11),MARGIN_DATE,105),PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE,SETT_NO,BUYQTY,BUYAMT,SELLQTY,SELLAMT,NETQTY,NETAMT,CL_RATE,MTOM,VARAMT,MINIMUM_MARGIN,ADNL_MRG
FROM MSAJAG.DBO.TBL_MG02 WITH (NOLOCK)
WHERE MARGIN_DATE > =  @RUNDATE  AND MARGIN_DATE < =  @RUNDATE + ' 23:59'
AND PARTY_CODE = @PARTY
ORDER BY PARTY_CODE

GO
