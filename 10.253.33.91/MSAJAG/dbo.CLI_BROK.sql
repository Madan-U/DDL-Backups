CREATE PROC CLI_BROK (@PARTY VARCHAR(11))
AS

SELECT DISTINCT PARTY_CODE,'EQ_INTRADAY' BROK_TYPE ,DAY_PUC,VAL_PERC FROM  Clientbrok_Scheme C,BROKTABLE B
where  party_code =@PARTY AND to_date >GETDATE() AND TRADE_TYPE ='NRM' AND Scheme_Type ='TRD'
AND C.TABLE_NO=B.Table_No  AND Upper_lim >'10000.00'   AND TRD_dEL<>'S'  
UNION ALL 
SELECT PARTY_CODE,'DELIVERY' BROK_TYPE ,NORMAL,VAL_PERC  FROM  Clientbrok_Scheme C,BROKTABLE B
where party_code =@PARTY and to_date >GETDATE() AND TRADE_TYPE ='NRM' AND Scheme_Type ='DEL'
AND C.TABLE_NO=B.Table_No  AND Upper_lim >'10000.00' 
UNION ALL
SELECT PARTY_CODE,'FUTURES' BROK_TYPE ,DAY_PUC,VAL_PERC FROM  ANGELFO.NSEFO.DBO.FOClientbrokScheme C,ANGELFO.NSEFO.DBO.BROKTABLE B
where party_code =@PARTY and DATE_TO >GETDATE() 
AND C.FUT_BROKTABLE=B.Table_No  AND Upper_lim >'10000.00'  AND TRD_dEL<>'S'
UNION ALL

SELECT SP_party_code PARTY_CODE,SP_SCRIP BROK_TYPE ,SP_MIN_BROKAMT,'V' FROM  ANGELFO.NSEFO.DBO.SCHEME_MAPPING C 
where SP_party_code =@PARTY and SP_DATE_tO>GETDATe()  AND SP_TRD_TYPE ='TRD' AND SP_SCRIP IN ('NIFTY','ALL')
UNION ALL
SELECT PARTY_CODE,'CURRENCY FUTURES' BROK_TYPE ,DAY_PUC,VAL_PERC FROM  ANGELFO.NSECURFO.DBO.FOClientbrokScheme C,ANGELFO.NSECURFO.DBO.BROKTABLE B
where party_code =@PARTY and DATE_TO >GETDATE() 
AND C.FUT_BROKTABLE=B.Table_No  AND Upper_lim >'10000.00'  AND TRD_dEL<>'S'
UNION ALL 
SELECT SP_party_code PARTY_CODE,'CURRENCY' BROK_TYPE ,SP_MIN_BROKAMT,'V'  FROM   ANGELFO.NSECURFO.DBO.SCHEME_MAPPING C 
where SP_party_code =@PARTY and SP_DATE_tO>GETDATe()  AND SP_TRD_TYPE ='TRD'  
UNION ALL 
SELECT PARTY_CODE,'COMMODITY FUTURES' BROK_TYPE ,DAY_PUC,VAL_PERC FROM  ANGELCOMMODITY.MCDX.DBO.FOClientbrokScheme C,
ANGELCOMMODITY.MCDX.DBO.BROKTABLE B
where party_code =@PARTY and DATE_TO >GETDATE() 
AND C.FUT_BROKTABLE=B.Table_No  AND Upper_lim >'10000.00'  AND TRD_dEL<>'S'
UNION ALL 
SELECT SP_party_code,'CURRENCY OPTIONS' BROK_TYPE ,SP_MIN_BROKAMT ,'V' FROM   ANGELCOMMODITY.MCDX.DBO.SCHEME_MAPPING C 
where SP_party_code =@PARTY and SP_DATE_tO>GETDATe()  AND SP_TRD_TYPE ='TRD'

GO
