CREATE PROC [dbo].[RPT_CORP_MASTER]  
(  
 @STATUSID VARCHAR(50),  
 @STATUSNAME VARCHAR(50),  
 @FROMDATE VARCHAR(11),  
 @TODATE  VARCHAR(11),  
 @FROMPARTY VARCHAR(10),  
 @TOPARTY VARCHAR(10),  
 @FROMSCRIP VARCHAR(12)='',  
 @TOSCRIP VARCHAR(12)='',  
 @OPT  VARCHAR(20)=''  
)  
  
AS  

SET	@TOSCRIP = (CASE WHEN @TOSCRIP = '' THEN 'ZZZZZ' ELSE @TOSCRIP END)

SELECT BC_RD_DATE = LEFT(BC_RD_DATE,11), ISIN,  
BSE_CODE, NSE_SYMBOL, NSE_SERIES, BC_RD_FLAG = BC_RD,  
TYPE_CP_ACTION, DIV_IN_RS, RATIO = SHARES_OFFERED, OLD_PAIDUP, NEW_PAIDUP, OFFER_PRICE   
FROM angeldemat.msajag.dbo.TBL_VIDOL_CPACT  
WHERE BC_RD_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'  
AND ISIN >= @FROMSCRIP AND ISIN <= @TOSCRIP  
ORDER BY BC_RD_DATE, ISIN

GO
