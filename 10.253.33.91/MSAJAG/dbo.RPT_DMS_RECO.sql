CREATE PROCEDURE RPT_DMS_RECO (@MARDATE DATETIME,@FPARTY VARCHAR(11),@TPARTY VARCHAR(11))

AS ---- EXEC RPT_DMS_RECO 'JUN 14 2022','',''

SELECT DISTINCT PARTY_CODE  INTO #MARG FROM TBL_COMBINE_REPORTING WITH(NOLOCK) WHERE MARGINDATE =@MARDATE AND PARTY_CODE <> '12798'
UNION 
SELECT DISTINCT PARTY_CODE FROM TBL_COMBINE_REPORTING_PEAK WITH(NOLOCK) WHERE MARGINDATE =@MARDATE  AND PARTY_CODE <> '12798'

SELECT DISTINCT PARTY_CODE AS MARGIN_PROCESS INTO #STMT FROM MARGIN_PROCESS

SELECT SRNO=1,REPORT='TOTAL COUNT OF DMS CLIENT ',COUNT (DISTINCT MARGIN_PROCESS) as CLIENTS INTO #RPT FROM #STMT
UNION ALL
SELECT SRNO=2,REPORT='TOTAL COUNT OF MARGIN  CLIENT ',COUNT (DISTINCT PARTY_CODE) as CLIENTS FROM #MARG
UNION ALL
SELECT SRNO=3,REPORT='MISSING CLIENTS IN DMS',(SELECT COUNT(*) FROM #MARG)-(SELECT COUNT(*) FROM #STMT) as CLIENTS

SELECT REPORT,CLIENTS FROM #RPT ORDER BY SRNO

GO
