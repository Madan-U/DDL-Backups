CREATE PROC [dbo].[NCX_UCC]
AS 
INSERT INTO WEEKLY_NCDX_CLIENT_DATA
SELECT BO_PARTYCODE,'A','V', CLEA_ACTIVE_FROM FROM ABVSCITRUS.CRMDB_A.DBO.CLIENT_MASTER M WITH(NOLOCK),ABVSCITRUS.CRMDB_A.DBO.CLIENT_EXCHANGE E WITH(NOLOCK)
WHERE CRN_NO=CLEA_CRN_NO  AND STATUS=5 AND CLEA_ACTIVE_STAT=1 AND CLEA_EXCHANGE_ID ='7'
AND CLEA_ACTIVE_FROM >=GETDATE()-10
AND  BO_PARTYCODE NOT IN 
(SELECT CL_CODE  FROM WEEKLY_NCDX_CLIENT_DATA(NOLOCK))

GO
