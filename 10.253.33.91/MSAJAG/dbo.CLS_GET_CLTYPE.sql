CREATE PROC [dbo].[CLS_GET_CLTYPE] (@STRTYPE VARCHAR(10)) AS

IF @STRTYPE='CL_STATUS'
BEGIN
	SELECT DISTINCT CL_STATUS, DESCRIPTION FROM CLIENTSTATUS
END
ELSE
BEGIN
	SELECT DISTINCT CL_TYPE, CL_TYPE FROM CLIENTTYPE
END

GO
