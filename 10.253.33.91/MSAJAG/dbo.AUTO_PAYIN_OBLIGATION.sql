SELECT SETT_NO,SETT_TYPE INTO #TEMP
   FROM SETT_MST WHERE START_DATE=CONVERT(VARCHAR(11),GETDATE(),120) AND SETT_TYPE  in ('N', 'W', 'M', 'Z')


SELECT    T.SETT_NO , F.SETT_TYPE, RTRIM(LTRIM(F.PARTY_CODE))PARTY_CODE	,F.scrip_Cd	,F.series,	F.Qty as PayinQty	,F.isin
FROM #FINAL1 F INNER JOIN #TEMP T on F.SETT_TYPE=T.SETT_TYPE
Order by SETT_NO, F.SETT_TYPE

