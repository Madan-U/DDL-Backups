CREATE PROC  [dbo].[AUTO_PAYIN_OBLIGATION]

AS

begin

SELECT PARTY_CODE,SCRIP_CD,SERIES,SUM(CASE WHEN SELL_BUY=1 THEN (CASE WHEN SETT_TYPE in ('N','M') THEN TRADEQTY ELSE 0 END) ELSE TRADEQTY*-1 END) QTY,SETT_TYPE INTO #SETT

 FROM SETTLEMENT WITH(NOLOCK)  WHERE SAUDA_DATE >=CONVERT(VARCHAR(11),GETDATE(),120)

 GROUP BY PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE



 INSERT INTO #SETT

 SELECT PARTY_CODE,SCRIP_CD,LEFT(SERIES,2),SUM(CASE WHEN SELL_BUY=1 THEN (CASE WHEN SETT_TYPE In ('RD','D') THEN TRADEQTY ELSE 0 END) ELSE TRADEQTY*-1 END) QTY 

 ,SETT_TYPE--INTO #SETT

 FROM AngelBSECM.BSEDB_AB.DBO.SETTLEMENT WITH(NOLOCK)  WHERE SAUDA_DATE >=CONVERT(VARCHAR(11),GETDATE(),120)

 GROUP BY PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE



 ALTER TABLE #SETT

 ADD ISIN VARCHAR(18)

 
 



UPDATE S SET ISIN=M.ISIN  FROM #SETT S,ANGELDEMAT.MSAJAG.DBO.MULTIISIN M
WHERE S.SCRIP_CD=M.SCRIP_CD AND S.SERIES=M.SERIES AND VALID=1



UPDATE S SET ISIN=M.ISIN  FROM #SETT S,ANGELDEMAT.BSEDB.DBO.MULTIISIN M
WHERE S.SCRIP_CD=M.SCRIP_CD   AND VALID=1  AND S.ISIN IS NULL

 



UPDATE S SET ISIN=T.ISIN FROM #SETT S,TBL_EXCHG_SCRIP_MAP T
 WHERE S.SCRIP_CD=NSE_SCRIP_CD AND S.ISIN IS NULL AND TO_DATE >GETDATE()



 UPDATE S SET ISIN=T.ISIN FROM #SETT S,TBL_EXCHG_SCRIP_MAP T

 WHERE S.SCRIP_CD=BSECODE AND S.ISIN IS NULL AND TO_DATE >GETDATE()



SELECT PARTY_CODE,ISIN,QTY*-1 AS QTY,SETT_TYPE INTO #FINAL1  FROM (

SELECT PARTY_CODE,ISIN,SUM(QTY) AS QTY ,
(
CASE WHEN SETT_TYPE IN ('N','D') THEN 'N'
     WHEN SETT_TYPE IN ('C','W') THEN 'W'
	 WHEN SETT_TYPE IN ('M','RD') THEN 'M'
	 WHEN SETT_TYPE IN ('Z','RC') THEN 'Z' 
	 ELSE SETT_TYPE END
) AS SETT_TYPE

FROM #SETT
GROUP BY PARTY_CODE,ISIN ,
	(
		CASE WHEN SETT_TYPE IN ('N','D') THEN 'N'
			 WHEN SETT_TYPE IN ('C','W') THEN 'W'  WHEN SETT_TYPE IN ('M','RD') THEN 'M'
	         WHEN SETT_TYPE IN ('Z','RC') THEN 'Z' 
	         ELSE SETT_TYPE END
	) 
) A
WHERE QTY <0   AND SETT_TYPE IN ('N','W', 'M', 'Z')



ALTER TABLE #FINAL1

ADD SCRIP_CD VARCHAR(10),SERIES VARCHAR(5)



UPDATE S SET  SCRIP_CD=T.NSE_SCRIP_CD,SERIES=T.NSE_SERIES
 FROM #FINAL1 S,TBL_EXCHG_SCRIP_MAP T With(nolock)
 WHERE isnull(s.SCRIP_CD,'')=''  AND TO_DATE >GETDATE()
 and s.isin = t.isin


UPDATE S SET SCRIP_CD=M.SCRIP_CD,SERIES=M.SERIES
FROM #FINAL1 S,ANGELDEMAT.MSAJAG.DBO.MULTIISIN M With(nolock)
WHERE S.ISIN=M.ISIN AND isnull(S.SCRIP_CD,'')='' 
AND VALID=1

 
UPDATE S SET SCRIP_CD=M.SCRIP_CD,SERIES=M.SERIES  
FROM #SETT S,ANGELDEMAT.BSEDB.DBO.MULTIISIN M With(nolock)
WHERE S.SCRIP_CD=M.SCRIP_CD   AND VALID=1  AND S.ISIN IS NULL

 


