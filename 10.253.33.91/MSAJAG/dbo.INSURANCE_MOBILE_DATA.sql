CREATE PROCEDURE [dbo].[INSURANCE_MOBILE_DATA]
(
@MOBILENO VARCHAR(20)
)
AS
BEGIN

SELECT A.CL_CODE,MOBILE_PAGER,'BROK'AS EXCHANGE,MAX(INACTIVE_FROM)AS INACTIVE_FROM
INTO #BROK
 FROM CLIENT_DETAILS A,CLIENT_BROK_DETAILS B  
 WHERE A.CL_cODE=B.CL_cODE AND MOBILE_PAGER  =@MOBILENO
 GROUP BY A.CL_CODE,MOBILE_PAGER
 
  SELECT PARTY_CODE AS CL_CODE,MOBILE_NO AS MOBILE_PAGER,'MFSS'AS EXCHANGE,(CASE WHEN INACTIVE_FROM>GETDATE()THEN 'ACTIVE'ELSE 'INACTIVE'END)AS STATUS
  INTO #MFSS
  FROM ANGELFO.BSEMFSS.DBO.MFSS_CLIENT
WHERE MOBILE_NO  =@MOBILENO

SELECT CL_CODE,MOBILE_PAGER,EXCHANGE,(CASE WHEN INACTIVE_FROM>GETDATE()THEN 'ACTIVE'ELSE 'INACTIVE'END)AS STATUS FROM #BROK
 UNION 
SELECT * FROM #MFSS
order by exchange
END

GO
