-- USP_GSTREVESAL_POSTING_DATA 'JUN 2017','0','zz','','LEDGER','CREDIT','EXCLUSIVE'



CREATE PROCEDURE [dbo].[USP_GSTREVESAL_POSTING_DATA]
	@INV_DATE VARCHAR(25),
	@FromClient VARCHAR(10),
	@ToClient VARCHAR(10),
	@Branch VARCHAR(10),
	--@INV_NO VARCHAR(20),
	@RECORDFOR VARCHAR(20),
	@INVOICE_TYPE VARCHAR(20),
	@ReversalTYPE VARCHAR(20)
AS


IF LEN(@INV_DATE) = 10
	AND CHARINDEX('/', @INV_DATE) > 0
BEGIN
	SET @INV_DATE = CONVERT(VARCHAR(25), CONVERT(DATETIME, @INV_DATE, 103), 109)
END



DECLARE
	@FROMDATE VARCHAR(11),
	@TODATE VARCHAR(11)

SET @FROMDATE = LEFT(CONVERT(DATETIME,@INV_DATE),11)

SET @TODATE = LEFT(DATEADD(M,1,CONVERT(DATETIME,@INV_DATE))-1,11)

--SELECT InvoiceData = PARTY_CODE + ',' + CONVERT(VARCHAR(10),SUM(TAXABLE_AMT))  FROM
SELECT A.PARTY_CODE , B.long_name AS Name, B.branch_cd , CONVERT(VARCHAR(10),SUM(TAXABLE_AMT)) TAXABLE_AMT ,INV_NO, 
		CASE WHEN UPPER(@ReversalTYPE) = 'EXCLUSIVE'  THEN CONVERT(VARCHAR(10),SUM(TAXABLE_AMT) - SUM(TAXABLE_AMT)*18/100) ELSE CONVERT(VARCHAR(10),SUM(TAXABLE_AMT)) END AS ReleasableAmount
FROM
(
	SELECT PARTY_CODE, TAXABLE_AMT = SUM(TAXABLE_AMT), INV_NO
	FROM TBL_GST_INVOICE--_TMP
	WHERE INVOICE_DATE BETWEEN @FROMDATE AND @TODATE
			AND RECORDFOR = @RECORDFOR
			AND INVOICE_TYPE = @INVOICE_TYPE
			AND (party_code BETWEEN @FromClient and @ToClient
				OR BRANCH_CODE = CASE WHEN @Branch = '' THEN NULL ELSE @Branch END )
	GROUP BY PARTY_CODE, INV_NO

	UNION ALL
	
	SELECT PARTY_CODE, TAXABLE_AMT = SUM(-REV_AMOUNT) , INV_NO
    FROM ACCOUNT.DBO.GST_REVERSAL_ENTRY_DETAILS
	WHERE RECORDFOR = @RECORDFOR
				AND INVOICE_TYPE = @INVOICE_TYPE
				AND (party_code BETWEEN @FromClient and @ToClient
				OR BRANCHCODE = CASE WHEN @Branch = '' THEN NULL ELSE @Branch END 
				)
	GROUP BY PARTY_CODE, INV_NO


	UNION ALL

	
	SELECT PARTYCODE, TAXABLE_AMT = CASE WHEN UPPER(ReversalType) = 'EXCLUSIVE' THEN SUM(-REVersalAMOUNT) + (SUM(-REVersalAMOUNT)*18/100) ELSE SUM(-REVersalAMOUNT) END  , InvoiceNo
    FROM GSTReversal_Preprocess
	WHERE RECORDFOR = @RECORDFOR
				AND INVOICETYPE = @INVOICE_TYPE
				AND (partycode BETWEEN @FromClient and @ToClient
				OR BRANCH = CASE WHEN @Branch = '' THEN NULL ELSE @Branch END 
				)
				AND [Status] = 'P' 
	GROUP BY PARTYCODE, InvoiceNo, ReversalType



) A
		INNER JOIN Client_Details B
		ON A.PARTY_CODE = B.cl_code
GROUP BY A.PARTY_CODE, B.long_name, B.branch_cd ,INV_NO
HAVING SUM(TAXABLE_AMT) >= 0

GO
