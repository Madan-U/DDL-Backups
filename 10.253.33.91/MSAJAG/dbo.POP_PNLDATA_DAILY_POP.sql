-- Object: PROCEDURE dbo.POP_PNLDATA_DAILY_POP
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [DBO].[POP_PNLDATA_DAILY_POP]                     
AS                        
                        
DECLARE @SAUDA_DATE VARCHAR(11),                        
  @YY  INT,                         
  @MM  INT,                         
  @DD  INT,                        
  @DATECUR    CURSOR,    
  @MCUR CURSOR,    
  @MDATE DATETIME,    
  @MPARTY VARCHAR(10),    
  @MISIN VARCHAR(12)                        
     
SET @MCUR = CURSOR FOR    
SELECT B.SAUDA_DATE, B.PARTY_CODE FROM BAK_DUPLI_BAK B  
ORDER BY 1, 2    
OPEN @MCUR    
FETCH NEXT FROM @MCUR INTO @MDATE, @MPARTY 
WHILE @@FETCH_STATUS = 0     
BEGIN    
  
 DELETE FROM TBL_PNL_DATA_05MAY    
 WHERE SAUDA_DATE >= @MDATE    
 AND PARTY_CODE = @MPARTY     

 SELECT SCRIP_CD, SERIES, BSECODE, ISIN INTO #ISIN FROM TBL_PNL_DATA_TEST_FINAL          
 WHERE SERIES <> 'BSE' AND PARTY_CODE = @MPARTY           
 GROUP BY SCRIP_CD, SERIES, BSECODE, ISIN           
          
 UPDATE TBL_PNL_DATA_TEST_FINAL SET SCRIP_CD = M.SCRIP_CD, SERIES = M.SERIES          
 FROM #ISIN M          
 WHERE TBL_PNL_DATA_TEST_FINAL.ISIN = M.ISIN  

 DROP TABLE #ISIN
  
 INSERT INTO TBL_PNL_DATA_05MAY  
 SELECT EXCHANGE,MARGINDATE,TRADETYPE,SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SERIES,BSECODE,ISIN,  
 USER_ID,CONTRACTNO,ORDER_NO,TRADE_NO,SAUDA_DATE,TRADEQTY,MARKETRATE,PNLRATE,MARKETTYPE,SELL_BUY,  
 INS_CHRG,TURN_TAX,OTHER_CHRG,SEBI_TAX,BROKER_CHRG,BILLFLAG,NBROKAPP,NSERTAX,N_NETRATE,OPPDATE,ORGDATE,RECTYPE   
 FROM TBL_PNL_DATA_TEST_FINAL  
 WHERE SAUDA_DATE >= @MDATE    
 AND PARTY_CODE = @MPARTY     
      
 DELETE FROM BAK_DUPLI_BAK  
 WHERE SAUDA_DATE = @MDATE    
 AND PARTY_CODE = @MPARTY  
      
 FETCH NEXT FROM @MCUR INTO @MDATE, @MPARTY 
END    
CLOSE @MCUR    
DEALLOCATE @MCUR

GO
