CREATE PROC [dbo].[DELPROC_DEL_BAK_29MAR2023]  
(  
 @SETT_NO VARCHAR(7),  
 @SETT_TYPE VARCHAR(2)  
)  
  
AS  
  
DELETE FROM DELNET 
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  

INSERT INTO DELNET  
SELECT * FROM ANGELDEMAT.MSAJAG.DBO.DELNET   
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE

GO
