-- Object: PROCEDURE dbo.DIY_INFO_MF
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE Proc [dbo].[DIY_INFO_MF]  
as  
  
DECLARE @FROMDATE DATETIME,@TODATE DATETIME   
  
SELECT @FROMDATE = MAX(START_DATE)  
FROM SETT_MST WHERE Start_date <= CONVERT(VARCHAR(11),GETDATE()-2,120)  
  
SELECT @TODATE = MAX(START_DATE)  
FROM SETT_MST WHERE Start_date <= CONVERT(VARCHAR(11),GETDATE(),120)  
  
  
SELECT DISTINCT C.CL_CODE,LONG_NAME,ACTIVE_DATE,EMAIL,MOBILE_PAGER,L_CITY INTO #CL  
from AngelNseCM.MSAJAG.DBO.CLIENT_DETAILS C,AngelNseCM.MSAJAG.DBO.CLIENT_BROK_DETAILS D  
WHERE C.CL_CODE =D.CL_CODE AND ACTIVE_DATE >=@FROMDATE AND ACTIVE_DATE <=@TODATE+ ' 23:59'  
AND C.CL_CODE LIKE 'DIY%'  
  
  create index ix_cl on #CL  
  (CL_CODE)  
  
SELECT CLTCODE,SUM( CASE WHEN VTYP=2 THEN VAMT ELSE 0 END ) AS VAMT,  
 SUM( CASE WHEN DRCR='D' THEN VAMT*-1 ELSE VAMT END ) AS TOTAL_BAL  INTO #BILL  
  FROM ACCOUNT.DBO.LEDGER_ALL L,#CL C WHERE CLTCODE =CL_CODE   
AND VDT >GETDATE()-10   
GROUP BY CLTCODE  
  
   
  
  
DECLARE @PARAMETER  DATETIME  
SET @PARAMETER = (SELECT TOP 1 Sdtcur  FROM [AngelFO].BBO_FA.DBO.PARAMETER WHERE CURYEAR=1)  
   
 SELECT CLTCODE,MAX(VDT) TRANS_DATE INTO #MF FROM [AngelFO].BBO_FA.DBO.MFSS_LEDGER_BSE WHERE CLTCODE IN (  
SELECT CL_CODE  FROM #CL )  
 AND VTYPE=15 AND VDT >=@PARAMETER  
 GROUP BY CLTCODE  
   
  
  
 SELECT  CL_CODE,LONG_NAME,ACTIVE_DATE,EMAIL,MOBILE_PAGER,L_CITY,ISNULL(VAMT,0) AS VAMT  ,  
 ISNULL(TOTAL_BAL,0) TOTAL_BAL,ISNULL(TRANS_DATE,'')TRANS_DATE  
  FROM #CL C  
 LEFT OUTER JOIN   
 #BILL  B ON C.cl_code=B.CLTCODE  
 LEFT OUTER JOIN   
 #MF M  ON C.cl_code =   M.CLTCODE

GO
