CREATE PROCEDURE [dbo].[PROC_CLIENT_COLLATREAL_ALLOC_VDT_EDT_New_CTE]
(
	@RUNDATE VARCHAR(11)
)	
AS
BEGIN
	create table #EXCHANGE
(
    idno int identity(1,1) not null,
	EXCHANGE varchar(20),
	SEGMENT varchar(20)
)
CREATE TABLE #NEWSEG
(
    idno int identity(1,1) not null,
	EXCHANGE varchar(20),
	SEGMENT varchar(20)
)
CREATE TABLE #dt
(
    idno int identity(1,1) not null,
	dt varchar(20)
)

insert into #EXCHANGE
SELECT DISTINCT EXCHANGE, SEGMENT 
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY WITH (NOLOCK)

insert into #dt
select 'VDT'
union ALL
SELECT 'EDT'

DECLARE @I2 INT, @MAXCNT2 INT
	SET @I2 = 1  
	SELECT @MAXCNT2 = COUNT(*) FROM #dt

WHILE (@I2 <=@MAXCNT2)  
BEGIN
declare @date as varchar (20)
select @date= dt from #dt where idno=@I2

--DECLARE @CNT INT ,@CNT1 INT,@I INT,@I1 INT 
DECLARE @I INT, @MAXCNT INT,@EXCHANGE varchar(20), @SEGMENT varchar(20)
SET @I = 1  
SELECT @MAXCNT = COUNT(*) FROM #EXCHANGE --where idno=4

WHILE (@I <=@MAXCNT)  
BEGIN
	PRINT 'LOOP1'

	--select @ID=idno from #EXCHANGE where idno=@I
	select @EXCHANGE=EXCHANGE,@SEGMENT=SEGMENT from  #EXCHANGE where idno=@I

	TRUNCATE TABLE #NEWSEG
	INSERT INTO #NEWSEG
	SELECT DISTINCT EXCHANGE,SEGMENT 
	FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
	WHERE 1 = (CASE WHEN EXCHANGE = @EXCHANGE AND SEGMENT =@SEGMENT THEN 2 ELSE 1 END) 

	DECLARE @I1 INT, @MAXCNT1 INT
	SET @I1 = 1  
	SELECT @MAXCNT1 = COUNT(*) FROM #NEWSEG 
	
	WHILE (@I1 <=@MAXCNT1)  
	BEGIN


		DECLARE @EXCHANGENEW varchar(20)='', @SEGMENTNEW varchar(20)=''
		SELECT @EXCHANGENEW=EXCHANGE,@SEGMENTNEW=SEGMENT FROM #NEWSEG WHERE idno=@I1
	   
	  
		;WITH CTE_vdtledger (PARTY_CODE,CP_CODE, VDT_LEDGER,OTHER_VDT_LEDGER)
		as
		(
			SELECT PARTY_CODE,CP_CODE, VDT_LEDGER=SUM(VDT_LEDGER), OTHER_VDT_LEDGER=SUM(OTHER_VDT_LEDGER) 
			FROM 
			(	
			SELECT PARTY_CODE,CP_CODE, VDT_LEDGER, OTHER_VDT_LEDGER=0 
			FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
			WHERE   EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT
			AND VDT_LEDGER < 0 
			UNION ALL
			SELECT PARTY_CODE,CP_CODE, VDT_LEDGER=0, OTHER_VDT_LEDGER=VDT_LEDGER FROM 
			CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
			WHERE    EXCHANGE = @EXCHANGENEW AND SEGMENT = @SEGMENTNEW and 
			 VDT_LEDGER > 0 
			) A
			GROUP BY PARTY_CODE,CP_CODE
			HAVING SUM(VDT_LEDGER) <> 0 AND SUM(OTHER_VDT_LEDGER) <> 0 
			AND 
			(
			SUM(VDT_LEDGER) > 0 AND SUM(OTHER_VDT_LEDGER) < 0 
			OR 	SUM(VDT_LEDGER) < 0 AND SUM(OTHER_VDT_LEDGER) > 0
			) 
		)		

		select * into #finalvdt from CTE_vdtledger

		insert into tbl_vdtledger_new
		select * from #finalvdt

				UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY SET 
				VDT_LEDGER = (CASE WHEN OTHER_VDT_LEDGER >= ABS(V.VDT_LEDGER) 
								   THEN OTHER_VDT_LEDGER + V.VDT_LEDGER
								   ELSE 0
							  END)
				FROM #finalvdt V
				WHERE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.EXCHANGE = @EXCHANGENEW 
				AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.SEGMENT = @SEGMENTNEW
				AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.PARTY_CODE = V.PARTY_CODE
				AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.CP_CODE = V.CP_CODE
				AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.VDT_LEDGER > 0 
		
				UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY SET 
				VDT_LEDGER = (CASE WHEN OTHER_VDT_LEDGER >= ABS(V.VDT_LEDGER) 
								   THEN 0
								   ELSE OTHER_VDT_LEDGER + V.VDT_LEDGER
							  END)
				FROM #finalvdt V
				WHERE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.EXCHANGE = @EXCHANGE 
				AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.SEGMENT = @SEGMENT
				AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.PARTY_CODE = V.PARTY_CODE
				AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.CP_CODE = V.CP_CODE
				AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.VDT_LEDGER < 0 


		--IF OBJECT_ID(N'#finalvdt') IS NOT NULL                
		DROP TABLE #finalvdt  
		--truncate table tbl_vdtledger_new

			SET @I1 = @I1+1  
	  END	
     SET @I = @I+1  
   END	
   SET @I2 = @I2+1  
End

END

GO
