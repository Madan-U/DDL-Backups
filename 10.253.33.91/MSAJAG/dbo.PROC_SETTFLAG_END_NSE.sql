	TURN_TAX = (S.TURN_TAX / S.TRADEQTY)*A.TRADEQTY,
	OTHER_CHRG = (S.OTHER_CHRG / S.TRADEQTY)*A.TRADEQTY,
	SEBI_TAX = (S.SEBI_TAX / S.TRADEQTY)*A.TRADEQTY,
	--BROKER_CHRG = (S.BROKER_CHRG / S.TRADEQTY)*A.TRADEQTY ,
	SERVICE_TAX = (S.SERVICE_TAX / S.TRADEQTY)*A.TRADEQTY ,
	NSERTAX = (S.NSERTAX / S.TRADEQTY)*A.TRADEQTY  
FROM #COMBINE_SETTLEMENT A, #SETTMARKING S
WHERE A.EXCHG = S.EXCHG
AND A.ORGSRNO = S.ORGSRNO

*/

SELECT SRNO, SETTFLAG, BILLFLAG, PARTIPANTCODE INTO #NSE_SETT_FLAG 
FROM MSAJAG.DBO.SETTLEMENT S
WHERE SRNO IN (SELECT ORGSRNO FROM  #COMBINE_SETTLEMENT WHERE EXCHG = 'NSE')

UPDATE #COMBINE_SETTLEMENT SET 
SETTFLAG = (CASE WHEN @BROK_SQ_OFF = 0 THEN S.SETTFLAG ELSE #COMBINE_SETTLEMENT.SETTFLAG END),
BILLFLAG = (CASE WHEN @BROK_SQ_OFF = 0 THEN S.BILLFLAG ELSE #COMBINE_SETTLEMENT.BILLFLAG END), 
PARTIPANTCODE = S.PARTIPANTCODE 
FROM #NSE_SETT_FLAG S
WHERE S.SRNO = ORGSRNO AND EXCHG = 'NSE'

UPDATE B SET INS_CHRG = A.INS_CHRG, AUCTIONPART = A.AUCTIONPART,
TURN_TAX = a.TURN_TAX,  OTHER_CHRG = a.OTHER_CHRG, SEBI_TAX = a.SEBI_TAX, BROKER_CHRG = a.BROKER_CHRG,
BROKAPPLIED = A.BROKAPPLIED, NBROKAPP = A.NBROKAPP, NETRATE = A.NETRATE, N_NETRATE = A.N_NETRATE,
BILLNO = A.BILLNO,SETTFLAG = A.SETTFLAG, BILLFLAG = A.BILLFLAG, SERVICE_TAX = A.SERVICE_TAX,
NSERTAX = A.NSERTAX 
FROM MSAJAG.DBO.SETTLEMENT B, #COMBINE_SETTLEMENT A WHERE EXCHG = 'NSE'  
AND B.SRNO = A.ORGSRNO  
AND ORGSRNO NOT IN (SELECT ORGSRNO FROM #SETTMARKING WHERE EXCHG = 'NSE')

DELETE FROM MSAJAG.DBO.SETTLEMENT 
WHERE SRNO IN (SELECT ORGSRNO FROM #SETTMARKING WHERE EXCHG = 'NSE')

INSERT INTO MSAJAG.DBO.SETTLEMENT
SELECT Contractno=org_contractno,Billno,Trade_No,Party_Code,Scrip_Cd=org_scrip_cd,User_Id,Tradeqty,Auctionpart,
	   Markettype,Series=org_series,Order_No,Marketrate,Sauda_Date,Table_No,Line_No,Val_Perc,Normal,
	   Day_Puc,Day_Sales,Sett_Purch,Sett_Sales,Sell_Buy,Settflag,Brokapplied,Netrate,Amount,
	   Ins_Chrg,Turn_Tax,Other_Chrg,Sebi_Tax,Broker_Chrg,Service_Tax,Trade_Amount,Billflag,
	   Sett_No=org_sett_no,Nbrokapp,Nsertax,N_Netrate,Sett_Type=org_sett_type,Partipantcode,Status,Pro_Cli,Cpid,
	   Instrument,Booktype,Branch_Id,Tmark,Scheme,Dummy1,Dummy2 
FROM #COMBINE_SETTLEMENT
WHERE ORGSRNO IN (SELECT ORGSRNO FROM #SETTMARKING WHERE EXCHG = 'NSE')
AND EXCHG = 'NSE'
