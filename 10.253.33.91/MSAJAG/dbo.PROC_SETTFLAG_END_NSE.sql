CREATE PROC [dbo].[PROC_SETTFLAG_END_NSE]
(
	@SETT_NO	VARCHAR(7),
	@SETT_TYPE	VARCHAR(2),
	@SAUDA_DATE	VARCHAR(11),
	@FROMPARTY	VARCHAR(10),
	@TOPARTY	VARCHAR(10)
)
AS


IF @TOPARTY = ''
BEGIN
	SET @TOPARTY = 'ZZZZZZZZZZ'
END

IF @SAUDA_DATE = ''
BEGIN
	SELECT @SAUDA_DATE = START_DATE FROM MSAJAG.DBO.SETT_MST 
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
END

IF @SETT_NO = ''
BEGIN
	SELECT @SETT_NO = SETT_NO FROM MSAJAG.DBO.SETT_MST 
	WHERE START_DATE LIKE @SAUDA_DATE + '%' AND SETT_TYPE = @SETT_TYPE  
END

DECLARE @BROK_SQ_OFF INT
SET @BROK_SQ_OFF = 0

SELECT @BROK_SQ_OFF = ISNULL(BROK_SQ_OFF,0)  FROM TBL_INTEROP_SETTING
WHERE  @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE 

SELECT * INTO #COMBINE_SETTLEMENT
FROM MSAJAG.DBO.COMBINE_SETTLEMENT
WHERE EXCHG = 'NSE'

SELECT EXCHG, 
	   ORGSRNO/*,  
	   TURN_TAX,    
       OTHER_CHRG,
	   SEBI_TAX,
	   BROKER_CHRG,
	   TRADEQTY = SUM(TRADEQTY),
	   BROKAPPLIED=MAX(BROKAPPLIED),
	   NBROKAPP=MAX(NBROKAPP),
	   SERVICE_TAX=MAX(SERVICE_TAX),
	   NSERTAX=MAX(NSERTAX)*/
INTO #SETTMARKING 
FROM #COMBINE_SETTLEMENT 
GROUP BY EXCHG, ORGSRNO 
HAVING COUNT(1) > 1

/*

