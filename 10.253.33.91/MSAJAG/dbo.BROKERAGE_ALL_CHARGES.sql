CREATE PROCEDURE [dbo].[BROKERAGE_ALL_CHARGES] (@FDATE VARCHAR(11),@TDATE VARCHAR(11))

AS --- EXEC BROKERAGE_ALL_CHARGES 'JUN 25 2021','JUN 30 2021'

SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),BROKERAGE = SUM(PBROKTRD+SBROKTRD+PBROKDEL+SBROKDEL), 
GST= SUM(SERVICE_TAX),TRANSACTION_CHARGES=SUM(TURN_TAX), SEBI_FEES=SUM(SEBI_TAX),STT=SUM(INS_CHRG), 
STAMP_DUTY =SUM(BROKER_CHRG),OTHER_CHRG=SUM(OTHER_CHRG),EXCHANGE='NSECM' INTO #CHARGES
FROM MSAJAG.DBO.CMBILLVALAN S WITH (NOLOCK)
WHERE S.SAUDA_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105)

INSERT INTO #CHARGES
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105), BROKERAGE = SUM(PBROKTRD+SBROKTRD+PBROKDEL+SBROKDEL), GST= SUM(SERVICE_TAX) , 
TRANSACTION_CHARGES=SUM(TURN_TAX), SEBI_FEES=SUM(SEBI_TAX),STT=SUM(INS_CHRG), 
STAMP_DUTY =SUM(BROKER_CHRG),OTHER_CHRG=SUM(OTHER_CHRG),EXCHANGE='BSECM'
FROM AngelBSECM.BSEDB_AB.DBO.CMBILLVALAN S WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105)

INSERT INTO #CHARGES
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),BROKERAGE =SUM(PBROKAMT+SBROKAMT),GST= SUM(SERVICE_TAX) , 
TRANSACTION_CHARGES=SUM(TURN_TAX), SEBI_FEES=SUM(SEBI_TAX),STT=SUM(INS_CHRG), 
STAMP_DUTY =SUM(BROKER_NOTE),OTHER_CHRG=SUM(OTHER_CHRG),EXCHANGE='NSEFO' 
FROM ANGELFO.NSEFO.DBO.FOBILLVALAN WITH (NOLOCK)
WHERE  TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105)

INSERT INTO #CHARGES
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),BROKERAGE =SUM(PBROKAMT+SBROKAMT),GST= SUM(SERVICE_TAX) , 
TRANSACTION_CHARGES=SUM(TURN_TAX), SEBI_FEES=SUM(SEBI_TAX),STT=SUM(INS_CHRG), 
STAMP_DUTY =SUM(BROKER_NOTE),OTHER_CHRG=SUM(OTHER_CHRG),EXCHANGE='NSECD' 
FROM ANGELFO.NSECURFO.DBO.FOBILLVALAN WITH (NOLOCK)
WHERE  TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105)

INSERT INTO #CHARGES
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),BROKERAGE =SUM(PBROKAMT+SBROKAMT),GST= SUM(SERVICE_TAX) , 
TRANSACTION_CHARGES=SUM(TURN_TAX), SEBI_FEES=SUM(SEBI_TAX),STT=SUM(INS_CHRG), 
STAMP_DUTY =SUM(BROKER_NOTE),OTHER_CHRG=SUM(OTHER_CHRG),EXCHANGE='MCDX' 
FROM ANGELCOMMODITY.MCDX.DBO.FOBILLVALAN WITH (NOLOCK)
WHERE  TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105)

INSERT INTO #CHARGES
SELECT SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,105),BROKERAGE =SUM(PBROKAMT+SBROKAMT),GST= SUM(SERVICE_TAX) , 
TRANSACTION_CHARGES=SUM(TURN_TAX), SEBI_FEES=SUM(SEBI_TAX),STT=SUM(INS_CHRG), 
STAMP_DUTY =SUM(BROKER_NOTE),OTHER_CHRG=SUM(OTHER_CHRG),EXCHANGE='NCDX' 
FROM ANGELCOMMODITY.NCDX.DBO.FOBILLVALAN WITH (NOLOCK)
WHERE  TRADETYPE = 'BT' AND SAUDA_DATE BETWEEN @FDATE AND @TDATE + ' 23:59'
GROUP BY CONVERT(VARCHAR(11),SAUDA_DATE,105)


