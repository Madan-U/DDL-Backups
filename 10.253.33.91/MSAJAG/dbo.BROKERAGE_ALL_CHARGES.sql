--[Total STT (Rs. In Crores)]=ROUND(SUM(STT)/10000000,2),
--[Total Tansaction (Rs. In Crores)]=ROUND(SUM(TRANSACTION_CHARGES)/10000000,2),
--[Total SEBI Turnover Fees (Rs.)]=ROUND(SUM(SEBI_FEES),2),
--[Total Stamp Duty (Rs. In Crores)]=ROUND(SUM(STAMP_DUTY)/10000000,2),
--[Total GST (Rs. In Crores)]=ROUND(SUM(GST)/10000000,2)
--FROM #CHARGES
--GROUP BY SAUDA_DATE


SELECT [Sauda Date] = SAUDA_DATE,
[Total Brokerage] = CAST(SUM(BROKERAGE) AS NUMERIC(18,2)),
[Total STT]= CAST( SUM(STT) AS NUMERIC(18,2)),
[Total Tansaction] = CAST(SUM(TRANSACTION_CHARGES) AS NUMERIC(18,2)),
[Total SEBI Turnover Fees] = CAST(SUM(SEBI_FEES) AS NUMERIC(18,2)),
[Total Stamp Duty] = CAST(SUM(STAMP_DUTY) AS NUMERIC(18,2)),
[Total GST]= CAST(SUM(GST) AS NUMERIC(18,2))
FROM #CHARGES
GROUP BY SAUDA_DATE

GO
