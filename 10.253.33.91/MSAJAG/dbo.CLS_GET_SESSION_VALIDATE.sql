CREATE PROC [dbo].[CLS_GET_SESSION_VALIDATE] (
	@USERNAME VARCHAR(30)
	,@UMSESSION VARCHAR(60)
	,@IPADDRESS VARCHAR(100)
	,@STEP SMALLINT
	)
	/*

EXEC CLS_GET_SESSION_VALIDATE 'BROKER','C81965E3-D812-4E27-9A44-1B63BA63EA2B','',1

exec cls_get_session_validate @USERNAME='broker',@UMSESSION='4D076764-DD71-4BA5-AB0C-D58D25E90CA5',@IPADDRESS='::1:10.228.50.72',@STEP='2'

truncate table TBLCLASSUSERLOGINS 

SELECT * FROM TBLCLASSUSERLOGINS WHERE  FLDUSERNAME ='BROKER'

*/
AS
DECLARE @@USER_SESSION VARCHAR(200)
	,@@USER_COUNT TINYINT
	,@ERRCODE INT
	,@ERRMSG VARCHAR(8000)
	,@SNO INT
	,@FLAG VARCHAR(10)

IF @UMSESSION = ''
	SET @@USER_SESSION = NEWID()
ELSE
	SET @@USER_SESSION = @UMSESSION

IF @STEP = 1
BEGIN
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

	SELECT @SNO = SNO
		,@FLAG = (
			CASE 
				WHEN (DATEDIFF(S, DATEADD(MI, FLDTIMEOUTPRD, FLDLASTVISIT), GETDATE())) <= 0
					THEN 'VALID'
				ELSE 'EXPIRED'
				END
			) --, 
	--FLDSTATUS, FLDSTNAME, FLDUSERNAME 
	FROM TBLCLASSUSERLOGINS(NOLOCK)
	WHERE FLDUSERNAME = @USERNAME
		AND FLDSESSION = @@USER_SESSION
		AND FLDIPADDRESS = @IPADDRESS

	IF @@ROWCOUNT > 0
	BEGIN
		IF @FLAG = 'VALID'
		BEGIN
			UPDATE TBLCLASSUSERLOGINS
			SET FLDLASTVISIT = GETDATE()
			WHERE SNO = @SNO

			SET @ERRCODE = 0
			SET @ERRMSG = @FLAG
		END
		ELSE
		BEGIN
			SET @ERRCODE = - 1
			SET @ERRMSG = 'LOGGED_IN'
		END
	END
	ELSE
	BEGIN
		SET @ERRCODE = - 1
		SET @ERRMSG = 'INVALID_SESSION'
	END
END

IF @STEP = 2
BEGIN
	SELECT @@USER_COUNT = COUNT(1)
	FROM TBLCLASSUSERLOGINS(NOLOCK)
	WHERE FLDUSERNAME = @USERNAME AND FLDSESSION <> ''

	IF ISNULL(@@USER_COUNT, 0) = 0
	BEGIN
		INSERT INTO TBLCLASSUSERLOGINS (
			FLDAUTO
			,FLDUSERNAME
			,FLDSTATUS
			,FLDSTNAME
			,FLDSESSION
			,FLDIPADDRESS
			,FLDLASTVISIT
			,FLDTIMEOUTPRD
			,FLDLASTLOGIN
			)
		SELECT P.FLDAUTO
			,P.FLDUSERNAME
			,A.FLDSTATUS
			,P.FLDSTNAME
			,@@USER_SESSION
			,@IPADDRESS
			,GETDATE()
			,ISNULL(M.FLDTIMEOUT, 1)
			,''
		FROM TBLPRADNYAUSERS P(NOLOCK)
		LEFT OUTER JOIN TBLUSERCONTROLMASTER M(NOLOCK) ON (M.FLDUSERID = P.FLDAUTO)
		INNER JOIN TBLADMIN A(NOLOCK) ON (P.FLDADMINAUTO = A.FLDAUTO_ADMIN)
		WHERE P.FLDUSERNAME = @USERNAME
	END
	ELSE
	BEGIN
		UPDATE TBLCLASSUSERLOGINS
		SET FLDIPADDRESS = @IPADDRESS
			,FLDSESSION = @@USER_SESSION
			,FLDLASTVISIT = GETDATE()
			,FLDLASTLOGIN = GETDATE()
		WHERE FLDUSERNAME = @USERNAME
	END

	IF @@ERROR <> 0
	BEGIN
		SET @ERRCODE = - 1
		SET @ERRMSG = 'UNABLE TO FETCH USER INFORMATION'
	END
	ELSE
	BEGIN
		SET @ERRCODE = 0
		SET @ERRMSG = @@USER_SESSION
	END
END

SELECT ERRCODE = @ERRCODE
	,ERRMSG = @ERRMSG

GO
