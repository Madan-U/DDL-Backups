-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[PROC_EPN_STATUS_UPLOAD_BKUP_30SEP20] 

(
	@TDATE		VARCHAR(10), 
	@FILEFOR	VARCHAR(20), 
	@FILETYPE	VARCHAR(50), 
	@PROGID		VARCHAR(50), 
	@UNAME		VARCHAR(50),
	@FILENAME	VARCHAR(100) = ''
)
AS

DECLARE @SETT_DATE DATETIME,
		@RECCNT		INT,
		@FILEDATE	VARCHAR(10)

SELECT @FILEDATE = .DBO.PIECE(FILE_DATA,',',5) FROM FILEDATA
WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) = '01'
 
SELECT @FILEDATE = LEFT(@FILEDATE,2) + '/' + RIGHT(LEFT(@FILEDATE,4),2) + '/' + RIGHT(@FILEDATE,4)

SELECT @SETT_DATE = CONVERT(DATETIME,@FILEDATE,103)

IF @SETT_DATE <> CONVERT(DATETIME,@TDATE,103)
BEGIN 
	SELECT STRMSGCODE = -1, STRMSG = 'SELECTED DATE IS NOT MATCHING WITH THE FILE DATE.'
	RETURN
END

SELECT * INTO #TBL_PAYIN_DATA 
FROM TBL_PAYIN_DATA
WHERE 1 = 2 
 
INSERT INTO #TBL_PAYIN_DATA
SELECT 
	RECORD_TYPE = 'EPN',
	SETT_NO = .DBO.PIECE(FILE_DATA,',',7), 
	SETT_TYPE = .DBO.PIECE(FILE_DATA,',',6),
	TRANSDATE = @SETT_DATE,
	PARTY_CODE = .DBO.PIECE(FILE_DATA,',',5),
	SCRIP_CD = .DBO.PIECE(FILE_DATA,',',2),
	SERIES = .DBO.PIECE(FILE_DATA,',',3),
	EPNQTY = .DBO.PIECE(FILE_DATA,',',8),
	EPN_STATUS = DBO.PIECE(FILE_DATA,',',9),
	NONEPNQTY = 0,
	AVGRATE = 0,
	OPENQTY = 0,
	SQOFFAMT = 0,
	NETAMT = 0,
	MARGIN_AMT = 0,
	UPLOAD_BY = @UNAME,
	UPLOAD_ON = GETDATE()
FROM FILEDATA
WHERE PROGID = @PROGID AND DBO.PIECE(FILE_DATA,',',1) = '20'

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #TBL_PAYIN_DATA
WHERE TRANSDATE = @SETT_DATE
AND RECORD_TYPE = 'EPN'

SELECT DISTINCT SETT_NO, SETT_TYPE INTO #EPNSETT FROM #TBL_PAYIN_DATA
  
DELETE FROM TBL_PAYIN_DATA
WHERE TRANSDATE = @SETT_DATE
AND RECORD_TYPE = 'EPN'
AND SETT_NO IN (SELECT SETT_NO FROM #EPNSETT WHERE #EPNSETT.SETT_NO = TBL_PAYIN_DATA.SETT_NO
AND #EPNSETT.SETT_TYPE = TBL_PAYIN_DATA.SETT_TYPE)

INSERT INTO TBL_PAYIN_DATA
SELECT * FROM #TBL_PAYIN_DATA

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
