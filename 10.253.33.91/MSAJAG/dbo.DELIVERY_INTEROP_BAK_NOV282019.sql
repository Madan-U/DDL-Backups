CREATE  PROC [dbo].[DELIVERY_INTEROP]
(
	@SETT_NO	VARCHAR(7),
	@SETT_TYPE	VARCHAR(2)
)
AS

DECLARE @OTHER_SETT_TYPE VARCHAR(2),
		@OTHER_SETT_NO	VARCHAR(7),
		@START_DATE VARCHAR(11),
		@CLEARINGCODE	VARCHAR(10),
		@GROSS_NET_FLAG	VARCHAR(1)
		
SELECT @START_DATE = START_DATE FROM SETT_MST WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  


SELECT @CLEARINGCODE = ISNULL(CLEARINGCODE,'') FROM TBL_INTEROP_SETTING
WHERE  @START_DATE BETWEEN FROM_DATE AND TO_DATE

IF @CLEARINGCODE = ''
BEGIN
	RETURN
END 
 
IF (SELECT ISNULL(COUNT(1),0) FROM TBL_INTEROP_SETT_TYPE WHERE NSE_SETT_TYPE = @SETT_TYPE AND @START_DATE BETWEEN FROM_DATE AND TO_DATE) > 0
BEGIN 
	SELECT @OTHER_SETT_TYPE = BSE_SETT_TYPE, @GROSS_NET_FLAG = DEL_LEVEL FROM TBL_INTEROP_SETT_TYPE WHERE NSE_SETT_TYPE = @SETT_TYPE AND @START_DATE BETWEEN FROM_DATE AND TO_DATE
	
	SELECT @OTHER_SETT_NO = SETT_NO FROM [ANAND].BSEDB_AB.DBO.SETT_MST WHERE START_DATE >= @START_DATE AND START_DATE <= @START_DATE + ' 23:59' AND SETT_TYPE = @OTHER_SETT_TYPE   
	/*
	SELECT * INTO DELIVERYCLT_ORG FROM DELIVERYCLT 
	WHERE 1 = 2 
	SELECT * INTO DELNET_ORG FROM DELNET 
	WHERE 1 = 2 
	*/
		
	DELETE FROM DELIVERYCLT_ORG 
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
	
	INSERT INTO DELIVERYCLT_ORG 
	SELECT * FROM DELIVERYCLT 
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE 

	DELETE FROM DELIVERYCLT_isin
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
	
		
	DELETE FROM DELIVERYCLT  
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE 
	  
	--DELETE FROM DELIVERYCLT_REPORT 
	--WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE   
	
	DELETE FROM DELNET  
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE 
	
	IF @CLEARINGCODE = 'NSCCL'   
	BEGIN 
		SELECT *, ISIN = CONVERT(VARCHAR(12),'') INTO #DEL_OP FROM [ANAND].BSEDB_AB.DBO.DELIVERYCLT_ORG
		WHERE SETT_NO = @OTHER_SETT_NO AND SETT_TYPE = @OTHER_SETT_TYPE  
		
		INSERT INTO #DEL_OP 
		SELECT *, ISIN = CONVERT(VARCHAR(12),'') FROM DELIVERYCLT_ORG
		WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  
		
		UPDATE #DEL_OP SET PARTIPANTCODE = MEMBERCODE, SETT_NO = @SETT_NO, SETT_TYPE = @SETT_TYPE FROM OWNER 
		
		UPDATE #DEL_OP SET ISIN = M.ISIN FROM TBL_EXCHG_SCRIP_MAP M
		WHERE M.BSECODE = SCRIP_CD AND SERIES = 'BSE' AND @START_DATE BETWEEN FROM_DATE AND TO_DATE 
		
		UPDATE #DEL_OP SET ISIN = M.ISIN FROM TBL_EXCHG_SCRIP_MAP M
		WHERE M.NSE_SCRIP_CD = SCRIP_CD AND NSE_SERIES  = SERIES  AND @START_DATE BETWEEN FROM_DATE AND TO_DATE 
		
		UPDATE #DEL_OP SET SCRIP_CD = NSE_SCRIP_CD, SERIES = NSE_SERIES FROM TBL_EXCHG_SCRIP_MAP M
		WHERE M.ISIN = #DEL_OP.ISIN AND @START_DATE BETWEEN FROM_DATE AND TO_DATE 
		
                                -------CHANGE ON JULY  3 2019-------
		UPDATE #DEL_OP SET SCRIP_CD = NSE_SCRIP_CD, SERIES = NSE_SERIES FROM TBL_EXCHG_SCRIP_MAP M
		WHERE M.ISIN = #DEL_OP.ISIN AND @START_DATE BETWEEN FROM_DATE AND TO_DATE 
		AND SCRIP_CD = BSECODE 
		AND SERIES = 'BSE'
                                -------CHANGE ON JULY  3 2019-------

		UPDATE #DEL_OP SET SERIES=LEFT(SERIES,2)
		
		IF @GROSS_NET_FLAG = 'N'
		BEGIN
			INSERT INTO DELIVERYCLT
			SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE, QTY = ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)),
			INOUT = (CASE WHEN SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END) > 0 THEN 'I' ELSE 'O' END),
			BRANCH_CD, PARTIPANTCODE 
			FROM #DEL_OP
			GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE,BRANCH_CD, PARTIPANTCODE 
			HAVING ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)) <> 0 

			INSERT INTO DELIVERYCLT_isin
			SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE, QTY = ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)),
			INOUT = (CASE WHEN SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END) > 0 THEN 'I' ELSE 'O' END),
			BRANCH_CD, PARTIPANTCODE, ISIN
			FROM #DEL_OP
			GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE,BRANCH_CD, PARTIPANTCODE , ISIN
			HAVING ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)) <> 0 
			
			--INSERT INTO DELIVERYCLT_REPORT
			--SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE, QTY = ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)),
			--INOUT = (CASE WHEN SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END) > 0 THEN 'I' ELSE 'O' END),
			--BRANCH_CD, PARTIPANTCODE 
			--FROM #DEL_OP
			--GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE,BRANCH_CD, PARTIPANTCODE 
			--HAVING ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)) <> 0 
			
			INSERT INTO DELNET
			SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, QTY = ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)),
			INOUT = (CASE WHEN SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END) > 0 THEN 'I' ELSE 'O' END) 
			FROM #DEL_OP
			GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES 
			HAVING ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)) <> 0 
		END
		ELSE
		BEGIN
			INSERT INTO DELIVERYCLT
			SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE, QTY = ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)),
			INOUT = (CASE WHEN SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END) > 0 THEN 'I' ELSE 'O' END),
			BRANCH_CD, PARTIPANTCODE 
			FROM #DEL_OP
			GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE,BRANCH_CD, PARTIPANTCODE,INOUT  
			HAVING ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)) <> 0 

			INSERT INTO DELIVERYCLT_isin
			SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE, QTY = ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)),
			INOUT = (CASE WHEN SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END) > 0 THEN 'I' ELSE 'O' END),
			BRANCH_CD, PARTIPANTCODE ,isin
			FROM #DEL_OP
			GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE,BRANCH_CD, PARTIPANTCODE,INOUT ,isin 
			HAVING ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)) <> 0 
			
			--INSERT INTO DELIVERYCLT_REPORT
			--SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE, QTY = ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)),
			--INOUT = (CASE WHEN SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END) > 0 THEN 'I' ELSE 'O' END),
			--BRANCH_CD, PARTIPANTCODE 
			--FROM #DEL_OP
			--GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE,BRANCH_CD, PARTIPANTCODE,INOUT  
			--HAVING ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)) <> 0 
			
			INSERT INTO DELNET
			SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, QTY = ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)),
			INOUT = (CASE WHEN SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END) > 0 THEN 'I' ELSE 'O' END) 
			FROM #DEL_OP
			GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES,INOUT  
			HAVING ABS(SUM(CASE WHEN INOUT = 'I' THEN QTY ELSE -QTY END)) <> 0 
		END		
	END 
END

GO
