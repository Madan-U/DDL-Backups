	CREATE PROC [dbo].[DEL_ZERO_BROK_CLIENT_CODE]

	AS

	SELECT DISTINCT B2C_SB INTO #B2C
	FROM [MIS].REMISIOR.DBO.B2C_SB S WITH(NOLOCK)  WHERE ISNULL(B2C_SB,'')  NOT IN ('AMRVT','ARL','PTN','VRCBR','RAJ3','XI','XN','BAN','BLSPR',
'NARYN','NDD','XC','BLGM','ANTPUR','ASNL','NLORE','CHTPR','UDUPI','BEWAR','AJMRBK','JPRT','WDLA','PTLNGR','VGPC','DSSFR','TRFBR','BRDOLI',
'SPPUNE','PKMFR','HYDYSG','BHBNSR','INDOYN','MEHSNA','HLDWNI','AMRHM','RHLA','DANISH'
) 

	INSERT INTO Del_Zero_Brok_CLIENT (PARTY_CODE,FROM_DATE,TO_DATE,CREATED_ON,FLAG)
	SELECT DISTINCT D.CL_CODE,ACTIVE_DATE, CONVERT(VARCHAR(11),ACTIVE_DATE+29,120) + ' 23:59',GETDATE(),'B2C'
	FROM #B2C S WITH(NOLOCK),CLIENT_DETAILS D WITH(NOLOCK),
	(SELECT CL_CODE,MIN(ACTIVE_DATE) ACTIVE_DATE FROM CLIENT_BROK_DETAILS WITH(NOLOCK)
	WHERE SEGMENT ='CAPITAL' GROUP BY  CL_CODE )T 
	WHERE  B2C_SB=sub_broker AND Active_Date BETWEEN CONVERT(VARCHAR(11),GETDATE()-8,120)
	AND CONVERT(VARCHAR(11),GETDATE(),120) + ' 23:59' and ACTIVE_DATE >='2019-09-06'
	AND D.CL_CODE = T.CL_CODE 
	and NOT EXISTS (SELECT PARTY_CODE FROM Del_Zero_Brok_CLIENT Z WHERE Z.PARTY_CODE =D.CL_CODE )
	AND branch_cd NOT IN (SELECT BRCODE FROM DEL_ZERO_BR_EXCULDE)


  DECLARE @BODYMSG NVARCHAR(MAX)    
  DECLARE @SUBJECT NVARCHAR(MAX)  
  DECLARE @TABLEHTML NVARCHAR(MAX)  
  DECLARE @FINALCOUNT NVARCHAR(MAX)


	SELECT @FINALCOUNT = COUNT(1) FROM Del_Zero_Brok_CLIENT WHERE CREATED_ON >= CONVERT (VARCHAR (10),GETDATE(),120) 

	--PRINT @FINALCOUNT

IF 
@FINALCOUNT <> 0 
 set @FINALCOUNT = '<big>' + '<FONT COLOR="blue">'+ '<b>' + @FINALCOUNT + ' ' +'</FONT>' + '</b>' +' CLIENT ADDED TODAY..!'+ '</big> '
 ELSE 
  set @FINALCOUNT ='<big>' + 'NO CLIENT ADDED FOR THE DAY..! ' + '</big> '
  
SET @SUBJECT = 'DEL_ZERO_BROK_CLIENT_COUNT'   


EXEC MSDB.DBO.SP_SEND_DBMAIL    
@PROFILE_NAME ='DBA',
@RECIPIENTS='bo.support@angelbroking.com;siva.kopparapu@angelbroking.com' ,    
@COPY_RECIPIENTS='',    
@SUBJECT = @SUBJECT,    
@BODY = @FINALCOUNT,     
@BODY_FORMAT = 'HTML' ;

GO
