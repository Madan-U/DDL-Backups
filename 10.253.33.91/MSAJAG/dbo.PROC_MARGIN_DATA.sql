	   EPNQTY=0,EPN_STATUS='S',NONEPNQTY=0,
	   AVGRATE=CONVERT(NUMERIC(18,4),(CASE WHEN SELLQTY > 0 THEN SELLAMT/SELLQTY ELSE 0 END)),
	   OPENQTY=BUYQTY-SELLQTY,
	   SQOFFAMT=(CASE WHEN  BUYQTY = SELLQTY
					  THEN BUYAMT - SELLAMT ELSE 0 END),
	   NETAMT=(CASE WHEN SELLQTY <> BUYQTY  
