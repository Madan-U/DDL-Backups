CREATE PROC [dbo].[PROC_MARGIN_DATA] (@SAUDA_DATE VARCHAR(11), @FROMPARTY VARCHAR(10), @TOPARTY VARCHAR(10))              
AS              
-- EXEC PROC_MARGIN_DATA 'AUG 25 2020', '0', 'ZZZZZZZ'

DECLARE @SETT_NO	VARCHAR(7),
		@SETT_TYPE	VARCHAR(2),
		@START_DATE	VARCHAR(11),
		@SETTCUR	CURSOR

DELETE FROM TBL_PAYIN_DATA
WHERE TRANSDATE = @SAUDA_DATE
AND RECORD_TYPE='MARGIN'
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY

/*
INSERT INTO TBL_PAYIN_DATA
