INSERT INTO TBL_PAYIN_DATA
SELECT RECORD_TYPE='MARGIN',SETT_NO,SETT_TYPE,TRANSDATE=MARGIN_DATE,PARTY_CODE,SCRIP_CD,SERIES,
	   EPNQTY=0,EPN_STATUS='S',NONEPNQTY=0,
	   AVGRATE=CONVERT(NUMERIC(18,4),0),
	   OPENQTY=0,
	   SQOFFAMT=CONVERT(NUMERIC(18,4),0),
	   NETAMT=CONVERT(NUMERIC(18,4),0),
	   MARGIN_AMT=MINIMUM_MARGIN,
	   UPLOAD_BY='',UPLOAD_ON=''
FROM TBL_MG02
WHERE MARGIN_DATE = @SAUDA_DATE
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
AND VARAMT > 0 
 
SET @SETTCUR = CURSOR FOR
	SELECT SETT_NO, SETT_TYPE, START_DATE FROM SETT_MST WHERE START_DATE <= @SAUDA_DATE AND SEC_PAYIN > @SAUDA_DATE
	AND SETT_TYPE IN ('N','W','D','C','M','Z','RD','RC')
	ORDER BY 1, 2
OPEN @SETTCUR
FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE, @START_DATE
WHILE @@FETCH_STATUS = 0
BEGIN
	EXEC PROC_SETTFLAG_START @SETT_NO, @SETT_TYPE, @START_DATE, @FROMPARTY, @TOPARTY
	IF @SETT_TYPE IN ('N','D','M','RD')
	BEGIN
		INSERT INTO TBL_PAYIN_DATA
		SELECT RECORD_TYPE='MARGIN',SETT_NO,SETT_TYPE,TRANSDATE=@SAUDA_DATE,PARTY_CODE,SCRIP_CD,SERIES,
			   EPNQTY=0,EPN_STATUS='S',NONEPNQTY=0,
			   AVGRATE=CONVERT(NUMERIC(18,4),SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END) 
					  / SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 1 END)),
			   OPENQTY=SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END),
			   SQOFFAMT=(CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END) = 0
							  THEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE -TRADEQTY*MARKETRATE END)
							  ELSE 0 
						 END),
			   NETAMT=(CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END) <= 0
							  THEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE -TRADEQTY*MARKETRATE END)
							  ELSE 0 
						 END),
			   MARGIN_AMT=0,
			   UPLOAD_BY='',UPLOAD_ON=''
		FROM COMBINE_SETTLEMENT
		WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE 
		AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		GROUP BY SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SERIES
		HAVING SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END) > 0 
	END
	ELSE
	BEGIN
		INSERT INTO TBL_PAYIN_DATA
		SELECT RECORD_TYPE='MARGIN',SETT_NO,SETT_TYPE,TRANSDATE=@SAUDA_DATE,PARTY_CODE,SCRIP_CD,SERIES,
			   EPNQTY=0,EPN_STATUS='S',NONEPNQTY=0,
			   AVGRATE=CONVERT(NUMERIC(18,4),SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END) 
					  / SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 1 END)),
			   OPENQTY=SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END),
			   SQOFFAMT=(CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END) = 0
							  THEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE -TRADEQTY*MARKETRATE END)
							  ELSE 0 
						 END),
			   NETAMT=(CASE WHEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END) <= 0
							  THEN SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE -TRADEQTY*MARKETRATE END)
							  ELSE 0 
						 END),
			   MARGIN_AMT=0,
			   UPLOAD_BY='',UPLOAD_ON=''
		FROM COMBINE_SETTLEMENT
		WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE 
		AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		AND SELL_BUY = 2
		GROUP BY SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SERIES
		HAVING SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END) > 0 
	END
	FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE, @START_DATE
END
CLOSE @SETTCUR
DEALLOCATE @SETTCUR

GO
