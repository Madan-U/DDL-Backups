-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[CLS_AUTOPROCESS_FILE_BSE]            
(            
 @STATUSID VARCHAR(50),            
 @STATUSNAME VARCHAR(50),            
 @FROMDATE VARCHAR(11),            
 @TODATE  VARCHAR(11),            
 @FROMPARTY VARCHAR(10),            
 @TOPARTY VARCHAR(10),            
 @FROMSCRIP VARCHAR(12)='',            
 @TOSCRIP VARCHAR(12)='',            
 @OPT VARCHAR(20)=''          
)           
        
as        
  
SELECT   
[FILE TYPE] = (CASE WHEN FLD_FILENAME LIKE '%IN%' THEN 'SPEED'  
     WHEN FLD_FILENAME LIKE '%HLD%' THEN 'NSDL HOLDING'  
     WHEN FLD_FILENAME LIKE '%DPC7%' THEN 'CDSL HOLDING'  
     WHEN FLD_FILENAME LIKE '%DPC9%' THEN 'CDSL SOT'   
     WHEN FLD_FILENAME LIKE '%127%' then 'PCM HOLDING'  
     ELSE 'OTHER'   
   END),  
[FILE NAME] = FLD_FILENAME,   
[FILE DATE] = CONVERT(VARCHAR,FLD_FILEDATE,103) + ' ' + CONVERT(VARCHAR,FLD_FILEDATE,108),  
[UPLOAD TIME]=CONVERT(VARCHAR,PROCESS_ON,103) + ' ' + CONVERT(VARCHAR,PROCESS_ON,108),
FLAG = SNO  
INTO #DATA  
FROM ANGELDEMAT.BSEDB.DBO.TBL_AUTO_PROCESS_FILE   
WHERE FLD_FILEDATE >= @FROMDATE AND FLD_FILEDATE <= @TODATE + ' 23:59:59'  
AND 1 = (CASE WHEN @OPT NOT IN ('ALL', 'OTH') AND FLD_FILENAME LIKE '%' + @OPT + '%' THEN 1   
     WHEN @OPT = 'ALL' THEN 1   
     WHEN @OPT = 'OTH' AND FLD_FILENAME NOT LIKE '%IN%' AND FLD_FILENAME NOT LIKE '%HLD%'  
                       AND FLD_FILENAME NOT LIKE '%DPC9%' AND FLD_FILENAME NOT LIKE '%127%'  
                       AND FLD_FILENAME NOT LIKE '%DPC7%'  
     THEN 1   
     ELSE 2  
   END)  
ORDER BY FLAG, FLD_FILENAME, FLD_FILEDATE  
  
ALTER TABLE #DATA   
ADD SNO INT IDENTITY(1,1)  
  
SELECT SNO, [FILE TYPE], [FILE NAME], [FILE DATE], [UPLOAD TIME] FROM #DATA  
ORDER BY SNO

GO
