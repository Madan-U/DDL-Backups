DECLARE
	@RECORD_CNT INT,
	@T_1_DATE DATETIME,
	@SETT_DATE TINYINT

SET @SETT_DATE = 0

SELECT @SETT_DATE = COUNT(1) FROM MSAJAG..SETT_MST WHERE SETT_TYPE = 'M' AND START_DATE = @RUNDATE


IF @SETT_DATE > 0
BEGIN
	SELECT @T_1_DATE = MIN(START_DATE) FROM 
	(SELECT TOP 2 START_DATE FROM MSAJAG..SETT_MST WHERE SETT_TYPE = 'M' AND START_DATE <= @RUNDATE ORDER BY START_DATE DESC) A

	SET @RECORD_CNT = 0
	SELECT @RECORD_CNT = COUNT(1) FROM ACCOUNT..SEBIPAYOUT_PROCESSDATE_EXTERNAL WHERE NEW_PROCESS_DATE > @T_1_DATE + ' 23:59' AND PROCESS_DATE < @RUNDATE

	IF @RECORD_CNT > 0
	BEGIN
		UPDATE #REPORT_DATA SET SETTLEMENT_AMT = SEBIPOAMT
		FROM (SELECT PARTYCODE = CLTCODE, SEBIPOAMT = AMOUNT FROM ACCOUNT..SEBIPAYOUT_PROCESSDATE_EXTERNAL P WHERE NEW_PROCESS_DATE > @T_1_DATE + ' 23:59' AND PROCESS_DATE < @RUNDATE) A
		WHERE UCC = PARTYCODE
	END
	ELSE
	BEGIN
		UPDATE #REPORT_DATA SET SETTLEMENT_AMT = SEBIPOAMT
		FROM (SELECT PARTYCODE, SEBIPOAMT FROM ACCOUNT..SEBIPAYOUT_ALL (NOLOCK) WHERE VCH_DT = @RUNDATE) A
		WHERE UCC = PARTYCODE
	END
END
