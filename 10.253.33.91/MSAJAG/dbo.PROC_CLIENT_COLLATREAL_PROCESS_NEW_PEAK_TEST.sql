DECLARE @MTFDATE VARCHAR(11)

SELECT @MTFDATE = ISNULL(MAX(START_DATE),@RUNDATE) FROM SETT_MST WHERE START_DATE <= @RUNDATE
AND SETT_TYPE IN ('M','N')

SELECT PARTY_CODE, ISIN, QTY=SUM(QTY),CL_RATE,HAIRCUT,APPFLAG='N',SEC_TYPE='NONCASH' 
INTO #MTFHOLD FROM MTFTRADE.DBO.TBL_PRODUCT_HOLD_DATA
WHERE SAUDA_DATE = @MTFDATE 
AND HOLDFLAG = 'MTFCOLL'
GROUP BY PARTY_CODE, ISIN,CL_RATE,HAIRCUT

SELECT * INTO #PLDHOLD FROM TBL_NONCASH_REPORT
WHERE PROCESS_DATE = @RUNDATE 

UPDATE #MTFHOLD SET SEC_TYPE = P.SEC_TYPE, APPFLAG = TM_APP_FLAG, CL_RATE = P.CL_RATE, HAIRCUT = P.HAIRCUT
FROM #PLDHOLD P
WHERE P.PARTY_CODE = #MTFHOLD.PARTY_CODE
AND P.ISIN = #MTFHOLD.ISIN
AND BCLTDPID IN (SELECT DPCLTNO FROM [AngelDemat].MSAJAG.dbo.DELIVERYDP_ADNL)

SELECT PARTY_CODE, 
[APPROVED_SEC_CASH_REC_TM]  = SUM(CASE WHEN SEC_TYPE = 'CASH' THEN QTY*CL_RATE-QTY*CL_RATE*HAIRCUT/100 ELSE 0 END),
[APPROVED_SEC_NONCASH_REC_TM] = SUM(CASE WHEN SEC_TYPE <> 'CASH' AND APPFLAG = 'Y' THEN QTY*CL_RATE-QTY*CL_RATE*HAIRCUT/100 ELSE 0 END),
[NONAPPROVED_SEC_REC_TM] = SUM(CASE WHEN SEC_TYPE <> 'CASH' AND APPFLAG <> 'Y' THEN QTY*CL_RATE-QTY*CL_RATE*HAIRCUT/100 ELSE 0 END)
INTO #MTFHOLD_SUM
FROM #MTFHOLD M
GROUP BY PARTY_CODE

