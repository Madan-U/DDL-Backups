SELECT TRANSDATE,EXCHANGE,SEGMENT,ACCOUNT_TYPE,ALLOC_LEVEL,  
SUM(CONVERT(NUMERIC(18,2),FDAMOUNT)) AS FDAMOUNT, SUM(CONVERT(NUMERIC(18,2),CASHAMOUNT)) AS CASHAMOUNT,
SUM(CONVERT(NUMERIC(18,2),BGAMOUNT)) AS BGAMOUNT,   
SUM(CONVERT(NUMERIC(18,2),FDAMOUNT+CASHAMOUNT+BGAMOUNT)) AS TOTAL
INTO #TBL_CMCC_DATA_P
FROM TBL_CMCC_DATA A WITH (NOLOCK)  WHERE TRANSDATE = @TRANSDATE AND ACCOUNT_TYPE = 'P' AND ALLOC_LEVEL = 'CC'  
GROUP BY TRANSDATE,EXCHANGE,SEGMENT,ACCOUNT_TYPE,ALLOC_LEVEL  
ORDER BY SEGMENT ASC,EXCHANGE DESC 



ALTER TABLE #TBL_CMCC_DATA_P ADD PROPFUND NUMERIC(18,2),PROCASH NUMERIC(18,2),MARGIN_SHORTFALL NUMERIC(18,2),REPLEDGEBLOCK NUMERIC(18,2)
,PROFUNDSHORT NUMERIC(18,2),TOTAL_PRO_REQ NUMERIC(18,2),TOTAL_FINAL NUMERIC(18,2),REMARKS VARCHAR(50),MAR_UTI NUMERIC(18,2)

UPDATE #TBL_CMCC_DATA_P SET PROPFUND = ISNULL(DIFFBALANCE,0), TOTAL = B.TOTAL + ISNULL(DIFFBALANCE,0)
FROM #TBL_CMCC_DATA A,#TBL_CMCC_DATA_P  B WHERE A.TRANSDATE = B.TRANSDATE AND A.EXCHANGE = B.EXCHANGE AND A.SEGMENT = B.SEGMENT


SELECT PROCESS_DT,EXCHANGE,SEGMENT,ACCOUNT_TYPE,SUM(PROCASH) AS PROCASH,SUM(ABS(MARGIN_SHORT_EXCESS)) AS MARGIN_SHORTFALL
,SUM(BLOCKFUND) AS REPLEDGEBLOCK,SUM(TOTALMARGIN) AS PROFUNDSHORT
INTO #TBL_CLIENT_COLL_ALLOCATE_P
FROM  TBL_CLIENT_COLL_ALLOCATE B  WITH (NOLOCK) WHERE PROCESS_DT = @TRANSDATE
AND  ACCOUNT_TYPE = 'C' GROUP BY PROCESS_DT,EXCHANGE,SEGMENT,ACCOUNT_TYPE


UPDATE #TBL_CMCC_DATA_P SET  PROCASH = B.PROCASH,MARGIN_SHORTFALL = B.MARGIN_SHORTFALL , 
REPLEDGEBLOCK = B.REPLEDGEBLOCK,PROFUNDSHORT = B.PROFUNDSHORT,TOTAL_PRO_REQ = B.PROCASH + B.MARGIN_SHORTFALL + B.REPLEDGEBLOCK + B.PROFUNDSHORT,
TOTAL_FINAL = TOTAL - ( B.PROCASH + B.MARGIN_SHORTFALL + B.REPLEDGEBLOCK + B.PROFUNDSHORT),
REMARKS = CASE WHEN TOTAL - ( B.PROCASH + B.MARGIN_SHORTFALL + B.REPLEDGEBLOCK + B.PROFUNDSHORT) < 0 THEN 'SHORTFALL' 
		 ELSE 'EXCESS' END,
MAR_UTI = CASE WHEN (B.PROCASH + B.MARGIN_SHORTFALL + B.REPLEDGEBLOCK + B.PROFUNDSHORT) /TOTAL * 100 > 100 THEN 100 ELSE
			(B.PROCASH + B.MARGIN_SHORTFALL + B.REPLEDGEBLOCK + B.PROFUNDSHORT) /TOTAL * 100 END 
FROM #TBL_CMCC_DATA_P A , #TBL_CLIENT_COLL_ALLOCATE_P B
WHERE   A.TRANSDATE = B.PROCESS_DT AND A.EXCHANGE = B.EXCHANGE AND A.SEGMENT = B.SEGMENT



