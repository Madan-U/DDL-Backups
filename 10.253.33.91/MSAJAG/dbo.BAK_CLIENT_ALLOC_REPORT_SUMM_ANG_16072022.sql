CREATE TABLE #FILEDATA (SRNO INT,TRANSDATE VARCHAR(50),EXCHANGE VARCHAR(50),SEGMENT VARCHAR(50),ACCOUNT_TYPE VARCHAR(50),ALLOC_LEVEL VARCHAR(50)
,FDAMOUNT VARCHAR(50),CASHAMOUNT VARCHAR(50),BGAMOUNT VARCHAR(50),TOTAL VARCHAR(50),CLIENTCOLL VARCHAR(50),DIFFBALANCE VARCHAR(50),BALANCECASH VARCHAR(50)
,SEGDR VARCHAR(50),SEGCR VARCHAR(50),SEG_RET_TM VARCHAR(50),SEG_PLACED_CM VARCHAR(50),SEG_PLACED_CC VARCHAR(50),MAR_UTI VARCHAR(50))

INSERT INTO #FILEDATA SELECT 1,'''',' ',' ',' ',' ',' ',' ',' ','<b>CLIENT LEVEL ALLOCATION<b/>',' ',' ',' ',' ',' ',' ',' ',' ',' '

INSERT INTO #FILEDATA SELECT 2,'TRANSDATE','EXCHANGE','SEGMENT','ACCOUNT_TYPE','ALLOC_LEVEL','FDAMOUNT','CASHAMOUNT','BGAMOUNT','TOTAL','CLIENTCASH','DIFFBALANCE','BALANCECASH','SEGDR','SEGCR','SEG_RET_TM','SEG_PLACED_CM','SEG_PLACED_CC',' ' 

SELECT TRANSDATE,EXCHANGE,SEGMENT,ACCOUNT_TYPE,ALLOC_LEVEL,
FDAMOUNT = SUM(CONVERT(NUMERIC(18,2),FDAMOUNT))   ,
SUM(CONVERT(NUMERIC(18,2),CASHAMOUNT)) AS CASHAMOUNT, 
SUM(CONVERT(NUMERIC(18,2),BGAMOUNT)) AS BGAMOUNT ,
SUM(CONVERT(NUMERIC(18,2),FDAMOUNT+CASHAMOUNT+BGAMOUNT)) AS TOTAL INTO #TBL_CMCC_DATA
FROM TBL_CMCC_DATA  WITH (NOLOCK) WHERE TRANSDATE = @TRANSDATE
AND   ALLOC_LEVEL = 'CC' AND ACCOUNT_TYPE = 'C'
GROUP BY TRANSDATE,EXCHANGE,SEGMENT,ACCOUNT_TYPE,ALLOC_LEVEL 


ALTER TABLE #TBL_CMCC_DATA ADD CLIENTCOLL NUMERIC(18,2),DIFFBALANCE NUMERIC(18,2),BALANCECASH NUMERIC(18,2),SEGDR NUMERIC(18,2),SEGCR NUMERIC(18,2),
SEG_RET_TM NUMERIC(18,2),SEG_PLACED_CM NUMERIC(18,2),SEG_PLACED_CC NUMERIC(18,2)

SELECT PROCESS_DT,EXCHANGE,SEGMENT,ACCOUNT_TYPE,SUM(CC_CM_CASH) AS CLIENTCOLL,SUM(BALANCECASH) AS BALANCECASH 
INTO #TBL_CLIENT_COLL_ALLOCATE
FROM  TBL_CLIENT_COLL_ALLOCATE B  WITH (NOLOCK) WHERE PROCESS_DT = @TRANSDATE
AND  ACCOUNT_TYPE = 'C' GROUP BY PROCESS_DT,EXCHANGE,SEGMENT,ACCOUNT_TYPE

UPDATE #TBL_CMCC_DATA SET  CLIENTCOLL = B.CLIENTCOLL,DIFFBALANCE = TOTAL -  B.CLIENTCOLL,BALANCECASH = B.BALANCECASH
FROM #TBL_CMCC_DATA A , #TBL_CLIENT_COLL_ALLOCATE B
WHERE  A.TRANSDATE = @TRANSDATE
AND  A.ACCOUNT_TYPE = B.ACCOUNT_TYPE -- AND A.ALLOC_LEVEL = 'CC' AND  B.ACCOUNT_TYPE = 'C'
AND A.TRANSDATE = B.PROCESS_DT AND A.EXCHANGE = B.EXCHANGE AND A.SEGMENT = B.SEGMENT


SELECT PROCESS_DT,EXCHANGE,SEGMENT,ACCOUNT_TYPE,SEGMENT_INDICATOR,DRAMOUNT = SUM(CASE WHEN EDT_LEDGER < 0 THEN EDT_LEDGER ELSE 0 END),
CRAMOUNT = SUM(CASE WHEN EDT_LEDGER >= 0 THEN EDT_LEDGER ELSE 0 END),
SUM(CASH_RET_TM+BG_RET_TM+FD_RET_TM) AS SEG_RET_TM,
SUM(CASH_WCM+BG_WCM+FD_WCM) AS SEG_PLACED_CM,
SUM(CASH_WNCL+BG_WNCL+FD_WNCL) AS SEG_PLACED_CC
INTO #CLIENT_COLLATERAL_REPORTING_DETAILS
FROM CLIENT_COLLATERAL_REPORTING_DETAILS WITH (NOLOCK) 
WHERE PROCESS_DT = @TRANSDATE
AND ACCOUNT_TYPE = 'C'
GROUP BY PROCESS_DT,EXCHANGE,SEGMENT,ACCOUNT_TYPE,SEGMENT_INDICATOR


UPDATE #TBL_CMCC_DATA SET  SEGDR = DRAMOUNT,SEGCR = CRAMOUNT,SEG_RET_TM = B.SEG_RET_TM,
SEG_PLACED_CM = B.SEG_PLACED_CM,SEG_PLACED_CC = B.SEG_PLACED_CC
FROM #TBL_CMCC_DATA A , #CLIENT_COLLATERAL_REPORTING_DETAILS B
WHERE  A.ACCOUNT_TYPE = B.ACCOUNT_TYPE -- AND A.ALLOC_LEVEL = 'CC' AND  B.ACCOUNT_TYPE = 'C'
AND A.TRANSDATE = B.PROCESS_DT AND A.EXCHANGE = B.EXCHANGE AND A.SEGMENT = B.SEGMENT

