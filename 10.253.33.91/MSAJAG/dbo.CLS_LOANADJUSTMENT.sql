CREATE PROC CLS_LOANADJUSTMENT
(
@FROMCLIENT VARCHAR(10),
@TOCLIENT VARCHAR(10)
)
AS
--EXEC CLS_LOANADJUSTMENT '0','zzzzz'
BEGIN


IF @TOCLIENT = ''
BEGIN
	SET @TOCLIENT = 'ZZZZZZZZZZ'
END

SELECT 
[From Sett]=D.OSETT_NO + '-' + D.OSETT_TYPE,
[Borrower Party]=D.FROMPARTY,
[Borrower Name]=C1.LONG_NAME,
--C1.LONG_NAME,
[Scrip Code]=D.SCRIP_CD,
Series=D.SERIES,
TOSETT_NO=ISNULL(B.SETT_NO,'') + '-' + B.SETT_TYPE,
--SETT_TYPE=ISNULL(B.SETT_TYPE,''),
--D.OSETT_NO, 
--D.OSETT_TYPE,
LenderParty=D.TOPARTY,
LenderName=C11.LONG_NAME,
LOANQTY=D.Qty,
--RecQty=D.QTY,
RECQTY = ISNULL(B.QTY,0),
BALQTY=D.QTY-ISNULL(B.QTY,0), 
REMARK=(CASE WHEN D.LOANFLAG= 'LG' THEN 'LOAN' ELSE 'ADJ' END)
--D.LOANFLAG,
--TONAME=C11.LONG_NAME 
FROM CLIENT1 C1, CLIENT2 C2, CLIENT1 C11, CLIENT2 C22, DELLOAN D 
LEFT OUTER JOIN 
DELLOAN B 
ON (D.FROMPARTY = B.FROMPARTY AND D.TOPARTY = B.TOPARTY AND D.SCRIP_CD = B.SCRIP_CD AND D.SERIES = B.SERIES AND B.LOANFLAG LIKE '%R') 
WHERE C1.CL_CODE = C2.CL_CODE 
AND C2.PARTY_CODE = D.FROMPARTY 
AND C11.CL_CODE = C22.CL_CODE 
AND D.LOANFLAG LIKE '%G' 
AND C22.PARTY_CODE = D.TOPARTY 
AND C2.PARTY_CODE >= @FROMCLIENT 
AND C2.PARTY_CODE <= @TOCLIENT 
ORDER BY D.FROMPARTY,D.SCRIP_CD,D.OSETT_NO

END

GO
