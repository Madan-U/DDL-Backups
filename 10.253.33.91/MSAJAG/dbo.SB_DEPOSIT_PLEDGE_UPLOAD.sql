CREATE PROC [dbo].[SB_DEPOSIT_PLEDGE_UPLOAD] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @RECCNT	INT

TRUNCATE TABLE SB_DEPOSIT_PLEDGE_CLIENTS

INSERT INTO SB_DEPOSIT_PLEDGE_CLIENTS
SELECT .DBO.PIECE(FILE_DATA,',',1) AS PARTY_CODE,.DBO.PIECE(FILE_DATA,',',2) AS ISIN,.DBO.PIECE(FILE_DATA,',',3) AS QTY,.DBO.PIECE(FILE_DATA,',',4) AS BOID
FROM FILEDATA 
WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) <> 'PARTY_CODE'

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM SB_DEPOSIT_PLEDGE_CLIENTS

DELETE FROM FILEDATA WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
