CREATE  PROC [dbo].[CLIENTWISEDATEWISE_PLDDETAILS_SEBI]          

(@PROCESSDATE    DATETIME)

AS          



DECLARE @PREVDATE DATETIME



SELECT @PREVDATE = CONVERT(VARCHAR(11),MAX(RMS_DATE),120) FROM [CSOKYC-6].PLEDGE.DBO.TBL_PARTY_PLEDGED_BANKWISE_DATA

WHERE RMS_DATE <@PROCESSDATE +' 23:59'







SELECT /* ROW_NUMBER() OVER(ORDER BY A.PROCESSDATE ASC) SRNO ,*/PROCESSDATE=@PROCESSDATE ,UPPER(RTRIM(LTRIM(PARTY_CODE))) AS PARTY_CODE,ISIN,  

QTY,PLEDGE_QTY,ACCNO FROM           

(SELECT PARTY_CODE,ISIN,SUM(QTY) AS QTY ,SUM(PLEDGE_QTY)PLEDGE_QTY ,AVG_CLOSING,SUM(PLEDGE_VALUE)/2 AS PLEDGE_VALUE ,BANK_CODE,LEDGER_BAL,ACCNO

FROM 

 [CSOKYC-6].PLEDGE.DBO.TBL_PARTY_PLEDGED_BANKWISE_DATA A WITH(NOLOCK)  

 WHERE RMS_DATE > = @PREVDATE  AND RMS_DATE <= @PREVDATE + ' 23:59' --AND PARTY_CODE ='JOD17256'

 GROUP BY PROCESSDATE,PARTY_CODE,ISIN,AVG_CLOSING,BANK_CODE,LEDGER_BAL,ACCNO

  ) A         

INNER JOIN [CSOKYC-6].PLEDGE.DBO.TBL_LAS_BANK_MASTER_PLEDGE  B  WITH(NOLOCK) ON A.BANK_CODE = B.BANK_CODE         

LEFT JOIN [CSOKYC-6].PLEDGE.DBO.TBL_PLEDGEUNPLEDGEBORROWING C  WITH(NOLOCK) ON B.BANK_CODE = C.BANK_CODE       

ORDER BY PARTY_CODE,ACCNO,ISIN

GO
