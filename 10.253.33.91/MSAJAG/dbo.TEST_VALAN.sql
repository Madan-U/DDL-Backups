-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC TEST_VALAN
AS
DECLARE @SETT_NO	VARCHAR(7),
		@SETT_TYPE	VARCHAR(2),
		@SETTCUR	CURSOR

SET @SETTCUR = CURSOR FOR
	SELECT M.SETT_NO, M.SETT_TYPE FROM SETT_MST M, (
			SELECT TOP 10 START_DATE FROM SETT_MST
			WHERE START_DATE <= LEFT(GETDATE(),11)
			AND SETT_TYPE = 'N'
			ORDER BY START_DATE DESC
	) A
	WHERE M.START_DATE = A.START_DATE
	AND SETT_TYPE IN ('N', 'W', 'A', 'X')
	ORDER BY SETT_NO, SETT_TYPE
OPEN @SETTCUR
FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE
WHILE @@FETCH_STATUS = 0
BEGIN
	EXEC PROC_NSEVALAN_DETAIL @SETT_NO, @SETT_TYPE
	FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE
END
CLOSE @SETTCUR
DEALLOCATE @SETTCUR

GO
