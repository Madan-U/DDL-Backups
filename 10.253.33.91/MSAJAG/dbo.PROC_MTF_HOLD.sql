-- Object: PROCEDURE dbo.PROC_MTF_HOLD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[PROC_MTF_HOLD]
AS

DROP TABLE MTF_DELDATA

	CREATE TABLE MTF_DELDATA
	(
		SNO			NUMERIC(18,0)	IDENTITY(1,1),
		ORGSNO		NUMERIC(18,0),
		TRTYPE		INT,
		PARTY_CODE	VARCHAR(10),
		ISIN		VARCHAR(12),
		QTY			INT,
		HOLDFLAG	VARCHAR(50),
		RECTYPE		VARCHAR(10),
		BDPTYPE		VARCHAR(4),
		BDPID		VARCHAR(8),
		BCLTDPID	VARCHAR(16)
	)

 insert into MTF_DELDATA
 SELECT ORGSNO = D.SNO, TRTYPE, PARTY_CODE, ISIN = CERTNO, QTY,         
HOLDFLAG = (CASE WHEN SEGMENT = 'FUTURES' THEN 'FOCOLL'         WHEN SEGMENT = 'CAPITAL' 
AND ACCOUNTTYPE = 'MAR' THEN 'CMCOLL'         ELSE 'BEN'          END),       RECTYPE = 'NSE', 
BDPTYPE, BDPID, BCLTDPID    FROM  MSAJAG.DBO.DELTRANS D WITH(NOLOCK), 
 MSAJAG.DBO.DELIVERYDP DP WITH(NOLOCK)   WHERE DRCR = 'D' AND FILLER2 = 1 
AND DELIVERED = '0'   AND   EXISTS (SELECT PARTY_CODE FROM MTFTRADE.DBO.TBLCLIENTMARGIN P        
 WHERE P.PARTY_CODE = D.PARTY_CODE  )  
 AND   D.BDPID = DP.DPID   AND   D.BCLTDPID = DP.DPCLTNO   AND 
  DP.EXCHANGE NOT IN ('MCX','NCX','MTF')      AND TRTYPE = 904   AND   DP.DESCRIPTION NOT LIKE '%POOL%' 
  AND   DP.DESCRIPTION NOT LIKE '%PRINC%'   AND   DP.DESCRIPTION NOT LIKE '%CUSA%'  


 insert into MTF_DELDATA

 SELECT ORGSNO = D.SNO, TRTYPE, PARTY_CODE, ISIN = CERTNO, QTY,         
HOLDFLAG = (CASE WHEN SEGMENT = 'FUTURES' THEN 'FOCOLL'         WHEN SEGMENT = 'CAPITAL' 
AND ACCOUNTTYPE = 'MAR' THEN 'CMCOLL'         ELSE 'BEN'          END),       RECTYPE = 'BSE', 
BDPTYPE, BDPID, BCLTDPID    FROM  BSEDB.DBO.DELTRANS D WITH(NOLOCK), 
 BSEDB.DBO.DELIVERYDP DP WITH(NOLOCK)   WHERE DRCR = 'D' AND FILLER2 = 1 
AND DELIVERED = '0'   AND   EXISTS (SELECT PARTY_CODE FROM MTFTRADE.DBO.TBLCLIENTMARGIN P        
 WHERE P.PARTY_CODE = D.PARTY_CODE  )  
 AND   D.BDPID = DP.DPID   AND   D.BCLTDPID = DP.DPCLTNO   AND 
  DP.EXCHANGE NOT IN ('MCX','NCX','MTF')      AND TRTYPE = 904   AND   DP.DESCRIPTION NOT LIKE '%POOL%' 
  AND   DP.DESCRIPTION NOT LIKE '%PRINC%' AND   DP.DESCRIPTION NOT LIKE '%CUSA%'

GO
