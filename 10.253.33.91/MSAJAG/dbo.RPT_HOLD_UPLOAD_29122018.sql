CREATE	PROC [dbo].[RPT_HOLD_UPLOAD]
		(
		@FROMDATE	VARCHAR(11),
		@FROMPARTY	VARCHAR(20) = '',
		@TOPARTY	VARCHAR(20) = 'zzzzzzzzzz'
		)

		AS

		/*
		EXEC	RPT_HOLD_UPLOAD 'DEC 26 2018','0','ZZZZZ'
		*/
		
		/*
		IF (SELECT CASE WHEN @FROMDATE = LEFT(GETDATE(),11) THEN 1 ELSE 0 END) = 0 
		BEGIN
			SELECT * FROM OWNER WHERE 1 = 2 
			RETURN
		END
		*/
		
		IF @TOPARTY = ''
		BEGIN
			SET @TOPARTY = 'zzzzzzzzzz'
		END

		DECLARE @FILENAME   VARCHAR(50)
		DECLARE @BATCHNO	INT  
		DECLARE @MEMBERCODE VARCHAR(20)
		DECLARE @MEMBERNAME	VARCHAR(500)

		SELECT @MEMBERCODE = MEMBERCODE, @MEMBERNAME = ISNULL(LTRIM(RTRIM(COMPANY)),'') FROM OWNER (NOLOCK)
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
		WHERE FLDFILENO = 'HS'  
		AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)

		IF @BATCHNO = 1  
		BEGIN  
			INSERT INTO ASIANFILEBATCHNO VALUES ('HS', @BATCHNO, GETDATE(), GETDATE())  
		END  
		ELSE  
		BEGIN  
			UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'HS' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		END  
		  
		SET	@FILENAME = @MEMBERCODE + '_HS_' + REPLACE(CONVERT(VARCHAR,GETDATE(),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) 
		
		
		CREATE TABLE #HOLD
		(
		MEMBER_DEMAT_ACC	VARCHAR(50),
		MEMBER_ACC_TYPE		VARCHAR(50),
		UCC_CODE			VARCHAR(20),
		CLIENT_NAME			VARCHAR(500),
		CLIENT_PAN			VARCHAR(50),
		ISIN				VARCHAR(100),
		SCRIP_NAME			VARCHAR(500),
		SECURITY_TYPE		VARCHAR(10),
		PLEDGED_BAL_QTY		NUMERIC(18,3),
		FREE_BAL_QTY		NUMERIC(18,3),
		TOTAL_QTY			NUMERIC(18,3)
		)
		
		CREATE CLUSTERED INDEX [DELHOLD] ON [DBO].[#HOLD] (
			[UCC_CODE] ASC,
			[SCRIP_NAME],
			[ISIN],
			[SECURITY_TYPE]			
			)
		
		INSERT	INTO #HOLD		
		SELECT 	MEMBER_DEMAT_ACC = (
				CASE
					WHEN LEN(BCLTDPID) >= 16 THEN BCLTDPID
					ELSE BDPID + BCLTDPID
				END),
				MEMBER_ACC_TYPE = D.MEMBER_ACC_TYPE,
				UCC_CODE = PARTY_CODE,
				CLIENT_NAME = ISNULL(C1.LONG_NAME,''),
				CLIENT_PAN = ISNULL(C1.PAN_GIR_NO,''),
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				SECURITY_TYPE = SERIES,
				PLEDGED_BAL_QTY = SUM(PLEDGED_BAL_QTY),
				FREE_BAL_QTY = SUM(FREE_BAL_QTY),
				TOTAL_QTY = SUM(DRQTY-CRQTY)
		FROM	TBL_DEL_HOLD_SEC D (NOLOCK),
				CLIENT1 C1 (NOLOCK)
		WHERE	D.PARTY_CODE = C1.CL_CODE
		GROUP BY
				CASE
					WHEN LEN(BCLTDPID) >= 16 THEN BCLTDPID
					ELSE BDPID + BCLTDPID
				END,
				D.MEMBER_ACC_TYPE,
				PARTY_CODE,
				CERTNO,
				SCRIP_CD,
				SERIES,
				ISNULL(C1.LONG_NAME,''),
				ISNULL(C1.PAN_GIR_NO,'')


		INSERT	INTO #HOLD		
		SELECT 	MEMBER_DEMAT_ACC = (
				CASE
					WHEN LEN(BCLTDPID) >= 16 THEN BCLTDPID
					ELSE BDPID + BCLTDPID
				END),
				MEMBER_ACC_TYPE = D.MEMBER_ACC_TYPE,
				UCC_CODE = PARTY_CODE,
				CLIENT_NAME = ISNULL(C1.LONG_NAME,''),
				CLIENT_PAN = ISNULL(C1.PAN_GIR_NO,''),
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				SECURITY_TYPE = SERIES,
				PLEDGED_BAL_QTY = SUM(PLEDGED_BAL_QTY),
				FREE_BAL_QTY = SUM(FREE_BAL_QTY),
				TOTAL_QTY = SUM(DRQTY-CRQTY)
		FROM	BSEDB.DBO.TBL_DEL_HOLD_SEC D (NOLOCK),
				CLIENT1 C1 (NOLOCK)
		WHERE	D.PARTY_CODE = C1.CL_CODE
		GROUP BY
				CASE
					WHEN LEN(BCLTDPID) >= 16 THEN BCLTDPID
					ELSE BDPID + BCLTDPID
				END,
				D.MEMBER_ACC_TYPE,
				PARTY_CODE,
				CERTNO,
				SCRIP_CD,
				SERIES,
				ISNULL(C1.LONG_NAME,''),
				ISNULL(C1.PAN_GIR_NO,'')


		UPDATE	#HOLD
		SET		SECURITY_TYPE = M.SERIES
		FROM	MULTIISIN M (NOLOCK)
		WHERE	M.ISIN = #HOLD.ISIN
				AND VALID = 1


		UPDATE	#HOLD
		SET		SECURITY_TYPE = (
				CASE
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),2) = 'MF' OR LEFT(ISNULL(ISIN,''),3) = 'INF' THEN 'MF'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'N' THEN 'BOND'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'G' THEN 'DEBT'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'P' THEN 'PREF'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'W' THEN 'WT'
					---WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'I' THEN 'IDR'
					ELSE 'EQ'
				END)


		SELECT	FILENAME = @FILENAME,
				[Members Demat Account No.] = MEMBER_DEMAT_ACC,
				[Member Account Type] = MEMBER_ACC_TYPE,
				[Unique Client Code (UCC)] = UCC_CODE,
				[Client Name] = CLIENT_NAME,
				[Client PAN] = CLIENT_PAN,
				[ISIN Code] = ISIN,
				[Scrip Name] = SCRIP_NAME,
				[Security Type] = SECURITY_TYPE,
				[Pledged Balance (Qty.)] = SUM(PLEDGED_BAL_QTY),
				[Free Balance (Qty.)] = ABS(SUM(FREE_BAL_QTY)),
				[Total (Qty.)] = ABS(SUM(TOTAL_QTY))
		FROM	#HOLD				
		GROUP BY
				MEMBER_DEMAT_ACC,
				MEMBER_ACC_TYPE,
				UCC_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				ISIN,
				SCRIP_NAME,
				SECURITY_TYPE
		ORDER BY
				UCC_CODE,
				SCRIP_NAME,
				ISIN
				
		DROP TABLE #HOLD

GO
