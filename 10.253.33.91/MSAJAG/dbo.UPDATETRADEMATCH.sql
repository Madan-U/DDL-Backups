-- Object: PROCEDURE dbo.UPDATETRADEMATCH
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[UPDATETRADEMATCH] AS      

DECLARE @SAUDA_DATE	VARCHAR(11)

SELECT TOP 1 @SAUDA_DATE = LEFT(SAUDA_DATE,11) FROM TRADE_MATCH_IMP

SELECT ORDER_NO, TRADE_NO, SCRIP_CD, SERIES, SELL_BUY
INTO #TRADE_MATCH FROM TRADE_MATCH
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'

DELETE FROM TRADE_MATCH_IMP
WHERE ORDER_NO IN 
	(SELECT ORDER_NO FROM #TRADE_MATCH
	 WHERE #TRADE_MATCH.ORDER_NO = TRADE_MATCH_IMP.ORDER_NO
	 AND   #TRADE_MATCH.TRADE_NO = TRADE_MATCH_IMP.TRADE_NO
	 AND   #TRADE_MATCH.SCRIP_CD = TRADE_MATCH_IMP.SCRIP_CD
	 AND   #TRADE_MATCH.SERIES = TRADE_MATCH_IMP.SERIES
	 AND   #TRADE_MATCH.SELL_BUY = TRADE_MATCH_IMP.SELL_BUY)

INSERT INTO TRADE_MATCH      
SELECT Trade_No,Status,Scrip_Cd,Series,Scripname,Instrument,Booktype,Markettype,User_Id,Branch_Id,Sell_Buy,
       Tradeqty,Marketrate,Pro_Cli,Party_Code,Partipantcode,Auctionpart,Auctionno,Settno,Sauda_Date,
	   Trademodified,Order_No,Cpid,OrderTime,CTCLID='' 
FROM TRADE_MATCH_IMP

GO
