CREATE PROC ODIN_BLOCKFILE       
(        
 @TRADEDATE VARCHAR(11)        
)        
AS        
-- EXEC ODIN_BLOCKFILE 'FEB 26 2013'        
        
SELECT DISTINCT EXCHANGE, PARTY_CODE,   
SCRIP_CD = (CASE WHEN EXCHANGE = 'NSE' THEN SCRIP_CD ELSE '' END),   
SERIES = (CASE WHEN EXCHANGE = 'NSE' THEN SERIES ELSE '' END),   
BSECODE = (CASE WHEN EXCHANGE = 'BSE' THEN SERIES ELSE '' END),  
GROUPID=CONVERT(VARCHAR(10),''),      
CATEGORY = 0, SERVERNAME = CONVERT(VARCHAR(50),''),  
ISIN = CONVERT(VARCHAR(12), '')  
INTO #RMS        
FROM TBL_RMS_SALE        
WHERE PROCESS_DATE = @TRADEDATE --AND EXCHANGE = 'BSE'        
and party_code in (Select PCODE from V_USERLIST      
         WHERE CATEGORY in (2,4))      
  
UPDATE #RMS SET ISIN = M.ISIN  
FROM ANGELDEMAT.MSAJAG.DBO.MULTIISIN M  
WHERE EXCHANGE = 'NSE' AND M.SCRIP_CD = #RMS.SCRIP_CD  
AND M.SERIES IN ('EQ', 'BE')  
AND #RMS.SERIES IN ('EQ', 'BE')  
AND VALID = 1   
  
UPDATE #RMS SET ISIN = M.ISIN  
FROM ANGELDEMAT.MSAJAG.DBO.MULTIISIN M  
WHERE EXCHANGE = 'NSE' AND M.SCRIP_CD = #RMS.SCRIP_CD  
AND M.SERIES = #RMS.SERIES  
AND #RMS.SERIES IN ('EQ', 'BE')  
AND VALID = 1 AND #RMS.ISIN = ''  
  
UPDATE #RMS SET ISIN = M.ISIN  
FROM ANGELDEMAT.BSEDB.DBO.MULTIISIN M  
WHERE EXCHANGE = 'BSE' AND M.SCRIP_CD = #RMS.BSECODE  
AND VALID = 1 AND #RMS.ISIN = ''  
  
UPDATE #RMS SET BSECODE = M.SCRIP_CD  
FROM ANGELDEMAT.BSEDB.DBO.MULTIISIN M  
WHERE EXCHANGE = 'NSE' AND M.ISIN = #RMS.ISIN  
AND VALID = 1 AND #RMS.BSECODE = ''  
  
UPDATE #RMS SET SCRIP_CD = M.SCRIP_CD, SERIES = M.SERIES  
FROM ANGELDEMAT.MSAJAG.DBO.MULTIISIN M  
WHERE EXCHANGE = 'BSE' AND M.ISIN = #RMS.ISIN  
AND VALID = 1 AND #RMS.SCRIP_CD = ''  
  
UPDATE #RMS SET GROUPID = V.GROUPID, CATEGORY = v.CATEGORY, SERVERNAME = SERVERMAPPED         
FROM V_USERLIST V        
WHERE PARTY_CODE = PCODE        
        
SELECT FILENAME='SQ_Suspend_' + SERVERNAME + '.txt', FILLER='1',PARTY_CODE, GROUPID,        
DEPOSIT = 0, SCRIPCODE = BSECODE, SYMBOL = '', SERIES = '',        
GROUPCD = '', PERIODICITY=6,INSTRU_NAME='',EXPIRY='',STRIKEPRICE='',OPTIONTYPE='',        
GROSS_EXP=-1,GROSSEXP_LIM=-1,NETEXP_MULTI=-1,NET_EXP=-1,NETSALE_MULTI=-1,        
NETSALE_LIM=-1,NETPOS_MULTI=-1,NETPOS_LIM=0,TURN_MULTI=-1,TURNOVER_LIM=-1,        
PEND_ORD_MULTI=0,PEND_ORD_LIM=-1,MTM_MULTI=-1,MTM_LIM=-1,INIT_MAR_MULTI=-1,        
INIT_LIM=0,NQ_LIM=-1, MAX_SING_LIM=1,MAX_SING_QTY_LIM=1,MIN_ORD_VAL='0.01',MIN_ORD_QTY=1,        
RET_MULTI=0,INC_MTMP=0,INC_NET_PRE=-1,GROSS_NET=(CASE WHEN CATEGORY = 2 THEN -1 ELSE 1 END),      
INS_EXP_MAR=-1,FILLER1='^'        
FROM #RMS        
WHERE BSECODE <> ''  
UNION        
SELECT FILENAME='SQ_Suspend_' + SERVERNAME + '.txt', FILLER='2',PARTY_CODE, GROUPID,        
DEPOSIT = 0, SCRIPCODE = BSECODE, SYMBOL = '', SERIES = '',        
GROUPCD = '', PERIODICITY=6,INSTRU_NAME='',EXPIRY='',STRIKEPRICE='',OPTIONTYPE='',        
GROSS_EXP=-1,GROSSEXP_LIM=-1,NETEXP_MULTI=-1,NET_EXP=-1,NETSALE_MULTI=-1,        
NETSALE_LIM=-1,NETPOS_MULTI=-1,NETPOS_LIM=0,TURN_MULTI=-1,TURNOVER_LIM=-1,        
PEND_ORD_MULTI=0,PEND_ORD_LIM=-1,MTM_MULTI=-1,MTM_LIM=-1,INIT_MAR_MULTI=-1,        
INIT_LIM=0,NQ_LIM=-1, MAX_SING_LIM=1,MAX_SING_QTY_LIM=1,MIN_ORD_VAL='0.01',MIN_ORD_QTY=1,        
RET_MULTI=0,INC_MTMP=0,INC_NET_PRE=-1,GROSS_NET=(CASE WHEN CATEGORY = 2 THEN -1 ELSE 1 END),      
INS_EXP_MAR=-1,FILLER1='^'        
FROM #RMS       
WHERE BSECODE <> ''  
UNION      
SELECT FILENAME='SQ_Suspend_' + SERVERNAME + '.txt', FILLER='1',PARTY_CODE, GROUPID,        
DEPOSIT = 0, SCRIPCODE = '', SYMBOL = SCRIP_CD, SERIES = SERIES,        
GROUPCD = '', PERIODICITY=1,INSTRU_NAME='',EXPIRY='',STRIKEPRICE='',OPTIONTYPE='',        
GROSS_EXP=-1,GROSSEXP_LIM=-1,NETEXP_MULTI=-1,NET_EXP=-1,NETSALE_MULTI=-1,        
NETSALE_LIM=-1,NETPOS_MULTI=-1,NETPOS_LIM=0,TURN_MULTI=-1,TURNOVER_LIM=-1,        
PEND_ORD_MULTI=0,PEND_ORD_LIM=-1,MTM_MULTI=-1,MTM_LIM=-1,INIT_MAR_MULTI=-1,        
INIT_LIM=0,NQ_LIM=-1, MAX_SING_LIM=1,MAX_SING_QTY_LIM=1,MIN_ORD_VAL='0.01',MIN_ORD_QTY=1,        
RET_MULTI=0,INC_MTMP=0,INC_NET_PRE=-1,GROSS_NET=(CASE WHEN CATEGORY = 2 THEN -1 ELSE 1 END),      
INS_EXP_MAR=-1,FILLER1='^'        
FROM #RMS        
WHERE SCRIP_CD <> ''    
UNION         
SELECT FILENAME='SQ_Suspend_' + SERVERNAME + '.txt', FILLER='1',PARTY_CODE, GROUPID,        
DEPOSIT = 0, SCRIPCODE = '', SYMBOL = SCRIP_CD, SERIES = (CASE WHEN SERIES = 'EQ' THEN 'BE' ELSE 'EQ' END),        
GROUPCD = '', PERIODICITY=1,INSTRU_NAME='',EXPIRY='',STRIKEPRICE='',OPTIONTYPE='',        
GROSS_EXP=-1,GROSSEXP_LIM=-1,NETEXP_MULTI=-1,NET_EXP=-1,NETSALE_MULTI=-1,        
NETSALE_LIM=-1,NETPOS_MULTI=-1,NETPOS_LIM=0,TURN_MULTI=-1,TURNOVER_LIM=-1,        
PEND_ORD_MULTI=0,PEND_ORD_LIM=-1,MTM_MULTI=-1,MTM_LIM=-1,INIT_MAR_MULTI=-1,        
INIT_LIM=0,NQ_LIM=-1, MAX_SING_LIM=1,MAX_SING_QTY_LIM=1,MIN_ORD_VAL='0.01',MIN_ORD_QTY=1,        
RET_MULTI=0,INC_MTMP=0,INC_NET_PRE=-1,GROSS_NET=(CASE WHEN CATEGORY = 2 THEN -1 ELSE 1 END),      
INS_EXP_MAR=-1,FILLER1='^'        
FROM #RMS        
WHERE SCRIP_CD <> ''    
AND SERIES IN ('EQ', 'BE')        
UNION        
SELECT FILENAME='SQ_Suspend_' + SERVERNAME + '.txt', FILLER='2',PARTY_CODE, GROUPID,        
DEPOSIT = 0, SCRIPCODE = '', SYMBOL = SCRIP_CD, SERIES = SERIES,        
GROUPCD = '', PERIODICITY=1,INSTRU_NAME='',EXPIRY='',STRIKEPRICE='',OPTIONTYPE='',        
GROSS_EXP=-1,GROSSEXP_LIM=-1,NETEXP_MULTI=-1,NET_EXP=-1,NETSALE_MULTI=-1,        
NETSALE_LIM=-1,NETPOS_MULTI=-1,NETPOS_LIM=0,TURN_MULTI=-1,TURNOVER_LIM=-1,        
PEND_ORD_MULTI=0,PEND_ORD_LIM=-1,MTM_MULTI=-1,MTM_LIM=-1,INIT_MAR_MULTI=-1,        
INIT_LIM=0,NQ_LIM=-1, MAX_SING_LIM=1,MAX_SING_QTY_LIM=1,MIN_ORD_VAL='0.01',MIN_ORD_QTY=1,        
RET_MULTI=0,INC_MTMP=0,INC_NET_PRE=-1,GROSS_NET=(CASE WHEN CATEGORY = 2 THEN -1 ELSE 1 END),      
INS_EXP_MAR=-1,FILLER1='^'        
FROM #RMS      
WHERE SCRIP_CD <> ''       
UNION         
SELECT FILENAME='SQ_Suspend_' + SERVERNAME + '.txt', FILLER='2',PARTY_CODE, GROUPID,        
DEPOSIT = 0, SCRIPCODE = '', SYMBOL = SCRIP_CD, SERIES = (CASE WHEN SERIES = 'EQ' THEN 'BE' ELSE 'EQ' END),        
GROUPCD = '', PERIODICITY=1,INSTRU_NAME='',EXPIRY='',STRIKEPRICE='',OPTIONTYPE='',        
GROSS_EXP=-1,GROSSEXP_LIM=-1,NETEXP_MULTI=-1,NET_EXP=-1,NETSALE_MULTI=-1,        
NETSALE_LIM=-1,NETPOS_MULTI=-1,NETPOS_LIM=0,TURN_MULTI=-1,TURNOVER_LIM=-1,        
PEND_ORD_MULTI=0,PEND_ORD_LIM=-1,MTM_MULTI=-1,MTM_LIM=-1,INIT_MAR_MULTI=-1,        
INIT_LIM=0,NQ_LIM=-1, MAX_SING_LIM=1,MAX_SING_QTY_LIM=1,MIN_ORD_VAL='0.01',MIN_ORD_QTY=1,        
RET_MULTI=0,INC_MTMP=0,INC_NET_PRE=-1,GROSS_NET=(CASE WHEN CATEGORY = 2 THEN -1 ELSE 1 END),      
INS_EXP_MAR=-1,FILLER1='^'        
FROM #RMS        
WHERE SCRIP_CD <> ''  
AND SERIES IN ('EQ', 'BE')        
ORDER BY 1, 2, 3

GO
