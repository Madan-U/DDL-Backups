-- Object: PROCEDURE dbo.PROC_CD_MARGIN_UPLOAD_ABL
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[PROC_CD_MARGIN_UPLOAD_ABL] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @RECCNT	INT

TRUNCATE TABLE TDAY_MARGIN_DATA_CD 

INSERT INTO TDAY_MARGIN_DATA_CD
SELECT CONVERT(DATETIME,@TDATE,103) CD_MARGIN_DATE,
DBO.PIECE(FILE_DATA,',',1) CM_CODE,
DBO.PIECE(FILE_DATA,',',2) CM_NAME,
DBO.PIECE(FILE_DATA,',',3) TM_CP_CODE,
DBO.PIECE(FILE_DATA,',',4) TM_CP_NAME,
DBO.PIECE(FILE_DATA,',',5) CLIENT_CODE,
DBO.PIECE(FILE_DATA,',',6) CASH,
DBO.PIECE(FILE_DATA,',',7) NON_CASH,
DBO.PIECE(FILE_DATA,',',8) INITIAL_MRG,
DBO.PIECE(FILE_DATA,',',9) CRYSTALLISED_MRG,
DBO.PIECE(FILE_DATA,',',10) DELIVERY_MRG,
DBO.PIECE(FILE_DATA,',',11) ELM_MARG,
DBO.PIECE(FILE_DATA,',',12) TOTAL_MRG,
DBO.PIECE(FILE_DATA,',',13) MTM_PNL,
DBO.PIECE(FILE_DATA,',',14) CLT_MRG_EXCESS_90,
DBO.PIECE(FILE_DATA,',',15) CLT_NON_CASH_EXCESS_90,
DBO.PIECE(FILE_DATA,',',16) ELIGIBLE_NON_CASH 
FROM FILEDATA WHERE PROGID = @PROGID AND DBO.PIECE(FILE_DATA,',',5) NOT IN ('','Client Code')

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=12 AND REPORT_DATE = CONVERT(DATETIME,@TDATE,103)

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA WHERE PROGID = @PROGID

DELETE FROM FILEDATA WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'CD MARGIN UPLOADED WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
