CREATE PROC [dbo].[CLS_UPDATE_PASSWORD]
(
	@PASSWORD VARCHAR(100),
	@USERID VARCHAR(25),
	@DAYCOUNT INT	,
	@OLDPASSWORD VARCHAR(100),
	@VERFYEMAIL VARCHAR(100)=''
)

AS


SET NOCOUNT ON

DECLARE @@FLDOLDPASSWORD TINYINT
DECLARE @@FLDAUTO INT
DECLARE @@PASSAUTO BIGINT
DECLARE @@OLDPASSSTRING VARCHAR(2000)
DECLARE @@OLDPASSCOUNT INT
DECLARE @@NEWPASSSTRING VARCHAR(2000)

DECLARE @@FLAG INT

DECLARE @ERRCODE  INT,
		@ERRMSG VARCHAR(MAX),
		@EMAILFROM VARCHAR(200),
		@EMAILTO VARCHAR(200),
		@EMAILSUBJECT VARCHAR(300),
		@EMAILMESSAGE VARCHAR(MAX),
		@SMTP_SRV_NAME VARCHAR(60),
		@COMPANYNAME VARCHAR(200),
		@PartyCode varchar(30)

SELECT @@FLDOLDPASSWORD = ISNULL(FLDOLDPASSWORD,1) FROM TBLGLOBALPARAMS

IF ISNULL(@@FLDOLDPASSWORD,0) = 0
BEGIN
	SET @@FLDOLDPASSWORD = 1
END

SELECT @@FLDAUTO = ISNULL(FLDAUTO,0),@PartyCode=isnull(fldstname,0) FROM TBLPRADNYAUSERS
WHERE FLDUSERNAME = @USERID
AND FLDPASSWORD = @OLDPASSWORD

IF ISNULL(@@FLDAUTO,0) = 0
BEGIN
	SELECT MSG = 'INVALID USER ID / PASSWORD'
	RETURN
END

if @VERFYEMAIL != ''
BEGIN

	SELECT @@FLAG = count(PARTY_CODE) FROM MSAJAG.DBO.CLIENT_DETAILS
	WHERE PARTY_CODE = @PartyCode
	AND CHARINDEX(';' + @VERFYEMAIL + ';', ';' + EMAIL + ';') > 0
	IF ISNULL(@@FLAG,0) = 0
	BEGIN
		SELECT MSG = 'INVALID EMAIL ID ,PLEASE ENTER VALID EMAIL ID'
		print @@FLAG;
		RETURN
	END
END

SELECT @@OLDPASSSTRING = ISNULL(FLDOLDPASSLISTING,''), @@PASSAUTO =
 ISNULL(FLDAUTO,0)  FROM TBLUSERPASSHIST
WHERE FLDUSERID = @@FLDAUTO

IF ISNULL(@@OLDPASSSTRING,'') = ''
BEGIN
	SET @@OLDPASSSTRING = ''
END

IF ISNULL(@@PASSAUTO,0) = 0
BEGIN
	SET @@PASSAUTO = 0
END


SELECT 
	@@OLDPASSCOUNT = COUNT(1)
FROM 
	CLS_FUN_SPLITSTRING (@@OLDPASSSTRING,'±')
WHERE 
	SPLITTED_VALUE = @PASSWORD
	AND SNO > (
						SELECT 
								ISNULL(ISNULL(MAX(SNO),0) - @@FLDOLDPASSWORD, 0)
						FROM 
							CLS_FUN_SPLITSTRING (@@OLDPASSSTRING,'±')
					)


IF ISNULL(@@OLDPASSCOUNT,0) > 0
BEGIN
	SELECT MSG = 'PASSWORD ALREADY USED DURING LAST ' + CAST(@@FLDOLDPASSWORD AS VARCHAR) + ' ATTEMPTS.  PLEASE CHANGE'
	RETURN
END

SELECT 
	@DAYCOUNT = B.FLDPWDEXPIRY FROM TBLPRADNYAUSERS A , TBLUSERCONTROLMASTER B 
WHERE 
	a.FLDAUTO = @@FLDAUTO 
	AND a.FLDAUTO = FLDUSERID

UPDATE TBLPRADNYAUSERS 
SET 
	FLDPASSWORD = @PASSWORD,
	PWD_EXPIRY_DATE = (GETDATE()+ @DAYCOUNT)
WHERE 
	FLDAUTO = @@FLDAUTO

					
UPDATE TBLUSERCONTROLMASTER 
SET 
	FLDATTEMPTCNT = 0,
	FLDFIRSTLOGIN = 'N'
WHERE 
	TBLUSERCONTROLMASTER.FLDUSERID = @@FLDAUTO 



SET @@NEWPASSSTRING = ''


DECLARE @@STRING VARCHAR(100)

DECLARE VENDOR_CURSOR CURSOR FOR 

SELECT SPLITTED_VALUE
FROM 
	CLS_FUN_SPLITSTRING (@@OLDPASSSTRING,'±')
WHERE  SNO > (
						SELECT 
								ISNULL(ISNULL(MAX(SNO),0) - @@FLDOLDPASSWORD, 0)
						FROM 
							CLS_FUN_SPLITSTRING (@@OLDPASSSTRING,'±')
					)
OPEN VENDOR_CURSOR

FETCH NEXT FROM VENDOR_CURSOR 
INTO @@STRING

WHILE @@FETCH_STATUS = 0
BEGIN
	SET @@NEWPASSSTRING = @@NEWPASSSTRING + @@STRING + '±'
FETCH NEXT FROM VENDOR_CURSOR 
INTO @@STRING
END 

CLOSE VENDOR_CURSOR
DEALLOCATE VENDOR_CURSOR

	SET @@NEWPASSSTRING = @@NEWPASSSTRING + @PASSWORD 

IF ISNULL(@@PASSAUTO,0) = 0
BEGIN
	INSERT INTO TBLUSERPASSHIST 
	(FLDUSERID, FLDOLDPASSLISTING) 
	VALUES
	(@@FLDAUTO, @@NEWPASSSTRING)
END
ELSE
BEGIN
	UPDATE TBLUSERPASSHIST
	SET FLDOLDPASSLISTING = @@NEWPASSSTRING
	WHERE FLDAUTO = @@PASSAUTO
	AND FLDUSERID = @@FLDAUTO 

END


SELECT @EMAILFROM = EMAIL,@SMTP_SRV_NAME = SMTP_SRV_NAME,@COMPANYNAME = COMPANY FROM MSAJAG.DBO.OWNER 

IF ISNULL(@EMAILFROM,'') = '' OR ISNULL(@SMTP_SRV_NAME,'') =''
BEGIN
	--SET @ERRCODE = 1
	SET @ERRMSG = 'PASSWORD UPDATED SUCCESSFULLY. NO EMAIL SEND. PLEASE CONTACT THE ADMINISTRATOR'			
	SELECT  ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @VERFYEMAIL, 
	EMAILSUBJECT = @EMAILSUBJECT, EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME
	RETURN
END

--Send Email only for client login
SET @ERRMSG = 'YOUR PASSWORD SUCCESSFULLY UPDATED'
SET @EMAILSUBJECT = 'AUTO-GENERATED E-MAIL FROM CUSTOMER CARE OF ' + @COMPANYNAME


SET @EMAILMESSAGE = 'DEAR CUSTOMER,<BR><BR>AS PER YOUR REQUEST, WE HAVE UPDATED YOUR PASSWORD.<BR>'

SET @EMAILMESSAGE = @EMAILMESSAGE +  '<BR><BR>REGARDS, <BR><BR>CUSTOMER CARE<BR>'
SET @EMAILMESSAGE = @EMAILMESSAGE +  @COMPANYNAME + '<BR>'

SELECT @EMAILTO = @VERFYEMAIL

SELECT 'PASSWORD UPDATED SUCCESSFULLY', ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = REPLACE( @EMAILSUBJECT, '&','~' ),   
EMAILMESSAGE = Replace (REPLACE(REPLACE( @EMAILMESSAGE, '<', '$' ),'>','@'),'&','~') , SMTP_SRV_NAME = @SMTP_SRV_NAME  
  

--SELECT MSG = 'PASSWORD UPDATED SUCCESSFULLY', ERRMSG = @ERRMSG , EMAILFROM = @EMAILFROM, EMAILTO = @EMAILTO, EMAILSUBJECT = @EMAILSUBJECT, 
--EMAILMESSAGE = @EMAILMESSAGE, SMTP_SRV_NAME = @SMTP_SRV_NAME

--SELECT MSG = 'PASSWORD UPDATED SUCCESSFULLY'

GO
