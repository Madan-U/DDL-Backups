CREATE PROCEDURE [dbo].[POP_COMMON_CONTRACTDATA_DATAFETCH_BSECURFO] (        
 @FROMDATE VARCHAR(11)       
 )        
AS     
    
    
--------------------------------NSE---------------------    
    
TRUNCATE TABLE CONTRACT_DATA_COMMON    
    
TRUNCATE TABLE CONTRACT_MASTER_COMMON    
    
    
    
INSERT INTO CONTRACT_DATA_COMMON    
SELECT D.* FROM SETT_MST M , CONTRACT_DATA D    
WHERE M.SETT_NO=D.SETT_NO    
AND  M.SETT_TYPE=D.SETT_TYPE    
AND M.START_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
    
INSERT INTO CONTRACT_MASTER_COMMON    
SELECT D.* FROM SETT_MST M , CONTRACT_MASTER D    
WHERE M.SETT_NO=D.SETT_NO    
AND  M.SETT_TYPE=D.SETT_TYPE    
AND M.START_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
------------------------------BSE----------------------------------    
    
DELETE AngelBSECM.BSEDB_AB.DBO.CONTRACT_DATA_COMMON    
    
DELETE AngelBSECM.BSEDB_AB.DBO.CONTRACT_MASTER_COMMON    
    
    
INSERT INTO AngelBSECM.BSEDB_AB.DBO.CONTRACT_DATA_COMMON    
SELECT D.* FROM AngelBSECM.BSEDB_AB.DBO.SETT_MST M , AngelBSECM.BSEDB_AB.DBO.CONTRACT_DATA D    
WHERE M.SETT_NO=D.SETT_NO    
AND  M.SETT_TYPE=D.SETT_TYPE    
AND M.START_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
INSERT INTO AngelBSECM.BSEDB_AB.DBO.CONTRACT_MASTER_COMMON    
SELECT D.* FROM AngelBSECM.BSEDB_AB.DBO.SETT_MST M , AngelBSECM.BSEDB_AB.DBO.CONTRACT_MASTER D    
WHERE M.SETT_NO=D.SETT_NO    
AND  M.SETT_TYPE=D.SETT_TYPE    
AND M.START_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
---------------------------NSEFO------------------------------------------    
    
    
DELETE ANGELFO.NSEFO.DBO.CONTRACT_DATA_COMMON    
    
DELETE ANGELFO.NSEFO.DBO.CONTRACT_MASTER_COMMON    
    
    
    
INSERT INTO ANGELFO.NSEFO.DBO.CONTRACT_MASTER_COMMON    
SELECT * FROM ANGELFO.NSEFO.DBO.CONTRACT_MASTER WHERE TRADE_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
    
INSERT INTO ANGELFO.NSEFO.DBO.CONTRACT_DATA_COMMON    
SELECT  * FROM  ANGELFO.NSEFO.DBO.CONTRACT_DATA WHERE SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
---------------------------NSECURFO---------------------------------------------    
    
DELETE ANGELFO.NSECURFO.DBO.CONTRACT_DATA_COMMON    
    
DELETE ANGELFO.NSECURFO.DBO.CONTRACT_MASTER_COMMON    
    
    
INSERT INTO ANGELFO.NSECURFO.DBO.CONTRACT_MASTER_COMMON    
SELECT * FROM ANGELFO.NSECURFO.DBO.CONTRACT_MASTER WHERE TRADE_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
INSERT INTO ANGELFO.NSECURFO.DBO.CONTRACT_DATA_COMMON    
SELECT  * FROM ANGELFO.NSECURFO.DBO.CONTRACT_DATA WHERE SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
----------------------------MCDXCDS--------------------------------------------    
    
    
DELETE ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_DATA_COMMON    
    
DELETE ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_MASTER_COMMON    
    
    
INSERT INTO ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_MASTER_COMMON    
SELECT * FROM ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_MASTER WHERE TRADE_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
INSERT INTO ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_DATA_COMMON    
SELECT * FROM ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_DATA WHERE SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'

GO
