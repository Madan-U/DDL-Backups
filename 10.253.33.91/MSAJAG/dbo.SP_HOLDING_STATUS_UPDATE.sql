CREATE  PROCEDURE [dbo].[SP_HOLDING_STATUS_UPDATE]
(
	@DATA VARCHAR(MAX),
	@STATUSNAME VARCHAR(50),
	@TYPE VARCHAR(1) 
)
AS
 
BEGIN
	TRUNCATE TABLE HOLDING_UPLOAD_STATUS
	EXECUTE(@DATA)
	
	IF @TYPE = 'H'
	BEGIN
		UPDATE B SET 
		CODE= A.STATUS,
		HDRSTATUS= A.HDRSTATUS,
		HDRCODE= A.CODE,
		ACDID= A.ACDID
		FROM HOLDING_UPLOAD_STATUS A , TBL_API_NSE_HOLDING B
		WHERE A.UID = B.UID 
	END 
	SELECT ERRCODE='1',MESSAGETEXT='DONE' 
END

GO
