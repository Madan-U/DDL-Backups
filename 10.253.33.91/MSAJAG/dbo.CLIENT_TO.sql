CREATE PROC [dbo].[CLIENT_TO] ( @FROMDATE DATETIME,@TODATE DATETIME,@PARTY_CODE VARCHAR(11))
 AS
SELECT * FROM 
(SELECT PARTY_CODE,TRD_DT = CONVERT(VARCHAR(11),SAUDA_DATE,103), --SETT_NO,SETT_TYPE,SCRIP_CD,SERIES,
BILLFLAG =(CASE WHEN BILLFLAG IN (4,5) THEN 'DELIVERY' ELSE 'TRADING' END),
SELL_BUY,QTY = SUM(TRADEQTY),RATE = SUM(TRADEQTY*MARKETRATE)/SUM(TRADEQTY),TRADE_AMOUNT = SUM(TRADEQTY*MARKETRATE),
-- BKG_UNIT = SUM(TRADEQTY*NBROKAPP)/SUM(TRADEQTY),NET_RATE = SUM(TRADEQTY*N_NETRATE)/SUM(TRADEQTY),
BROKERAGE = SUM(TRADEQTY*NBROKAPP),STT = SUM(INS_CHRG),TO_TAX = SUM(TURN_TAX),SEBI_TAX = SUM(SEBI_TAX),
STAMPDUTY = SUM(BROKER_CHRG),SER_TAX = SUM(NSERTAX) ,'NSE' AS EXCHANGE
FROM AngelNseCM.MSAJAG.DBO.HISTORY WITH (NOLOCK) WHERE PARTY_CODE = @PARTY_CODE
---AND SETT_NO = '2016061' AND SETT_TYPE = 'N' AND SCRIP_CD = 'EQUITAS' AND SELL_BUY = '2'
AND SAUDA_DATE >=@FROMDATE AND SAUDA_DATE <=@TODATE +' 23:59' AND MARKETRATE <> '0'
AND SETT_TYPE IN ('N','W')
GROUP BY PARTY_CODE,CONVERT(VARCHAR(11),SAUDA_DATE,103),BILLFLAG,SELL_BUY
--- HISTORY WITH (NOLOCK) WHERE PARTY_CODE = 'D20105' AND SETT_NO = '2016061'
UNION ALL
SELECT PARTY_CODE,TRD_DT = CONVERT(VARCHAR(11),SAUDA_DATE,103), --SETT_NO,SETT_TYPE,SCRIP_CD,SERIES,
BILLFLAG =(CASE WHEN BILLFLAG IN (4,5) THEN 'DELIVERY' ELSE 'TRADING' END),SELL_BUY,QTY = SUM(TRADEQTY),RATE = SUM(TRADEQTY*MARKETRATE)/SUM(TRADEQTY),TRADE_AMOUNT = SUM(TRADEQTY*MARKETRATE),
-- BKG_UNIT = SUM(TRADEQTY*NBROKAPP)/SUM(TRADEQTY),NET_RATE = SUM(TRADEQTY*N_NETRATE)/SUM(TRADEQTY),
BROKERAGE = SUM(TRADEQTY*NBROKAPP),STT = SUM(INS_CHRG),TO_TAX = SUM(TURN_TAX),SEBI_TAX = SUM(SEBI_TAX),
STAMPDUTY = SUM(BROKER_CHRG),SER_TAX = SUM(NSERTAX)  ,'NSE' AS EXCHANGE
FROM AngelNseCM.MSAJAG.DBO.SETTLEMENT WITH (NOLOCK) WHERE PARTY_CODE = @PARTY_CODE
---AND SETT_NO = '2016061' AND SETT_TYPE = 'N' AND SCRIP_CD = 'EQUITAS' AND SELL_BUY = '2'
AND SAUDA_DATE >=@FROMDATE AND SAUDA_DATE <=@TODATE +' 23:59' AND MARKETRATE <> '0'
AND SETT_TYPE IN ('N','W')
GROUP BY PARTY_CODE,CONVERT(VARCHAR(11),SAUDA_DATE,103),BILLFLAG,SELL_BUY
UNION ALL 
/*
BillFlag 2 & 3 means Trading  
BillFlag 4 & 5 means Delivery  
*/

--- BSECM
SELECT PARTY_CODE,TRD_DT = CONVERT(VARCHAR(11),SAUDA_DATE,103), --SETT_NO,SETT_TYPE,SCRIP_CD,SERIES,
BILLFLAG =(CASE WHEN BILLFLAG IN (4,5) THEN 'DELIVERY' ELSE 'TRADING' END),SELL_BUY,QTY = SUM(TRADEQTY),RATE = SUM(TRADEQTY*MARKETRATE)/SUM(TRADEQTY),TRADE_AMOUNT = SUM(TRADEQTY*MARKETRATE),
-- BKG_UNIT = SUM(TRADEQTY*NBROKAPP)/SUM(TRADEQTY),NET_RATE = SUM(TRADEQTY*N_NETRATE)/SUM(TRADEQTY),
BROKERAGE = SUM(TRADEQTY*NBROKAPP),STT = SUM(INS_CHRG),TO_TAX = SUM(TURN_TAX),SEBI_TAX = SUM(SEBI_TAX),
STAMPDUTY = SUM(BROKER_CHRG),SER_TAX = SUM(NSERTAX)  ,'BSE' AS EXCHANGE
FROM AngelBSECM.BSEDB_AB.DBO.HISTORY WITH (NOLOCK) WHERE PARTY_CODE = @PARTY_CODE
---AND SETT_NO = '2016061' AND SETT_TYPE = 'N' AND SCRIP_CD = 'EQUITAS' AND SELL_BUY = '2'
AND SAUDA_DATE >=@FROMDATE AND SAUDA_DATE <=@TODATE +' 23:59' AND MARKETRATE <> '0'
AND SETT_TYPE IN ('C','D')
GROUP BY PARTY_CODE,CONVERT(VARCHAR(11),SAUDA_DATE,103),BILLFLAG,SELL_BUY

UNION ALL 
SELECT PARTY_CODE,TRD_DT = CONVERT(VARCHAR(11),SAUDA_DATE,103), --SETT_NO,SETT_TYPE,SCRIP_CD,SERIES,
BILLFLAG =(CASE WHEN BILLFLAG IN (4,5) THEN 'DELIVERY' ELSE 'TRADING' END),SELL_BUY,QTY = SUM(TRADEQTY),
RATE = SUM(TRADEQTY*MARKETRATE)/SUM(TRADEQTY),TRADE_AMOUNT = SUM(TRADEQTY*MARKETRATE),
-- BKG_UNIT = SUM(TRADEQTY*NBROKAPP)/SUM(TRADEQTY),NET_RATE = SUM(TRADEQTY*N_NETRATE)/SUM(TRADEQTY),
BROKERAGE = SUM(TRADEQTY*NBROKAPP),STT = SUM(INS_CHRG),TO_TAX = SUM(TURN_TAX),SEBI_TAX = SUM(SEBI_TAX),
STAMPDUTY = SUM(BROKER_CHRG),SER_TAX = SUM(NSERTAX) ,'BSE' AS EXCHANGE
FROM AngelBSECM.BSEDB_AB.DBO.SETTLEMENT WITH (NOLOCK) WHERE PARTY_CODE = @PARTY_CODE
---AND SETT_NO = '2016061' AND SETT_TYPE = 'N' AND SCRIP_CD = 'EQUITAS' AND SELL_BUY = '2'
AND SAUDA_DATE >=@FROMDATE AND SAUDA_DATE <=@TODATE +' 23:59' AND MARKETRATE <> '0'
AND SETT_TYPE IN ('C','D')
GROUP BY PARTY_CODE,CONVERT(VARCHAR(11),SAUDA_DATE,103),BILLFLAG,SELL_BUY )A
ORDER BY PARTY_CODE,TRD_DT,BILLFLAG,SELL_BUY

GO
