-- Object: PROCEDURE dbo.NSE_SETTPRICEINSERT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[NSE_SETTPRICEINSERT]
(
	@CLDATE	VARCHAR(11)
)
AS

UPDATE CLOSING SET CL_RATE = C.CL_RATE
FROM CLOSING_MTM C
WHERE CLOSING.SYSDATE = @CLDATE 
AND C.SYSDATE = @CLDATE
AND C.SCRIP_CD = CLOSING.SCRIP_CD
AND C.SERIES = CLOSING.SERIES

INSERT INTO CLOSING
SELECT MARKET='NORMAL', SCRIP_CD, SERIES, CL_RATE, SYSDATE
,Pre_Cl_Rate = 0,
Open_Rate = 0,
Hi_Rate = 0,
Low_Rate = 0,
TotTrdQty = 0,
TotTurnOver = 0,
YearHiRate = 0,
YearLowRate = 0
FROM CLOSING_MTM C
WHERE SYSDATE = @CLDATE
AND SCRIP_CD NOT IN (SELECT SCRIP_CD FROM CLOSING
					 WHERE CLOSING.SYSDATE = @CLDATE 
					 AND C.SCRIP_CD = CLOSING.SCRIP_CD
					 AND C.SERIES = CLOSING.SERIES)

GO
