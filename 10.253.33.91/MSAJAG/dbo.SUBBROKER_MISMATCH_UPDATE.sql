CREATE PROC [dbo].[SUBBROKER_MISMATCH_UPDATE]

AS
BEGIN
SELECT CL_CODE,RELATIONSHIP_SUBBROKER,SUB_BROKER,BOPARTYCODE,RELATIONSHIP_BRANCH,RELATIONSHIP_TRADER
INTO #SUB_BROKER
--UPDATE A SET SUB_BROKER=RELATIONSHIP_SUBBROKER,BRANCH_CD=RELATIONSHIP_BRANCH,TRADER=RELATIONSHIP_TRADER
 FROM CLIENT_DETAILS A WITH (NOLOCK) ,ABVSCITRUS.CRMDB_A.DBO.API_KYC B WITH (NOLOCK)
 WHERE CREATED_DATE >= GETDATE()-1
 AND RELATIONSHIP_SUBBROKER <>SUB_BROKER
 AND PARTY_CODE =BOPARTYCODE
 AND MODIFIDEDON >=GETDATE()-1

 --UPDATE A SET A.SUB_BROKER=RELATIONSHIP_SUBBROKER,BRANCH_CD=RELATIONSHIP_BRANCH,TRADER=RELATIONSHIP_TRADER,IMP_STATUS=0
 SELECT *
 FROM CLIENT_DETAILS A WITH (NOLOCK) ,#SUB_BROKER B WITH (NOLOCK)
 WHERE  PARTY_CODE =BOPARTYCODE
 AND RELATIONSHIP_SUBBROKER <>A.SUB_BROKER

 END

GO
