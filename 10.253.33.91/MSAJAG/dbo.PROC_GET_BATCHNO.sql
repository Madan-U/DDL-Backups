CREATE PROC PROC_GET_BATCHNO (@REPORT_DATE VARCHAR(11), @FILENAME VARCHAR(50), @GENERATEBY VARCHAR(50))
AS

DECLARE @BATCHNO INT
DECLARE @SNO INT

SELECT @SNO = ISNULL(MAX(SNO),0)
FROM CLASS_BATCHNO
WHERE REPORT_DATE LIKE @REPORT_DATE + '%'
AND FILENAME = @FILENAME

IF @SNO = 0 
	SELECT @BATCHNO = 1
ELSE
	SELECT @BATCHNO = BATCHNO + 1
	FROM CLASS_BATCHNO
	WHERE SNO = @SNO

IF @BATCHNO >= 100
BEGIN
	SELECT @BATCHNO = 1
END

SELECT BATCHNO = @BATCHNO

GO
