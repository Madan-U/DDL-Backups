-- Object: PROCEDURE dbo.RPT_HOLD_UPLOAD_18122018
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------






CREATE	PROC [dbo].[RPT_HOLD_UPLOAD_18122018]
		(
		@FROMDATE	VARCHAR(11) 
		)

		AS

		/*
		EXEC	RPT_HOLD_UPLOAD 'DEC 11 2018'
		*/
		
		/*
		IF (SELECT CASE WHEN @FROMDATE = LEFT(GETDATE(),11) THEN 1 ELSE 0 END) = 0 
		BEGIN
			SELECT * FROM OWNER WHERE 1 = 2 
			RETURN
		END
		*/

		DECLARE @FILENAME   VARCHAR(50)
		DECLARE @BATCHNO	INT  
		DECLARE @MEMBERCODE VARCHAR(20)
		DECLARE @MEMBERNAME	VARCHAR(500)

		SELECT @MEMBERCODE = MEMBERCODE, @MEMBERNAME = ISNULL(LTRIM(RTRIM(COMPANY)),'') FROM OWNER (NOLOCK)
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
		WHERE FLDFILENO = 'HS'  
		AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)

		IF @BATCHNO = 1  
		BEGIN  
			INSERT INTO ASIANFILEBATCHNO VALUES ('HS', @BATCHNO, GETDATE(), GETDATE())  
		END  
		ELSE  
		BEGIN  
			UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'HS' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		END  
		  
		SET		@FILENAME = @MEMBERCODE + '_HS_' + REPLACE(CONVERT(VARCHAR,GETDATE(),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) 
		

		SELECT	MEMBER_DEMAT_ACC = (
				CASE 
					WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
					ELSE D.BCLTDPID 
				END),
				MEMBER_ACC_TYPE = ACCOUNTTYPE + (CASE WHEN SEGMENT = 'MTF' THEN '-'+ SEGMENT ELSE '' END),
				UCC_CODE = D.PARTY_CODE,
				CLIENT_NAME = C1.LONG_NAME,
				CLIENT_PAN = C1.PAN_GIR_NO,
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				SECURITY_TYPE = CONVERT(VARCHAR(5),SERIES),
				PLEDGED_BAL_QTY = SUM(
				CASE 
					WHEN TRTYPE = 909 OR DESCRIPTION LIKE '%PLEDGE%' THEN QTY 
					ELSE 0 
				END),
				FREE_BAL_QTY = SUM(
				CASE 
					WHEN TRTYPE <> 909 AND DESCRIPTION NOT LIKE '%PLEDGE%' THEN QTY 
					ELSE 0 
				END),
				TOTAL_QTY = SUM(QTY),
				CL_RATE = CONVERT(NUMERIC(18, 4), 0),
				HOLDAMT = CONVERT(NUMERIC(18, 4), 0),
				EXCHANGE = P.EXCHANGE
		INTO	#HOLD
		FROM	ANGELDEMAT.MSAJAG.DBO.DELTRANS_REPORT D (NOLOCK),
				CLIENT1 C1 (NOLOCK),
				CLIENT2 C2 (NOLOCK),
				ANGELDEMAT.MSAJAG.DBO.DELIVERYDP P (NOLOCK)
		WHERE	D.TRANSDATE <= @FROMDATE
				AND D.PARTY_CODE = C2.PARTY_CODE
				AND C1.CL_CODE = C2.CL_CODE
				AND DRCR = 'D'
				AND FILLER2 = 1
				AND DELIVERED = '0' 
				AND SHARETYPE = 'DEMAT'
				AND D.BDPID = P.DPID
				AND D.BCLTDPID = P.DPCLTNO
				AND d.PARTY_CODE='DDNB1025'
		GROUP BY 
				CASE 
					WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
					ELSE D.BCLTDPID 
				END,
				ACCOUNTTYPE + (CASE WHEN SEGMENT = 'MTF' THEN '-'+ SEGMENT ELSE '' END),
				D.PARTY_CODE,
				C1.LONG_NAME,
				C1.PAN_GIR_NO,
				CERTNO,
				SCRIP_CD,
				SERIES,
				P.EXCHANGE
		HAVING
				SUM(QTY) <> 0


		INSERT	INTO #HOLD
		SELECT	MEMBER_DEMAT_ACC = (
				CASE 
					WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
					ELSE D.BCLTDPID 
				END),
				MEMBER_ACC_TYPE = ACCOUNTTYPE + (CASE WHEN SEGMENT = 'MTF' THEN '-'+ SEGMENT ELSE '' END),
				UCC_CODE = D.PARTY_CODE,
				CLIENT_NAME = C1.LONG_NAME,
				CLIENT_PAN = C1.PAN_GIR_NO,
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				SECURITY_TYPE = SERIES,
				PLEDGED_BAL_QTY = SUM(
				CASE 
					WHEN TRTYPE = 909 OR DESCRIPTION LIKE '%PLEDGE%' THEN QTY 
					ELSE 0 
				END),
				FREE_BAL_QTY = SUM(
				CASE 
					WHEN TRTYPE <> 909 AND DESCRIPTION NOT LIKE '%PLEDGE%' THEN QTY 
					ELSE 0 
				END),
				TOTAL_QTY = SUM(QTY),
				CL_RATE = CONVERT(NUMERIC(18, 4), 0),
				HOLDAMT = CONVERT(NUMERIC(18, 4), 0),
				EXCHANGE = P.EXCHANGE
		FROM	ANGELDEMAT.BSEDB.DBO.DELTRANS_REPORT D (NOLOCK),
				ANAND.BSEDB_AB.DBO.CLIENT1 C1 (NOLOCK),
				ANAND.BSEDB_AB.DBO.CLIENT2 C2 (NOLOCK),
				ANGELDEMAT.BSEDB.DBO.DELIVERYDP P (NOLOCK)
		WHERE	D.TRANSDATE <= @FROMDATE
				AND D.PARTY_CODE = C2.PARTY_CODE
				AND C1.CL_CODE = C2.CL_CODE
				AND DRCR = 'D'
				AND FILLER2 = 1
				AND DELIVERED = '0' 
				AND SHARETYPE = 'DEMAT'
				AND D.BDPID = P.DPID
				AND D.BCLTDPID = P.DPCLTNO
				AND d.PARTY_CODE='DDNB1025'
		GROUP BY 
				CASE 
					WHEN D.BDPTYPE = 'NSDL' THEN D.BDPID + D.BCLTDPID 
					ELSE D.BCLTDPID 
				END,
				ACCOUNTTYPE + (CASE WHEN SEGMENT = 'MTF' THEN '-'+ SEGMENT ELSE '' END),
				D.PARTY_CODE,
				C1.LONG_NAME,
				C1.PAN_GIR_NO,
				CERTNO,
				SCRIP_CD,
				SERIES,
				P.EXCHANGE
		HAVING
				SUM(QTY) <> 0


		UPDATE	#HOLD
		SET		SECURITY_TYPE = ISNULL(M.SERIES,'')
		FROM	ANGELDEMAT.MSAJAG.DBO.MULTIISIN M (NOLOCK)
		WHERE	M.ISIN = #HOLD.ISIN
				AND M.VALID = 1
				

		SELECT	FILENAME = @FILENAME,
				[Members Demat Account No.] = MEMBER_DEMAT_ACC,
				[Member Account Type] = MEMBER_ACC_TYPE,
				[Unique Client Code (UCC)] = UCC_CODE,
				[Client Name] = CLIENT_NAME,
				[Client PAN] = CLIENT_PAN,
				[ISIN Code] = ISIN,
				[Scrip Name] = SCRIP_NAME,
				[Security Type] = SECURITY_TYPE,
				[Pledged Balance (Qty.)] = SUM(PLEDGED_BAL_QTY),
				[Free Balance (Qty.)] = SUM(FREE_BAL_QTY),
				[Total (Qty.)] = SUM(TOTAL_QTY)
		FROM	#HOLD				
		GROUP BY
				MEMBER_DEMAT_ACC,
				MEMBER_ACC_TYPE,
				UCC_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				ISIN,
				SCRIP_NAME,
				SECURITY_TYPE
		ORDER BY
				UCC_CODE,
				SCRIP_NAME,
				ISIN
				
		DROP TABLE #HOLD

GO
