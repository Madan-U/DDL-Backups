CREATE PROCEDURE [dbo].[Brok_detail_modifcation_VBB] ( @PARTY_CODE varchar(10) , @BROK_SCHEME VARCHAR(50)) 
 
AS 
  
DECLARE @CNT INT ,@BROKCNT INT 
SELECT DISTINCT TABLE_NO INTO #TABLE_NO FROM BROKTABLE WHERE TABLE_NAME LIKE '%_VBB' 

   --SELECT @BROKCNT=count( TABLE_NO) From  #TABLE_NO  
 
IF (@BROKCNT=0) 
  
   BEGIN  
     SELECT 'PLEASE ENTER VALID BROKERAGE PLAN' 
 
   RETURN 
	END 
 
 
 
SELECT @CNT =COUNT(1)  FROM  CLIENT_DETAILS WHERE CL_CODE =@PARTY_CODE  
SELECT @CNT 
  
 IF (@CNT =0 ) 
   BEGIN  
     Select 'Code Not Exist' 
 
   RETURN 
   END 
 
SET @CNT=0 
 
 
Select  @CNT = COUNT(Distinct CL_CODE) FROM  
( SELECT DISTINCT SP_PARTY_CODE AS CL_CODE  FROM Scheme_Mapping   
WHERE SP_PARTY_CODE =@PARTY_CODE AND SP_Computation_Level ='O' AND SP_DATE_TO > GETDATE() 
 UNION  ALL 
 SELECT DISTINCT SP_PARTY_CODE AS CL_CODE  FROM ANGELFO.NSEFO.DBO.Scheme_Mapping   
 WHERE SP_PARTY_CODE =@PARTY_CODE AND SP_Computation_Level ='O' AND SP_DATE_TO > GETDATE() 
 UNION  ALL 
 SELECT DISTINCT SP_PARTY_CODE AS CL_CODE  FROM ANGELCOMMODITY.MCDX.DBO.Scheme_Mapping   
 WHERE SP_PARTY_CODE =@PARTY_CODE AND SP_Computation_Level ='O' AND SP_DATE_TO > GETDATE() 
)A 
  
 
 
IF (@CNT >1) 
 BEGIN  
   
   SELECT 'Client Already Using VBB Option' REMARK 
 
   RETURN 
  
 END 
 
EXEC MSAJAG.DBO.VBB_CI_NEW_CLIENT   @PARTY_CODE ,@BROK_SCHEME 
EXEC AngelBSECM.BSEDB_AB.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME 
EXEC ANGELFO.NSEFO.DBO.VBB_CI_NEW_CLIENT   @PARTY_CODE ,@BROK_SCHEME 
EXEC ANGELFO.NSECURFO.DBO.VBB_CI_NEW_CLIENT   @PARTY_CODE ,@BROK_SCHEME 
EXEC ANGELcommodity.BSECURFO.DBO.VBB_CI_NEW_CLIENT   @PARTY_CODE ,@BROK_SCHEME 
EXEC ANGELcommodity.MCDX.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME  
EXEC ANGELcommodity.NCDX.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME 
EXEC ANGELcommodity.BSEFO.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME 
EXEC ANGELcommodity.NCE.DBO.VBB_CI_NEW_CLIENT  @PARTY_CODE ,@BROK_SCHEME 
 
--SELECT DISTINCT SCHEME_NAME FROM MAPPING_TABLE

GO
