---DECLARE @TDATE DATETIME 


---COMB_MARGIN_CALC_REPORT '2018-08-01','2018-08-13','',''

---EXEC COMB_MARGIN_CALC_REPORT'Aug  1 2018', 'Aug  8 2018', 'A0001', 'ZZ999','NSEFO'

---EXEC COMB_MARGIN_CALC_REPORT'Aug  1 2018', 'Aug  8 2018', 'A0001', 'ZZ999','NSX'

CREATE PROCEDURE [dbo].[COMB_MARGIN_CALC_REPORT_NEW]
(

@FROMDATE DATETIME,
@TODATE DATETIME,
@FROMPARTY VARCHAR(20),
@TOPARTY VARCHAR(20),
@CONSOL VARCHAR(10)   ---10  CONSOL INDICATES THAT WHETHER IS "NSx","NSEFO"

)

AS 
IF @FROMPARTY  =''  

   BEGIN 
   SET @FROMPARTY ='A00'
   END 

IF @TOPARTY  =''  
   BEGIN 

   SET @TOPARTY ='ZZZZZ'

   END 

 IF @FROMDATE  =''  

   BEGIN 

   SET @FROMDATE ='APR  1 1900'

   END 


IF @TODATE  =''  

   BEGIN 

   SET @TODATE = REPLACE(CONVERT(VARCHAR(12),GETDATE(),107),',','')

   END 


BEGIN 

 DECLARE @MAXDATE DATETIME

 SET @MAXDATE =(SELECT TOP 1 MARGINDATE  FROM TBL_COMBINE_REPORTING ORDER BY   MARGINDATE DESC )

IF @MAXDATE <'2019-07-29' AND @FROMDATE <'2019-07-29' 
		 BEGIN
		SELECT  EXCHANGE,MARGINDATE,PARTY_CODE,TDAY_LEDGER,TDAY_MARGIN,TDAY_MTM,TDAY_NONCASH,T1DAY_LEDGER,T1DAY_MARGIN,T1DAY_NONCASH,T_MARGINAVL,
		T1_MTMAVL =(CASE WHEN TDAY_MTM =0 THEN 0 ELSE ( CASE WHEN  T1_MTMAVL>TDAY_MTM THEN TDAY_MTM  ELSE T1_MTMAVL  END) END)  into #temp
		 FROM ( 
		SELECT EXCHANGE,MARGINDATE,PARTY_CODE,TDAY_LEDGER,	TDAY_MARGIN,	TDAY_MTM	,TDAY_NONCASH,
		T1DAY_LEDGER,	T1DAY_MARGIN,T1DAY_NONCASH, 
		 T_MARGINAVL = (CASE WHEN TDAY_MARGIN >0 THEN  ( CASE  WHEN  (TDAY_LEDGER+TDAY_NONCASH) <= 0  THEN   -TDAY_MARGIN ELSE   (TDAY_LEDGER+TDAY_NONCASH -TDAY_MARGIN) END)
										ELSE 0 END ),
		 T1_MTMAVL =   ( CASE WHEN MARGINDATE = @MAXDATE THEN TDAY_MTM_SHORT ELSE (CASE WHEN TDAY_MARGIN_SHORT > 0 THEN TDAY_MARGIN_SHORT ELSE 0 END) 
							+ PRADNYA.DBO.FNMAX((T1DAY_CASH+T1DAY_NONCASH+TDAY_MTM)-(TDAY_CASH + TDAY_NONCASH),0)
								 + PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN,0)- TDAY_MTM END)
		  FROM TBL_COMBINE_REPORTING with(nolock) WHERE 
		  --CONVERT(varchar, '2017-08-25', 101)
		  convert(varchar,MARGINDATE ,101)>=@FROMDATE 
		  AND convert(varchar,MARGINDATE ,101)<= @TODATE 
		  AND EXCHANGE = @CONSOL
		  AND SEGMENT ='FUTURES'
		 AND PARTY_CODE >=@FROMPARTY and PARTY_CODE <=@TOPARTY)A 

		  SELECT  *,
   REDUCED_MARGIN_BENIFIT= ( CASE WHEN MARGINDATE = @MAXDATE THEN 0 ELSE  (CASE WHEN  TDAY_MTM =0 THEN 0 ELSE   (CASE WHEN TDAY_MARGIN -T1DAY_MARGIN >=0 THEN  TDAY_MARGIN -T1DAY_MARGIN ELSE 0 END ) END ) END)
   ,LEDGER_BENIFIT= ( CASE WHEN MARGINDATE = @MAXDATE THEN 0 ELSE (CASE WHEN  TDAY_MTM =0 THEN 0 ELSE(CASE WHEN T1DAY_LEDGER -TDAY_LEDGER >=0 THEN  T1DAY_LEDGER -TDAY_LEDGER  ELSE 0 END )END )END)
   ,Collateral_BENIFIT= ( CASE WHEN MARGINDATE = @MAXDATE THEN 0 ELSE (CASE WHEN  TDAY_MTM =0 THEN 0  ELSE PRADNYA.DBO.FNMAX((T1DAY_LEDGER+T1DAY_NONCASH+TDAY_MTM)-(TDAY_LEDGER + TDAY_NONCASH),0) + PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN,0) END)END),
   LEDGER_MARGIN_BENFIT =  ( CASE WHEN MARGINDATE = @MAXDATE THEN 0 ELSE (CASE WHEN  TDAY_MTM =0 THEN 0 ELSE (CASE WHEN TDAY_LEDGER -TDAY_MARGIN >=0 THEN  TDAY_LEDGER -TDAY_MARGIN  ELSE 0 END ) END )END),
   REPORT_MTM =(CASE WHEN T1_MTMAVL >0 THEN TDAY_MTM ELSE TDAY_MTM+T1_MTMAVL END )
     FROM #temp    
	 
	order by MARGINDATE,party_code



		 END
	ELSE 
	 BEGIN 
		 SELECT  EXCHANGE,MARGINDATE,PARTY_CODE,TDAY_LEDGER,TDAY_MARGIN,TDAY_MTM,TDAY_NONCASH,T1DAY_LEDGER,T1DAY_MARGIN,T1DAY_NONCASH,T_MARGINAVL,
		T1_MTMAVL =(CASE WHEN TDAY_MTM =0 THEN 0 ELSE ( CASE WHEN  T1_MTMAVL>TDAY_MTM THEN TDAY_MTM  ELSE T1_MTMAVL  END) END)  into #temp1
		 FROM ( 
		SELECT EXCHANGE,MARGINDATE,PARTY_CODE,TDAY_LEDGER,	TDAY_MARGIN,	TDAY_MTM	,TDAY_NONCASH,
		T1DAY_LEDGER,	T1DAY_MARGIN,T1DAY_NONCASH, 
		 T_MARGINAVL = (CASE WHEN TDAY_MARGIN >0 THEN  ( CASE  WHEN  (TDAY_LEDGER+TDAY_NONCASH) <= 0  THEN   -TDAY_MARGIN ELSE   (TDAY_LEDGER+TDAY_NONCASH -TDAY_MARGIN) END)
										ELSE 0 END ),
		 T1_MTMAVL =   ( CASE WHEN MARGINDATE = @MAXDATE THEN TDAY_MTM_SHORT ELSE (CASE WHEN TDAY_MARGIN_SHORT > 0 THEN TDAY_MARGIN_SHORT ELSE 0 END) 
							+ PRADNYA.DBO.FNMAX((T1DAY_CASH+T1DAY_NONCASH+TDAY_MTM)-(TDAY_CASH + TDAY_NONCASH),0)
								 + PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN,0)- TDAY_MTM END)
		  FROM TBL_COMBINE_REPORTING with(nolock) WHERE 
		  --CONVERT(varchar, '2017-08-25', 101)
		  convert(varchar,MARGINDATE ,101)>=@FROMDATE 
		  AND convert(varchar,MARGINDATE ,101)<= @TODATE 
		 --- AND EXCHANGE = @CONSOL
		---  AND SEGMENT ='FUTURES'
		 AND PARTY_CODE >=@FROMPARTY and PARTY_CODE <=@TOPARTY)A 


		  SELECT  *,
   REDUCED_MARGIN_BENIFIT= ( CASE WHEN MARGINDATE = @MAXDATE THEN 0 ELSE  (CASE WHEN  TDAY_MTM =0 THEN 0 ELSE   (CASE WHEN TDAY_MARGIN -T1DAY_MARGIN >=0 THEN  TDAY_MARGIN -T1DAY_MARGIN ELSE 0 END ) END ) END)
   ,LEDGER_BENIFIT= ( CASE WHEN MARGINDATE = @MAXDATE THEN 0 ELSE (CASE WHEN  TDAY_MTM =0 THEN 0 ELSE(CASE WHEN T1DAY_LEDGER -TDAY_LEDGER >=0 THEN  T1DAY_LEDGER -TDAY_LEDGER  ELSE 0 END )END )END)
   ,Collateral_BENIFIT= ( CASE WHEN MARGINDATE = @MAXDATE THEN 0 ELSE (CASE WHEN  TDAY_MTM =0 THEN 0  ELSE PRADNYA.DBO.FNMAX((T1DAY_LEDGER+T1DAY_NONCASH+TDAY_MTM)-(TDAY_LEDGER + TDAY_NONCASH),0) + PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN,0) END)END),
   LEDGER_MARGIN_BENFIT =  ( CASE WHEN MARGINDATE = @MAXDATE THEN 0 ELSE (CASE WHEN  TDAY_MTM =0 THEN 0 ELSE (CASE WHEN TDAY_LEDGER -TDAY_MARGIN >=0 THEN  TDAY_LEDGER -TDAY_MARGIN  ELSE 0 END ) END )END),
   REPORT_MTM =(CASE WHEN T1_MTMAVL >0 THEN TDAY_MTM ELSE TDAY_MTM+T1_MTMAVL END )
     FROM #temp1    
	 
	order by MARGINDATE,party_code


  END


 
 
 END

GO
