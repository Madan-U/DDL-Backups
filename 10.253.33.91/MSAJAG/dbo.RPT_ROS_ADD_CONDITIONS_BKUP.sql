CREATE PROC [dbo].[RPT_ROS_ADD_CONDITIONS] (
	@CONPOS INT = 0
	)

	AS

	IF @CONPOS = 1
	BEGIN
		UPDATE #DELHOLDSECDATA SET PLEDGED_BAL_QTY = #DELHOLDSECDATA.PLEDGED_BAL_QTY WHERE 1 = 2
	END

	IF @CONPOS = 2
	BEGIN
		UPDATE #DEL_TRANSSTAT_1 SET PLEDGE_UNPLEDGE_QTY = #DEL_TRANSSTAT_1.PLEDGE_UNPLEDGE_QTY  WHERE 1 = 2
	END

GO
