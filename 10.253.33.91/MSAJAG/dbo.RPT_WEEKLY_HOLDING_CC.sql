-- Object: PROCEDURE dbo.RPT_WEEKLY_HOLDING_CC
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE	PROC [dbo].[RPT_WEEKLY_HOLDING_CC]
		(
		@FROMDATE	VARCHAR(11),
		@TODATE		VARCHAR(11),
		@EXCHANGE	VARCHAR(3)  = 'NSE',
		@FROMPARTY	VARCHAR(20) = '',
		@TOPARTY	VARCHAR(20) = 'zzzzzzzzzz'
		)
		
		AS

		/*
		EXEC	RPT_WEEKLY_HOLDING_CC 'NOV 10 2018','NOV 15 2018','NSE','0','ZZZZZ'
		*/

		DECLARE @FROMDT		INT,
				@TODT		INT,
				@RPTDATE	VARCHAR(11),
				@FILENAMEHS	VARCHAR(100),
				@FILENAMENH	VARCHAR(100),
				@PANNO		VARCHAR(20),
				@BATCHNO	INT  
				
		SET		@FROMDT = 0
		SELECT	@TODT = DATEDIFF(DAY, @FROMDATE, @TODATE)

		IF (@EXCHANGE = 'NSE')
		BEGIN
			SELECT	@PANNO = ISNULL(LTRIM(RTRIM(PANNO)),'') FROM OWNER (NOLOCK)
			SELECT	@BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
			WHERE	FLDFILENO = 'CC'  
			AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)

			IF @BATCHNO = 1  
			BEGIN  
				INSERT INTO ASIANFILEBATCHNO VALUES ('CC', @BATCHNO, GETDATE(), GETDATE())  
			END  
			ELSE  
			BEGIN  
				UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'CC' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
			END  

			SET	@FILENAMEHS = @PANNO + '_Collat_Sec_' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE,109),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'

		END
		ELSE
		BEGIN
			SELECT @PANNO = ISNULL(LTRIM(RTRIM(PANNO)),'') FROM BSEDB.DBO.OWNER (NOLOCK)

			SET	@FILENAMEHS = @PANNO + '_Collat_Sec_' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE,109),103),'/','') + '.CSV'
		END		

		/*
		CREATE TABLE #HOLD_W
			(
			CLEARING_MEMBER_ID			VARCHAR(20),
			CLEARING_MEMBER_PAN			VARCHAR(20),
			HOLDING_DATE				VARCHAR(11),
			CLEARING_MEMBER_DEMAT_ACCNO VARCHAR(20),
			CLEARING_MEMBER_ACC_TYPE	VARCHAR(10),
			TRADING_MEMBER_NAME			VARCHAR(100),
			TRADING_MEMBER_PAN			VARCHAR(20),
			UCC_CODE					VARCHAR(20),
			CLIENT_PAN					VARCHAR(20),
			ISIN						VARCHAR(20),
			SECURITY_TYPE				VARCHAR(10),
			TOTAL_QTY					VARCHAR(20),
			ACTION						VARCHAR(2)			
			)
		
		CREATE CLUSTERED INDEX [DELHOLD] ON [DBO].[#HOLD_W] (
			[UCC_CODE] ASC,
			[ISIN],
			[SECURITY_TYPE]			
			)
		*/	
					
		DECLARE @WEEKLYHOLD  TABLE (
				CLEARING_MEMBER_ID			VARCHAR(50) DEFAULT (''),
				CLEARING_MEMBER_PAN			VARCHAR(50) DEFAULT (''),
				HOLDING_DATE				VARCHAR(50) DEFAULT (''),
				CLEARING_MEMBER_DEMAT_ACCNO VARCHAR(50) DEFAULT (''),
				CLEARING_MEMBER_ACC_TYPE	VARCHAR(50) DEFAULT (''),
				TRADING_MEMBER_NAME			VARCHAR(200) DEFAULT (''),
				TRADING_MEMBER_PAN			VARCHAR(50) DEFAULT (''),
				UCC_CODE					VARCHAR(50) DEFAULT (''),
				CLIENT_PAN					VARCHAR(50) DEFAULT (''),
				ISIN						VARCHAR(50) DEFAULT (''),
				SECURITY_TYPE				VARCHAR(50) DEFAULT (''),
				TOTAL_QTY					VARCHAR(50) DEFAULT (''),
				ACTION						VARCHAR(50)	DEFAULT (''),
				ORDFLAG						INT			DEFAULT (0)
				)

		INSERT	INTO @WEEKLYHOLD
		SELECT	'Clearing Member ID (Assigned by CC)',
				'Clearing Member PAN',
				'Holding Date',
				'Clearing Member''s Demat Account No.',
				'Clearing Member Account Type',
				'Trading member Name',
				'Trading member PAN',
				'Client UCC',
				'Client PAN',
				'ISIN',
				'Security Type',
				'Total Quantity',
				'Action',
				ORDFLAG=1

		WHILE	@FROMDT <= @TODT
		BEGIN
				SELECT @RPTDATE = LEFT((DATEADD(D,@FROMDT,@FROMDATE)),11)		
				
				INSERT INTO @WEEKLYHOLD
				EXEC RPT_HOLD_CLEARING_UPLOAD @RPTDATE, @EXCHANGE, @FROMPARTY, @TOPARTY

				SET @FROMDT = @FROMDT + 1
		END

		SELECT	@FILENAMEHS,
				*
		FROM	@WEEKLYHOLD
		ORDER BY 
				ORDFLAG,
				HOLDING_DATE,
				UCC_CODE,
				ISIN

GO
