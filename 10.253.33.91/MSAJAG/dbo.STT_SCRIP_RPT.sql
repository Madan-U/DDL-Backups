CREATE PROCEDURE [dbo].[STT_SCRIP_RPT]
(
@SCRIP_CD VARCHAR(30)
)
AS
BEGIN 


select SCRIP_CD,DATEFROM,DATETO,ADDEDBY,ADDEDON,MODIFIEDBY,MODIFIEDON,SERIES,CHARGE_FLAG from TBL_STT_SCRIP WHERE CHARGE_FLAG='2'AND SCRIP_CD=@SCRIP_CD
UNION ALL
select SCRIP_CD,DATEFROM,DATETO,ADDEDBY,ADDEDON,MODIFIEDBY,MODIFIEDON,SERIES,CHARGE_FLAG from AngelBSECM.BSEDB_AB.DBO.TBL_STT_SCRIP WHERE CHARGE_FLAG='2'AND SCRIP_CD=@SCRIP_CD
END

GO
