-- Object: PROCEDURE dbo.CLS_STT_CERTIFICATE
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROCEDURE [dbo].[CLS_STT_CERTIFICATE] (
	@DATEFROM VARCHAR(11),
	@DATETO VARCHAR(11),
	@PARTYFROM VARCHAR(25),
	@PARTYTO VARCHAR(25),
	@STATUSID VARCHAR(25),
	@STATUSNAME VARCHAR(25),
	@STRBRANCH VARCHAR(25),
	@STRBRANCHTO VARCHAR(25),
	@STRSUBBROKFROM VARCHAR(25),
	@STRSUBBROKTO VARCHAR(25),
	@DIGIFLAG VARCHAR(2) 
	)
AS


IF LEN(LTRIM(RTRIM(@PARTYTO))) = 0
BEGIN
	SET @PARTYTO = 'ZZZZZZZZZ'
END

IF LEN(LTRIM(RTRIM(@STRBRANCHTO))) = ''
BEGIN
	SET @STRBRANCHTO = 'ZZZZZZZZZZZZZZ'
END

IF LEN(LTRIM(RTRIM(@STRSUBBROKTO))) = ''
BEGIN
	SET @STRSUBBROKTO = 'ZZZZZZZZZZZ'
END

IF LEN(LTRIM(RTRIM(@DIGIFLAG))) = ''
BEGIN
	SET @DIGIFLAG = '0'
END

  IF LEN(@DATEFROM) = 10 AND CHARINDEX('/',@DATEFROM) > 0    
  SET @DATEFROM=CONVERT(VARCHAR (11), CONVERT(DATETIME,  @DATEFROM,103),109)
  
  IF LEN(@DATETO) = 10 AND CHARINDEX('/',@DATETO) > 0    
  SET @DATETO=CONVERT(VARCHAR (11), CONVERT(DATETIME,  @DATETO,103),109)        

-- EXEC MSAJAG.DBO.CLS_STT_CERTIFICATE '10/02/2013','10/02/2014','00','ZZZ','BROKER','BROKER','','ZZZZZZZZZZZ','','ZZZZZZZZZZ',''  
-- EXEC MSAJAG.DBO.CLS_STT_CERTIFICATE 'FEB 10 2013','JUL 10 2014','00','ZZZ','BROKER','BROKER','','ZZZZZZZZZZZ','','ZZZZZZZZZZ',''  
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT PARTY_CODE,
	SAUDA_DATE = LEFT(SAUDA_DATE, 11),
	TRANSACTIONCODE = '01',
	TURNOVER = SUM(PAMTDEL),
	STT = (CASE WHEN SUM(PSTTDEL) > 0 THEN SUM(PSTTDEL) + (ROUND((SUM(PSTTDEL) + SUM(SSTTDEL) + SUM(SSTTTRD)), 0) - (SUM(PSTTDEL) + SUM(SSTTDEL) + SUM(SSTTTRD))) ELSE SUM(PSTTDEL) END)
INTO #STT02
FROM STT_CLIENTDETAIL
WHERE PARTY_CODE BETWEEN @PARTYFROM AND @PARTYTO
	AND SAUDA_DATE BETWEEN @DATEFROM AND @DATETO + ' 23:59'
	AND RECTYPE = 30
GROUP BY PARTY_CODE,
	LEFT(SAUDA_DATE, 11),
	CONTRACTNO

SELECT PARTY_CODE,
	SAUDA_DATE = LEFT(SAUDA_DATE, 11),
	TRANSACTIONCODE = '02',
	TURNOVER = SUM(SAMTDEL),
	STT = (
		CASE WHEN SUM(PSTTDEL) = 0
				AND SUM(SSTTDEL) > 0 THEN SUM(SSTTDEL) + (ROUND((SUM(PSTTDEL) + SUM(SSTTDEL) + SUM(SSTTTRD)), 0) - (SUM(PSTTDEL) + SUM(SSTTDEL) + SUM(SSTTTRD))) ELSE SUM(SSTTDEL) END
		)
INTO #STT03
FROM STT_CLIENTDETAIL
WHERE PARTY_CODE BETWEEN @PARTYFROM AND @PARTYTO
	AND SAUDA_DATE BETWEEN @DATEFROM AND @DATETO + ' 23:59'
	AND RECTYPE = 30
GROUP BY PARTY_CODE,
	LEFT(SAUDA_DATE, 11),
	CONTRACTNO

SELECT PARTY_CODE,
	SAUDA_DATE = LEFT(SAUDA_DATE, 11),
	TRANSACTIONCODE = '03',
	TURNOVER = SUM(SAMTTRD),
	STT = (
		CASE WHEN SUM(PSTTDEL) = 0
				AND SUM(SSTTDEL) = 0
				AND SUM(SSTTTRD) > 0 THEN SUM(SSTTTRD) + (ROUND((SUM(PSTTDEL) + SUM(SSTTDEL) + SUM(SSTTTRD)), 0) - (SUM(PSTTDEL) + SUM(SSTTDEL) + SUM(SSTTTRD))) ELSE SUM(SSTTTRD) END
		)
INTO #STT01
FROM STT_CLIENTDETAIL
WHERE PARTY_CODE BETWEEN @PARTYFROM AND @PARTYTO
	AND SAUDA_DATE BETWEEN @DATEFROM AND @DATETO + ' 23:59'
	AND RECTYPE = 30
GROUP BY PARTY_CODE,
	LEFT(SAUDA_DATE, 11),
	CONTRACTNO

CREATE INDEX [PARTYCD] ON [DBO].[#STT01] ([PARTY_CODE])
CREATE INDEX [PARTYCD] ON [DBO].[#STT02] ([PARTY_CODE])
CREATE INDEX [PARTYCD] ON [DBO].[#STT03] ([PARTY_CODE])

SELECT COMPANY,
	ADDR1,
	ADDR2,
	CITY,
	ZIP,
	PHONE,
	EXCHANGECODE,
	MEMBERCODE,
	C2.PARTY_CODE,
	LONG_NAME,
	L_ADDRESS1,
	L_ADDRESS2,
	L_ADDRESS3,
	L_CITY,
	PAN_GIR_NO,
	' ' MAPIDID,
	L_ZIP
INTO #CLIENT
FROM CLIENT1 C1(NOLOCK),
	CLIENT2 C2(NOLOCK),
	OWNER (NOLOCK),
	CLIENT_PRINT_SETTINGS CP(NOLOCK)
WHERE C1.CL_CODE = C2.CL_CODE
	AND PARTY_CODE >= @PARTYFROM
	AND PARTY_CODE <= @PARTYTO
	AND BRANCH_CD >= @STRBRANCH
	AND BRANCH_CD <= @STRBRANCHTO
	AND SUB_BROKER >= @STRSUBBROKFROM
	AND SUB_BROKER <= @STRSUBBROKTO
	AND C2.PRINTF = CP.PRINT_FLAG
	AND CP.VALID_REPORT LIKE (CASE WHEN @DIGIFLAG = 0 THEN '%' WHEN @DIGIFLAG = 1 THEN '%CONTRACT%' WHEN @DIGIFLAG = 2 THEN '%DIGITAL%' ELSE CP.VALID_REPORT END)
	AND BRANCH_CD LIKE (CASE WHEN @STATUSID = 'BRANCH' THEN @STATUSNAME ELSE '%' END)
	AND SUB_BROKER LIKE (CASE WHEN @STATUSID = 'SUBBROKER' THEN @STATUSNAME ELSE '%' END)
	AND TRADER LIKE (CASE WHEN @STATUSID = 'TRADER' THEN @STATUSNAME ELSE '%' END)
	AND C1.FAMILY LIKE (CASE WHEN @STATUSID = 'FAMILY' THEN @STATUSNAME ELSE '%' END)
	AND C1.CL_CODE LIKE (CASE WHEN @STATUSID = 'CLIENT' THEN @STATUSNAME ELSE '%' END)

CREATE INDEX [PARTYCD] ON [DBO].[#CLIENT] ([PARTY_CODE])

INSERT INTO #STT01
SELECT PARTY_CODE, @DATEFROM, '01', 0, 0
FROM #CLIENT
WHERE PARTY_CODE NOT IN (
		SELECT PARTY_CODE
		FROM #STT01
		)

INSERT INTO #STT02
SELECT PARTY_CODE, @DATEFROM, '02', 0, 0
FROM #CLIENT
WHERE PARTY_CODE NOT IN (
		SELECT PARTY_CODE
		FROM #STT02
		)

INSERT INTO #STT03
SELECT PARTY_CODE, @DATEFROM, '03', 0, 0
FROM #CLIENT
WHERE PARTY_CODE NOT IN (
		SELECT PARTY_CODE
		FROM #STT03
		)

SELECT PARTY_CODE, '04' TRANSACTIONCODE, 0 TURNOVER, 0 STT
INTO #STT04
FROM #CLIENT

SELECT PARTY_CODE, '05' TRANSACTIONCODE, 0 TURNOVER, 0 STT
INTO #STT05
FROM #CLIENT

SELECT S.PARTY_CODE, TRANSACTIONCODE, TURNOVER, STT
INTO #STT00
FROM (
	SELECT PARTY_CODE, TRANSACTIONCODE, TURNOVER = SUM(TURNOVER), STT = SUM(STT)
	FROM #STT01
	GROUP BY PARTY_CODE, TRANSACTIONCODE
	
	UNION
	
	SELECT PARTY_CODE, TRANSACTIONCODE, TURNOVER = SUM(TURNOVER), STT = SUM(STT)
	FROM #STT02
	GROUP BY PARTY_CODE, TRANSACTIONCODE
	
	UNION
	
	SELECT PARTY_CODE, TRANSACTIONCODE, TURNOVER = SUM(TURNOVER), STT = SUM(STT)
	FROM #STT03
	GROUP BY PARTY_CODE, TRANSACTIONCODE
	
	UNION
	
	SELECT PARTY_CODE, TRANSACTIONCODE, TURNOVER = SUM(TURNOVER), STT = SUM(STT)
	FROM #STT04
	GROUP BY PARTY_CODE, TRANSACTIONCODE
	
	UNION
	
	SELECT PARTY_CODE, TRANSACTIONCODE, TURNOVER = SUM(TURNOVER), STT = SUM(STT)
	FROM #STT05
	GROUP BY PARTY_CODE, TRANSACTIONCODE
	) S
ORDER BY UPPER(S.PARTY_CODE), TRANSACTIONCODE

DROP TABLE #STT01
DROP TABLE #STT02
DROP TABLE #STT03
DROP TABLE #STT04
DROP TABLE #STT05




CREATE INDEX [PARTYCD] ON [DBO].[#STT00] ([PARTY_CODE])

SELECT COMPANY,
	ADDR1,
	ADDR2,
	CITY,
	ZIP,
	PHONE,
	EXCHANGECODE,
	MEMBERCODE,
	LONG_NAME,
	L_ADDRESS1,
	L_ADDRESS2,
	L_ADDRESS3,
	L_CITY,
	L_ZIP,
	PAN_GIR_NO,
	ISNULL(U.MAPIDID, '') MAPINID,
	S.PARTY_CODE,
	TRANSACTIONCODE,
	TURNOVER,
	STT,
	REPLACE(CONVERT(VARCHAR, GETDATE(), 111), '/', '') CURDATE,
	CONVERT(VARCHAR, GETDATE(), 103) TODATE,
	REC_TYPE = 1
	INTO #STTCER
FROM #CLIENT C,
	 #STT00 S
LEFT JOIN UCC_CLIENT U
	ON (U.PARTY_CODE = S.PARTY_CODE)
WHERE S.PARTY_CODE = C.PARTY_CODE
	AND S.PARTY_CODE IN (
		SELECT PARTY_CODE
		FROM #STT00
		GROUP BY PARTY_CODE
		HAVING SUM(STT) > 0
		)
ORDER BY UPPER(S.PARTY_CODE),
	TRANSACTIONCODE
	
	SELECT 
		COMPANY,
		ADDR1,
		ADDR2,
		CITY,
		ZIP,
		PHONE,
		EXCHANGECODE,
		MEMBERCODE,
		LONG_NAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_ZIP,
		PAN_GIR_NO,
		MAPINID,
		PARTY_CODE=UPPER(LTRIM(RTRIM(PARTY_CODE))),
		TRANSACTIONCODE,
		TURNOVER=CONVERT(NUMERIC(18,2),ROUND(TURNOVER,2)),
		STT=CONVERT(NUMERIC(18,2),ROUND(STT,2)),
		CURDATE,
		TODATE,
		REC_TYPE
	FROM 
	    #STTCER
UNION ALL 
	SELECT 
		COMPANY,
		ADDR1,
		ADDR2,
		CITY,
		ZIP,
		PHONE,
		EXCHANGECODE,
		MEMBERCODE,
		LONG_NAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_ZIP,
		PAN_GIR_NO,
		MAPINID,
		PARTY_CODE=UPPER(LTRIM(RTRIM(PARTY_CODE))),
		TRANSACTIONCODE='TOTAL',
		TURNOVER=CONVERT(NUMERIC(18,2),ROUND(SUM(TURNOVER),2)),
		STT=CONVERT(NUMERIC(18,2),ROUND(SUM(STT),2)),
		CURDATE,
		TODATE,
		REC_TYPE=2
	FROM 
	    #STTCER 
	 GROUP BY 
	   COMPANY,
		ADDR1,
		ADDR2,
		CITY,
		ZIP,
		PHONE,
		EXCHANGECODE,
		MEMBERCODE,
		LONG_NAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_ZIP,
		PAN_GIR_NO,
		MAPINID,
		PARTY_CODE,
		CURDATE,
		TODATE
	 ORDER BY
	  PARTY_CODE,
	  REC_TYPE

GO
