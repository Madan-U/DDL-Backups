    SET @STRSQL = @STRSQL + ' SELECT ''' + @MARGINDATE + ''', ''' + @EXCHANGE_NEW + ''', ''' + @SEGMENT_NEW + ''', PARTY_CODE, LEDBAL '      
      
    SET @STRSQL = @STRSQL + ' FROM ' + @ACCOUNTSERVER + @ACCOUNTDB + '.DBO.FOMARGINNEW_LEDGERBAL WHERE MARGINDATE = ''' + @MARGINDATENEW + ''' '       
      
    EXEC (@STRSQL)      
      
      
      
   END      
      
      
      
   IF @CNT = 3      
      
