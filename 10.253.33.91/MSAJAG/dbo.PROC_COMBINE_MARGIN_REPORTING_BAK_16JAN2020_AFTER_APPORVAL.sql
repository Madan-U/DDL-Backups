    EXEC (@STRSQL)            
      
    FETCH NEXT FROM @MRGCUR INTO @STRSQL                
      
   END                 
      
   CLOSE @MRGCUR                
      
   DEALLOCATE @MRGCUR        
      
      
      
  END      
      
      
      
