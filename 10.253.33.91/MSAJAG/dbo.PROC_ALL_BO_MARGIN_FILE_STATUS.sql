CREATE PROCEDURE PROC_ALL_BO_MARGIN_FILE_STATUS ( @RPT_DATE VARCHAR(11),@REPORT VARCHAR(10))

AS ---- EXEC PROC_ALL_BO_MARGIN_FILE_STATUS 'OCT 04 2022'

CREATE TABLE #RPT (RPT_DATE DATETIME, REPORT VARCHAR(1000))

IF (SELECT COUNT(*) FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' ) <> 0
BEGIN
INSERT INTO #RPT
SELECT Report_Date=@RPT_DATE,Report='Exchange Cash Margin File - Updated'
END
ELSE
BEGIN
INSERT INTO #RPT
SELECT Report_Date=@RPT_DATE,Report='Exchange Cash Margin File - Pending'
END

IF (SELECT COUNT(*) FROM ANGELFO.NSEFO.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' ) <> 0
BEGIN
INSERT INTO #RPT
SELECT Report_Date=@RPT_DATE,Report='Exchange FNO Margin File - Updated'
END
ELSE
BEGIN
INSERT INTO #RPT
SELECT Report_Date=@RPT_DATE,Report='Exchange FNO Margin File - Pending'
END

IF (SELECT COUNT(*) FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' ) <> 0
BEGIN
INSERT INTO #RPT
SELECT Report_Date=@RPT_DATE,Report='Exchange Currency Margin File - Updated'
END
ELSE
BEGIN
INSERT INTO #RPT
SELECT Report_Date=@RPT_DATE,Report='Exchange Currency Margin File - Pending'
END

SELECT * FROM #RPT

GO
