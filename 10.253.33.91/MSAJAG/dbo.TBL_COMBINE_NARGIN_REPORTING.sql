-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC TBL_COMBINE_NARGIN_REPORTING
(@STATUSID VARCHAR(25),
@STATUSNAME VARCHAR(25),
@FROMDATE VARCHAR(11),
@TODATE VARCHAR(11),
@FROMPARTY VARCHAR(10),
@TOPARTY VARCHAR(10)
)

AS
SELECT MARGINDATE,EXCHANGE,SEGMENT,M.PARTY_CODE,TDAY_LEDGER,TDAY_MARGIN,TDAY_MTM,TDAY_CASHCOLL,TDAY_FDBG,TDAY_NONCASH,TDAY_MARGIN_SHORT,TDAY_MTM_SHORT,T1DAY_LEDGER,T1DAY_MARGIN,T1DAY_MTM,T1DAY_CASHCOLL,T1DAY_FDBG,T1DAY_NONCASH,T1DAY_MARGIN_SHORT,T1DAY_MTM_SHORT,T2DAY_LEDGER,T2DAY_MARGIN,T2DAY_MTM,T2DAY_CASHCOLL,T2DAY_FDBG,T2DAY_NONCASH,T2DAY_MARGIN_SHORT,T2DAY_MTM_SHORT,T_MARGINAVL,T_MTMAVL
INTO #TBL_COMBINE_NARGIN_REPORTING
FROM TBL_COMBINE_REPORTING_DETAIL M ,CLIENT_DETAILS C
WHERE M.MARGINDATE BETWEEN @FROMDATE AND @TODATE
AND M.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
AND M.PARTY_CODE=C.CL_CODE
AND @STATUSNAME =             
					  (CASE             
							WHEN @STATUSID = 'BRANCH' THEN C.BRANCH_CD            
							WHEN @STATUSID = 'SUBBROKER' THEN C.SUB_BROKER            
							WHEN @STATUSID = 'TRADER' THEN C.TRADER            
							WHEN @STATUSID = 'FAMILY' THEN C.FAMILY            
							WHEN @STATUSID = 'AREA' THEN C.AREA            
							WHEN @STATUSID = 'REGION' THEN C.REGION            
							WHEN @STATUSID = 'CLIENT' THEN C.CL_CODE
					  ELSE             
							'BROKER'            
					  END)    


SELECT * FROM #TBL_COMBINE_NARGIN_REPORTING

DROP TABLE #TBL_COMBINE_NARGIN_REPORTING

GO
