CREATE PROC [dbo].[CLS_GET_ASPMENU]
(
	@RPTCODE VARCHAR(100)
)
/*
	CLS_GET_ASPMENU 'BAL_CONFIRM'
*/
AS 
	SELECT C.FLDPATH FROM CLS_ASP_ACCESS C, (SELECT A.FLDREPORTCODE FROM CLS_TBLREPORTS A, TBLREPORTS B WHERE FLDREPORTPATH = FLDPATH AND A.FLDREPORTCODE = @RPTCODE) D WHERE C.FLDREPORTCODE = D.FLDREPORTCODE

GO
