---OB_DATA '2019028','OB'  
CREATE PROCEDURE OB_DATA  
(  
@SETT_NO VARCHAR(10),  
@SETT_TYPE VARCHAR(2))  
AS BEGIN  
  
SELECT 'NSE'AS SEGMENT,* FROM [AngelDemat].msajag.dbo.DELTRANS WITH(NOLOCK)WHERE ISETT_NO=@SETT_NO AND ISett_Type=@SETT_TYPE  
UNION   
SELECT 'NSE'AS SEGMENT,* FROM [AngelDemat].msajag.dbo.DELTRANS_REPORT WITH(NOLOCK)WHERE ISETT_NO=@SETT_NO AND ISett_Type=@SETT_TYPE  
UNION   
SELECT 'BSE'AS SEGMENT,* FROM [AngelDemat].BSEDB.dbo.DELTRANS WITH(NOLOCK)WHERE ISETT_NO=@SETT_NO AND ISett_Type=@SETT_TYPE  
UNION   
SELECT 'BSE'AS SEGMENT,* FROM [AngelDemat].BSEDB.dbo.DELTRANS_REPORT WITH(NOLOCK)WHERE ISETT_NO=@SETT_NO AND ISett_Type=@SETT_TYPE  
END

GO
