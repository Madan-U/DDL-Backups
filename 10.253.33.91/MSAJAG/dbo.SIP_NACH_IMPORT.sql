CREATE PROCEDURE [dbo].[SIP_NACH_IMPORT] (@FILENAME VARCHAR(1000), @UNAME VARCHAR(100))

AS ---- EXEC SIP_NACH_IMPORT 'TESTING_02.07.2021.CSV','E83107'

DECLARE @NSECMJV NVARCHAR(4000) = 'ACCOUNT.DBO.TBL_MFSS_SIP_NACH_TEMP'

DECLARE @NSECMBCP VARCHAR(4000) = 'BCP "' + @NSECMJV + '" IN "J:\BACKOFFICE\IMPORT\' + @FILENAME + '" -c -t"," -r"\n" -T'

TRUNCATE TABLE ACCOUNT.DBO.TBL_MFSS_SIP_NACH_TEMP

EXEC MASTER..XP_CMDSHELL @NSECMBCP, NO_OUTPUT

DELETE FROM ANGELFO.BBO_FA.DBO.TBL_MFSS_SIP_NACH_TEMP

INSERT INTO ANGELFO.BBO_FA.DBO.TBL_MFSS_SIP_NACH_TEMP
SELECT CLTCODE=RTRIM(LTRIM(CLTCODE)),VDT=CONVERT(DATETIME,VDT,105),BANKCODE='0'+RTRIM(LTRIM(BANKCODE)),
RELDATE=CONVERT(DATETIME,RELDATE,105),VNO=RTRIM(LTRIM(VNO)),EXCHANGE,VAMT,DRCR,DDNO,VTYP,'N',GETDATE(),@UNAME,0
FROM ACCOUNT.DBO.TBL_MFSS_SIP_NACH_TEMP


EXEC ANGELFO.BBO_FA.DBO.MFSS_AUTO_RECON_NACH_SIP

GO
