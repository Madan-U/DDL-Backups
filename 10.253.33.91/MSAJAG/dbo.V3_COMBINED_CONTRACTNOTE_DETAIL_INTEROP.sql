	DEALLOCATE @EXCHANGEWISE_CURSOR1
	



	SELECT @INTEROP		= VALUE FROM #INTEROP_SETT WHERE SEGMENT IN ('NSECAPITAL','BSECAPITAL')
	SELECT @INTEROPFNO	= VALUE FROM #INTEROP_SETT WHERE SEGMENT IN ('NSEFUTURES','BSEFUTURES')
