	AND LEFT(BFCF_FLAG, 2) <> (CASE WHEN @FNO_CONTRACT_BILL = 0 THEN 'BF' ELSE 'DF' END)
	AND EXISTS (SELECT EXCHANGE FROM #EXCHANGELIST E WHERE E.EXCHANGE = LTRIM(RTRIM(C.EXCHANGE))+LTRIM(RTRIM(C.SEGMENT)) )


