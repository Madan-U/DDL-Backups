				SET @STRSQL1 = @STRSQL1 + 'BEGIN '
				SET @STRSQL1 = @STRSQL1 + 'INSERT INTO #INTEROP_SETT '
				SET @STRSQL1 = @STRSQL1 + 'SELECT ''NSEFUTURES'',CLEARINGCODE FROM ' + @SHARESERVER1 + @SHAREDB1 + '.DBO.TBL_INTEROP_SETTING '
