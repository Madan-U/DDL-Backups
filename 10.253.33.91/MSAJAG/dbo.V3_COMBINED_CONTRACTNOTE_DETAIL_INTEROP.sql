UPDATE	#CONTRACT
SET		CONTRACTNO = ISNULL(C.CONTRACTNO,''),
		SETT_NO = ISNULL(C.SETT_NO,''),
		SETT_DATE = ISNULL(C.SETT_DATE,''),
		SETT_TYPE = (
		CASE
			WHEN C.SETT_TYPE = (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'D' ELSE 'N' END) THEN (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'N' ELSE 'D' END)
			WHEN C.SETT_TYPE = (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'C' ELSE 'W' END) THEN (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'W' ELSE 'C' END)
			WHEN C.SETT_TYPE = (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'AD' ELSE 'A' END) THEN (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'A' ELSE 'AD' END)
			WHEN C.SETT_TYPE = (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'AC' ELSE 'X' END) THEN (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'X' ELSE 'AC' END)
			ELSE C.SETT_TYPE
		END),
		EXCHANGE = (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'NSE' ELSE 'BSE' END),
		SETTTYPE_DESC = (
		CASE
			WHEN INTEROPCODE = 'NSCCL'	AND C.SETT_TYPE = 'D' THEN 'Normal'
			WHEN INTEROPCODE = 'NSCCL'	AND C.SETT_TYPE = 'C' THEN 'Trade For Trade'
			WHEN INTEROPCODE = 'NSCCL'	AND C.SETT_TYPE = 'A' THEN 'Auction'
			WHEN INTEROPCODE = 'NSCCL'	AND C.SETT_TYPE = 'X' THEN 'Auction Trade For Trade'
			WHEN INTEROPCODE = 'ICCl'	AND C.SETT_TYPE = 'N' THEN 'Rolling Market'
			WHEN INTEROPCODE = 'ICCl'	AND C.SETT_TYPE = 'W' THEN 'Odd Lot'
			WHEN INTEROPCODE = 'ICCl'	AND C.SETT_TYPE = 'AD' THEN 'Auction Rolling'
			WHEN INTEROPCODE = 'ICCl'	AND C.SETT_TYPE = 'AC' THEN 'Auction Odd Lot'
			ELSE C.SETTTYPE_DESC
		END)
FROM	#CONTDATA C
WHERE	LEFT(CONVERT(DATETIME,#CONTRACT.SAUDA_DATE,103),11) = LEFT(C.SAUDA_DATE,11)
		AND #CONTRACT.PARTY_CODE = C.PARTY_CODE
		AND #CONTRACT.EXCHANGE = (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'BSE' ELSE 'NSE' END)
		AND #CONTRACT.SEGMENT = 'CAPITAL'
		AND C.EXCHANGE = (CASE WHEN INTEROPCODE = 'NSCCL' THEN 'NSE' ELSE 'BSE' END)
		AND C.SEGMENT = 'CAPITAL'
		AND C.SETT_TYPE IN ('N','W','D','C','AD','AC','A','X','M','Z')
		AND #CONTRACT.SETT_TYPE IN ('N','W','D','C','AD','AC','A','X')
		AND INTEROPCODE <> ''


