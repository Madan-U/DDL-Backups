	FOR
	SELECT	DISTINCT 
			EXCHANGE,
			SHAREDB,
			SHARESERVER
	FROM	#EXCHANGELIST M(NOLOCK)
	WHERE	EXCHANGE IN ('BSECAPITAL','NSECAPITAL','BSEFUTURES','NSEFUTURES','NSXFUTURES','BSXFUTURES')
		
	OPEN @EXCHANGEWISE_CURSOR1

	FETCH NEXT
	FROM @EXCHANGEWISE_CURSOR1
	INTO
		@EXCHANGE_NEW1,
		@SHAREDB1,
		@SHARESERVER1

	WHILE @@FETCH_STATUS = 0
	BEGIN
		SET @STRSQL1 = ''
		IF @SHARESERVER1 = @@SERVERNAME 
		BEGIN
			SET @SHARESERVER1 = ''
		END
		ELSE
		BEGIN
			SET @SHARESERVER1 = @SHARESERVER1 + '.'
		END
		
		BEGIN
			
			IF (@EXCHANGE_NEW1 = 'NSECAPITAL')
			BEGIN
				SET @STRSQL1 = 'IF (SELECT COUNT(1) FROM ' + @SHARESERVER1 + @SHAREDB1 + '.SYS.TABLES WHERE NAME=''TBL_INTEROP_SETTING'') > 0 '
