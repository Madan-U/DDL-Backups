CREATE PROCEDURE [dbo].[B2B_B2C_BIF]
(
@CL_cODE VARCHAR(20)
)
AS 
BEGIN 

SELECT CL_CODE,(CASE WHEN B2C='Y'THEN 'B2C'ELSE 'B2B'END)AS B2B_B2C
 FROM INTRANET.RISK.DBO.CLIENT_DETAILS
WHERE CL_CODE=@CL_cODE
END

GO
