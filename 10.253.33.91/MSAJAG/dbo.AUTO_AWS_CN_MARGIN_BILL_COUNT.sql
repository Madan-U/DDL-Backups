CREATE PROC [dbo].[AUTO_AWS_CN_MARGIN_BILL_COUNT]   
( @EXCHANGE VARCHAR(20)  
)  
AS  
 BEGIN  
 IF @EXCHANGE ='MARGIN_STATEMENT'   
 BEGIN   
   SELECT COUNT(DISTINCT PARTY_CODE) AS STATUS FROM MARGIN_PROCESS WHERE SNO=1  
 END  
IF @EXCHANGE ='COMM_CONTRACT_NOTE'   
 BEGIN   
   SELECT COUNT(DISTINCT PARTY_CODE) AS STATUS FROM [AngelCommodity].MCDX.DBO.COMM_BULK_PROCESS WHERE SNO=1  
 END  
IF @EXCHANGE ='COMM_BILL'   
 BEGIN   
   SELECT COUNT(DISTINCT PARTY_CODE) AS STATUS FROM [AngelCommodity].MCDX.DBO.COMM_BILL_PROCESS WHERE SNO=1  
 END  
IF @EXCHANGE ='CURR'   
 BEGIN   
   SELECT COUNT(DISTINCT PARTY_CODE) AS STATUS FROM CUR_BULK_PROCESS-- WHERE SNO=1  
 END  
 IF @EXCHANGE ='FO'   
 BEGIN   
   SELECT COUNT(DISTINCT PARTY_CODE) AS STATUS FROM NSE_BULK_PROCESS WHERE SNO=1  
 END  
 IF @EXCHANGE ='CASHFO'   
 BEGIN   
   SELECT COUNT(DISTINCT PARTY_CODE) AS STATUS FROM NSE_BULK_PROCESS WHERE SNO=1  
 END  
 IF @EXCHANGE ='CASH'   
 BEGIN   
   SELECT COUNT(DISTINCT PARTY_CODE) AS STATUS FROM NSE_BULK_PROCESS WHERE SNO=1  
 END      
 IF @EXCHANGE IS NULL  
  
 BEGIN   
  
  SELECT ' PROCESS TESTED COMPLETED SUCCESSFULLY.....!!!!' AS [STATUS]  
  
 END  
  
   
 END

GO
