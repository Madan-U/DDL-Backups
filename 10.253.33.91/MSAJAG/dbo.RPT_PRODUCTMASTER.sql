CREATE PROCEDURE RPT_PRODUCTMASTER
(  
 @FROMDATE VARCHAR(11), @TODATE VARCHAR(11),                 
 @FROMPARTY VARCHAR(10), @TOPARTY VARCHAR(10)                
)
--EXEC RPT_PRODUCTMASTER 'dec  1 2007','dec  1 2010','0','zzzz'                                     
AS                         
SELECT   
	MAINPARTY_CODE,
	SUBPARTY_CODE,
	USER_ID,
	DATEFROM=CONVERT(VARCHAR,DATEFROM,103),
	DATETO=CONVERT(VARCHAR,DATETO,103),
	ADDEDBY,
	ADDEDON=CONVERT(VARCHAR,ADDEDON,103)
FROM TBLPRODUCTMAPPING PM
WHERE 
 DATEFROM BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'                      
 AND MAINPARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY

GO
