CREATE PROC [dbo].[CLS_GET_CLIENTDETAILS] (
	@FR_CLTCODE VARCHAR(10)
	,@TO_CLTCODE VARCHAR(10)
	,@STATUSID VARCHAR(25)
	,@STATUSNAME VARCHAR(30)
	,@GROUP_CODE VARCHAR(20)
	,@RETCOLS VARCHAR(500) = '*'
	,@WHERECLAUSE VARCHAR(500)
	,@ENTITY VARCHAR(25) = ''
	,@ENTITY_LIST  VARCHAR(500) = ''
	)
AS
/*
select distinct area from client_Details
EXEC CLS_GET_CLIENTDETAILS '0','ZZ','broker','broker','','CL_CODE,BRANCH_CD,SUB_BROKER,AREA','SUB_BROKER LIKE ''%h%''','AREA','FORT'
*/
BEGIN
	SET @RETCOLS = CASE WHEN @RETCOLS = '' THEN '*' ELSE @RETCOLS END
	IF @FR_CLTCODE = ''	
		SET @FR_CLTCODE = '0'
		
	IF @TO_CLTCODE = ''	
		SET @TO_CLTCODE = 'ZZZZZZZZ'		
	
	DECLARE @@SQL VARCHAR(MAX)
		,@STATUSID_COL VARCHAR(25)
		
	--SELECT @STATUSID_COL =''
	 --MSAJAG.[DBO].[CLS_GET_ENTITY_FIELD](@STATUSID)	
	 SELECT @STATUSID_COL = MSAJAG.[DBO].[CLS_GET_ENTITY_FIELD](@STATUSID)

	SET @@SQL = " SELECT A." + @RETCOLS + " FROM "
	SET @@SQL = @@SQL + " CLIENT_DETAILS A "

	IF @GROUP_CODE <> ''
		AND @GROUP_CODE <> '0'
	BEGIN
		SET @@SQL = @@SQL + " INNER JOIN CLS_TBLGROUP_MASTER B ON "
		SET @@SQL = @@SQL + " A.SUB_CATEGORY = B.SUBCATEGORY "
		SET @@SQL = @@SQL + " WHERE "
		SET @@SQL = @@SQL + " A.SUB_CATEGORY <> '' AND B.GROUP_CODE = '" + @GROUP_CODE + "' AND "
	END
	ELSE
	BEGIN
		SET @@SQL = @@SQL + " WHERE "
	END

	SET @@SQL = @@SQL + "A.CL_CODE >= '" + @FR_CLTCODE + "' AND A.CL_CODE <=  '" + @TO_CLTCODE + "'"

	IF @STATUSID <> 'BROKER'
	BEGIN
		--SET @@SQL = @@SQL + " AND " + @STATUSID_COL + " = '" + "'" + @STATUSNAME + "'"
		SET @@SQL = @@SQL + " AND " + @STATUSID_COL + " = '" + @STATUSNAME + "'"
		--SET @@SQL = @@SQL + "A.CL_CODE >= '" + @FR_CLTCODE + "' AND A.CL_CODE <=  '" + @TO_CLTCODE + "'"
		PRINT @@SQL
	END
	IF @ENTITY_LIST <> ''
	BEGIN
	PRINT 'AA'
		DECLARE @ENTITY_COL VARCHAR(25)
		SELECT @ENTITY_COL = MSAJAG.[DBO].[CLS_GET_ENTITY_FIELD](@ENTITY)		
		SET @@SQL = @@SQL + " AND " + @ENTITY_COL + " IN ('" + REPLACE(@ENTITY_LIST,',',''',''') + "') "
		
	END 
	IF @WHERECLAUSE <> ''
	BEGIN
	PRINT 'BB'
		SET @@SQL = @@SQL + " AND " + @WHERECLAUSE
	END
	PRINT @@SQL

	EXEC (@@SQL)
END

GO
