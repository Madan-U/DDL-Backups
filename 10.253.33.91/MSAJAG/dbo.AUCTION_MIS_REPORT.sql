CREATE PROCEDURE AUCTION_MIS_REPORT (@FDATE DATETIME,@TDATE DATETIME)

AS --- EXEC AUCTION_MIS_REPORT 'JUN 20 2021','JUN 30 2021'

SELECT SAUDA_DATE,SELL_BUY=CASE WHEN SELL_BUY=1 THEN 'PAYIN' ELSE 'PAYOUT' END,
PAYINPAYOUT=COUNT(*) INTO #ATRADE
FROM MSAJAG.DBO.SETTLEMENT WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN @FDATE AND @TDATE + ' 23:59' 
AND SETT_TYPE IN ('A','X') AND AUCTIONPART IN ('FC','FS')
GROUP BY SAUDA_DATE,SELL_BUY

--DROP TABLE #ADATA
SELECT SAUDA_DATE,[PAYIN], [PAYOUT],TOTAL = [PAYIN]+[PAYOUT],UCC=CONVERT(INT,'') INTO #ADATA
FROM (
SELECT SAUDA_DATE,SELL_BUY,PAYINPAYOUT FROM #ATRADE) AS SourceTable
PIVOT
(SUM(PAYINPAYOUT) FOR SELL_BUY IN ([PAYIN], [PAYOUT])) AS PivotTable

UPDATE #ADATA SET UCC = DCOUNT FROM #ADATA A,
(SELECT SAUDA_DATE,DCOUNT=COUNT (DISTINCT PARTY_CODE) FROM MSAJAG.DBO.SETTLEMENT WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN @FDATE AND @TDATE + ' 23:59' AND SETT_TYPE IN ('A','X') AND AUCTIONPART IN ('FC','FS')
GROUP BY SAUDA_DATE ) B WHERE A.SAUDA_DATE = B.SAUDA_DATE

SELECT [Sauda Date]=CONVERT(VARCHAR(11),SAUDA_DATE,105),
[For Payin]=PAYIN,[For Payout]=PAYOUT,[Grand Total]=TOTAL,UCC 
FROM #ADATA ORDER BY 1

GO
