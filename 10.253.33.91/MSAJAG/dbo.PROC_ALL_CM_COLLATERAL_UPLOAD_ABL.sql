CREATE PROC [dbo].[PROC_ALL_CM_COLLATERAL_UPLOAD_ABL] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @RECCNT	INT

TRUNCATE TABLE TBL_ALL_ELIGIBLE_QTY_CM 

INSERT INTO TBL_ALL_ELIGIBLE_QTY_CM
SELECT CONVERT(DATETIME,@TDATE,103),
LTRIM(RTRIM(DBO.PIECE(FILE_DATA,',',1))) CM_COME,
LTRIM(RTRIM(DBO.PIECE(FILE_DATA,',',2))) TM_COME,
LTRIM(RTRIM(DBO.PIECE(FILE_DATA,',',3))) PARTY_CODE,
LTRIM(RTRIM(DBO.PIECE(FILE_DATA,',',4))) ISIN,
LTRIM(RTRIM(DBO.PIECE(FILE_DATA,',',5))) SYMBOL,
DBO.PIECE(FILE_DATA,',',6) TOTAL_QTY,
DBO.PIECE(FILE_DATA,',',7) ELIGIBLE_QTY,0 
FROM FILEDATA WHERE PROGID = @PROGID AND DBO.PIECE(FILE_DATA,',',2) NOT IN ('TM Code','')

UPDATE TBL_ALL_ELIGIBLE_QTY_CM SET DIFF_QTY = TOTAL_QTY - ELIGIBLE_QTY

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=4 AND REPORT_DATE = CONVERT(DATETIME,@TDATE,103)

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA WHERE PROGID = @PROGID

DELETE FROM FILEDATA WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
