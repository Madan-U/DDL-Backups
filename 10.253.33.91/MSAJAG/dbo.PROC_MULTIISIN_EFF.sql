-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------




CREATE PROC [dbo].[PROC_MULTIISIN_EFF]
AS

SELECT SCRIP_CD, SERIES, ISIN, FLAG = 0 
INTO #DATA
FROM SCRIP_IMP_TMP 
WHERE ISIN LIKE 'IN%'
AND FILLER1 = '6'
AND LEN(ISIN) = 12

SELECT ISIN
INTO #DUPLI FROM #DATA
GROUP BY ISIN
HAVING COUNT(1) > 1

DELETE FROM #DATA
WHERE EXISTS (SELECT ISIN FROM #DUPLI WHERE #DATA.ISIN = #DUPLI.ISIN)

UPDATE #DATA SET FLAG = 1
FROM MULTIISIN_NEW
WHERE MULTIISIN_NEW.SCRIP_CD = #DATA.SCRIP_CD
AND MULTIISIN_NEW.SERIES = #DATA.SERIES
AND MULTIISIN_NEW.ISIN = #DATA.ISIN
AND EFF_TO >= GETDATE()

UPDATE MULTIISIN_NEW SET EFF_TO = LEFT(GETDATE()-1,11) + ' 23:59', VALID = 0 
FROM #DATA
WHERE MULTIISIN_NEW.SCRIP_CD = #DATA.SCRIP_CD
AND MULTIISIN_NEW.SERIES = #DATA.SERIES
AND EFF_TO >= GETDATE()
AND FLAG = 0

UPDATE MULTIISIN_NEW SET EFF_TO = LEFT(GETDATE()-1,11) + ' 23:59', VALID = 0 
FROM #DATA
WHERE MULTIISIN_NEW.ISIN = #DATA.ISIN
AND EFF_TO >= GETDATE()
AND FLAG = 0

INSERT INTO MULTIISIN_NEW
SELECT SCRIP_CD, SERIES, ISIN, VALID = 1, EFF_FROM=LEFT(GETDATE(),11), EFF_TO='DEC 31 2049 23:59'
FROM #DATA
WHERE FLAG = 0

DROP TABLE #DATA
DROP TABLE #DUPLI

GO
