-- Object: PROCEDURE dbo.CLS_GET_USER_VALIDATE_BKP_01OCT2022
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROCEDURE [dbo].[CLS_GET_USER_VALIDATE_BKP_01OCT2022] (
	@USERNAME VARCHAR(30),
	@PASSWORD VARCHAR(60),
	@IPADDRESS VARCHAR(100),
	@MACID VARCHAR(100)
	)
AS
DECLARE @FIRSTLOGIN INT,
	@ERRCODE INT,
	@ERRMSG VARCHAR(8000)
DECLARE @FLDUSERNAME VARCHAR(30),
	@FLDCATEGORY VARCHAR(30),
	@FLDSTNAME VARCHAR(30),
	@FLDPWDEXPIRY INT,
	@FLDATTEMPTCNT INT,
	@FLDSTATUS INT,
	@FLDLOGINFLAG INT,
	@FLDACCESSLVL CHAR(1),
	@FLDIPADD VARCHAR(100),
	@FLDFIRSTLOGIN CHAR(1),
	@FLDFORCELOGOUT INT,
	@LOOPI INT,
	@SPLITIP VARCHAR(20),
	
  @IPLOGIN BIT,
	@USERID INT,
	@PWD VARCHAR(50),
	@LASTLOGIN_LOG VARCHAR(30),
	@FLDADMIN VARCHAR(30),
	@PWD_EXPIRY_DATE VARCHAR(100),
	@FLD_MAC_ID VARCHAR(100),
	@TODAY VARCHAR(10),
	@GROUPID VARCHAR(20)

SET @ERRCODE = 0
SET @ERRMSG = ''
SET @FIRSTLOGIN = 0
SET @FLDUSERNAME = ''

SET @FLDCATEGORY = ''
SET @FLDSTNAME = ''
SET @LASTLOGIN_LOG = ''
SET @FLDADMIN = ''
SET @PWD_EXPIRY_DATE = ''
SET @USERID = ''
SET @GROUPID = 0
	

IF (
		SELECT COUNT(1)
		FROM CLS_tblPradnyaUsers_VW(NOLOCK)
		WHERE FLDUSERNAME = @USERNAME
			AND FLDPASSWORD = CASE WHEN @PASSWORD = '' THEN FLDPASSWORD ELSE @PASSWORD END
		) = 
0
BEGIN
	SET @ERRCODE = - 1
	SET @ERRMSG = 'INVALID USER ID / PASSWORD'

	INSERT INTO V2_LOGIN_LOG (
		USERID,
		USERNAME,
		STATUSNAME,
		STATUSID,
		IPADD,
		ACTION,
		ADDDT
		)
	VALUES (
		@USERID,
		@FLDUSERNAME,
		@USERNAME,
		@FLDCATEGORY,
		@IPADDRESS,
		'LOG_FL',
		GETDATE()
		)

	SELECT FLDUSERNAME = @USERNAME,
		FLDCATEGORY = @FLDCATEGORY,
		FLDSTNAME = @FLDSTNAME,
		FIRSTLOGIN = @FIRSTLOGIN,
		ERRCODE = @ERRCODE,
		ERRMSG = @ERRMSG,
		LASTLOGIN_LOG = @LASTLOGIN_LOG,
		FLDADMIN = @FLDADMIN,
		PWD_EXPIRY_DATE = @PWD_EXPIRY_DATE,
		FLDUSERID = @USERID,
		SQLSCHEMA = '',FLDCATEGORYNAME='',TODAY = '', GROUPID = ''

	RETURN
END

SELECT @USERID = A.FLDAUTO,
	@PWD = A.FLDPASSWORD,
	@FLDUSERNAME = FLDUSERNAME,
	@FLDCATEGORY = C.FLDCATEGORYCODE,
	@FLDSTNAME = A.FLDSTNAME,
	@FLDPWDEXPIRY = CASE 
		WHEN A.PWD_EXPIRY_DATE < GETDATE()
			THEN 1
		ELSE 0
		END,
	@FLDATTEMPTCNT = CASE 
		WHEN B.FLDATTEMPTCNT > B.FLDMAXATTEMPT
			THEN 1
		ELSE 0
		END,
	@FLDSTATUS = B.FLDSTATUS,
	@FLDLOGINFLAG = B.FLDLOGINFLAG,
	@FLDACCESSLVL = B.FLDACCESSLVL,
	@FLDIPADD = B.FLDIPADD,
	@FLD_MAC_ID = LTRIM(RTRIM(B.FLD_MAC_ID)),
	@FLDFIRSTLOGIN = B.FLDFIRSTLOGIN,
	@FLDFORCELOGOUT = B.FLDFORCELOGOUT,
	@FLDADMIN = D.FLDSTATUS,
	@GROUPID =  A.GROUP_ID
FROM CLS_tblPradnyaUsers_VW A(NOLOCK)
LEFT JOIN TBLUSERCONTROLMASTER B(NOLOCK
)
	ON (A.FLDAUTO = B.FLDUSERID),
		TBLCATEGORY C(NOLOCK),
		TBLADMIN D(NOLOCK)
WHERE C.FLDCATEGORYCODE = A.FLDCATEGORY
	AND C.FLDADMINAUTO = A.FLDADMINAUTO
	AND D.FLDAUTO_ADMIN = A.FLDADMINAUTO
	AND FLDUSERNAME = @USERNAME

SET @FIRSTLOGIN = CASE 
		WHEN 
@FLDFIRSTLOGIN = 'N'
			THEN 0
		ELSE 1
		END

IF @FLDPWDEXPIRY > 0
BEGIN
	SET @ERRCODE = - 1
	SET @ERRMSG = 'ACCESS DENIED. YOUR PASSWORD IS EXPIRED'

	SELECT FLDUSERNAME = @FLDUSERNAME,
		FLDCATEGORY = @FLDCATEGORY,
		FLDSTNAME = @FLDSTNAME,
		FIRSTLOGIN = @FIRSTLOGIN,
		ERRCODE = @ERRCODE,
		ERRMSG = @ERRMSG,
		LASTLOGIN_LOG = @LASTLOGIN_LOG,
		FLDADMIN = @FLDADMIN,
		PWD_EXPIRY_DATE = @PWD_EXPIRY_DATE,
		FLDUSERID = @USERID,
		SQLSCHEMA = '',FLDCATEGORYNAME='',TODAY = '', GROUPID = ''

	INSERT INTO V2_LOGIN_LOG (
	
	USERID,
		USERNAME,
		STATUSNAME,
		STATUSID,
		IPADD,
		ACTION,
		ADDDT
		)
	VALUES (
		@USERID,
		@FLDUSERNAME,
		@USERNAME,
		@FLDCATEGORY,
		@IPADDRESS,
		'LOG_FL',
		GETDATE()
		)

	RETURN
END

IF @FLDSTATUS > 0
	OR @FLDATTEMPTCNT > 0
	OR @FLDFORCELOGOUT > 0
BEGIN
	SET @ERRCODE = - 1
	SET @ERRMSG = 'ACCESS DENIED. LOGIN ID DISABLED'

	SELECT FLDUSERNAME = @FLDUSERNAME,
		FLDCATEGORY = @FLDCATEGORY,
		FLDSTNAME = @FLDSTNAME,
		FIRSTLOGIN = @FIRSTLOGIN,
		ERRCODE = @ERRCODE,
		ERRMSG = @ERRMSG,
		LASTLOGIN_LOG = @LASTLOGIN_LOG,
		FLDADMIN = @FLDADMIN,
		PWD_EXPIRY_DATE = @PWD_EXPIRY_DATE,
		FLDUSERID = @USERID,
		SQLSCHEMA = '',FLDCATEGORYNAME='',TODAY = '', GROUPID = ''

	INSERT INTO V2_LOGIN_LOG (
		USERID,
		USERNAME,
		STATUSNAME,
		STATUSID,
		IPADD,
		ACTION,

		ADDDT
		)
	VALUES (
		@USERID,
		@FLDUSERNAME,
		@USERNAME,
		@FLDCATEGORY,
		@IPADDRESS,
		'LOG_FL',
		GETDATE()
		)

	RETURN
END

IF @FLDACCESSLVL = 'L'
BEGIN
	SET @IPLOGIN = 0

	IF LEN(@FLDIPADD) > 0
	BEGIN
		SET @LOOPI = 1

		WHILE 1 = 1
		BEGIN
		
	SET @SPLITIP =.dbo.CLS_Piece(@FLDIPADD, '|', @LOOPI)

			IF @SPLITIP IS NULL
				OR @SPLITIP = ''
			BEGIN
				BREAK
			END

			IF LEFT(LTRIM(RTRIM(@IPADDRESS)), LEN(LTRIM(RTRIM(@SPLITIP)))) = LTRIM(RTRIM(@SPLITIP))
			BEGIN
				SET @IPLOGIN = 1

				BREAK
	
		END

			SET @LOOPI = @LOOPI + 1
		END
	END
	
	IF @IPLOGIN = 0
	BEGIN 
		IF @FLD_MAC_ID <> '' AND UPPER(@FLD_MAC_ID) = UPPER(@MACID)
		BEGIN
			SET @IPLOGIN = 1
		END
	END
	
	IF @IPLOGIN = 0
	BEGIN
		SET @ERRCODE = - 1
		SET @ERRMSG = 'ACCESS DENIED. YOUR IP ADDRESS / MAC ID IS NOT REGISTERED IN THE SYSTEM'

		SELECT FLDUSERNAME = @FLDUSERNAME,
			FLDCATEGORY = @FLDCATEGORY,
			FLDSTNAME = @FLDSTNAME,
			FIRSTLOGIN = @FIRSTLOGIN,
			ERRCODE = @ERRCODE,
			ERRMSG = @ERRMSG,
			LASTLOGIN_LOG = @LASTLOGIN_LOG,
			FLDADMIN = @FLDADMIN,
			PWD_EXPIRY_DATE = @PWD_EXPIRY_DATE,
		  FLDUSERID = @USERID,
		  SQLSCHEMA = '',FLDCATEGORYNAME='',TODAY = '', GROUPID = ''

		INSERT INTO V2_LOGIN_LOG (
			USERID,
			USERNAME,
			STATUSNAME,
			STATUSID,
			IPADD,
			ACTION,
			ADDDT
	
		)
		VALUES (
			@USERID,
			@FLDUSERNAME,
			@USERNAME,
			@FLDCATEGORY,
			@IPADDRESS,
			'LOG_FL',
			GETDATE()
			)

		RETURN
	END
END

SELECT @LASTLOGIN_LOG = CONVERT(VARCHAR(20), ADDDT, 113)
FROM V2_LOGIN_LOG(NOLOCK)
WHERE SNO = (
		SELECT MAX(SNO)

		FROM V2_LOGIN_LOG(NOLOCK)
		WHERE USERID = @USERID
		)

INSERT INTO V2_LOGIN_LOG (
	USERID,
	USERNAME,
	STATUSNAME,
	STATUSID,
	IPADD,
	ACTION,
	ADDDT
	)
VALUES (
	@USERID,
	@FLDUSERNAME,
	@USERNAME,
	@FLDCATEGORY,
	@IPADDRESS,
	'LOG_FL',
	GETDATE()
	)


SELECT @PWD_EXPIRY_DATE = 'PASSWORD EXPIRES ON - ' + CASE 
		WHEN DATEDIFF(DD, GETDATE(), T.PWD_EXPIRY_DATE) = 2
			THEN 'THE DAY AFTER TOMORROW'
		ELSE CASE 
				WHEN DATEDIFF(DD, GETDATE(), T.PWD_EXPIRY_DATE) = 1
					THEN 'TOMORROW'
				ELSE CASE 
		
				WHEN DATEDIFF(DD, GETDATE(), T.PWD_EXPIRY_DATE) = 0
							THEN '<U>TODAY</U>'
						ELSE LEFT(CONVERT(VARCHAR, T.PWD_EXPIRY_DATE, 109), 11)
						END
				END
		END
FROM CLS_tblPradnyaUsers_VW T,
	TBLADMIN A
WHERE T.FLDADMINAUTO = A.Fldauto_Admin	AND T.FLDUSERNAME = @FLDUSERNAME		AND GETDATE() <= T.PWD_EXPIRY_DATE
 
 DECLARE @FLDCATEGORYNAME VARCHAR(50)
 
 SELECT @FLDCATEGORYNAME =FLDCATEGORYNAME FROM TBLCATEGORY WHERE FLDCATEGORYCODE= @FLDCATEGORY
 

SELECT FLDUSERNAME = @FLDUSERNAME,
	FLDCATEGORY = @FLDCATEGORY,
	FLDSTNAME = @FLDSTNAME,		-- STATUSNAME
	FIRSTLOGIN = @FIRSTLOGIN,
	ERRCODE = @ERRCODE,
	ERRMSG = @ERRMSG,
	LASTLOGIN_LOG = @LASTLOGIN_LOG,
	FLDADMIN = @FLDADMIN,		-- STATUSID
	PWD_EXPIRY_DATE = @PWD_EXPIRY_DATE,
	FLDUSERID = @USERID,
	SQLSCHEMA = '',
	FLDCATEGORYNAME= @FLDCATEGORYNAME, TODAY = CONVERT(VARCHAR(10),GETDATE(),103), GROUPID = @GROUPID
	/*------------------------------ END OF THE PROC ---------------------------------*/

GO
