INTO #SEGLED
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY WITH (NOLOCK)
WHERE PROCESS_DT = @RUNDATE  
AND EDT_LEDGER > 0 
AND EXCHANGE <> 'MTF' 
AND 1 = 2 

SELECT @MAXDATE = ISNULL(MAX(TRANSDATE),@RUNDATE) FROM TBL_CMCC_DATA
WHERE TRANSDATE <= @RUNDATE 

ALTER TABLE #SEGLED
ADD SNO INT IDENTITY(1,1)

CREATE CLUSTERED INDEX IDXDT ON #SEGLED
(
	SNO
)

CREATE INDEX #PARTY ON #SEGLED (PARTY_CODE,EXCHANGE,SEGMENT)

