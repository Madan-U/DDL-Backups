	CASH_WCM = C.CASH_WCM  
	FROM #SEGLED C WITH(NOLOCK),CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY CO WITH(NOLOCK)
	WHERE PROCESS_DT = @RUNDATE
	AND C.PARTY_CODE = CO.PARTY_CODE
	AND C.EXCHANGE = CO.EXCHANGE
	AND C.SEGMENT = CO.SEGMENT
	FETCH NEXT FROM @SEGCUR INTO @EXCHANGE, @SEGMENT, @FDAMOUNT, @ACCOUNT_TYPE
END
