			CASH_WNCL = CASH_WNCL + @LEDBAL
			WHERE SNO = @SNO

			SET @FDAMOUNT = @FDAMOUNT - @LEDBAL
		END	
		IF @FDAMOUNT <= 0 
		BEGIN
			BREAK;
		END
		FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @LEDBAL	 
	END
