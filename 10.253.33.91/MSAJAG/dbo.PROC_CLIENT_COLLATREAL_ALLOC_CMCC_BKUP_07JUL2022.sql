	AND PARTY_CODE= @PARTY_CODE

	UPDATE #SEGLED SET CASH_RET_TM = (CASE WHEN CASH_RET_TM - @FDAMOUNT < 0 THEN 0 ELSE CASH_RET_TM - @FDAMOUNT END),
			CASH_WCM = CASH_WCM + @FDAMOUNT,
			CASH_WNCL = CASH_WNCL + @FDAMOUNT
	WHERE PARTY_CODE = @PARTY_CODE		 
	
