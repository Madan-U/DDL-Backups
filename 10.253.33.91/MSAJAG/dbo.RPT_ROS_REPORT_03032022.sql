				SUB_BROKER	= ISNULL(C.SUB_BROKER,''),
				GST_NO		= ISNULL(C.GST_NO,''),
				GST_LOCATION = ISNULL(C.GST_LOCATION,'')				 
		FROM	TBL_ROS_DATA T(NOLOCK),
				CLIENT_DETAILS C (NOLOCK)
		WHERE	--TRANS_DATE >= @FROMDATE AND 
				TRANS_DATE <= @TODATE + ' 23:59:59'
				AND T.UCC_CODE >= @FROMPARTY AND  T.UCC_CODE <= @TOPARTY
				AND T.UCC_CODE = C.CL_CODE
				AND C.BRANCH_CD >= @FROMBRANCH 
				AND C.BRANCH_CD <= @TOBRANCH 
				AND C.SUB_BROKER >= @FROMSUBBROKER 
				AND C.SUB_BROKER <= @TOSUBBROKER 
				AND @STATUSNAME = (
				CASE 
					WHEN @STATUSID = 'BRANCH' THEN BRANCH_CD 
					WHEN @STATUSID = 'SUBBROKER' THEN SUB_BROKER 
					WHEN @STATUSID = 'TRADER' THEN TRADER 
					WHEN @STATUSID = 'FAMILY' THEN FAMILY 
					WHEN @STATUSID = 'AREA' THEN AREA 
					WHEN @STATUSID = 'REGION' THEN REGION 
					WHEN @STATUSID = 'CLIENT' THEN T.UCC_CODE 
					ELSE 'BROKER' 
				END)
				AND EXISTS (SELECT PARTYCODE FROM #PARTY WHERE #PARTY.PARTYCODE = T.UCC_CODE)

