CREATE PROC [dbo].[PROV_SMS_RECO](@FROMDATE DATETIME,@FROMPARTY VARCHAR(10) = 'A000000000',@TOPARTY VARCHAR(10) = 'ZZZZZZZZZZ')  
  
AS  
  
--DECLARE @FROMDATE DATETIME = 'MAY 25 2022'  
  
DECLARE @FROMDATE_SMS varchar(50) = FORMAT(@FROMDATE,'dd/MM/yyyy')   
   
 SELECT [DATE],PARTY_CODE,AMOUNT  INTO #SMS_DATA_MARGIN FROM [AngelFO].NSEFO.DBO.SMS_DATA_MARGIN   
 WHERE 1 <> 1  
  
IF (SELECT COUNT(1) FROM [AngelFO].NSEFO.DBO.SMS_DATA WHERE [DATE] = @FROMDATE_SMS ) > 1  
  BEGIN   
  INSERT INTO #SMS_DATA_MARGIN  
  SELECT [DATE],PARTY_CODE,AMOUNT FROM [AngelFO].NSEFO.DBO.SMS_DATA WHERE [DATE] = @FROMDATE_SMS  
  END  
ELSE  
 BEGIN  
 --SELECT * INTO #SMS_DATA_MARGIN FROM [AngelFO].NSEFO.DBO.SMS_DATA_MARGIN WHERE   [DATE] = @FROMDATE_SMS  
 INSERT INTO  #SMS_DATA_MARGIN  
 SELECT [DATE],PARTY_CODE,AMOUNT FROM [AngelFO].NSEFO.DBO.SMS_DATA_MARGIN WHERE   [DATE] = @FROMDATE_SMS  
  
 END  
  
  
   SELECT PARTY_CODE,SUM(LEDGER_EOD) AS LEDGER_EOD,SUM(LEDGER_PEAK) AS LEDGER_PEAK,SUM(TDAY_MARGIN_SHORT)TDAY_MARGIN_SHORT,SUM(TDAY_MTM_SHORT)TDAY_MTM_SHORT,    
   SUM(TOTAL_MARGIN) TOTAL_MARGIN,SUM(PEAK_MARGIN) AS PEAK_MARGIN     
   ,MARGINDATE ,SUM(SMS_SHRT) AS SMS_SHRT ,TDAY_CASHCOLL_EOD = SUM(TDAY_CASHCOLL_EOD),TDAY_NONCASH_EOD = SUM(TDAY_NONCASH_EOD)  
   ,TDAY_CASHCOLL_PEAK = SUM(TDAY_CASHCOLL_PEAK),TDAY_NONCASH_peak = SUM(TDAY_NONCASH_PEAK)  
   INTO #FINAL  
   FROM  
   (   
     
  SELECT PARTY_CODE,SUM(TDAY_LEDGER) AS LEDGER_EOD,0 AS LEDGER_PEAK,TDAY_MARGIN_SHORT,TDAY_MTM_SHORT,    
SUM((CASE WHEN TDAY_MARGIN_SHORT > 0 THEN 0 ELSE TDAY_MARGIN_SHORT END)+    
(CASE WHEN TDAY_MTM_SHORT>0 THEN 0 ELSE TDAY_MTM_SHORT END)) AS TOTAL_MARGIN,0 AS PEAK_MARGIN ,    
FORMAT(MARGINDATE,'dd/MM/yyyy') AS  MARGINDATE  , 0 AS SMS_SHRT   ,TDAY_CASHCOLL_EOD = SUM(TDAY_CASHCOLL),TDAY_NONCASH_EOD = SUM(TDAY_NONCASH),  
TDAY_CASHCOLL_PEAK = 0 ,TDAY_NONCASH_PEAK = 0  
FROM AngelNSECM.MSAJAG.DBO.TBL_COMBINE_REPORTING WITH(NOLOCK)   
WHERE MARGINDATE = @FROMDATE AND (TDAY_MARGIN_SHORT <0 OR TDAY_MTM_SHORT <0)   ---AND PARTY_CODE ='HEMC004'    
GROUP BY PARTY_CODE,TDAY_MARGIN_SHORT,TDAY_MTM_SHORT,MARGINDATE    
  
UNION ALL    
SELECT PARTY_CODE,0 AS LEDGER_EOD,SUM(TDAY_LEDGER) AS LEDGER_PEAK,0,0,SUM(0+0) AS TOTAL_MARGIN,TDAY_MARGIN*-1 AS PEAK_MARGIN ,FORMAT(MARGINDATE,'dd/MM/yyyy') AS  MARGINDATE , 0 AS SMS_SHRT     
,TDAY_CASHCOLL_EOD = 0,TDAY_NONCASH_EOD = 0,TDAY_CASHCOLL_PEAK = SUM(TDAY_CASHCOLL),TDAY_NONCASH_PEAK = SUM(TDAY_NONCASH)  
FROM AngelNSECM.MSAJAG.DBO.TBL_COMBINE_REPORTING_PEAK WITH(NOLOCK)  
WHERE MARGINDATE = @FROMDATE AND (TDAY_MARGIN_SHORT <0 OR TDAY_MTM_SHORT <0)  --AND PARTY_CODE ='HEMC004'    
GROUP BY PARTY_CODE,TDAY_MARGIN,MARGINDATE    
  
UNION ALL    
SELECT PARTY_CODE,0 AS LEDGER_EOD,0 AS LEDGER_PEAK,0,0,SUM(0+0) AS TOTAL_MARGIN,0 AS PEAK_MARGIN ,DATE AS MARGINDATE,ISNULL(AMOUNT,0) AS SMS_SHRT    
,TDAY_CASHCOLL_EOD = 0,TDAY_NONCASH_EOD = 0,TDAY_CASHCOLL_PEAK = 0,TDAY_NONCASH_PEAK = 0  
FROM #SMS_DATA_MARGIN WITH(NOLOCK)  
WHERE DATE = @FROMDATE_SMS  
GROUP BY PARTY_CODE,AMOUNT,DATE  ) A GROUP BY PARTY_CODE,MARGINDATE  
  
ALTER TABLE #FINAL ADD SHORT NUMERIC(18,2) ,  DIFF NUMERIC(18,2) , NET_DEBIT NUMERIC(18,2) , NET_DEBIT_PEAK NUMERIC(18,2)  
  
UPDATE #FINAL SET NET_DEBIT = LEDGER_EOD + TDAY_CASHCOLL_EOD + TDAY_NONCASH_EOD  
  
UPDATE #FINAL SET NET_DEBIT_PEAK = LEDGER_PEAK + TDAY_CASHCOLL_PEAK + TDAY_NONCASH_PEAK  
  
  
--UPDATE #FINAL SET SHORT = CASE WHEN TOTAL_MARGIN+ CASE WHEN NET_DEBIT < 0 THEN NET_DEBIT ELSE 0 END <   
--PEAK_MARGIN + CASE WHEN NET_DEBIT_PEAK < 0 THEN NET_DEBIT_PEAK ELSE 0 END   
--THEN TOTAL_MARGIN+CASE WHEN NET_DEBIT < 0 THEN NET_DEBIT ELSE 0 END  
--ELSE PEAK_MARGIN+CASE WHEN NET_DEBIT_PEAK < 0 THEN NET_DEBIT_PEAK ELSE 0 END  END   
  
  
UPDATE #FINAL SET SHORT = CASE WHEN TOTAL_MARGIN+ CASE WHEN NET_DEBIT < 0 THEN NET_DEBIT ELSE 0 END <   
NET_DEBIT_PEAK + PEAK_MARGIN   
THEN TOTAL_MARGIN+CASE WHEN NET_DEBIT < 0 THEN NET_DEBIT ELSE 0 END  
ELSE NET_DEBIT_PEAK + PEAK_MARGIN   END   
  
  
UPDATE #FINAL SET DIFF = SMS_SHRT + SHORT   
  
SELECT * FROM #FINAL  WHERE PARTY_CODE <> '12798'  
ORDER BY PARTY_CODE ASC  
  
DROP TABLE #FINAL  
DROP TABLE #SMS_DATA_MARGIN

GO
