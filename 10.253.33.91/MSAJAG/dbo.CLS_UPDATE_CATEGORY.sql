--------------------------------------------------




CREATE PROC [dbo].[CLS_UPDATE_CATEGORY]
(
	@CATEGORY_CODE INT,
	@CATEGORY_NAME VARCHAR(50),
	@DESC VARCHAR(80),
	@ADMINAUTO INT,
	@REPORTS VARCHAR(8000),
	@UNAME VARCHAR(50),
	@REMOTE_ADDR VARCHAR(500)

)

/*
SELECT * FROM TBLCATEGORY
SELECT * FROM TBLCATEGORY_log
EXEC CLS_ADD_CATEGORY 'ALL','', 2
SELECT * FROM TBLUSERCONTROLGLOBALS
*/
AS
DECLARE @@TODAY AS DATETIME,
		@@NEWCATCODE AS INT,
		@@REPORTCODE AS INT,
		@@MAINREC AS CURSOR
		
SET @@TODAY = GETDATE()



IF ((SELECT COUNT(*) FROM TBLCATEGORY WHERE Fldcategoryname = @CATEGORY_NAME and Fldcategorycode <> @CATEGORY_CODE) > 0 )
 BEGIN
	SELECT ErrorMessage = 'Category Name exists! Choose another Name.'
 END
ELSE
 BEGIN
	BEGIN TRY
		BEGIN TRAN

		INSERT INTO TBLCATEGORY_LOG
		SELECT *,'I',@UNAME, @@TODAY FROM TBLCATEGORY WHERE FLDCATEGORYCODE = @CATEGORY_CODE 
		
		UPDATE TBLCATEGORY SET FLDCATEGORYNAME= @CATEGORY_NAME,FLDDESC= @DESC WHERE FLDCATEGORYCODE = @CATEGORY_CODE

		IF LEN(@REPORTS) > 0
		 BEGIN

			INSERT INTO TBLCATMENU_LOG
			SELECT FLDREPORTCODE = CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))),
					FLDADMINAUTO = @ADMINAUTO, 
					'',FLDCATEGORYCODE = ',' + CONVERT(VARCHAR,@@NEWCATCODE) + ',','E',@UNAME,@@TODAY,@REMOTE_ADDR
			FROM   PRADNYA.DBO.FUN_SPLITSTRING (@REPORTS,',' ) R 
			WHERE  EXISTS (SELECT FLDREPORTCODE 
								FROM TBLCATMENU C 
								WHERE CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))) = FLDREPORTCODE AND SPLITTED_VALUE <> '') 

			SELECT * INTO #TBLCATMENU FROM TBLCATMENU
			WHERE FLDADMINAUTO = @ADMINAUTO

			UPDATE T
			SET    FLDCATEGORYCODE = REPLACE(FLDCATEGORYCODE,',' + CONVERT(VARCHAR,@CATEGORY_CODE) + ',',',') 
			FROM TBLCATMENU T, TBLREPORTS TR
			WHERE  FLDCATEGORYCODE LIKE '%,' + CONVERT(VARCHAR,@CATEGORY_CODE) + ',%' AND FLDADMINAUTO = @ADMINAUTO
			AND T.FLDREPORTCODE = TR.FLDREPORTCODE AND (TR.FLDPATH LIKE '%.ASPX%' OR TR.FLDPATH = '')

			UPDATE C
			SET    FLDCATEGORYCODE = FLDCATEGORYCODE + CONVERT(VARCHAR,@CATEGORY_CODE) + ','
			FROM   PRADNYA.DBO.FUN_SPLITSTRING (@REPORTS,',' ) R,
					TBLCATMENU C
			WHERE  C.FLDREPORTCODE = CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE)))
					AND FLDADMINAUTO = @ADMINAUTO AND SPLITTED_VALUE <> ''


			

	
			INSERT INTO TBLCATMENU
			SELECT FLDREPORTCODE = CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))),
					FLDADMINAUTO = @ADMINAUTO,
					FLDCATEGORYCODE = ','+ CONVERT(VARCHAR,@CATEGORY_CODE) +','
			FROM   PRADNYA.DBO.FUN_SPLITSTRING (@REPORTS,',' ) R
			WHERE  NOT EXISTS (SELECT FLDREPORTCODE
								FROM   TBLCATMENU C
								WHERE  CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))) = FLDREPORTCODE AND FLDADMINAUTO = @ADMINAUTO) AND SPLITTED_VALUE <> ''
				
			INSERT INTO TBLCATMENU_LOG 
			SELECT M.FLDREPORTCODE,M.FLDADMINAUTO,T.FLDCATEGORYCODE,M.FLDCATEGORYCODE,'E',@UNAME,@@TODAY,  
			MACHINE_IP = @REMOTE_ADDR
			FROM TBLCATMENU M, #TBLCATMENU T  
			WHERE M.FLDADMINAUTO = T.FLDADMINAUTO AND M.FLDREPORTCODE = T.FLDREPORTCODE AND PRADNYA.DBO.FUN_SPLITSTRING_2NUM(REPLACE(T.FLDCATEGORYCODE,',','|'),'|') <> PRADNYA.DBO.FUN_SPLITSTRING_2NUM(REPLACE(M.FLDCATEGORYCODE,',','|'),'|') 
	
			CREATE TABLE #TEMP(REPORTCODE INT)	
			INSERT INTO #TEMP
				SELECT DISTINCT( A.CHILDMAP) FROM CLS_TBLREPORTS A, TBLREPORTS B INNER JOIN (SELECT FLDREPORTCODE = CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))) FROM  PRADNYA.DBO.FUN_SPLITSTRING (@REPORTS,',' )) X ON B.FLDREPORTCODE = X.FLDREPORTCODE
				WHERE A.FLDREPORTPATH = B.FLDPATH AND CHILDMAP  <> 0 
			
			UPDATE C
				SET FLDCATEGORYCODE = REPLACE(FLDCATEGORYCODE,',' + CONVERT(VARCHAR,@CATEGORY_CODE) + ',',',')  FROM TBLCATMENU C WHERE FLDREPORTCODE IN( 
				select REPORTCODE from #TEMP)
			
			UPDATE C 
				SET FLDCATEGORYCODE = FLDCATEGORYCODE + CONVERT(VARCHAR,@CATEGORY_CODE) + ',' FROM TBLCATMENU C WHERE FLDREPORTCODE IN( 
				select REPORTCODE from #TEMP)

			INSERT INTO TBLCATMENU
			SELECT FLDREPORTCODE = REPORTCODE, FLDADMINAUTO = @ADMINAUTO, FLDCATEGORYCODE = ','+ CONVERT(VARCHAR,@CATEGORY_CODE) +',' FROM #TEMP T  WHERE NOT EXISTS
			  (SELECT FLDREPORTCODE
								FROM   TBLCATMENU C
								WHERE  T.REPORTCODE = FLDREPORTCODE AND FLDADMINAUTO = @ADMINAUTO) 
			
			DROP TABLE  #TBLCATMENU


			COMMIT
			SELECT ErrorMessage = 'updated'
		 END
	END TRY  
	BEGIN CATCH
		SELECT ERROR_MESSAGE() AS ErrorMessage; 
		ROLLBACK
	END CATCH  
 END

GO
