-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------




CREATE PROCEDURE [dbo].[CLS_ADVANCE_FILTER_SAVE]
(
@SPARAMETER NVARCHAR(500),
@SESSIONID NVARCHAR(50),
@UNAME NVARCHAR(50)
)

AS

BEGIN
	
	DECLARE @KEY NVARCHAR(500),@VAL NVARCHAR(500)

	SELECT  @KEY=.DBO.PIECE(@SPARAMETER,'$',1)
	SELECT @VAL=.DBO.PIECE(@SPARAMETER,'$',2)

	CREATE TABLE #PARAKEY
	(SRNO INT IDENTITY(1,1),
	SKEY NVARCHAR(100)
	)

	CREATE TABLE #PARAVALUE
	(SRNO INT IDENTITY(1,1),
	SVALUE NVARCHAR(100)
	)

	INSERT INTO #PARAKEY
	SELECT ITEMS FROM DBO.CLS_SPLIT(@KEY,'|')
	INSERT INTO #PARAVALUE
	SELECT ITEMS FROM DBO.CLS_SPLIT(@VAL,'|')

	UPDATE V SET SVALUE= CASE WHEN SVALUE='TRUE' THEN '1' ELSE '0' END
	FROM #PARAKEY P,#PARAVALUE  V
	WHERE P.SRNO=V.SRNO
	AND P.SKEY IN('SHOWZEROCQ','SHOWCHQ','CHK_SHOWMARGIN','MRFREQ','CHK_QUARTELY','Chk_ECN','Chk_NONECN','CHK_BOUNCE')


	DELETE FROM CLS_ADVANCE_FILTER_PARAMETERS WHERE SESSIONID=@SESSIONID
	INSERT INTO CLS_ADVANCE_FILTER_PARAMETERS(PKEY,PVALUE,SESSIONID,ADDDT)
	SELECT SKEY,SVALUE,@SESSIONID,GETDATE()
	FROM #PARAKEY K,#PARAVALUE V
	WHERE K.SRNO=V.SRNO
	AND SKEY<>'' AND SVALUE<>''

	
	DROP TABLE #PARAKEY
	DROP TABLE #PARAVALUE
	
	SELECT 'SUCCESSFULLY'

END

GO
