-- Object: PROCEDURE dbo.CLS_RPT_DETAILCAL
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------







CREATE PROC [dbo].[CLS_RPT_DETAILCAL]
(
	@SETT_NO VARCHAR(7),
	@SETT_TYPE VARCHAR(6),
	@STR VARCHAR(MAX),
	@FLAG INT
	
)
AS 
BEGIN
	IF @FLAG = 1
	BEGIN
		SELECT 
			   
			   SETT_NO,	
			   SETT_TYPE,
			   CONVERT(VARCHAR(10),START_DATE,103)START_DATE,
			   CONVERT(VARCHAR(10),END_DATE,103)END_DATE,
			   CONVERT(VARCHAR(10),FUNDS_PAYIN,103)FUNDS_PAYIN,
			   CONVERT(VARCHAR(10),FUNDS_PAYOUT,103)FUNDS_PAYOUT,
			   CONVERT(VARCHAR(10),SEC_PAYIN,103)SEC_PAYIN,
			   CONVERT(VARCHAR(10),SEC_PAYOUT,103)SEC_PAYOUT,	
			   SERIES
		FROM SETT_MST 
		WHERE SETT_NO like @SETT_NO+'%'
		AND SETT_TYPE = CASE WHEN ( @SETT_TYPE <> 'SELECT' AND @SETT_TYPE <> '')  THEN @SETT_TYPE ELSE SETT_TYPE END
	END
	IF @FLAG = 2
	BEGIN TRY
		BEGIN TRAN
		IF EXISTS(SELECT COUNT(1) FROM SETT_MST WHERE SETT_NO = @SETT_NO AND SETT_TYPE=@SETT_TYPE)
		BEGIN
			DELETE FROM SETT_MST WHERE SETT_NO = @SETT_NO AND SETT_TYPE= @SETT_TYPE
		END
		
		INSERT INTO SETT_MST
		SELECT
		DBO.PIECE(@STR,'|',11),
		DBO.PIECE(@STR,'|',1),
		DBO.PIECE(@STR,'|',2),
		CONVERT(DATETIME,DBO.PIECE(@STR,'|',3),103),
		CONVERT(DATETIME,DBO.PIECE(@STR,'|',4),103) + ' 23:59:00',
		CONVERT(DATETIME,DBO.PIECE(@STR,'|',5),103),
		CONVERT(DATETIME,DBO.PIECE(@STR,'|',6),103) + ' 23:59:00',
		CONVERT(DATETIME,DBO.PIECE(@STR,'|',7),103),
		CONVERT(DATETIME,DBO.PIECE(@STR,'|',8),103) + ' 23:59:00',
		DBO.PIECE(@STR,'|',9),--'BSE',
		DBO.PIECE(@STR,'|',10)
		IF @@ROWCOUNT > 0
		BEGIN
			SELECT 'DATA SAVED SUCCESSFULY'
		END
		
		COMMIT TRAN
		
	END TRY
	BEGIN CATCH
		ROLLBACK 
	END CATCH


END

GO
