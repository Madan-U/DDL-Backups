-- Object: PROCEDURE dbo.CLS_PROC_PLD_DELBRANCHMARK
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


  
CREATE PROC [dbo].[CLS_PROC_PLD_DELBRANCHMARK]  
(  
 @PROCESS_FLAG VARCHAR(10),  
 @UNAME VARCHAR(20) = ''  
)  
AS  
-- EXEC CLS_PROC_PLD_DELBRANCHMARK 'APPROVE'  
  
DECLARE @SRNO   INT,  
  @SNO   INT,  
  @EXCHANGE  VARCHAR(3),  
  @SEGMENT  VARCHAR(10),  
  @EXCHG   VARCHAR(3),  
  @PARTY_CODE  VARCHAR(10),  
  @SCRIP_CD  VARCHAR(12),  
  @SERIES   VARCHAR(3),  
  @CERTNO   VARCHAR(12),  
  @APRQTY   INT,  
  @QTY   INT,  
  @DELCUR   CURSOR,  
  @SETTCUR  CURSOR,  
  @LICENCENO  VARCHAR(16),  
  @CL_TYPE  VARCHAR(3),  
  @DPID   VARCHAR(8),  
  @CLTDPID  VARCHAR(16),  
  @DPTYPE   VARCHAR(4)  
  
  
IF @PROCESS_FLAG = 'REQUEST'  
BEGIN  
 SELECT * INTO #V2_DELBRANCHMARK  
 FROM V2_DELBRANCHMARK  
 WHERE REQQTY > 0 AND PAYQTY = 0  
 AND REQSTATUS <> 'REJ'  
  
 INSERT INTO V2_DELBRANCHMARK_LOG  
 SELECT EXCHANGE,SEGMENT,EXCHG,PARTY_CODE,LONG_NAME,BRANCH_CD,SUB_BROKER,SCRIP_CD,SERIES,CERTNO,  
     REQHOLDQTY,REQPROCESSDATE,CL_RATE,REQQTY,REQSUBBY,REQSUBDATE,REQBY,REQDATE,APRHOLDQTY,  
     APRPROCESSDATE,APRQTY,APRBY,APRDATE,REQFLAG,REQSTATUS,PROCESSSTATUS,REMARK,RELEASEQTY,  
     RELEASEBY,RELEASEDATE,PAYQTY,PAYBY,PAYDATE,DPID,CLTDPID,CL_CATEGORY  
 FROM V2_DELBRANCHMARK  
 WHERE REQSTATUS = 'REJ'  
  
 INSERT INTO V2_DELBRANCHMARK_LOG  
 SELECT EXCHANGE,SEGMENT,EXCHG,PARTY_CODE,LONG_NAME,BRANCH_CD,SUB_BROKER,SCRIP_CD,SERIES,CERTNO,  
     REQHOLDQTY,REQPROCESSDATE,CL_RATE,REQQTY,REQSUBBY,REQSUBDATE,REQBY,REQDATE,APRHOLDQTY,  
     APRPROCESSDATE,APRQTY,APRBY,APRDATE,REQFLAG,REQSTATUS,PROCESSSTATUS,REMARK,RELEASEQTY,  
     RELEASEBY,RELEASEDATE,PAYQTY,PAYBY,PAYDATE,DPID,CLTDPID,CL_CATEGORY  
 FROM V2_DELBRANCHMARK  
 WHERE PAYQTY > 0  
  
 DELETE FROM V2_DELBRANCHMARK  
  
 INSERT INTO V2_DELBRANCHMARK  
 SELECT EXCHANGE,SEGMENT,EXCHG='NSE',D.PARTY_CODE,C1.LONG_NAME,C1.BRANCH_CD,C1.SUB_BROKER,D.SCRIP_CD,D.SERIES,D.CERTNO,  
 REQHOLDQTY=SUM(QTY),REQPROCESSDATE=GETDATE(),CL_RATE=0,REQQTY=0,REQSUBBY='',REQSUBDATE='',REQBY='',REQDATE='',APRHOLDQTY=0,  
 APRPROCESSDATE='',APRQTY=0,APRBY='',APRDATE='',REQFLAG=0,REQSTATUS='REQ',PROCESSSTATUS='REQ',REMARK='',RELEASEQTY=0,RELEASEBY='',RELEASEDATE='',  
 PAYQTY=0,PAYBY='',PAYDATE='',DPID='',CLTDPID='',CL_CATEGORY=''  
 FROM DELTRANS D, CLIENT1 C1, DELIVERYDP DP  
 WHERE D.BDPID = DP.DPID  
 AND D.BCLTDPID = DPCLTNO  
 AND DESCRIPTION LIKE '%PLEDGE%'  
 AND ACCOUNTTYPE = 'MAR'  
 AND D.PARTY_CODE = C1.CL_CODE   
 AND D.DRCR = 'D'  
 AND FILLER2 = 1   
 AND TRTYPE = 904  
 AND DELIVERED = '0'  
 GROUP BY EXCHANGE,SEGMENT,D.PARTY_CODE,C1.LONG_NAME,C1.BRANCH_CD,C1.SUB_BROKER,D.SCRIP_CD,D.SERIES,D.CERTNO  
  
 INSERT INTO V2_DELBRANCHMARK  
 SELECT DP.EXCHANGE,SEGMENT,EXCHG='NSE',D.PARTY_CODE,C1.LONG_NAME,C1.BRANCH_CD,C1.SUB_BROKER,D.SCRIP_CD,S2.SCRIP_CD,D.CERTNO,  
 REQHOLDQTY=SUM(QTY),REQPROCESSDATE=GETDATE(),CL_RATE=0,REQQTY=0,REQSUBBY='',REQSUBDATE='',REQBY='',REQDATE='',APRHOLDQTY=0,  
 APRPROCESSDATE='',APRQTY=0,APRBY='',APRDATE='',REQFLAG=0,REQSTATUS='REQ',PROCESSSTATUS='REQ',REMARK='',RELEASEQTY=0,RELEASEBY='',RELEASEDATE='',  
 PAYQTY=0,PAYBY='',PAYDATE='',DPID='',CLTDPID='',CL_CATEGORY=''  
 FROM ANGELDEMAT.BSEDB.dbo.DELTRANS D, ANGELDEMAT.BSEDB.dbo.CLIENT1 C1, ANGELDEMAT.BSEDB.dbo.DELIVERYDP DP, ANGELDEMAT.BSEDB.dbo.SCRIP2 S2
 WHERE D.BDPID = DP.DPID  
 AND D.BCLTDPID = DPCLTNO  
 AND DESCRIPTION LIKE '%PLEDGE%'  
 AND ACCOUNTTYPE = 'MAR'  
 AND D.PARTY_CODE = C1.CL_CODE   
 AND D.DRCR = 'D'  
 AND FILLER2 = 1   
 AND TRTYPE = 904  
 AND DELIVERED = '0'  
 AND BSECODE = D.SCRIP_CD  
 GROUP BY DP.EXCHANGE,SEGMENT,D.PARTY_CODE,C1.LONG_NAME,C1.BRANCH_CD,C1.SUB_BROKER,D.SCRIP_CD,S2.SCRIP_CD,D.CERTNO  
  
 UPDATE V2_DELBRANCHMARK SET DPID = C4.BANKID, CLTDPID = C4.CLTDPID  
 FROM CLIENT4 C4  
 WHERE V2_DELBRANCHMARK.PARTY_CODE = C4.PARTY_CODE  
 AND DEPOSITORY IN ('CDSL', 'NSDL')  
 AND DEFDP = 1   
  
 UPDATE V2_DELBRANCHMARK SET DPID = C4.BANKID, CLTDPID = C4.CLTDPID  
 FROM ANGELDEMAT.BSEDB.dbo.CLIENT4 C4  
 WHERE V2_DELBRANCHMARK.PARTY_CODE = C4.PARTY_CODE  
 AND DEPOSITORY IN ('CDSL', 'NSDL')  
 AND DEFDP = 1   
 AND DPID = ''  
  
 DELETE FROM V2_DELBRANCHMARK  
 WHERE DPID = ''  
  
 UPDATE V2_DELBRANCHMARK SET   
 REQQTY = D.REQQTY,  
 REQSUBBY = D.REQSUBBY,  
 REQSUBDATE = D.REQSUBDATE,  
 REQBY = D.REQBY,  
 REQDATE = D.REQDATE,  
 APRHOLDQTY = D.APRHOLDQTY,  
 APRPROCESSDATE = D.APRPROCESSDATE,  
 APRQTY = D.APRQTY,  
 APRBY = D.APRBY,  
 APRDATE = D.APRDATE,  
 REQFLAG = D.REQFLAG,  
 REQSTATUS = D.REQSTATUS,  
 PROCESSSTATUS = D.PROCESSSTATUS,  
 REMARK = D.REMARK,  
 RELEASEQTY = D.RELEASEQTY,  
 RELEASEBY = D.RELEASEBY,  
 RELEASEDATE = D.RELEASEDATE,  
 DPID = D.DPID,  
 CLTDPID = D.CLTDPID  
 FROM #V2_DELBRANCHMARK D  
 WHERE V2_DELBRANCHMARK.EXCHG = D.EXCHG  
 AND V2_DELBRANCHMARK.EXCHANGE = D.EXCHANGE  
 AND V2_DELBRANCHMARK.SEGMENT = D.SEGMENT  
 AND V2_DELBRANCHMARK.PARTY_CODE = D.PARTY_CODE  
 AND V2_DELBRANCHMARK.SCRIP_CD = D.SCRIP_CD  
 AND V2_DELBRANCHMARK.SERIES = D.SERIES  
 AND V2_DELBRANCHMARK.CERTNO = D.CERTNO  
END  
  
IF @PROCESS_FLAG = 'APPROVAL'  
BEGIN  
 SELECT * INTO #V2_DELBRANCHMARK_APR  
 FROM V2_DELBRANCHMARK  
 WHERE 1 = 2  
   
 INSERT INTO #V2_DELBRANCHMARK_APR  
 SELECT EXCHANGE,SEGMENT,EXCHG='NSE',D.PARTY_CODE,C1.LONG_NAME,C1.BRANCH_CD,C1.SUB_BROKER,D.SCRIP_CD,D.SERIES,D.CERTNO,  
 REQHOLDQTY=0,REQPROCESSDATE=GETDATE(),CL_RATE=0,REQQTY=0,REQSUBBY='',REQSUBDATE='',REQBY='',REQDATE='',APRHOLDQTY=SUM(QTY),  
 APRPROCESSDATE=GETDATE(),APRQTY=0,APRBY='',APRDATE='',REQFLAG=0,REQSTATUS='REQ',PROCESSSTATUS='APR',REMARK='',RELEASEQTY=0,RELEASEBY='',RELEASEDATE='',  
 PAYQTY=0,PAYBY='',PAYDATE='',DPID='',CLTDPID='',CL_CATEGORY=''  
 FROM DELTRANS D, CLIENT1 C1, DELIVERYDP DP  
 WHERE D.BDPID = DP.DPID  
 AND D.BCLTDPID = DPCLTNO  
 AND DESCRIPTION LIKE '%PLEDGE%'  
 AND ACCOUNTTYPE = 'MAR'  
 AND D.PARTY_CODE = C1.CL_CODE   
 AND D.DRCR = 'D'  
 AND FILLER2 = 1   
 AND TRTYPE = 904  
 AND DELIVERED = '0'  
 GROUP BY EXCHANGE,SEGMENT,D.PARTY_CODE,C1.LONG_NAME,C1.BRANCH_CD,C1.SUB_BROKER,D.SCRIP_CD,D.SERIES,D.CERTNO  
  
 INSERT INTO #V2_DELBRANCHMARK_APR  
 SELECT DP.EXCHANGE,SEGMENT,EXCHG='NSE',D.PARTY_CODE,C1.LONG_NAME,C1.BRANCH_CD,C1.SUB_BROKER,D.SCRIP_CD,S2.SCRIP_CD,D.CERTNO,  
 REQHOLDQTY=0,REQPROCESSDATE=GETDATE(),CL_RATE=0,REQQTY=0,REQSUBBY='',REQSUBDATE='',REQBY='',REQDATE='',APRHOLDQTY=SUM(QTY),  
 APRPROCESSDATE=GETDATE(),APRQTY=0,APRBY='',APRDATE='',REQFLAG=0,REQSTATUS='REQ',PROCESSSTATUS='APR',REMARK='',RELEASEQTY=0,RELEASEBY='',RELEASEDATE='',  
 PAYQTY=0,PAYBY='',PAYDATE='',DPID='',CLTDPID='',CL_CATEGORY=''  
 FROM ANGELDEMAT.BSEDB.dbo.DELTRANS D, ANGELDEMAT.BSEDB.dbo.CLIENT1 C1, ANGELDEMAT.BSEDB.dbo.DELIVERYDP DP, ANGELDEMAT.BSEDB.dbo.SCRIP2 S2  
 WHERE D.BDPID = DP.DPID  
 AND D.BCLTDPID = DPCLTNO  
 AND DESCRIPTION LIKE '%PLEDGE%'  
 AND ACCOUNTTYPE = 'MAR'  
 AND D.PARTY_CODE = C1.CL_CODE   
 AND D.DRCR = 'D'  
 AND FILLER2 = 1   
 AND TRTYPE = 904  
 AND DELIVERED = '0'  
 AND BSECODE = D.SCRIP_CD  
 GROUP BY DP.EXCHANGE,SEGMENT,D.PARTY_CODE,C1.LONG_NAME,C1.BRANCH_CD,C1.SUB_BROKER,D.SCRIP_CD,S2.SCRIP_CD,D.CERTNO  
  
 UPDATE V2_DELBRANCHMARK SET   
 APRHOLDQTY = D.APRHOLDQTY,  
 APRPROCESSDATE = D.APRPROCESSDATE,  
 REQSTATUS = D.REQSTATUS,  
 PROCESSSTATUS = D.PROCESSSTATUS  
 FROM #V2_DELBRANCHMARK_APR D  
 WHERE V2_DELBRANCHMARK.EXCHG = D.EXCHG  
 AND V2_DELBRANCHMARK.EXCHANGE = D.EXCHANGE  
 AND V2_DELBRANCHMARK.SEGMENT = D.SEGMENT  
 AND V2_DELBRANCHMARK.PARTY_CODE = D.PARTY_CODE  
 AND V2_DELBRANCHMARK.SCRIP_CD = D.SCRIP_CD  
 AND V2_DELBRANCHMARK.SERIES = D.SERIES  
 AND V2_DELBRANCHMARK.CERTNO = D.CERTNO  
 AND V2_DELBRANCHMARK.REQSTATUS <> 'REJ'  
 AND V2_DELBRANCHMARK.REQSTATUS = 'REQ'  
 AND V2_DELBRANCHMARK.REQQTY > 0  
 AND V2_DELBRANCHMARK.APRQTY = 0  
 AND V2_DELBRANCHMARK.APRHOLDQTY = 0  
END  
  
IF @PROCESS_FLAG = 'RELEASE'  
BEGIN  
 SET @DELCUR = CURSOR FOR  
 SELECT SRNO, EXCHG, EXCHANGE, SEGMENT, PARTY_CODE, SCRIP_CD, SERIES, CERTNO, APRQTY  
 FROM V2_DELBRANCHMARK   
 WHERE APRQTY > 0   
 AND RELEASEQTY = 0  
 ORDER BY EXCHG, PARTY_CODE  
 OPEN @DELCUR  
 FETCH NEXT FROM @DELCUR INTO @SRNO, @EXCHG, @EXCHANGE, @SEGMENT, @PARTY_CODE, @SCRIP_CD, @SERIES, @CERTNO, @APRQTY  
 WHILE @@FETCH_STATUS = 0  
 BEGIN  
  SET @CL_TYPE = 'NRM'  
  SET @DPID = ''  
  SET @CLTDPID = ''  
  SET @DPTYPE = ''  
  SELECT @CL_TYPE = CL_TYPE FROM CLIENT1 WHERE CL_CODE = @PARTY_CODE  
  SELECT @CL_TYPE = (CASE WHEN ISNULL(@CL_TYPE,'') = '' THEN 'NRM' ELSE @CL_TYPE END)  
  SELECT @CL_TYPE  
  IF @CL_TYPE = 'PRO'   
  BEGIN  
   SELECT @DPTYPE = DEPOSITORY, @DPID = BANKID, @CLTDPID = CLTDPID FROM CLIENT4   
   WHERE CL_CODE = @PARTY_CODE  
   AND DEFDP = 1 AND DEPOSITORY IN ('NSDL', 'CDSL')  
  END  
    
  IF @EXCHG = 'NSE'  
  BEGIN  
   SET @SETTCUR = CURSOR FOR  
    SELECT D.SNO, QTY, LICENCENO   
    FROM MSAJAG.DBO.DELTRANS D, MSAJAG.DBO.DELIVERYDP DP  
    WHERE D.BDPID = DP.DPID  
    AND D.BCLTDPID = DPCLTNO  
    AND DESCRIPTION LIKE '%PLEDGE%'  
    AND ACCOUNTTYPE = 'MAR'  
    AND D.DRCR = 'D'  
    AND FILLER2 = 1   
    AND TRTYPE = 904  
    AND DELIVERED = '0'  
    AND EXCHANGE = @EXCHANGE  
    AND SEGMENT = @SEGMENT  
    AND PARTY_CODE = @PARTY_CODE  
    AND SCRIP_CD = @SCRIP_CD  
    AND SERIES = @SERIES  
    AND CERTNO = @CERTNO  
   OPEN @SETTCUR  
   FETCH NEXT FROM @SETTCUR INTO @SNO, @QTY, @LICENCENO  
   WHILE @@FETCH_STATUS = 0 AND @APRQTY > 0  
   BEGIN  
    IF @CL_TYPE <> 'PRO'   
    BEGIN  
     SET @DPID = ''  
     SET @CLTDPID = ''  
     SET @DPTYPE = ''  
     SELECT @DPTYPE = DPTYPE, @DPID = DPID, @CLTDPID = DPCLTNO FROM DELIVERYDP    
     WHERE DPCLTNO = @LICENCENO  
    END  
    IF @APRQTY >= @QTY  
    BEGIN  
     UPDATE MSAJAG.DBO.DELTRANS SET TRTYPE = (CASE WHEN @CL_TYPE = 'PRO' THEN 905 ELSE 1002 END),   
     HOLDERNAME = (CASE WHEN @CL_TYPE <> 'PRO' THEN 'BEN ' + @LICENCENO ELSE 'PAY-OUT' END),  
     DPTYPE = @DPTYPE,  
     DPID = @DPID,  
     CLTDPID = @CLTDPID  
     WHERE SNO = @SNO  
  
     UPDATE V2_DELBRANCHMARK SET RELEASEQTY = RELEASEQTY + @QTY, RELEASEBY = @UNAME, RELEASEDATE = GETDATE(),REQSTATUS='REL',PROCESSSTATUS='REL'  
     WHERE SRNO = @SRNO  
  
     SET @APRQTY = @APRQTY - @QTY  
    END  
    ELSE  
    BEGIN  
     INSERT INTO MSAJAG.DBO.DELTRANS  
     SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,  
         QTY=@QTY-@APRQTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,  
         DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,  
         SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5   
     FROM DELTRANS WHERE SNO = @SNO  
  
     UPDATE MSAJAG.DBO.DELTRANS SET TRTYPE = (CASE WHEN @CL_TYPE = 'PRO' THEN 905 ELSE 1002 END),   
     HOLDERNAME = (CASE WHEN @CL_TYPE <> 'PRO' THEN 'BEN ' + @LICENCENO ELSE 'PAY-OUT' END),  
     DPTYPE = @DPTYPE,  
     DPID = @DPID,  
     CLTDPID = @CLTDPID,  
     QTY = @APRQTY  
     WHERE SNO = @SNO  
  
     UPDATE V2_DELBRANCHMARK SET RELEASEQTY = RELEASEQTY + @APRQTY, RELEASEBY = @UNAME, RELEASEDATE = GETDATE(),REQSTATUS='REL',PROCESSSTATUS='REL'  
     WHERE SRNO = @SRNO  
  
     SET @APRQTY = 0  
    END  
    FETCH NEXT FROM @SETTCUR INTO @SNO, @QTY, @LICENCENO  
   END  
   CLOSE @SETTCUR  
   DEALLOCATE @SETTCUR  
  END  
  ELSE  
  BEGIN  
   SET @SETTCUR = CURSOR FOR  
    SELECT D.SNO, QTY, LICENCENO   
    FROM ANGELDEMAT.BSEDB.dbo.DELTRANS D, ANGELDEMAT.BSEDB.dbo.DELIVERYDP DP  
    WHERE D.BDPID = DP.DPID  
    AND D.BCLTDPID = DPCLTNO  
    AND DESCRIPTION LIKE '%PLEDGE%'  
    AND ACCOUNTTYPE = 'MAR'  
    AND D.DRCR = 'D'  
    AND FILLER2 = 1   
    AND TRTYPE = 904  
    AND DELIVERED = '0'  
    AND EXCHANGE = @EXCHANGE  
    AND SEGMENT = @SEGMENT  
    AND PARTY_CODE = @PARTY_CODE  
    AND SCRIP_CD = @SCRIP_CD  
    AND SERIES = @SERIES  
    AND CERTNO = @CERTNO  
   OPEN @SETTCUR  
   FETCH NEXT FROM @SETTCUR INTO @SNO, @QTY, @LICENCENO  
   WHILE @@FETCH_STATUS = 0 AND @APRQTY > 0  
   BEGIN  
    IF @CL_TYPE <> 'PRO'   
    BEGIN  
     SET @DPID = ''  
     SET @CLTDPID = ''  
     SET @DPTYPE = ''  
     SELECT @DPTYPE = DPTYPE, @DPID = DPID, @CLTDPID = DPCLTNO FROM ANGELDEMAT.BSEDB.dbo.DELIVERYDP    
     WHERE DPCLTNO = @LICENCENO  
    END  
    IF @APRQTY >= @QTY  
    BEGIN  
     UPDATE ANGELDEMAT.BSEDB.dbo.DELTRANS SET TRTYPE = (CASE WHEN @CL_TYPE = 'PRO' THEN 905 ELSE 1002 END),   
     HOLDERNAME = (CASE WHEN @CL_TYPE <> 'PRO' THEN 'BEN ' + @LICENCENO ELSE 'PAY-OUT' END),  
     DPTYPE = @DPTYPE,  
     DPID = @DPID,  
     CLTDPID = @CLTDPID  
     WHERE SNO = @SNO  
  
     UPDATE V2_DELBRANCHMARK SET RELEASEQTY = RELEASEQTY + @QTY, RELEASEBY = @UNAME, RELEASEDATE = GETDATE(),REQSTATUS='REL',PROCESSSTATUS='REL'  
     WHERE SRNO = @SRNO  
  
     SET @APRQTY = @APRQTY - @QTY  
    END  
    ELSE  
    BEGIN  
     INSERT INTO ANGELDEMAT.BSEDB.dbo.DELTRANS (SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,  
         QTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,  
         DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,  
         SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5)
     SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,  
         QTY=@QTY-@APRQTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,  
         DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,  
         SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5   
     FROM ANGELDEMAT.BSEDB.dbo.DELTRANS   
     WHERE SNO = @SNO  
  
     UPDATE ANGELDEMAT.BSEDB.dbo.DELTRANS SET TRTYPE = (CASE WHEN @CL_TYPE = 'PRO' THEN 905 ELSE 1002 END),   
     HOLDERNAME = (CASE WHEN @CL_TYPE <> 'PRO' THEN 'BEN ' + @LICENCENO ELSE 'PAY-OUT' END),  
     DPTYPE = @DPTYPE,  
     DPID = @DPID,  
     CLTDPID = @CLTDPID,  
     QTY = @APRQTY  
     WHERE SNO = @SNO  
  
     UPDATE V2_DELBRANCHMARK SET RELEASEQTY = RELEASEQTY + @APRQTY, RELEASEBY = @UNAME, RELEASEDATE = GETDATE(),REQSTATUS='REL',PROCESSSTATUS='REL'  
     WHERE SRNO = @SRNO  
  
     SET @APRQTY = 0  
    END  
    FETCH NEXT FROM @SETTCUR INTO @SNO, @QTY, @LICENCENO  
   END  
   CLOSE @SETTCUR  
   DEALLOCATE @SETTCUR  
  END  
  FETCH NEXT FROM @DELCUR INTO @SRNO, @EXCHG, @EXCHANGE, @SEGMENT, @PARTY_CODE, @SCRIP_CD, @SERIES, @CERTNO, @APRQTY  
 END  
 CLOSE @DELCUR  
 DEALLOCATE @DELCUR  
END  
  
IF @PROCESS_FLAG = 'PAY-OUT'  
BEGIN  
 SET @DELCUR = CURSOR FOR  
 SELECT SRNO, EXCHG, EXCHANGE, SEGMENT, PARTY_CODE, SCRIP_CD, SERIES, CERTNO, RELEASEQTY,   
 DPTYPE = (CASE WHEN DPID LIKE 'IN%' THEN 'NSDL' ELSE 'CDSL' END), DPID, CLTDPID  
 FROM V2_DELBRANCHMARK   
 WHERE RELEASEQTY > 0   
 AND PAYQTY = 0  
 ORDER BY EXCHG, PARTY_CODE  
 OPEN @DELCUR  
 FETCH NEXT FROM @DELCUR INTO @SRNO, @EXCHG, @EXCHANGE, @SEGMENT, @PARTY_CODE, @SCRIP_CD, @SERIES, @CERTNO, @APRQTY, @DPTYPE, @DPID, @CLTDPID  
 WHILE @@FETCH_STATUS = 0  
 BEGIN  
  IF @EXCHG = 'NSE'  
  BEGIN  
   SET @SETTCUR = CURSOR FOR  
    SELECT D.SNO, QTY, LICENCENO   
    FROM MSAJAG.DBO.DELTRANS D, MSAJAG.DBO.DELIVERYDP DP  
    WHERE D.BDPID = DP.DPID  
    AND D.BCLTDPID = DPCLTNO  
    AND DESCRIPTION NOT LIKE '%PLEDGE%'  
    AND ACCOUNTTYPE = 'MAR'  
    AND D.DRCR = 'D'  
    AND FILLER2 = 1   
    AND TRTYPE = 904  
    AND DELIVERED = '0'  
    AND EXCHANGE = @EXCHANGE  
    AND SEGMENT = @SEGMENT  
    AND PARTY_CODE = @PARTY_CODE  
    AND SCRIP_CD = @SCRIP_CD  
    AND SERIES = @SERIES  
    AND CERTNO = @CERTNO  
   OPEN @SETTCUR  
   FETCH NEXT FROM @SETTCUR INTO @SNO, @QTY, @LICENCENO  
   WHILE @@FETCH_STATUS = 0 AND @APRQTY > 0  
   BEGIN  
    IF @APRQTY >= @QTY  
    BEGIN  
     UPDATE MSAJAG.DBO.DELTRANS SET TRTYPE = 905,   
     HOLDERNAME = 'PAY-OUT',  
     DPTYPE = @DPTYPE,  
     DPID = @DPID,  
     CLTDPID = @CLTDPID  
     WHERE SNO = @SNO  
  
     UPDATE V2_DELBRANCHMARK SET PAYQTY = PAYQTY + @QTY, PAYBY = @UNAME, PAYDATE = GETDATE(),REQSTATUS='PAY',PROCESSSTATUS='PAY'  
     WHERE SRNO = @SRNO  
  
     SET @APRQTY = @APRQTY - @QTY  
    END  
    ELSE  
    BEGIN  
     INSERT INTO MSAJAG.DBO.DELTRANS  
     SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,  
         QTY=@QTY-@APRQTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,  
         DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,  
         SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5   
     FROM DELTRANS WHERE SNO = @SNO  
  
     UPDATE MSAJAG.DBO.DELTRANS SET TRTYPE = 905,   
     HOLDERNAME = 'PAY-OUT',  
     DPTYPE = @DPTYPE,  
     DPID = @DPID,  
     CLTDPID = @CLTDPID,  
     QTY = @APRQTY  
     WHERE SNO = @SNO  
  
     UPDATE V2_DELBRANCHMARK SET PAYQTY = PAYQTY + @APRQTY, PAYBY = @UNAME, PAYDATE = GETDATE(),REQSTATUS='PAY',PROCESSSTATUS='PAY'  
     WHERE SRNO = @SRNO  
  
     SET @APRQTY = 0  
    END  
    FETCH NEXT FROM @SETTCUR INTO @SNO, @QTY, @LICENCENO  
   END  
   CLOSE @SETTCUR  
   DEALLOCATE @SETTCUR  
  END  
  ELSE  
  BEGIN  
   SET @SETTCUR = CURSOR FOR  
    SELECT D.SNO, QTY, LICENCENO   
    FROM ANGELDEMAT.BSEDB.dbo.DELTRANS D, ANGELDEMAT.BSEDB.dbo.DELIVERYDP DP  
    WHERE D.BDPID = DP.DPID  
    AND D.BCLTDPID = DPCLTNO  
    AND DESCRIPTION NOT LIKE '%PLEDGE%'  
    AND ACCOUNTTYPE = 'MAR'  
    AND D.DRCR = 'D'  
    AND FILLER2 = 1   
    AND TRTYPE = 904  
    AND DELIVERED = '0'  
    AND EXCHANGE = @EXCHANGE  
    AND SEGMENT = @SEGMENT  
    AND PARTY_CODE = @PARTY_CODE  
    AND SCRIP_CD = @SCRIP_CD  
    AND SERIES = @SERIES  
    AND CERTNO = @CERTNO  
   OPEN @SETTCUR  
   FETCH NEXT FROM @SETTCUR INTO @SNO, @QTY, @LICENCENO  
   WHILE @@FETCH_STATUS = 0 AND @APRQTY > 0  
   BEGIN  
    IF @APRQTY >= @QTY  
    BEGIN  
     UPDATE ANGELDEMAT.BSEDB.dbo.DELTRANS SET TRTYPE = 905,   
     HOLDERNAME = 'PAY-OUT',  
     DPTYPE = @DPTYPE,  
     DPID = @DPID,  
     CLTDPID = @CLTDPID  
     WHERE SNO = @SNO  
  
     UPDATE V2_DELBRANCHMARK SET PAYQTY = PAYQTY + @QTY, PAYBY = @UNAME, PAYDATE = GETDATE(),REQSTATUS='PAY',PROCESSSTATUS='PAY'  
     WHERE SRNO = @SRNO  
  
     SET @APRQTY = @APRQTY - @QTY  
    END  
    ELSE  
    BEGIN  
     INSERT INTO ANGELDEMAT.BSEDB.dbo.DELTRANS(SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,  
         QTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,  
         DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,  
         SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5)
     SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,  
         QTY=@QTY-@APRQTY,FROMNO,TONO,CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,  
         DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,  
         SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5   
     FROM ANGELDEMAT.BSEDB.dbo.DELTRANS   
     WHERE SNO = @SNO  
  
     UPDATE ANGELDEMAT.BSEDB.dbo.DELTRANS SET TRTYPE = 905,   
     HOLDERNAME = 'PAY-OUT',  
     DPTYPE = @DPTYPE,  
     DPID = @DPID,  
     CLTDPID = @CLTDPID,  
     QTY = @APRQTY  
     WHERE SNO = @SNO  
  
     UPDATE V2_DELBRANCHMARK SET PAYQTY = PAYQTY + @APRQTY, PAYBY = @UNAME, PAYDATE = GETDATE(),REQSTATUS='PAY',PROCESSSTATUS='PAY'  
     WHERE SRNO = @SRNO  
  
     SET @APRQTY = 0  
    END  
    FETCH NEXT FROM @SETTCUR INTO @SNO, @QTY, @LICENCENO  
   END  
   CLOSE @SETTCUR  
   DEALLOCATE @SETTCUR  
  END  
  FETCH NEXT FROM @DELCUR INTO @SRNO, @EXCHG, @EXCHANGE, @SEGMENT, @PARTY_CODE, @SCRIP_CD, @SERIES, @CERTNO, @APRQTY, @DPTYPE, @DPID, @CLTDPID  
 END  
 CLOSE @DELCUR  
 DEALLOCATE @DELCUR  
END

