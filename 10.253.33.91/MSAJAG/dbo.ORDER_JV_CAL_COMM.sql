CREATE PROC [dbo].[ORDER_JV_CAL_COMM]
AS

  DECLARE @COUNT INT 
  DECLARE @BODYMSG NVARCHAR(MAX)    
  DECLARE @SUBJECT NVARCHAR(MAX)  
  DECLARE @TABLEHTML NVARCHAR(MAX)  
  DECLARE @FINALCOUNT NVARCHAR(MAX)
  DECLARE @TO_DATE DATE

  SET @TO_DATE =  CONVERT (VARCHAR (10),GETDATE()-1,120)

  IF OBJECT_ID('tempdb..#trd1') IS NOT NULL
    DROP TABLE #trd1
  SELECT DISTINCT CD_Party_Code into #trd1 FROM ANGELCOMMODITY.MCDX.DBO.Charges_Detail WHERE CD_ComputationLevel ='O' AND CD_Sauda_Date > = @TO_DATE
  UNION ALL
  SELECT DISTINCT CD_Party_Code  FROM ANGELCOMMODITY.NCDX.DBO.Charges_Detail WHERE  CD_ComputationLevel ='O' AND CD_Sauda_Date > = @TO_DATE
  UNION ALL
  SELECT DISTINCT CD_Party_Code  FROM ANGELCOMMODITY.BSECURFO.DBO.Charges_Detail WHERE  CD_ComputationLevel ='O' AND CD_Sauda_Date > = @TO_DATE
    UNION ALL
  SELECT DISTINCT CD_Party_Code  FROM ANGELCOMMODITY.NCE.DBO.Charges_Detail WHERE  CD_ComputationLevel ='O' AND CD_Sauda_Date > = @TO_DATE

IF @@ROWCOUNT <> 0 

	BEGIN 

    IF OBJECT_ID('tempdb..#TEMP') IS NOT NULL
    DROP TABLE #TEMP

	
  Select * into #comm
  FROM ANGELCOMMODITY.MCDX.DBO.FILEDATA WITH(NOLOCK) where UploadDateTime >=getdate()-5


	SELECT 
		 SAUDA_DATE = LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',1))) , 
		PARTY_CODE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',23))),
		Delaer=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',2))),
		--SYMBOL=(CASE WHEN LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',4))) = 'BSE' AND LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',8))) = ''
		--			 THEN LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',9))) 
		--			 ELSE LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',10)))
		--		END),
		--EXPIRYDATE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',12))),
		--STRIKE_PRICE=(CASE WHEN LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',13))) = '' THEN '0' ELSE LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',13))) END),
		--OPTION_TYPE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',14))),
	 --   INTIATED_BY=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',2))),
		 PRODUCT_TYPE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',32))),
		ORDER_NO=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',25))),
		ORDER_INIT_FROM=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',35))),
		ORDER_MODY_FROM=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',36))),
		SEGMENT=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',4))),
		INST_TYPE=LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',8))),
		UPLOADED_BY = 'AUTO',
		UPLOADED_ON = GETDATE() INTO #TEMP 
	FROM #comm WITH(NOLOCK)
	WHERE  ISNUMERIC(.DBO.PIECE(FILE_DATA,',',18)) = 1 ---and LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',35))) ='TWS'
	---AND LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',1))) = @TDATE
 	 AND LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',23))) in (SELECT CD_Party_Code FROM #trd1 GROUP BY CD_Party_Code)

	 CREATE INDEX #T1 ON #TEMP(PARTY_CODE)
   
    IF OBJECT_ID('tempdb..#testr') IS NOT NULL
    DROP TABLE #testr
  select * into #testr   from #TEMP WHERE 
               PARTY_CODE IN (SELECT  CD_Party_Code from #trd1 GROUP BY CD_Party_Code )



INSERT INTO ORDER_TRANS_JV_CALC
SELECT A.Party_Code,A.SUB_BROKER,PRODUCT_TYPE,A.ORDER_NO,ORDER_TYPE,EXCHANGE,SEGMENT,ORDER_FLAG,A.CL_TYPE,TRADE_DATE,l_state FROM (
SELECT  DISTINCT A.Party_Code,SUB_BROKER=Delaer,PRODUCT_TYPE=ORDER_INIT_FROM,A.ORDER_NO ,
( CASE WHEN ORDER_INIT_FROM IN ('AngelEye','AEMobile','RISKADMIN','TIMERADMIN','DIET') THEN 'ONLINE' ELSE 'OFFLINE' END) AS ORDER_TYPE, 
EXCHANGE,A.SEGMENT ,ORDER_FLAG ='',CL_TYPE='' ,TRADE_DATE  FROM (

SELECT DISTINCT PARTY_CODE,ORDER_NO,EXCHANGE='MCX',SEGMENT='FUTURES',CONVERT(VARCHAR(11),SAUDA_DATE,120) TRADE_DATE 
FROM ANGELCOMMODITY.MCDX.DBO.FOSETTLEMENT  WHERE SAUDA_DATE >= @TO_DATE AND AuctionPart <> 'CA' AND PARTY_CODE IN ( SELECT  * from #trd1)  
union all
SELECT DISTINCT PARTY_CODE,ORDER_NO,EXCHANGE='NCDEX',SEGMENT='FUTURES',CONVERT(VARCHAR(11),SAUDA_DATE,120) TRADE_DATE 
FROM ANGELCOMMODITY.NCDX.DBO.FOSETTLEMENT  WHERE SAUDA_DATE >= @TO_DATE AND AuctionPart <> 'CA' AND PARTY_CODE IN ( SELECT  * from #trd1) 
union all
SELECT DISTINCT PARTY_CODE,ORDER_NO,EXCHANGE='BSX',SEGMENT='FUTURES',CONVERT(VARCHAR(11),SAUDA_DATE,120) TRADE_DATE 
FROM ANGELCOMMODITY.BSECURFO.DBO.FOSETTLEMENT  WHERE SAUDA_DATE >= @TO_DATE  AND AuctionPart <> 'CA' AND Price <> 0 AND PARTY_CODE IN ( SELECT  * from #trd1)  
union all
SELECT DISTINCT PARTY_CODE,ORDER_NO,EXCHANGE='NCE',SEGMENT='FUTURES',CONVERT(VARCHAR(11),SAUDA_DATE,120) TRADE_DATE 
FROM ANGELCOMMODITY.NCE.DBO.FOSETTLEMENT  WHERE SAUDA_DATE >= @TO_DATE  AND AuctionPart <> 'CA' AND Price <> 0 AND PARTY_CODE IN ( SELECT  * from #trd1)  
 ) A
LEFT OUTER JOIN 
( SELECT DISTINCT PARTY_CODE,Delaer =(CASE WHEN Delaer=PARTY_CODE THEN '' ELSE Delaer END),ORDER_INIT_FROM,SEGMENT ,INST_TYPE ,ORDER_NO 
 FROM  #testr  WHERE SAUDA_DATE= CONVERT (varchar(10),GETDATE()-1,103)   ) R 
ON A.Party_Code =R.PARTY_CODE AND  A.ORDER_NO=R.ORDER_NO AND R.SEGMENT=EXCHANGE AND 
(CASE WHEN isnuLL(INST_TYPE,'')='' THEN 'CAPITAL' ELSE 'FUTURES' END)  = A.SEGMENT) A ,
Client_Details B WHERE A.Party_Code =B.cl_code

	

END
 ELSE 
 BEGIN

	SELECT 'NO TRADE FOR THE DAY'

 END

SELECT @FINALCOUNT = COUNT(1) FROM ORDER_TRANS_JV_CALC WHERE TRADE_DATE = CONVERT (VARCHAR (10),GETDATE()-1,120) and EXCHANGE IN ('MCX','NCDEX')

IF 
@FINALCOUNT <> 0 
 set @FINALCOUNT ='<big>' +'TRADE COUNT'+ ' ' + '<FONT COLOR="blue">'+ '<b>' + @FINALCOUNT + ' ' +'</FONT>' + '</b>' +'FOR COMM..!'+ '</big> '
 ELSE 
  set @FINALCOUNT ='<big>' + 'NO TRADE FOR THE DAY..! ' + '</big> '
  
SET @SUBJECT = 'ORDER TRANS JV CALC'   


EXEC MSDB.DBO.SP_SEND_DBMAIL    
@PROFILE_NAME ='DBA',
@RECIPIENTS='bo.support@angelbroking.com' ,    
@COPY_RECIPIENTS='',    
@SUBJECT = @SUBJECT,    
@BODY = @FINALCOUNT,     
@BODY_FORMAT = 'HTML' ;

GO
