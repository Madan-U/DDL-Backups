CREATE PROCEDURE [dbo].[CLS_GET_MENU_MEGA]
(
	@STRCATEGORY VARCHAR(20),
	@STRAPPNAME VARCHAR(256),
	@USERID VARCHAR(20)
)
/*

	EXEC CLS_GET_MENU_MEGA '546','','55'
*/
AS

DECLARE 
	@FLDMENUNAME VARCHAR(200)
	,@FLDMENUNAME_1 VARCHAR(200)
	,@FLDGRPNAME VARCHAR(200)
	,@FLDGRPNAME_1 VARCHAR(200)
	,@FLDREPORTNAME VARCHAR(200)
	,@FLDPATH VARCHAR(200)
	,@FLDREPORTCODE VARCHAR(200)
	,@FLDREPORTID VARCHAR(200)
	,@MENUSTRING VARCHAR(MAX)
	,@MENUCUR CURSOR
	,@FLDMENUGRP INT
	,@FLDORDER INT
	,@LOOP INT
	,@FLDADMINAUTO INT

	
	SELECT @FLDADMINAUTO = FLDADMINAUTO FROM TBLPRADNYAUSERS  (NOLOCK) WHERE FLDAUTO = @USERID
	print @FLDADMINAUTO
	SET @LOOP = 0
SET @MENUCUR = CURSOR
FOR

	SELECT /*FLDMENUGRPDESC = CASE 
			WHEN A.FLDMENUGRP < 4
				THEN 'SHARE ACCOUNTING '
			WHEN A.FLDMENUGRP > 3
				AND A.FLDMENUGRP < 7
				THEN ' FINANCE ACCOUNTING '
			ELSE 'UTILITIES'
			END*/
		FLDMENUNAME
		,FLDGRPNAME = REPLACE(FLDGRPNAME,'_',' ')
		,FLDREPORTNAME = REPLACE(FLDREPORTNAME,'_',' ')
		,FLDPATH
		,FLDREPORTCODE = A.FLDREPORTCODE
		,FLDREPORTID = F.FLDREPORTCODE
		,FLDMENUGRP = A.FLDMENUGRP
		,FLDORDER	
	FROM TBLREPORTS A(NOLOCK)
		,TBLREPORTGRP B(NOLOCK)
		,TBLCATMENU C(NOLOCK)
		,TBLCATEGORY D(NOLOCK)
		,TBLMENUHEAD E(NOLOCK)
		,CLS_TBLREPORTS F(NOLOCK)
	WHERE A.FLDMENUGRP = B.FLDMENUGRP
		AND A.FLDREPORTGRP = B.FLDREPORTGRP
		AND C.FLDREPORTCODE = A.FLDREPORTCODE
		AND E.FLDMENUCODE = A.FLDMENUGRP
		AND D.FLDCATEGORYCODE = @STRCATEGORY
		AND C.FLDCATEGORYCODE LIKE '%,' + CONVERT(VARCHAR, D.FLDCATEGORYCODE) + ',%'
		AND CHARINDEX('ASPX',CAST(UPPER(FLDPATH) AS VARCHAR(500))) > 0
		AND FLDREPORTPATH = A.FLDPATH
		AND C.FLDADMINAUTO  = @FLDADMINAUTO
	ORDER BY 
		FLDMENUGRP, FLDGRPNAME, FLDMENUNAME, A.fldorder	

OPEN @MENUCUR

SET @MENUSTRING = '<ul id = "mega"><li class=''clstitle''><a href=''#'' onclick = "javascript:postToDashBord()">&nbsp;&nbsp;Dashboard&nbsp;&nbsp;</a></li>'
SET @FLDGRPNAME_1 = ''
--SET @FLDMENUGRPDESC_1 = ''
SET @FLDMENUNAME_1 = ''

FETCH NEXT
FROM @MENUCUR
INTO @FLDMENUNAME
	,@FLDGRPNAME
	,@FLDREPORTNAME
	,@FLDPATH
	,@FLDREPORTCODE
	,@FLDREPORTID
	,@FLDMENUGRP
	,@FLDORDER

WHILE @@FETCH_STATUS = 0
BEGIN
	IF @FLDMENUNAME_1 <> @FLDMENUNAME
	BEGIN
		IF @FLDMENUNAME_1 <> ''
		BEGIN
			SET @MENUSTRING = @MENUSTRING + '</div></li>'
		END
		print @LOOP
		IF @LOOP <= 3
			SET @MENUSTRING = @MENUSTRING + '<li class=''clstitle''><a href=''#''>' + lower('&nbsp;&nbsp;') + @FLDMENUNAME + lower('&nbsp;&nbsp;') + '</a><div>'
		ELSE
			SET @MENUSTRING = @MENUSTRING + '<li class=''dif clstitle''><a href=''#''>' + lower('&nbsp;&nbsp;') + @FLDMENUNAME + lower('&nbsp;&nbsp;') + '</a><div>'
		SET @FLDMENUNAME_1 = @FLDMENUNAME
		SET @FLDGRPNAME_1 = ''
		SET @LOOP = @LOOP + 1
	END

	IF @FLDGRPNAME_1 <> @FLDGRPNAME
	BEGIN
		/*IF @FLDGRPNAME_1 <> ''
		BEGIN
			SET @MENUSTRING = @MENUSTRING + '</LI>'
		END*/

		SET @MENUSTRING = @MENUSTRING + '<h2>' + @FLDGRPNAME + '</h2>'
		SET @FLDGRPNAME_1 = @FLDGRPNAME
	END
	/*---------------------	*/
		IF ((LEN(@FLDREPORTNAME) > 20) AND  CHARINDEX('REPORT',UPPER(@FLDREPORTNAME)) > 0)
		BEGIN 
				SET @FLDREPORTNAME = REPLACE(UPPER(@FLDREPORTNAME),'REPORT','')
		END 
	/*----------------------*/
	SET @MENUSTRING = @MENUSTRING + '<p><a href=''#'' onclick = "javascript:callreportmenu(''' + @STRAPPNAME + @FLDPATH + ''',''' + @FLDREPORTID + ''')">' + LEFT(.DBO.CLS_ProperCase(@FLDREPORTNAME), 25) + '</a></p>'
	FETCH NEXT
	FROM @MENUCUR
	INTO @FLDMENUNAME
		,@FLDGRPNAME
		,@FLDREPORTNAME
		,@FLDPATH
		,@FLDREPORTCODE
		,@FLDREPORTID
		,@FLDMENUGRP
		,@FLDORDER
END

CLOSE @MENUCUR

DEALLOCATE @MENUCUR


SET @MENUSTRING = @MENUSTRING + '</div></li></ul>'
SELECT MENUSTRING =  @MENUSTRING
--SELECT MENUSTRING =  Replace (REPLACE(REPLACE( @MENUSTRING, '<', '$' ),'>','@'),'&','~')

---------------------------THIS RECORDSET ADDED FOR PLAIN TEXT--------------------------
SELECT /*CHARINDEX('ASPX',UPPER(FLDPATH)) , FLDMENUGRPDESC = CASE 
		WHEN A.FLDMENUGRP < 4
			THEN 'SHARE ACCOUNTING '
		WHEN A.FLDMENUGRP > 3
			AND A.FLDMENUGRP < 7
			THEN ' FINANCE ACCOUNTING '
		ELSE 'UTILITIES'
		END
	,*/FLDMENUNAME
	,FLDGRPNAME = REPLACE(FLDGRPNAME,'_',' ')
	,FLDREPORTNAME = REPLACE(FLDREPORTNAME,'_',' ')
	,FLDPATH
	,FLDREPORTCODE = A.FLDREPORTCODE
	,FLDREPORTID = F.FLDREPORTCODE
	,FLDMENUGRP = A.FLDMENUGRP
	,FLDORDER	
FROM TBLREPORTS A(NOLOCK)
	,TBLREPORTGRP B(NOLOCK)
	,TBLCATMENU C(NOLOCK)
	,TBLCATEGORY D(NOLOCK)
	,TBLMENUHEAD E(NOLOCK)
	,CLS_TBLREPORTS F(NOLOCK)
WHERE A.FLDMENUGRP = B.FLDMENUGRP
	AND A.FLDREPORTGRP = B.FLDREPORTGRP
	AND C.FLDREPORTCODE = A.FLDREPORTCODE
	AND E.FLDMENUCODE = A.FLDMENUGRP
	AND D.FLDCATEGORYCODE = @STRCATEGORY
	AND C.FLDCATEGORYCODE LIKE '%,' + CONVERT(VARCHAR, D.FLDCATEGORYCODE) + ',%'
	AND CHARINDEX('ASPX',CAST(UPPER(FLDPATH) AS VARCHAR(500))) > 0
	AND FLDREPORTPATH = A.FLDPATH
	
/*---------------------------------------------- END OF THE PROC ----------------------------*/

GO
