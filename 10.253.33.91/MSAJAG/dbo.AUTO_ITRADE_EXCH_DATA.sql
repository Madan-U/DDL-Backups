-- Object: PROCEDURE dbo.AUTO_ITRADE_EXCH_DATA
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC AUTO_ITRADE_EXCH_DATA      
 AS       
      
 BEGIN      
SELECT A.CL_CODE,EXCHANGE,SEGMENT,PRINT_OPTIONS,L_STATE,CONVERT(VARCHAR(10),ACTIVE_DATE,120)ACTIVE_DATE INTO #CL_DATA      
 FROM CLIENT_DETAILS A WITH (NOLOCK),CLIENT_BROK_DETAILS  B WITH (NOLOCK)      
WHERE A.CL_CODE=B.CL_CODE      
AND A.CL_CODE IN (SELECT * FROM AUTO_ITRADE_DATA)      
      
SELECT SP_PARTY_CODE,SP_DATE_FROM INTO #SCHME FROM SCHEME_MAPPING WITH (NOLOCK)      
WHERE SP_DATE_TO > GETDATE()      
AND SP_PARTY_CODE IN (SELECT * FROM AUTO_ITRADE_DATA)      
AND SP_COMPUTATION_LEVEL='O'      
UNION ALL      
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM [AngelBSECM].BSEDB_AB.DBO.SCHEME_MAPPING WITH (NOLOCK)      
WHERE SP_DATE_TO > GETDATE()      
AND SP_PARTY_CODE IN (SELECT * FROM AUTO_ITRADE_DATA)      
AND SP_COMPUTATION_LEVEL='O'      
UNION ALL      
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM [AngelFO].NSEFO.DBO.SCHEME_MAPPING WITH (NOLOCK)      
WHERE SP_DATE_TO > GETDATE()      
AND SP_PARTY_CODE IN (SELECT * FROM AUTO_ITRADE_DATA)      
AND SP_COMPUTATION_LEVEL='O'      
UNION ALL      
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM [AngelFO].NSECURFO.DBO.SCHEME_MAPPING WITH (NOLOCK)      
WHERE SP_DATE_TO > GETDATE()      
AND SP_PARTY_CODE IN (SELECT * FROM AUTO_ITRADE_DATA)      
AND SP_COMPUTATION_LEVEL='O'      
UNION ALL      
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM [AngelCommodity].MCDX.DBO.SCHEME_MAPPING WITH (NOLOCK)      
WHERE SP_DATE_TO > GETDATE()      
AND SP_PARTY_CODE IN (SELECT * FROM AUTO_ITRADE_DATA)      
AND SP_COMPUTATION_LEVEL='O'      
UNION ALL      
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM [AngelCommodity].NCDX.DBO.SCHEME_MAPPING WITH (NOLOCK)      
WHERE SP_DATE_TO > GETDATE()      
AND SP_PARTY_CODE IN (SELECT * FROM AUTO_ITRADE_DATA)      
AND SP_COMPUTATION_LEVEL='O'      
UNION ALL      
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM [AngelCommodity].BSECURFO.DBO.SCHEME_MAPPING WITH (NOLOCK)      
WHERE SP_DATE_TO > GETDATE()      
AND SP_PARTY_CODE IN (SELECT * FROM AUTO_ITRADE_DATA)      
AND SP_COMPUTATION_LEVEL='O'      
      
SELECT DISTINCT SP_PARTY_CODE AS I_TRADE_CLIENT, MAX(CONVERT(VARCHAR(10),SP_DATE_FROM,120))ITRADE_ACTIVE_DATE INTO #SCHME_FIN  FROM #SCHME GROUP BY SP_PARTY_CODE      
      
TRUNCATE TABLE AUTO_ITRADE_DATA_OUT      
INSERT INTO AUTO_ITRADE_DATA_OUT      
SELECT *  FROM #CL_DATA A LEFT OUTER JOIN  #SCHME_FIN B      
ON I_TRADE_CLIENT= CL_CODE      
END

GO
