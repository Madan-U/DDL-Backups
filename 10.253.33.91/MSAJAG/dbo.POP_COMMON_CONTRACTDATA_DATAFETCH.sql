CREATE   PROCEDURE [dbo].[POP_COMMON_CONTRACTDATA_DATAFETCH] (        
 @FROMDATE VARCHAR(11)       
 )        
AS    

--SET XACT_ABORT ON   
    
    SET NOCOUNT ON;
--------------------------------NSE---------------------    
    
TRUNCATE TABLE CONTRACT_DATA_COMMON    
    
TRUNCATE TABLE CONTRACT_MASTER_COMMON    
    
    
    
INSERT INTO CONTRACT_DATA_COMMON    
SELECT D.* FROM SETT_MST M (NOLOCK)    , CONTRACT_DATA D  (NOLOCK)   
WHERE M.SETT_NO=D.SETT_NO    
AND  M.SETT_TYPE=D.SETT_TYPE    
AND M.START_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
    
INSERT INTO CONTRACT_MASTER_COMMON    
SELECT D.* FROM SETT_MST M (NOLOCK)    , CONTRACT_MASTER D (NOLOCK)      
WHERE M.SETT_NO=D.SETT_NO    
AND  M.SETT_TYPE=D.SETT_TYPE    
AND M.START_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    


DELETE T FROM TBL_CONTRACT_TERMINAL_MAPPING T WITH(NOLOCK) WHERE REMARK_ID='R'

INSERT INTO TBL_CONTRACT_TERMINAL_MAPPING (TERMINAL_ID,ORDER_NO,REMARK_ID,REMARK_DESC,FROM_DATE,END_DATE,INSERTEDON,INSERTEDBY)
SELECT '',ORDER_NO,'R','REs',@FROMDATE,'2049-12-31 00:00:00.000',GETDATE(),'AUTOPROCESS'
FROM  SETTLEMENT  D  (NOLOCK)   
WHERE RIGHT(SCRIP_CD,3)='-RE' 
AND  SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    


    
------------------------------BSE----------------------------------    
    
DELETE AngelBSECM.BSEDB_AB.DBO.CONTRACT_DATA_COMMON    
    
DELETE AngelBSECM.BSEDB_AB.DBO.CONTRACT_MASTER_COMMON    
    
    
INSERT INTO AngelBSECM.BSEDB_AB.DBO.CONTRACT_DATA_COMMON    
SELECT D.* FROM AngelBSECM.BSEDB_AB.DBO.SETT_MST M WITH(NOLOCK)  , AngelBSECM.BSEDB_AB.DBO.CONTRACT_DATA D   WITH(NOLOCK) 
WHERE M.SETT_NO=D.SETT_NO    
AND  M.SETT_TYPE=D.SETT_TYPE    
AND M.START_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
INSERT INTO AngelBSECM.BSEDB_AB.DBO.CONTRACT_MASTER_COMMON    
SELECT D.* FROM AngelBSECM.BSEDB_AB.DBO.SETT_MST M WITH(NOLOCK) , AngelBSECM.BSEDB_AB.DBO.CONTRACT_MASTER D   WITH(NOLOCK)  
WHERE M.SETT_NO=D.SETT_NO    
AND  M.SETT_TYPE=D.SETT_TYPE    
AND M.START_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'  


DELETE T FROM AngelBSECM.BSEDB_AB.DBO.TBL_CONTRACT_TERMINAL_MAPPING T WITH(NOLOCK) WHERE REMARK_ID='R'

INSERT INTO TBL_CONTRACT_TERMINAL_MAPPING (TERMINAL_ID,ORDER_NO,REMARK_ID,REMARK_DESC,FROM_DATE,END_DATE,INSERTEDON,INSERTEDBY)
SELECT '',ORDER_NO,'R','REs',@FROMDATE,'2049-12-31 00:00:00.000',GETDATE(),'AUTOPROCESS'
FROM  AngelBSECM.BSEDB_AB.DBO.SETTLEMENT  D WITH (NOLOCK)   
WHERE LEFT(SCRIP_CD,3)='750' 
AND  SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'   



    
---------------------------NSEFO------------------------------------------    
  /*  
    
DELETE T FROM ANGELFO.NSEFO.DBO.CONTRACT_DATA_COMMON  T  WITH(NOLOCK)
    
DELETE T FROM ANGELFO.NSEFO.DBO.CONTRACT_MASTER_COMMON   T  WITH(NOLOCK)
    
    
    
INSERT INTO ANGELFO.NSEFO.DBO.CONTRACT_MASTER_COMMON    
SELECT * FROM ANGELFO.NSEFO.DBO.CONTRACT_MASTER  WITH(NOLOCK)  WHERE TRADE_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
    
INSERT INTO ANGELFO.NSEFO.DBO.CONTRACT_DATA_COMMON    
SELECT  * FROM  ANGELFO.NSEFO.DBO.CONTRACT_DATA WITH(NOLOCK)  WHERE SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    */


EXEC ANGELFO.NSEFO.DBO.CONTRACTDATA_DATAFETCH @FROMDATE

EXEC ANGELCOMMODITY.BSEFO.DBO.CONTRACTDATA_DATAFETCH @FROMDATE
    
---------------------------NSECURFO---------------------------------------------    
    
DELETE ANGELFO.NSECURFO.DBO.CONTRACT_DATA_COMMON    
    
DELETE ANGELFO.NSECURFO.DBO.CONTRACT_MASTER_COMMON    
    
    
INSERT INTO ANGELFO.NSECURFO.DBO.CONTRACT_MASTER_COMMON    
SELECT * FROM ANGELFO.NSECURFO.DBO.CONTRACT_MASTER WITH(NOLOCK)  WHERE TRADE_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
INSERT INTO ANGELFO.NSECURFO.DBO.CONTRACT_DATA_COMMON    
SELECT  * FROM ANGELFO.NSECURFO.DBO.CONTRACT_DATA WITH(NOLOCK)  WHERE SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
----------------------------MCDXCDS--------------------------------------------    
    
    
DELETE ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_DATA_COMMON    
    
DELETE ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_MASTER_COMMON    
    
    
INSERT INTO ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_MASTER_COMMON    
SELECT * FROM ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_MASTER WITH(NOLOCK)  WHERE TRADE_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
INSERT INTO ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_DATA_COMMON    
SELECT * FROM ANGELCOMMODITY.MCDXCDS.DBO.CONTRACT_DATA WITH(NOLOCK)  WHERE SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
---------------------------BSECURFO---------------------------------------------    
    
DELETE ANGELCOMMODITY.BSECURFO.DBO.CONTRACT_DATA_COMMON    
    
DELETE ANGELCOMMODITY.BSECURFO.DBO.CONTRACT_MASTER_COMMON    
    
    
INSERT INTO ANGELCOMMODITY.BSECURFO.DBO.CONTRACT_MASTER_COMMON    
SELECT TRADE_DATE,PARTY_CODE,PARTYNAME,L_ADDRESS1,L_ADDRESS2,L_ADDRESS3,L_CITY,L_STATE,L_ZIP,BRANCH_CD,SUBBROKER,TRADER,AREA,REGION,FAMILY,PAN_GIR_NO,OFF_PHONE1,OFF_PHONE2,PRINTF,MAPIDID,UCC_CODE,SERVICE_CHRG,BROKERNOTE,TURNOVER_TAX,SEBI_TURN_TAX,OTHER_CHRG,INSURANCE_CHRG,SEBI_NO
 FROM ANGELCOMMODITY.BSECURFO.DBO.CONTRACT_MASTER WITH(NOLOCK)  WHERE TRADE_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    
    
INSERT INTO ANGELCOMMODITY.BSECURFO.DBO.CONTRACT_DATA_COMMON    
SELECT CONTRACTNO,ORDER_NO,ORDER_TIME,TRADE_NO,TRADETIME,SAUDA_DATE,PARTY_CODE,
INST_TYPE,SYMBOL,EXPIRYDATE,STRIKE_PRICE,OPTION_TYPE,AUCTIONPART,PQTY,SQTY,PRATE,
SRATE,PBROK,SBROK,PNETRATE,SNETRATE,AMOUNT,PAMT,SAMT,SELL_BUY,PSERVICE_TAX,SSERVICE_TAX,
BROKER_CHRG,TURN_TAX,SEBI_TAX,OTHER_CHRG,INS_CHRG,SERVICE_TAX,NSERTAX,BROKERAGE,ORDFLG
 FROM ANGELCOMMODITY.BSECURFO.DBO.CONTRACT_DATA WITH(NOLOCK)  WHERE SAUDA_DATE BETWEEN  @FROMDATE AND @FROMDATE + ' 23:59:59'    

--SET XACT_ABORT OFF

GO
