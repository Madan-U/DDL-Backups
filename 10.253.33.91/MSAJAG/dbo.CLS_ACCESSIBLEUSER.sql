	select * from CLS_TBLREPORTS
	SELECT A.FLDREPORTCODE FROM TBLREPORTS A, CLS_TBLREPORTS B WHERE A.FLDPATH = B.FLDREPORTPATH AND  B.FLDREPORTCODE = 'CLIENT_LEDGER'
	SELECT * FROM TBLCATMENU WHERE FLDREPORTCODE = 64 AND FLDCATEGORYCODE LIKE '%' + '388' + '%'
	select * from tblUserControlGlobals where FLDCATEGORYID = 22

*/
DECLARE @RPTCODE INT,
		@NEWSESSION INT
SELECT @RPTCODE = A.FLDREPORTCODE FROM TBLREPORTS A, CLS_TBLREPORTS B WHERE A.FLDPATH = B.FLDREPORTPATH AND  B.FLDREPORTCODE = @REPORTCODE

IF (SELECT COUNT(1) FROM TBLCATMENU WHERE FLDREPORTCODE = @RPTCODE AND FLDCATEGORYCODE LIKE '%' + @USERCATEGORY + '%') > 0
 BEGIN
  print 1
	select @NEWSESSION = FLDTIMEOUT from tblUserControlGlobals where FLDCATEGORYID = @USERCATEGORY
	SELECT ALLOWED = 1,NEWSESSION = @NEWSESSION
 END
ELSE
 BEGIN
 print 2
	SELECT ALLOWED =1, NEWSESSION =  @NEWSESSION
 END

GO
