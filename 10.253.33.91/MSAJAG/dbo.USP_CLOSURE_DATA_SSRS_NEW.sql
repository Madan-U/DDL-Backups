CREATE PROC [dbo].[USP_CLOSURE_DATA_SSRS_NEW]  
AS    
  
BEGIN    
  
TRUNCATE TABLE CLOSURE_DATA_SSRS_NEW  
  
INSERT INTO CLOSURE_DATA_SSRS_NEW VALUES ('CL_CODE','ACTIVE_DATE','INACTIVE_FROM','LONG_NAME','BRANCH_CD','SUB_BROKER','DEACTIVE_VALUE','A_B2C','DEACTIVE_REMARKS')  
  
INSERT INTO CLOSURE_DATA_SSRS_NEW  
SELECT DISTINCT(A.CL_CODE),  
MIN(ACTIVE_DATE)AS ACTIVE_DATE,  
MAX(INACTIVE_FROM)AS INACTIVE_FROM,  
LONG_NAME,  
BRANCH_CD,  
SUB_BROKER,  
DEACTIVE_VALUE  ,        
(case when B2C='Y' then 'B2C' ELSE 'B2B'END)AS B2C,        
DEACTIVE_REMARKS  
FROM CLIENT_BROK_DETAILS A WITH (NOLOCK), INTRANET.RISK.dbo.client_details  B WITH (NOLOCK)        
WHERE A.CL_CODE=B.CL_CODE          
AND INACTIVE_FROM<=GETDATE()          
AND Deactive_value='C'        
GROUP BY A.CL_CODE,long_name,branch_cd,SUB_BROKER,b2c,DEACTIVE_REMARKS,DEACTIVE_VALUE    
ORDER BY A.CL_CODE    
  
  
DECLARE @REPORTDATE VARCHAR(11), @PATH VARCHAR(100), @QRY NVARCHAR(4000), @MAINQRY VARCHAR(4000)  
  
SET @REPORTDATE=REPLACE(CONVERT(VARCHAR(10),GETDATE(),3),'/','')  
SET @PATH= 'J:\Backoffice\AUTOMATION\CLOSURE_DATA\' +'CLOSURE_DATA_' + @REPORTDATE + '.CSV'  
SET @QRY = 'SELECT * FROM [MSAJAG].DBO.CLOSURE_DATA_SSRS_NEW order by 8'  
SET @MAINQRY= 'BCP "' + @QRY + '" QUERYOUT "' + @PATH + '" -c -t"," -r"\n" -T'    
    
--PRINT @REPORTDATE  
--PRINT @PATH  
--PRINT @QRY  
PRINT @MAINQRY  
  
EXEC MASTER..XP_CMDSHELL @MAINQRY, NO_OUTPUT

SELECT 'TODAYS FILE HAS BEEN CREATED ON J:\Backoffice\AUTOMATION\CLOSURE_DATA'
  
END

GO
