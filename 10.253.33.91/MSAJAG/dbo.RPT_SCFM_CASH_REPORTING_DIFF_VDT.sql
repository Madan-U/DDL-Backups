  GROUP BY CLTCODE ORDER BY CLTCODE ASC          
      */

	  SELECT CLTCODE,ROUND(VAMT,2) AS TOTAL INTO #FINAL  FROM #NSE ,CLIENT_DETAILS WITH (NOLOCK)
	  WHERE CLTCODE = CL_CODE

	  SELECT CLTCODE,SUM(ROUND(TOTAL,2)) AS TOTAL INTO #TEMP_VDT  FROM #FINAL 
	  WHERE CLTCODE <>'BBBB'   AND CLTCODE >='A0' AND CLTCODE <='ZZZZZZZZZZZZ'    
	  GROUP BY CLTCODE
        
	  DELETE FROM #CCERPT_VDT WHERE UCC='BBBB'      
      
  /*    
SELECT REPORT_DATE,UCC=LTRIM(RTRIM(ISNULL(UCC, ''))),VDT_BAL=SUM(VDT_BAL) INTO #CASHREPORTING_VDT        
FROM #CCERPT_VDT WHERE UCC BETWEEN 'A' AND 'ZZZZZZZZZZZZZZZZZ9999' GROUP BY REPORT_DATE,UCC        
  */    


SELECT REPORT_DATE,UCC,VDT_BAL=SUM(VDT_BAL) INTO #CASHREPORTING_VDT        
FROM #CCERPT_VDT WHERE UCC BETWEEN 'A' AND 'ZZZZZZZZZZZZZZZZZ9999' GROUP BY REPORT_DATE,UCC        
        
SELECT Report_Date=CONVERT(VARCHAR(11),@FROMDATE,105),UCC,      
TB_VDT_AMOUNT=ROUND(SUM(CLASS_LEDGER),2),CCE_VDT_AMOUNT=SUM(CASH_VDT_BAL) INTO #DIFF_DATA_VDT        
FROM (        
SELECT UCC,CLASS_LEDGER=CONVERT(MONEY,0),CASH_VDT_BAL=VDT_BAL FROM #CASHREPORTING_VDT        
UNION ALL        
SELECT CLTCODE,CLASS_LEDGER=TOTAL,CASH_VDT_BAL=CONVERT(MONEY,0) FROM #TEMP_VDT ) AS A        
GROUP BY UCC        
        
      
TRUNCATE TABLE SFCM_DIFF_DATA_NEW_VDT        
        
        
INSERT INTO SFCM_DIFF_DATA_NEW_VDT  SELECT * FROM #DIFF_DATA_VDT  WHERE (ABS(TB_VDT_AMOUNT) <> ABS(CCE_VDT_AMOUNT))        
  
CREATE TABLE #EXCHANGE_CLINETS(CL_CODE VARCHAR(10),EXCHANGE VARCHAR(3))  

ALTER TABLE #EXCHANGE_CLINETS ADD CONSTRAINT EXCH DEFAULT 'MCX' FOR EXCHANGE
  
INSERT INTO #EXCHANGE_CLINETS(CL_CODE) EXEC GET_EXCH_REG_CLIENTS 'MCX', 'A000000000', 'ZZZZZZZZZZ', @FROMDATE      
  
ALTER TABLE #EXCHANGE_CLINETS DROP CONSTRAINT EXCH 

ALTER TABLE #EXCHANGE_CLINETS ADD CONSTRAINT EXCH DEFAULT 'NCX' FOR EXCHANGE
    
INSERT INTO #EXCHANGE_CLINETS(CL_CODE) EXEC GET_EXCH_REG_CLIENTS 'NCX', 'A000000000', 'ZZZZZZZZZZ', @FROMDATE   

  
--CREATE INDEX CLIIDX ON #EXCHANGE_CLINETS(CL_CODE,EXCHANGE)      
--CREATE INDEX CLIIDX1 ON #TEMP_VDT(CLTCODE)      
--CREATE INDEX CLIIDX2 ON #CASHREPORTING_VDT(UCC)      
  
  
TRUNCATE TABLE TBL_REPORTING_DIFF_DATA_VDT      
        
INSERT INTO TBL_REPORTING_DIFF_DATA_VDT      
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),Report='TB-Debtors',[Balance In CR]=ABS(SUM(TOTAL))/10000000,[Balance In Rs]=ABS(SUM(TOTAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #TEMP_VDT WHERE TOTAL > 0         
UNION ALL        
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='TB-Creditors',AMOUNT_IN_CR=ABS(SUM(TOTAL))/10000000,AMOUNT=ABS(SUM(TOTAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #TEMP_VDT WHERE TOTAL < 0        
UNION ALL        
SELECT REPORT_DATE=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='WER-Debtors',AMOUNT_IN_CR=ABS(SUM(VDT_BAL))/10000000,AMOUNT=ABS(SUM(VDT_BAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #CASHREPORTING_VDT WHERE VDT_BAL < 0 GROUP BY REPORT_DATE        
UNION ALL        
SELECT REPORT_DATE=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='WER-Creditors',AMOUNT_IN_CR=ABS(SUM(VDT_BAL))/10000000,AMOUNT=ABS(SUM(VDT_BAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #CASHREPORTING_VDT WHERE VDT_BAL > 0 GROUP BY REPORT_DATE        
--MCDX  
UNION ALL   
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),Report='TB-Debtors - MCX',[Balance In CR]=ABS(SUM(TOTAL))/10000000,[Balance In Rs]=ABS(SUM(TOTAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #TEMP_VDT , #EXCHANGE_CLINETS WHERE TOTAL > 0 AND CLTCODE = CL_CODE  AND EXCHANGE = 'MCX'  
UNION ALL        
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='TB-Creditors - MCX',AMOUNT_IN_CR=ABS(SUM(TOTAL))/10000000,AMOUNT=ABS(SUM(TOTAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #TEMP_VDT , #EXCHANGE_CLINETS WHERE TOTAL < 0  AND CLTCODE = CL_CODE  AND EXCHANGE = 'MCX'      
UNION ALL        
SELECT REPORT_DATE=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='WER-Debtors - MCX',AMOUNT_IN_CR=ABS(SUM(VDT_BAL))/10000000,AMOUNT=ABS(SUM(VDT_BAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #CASHREPORTING_VDT , #EXCHANGE_CLINETS WHERE VDT_BAL < 0 AND UCC = CL_CODE  AND EXCHANGE = 'MCX' GROUP BY REPORT_DATE        
UNION ALL        
SELECT REPORT_DATE=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='WER-Creditors - MCX',AMOUNT_IN_CR=ABS(SUM(VDT_BAL))/10000000,AMOUNT=ABS(SUM(VDT_BAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)   
FROM #CASHREPORTING_VDT , #EXCHANGE_CLINETS    WHERE VDT_BAL > 0 AND UCC = CL_CODE  AND EXCHANGE = 'MCX' GROUP BY REPORT_DATE        
--NCDX  
UNION ALL   
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),Report='TB-Debtors - NCX',[Balance In CR]=ABS(SUM(TOTAL))/10000000,[Balance In Rs]=ABS(SUM(TOTAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #TEMP_VDT , #EXCHANGE_CLINETS WHERE TOTAL > 0 AND CLTCODE = CL_CODE  AND EXCHANGE = 'NCX'  
UNION ALL        
SELECT Report_Date=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='TB-Creditors - NCX',AMOUNT_IN_CR=ABS(SUM(TOTAL))/10000000,AMOUNT=ABS(SUM(TOTAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #TEMP_VDT , #EXCHANGE_CLINETS WHERE TOTAL < 0  AND CLTCODE = CL_CODE  AND EXCHANGE = 'NCX'      
UNION ALL        
SELECT REPORT_DATE=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='WER-Debtors - NCX',AMOUNT_IN_CR=ABS(SUM(VDT_BAL))/10000000,AMOUNT=ABS(SUM(VDT_BAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #CASHREPORTING_VDT , #EXCHANGE_CLINETS WHERE VDT_BAL < 0 AND UCC = CL_CODE  AND EXCHANGE = 'NCX' GROUP BY REPORT_DATE        
UNION ALL        
SELECT REPORT_DATE=CONVERT(VARCHAR(11),CONVERT(DATETIME,@FROMDATE),105),REPORT='WER-Creditors - NCX',AMOUNT_IN_CR=ABS(SUM(VDT_BAL))/10000000,AMOUNT=ABS(SUM(VDT_BAL)),        
DIFF_TB_CCE=CONVERT(MONEY,0)      
FROM #CASHREPORTING_VDT , #EXCHANGE_CLINETS WHERE VDT_BAL > 0 AND UCC = CL_CODE  AND EXCHANGE = 'NCX' GROUP BY REPORT_DATE        
      
      
UPDATE TBL_REPORTING_DIFF_DATA_VDT SET DIFF_TB_CCE =         
(SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='TB-Debtors') - (SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='WER-Debtors')        
WHERE Report='WER-Debtors'        
        
UPDATE TBL_REPORTING_DIFF_DATA_VDT SET DIFF_TB_CCE =         
(SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='TB-Creditors') - (SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='WER-Creditors')        
WHERE Report='WER-Creditors'  
  
UPDATE TBL_REPORTING_DIFF_DATA_VDT SET DIFF_TB_CCE =         
(SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='TB-Debtors - MCX') - (SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='WER-Debtors - MCX')        
WHERE Report='WER-Debtors - MCX'        
        
UPDATE TBL_REPORTING_DIFF_DATA_VDT SET DIFF_TB_CCE =         
(SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='TB-Creditors - MCX') - (SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='WER-Creditors - MCX')        
WHERE Report='WER-Creditors - MCX'        
        
UPDATE TBL_REPORTING_DIFF_DATA_VDT SET DIFF_TB_CCE =         
(SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='TB-Debtors - NCX') - (SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='WER-Debtors - NCX')        
WHERE Report='WER-Debtors - NCX'        
        
UPDATE TBL_REPORTING_DIFF_DATA_VDT SET DIFF_TB_CCE =         
(SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='TB-Creditors - NCX') - (SELECT [Balance In Rs] FROM TBL_REPORTING_DIFF_DATA_VDT WHERE Report='WER-Creditors - NCX')        
WHERE Report='WER-Creditors - NCX'        
     
 
DECLARE @FNOFILENAME VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'SCFM_CASH_DIFF_REPORT_VDT_' + CONVERT(VARCHAR,CONVERT(DATETIME, @FROMDATE), 112) + '.CSV'        
DECLARE @FNO VARCHAR(MAX)                                    
SET @FNO = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT_DATE'''',''''CLIENT_CODE'''',''''TB_VDT_AMOUNT'''',''''WER_VDT_AMOUNT'''''         
SET @FNO = @FNO + ' UNION ALL SELECT REPORT_DATE,LTRIM(RTRIM(UCC)),CONVERT(VARCHAR,TB_VDT_AMOUNT),CONVERT(VARCHAR,CCE_VDT_AMOUNT) FROM MSAJAG.DBO.SFCM_DIFF_DATA_NEW_VDT" QUERYOUT ' +@FNOFILENAME+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'        
EXEC(@FNO)      
      
DECLARE @FNOFILENAMES VARCHAR(100) = 'J:\BACKOFFICE\EXPORT\' +'SCFM_CASH_DIFF_REPORT_SUMMARY_VDT_' + CONVERT(VARCHAR,CONVERT(DATETIME, @FROMDATE), 112) + '.CSV'      
DECLARE @FNOS VARCHAR(MAX)                                    
SET @FNOS = 'EXEC MASTER.DBO.XP_CMDSHELL ''BCP "SELECT ''''REPORT_DATE'''',''''REPORT'''',''''Balance In CR'''',''''Balance In Rs'''',''''DIFF_TB_WER'''''         
SET @FNOS = @FNOS + ' UNION ALL SELECT REPORT_DATE,LTRIM(RTRIM(Report)),CONVERT(VARCHAR,[Balance In CR]),CONVERT(VARCHAR,[Balance In Rs]),CONVERT(VARCHAR,DIFF_TB_CCE) FROM MSAJAG.DBO.TBL_REPORTING_DIFF_DATA_VDT" QUERYOUT ' +@FNOFILENAMES+ ' -c -t"," -c -t"," -r"\n" -T'', NO_OUTPUT'        
EXEC(@FNOS)      
  
      
SELECT [REPORT_DATE],[Report],[Balance In CR],[Balance In Rs],[DIFF_TB_CCE] AS [DIFF_TB_WER] FROM TBL_REPORTING_DIFF_DATA_VDT ORDER BY [Balance In Rs],REPORT        
      
      
