 VDT<=@FROMDATE  + ' 23:59' GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
  HAVING SUM(VAMT) <>0         
          
 ------------NSEFO          
INSERT INTO #NSE
 SELECT CLTCODE,SUM(VAMT)AS VAMT  --INTO #NSEFO 
