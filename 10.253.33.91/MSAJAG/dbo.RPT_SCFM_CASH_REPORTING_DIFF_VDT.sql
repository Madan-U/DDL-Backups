CREATE PROC [dbo].[RPT_SCFM_CASH_REPORTING_DIFF_VDT] (@FROMDATE VARCHAR(11),@TODATE VARCHAR(11))      
AS      
      
--RPT_SCFM_CASH_REPORTING_DIFF_VDT 'NOV  2 2022','NOV  2 2022'           
      
SELECT REPORT_DATE,SNO INTO #CCE_VDT FROM ACCOUNT.DBO.CLIENT_CASH_DETAILS(NOLOCK)         
WHERE REPORT_DATE BETWEEN @FROMDATE AND @FROMDATE + ' 23:59'      
      
      
SELECT REPORT_DATE,UCC=LTRIM(RTRIM(ISNULL(PARTY_CODE, ''))),        
EXCHANGE=CASE WHEN L.EXCHANGE='MTF' THEN 'MTF' ELSE 'BROKING' END,VDT_BAL=ROUND(L.VDT_BAL,2) INTO #CCERPT_VDT        
FROM #CCE_VDT C, ACCOUNT.DBO.CLIENT_CASH_LED_BALANCES L WITH (NOLOCK) WHERE C.SNO = L.SNO        
  

DELETE FROM #CCERPT_VDT WHERE UCC='BBBB'   
 
      
DECLARE @FINSTDT DATETIME       
      
SELECT @FINSTDT = SDTCUR FROM ACCOUNT.DBO.PARAMETER  WITH (NOLOCK) WHERE @FROMDATE BETWEEN SDTCUR AND LDTCUR      

------------NSE          
          
SELECT CLTCODE,SUM(VAMT)AS VAMT  INTO #NSE FROM (          
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   ACCOUNT.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE + ' 23:59'  GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
  HAVING SUM(VAMT) <>0         
          
           
----NSEBLBS         
INSERT INTO #NSE
SELECT CLTCODE,SUM(VAMT)AS VAMT  --INTO #SLBS 
