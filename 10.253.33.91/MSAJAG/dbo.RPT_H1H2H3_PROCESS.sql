-- Object: PROCEDURE dbo.RPT_H1H2H3_PROCESS
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[RPT_H1H2H3_PROCESS]
	(
	@PROCESSDATE VARCHAR(11)
	)

	AS
	
	-- EXEC RPT_H1H2H3_PROCESS 'JUN 30 2010'

	DECLARE @@FROMDATE	VARCHAR(11)
	DECLARE @@TODATE	VARCHAR(11)
	DECLARE @@STARTQ	VARCHAR(10)
	DECLARE @@C_MONTH	VARCHAR(2)	
	DECLARE @@Q_POS		INT
	DECLARE	@@Q_STATUS	INT

	SET @@FROMDATE	= LEFT(@PROCESSDATE,3) + '  1 ' + RIGHT(@PROCESSDATE,4)
	SET @@TODATE	= @PROCESSDATE
	SET @@STARTQ	= '1,4,7,10'
	SET @@C_MONTH	= MONTH(@@FROMDATE)


	IF (CHARINDEX(@@C_MONTH,@@STARTQ) > 0)
	BEGIN
		TRUNCATE TABLE PRADNYA.DBO.TBL_H_PRINTFLAG
	END


	WHILE @@C_MONTH > 3
	BEGIN
		IF (@@C_MONTH >= 3)
		BEGIN
			SET @@C_MONTH = @@C_MONTH - 3
		END
	END
	SET @@Q_POS = @@C_MONTH


	SELECT
		PARTY_CODE,
		Q_MONTH,
		Q1_TOT_BROK_1	= SUM(Q1_TOT_BROK),
		Q2_TOT_BROK_1	= SUM(Q2_TOT_BROK),
		Q3_TOT_BROK_1	= SUM(Q3_TOT_BROK),
		PRINT_FLAG
	INTO
		#BROKDETAIL
	FROM
		(		
		SELECT 
			PARTY_CODE	= PARENTCODE,
			Q_MONTH		= MONTH(SAUDA_DATE),
			Q1_TOT_BROK	= (CASE WHEN @@Q_POS = 1 THEN SUM(PBROKTRD+SBROKTRD+SBROKDEL+PBROKDEL) ELSE 0 END),
			Q2_TOT_BROK	= (CASE WHEN @@Q_POS = 2 THEN SUM(PBROKTRD+SBROKTRD+SBROKDEL+PBROKDEL) ELSE 0 END),
			Q3_TOT_BROK	= (CASE WHEN @@Q_POS = 3 THEN SUM(PBROKTRD+SBROKTRD+SBROKDEL+PBROKDEL) ELSE 0 END),
			PRINT_FLAG	= CONVERT(VARCHAR(5),'')
		FROM
			AngelNSECM.MSAJAG.DBO.CMBILLVALAN C WITH (NOLOCK),
			AngelNSECM.MSAJAG.DBO.CLIENT2 C2 WITH (NOLOCK)
		WHERE
			C.PARTY_CODE = C2.PARTY_CODE
			AND SAUDA_DATE BETWEEN @@FROMDATE AND @@TODATE + ' 23:59:59'
			--AND PARTY_CODE NOT IN (SELECT DISTINCT PARTY_CODE FROM PRADNYA.DBO.TBL_H_PRINTFLAG)
		GROUP BY
			PARENTCODE,
			MONTH(SAUDA_DATE)
		
		UNION ALL

		SELECT 
			PARTY_CODE	= PARENTCODE,
			Q_MONTH		= MONTH(SAUDA_DATE),
			Q1_TOT_BROK	= (CASE WHEN @@Q_POS = 1 THEN SUM(PBROKTRD+SBROKTRD+SBROKDEL+PBROKDEL) ELSE 0 END),
			Q2_TOT_BROK	= (CASE WHEN @@Q_POS = 2 THEN SUM(PBROKTRD+SBROKTRD+SBROKDEL+PBROKDEL) ELSE 0 END),
			Q3_TOT_BROK	= (CASE WHEN @@Q_POS = 3 THEN SUM(PBROKTRD+SBROKTRD+SBROKDEL+PBROKDEL) ELSE 0 END),
			PRINT_FLAG	= CONVERT(VARCHAR(5),'')
		FROM
			AngelBSECM.BSEDB_AB.DBO.CMBILLVALAN C WITH (NOLOCK),
			AngelBSECM.BSEDB_AB.DBO.CLIENT2 C2 WITH (NOLOCK)
		WHERE
			C.PARTY_CODE = C2.PARTY_CODE
			AND SAUDA_DATE BETWEEN @@FROMDATE AND @@TODATE + ' 23:59:59'
			--AND PARTY_CODE NOT IN (SELECT DISTINCT PARTY_CODE FROM PRADNYA.DBO.TBL_H_PRINTFLAG)
		GROUP BY
			PARENTCODE,
			MONTH(SAUDA_DATE)

		UNION ALL

		SELECT 
			PARTY_CODE	= PARENTCODE,
			Q_MONTH		= MONTH(SAUDA_DATE),
			Q1_TOT_BROK	= (CASE WHEN @@Q_POS = 1 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q2_TOT_BROK	= (CASE WHEN @@Q_POS = 2 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q3_TOT_BROK	= (CASE WHEN @@Q_POS = 3 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			PRINT_FLAG	= CONVERT(VARCHAR(5),'')
		FROM
			ANGELFO.NSEFO.DBO.FOBILLVALAN C WITH (NOLOCK),
			ANGELFO.NSEFO.DBO.CLIENT2 C2 WITH (NOLOCK)
		WHERE
			C.PARTY_CODE = C2.PARTY_CODE
			AND SAUDA_DATE BETWEEN @@FROMDATE AND @@TODATE + ' 23:59:59'
			--AND PARTY_CODE NOT IN (SELECT DISTINCT PARTY_CODE FROM PRADNYA.DBO.TBL_H_PRINTFLAG)
		GROUP BY
			PARENTCODE,
			MONTH(SAUDA_DATE)

		UNION ALL

		SELECT 
			PARTY_CODE	= PARENTCODE,
			Q_MONTH		= MONTH(SAUDA_DATE),
			Q1_TOT_BROK	= (CASE WHEN @@Q_POS = 1 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q2_TOT_BROK	= (CASE WHEN @@Q_POS = 2 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q3_TOT_BROK	= (CASE WHEN @@Q_POS = 3 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			PRINT_FLAG	= CONVERT(VARCHAR(5),'')
		FROM
			ANGELCOMMODITY.MCDX.DBO.FOBILLVALAN C WITH (NOLOCK),
			ANGELCOMMODITY.MCDX.DBO.CLIENT2 C2 WITH (NOLOCK)
		WHERE
			C.PARTY_CODE = C2.PARTY_CODE
			AND SAUDA_DATE BETWEEN @@FROMDATE AND @@TODATE + ' 23:59:59'
			--AND PARTY_CODE NOT IN (SELECT DISTINCT PARTY_CODE FROM PRADNYA.DBO.TBL_H_PRINTFLAG)
		GROUP BY
			PARENTCODE,
			MONTH(SAUDA_DATE)

		UNION ALL

		SELECT 
			PARTY_CODE	= PARENTCODE,
			Q_MONTH		= MONTH(SAUDA_DATE),
			Q1_TOT_BROK	= (CASE WHEN @@Q_POS = 1 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q2_TOT_BROK	= (CASE WHEN @@Q_POS = 2 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q3_TOT_BROK	= (CASE WHEN @@Q_POS = 3 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			PRINT_FLAG	= CONVERT(VARCHAR(5),'')
		FROM
			ANGELCOMMODITY.NCDX.DBO.FOBILLVALAN C WITH (NOLOCK),
			ANGELCOMMODITY.NCDX.DBO.CLIENT2 C2 WITH (NOLOCK)
		WHERE
			C.PARTY_CODE = C2.PARTY_CODE
			AND SAUDA_DATE BETWEEN @@FROMDATE AND @@TODATE + ' 23:59:59'
			--AND PARTY_CODE NOT IN (SELECT DISTINCT PARTY_CODE FROM PRADNYA.DBO.TBL_H_PRINTFLAG)
		GROUP BY
			PARENTCODE,
			MONTH(SAUDA_DATE)

		UNION ALL

		SELECT 
			PARTY_CODE	= PARENTCODE,
			Q_MONTH		= MONTH(SAUDA_DATE),
			Q1_TOT_BROK	= (CASE WHEN @@Q_POS = 1 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q2_TOT_BROK	= (CASE WHEN @@Q_POS = 2 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q3_TOT_BROK	= (CASE WHEN @@Q_POS = 3 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			PRINT_FLAG	= CONVERT(VARCHAR(5),'')
		FROM
			ANGELFO.NSECURFO.DBO.FOBILLVALAN C WITH (NOLOCK),
			ANGELFO.NSECURFO.DBO.CLIENT2 C2 WITH (NOLOCK)
		WHERE
			C.PARTY_CODE = C2.PARTY_CODE
			AND SAUDA_DATE BETWEEN @@FROMDATE AND @@TODATE + ' 23:59:59'
			--AND PARTY_CODE NOT IN (SELECT DISTINCT PARTY_CODE FROM PRADNYA.DBO.TBL_H_PRINTFLAG)
		GROUP BY
			PARENTCODE,
			MONTH(SAUDA_DATE)

		UNION ALL

		SELECT 
			PARTY_CODE	= PARENTCODE,
			Q_MONTH		= MONTH(SAUDA_DATE),
			Q1_TOT_BROK	= (CASE WHEN @@Q_POS = 1 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q2_TOT_BROK	= (CASE WHEN @@Q_POS = 2 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			Q3_TOT_BROK	= (CASE WHEN @@Q_POS = 3 THEN SUM(PBROKAMT+SBROKAMT) ELSE 0 END),
			PRINT_FLAG	= CONVERT(VARCHAR(5),'')
		FROM
			ANGELCOMMODITY.MCDXCDS.DBO.FOBILLVALAN C WITH (NOLOCK),
			ANGELCOMMODITY.MCDXCDS.DBO.CLIENT2 C2 WITH (NOLOCK)
		WHERE
			C.PARTY_CODE = C2.PARTY_CODE
			AND SAUDA_DATE BETWEEN @@FROMDATE AND @@TODATE + ' 23:59:59'
			--AND PARTY_CODE NOT IN (SELECT DISTINCT PARTY_CODE FROM PRADNYA.DBO.TBL_H_PRINTFLAG)
		GROUP BY
			PARENTCODE,
			MONTH(SAUDA_DATE)
		) BROK
	GROUP BY
		PARTY_CODE,
		Q_MONTH,
		PRINT_FLAG
	ORDER BY
		PARTY_CODE,
		Q_MONTH


	/* ------------ NEW CLIENT INSERT  ------------ */


	INSERT INTO PRADNYA.DBO.TBL_H_PRINTFLAG
	SELECT 
		* 
	FROM 
		#BROKDETAIL
	WHERE
		ISNULL(PARTY_CODE,'') NOT IN (SELECT DISTINCT ISNULL(PARTY_CODE,'') FROM PRADNYA.DBO.TBL_H_PRINTFLAG (NOLOCK))

	/* ------------ NEW CLIENT INSERT  ------------ */



	/* ------------ EXISTING CLIENT UPDATE  ------------ */

	UPDATE
		PRADNYA.DBO.TBL_H_PRINTFLAG
	SET
		Q1_TOT_BROK	= (CASE WHEN Q1_TOT_BROK_1 <> 0 THEN Q1_TOT_BROK_1 ELSE Q1_TOT_BROK END),
		Q2_TOT_BROK	= (CASE WHEN Q2_TOT_BROK_1 <> 0 THEN Q2_TOT_BROK_1 ELSE Q2_TOT_BROK END),
		Q3_TOT_BROK	= (CASE WHEN Q3_TOT_BROK_1 <> 0 THEN Q3_TOT_BROK_1 ELSE Q3_TOT_BROK END)
	FROM
		#BROKDETAIL
	WHERE
		TBL_H_PRINTFLAG.PARTY_CODE = #BROKDETAIL.PARTY_CODE

	/* ------------ EXISTING CLIENT UPDATE  ------------ */



	/* ------------ PRINTFLAG UPDATE   ------------ */

	IF (@@Q_POS = 1)
	BEGIN
		UPDATE
			PRADNYA.DBO.TBL_H_PRINTFLAG
		SET
			PRINT_FLAG = (
			CASE 
				WHEN @@Q_POS = 1 AND Q1_TOT_BROK >= 1000 THEN 'H1' 
				ELSE ''
			END)
	END


	IF (@@Q_POS = 2)
	BEGIN
		UPDATE
			PRADNYA.DBO.TBL_H_PRINTFLAG
		SET
			PRINT_FLAG = (
			CASE 
				WHEN (Q1_TOT_BROK >= 1000 AND Q2_TOT_BROK >= 1000) THEN 'H1'
				WHEN (Q1_TOT_BROK = 0 AND Q2_TOT_BROK >= 1000) THEN 'H1'
				--WHEN (Q1_TOT_BROK < 1000 OR Q2_TOT_BROK < 1000) AND ((Q2_TOT_BROK + Q1_TOT_BROK) >= 1000) THEN 'H2'
				WHEN (Q1_TOT_BROK > 0) AND (Q1_TOT_BROK < 1000) AND (Q2_TOT_BROK >= 1000) THEN 'H2'
				ELSE ''
			END)
		/*
		UPDATE
			PRADNYA.DBO.TBL_H_PRINTFLAG
		SET
			PRINT_FLAG = (
			CASE 
				WHEN (Q1_TOT_BROK > 0) AND (Q1_TOT_BROK < 1000) AND (Q2_TOT_BROK >= 1000) THEN 'H2'
				--WHEN (Q1_TOT_BROK < 1000 OR Q2_TOT_BROK < 1000) AND ((Q2_TOT_BROK + Q1_TOT_BROK) >= 1000) THEN 'H2'
				ELSE ''
			END)
		*/
	END


	IF (@@Q_POS = 3)
	BEGIN
		UPDATE
			PRADNYA.DBO.TBL_H_PRINTFLAG
		SET
			PRINT_FLAG = (
			CASE 
				WHEN (Q1_TOT_BROK >= 1000 AND Q2_TOT_BROK >= 1000 AND Q3_TOT_BROK >= 1000) THEN 'H1'
				WHEN ((Q1_TOT_BROK >= 1000 AND Q2_TOT_BROK >= 1000) OR (Q2_TOT_BROK >= 1000)) AND Q3_TOT_BROK > 0 THEN 'H1'
				WHEN (Q1_TOT_BROK >= 1000) AND (Q2_TOT_BROK < 1000) AND (Q2_TOT_BROK + Q3_TOT_BROK) > 0 THEN 'H2'
				WHEN (Q1_TOT_BROK = 0) AND (Q2_TOT_BROK < 1000) AND (Q3_TOT_BROK < 1000) AND (Q2_TOT_BROK + Q3_TOT_BROK) > 1000 THEN 'H2'
				WHEN (Q1_TOT_BROK < 1000 AND Q2_TOT_BROK < 1000 AND Q3_TOT_BROK < 1000) AND (Q1_TOT_BROK + Q2_TOT_BROK + Q3_TOT_BROK) >= 1000 THEN 'H3'
				WHEN (Q3_TOT_BROK >= 1000) AND (Q1_TOT_BROK + Q2_TOT_BROK) < 1000 THEN 'H3'
				ELSE ''
			END)
	END

	/* ------------ PRINTFLAG UPDATE   ------------ */

	---SELECT * FROM PRADNYA.DBO.TBL_H_PRINTFLAG (NOLOCK) ORDER BY PARTY_CODE
	DROP TABLE #BROKDETAIL

	RETURN

GO
