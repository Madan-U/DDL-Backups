DELETE CO FROM CLIENT_COLLATERAL_REPORTING_DETAILS  CO (NOLOCK)
WHERE PROCESS_DT BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'  
  
INSERT INTO CLIENT_COLLATERAL_REPORTING_DETAILS  
SELECT * FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY  (NOLOCK)
WHERE PROCESS_DT BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'  
  
UPDATE CLIENT_COLLATERAL_REPORTING_DETAIL_LOG SET PROCESS_END_TIME = GETDATE(),   
PROCESS_STATUS = 'END' WHERE PROCESS_DT = @RUNDATE AND PROCESS_STATUS = 'STARTED'  
 
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            
 
 INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES ('COLLATREAL_PROCESS_NEW', GETDATE())            
SET @ID = @@IDENTITY 
	 EXEC MSAJAG.DBO.PROC_CLIENT_COLLATREAL_PROCESS_NEW @PROCESSTYPE,@RUNDATE

UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID     

/*New Process Introduced on 5th Jan 2022 this is the regulatory changes ORE-1827*/

 /*End the Process*/
 SELECT STATUS='AWS PROCESS COMPLETED SUCESSFULLY FOR '+@RUNDATE,RUNDATE=@RUNDATE  

END

