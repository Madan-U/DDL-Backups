CREATE PROCEDURE [dbo].[PROC_CLIENT_COLLATREAL_PROCESS_AWS] 
@PROCESSTYPE VARCHAR(20)='',
@RUNDATE VARCHAR(11)  
AS  
BEGIN
DECLARE @PREVDATE VARCHAR(11),  
  @NAVDATE  VARCHAR(11),  
  @NAVDATE_NSE VARCHAR(11),  
  @mrgcur cursor,  
  @STRSQL VARCHAR(MAX),  
  @TRADINGPAN VARCHAR(10),  
  @MEMBERCODE VARCHAR(10),  
  @DEFEXCHG VARCHAR(3),
  @CURFLAG	INT, 
  @ID BIGINT
  
SET NOCOUNT ON   


/* Non Cash Report data integration from AWS to BO */
INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES('DELETE_NONCASH',GETDATE())
SET @ID = @@IDENTITY 
	DELETE FROM TBL_NONCASH_REPORT  
	WHERE  PROCESS_DATE = @RUNDATE  
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            

INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES('INSERT_NONCASH',GETDATE()) 
SET @ID = @@IDENTITY 
	INSERT INTO TBL_NONCASH_REPORT  
	SELECT * FROM TBL_NONCASH_REPORT_AWS    
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            

/* Collateral reporting data integration from AWS to BO */
TRUNCATE TABLE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY

INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES('INSERT_COLLTODAY',GETDATE()) 
SET @ID = @@IDENTITY 
	INSERT INTO CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY  
	SELECT * FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY_AWS  
	WHERE PROCESS_DT BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            


INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES ('PROC_CLIENT_COLLATREAL_ALLOC_VDT_EDT', GETDATE())            
SET @ID = @@IDENTITY 
	EXEC PROC_CLIENT_COLLATREAL_ALLOC_VDT_EDT @RUNDATE  
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            

INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES ('PROC_CLIENT_COLLATREAL_ALLOC_SEC', GETDATE())            
SET @ID = @@IDENTITY 
	EXEC PROC_CLIENT_COLLATREAL_ALLOC_SEC @RUNDATE  
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            


INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES ('PROC_CLIENT_COLLATREAL_ALLOC', GETDATE())            
SET @ID = @@IDENTITY 
	EXEC PROC_CLIENT_COLLATREAL_ALLOC @RUNDATE  
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            

INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES ('PROC_CLIENT_COLLATREAL_ALLOC_LED', GETDATE())            
SET @ID = @@IDENTITY 
	EXEC PROC_CLIENT_COLLATREAL_ALLOC_LED @RUNDATE  
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            

INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES ('PROC_CLIENT_COLLATREAL_ALLOC_CMCC', GETDATE())            
SET @ID = @@IDENTITY 
	EXEC PROC_CLIENT_COLLATREAL_ALLOC_CMCC @RUNDATE  
UPDATE PROC_CLIENT_COLLATREAL_PROCES_LOG SET PROC_END_TIME = GETDATE() WHERE RUNID = @ID            

INSERT INTO PROC_CLIENT_COLLATREAL_PROCES_LOG (PROC_NAME, PROC_START_TIME) 
VALUES ('#PEAK', GETDATE())            
SET @ID = @@IDENTITY 

SELECT PARTY_CODE, CLIENT_PAN, PEAK_LEDGER = SUM(ISNULL(PEAK_LEDGER,0))  
INTO #PEAK  
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY  WITH(NOLOCK)
WHERE PROCESS_DT = @RUNDATE  
AND ACCOUNT_TYPE <> 'P'  
GROUP BY PARTY_CODE,CLIENT_PAN  
HAVING SUM(ISNULL(PEAK_LEDGER,0))  = 0 /* Added on  Sep 01 2023*/
  
CREATE INDEX #PIND ON #PEAK (PARTY_CODE,CLIENT_PAN)
  
UPDATE CO SET PEAK_LEDGER = C.PEAK_LEDGER  
FROM #PEAK C  With(NOlock),CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY CO(NOLOCK)
WHERE PROCESS_DT = @RUNDATE  
AND C.PARTY_CODE = CO.PARTY_CODE  
AND C.CLIENT_PAN = CO.CLIENT_PAN  
AND ACCOUNT_TYPE <> 'P'  
  
UPDATE CO SET PARTY_CODE = '', CLIENT_PAN = '' 
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY CO(NOLOCK) WHERE PROCESS_DT = @RUNDATE   
AND CLIENT_PAN = TRADING_PAN AND ACCOUNT_TYPE = 'P'  
  
UPDATE CO SET APPROVED_SEC_CASH_WCM=APPROVED_SEC_CASH_WCM-APPROVED_SEC_CASH_WNCL,  
APPROVED_SEC_NONCASH_WCM=APPROVED_SEC_NONCASH_WCM-APPROVED_SEC_NONCASH_WNCL,  
FD_WCM=FD_WCM-FD_WNCL,BG_WCM=BG_WCM-BG_WNCL,CASH_WCM=CASH_WCM-CASH_WNCL  
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY CO (NOLOCK)
WHERE PROCESS_DT = @RUNDATE  
  
  
UPDATE CO SET OTHER_COLL_REC_TM = CASHAMOUNT+FDAMOUNT+BGAMOUNT,  
OTHER_COLL_WCM= CASHAMOUNT+FDAMOUNT+BGAMOUNT  
FROM TBL_CMCC_DATA_NEW T(NOLOCK),CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY CO WITH(NOLOCK) WHERE PROCESS_DT BETWEEN @RUNDATE AND @RUNDATE + ' 23:59'   
AND TRANSDATE = (SELECT MAX(TRANSDATE) FROM TBL_CMCC_DATA_NEW  (NOLOCK)
           WHERE TRANSDATE <= @RUNDATE)  
AND CO.EXCHANGE = T.EXCHANGE  
AND CO.SEGMENT = T.SEGMENT  
AND ALLOC_LEVEL = 'CM' AND T.ACCOUNT_TYPE = 'E' and CO.ACCOUNT_TYPE='P'  
  
DELETE CO FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY CO  WITH(NOLOCK)
WHERE PROCESS_DT = @RUNDATE AND  
MTOM = 0 AND  
VDT_LEDGER = 0 AND  
EDT_LEDGER = 0 AND  
PEAK_LEDGER = 0 AND  
BG_REC_TM = 0 AND  
FD_REC_TM = 0 AND  
APPROVED_SEC_CASH_REC_TM = 0 AND  
APPROVED_SEC_NONCASH_REC_TM = 0 AND  
NONAPPROVED_SEC_REC_TM = 0 AND  
CC_APPROVED_COMM_REC_TM = 0 AND  
OTHER_COLL_REC_TM = 0 AND  
LIEU_EPI_LEDGER_CLTM = 0 AND  
POOL_ACCOUNT_CLTM = 0 AND  
CASH_RET_TM = 0 AND  
APPROVED_SEC_CASH_RET_TM = 0 AND  
APPROVED_SEC_NONCASH_RET_TM = 0 AND  
NONAPPROVED_SEC_RET_TM = 0 AND  
APPROVED_SEC_NONCASH_RET_TM = 0 AND  
NONAPPROVED_SEC_RET_TM = 0 AND  
CC_APPROVED_COMM_RET_TM = 0 AND  
OTHER_COLL_RET_TM = 0 AND  
CASH_WCM = 0 AND  
APPROVED_SEC_CASH_WCM = 0 AND  
APPROVED_SEC_NONCASH_WCM = 0 AND  
NONAPPROVED_SEC_RET_WCM = 0 AND  
APPROVED_SEC_NONCASH_WCM = 0 AND  
NONAPPROVED_SEC_RET_WCM = 0 AND  
CC_APPROVED_COMM_WCM = 0 AND  
OTHER_COLL_WCM = 0 AND  
CASH_RET_WCM = 0 AND  
APPROVED_SEC_CASH_RET_CM = 0 AND  
APPROVED_SEC_NONCASH_RET_CM = 0 AND  
NONAPPROVED_SEC_RET_RET_CM = 0 AND  
APPROVED_SEC_NONCASH_RET_CM = 0 AND  
NONAPPROVED_SEC_RET_RET_CM = 0 AND  
CC_APPROVED_COMM_RET_CM = 0 AND  
OTHER_COLL_RET_CM = 0 AND  
CASH_WNCL = 0 AND  
APPROVED_SEC_CASH_WNCL = 0 AND  
APPROVED_SEC_NONCASH_WNCL = 0 AND  
CC_APPROVED_COMM_WNCL = 0 AND  
APPROVED_SEC_NONCASH_WNCL = 0 AND  
CC_APPROVED_COMM_WNCL = 0 AND   
UPFRONT_MARGIN = 0 AND   
PEAK_MARGIN = 0 AND Account_TYPE <> 'P'  
  
  
SELECT DISTINCT EXCHANGE, SEGMENT, ALLOC_LEVEL INTO #WCM FROM TBL_CMCC_DATA_NEW   (NOLOCK)
WHERE TRANSDATE = (SELECT MAX(TRANSDATE) FROM TBL_CMCC_DATA_NEW WHERE TRANSDATE<=@RUNDATE)  
AND ALLOC_LEVEL IN ('CC','CM')  
  
UPDATE CO SET   
 APPROVED_SEC_CASH_RET_CM = 0, APPROVED_SEC_NONCASH_RET_CM = 0, NONAPPROVED_SEC_RET_RET_CM = 0,  
 APPROVED_SEC_CASH_WNCL=0,APPROVED_SEC_NONCASH_WNCL=0,  
 APPROVED_SEC_CASH_WCM = APPROVED_SEC_CASH_WCM+APPROVED_SEC_CASH_WNCL,  
 APPROVED_SEC_NONCASH_WCM=APPROVED_SEC_NONCASH_WCM+APPROVED_SEC_NONCASH_WNCL 
 FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY CO (NOLOCK) 
WHERE PROCESS_DT=@RUNDATE  
AND APPROVED_SEC_CASH_RET_CM+APPROVED_SEC_NONCASH_RET_CM+NONAPPROVED_SEC_RET_RET_CM+APPROVED_SEC_CASH_WNCL+APPROVED_SEC_NONCASH_WNCL <> 0    
AND EXCHANGE IN (SELECT EXCHANGE FROM #WCM WHERE #WCM.EXCHANGE = CO.EXCHANGE  
AND #WCM.SEGMENT = CO.SEGMENT AND ALLOC_LEVEL='CM')  
  
UPDATE CO SET    
 APPROVED_SEC_CASH_REC_TM = APPROVED_SEC_CASH_REC_TM + (CASE WHEN (APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_WNCL) >= 0 THEN 0 ELSE ABS(APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_WNCL) END),  
 APPROVED_SEC_CASH_RET_TM = (CASE WHEN (APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_WNCL) >= 0 THEN (APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_WNCL) ELSE 0 END),  
 APPROVED_SEC_NONCASH_REC_TM = APPROVED_SEC_NONCASH_REC_TM+(CASE WHEN APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_WNCL >= 0 THEN 0 ELSE ABS(APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_WNCL) END),  
 APPROVED_SEC_NONCASH_RET_TM = (CASE WHEN APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_WNCL >= 0 THEN APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_WNCL ELSE 0 END),  
 NONAPPROVED_SEC_RET_TM = NONAPPROVED_SEC_REC_TM,  
 APPROVED_SEC_CASH_RET_CM = 0, APPROVED_SEC_NONCASH_RET_CM = 0, NONAPPROVED_SEC_RET_WCM = 0,  
 APPROVED_SEC_CASH_WCM=0, APPROVED_SEC_NONCASH_WCM=0,NONAPPROVED_SEC_RET_RET_CM=0  
 FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY CO(NOLOCK)
WHERE PROCESS_DT=@RUNDATE  
AND APPROVED_SEC_CASH_RET_CM+APPROVED_SEC_NONCASH_RET_CM+NONAPPROVED_SEC_RET_WCM + APPROVED_SEC_CASH_WCM+APPROVED_SEC_NONCASH_WCM+NONAPPROVED_SEC_RET_RET_CM <> 0    
AND EXCHANGE IN (SELECT EXCHANGE FROM #WCM (NOLOCK) WHERE #WCM.EXCHANGE = CO.EXCHANGE  
AND #WCM.SEGMENT = CO.SEGMENT AND ALLOC_LEVEL='CC')  

--SET NOCOUNT ON;
