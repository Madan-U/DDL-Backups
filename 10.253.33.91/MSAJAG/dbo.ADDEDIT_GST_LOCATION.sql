CREATE PROCEDURE [dbo].[ADDEDIT_GST_LOCATION]
(
	@LOC_CODE VARCHAR(20) ,
	@LOCATION VARCHAR(80) ,
	@LONG_NAME VARCHAR(100) ,
	@ADDRESS1 VARCHAR(100) ,
	@ADDRESS2 VARCHAR(100) ,
	@CITY VARCHAR(50) ,
	@STATE VARCHAR(50) ,
	@NATION VARCHAR(50) ,
	@ZIP VARCHAR(30) ,
	@PHONE1 VARCHAR(30) ,
	@PHONE2 VARCHAR(30) ,
	@FAX VARCHAR(30) ,
	@EMAIL VARCHAR(100) ,
	@GST_NO  VARCHAR(20),
	@SRNO INT,
	@RECTYPE VARCHAR(10)
)
AS

BEGIN 
	IF @RECTYPE='ADD' 
	BEGIN
		INSERT INTO TBL_GST_LOCATION 
		VALUES(@LOC_CODE,@LOCATION,@LONG_NAME,@ADDRESS1,@ADDRESS2,@CITY,@STATE,@NATION,@ZIP,@PHONE1,@PHONE2,@FAX,@EMAIL,@GST_NO)
	END	

	IF @RECTYPE='EDIT'
	BEGIN
		DELETE FROM TBL_GST_LOCATION WHERE SRNO = @SRNO		
		
		INSERT INTO TBL_GST_LOCATION 
		VALUES(@LOC_CODE,@LOCATION,@LONG_NAME,@ADDRESS1,@ADDRESS2,@CITY,@STATE,@NATION,@ZIP,@PHONE1,@PHONE2,@FAX,@EMAIL,@GST_NO)	
	END
	 
	

END

GO
