CREATE PROCEDURE [dbo].[CUSTOM_DPID_FREEZED_DATA] (@RUNDATE VARCHAR(11))

AS --- EXEC CUSTOM_DPID_FREEZED_DATA 'MAY 23 2022'

SELECT CLIENT_CODE=DPAM_SBA_NO,TRADING_CODE=CONVERT(VARCHAR(20),''),
(CASE WHEN FRE_ACTION = 'F' THEN 'FREEZED' ELSE '' END ) STATUS_FREE,B.FRE_RMKS INTO #DPDATA
FROM AGMUBODPL3.DMAT.CITRUS_USR.DP_ACCT_MSTR A,AGMUBODPL3.DMAT.CITRUS_USR.FREEZE_UNFREEZE_DTLS  B
WHERE A.DPAM_ID =B.FRE_DPAM_ID AND
B.FRE_ID IN (SELECT MAX(FRE_ID) FROM AGMUBODPL3.DMAT.CITRUS_USR.FREEZE_UNFREEZE_DTLS B1 WHERE B.FRE_DPAM_ID = B1.FRE_DPAM_ID)
AND A.DPAM_SBA_NO IN (SELECT LTRIM(RTRIM(PARTY_CODE)) FROM MSAJAG.DBO.IMPORT_PARTY_CODE)

INSERT INTO #DPDATA
SELECT CLIENT_CODE=DPAM_SBA_NO,TRADING_CODE=CONVERT(VARCHAR(20),''),
(CASE WHEN FRE_ACTION = 'F' THEN 'FREEZED' ELSE '' END ) STATUS_FREE,B.fre_rmks
FROM AngelDP5.DMAT.CITRUS_USR.DP_ACCT_MSTR A,AngelDP5.DMAT.CITRUS_USR.FREEZE_UNFREEZE_DTLS  B
WHERE A.DPAM_ID =B.FRE_DPAM_ID AND
B.FRE_ID IN (SELECT MAX(FRE_ID) FROM AngelDP5.DMAT.CITRUS_USR.FREEZE_UNFREEZE_DTLS B1 WHERE B.FRE_DPAM_ID = B1.FRE_DPAM_ID)
AND A.DPAM_SBA_NO IN (SELECT LTRIM(RTRIM(PARTY_CODE)) FROM MSAJAG.DBO.IMPORT_PARTY_CODE)

UPDATE #DPDATA SET TRADING_CODE = B.NISE_PARTY_CODE FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER B WITH (NOLOCK)
WHERE #DPDATA.CLIENT_CODE = B.CLIENT_CODE

UPDATE #DPDATA SET TRADING_CODE = B.NISE_PARTY_CODE FROM AngelDP5.DMAT.CITRUS_USR.TBL_CLIENT_MASTER B WITH (NOLOCK)
WHERE #DPDATA.CLIENT_CODE = B.CLIENT_CODE

SELECT * FROM #DPDATA ORDER BY TRADING_CODE

GO
