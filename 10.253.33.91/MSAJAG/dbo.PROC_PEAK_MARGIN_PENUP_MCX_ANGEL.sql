-- Object: PROCEDURE dbo.PROC_PEAK_MARGIN_PENUP_MCX_ANGEL
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

  
    
CREATE PROC [dbo].[PROC_PEAK_MARGIN_PENUP_MCX_ANGEL]     
(  @TDATE VARCHAR(10), @FILEFOR VARCHAR(100), @FILETYPE VARCHAR(100),     
 @PROGID VARCHAR(100), @UNAME VARCHAR(100),@FILENAME VARCHAR(100) = ''   
)    
    
AS   
  
DECLARE @RECCNT INT,@DELDATE DATETIME, @STRSQL VARCHAR(500)    
  


IF @FILETYPE = 'BLK'  
BEGIN  
 SELECT * INTO #MCX_SHRT_PEAK_FILE FROM ACCOUNT.DBO.MCX_SHRT_PEAK_FILE WHERE 1 <> 1

 SET @STRSQL = LOWER('BULK INSERT #MCX_SHRT_PEAK_FILE FROM ''' + @FILENAME  + '''  WITH (FIELDTERMINATOR = '','',ROWTERMINATOR = ''\n'',FIRSTROW = ''1'')')            
 EXEC(@STRSQL)  
 
 SELECT DISTINCT @DELDATE =  [DATE] FROM  #MCX_SHRT_PEAK_FILE  
 
 DELETE ACCOUNT.DBO.MCX_SHRT_PEAK_FILE WHERE [DATE] = @DELDATE  
 
 INSERT INTO ACCOUNT.DBO.MCX_SHRT_PEAK_FILE SELECT * FROM  #MCX_SHRT_PEAK_FILE  WHERE [DATE] = @DELDATE  
 
 DROP TABLE #MCX_SHRT_PEAK_FILE

END  
ELSE
BEGIN
SELECT DISTINCT .DBO.PIECE(FILE_DATA,',',1) AS [DATE] INTO #FILDATA FROM FILEDATA  WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) <> 'Business Date'   
  
SELECT DISTINCT @DELDATE =  [DATE] FROM  #FILDATA  
  
DROP TABLE #FILDATA  
  
DELETE ACCOUNT.DBO.MCX_SHRT_PEAK_FILE WHERE [DATE] = @DELDATE  
  
INSERT INTO ACCOUNT.DBO.MCX_SHRT_PEAK_FILE SELECT .DBO.PIECE(FILE_DATA,',',1) AS [Business Date],  
.DBO.PIECE(FILE_DATA,',',2) AS [Snapshot Interval],  
.DBO.PIECE(FILE_DATA,',',3) AS [CM ID],  
.DBO.PIECE(FILE_DATA,',',4) AS [TM ID],  
.DBO.PIECE(FILE_DATA,',',5) AS [Client ID],  
.DBO.PIECE(FILE_DATA,',',6) AS [Initial margin],  
.DBO.PIECE(FILE_DATA,',',7) AS [ELM],  
.DBO.PIECE(FILE_DATA,',',8) AS [Net Buy Premium],  
.DBO.PIECE(FILE_DATA,',',9) AS [Total Upfront Margin],  
.DBO.PIECE(FILE_DATA,',',10) AS [Client margin allocation],  
.DBO.PIECE(FILE_DATA,',',11) AS [Client other collaterals],  
.DBO.PIECE(FILE_DATA,',',12) AS [Short allocation],  
.DBO.PIECE(FILE_DATA,',',13) AS [Batch]  
FROM FILEDATA  WHERE PROGID = @PROGID  
AND .DBO.PIECE(FILE_DATA,',',1) <> 'Business Date'  
  
DELETE FROM FILEDATA  WHERE PROGID = @PROGID  
END
  
SELECT @RECCNT = ISNULL(COUNT(1),0) FROM ACCOUNT.DBO.MCX_SHRT_PEAK_FILE  WHERE  [DATE] = @DELDATE  
  
IF @RECCNT > 0     
BEGIN    
 SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)    
END    
ELSE    
BEGIN    
 SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'    
END

GO
