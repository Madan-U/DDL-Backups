/*
	EXEC CLS_GET_RECENT_ACCESS 'BROKER'
	SELECT * FROM CLS_APPLOG where REPCODE = 'undefined'
*/
Create PROC [dbo].[CLS_GET_RECENT_ACCESS]
(
	@USERNAME VARCHAR(25) 
)
AS


SELECT DISTINCT TOP 5 REPCODE AS FLDREPORTCODE, CONVERT(DATETIME, CONVERT(VARCHAR(11), TIMESTAMP, 109)) AS TIMESTAMP FROM CLS_APPLOG(NOLOCK) WHERE USERNAME = @USERNAME AND REPCODE <> '0' AND ISERROR = 0
ORDER BY CONVERT(DATETIME, CONVERT(VARCHAR(11), TIMESTAMP, 109)) DESC

GO
