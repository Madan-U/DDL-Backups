CREATE PROC [dbo].[FNO_BROK_NBFC] (@FROMDATE DATETIME)
AS

--EXEC FNO_BROK_NBFC 'FEB 14 2022'

DECLARE  @TODATE  DATETIME = @FROMDATE

SELECT PARTY_CODE,SUM(PBROKAMT+SBROKAMT) AS BROKERAGE ,
SUM((CASE WHEN BILLNO <>0 THEN ( CASE WHEN SYMBOL ='NIFTY' THEN (PQTY+SQTY)/BILLNO  ELSE 0 END) ELSE 0 END)) AS NO_OF_LOTS_NIFTY,
SUM((CASE WHEN BILLNO <>0 THEN ( CASE WHEN SYMBOL ='BANKNIFTY' THEN (PQTY+SQTY)/BILLNO  ELSE 0 END) ELSE 0 END)) AS NO_OF_LOTS_BANKNIFTY,
SUM((CASE WHEN BILLNO <>0 THEN ( CASE WHEN SYMBOL NOT IN ('BANKNIFTY','NIFTY') THEN (PQTY+SQTY)/BILLNO  ELSE 0 END) ELSE 0 END)) AS NO_OF_LOTS_OTHERS
INTO #TEMP
FROM ANGELFO.NSEFO.DBO.FOBILLVALAN  WITH (NOLOCK)
WHERE SAUDA_DATE >=@FROMDATE
AND SAUDA_DATE<=@TODATE + ' 23:59'
GROUP BY PARTY_CODE
HAVING SUM(PBROKAMT+SBROKAMT) >5000



SELECT T.*,B2C INTO #CLI FROM #TEMP T,INTRANET.RISK.DBO.CLIENT_DETAILS C WITH (NOLOCK) WHERE T.PARTY_CODE=C.CL_CODE
 
DECLARE @CL_RATE_DATE DATETIME      
SELECT @CL_RATE_DATE = MAX(TRADE_DATE) FROM ANGELFO.NSEFO.DBO.FOCLOSING_VIEW  WITH (NOLOCK) WHERE TRADE_DATE <= @TODATE + ' 23:59'      
     
     
SELECT @FROMDATE = LEFT(MIN(SAUDA_DATE),11) FROM ANGELFO.NSEFO.DBO.FOBILLVALAN   WITH (NOLOCK)    
WHERE SAUDA_DATE >= @FROMDATE    
     
SELECT      
 [CLIENT CODE] = PARTY_CODE,      
 [CLIENT NAME ] = PARTY_NAME,      
 [SYMBOL] = SYMBOL,      
 [INST TYPE] = INST_TYPE,      
 [EXPIRY DATE ] =  EXPIRYDATE,      
 [STRIKE PRICE] = STRIKE_PRICE,      
 [OPTION TYPE] = OPTION_TYPE,      
       
 [OPEN QTY] = SUM(CASE WHEN SAUDA_DATE BETWEEN @FROMDATE AND  @FROMDATE + ' 23:59' AND TRADETYPE = 'BF' THEN PQTY - SQTY ELSE 0 END),      
       
 [BUY QTY] = SUM(CASE WHEN TRADETYPE = 'BT' THEN PQTY ELSE 0 END),      
       
 [SALE QTY] = SUM(CASE WHEN TRADETYPE = 'BT' THEN SQTY ELSE 0 END),      
       
 [BUY AMT] = SUM(CASE WHEN TRADETYPE = 'BT' THEN (PRATE * PQTY) ELSE 0 END),      
       
 [BUY RATE] = CASE WHEN SUM(CASE WHEN TRADETYPE = 'BT' THEN PQTY ELSE 0 END) > 0      
   THEN ( SUM(CASE WHEN TRADETYPE = 'BT' THEN (PRATE * PQTY) ELSE 0 END) /      
     SUM(CASE WHEN TRADETYPE = 'BT'THEN PQTY ELSE 0 END)      
     )      
   ELSE 0      
   END ,      
       
 [SALE AMT] = SUM(CASE WHEN TRADETYPE = 'BT' THEN (SRATE * SQTY) ELSE 0 END),      
       
 [SALE RATE] = CASE WHEN SUM(CASE WHEN TRADETYPE = 'BT' THEN SQTY ELSE 0 END) > 0      
   THEN ( SUM(CASE WHEN TRADETYPE = 'BT' THEN (SRATE * SQTY) ELSE 0 END) /      
     SUM(CASE WHEN TRADETYPE = 'BT'THEN SQTY ELSE 0 END)      
     )      
   ELSE 0      
   END ,      
       
 [NET QTY] = CASE WHEN [EXPIRYDATE] >= @TODATE + ' 23:59' THEN      
     SUM(CASE WHEN TRADETYPE = 'BT' THEN (PQTY-SQTY) ELSE 0 END)      
    ELSE 0 END,      
       
 [NET AMT] = SUM(CASE WHEN TRADETYPE = 'BT' THEN ((PQTY*PRATE)-(SQTY*SRATE)) ELSE 0 END),      
     
 [AVG PRICE] = (CASE WHEN SUM(CASE WHEN TRADETYPE = 'BT' THEN (PQTY) ELSE 0 END)  
        - SUM(CASE WHEN TRADETYPE = 'BT' THEN (SQTY) ELSE 0 END) <> 0 THEN  
        ((SUM(CASE WHEN TRADETYPE = 'BT' THEN  
        ((PRATE)*PQTY) ELSE 0 END))  
         - (SUM(CASE WHEN TRADETYPE = 'BT' THEN ((SRATE)*SQTY) ELSE 0 END)) )/  
          (SUM(CASE WHEN TRADETYPE = 'BT' THEN (PQTY) ELSE 0 END) - SUM(CASE WHEN TRADETYPE = 'BT'  
          THEN (SQTY) ELSE 0 END)) ELSE 0 END) ,      
       
 [CLOSE QTY] = CASE WHEN [EXPIRYDATE] >= @TODATE + ' 23:59' THEN      
      SUM(CASE WHEN SAUDA_DATE BETWEEN @FROMDATE AND  @FROMDATE + ' 23:59' AND TRADETYPE = 'BF'      
        THEN PQTY - SQTY ELSE 0 END)+SUM(CASE WHEN TRADETYPE = 'BT' THEN (PQTY-SQTY) ELSE 0 END)      
    ELSE 0 END,      
       
 [PROFIT OR LOSS] = SUM(SBILLAMT - PBILLAMT + PBROKAMT + SBROKAMT),      
 [CLOSING RATE] = CONVERT(NUMERIC(18,4),0),      
 [TOTAL CHARGES] = SUM(PBROKAMT + SBROKAMT + SERVICE_TAX + SEBI_TAX + TURN_TAX + BROKER_NOTE + INS_CHRG + OTHER_CHRG)      
INTO      
 #PNL      
FROM ANGELFO.NSEFO.DBO.FOBILLVALAN  WITH (NOLOCK)
WHERE  
 SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'      
 AND PARTY_CODE IN (SELECT PARTY_CODE  FROM #CLI)
 AND ( PQTY + SQTY > 0  OR STATUSNAME ='BRK')    
 --AND AUCTIONPART <> 'CA'      
GROUP BY PARTY_CODE ,PARTY_NAME,SYMBOL, INST_TYPE, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE      
 
     
SELECT        
 [CLIENT CODE],[CLIENT NAME],      
 [GROSS PROFIT OR LOSS]= CONVERT(NUMERIC(18,2), SUM([PROFIT OR LOSS])),      
 [NET PROFIT OR LOSS] = CONVERT(NUMERIC(18,2), SUM(([PROFIT OR LOSS] - [TOTAL CHARGES])))      INTO #FINAL
FROM #PNL      
GROUP BY [CLIENT CODE] ,[CLIENT NAME]


SELECT F.*,BROKERAGE,NO_OF_LOTS_NIFTY, NO_OF_LOTS_BANKNIFTY, NO_OF_LOTS_OTHERS, B2C ,SUB_BROKER   FROM #FINAL F,#CLI C,CLIENT_DETAILS C1 WITH (NOLOCK)
WHERE [CLIENT CODE] = C1.PARTY_CODE  AND C.PARTY_CODE=C1.CL_CODE  
AND B2C='N' ORDER BY C1.CL_CODE  ASC

DROP TABLE #FINAL
DROP TABLE #CLI
DROP TABLE #PNL
DROP TABLE #TEMP

GO
