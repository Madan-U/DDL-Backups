  BROKERAGE = (CASE WHEN SELL_BUY ='BUY'               
     THEN (CASE WHEN TMARK = '' THEN CD_TRDBUYBROKERAGE ELSE CD_DELBUYBROKERAGE END)              
     ELSE (CASE WHEN TMARK = '' THEN CD_TRDSELLBROKERAGE ELSE CD_DELSELLBROKERAGE END)              
       END)              
           + (CASE WHEN SERVICE_CHRG = 1               
     THEN CASE WHEN SELL_BUY ='BUY'               
        THEN (CASE WHEN TMARK = '' THEN CD_TRDBUYSERTAX ELSE CD_DELBUYSERTAX END)              
        ELSE (CASE WHEN TMARK = '' THEN CD_TRDSELLSERTAX ELSE CD_DELSELLSERTAX END)              
   END                  
            ELSE 0               
 END),
 SERVICETAX = SERVICETAX + CD_TOTALSERTAX
FROM      
 CHARGES_DETAIL ,CLIENT2    
WHERE      
 CONVERT(VARCHAR,CD_SAUDA_DATE,103) = TRADEDATE     
 AND CD_PARTY_CODE = #SETT.CLIENTCODE                  
 AND CD_SCRIP_CD = #SETT.SCRIP_CD                
 AND CD_TRADE_NO = TRADENO                  
 AND CD_ORDER_NO = ORDERNO        
 AND CLIENT2.PARTY_CODE = CD_PARTY_CODE    
    
CREATE INDEX [INXFOSETT]           
        ON [DBO].[#SETT]           
        (           
               [CLIENTCODE]           
        ) 
        
   
   
  
