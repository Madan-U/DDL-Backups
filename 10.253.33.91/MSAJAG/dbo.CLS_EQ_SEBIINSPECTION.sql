 #SETT         
SET NETRATE= (CASE WHEN SELL_BUY = 'BUY' THEN ((MKTRATE*QUANTITY)+(BROKERAGE+TRANSACTIONCHARGES+STAMPDUTY+SERVICETAX+SEBIFEES+STT))/QUANTITY ELSE -((MKTRATE*QUANTITY)-(BROKERAGE+TRANSACTIONCHARGES+STAMPDUTY+SERVICETAX+SEBIFEES+STT))/QUANTITY END)
        
