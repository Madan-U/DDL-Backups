[ORDER NO]=ORDERNO,
[ORDER TIME]=ORDERTIME,
[TRADE NO]=TRADENO,
[TRADE TIME]=TRADETIME,
[TRADE DATE]=TRADEDATE,
[CLIENT CODE]=CLIENTCODE,
[CLIENT NAME]=CLIENTNAME,
[SCRIP CODE]=SCRIP_CD,
[SCRIP NAME]=LONG_NAME,
QUANTITY,
[MARKET RATE]=CONVERT(NUMERIC(18,4),ROUND(MKTRATE,4)),
[NET RATE]=CONVERT(NUMERIC(18,4),ROUND(NETRATE,4)),
BROKERAGE=CONVERT(NUMERIC(18,2),ROUND(BROKERAGE,2)),
SELL_BUY,
