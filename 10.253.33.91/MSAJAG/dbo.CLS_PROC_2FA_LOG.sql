CREATE PROC [dbo].[CLS_PROC_2FA_LOG]
(
	@API_CODE VARCHAR(20),
	@FLDUSER VARCHAR(25),
	@OTP VARCHAR(25),
	@MOBILE VARCHAR(25),
	@URI VARCHAR(8000),
	@SESSIONID varchar(100),
	@UPD_BY VARCHAR(100),
	@FLAG VARCHAR(10),
	@RESULT VARCHAR(8000)
	
)
/*

*/
AS

	DECLARE @RETURN VARCHAR(20),
			@CNT int,
			@OTP_EXPIRE	INT
			--SELECT * FROM CLS_2FA_SETTING
	SELECT @OTP_EXPIRE = OTP_EXPIRE FROM CLS_2FA_SETTING WHERE API_CODE = @API_CODE

IF(@FLAG = 'LOG')
 BEGIN
	INSERT INTO CLS_2FA_OTP_LOG VALUES (@API_CODE, @FLDUSER, @OTP, @MOBILE, @URI, @SESSIONID, GETDATE(), @UPD_BY, 1, @RESULT)
 END

 IF(@FLAG = 'VALIDATE')
 BEGIN
	SELECT @CNT = COUNT(*) FROM CLS_2FA_OTP_LOG WHERE OTP = @OTP AND @SESSIONID = SESSIONID AND @FLDUSER = FLDUSER AND ACTIVE = 1
		--AND DATEDIFF('Minute', UPD_DT, GETDATE()) > @OTP_EXPIRE
	IF(@CNT = 0)
	 begin
		select 'INVALID OTP'
		return
	 end
	Else IF (SELECT COUNT(*) FROM CLS_2FA_OTP_LOG WHERE OTP = @OTP AND SESSIONID = @SESSIONID AND FLDUSER = @FLDUSER AND ACTIVE = 1 AND DATEDIFF(Minute, UPD_DT, GETDATE()) <= @OTP_EXPIRE) = 0
	 BEGIN
		select 'OTP EXPIRED'
		return
	 END
	ELSE
	 select 'VALIDATED'
 END

GO
