CREATE PROCEDURE SP_HOLDING_FILE_UPDATE_NEW
(
	@USERID VARCHAR(20),
	@HTYPE VARCHAR(1),
	@LASTWEEKDAY VARCHAR(11)
	
)
AS
--exec SP_HOLDING_FILE_UPDATE 'HF','H','05/02/2022'
BEGIN 
		IF @HTYPE='H'
		BEGIN
			
			INSERT INTO TBL_API_NSE_HOLDING_LOG SELECT SRNO,MEMBERPAN,LASTWEEKDAY,WEEKDAY,DMAT,ACCOUNTTYPE,UCC,CLIENTNAME,PAN,ISIN,
			SECURITYTYPE,TOTALPLDGQTY,FREEBALQTY,TOTALQTY,ACTION,Code,STATUS,HDRSTATUS,HDRCODE,ACDID,BATCH_COUNTER,PROCESSED_BY,
			PROCESSED_ON,@USERID,GETDATE() FROM TBL_API_NSE_HOLDING
			WHERE LASTWEEKDAY LIKE CONVERT(VARCHAR(11), CONVERT(DATETIME, @LASTWEEKDAY, 103), 109)  + '%'

			TRUNCATE TABLE TBL_API_NSE_HOLDING
 
			INSERT INTO TBL_API_NSE_HOLDING(UID,MEMBERPAN,LASTWEEKDAY,WEEKDAY,DMAT,ACCOUNTTYPE,UCC,CLIENTNAME,PAN,ISIN,
			SECURITYTYPE,TOTALPLDGQTY,FREEBALQTY,TOTALQTY,
			ACTION,CODE,STATUS,HDRSTATUS,HDRCODE,ACDID,BATCH_COUNTER,PROCESSED_BY,
			PROCESSED_ON,UNITTYPE,NAMEOFSECORCOMMODITY) 
			SELECT  
			UID = CONVERT(BIGINT,RIGHT(CONVERT(VARCHAR,GETDATE(),112),6)+REPLACE(CONVERT(VARCHAR(20), GETDATE(), 114),':',''))+ABS(CAST(CAST(NEWID() AS VARBINARY) AS INT)),
			MEMBERPAN ,
			LASTWEEKDAY =  CONVERT(VARCHAR(11), CONVERT(DATETIME, @LASTWEEKDAY, 103), 109),
			WEEKDAY  = CONVERT(VARCHAR(11), CONVERT(DATETIME,REPLACE(WEEKDAY,'-','/'), 103), 109), 
			DMAT = REPLACE(DMAT,'"',''),
			ACCOUNTTYPE,
			UCC,
			CLIENTNAME =  REPLACE(CLIENTNAME,'#',''''),
			PAN,
			ISIN,
			SECURITYTYPE,
			TOTALPLDGQTY,
			FREEBALQTY,
			TOTALQTY=REPLACE(TOTALQTY, CHAR(13), ''), 
			ACTION='A',
			CODE='',
			STATUS='',
			HDRSTATUS = '',
			HDRCODE = '',
			ACDID = '',
			BATCH_COUNTER = '1', 
			USERID='',
			GETDATE(),
			UNITTYPE,
			NAMEOFSECORCOMMODITY

			FROM TBL_API_NSE_HOLDING_TEMP WHERE memberPan NOT LIKE 'MEMBER%'  			 

			UPDATE TBL_API_NSE_HOLDING SET UID = SRNO+CONVERT(BIGINT,UID) 
		END 
	  
		SELECT '1'
	END

GO
