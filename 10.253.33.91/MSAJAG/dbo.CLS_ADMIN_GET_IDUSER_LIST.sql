-- Object: PROCEDURE dbo.CLS_ADMIN_GET_IDUSER_LIST
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

create PROC [dbo].[CLS_ADMIN_GET_IDUSER_LIST]  
(  
 @IDUSER VARCHAR(30)  
)  
  
AS  
  
SET @IDUSER = UPPER(@IDUSER)  
  
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED   
IF @IDUSER = 'BRANCH'  
 BEGIN  
  SELECT DISTINCT BRANCH_CODE as ent_code,UPPER(BRANCH) as  [entity_Name]  FROM BRANCH (NOLOCK) ORDER BY  [entity_Name]   
 END  
ELSE IF @IDUSER = 'SUBBROKER'  
 BEGIN   
  SELECT DISTINCT SUB_BROKER as ent_code,  UPPER(NAME) as  [entity_Name]  FROM SUBBROKERS (NOLOCK) WHERE SUB_BROKER <> '' AND NAME <> '' ORDER BY  [entity_Name]    
 END  
ELSE IF @IDUSER = 'TRADER'  
 BEGIN  
 SELECT DISTINCT SHORT_NAME as ent_code,LONG_NAME as [entity_Name] FROM BRANCHES (NOLOCK) WHERE SHORT_NAME <> '' AND LONG_NAME <> '' ORDER BY  [entity_Name]    
 END  
ELSE IF @IDUSER = 'AREA'  
 BEGIN  
  SELECT DISTINCT AREACODE as ent_code,DESCRIPTION as [entity_Name] FROM AREA (NOLOCK) ORDER BY  [entity_Name]   
 END   
ELSE IF @IDUSER = 'REGION'  
 BEGIN  
  SELECT DISTINCT REGIONCODE as ent_code,DESCRIPTION as [entity_Name] FROM REGION (NOLOCK) ORDER BY  [entity_Name]   
 END  
ELSE IF @IDUSER = 'FAMILY'  
 BEGIN  
  SELECT DISTINCT FAMILY as ent_code, FAMILY AS [entity_Name] FROM MSAJAG.DBO.CLIENT_DETAILS (NOLOCK) WHERE FAMILY <> '' AND FAMILY <> CL_CODE ORDER BY  [entity_Name]   
 END

GO
