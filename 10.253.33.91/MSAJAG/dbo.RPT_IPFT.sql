-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[RPT_IPFT]
(
@SAUDA_DATE VARCHAR(11),
@FPARTY VARCHAR(20),
@TPARTY VARCHAR(20),
@DFLAG  VARCHAR(2)='D'


)

/*
EXEC RPT_IPFT 'jun 19 2023','0','zzzzz','S'
EXEC RPT_IPFT 'jun 19 2023','0','zzzzz','D


EXEC RPT_IPFT 'APR  3 2020','00','zzz',''
*/

AS
IF LEN(@SAUDA_DATE) = 10 AND CHARINDEX('/', @SAUDA_DATE) > 0
BEGIN
	SET @SAUDA_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @SAUDA_DATE, 103), 109)
END

BEGIN

SELECT EXCHANGE=CONVERT(VARCHAR(3),''),SEGMENT=CONVERT(VARCHAR(10),''),PARTY_CODE,SAUDA_DATE,SETT_NO,SETT_TYPE,CONTRACTNO,ORDER_NO,TRADE_NO,SCRIPNAME=CONVERT(VARCHAR(100),''),
MARKETRATE,TRADEQTY,AMOUNT,SELL_BUY,TRDTYPE,IPFT_CHRG
INTO #IPFTCHRG
FROM TBL_ADDITIONAL_TAX_DATA WHERE 1=2

INSERT INTO #IPFTCHRG
SELECT EXCHANGE='NSE',SEGMENT='CAPITAL',PARTY_CODE,SAUDA_DATE,SETT_NO,SETT_TYPE,CONTRACTNO,ORDER_NO,TRADE_NO,SCRIPNAME=SCRIP_CD+'-'+SERIES,MARKETRATE,TRADEQTY,
AMOUNT,SELL_BUY,TRDTYPE,IPFT_CHRG
FROM TBL_ADDITIONAL_TAX_DATA WHERE SAUDA_DATE BETWEEN  @SAUDA_DATE AND @SAUDA_DATE+ ' 23:59:59'
AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY

INSERT INTO #IPFTCHRG
SELECT EXCHANGE='NSE',SEGMENT='FUTURES',PARTY_CODE,SAUDA_DATE,SETT_NO,SETT_TYPE,CONTRACTNO,ORDER_NO,TRADE_NO,
SCRIPNAME=INST_TYPE+' '+LTRIM(RTRIM(SYMBOL))+' '+ LEFT(EXPIRYDATE,11)+' '+ CONVERT(VARCHAR,STRIKE_PRICE)+' '+OPTION_TYPE,PRICE,TRADEQTY,
AMOUNT,SELL_BUY,TRDTYPE,IPFT_CHRG
FROM [ANGELFO].NSEFO.DBO.TBL_ADDITIONAL_TAX_DATA WHERE SAUDA_DATE BETWEEN  @SAUDA_DATE AND @SAUDA_DATE+ ' 23:59:59'
AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY

INSERT INTO #IPFTCHRG
SELECT EXCHANGE='NSX',SEGMENT='FUTURES',PARTY_CODE,SAUDA_DATE,SETT_NO,SETT_TYPE,CONTRACTNO,ORDER_NO,TRADE_NO,
SCRIPNAME=INST_TYPE+' '+LTRIM(RTRIM(SYMBOL))+' '+ LEFT(EXPIRYDATE,11)+' '+ CONVERT(VARCHAR,STRIKE_PRICE)+' '+OPTION_TYPE,PRICE,TRADEQTY,
AMOUNT,SELL_BUY,TRDTYPE,IPFT_CHRG
FROM [ANGELFO].NSECURFO.DBO.TBL_ADDITIONAL_TAX_DATA WHERE SAUDA_DATE BETWEEN  @SAUDA_DATE AND @SAUDA_DATE+ ' 23:59:59'
AND PARTY_CODE BETWEEN @FPARTY AND @TPARTY


IF @DFLAG='S'
	BEGIN
		SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO_NEW=CONTRACTNO,
		TURNOVER=SUM(AMOUNT),
		IPFT_CHRG=SUM(IPFT_CHRG)
		FROM #IPFTCHRG(NOLOCK)
		GROUP BY CONVERT(VARCHAR,SAUDA_DATE,103),UPPER(PARTY_CODE),CONTRACTNO
		HAVING SUM(IPFT_CHRG)>0
		ORDER BY CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE,CONTRACTNO
	END
ELSE IF @DFLAG='D'
	BEGIN

		SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO,
			   EXCHANGE,SEGMENT,SETT_NO,SETT_TYPE,
			   SCRIPNAME,TRADEQTY=CONVERT(VARCHAR,SUM(TRADEQTY)),TURNOVER=CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(SUM(AMOUNT),4))), SELL_BUY=(CASE WHEN SELL_BUY='1' THEN 'BUY' ELSE 'SELL' END),IPFT_CHRG=CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(SUM(IPFT_CHRG),4))),
			   ORDFLAG=1 
		INTO  #IPFTDATA_1
		from  #IPFTCHRG
		GROUP BY CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE,CONTRACTNO,EXCHANGE,SEGMENT,SETT_NO,SETT_TYPE,SCRIPNAME,
		SELL_BUY  
		--HAVING SUM(IPFT_CHRG)>0
		ORDER BY CONVERT(VARCHAR,SAUDA_DATE,103),CONTRACTNO,PARTY_CODE,EXCHANGE,SEGMENT,SCRIPNAME

		
		INSERT INTO #IPFTDATA_1
		SELECT SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE=UPPER(PARTY_CODE),CONTRACTNO,
			   EXCHANGE='',SEGMENT='',SETT_NO='',SETT_TYPE='',
		       SCRIPNAME='TOTAL',TRADEQTY='',TURNOVER=CONVERT(NUMERIC(18,4),ROUND(SUM(AMOUNT),4)),SELL_BUY='',IPFT_CHRG=SUM(IPFT_CHRG),
			   ORDFLAG =2
		FROM #IPFTCHRG(NOLOCK)
		GROUP BY SAUDA_DATE,PARTY_CODE,CONTRACTNO
		HAVING SUM(IPFT_CHRG)>0
		
		
		SELECT SAUDA_DATE,PARTY_CODE,CONTRACTNO_NEW=CONTRACTNO,EXCHANGE,SEGMENT,SETT_NO,SETT_TYPE,SCRIPNAME,TRADEQTY,TURNOVER,SELL_BUY,IPFT_CHRG
		FROM #IPFTDATA_1 
		ORDER BY SAUDA_DATE,PARTY_CODE,CONTRACTNO,ORDFLAG
		  
		DROP TABLE #IPFTDATA_1
	END
ELSE IF @DFLAG=''
	BEGIN
		SELECT DISTINCT PARTY_CODE=UPPER(PARTY_CODE)
		--EXCHANGE,SEGMENT,
		from #IPFTCHRG(NOLOCK)
	
	END


	DROP TABLE #IPFTCHRG

END

GO
