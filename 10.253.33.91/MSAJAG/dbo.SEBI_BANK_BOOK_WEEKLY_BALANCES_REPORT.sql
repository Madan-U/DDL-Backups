CREATE TABLE #BANK_BOOK_BAL_FINAL
(
	BANK_CODE VARCHAR(30),
	BANK_IFSC VARCHAR(30),
	BANK_TYPE VARCHAR(10),
	REPORT_DATE DATETIME,
	AMOUNT MONEY,
	BANK_BALANCE MONEY,
	SNO INT IDENTITY(1, 1)
)


INSERT INTO #BANK_BOOK_BAL_FINAL
SELECT B.ACCOUNTNO, B.BANK_IFSC, PURPOSE, EFF_DATE, 0, BANK_BALANCE 
FROM MSAJAG..BANK_PURPOSE_MASTER B, ACCOUNT..TBL_CLS_BANK_BALANCE T
WHERE B.BANKCODE = T.BANK_GL_CODE
AND EFF_DATE BETWEEN @DATE_FROM AND @REPORT_DATE + ' 23:59'

DECLARE @RPTDATE	VARCHAR(11),
		@FILENAMEHS	VARCHAR(100),
		@FILENAMENH	VARCHAR(100),
