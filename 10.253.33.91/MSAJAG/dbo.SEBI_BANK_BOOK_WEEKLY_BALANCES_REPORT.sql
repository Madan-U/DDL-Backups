SELECT @FILENAMEHS, 'Bank_Account_No, IFSC, Bank_Account_Type, ' + REPLACE(CONVERT(VARCHAR(10), CONVERT(DATETIME, @DATE_FROM, 109), 103), '/', '-') + ',' + REPLACE(CONVERT(VARCHAR(10), CONVERT(DATETIME, DATEADD(D, 1, @DATE_FROM), 109), 103), '/', '-') + ',' + REPLACE(CONVERT(VARCHAR(10), CONVERT(DATETIME, DATEADD(D, 2, @DATE_FROM), 109), 103), '/', '-') + ',' + REPLACE(CONVERT(VARCHAR(10), CONVERT(DATETIME, DATEADD(D, 3, @DATE_FROM), 109), 103), '/', '-') + ',' + REPLACE(CONVERT(VARCHAR(10), CONVERT(DATETIME, DATEADD(D, 3, @DATE_FROM), 109), 103), '/', '-') + ',' + REPLACE(CONVERT(VARCHAR(10), CONVERT(DATETIME, DATEADD(D, 4, @DATE_FROM), 109), 103), '/', '-')

INSERT INTO #FILE_DATA
SELECT @FILENAMEHS, BANK_CODE + ',' + BANK_IFSC + ',' + BANK_TYPE + ',' + CONVERT(VARCHAR, SUM(DAY1))  + ',' + CONVERT(VARCHAR, SUM(DAY2)) + ',' + CONVERT(VARCHAR, SUM(DAY3)) + ',' + CONVERT(VARCHAR, SUM(DAY4)) + ',' + CONVERT(VARCHAR, SUM(DAY5)) + ',' + CONVERT(VARCHAR, SUM(DAY6))     
FROM 
( SELECT BANK_CODE,BANK_IFSC, BANK_TYPE, 
	DAY1 = CASE WHEN REPORT_DATE = @DATE_FROM THEN BANK_BALANCE ELSE 0 END,
	DAY2 = CASE WHEN REPORT_DATE = DATEADD(D, 1, @DATE_FROM) THEN BANK_BALANCE ELSE 0 END, 
	DAY3 = CASE WHEN REPORT_DATE = DATEADD(D, 2, @DATE_FROM) THEN BANK_BALANCE ELSE 0 END, 
	DAY4 = CASE WHEN REPORT_DATE = DATEADD(D, 3, @DATE_FROM) THEN BANK_BALANCE ELSE 0 END, 
	DAY5 = CASE WHEN REPORT_DATE = DATEADD(D, 4, @DATE_FROM) THEN BANK_BALANCE ELSE 0 END, 
	DAY6 = CASE WHEN REPORT_DATE = DATEADD(D, 5, @DATE_FROM) THEN BANK_BALANCE ELSE 0 END
FROM #BANK_BOOK_BAL_FINAL) A
GROUP BY BANK_CODE,BANK_IFSC, BANK_TYPE
ORDER BY BANK_CODE

SELECT * FROM #FILE_DATA
RETURN

