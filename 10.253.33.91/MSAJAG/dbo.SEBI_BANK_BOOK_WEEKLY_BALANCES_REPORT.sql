		@BATCHNO	INT,
		@@SQL VARCHAR(5000)

SELECT	@BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
WHERE	FLDFILENO = 'WB'  
AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)

	SELECT	@PANNO = ISNULL(LTRIM(RTRIM(PANNO)),'') FROM OWNER (NOLOCK)
	
IF @BATCHNO = 1  
BEGIN  
	INSERT INTO ASIANFILEBATCHNO VALUES ('WB', @BATCHNO, GETDATE(), GETDATE())  
END  
ELSE  
BEGIN  
	UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'WB' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
END  

SET	@FILENAMEHS = @PANNO + '_BA_' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@REPORT_DATE,109),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'

