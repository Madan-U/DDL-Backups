CREATE PROC [DBO].[TSS_CLIENT_INFOR_INCREMENTAL_bkp_20220121]              
AS              

SELECT DISTINCT C.CL_CODE INTO #CL  FROM CLIENT_DETAILS C (NOLOCK),CLIENT_BROK_DETAILS B
WHERE (MODIFIDEDON >=CONVERT(VARCHAR,GETDATE()-5,120)  OR MODIFIDEDON >=CONVERT(VARCHAR,GETDATE()-5,120) )
AND C.CL_CODE=B.CL_CODE 

SELECT DISTINCT C.CL_CODE,MIN(ACTIVE_DATE) AS ACTIVE_DATE,MAX(INACTIVE_FROM) AS INACTIVE_FROM INTO #ACTIVE FROM CLIENT_BROK_DETAILS C (NOLOCK),#CL W (NOLOCK)
WHERE C.CL_CODE=W.CL_CODE
AND DEACTIVE_VALUE NOT IN ('C','T','P')
GROUP BY C.CL_CODE

SELECT PARTY_CODE,FATHERNAME,OCCUPATION,PEP INTO #UCC  FROM CLIENT_MASTER_UCC_DATA C,#CL W (NOLOCK)
WHERE C.PARTY_CODE=W.CL_CODE


 SELECT  A.CL_CODE,A.LONG_NAME,A.PAN_GIR_NO,DOB,ACTIVE_DATE AS ACCOPENDATE,              
          INACTIVE_FROM AS ACCCLOSINGDATE,              
          A.CL_STATUS, (CASE WHEN A.CL_TYPE ='INS' THEN 'INSTITUTION'  
                WHEN A.CL_TYPE ='PRO' THEN 'PRO'  
                ELSE 'CLIENT' END) CL_TYPE  
          ,A.L_ADDRESS1,A.L_ADDRESS2,A.L_ADDRESS3,A.L_CITY,A.L_STATE,A.L_NATION,A.L_ZIP,              
          A.RES_PHONE1,A.RES_PHONE2,              
          A.P_ADDRESS1,A.P_ADDRESS2,A.P_ADDRESS3,              
          A.P_CITY,A.P_STATE,A.P_NATION,A.P_ZIP,A.P_PHONE,              
          A.MOBILE_PAGER,A.EMAIL,A.SEX,C.FATHERNAME,              
          A.OFF_PHONE1,A.OFF_PHONE2,A.MODIFIDEDBY,              
          A.SUB_BROKER,C.OCCUPATION,PEP      INTO #CLIENT_INFO        
 FROM CLIENT_DETAILS A WITH (NOLOCK) ,              
 #ACTIVE B WITH (NOLOCK),              
  #UCC C WITH (NOLOCK)              
 WHERE A.CL_CODE=B.CL_CODE AND              
 A.CL_CODE=C.PARTY_CODE    
 AND A.CL_CODE >'A00' AND A.CL_CODE <='ZZZZZZZZZZZZZ'

SELECT CL_CODE,LONG_NAME,PAN_GIR_NO,DOB,ACCOPENDATE,ACCCLOSINGDATE,
ISNULL(CONSTITUTIONTYPE,S.CL_STATUS)CL_STATUS
,CL_TYPE,
 L_ADDRESS1,L_ADDRESS2,L_ADDRESS3 ,L_CITY,ISNULL(TS.CODE,L_STATE)L_STATE ,L_NATION,L_ZIP,
 RES_PHONE1,RES_PHONE2,              
P_ADDRESS1,P_ADDRESS2,P_ADDRESS3,              
P_CITY,P_STATE,P_NATION,P_ZIP,P_PHONE,              
MOBILE_PAGER,EMAIL,SEX,FATHERNAME,              
OFF_PHONE1,OFF_PHONE2,MODIFIDEDBY,              
SUB_BROKER,ISNULL(TOS.CODE,S.OCCUPATION)OCCUPATION,PEP  INTO #FINAL --,MARITAL_STATUS,IPV_DATE,IPV_NAME,INTRODUCER_NAME
 FROM              
( SELECT  * FROM #CLIENT_INFO )S

 LEFT OUTER JOIN
 TSS_CLTYPE AS TC ON S.CL_STATUS=TC.CL_STATUS 
 LEFT OUTER JOIN 
 TSS_STATE AS TS ON S.L_STATE =STATE
 LEFT OUTER JOIN
 TSS_OCCUPATION AS TOS ON TOS.OCCUPATION=S.OCCUPATION

 CREATE INDEX #S ON #FINAL 
 (CL_CODE)

 SELECT CL_CODE,LONG_NAME,PAN_GIR_NO,DOB,ACCOPENDATE,ACCCLOSINGDATE,CL_STATUS,CL_TYPE,L_ADDRESS1,L_ADDRESS2,L_ADDRESS3,L_CITY,L_STATE,L_NATION,L_ZIP,RES_PHONE1,RES_PHONE2,P_ADDRESS1,P_ADDRESS2,P_ADDRESS3,P_CITY,
        P_STATE,P_NATION,P_ZIP,P_PHONE,MOBILE_PAGER,EMAIL,SEX,FATHERNAME,OFF_PHONE1,OFF_PHONE2,MODIFIDEDBY,SUB_BROKER,OCCUPATION,PEP,BOPARTYCODE, MARITAL_STATUS AS MARITAL_STATUS_CODE,IPV_DATE,IPV_NAME,INTRODUCER_NAME 
       ,MARITAL_STATUS=(CASE WHEN MARITAL_STATUS='01' THEN  'MARRIED' 
                       WHEN MARITAL_STATUS ='02' THEN 'SINGLE' END) --INTO TSS_INCREMENTAL
										 FROM #FINAL S
 LEFT OUTER JOIN 
 ( SELECT BOPARTYCODE,MARITAL_STATUS,IPV_DATE,IPV_NAME,INTRODUCER_NAME
FROM [172.31.16.57].CRMDB_A.DBO.API_KYC WHERE CREATED_DATE >='2016-08-01' ) TKYC
ON S.CL_CODE =BOPARTYCODE

GO
