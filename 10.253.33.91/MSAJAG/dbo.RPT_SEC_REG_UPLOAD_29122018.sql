CREATE	PROC [dbo].[RPT_SEC_REG_UPLOAD]
		(
		@FROMDATE	VARCHAR(11),
		@TODATE		VARCHAR(11),
		@FROMPARTY	VARCHAR(20) = '',
		@TOPARTY	VARCHAR(20) = 'zzzzzzzzzz'
		)
	
		AS

		/*
		EXEC	RPT_SEC_REG_UPLOAD 'FEB 20 2011', 'APR 13 2018','',''
		*/		
		
		IF @TOPARTY = ''
		BEGIN
			SET @TOPARTY = 'zzzzzzzzzz'
		END

		DECLARE @FILENAME   VARCHAR(50)
		DECLARE @BATCHNO	INT  
		DECLARE @MEMBERCODE VARCHAR(20)
		DECLARE @MEMBERNAME	VARCHAR(500)
		DECLARE @NEWFROMDATE VARCHAR(11)

		SELECT @MEMBERCODE = MEMBERCODE, @MEMBERNAME = ISNULL(LTRIM(RTRIM(COMPANY)),'') FROM OWNER (NOLOCK)
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
		WHERE FLDFILENO = 'SR'  
		AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		
		  
		IF @BATCHNO = 1  
		BEGIN  
			INSERT INTO ASIANFILEBATCHNO VALUES ('SR', @BATCHNO, GETDATE(), GETDATE())  
		END  
		ELSE  
		BEGIN  
			UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'SR' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		END  
		  
		SET @FILENAME = @MEMBERCODE + '_SR_' + REPLACE(CONVERT(VARCHAR,GETDATE(),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) 

		
		CREATE TABLE #SECREGSTOCK
		(
		TRANS_DATE	VARCHAR(11),
		EXE_DATE	VARCHAR(11),
		EXCHANGE	VARCHAR(3),
		SEGMENT		VARCHAR(7),
		UCC_CODE	VARCHAR(20),
		PARTY_CODE	VARCHAR(20),
		CLIENT_NAME VARCHAR(100),
		CLIENT_PAN	VARCHAR(50),
		MEMBER_DEMAT_ACC		VARCHAR(50),
		COUNTERPARTY_DEMAT_ACC	VARCHAR(50),
		SETT_NO					VARCHAR(10),
		ISIN					VARCHAR(20),
		SCRIP_NAME				VARCHAR(30),
		QTY_DELIVERED			NUMERIC(18,3),
		QTY_RECEIVED			NUMERIC(18,3),
		BALANCE_QTY				NUMERIC(18,3),
		PLEDGE_UNPLEDGE_QTY		NUMERIC(18,3),
		TRF_REF_NO				VARCHAR(20),
		POA_FLAG				VARCHAR(5),
		TRANSACTION_TYPE		VARCHAR(20),
		PURPOSE					VARCHAR(1000),
		ORDFLAG		INT,
		ORDDATE		VARCHAR(11),
		DPID		VARCHAR(20),
		CLTDPID		VARCHAR(20),
		BDPID		VARCHAR(20),
		BCLTDPID	VARCHAR(20)
		)

		CREATE INDEX [DELHOLD_SECREG] ON [DBO].[#SECREGSTOCK] (
			[UCC_CODE],
			[EXCHANGE],
			[SEGMENT],
			[SCRIP_NAME],
			[ISIN],
			[ORDDATE],
			[ORDFLAG]
			)
			
				
		INSERT INTO #SECREGSTOCK
		EXEC MSAJAG.DBO.[RPT_TRANSSTATEMENTBEN_NEW_UPD_NEW] @FROMDATE, @TODATE, @FROMPARTY, @TOPARTY
	
		
		INSERT INTO #SECREGSTOCK
		EXEC BSEDB.DBO.[RPT_TRANSSTATEMENTBEN_NEW_UPD_NEW] @FROMDATE, @TODATE, @FROMPARTY, @TOPARTY
		

		--SELECT	* FROM #SECREGSTOCK		
		--RETURN

		SELECT	FILENAME = @FILENAME, DATA = @MEMBERNAME +','+ REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE,109),103),'/','') +' to ' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE,109),103),'/','') 
				/*,EXCHANGE = '',
				SEGMENT = '',
				UCC_CODE = '',
				ORDDATE = '',
				SCRIP_NAME = '',
				ISIN = '',
				ORDFLAG = 0*/
		INTO #DATA
		UNION ALL

		SELECT	FILENAME = @FILENAME,
				DATA =	TRANS_DATE +','+ EXE_DATE +','+ EXCHANGE +','
						+ SEGMENT +','+ LTRIM(RTRIM(UCC_CODE)) +','+ LTRIM(RTRIM(PARTY_CODE)) +','
						+ CLIENT_NAME +','+ CLIENT_PAN +','+ MEMBER_DEMAT_ACC +','
						+ COUNTERPARTY_DEMAT_ACC +','+ SETT_NO +','+ ISIN +','
						+ SCRIP_NAME +','+ CONVERT(VARCHAR,QTY_DELIVERED) +','
						+ CONVERT(VARCHAR,QTY_RECEIVED) +','+ CONVERT(VARCHAR,BALANCE_QTY) +','
						+ CONVERT(VARCHAR,PLEDGE_UNPLEDGE_QTY) +','+ CONVERT(VARCHAR,TRF_REF_NO) +','
						+ POA_FLAG +','+ TRANSACTION_TYPE +','+ PURPOSE						
				/*,EXCHANGE,
				SEGMENT,
				UCC_CODE,
				ORDDATE,
				SCRIP_NAME,
				ISIN,
				ORDFLAG*/
		FROM	#SECREGSTOCK
		/*
		ORDER BY
				UCC_CODE,
				EXCHANGE,
				SEGMENT,
				SCRIP_NAME,
				ISIN,
				ORDDATE,
				ORDFLAG
				*/

		SELECT * FROM #DATA


		DROP TABLE #SECREGSTOCK
		DROP TABLE #DATA

GO
