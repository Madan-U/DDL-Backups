CREATE PROC [dbo].[TSS_CKYC_PROC]  
AS 

SELECT * INTO #TEST FROM [MIS].KYC_CI.DBO.Vi_GetDetailsForCkyc_Tss WITH(NOLOCK)

SELECT DISTINCT T.*,T1.FNAME,T1.MNAME,T1.LNAME,S.DESCRIPTION AS PROOF_ADDRESS,COR_ADD_PROOF_REF_ID,COR_ADDRESS_PROOF_REF_DATE,  
P.DESCRIPTION AS PERMANENT_ADDRESS,PERMFOREIGN_REFID AS PERMANENT_ADDRESS_ID,PERMFOREIGN_REFID_DATE AS PERMANENT_ADDRESS_DATE  
FROM #TEST T WITH(NOLOCK) , ABVSCITRUS.crmdb_a.dbo. api_kyc t1 WITH(NOLOCK),
CLIENT_MASTER_UCC_DATA U  
LEFT OUTER JOIN   
CLIENT_STATIC_CODES S WITH(NOLOCK) ON S.CODE =ISNULL(PROOF_OF_ADDRESS,'')  
LEFT OUTER JOIN   
CLIENT_STATIC_CODES P  WITH(NOLOCK) ON P.CODE =ISNULL(PERMFOREIGN_ADDRESS_PROFF,'')  
WHERE T.PARTY_CODE=U.PARTY_CODE   
and T.PARTY_CODE=T1.BOPARTYCODE  
AND S.CATEGORY='ADDRESSPROFF' AND S.KRA_TYPE ='CDSL'  AND P.CATEGORY='ADDRESSPROFF' AND P.KRA_TYPE ='CDSL'

GO
