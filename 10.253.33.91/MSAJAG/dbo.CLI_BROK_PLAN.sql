CREATE PROC [dbo].[CLI_BROK_PLAN] ( @CL_CODE VARCHAR(11))

AS
 

SELECT TOP 1 * INTO #CL FROM (
SELECT DISTINCT SP_PARTY_CODE CL_CODE,SCHEME_NAME AS BROK_PLAN FROM SCHEME_MAPPING S WITH(NOLOCK) ,MAPPING_TABLE M
WHERE SP_PARTY_CODE =@CL_CODE AND SP_DATE_TO >GETDATE() AND SP_COMPUTATION_LEVEL ='O'
AND TRD_SCHEME_ID = SP_SCHEME_ID 
UNION ALL
SELECT DISTINCT CL_CODE,TABLE_NAME  FROM  CLIENT_BROK_DETAILS  C WITH(NOLOCK) ,BROKTABLE B  WHERE C.TRD_BROK = B.TABLE_NO
AND (TABLE_NAME LIKE '%VBB' OR TABLE_NAME LIKE '%PLUS' OR TABLE_NAME LIKE '%VALUE ADD PLAN%' ) AND CL_CODE =@CL_CODE AND SEGMENT='CAPITAL'
AND CL_CODE NOT IN (SELECT DISTINCT SP_PARTY_CODE AS BROK_PLAN FROM SCHEME_MAPPING S WITH(NOLOCK) ,MAPPING_TABLE M
WHERE SP_PARTY_CODE =@CL_CODE AND SP_DATE_TO >GETDATE() AND SP_COMPUTATION_LEVEL ='O'
AND TRD_SCHEME_ID = SP_SCHEME_ID ) ) A


IF @@ROWCOUNT <> 0
	 BEGIN 
		SELECT  CL_CODE, BROK_PLAN= ( Case when BROK_PLAN='iTrade Premier VBB' Then 'ITrade Premier' Else BROK_PLAN End) 
		FROM #CL

		Return
	END 
ELSE

	BEGIN 

		SELECT TOP 1 CL_CODE,Trd_Brok  INTO #CL1
		FROM CLIENT_BROK_DETAILS WITH(NOLOCK) WHERE CL_CODE =@CL_CODE AND Segment ='CAPITAL'

	SELECT DISTINCT CL_CODE,  ISNULL(UPPER(TABLE_NAME),'CUSTOM') as BROK_PLAN FROM #CL1 C
	LEFT OUTER JOIN
	BROKTABLE B
	ON C.Trd_Brok =Table_No 
	AND Table_name LIKE 'ANGEL%'



   END

GO
