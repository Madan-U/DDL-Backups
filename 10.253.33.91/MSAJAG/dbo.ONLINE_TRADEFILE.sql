CREATE PROC ONLINE_TRADEFILE
(@SAUDA_DATE VARCHAR(11))
AS 
SELECT TRADE_NO=REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(TRADE_NO,'R',''),'B',''),'A',''),'T',''),'Z',''),'S',''),
VALUE=0,EXCHANGE='NSE',
SELL_BUY=(CASE WHEN SELL_BUY = 1 THEN 'B' ELSE 'S' END),SCRIP_CD=RTrim(SCRIP_CD)+Rtrim(Series),
TRADEQTY, MARKETRATE, SAUDA_DATE = REPLACE(CONVERT(VARCHAR,SAUDA_DATE,103),'/',''), S.PARTY_CODE, ORDER_NO
FROM SETTLEMENT S, CLIENT1 C1, CLIENT2 C2 
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND C1.CL_CODE = C2.CL_CODE
AND C2.PARTY_CODE = S.PARTY_CODE
AND C2.DUMMY10 = 'ONLINE'
Union All
SELECT TRADE_NO=REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(TRADE_NO,'R',''),'B',''),'A',''),'T',''),'Z',''),'S',''),
VALUE=0,EXCHANGE='BSE',
SELL_BUY=(CASE WHEN SELL_BUY = 1 THEN 'B' ELSE 'S' END),SCRIP_CD,
TRADEQTY, MARKETRATE, SAUDA_DATE = REPLACE(CONVERT(VARCHAR,SAUDA_DATE,103),'/',''), S.PARTY_CODE, ORDER_NO
FROM BSEDB.DBO.SETTLEMENT S, BSEDB.DBO.CLIENT1 C1, BSEDB.DBO.CLIENT2 C2 
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND C1.CL_CODE = C2.CL_CODE
AND C2.PARTY_CODE = S.PARTY_CODE
AND C2.DUMMY10 = 'ONLINE'
ORDER BY S.PARTY_CODE, SCRIP_CD, 3

GO
