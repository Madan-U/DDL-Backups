CREATE Proc HOLDINGASONDATE AS
Truncate table TBLHOLDINGASONDATE 
Insert Into TBLHOLDINGASONDATE 
SELECT Party_Code,Scrip_cd,CertNo,Qty FROM DELTRANS WHERE BDPTYPE = 'CDSL'
AND BCLTDPID = '1202020000000937' AND FILLER2 = 1 
AND TRANSDATE <= 'MAR 31 2003' AND DRCR = 'D' 
AND DELIVERED = '0' And TrType <> 906 

Insert Into TBLHOLDINGASONDATE 
SELECT Party_Code,Scrip_cd,CertNo,Qty FROM DELTRANS D WHERE BDPTYPE = 'CDSL'
AND BCLTDPID = '1202020000000937' AND FILLER2 = 1 
AND TRANSDATE > 'MAR 31 2003 23:59' AND DRCR = 'D' 
AND DELIVERED In ('G','D') 
And TCode in ( SELECT TCode FROM DELTRANS, DeliveryDp WHERE FILLER2 = 0 
AND TRANSDATE < 'MAR 31 2003 23:59' AND DRCR = 'D' 
AND DELIVERED = 'G' and certno = D.CertNo
And BcltDpId = DeliveryDp.DpCltNo And D.Party_Code = Party_Code
And Description Like '%POOL%' )
And TrType <> 906 

Insert Into TBLHOLDINGASONDATE 
SELECT Party_Code,Scrip_cd,CertNo,Qty FROM DELTRANS D WHERE BDPTYPE = 'CDSL'
AND BCLTDPID = '1202020000000937' AND FILLER2 = 0 
AND TRANSDATE > 'MAR 31 2003 23:59' AND DRCR = 'D' 
AND DELIVERED = 'G' 
And TCode in ( SELECT TCode FROM DELTRANS, DeliveryDp WHERE FILLER2 = 0 
AND TRANSDATE < 'MAR 31 2003 23:59' AND DRCR = 'D' 
AND DELIVERED = 'G' and certno = D.CertNo
And BcltDpId = DeliveryDp.DpCltNo And D.Party_Code = Party_Code
And Description Like '%POOL%' )
And HolderName Like 'Ben 10384163'
And TrType <> 906 

Insert Into TBLHOLDINGASONDATE 
SELECT Party_Code,Scrip_cd,CertNo,Qty FROM DELTRANS D WHERE BDPTYPE = 'CDSL'
AND BCLTDPID = '1202020000000937' AND FILLER2 = 1 
AND TRANSDATE > 'MAR 31 2003 23:59' AND DRCR = 'D' 
AND DELIVERED In ('G','D') 
And TCode = '0'
And TrType <> 906 

SELECT CERTNO,Scrip_cd,QTY=SUM(QTY) FROM tblHOLDINGASONDATE
GROUP BY CERTNO,Scrip_cd
ORDER BY Scrip_cd

GO
