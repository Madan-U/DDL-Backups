CREATE PROC  [PLAN] ( @CL_CODE VARCHAR(11) )
AS

DECLARE @COUNT INT

SELECT  @COUNT = COUNT (1) FROM SCHEME_MAPPING WITH (NOLOCK)
WHERE SP_Party_Code = @CL_CODE

 IF @COUNT > 0

SELECT  DISTINCT SP_Party_Code,SCHEME_NAME FROM MAPPING_TABLE A WITH (NOLOCK) ,SCHEME_MAPPING B WITH (NOLOCK)
WHERE SP_Party_Code = @CL_CODE
AND TRD_SCHEME_ID = SP_Scheme_Id 

ELSE 

SELECT TOP 1 CL_CODE,Trd_Brok,Del_Brok INTO #CL
FROM CLIENT_BROK_DETAILS WHERE CL_CODE =@CL_CODE AND Segment ='CAPITAL'

SELECT DISTINCT CL_CODE,ISNULL(TABLE_NAME,'Custom') as table_NAME FROM #CL C
LEFT OUTER JOIN
BROKTABLE B
ON C.Trd_Brok =Table_No 
WHERE Table_name LIKE 'ANGEL%'

GO
