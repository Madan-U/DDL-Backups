CREATE PROC [dbo].[CLS_DELIVERY_CLOSING]
(
	@EXEDATE VARCHAR(11),
	@TYPE VARCHAR(20),
	@BATCH VARCHAR(10),
	@SLIP VARCHAR(10),
	@SETTNO VARCHAR(7),
	@SETTTYPE VARCHAR(2),
	@PARTYCODE VARCHAR(10),
	@SCRIP_CD VARCHAR(16),
	@SNO VARCHAR(MAX),
	@CHKLIST VARCHAR(MAX),
	@TOREMOVE INT,
	@STEPID INT
)
AS
/*
	EXEC CLS_DELIVERY_CLOSING @EXEDATE = '', @BATCH = '', @SLIP = '', @SETTNO = '', @SETTTYPE = '', @PARTYCODE = '', @SCRIP_CD = '', @SNO = '', @TOREMOVE = 0, @STEPID = 0
	EXEC CLS_DELIVERY_CLOSING @EXEDATE = 'Feb 21 2013', @BATCH = '', @SLIP = '', @SETTNO = '', @SETTTYPE = '', @PARTYCODE = '', @SCRIP_CD = '', @SNO = '', @TOREMOVE = 0, @STEPID = 1
	EXEC CLS_DELIVERY_CLOSING @EXEDATE = 'Feb 21 2013', @BATCH = '116', @SLIP = '', @SETTNO = '', @SETTTYPE = '', @PARTYCODE = '', @SCRIP_CD = '', @SNO = '', @TOREMOVE = 0, @STEPID = 2
	EXEC CLS_DELIVERY_CLOSING @EXEDATE = 'Feb 21 2013', @BATCH = '116', @SLIP = '5541', @SETTNO = '', @SETTTYPE = '', @PARTYCODE = '', @SCRIP_CD = '', @SNO = '', @TOREMOVE = 0, @STEPID = 3
	EXEC CLS_DELIVERY_CLOSING @EXEDATE = 'Feb 21 2013', @BATCH = '116', @SLIP = '5541', @SETTNO = '2000000', @SETTTYPE = '', @PARTYCODE = '', @SCRIP_CD = '', @SNO = '', @TOREMOVE = 0, @STEPID = 4
	EXEC CLS_DELIVERY_CLOSING @EXEDATE = 'Feb 21 2013', @BATCH = '116', @SLIP = '5541', @SETTNO = '2000000', @SETTTYPE = 'D', @PARTYCODE = '', @SCRIP_CD = '', @SNO = '', @TOREMOVE = 0, @STEPID = 5
	EXEC CLS_DELIVERY_CLOSING @EXEDATE = 'Feb 21 2013', @BATCH = '116', @SLIP = '5541', @SETTNO = '2000000', @SETTTYPE = 'D', @PARTYCODE = '51B005', @SCRIP_CD = '', @SNO = '', @TOREMOVE = 0, @STEPID = 6
	EXEC CLS_DELIVERY_CLOSING @EXEDATE = 'Feb 21 2013', @BATCH = '116', @SLIP = '5541', @SETTNO = '2000000', @SETTTYPE = 'D', @PARTYCODE = '51B005', @SCRIP_CD = '512289', @SNO = '', @TOREMOVE = 0, @STEPID = 7
	EXEC CLS_DELIVERY_CLOSING '27/02/2013', '', '', '', '', '', '', '', 0, 7
	
	begin tran
	EXEC CLS_DELIVERY_CLOSING '', '', '', '', '', '', '', '8035,8035,8035,8073,', 1, 8
	Select * from DELTRANSTEMP
	rollback
*/
IF LEN(@EXEDATE) = 10 AND CHARINDEX('/', @EXEDATE) > 0
	SET @EXEDATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @EXEDATE, 103), 109)
IF @STEPID = 0
	BEGIN
		SELECT TOP 1 CONVERT(VARCHAR(10),TRANSDATE,103) AS CURRDATE FROM DELTRANSTEMP ORDER BY TRANSDATE DESC
	END
ELSE IF @STEPID = 1
	BEGIN
		SELECT	DISTINCT BATCHNO = CONVERT(INT, BATCHNO) FROM DELTRANSTEMP WHERE TRANSDATE LIKE @EXEDATE + '%' AND FILLER2 = 1 AND TRTYPE <> '907'
				AND PARTY_CODE <> 'BROKER'
		ORDER BY CONVERT(INT,BATCHNO)
	END
ELSE IF @STEPID = 2
	BEGIN
		SELECT DISTINCT SLIPNO FROM DELTRANSTEMP WHERE TRANSDATE LIKE @EXEDATE + '%' AND FILLER2= 1 AND TRTYPE <> '907' AND PARTY_CODE <> 'BROKER'
		AND CONVERT(INT, BATCHNO) = CASE WHEN @BATCH <> '' THEN CONVERT(INT, @BATCH) ELSE CONVERT(INT, BATCHNO) END
		ORDER BY SLIPNO
	END
ELSE IF @STEPID = 3
	BEGIN
		SELECT DISTINCT SETT_NO FROM DELTRANSTEMP WHERE TRANSDATE LIKE @EXEDATE + '%' AND FILLER2= 1 AND TRTYPE <> '907' AND PARTY_CODE <> 'BROKER'
		AND CONVERT(INT,BATCHNO) = CASE WHEN @BATCH <> '' THEN CONVERT(INT, @BATCH) ELSE CONVERT(INT, BATCHNO) END
		AND SLIPNO = CASE WHEN @SLIP <> '' THEN @SLIP ELSE SLIPNO END
		ORDER BY SETT_NO
	END
ELSE IF @STEPID = 4
	BEGIN
		SELECT DISTINCT SETT_TYPE FROM  DELTRANSTEMP WHERE TRANSDATE LIKE @EXEDATE + '%' AND FILLER2= 1 AND TRTYPE <> '907' AND PARTY_CODE <> 'BROKER'
		AND CONVERT(INT,BATCHNO) = CASE WHEN @BATCH <> '' THEN CONVERT(INT, @BATCH) ELSE CONVERT(INT, BATCHNO) END
		AND SLIPNO = CASE WHEN @SLIP <> '' THEN @SLIP ELSE SLIPNO END
		AND SETT_NO = CASE WHEN @SETTNO <> '' THEN @SETTNO ELSE SETT_NO END
		ORDER BY SETT_TYPE
	END
ELSE IF @STEPID = 5
	BEGIN
		SELECT DISTINCT PARTY_CODE FROM  DELTRANSTEMP WHERE TRANSDATE LIKE @EXEDATE + '%' AND FILLER2= 1 AND PARTY_CODE <> 'BROKER'
		AND CONVERT(INT, BATCHNO) = CASE WHEN @BATCH <> '' THEN CONVERT(INT, @BATCH) ELSE CONVERT(INT, BATCHNO) END
		AND SLIPNO = CASE WHEN @SLIP <> '' THEN @SLIP ELSE SLIPNO END
		AND SETT_NO = CASE WHEN @SETTNO <> '' THEN @SETTNO ELSE SETT_NO END
		AND SETT_TYPE = CASE WHEN @SETTTYPE <> '' THEN @SETTTYPE ELSE SETT_TYPE END
		ORDER BY PARTY_CODE
	END
ELSE IF @STEPID = 6
	BEGIN
		SELECT DISTINCT SCRIP_CD FROM DELTRANSTEMP WHERE TRANSDATE LIKE @EXEDATE + '%' AND FILLER2= 1 AND PARTY_CODE <> 'BROKER'
		AND CONVERT(INT,BATCHNO) = CASE WHEN @BATCH <> '' THEN CONVERT(INT, @BATCH) ELSE CONVERT(INT, BATCHNO) END
		AND SLIPNO = CASE WHEN @SLIP <> '' THEN @SLIP ELSE SLIPNO END
		AND SETT_NO = CASE WHEN @SETTNO <> '' THEN @SETTNO ELSE SETT_NO END
		AND SETT_TYPE = CASE WHEN @SETTTYPE <> '' THEN @SETTTYPE ELSE SETT_TYPE END
		AND PARTY_CODE = CASE WHEN @PARTYCODE <> '' THEN @PARTYCODE ELSE PARTY_CODE END ORDER BY SCRIP_CD
	END
ELSE IF @STEPID = 7
	BEGIN
		SELECT SNO, SETT_NO, SETT_TYPE, PARTY_CODE, DPID, CLTDPID, HOLDERNAME, DELTRANS.SCRIP_CD, QTY, ISIN = CERTNO, FLAG = (CASE WHEN DELIVERED = 'D' THEN 1 ELSE 0 END)
		FROM DELTRANSTEMP DELTRANS
		WHERE
			TRANSDATE LIKE @EXEDATE + '%'
			AND FILLER2= 1
			AND PARTY_CODE <> 'BROKER'
			AND CONVERT(INT, BATCHNO) = CASE WHEN @BATCH <> '' THEN CONVERT(INT, @BATCH) ELSE CONVERT(INT, BATCHNO) END
			AND SLIPNO = CASE WHEN @SLIP <> '' THEN @SLIP ELSE SLIPNO END
			AND SETT_NO = CASE WHEN @SETTNO <> '' THEN @SETTNO ELSE SETT_NO END
			AND SETT_TYPE = CASE WHEN @SETTTYPE <> '' THEN @SETTTYPE ELSE SETT_TYPE END
			AND PARTY_CODE = CASE WHEN @PARTYCODE <> '' THEN @PARTYCODE ELSE PARTY_CODE END
			AND SCRIP_CD = CASE WHEN @SCRIP_CD <> '' THEN @SCRIP_CD ELSE SCRIP_CD END
			AND 1 = (CASE WHEN UPPER(@TYPE) = UPPER('Margin Pledge') AND BRANCHCD = 'PLEDGE' THEN 1										 
										  WHEN UPPER(@TYPE) = UPPER('Margin Pledge') AND BRANCHCD <> 'PLEDGE' THEN 2
										  WHEN UPPER(@TYPE) <> UPPER('Margin Pledge') AND BRANCHCD = 'PLEDGE' THEN 2
										  ELSE 1 END)	
		ORDER BY
			SETT_NO,
			SETT_TYPE,
			CASE WHEN @PARTYCODE <> '' AND @SCRIP_CD = '' THEN SCRIP_CD ELSE PARTY_CODE END,
			CASE WHEN @SCRIP_CD <> '' AND @PARTYCODE = '' THEN PARTY_CODE ELSE SCRIP_CD END
	END
ELSE IF @STEPID = 8
	BEGIN
		DECLARE
			@SRNO VARCHAR(10),
			@SRPOS INT
		CREATE TABLE #DELTRANS
		(
			SNO INT,
			FILLER2 INT,
			BDPTYPE VARCHAR(10),
			BDPID VARCHAR(16),
			BCLTDPID VARCHAR(16),
			ISCHECKED VARCHAR(1)
		)
		SET @SRPOS = 1
		WHILE 1 = 1
			BEGIN
				SET @SRNO = .dbo.CLS_Piece(@SNO, ',', @SRPOS)
				IF @SRNO IS NULL
					BREAK
				IF @SRNO = ''
					BREAK
				
				IF UPPER(@TYPE) = UPPER('Margin RePledge') 
				 BEGIN
					INSERT INTO #DELTRANS (SNO, FILLER2, BDPTYPE, BDPID, BCLTDPID, ISCHECKED)
					SELECT SNO, FILLER2, BDPTYPE, BDPID, BCLTDPID, .dbo.CLS_Piece(@CHKLIST, ',', @SRPOS) FROM DelTrans_Pld WHERE FILLER2 = 1 AND SNO = CONVERT(INT, @SRNO)
				 END
				ELSE
				 BEGIN
					INSERT INTO #DELTRANS (SNO, FILLER2, BDPTYPE, BDPID, BCLTDPID, ISCHECKED)
					SELECT SNO, FILLER2, BDPTYPE, BDPID, BCLTDPID, .dbo.CLS_Piece(@CHKLIST, ',', @SRPOS) FROM DELTRANSTEMP WHERE FILLER2 = 1 AND SNO = CONVERT(INT, @SRNO)
				 END
				SET @SRPOS = @SRPOS + 1
			END

			
		IF (SELECT COUNT(1) FROM #DELTRANS) > 0
			BEGIN
				IF UPPER(@TYPE) = UPPER('Margin RePledge') 
				BEGIN
					UPDATE DELTRANS_PLD SET DELIVERED = 0, FILLER2 = 0, HOLDERNAME = 'REJECTED' FROM #DELTRANS H WHERE DELTRANS_PLD.SNO = H.SNO AND H.ISCHECKED <> '1'
					INSERT INTO DELTRANS_PLD (	SETT_NO, SETT_TYPE, REFNO, TCODE, TRTYPE, PARTY_CODE, SCRIP_CD, SERIES, QTY, FROMNO, TONO, CERTNO, FOLIONO, HOLDERNAME,
										REASON, DRCR, DELIVERED, ORGQTY, DPTYPE, DPID, CLTDPID, BRANCHCD, PARTIPANTCODE, SLIPNO, BATCHNO, ISETT_NO,
										ISETT_TYPE, SHARETYPE, TRANSDATE, FILLER1, FILLER2, FILLER3, BDPTYPE, BDPID, BCLTDPID, FILLER4, FILLER5)
					SELECT	D.SETT_NO, D.SETT_TYPE, D.REFNO, D.TCODE, TRTYPE = CASE WHEN @TOREMOVE = 1 THEN 904 ELSE D.TRTYPE END, D.PARTY_CODE, D.SCRIP_CD,
							D.SERIES, D.QTY, D.FROMNO, D.TONO, D.CERTNO, D.FOLIONO, D.HOLDERNAME, D.REASON, D.DRCR, 0, D.ORGQTY, D.DPTYPE, D.DPID, D.CLTDPID,
							D.BRANCHCD, D.PARTIPANTCODE, D.SLIPNO, D.BATCHNO, D.ISETT_NO, D.ISETT_TYPE, D.SHARETYPE, D.TRANSDATE, D.FILLER1, D.FILLER2,
							D.FILLER3, H.BDPTYPE, H.BDPID, H.BCLTDPID, D.FILLER4, D.FILLER5
					FROM 
						DELTRANSTEMP D, #DELTRANS H
					WHERE 
						D.SNO = H.SNO
						AND H.ISCHECKED <> '1'
				 END
				ELSE
				 BEGIN
					UPDATE DELTRANS SET DELIVERED = 0, FILLER2 = 0, HOLDERNAME = 'REJECTED' FROM #DELTRANS H WHERE DELTRANS.SNO = H.SNO AND H.ISCHECKED <> '1'
					INSERT INTO DELTRANS (	SETT_NO, SETT_TYPE, REFNO, TCODE, TRTYPE, PARTY_CODE, SCRIP_CD, SERIES, QTY, FROMNO, TONO, CERTNO, FOLIONO, HOLDERNAME,
											REASON, DRCR, DELIVERED, ORGQTY, DPTYPE, DPID, CLTDPID, BRANCHCD, PARTIPANTCODE, SLIPNO, BATCHNO, ISETT_NO,
											ISETT_TYPE, SHARETYPE, TRANSDATE, FILLER1, FILLER2, FILLER3, BDPTYPE, BDPID, BCLTDPID, FILLER4, FILLER5)
					SELECT	D.SETT_NO, D.SETT_TYPE, D.REFNO, D.TCODE, TRTYPE = CASE WHEN @TOREMOVE = 1 THEN 904 ELSE D.TRTYPE END, D.PARTY_CODE, D.SCRIP_CD,
							D.SERIES, D.QTY, D.FROMNO, D.TONO, D.CERTNO, D.FOLIONO, D.HOLDERNAME, D.REASON, D.DRCR, 0, D.ORGQTY, D.DPTYPE, D.DPID, D.CLTDPID,
							D.BRANCHCD, D.PARTIPANTCODE, D.SLIPNO, D.BATCHNO, D.ISETT_NO, D.ISETT_TYPE, D.SHARETYPE, D.TRANSDATE, D.FILLER1, D.FILLER2,
							D.FILLER3, H.BDPTYPE, H.BDPID, H.BCLTDPID, D.FILLER4, D.FILLER5
					FROM DELTRANSTEMP D, #DELTRANS H
					WHERE D.SNO = H.SNO
					AND H.ISCHECKED <> '1'
				 END

				DELETE D FROM DELTRANSTEMP D, #DELTRANS H WHERE D.SNO = H.SNO AND H.ISCHECKED <> '1'
				

				IF UPPER(@TYPE) = UPPER('Margin RePledge') 
				 BEGIN
					UPDATE DELTRANS_PLD SET FILLER2 = 0 FROM #DELTRANS T WHERE DELTRANS_PLD.SNO = T.SNO

					INSERT INTO DELTRANS_PLD (SETT_NO, SETT_TYPE, REFNO, TCODE, TRTYPE, PARTY_CODE, SCRIP_CD, SERIES, QTY, FROMNO, TONO, CERTNO, FOLIONO,
								HOLDERNAME, REASON, DRCR, DELIVERED, ORGQTY, DPTYPE, DPID, CLTDPID, BRANCHCD, PARTIPANTCODE, SLIPNO, BATCHNO, ISETT_NO,
								ISETT_TYPE, SHARETYPE, TRANSDATE, FILLER1, FILLER2, FILLER3, BDPTYPE, BDPID, BCLTDPID, FILLER4, FILLER5)
					SELECT	D.SETT_NO, D.SETT_TYPE, D.REFNO, D.TCODE, TRTYPE = (CASE WHEN D.TRTYPE = 905 THEN 905 ELSE 904 END), D.PARTY_CODE, D.SCRIP_CD, D.SERIES,
							D.QTY, D.FROMNO, D.TONO, D.CERTNO, D.FOLIONO, D.HOLDERNAME, D.REASON, D.DRCR, D.DELIVERED, D.ORGQTY, D.DPTYPE, D.DPID, D.CLTDPID, D.BRANCHCD, D.PARTIPANTCODE,
							D.SLIPNO, D.BATCHNO, D.ISETT_NO, D.ISETT_TYPE, D.SHARETYPE, D.TRANSDATE, D.FILLER1, D.FILLER2, D.FILLER3, D.BDPTYPE, D.BDPID, D.BCLTDPID, D.FILLER4, D.FILLER5
					FROM DELTRANSTEMP D, #DELTRANS H
					WHERE D.SNO = H.SNO
				 END
				ELSE
				 BEGIN
					UPDATE DELTRANS 
					SET FILLER2 = 0 
					FROM #DELTRANS T, DelTransTemp T2 
					WHERE T.SNO = T2.SNO 
							AND DELTRANS.SNO = T.SNO 
							AND 1 = (CASE WHEN UPPER(@TYPE) = UPPER('Margin Pledge') AND T2.BRANCHCD = 'PLEDGE' THEN 1										 
										  WHEN UPPER(@TYPE) = UPPER('Margin Pledge') AND T2.BRANCHCD <> 'PLEDGE' THEN 2
										  WHEN UPPER(@TYPE) <> UPPER('Margin Pledge') AND T2.BRANCHCD = 'PLEDGE' THEN 2
										  ELSE 1 END)		

					INSERT INTO DELTRANS (SETT_NO, SETT_TYPE, REFNO, TCODE, TRTYPE, PARTY_CODE, SCRIP_CD, SERIES, QTY, FROMNO, TONO, CERTNO, FOLIONO,
								HOLDERNAME, REASON, DRCR, DELIVERED, ORGQTY, DPTYPE, DPID, CLTDPID, BRANCHCD, PARTIPANTCODE, SLIPNO, BATCHNO, ISETT_NO,
								ISETT_TYPE, SHARETYPE, TRANSDATE, FILLER1, FILLER2, FILLER3, BDPTYPE, BDPID, BCLTDPID, FILLER4, FILLER5)
					SELECT	D.SETT_NO, D.SETT_TYPE, D.REFNO, D.TCODE, TRTYPE = (CASE WHEN D.TRTYPE = 905 THEN 905 ELSE 904 END), D.PARTY_CODE, D.SCRIP_CD, D.SERIES,
							D.QTY, D.FROMNO, D.TONO, D.CERTNO, D.FOLIONO, D.HOLDERNAME, D.REASON, D.DRCR, D.DELIVERED, D.ORGQTY, D.DPTYPE, D.DPID, D.CLTDPID, D.BRANCHCD, D.PARTIPANTCODE,
							D.SLIPNO, D.BATCHNO, D.ISETT_NO, D.ISETT_TYPE, D.SHARETYPE, D.TRANSDATE, D.FILLER1, D.FILLER2, D.FILLER3, D.BDPTYPE, D.BDPID, D.BCLTDPID, D.FILLER4, D.FILLER5
					FROM DELTRANSTEMP D, #DELTRANS H
					WHERE D.SNO = H.SNO		
					AND 1 = (CASE WHEN UPPER(@TYPE) = UPPER('Margin Pledge') AND D.BRANCHCD = 'PLEDGE' THEN 1										 
										  WHEN UPPER(@TYPE) = UPPER('Margin Pledge') AND D.BRANCHCD <> 'PLEDGE' THEN 2
										  WHEN UPPER(@TYPE) <> UPPER('Margin Pledge') AND D.BRANCHCD = 'PLEDGE' THEN 2
										  ELSE 1 END)
				 END

					DELETE D FROM DELTRANSTEMP D, #DELTRANS H WHERE D.SNO = H.SNO AND 1 = (CASE WHEN UPPER(@TYPE) = UPPER('Margin Pledge') AND D.BRANCHCD = 'PLEDGE' THEN 1										 
										  WHEN UPPER(@TYPE) = UPPER('Margin Pledge') AND D.BRANCHCD <> 'PLEDGE' THEN 2
										  WHEN UPPER(@TYPE) <> UPPER('Margin Pledge') AND D.BRANCHCD = 'PLEDGE' THEN 2
										  ELSE 1 END)
				
			END
	END

	ELSE IF @STEPID = 9
	BEGIN
		SELECT TYPE = 'Margin Pledge'
		UNION
		SELECT TYPE = 'Margin RePledge'
		UNION
		SELECT TYPE = 'Normal'
	END

GO
