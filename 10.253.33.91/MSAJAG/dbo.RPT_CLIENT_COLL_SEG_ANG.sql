CREATE PROCEDURE [dbo].[RPT_CLIENT_COLL_SEG_ANG] (@REPORT_DATE VARCHAR(11))    
AS    
    
	IF CONVERT(DATETIME,@REPORT_DATE) < 'JAN 21 2023'
	BEGIN 
	EXEC [RPT_CLIENT_COLL_SEG_ANG_OLD_210123] @REPORT_DATE
	END
	ELSE
	BEGIN
	EXEC RPT_CLIENT_COLL_SEG_ANG_NEW @REPORT_DATE
	END

GO
