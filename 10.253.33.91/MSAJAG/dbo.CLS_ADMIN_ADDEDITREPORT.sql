CREATE PROC [dbo].[CLS_ADMIN_ADDEDITREPORT]  
(  
 @REPORTID INT,  
 @REPORT VARCHAR(35),  
 @PATH VARCHAR(500),  
 @TARGET VARCHAR(25),  
 @DESC VARCHAR(80),  
 @REPORTGRP VARCHAR(35),  
 @STATUS VARCHAR(20),  
 @ORDER INT,  
 @CREATED_BY VARCHAR(50),  
 @ACTION VARCHAR(10)   
)  
/*  
  
TBLREPORTS_LOG  
CLS_ADMIN_ADDEDITREPORT '2634','test','test1','','test',0,1,'all',0,'Admin','delete'  
select * from TBLREPORTS where fldreportname = 'test'  
select * from TBLREPORTS_LOG where fldreportname = 'test'  
 SELECT *  FROM TBLREPORTGRP WHERE fldreportgrp = 89  
*/  
AS  
 DECLARE @RESULT VARCHAR(50),  
     @COUNT INT,  
     @RPTCODE INT,  
     @TODAT DATETIME,  
     @RPTID INT,  
     @MENUGRP VARCHAR(3)  
  
 SET @STATUS = 'ALL'  
 SET @TODAT =  GETDATE()  
 SELECT @MENUGRP=FLDMENUGRP  FROM TBLREPORTGRP WHERE fldreportgrp = @REPORTGRP  
  
BEGIN TRY  
 SET NOCOUNT ON;  
  IF @ACTION = 'ADD'  
  BEGIN  
  print'suresh'
   IF ((SELECT COUNT(*) FROM TBLREPORTS WHERE UPPER(RTRIM(LTRIM(FLDREPORTNAME))) = UPPER(RTRIM(LTRIM(@REPORT)))) > 0 )  
    BEGIN  
    print'suresh1'
    SET @RESULT = 'REPORT NAME ALREADY EXIST...'  
    SELECT @RESULT AS MESSAGE  
    RETURN;  
    END  
   ELSE  
    BEGIN  
     INSERT INTO TBLREPORTS VALUES(@REPORT,@PATH,@TARGET,@DESC,@REPORTGRP,@MENUGRP,@STATUS,@ORDER)  
      
    SET @RPTID = Scope_Identity()  
    INSERT INTO tblreports_LOG(FLDREPORTCODE,FLDREPORTNAME,FLDPATH,FLDTARGET,FLDDESC,FLDREPORTGRP,FLDMENUGRP,FLDSTATUS,FLDORDER,EDIT_FLAG,CREATED_BY,CREATED_ON)  
    SELECT @RPTID,FLDREPORTNAME,FLDPATH,FLDTARGET,FLDDESC,FLDREPORTGRP,FLDMENUGRP,FLDSTATUS,FLDORDER,'E',@CREATED_BY,@TODAT from tblreports WHERE Fldreportcode = @REPORTID  
  
    SET @RESULT = 'REPORT INSERTED SUCCESSFULLY!'  
    IF @@ERROR = 0  
     SELECT @RESULT AS MESSAGE  
    
    END   
  END  
 Else IF @ACTION = 'DELETE'  
  Begin  
   if((SELECT COUNT(*) FROM TBLREPORTS WHERE Fldreportcode = @REPORTID) =0)  
    Begin   
     SET @RESULT = 'NO SUCH REPORT EXISTS IN THE SYSTEM!'  
     SELECT @RESULT AS MESSAGE  
    End  
   Else  
    Begin  
    INSERT INTO tblreports_LOG(FLDREPORTCODE,FLDREPORTNAME,FLDPATH,FLDTARGET,FLDDESC,FLDREPORTGRP,FLDMENUGRP,FLDSTATUS,FLDORDER,EDIT_FLAG,CREATED_BY,CREATED_ON)  
    SELECT FLDREPORTCODE,FLDREPORTNAME,FLDPATH,FLDTARGET,FLDDESC,FLDREPORTGRP,FLDMENUGRP,FLDSTATUS,FLDORDER,'D',@CREATED_BY,@TODAT from tblreports WHERE Fldreportcode = @REPORTID  
      
    Delete from tblreports WHERE Fldreportcode = @REPORTID  
  
    SET @RESULT = 'REPORT DELEETED SUCCESSFULLY!'  
    IF @@ERROR = 0  
     SELECT @RESULT AS MESSAGE  
    End  
  End  
 Else IF @ACTION = 'EDIT'  
  Begin  
  INSERT INTO tblreports_LOG(FLDREPORTCODE,FLDREPORTNAME,FLDPATH,FLDTARGET,FLDDESC,FLDREPORTGRP,FLDMENUGRP,FLDSTATUS,FLDORDER,EDIT_FLAG,CREATED_BY,CREATED_ON)  
  SELECT FLDREPORTCODE,FLDREPORTNAME,FLDPATH,FLDTARGET,FLDDESC,FLDREPORTGRP,FLDMENUGRP,FLDSTATUS,FLDORDER,'E',@CREATED_BY,@TODAT from tblreports WHERE Fldreportcode = @REPORTID  
   
  update tblreports set  
   Fldreportname = @REPORT,  
   Fldpath = @PATH,  
   Fldreportgrp = @REPORTGRP,  
   Fldmenugrp = @MENUGRP,  
   Fldorder = @ORDER,  
   Fldstatus = @STATUS,  
   Flddesc = @DESC,  
   Fldtarget = @TARGET  
  WHERE   
   Fldreportcode = @REPORTID  
  
   SET @RESULT = 'REPORT UPDATED SUCCESSFULLY!'  
    IF @@ERROR = 0  
     SELECT @RESULT AS MESSAGE  
  END  
    
END TRY  
BEGIN CATCH  
 SELECT   
        ERROR_MESSAGE() AS MESSAGE;  
END CATCH

GO
