-- Object: PROCEDURE dbo.FOMARGIN_REPORT_STAT_UPD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[FOMARGIN_REPORT_STAT_UPD] 
(
	@FILEDATE VARCHAR(11),
	@USERID VARCHAR(20),
	@SESSIONID VARCHAR(30),
	@MODE INT
) AS

DECLARE @SYSTEMDATE DATETIME
DECLARE @INT_COMPUTATION_ON INT
 
SET @SYSTEMDATE = GETDATE()

IF @MODE = 1
BEGIN

	TRUNCATE TABLE FOMARGIN_REPORT_STAT_IMP

	INSERT INTO FOMARGIN_REPORT_STAT_IMP
	SELECT 
		MARGIN_DATE =   DBO.PIECE(FILE_DATA,',',1),
		PARTY_CODE =   DBO.PIECE(FILE_DATA,',',2),
		INT_MARGIN =   DBO.PIECE(FILE_DATA,',',3),
		NET_PREMIUM =   DBO.PIECE(FILE_DATA,',',4),
		TOTAL_MARGIN =  (CASE WHEN DBO.PIECE(FILE_DATA,',',7) = 'C' OR DBO.PIECE(FILE_DATA,',',7) = 'P' 
							  THEN DBO.PIECE(FILE_DATA,',',5) ELSE DBO.PIECE(FILE_DATA,',',8) END),
		EXP_MARGIN =   (CASE WHEN DBO.PIECE(FILE_DATA,',',7) = 'C' OR DBO.PIECE(FILE_DATA,',',7) = 'P' 
							  THEN DBO.PIECE(FILE_DATA,',',6) ELSE DBO.PIECE(FILE_DATA,',',5) END),
		CLIENT_TYPE =   (CASE WHEN DBO.PIECE(FILE_DATA,',',7) = 'C' OR DBO.PIECE(FILE_DATA,',',7) = 'P' 
							  THEN DBO.PIECE(FILE_DATA,',',7) ELSE DBO.PIECE(FILE_DATA,',',9) END),
		REPORTED_MARGIN =  (CASE WHEN DBO.PIECE(FILE_DATA,',',7) = 'C' OR DBO.PIECE(FILE_DATA,',',7) = 'P' 
							  THEN DBO.PIECE(FILE_DATA,',',8) ELSE DBO.PIECE(FILE_DATA,',',10) END),
		SHORTAGE =   (CASE WHEN DBO.PIECE(FILE_DATA,',',7) = 'C' OR DBO.PIECE(FILE_DATA,',',7) = 'P' 
							  THEN DBO.PIECE(FILE_DATA,',',9) ELSE DBO.PIECE(FILE_DATA,',',11) END),
		STATUS_FLAG =   (CASE WHEN DBO.PIECE(FILE_DATA,',',7) = 'C' OR DBO.PIECE(FILE_DATA,',',7) = 'P' 
							  THEN DBO.PIECE(FILE_DATA,',',10) ELSE DBO.PIECE(FILE_DATA,',',12) END)
	FROM CLASSFILEUPD
	WHERE
		SESSION_ID= @SESSIONID
		AND CONVERT(DATETIME,DBO.PIECE(FILE_DATA,',',1)) = @FILEDATE

	DELETE CLASSFILEUPD
	WHERE SESSION_ID= @SESSIONID
END
ELSE
BEGIN

	SELECT TOP 1  @INT_COMPUTATION_ON = COMPUTATION_ON FROM FOMARGIN_PENALTY_MST (NOLOCK)
	WHERE @FILEDATE BETWEEN FROM_DATE AND TO_DATE
	
	/*
	@INT_COMPUTATION_ON 1 - EXCHANGE GIVEN
	@INT_COMPUTATION_ON 2 - INTMARG
	@INT_COMPUTATION_ON 3 - INTMRG+EXPMRG
	*/
	
	UPDATE FOMARGIN_REPORT_STAT_IMP SET PARTY_CODE = NEWPARTY_CODE 
	FROM PARTYMAPPING (NOLOCK)
	WHERE FOMARGIN_REPORT_STAT_IMP.PARTY_CODE = OLDPARTY_CODE 

	DELETE FOMARGIN_REPORT_STAT WHERE MARGIN_DATE = @FILEDATE

	INSERT INTO FOMARGIN_REPORT_STAT
	SELECT MARGIN_DATE,PARTY_CODE,INT_MARGIN,NET_PREMIUM,TOTAL_MARGIN,EXP_MARGIN,CLIENT_TYPE,REPORTED_MARGIN,
	EXCHG_SHORTAGE = SHORTAGE,	
	SHORTAGE,	
	STATUS_FLAG , @USERID, @SYSTEMDATE
	FROM FOMARGIN_REPORT_STAT_IMP
	WHERE STATUS_FLAG = '8'		-- EXCESS

	INSERT INTO FOMARGIN_REPORT_STAT
	SELECT MARGIN_DATE,PARTY_CODE,INT_MARGIN,NET_PREMIUM,TOTAL_MARGIN,EXP_MARGIN,CLIENT_TYPE,REPORTED_MARGIN,
	EXCHG_SHORTAGE = SHORTAGE,	
	SHORTAGE  = CASE WHEN @INT_COMPUTATION_ON = 2
					THEN
						(INT_MARGIN - (CASE WHEN REPORTED_MARGIN > 0 THEN REPORTED_MARGIN ELSE 0 END))
					WHEN @INT_COMPUTATION_ON = 3
					THEN 
						((INT_MARGIN + EXP_MARGIN)- (CASE WHEN REPORTED_MARGIN > 0 THEN REPORTED_MARGIN ELSE 0 END))
					ELSE
						(CASE WHEN SHORTAGE = 0 THEN - INT_MARGIN ELSE SHORTAGE END)
					END	,
	STATUS_FLAG , @USERID, @SYSTEMDATE
	FROM FOMARGIN_REPORT_STAT_IMP
	WHERE STATUS_FLAG <> '8'		-- DEFICIT

END

/*------------------------------ END OF PROC ------------------------*/

GO
