CREATE PROCEDURE [dbo].[CLS_BTST_SHORTAGE] (@TSETTNO VARCHAR(10),@TSETTYPE VARCHAR(2))

AS 
--- EXEC CLS_BTST_SHORTAGE '2021148','N'

DECLARE @PDAYSETT INT
DECLARE @TDAYSETT INT

SET @TDAYSETT=CONVERT(int,@TSETTNO)



--SELECT @TDAYSETT = SETT_NO FROM SETT_MST WHERE START_DATE > =  @RUNDATE AND START_DATE < = @RUNDATE AND SETT_TYPE='N'

SELECT @PDAYSETT = @TDAYSETT-1

TRUNCATE TABLE BUY_SHORTAGE
TRUNCATE TABLE SELL_SHORTAGE


INSERT INTO BUY_SHORTAGE
EXEC ANGELDEMAT.MSAJAG.DBO.RPT_SHORTAGEPAYOUT 'BROKER','BROKER',@PDAYSETT,@PDAYSETT,@TSETTYPE,'%',-1

INSERT INTO SELL_SHORTAGE
EXEC ANGELDEMAT.MSAJAG.DBO.RPT_SHORTAGEPAYIN  'BROKER','BROKER',@TDAYSETT,@TDAYSETT,@TSETTYPE,'%',-1

SELECT * INTO #BTSTSELL FROM MSAJAG.DBO.DELIVERYCLT WITH (NOLOCK) WHERE SETT_NO= @TDAYSETT AND INOUT='I'

SELECT A.SETT_NO,A.SETT_TYPE,A.PARTY_CODE,A.SHORT_NAME,A.SCRIP_CD,A.SERIES,SELLSHORTAGE=A.BUYSHORTAGE,SELL_QTY = B.QTY
FROM MSAJAG.DBO.BUY_SHORTAGE A, #BTSTSELL B
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.SCRIP_CD = B.SCRIP_CD AND A.SETT_TYPE = B.SETT_TYPE
AND LTRIM(RTRIM(A.PARTY_CODE)) + A.SCRIP_CD IN (SELECT LTRIM(RTRIM(PARTY_CODE)) + SCRIP_CD FROM SELL_SHORTAGE)

GO
