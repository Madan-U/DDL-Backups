CREATE PROCEDURE [dbo].[ALL_NET_POSITION_DERAILS] (@FDATE VARCHAR(11),@TDATE VARCHAR(11))

AS ---- EXEC ALL_NET_POSITION_DERAILS 'JUN 20 2021','JUN 30 2021'

SELECT SAUDA_DATE,SYMBOL, INST_TYPE, CONVERT(VARCHAR,EXPIRYDATE,103) AS EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE,  
SUM(PQTY) AS PQTY, SUM(SQTY) AS SQTY, (SUM(PRATE*PQTY)) AS PAMT, (SUM(SRATE*SQTY)) AS SAMT, 
(SUM(PQTY) - SUM(SQTY)) AS NETQTY, ABS((SUM(PRATE*PQTY) - SUM(SRATE*SQTY))) AS NETVALUE,
(CASE WHEN SUM(PQTY) > SUM(SQTY) THEN SUM(PQTY*PRATE)/SUM(PQTY) WHEN SUM(SQTY)  > 0THEN SUM(SQTY*SRATE)/SUM(SQTY) ELSE 0 END)AS AVGPRICE, 
SUM((PQTY - SQTY)*CL_RATE)  AS NETOPENVALUE,   
(CASE WHEN SUM(PQTY+SQTY) <> 0 THEN  ABS(SUM((PQTY + SQTY)*CL_RATE) / SUM(PQTY + SQTY)) ELSE 0 END ) AS CLOSEPRICE,EXCHANGE='NSEFO' INTO #POS
FROM ANGELFO.NSEFO.DBO.FOBILLVALAN WITH (NOLOCK) 
WHERE PARTY_CODE >= 'A' AND PARTY_CODE <= 'ZZZZZZZZZZ99999' 
AND SAUDA_DATE >= @FDATE + ' 00:00:00' AND SAUDA_DATE <= @TDATE + ' 23:59:59'
GROUP BY SYMBOL, INST_TYPE, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE,SAUDA_DATE
HAVING SUM(PQTY - SQTY) <> 0 

INSERT INTO #POS 
SELECT SAUDA_DATE,SYMBOL, INST_TYPE, CONVERT(VARCHAR,EXPIRYDATE,103) AS EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE, 
---PARTY_CODE,
SUM(PQTY) AS PQTY, SUM(SQTY) AS SQTY, (SUM(PRATE*PQTY)) AS PAMT, (SUM(SRATE*SQTY)) AS SAMT, (SUM(PQTY) - SUM(SQTY)) AS NETQTY,  
ABS((SUM(PRATE*PQTY) - SUM(SRATE*SQTY))) AS NETVALUE, 
(CASE WHEN SUM(PQTY) > SUM(SQTY) THEN SUM(PQTY*PRATE)/SUM(PQTY) WHEN SUM(SQTY)  > 0THEN SUM(SQTY*SRATE)/SUM(SQTY) ELSE 0 END)AS AVGPRICE, 
SUM((PQTY - SQTY)*CL_RATE)  AS NETOPENVALUE,   
(CASE WHEN SUM(PQTY+SQTY) <> 0 THEN  ABS(SUM((PQTY + SQTY)*CL_RATE) / SUM(PQTY + SQTY)) ELSE 0 END )AS CLOSEPRICE,EXCHANGE='NSECD'
FROM ANGELFO.NSECURFO.DBO.FOBILLVALAN WITH (NOLOCK)
WHERE PARTY_CODE >= 'A' AND PARTY_CODE <= 'ZZZZZZZZZZ99999' 
AND SAUDA_DATE >= @FDATE + ' 00:00:00' AND SAUDA_DATE <= @TDATE + ' 23:59:59' 
GROUP BY SYMBOL, INST_TYPE, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE,PARTY_CODE,SAUDA_DATE
HAVING SUM(PQTY - SQTY) <> 0 

INSERT INTO #POS 
SELECT SAUDA_DATE,SYMBOL, INST_TYPE, CONVERT(VARCHAR,EXPIRYDATE,103) AS EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE, 
SUM(CASE WHEN BILLNO >0 THEN PQTY/BILLNO ELSE PQTY END) AS PQTY,
SUM(CASE WHEN BILLNO >0 THEN SQTY/BILLNO ELSE SQTY END) AS SQTY, 
(SUM(PRATE*PQTY*NUMERATOR/DENOMINATOR)) AS PAMT, (SUM(SRATE*SQTY*NUMERATOR/DENOMINATOR)) AS SAMT,  
SUM(CASE WHEN BILLNO >0 THEN PQTY/BILLNO ELSE PQTY END) - SUM(CASE WHEN BILLNO >0 THEN SQTY/BILLNO ELSE SQTY END) AS NETQTY, 
ABS((SUM(PRATE*PQTY*NUMERATOR/DENOMINATOR) - SUM(SRATE*SQTY*NUMERATOR/DENOMINATOR))) AS NETVALUE, 
(CASE WHEN SUM(PQTY) > SUM(SQTY) THEN SUM(PQTY*PRATE)/SUM(PQTY) WHEN SUM(SQTY)  > 0THEN SUM(SQTY*SRATE)/SUM(SQTY) ELSE 0 END)AS AVGPRICE,  
SUM((PQTY - SQTY)*CL_RATE)  AS NETOPENVALUE,   
(CASE WHEN SUM(PQTY+SQTY) <> 0 THEN  ABS(SUM((PQTY + SQTY)*CL_RATE) / SUM(PQTY + SQTY)) ELSE 0 END )AS CLOSEPRICE,EXCHANGE='MCDX'
FROM ANGELCOMMODITY.MCDX.DBO.FOBILLVALAN WITH (NOLOCK) 
WHERE PARTY_CODE >= 'A' AND PARTY_CODE <= 'ZZZZZZZZZZ99999' 
AND SAUDA_DATE >= @FDATE + ' 00:00:00' AND SAUDA_DATE <= @TDATE + ' 23:59:59' 
GROUP BY SYMBOL, INST_TYPE, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE,SAUDA_DATE
HAVING SUM(PQTY - SQTY) <> 0 

INSERT INTO #POS 
SELECT SAUDA_DATE,SYMBOL, INST_TYPE, CONVERT(VARCHAR,EXPIRYDATE,103) AS EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE, 
SUM(CASE WHEN BILLNO >0 THEN PQTY/BILLNO ELSE PQTY END) AS PQTY,
SUM(CASE WHEN BILLNO >0 THEN SQTY/BILLNO ELSE SQTY END) AS SQTY, 
(SUM(PRATE*PQTY*NUMERATOR/DENOMINATOR)) AS PAMT, (SUM(SRATE*SQTY*NUMERATOR/DENOMINATOR)) AS SAMT,  
SUM(CASE WHEN BILLNO >0 THEN PQTY/BILLNO ELSE PQTY END) - SUM(CASE WHEN BILLNO >0 THEN SQTY/BILLNO ELSE SQTY END) AS NETQTY, 
ABS((SUM(PRATE*PQTY*NUMERATOR/DENOMINATOR) - SUM(SRATE*SQTY*NUMERATOR/DENOMINATOR))) AS NETVALUE, 
(CASE WHEN SUM(PQTY) > SUM(SQTY) THEN SUM(PQTY*PRATE)/SUM(PQTY) WHEN SUM(SQTY)  > 0THEN SUM(SQTY*SRATE)/SUM(SQTY) ELSE 0 END)AS AVGPRICE,  
SUM((PQTY - SQTY)*CL_RATE)  AS NETOPENVALUE,   
(CASE WHEN SUM(PQTY+SQTY) <> 0 THEN  ABS(SUM((PQTY + SQTY)*CL_RATE) / SUM(PQTY + SQTY)) ELSE 0 END )AS CLOSEPRICE,EXCHANGE='NCDX'
FROM ANGELCOMMODITY.NCDX.DBO.FOBILLVALAN WITH (NOLOCK) 
WHERE PARTY_CODE >= 'A' AND PARTY_CODE <= 'ZZZZZZZZZZ99999' 
AND SAUDA_DATE >= @FDATE + ' 00:00:00' AND SAUDA_DATE <= @TDATE + ' 23:59:59' 
GROUP BY SYMBOL, INST_TYPE, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE,SAUDA_DATE
HAVING SUM(PQTY - SQTY) <> 0 
ORDER BY SYMBOL, INST_TYPE, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE 

SELECT SAUDA_DATE,EXCHANGE,NETVALUE=CAST(SUM(NETVALUE) / 10000000 AS NUMERIC (18,2)) INTO #NETPOS
FROM #POS 
GROUP BY EXCHANGE,SAUDA_DATE


SELECT [Sauda Date] = CONVERT(VARCHAR(11),SAUDA_DATE,105),
[FNO Position C/F (Rs. In Crores)] = NSEFO,
[CD Position C/F (Rs. In Crores)] = NSECD,
[COMEX Position C/F (Rs. Crores)] = MCDX + NCDX
FROM (
SELECT SAUDA_DATE,EXCHANGE,NETVALUE FROM #NETPOS) AS SourceTable
PIVOT
(SUM(NETVALUE) FOR EXCHANGE IN (NSEFO,NSECD,MCDX,NCDX)) AS PivotTable
ORDER BY SAUDA_DATE

GO
