CREATE PROCEDURE [dbo].[PROC_ALL_CMP_CUSA_CALCULATION] (@RPT_DATE VARCHAR(11),@REPORT VARCHAR(10))

AS ---- PROC_ALL_CMP_CUSA_CALCULATION 'SEP 21 2022',''


DECLARE @PRV_DATE VARCHAR(11)
DECLARE @EPN_SETT_NO VARCHAR(11)

SELECT @EPN_SETT_NO = SETT_NO-1 FROM SETT_MST WHERE SETT_TYPE='N' AND START_DATE  BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59:59'
SELECT @PRV_DATE = LEFT(START_DATE,11) FROM SETT_MST WHERE SETT_TYPE='N' AND SETT_NO = @EPN_SETT_NO


CREATE TABLE #CMPHOLD ( [SCRIP_CD] VARCHAR(12), [SERIES] VARCHAR(3), 
[PARTY_CODE] VARCHAR(10), [SETT_NO] VARCHAR(7), [SETT_TYPE] VARCHAR(2), 
[QTY] DECIMAL(38,0), [CERTNO] VARCHAR(16), [BDPID] VARCHAR(16), [BCLTDPID] VARCHAR(16),
[INVQTY] DECIMAL(38,0), [FREE_QTY] DECIMAL(38,0), [REPLEDGEQTY] DECIMAL(38,0), 
[PARTY_NAME] VARCHAR(100), [LONG_NAME] VARCHAR(100), [CL_TYPE] VARCHAR(3))

INSERT INTO #CMPHOLD
EXEC ANGELDEMAT.MSAJAG.DBO.RPT_NSEDELHOLDPARTY 'BROKER','BROKER','A','ZZZZZZZZZZZZZZ','0','ZZZZ','','','%','%','ALL'


----CMP FREE HOLDING 
SELECT REPORT_DATE=@RPT_DATE,PARTY_CODE,SCRIP_CD,SERIES,CERTNO,BCLTDPID,FREE_QTY,CLRATE=CONVERT(MONEY,0),
HVALUE=CONVERT(MONEY,0),HVALUE_AFTER_VAR=CONVERT(MONEY,0) INTO #CMP_FREE
FROM #CMPHOLD WHERE BCLTDPID='1203320030135814' AND FREE_QTY <> 0

--AND PARTY_CODE IN (SELECT CLIENT_CODE FROM ALL_REPORT_ALLOCATION WHERE TOTAL_TDAY_MAG > 0)
--AND PARTY_CODE IN (
--SELECT CLIENT_CODE FROM TDAY_MARGIN_DATA_CM WITH (NOLOCK) WHERE CONVERT(MONEY,TOTAL_MRG) <> 0 AND CM_MARGIN_DATE = @RPT_DATE
--UNION 
--SELECT CLIENT_CODE FROM TDAY_MARGIN_DATA_FNO WITH (NOLOCK) WHERE CONVERT(MONEY,TOTAL_MRG) <> 0 AND FO_MARGIN_DATE = @RPT_DATE
--UNION
--SELECT CLIENT_CODE FROM TDAY_MARGIN_DATA_CD WITH (NOLOCK) WHERE CONVERT(MONEY,TOTAL_MRG) <> 0  AND CD_MARGIN_DATE = @RPT_DATE)

/* LAST CLOSING RATE UPDATE */

UPDATE A SET CLRATE = CL_RATE FROM #CMP_FREE A,
(SELECT SCRIP_CD,SERIES,CL_RATE
FROM CLOSING WITH (NOLOCK)  WHERE SysDate BETWEEN @PRV_DATE AND @PRV_DATE + ' 23:59:59') B
WHERE A.SCRIP_CD = B.SCRIP_CD AND A.SERIES = B.SERIES

UPDATE A SET CLRATE = CL_RATE FROM #CMP_FREE A,
(SELECT SYSDATE,SCRIP_CD,SERIES,ISIN,CL_RATE FROM AngelNSECM.MSAJAG.DBO.CLOSING_MTM B WITH (NOLOCK) WHERE SYSDATE = @PRV_DATE) B
WHERE A.CERTNO = B.ISIN AND A.SERIES = B.SERIES AND A.CLRATE = 0

UPDATE A SET CLRATE = NAV_VALUE FROM #CMP_FREE A,
(SELECT ISIN,NAV_VALUE FROM ANGELFO.BSEMFSS.DBO.MFSS_NAV WITH (NOLOCK) WHERE NAV_DATE BETWEEN @PRV_DATE AND @PRV_DATE + ' 23:59:59') B
WHERE A.CERTNO = B.ISIN AND CLRATE=0

SELECT DISTINCT ISIN,NAV_VALUE INTO #HIST FROM ANGELFO.BSEMFSS.DBO.MFSS_NAV A1 WITH (NOLOCK) 
WHERE NAV_DATE IN (SELECT MAX(NAV_DATE)FROM ANGELFO.BSEMFSS.DBO.MFSS_NAV A2 WITH (NOLOCK) WHERE A1.ISIN = A2.ISIN)

UPDATE A SET CLRATE = NAV_VALUE FROM #CMP_FREE A,#HIST B WHERE A.CERTNO = B.ISIN AND A.CLRATE=0 

UPDATE #CMP_FREE SET CLRATE = CLOPM_CDSL_RT FROM #CMP_FREE A1,
(SELECT CLOPM_ISIN_CD,CLOPM_CDSL_RT,CLOPM_DT FROM AGMUBODPL3.DMAT.CITRUS_USR.CLOSING_LAST_CDSL A 
WHERE CLOPM_DT IN (SELECT MAX(CLOPM_DT) 
FROM AGMUBODPL3.DMAT.CITRUS_USR.CLOSING_LAST_CDSL B WHERE A.CLOPM_ISIN_CD = B.CLOPM_ISIN_CD)) A2
WHERE A1.CERTNO = A2.CLOPM_ISIN_CD AND CLRATE=0

UPDATE #CMP_FREE SET HVALUE = FREE_QTY * CLRATE

UPDATE A SET HVALUE_AFTER_VAR = HVALUE - (HVALUE * APPVAR /100) FROM #CMP_FREE A,
(SELECT ISIN,APPVAR=CASE WHEN APPVAR < '20' THEN '20' ELSE APPVAR END
FROM VARDETAIL WHERE DETAILKEY=REPLACE(CONVERT(VARCHAR(11),CONVERT(DATETIME,@PRV_DATE),105),'-','')) B
WHERE A.CERTNO = B.ISIN

UPDATE #CMP_FREE SET HVALUE_AFTER_VAR = HVALUE*0.9 WHERE SERIES='MF' AND HVALUE_AFTER_VAR=0

TRUNCATE TABLE TBL_ALL_CMP_FREE_HOLDING

INSERT INTO TBL_ALL_CMP_FREE_HOLDING
SELECT *, GETDATE() FROM #CMP_FREE



/* CUSA HOLDING */

SELECT REPORT_DATE=@RPT_DATE,PARTY_CODE,SCRIP_CD,SERIES,CERTNO,BCLTDPID,FREE_QTY,CLRATE=CONVERT(MONEY,0),
HVALUE=CONVERT(MONEY,0),HVALUE_AFTER_VAR=CONVERT(MONEY,0) INTO #CUSA
FROM #CMPHOLD WHERE BCLTDPID='1203320018512051' AND FREE_QTY <> 0
--AND PARTY_CODE IN (SELECT CLIENT_CODE FROM ALL_REPORT_ALLOCATION WHERE TOTAL_TDAY_MAG > 0)


/* LAST CLOSING RATE UPDATE FOR CUSA HOLDING */

UPDATE A SET CLRATE = CL_RATE FROM #CUSA A,
 (SELECT SCRIP_CD,SERIES,CL_RATE
 FROM CLOSING WITH (NOLOCK)  WHERE SysDate BETWEEN @PRV_DATE AND @PRV_DATE + ' 23:59:59') B
 WHERE A.SCRIP_CD = B.SCRIP_CD AND A.SERIES = B.SERIES

UPDATE A SET CLRATE = CL_RATE FROM #CUSA A,
(SELECT SYSDATE,SCRIP_CD,SERIES,ISIN,CL_RATE FROM AngelNSECM.MSAJAG.DBO.CLOSING_MTM B WITH (NOLOCK) WHERE SYSDATE = @PRV_DATE) B
WHERE A.CERTNO = B.ISIN AND A.SERIES = B.SERIES AND A.CLRATE = 0

UPDATE A SET CLRATE = NAV_VALUE FROM #CUSA A,
(SELECT ISIN,NAV_VALUE FROM ANGELFO.BSEMFSS.DBO.MFSS_NAV WITH (NOLOCK) WHERE NAV_DATE BETWEEN @PRV_DATE AND @PRV_DATE + ' 23:59:59') B
WHERE A.CERTNO = B.ISIN AND CLRATE=0

UPDATE A SET CLRATE = NAV_VALUE FROM #CUSA A,#HIST B WHERE A.CERTNO = B.ISIN AND A.CLRATE=0 

UPDATE #CUSA SET CLRATE = CLOPM_CDSL_RT FROM #CUSA A1,
(SELECT CLOPM_ISIN_CD,CLOPM_CDSL_RT,CLOPM_DT FROM AGMUBODPL3.DMAT.CITRUS_USR.CLOSING_LAST_CDSL A 
WHERE CLOPM_DT IN (SELECT MAX(CLOPM_DT) 
FROM AGMUBODPL3.DMAT.CITRUS_USR.CLOSING_LAST_CDSL B WHERE A.CLOPM_ISIN_CD = B.CLOPM_ISIN_CD)) A2
WHERE A1.CERTNO = A2.CLOPM_ISIN_CD AND CLRATE=0

UPDATE #CUSA SET HVALUE = FREE_QTY * CLRATE

UPDATE A SET HVALUE_AFTER_VAR = HVALUE - (HVALUE * APPVAR /100) FROM #CUSA A,
(SELECT ISIN,APPVAR=CASE WHEN APPVAR < '20' THEN '20' ELSE APPVAR END
FROM VARDETAIL WHERE DETAILKEY=REPLACE(CONVERT(VARCHAR(11),CONVERT(DATETIME,@PRV_DATE),105),'-','')) B
WHERE A.CERTNO = B.ISIN

UPDATE #CUSA SET HVALUE_AFTER_VAR = HVALUE*0.9 WHERE SERIES='MF' AND HVALUE_AFTER_VAR=0

TRUNCATE TABLE TBL_ALL_CUSA_HOLDING

INSERT INTO TBL_ALL_CUSA_HOLDING
SELECT *,GETDATE() FROM #CUSA

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=6 AND REPORT_DATE = @RPT_DATE

SELECT 'CMP & CUSA Stock Calculation Process Completed Successfully'


/*

