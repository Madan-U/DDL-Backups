-- Object: PROCEDURE dbo.PROC_CCCM_SCRIP_UPLOAD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[PROC_CCCM_SCRIP_UPLOAD] (@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), @PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = '')

AS

-- EXEC PROC_CCCM_SCRIP_UPLOAD '22/11/2021', '', '', '1234567890', ''
DECLARE @RECCNT INT,
		@FILEDATE	DATETIME,
		@EXCHANGE	VARCHAR(3),
		@SEGMENT	VARCHAR(7),
		@FILE_LEVEL VARCHAR(10),
		@MEMBERCODE	varchar(10)
		
SELECT @FILE_LEVEL = ''
SELECT @EXCHANGE = ''
SELECT @SEGMENT = ''
select @MEMBERCODE = membercode from owner 
 
IF @FILETYPE = 'ICICI NSEFO COLL' 
BEGIN
	SET @FILEDATE = CONVERT(DATETIME,@TDATE,103) 
	SELECT @FILE_LEVEL = 'CM' 
	SELECT @EXCHANGE = 'NSE' 
	SELECT @SEGMENT = 'FUTURES' 
END
ELSE
BEGIN
	SELECT @FILEDATE = CONVERT(DATETIME,LEFT(LEFT(RIGHT(@FILENAME,12),8),2) + '/' 
					 + RIGHT(LEFT(LEFT(RIGHT(@FILENAME,12),8),4),2) + '/' + LEFT(RIGHT(@FILENAME,8),4),103)				  

	SELECT @FILE_LEVEL = (CASE WHEN @FILENAME LIKE '%C_'+@MEMBERCODE +'%' THEN 'CC' ELSE 'CM' END)
	SELECT @EXCHANGE = (CASE WHEN @FILENAME LIKE '%C_'+@MEMBERCODE +'%' THEN 'NSE' ELSE '' END)
	SELECT @SEGMENT = (CASE WHEN @FILENAME LIKE '%C_'+@MEMBERCODE +'%' THEN 'CAPITAL' ELSE '' END)

	IF @EXCHANGE = '' 
	BEGIN
		SELECT @FILE_LEVEL = (CASE WHEN @FILENAME LIKE '%F_'+@MEMBERCODE +'%' THEN 'CC' ELSE 'CM' END)
		SELECT @EXCHANGE = (CASE WHEN @FILENAME LIKE '%F_'+@MEMBERCODE +'%' THEN 'NSE' ELSE '' END)
		SELECT @SEGMENT = (CASE WHEN @FILENAME LIKE '%F_'+@MEMBERCODE +'%' THEN 'FUTURES' ELSE '' END)
	END

	IF @EXCHANGE = '' 
	BEGIN 
		SELECT @FILE_LEVEL = (CASE WHEN @FILENAME LIKE '%X_'+@MEMBERCODE +'%' THEN 'CC' ELSE 'CM' END)
		SELECT @EXCHANGE = (CASE WHEN @FILENAME LIKE '%X_'+@MEMBERCODE +'%' THEN 'NSX' ELSE '' END)
		SELECT @SEGMENT = (CASE WHEN @FILENAME LIKE '%X_'+@MEMBERCODE +'%' THEN 'FUTURES' ELSE '' END)
	END

	IF @EXCHANGE = '' 
	BEGIN
		SELECT @FILE_LEVEL = (CASE WHEN @FILENAME LIKE '%S_'+@MEMBERCODE +'%' THEN 'CC' ELSE 'CM' END)
		SELECT @EXCHANGE = (CASE WHEN @FILENAME LIKE '%S_'+@MEMBERCODE +'%' THEN 'NSE' ELSE '' END)
		SELECT @SEGMENT = (CASE WHEN @FILENAME LIKE '%S_'+@MEMBERCODE +'%' THEN 'SLBS' ELSE '' END)
	END
END

IF @EXCHANGE = '' OR @FILEDATE <> CONVERT(DATETIME,@TDATE,103) 
BEGIN	
	SELECT STRMSGCODE = -1, STRMSG = 'INVALID FILE OR DATE.'
	RETURN
END

SELECT * 
INTO #TBL_CCCM_SCRIP_DATA
FROM TBL_CCCM_SCRIP_DATA
WHERE 1 = 2 

IF @FILETYPE = 'ICICI NSEFO COLL' 
BEGIN
	INSERT INTO #TBL_CCCM_SCRIP_DATA (RECORDDATE,EXCHANGE,SEGMENT,FILETYPE,
									  TMCODE,CLIENT_CODE,INSTRUMENT_TYPE,SYMBOL,
									  ISIN,SERIES,QTY,VALUATION_PRICE,
									  GROSS_VALUE,HAIRCUT,NET_VALUE,OVERALL_PER_QTY,
									  MEM_PER_QTY,NET_EFF_QTY,NET_EFF_VALUE,EXCESS_QTY,
									  EFFECTIVE_CASH,LIMIT_BASED_CASH_COM,LIMIT_BASED_TOTAL_LIQ_ASSET,FINAL_EFFECTIVE_VALUE,
									  UPLOAD_BY,UPLOAD_ON)  
	SELECT @FILEDATE, @EXCHANGE, @SEGMENT, @FILE_LEVEL,
		   .DBO.PIECE(FILE_DATA,',',1),.DBO.PIECE(FILE_DATA,',',2),.DBO.PIECE(FILE_DATA,',',3),.DBO.PIECE(FILE_DATA,',',4),
		   .DBO.PIECE(FILE_DATA,',',5),.DBO.PIECE(FILE_DATA,',',6),.DBO.PIECE(FILE_DATA,',',7),.DBO.PIECE(FILE_DATA,',',8),
		   .DBO.PIECE(FILE_DATA,',',9),.DBO.PIECE(FILE_DATA,',',10),.DBO.PIECE(FILE_DATA,',',11),
		   0, 0, .DBO.PIECE(FILE_DATA,',',12),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',13)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',13)) END),
		   0, 0, 0, 0, 
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',13)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',13)) END),
			UPLOAD_BY = @UNAME, UPLOAD_ON = GETDATE()
	FROM FILEDATA
	WHERE PROGID = @PROGID AND ISNULL(.DBO.PIECE(FILE_DATA,',',5),'')  LIKE 'IN%'
	AND ISNULL(.DBO.PIECE(FILE_DATA,',',1),'') = @MEMBERCODE
END
ELSE
BEGIN
	INSERT INTO #TBL_CCCM_SCRIP_DATA (RECORDDATE,EXCHANGE,SEGMENT,FILETYPE,
									  TMCODE,CLIENT_CODE,INSTRUMENT_TYPE,SYMBOL,
									  ISIN,SERIES,QTY,VALUATION_PRICE,
									  GROSS_VALUE,HAIRCUT,NET_VALUE,OVERALL_PER_QTY,
									  MEM_PER_QTY,NET_EFF_QTY,NET_EFF_VALUE,EXCESS_QTY,
									  EFFECTIVE_CASH,LIMIT_BASED_CASH_COM,LIMIT_BASED_TOTAL_LIQ_ASSET,FINAL_EFFECTIVE_VALUE,
									  UPLOAD_BY,UPLOAD_ON)
	SELECT @FILEDATE, @EXCHANGE, @SEGMENT, @FILE_LEVEL,
		   .DBO.PIECE(FILE_DATA,',',1),.DBO.PIECE(FILE_DATA,',',2),.DBO.PIECE(FILE_DATA,',',3),.DBO.PIECE(FILE_DATA,',',4),
		   .DBO.PIECE(FILE_DATA,',',5),.DBO.PIECE(FILE_DATA,',',6),		   
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',7)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',7)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',8)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',8)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',9)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',9)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',10)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',10)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',11)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',11)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',12)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',12)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',13)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',13)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',14)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',14)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',15)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',15)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',16)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',16)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',17)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',17)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',18)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',18)) END),
		   (CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',19)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',19)) END),
		   ISNULL((CASE WHEN CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',20)) = 'NA' THEN '0' ELSE CONVERT(VARCHAR,.DBO.PIECE(FILE_DATA,',',20)) END),0), 
			UPLOAD_BY = @UNAME, UPLOAD_ON = GETDATE()
	FROM FILEDATA
	WHERE PROGID = @PROGID AND ISNULL(.DBO.PIECE(FILE_DATA,',',5),'')  LIKE 'IN%'
	AND ISNULL(.DBO.PIECE(FILE_DATA,',',1),'') = @MEMBERCODE
END
SELECT @RECCNT = 0 
SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #TBL_CCCM_SCRIP_DATA

IF @RECCNT > 0 
BEGIN 
	DELETE FROM TBL_CCCM_SCRIP_DATA 
	WHERE RECORDDATE= @FILEDATE
	AND EXCHANGE = @EXCHANGE
	AND SEGMENT = @SEGMENT

	INSERT INTO TBL_CCCM_SCRIP_DATA
	SELECT * FROM #TBL_CCCM_SCRIP_DATA

	/*Added on Aug 07 2023 to check file upload status*/
	declare @Upload_on datetime,@Report_Date datetime,@filecount int
	set @Upload_on=(select MAX(UPLOAD_ON) from TBL_CCCM_SCRIP_DATA with (nolock))
	set @Report_Date=(select MAX(RECORDDATE) from TBL_CCCM_SCRIP_DATA with (nolock))
	
	set @filecount=(select count(1) from TBL_CCCM_SCRIP_DATA with (nolock) 
	                where RECORDDATE  =(select MAX(RECORDDATE) from TBL_CCCM_SCRIP_DATA with (nolock)))
	
	--truncate table Tbl_Pledge_CMCC_Filelog 
	insert into Tbl_Pledge_CMCC_Filelog
	select 'Pledge',@filecount,@Report_Date,@Upload_on
END

/*

DELETE FROM FILEDATA

WHERE PROGID = @PROGID

*/
SELECT STRMSGCODE = 1, STRMSG = CONVERT(VARCHAR,@RECCNT) + ' RECORD(S) UPLOADED.'

GO
