CREATE PROC [dbo].[SETT_NO_T1]
AS

DECLARE @STIME DATETIME ,@START_DATE DATETIME ,@TDATE DATETIME
SET @STIME= CONVERT(VARCHAR(11),GETDATE(),108)
IF @STIME>='16:00:00'
   BEGIN 
   SET @START_DATE=CONVERT(VARCHAR(11),GETDATE()+1,120)
   END
 ELSE
  BEGIN 
   SET @START_DATE=CONVERT(VARCHAR(11),GETDATE(),120)
   END

   SELECT @TDATE =MIN(START_DATE) FROM SETT_MST WHERE START_DATE >=@START_DATE AND SETT_TYPE ='M'

   SELECT TOP 5  SETT_NO, START_DATE AS TRADE_DATE,SEC_PAYIN PAYIN_DATE FROM SETT_MST WHERE START_DATE >=@TDATE AND START_DATE <=@TDATE+10 AND SETT_TYPE ='M'
   ORDER BY SETT_NO

GO
