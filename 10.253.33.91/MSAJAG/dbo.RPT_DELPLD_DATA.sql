-- Object: PROCEDURE dbo.RPT_DELPLD_DATA
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC RPT_DELPLD_DATA
(
	@RUNDATE VARCHAR(11),
	@DEFEXCHG VARCHAR(3)
)
AS
SELECT PARTY_CODE,SCRIP_CD=MAX(SCRIP_CD),SERIES=CONVERT(VARCHAR(3),MAX(SERIES)),BSECODE=CONVERT(VARCHAR(6),''),ISIN=CERTNO,
	   QTY=SUM(CASE WHEN DRCR = 'C' THEN QTY 
				    WHEN DRCR = 'D' AND TRANSDATE <= @RUNDATE AND DELIVERED <> '0' THEN -QTY 
					ELSE 0 END),
	   PLDQTY = 0,
	   REPLDQTY = 0,
	   CL_RATE = CONVERT(NUMERIC(18,4),0),
	   HAIRCUT = CONVERT(NUMERIC(18,4),100),
	   BDPID, BCLTDPID, BDPID1=BDPID, BCLTDPID1=BCLTDPID, BDPID2=BDPID, BCLTDPID2=BCLTDPID,
	   PSNNO1=FROMNO,
	   PSNNO2=FROMNO,
	   PSNNO3=FROMNO,
	   SEC_TYPE = 'NONCASH',
	   TM_APP_FLAG='Y',
	   CM_APP_FLAG='Y',
	   CC_APP_FLAG='Y' ,
	   --EXCHG_SEC=CONVERT(VARCHAR(50),DP.EXCHANGE+'#'+DP.SEGMENT),
	   EXCHG_SEC=CONVERT(VARCHAR(50),@DEFEXCHG+'#'+'CAPITAL'),
	   PLDEXCHG_SEC=CONVERT(VARCHAR(50),@DEFEXCHG+'#'+'CAPITAL'),
	   REPLDEXCHG_SEC=CONVERT(VARCHAR(50),@DEFEXCHG+'#'+'CAPITAL')
INTO #PLDDATA
FROM MSAJAG.DBO.DELTRANS_REPORT D, MSAJAG.DBO.DELIVERYDP DP 
WHERE D.BDPID = DP.DPID
	  AND D.BCLTDPID = DP.DPCLTNO
	  AND FILLER2 = 1 
	  AND BRANCHCD = 'PLEDGE'
	  AND QTY > 0 
	  AND TRANSDATE <= @RUNDATE  
	  AND 1 = (CASE WHEN DRCR = 'C' THEN 1 
				    WHEN DRCR = 'D' AND TRANSDATE <= @RUNDATE AND DELIVERED <> '0' THEN 1
					ELSE 0 END) 
	  AND 1 = (CASE WHEN EXCHANGE='MTF' AND SEGMENT='MTF' AND AccountType='BEN' THEN 2 ELSE 1 END)
GROUP BY PARTY_CODE, CERTNO, BDPID, BCLTDPID, FROMNO,DP.EXCHANGE,DP.SEGMENT 
having SUM(CASE WHEN DRCR = 'C' THEN QTY 
				    WHEN DRCR = 'D' AND TRANSDATE <= @RUNDATE AND DELIVERED <> '0' THEN -QTY 
					ELSE 0 END) > 0 

INSERT INTO #PLDDATA
SELECT 	   PARTY_CODE,SCRIP_CD=MAX(SCRIP_CD),SERIES=MAX(SERIES),BSECODE=CONVERT(VARCHAR(6),''),ISIN=CERTNO,
	   QTY=SUM(CRQTY-DRQTY),
	   PLDQTY = 0,
	   REPLDQTY = 0,
	   CL_RATE = CONVERT(NUMERIC(18,4),0),
	   HAIRCUT = CONVERT(NUMERIC(18,4),20),
	   BDPID, BCLTDPID, BDPID1='', BCLTDPID1='', BDPID2='', BCLTDPID2='',
	   PSNNO1='',
	   PSNNO2='',
	   PSNNO3='',
	   SEC_TYPE = 'NONCASH',
	   TM_APP_FLAG='Y',
	   CM_APP_FLAG='Y',
	   CC_APP_FLAG='Y' ,
	   EXCHG_SEC=CONVERT(VARCHAR(50),DP.EXCHANGE+'#'+DP.SEGMENT),
	   PLDEXCHG_SEC=CONVERT(VARCHAR(50),DP.EXCHANGE+'#'+DP.SEGMENT),
	   REPLDEXCHG_SEC=CONVERT(VARCHAR(50),DP.EXCHANGE+'#'+DP.SEGMENT)
--INTO #PLDDATA
FROM MSAJAG.DBO.tbl_del_hold_sec D, MSAJAG.DBO.DELIVERYDP DP 
WHERE D.BDPID = DP.DPID
	  AND D.BCLTDPID = DP.DPCLTNO 
	  AND Description like '%cusa%' 
	  AND TRANSDATE <= @RUNDATE 
	  AND PARTY_CODE NOT IN ('PARTY','BROKER','EXE','NSE','BSE')
GROUP BY PARTY_CODE, CERTNO, BDPID, BCLTDPID,DP.EXCHANGE, DP.SEGMENT
HAVING SUM(CRQTY-DRQTY) <> 0 

INSERT INTO #PLDDATA
SELECT 	   PARTY_CODE,SCRIP_CD=MAX(SCRIP_CD),SERIES=MAX(SERIES),BSECODE=CONVERT(VARCHAR(6),''),ISIN=CERTNO,
	   QTY=SUM(CRQTY-DRQTY),
	   PLDQTY = 0,
	   REPLDQTY = 0,
	   CL_RATE = CONVERT(NUMERIC(18,4),0),
	   HAIRCUT = CONVERT(NUMERIC(18,4),20),
	   BDPID, BCLTDPID, BDPID1='', BCLTDPID1='', BDPID2='', BCLTDPID2='',
	   PSNNO1='',
	   PSNNO2='',
	   PSNNO3='',
	   SEC_TYPE = 'NONCASH',
	   TM_APP_FLAG='Y',
	   CM_APP_FLAG='Y',
	   CC_APP_FLAG='Y' ,
	   --EXCHG_SEC=CONVERT(VARCHAR(50),DP.EXCHANGE+'#'+DP.SEGMENT),
	   EXCHG_SEC=CONVERT(VARCHAR(50),'NSE'+'#'+'CAPITAL'),
	   PLDEXCHG_SEC=CONVERT(VARCHAR(50),DP.EXCHANGE+'#'+DP.SEGMENT),
	   REPLDEXCHG_SEC=CONVERT(VARCHAR(50),DP.EXCHANGE+'#'+DP.SEGMENT)
--INTO #PLDDATA
FROM BSEDB.DBO.tbl_del_hold_sec D, BSEDB.DBO.DELIVERYDP DP 
WHERE D.BDPID = DP.DPID
	  AND D.BCLTDPID = DP.DPCLTNO 
	  AND Description like '%cusa%' 
	  AND TRANSDATE <= @RUNDATE 
	  AND PARTY_CODE NOT IN ('PARTY','BROKER','EXE','NSE','BSE')
GROUP BY PARTY_CODE, CERTNO, BDPID, BCLTDPID,DP.EXCHANGE, DP.SEGMENT
HAVING SUM(CRQTY-DRQTY) <> 0 

UPDATE #PLDDATA SET PLDQTY = T.QTY, BDPID1=T.BDPID, BCLTDPID1=BCLTACCNO, PSNNO2=TRANSNO,
PLDEXCHG_SEC=T.EXCHG_SEC FROM 
	(SELECT BDPID,BCLTACCNO,DPID,CLTACCNO,ISIN, TRANSNO, PARENTPSN_NO, 
	EXCHG_SEC=CONVERT(VARCHAR(50),MAX(PLEDGE_EXCHG+'#'+PLEDGE_SEG)),
		QTY = SUM(CASE WHEN DRCR = 'C' THEN QTY ELSE -QTY END) 
		    FROM MSAJAG.DBO.TBL_MARGIN_PLEDGE T	
			WHERE TRDATE <= @RUNDATE
			AND TRANSTYPE = 'MARGIN REPLEDGE'
			AND TRANSNO <> PARENTPSN_NO
	 GROUP BY BDPID,BCLTACCNO,DPID,CLTACCNO,ISIN, PARENTPSN_NO,TRANSNO ) T
WHERE #PLDDATA.ISIN = T.ISIN 
  AND #PLDDATA.PSNNO1 = T.PARENTPSN_NO
  AND #PLDDATA.BDPID = T.DPID
  AND #PLDDATA.BCLTDPID = T.CLTACCNO
   
UPDATE #PLDDATA SET PLDQTY = T.QTY, BDPID1=T.BDPID, BCLTDPID1=BCLTACCNO, PSNNO2=TRANSNO,PLDEXCHG_SEC=T.EXCHG_SEC FROM 
	(SELECT BDPID,BCLTACCNO,DPID,CLTACCNO,ISIN, TRANSNO, EXCHG_SEC=CONVERT(VARCHAR(50),MAX(PLEDGE_EXCHG+'#'+PLEDGE_SEG)),
		QTY = SUM(CASE WHEN DRCR = 'C' THEN QTY ELSE -QTY END) 
		    FROM MSAJAG.DBO.TBL_MARGIN_PLEDGE T	
			WHERE TRDATE <= @RUNDATE
			AND TRANSTYPE = 'MARGIN REPLEDGE' 
	 GROUP BY BDPID,BCLTACCNO,DPID,CLTACCNO,ISIn,TRANSNO ) T
WHERE #PLDDATA.ISIN = T.ISIN
  AND #PLDDATA.PSNNO2 = T.TRANSNO
  AND #PLDDATA.BDPID = T.DPID
  AND #PLDDATA.BCLTDPID = T.CLTACCNO
   
UPDATE #PLDDATA SET REPLDQTY = T.QTY, BDPID2=T.BDPID, BCLTDPID2=BCLTACCNO, PSNNO3=TRANSNO,REPLDEXCHG_SEC=T.EXCHG_SEC FROM 
	(SELECT BDPID,BCLTACCNO,DPID,CLTACCNO,ISIN,TRANSNO, EXCHG_SEC=CONVERT(VARCHAR(50),MAX(PLEDGE_EXCHG+'#'+PLEDGE_SEG)),
	PARENTPSN_NO, QTY = SUM(CASE WHEN DRCR = 'C' THEN QTY ELSE -QTY END) 
		    FROM MSAJAG.DBO.TBL_MARGIN_PLEDGE T	
			WHERE TRDATE <= @RUNDATE
			AND   TRANSTYPE = 'MARGIN REPLEDGE'
			AND TRANSNO <> PARENTPSN_NO
	 GROUP BY BDPID,BCLTACCNO,DPID,CLTACCNO,ISIN,TRANSNO, PARENTPSN_NO ) T
WHERE #PLDDATA.ISIN = T.ISIN
  AND #PLDDATA.PSNNO2 = T.PARENTPSN_NO
  AND #PLDDATA.BDPID1 = T.DPID
  AND #PLDDATA.BCLTDPID1 = T.CLTACCNO
   
UPDATE #PLDDATA SET REPLDQTY = T.QTY, BDPID2=T.BDPID, BCLTDPID2=BCLTACCNO, PSNNO3=TRANSNO,REPLDEXCHG_SEC=T.EXCHG_SEC FROM 
	(SELECT BDPID,BCLTACCNO,DPID,CLTACCNO,ISIN, TRANSNO, EXCHG_SEC=CONVERT(VARCHAR(50),MAX(PLEDGE_EXCHG+'#'+PLEDGE_SEG)),
		QTY = SUM(CASE WHEN DRCR = 'C' THEN QTY ELSE -QTY END) 
		    FROM MSAJAG.DBO.TBL_MARGIN_PLEDGE T	
			WHERE TRDATE <= @RUNDATE
			AND TRANSTYPE = 'MARGIN REPLEDGE' 
	 GROUP BY BDPID,BCLTACCNO,DPID,CLTACCNO,ISIN,TRANSNO ) T
WHERE #PLDDATA.ISIN = T.ISIN
  AND #PLDDATA.PSNNO3 = T.TRANSNO
  AND #PLDDATA.BDPID1 = T.DPID
  AND #PLDDATA.BCLTDPID1 = T.CLTACCNO

  SELECT * FROM #PLDDATA

GO
