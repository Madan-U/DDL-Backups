-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[CLS_CLEARSESSION](
@SPARAMETER NVARCHAR(500),
@SESSIONID NVARCHAR(50),
@UNAME NVARCHAR(50)
)

AS
BEGIN
DELETE FROM CLS_ADVANCE_FILTER_PARAMETERS
WHERE SESSIONID = @SESSIONID AND DATEDIFF(day, ADDDT,getdate()) > 1

DELETE FROM  CLS_ENTITY_FILTER_CODE
WHERE SESSIONID = @SESSIONID
END

GO
