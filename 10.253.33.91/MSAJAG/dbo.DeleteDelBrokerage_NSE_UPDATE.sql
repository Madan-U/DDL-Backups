CREATE PROC DeleteDelBrokerage_NSE_UPDATE
(
@DATE VARCHAR(100)
)

AS
DECLARE @C INT

		SELECT @C= COUNT(1) FROM DeleteDelBrokerageNseSettlementLog D WITH(NOLOCK),TBL_ADDITIONAL_BROEKRAGE  C WITH(NOLOCK)
		WHERE D.Party_Code = C.PARTY_CODE AND DEL_BROK_VAL <>100
		AND @DATE BETWEEN FROMDATE AND TODATE
		AND sauda_date BETWEEN @DATE AND @DATE + ' 23:59:59'
IF @C > 0
	BEGIN
		UPDATE C SET DEL_BROK_VAL =100 FROM DeleteDelBrokerageNseSettlementLog D WITH(NOLOCK),TBL_ADDITIONAL_BROEKRAGE  C WITH(NOLOCK)
		WHERE D.Party_Code = C.PARTY_CODE AND DEL_BROK_VAL <>100
		AND @DATE BETWEEN FROMDATE AND TODATE
		AND sauda_date BETWEEN @DATE AND @DATE + ' 23:59:59'

		END

GO
