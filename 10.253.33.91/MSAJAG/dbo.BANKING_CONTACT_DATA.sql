CREATE PROCEDURE [dbo].[BANKING_CONTACT_DATA]
(
@DATE DATE
)

 AS 
 BEGIN 
 
  EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'BANKING_CLI_DATA'   
 WAITFOR DELAY '00:0:45';  
 
 SELECT CL_CODE,SHORT_NAME,EMAIL,MOBILE_PAGER,BRANCH_CD,SUB_BROKER,REGION
  FROM CLIENT_DETAILS WITH(NOLOCK) WHERE CL_CODE IN (SELECT *FROM SSIS_PAN_NO)

END

GO
