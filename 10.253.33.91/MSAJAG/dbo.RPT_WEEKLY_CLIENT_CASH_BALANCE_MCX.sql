CREATE	PROC [dbo].[RPT_WEEKLY_CLIENT_CASH_BALANCE_MCX]
		(
		@FROMDATE	VARCHAR(11),
		@TODATE		VARCHAR(11),
		@EXCHANGE	VARCHAR(3)  = 'MCX',
		@FROMPARTY	VARCHAR(20) = '000',
		@TOPARTY	VARCHAR(20) = 'zzzzzzzzzz'
		)
		
		AS

		/*
		EXEC	RPT_WEEKLY_CLIENT_CASH_BALANCE_MCX 'MAR 16 2020','MAR 20 2020','NSE','0','ZZZZZ'
		*/

		DECLARE @FROMDT		INT,
				@TODT		INT,
				@RPTDATE	VARCHAR(11),
				@FILENAMEHS	VARCHAR(100),
				@FILENAMENH	VARCHAR(100),
				@PANNO		VARCHAR(20),
				@BATCHNO	INT,
				@FROFYMDATE	VARCHAR(11),
				@FRDATE_NEXT VARCHAR(11)
				
		SET		@FROMDT = 0
		SELECT	@TODT = DATEDIFF(DAY, @FROMDATE, @TODATE)
		SELECT	@FRDATE_NEXT = DATEADD(D, 1, @FROMDATE)

		--SELECT	@PANNO = ISNULL(LTRIM(RTRIM(PANNO)),'') FROM OWNER (NOLOCK)
		

		SET		@FROMDT = 0
		
		SELECT	@TODT = DATEDIFF(DAY, @FROMDATE, @TODATE)
		
		WHILE	@FROMDT <= @TODT
		BEGIN
				SELECT @RPTDATE = LEFT((DATEADD(D,@FROMDT,@FROMDATE)),11)		
				
				--EXEC GET_AUDIT_DATA_DAILY @RPTDATE

				EXEC SEBI_AUDIT_CLIENT_MCX_DAILY @RPTDATE
				
				EXEC SEBI_AUDIT_ISIN_DETAILS_MCDX_DAILY @RPTDATE

				SET @FROMDT = @FROMDT + 1
		END

GO
