-- Object: PROCEDURE dbo.VBB_CLIENT_SCHEME
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

  
CREATE PROC [dbo].[VBB_CLIENT_SCHEME]  
(   
 @PARTY_CODE VARCHAR(20)  
 )  
 AS  
BEGIN

 
  
SELECT CL_CODE,EXCHANGE,SEGMENT INTO #CLIENT_BROK_DETAILS FROM CLIENT_BROK_DETAILS WITH (NOLOCK)  
WHERE CL_CODE = @PARTY_CODE  
AND InActive_From >= GETDATE()  
  
CREATE INDEX #CL ON #CLIENT_BROK_DETAILS (CL_CODE)  
  
  
SELECT SP_Party_Code,SCHEME_NAME,A.EXCHANGE,A.SEGMENT FROM   
( select distinct  SP_Party_Code,SCHEME_NAME,EXCHANGE,SEGMENT  from MAPPING_TABLE WITH (NOLOCK), Scheme_Mapping  WITH (NOLOCK)  
WHERE SP_Scheme_Id in (52,53,54,55,56,57,58,59,85,86,87,91,92)  
and SP_Computation_Level = 'o' AND (TRD_SCHEME_ID = SP_Scheme_Id OR DEL_SCHEME_ID = SP_Scheme_Id)  
AND SP_DATE_TO >= GETDATE()  
AND EXCHANGE = 'NSE' AND SEGMENT ='CAPITAL'  
UNION ALL  
select distinct  SP_Party_Code,SCHEME_NAME,EXCHANGE,SEGMENT from AngelBSECM.BSEDB_AB.DBO.Vw_MAPPING_TABLE WITH (NOLOCK) , [AngelBSECM].BSEDB_AB.DBO.Scheme_Mapping WITH (NOLOCK)  
WHERE SP_Scheme_Id in (52,53,54,55,56,57,58,59,85,86,87,91,92)  
AND SP_Computation_Level = 'o' AND (TRD_SCHEME_ID = SP_Scheme_Id OR DEL_SCHEME_ID = SP_Scheme_Id)  
AND SP_DATE_TO >= GETDATE()  
AND EXCHANGE = 'BSE' AND SEGMENT ='CAPITAL'  
UNION ALL  
select distinct  SP_Party_Code,SCHEME_NAME,EXCHANGE,SEGMENT from ANGELFO.NSEFO.DBO.Vw_MAPPING_TABLE WITH (NOLOCK), ANGELFO.NSEFO.DBO.Scheme_Mapping N WITH (NOLOCK)  
 WHERE SP_Scheme_Id in (52,53,54,55,56,57,58,59,85,86,87,91,92)  
AND SP_Computation_Level = 'o' AND (TRD_SCHEME_ID = N.SP_Scheme_Id OR DEL_SCHEME_ID = N.SP_Scheme_Id)  
AND SP_DATE_TO >= GETDATE()  
AND EXCHANGE = 'NSE' AND SEGMENT ='FUTURES'  
UNION ALL  
select distinct  SP_Party_Code,SCHEME_NAME,EXCHANGE,SEGMENT from ANGELFO.NSECURFO.DBO.Vw_MAPPING_TABLE WITH (NOLOCK) , ANGELFO.NSECURFO.DBO.Scheme_Mapping WITH (NOLOCK)  
WHERE SP_Scheme_Id in (52,53,54,55,56,57,58,59,85,86,87,91,92)  
and SP_Computation_Level = 'o' AND (TRD_SCHEME_ID = SP_Scheme_Id OR DEL_SCHEME_ID = SP_Scheme_Id)  
AND SP_DATE_TO >= GETDATE()  
AND EXCHANGE = 'NSX' AND SEGMENT ='FUTURES'  
UNION ALL  
select distinct  SP_Party_Code,SCHEME_NAME,EXCHANGE,SEGMENT from ANGELCOMMODITY.BSECURFO.DBO.Vw_MAPPING_TABLE WITH (NOLOCK), ANGELCOMMODITY.BSECURFO.DBO.Scheme_Mapping  WITH (NOLOCK)  
WHERE SP_Scheme_Id in (52,53,54,55,56,57,58,59,85,86,87,91,92)  
and SP_Computation_Level = 'o' AND (TRD_SCHEME_ID = SP_Scheme_Id OR DEL_SCHEME_ID = SP_Scheme_Id)  
AND SP_DATE_TO >= GETDATE()  
AND EXCHANGE = 'BSX' AND SEGMENT ='FUTURES'  
UNION ALL  
select distinct  SP_Party_Code,SCHEME_NAME,EXCHANGE,SEGMENT from ANGELCOMMODITY.MCDX.DBO.Vw_MAPPING_TABLE WITH (NOLOCK) , ANGELCOMMODITY.MCDX.DBO.Scheme_Mapping WITH (NOLOCK)  
WHERE SP_Scheme_Id in (52,53,54,55,56,57,58,59,85,86,87,91,92)  
and SP_Computation_Level = 'o' AND (TRD_SCHEME_ID = SP_Scheme_Id OR DEL_SCHEME_ID = SP_Scheme_Id)  
AND SP_DATE_TO >= GETDATE()  
AND EXCHANGE = 'MCX' AND SEGMENT ='FUTURES'  
UNION ALL  
select distinct  SP_Party_Code,SCHEME_NAME,EXCHANGE,SEGMENT from ANGELCOMMODITY.NCDX.DBO.Vw_MAPPING_TABLE WITH (NOLOCK) , ANGELCOMMODITY.NCDX.DBO.Scheme_Mapping WITH (NOLOCK)  
WHERE SP_Scheme_Id in (52,53,54,55,56,57,58,59,85,86,87,91,92)  
and SP_Computation_Level = 'o' AND (TRD_SCHEME_ID = SP_Scheme_Id OR DEL_SCHEME_ID = SP_Scheme_Id)  
AND SP_DATE_TO >= GETDATE()  
AND EXCHANGE = 'NCX' AND SEGMENT ='FUTURES') A , #CLIENT_BROK_DETAILS B WITH (NOLOCK)  
WHERE CL_CODE =SP_Party_Code  
AND B.Exchange = A.EXCHANGE  
AND B.Segment = A.Segment  
ORDER BY SP_Party_Code   
  
END

GO
