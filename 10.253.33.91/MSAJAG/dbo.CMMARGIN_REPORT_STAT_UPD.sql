-- Object: PROCEDURE dbo.CMMARGIN_REPORT_STAT_UPD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[CMMARGIN_REPORT_STAT_UPD] 
(
	@FILEDATE VARCHAR(11),
	@USERID VARCHAR(20),
	@SESSIONID VARCHAR(30),
	@MODE INT
) AS

DECLARE @SYSTEMDATE DATETIME
DECLARE @INT_COMPUTATION_ON INT
  
SET @SYSTEMDATE = GETDATE()

IF @MODE = 1
BEGIN

	TRUNCATE TABLE CMMARGIN_REPORT_STAT_IMP

	INSERT INTO CMMARGIN_REPORT_STAT_IMP
	SELECT 
		MARGINDATE =   DBO.PIECE(FILE_DATA,',',1),
		PARTY_CODE =   DBO.PIECE(FILE_DATA,',',2),
		VARMARGIN =   DBO.PIECE(FILE_DATA,',',3),
		EXTREME_LOSS_MARGIN =   DBO.PIECE(FILE_DATA,',',4),
		ADDITIONAL_MARGIN =   DBO.PIECE(FILE_DATA,',',5),
		MTOM_LOSS =   DBO.PIECE(FILE_DATA,',',6),
		TOTAL_MARGIN_VAR =   DBO.PIECE(FILE_DATA,',',7),
		TOTAL_MARGIN =   DBO.PIECE(FILE_DATA,',',8),
		PEAKMARGIN =   DBO.PIECE(FILE_DATA,',',9),
		CL_FLAG =   DBO.PIECE(FILE_DATA,',',10),
		EOD_MARGIN_COLLECTED =   DBO.PIECE(FILE_DATA,',',11),
		PEAK_MARGIN_COLLECTED =   DBO.PIECE(FILE_DATA,',',12),
		SHORTFALL =   DBO.PIECE(FILE_DATA,',',13),
		SHORTFALL_STATUS =   DBO.PIECE(FILE_DATA,',',14),
		SHORTAGE_FLAG= ''
	FROM CLASSFILEUPD
	WHERE
		SESSION_ID= @SESSIONID
		AND CONVERT(DATETIME,DBO.PIECE(FILE_DATA,',',1)) = @FILEDATE

	UPDATE CMMARGIN_REPORT_STAT_IMP SET PEAKMARGIN = PEAKMARGIN * PEAK_PER / 100 
	FROM MSAJAG.DBO.V2_TBL_EXCHANGE_MARGIN_REPORT_PEAK
	WHERE MARGINDATE BETWEEN FROM_DATE AND TO_DATE AND EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL'

	UPDATE CMMARGIN_REPORT_STAT_IMP SET PARTY_CODE = NEWPARTY_CODE 
	FROM PARTYMAPPING (NOLOCK)
	WHERE CMMARGIN_REPORT_STAT_IMP.PARTY_CODE = OLDPARTY_CODE 
	
	/*
	DELETE CLASSFILEUPD
	WHERE SESSION_ID= @SESSIONID*/
END
ELSE
BEGIN
 
	UPDATE CMMARGIN_REPORT_STAT_IMP SET PARTY_CODE = NEWPARTY_CODE 
	FROM PARTYMAPPING (NOLOCK)
	WHERE CMMARGIN_REPORT_STAT_IMP.PARTY_CODE = OLDPARTY_CODE 

	DELETE CMMARGIN_REPORT_STAT WHERE MARGINDATE = @FILEDATE

	UPDATE CMMARGIN_REPORT_STAT_IMP SET SHORTAGE_FLAG = (CASE WHEN TOTAL_MARGIN - EOD_MARGIN_COLLECTED > PEAKMARGIN - PEAK_MARGIN_COLLECTED THEN 'E' ELSE 'P' END)
	
	INSERT INTO CMMARGIN_REPORT_STAT
	SELECT MARGINDATE,PARTY_CODE,VARMARGIN,EXTREME_LOSS_MARGIN,ADDITIONAL_MARGIN,MTOM_LOSS,TOTAL_MARGIN_VAR,TOTAL_MARGIN,PEAKMARGIN,CL_FLAG,
	EOD_MARGIN_COLLECTED,PEAK_MARGIN_COLLECTED,SHORTFALL,SHORTFALL_STATUS, @SYSTEMDATE, @USERID,SHORTAGE_FLAG
	FROM CMMARGIN_REPORT_STAT_IMP 

END

/*------------------------------ END OF PROC ------------------------*/

GO
