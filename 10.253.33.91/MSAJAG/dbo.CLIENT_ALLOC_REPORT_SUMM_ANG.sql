CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),BGAMOUNT))) AS BGAMOUNT, CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),PROPFUND))) AS PROPFUND,  
CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),TOTAL))) AS TOTAL ,
CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),PROCASH))) AS PROCASH,CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),MARGIN_SHORTFALL))) AS MARGIN_SHORTFALL,
CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),REPLEDGEBLOCK))) AS REPLEDGEBLOCK,CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),PROFUNDSHORT))) AS PROFUNDSHORT,
CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),TOTAL_PRO_REQ))) AS TOTAL_PRO_REQ,CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,0),TOTAL_FINAL))) AS TOTAL_FINAL,REMARKS,
CONVERT(VARCHAR(MAX),SUM(CONVERT(NUMERIC(18,2),MAR_UTI)))+'%' AS MAR_UTI
FROM #TBL_CMCC_DATA_P A WHERE TRANSDATE = @TRANSDATE AND ACCOUNT_TYPE = 'P' AND ALLOC_LEVEL = 'CC'  
GROUP BY TRANSDATE,EXCHANGE,SEGMENT,ACCOUNT_TYPE,ALLOC_LEVEL,REMARKS,MAR_UTI  
ORDER BY SEGMENT ASC,EXCHANGE DESC 


