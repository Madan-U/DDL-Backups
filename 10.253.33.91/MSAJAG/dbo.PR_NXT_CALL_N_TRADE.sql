AS  
BEGIN  
  
SELECT * INTO #T FROM ORDER_TRANS_JV_CALC WITH (NOLOCK)  
WHERE TRADE_DATE>=CONVERT(VARCHAR(10),GETDATE()-1,120) --AND TRADE_DATE<'2021-04-13'  
AND ORDER_TYPE ='OFFLINE'  
  
--SELECT DISTINCT  A.*,  
--ORDER_TIME,SCRIPNAME,SELL_BUY,QTY INTO #MCX FROM #T A,[AngelCommodity].MCDX.DBO.FILE_DATA_12 B  
