--AND A.ORDER_NO=B.ORDER_NO  
--AND A.PARTY_CODE=B.PARTY_CDE  
--6955  
SELECT TRADE_DATE,A.PARTY_CODE,PRODUCT_TYPE ,A.ORDER_NO, ORDER_TYPE ,A.EXCHANGE ,A.SEGMENT,SUM(QTY)QTY,MAX(ORDER_TIME)ORDER_TIME,SCRIPNAME,SELL_BUY  INTO #EQ FROM #T A,COMMON_CONTRACT_DATA B WITH (NOLOCK)  
WHERE  A.EXCHANGE = B.EXCHANGE  
AND A.SEGMENT =B.SEGMENT  
AND B.SAUDA_DATE  >=CONVERT(VARCHAR(10),GETDATE()-1,120) AND B.SAUDA_DATE<CONVERT(VARCHAR(10),GETDATE()-1,120)  
AND A.ORDER_NO=B.ORDER_NO  
AND A.PARTY_CODE=B.PARTY_CODE  
GROUP BY TRADE_DATE,A.PARTY_CODE,PRODUCT_TYPE ,A.ORDER_NO, ORDER_TYPE ,A.EXCHANGE ,A.SEGMENT,SCRIPNAME,SELL_BUY  
  
INSERT INTO #EQ  
SELECT TRADE_DATE,A.PARTY_CODE,PRODUCT_TYPE ,A.ORDER_NO, ORDER_TYPE ,A.EXCHANGE ,A.SEGMENT,SUM(QTY)QTY,MAX(ORDER_TIME)ORDER_TIME,SCRIPNAME,SELL_BUY   
 FROM #T A,[AngelCommodity].MCDX.DBO.COMMON_CONTRACT_DATA B WITH (NOLOCK)  
WHERE  A.EXCHANGE = B.EXCHANGE  
AND A.SEGMENT =B.SEGMENT  
AND B.SAUDA_DATE  >=CONVERT(VARCHAR(10),GETDATE()-1,120) AND B.SAUDA_DATE<CONVERT(VARCHAR(10),GETDATE()-1,120)  
AND A.ORDER_NO=B.ORDER_NO  
AND A.PARTY_CODE=B.PARTY_CODE  
GROUP BY TRADE_DATE,A.PARTY_CODE,PRODUCT_TYPE ,A.ORDER_NO, ORDER_TYPE ,A.EXCHANGE ,A.SEGMENT,SCRIPNAME,SELL_BUY  
  
  
  
  
SELECT * FROM #EQ  
  
