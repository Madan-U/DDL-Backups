-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROCEDURE [dbo].[CLS_CHECK_ADVANCEFILTER_APPLIED]
(@USERNAME VARCHAR(50),
@USERID VARCHAR(50),
@REPORTCODE VARCHAR(100)
)

AS
BEGIN

IF EXISTS (SELECT
		*
	FROM CLS_ADVANCEFILTER
	WHERE FLAG = 1
	AND FLDREPORTCODE = @REPORTCODE) BEGIN
SELECT
	'TRUE'
END ELSE BEGIN
SELECT
	'FALSE'
END

END

GO
