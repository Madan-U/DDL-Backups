-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[Rpt_DelNSEEarlyFile_BuyBack] (          
@TRANSDATE VARCHAR(11),            
@FROMBATCHNO INT,          
@TOBATCHNO INT,          
@FROMSLIPNO VARCHAR(10),          
@TOSLIPNO VARCHAR(10) ) AS          

SELECT RECTYPE = 20, SETT_NO, SETT_TYPE, SCRIP_CD = UPPER(SCRIP_CD),     
SERIES = UPPER(SERIES), PARTY_CODE, QTY = SUM(QTY)      
INTO #DELTRANSEPN   
FROM DELTRANS D        
WHERE FILLER2 = 1 AND DRCR = 'C' AND CERTNO LIKE 'IN%'        
AND TCODE IN (SELECT TCODE FROM DELTRANS D1        
WHERE D.SETT_NO = D1.SETT_NO
AND D.SETT_TYPE = D1.SETT_TYPE
AND FILLER2 = 1 AND DRCR = 'D' AND TRTYPE = 906 AND DELIVERED <> '0'        
AND ( REASON LIKE 'EARLY%' OR FILLER1 LIKE 'EARLY%' ) AND D.CERTNO = D1.CERTNO        
AND CONVERT(INT,BATCHNO) >= @FROMBATCHNO        
AND CONVERT(INT,BATCHNO) <= @TOBATCHNO        
AND SLIPNO >= @FROMSLIPNO        
AND SLIPNO <= @TOSLIPNO       
AND TRANSDATE LIKE @TRANSDATE + '%')
AND D.SETT_TYPE = 'BX'
GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE

INSERT INTO #DELTRANSEPN
SELECT RECTYPE = 20, ISETT_NO, ISETT_TYPE, SCRIP_CD = UPPER(SCRIP_CD),     
SERIES = UPPER(SERIES), PARTY_CODE, QTY = SUM(QTY) 
FROM DELTRANS 
WHERE CONVERT(INT,BATCHNO) >= @FROMBATCHNO        
AND CONVERT(INT,BATCHNO) <= @TOBATCHNO        
AND SLIPNO >= @FROMSLIPNO        
AND SLIPNO <= @TOSLIPNO       
AND TRANSDATE LIKE @TRANSDATE + '%'
AND FILLER2 = 1 AND DRCR = 'D' AND TRTYPE = 1000 
AND DELIVERED <> '0'        
AND ( REASON LIKE 'EARLY%' OR FILLER1 LIKE 'EARLY%' OR HOLDERNAME = 'EARLY PAYIN')
AND SETT_TYPE = 'BX'
GROUP BY ISETT_NO, ISETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE

SELECT FileName = 'CLNTEPI_X_', RECTYPE, SETT_NO, SETT_TYPE = 'X', SCRIP_CD, SERIES, PARTY_CODE, QTY = SUM(QTY)   
FROM #DELTRANSEPN  
GROUP BY RECTYPE, SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE  
ORDER BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, PARTY_CODE

GO
