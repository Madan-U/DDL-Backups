-- Object: PROCEDURE dbo.CLS_HOLDINGSTATEMENT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROCEDURE [dbo].[CLS_HOLDINGSTATEMENT] 
(
	@STATUSID   VARCHAR(25),
	@STATUSNAME VARCHAR(25),
	@FROMPARTY  VARCHAR(20),
	@TOPARTY	VARCHAR(20),
	@FROMSCRIP	VARCHAR(20),
	@TOSCRIP	VARCHAR(20),
	@BDPID		VARCHAR(20),
	@BCLTDPID	VARCHAR(20),
	@BRANCH		VARCHAR(20),
	@FLAG		VARCHAR(1),
	@REPORTTYPE VARCHAR(1),
	@OBLIGATIONFLAG VARCHAR(1)
	)
AS

--Exec CLS_HOLDINGSTATEMENT 'BROKER','BROKER','00','zzzzzzz','00','zzzzz','12033200','1203320000062556','%','P','1','1'
-- Exec CLS_HOLDINGSTATEMENT 'BROKER','BROKER','00','zzzzz','000','zzzzz','12033200','1203320000062556','%','S','1','1'
--Exec CLS_HOLDINGSTATEMENT_DETAIL 'BROKER','BROKER','0A141','0A141','500389','BSE','12033200','1203320000062556','2007097','D'

SET @BRANCH = (
		CASE 
			WHEN @BRANCH = ''
				THEN '%'
			ELSE @BRANCH
			END
		)
SET @BDPID = (
		CASE 
			WHEN @BDPID = ''
				THEN '%'
			ELSE @BDPID
			END
		)
SET @BCLTDPID = (
		CASE 
			WHEN @BCLTDPID = ''
				THEN '%'
			ELSE @BCLTDPID
			END
		)
SET @FROMPARTY = (
		CASE 
			WHEN @FROMPARTY = ''
				THEN '0'
			ELSE @FROMPARTY
			END
		)
SET @TOPARTY = (
		CASE 
			WHEN @TOPARTY = ''
				THEN 'ZZZZZZZZZZ'
			ELSE @TOPARTY
			END
		)
SET @FROMSCRIP = (
		CASE 
			WHEN @FROMSCRIP = ''
				THEN '0'
			ELSE @FROMSCRIP
			END
		)
SET @TOSCRIP = (
		CASE 
			WHEN @TOSCRIP = ''
				THEN 'ZZZZZZZZZZ'
			ELSE @TOSCRIP
			END
		)



BEGIN
IF @FLAG = 'P'
	BEGIN
	
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	SELECT 
		D.PARTY_CODE,
		PARTYNAME=C1.LONG_NAME,
		D.SCRIP_CD, 
		D.SERIES ,
		ISIN=CERTNO,
		DPID=BDPID,
		CLIENTID=BCLTDPID,
		SETTNO=SETT_NO, 
		SETTTYPE=SETT_TYPE, 
		FREEQTY=CAST(SUM(CASE WHEN TRTYPE <> 909 THEN QTY ELSE 0 END) AS decimal(18,0)) ,   
		PLEDGEQTY=CAST(SUM(CASE WHEN TRTYPE = 909 THEN QTY ELSE 0 END) AS decimal(18,0)),
		BALANCEQTY=CAST(SUM(QTY) AS decimal(18,0)),
		TOTALQTY=CAST(SUM(QTY) AS decimal(18,0))
	FROM 
		DELTRANS D (NOLOCK), 
		CLIENT1 C1(NOLOCK), 
		CLIENT2 C2 (NOLOCK) ,
		MULTIISIN MT
	WHERE 
		BDPID LIKE @BDPID+'%' AND BCLTDPID LIKE @BCLTDPID+'%' AND C1.CL_CODE = C2.CL_CODE AND D.PARTY_CODE = C2.PARTY_CODE
		AND D.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		AND D.SCRIP_CD BETWEEN @FROMSCRIP AND @TOSCRIP AND DRCR = 'D' AND FILLER2 = 1  AND DELIVERED = '0'  AND TRTYPE <> 907 
		AND D.PARTY_CODE <> 'BROKER' AND TRTYPE <> 906 AND CERTNO NOT LIKE 'AUCTION'
		AND C1.BRANCH_CD LIKE (CASE WHEN @STATUSID = 'BRANCH' THEN @STATUSNAME ELSE '%' END )
		AND C1.SUB_BROKER LIKE (CASE WHEN @STATUSID = 'SUBBROKER' THEN @STATUSNAME ELSE '%' END )
		AND C1.TRADER LIKE (CASE WHEN @STATUSID = 'TRADER' THEN @STATUSNAME ELSE '%' END )
		AND C1.FAMILY LIKE (CASE WHEN @STATUSID = 'FAMILY' THEN @STATUSNAME ELSE '%' END )
		AND C2.PARTY_CODE LIKE (CASE WHEN @STATUSID = 'CLIENT' THEN @STATUSNAME ELSE '%' END )
		AND C1.BRANCH_CD LIKE @BRANCH
		AND D.CERTNO=ISIN
		AND VALID=1
	GROUP BY 
		D.SCRIP_CD,CERTNO, D.SERIES ,D.PARTY_CODE,C1.LONG_NAME, SETT_NO, SETT_TYPE,BDPID,BCLTDPID 
	HAVING SUM(QTY) > 0 
	ORDER BY 
		D.PARTY_CODE,SCRIP_CD,SERIES,SETT_NO, SETT_TYPE

	END
ELSE
	BEGIN
	SELECT 
		SCRIP_CD, 
		SERIES ,
		ISIN=CERTNO,
		D.PARTY_CODE,
		PARTYNAME=C1.LONG_NAME,
		DPID=BDPID,
		CLIENTID=BCLTDPID,
		SETTNO=SETT_NO, 
		SETTTYPE=SETT_TYPE, 
		FREEQTY=CAST(SUM(CASE WHEN TRTYPE <> 909 THEN QTY ELSE 0 END) AS decimal(18,0)) ,   
		PLEDGEQTY=CAST(SUM(CASE WHEN TRTYPE = 909 THEN QTY ELSE 0 END) AS decimal(18,0)),
		BALANCEQTY=CAST(SUM(QTY) AS decimal(18,0)),
		TOTALQTY=CAST(SUM(QTY) AS decimal(18,0))

	FROM 
		DELTRANS D, CLIENT1 C1, CLIENT2 C2
	WHERE 
		BDPID LIKE @BDPID+'%' AND BCLTDPID LIKE @BCLTDPID+'%' AND C1.CL_CODE = C2.CL_CODE AND D.PARTY_CODE = C2.PARTY_CODE
		AND D.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		AND SCRIP_CD BETWEEN @FROMSCRIP AND @TOSCRIP AND DRCR = 'D' AND FILLER2 = 1  AND DELIVERED = '0'  AND TRTYPE <> 907 
		AND D.PARTY_CODE <> 'BROKER' AND TRTYPE <> 906 AND CERTNO NOT LIKE 'AUCTION'
		AND C1.BRANCH_CD LIKE (CASE WHEN @STATUSID = 'BRANCH' THEN @STATUSNAME ELSE '%' END )
		AND C1.SUB_BROKER LIKE (CASE WHEN @STATUSID = 'SUBBROKER' THEN @STATUSNAME ELSE '%' END )
		AND C1.TRADER LIKE (CASE WHEN @STATUSID = 'TRADER' THEN @STATUSNAME ELSE '%' END )
		AND C1.FAMILY LIKE (CASE WHEN @STATUSID = 'FAMILY' THEN @STATUSNAME ELSE '%' END )
		AND C2.PARTY_CODE LIKE (CASE WHEN @STATUSID = 'CLIENT' THEN @STATUSNAME ELSE '%' END )
		AND C1.BRANCH_CD LIKE @BRANCH
	GROUP BY 
		SCRIP_CD,CERTNO, SERIES ,D.PARTY_CODE,C1.LONG_NAME, SETT_NO, SETT_TYPE,BDPID,BCLTDPID 
	HAVING SUM(QTY) > 0 
	ORDER BY 
		SCRIP_CD,SERIES,D.PARTY_CODE,SETT_NO, SETT_TYPE

	
	END 
END

GO
