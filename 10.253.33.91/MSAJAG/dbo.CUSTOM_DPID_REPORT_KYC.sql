--------------------------------------------------


CREATE PROCEDURE [dbo].[CUSTOM_DPID_REPORT_KYC] (@RUNDATE VARCHAR(11))

AS --- EXEC CUSTOM_DPID_REPORT_KYC 'MAR 11 2022'

SELECT DEMAT_CODE=CLIENT_CODE,TRADING_CODE=NISE_PARTY_CODE,
BRANCH_CODE=CONVERT(VARCHAR(50),''),SUB_BROKER=CONVERT(VARCHAR(50),''),BTB_BTC=CONVERT(VARCHAR(10),''),FIRST_HOLD_PAN,
DEMAT_ACTIVE_DATE=ACTIVE_DATE,DEMAT_INACTIVE_DATE=CLOSURE_DATE,DP_STATUS=STATUS,DEMAT_FREEZE_REMARK=CONVERT(VARCHAR(10),''),
LAST_TRADE_DATE=CONVERT(VARCHAR(10),''),DP_HOLDING=CONVERT(VARCHAR(10),'No'),MOBILE=FIRST_HOLD_PHONE,EMAIL_ADD INTO #DATA
FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER WITH (NOLOCK) WHERE CLIENT_CODE IN (SELECT * FROM IMPORT_PARTY_CODE)

INSERT INTO #DATA
SELECT DEMAT_CODE=CLIENT_CODE,TRADING_CODE=NISE_PARTY_CODE,
BRANCH_CODE=CONVERT(VARCHAR(50),''),SUB_BROKER=CONVERT(VARCHAR(50),''),BTB_BTC=CONVERT(VARCHAR(10),''),FIRST_HOLD_PAN,
DEMAT_ACTIVE_DATE=ACTIVE_DATE,DEMAT_INACTIVE_DATE=CLOSURE_DATE,DP_STATUS=STATUS,DEMAT_FREEZE_REMARK=CONVERT(VARCHAR(10),''),
LAST_TRADE_DATE=CONVERT(VARCHAR(10),''),DP_HOLDING=CONVERT(VARCHAR(10),'No'),MOBILE=FIRST_HOLD_PHONE,EMAIL_ADD
FROM AngelDP5.DMAT.CITRUS_USR.TBL_CLIENT_MASTER WITH (NOLOCK) WHERE CLIENT_CODE IN (SELECT * FROM IMPORT_PARTY_CODE)

UPDATE #DATA SET BRANCH_CODE = BC,SUB_BROKER=SB,BTB_BTC=B2B_B2C,LAST_TRADE_DATE=LTD FROM #DATA A,
(SELECT CL_CODE,BC=B.BRANCH_CD,SB=B.SUB_BROKER,(CASE WHEN B2C='Y'THEN 'B2C'ELSE 'B2B'END)AS B2B_B2C,LTD=ISNULL(CONVERT(VARCHAR(11),B.COMB_LAST_DATE,105),'')
FROM INTRANET.RISK.DBO.CLIENT_DETAILS B WITH (NOLOCK)) B WHERE A.TRADING_CODE = B.CL_CODE

UPDATE #DATA SET DEMAT_FREEZE_REMARK = STATUS_FREE FROM #DATA A,
(SELECT  CLIENT_CODE=DPAM_SBA_NO,(CASE WHEN FRE_ACTION = 'F' THEN 'FREEZED' ELSE '' END ) STATUS_FREE
FROM AGMUBODPL3.DMAT.CITRUS_USR.DP_ACCT_MSTR A,AGMUBODPL3.DMAT.CITRUS_USR.FREEZE_UNFREEZE_DTLS  B
WHERE A.DPAM_ID =B.FRE_DPAM_ID AND
B.FRE_ID IN (SELECT MAX(FRE_ID) FROM AGMUBODPL3.DMAT.CITRUS_USR.FREEZE_UNFREEZE_DTLS B1 WHERE B.FRE_DPAM_ID = B1.FRE_DPAM_ID)) B
WHERE A.DEMAT_CODE = B.CLIENT_CODE

UPDATE #DATA SET DEMAT_FREEZE_REMARK = STATUS_FREE FROM #DATA A,
(SELECT  CLIENT_CODE=DPAM_SBA_NO,(CASE WHEN FRE_ACTION = 'F' THEN 'FREEZED' ELSE '' END ) STATUS_FREE
FROM AngelDP5.DMAT.CITRUS_USR.DP_ACCT_MSTR A,AngelDP5.DMAT.CITRUS_USR.FREEZE_UNFREEZE_DTLS  B
WHERE A.DPAM_ID =B.FRE_DPAM_ID AND
B.FRE_ID IN (SELECT MAX(FRE_ID) FROM AngelDP5.DMAT.CITRUS_USR.FREEZE_UNFREEZE_DTLS B1 WHERE B.FRE_DPAM_ID = B1.FRE_DPAM_ID)) B
WHERE A.DEMAT_CODE = B.CLIENT_CODE

UPDATE #DATA SET DP_HOLDING = DP FROM #DATA A,
(SELECT HLD_AC_CODE,DP='Yes' FROM AGMUBODPL3.DMAT.CITRUS_USR.HOLDING WHERE NETQTY<>0) B
WHERE A.DEMAT_CODE = B.HLD_AC_CODE

UPDATE #DATA SET DP_HOLDING = DP FROM #DATA A,
(SELECT HLD_AC_CODE,DP='Yes' FROM AngelDP5.DMAT.CITRUS_USR.HOLDING WHERE NETQTY<>0) B
WHERE A.DEMAT_CODE = B.HLD_AC_CODE

UPDATE #DATA SET LAST_TRADE_DATE = CONVERT(VARCHAR(11),VDT,105) FROM #DATA A,
(SELECT CLTCODE,VDT=MAX(VDT) FROM ACCOUNT.DBO.LEDGER_ALL WITH (NOLOCK) WHERE VTYP='15' GROUP BY CLTCODE) B
WHERE A.TRADING_CODE = B.CLTCODE

SELECT DEMAT_CODE=''''+DEMAT_CODE,TRADING_CODE,BRANCH_CODE,SUB_BROKER,BTB_BTC,FIRST_HOLD_PAN,DEMAT_ACTIVE_DATE,DEMAT_INACTIVE_DATE,DP_STATUS,DEMAT_FREEZE_REMARK,
LAST_TRADE_DATE,DP_HOLDING,MOBILE,EMAIL_ADD FROM #DATA ORDER BY 1

GO
