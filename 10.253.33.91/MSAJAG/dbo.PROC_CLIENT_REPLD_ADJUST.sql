-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[PROC_CLIENT_REPLD_ADJUST]  
(  
 @RUNDATE VARCHAR(11)  
)  
AS  
  
-- EXEC PROC_CLIENT_REPLD_ADJUST 'APR 18 2022'  
  
DECLARE @PLDDATE VARCHAR(11)  
  
SELECT @PLDDATE = ISNULL(MAX(PROCESS_DATE),@RUNDATE) FROM TBL_NONCASH_REPORT T WHERE PROCESS_DATE <= @RUNDATE  
  
SELECT PARTY_CODE, EXCHANGE, SEGMENT, COLL_EXC_SHORT=CONVERT(NUMERIC(18,2),EDT_LEDGER+LIEU_EPI_LEDGER_CLTM+POOL_ACCOUNT_CLTM+BG_REC_TM+FD_REC_TM+APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM) - UPFRONT_MARGIN   
INTO #SHORT FROM CLIENT_COLLATERAL_REPORTING_DETAILS  
WHERE PROCESS_DT=@RUNDATE  
AND CONVERT(NUMERIC(18,2),EDT_LEDGER+LIEU_EPI_LEDGER_CLTM+POOL_ACCOUNT_CLTM+BG_REC_TM+FD_REC_TM+APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM) - UPFRONT_MARGIN < 0   
AND UPFRONT_MARGIN > 0   

CREATE CLUSTERED INDEX IDXPARTY ON #SHORT 
(
	PARTY_CODE
)  

SELECT PARTY_CODE, EXCHANGE, SEGMENT, COLL_EXC_SHORT=CONVERT(NUMERIC(18,2),EDT_LEDGER+LIEU_EPI_LEDGER_CLTM+POOL_ACCOUNT_CLTM+BG_REC_TM+FD_REC_TM+APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM) - UPFRONT_MARGIN    
INTO #EXCESS FROM CLIENT_COLLATERAL_REPORTING_DETAILS  
WHERE PROCESS_DT=@RUNDATE  
AND CONVERT(NUMERIC(18,2),EDT_LEDGER+LIEU_EPI_LEDGER_CLTM+POOL_ACCOUNT_CLTM+BG_REC_TM+FD_REC_TM+APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM) - UPFRONT_MARGIN > 0   
AND EXISTS (SELECT PARTY_CODE FROM #SHORT WHERE #SHORT.PARTY_CODE = CLIENT_COLLATERAL_REPORTING_DETAILS.PARTY_CODE)  
  
DELETE FROM #EXCESS WHERE PARTY_CODE NOT IN (SELECT PARTY_CODE FROM #SHORT)  
DELETE FROM #SHORT WHERE PARTY_CODE NOT IN (SELECT PARTY_CODE FROM #EXCESS)  
  
SELECT PARTY_CODE, EXCHANGE=LEFT(PLDEXCHG_SEC,3), SEGMENT=RIGHT(PLDEXCHG_SEC,LEN(PLDEXCHG_SEC)-4),   
SCRIP_CD,ISIN, PLDQTY, PSNNO2, AMT=PLDQTY*CL_RATE-PLDQTY*CL_RATE*HAIRCUT/100,  
ALLOCATEQTY=CONVERT(numeric(18,3),0),  
ALLOCATEAMT=CONVERT(numeric(18,3),0)  
INTO #PLDPSNDATA  
    FROM TBL_NONCASH_REPORT T WHERE PROCESS_DATE=@PLDDATE AND PSNNO2 <> ''  
    AND PARTY_CODE IN (SELECT PARTY_CODE FROM #EXCESS A  
    WHERE T.PARTY_CODE = A.PARTY_CODE AND T.PLDEXCHG_SEC = A.EXCHANGE+'#'+A.SEGMENT)  
      
SELECT PARTY_CODE, EXCHANGE=LEFT(PLDEXCHG_SEC,3), SEGMENT=RIGHT(PLDEXCHG_SEC,LEN(PLDEXCHG_SEC)-4),   
SCRIP_CD,ISIN, PLDQTY,  
PLDAMT=CONVERT(numeric(18,3),0),   
PSNNO2, NEWEXCHANGE=RIGHT(PLDEXCHG_SEC,3), NEWSEGMENT=RIGHT(PLDEXCHG_SEC,LEN(PLDEXCHG_SEC)-4)  
INTO #PLDPSNDATA_ADJUST  
    FROM TBL_NONCASH_REPORT T WHERE 1 = 2   
  
ALTER TABLE  #PLDPSNDATA  
ADD SNO INT IDENTITY(1,1)   
  
CREATE CLUSTERED INDEX IDXSNO ON #PLDPSNDATA  
(  
 SNO  
)  
  
CREATE NONCLUSTERED INDEX IDXPARTY ON #PLDPSNDATA  
(  
 PARTY_CODE, EXCHANGE, SEGMENT  
)  
  
CREATE CLUSTERED INDEX IDXSNO ON #EXCESS  
(  
 PARTY_CODE  
)  
   
DECLARE @PARTY_CODE VARCHAR(10),  
  @EXCESS_EXC VARCHAR(3),  
  @EXCESS_SEG VARCHAR(7),  
  @SHORT_EXC VARCHAR(3),  
  @SHORT_SEG VARCHAR(7),  
  @EXCESSAMT NUMERIC(18,4),  
  @SHORTAMT NUMERIC(18,4),  
  @ALLOCAMT NUMERIC(18,4),  
  @EXCCUR  CURSOR,  
  @SHORTCUR CURSOR,  
  @REPLDCUR CURSOR,  
  @SNO  INT,   
  @ISIN  VARCHAR(12),   
  @SCRIP_CD VARCHAR(12),   
  @PLDQTY  NUMERIC(18,3),  
  @NEWPLDQTY  NUMERIC(18,3),  
  @PSNNO  VARCHAR(20),   
  @PLDAMT  NUMERIC(18,4)  
     
SET @EXCCUR = CURSOR FOR   
SELECT PARTY_CODE, EXCHANGE, SEGMENT, COLL_EXC_SHORT FROM #EXCESS   
WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #SHORT WHERE #EXCESS.PARTY_CODE = #SHORT.PARTY_CODE  )   
ORDER BY PARTY_CODE , EXCHANGE, SEGMENT   
OPEN @EXCCUR  
FETCH NEXT FROM @EXCCUR INTO @PARTY_CODE, @EXCESS_EXC, @EXCESS_SEG, @EXCESSAMT   
WHILE @@FETCH_STATUS = 0   
BEGIN  
 IF @EXCESSAMT > 0   
 BEGIN  
  SET @SHORTCUR = CURSOR FOR  
  SELECT EXCHANGE, SEGMENT,  COLL_EXC_SHORT = ABS(COLL_EXC_SHORT) FROM #SHORT   
  WHERE PARTY_CODE = @PARTY_CODE   
  ORDER BY COLL_EXC_SHORT ASC   
  OPEN @SHORTCUR  
  FETCH NEXT FROM @SHORTCUR INTO @SHORT_EXC, @SHORT_SEG, @SHORTAMT   
  WHILE @@FETCH_STATUS = 0   
  BEGIN  
   SELECT @ALLOCAMT = (CASE WHEN @EXCESSAMT > @SHORTAMT THEN @SHORTAMT ELSE @EXCESSAMT END)  
   IF @SHORTAMT > 0 AND @EXCESSAMT > 0   
   BEGIN   
    SET @REPLDCUR = CURSOR FOR   
    SELECT SNO,SCRIP_CD,ISIN, PLDQTY=PLDQTY-ALLOCATEQTY, PSNNO2, AMT=(AMT/PLDQTY)*(PLDQTY-ALLOCATEQTY)  
    FROM #PLDPSNDATA WHERE PARTY_CODE=@PARTY_CODE AND EXCHANGE=@EXCESS_EXC  
    AND SEGMENT = @EXCESS_SEG  
    AND PSNNO2 <> ''  
    AND (PLDQTY-ALLOCATEQTY) > 0   
    ORDER BY 6 DESC  
    OPEN @REPLDCUR  
    FETCH NEXT FROM @REPLDCUR INTO @SNO,@SCRIP_CD,@ISIN, @PLDQTY, @PSNNO, @PLDAMT  
    WHILE @@FETCH_STATUS = 0   
    BEGIN    
     IF @ALLOCAMT > 0  
     BEGIN      
      IF @ALLOCAMT > @PLDAMT   
      BEGIN  
       UPDATE #PLDPSNDATA SET ALLOCATEQTY = ALLOCATEQTY+@PLDQTY, ALLOCATEAMT=ALLOCATEAMT+@PLDAMT WHERE SNO = @SNO   
       INSERT INTO #PLDPSNDATA_ADJUST  
       SELECT @PARTY_CODE, @EXCESS_EXC, @EXCESS_SEG, @SCRIP_CD,@ISIN, @PLDQTY,@PLDAMT, @PSNNO, @SHORT_EXC,@SHORT_SEG  
       SELECT @ALLOCAMT = @ALLOCAMT - @PLDAMT  
       SELECT @EXCESSAMT = @EXCESSAMT - @PLDAMT  
      END  
      ELSE  
      BEGIN   
       SET @NEWPLDQTY = FLOOR(@ALLOCAMT / (@PLDAMT/@PLDQTY))  
       IF @NEWPLDQTY > @PLDAMT   
       BEGIN  
        SET @NEWPLDQTY = @PLDQTY   
       END  
       IF @NEWPLDQTY > 0   
       BEGIN  
        UPDATE #PLDPSNDATA SET ALLOCATEQTY = ALLOCATEQTY+@NEWPLDQTY, ALLOCATEAMT=ALLOCATEAMT+@NEWPLDQTY*(@PLDAMT/@PLDQTY) WHERE SNO = @SNO   
        INSERT INTO #PLDPSNDATA_ADJUST  
        SELECT @PARTY_CODE, @EXCESS_EXC, @EXCESS_SEG,@SCRIP_CD, @ISIN, @NEWPLDQTY,@NEWPLDQTY*(@PLDAMT/@PLDQTY), @PSNNO, @SHORT_EXC,@SHORT_SEG  
        SELECT @ALLOCAMT = @ALLOCAMT - @NEWPLDQTY*(@PLDAMT/@PLDQTY)  
        SELECT @EXCESSAMT = @EXCESSAMT - @NEWPLDQTY*(@PLDAMT/@PLDQTY)  
       END  
      END  
     END  
     FETCH NEXT FROM @REPLDCUR INTO @SNO, @SCRIP_CD,@ISIN, @PLDQTY, @PSNNO, @PLDAMT  
    END  
    CLOSE @REPLDCUR  
    DEALLOCATE @REPLDCUR  
   END  
   FETCH NEXT FROM @SHORTCUR INTO @SHORT_EXC, @SHORT_SEG, @SHORTAMT   
  END  
  CLOSE @SHORTCUR   
  DEALLOCATE @SHORTCUR  
 END  
 FETCH NEXT FROM @EXCCUR INTO @PARTY_CODE, @EXCESS_EXC, @EXCESS_SEG, @EXCESSAMT   
END  
CLOSE @EXCCUR  
DEALLOCATE @EXCCUR  
  
SELECT FILENAME='EXCESS' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','') + '.CSV', RECTYPE='EXCESS', * FROM #EXCESS   
--where  PARTY_CODE='CHND5062'  
ORDER BY PARTY_CODE   
  
SELECT FILENAME='SHORT' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','') + '.CSV', RECTYPE='SHORT',* FROM #SHORT   
--where  PARTY_CODE='CHND5062'  
ORDER BY PARTY_CODE   
  
SELECT FILENAME='TRNF' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','') + '.CSV', RECTYPE='TRNF',* FROM #PLDPSNDATA_ADJUST  
--where  PARTY_CODE='CHND5062'  
ORDER BY PARTY_CODE   
  
SELECT FILENAME='ORG' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','') + '.CSV', RECTYPE='ORG',* FROM #PLDPSNDATA   
--where  PARTY_CODE='CHND5062'  
ORDER BY PARTY_CODE   
  
ALTER TABLE #PLDPSNDATA_ADJUST  
ADD SEG_TYPE VARCHAR(1)  
ALTER TABLE #PLDPSNDATA_ADJUST  
ADD TRN_SEG_TYPE VARCHAR(1)  
  
UPDATE #PLDPSNDATA_ADJUST SET SEG_TYPE = (CASE WHEN EXCHANGE = 'NSE' AND SEGMENT = 'FUTURES' THEN 'F'  
WHEN EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL'  THEN 'C'   
WHEN EXCHANGE = 'NSE' AND SEGMENT = 'SLBS'  THEN 'SB'   
WHEN EXCHANGE = 'NSX' AND SEGMENT = 'FUTURES'  THEN 'X' ELSE 'C' END)  
  
UPDATE #PLDPSNDATA_ADJUST SET TRN_SEG_TYPE = (CASE WHEN NEWEXCHANGE = 'NSE' AND NEWSEGMENT = 'FUTURES' THEN 'F'  
WHEN NEWEXCHANGE = 'NSE' AND NEWSEGMENT = 'CAPITAL'  THEN 'C'   
WHEN NEWEXCHANGE = 'NSE' AND NEWSEGMENT = 'SLBS'  THEN 'SB'   
WHEN NEWEXCHANGE = 'NSX' AND NEWSEGMENT = 'FUTURES'  THEN 'X' ELSE 'C' END)  
  
  
DECLARE @SEG_TYPE VARCHAR(2),  
  @FILENAME VARCHAR(100),  
  @FILETYPE VARCHAR(3),  
  @CMID VARCHAR(16),  
  @ACC_TYPE VARCHAR(4),   
  @BATCHNO INT,  
  @FILECUR  CURSOR  
     
  
SET @FILECUR = CURSOR FOR  
SELECT DISTINCT SEG_TYPE FROM #PLDPSNDATA_ADJUST   
WHERE EXCHANGE='NSE' AND NEWEXCHANGE IN ('NSE','NSX','NCE')  
ORDER BY 1  
OPEN @FILECUR   
FETCH NEXT FROM @FILECUR INTO @SEG_TYPE   
WHILE @@FETCH_STATUS = 0   
BEGIN   
 SELECT @BATCHNO = 0      
      
 SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)   
 FROM ASIANFILEBATCHNO      
 WHERE FLDFILENO = @SEG_TYPE+'T'     
 AND FLDFILEDATE = @RUNDATE    
   
 SET @BATCHNO = @BATCHNO + 1   
  
 INSERT INTO ASIANFILEBATCHNO      
 SELECT @SEG_TYPE+'T', @BATCHNO, @RUNDATE, GETDATE()        
  
 SET @FILENAME = @SEG_TYPE + '_TRG_' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','')   
 + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'  
    
 SELECT FILENAME= @FILENAME, SEG_TYPE, MemberCode,COLL_TYPE= 'ABC',SEC_DEP='SDP',INSTRUMENT_ID='',   
 CUSTODIAN='CDSL',SCRIP_CD,PLDQTY=SUM(PLDQTY),AMT='NA',ISIN,TMCODE=MemberCode,TRADING_ID=PARTY_CODE,  
 BANK_CODE='NA',Fungible_Flag='Y',Destination_segment=TRN_SEG_TYPE  
 FROM #PLDPSNDATA_ADJUST, OWNER WHERE SEG_TYPE = @SEG_TYPE AND NEWEXCHANGE IN ('NSE','NSX','NCE')   
 GROUP BY SEG_TYPE, MemberCode, SCRIP_CD, ISIN, PARTY_CODE,TRN_SEG_TYPE  
 ORDER BY PARTY_CODE, SCRIP_CD  
  
 FETCH NEXT FROM @FILECUR INTO @SEG_TYPE   
END  
CLOSE @FILECUR  
DEALLOCATE @FILECUR  
  
  
SET @FILECUR = CURSOR FOR  
SELECT DISTINCT SEG_TYPE FROM #PLDPSNDATA_ADJUST   
WHERE EXCHANGE='NSE' AND NEWEXCHANGE NOT IN ('NSE','NSX','NCE')  
ORDER BY 1  
OPEN @FILECUR   
FETCH NEXT FROM @FILECUR INTO @SEG_TYPE   
WHILE @@FETCH_STATUS = 0   
BEGIN   
 SELECT @BATCHNO = 0      
      
 SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)   
 FROM ASIANFILEBATCHNO      
 WHERE FLDFILENO = @SEG_TYPE+'R'     
 AND FLDFILEDATE = @RUNDATE    
   
 SET @BATCHNO = @BATCHNO + 1   
  
 INSERT INTO ASIANFILEBATCHNO      
 SELECT @SEG_TYPE+'R', @BATCHNO, @RUNDATE, GETDATE()        
  
 SET @FILENAME = @SEG_TYPE + '_RRQ_' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','')   
 + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'  
    
 SELECT FILENAME= @FILENAME, SEG_TYPE, MemberCode,COLL_TYPE= 'ABC',SEC_DEP='SDP',INSTRUMENT_ID='',   
 CUSTODIAN='CDSL',SCRIP_CD,PLDQTY=SUM(PLDQTY),AMT='NA',ISIN,TMCODE=MemberCode,TRADING_ID=PARTY_CODE,  
 BANK_CODE='NA',Fungible_Flag='Y'   
 FROM #PLDPSNDATA_ADJUST, owner WHERE SEG_TYPE = @SEG_TYPE AND NEWEXCHANGE NOT IN ('NSE','NSX','NCE')  
 GROUP BY SEG_TYPE, MemberCode, SCRIP_CD, ISIN, PARTY_CODE  
 ORDER BY PARTY_CODE, SCRIP_CD  
  
 FETCH NEXT FROM @FILECUR INTO @SEG_TYPE   
END  
CLOSE @FILECUR  
DEALLOCATE @FILECUR

GO
