CREATE PROC [dbo].[RPT_CNT_NSECM] (@REPORTDATE DATETIME)    
    
AS --- EXEC RPT_CNT_NSECM 'SEP  1 2022','SEP 30 2022'    
    
SELECT PARTY_CODE,STATE=UPPER(STATE),PRODUCT_TYPE=CASE WHEN PRODUCT_TYPE IN ('ADMIN','RISK-SQ-OFF') THEN 'SQOFF' ELSE 'CNT' END,    
ORDER_NO,EXCHANGE,SEGMENT--,CONVERT(DATETIME,TRADE_DATE) AS TRADE_DATE   
INTO #CALLNTRADE    
FROM ORDER_TRANS_JV_CALC (NOLOCK) WHERE CONVERT(DATETIME,TRADE_DATE) BETWEEN @REPORTDATE AND @REPORTDATE + ' 23:59'    
AND ORDER_TYPE='OFFLINE' AND EXCHANGE IN ('NSE','BSE') AND SEGMENT='CAPITAL'    
AND PARTY_CODE NOT IN (SELECT PARTY_CODE FROM MSAJAG.DBO.CNT_EXCEPTION_MASTER)    
    
    
CREATE TABLE #JV_TABLE_TMP    
(SRNO INT IDENTITY(1,1),CLTCODE VARCHAR(20),GLCODE VARCHAR(20),GST_TYPE INT,GST_STATE VARCHAR(50),GSTRATE MONEY,    
CSGT VARCHAR(50),USGT VARCHAR(50),SSGT VARCHAR(50),ISGT VARCHAR(50),CHARGAMT MONEY,CSGT_CHARGES MONEY,USGT_CHARGES MONEY,    
SSGT_CHARGES MONEY,ISGT_CHARGES MONEY,NARRATION VARCHAR(500),BRANCH  VARCHAR(12),PRODUCT  VARCHAR(20))    
    
    
INSERT INTO #JV_TABLE_TMP    
SELECT PARTY_CODE,'61411', '',STATE,'18.00','','','','',COUNT(1)*20 ,0,0,0,0 ,'', 'ALL',PRODUCT_TYPE
FROM #CALLNTRADE    
GROUP BY PARTY_CODE,STATE,PRODUCT_TYPE
    
    
UPDATE #JV_TABLE_TMP SET NARRATION ='Call & Trade charges for '+ REPLACE(CONVERT(VARCHAR(11),@REPORTDATE,106),' ','-') WHERE PRODUCT='CNT'    
    
UPDATE #JV_TABLE_TMP SET NARRATION ='Square off charges for '+ REPLACE(CONVERT(VARCHAR(11),@REPORTDATE,106),' ','-') WHERE PRODUCT='SQOFF'    
    
UPDATE #JV_TABLE_TMP SET GST_TYPE = CASE WHEN J.GST_STATE = 'MAHARASHTRA' THEN 1 ELSE 0 END FROM #JV_TABLE_TMP J    
    
    
UPDATE J SET GSTRATE = CASE WHEN (GST_TYPE = 0 AND UTI_FLAG = 1 )THEN IGST_PER     
 WHEN GST_TYPE = 0 THEN CGST_PER + UGST_PER     
WHEN GST_TYPE = 1 THEN CGST_PER + SGST_PER  END     
FROM  #JV_TABLE_TMP J, MSAJAG..TBL_STATE_GST_DATA WITH (NOLOCK)    
WHERE STATE_NAME = GST_STATE    
    
UPDATE #JV_TABLE_TMP SET CSGT = 'MH_CGST',SSGT = 'MH_SGST' WHERE GST_STATE='MAHARASHTRA' AND GST_TYPE = 1    
UPDATE #JV_TABLE_TMP SET ISGT = 'MH_IGST' WHERE GST_STATE!='MAHARASHTRA' AND GST_TYPE = 0    
    
UPDATE #JV_TABLE_TMP SET ISGT_CHARGES = ROUND(CASE WHEN (ISGT IS NOT NULL AND ISGT  <> '') THEN CHARGAMT * GSTRATE/100  ELSE 0 END,2) ,     
CSGT_CHARGES = ROUND(CASE WHEN (CSGT IS NOT NULL  AND CSGT  <> '')THEN CHARGAMT * GSTRATE/2 /100 ELSE 0 END,2) ,     
USGT_CHARGES = ROUND(CASE WHEN (USGT IS NOT NULL  AND USGT  <> '') THEN CHARGAMT * GSTRATE/2 /100 ELSE 0 END,2) ,     
SSGT_CHARGES = ROUND(CASE WHEN (SSGT IS NOT NULL AND SSGT  <> '') THEN CHARGAMT * GSTRATE/2 /100 ELSE 0 END,2)    
FROM #JV_TABLE_TMP A    
    
    
SELECT SRNO,CLTCODE,GLCODE,GST_TYPE,GST_STATE,GSTRATE,CSGT,SSGT,ISGT,CHARGAMT,CSGT_CHARGES,SSGT_CHARGES,ISGT_CHARGES,EXCHANGE='NSECM',PRODUCT    
FROM #JV_TABLE_TMP     
ORDER BY SRNO

GO
