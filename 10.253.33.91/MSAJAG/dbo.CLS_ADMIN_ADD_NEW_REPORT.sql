CREATE PROC [dbo].[CLS_ADMIN_ADD_NEW_REPORT]  
AS  
SELECT * FROM CLS_TBLREPORTS A WHERE NOT EXISTS(SELECT FLDPATH FROM TBLREPORTS B WHERE A.FLDREPORTPATH =  FLDPATH)

GO
