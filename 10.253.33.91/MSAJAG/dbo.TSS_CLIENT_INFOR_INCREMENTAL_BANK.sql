CREATE PROCEDURE [dbo].[TSS_CLIENT_INFOR_INCREMENTAL_BANK]              
AS              
BEGIN

IF OBJECT_ID('tempdb..#Results') IS NOT NULL
    DROP TABLE #Results
SELECT C.CL_CODE INTO #Results  
FROM MSAJAG.dbo.CLIENT_DETAILS C WITH(NOLOCK) 
INNER JOIN MSAJAG.dbo.CLIENT_BROK_DETAILS B WITH(NOLOCK) ON C.CL_CODE=B.CL_CODE 
WHERE (MODIFIDEDON >=CONVERT(VARCHAR,GETDATE()-5,120)  OR MODIFIDEDON >=CONVERT(VARCHAR,GETDATE()-5,120) )
GROUP BY C.CL_CODE

CREATE INDEX IX_#CL_CL_CODE ON #Results (CL_CODE)

IF OBJECT_ID('tempdb..#BNKDTL') IS NOT NULL
    DROP TABLE #BNKDTL
  SELECT PARTY_CODE,NEW_BANK_NAME AS BANK_NAME,NEW_BANK_BRANCH  AS BRANCH_NAME, ACNUM AS ACCNO ,IFSC_CODE,MICR_CODE,  
                 IsNetBanking,DEFAULT_ID DEFALUT_ID,UPD_DATE    
  INTO #BNKDTL
  FROM ABVSCITRUS.CRMDB_A.dbo.CLIENT_BANK_MAPP X WITH(NOLOCK)  
  WHERE IFSC_CODE <> ''  
  AND  EXISTS (SELECT CL_CODE FROm #Results A WHEre A.Cl_Code =X.PARTY_CODE)

  
--IF OBJECT_ID('tempdb..#PRTYBANK') IS NOT NULL
--    DROP TABLE #PRTYBANK
--  Select X.* INTO #PRTYBANK from Msajag.dbo.Party_Bank_Details X  WITH(NOLOCK)
--  WHERE EXISTS (Select CL_CODE FROm #Results A WHEre A.Cl_Code =X.PARTY_CODE)

  ALTER TABLE #BNKDTL 
  ADD AcType VARCHAR (10)
   
create index IX_#BNKDTL on #BNKDTL (PARTY_CODE,ACCNO) 

  UPDATE A 
  SET A.AcType = B.AcType 
  from #BNKDTL A 
  INNER JOIN  Msajag.dbo.Party_Bank_Details B  WITH(NOLOCK) ON A.ACCNO =B.AcNum  AND A.PARTY_CODE =B.Party_Code


  --UPDATE A 
  --SET AcType = B.AcType 
  --from #BNKDTL A 
  --INNER JOIN  #PRTYBANK B on A.ACCNO =B.AcNum  AND A.PARTY_CODE =B.Party_Code

  SELECT 
  PARTY_CODE	,BANK_NAME	,BRANCH_NAME,	ACCNO,	IFSC_CODE,	MICR_CODE,	
  AcType ,DEFALUT_ID,	UPD_DATE	
  from #BNKDTL
  GROUP BY PARTY_CODE	,BANK_NAME	,BRANCH_NAME,	ACCNO,	IFSC_CODE,	MICR_CODE,	
  AcType ,DEFALUT_ID,	UPD_DATE
  order by PARTY_CODE


END

GO
