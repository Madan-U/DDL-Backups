-- Object: PROCEDURE dbo.EPN_MARGIN_ADJUSTMENT_MG13_MG02
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

  
  
CREATE PROCEDURE [dbo].[EPN_MARGIN_ADJUSTMENT_MG13_MG02] (@TDAY DATETIME,@UNAME VARCHAR(50))  
  
AS ---- EPN_MARGIN_ADJUSTMENT_MG13_MG02 'JUL  4 2021'  
  
  
/* BACKUP OF  TABLES */  
  
INSERT INTO EPN_MARGIN_ADJUSTMENT_PROCESS  
SELECT @TDAY,GETDATE(),@UNAME,'',DEC.LOCAL_NET_ADDRESS,DEC.CLIENT_NET_ADDRESS,''  
FROM SYS.DM_EXEC_CONNECTIONS AS DEC WHERE DEC.SESSION_ID = @@SPID;  
  
UPDATE A SET CLIENT_HOSTNAME = HOSTNAME   
FROM EPN_MARGIN_ADJUSTMENT_PROCESS A, SYS.SYSPROCESSES B  
WHERE A.EPN_ADJUSTMET_DATE = @TDAY AND B.SPID = @@SPID;  
  
TRUNCATE TABLE MSAJAG.DBO.TBL_MG02_LASTDAY  
TRUNCATE TABLE MSAJAG.DBO.TBL_CMMARGIN_LASTDAY --- Store one day data of MG13 Provisional  
TRUNCATE TABLE MSAJAG.DBO.TBL_CMMARGIN_ADJUST --- Store one day data of MG13 EPN Adjusted  
  
INSERT INTO MSAJAG.DBO.TBL_MG02_LASTDAY ----MG02  
SELECT * FROM MSAJAG.DBO.TBL_MG02 WITH (NOLOCK) WHERE MARGIN_DATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'  
  
INSERT INTO MSAJAG.DBO.TBL_CMMARGIN_LASTDAY ----MG13  
SELECT * FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'  
  
  
--- PROVISIONAL MG02 FILE DATA   
SELECT REC_TYPE,PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE,SETT_NO,BUYQTY,BUYAMT,SELLQTY,SELLAMT,NETQTY,NETAMT,CL_RATE,MTOM,VARAMT,  
MINIMUM_MARGIN,ADNL_MRG,EPN_QTY=CONVERT(MONEY,0),NETOBL=CONVERT(MONEY,0),UPDATEFLAG=CONVERT(VARCHAR(2),'') INTO #PROVISIONAL_MG02  
FROM MSAJAG.DBO.TBL_MG02_LASTDAY  
  
--- EPN DATA   
SELECT PARTY_CODE,SETT_NO,SETT_TYPE,SCRIP_CD,SERIES,EPNQTY,EPN_STATUS,RECORD_TYPE INTO #EPN  
FROM MSAJAG.DBO.TBL_PAYIN_DATA WITH (NOLOCK)   
WHERE TRANSDATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59' AND RECORD_TYPE='EPN'  
  
  CREATE NONCLUSTERED INDEX [PROVISIONAL_MG02_temp]
ON [dbo].[#PROVISIONAL_MG02] ([PARTY_CODE],[SCRIP_CD],[SETT_NO],[SETT_TYPE],[SERIES])

--- UPDATE EPN QTY  
UPDATE #PROVISIONAL_MG02 SET EPN_QTY = -EPNQTY FROM #PROVISIONAL_MG02 A,  
(SELECT PARTY_CODE,SETT_NO,SETT_TYPE,SCRIP_CD,SERIES,EPNQTY FROM #EPN) B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.SCRIP_CD = B.SCRIP_CD  
AND A.SETT_NO = B.SETT_NO AND A.SETT_TYPE = B.SETT_TYPE AND A.SERIES = B.SERIES  
  
  
--- UPDATE NET OBLIGATION  
UPDATE #PROVISIONAL_MG02 SET NETOBL = NETMG FROM #PROVISIONAL_MG02 A,  
(SELECT PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE,SETT_NO,NETMG=SUM(BUYQTY)-SUM(SELLQTY)  
FROM #PROVISIONAL_MG02 GROUP BY PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE,SETT_NO) B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.SCRIP_CD = B.SCRIP_CD  
AND A.SETT_NO = B.SETT_NO AND A.SETT_TYPE = B.SETT_TYPE AND A.SERIES = B.SERIES  
  
  
/* SETTELMENT TYPE - N */  
  
--- NO CHANGE IN MG02 EXISTING MARGIN  
UPDATE #PROVISIONAL_MG02 SET UPDATEFLAG='N' WHERE NETOBL > =  0 AND SETT_TYPE='N'  
UPDATE #PROVISIONAL_MG02 SET UPDATEFLAG='N' WHERE EPN_QTY = 0 AND SETT_TYPE='N'  
UPDATE #PROVISIONAL_MG02 SET UPDATEFLAG='N' WHERE NETQTY = 0 AND SETT_TYPE='N'  
  
  
--- EPN BENEFITS-1  
UPDATE A SET MTOM=0,VARAMT=0,MINIMUM_MARGIN=0,ADNL_MRG=0,UPDATEFLAG='Y'  
FROM #PROVISIONAL_MG02 A  
WHERE SETT_TYPE='N' AND UPDATEFLAG!='N'    
AND NETOBL > = EPN_QTY AND EPN_QTY < = NETQTY   
  
  
--- NO CHANGE IN MG02 EXISTING MARGIN  
UPDATE A SET UPDATEFLAG='N'  
FROM #PROVISIONAL_MG02 A  
WHERE SETT_TYPE='N' AND UPDATEFLAG NOT IN ('N','Y')    
AND NETOBL = EPN_QTY AND NETQTY=0   
  
  
--- EPN BENEFITS-2  
UPDATE A SET MTOM=0,VARAMT=0,MINIMUM_MARGIN=0,ADNL_MRG=0,UPDATEFLAG='Y'  
FROM #PROVISIONAL_MG02 A  
WHERE SETT_TYPE='N' AND UPDATEFLAG NOT IN ('N','Y')   
AND NETOBL = EPN_QTY AND ABS(NETOBL) > ABS(NETQTY)  
  
  
--- EPN BENEFITS-3 WITH CALCUATION-1  
UPDATE #PROVISIONAL_MG02 SET MTOM = PMTOM,VARAMT = PVARAMT,UPDATEFLAG='Y',   
MINIMUM_MARGIN = PMINIMUM_MARGIN,ADNL_MRG=PADNL_MRG FROM #PROVISIONAL_MG02 A,  
(SELECT PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE,SETT_NO,  
PMTOM=(MTOM/NETQTY)*(ABS(EPN_QTY)-ABS(NETQTY)),PVARAMT=(VARAMT/NETQTY)*(ABS(EPN_QTY)-ABS(NETQTY)),  
PMINIMUM_MARGIN=(MINIMUM_MARGIN/NETQTY)*(ABS(EPN_QTY)-ABS(NETQTY)),PADNL_MRG=(ADNL_MRG/NETQTY)*(ABS(EPN_QTY)-ABS(NETQTY))   
FROM #PROVISIONAL_MG02 WHERE SETT_TYPE='N' AND UPDATEFLAG NOT IN ('N','Y') AND  ABS(NETOBL) = ABS(NETQTY)) B  
WHERE A.Party_Code = B.Party_Code AND A.Scrip_Cd = B.Scrip_Cd   
AND A.Sett_No = B.Sett_No AND A.Sett_Type = B.Sett_Type  
  
  
--- NO CHANGE IN MG02 EXISTING MARGIN  
UPDATE A SET UPDATEFLAG='N'  
FROM #PROVISIONAL_MG02 A  
WHERE SETT_TYPE='N' AND UPDATEFLAG NOT IN ('N','Y')   
AND  ABS(ABS(NETOBL) - ABS(EPN_QTY)) = ABS(NETQTY)  
  
  
--- EPN BENEFITS-4 WITH CALCUATION-2  
UPDATE #PROVISIONAL_MG02 SET MTOM = PMTOM,VARAMT = ABS(PVARAMT),UPDATEFLAG='Y',   
MINIMUM_MARGIN = ABS(PMINIMUM_MARGIN),ADNL_MRG=ABS(PADNL_MRG) FROM #PROVISIONAL_MG02 A,  
(SELECT PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE,SETT_NO,  
PMTOM=CASE WHEN MTOM < 0 THEN ((MTOM/NETQTY)*(ABS(NETOBL)-ABS(EPN_QTY)))*-1 ELSE ABS((MTOM/NETQTY)*(ABS(NETOBL)-ABS(EPN_QTY))) END,  
PVARAMT=(VARAMT/NETQTY)*(ABS(NETOBL)-ABS(EPN_QTY)),  
PMINIMUM_MARGIN=(MINIMUM_MARGIN/NETQTY)*(ABS(NETOBL)-ABS(EPN_QTY)),PADNL_MRG=(ADNL_MRG/NETQTY)*(ABS(NETOBL)-ABS(EPN_QTY))   
FROM #PROVISIONAL_MG02 WHERE SETT_TYPE='N' AND UPDATEFLAG NOT IN ('N','Y') AND  ABS(ABS(NETOBL) - ABS(EPN_QTY)) <> ABS(NETQTY)) B  
WHERE A.Party_Code = B.Party_Code AND A.Scrip_Cd = B.Scrip_Cd   
AND A.Sett_No = B.Sett_No AND A.Sett_Type = B.Sett_Type  
  
  
  
/* SETTELMENT TYPE - W */  
  
--- NO CHANGE IN MG02 EXISTING MARGIN  
UPDATE #PROVISIONAL_MG02 SET UPDATEFLAG='N' WHERE EPN_QTY = 0 AND SETT_TYPE='W'  
UPDATE #PROVISIONAL_MG02 SET UPDATEFLAG='N' WHERE SETT_TYPE='W' AND UPDATEFLAG NOT IN ('N','Y') AND NETQTY = 0  
  
  
--- EPN BENEFITS-1  
UPDATE A SET MTOM=0,VARAMT=0,MINIMUM_MARGIN=0,ADNL_MRG=0,UPDATEFLAG='Y'  
FROM #PROVISIONAL_MG02 A  
WHERE SETT_TYPE='W' AND UPDATEFLAG NOT IN ('N','Y')  
AND NETOBL = EPN_QTY AND ABS(EPN_QTY) = ABS(NETQTY)   
  
  
--- NO CHANGE IN MG02 EXISTING MARGIN  
UPDATE A SET UPDATEFLAG='N'  
FROM #PROVISIONAL_MG02 A  
WHERE SETT_TYPE='W' AND UPDATEFLAG NOT IN ('N','Y')  
AND NETQTY = ABS(EPN_QTY) AND SELLQTY = 0  
  
  
--- NO CHANGE IN MG02 EXISTING MARGIN  
UPDATE A SET UPDATEFLAG='N'  
FROM #PROVISIONAL_MG02 A  
WHERE SETT_TYPE='W' AND UPDATEFLAG NOT IN ('N','Y')  
AND BUYQTY = NETQTY  
  
  
--- EPN BENEFITS-2  
UPDATE A SET MTOM=0,VARAMT=0,MINIMUM_MARGIN=0,ADNL_MRG=0,UPDATEFLAG='Y'  
FROM #PROVISIONAL_MG02 A  
WHERE SETT_TYPE='W' AND UPDATEFLAG NOT IN ('N','Y')  
AND BUYQTY = 0 AND NETQTY < = ABS(EPN_QTY)   
  



--- EPN BENEFITS-3 WITH CALCUATION-1  
UPDATE #PROVISIONAL_MG02 SET MTOM = PMTOM,VARAMT =PVARAMT,MINIMUM_MARGIN = PMINIMUM_MARGIN,UPDATEFLAG='Y' FROM #PROVISIONAL_MG02 A,  
(SELECT PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE,PMTOM=0,PVARAMT=VARAMT-SELLAMT,PMINIMUM_MARGIN=(VARAMT-SELLAMT)*20/100   
FROM #PROVISIONAL_MG02  WHERE UPDATEFLAG NOT IN ('N','Y') AND SETT_TYPE='W' AND SELLQTY = ABS(EPN_QTY)) B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.SCRIP_CD = B.SCRIP_CD   
AND A.SETT_NO = B.SETT_NO AND A.SETT_TYPE =B.SETT_TYPE  
  
  
--- EPN BENEFITS-4 WITH CALCUATION-2  
UPDATE #PROVISIONAL_MG02 SET MTOM = PMTOM,VARAMT =PVARAMT,MINIMUM_MARGIN = PMINIMUM_MARGIN,UPDATEFLAG='Y' FROM #PROVISIONAL_MG02 A,  
(SELECT PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE,PMTOM=(MTOM/NETQTY)*(NETQTY+EPN_QTY),  
PVARAMT=(VARAMT/NETQTY)*(NETQTY+EPN_QTY),PMINIMUM_MARGIN=((VARAMT/NETQTY)*(NETQTY+EPN_QTY))*20/100   
FROM #PROVISIONAL_MG02  WHERE UPDATEFLAG NOT IN ('N','Y') AND SETT_TYPE='W' AND BUYQTY = 0 AND NETQTY > ABS(EPN_QTY))B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.SCRIP_CD = B.SCRIP_CD AND A.SETT_NO = B.SETT_NO AND A.SETT_TYPE =B.SETT_TYPE  
  
  
--- EPN BENEFITS-5 WITH CALCUATION-3  
UPDATE #PROVISIONAL_MG02 SET MTOM = PMTOM,VARAMT =PVARAMT,MINIMUM_MARGIN = PMINIMUM_MARGIN,UPDATEFLAG='Y' FROM #PROVISIONAL_MG02 A,  
(SELECT PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE,PMTOM=(MTOM/SELLQTY)*(SELLQTY+EPN_QTY),  
PVARAMT=((SELLAMT/SELLQTY)*(SELLQTY+EPN_QTY))+BUYAMT,  
PMINIMUM_MARGIN=(((SELLAMT/SELLQTY)*(SELLQTY+EPN_QTY))+BUYAMT)*20/100   
FROM #PROVISIONAL_MG02  WHERE UPDATEFLAG NOT IN ('N','Y') AND SETT_TYPE='W' AND SELLQTY > ABS(EPN_QTY)) B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.SCRIP_CD = B.SCRIP_CD AND A.SETT_NO = B.SETT_NO AND A.SETT_TYPE =B.SETT_TYPE  
  
  
--- EPN BENEFITS-6 WITH CALCUATION-4  
UPDATE #PROVISIONAL_MG02 SET MTOM = 0,VARAMT =PVARAMT,MINIMUM_MARGIN = PMINIMUM_MARGIN,UPDATEFLAG='Y' FROM #PROVISIONAL_MG02 A,  
(SELECT PARTY_CODE,SCRIP_CD,SETT_NO,SETT_TYPE,PVARAMT=BUYAMT,  
PMINIMUM_MARGIN=(BUYAMT)*20/100   
FROM #PROVISIONAL_MG02  WHERE UPDATEFLAG NOT IN ('N','Y') AND SETT_TYPE='W' AND SELLQTY < ABS(EPN_QTY)) B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.SCRIP_CD = B.SCRIP_CD AND A.SETT_NO = B.SETT_NO AND A.SETT_TYPE =B.SETT_TYPE  
  
  
-- SELECT * FROM #PROVISIONAL_MG02  WHERE  UPDATEFLAG NOT IN ('N','Y')  
  
--- AFFECTED CLIENTS  
SELECT DISTINCT PARTY_CODE INTO #EPNMAST FROM #PROVISIONAL_MG02 WHERE UPDATEFLAG='Y'  
  
  
DECLARE @MAXSETT INT, @MINSETT INT  
SELECT @MAXSETT = MAX(SETT_NO) FROM #PROVISIONAL_MG02  
SELECT @MINSETT = MIN(SETT_NO) FROM #PROVISIONAL_MG02  
  
PRINT @MAXSETT  
PRINT @MINSETT  
  
  
--- FINAL MG02 UPDATE  
UPDATE A SET MTOM = PMTOM,VARAMT = PVARAMT,MINIMUM_MARGIN = PMINIMUM_MARGIN,ADNL_MRG=PADNL_MRG FROM MSAJAG.DBO.TBL_MG02 A,  
(SELECT PARTY_CODE,SCRIP_CD,SERIES,SETT_TYPE,SETT_NO,  
PMTOM=MTOM,PVARAMT=VARAMT,PMINIMUM_MARGIN=MINIMUM_MARGIN,PADNL_MRG=ADNL_MRG  
FROM #PROVISIONAL_MG02 WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #EPNMAST))B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.SCRIP_CD = B.SCRIP_CD AND A.SETT_NO = B.SETT_NO AND A.SETT_TYPE = B.SETT_TYPE  
AND A.MARGIN_DATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'  
  
  
--- FINAL MG13 MTM UPDATE  
SELECT PARTY_CODE,MTOMMG02=SUM(MTOM) INTO #MTM FROM (  
SELECT PARTY_CODE,MTOM=CASE WHEN SUM(MTOM) <  0 THEN SUM(MTOM) ELSE 0 END FROM #PROVISIONAL_MG02   
WHERE SETT_NO=@MAXSETT AND SETT_TYPE='N' AND Party_Code IN (SELECT PARTY_CODE FROM #EPNMAST) GROUP BY PARTY_CODE  
UNION ALL  
SELECT PARTY_CODE,MTOM=CASE WHEN SUM(MTOM) <  0 THEN SUM(MTOM) ELSE 0 END FROM #PROVISIONAL_MG02  
WHERE SETT_NO=@MINSETT AND SETT_TYPE='N' AND Party_Code IN (SELECT PARTY_CODE FROM #EPNMAST) GROUP BY PARTY_CODE  
UNION ALL  
SELECT PARTY_CODE,MTOM=CASE WHEN SUM(MTOM) <  0 THEN SUM(MTOM) ELSE 0 END FROM #PROVISIONAL_MG02  
WHERE Sett_No=@MAXSETT AND SETT_TYPE='W' AND Party_Code IN (SELECT PARTY_CODE FROM #EPNMAST) GROUP BY PARTY_CODE  
UNION ALL  
SELECT PARTY_CODE,MTOM=CASE WHEN SUM(MTOM) <  0 THEN SUM(MTOM) ELSE 0 END FROM #PROVISIONAL_MG02  
WHERE  Sett_No=@MINSETT AND SETT_TYPE='W' AND Party_Code IN (SELECT PARTY_CODE FROM #EPNMAST) GROUP BY PARTY_CODE ) A   
GROUP BY PARTY_CODE  
  
UPDATE A SET A.MTOM_LOSS = B.MTOMMG02 FROM MSAJAG.DBO.TBL_CMMARGIN A,#MTM B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.MARGINDATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'  
  
--- FINAL MG13 ALL MARGIN UPDATE  
UPDATE A SET VARMARGIN = VARMARGIN,EXTREME_LOSS_MARGIN=PEXTREME_LOSS_MARGIN,  
ADDITIONAL_MARGIN=PADDITIONAL_MARGIN,TOTAL_MARGIN=PTOTAL_MARGIN FROM MSAJAG.DBO.TBL_CMMARGIN A,  
(SELECT PARTY_CODE,PVARMARGIN=SUM(VARAMT),PEXTREME_LOSS_MARGIN=SUM(MINIMUM_MARGIN),  
PADDITIONAL_MARGIN=SUM(ADNL_MRG),PTOTAL_MARGIN=SUM(MINIMUM_MARGIN)+SUM(ADNL_MRG),  
TOTAL_MARGIN_VAR=SUM(VARAMT)+SUM(ADNL_MRG) FROM #PROVISIONAL_MG02   
WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #EPNMAST) GROUP BY PARTY_CODE) B  
WHERE A.PARTY_CODE = B.PARTY_CODE AND A.MARGINDATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'  
  
--- ADJ DATA STORE  
INSERT INTO MSAJAG.DBO.TBL_CMMARGIN_ADJUST  
SELECT * FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'  
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #EPNMAST)   
  
UPDATE EPN_MARGIN_ADJUSTMENT_PROCESS SET PROCESS_END_DATE = GETDATE () WHERE EPN_ADJUSTMET_DATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'

GO
