-- Object: PROCEDURE dbo.VALAN_ADDITIONAL_CHARGES_UPDATE
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE	PROC [dbo].[VALAN_ADDITIONAL_CHARGES_UPDATE]
		(
		@SETT_NO		VARCHAR(7),    
		@SETT_TYPE		VARCHAR(2) 
		)

		AS

		/*
		EXEC VALAN_ADDITIONAL_CHARGES_UPDATE '2022521','M'
		*/

		DECLARE	@IPFTCHRG	NUMERIC(18,4),
				@IPFTCHRG_INS NUMERIC(18,4),
				@EXCHANGE	VARCHAR(3),
				@SAUDA_DATE	VARCHAR(11)

		SELECT	@SAUDA_DATE = LEFT(START_DATE,11) FROM SETT_MST(NOLOCK) WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE

		SELECT	@IPFTCHRG = ISNULL(IPFTCHRG,0), @IPFTCHRG_INS = ISNULL(IPFTCHRG_INS,0), @EXCHANGE = ISNULL(EXCHANGE,'') 
		FROM	GLOBALS 
		WHERE	@SAUDA_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT

		IF @IPFTCHRG <> 0
		BEGIN

			SELECT	PARTY_CODE,
					SETT_NO,
					SETT_TYPE,
					SCRIP_CD,
					SERIES,
					SELL_BUY,
					IPFT_CHRG = ROUND(SUM(IPFT_CHRG),2),    
					TRDTYPE
			INTO	#ADD_CHRG
			FROM	TBL_ADDITIONAL_TAX_DATA T (NOLOCK)
			WHERE	T.SETT_NO = @SETT_NO    
					AND T.SETT_TYPE = @SETT_TYPE 
			GROUP BY
					PARTY_CODE,
					SETT_NO,
					SETT_TYPE,
					SCRIP_CD,
					SERIES,
					SELL_BUY,
					TRDTYPE
			HAVING	SUM(IPFT_CHRG) <> 0


			IF @EXCHANGE = 'NSE'
			BEGIN
				UPDATE	NSEBILLVALAN
				SET		PAMT = PAMT + (CASE WHEN #ADD_CHRG.SELL_BUY = 1 THEN IPFT_CHRG ELSE 0 END),
						SAMT = SAMT + (CASE WHEN #ADD_CHRG.SELL_BUY = 2 THEN (CASE WHEN #ADD_CHRG.TRDTYPE = 'I' THEN IPFT_CHRG ELSE -IPFT_CHRG END) ELSE 0 END)
				FROM	#ADD_CHRG
				WHERE	#ADD_CHRG.SETT_NO = NSEBILLVALAN.SETT_NO
						AND #ADD_CHRG.SETT_TYPE = NSEBILLVALAN.SETT_TYPE
						AND #ADD_CHRG.PARTY_CODE = NSEBILLVALAN.PARTY_CODE
						AND #ADD_CHRG.SCRIP_CD = NSEBILLVALAN.SCRIP_CD
						AND #ADD_CHRG.SERIES = NSEBILLVALAN.SERIES
			END
			IF @EXCHANGE = 'BSE'
			BEGIN
				UPDATE	BSEBILLVALAN
				SET		PAMT = PAMT + (CASE WHEN #ADD_CHRG.SELL_BUY = 1 THEN IPFT_CHRG ELSE 0 END),
						SAMT = SAMT + (CASE WHEN #ADD_CHRG.SELL_BUY = 2 THEN (CASE WHEN #ADD_CHRG.TRDTYPE = 'I' THEN IPFT_CHRG ELSE -IPFT_CHRG END) ELSE 0 END)
				FROM	#ADD_CHRG
				WHERE	#ADD_CHRG.SETT_NO = BSEBILLVALAN.SETT_NO
						AND #ADD_CHRG.SETT_TYPE = BSEBILLVALAN.SETT_TYPE
						AND #ADD_CHRG.PARTY_CODE = BSEBILLVALAN.PARTY_CODE
						AND #ADD_CHRG.SCRIP_CD = BSEBILLVALAN.SCRIP_CD
						AND #ADD_CHRG.SERIES = BSEBILLVALAN.SERIES
			END

			DROP TABLE #ADD_CHRG

		END

GO
