-- EXEC GET_SEBI_AUDIT_CLIENT_LIST '', 'JUN  7 2021', 1
CREATE PROCEDURE [dbo].[GET_SEBI_AUDIT_CLIENT_LIST]
	@EXCHANGE VARCHAR(10),
	@FROMDATE VARCHAR(11) = 'APR  1 2020',
	@FLAG BIT = 0
AS

CREATE TABLE #CL_LIST
(
	CLTCODE VARCHAR(10)
)

INSERT INTO #CL_LIST
EXEC CLIENTACC @FROMDATE



IF @FLAG = 0
SELECT CL_CODE FROM CLIENT_BROK_DETAILS WHERE EXCHANGE = CASE WHEN @EXCHANGE <> '' THEN @EXCHANGE ELSE EXCHANGE END

IF @FLAG = 1
BEGIN	
	INSERT INTO #CLIENT_LIST
	SELECT CL_CODE FROM CLIENT_BROK_DETAILS WHERE EXCHANGE = CASE WHEN @EXCHANGE <> '' THEN @EXCHANGE ELSE EXCHANGE END 
	AND Active_Date <= @FROMDATE + ' 23:59'
	AND EXISTS(SELECT CLTCODE FROM #CL_LIST WHERE CL_CODE = CLTCODE)
END

GO
