CREATE PROC [dbo].[DELAY_GET_COLLATERAL_COMBINE_MARGIN_REPORTING_AUTO]
(
	@FLAG VARCHAR(20),

	@SAUDA VARCHAR(11)
)

AS

	SET @SAUDA=CONVERT(VARCHAR(11),@SAUDA,100)
	
IF @FLAG = 'DELAY_GET_COLLATERAL'

	BEGIN 
	
		INSERT INTO  CMR_AUTO_PROCESS_LOG
		SELECT @SAUDA,GETDATE(),'DELAY_GET_COLL','START'

		 EXEC V2_PR_COLLATERAL_MARGIN_COLLATERAL @SAUDA      

		INSERT INTO  CMR_AUTO_PROCESS_LOG
		SELECT @SAUDA,GETDATE(),'DELAY_GET_COLL','END'

		SELECT 'COLLATERAL DATA IS READY FOR COMBINE_MARGIN_REPORTING PROCESS  ' + @SAUDA  AS [STATUS]

	END

IF (@SAUDA IS NULL OR @FLAG IS NULL)

	BEGIN
		SELECT ' PROCESS TESTED COMPLETED SUCCESSFULLY.....!!!!' AS [STATUS] 
	END

GO
