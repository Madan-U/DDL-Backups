CREATE PROC MF_PHY_CLI
(
@CL_CODE VARCHAR(10),
@ACNUM VARCHAR(25)
)
AS 

BEGIN 


	SELECT Party_Code,
	PARTY_NAME , (ADDR1+','+ADDR2+','+ADDR3) AS l_Address,CITY,STATE ,ZIP ,
	Region,
	BRANCH_CD ,
	SUB_BROKER ,BANK_NAME,ACC_NO,NEFTCODE FROM ANGELFO.BSEMFSS.DBO.MFSS_CLIENT WHERE DP_TYPE ='PHY' 
	AND PARTY_CODE NOT IN (SELECT CL_CODE FROM CLIENT_DETAILS)
	AND( PARTY_CODE LIKE '%'+@CL_CODE+'%'AND  ACC_NO LIKE '%'+@ACNUM+'%')

END

GO
