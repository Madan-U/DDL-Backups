-- Object: PROCEDURE dbo.RPT_KRA_REPORT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[RPT_KRA_REPORT]
	(
	@FROMDATE	VARCHAR(11),
	@TODATE		VARCHAR(11),
	@KRA_TYPE	VARCHAR(5),
	@KRA_STATUS	VARCHAR(10),
	@RPT_TYPE	INT = 0,
	@STATUS_FLAG 	VARCHAR(10) = ''
	)

	AS

	/*
	EXEC RPT_KRA_REPORT 'JAN  1 2012','DEC 31 2012','NSDL','DOWNLOAD',0
	EXEC RPT_KRA_REPORT 'JAN  1 2012','DEC 31 2012','CDSL','DOWNLOAD',0

	EXEC RPT_KRA_REPORT 'JAN  1 2010','DEC 31 2012','NSDL','UPLOAD',0,'Accepted'
	EXEC RPT_KRA_REPORT 'JAN  1 2010','DEC 31 2012','CDSL','UPLOAD',0,'01'
	*/

	IF (@KRA_STATUS = 'DOWNLOAD')
	BEGIN
		IF (@RPT_TYPE = 0)
		BEGIN
			SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
			SELECT 
				DATE = CONVERT(VARCHAR(11),CREATEDON,103), 
				PARTY_COUNT = COUNT(DISTINCT PARTY_CODE),
				ORD_DATE = CONVERT(VARCHAR(11),CREATEDON,112)
			FROM 
				TBL_KRA_LOG (NOLOCK)
			WHERE 
				BATCH_STATUS = @KRA_STATUS
				AND CREATEDON BETWEEN @FROMDATE AND @TODATE +' 23:59:59'
				AND KRA_TYPE = @KRA_TYPE
				AND KRA_DATA_TYPE = 'COMMON' 
				AND I_STATUS = 0
			GROUP BY
				CONVERT(VARCHAR(11),CREATEDON,103),
				CONVERT(VARCHAR(11),CREATEDON,112)
			ORDER BY
				CONVERT(VARCHAR(11),CREATEDON,112)
		END

	END

	IF (@KRA_STATUS = 'UPLOAD')
	BEGIN
		IF (@KRA_TYPE = 'NSDL' AND @RPT_TYPE = 0)
		BEGIN
			SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
			SELECT 
				DATE		= CONVERT(VARCHAR(11),CREATEDON,103), 
				PARTY_COUNT = COUNT(DISTINCT PAN_NO),
				ACCEPTED_COUNT	= COUNT( 
										DISTINCT (CASE 
											WHEN KRA_STATUS <> '' THEN
												(CASE WHEN KRA_STATUS = '00' THEN KRA_STATUS END)
											ELSE  
												(CASE WHEN UPDATE_FLAG = 'A' THEN UPDATE_FLAG END)
										END)
										),
				REJECTED_COUNT	= COUNT( 
										DISTINCT (CASE 
											WHEN KRA_STATUS <> '' THEN
												(CASE WHEN KRA_STATUS <> '00' THEN KRA_STATUS END)
											ELSE  
												(CASE WHEN UPDATE_FLAG <> 'A' THEN UPDATE_FLAG END)
										END)
										),
				ORD_DATE	= CONVERT(VARCHAR(11),CREATEDON,112)
			FROM 
				TBL_KRA_LOG (NOLOCK)
			WHERE 
				BATCH_STATUS = @KRA_STATUS
				AND CREATEDON BETWEEN @FROMDATE AND @TODATE +' 23:59:59'
				AND KRA_TYPE = @KRA_TYPE
				AND KRA_DATA_TYPE = 'COMMON' 
				AND I_STATUS = 1
			GROUP BY
				CONVERT(VARCHAR(11),CREATEDON,103),
				CONVERT(VARCHAR(11),CREATEDON,112)
			ORDER BY
				CONVERT(VARCHAR(11),CREATEDON,112)
		END

		IF (@KRA_TYPE = 'CDSL' AND @RPT_TYPE = 0)
		BEGIN
			SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
			SELECT 
				DATE		= CONVERT(VARCHAR(11),CREATEDON,103), 
				PARTY_COUNT = COUNT(DISTINCT PARTY_CODE),
				/*
				SUBMITTED	= COUNT(DISTINCT (CASE WHEN KRA_STATUS = '01' THEN PARTY_CODE END)),
				INPROCESS	= COUNT(DISTINCT (CASE WHEN KRA_STATUS = '02' THEN PARTY_CODE END)),
				UNDERVERIFICATION	= COUNT(DISTINCT (CASE WHEN KRA_STATUS = '03' THEN PARTY_CODE END)),
				HOLD		= COUNT(DISTINCT (CASE WHEN KRA_STATUS = '04' THEN PARTY_CODE END)),
				VERIFIED	= COUNT(DISTINCT (CASE WHEN KRA_STATUS = '98' THEN PARTY_CODE END)),
				REJECTED	= COUNT(DISTINCT (CASE WHEN KRA_STATUS = '99' THEN PARTY_CODE END)),
				*/
				ACCEPTED_COUNT	= COUNT( 
										DISTINCT (
										CASE 
											WHEN KRA_ERROR_DESC = 'ERR-00000' THEN PARTY_CODE
										END)
										),
				REJECTED_COUNT	= COUNT( 
										DISTINCT (
										CASE 
											WHEN KRA_ERROR_DESC <> 'ERR-00000' THEN PARTY_CODE
										END)
										),
				ORD_DATE	= CONVERT(VARCHAR(11),CREATEDON,112)
			FROM 
				TBL_KRA_LOG (NOLOCK)
			WHERE 
				BATCH_STATUS = @KRA_STATUS
				AND CREATEDON BETWEEN @FROMDATE AND @TODATE +' 23:59:59'
				AND KRA_TYPE = @KRA_TYPE
				AND KRA_DATA_TYPE = 'COMMON' 
				AND I_STATUS = 1
			GROUP BY
				CONVERT(VARCHAR(11),CREATEDON,103),
				CONVERT(VARCHAR(11),CREATEDON,112)
			ORDER BY
				CONVERT(VARCHAR(11),CREATEDON,112)
		END

	END


	IF (@RPT_TYPE = 1)
	BEGIN
		SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
		SELECT 
			DISTINCT
			PARTY_CODE	= (CASE WHEN T.KRA_TYPE = 'NSDL' AND @KRA_STATUS = 'UPLOAD' THEN ISNULL(C3.PARTY_CODE,'') ELSE T.PARTY_CODE END),
			PARTY_NAME	= ISNULL(C3.LONG_NAME,''),
			PAN_NO,
			KRA_STATUS	= (
			CASE
				WHEN T.KRA_TYPE = 'CDSL' THEN ISNULL(C1.DESCRIPTION,'')
				WHEN T.KRA_TYPE = 'NSDL' THEN 
					(CASE 
						WHEN KRA_STATUS <> '' THEN
							(CASE WHEN KRA_STATUS = '00' THEN 'Accepted' ELSE 'Rejected' END)
						ELSE  
							(CASE WHEN UPDATE_FLAG = 'A' THEN 'Accepted' ELSE 'Rejected' END)
					END)
			END),
			KRA_ERROR_DESC = (
			CASE
				WHEN T.KRA_TYPE = 'CDSL' THEN ISNULL(C2.DESCRIPTION,'')
				ELSE KRA_ERROR_DESC
			END)
		FROM 
			TBL_KRA_LOG T (NOLOCK)
			LEFT OUTER JOIN CLIENT_STATIC_CODES C1 (NOLOCK)
			ON
			(
				C1.CODE = T.KRA_STATUS
				AND C1.CATEGORY  = 'KYCSTATUS'
				AND C1.KRA_TYPE = 'CDSL'
			)
			LEFT OUTER JOIN CLIENT_STATIC_CODES C2 (NOLOCK)
			ON
			(
				C2.CODE = T.KRA_ERROR_DESC
				AND C2.CATEGORY  = 'REJECTION_REASONS'
				AND C2.KRA_TYPE = 'CDSL'
			)
			LEFT OUTER JOIN CLIENT_DETAILS C3 (NOLOCK)
			ON
			(
				C3.PAN_GIR_NO = PAN_NO
				AND T.PARTY_CODE = (CASE WHEN T.KRA_TYPE = 'NSDL' AND @KRA_STATUS = 'UPLOAD' THEN T.PARTY_CODE ELSE C3.PARTY_CODE END)
			)
		WHERE 
			BATCH_STATUS = @KRA_STATUS
			AND CREATEDON BETWEEN @FROMDATE AND @TODATE +' 23:59:59'
			AND T.KRA_TYPE = @KRA_TYPE
			AND KRA_DATA_TYPE = 'COMMON' 
			AND I_STATUS = (CASE WHEN @KRA_STATUS = 'UPLOAD' THEN 1 ELSE 0 END)
			AND (CASE 
					WHEN @KRA_STATUS = 'UPLOAD' THEN
						(CASE
							WHEN T.KRA_TYPE = 'CDSL' THEN 
								(CASE
									WHEN KRA_ERROR_DESC = 'ERR-00000' THEN 'Accepted'
									WHEN KRA_ERROR_DESC <> 'ERR-00000' THEN 'Rejected'
								END)	
							WHEN T.KRA_TYPE = 'NSDL' THEN 
								(CASE 
									WHEN KRA_STATUS <> '' THEN
										(CASE WHEN KRA_STATUS = '00' THEN 'Accepted' ELSE 'Rejected' END)
									ELSE  
										(CASE WHEN UPDATE_FLAG = 'A' THEN 'Accepted' ELSE 'Rejected' END)
								END)
						END) 
					ELSE @STATUS_FLAG 
				END) = @STATUS_FLAG
	END

GO
