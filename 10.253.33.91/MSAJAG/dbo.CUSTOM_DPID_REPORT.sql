CREATE PROCEDURE [dbo].[CUSTOM_DPID_REPORT] (@RUNDATE VARCHAR(11))

AS ---- EXEC CUSTOM_DPID_REPORT 'MAR  7 2022'

SELECT PARTY_CODE=NISE_PARTY_CODE,CLIENT_CODE,DP_STATUS =  STATUS INTO #CLT
FROM AngelDP4.DMAT.CITRUS_USR.TBL_CLIENT_MASTER WITH (NOLOCK) 
WHERE CLIENT_CODE IN (SELECT LTRIM(RTRIM(PARTY_CODE)) FROM MSAJAG.DBO.IMPORT_PARTY_CODE)

SELECT CL_CODE,EXCHANGE,CLSTATUS=CASE WHEN INACTIVE_FROM > GETDATE() THEN 'ACTIVE' ELSE 'INACTIVE' END INTO #STATUS
FROM MSAJAG.DBO.CLIENT_BROK_DETAILS WITH (NOLOCK)
WHERE CL_CODE IN (SELECT PARTY_CODE FROM #CLT) AND SEGMENT='CAPITAL'

SELECT CL_CODE,NSE_CASH=[NSE],BSE_CASH=[BSE] INTO #BOSTATUS
FROM (SELECT * FROM #STATUS ) AS SOURCETABLE
PIVOT
(MIN(CLSTATUS) FOR EXCHANGE IN ([NSE],[BSE])) AS PIVOTTABLE

SELECT SRNO=ROW_NUMBER() OVER (ORDER BY A.PARTY_CODE),TRADING_CODE=A.PARTY_CODE,DEMAT_ACCOUNT_NO=A.CLIENT_CODE,
DP_POA_STATUS=CONVERT(VARCHAR(50),'N'),B.NSE_CASH,B.BSE_CASH,DP_STATUS INTO #DATA
FROM #CLT A FULL OUTER JOIN #BOSTATUS B ON (A.PARTY_CODE = B.Cl_Code)

UPDATE #DATA SET DP_POA_STATUS = POA_STATUS FROM #DATA A,
(SELECT * FROM AGMUBODPL3.DMAT.CITRUS_USR.DP_POA_STATUS) B
WHERE A.DEMAT_ACCOUNT_NO = B.CLIENTDP

SELECT *  FROM #DATA ORDER BY SRNO

GO
