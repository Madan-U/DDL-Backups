-- Object: PROCEDURE dbo.RPT_HOLD_UPLOAD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE	PROC [dbo].[RPT_HOLD_UPLOAD]
		(
		@FROMDATE	VARCHAR(11),
		@FROMPARTY	VARCHAR(20) = '',
		@TOPARTY	VARCHAR(20) = 'zzzzzzzzzz'
		)

		AS

		/*
		EXEC	RPT_HOLD_UPLOAD_NEW_1 'DEC 26 2018','0','ZZZZZ'
		*/
		
		/*
		IF (SELECT CASE WHEN @FROMDATE = LEFT(GETDATE(),11) THEN 1 ELSE 0 END) = 0 
		BEGIN
			SELECT * FROM OWNER WHERE 1 = 2 
			RETURN
		END
		*/
		
		IF @TOPARTY = ''
		BEGIN
			SET @TOPARTY = 'zzzzzzzzzz'
		END

		DECLARE @FILENAME   VARCHAR(50)
		DECLARE @BATCHNO	INT  
		DECLARE @MEMBERCODE VARCHAR(20)
		DECLARE @MEMBERNAME	VARCHAR(500)

		SELECT @MEMBERCODE = MEMBERCODE, @MEMBERNAME = ISNULL(LTRIM(RTRIM(COMPANY)),'') FROM OWNER (NOLOCK)
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
		WHERE FLDFILENO = 'HS'  
		AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)

		IF @BATCHNO = 1  
		BEGIN  
			INSERT INTO ASIANFILEBATCHNO VALUES ('HS', @BATCHNO, GETDATE(), GETDATE())  
		END  
		ELSE  
		BEGIN  
			UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'HS' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		END  
		  
		SET	@FILENAME = @MEMBERCODE + '_HS_' + REPLACE(CONVERT(VARCHAR,GETDATE(),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV' 
		
		
		CREATE TABLE #HOLD
		(
		MEMBER_DEMAT_ACC	VARCHAR(50),
		MEMBER_ACC_TYPE		VARCHAR(50),
		UCC_CODE			VARCHAR(20),
		CLIENT_NAME			VARCHAR(500),
		CLIENT_PAN			VARCHAR(50),
		ISIN				VARCHAR(100),
		SCRIP_NAME			VARCHAR(500),
		SECURITY_TYPE		VARCHAR(10),
		PLEDGED_BAL_QTY		NUMERIC(18,3),
		FREE_BAL_QTY		NUMERIC(18,3),
		TOTAL_QTY			NUMERIC(18,3)
		)
		
		CREATE INDEX [DELHOLD] ON [DBO].[#HOLD] (
			[UCC_CODE] ASC,
			[SCRIP_NAME],
			[ISIN],
			[SECURITY_TYPE]			
			)
		

		CREATE TABLE #HOLD_NEW
		(
		PARTY_CODE			VARCHAR(20),
		SCRIP_CD			VARCHAR(50),
		CERTNO				VARCHAR(50),
		SERIES				VARCHAR(5),
		EXCHANGE			VARCHAR(6)
		)

		CREATE CLUSTERED INDEX [DELHOLD_NEW] ON [DBO].[#HOLD_NEW] (
			[PARTY_CODE] ASC,
			[SCRIP_CD],
			[CERTNO],
			[SERIES]			
			)

		INSERT INTO #HOLD_NEW
		SELECT	PARTY_CODE,
				SCRIP_CD,
				CERTNO,
				SERIES,
				EXCHANGE = 'NSECM'
		FROM	Angeldemat.Msajag.dbo.TBL_DEL_HOLD_SEC D WITH (NOLOCK)
		WHERE	TRANSDATE <= @FROMDATE
				AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		GROUP BY
				PARTY_CODE,
				SCRIP_CD,
				CERTNO,
				SERIES
		HAVING	SUM(DRQTY-CRQTY) <> 0
		

		INSERT INTO #HOLD_NEW
		SELECT	PARTY_CODE,
				SCRIP_CD,
				CERTNO,
				SERIES,
				EXCHANGE = 'BSECM'
		FROM	Angeldemat.BSEDB.DBO.TBL_DEL_HOLD_SEC D WITH(NOLOCK)
		WHERE	TRANSDATE <= @FROMDATE
				AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		GROUP BY
				PARTY_CODE,
				SCRIP_CD,
				CERTNO,
				SERIES
		HAVING	SUM(DRQTY-CRQTY) <> 0


		INSERT	INTO #HOLD		
		SELECT 	MEMBER_DEMAT_ACC = (
				CASE
					WHEN LEN(BCLTDPID) >= 16 THEN BCLTDPID
					ELSE BDPID + BCLTDPID
				END),
				--MEMBER_ACC_TYPE = D.MEMBER_ACC_TYPE,
				MEMBER_ACC_TYPE = (
				CASE
					WHEN DP.ACCOUNTTYPE = 'BEN' THEN 'CLNTBENE'
					WHEN DP.ACCOUNTTYPE = 'BEN' AND (DP.EXCHANGE = 'MTF' OR DP.EXCHANGE = 'MTF') THEN 'CLNTBENE-MTF'
					WHEN DP.ACCOUNTTYPE = 'POOL' THEN 'POOL'
					WHEN DP.ACCOUNTTYPE = 'EPI' THEN 'EPI'
					WHEN DP.DESCRIPTION LIKE '%COLL%' AND DP.SEGMENT = 'CAPITAL' THEN 'CM COLL'
					WHEN DP.DESCRIPTION LIKE '%COLL%' AND (DP.EXCHANGE = 'MTF' OR DP.SEGMENT = 'MTF') THEN 'COLL-MTF'
					WHEN DP.DESCRIPTION LIKE '%COLL%' THEN 'COLL'
					WHEN DP.DESCRIPTION LIKE '%COLL%' THEN 'COLL'
					ELSE DP.ACCOUNTTYPE
				END),
				UCC_CODE = PARTY_CODE,
				CLIENT_NAME = ISNULL(C1.LONG_NAME,''),
				CLIENT_PAN = ISNULL(C1.PAN_GIR_NO,''),
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				SECURITY_TYPE = SERIES,
				PLEDGED_BAL_QTY = SUM(CASE WHEN CRQTY > 0 THEN -PLEDGED_BAL_QTY ELSE PLEDGED_BAL_QTY END),
				FREE_BAL_QTY = SUM(CASE WHEN CRQTY > 0 THEN -FREE_BAL_QTY ELSE FREE_BAL_QTY END),
				TOTAL_QTY = SUM(DRQTY-CRQTY)
		FROM	Angeldemat.MSAJAG.DBO.TBL_DEL_HOLD_SEC D WITH(NOLOCK),
				Angeldemat.MSAJAG.DBO.DELIVERYDP DP WITH(NOLOCK),
				CLIENT1 C1 (NOLOCK)
		WHERE	TRANSDATE <= @FROMDATE
				AND D.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
				AND D.BDPID = DP.DPID
				AND D.BCLTDPID = DP.DPCLTNO
				AND D.PARTY_CODE = C1.CL_CODE
				AND EXISTS (SELECT PARTY_CODE FROM #HOLD_NEW D1
								WHERE D1.PARTY_CODE = D.PARTY_CODE
								AND D1.CERTNO = D.CERTNO
								AND D1.SCRIP_CD = D.SCRIP_CD
								AND D1.SERIES = D.SERIES
								AND D1.EXCHANGE = 'NSECM'
								)
		GROUP BY
				CASE
					WHEN LEN(BCLTDPID) >= 16 THEN BCLTDPID
					ELSE BDPID + BCLTDPID
				END,
				--D.MEMBER_ACC_TYPE,
				CASE
					WHEN DP.ACCOUNTTYPE = 'BEN' THEN 'CLNTBENE'
					WHEN DP.ACCOUNTTYPE = 'BEN' AND (DP.EXCHANGE = 'MTF' OR DP.EXCHANGE = 'MTF') THEN 'CLNTBENE-MTF'
					WHEN DP.ACCOUNTTYPE = 'POOL' THEN 'POOL'
					WHEN DP.ACCOUNTTYPE = 'EPI' THEN 'EPI'
					WHEN DP.DESCRIPTION LIKE '%COLL%' AND DP.SEGMENT = 'CAPITAL' THEN 'CM COLL'
					WHEN DP.DESCRIPTION LIKE '%COLL%' AND (DP.EXCHANGE = 'MTF' OR DP.SEGMENT = 'MTF') THEN 'COLL-MTF'
					WHEN DP.DESCRIPTION LIKE '%COLL%' THEN 'COLL'
					WHEN DP.DESCRIPTION LIKE '%COLL%' THEN 'COLL'
					ELSE DP.ACCOUNTTYPE
				END,
				PARTY_CODE,
				CERTNO,
				SCRIP_CD,
				SERIES,
				ISNULL(C1.LONG_NAME,''),
				ISNULL(C1.PAN_GIR_NO,'')
		HAVING	SUM(DRQTY-CRQTY) <> 0


		INSERT	INTO #HOLD		
		SELECT 	MEMBER_DEMAT_ACC = (
				CASE
					WHEN LEN(BCLTDPID) >= 16 THEN BCLTDPID
					ELSE BDPID + BCLTDPID
				END),
				--MEMBER_ACC_TYPE = D.MEMBER_ACC_TYPE,
				MEMBER_ACC_TYPE = (
				CASE
					WHEN DP.ACCOUNTTYPE = 'BEN' THEN 'CLNTBENE'
					WHEN DP.ACCOUNTTYPE = 'BEN' AND (DP.EXCHANGE = 'MTF' OR DP.EXCHANGE = 'MTF') THEN 'CLNTBENE-MTF'
					WHEN DP.ACCOUNTTYPE = 'POOL' THEN 'POOL'
					WHEN DP.ACCOUNTTYPE = 'EPI' THEN 'EPI'
					WHEN DP.DESCRIPTION LIKE '%COLL%' AND DP.SEGMENT = 'CAPITAL' THEN 'CM COLL'
					WHEN DP.DESCRIPTION LIKE '%COLL%' AND (DP.EXCHANGE = 'MTF' OR DP.SEGMENT = 'MTF') THEN 'COLL-MTF'
					WHEN DP.DESCRIPTION LIKE '%COLL%' THEN 'COLL'
					WHEN DP.DESCRIPTION LIKE '%COLL%' THEN 'COLL'
					ELSE DP.ACCOUNTTYPE
				END),
				UCC_CODE = PARTY_CODE,
				CLIENT_NAME = ISNULL(C1.LONG_NAME,''),
				CLIENT_PAN = ISNULL(C1.PAN_GIR_NO,''),
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				SECURITY_TYPE = SERIES,
				PLEDGED_BAL_QTY = SUM(CASE WHEN CRQTY > 0 THEN -PLEDGED_BAL_QTY ELSE PLEDGED_BAL_QTY END),
				FREE_BAL_QTY = SUM(CASE WHEN CRQTY > 0 THEN -FREE_BAL_QTY ELSE FREE_BAL_QTY END),
				TOTAL_QTY = SUM(DRQTY-CRQTY)
		FROM	Angeldemat.BSEDB.DBO.TBL_DEL_HOLD_SEC D WITH(NOLOCK),
				Angeldemat.BSEDB.DBO.DELIVERYDP DP WITH(NOLOCK),
				AngelBSECM.BSEDB_AB.DBO.CLIENT1 C1 (NOLOCK)
		WHERE	TRANSDATE <= @FROMDATE
				AND D.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
				AND D.BDPID = DP.DPID
				AND D.BCLTDPID = DP.DPCLTNO
				AND D.PARTY_CODE = C1.CL_CODE
				AND SETT_NO<>''
				AND sett_type<>''
				AND EXISTS (SELECT PARTY_CODE FROM #HOLD_NEW D1
								WHERE D1.PARTY_CODE = D.PARTY_CODE
								AND D1.CERTNO = D.CERTNO
								AND D1.SCRIP_CD = D.SCRIP_CD
								AND D1.SERIES = D.SERIES
								AND D1.EXCHANGE = 'BSECM'
								)
		GROUP BY
				CASE
					WHEN LEN(BCLTDPID) >= 16 THEN BCLTDPID
					ELSE BDPID + BCLTDPID
				END,
				--D.MEMBER_ACC_TYPE,
				CASE
					WHEN DP.ACCOUNTTYPE = 'BEN' THEN 'CLNTBENE'
					WHEN DP.ACCOUNTTYPE = 'BEN' AND (DP.EXCHANGE = 'MTF' OR DP.EXCHANGE = 'MTF') THEN 'CLNTBENE-MTF'
					WHEN DP.ACCOUNTTYPE = 'POOL' THEN 'POOL'
					WHEN DP.ACCOUNTTYPE = 'EPI' THEN 'EPI'
					WHEN DP.DESCRIPTION LIKE '%COLL%' AND DP.SEGMENT = 'CAPITAL' THEN 'CM COLL'
					WHEN DP.DESCRIPTION LIKE '%COLL%' AND (DP.EXCHANGE = 'MTF' OR DP.SEGMENT = 'MTF') THEN 'COLL-MTF'
					WHEN DP.DESCRIPTION LIKE '%COLL%' THEN 'COLL'
					WHEN DP.DESCRIPTION LIKE '%COLL%' THEN 'COLL'
					ELSE DP.ACCOUNTTYPE
				END,
				PARTY_CODE,
				CERTNO,
				SCRIP_CD,
				SERIES,
				ISNULL(C1.LONG_NAME,''),
				ISNULL(C1.PAN_GIR_NO,'')
		HAVING	SUM(DRQTY-CRQTY) <> 0


		UPDATE	#HOLD
		SET		SECURITY_TYPE = M.SERIES
		FROM	Angeldemat.MSAJAG.DBO.MULTIISIN M WITH (NOLOCK)
		WHERE	M.ISIN = #HOLD.ISIN
				AND VALID = 1


		UPDATE	#HOLD
		SET		SECURITY_TYPE = (
				CASE
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),2) = 'MF' OR LEFT(ISNULL(ISIN,''),3) = 'INF' THEN 'MF'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'N' THEN 'BOND'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'G' THEN 'DEBT'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'P' THEN 'PREF'
					WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'W' THEN 'WT'
					---WHEN LEFT(ISNULL(SECURITY_TYPE,''),1) = 'I' THEN 'IDR'
					ELSE 'EQ'
				END)


		SELECT	FILENAME = @FILENAME,
				[Members Demat Account No.] = MEMBER_DEMAT_ACC,
				[Member Account Type] = MEMBER_ACC_TYPE,
				[Unique Client Code (UCC)] = UCC_CODE,
				[Client Name] = CLIENT_NAME,
				[Client PAN] = CLIENT_PAN,
				[ISIN Code] = ISIN,
				[Scrip Name] = SCRIP_NAME,
				[Security Type] = SECURITY_TYPE,
				[Pledged Balance (Qty.)] = SUM(PLEDGED_BAL_QTY),
				[Free Balance (Qty.)] = ABS(SUM(FREE_BAL_QTY)),
				[Total (Qty.)] = ABS(SUM(TOTAL_QTY))
		FROM	#HOLD				
		GROUP BY
				MEMBER_DEMAT_ACC,
				MEMBER_ACC_TYPE,
				UCC_CODE,
				CLIENT_NAME,
				CLIENT_PAN,
				ISIN,
				SCRIP_NAME,
				SECURITY_TYPE
		ORDER BY
				UCC_CODE,
				SCRIP_NAME,
				ISIN
				
		DROP TABLE #HOLD
		DROP TABLE #HOLD_NEW

GO
