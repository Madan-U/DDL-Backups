CREATE PROC [dbo].[SEBI_AUDIT_ISIN_DETAILS_MCDX_DAILY]
(
	@REPORTDATE	VARCHAR(11)
)
AS

CREATE TABLE #CLIENT_LIST
(
	CLTCODE VARCHAR(10)
)


--INSERT INTO #CLIENT_LIST

EXEC GET_SEBI_AUDIT_CLIENT_LIST_COMM 'MCX', @REPORTDATE
--14350

DECLARE @NSEMEMBERCODE VARCHAR(10),        
  @BSEMEMBERCODE VARCHAR(10),
  @SEGMENT VARCHAR(3)

SET @SEGMENT = ''  
--SET @SEGMENT = 'BSE'  

SELECT @BSEMEMBERCODE = MEMBERCODE FROM [ANGELCOMMODITY].MCDX.DBO.FOOWNER        

SELECT PARTY_CODE,PARTY_NAME = CONVERT(VARCHAR(100), ''), PAN_GIR_NO = CONVERT(VARCHAR(35), ''),
ISIN = CERTNO, Name_of_the_Securities = SCRIP_CD, 
Quantity_of_securities_ISINwise = SUM(CRQTY-DRQTY),
Quantity_of_securities_pledged_ISINwise = 0,
Funds_raised_from_Pledging_ISIN_wise = convert(numeric(18,4),0),ACTION='A',SERIES
INTO #ISIN 
FROM [ANGELDEMAT].MSAJAG.DBO.TBL_DEL_HOLD_SEC D WITH (NOLOCK),
#CLIENT_LIST C
WHERE PARTY_CODE = CLTCODE AND TRANSDATE <= @REPORTDATE + ' 23:59:59'
AND BCLTDPID IN (SELECT DPCLTNO FROM [ANGELDEMAT].MSAJAG.DBO.DELIVERYDP WITH (NOLOCK))
and party_code not in ('NSE','BSE','EXE','BROKER')
GROUP BY PARTY_CODE, CERTNO, SCRIP_CD,SERIES
HAVING SUM(CRQTY-DRQTY) > 0 
ORDER BY 1, 4 

INSERT INTO #ISIN 
SELECT PARTY_CODE,PARTY_NAME = CONVERT(VARCHAR(100), ''), PAN_GIR_NO = CONVERT(VARCHAR(35), ''),
ISIN = CERTNO, Name_of_the_Securities = SCRIP_CD, 
Quantity_of_securities_ISINwise = SUM(CRQTY-DRQTY),
Quantity_of_securities_pledged_ISINwise = 0,
Funds_raised_from_Pledging_ISIN_wise = convert(numeric(18,4),0),ACTION='A',SERIES
FROM [ANGELDEMAT].BSEDB.DBO.TBL_DEL_HOLD_SEC D WITH (NOLOCK), #CLIENT_LIST C
WHERE PARTY_CODE = CLTCODE AND TRANSDATE <= @REPORTDATE + ' 23:59:59'
AND BCLTDPID IN (SELECT DPCLTNO FROM [ANGELDEMAT].BSEDB.DBO.DELIVERYDP WITH (NOLOCK))
and party_code not in ('NSE','BSE','EXE','BROKER')
GROUP BY PARTY_CODE, CERTNO, SCRIP_CD,SERIES
HAVING SUM(CRQTY-DRQTY) > 0 
ORDER BY 1, 4 

UPDATE #ISIN SET PARTY_NAME = LONG_NAME, PAN_GIR_NO = c.PAN_GIR_NO FROM CLIENT_dETAILS C WHERE #ISIN.PARTY_CODE = C.PARTY_CODE

select * into #Nseisin from #isin where series <> 'BSE'

UPDATE #ISIN SET NAME_OF_THE_SECURITIES = I.NAME_OF_THE_SECURITIES
FROM #Nseisin I
WHERE #ISIN.SERIES = 'BSE'
AND #ISIN.ISIN = I.ISIN 
AND I.SERIES <> 'BSE'

UPDATE #ISIN SET NAME_OF_THE_SECURITIES = M.SCRIP_CD FROM MULTIISIN_NEW M
WHERE M.ISIN = #ISIN.ISIN AND @REPORTDATE BETWEEN EFF_FROM AND EFF_TO

DECLARE
	@@REC_COUNT INT

SET @@REC_COUNT = 0

SELECT @@REC_COUNT = COUNT(1) FROM #ISIN  

CREATE TABLE #FILE_DATA
(
	FILENAME varchar(100),
	DATA VARCHAR(2000),
	SRNO INT IDENTITY(1, 1)
)

DECLARE
	@BATCHNO INT  

SELECT @BATCHNO = 0  

IF @@REC_COUNT > 0
BEGIN
	SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0) FROM ASIANFILEBATCHNO  
	WHERE FLDFILENO = 'MI'  
	AND FLDFILEDATE = @REPORTDATE  

	SET @BATCHNO = @BATCHNO + 1  

	INSERT INTO ASIANFILEBATCHNO  
	SELECT 'MI', @BATCHNO, @REPORTDATE, GETDATE()
	
	--INSERT INTO #FILE_DATA
	--SELECT FILENAME =  CONVERT(VARCHAR(3), CONVERT(DATETIME, @REPORTDATE) )+'_'+RIGHT(CONVERT(VARCHAR(11), CONVERT(DATETIME, @REPORTDATE)),2) + '_' + RIGHT('00' + CONVERT(VARCHAR,@BATCHNO),2),DATA = CONVERT(VARCHAR(3), CONVERT(DATETIME, @REPORTDATE) )+'-'+RIGHT(CONVERT(VARCHAR(11), CONVERT(DATETIME, @REPORTDATE)),2) + ',' + CONVERT(VARCHAR, @@REC_COUNT)
		
	 INSERT INTO #FILE_DATA  
	 SELECT FILENAME = 'MCDEX_' + @BSEMEMBERCODE + '_MONTHLY_FUNDSECBALDTL' + '_' +  REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@REPORTDATE,109),103),'/','')  + '.csv',
	 DATA ='Client name,Client Id,Client PAN,ISIN_of_the_security,Security_Name,Quantity_of_securities,Quantity of securities pledged,Funds raised from pledging of securities (Rs.)'

		
	INSERT INTO #FILE_DATA
	SELECT FILENAME = 'MCDEX_' + @BSEMEMBERCODE + '_MONTHLY_FUNDSECBALDTL' + '_' +  REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@REPORTDATE,109),103),'/','')  + '.csv',
	--SELECT FILENAME =  CONVERT(VARCHAR(3), CONVERT(DATETIME, @REPORTDATE) )+'_'+RIGHT(CONVERT(VARCHAR(11), CONVERT(DATETIME, @REPORTDATE)),2) 
	--+ '_' + RIGHT('00' + CONVERT(VARCHAR,@BATCHNO),2),
	DATA = PARTY_NAME + ',' + LTRIM(RTRIM(I.PARTY_CODE)) + ',' + pan_gir_no + ',' + ISIN + ',' + convert(varchar, MAX(Name_of_the_Securities)) + ',' + convert(varchar, sum(Quantity_of_securities_ISINwise)) + ',' + convert(varchar, Quantity_of_securities_pledged_ISINwise) + ',' + convert(varchar, Funds_raised_from_Pledging_ISIN_wise) 
	FROM #ISIN  i
	group by LTRIM(RTRIM(I.PARTY_CODE)), ISIN, Quantity_of_securities_pledged_ISINwise, Funds_raised_from_Pledging_ISIN_wise, PARTY_NAME , pan_gir_no

	SELECT [FILENAME], DATA FROM #FILE_DATA
END
ELSE
BEGIN
	SELECT [Client_Name] = PARTY_NAME, [Client_ID] = LTRIM(RTRIM(I.PARTY_CODE)), [Client_PAN] = pan_gir_no, [ISIN_of_the_security] = ISIN, 
	[Security_Name] = MAX(Name_of_the_Securities), [Quantity_of_securities] = sum(Quantity_of_securities_ISINwise), 
	[Quantity of securities pledged] = Quantity_of_securities_pledged_ISINwise, [Funds raised from pledging of securities (Rs.)] = Funds_raised_from_Pledging_ISIN_wise 
	FROM #ISIN  i
	group by LTRIM(RTRIM(I.PARTY_CODE)), ISIN, Quantity_of_securities_pledged_ISINwise, Funds_raised_from_Pledging_ISIN_wise, PARTY_NAME , pan_gir_no
	ORDER BY LTRIM(RTRIM(I.PARTY_CODE))
END

GO
