-- Object: PROCEDURE dbo.CLS_COMBINED_COLLATERAL
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[CLS_COMBINED_COLLATERAL]
(
	@CL_TYPE VARCHAR(10),
	@REPORT_DATE VARCHAR(11),
	@FROM_PARTY VARCHAR(10),
	@TO_PARTY VARCHAR(10),
	@EXCH_SEG VARCHAR(MAX),
	@SUB_FLAG BIT,
	@REPORTLEVEL TINYINT
)
AS

SET @REPORT_DATE = LEFT(CONVERT(DATETIME,@REPORT_DATE,103),11)

CREATE TABLE #COMBINED_COLLATERAL
(
	SRNO INT IDENTITY(1,1),
	CLIENT_CODE VARCHAR(10),
	CLIENT_NAME VARCHAR(100),
	EXCHANGE VARCHAR(3),
	SEGMENT VARCHAR(15),
	SUB_BROKER VARCHAR(25),
	SUM_OF_CASH MONEY, 
	SUM_OF_NON_CASH MONEY, 
	SUM_OF_ACTUAL_CASH MONEY, 
	SUM_OF_ACTUAL_NON_CASH MONEY, 
	SUM_OF_EFFECTIVE_COLLATERAL MONEY, 
	ORGCASH MONEY, 
	ORGNONCASH MONEY
)  


INSERT INTO #COMBINED_COLLATERAL
SELECT 
	CLIENT_CODE = C.PARTY_CODE,
	CLIENT_NAME = LONG_NAME,
	C.EXCHANGE,
	C.SEGMENT,
	SUB_BROKER = B.SUB_BROKER,
	SUM_OF_CASH = CASH, 
	SUM_OF_NON_CASH = NONCASH, 
	SUM_OF_ACTUAL_CASH = ACTUALCASH, 
	SUM_OF_ACTUAL_NON_CASH = ACTUALNONCASH, 
	SUM_OF_EFFECTIVE_COLLATERAL = EFFECTIVECOLL, 
	ORGCASH = ISNULL(ORGCASH,0) , 
	ORGNONCASH = ISNULL(ORGNONCASH,0)  
FROM 
	MSAJAG.DBO.COLLATERAL C (NOLOCK), 
	(
		SELECT DISTINCT PARTY_CODE,CLIENTTYPE,EXCHANGE,SEGMENT 
		FROM MSAJAG.DBO.COLLATERALDETAILS WHERE EFFDATE BETWEEN @REPORT_DATE AND @REPORT_DATE + ' 23:59'
	) C1, 
	MSAJAG.DBO.CLIENT2 A,
	MSAJAG.DBO.CLIENT1 B 
WHERE 
	C1.CLIENTTYPE = CASE WHEN @CL_TYPE ='' THEN C1.CLIENTTYPE  ELSE @CL_TYPE END
	AND C.PARTY_CODE = C1.PARTY_CODE 
	AND C.PARTY_CODE=A.PARTY_CODE 
	AND A.CL_CODE=B.CL_CODE 
	AND TRANS_DATE BETWEEN @REPORT_DATE AND @REPORT_DATE + ' 23:59'
	AND C.EXCHANGE = C1.EXCHANGE 
	AND C.SEGMENT = C1.SEGMENT 
	AND C.PARTY_CODE >= @FROM_PARTY AND C.PARTY_CODE <= @TO_PARTY 
	AND EXISTS(SELECT ITEMS FROM CLS_SPLIT(@EXCH_SEG, '|') S WHERE C.EXCHANGE + '-' + C.SEGMENT = S.ITEMS)
/*	
GROUP BY 
	LONG_NAME,
	C.PARTY_CODE,
	C.EXCHANGE,
	C.SEGMENT,
	B.SUB_BROKER
ORDER BY 
	C.PARTY_CODE, 
	C.EXCHANGE, 
	C.SEGMENT
*/	

IF @REPORTLEVEL = 1
BEGIN
	SELECT 
		SRNO = MAX(SRNO),
		CLIENT_CODE,
		CLIENT_NAME,
		SUB_BROKER,
		SUM_OF_CASH = SUM(SUM_OF_CASH), 
		SUM_OF_NON_CASH = SUM(SUM_OF_NON_CASH), 
		SUM_OF_ACTUAL_CASH = SUM(SUM_OF_ACTUAL_CASH), 
		SUM_OF_ACTUAL_NON_CASH = SUM(SUM_OF_ACTUAL_NON_CASH), 
		SUM_OF_EFFECTIVE_COLLATERAL = SUM(SUM_OF_EFFECTIVE_COLLATERAL), 
		ORGCASH = SUM(ORGCASH), 
		ORGNONCASH = SUM(ORGNONCASH)
	FROM #COMBINED_COLLATERAL
	GROUP BY
		CLIENT_CODE,
		CLIENT_NAME,
		SUB_BROKER
END
ELSE
BEGIN
	SELECT 
		SRNO = MAX(SRNO),
		CLIENT_CODE,
		EXCHANGE,
		SEGMENT,
		SUM_OF_CASH = SUM(SUM_OF_CASH), 
		SUM_OF_NON_CASH = SUM(SUM_OF_NON_CASH), 
		SUM_OF_ACTUAL_CASH = SUM(SUM_OF_ACTUAL_CASH), 
		SUM_OF_ACTUAL_NON_CASH = SUM(SUM_OF_ACTUAL_NON_CASH), 
		SUM_OF_EFFECTIVE_COLLATERAL = SUM(SUM_OF_EFFECTIVE_COLLATERAL), 
		ORGCASH = SUM(ORGCASH), 
		ORGNONCASH = SUM(ORGNONCASH)
	FROM #COMBINED_COLLATERAL
	GROUP BY
		CLIENT_CODE,
		EXCHANGE,
		SEGMENT
END

GO
