-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC  [dbo].[MONTHLY_LTD_POA_DATA]  
AS  
BEGIN  
SELECT MAX(SAUDA_DATE)SAUDA_DATE ,PARTY_CODE,  
(CASE WHEN EXCHANGE='NSE' AND SEGMENT='FUTURES' THEN 'NSEFO' ELSE EXCHANGE END ) AS EXCHANGE  
 INTO #CCD_1  
 FROM COMMON_CONTRACT_DATA WITH (NOLOCK)  
WHERE --PARTY_CODE IN ('A100002','A100351')  
--AND  
SAUDA_DATE>=CONVERT(VARCHAR(10),GETDATE()-31,120)  
GROUP BY PARTY_CODE,(CASE WHEN EXCHANGE='NSE' AND SEGMENT='FUTURES' THEN 'NSEFO' ELSE EXCHANGE END )  
  
SELECT MAX(SAUDA_DATE)SAUDA_DATE ,PARTY_CODE,  
 EXCHANGE  
 INTO #CCD_2  
 FROM [AngelCommodity].MCDX.DBO.COMMON_CONTRACT_DATA WITH (NOLOCK)  
WHERE --PARTY_CODE IN ('A100002','A100351')  
--AND  
SAUDA_DATE>=CONVERT(VARCHAR(10),GETDATE()-31,120)  
GROUP BY PARTY_CODE,EXCHANGE  
  
  
SELECT * INTO #ALL FROM #CCD_1  
UNION  
SELECT * FROM #CCD_2 ORDER BY PARTY_CODE  
  
SELECT MAX(SAUDA_DATE)MAX_DATE ,PARTY_CODE INTO #MAX FROM #ALL  
GROUP BY PARTY_CODE  
  
--HAVING COUNT(1 )>1  
  
  
--SELECT DISTINCT  FROM #CCD_1 WHERE PARTY_CODE IN ('S572325','L26309')  
  
SELECT PARTY_CODE,[BSE], [NSE], [NSEFO],[NSX],[MCX],[NCX]  INTO #PIV  
FROM    
(SELECT * FROM #ALL ) AS SourceTable    
PIVOT    
(    
MAX(SAUDA_DATE)    
FOR EXCHANGE IN ([BSE], [NSE], [NSEFO],[NSX],[MCX],[NCX])    
) AS PivotTable;  
  
  
SELECT B.*,[BSE], [NSE], [NSEFO],[NSX],[MCX],[NCX] INTO #LLD FROM #PIV A,#MAX B  
WHERE A.PARTY_CODE=B.PARTY_CODE  
--AND A.PARTY_CODE='A100000'  
  
  
--761256  
  
SELECT B.*,B2C INTO #OCC  
 FROM INTRANET.RISK.dbo.CLIENT_DETAILS A WITH (NOLOCK) ,#LLD B  
 WHERE A.PARTY_CODE=B.PARTY_CODE  
  
  
-- SELECT TOP 10000 * INTO #OCC FROM #B2C  
  
  
SELECT Z.* ,POA_STATUS INTO  #POA FROM (  
SELECT  DISTINCT C.*,B.CLIENT_CODE FROM  
AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER B RIGHT OUTER JOIN #OCC C  
ON B.NISE_PARTY_CODE =C.PARTY_CODE  
)Z LEFT OUTER JOIN AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_POA A  
ON Z.CLIENT_CODE =A.CLIENT_CODE  
  
  
 SELECT DISTINCT PARTY_CODE,MAX_DATE,[BSE], [NSE], [NSEFO],[NSX],[MCX],[NCX],B2C,poa_status  
FROM #POA   
--ORDER BY PARTY_CODE  
  
END

GO
