CREATE PROC CREDITOR_DEBTOR_SUMMARY (@TDATE VARCHAR(11),@FPARTY VARCHAR(11),@TPARTY VARCHAR(11))

AS

/*

EXEC CREDITOR_DEBTOR_SUMMARY 'APR 17 2021','A','ZZZ'

*/

SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT = 'Debtors', CONVERT(VARCHAR,(SUM(BALANCE)/10000000)) AS BALANCE 
FROM ACCOUNT.DBO.TBL_SFCM_REPORT WHERE REPORT_DATE BETWEEN @TDATE AND @TDATE AND DRCR_STATUS='DR' GROUP BY REPORT_DATE
UNION ALL
SELECT REPORT_DATE=CONVERT(VARCHAR(11),REPORT_DATE,105),REPORT = 'Creditors', CONVERT(VARCHAR,(SUM(BALANCE)/10000000)) AS BALANCE 
FROM ACCOUNT.DBO.TBL_SFCM_REPORT WHERE REPORT_DATE BETWEEN @TDATE AND @TDATE AND DRCR_STATUS='CR' GROUP BY REPORT_DATE

GO
