CREATE PROC [dbo].[PEAK_EPN_BENEFIT] (@MDATE DATETIME)
 AS 
 
 SELECT   * INTO #FILEDATA FROM TBL_PAYIN_DATA WHERE TRANSDATE = @MDATE  AND RECORD_TYPE='EPN'

 SELECT * INTO #MAR  FROM MSAJAG.DBO.TBL_MG02 WHERE MARGIN_DATE=@MDATE

 SELECT E.*,(CASE WHEN EPNQTY >SELLQTY THEN SELLQTY ELSE E.EPNQTY END) AS EPN_QTY,
 (SELLAMT/SELLQTY)*(CASE WHEN EPNQTY >SELLQTY THEN SELLQTY ELSE EPNQTY END) AS EPN_VALUE  ,SELLQTY,SELLAMT  INTO #FINAL_EPN
 FROM #FILEDATA E,#MAR J
 WHERE E.PARTY_CODE=J.PARTY_CODE AND E.SCRIP_CD=J.SCRIP_CD AND E.SERIES=J.SERIES 
 AND E.SETT_TYPE='M'  AND SELLQTY <>0 

 DELETE FROM TBL_EPN_BENEFIT_PEAK WHERE TRANSDATE =@MDATE 

 INSERT INTO TBL_EPN_BENEFIT_PEAK
 SELECT SETT_NO,SETT_TYPE,TRANSDATE,PARTY_CODE,SCRIP_CD,SERIES,SELLQTY,EPNQTY,0,0,SELLAMT,0,EPN_VALUE,0
 FROM #FINAL_EPN
  
 INSERT INTO TBL_EPN_BENEFIT_PEAK
 SELECT * FROM TBL_EPN_BENEFIT WHERE TRANSDATE = @MDATE AND SETT_TYPE ='Z'

GO
