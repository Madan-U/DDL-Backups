CREATE PROC SFCM_DEBTORS ( @TDATE VARCHAR(11),@FPARTY VARCHAR(11),@TPARTY VARCHAR(11))
AS
/*

EXEC SFCM_DEBTORS 'APR 16 2021','A','ZZZZ'

*/
SELECT * FROM ACCOUNT.DBO.TBL_SFCM_REPORT 
WHERE REPORT_DATE BETWEEN @TDATE AND @TDATE
AND CLTCODE BETWEEN @FPARTY AND @TPARTY 
AND DRCR_STATUS='DR'
ORDER BY 3

GO
