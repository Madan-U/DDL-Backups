CREATE PROC [dbo].[NBFC_AUCTION_DATA] (
	@TRANSDATE VARCHAR(11)
) AS

--exec NBFC_AUCTION_DATA 'OCT  7 2014'
/*
CREATE TABLE NBFC_AUCTION
(  
 PARTY_CODE VARCHAR(10),  
 QTY   NUMERIC(18,3),  
 SCRIP_CD VARCHAR(12),  
 SERIES  VARCHAR(3),  
 D_ISIN  VARCHAR(12)  
)
*/

TRUNCATE TABLE NBFC_AUCTION

IF (SELECT ISNULL(COUNT(1),0) FROM SETTLEMENT  
WHERE SAUDA_DATE > @TRANSDATE + ' 23:59'  
AND SETT_TYPE IN ('A', 'X')  
--AND PARTY_CODE = 'ANS055'  
AND ORDER_NO IN (  
SELECT SETT_NO FROM SETT_MST WHERE SEC_PAYIN LIKE @TRANSDATE + '%' AND SETT_TYPE = 'N')  
AND AUCTIONPART LIKE 'F%') >0   
BEGIN  
 INSERT INTO NBFC_AUCTION
 SELECT PARTY_CODE, QTY=SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END), SCRIP_CD, SERIES,  
 D_ISIN = CONVERT(VARCHAR(12),'')   
 FROM SETTLEMENT  
 WHERE SAUDA_DATE > @TRANSDATE + ' 23:59'  
 AND SETT_TYPE IN ('A', 'X')  
 --AND PARTY_CODE = 'ANS055'  
 AND ORDER_NO IN (  
 SELECT SETT_NO FROM SETT_MST WHERE SEC_PAYIN LIKE @TRANSDATE + '%' AND SETT_TYPE = 'N')  
 AND AUCTIONPART LIKE 'F%'  
 AND PARTY_CODE IN (SELECT PARTY_CODE FROM ABVSCITRUS.NBFC.DBO.TBLCLIENTMARGIN WHERE SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE)  
 GROUP BY PARTY_CODE,SCRIP_CD, SERIES   
END  
ELSE   
BEGIN  
 INSERT INTO NBFC_AUCTION
 SELECT PARTY_CODE, QTY=SUM(CASE WHEN SELL_BUY = 1 THEN -TRADEQTY ELSE TRADEQTY END), SCRIP_CD, SERIES,  
 D_ISIN = CONVERT(VARCHAR(12),'')   
 FROM SETTLEMENT  
 WHERE SAUDA_DATE > @TRANSDATE + ' 23:59'  
 AND SETT_TYPE IN ('A', 'X')  
 --AND PARTY_CODE = 'ANS055'  
 AND ORDER_NO IN (  
 SELECT SETT_NO FROM SETT_MST WHERE SEC_PAYIN LIKE @TRANSDATE + '%' AND SETT_TYPE = 'N')  
 AND AUCTIONPART = 'AP'  
 AND PARTY_CODE IN (SELECT PARTY_CODE FROM ABVSCITRUS.NBFC.DBO.TBLCLIENTMARGIN WHERE SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE)  
 GROUP BY PARTY_CODE,SCRIP_CD, SERIES   
END

GO
