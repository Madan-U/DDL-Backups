-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC EMAIL_REGUPDATE
AS
BEGIN TRAN

UPDATE E SET STATUS =2,REMARKS ='EMAIL NOT AVAIALBLE'
FROM CLIENT_DETAILS C,EMAIL_API E
WHERE E.CL_CODE =C.CL_CODE AND E.STATUS =0
AND ISNULL(C.EMAIL,'') ='' 


SELECT DISTINCT CL_CODE,ECN_REGISTRATION_NO,reg_DATE INTO #ECN
FROM EMAIL_API WHERE STATUS =0


UPDATE C SET repatriat_bank_ac_no =ECN_REGISTRATION_NO,Director_name=reg_DATE,
ModifidedBy = 'ECN-API' ,	ModifidedOn =GETDATE()
FROM CLIENT_DETAILS C,#ECN E
WHERE C.CL_CODE=E.CL_CODE
 
DROP TABLE #ECN

COMMIT

GO
