AND T.EXCHANGE IN ('BSE','BSX','BCX')
GROUP BY CMCODE, TMCODE, T.EXCHANGE, T.SEGMENT, SEGMENT_INDICATOR, PARTY_CODE, CP_CODE, ACCOUNT_TYPE
