CREATE PROC [dbo].[INSPROC_DEL_29MAR2023]  
(  
 @SETT_NO VARCHAR(7),  
 @SETT_TYPE VARCHAR(2)  
)  
  
AS  
  
DELETE FROM DELIVERYCLT 
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE  

INSERT INTO DELIVERYCLT  
SELECT * FROM ANGELDEMAT.MSAJAG.DBO.DELIVERYCLT   
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE

GO
