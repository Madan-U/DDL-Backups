-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------




CREATE PROC [dbo].[PROC_SHORT_MARGIN_DWLOAD]
(
      @EFFDATE    VARCHAR(11) 
)
AS

SET NOCOUNT ON 

----EXEC PROC_SHORT_MARGIN_DWLOAD 'MAY 1 2023'


DECLARE @CMFILENAME VARCHAR(50)
DECLARE @FOFILENAME VARCHAR(50)
DECLARE @CDFILENAME VARCHAR(50)
DECLARE @BATCHNO INT
DECLARE @MEMBERCODE VARCHAR(10)
DECLARE @FILEDATE VARCHAR(10)
DECLARE @BSETMCODE VARCHAR(10)

IF  (SELECT COUNT(1) FROM ASIANFILEMARGINBATCHNO WHERE  LEFT(FILEDATE,11) = LEFT(@EFFDATE,11) ) = 0 
      BEGIN 
            INSERT INTO ASIANFILEMARGINBATCHNO 
            SELECT '1' ,LEFT(@EFFDATE,11),LEFT(@EFFDATE,11)
      END
ELSE 
      BEGIN 
            SELECT @BATCHNO = MAX(FILEBATCHNO)+1 FROM ASIANFILEMARGINBATCHNO  
            WHERE LEFT(FILEDATE,11) = LEFT(@EFFDATE,11)
            UPDATE ASIANFILEMARGINBATCHNO SET FILEBATCHNO = @BATCHNO WHERE LEFT(FILEDATE,11) = LEFT(@EFFDATE,11)
      END

SET @FILEDATE = REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@EFFDATE),103),'/','')

SELECT @BATCHNO = MAX(FILEBATCHNO) FROM ASIANFILEMARGINBATCHNO  
            WHERE LEFT(FILEDATE,11) = LEFT(@EFFDATE,11)

SELECT @MEMBERCODE = MEMBERCODE FROM OWNER
SELECT @BSETMCODE = MEMBERCODE FROM AngelBSECM.BSEDB_AB.DBO.OWNER

SET @CMFILENAME = 'C_SA_' + 'TM' + '_' + @FILEDATE + '_' + RIGHT('0'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'
SET @FOFILENAME = 'F_SA_' + 'TM' + '_' + @FILEDATE + '_' + RIGHT('0'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'
SET @CDFILENAME = 'X_SA_' + 'TM' + '_' + @FILEDATE + '_' + RIGHT('0'+CONVERT(VARCHAR,@BATCHNO),2) + '.CSV'

SELECT *, COLL_AVL=CONVERT(NUMERIC(18,2),0),REASON_CODE=CONVERT(VARCHAR(2),''),CC_CODE=CONVERT(VARCHAR(2),'')
INTO #TBL_SHORT_ALLOC FROM TBL_SHORT_ALLOC
WHERE MARGINDATE = @EFFDATE

SELECT *, COLL_AVL=CONVERT(NUMERIC(18,2),0),REASON_CODE=CONVERT(VARCHAR(2),''),CC_CODE=CONVERT(VARCHAR(2),'')
INTO #TBL_SHORT_ALLOC_ICCL FROM TBL_SHORT_ALLOC_ICCL
WHERE MARGINDATE = @EFFDATE

UPDATE #TBL_SHORT_ALLOC SET INDICATOR = RIGHT('00'+INDICATOR,2) WHERE INDICATOR <> 'E'

UPDATE #TBL_SHORT_ALLOC SET COLL_AVL = OTHER_SEG_COLL, 
REASON_CODE = C.REASONCODE,
CC_CODE = C.CC_CODE
FROM TBL_SHORT_ALLOC_ADJUST T, VW_SHORT_ALLOC_CODE C
WHERE T.MARGINDATE = @EFFDATE
AND T.PARTY_CODE = #TBL_SHORT_ALLOC.CLIENT_CODE 
AND T.EXCHANGE = #TBL_SHORT_ALLOC.EXCHANGE 
AND T.SEGMENT = #TBL_SHORT_ALLOC.SEGMENT 
AND T.INDICATOR = #TBL_SHORT_ALLOC.INDICATOR
AND C.EXCHG_SEG = T.OTHER_EXCHG_SEG
AND OTHER_SEG_COLL > 0 

UPDATE #TBL_SHORT_ALLOC SET COLL_AVL = OTHER_SEG_COLL 
FROM (SELECT PARTY_CODE,EXCHANGE,SEGMENT,INDICATOR,OTHER_SEG_COLL=SUM(OTHER_SEG_COLL) 
FROM TBL_SHORT_ALLOC_ADJUST T  
WHERE T.MARGINDATE = @EFFDATE AND OTHER_SEG_COLL > 0 AND EXCHANGE IN ('NSE','NSX') 
GROUP BY PARTY_CODE,EXCHANGE,SEGMENT,INDICATOR) T
WHERE T.PARTY_CODE = #TBL_SHORT_ALLOC.CLIENT_CODE 
AND T.EXCHANGE = #TBL_SHORT_ALLOC.EXCHANGE 
AND T.SEGMENT = #TBL_SHORT_ALLOC.SEGMENT
AND T.INDICATOR = #TBL_SHORT_ALLOC.INDICATOR

UPDATE #TBL_SHORT_ALLOC SET COLL_AVL = EOD_SHORT_ALLOC, REASON_CODE = '04'
FROM Client_Details C
WHERE C.party_code = #TBL_SHORT_ALLOC.Client_CODE
AND cl_type = 'NRI'
AND EOD_SHORT_ALLOC > 0 

SELECT * INTO #TBL_SHORT_ALLOC_TMP FROM #TBL_SHORT_ALLOC
WHERE EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL' AND INDICATOR <> 'E'
AND EOD_SHORT_ALLOC > COLL_AVL AND COLL_AVL > 0 

UPDATE #TBL_SHORT_ALLOC SET CC_CODE='' WHERE REASON_CODE='04'

UPDATE #TBL_SHORT_ALLOC_TMP SET EOD_SHORT_ALLOC = EOD_SHORT_ALLOC - COLL_AVL, COLL_AVL = 0 ,REASON_CODE = ''

SELECT PARTY_CODE, CASHBENEFIT = SUM(CASHBENEFIT)*20/100
INTO #EPN_ADJ
FROM TBL_EPN_BENEFIT WHERE TRANSDATE = @EFFDATE and CASHBENEFIT > 0 
GROUP BY PARTY_CODE

UPDATE #TBL_SHORT_ALLOC_TMP SET COLL_AVL = (CASE WHEN CASHBENEFIT > EOD_SHORT_ALLOC THEN EOD_SHORT_ALLOC ELSE CASHBENEFIT END), REASON_CODE = '02'
FROM #EPN_ADJ
WHERE #TBL_SHORT_ALLOC_TMP.CLIENT_CODE = #EPN_ADJ.PARTY_CODE

UPDATE #TBL_SHORT_ALLOC SET COLL_AVL = (CASE WHEN CASHBENEFIT > EOD_SHORT_ALLOC THEN EOD_SHORT_ALLOC ELSE CASHBENEFIT END), REASON_CODE = '02'
FROM #EPN_ADJ
WHERE #TBL_SHORT_ALLOC.CLIENT_CODE = #EPN_ADJ.PARTY_CODE
AND COLL_AVL <= 0 AND INDICATOR <> 'E'
AND EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL'

DELETE FROM #TBL_SHORT_ALLOC_TMP WHERE COLL_AVL <= 0 

INSERT INTO #TBL_SHORT_ALLOC
SELECT * FROM #TBL_SHORT_ALLOC_TMP
WHERE REASON_CODE <> ''

SELECT PARTY_CODE, CASHBENEFIT = SUM(CASHBENEFIT)*20/100
INTO #EPN_ADJ_BSE
FROM AngelBSECM.BSEDB_AB.DBO.TBL_EPN_BENEFIT WHERE TRANSDATE = @EFFDATE and CASHBENEFIT > 0 
GROUP BY PARTY_CODE
   
UPDATE #TBL_SHORT_ALLOC_ICCL SET COLL_AVL = (CASE WHEN CASHBENEFIT > EOD_SHORT_ALLOC THEN EOD_SHORT_ALLOC ELSE CASHBENEFIT END), REASON_CODE = '02'
FROM #EPN_ADJ_BSE
WHERE #TBL_SHORT_ALLOC_ICCL.CLIENT_CODE = #EPN_ADJ_BSE.PARTY_CODE
AND COLL_AVL <= 0 AND INDICATOR <> 'E'

SELECT * INTO #TBL_MCX_SHORT_ALLOC FROM TBL_MCX_SHORT_ALLOC
WHERE MARGINDATE = @EFFDATE

SELECT *,EXCESS_NCL=EOD_SHORT_ALLOC,
EXCESS_ICCL=EOD_SHORT_ALLOC,
EXCESS_NCCL=EOD_SHORT_ALLOC,
EXCESS_MCCIL=EOD_SHORT_ALLOC,
EPN_COMM=EOD_SHORT_ALLOC,
ALLOC_PENDING=EOD_SHORT_ALLOC,
WRONG_UCC=EOD_SHORT_ALLOC,
WRING_CLIENT_CODE=EOD_SHORT_ALLOC 
INTO #TBL_MCX_SHORT_ALLOC_INTRA FROM TBL_MCX_SHORT_ALLOC_INTRA
WHERE MARGINDATE = @EFFDATE

UPDATE #TBL_MCX_SHORT_ALLOC_INTRA SET 
EXCESS_NCL = 0, 
EXCESS_ICCL=0,
EXCESS_NCCL=0,
EXCESS_MCCIL=0,
EPN_COMM=0,
ALLOC_PENDING=0,
WRONG_UCC=0,
WRING_CLIENT_CODE=0 

UPDATE #TBL_MCX_SHORT_ALLOC SET EXCESS_ICCL = ICL_COLL, EXCESS_NCL = NCL_COLL, EXCESS_NCCL = NCX_COLL, EXCESS_MCCIL = OTHER_COLL
FROM (SELECT PARTY_CODE,
				ICL_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'ICL' THEN OTHER_SEG_COLL ELSE 0 END),
				NCL_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'NCL' THEN OTHER_SEG_COLL ELSE 0 END),
				NCX_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'NCX' THEN OTHER_SEG_COLL ELSE 0 END),
				OTHER_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) NOT IN ('ICL','NCL','NCX') THEN OTHER_SEG_COLL ELSE 0 END)
		FROM MSAJAG.DBO.TBL_SHORT_ALLOC_ADJUST T  
		WHERE T.MARGINDATE = @EFFDATE AND OTHER_SEG_COLL > 0 AND EXCHANGE = 'MCX' AND INDICATOR = 'E'
		GROUP BY PARTY_CODE,EXCHANGE,SEGMENT) T
WHERE T.PARTY_CODE = #TBL_MCX_SHORT_ALLOC.CLIENT_CODE  

UPDATE #TBL_MCX_SHORT_ALLOC_INTRA SET EXCESS_ICCL = ICL_COLL, EXCESS_NCL = NCL_COLL, EXCESS_NCCL = NCX_COLL, EXCESS_MCCIL = OTHER_COLL
FROM (SELECT PARTY_CODE,
				ICL_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'ICL' THEN OTHER_SEG_COLL ELSE 0 END),
				NCL_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'NCL' THEN OTHER_SEG_COLL ELSE 0 END),
				NCX_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'NCX' THEN OTHER_SEG_COLL ELSE 0 END),
				OTHER_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) NOT IN ('ICL','NCL','NCX') THEN OTHER_SEG_COLL ELSE 0 END)
		FROM MSAJAG.DBO.TBL_SHORT_ALLOC_ADJUST T  
		WHERE T.MARGINDATE = @EFFDATE AND OTHER_SEG_COLL > 0 AND EXCHANGE = 'MCX' AND INDICATOR <> 'E'
		GROUP BY PARTY_CODE,EXCHANGE,SEGMENT) T
WHERE T.PARTY_CODE = #TBL_MCX_SHORT_ALLOC_INTRA.CLIENT_CODE  

SELECT *
INTO #TBL_NCX_SHORT_ALLOC_INTRA FROM TBL_NCX_SHORT_ALLOC_INTRA
WHERE MARGINDATE = @EFFDATE

UPDATE #TBL_NCX_SHORT_ALLOC_INTRA SET 
EXCESS_NCL = 0, 
EXCESS_ICCL=0,
EXCESS_OTHER=0,
EXCESS_MCXCCL=0, 
ALLOC_PENDING=0, 
WRING_CLIENT_CODE=0 

UPDATE #TBL_NCX_SHORT_ALLOC_INTRA SET EXCESS_ICCL = ICL_COLL, EXCESS_NCL = NCL_COLL, EXCESS_MCXCCL = MCX_COLL, EXCESS_OTHER = OTHER_COLL
FROM (SELECT PARTY_CODE,
				ICL_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'ICL' THEN OTHER_SEG_COLL ELSE 0 END),
				NCL_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'NCL' THEN OTHER_SEG_COLL ELSE 0 END),
				MCX_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) = 'MCX' THEN OTHER_SEG_COLL ELSE 0 END),
				OTHER_COLL=SUM(CASE WHEN LEFT(OTHER_EXCHG_SEG,3) NOT IN ('ICL','NCL','MCX') THEN OTHER_SEG_COLL ELSE 0 END)
		FROM MSAJAG.DBO.TBL_SHORT_ALLOC_ADJUST T  
		WHERE T.MARGINDATE = @EFFDATE AND OTHER_SEG_COLL > 0 AND EXCHANGE = 'NCX' AND INDICATOR <> 'E'
		GROUP BY PARTY_CODE,EXCHANGE,SEGMENT) T
WHERE T.PARTY_CODE = #TBL_NCX_SHORT_ALLOC_INTRA.CLIENT_CODE  

SELECT CLIENT_CODE=PARTY_CODE,
DATA=@FILEDATE
+ ',' +  LTRIM(RTRIM(EXCHANGE)) 
+ ',' +  LTRIM(RTRIM(SEGMENT)) 
+ ',' +  LTRIM(RTRIM(PARTY_CODE)) 
+ ',' +  LTRIM(RTRIM(INDICATOR)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EXCESS_COLLATERAL)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EOD_SHORT))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ADJUST_AMT))
+ ',' +  LTRIM(RTRIM(ADJUST_EXCHG_SEG)) 
+ ',' + CONVERT(VARCHAR,LEFT(RUNDATE,11)) + ' ' + CONVERT(VARCHAR,RUNDATE,108),
FILENAME_EXCHG = 'EOD_SHORT_WORKING_' + @FILEDATE + '.CSV',
FLAG=1
FROM TBL_SHORT_EOD_WORKING
WHERE MARGINDATE = @EFFDATE 
AND ADJUST_AMT <> 0  
UNION ALL
SELECT CLIENT_CODE, 
DATA = UPPER(REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@EFFDATE),106),' ','-'))
+ ',' +  RIGHT(LTRIM(RTRIM(INDICATOR)),1) 
+ ',' +  LTRIM(RTRIM(CLIENT_CODE)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),MARGIN_REQ)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_CASH_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_NONCASH_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EXCESS_OTHER_SEG))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_VALUE_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EOD_SHORT_ALLOC))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),COLL_AVL))
+ ',' + CONVERT(VARCHAR,REASON_CODE)
+ ',' + CONVERT(VARCHAR,CC_CODE),
FILENAME_EXCHG = (CASE WHEN EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL' THEN 'NOT_'+@CMFILENAME
                        WHEN EXCHANGE = 'NSE' AND SEGMENT = 'FUTURES' THEN 'NOT_'+@FOFILENAME
                        WHEN EXCHANGE = 'NSX' AND SEGMENT = 'FUTURES' THEN 'NOT_'+@CDFILENAME
                        ELSE '123'
                  END),
				  FLAG=1
FROM #TBL_SHORT_ALLOC
WHERE MARGINDATE = @EFFDATE AND EOD_SHORT_ALLOC > 0 AND COLL_AVL = 0  
UNION ALL
SELECT CLIENT_CODE, 
DATA = UPPER(REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@EFFDATE),106),' ','-'))
+ ',' +  RIGHT(LTRIM(RTRIM(INDICATOR)),1) 
+ ',' +  LTRIM(RTRIM(CLIENT_CODE)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),MARGIN_REQ)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_CASH_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_NONCASH_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EXCESS_OTHER_SEG))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_VALUE_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EOD_SHORT_ALLOC))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),COLL_AVL))
+ ',' + CONVERT(VARCHAR,REASON_CODE)
+ ',' + CONVERT(VARCHAR,CC_CODE),
FILENAME_EXCHG = (CASE WHEN EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL' THEN @CMFILENAME
                        WHEN EXCHANGE = 'NSE' AND SEGMENT = 'FUTURES' THEN @FOFILENAME
                        WHEN EXCHANGE = 'NSX' AND SEGMENT = 'FUTURES' THEN @CDFILENAME
                        ELSE '123'
                  END),
				  FLAG=1
FROM #TBL_SHORT_ALLOC
WHERE MARGINDATE = @EFFDATE AND EOD_SHORT_ALLOC > 0 AND COLL_AVL > 0  
/*UNION ALL
SELECT DISTINCT CLIENT_CODE='',
DATA='Date,CM ID,TM ID,End Client/CP Code,EOD Short Allocation (Rs),EOD Short Allocation Reported (Rs),Excess collateral with NCL (Rs.),Excess collateral with ICCL(Rs.),Excess collateral with MCCIL(Rs.),Excess collateral with NCCL(Rs.),',
FILENAME_EXCHG =  REPLACE(FLDFILENAME,'.csv','_R' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.csv'),FLAG=1
FROM #TBL_MCX_SHORT_ALLOC
WHERE MARGINDATE = @EFFDATE*/
UNION ALL
SELECT CLIENT_CODE,
DATA=REPLACE(CONVERT(VARCHAR,MARGINDATE,103),'/','-')+','+LTRIM(RTRIM(CMID))+','+LTRIM(RTRIM(TMID))+','+LTRIM(RTRIM(CLIENT_CODE))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EOD_SHORT_ALLOC))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_NCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_ICCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_NCCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_MCCIL)),
FILENAME_EXCHG = REPLACE(FLDFILENAME,'.csv','_R' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.csv'),FLAG=2
FROM #TBL_MCX_SHORT_ALLOC
WHERE MARGINDATE = @EFFDATE  
/*UNION ALL
SELECT DISTINCT CLIENT_CODE='',
DATA='Date,CM ID,TM ID,End Client/CP Code,EOD Short Allocation (Rs),EOD Short Allocation Reported (Rs),Excess collateral with NCL (Rs.),Excess collateral with ICCL(Rs.),Excess collateral with MCCIL(Rs.),Excess collateral with NCCL(Rs.),',
FILENAME_EXCHG =  REPLACE(FLDFILENAME,'.csv','_R' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.csv'),FLAG=1
FROM #TBL_MCX_SHORT_ALLOC_INTRA
WHERE MARGINDATE = @EFFDATE */
UNION ALL
SELECT CLIENT_CODE,
DATA=REPLACE(CONVERT(VARCHAR,MARGINDATE,106),' ','')+','+LTRIM(RTRIM(CMID))+','+LTRIM(RTRIM(TMID))+','+LTRIM(RTRIM(CLIENT_CODE))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EOD_SHORT_ALLOC)) 
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_NCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_ICCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_NCCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_MCCIL))+',0,0,0,0,0,0,0',
FILENAME_EXCHG = REPLACE(FLDFILENAME,'.csv','_R' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) + '.csv'),FLAG=2
FROM #TBL_MCX_SHORT_ALLOC_INTRA
WHERE MARGINDATE = @EFFDATE  
UNION ALL
/*SELECT DISTINCT CLIENT_CODE='',
DATA='Trade Date,Clearing Member code,Trading Member code,Client Code,CP Code,Allocated Collateral (I),Margin Required (J),Short Allocation (K= I-J),Indicator,Reason Code,Excess Collateral with NCL,Excess Collateral with MCCIL,Excess Collateral with MCXCCL,Excess Collateral with NCCL',
FILENAME_EXCHG = 'SHORTALLOCATION_TM_'+@BSETMCODE+'_'+@FILEDATE+'_'+ RIGHT('0'+CONVERT(VARCHAR,@BATCHNO),2)+'.CSV',FLAG=1
FROM #TBL_SHORT_ALLOC_ICCL
WHERE MARGINDATE = @EFFDATE 
UNION ALL*/
SELECT CLIENT_CODE,
DATA=REPLACE(CONVERT(VARCHAR,MARGINDATE,103),'/','-')+','+LTRIM(RTRIM(CMCODE))+','+LTRIM(RTRIM(TMCODE))+','+LTRIM(RTRIM(CLIENT_CODE))+','+LTRIM(RTRIM(CPCODE))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),Total_Coll))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),Margin_Req))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EOD_Short_Alloc))
	+','+CONVERT(VARCHAR,INDICATOR)
	+ ',' + CONVERT(VARCHAR,REASON_CODE)
	+ ',' + CONVERT(VARCHAR,CC_CODE)
	+',0,0,0,0',	
FILENAME_EXCHG = 'SHORTALLOCATION_TM_'+@BSETMCODE+'_'+@FILEDATE+'_'+ RIGHT('0'+CONVERT(VARCHAR,@BATCHNO),2)+'.CSV',FLAG=2
FROM #TBL_SHORT_ALLOC_ICCL
WHERE MARGINDATE = @EFFDATE  
UNION ALL
SELECT DISTINCT
CLIENT_CODE='',
DATA='Trade Date,Trading Member ID,Client ID,Excess Collateral With Other CCs at PSA (ICCL),Excess Collateral With Other CCs at PSA (MCXCCL),Excess Collateral With Other CCs at PSA (NCL),Excess Collateral With Other CCs at PSA (Others),Delay In Processing Allocation Request at PSA,Trade in wrong client code (PSA),Total Shortfall (Peak Short Allocation),Peak Short Allocation Snapshot Time',
FILENAME_EXCHG = FLDFILENAME,FLAG=1
FROM #TBL_NCX_SHORT_ALLOC_INTRA
WHERE MARGINDATE = @EFFDATE  
UNION ALL
SELECT  
CLIENT_CODE,
DATA=''''+REPLACE(CONVERT(VARCHAR,MARGINDATE,103),'/','-')+','''+LTRIM(RTRIM(TMID))+','''+LTRIM(RTRIM(CLIENT_CODE))+
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_ICCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_MCXCCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_NCL))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EXCESS_OTHER))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),ALLOC_PENDING))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),WRING_CLIENT_CODE))
	+','+convert(varchar,CONVERT(NUMERIC(18,2),EOD_SHORT_ALLOC)) 
	+','+CONVERT(VARCHAR,PEAK_SNAP_TIME),	
FILENAME_EXCHG = FLDFILENAME,FLAG=2
FROM #TBL_NCX_SHORT_ALLOC_INTRA
WHERE MARGINDATE = @EFFDATE  
ORDER BY FILENAME_EXCHG,FLAG,CLIENT_CODE

GO
