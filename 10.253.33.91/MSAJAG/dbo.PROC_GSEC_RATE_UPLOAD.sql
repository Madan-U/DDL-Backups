CREATE PROC [dbo].[PROC_GSEC_RATE_UPLOAD] (@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), @PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = '')

AS

-- EXEC PROC_ILLIQUID_UPLOAD '09/04/2018', '', '', '1234567890', ''
DECLARE @RECCNT INT,
		@FILEDATE	DATETIME

SELECT @FILEDATE = CONVERT(DATETIME,LEFT(LEFT(RIGHT(@FILENAME,12),8),2) + '/' + RIGHT(LEFT(LEFT(RIGHT(@FILENAME,12),8),4),2) + '/' + LEFT(RIGHT(@FILENAME,8),4),103)

SELECT SYSDATE, ISIN, CL_RATE, UPLOAD_BY, UPLOAD_ON 
INTO #DATA
FROM TBL_GSEC_RATE
WHERE 1 = 2 

INSERT INTO #DATA (SYSDATE, ISIN, CL_RATE, UPLOAD_BY, UPLOAD_ON)
SELECT @FILEDATE, .DBO.PIECE(FILE_DATA,',',2),.DBO.PIECE(FILE_DATA,',',6), 
		UPLOAD_BY = @UNAME, UPLOAD_ON = GETDATE()
FROM FILEDATA
WHERE PROGID = @PROGID AND ISNULL(.DBO.PIECE(FILE_DATA,',',2),'')  LIKE 'IN%'

SELECT @RECCNT = 0 
SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #DATA

IF @RECCNT > 0 
BEGIN 
	DELETE FROM TBL_GSEC_RATE
	WHERE SYSDATE= @FILEDATE

	INSERT INTO TBL_GSEC_RATE
	SELECT * FROM #DATA
END

/*

DELETE FROM FILEDATA

WHERE PROGID = @PROGID

*/
SELECT STRMSGCODE = 1, STRMSG = CONVERT(VARCHAR,@RECCNT) + ' RECORD(S) UPLOADED.'

GO
