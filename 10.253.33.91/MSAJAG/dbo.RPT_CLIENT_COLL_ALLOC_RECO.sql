CREATE PROC [dbo].[RPT_CLIENT_COLL_ALLOC_RECO]
(
	@RUNDATE	VARCHAR(11)
)
AS
DECLARE @PREVDATE VARCHAR(11),
		@FILENAME	VARCHAR(50),
		@BATCHNO	INT,
		@SNO	INT,
		@NOOFREC	INT,
		@FILEREC	INT,
		@MEMBERCODE	VARCHAR(10),
		@CMCUR		CURSOR,
		@ORG_MEMBERCODE	VARCHAR(10)

-- EXEC RPT_CLIENT_COLL_ALLOC_RECO @RUNDATE = 'FEB 10 2022'

SET @SNO=0
SET @FILEREC = 1000


 
DECLARE @FILEDATE VARCHAR(11)
 
SELECT @FILEDATE = ISNULL(MAX(PROCESS_DT),'APR  1 2022') FROM TBL_CLIENT_COLL_ALLOCATE
WHERE PROCESS_DT <= @RUNDATE

SELECT EXCHANGE, SEGMENT, PARTY_CODE=CLI_CODE,UPLOAD_ON=MAX(UPLOAD_ON) INTO #ALLOC_SEG FROM TBL_CLIENT_COLL_FILE 
WHERE FILEDATE = @RUNDATE AND BATCH_STATUS IN ('G','D') 
GROUP BY EXCHANGE, SEGMENT, CLI_CODE

UPDATE TBL_CLIENT_COLL_RECO SET CMCODE = C.CMCODE 
From TBL_CMCC_DETAIL C
WHERE TBL_CLIENT_COLL_RECO.EXCHANGE = C.EXCHANGE AND TBL_CLIENT_COLL_RECO.SEGMENT = C.SEGMENT
AND @RUNDATE BETWEEN FROM_DATE AND TO_DATE 

SELECT EXCHANGE, SEGMENT, CMCODE, TMCODE, CLIENTCODE, BOAMOUNT=CONVERT(NUMERIC(18,4),0), EXCHGAMOUNT=AMOUNT
INTO #RECO  FROM TBL_CLIENT_COLL_RECO
WHERE TRANSDATE = @RUNDATE   

SELECT @FILEDATE = ISNULL(MAX(PROCESS_DT),'APR  1 2022') FROM TBL_CLIENT_COLL_ALLOCATE
WHERE PROCESS_DT <= @RUNDATE

INSERT INTO #RECO
SELECT A.EXCHANGE, A.SEGMENT, CMCODE, TMCODE, PARTY_CODE, BOAMOUNT=ALLOCATECASH, EXCHGAMOUNT=CONVERT(NUMERIC(18,4),0) 
FROM TBL_CLIENT_COLL_ALLOCATE A, TBL_CMCC_DETAIL C
WHERE PROCESS_DT= @FILEDATE   
AND ALLOCATECASH > 0 
AND A.EXCHANGE = C.EXCHANGE 
AND A.SEGMENT = C.SEGMENT 
AND @RUNDATE BETWEEN FROM_DATE AND TO_DATE 
 
SELECT EXCHANGE, SEGMENT, CMCODE=MAX(CMCODE), TMCODE, CLIENTCODE, BOAMOUNT = SUM(BOAMOUNT), EXCHGAMOUNT = SUM(EXCHGAMOUNT),
DIFFAMOUNT = SUM(BOAMOUNT) - SUM(EXCHGAMOUNT) INTO #FINALRECO FROM #RECO  
WHERE EXCHANGE IN ('N11SX','MCX') --AND  CLIENTCODE ='AF5275'
GROUP BY EXCHANGE, SEGMENT, TMCODE, CLIENTCODE 
HAVING SUM(BOAMOUNT) - SUM(EXCHGAMOUNT) <> 0 

SELECT @MEMBERCODE = MEMBERCODE, @ORG_MEMBERCODE = MEMBERCODE  FROM OWNER 
 
SELECT PARTY_CODE, SEGMENT_INDICATOR, CP_CODE, ACCOUNT_TYPE, 
P_ALLOCATECASH=(CASE WHEN ALLOCATECASH > 0 THEN ALLOCATECASH ELSE CONVERT(NUMERIC(18,2),0) END),
C_ALLOCATECASH=CONVERT(NUMERIC(18,2),0),EXCHANGE,SEGMENT 
INTO #DATA FROM TBL_CLIENT_COLL_ALLOCATE
WHERE PROCESS_DT = @RUNDATE
AND ALLOCATECASH > 0
AND 1 = 2 

INSERT INTO #DATA 
SELECT CLIENTCODE, SEGMENT_INDICATOR = '', CP_CODE = '', ACCOUNT_TYPE = (CASE WHEN CLIENTCODE = '' THEN 'P' ELSE 'C' END),
P_ALLOCATECASH = EXCHGAMOUNT, C_ALLOCATECASH = BOAMOUNT, EXCHANGE,SEGMENT 
FROM #FINALRECO
ORDER BY  EXCHANGE,SEGMENT, CLIENTCODE

UPDATE #DATA SET SEGMENT_INDICATOR = SEG_TYPE
FROM TBL_CMCC_DETAIL T
WHERE @RUNDATE BETWEEN FROM_DATE AND TO_DATE 
AND #DATA.EXCHANGE = T.EXCHANGE AND #DATA.SEGMENT = T.SEGMENT 
 
SELECT [CURRENT DATE]=REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),106),' ','-'),
	   T.EXCHANGE, T.SEGMENT, 
	   SEGMENT_INDICATOR,
	   [CM CODE]=CMCODE,
	   [TM CODE]=(CASE WHEN ACCOUNT_TYPE = 'P' THEN '' ELSE TMCODE END),
	   [CP CODE]=CP_CODE,
	   [CLI CODE]=PARTY_CODE,
	[ACCOUNT TYPE]=ACCOUNT_TYPE,
	AMOUNT=SUM(C_ALLOCATECASH), 
	FILLER1='',
	FILLER2='',
	FILLER3='',
	FILLER4='',
	FILLER5='',
	FILLER6='',
	ACTION=(CASE WHEN SUM(C_ALLOCATECASH)-SUM(P_ALLOCATECASH) > 0 THEN 'U' ELSE 'D' END)
	INTO #FILEDATA
FROM #DATA T, TBL_CMCC_DETAIL C
WHERE @RUNDATE BETWEEN FROM_DATE AND TO_DATE
AND T.EXCHANGE = C.EXCHANGE AND T.SEGMENT=C.SEGMENT
AND T.EXCHANGE IN ('NSE','NSX','NCE')
GROUP BY CMCODE, TMCODE, T.EXCHANGE, T.SEGMENT, SEGMENT_INDICATOR, PARTY_CODE, CP_CODE, ACCOUNT_TYPE
HAVING SUM(C_ALLOCATECASH) <> SUM(P_ALLOCATECASH)
ORDER BY CMCODE, TMCODE, ACTION, PARTY_CODE, SEGMENT_INDICATOR

ALTER TABLE #FILEDATA
ADD SNO INT IDENTITY(1,1)

SELECT FILENME='NSEFULLFILE_'+REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','')+'.CSV',
[CURRENT DATE], [SEGMENT_INDICATOR], [CM CODE], [TM CODE], [CP CODE], [CLI CODE], [ACCOUNT TYPE], [AMOUNT], [FILLER1], [FILLER2], [FILLER3], [FILLER4], [FILLER5], [FILLER6], [ACTION]
FROM #FILEDATA
ORDER BY SNO 

SET @NOOFREC = 0

SELECT @NOOFREC = ISNULL(COUNT(1),0) FROM #FILEDATA
IF @NOOFREC > 0 
BEGIN
	SET @CMCUR	= CURSOR FOR
	SELECT DISTINCT [CM CODE] FROM #FILEDATA
	WHERE [CM CODE] = @ORG_MEMBERCODE
	OPEN @CMCUR
	FETCH NEXT FROM @CMCUR INTO @MEMBERCODE
	WHILE @@FETCH_STATUS = 0
	BEGIN	
		SELECT @BATCHNO = 0    
		SET @SNO = 0  
    
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0) FROM ASIANFILEBATCHNO    
		WHERE FLDFILENO = @MEMBERCODE    
		AND FLDFILEDATE = @RUNDATE  
 
		WHILE @SNO <= @NOOFREC
		BEGIN
			SET @BATCHNO = @BATCHNO + 1 
			SET @FILENAME = @MEMBERCODE + '_ALLOC_' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','') +'.T'+RIGHT('0000'+CONVERT(VARCHAR,@BATCHNO),4)

			SELECT FILENAME=@FILENAME,
			[CURRENT DATE], [SEGMENT_INDICATOR], [CM CODE], [TM CODE], [CP CODE], [CLI CODE], [ACCOUNT TYPE], [AMOUNT], [FILLER1], [FILLER2], [FILLER3], [FILLER4], [FILLER5], [FILLER6], [ACTION]
			FROM #FILEDATA
			WHERE SNO > @SNO AND SNO <= @SNO + @FILEREC
			AND [CM CODE] = @MEMBERCODE
			ORDER BY SNO 
			
			INSERT INTO TBL_CLIENT_COLL_FILE 
			SELECT FILENAME=@FILENAME,
			[CURRENT DATE], EXCHANGE, SEGMENT, [SEGMENT_INDICATOR], [CM CODE], [TM CODE], [CP CODE], [CLI CODE], [ACCOUNT TYPE], [AMOUNT], [FILLER1], [FILLER2], [FILLER3], [FILLER4], [FILLER5], [FILLER6], [ACTION],
			@BATCHNO,'CLASSBO','',GETDATE(),'G','','',GETDATE()	
			FROM #FILEDATA
			WHERE SNO > @SNO AND SNO <= @SNO + @FILEREC
			AND [CM CODE] = @MEMBERCODE
			ORDER BY SNO 			 
			
			SET @SNO = @SNO + @FILEREC
		END

		INSERT INTO ASIANFILEBATCHNO    
		SELECT @MEMBERCODE, @BATCHNO, @RUNDATE, GETDATE()

		FETCH NEXT FROM @CMCUR INTO @MEMBERCODE
	END
END


SET @NOOFREC = 0

SELECT @NOOFREC = ISNULL(COUNT(1),0) FROM #FILEDATA
IF @NOOFREC > 0 
BEGIN
	SET @CMCUR	= CURSOR FOR
	SELECT DISTINCT [CM CODE] FROM #FILEDATA
	WHERE [CM CODE] <> @ORG_MEMBERCODE
	OPEN @CMCUR
	FETCH NEXT FROM @CMCUR INTO @MEMBERCODE
	WHILE @@FETCH_STATUS = 0
	BEGIN	
		SELECT @BATCHNO = 0    
		SET @SNO = 0  
    
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0) FROM ASIANFILEBATCHNO    
		WHERE FLDFILENO = @MEMBERCODE    
		AND FLDFILEDATE = @RUNDATE  
  
		SET @BATCHNO = @BATCHNO + 1 
		SET @FILENAME = @MEMBERCODE + '_ALLOC_' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),103),'/','') +'.T'+RIGHT('0000'+CONVERT(VARCHAR,@BATCHNO),4)

		SELECT FILENAME=@FILENAME,
		[CURRENT DATE], [SEGMENT_INDICATOR], [CM CODE], [TM CODE], [CP CODE], [CLI CODE], [ACCOUNT TYPE], [AMOUNT], [FILLER1], [FILLER2], [FILLER3], [FILLER4], [FILLER5], [FILLER6], [ACTION]
		FROM #FILEDATA
		WHERE [CM CODE] = @MEMBERCODE
		ORDER BY SNO 
			
		INSERT INTO TBL_CLIENT_COLL_FILE 
		SELECT FILENAME=@FILENAME,
		[CURRENT DATE], EXCHANGE, SEGMENT, [SEGMENT_INDICATOR], [CM CODE], [TM CODE], [CP CODE], [CLI CODE], [ACCOUNT TYPE], [AMOUNT], [FILLER1], [FILLER2], [FILLER3], [FILLER4], [FILLER5], [FILLER6], [ACTION],
		@BATCHNO,'CLASSBO','',GETDATE(),'G','','',GETDATE()	
		FROM #FILEDATA
		WHERE [CM CODE] = @MEMBERCODE
		ORDER BY SNO 			 
			 
		INSERT INTO ASIANFILEBATCHNO    
		SELECT @MEMBERCODE, @BATCHNO, @RUNDATE, GETDATE()

		FETCH NEXT FROM @CMCUR INTO @MEMBERCODE
	END
END

SELECT [CURRENT DATE]=REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RUNDATE),106),' ','-'),
	   T.EXCHANGE, T.SEGMENT,
	   SEGMENT_INDICATOR,
	   [CM CODE]=CMCODE,
	   [TM CODE]=(CASE WHEN ACCOUNT_TYPE = 'P' THEN '' ELSE TMCODE END),
	   [CP CODE]=CP_CODE,
	   [CLI CODE]=PARTY_CODE,
	[ACCOUNT TYPE]=ACCOUNT_TYPE,
	AMOUNT=SUM(C_ALLOCATECASH), 
	FILLER1='',
	FILLER2='',
	FILLER3='',
	FILLER4='',
	FILLER5='',
	FILLER6='',
	ACTION='A'
	INTO #FILEDATA_ICCL
FROM #DATA T, TBL_CMCC_DETAIL C
WHERE @RUNDATE BETWEEN FROM_DATE AND TO_DATE
AND T.EXCHANGE = C.EXCHANGE AND T.SEGMENT=C.SEGMENT
AND T.EXCHANGE IN ('BSE','BSX','BCX')
GROUP BY CMCODE, TMCODE, T.EXCHANGE, T.SEGMENT, SEGMENT_INDICATOR, PARTY_CODE, CP_CODE, ACCOUNT_TYPE
HAVING SUM(C_ALLOCATECASH) <> SUM(P_ALLOCATECASH)
ORDER BY CMCODE, TMCODE, ACTION, PARTY_CODE, SEGMENT_INDICATOR

