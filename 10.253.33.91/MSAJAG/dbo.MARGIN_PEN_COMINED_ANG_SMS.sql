-- Object: PROCEDURE dbo.MARGIN_PEN_COMINED_ANG_SMS
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

    
CREATE PROC [dbo].[MARGIN_PEN_COMINED_ANG_SMS](@FROMDATE VARCHAR(11),@FROMPARTY VARCHAR(10)= 'A000000000',@TOPARTY VARCHAR(10)= 'Z9999999999')--,@TODATE VARCHAR(11))    
    
AS    
    
IF LEN(@FROMDATE) = 10 AND CHARINDEX('/', @FROMDATE) > 0      
BEGIN      
  SET @FROMDATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @FROMDATE, 103), 109)      
END      
    
DECLARE @TODATE VARCHAR(11) = @FROMDATE--,@FROMPARTY VARCHAR(10)= 'A000000000',@TOPARTY VARCHAR(10)= 'Z9999999999'    
    
-- EXEC MARGIN_PEN_COMINED_ANG_SMS 'APR 25 2022','000','ZZZ'    
    
SELECT  FORMAT(MARGIN_DATE,'dd/MM/yyyy') as MARGIN_DATE,PARTY_CODE,SHORTAGE,PENALTY_PERC,PENALTY_AMT = CONVERT(NUMERIC (18,2) , PENALTY_AMT),    
  DAYS_3_COUNT,MONTH_5_COUNT,LEDGER_POST_FLAG,SERVICE_TAX = CONVERT(NUMERIC (18,2) , SERVICE_TAX),EXCHANGE     
  INTO #MARGIN_PENALTY     
FROM (    
SELECT  MARGIN_DATE,PARTY_CODE,SHORTAGE,PENALTY_PERC,PENALTY_AMT,DAYS_3_COUNT,MONTH_5_COUNT,LEDGER_POST_FLAG,SERVICE_TAX,EXCHANGE = 'NSE'     
FROM AngelNSECM.MSAJAG.DBO.CMMARGIN_PENALTY WITH (NOLOCK)     
WHERE MARGIN_DATE BETWEEN CONVERT(DATETIME,@FROMDATE) AND @TODATE + ' 23:59' --AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY    
UNION ALL    
SELECT MARGIN_DATE,PARTY_CODE,SHORTAGE,PENALTY_PERC,PENALTY_AMT,DAYS_3_COUNT,MONTH_5_COUNT,LEDGER_POST_FLAG,SERVICE_TAX,EXCHANGE = 'NSEFO'     
FROM ANGELFO.NSEFO.DBO.FOMARGIN_PENALTY WITH (NOLOCK)     
WHERE MARGIN_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59' --AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY    
UNION ALL    
SELECT MARGIN_DATE,PARTY_CODE,SHORTAGE,PENALTY_PERC,PENALTY_AMT,DAYS_3_COUNT,MONTH_5_COUNT,LEDGER_POST_FLAG,SERVICE_TAX,EXCHANGE = 'NSECUR'     
FROM ANGELFO.NSECURFO.DBO.FOMARGIN_PENALTY WITH (NOLOCK)     
WHERE MARGIN_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59' --AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY    
UNION ALL    
SELECT  MARGIN_DATE,PARTY_CODE,SHORTAGE,PENALTY_PERC,PENALTY_AMT,DAYS_3_COUNT,MONTH_5_COUNT,LEDGER_POST_FLAG,SERVICE_TAX,EXCHANGE = 'NCDX'     
FROM ANGELCOMMODITY.NCDX.DBO.FOMARGIN_PENALTY WITH (NOLOCK)     
WHERE MARGIN_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59' --AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY    
UNION ALL    
SELECT  MARGIN_DATE,PARTY_CODE,SHORTAGE,PENALTY_PERC,PENALTY_AMT,DAYS_3_COUNT,MONTH_5_COUNT,LEDGER_POST_FLAG,SERVICE_TAX,EXCHANGE = 'MCDX'     
FROM ANGELCOMMODITY.MCDX.DBO.FOMARGIN_PENALTY WITH (NOLOCK)     
WHERE MARGIN_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59' --AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY    
 ) AS A      
    
    
SELECT PARTY_CODE,SUM(SHORTAGE) AS SHORTAGE,SUM(PENALTY_AMT+SERVICE_TAX) AS PENALTY_AMT,    
 CASE WHEN EXCHANGE = 'NSE' THEN  'Cash segment'    
      WHEN EXCHANGE = 'MCDX' THEN  'MCX'    
   WHEN EXCHANGE = 'NCDX' THEN  'NCDEX'    
   WHEN EXCHANGE = 'NSECUR' THEN  'NSE Currency'    
   WHEN EXCHANGE = 'NSEFO' THEN  'F&O' END AS EXCH,MARGIN_DATE INTO #MARGIN_PENALTY_FINAL    
FROM #MARGIN_PENALTY     
WHERE SHORTAGE >= 11    
GROUP BY PARTY_CODE,EXCHANGE,MARGIN_DATE     
ORDER BY PARTY_CODE,EXCHANGE    
    
SELECT PARTY_CODE,TEXTDATA = CONVERT (VARCHAR(1000), CONVERT(VARCHAR(1000),'Dear Client ('+ PARTY_CODE + '), ' + EXCH + '  margin shortage of Rs. ' + CONVERT(VARCHAR(1000),SHORTAGE) + ' for trading date '+     
MARGIN_DATE +' has resulted in a penalty of Rs. '+ CONVERT(VARCHAR(1000),PENALTY_AMT) +' This is posted in your ledger.') )    
FROM #MARGIN_PENALTY_FINAL ORDER BY EXCH,PARTY_CODE    
    
DROP TABLE #MARGIN_PENALTY    
DROP TABLE #MARGIN_PENALTY_FINAL

GO
