-- Object: PROCEDURE dbo.PROC_ALLOWED_INTERSETT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[PROC_ALLOWED_INTERSETT] (@SEC_PAYIN VARCHAR(11), @FLAG INT=1)
AS

--Value @FLAG  "1"  Allowed 
--Value @FLAG  "2"  Dis Allowed 
							
DECLARE @SETT_NO VARCHAR(7),
@SETTCUR CURSOR,
@SETTTYPE CURSOR,
@SETT_TYPE VARCHAR(2),
@ROW INT,
@payinflag	int

set @payinflag = 0 

IF (SELECT ISNULL(COUNT(1), 0) 
FROM (SELECT DISTINCT SETT_NO FROM SETT_MST
WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
AND SETT_TYPE = 'N') A ) >= 2
BEGIN
	set @payinflag = 1
	SET @SETTTYPE = CURSOR FOR 
		SELECT DISTINCT SETT_TYPE FROM SETT_MST
		WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
		AND SETT_TYPE IN ('N', 'W')
		ORDER BY SETT_TYPE
	OPEN @SETTTYPE
	FETCH NEXT FROM @SETTTYPE INTO @SETT_TYPE
	WHILE @@FETCH_STATUS = 0
	BEGIN
		SET @ROW = 1

		IF @FLAG = 1 
		BEGIN 
			SET @SETTCUR = CURSOR FOR
				SELECT DISTINCT SETT_NO FROM SETT_MST
				WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
				AND SETT_TYPE = @SETT_TYPE
				ORDER BY SETT_NO
			OPEN @SETTCUR
			FETCH NEXT FROM @SETTCUR INTO @SETT_NO
			WHILE @@FETCH_STATUS = 0
			BEGIN
				IF @ROW = 1 
				BEGIN
					UPDATE SETT_MST SET SEC_PAYIN = @SEC_PAYIN, SEC_PAYOUT = @SEC_PAYIN + ' 11:59:59'
					WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
					AND SETT_TYPE = @SETT_TYPE
					AND SETT_NO = @SETT_NO				
				END 
				IF @ROW = 2
				BEGIN
					UPDATE SETT_MST SET SEC_PAYIN = @SEC_PAYIN + ' 12:00', SEC_PAYOUT = @SEC_PAYIN + ' 23:59:59'
					WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
					AND SETT_TYPE = @SETT_TYPE
					AND SETT_NO = @SETT_NO
				END
				SET @ROW = @ROW + 1
				FETCH NEXT FROM @SETTCUR INTO @SETT_NO
			END
			CLOSE @SETTCUR
			DEALLOCATE @SETTCUR
		END
		IF @FLAG = 2 
		BEGIN
			UPDATE SETT_MST SET SEC_PAYIN = @SEC_PAYIN, SEC_PAYOUT = @SEC_PAYIN + ' 23:59:59'
			WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
			AND SETT_TYPE IN ('N', 'W')
		END
		FETCH NEXT FROM @SETTTYPE INTO @SETT_TYPE
	END
END


IF (SELECT ISNULL(COUNT(1), 0) 
FROM (SELECT DISTINCT SETT_NO FROM SETT_MST
WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
AND SETT_TYPE = 'M') A ) >= 2
BEGIN
	set @payinflag = 1
	SET @SETTTYPE = CURSOR FOR 
		SELECT DISTINCT SETT_TYPE FROM SETT_MST
		WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
		AND SETT_TYPE IN ('M', 'Z')
		ORDER BY SETT_TYPE
	OPEN @SETTTYPE
	FETCH NEXT FROM @SETTTYPE INTO @SETT_TYPE
	WHILE @@FETCH_STATUS = 0
	BEGIN
		SET @ROW = 1

		IF @FLAG = 1 
		BEGIN
			SET @SETTCUR = CURSOR FOR
				SELECT DISTINCT SETT_NO FROM SETT_MST
				WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
				AND SETT_TYPE = @SETT_TYPE
				ORDER BY SETT_NO
			OPEN @SETTCUR
			FETCH NEXT FROM @SETTCUR INTO @SETT_NO
			WHILE @@FETCH_STATUS = 0
			BEGIN
				IF @ROW = 1 
				BEGIN
					UPDATE SETT_MST SET SEC_PAYIN = @SEC_PAYIN, SEC_PAYOUT = @SEC_PAYIN + ' 11:59:59'
					WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
					AND SETT_TYPE = @SETT_TYPE
					AND SETT_NO = @SETT_NO				
				END 
				IF @ROW = 2
				BEGIN
					UPDATE SETT_MST SET SEC_PAYIN = @SEC_PAYIN + ' 12:00', SEC_PAYOUT = @SEC_PAYIN + ' 23:59:59'
					WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
					AND SETT_TYPE = @SETT_TYPE
					AND SETT_NO = @SETT_NO
				END
				SET @ROW = @ROW + 1
				FETCH NEXT FROM @SETTCUR INTO @SETT_NO
			END
			CLOSE @SETTCUR
			DEALLOCATE @SETTCUR
		END
		IF @FLAG = 2 
		BEGIN
			UPDATE SETT_MST SET SEC_PAYIN = @SEC_PAYIN, SEC_PAYOUT = @SEC_PAYIN + ' 23:59:59'
			WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
			AND SETT_TYPE IN ('M', 'Z')
		END
		FETCH NEXT FROM @SETTTYPE INTO @SETT_TYPE
	END
END

if @payinflag = 1 
begin
	SELECT SETT_NO, SETT_TYPE, 
	START_DATE=CONVERT(VARCHAR,START_DATE,103) + ' ' + CONVERT(VARCHAR,START_DATE,108),
	END_DATE=CONVERT(VARCHAR,END_DATE,103) + ' ' + CONVERT(VARCHAR,END_DATE,108), 
	SEC_PAYIN=CONVERT(VARCHAR,SEC_PAYIN,103) + ' ' + CONVERT(VARCHAR,SEC_PAYIN,108), 
	SEC_PAYOUT=CONVERT(VARCHAR,SEC_PAYOUT,103) + ' ' + CONVERT(VARCHAR,SEC_PAYOUT,108)
	FROM SETT_MST
	WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
			AND SETT_TYPE IN ('N', 'W','M','Z')
END
ELSE
BEGIN
	SELECT SETT_NO, SETT_TYPE, 
	START_DATE=CONVERT(VARCHAR,START_DATE,103) + ' ' + CONVERT(VARCHAR,START_DATE,108),
	END_DATE=CONVERT(VARCHAR,END_DATE,103) + ' ' + CONVERT(VARCHAR,END_DATE,108), 
	SEC_PAYIN=CONVERT(VARCHAR,SEC_PAYIN,103) + ' ' + CONVERT(VARCHAR,SEC_PAYIN,108), 
	SEC_PAYOUT=CONVERT(VARCHAR,SEC_PAYOUT,103) + ' ' + CONVERT(VARCHAR,SEC_PAYOUT,108)
	FROM SETT_MST
	WHERE SEC_PAYIN LIKE @SEC_PAYIN + '%'
		  AND SETT_TYPE IN ('N', 'W','M','Z')
	      AND 1 = 2 
END

GO
