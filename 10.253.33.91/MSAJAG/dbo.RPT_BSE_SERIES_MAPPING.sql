CREATE PROCEDURE RPT_BSE_SERIES_MAPPING (@TDAY VARCHAR(11))

AS --- EXEC RPT_BSE_SERIES_MAPPING 'OCT 19 2022'

SELECT RPT_DATE,ISIN,SCRIP_SERIES INTO #TDAY FROM TBL_SCRIP_SERIES_MAPPING WHERE RPT_DATE = @TDAY
SELECT * INTO #PDAY FROM TBL_SCRIP_SERIES_MAPPING A WHERE RPT_DATE IN (SELECT MAX(RPT_DATE) FROM TBL_SCRIP_SERIES_MAPPING B WHERE RPT_DATE < @TDAY)

SELECT A.RPT_DATE,A.ISIN,SCRIP_CODE_BSE,SCRIP_GROUP_BSE,SCRIP_NAME,SCRIP_SYMBOL,OLD_SERIES=B.SCRIP_SERIES,NEW_SERIES=A.SCRIP_SERIES
FROM #TDAY A, #PDAY B 
WHERE A.ISIN = B.ISIN AND A.SCRIP_SERIES <> B.SCRIP_SERIES
ORDER BY 2

GO
