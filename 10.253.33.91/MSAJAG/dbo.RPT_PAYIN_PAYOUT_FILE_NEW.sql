-- Object: PROCEDURE dbo.RPT_PAYIN_PAYOUT_FILE_NEW
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[RPT_PAYIN_PAYOUT_FILE_NEW]  
(  
 @SEC_PAYIN VARCHAR(11)  
)  
AS    
DECLARE @RPTDATE VARCHAR(11)
SELECT @RPTDATE = GETDATE()
DECLARE @FILENAME   VARCHAR(50)  
  
DECLARE @BATCHNO INT  
  
SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
WHERE FLDFILENO = 'SS'  
AND FLDFILEDATE = @SEC_PAYIN  
  
IF @BATCHNO = 1  
BEGIN  
 INSERT INTO ASIANFILEBATCHNO VALUES ('SS', @BATCHNO, @SEC_PAYIN, GETDATE())  
END  
ELSE  
BEGIN  
 UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'SS' AND FLDFILEDATE = @SEC_PAYIN  
END  
  
SET @FILENAME = 'Shortage_reporting_'   
        + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@RPTDATE),103),'/','') + '.T'   
     + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2)  
--ddmmyyyy.Xnn   
  
 -- EXEC RPT_PAYIN_PAYOUT_FILE 'NOV  5 2018'         
  
SELECT D.SETT_NO, D.SETT_TYPE,   
MARKETTYPE = (CASE WHEN D.SETT_TYPE = 'N' THEN '01'   
       WHEN D.SETT_TYPE = 'W' THEN '02'  
	   WHEN D.SETT_TYPE = 'M' THEN '02'  
	   WHEN D.SETT_TYPE = 'Z' THEN '02'
       WHEN D.SETT_TYPE = 'A' THEN '03'  
       WHEN D.SETT_TYPE = 'L' THEN '04'  
       WHEN D.SETT_TYPE = 'P' THEN '04'  
       WHEN D.SETT_TYPE = 'F' THEN '05'   
    ELSE ''  
   END),  
D.PARTY_CODE, ISIN=CONVERT(VARCHAR(12),''),SCRIP_CD, D.SERIES, SELL_BUY = (CASE WHEN DRCR = 'C' THEN 'S' ELSE 'B' END),    
SHORTQTY = CONVERT(NUMERIC(18,0),QTY), AUCPAYOUTQTY = 0, AUCCLOSEOUTQTY = 0, BALANCEQTY = 0, RECOFLAG = '0',     
AUCSETTNO=FILLER1,  AUCSETTTYPE=(CASE WHEN D.SETT_TYPE = 'N' THEN 'A'    
              WHEN D.SETT_TYPE = 'W' THEN 'X'    
              WHEN D.SETT_TYPE = 'F' THEN 'AF'    
			  WHEN D.SETT_TYPE = 'M' THEN 'A'   
			  WHEN D.SETT_TYPE = 'Z' THEN 'X'
              WHEN D.SETT_TYPE = 'L' THEN 'Q'    
              WHEN D.SETT_TYPE = 'P' THEN 'Q'    
           ELSE '' END), SETTDATE = REPLACE(CONVERT(VARCHAR,SEC_PAYIN,103),'/','')  
INTO #DATA    
FROM ANGELDEMAT.MSAJAG.DBO.DELTRANS_REPORT D, /*DELSERVER.*/MSAJAG.DBO.SETT_MST S  
WHERE D.SETT_NO = S.SETT_NO AND D.SETT_TYPE = S.SETT_TYPE    
AND FILLER2 = 1 AND SHARETYPE = 'AUCTION'    
AND TRTYPE <> 906     
AND S.SEC_PAYIN >= @SEC_PAYIN AND S.SEC_PAYIN <= @SEC_PAYIN + ' 23:59'  
AND D.SETT_TYPE IN ('N', 'W', 'L','P', 'F')  
    
UPDATE #DATA SET ISIN = M.ISIN FROM ANGELDEMAT.MSAJAG.DBO.MULTIISIN M    
WHERE M.SCRIP_CD = #DATA.SCRIP_CD     
AND M.SERIES IN ('EQ','BE','BZ')    
AND #DATA.series IN ('EQ','BE','BZ')    
AND VALID = 1     
    
UPDATE #DATA SET ISIN = M.ISIN FROM ANGELDEMAT.MSAJAG.DBO.MULTIISIN M    
WHERE M.SCRIP_CD = #DATA.SCRIP_CD     
AND M.SERIES = #DATA.series     
AND #DATA.series NOT IN ('EQ','BE','BZ')    
AND VALID = 1     
    

UPDATE #DATA  SET AUCPAYOUTQTY = QTY     
FROM (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM SETTLEMENT D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FS'   AND MarketRate = 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D  
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE   
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES  
  
UPDATE #DATA  SET AUCCLOSEOUTQTY = QTY   
FROM (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM SETTLEMENT D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FC' --  AND MarketRate > 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D  
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE   
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES  


UPDATE #DATA  SET AUCPAYOUTQTY = QTY     
FROM (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM HISTORY D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FS'   AND MarketRate = 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D  
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE   
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES  
  
UPDATE #DATA  SET AUCCLOSEOUTQTY = QTY   
FROM (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM HISTORY D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FC' --  AND MarketRate > 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D  
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE   
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES 

SELECT #DATA.*,QTY
INTO #CLOUT
FROM   (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM SETTLEMENT D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FC' --  AND MarketRate > 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D , #DATA 
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE 
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES 

INSERT INTO #CLOUT
SELECT #DATA.*,QTY
FROM   (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM HISTORY D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FC' --  AND MarketRate > 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D , #DATA 
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE 
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES 

SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, QTY = SUM(CASE WHEN SELL_BUY = 'S' THEN -QTY ELSE QTY END) 
INTO #CLOUTFINAL FROM #CLOUT 
WHERE SETT_TYPE IN ('D','N', 'F', 'M')
GROUP BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES
HAVING SUM(CASE WHEN SELL_BUY = 'S' THEN -QTY ELSE QTY END) > 0 

UPDATE  #DATA  SET AUCCLOSEOUTQTY = 0 WHERE SETT_TYPE IN ('D','N', 'F', 'M')
  
DECLARE @SETTCUR CURSOR,
		@DELCUR	CURSOR,
		@SETT_NO	VARCHAR(7),
		@SETT_TYPE	VARCHAR(2),
		@PARTY_CODE VARCHAR(10),
		@SCRIP_CD	VARCHAR(12),
		@SERIES	VARCHAR(3),
		@QTY		INT,
		@AUCQTY		INT 

SET @SETTCUR = CURSOR FOR 				    
 SELECT SETT_NO, SETT_TYPE, SCRIP_CD, SERIES, QTY FROM #CLOUTFINAL
 WHERE QTY > 0 
 ORDER BY SETT_NO, SETT_TYPE, SCRIP_CD, SERIES
OPEN @SETTCUR
FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE, @SCRIP_CD, @SERIES, @QTY
WHILE @@FETCH_STATUS = 0
BEGIN
	IF @QTY > 0 
	BEGIN
		SET @DELCUR = CURSOR FOR
		SELECT PARTY_CODE, AUCQTY=SHORTQTY - AUCPAYOUTQTY FROM #DATA 
		WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE
		AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES 
		AND SELL_BUY = 'B' AND (SHORTQTY - AUCPAYOUTQTY) > 0 
		ORDER BY PARTY_CODE 
		OPEN @DELCUR
		FETCH NEXT FROM @DELCUR INTO @PARTY_CODE, @AUCQTY
		WHILE @@FETCH_STATUS = 0 
		BEGIN
			IF @QTY > 0  
			BEGIN
				IF @QTY >= @AUCQTY 
				BEGIN
					UPDATE #DATA SET AUCCLOSEOUTQTY = AUCCLOSEOUTQTY + @AUCQTY 
					WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE
					AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES 
					AND SELL_BUY = 'B' AND PARTY_CODE =@PARTY_CODE

					SET @QTY = @QTY - @AUCQTY
				END
				ELSE
				BEGIN
					UPDATE #DATA SET AUCCLOSEOUTQTY = AUCCLOSEOUTQTY + @QTY 
					WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE
					AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES 
					AND SELL_BUY = 'B' AND PARTY_CODE =@PARTY_CODE

					SET @QTY = 0
				END
			END
			ELSE
			BEGIN
				BREAK;
			END
			FETCH NEXT FROM @DELCUR INTO @PARTY_CODE, @AUCQTY
		END
		CLOSE @DELCUR
		DEALLOCATE @DELCUR
	END
	FETCH NEXT FROM @SETTCUR INTO @SETT_NO, @SETT_TYPE, @SCRIP_CD, @SERIES, @QTY
END 
CLOSE @SETTCUR
DEALLOCATE @SETTCUR

UPDATE #DATA  SET AUCPAYOUTQTY = AUCPAYOUTQTY+QTY    
FROM (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM SETTLEMENT D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FS'   AND MarketRate > 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D  
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE   
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES  
  
UPDATE #DATA  SET AUCPAYOUTQTY = AUCPAYOUTQTY+QTY    
FROM (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM HISTORY D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FS'   AND MarketRate > 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D  
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE   
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES 
 
UPDATE #DATA  SET RECOFLAG=1  
FROM (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM SETTLEMENT D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FP'   AND MarketRate = 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D  
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE   
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES  
  
UPDATE #DATA  SET RECOFLAG=1
FROM (SELECT  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES, QTY = SUM(TRADEQTY)  
   FROM HISTORY D, SETT_MST S WHERE D.SETT_NO = S.SETT_NO AND S.SETT_TYPE = D.SETT_TYPE  
  AND S.START_DATE >= CONVERT(DATETIME,@SEC_PAYIN) - 1 AND S.START_DATE <= CONVERT(DATETIME,@SEC_PAYIN) + 10 
  AND AuctionPart = 'FP'   AND MarketRate = 0   
  GROUP BY  D.SETT_NO, D.SETT_TYPE, PARTY_CODE, SCRIP_CD, D.SERIES) D  
WHERE D.SETT_NO = #DATA.AUCSETTNO AND D.SETT_TYPE = #DATA.AUCSETTTYPE   
AND D.PARTY_CODE = #DATA.PARTY_CODE AND D.Scrip_Cd = #DATA.scrip_cd  AND D.SERIES = #DATA.SERIES 
  
  
UPDATE #DATA SET BALANCEQTY = SHORTQTY - (AUCPAYOUTQTY + AUCCLOSEOUTQTY) 
UPDATE #DATA SET RECOFLAG = 1 WHERE PARTY_CODE = 'Y04041'  
--UPDATE #DATA SET RECOFLAG = 2 WHERE BALANCEQTY > 0 AND RECOFLAG = 0
UPDATE #DATA SET RECOFLAG = 3 WHERE RECOFLAG = 0 AND BALANCEQTY > 0  

UPDATE #DATA SET AUCCLOSEOUTQTY =AUCCLOSEOUTQTY+AUCPAYOUTQTY,AUCPAYOUTQTY=0 
WHERE SELL_BUY = 'S' AND AUCPAYOUTQTY > 0 

---Shoratage reporting changes Start
UPDATE  #DATA SET RECOFLAG = 0 , AUCCLOSEOUTQTY = AUCCLOSEOUTQTY + BALANCEQTY, BALANCEQTY =0 WHERE RECOFLAG = 3
---Shoratage reporting changes End

 
SELECT FLAG = 1, DATA = '10,' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@SEC_PAYIN),103),'/','') + ',' +   
  CONVERT(VARCHAR,COUNT(1)),   
  SETT_NO='', SETT_TYPE='', PARTY_CODE='', SCRIP_CD=''  
INTO #REPORT  
FROM #DATA  
UNION ALL  
SELECT FLAG = 2, DATA = '20,'  
  + CONVERT(VARCHAR,MARKETTYPE) + ','   
  + CONVERT(VARCHAR,LTRIM(RTRIM(SETT_TYPE))) + CONVERT(VARCHAR,LTRIM(RTRIM(SETT_NO))) + ','   
  + CONVERT(VARCHAR,LTRIM(RTRIM(PARTY_CODE))) + ','   
  + CONVERT(VARCHAR,ISIN) + ','   
  + CONVERT(VARCHAR,LTRIM(RTRIM(SCRIP_CD))) + ','   
  + CONVERT(VARCHAR,SELL_BUY) + ','   
  + CONVERT(VARCHAR,SHORTQTY) + ','   
  + CONVERT(VARCHAR,AUCPAYOUTQTY) + ','   
  + CONVERT(VARCHAR,AUCCLOSEOUTQTY) + ','   
  + CONVERT(VARCHAR,BALANCEQTY) + ','   
  + CONVERT(VARCHAR,RECOFLAG),   
  SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD    
FROM #DATA   
ORDER BY FLAG, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD  
  
SELECT FILENAME = @FILENAME, DATA FROM #REPORT

GO
