-- Object: PROCEDURE dbo.RPT_RMSREPORT_ORG
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [DBO].[RPT_RMSREPORT_ORG]         
(    
 @STATUSID VARCHAR(25),        
 @STATUSNAME VARCHAR(25),        
 @RMS_DATE VARCHAR(11),        
 @BRANCH_CD VARCHAR(10),    
 @CL_TYPE VARCHAR(3),        
 @FROMPARTY VARCHAR(10),        
 @TOPARTY VARCHAR(10),    
 @RPTTYPE VARCHAR(1),    
 @RPTSTYLE VARCHAR(1),      
 @C_FLAG SMALLINT    
)    
    
AS        
      
-- EXEC RPT_RMSREPORT 'BROKER', 'BROKER', 'FEB 16 2006', '%', '%', '0', 'ZZZZZZZZZ'        
      
SET   @FROMPARTY = (CASE WHEN LEN(@FROMPARTY) = 0 THEN '0' ELSE @FROMPARTY END)      
SET   @TOPARTY = (CASE WHEN LEN(@TOPARTY) = 0 THEN 'ZZZZZZZZZ' ELSE @TOPARTY END)      
    
IF @RPTTYPE = 'S' AND @RPTSTYLE = 'P' -- SUMMARY AT PARENT LEVEL      
BEGIN       
 SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED        
 SELECT C1.PARENTCODE, C1.CL_TYPE, C1.LONG_NAME, C1.BRANCH_CD, C1.SUB_BROKER,        
 CNC = 0,        
 MLM = SUM(MLM),        
 FUNDSPAYOUT = SUM(FUNDS_PAYOUT),        
 STOCKPAYOUT = SUM(SEC_PAYOUT),      
 VDTLEDBAL=SUM(VDTLEDBAL),EDTLEDBAL=SUM(EDTLEDBAL),      
 UNCLEDBAL=SUM(UNCLEDBAL),OPENDEBITBILL=SUM(OPENDEBITBILL),      
 FOMARGIN=SUM(FOMARGIN),    
 CASHCOLL=SUM(CASHCOLL),    
 UTIL_CC = (CASE WHEN SUM(CASHCOLL) + SUM(NONCASHCOLL) - SUM(FOMARGIN) > 0 AND SUM(NONCASHCOLL) - SUM(FOMARGIN) < 0    
     THEN SUM(FOMARGIN) - SUM(NONCASHCOLL)    
     ELSE (CASE WHEN SUM(NONCASHCOLL) - SUM(FOMARGIN) < 0 AND SUM(CASHCOLL) > 0     
       THEN SUM(FOMARGIN) - SUM(NONCASHCOLL) - SUM(CASHCOLL)    
       ELSE 0     
        END)    
   END),    
 NONCASHCOLL=SUM(NONCASHCOLL),    
 UTIL_NCC = (CASE WHEN SUM(NONCASHCOLL) >= SUM(FOMARGIN) THEN SUM(FOMARGIN) ELSE SUM(NONCASHCOLL) END),    
 UN_BENHOLD=SUM(UN_BENHOLD),BENHOLD=SUM(BENHOLD),      
 UN_POAHOLD=SUM(UN_POAHOLD),POAHOLD=SUM(POAHOLD),POOLHOLD=SUM(POOLHOLD),    
 UN_BUYOPENPOS=SUM(UN_BUYOPENPOS),BUYOPENPOS=SUM(BUYOPENPOS),      
 SELLOPENPOS=SUM(SELLOPENPOS),SELLSHORT=SUM(SELLSHORT),    
 FOMARGINSHORTFALL=SUM(FOMARGINSHORTFALL),NDBUYPOS=SUM(NDBUYPOS),EPN_PNL=SUM(EPN_PNL),  
 CREDIT_LIMIT = SUM(CREDIT_LIMIT),  
 DPC = SUM(DPC), SPC = SUM(SPC), NDA = SUM(NDA), TAV = SUM(TAV),
 UN_BENHOLD_ORG=SUM(UN_BENHOLD_ORG),
 BENHOLD_ORG=SUM(BENHOLD_ORG),
 POOLHOLD_ORG  =SUM(POOLHOLD_ORG)
 INTO #RMS_1    
 FROM TBL_RMS_SUMMARY T, CLIENT_DETAILS C1        
 WHERE RMS_DATE = @RMS_DATE        
 AND T.CL_TYPE LIKE (CASE WHEN @CL_TYPE = '%' THEN T.CL_TYPE ELSE @CL_TYPE END)        
 AND T.PARTY_CODE = C1.CL_CODE        
 AND C1.PARENTCODE BETWEEN @FROMPARTY AND @TOPARTY        
 AND C1.BRANCH_CD LIKE (CASE WHEN @BRANCH_CD = '%' THEN BRANCH_CD ELSE @BRANCH_CD END)        
 AND @STATUSNAME =                   
       (CASE                   
     WHEN @STATUSID = 'BRANCH' THEN C1.BRANCH_CD                  
     WHEN @STATUSID = 'SUBBROKER' THEN C1.SUB_BROKER                  
     WHEN @STATUSID = 'TRADER' THEN C1.TRADER                  
     WHEN @STATUSID = 'FAMILY' THEN C1.FAMILY                  
     WHEN @STATUSID = 'AREA' THEN C1.AREA                  
     WHEN @STATUSID = 'REGION' THEN C1.REGION                  
     WHEN @STATUSID = 'CLIENT' THEN C1.CL_CODE                  
       ELSE                   
    'BROKER'                  
       END)        
 GROUP BY C1.PARENTCODE, C1.CL_TYPE, C1.LONG_NAME, C1.BRANCH_CD, C1.SUB_BROKER      
    
 SELECT * FROM  #RMS_1     
 ORDER BY PARENTCODE, LONG_NAME      
END      
ELSE IF   @RPTTYPE = 'S' AND @RPTSTYLE = 'C' -- SUMMARY AT CHILD LEVEL      
BEGIN       
 SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED        
 SELECT  C1.PARENTCODE, T.PARTY_CODE, T.CL_TYPE, C1.LONG_NAME, BRANCH_CD, SUB_BROKER,        
 CNC = 0,        
 MLM = MLM,        
 FUNDSPAYOUT = FUNDS_PAYOUT,        
 STOCKPAYOUT = SEC_PAYOUT,      
 VDTLEDBAL,EDTLEDBAL,UNCLEDBAL,OPENDEBITBILL,FOMARGIN,    
 CASHCOLL,    
 UTIL_CC = (CASE WHEN CASHCOLL + NONCASHCOLL - FOMARGIN > 0 AND NONCASHCOLL - FOMARGIN < 0    
     THEN FOMARGIN - NONCASHCOLL    
     ELSE (CASE WHEN NONCASHCOLL - FOMARGIN < 0 AND CASHCOLL > 0     
       THEN FOMARGIN - NONCASHCOLL - CASHCOLL    
       ELSE 0     
        END)    
   END),    
 NONCASHCOLL,    
 UTIL_NCC = (CASE WHEN NONCASHCOLL >= FOMARGIN THEN FOMARGIN ELSE NONCASHCOLL END),    
 UN_BENHOLD,BENHOLD,      
 UN_POAHOLD,POAHOLD,POOLHOLD,UN_BUYOPENPOS,BUYOPENPOS,SELLOPENPOS,SELLSHORT,FOMARGINSHORTFALL,NDBUYPOS,EPN_PNL,  
 CREDIT_LIMIT, DPC, SPC, NDA, TAV,
 UN_BENHOLD_ORG,
 BENHOLD_ORG,
 POOLHOLD_ORG
 INTO #RMS_2    
 FROM TBL_RMS_SUMMARY T, CLIENT_DETAILS C1        
 WHERE RMS_DATE = @RMS_DATE        
 AND T.CL_TYPE LIKE (CASE WHEN @CL_TYPE = '%' THEN T.CL_TYPE ELSE @CL_TYPE END)        
 AND T.PARTY_CODE = C1.CL_CODE        
 AND T.PARTY_CODE BETWEEN (CASE WHEN @C_FLAG = 0 THEN @FROMPARTY ELSE '0' END)       
      AND (CASE WHEN @C_FLAG = 0 THEN @TOPARTY ELSE 'ZZZZZZZZZZ' END)       
 AND C1.PARENTCODE BETWEEN (CASE WHEN @C_FLAG = 1 THEN @FROMPARTY ELSE '0' END)       
       AND (CASE WHEN @C_FLAG = 1 THEN @TOPARTY ELSE 'ZZZZZZZZZZ' END)       
 AND C1.BRANCH_CD LIKE (CASE WHEN @BRANCH_CD = '%' THEN BRANCH_CD ELSE @BRANCH_CD END)        
 AND @STATUSNAME =                   
       (CASE                   
     WHEN @STATUSID = 'BRANCH' THEN C1.BRANCH_CD                  
     WHEN @STATUSID = 'SUBBROKER' THEN C1.SUB_BROKER                  
     WHEN @STATUSID = 'TRADER' THEN C1.TRADER                  
     WHEN @STATUSID = 'FAMILY' THEN C1.FAMILY                  
     WHEN @STATUSID = 'AREA' THEN C1.AREA                  
     WHEN @STATUSID = 'REGION' THEN C1.REGION                  
     WHEN @STATUSID = 'CLIENT' THEN C1.CL_CODE                  
       ELSE                   
     'BROKER'                  
       END)        
    
 SELECT * FROM #RMS_2    
 ORDER BY PARTY_CODE, LONG_NAME      
END      
ELSE IF   @RPTTYPE = 'D' AND @RPTSTYLE = 'P' -- DETAIL AT PARENT LEVEL      
BEGIN       
 SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED        
 SELECT T.EXCHANGE, T.SEGMENT, C1.PARENTCODE, C1.CL_TYPE, C1.LONG_NAME, C1.BRANCH_CD, C1.SUB_BROKER,        
 CNC = 0,        
 MLM = SUM(MLM),        
 FUNDSPAYOUT = SUM(FUNDS_PAYOUT),        
 STOCKPAYOUT = SUM(SEC_PAYOUT),       
 VDTLEDBAL=SUM(VDTLEDBAL),EDTLEDBAL=SUM(EDTLEDBAL),      
    UNCLEDBAL=SUM(UNCLEDBAL),OPENDEBITBILL=SUM(OPENDEBITBILL),      
 FOMARGIN=SUM(FOMARGIN),    
 CASHCOLL=SUM(CASHCOLL),    
 UTIL_CC = (CASE WHEN SUM(CASHCOLL) + SUM(NONCASHCOLL) - SUM(FOMARGIN) > 0 AND SUM(NONCASHCOLL) - SUM(FOMARGIN) < 0    
     THEN SUM(FOMARGIN) - SUM(NONCASHCOLL)    
     ELSE (CASE WHEN SUM(NONCASHCOLL) - SUM(FOMARGIN) < 0 AND SUM(CASHCOLL) > 0     
       THEN SUM(FOMARGIN) - SUM(NONCASHCOLL) - SUM(CASHCOLL)    
       ELSE 0     
        END)    
   END),    
 NONCASHCOLL=SUM(NONCASHCOLL),    
 UTIL_NCC = (CASE WHEN SUM(NONCASHCOLL) >= SUM(FOMARGIN) THEN SUM(FOMARGIN) ELSE SUM(NONCASHCOLL) END),    
 UN_BENHOLD=SUM(UN_BENHOLD),BENHOLD=SUM(BENHOLD),      
 UN_POAHOLD=SUM(UN_POAHOLD),POAHOLD=SUM(POAHOLD),POOLHOLD=SUM(POOLHOLD),UN_BUYOPENPOS=SUM(UN_BUYOPENPOS),BUYOPENPOS=SUM(BUYOPENPOS),      
 SELLOPENPOS=SUM(SELLOPENPOS),SELLSHORT=SUM(SELLSHORT),FOMARGINSHORTFALL=SUM(FOMARGINSHORTFALL),NDBUYPOS=SUM(NDBUYPOS),EPN_PNL=SUM(EPN_PNL),  
 CREDIT_LIMIT = SUM(CREDIT_LIMIT), DPC = SUM(DPC), SPC = SUM(SPC), NDA = SUM(NDA), TAV = SUM(TAV),
 UN_BENHOLD_ORG=SUM(UN_BENHOLD_ORG),
 BENHOLD_ORG=SUM(BENHOLD_ORG),
 POOLHOLD_ORG  =SUM(POOLHOLD_ORG)  
 INTO #RMS_3    
 FROM TBL_RMS_DATA T, CLIENT_DETAILS C1        
 WHERE RMS_DATE = @RMS_DATE        
 AND T.CL_TYPE LIKE (CASE WHEN @CL_TYPE = '%' THEN T.CL_TYPE ELSE @CL_TYPE END)        
 AND T.PARTY_CODE = C1.CL_CODE        
 AND C1.PARENTCODE BETWEEN @FROMPARTY AND @TOPARTY        
 AND C1.BRANCH_CD LIKE (CASE WHEN @BRANCH_CD = '%' THEN BRANCH_CD ELSE @BRANCH_CD END)        
 AND @STATUSNAME =                   
   (CASE                   
       WHEN @STATUSID = 'BRANCH' THEN C1.BRANCH_CD                  
       WHEN @STATUSID = 'SUBBROKER' THEN C1.SUB_BROKER                  
       WHEN @STATUSID = 'TRADER' THEN C1.TRADER                  
       WHEN @STATUSID = 'FAMILY' THEN C1.FAMILY                  
       WHEN @STATUSID = 'AREA' THEN C1.AREA                  
       WHEN @STATUSID = 'REGION' THEN C1.REGION                  
       WHEN @STATUSID = 'CLIENT' THEN C1.CL_CODE                  
       ELSE                   
       'BROKER'                  
   END)    
 GROUP BY T.EXCHANGE, T.SEGMENT, C1.PARENTCODE, C1.CL_TYPE, C1.LONG_NAME, C1.BRANCH_CD, C1.SUB_BROKER      
    
 SELECT * FROM #RMS_3    
 ORDER BY PARENTCODE, LONG_NAME, EXCHANGE, SEGMENT      
    
END      
ELSE IF   @RPTTYPE = 'D' AND @RPTSTYLE = 'C' -- DETAIL AT CHILD LEVEL      
BEGIN       
 SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED        
 SELECT  T.EXCHANGE, T.SEGMENT, C1.PARENTCODE, T.PARTY_CODE, T.CL_TYPE, C1.LONG_NAME, BRANCH_CD, SUB_BROKER,        
 CNC = 0,        
 MLM = MLM,        
 FUNDSPAYOUT = FUNDS_PAYOUT,        
 STOCKPAYOUT = SEC_PAYOUT,     
 VDTLEDBAL,EDTLEDBAL,UNCLEDBAL,OPENDEBITBILL,FOMARGIN,    
 CASHCOLL,    
 UTIL_CC = (CASE WHEN CASHCOLL + NONCASHCOLL - FOMARGIN > 0 AND NONCASHCOLL - FOMARGIN < 0    
     THEN FOMARGIN - NONCASHCOLL    
     ELSE (CASE WHEN NONCASHCOLL - FOMARGIN < 0 AND CASHCOLL > 0     
       THEN FOMARGIN - NONCASHCOLL - CASHCOLL    
       ELSE 0     
        END)    
   END),    
 NONCASHCOLL,    
 UTIL_NCC = (CASE WHEN NONCASHCOLL >= FOMARGIN THEN FOMARGIN ELSE NONCASHCOLL END),    
 UN_BENHOLD,BENHOLD,      
 UN_POAHOLD,POAHOLD,POOLHOLD,UN_BUYOPENPOS,BUYOPENPOS,SELLOPENPOS,SELLSHORT,FOMARGINSHORTFALL,NDBUYPOS,EPN_PNL,  
 CREDIT_LIMIT, DPC, SPC, NDA, TAV,
 UN_BENHOLD_ORG,
 BENHOLD_ORG,
 POOLHOLD_ORG
 INTO #RMS_4    
 FROM TBL_RMS_DATA T, CLIENT_DETAILS C1        
 WHERE RMS_DATE = @RMS_DATE        
 AND T.CL_TYPE LIKE (CASE WHEN @CL_TYPE = '%' THEN T.CL_TYPE ELSE @CL_TYPE END)        
 AND T.PARTY_CODE = C1.CL_CODE        
 AND T.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY        
 AND C1.BRANCH_CD LIKE (CASE WHEN @BRANCH_CD = '%' THEN BRANCH_CD ELSE @BRANCH_CD END)        
 AND @STATUSNAME =                   
       (CASE                   
       WHEN @STATUSID = 'BRANCH' THEN C1.BRANCH_CD                  
       WHEN @STATUSID = 'SUBBROKER' THEN C1.SUB_BROKER                  
       WHEN @STATUSID = 'TRADER' THEN C1.TRADER                  
       WHEN @STATUSID = 'FAMILY' THEN C1.FAMILY                  
       WHEN @STATUSID = 'AREA' THEN C1.AREA                  
       WHEN @STATUSID = 'REGION' THEN C1.REGION                  
       WHEN @STATUSID = 'CLIENT' THEN C1.CL_CODE                  
       ELSE                   
       'BROKER'                  
       END)    
    
 SELECT * FROM #RMS_4    
 ORDER BY PARTY_CODE, LONG_NAME, EXCHANGE, SEGMENT      
END

GO
