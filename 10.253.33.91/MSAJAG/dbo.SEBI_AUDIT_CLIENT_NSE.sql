CREATE PROC [dbo].[SEBI_AUDIT_CLIENT_NSE]  
(  
 @REPORTDATE VARCHAR(11)  
)  
AS  
  
DECLARE @NSEMEMBERCODE VARCHAR(10),          
  @BSEMEMBERCODE VARCHAR(10),  
  @SEGMENT VARCHAR(3)  
  
SET @SEGMENT = ''    
--SET @SEGMENT = 'NSE'        
          
SELECT @NSEMEMBERCODE = MEMBERCODE FROM MSAJAG.DBO.OWNER          
SELECT @BSEMEMBERCODE = MEMBERCODE FROM [AngelBSECM].BSEDB_AB.DBO.OWNER  
  
CREATE TABLE #CLIENT_LIST  
(  
 CLTCODE VARCHAR(10)  
)  
  
INSERT INTO #CLIENT_LIST  
EXEC GET_SEBI_AUDIT_CLIENT_LIST 'NSE' /*,@REPORTDATE  */      
          
SELECT PARTY_NAME, PARTY_CODE, PAN_GIR_NO = CONVERT(VARCHAR(35), ''),    
EMAIL_ID = CONVERT(VARCHAR(100), ''), MOBILENO = CONVERT(VARCHAR(50), ''),  
NSECMBAL = SUM(ISNULL(CM_VDTBAL,0)) + SUM(ISNULL(FO_VDTBAL,0)) + SUM(ISNULL(NSX_VDTBAL,0)) + SUM(ISNULL(NSEMF_VDTBAL,0)),  
TOTAL_VDTBAL = SUM(ISNULL( TOTAL_VDTBAL ,0)),   
TOT_ISIN = 0, TOT_ISIN_QTY = CONVERT(NUMERIC(18,3),0),
TOT_ISIN_PLEDGE=0,  
QTY_PLEDGE = CONVERT(NUMERIC(18,3),0),  
FUND_RAISE = CONVERT(NUMERIC(18,4),0),  
LAST_SETT_DATE = CONVERT(VARCHAR(9), '')  
INTO #DATA  
FROM ACCOUNT.DBO.FINAL_AUDIT_DATA D  
WHERE EFFDATE = @REPORTDATE  
AND CL_TYPE <> 'PRO'          
AND PARTY_CODE NOT IN (@NSEMEMBERCODE, @BSEMEMBERCODE)          
AND NOT EXISTS (SELECT PARTY_CODE FROM ACCOUNT.DBO.SEBI_EXCEP_PARTY S       
       WHERE S.PARTY_CODE = D.PARTY_CODE)   
AND EXISTS(SELECT CLTCODE FROM #CLIENT_LIST L WHERE D.PARTY_CODE = CLTCODE)  
GROUP BY PARTY_CODE, PARTY_NAME 
order by PARTY_CODE
  
SELECT PARTY_CODE, ISIN = CERTNO, QTY = SUM(DEBITQTY + FUTQTY + PAYQTY), CASH_COLL = CONVERT(NUMERIC(18,3),0), TOT_CASH_COLL = CONVERT(NUMERIC(18,3),0)  
INTO #ISIN  
FROM MSAJAG.DBO.DELDEBITSUMMARYNEW  
WHERE RUNDATE >= @REPORTDATE AND RUNDATE <= @REPORTDATE + ' 23:59:59'  
--AND HOLDFLAG NOT IN ('POA')  
and holdflag in ('HOLD')
--AND BRANCH_CD = CASE WHEN @SEGMENT <> '' THEN @SEGMENT ELSE BRANCH_CD END  
GROUP BY PARTY_CODE, CERTNO  

INSERT INTO #ISIN  
SELECT PARTY_CODE, ISIN, QTY = SUM(QTY), CASH_COLL = SUM(CASE WHEN COLL_TYPE = 'MARGIN' AND CASH_NCASH = 'C' AND EXCHANGE IN('NSE', 'NSX') THEN FINALAMOUNT ELSE 0 END), TOT_CASH_COLL = SUM(CASE WHEN COLL_TYPE = 'MARGIN' AND CASH_NCASH = 'C' THEN FINALAMOUNT ELSE 0 END)  
FROM MSAJAG.DBO.COLLATERALDETAILS  
WHERE EFFDATE >= @REPORTDATE AND EFFDATE <= @REPORTDATE + ' 23:59:59'  
AND EXCHANGE = CASE WHEN @SEGMENT <> '' THEN @SEGMENT ELSE EXCHANGE END  
GROUP BY PARTY_CODE, ISIN  
--HAVING SUM(QTY) > 0  

UPDATE #DATA SET NSECMBAL = NSECMBAL + ISNULL(CASH_COLL, 0), TOTAL_VDTBAL = TOTAL_VDTBAL + ISNULL(TOT_CASH_COLL, 0)  
FROM (SELECT PARTY_CODE, ISIN_CNT = CASE WHEN SUM(QTY) > 0 THEN COUNT(DISTINCT ISIN) ELSE 0 END, QTY_SUM = SUM(QTY), CASH_COLL = SUM(CASH_COLL), TOT_CASH_COLL = SUM(TOT_CASH_COLL) FROM #ISIN   
   GROUP BY PARTY_CODE) A  
WHERE #DATA.PARTY_CODE = A.PARTY_CODE  
  
DELETE FROM #ISIN WHERE QTY = 0  

INSERT INTO #ISIN  
SELECT D.PARTY_CODE, D.CERTNO, QTY = SUM(QTY), CASH_COLL = 0, TOT_CASH_COLL = 0   
FROM DELTRANS D ,SETT_MST S WHERE DRCR='C' AND FILLER2='1'  
AND SHARETYPE<>'AUCTION' AND D.SETT_TYPE=S.SETT_TYPE  
AND D.SETT_NO=S.SETT_NO AND S.SEC_PAYIN > @REPORTDATE AND START_DATE <= @REPORTDATE  
GROUP BY D.PARTY_CODE, D.CERTNO  

INSERT INTO #ISIN
SELECT D.PARTY_CODE, D.CERTNO, QTY = SUM(QTY), CASH_COLL = 0, TOT_CASH_COLL = 0   
FROM [AngelBSECM].BSEDB_AB.dbo.DELTRANS D , [AngelBSECM].BSEDB_AB.dbo.SETT_MST S WHERE DRCR='C' AND FILLER2='1'  
AND SHARETYPE<>'AUCTION' AND D.SETT_TYPE=S.SETT_TYPE  
AND D.SETT_NO=S.SETT_NO AND S.SEC_PAYIN > @REPORTDATE AND START_DATE <= @REPORTDATE 
AND D.Party_Code IN  (SELECT Party_Code FROM #ISIN)
GROUP BY D.PARTY_CODE, D.CERTNO  

  
UPDATE #DATA SET PARTY_NAME = CASE WHEN ISNULL(UPPER(ADDEMAILID), '') <> '' THEN UPPER(ADDEMAILID) ELSE PARTY_NAME END, PAN_GIR_NO = C.PAN_GIR_NO, EMAIL_ID = C.EMAIL, MOBILENO = C.MOBILE_PAGER  
FROM MSAJAG.DBO.CLIENT_DETAILS C  
WHERE C.CL_CODE = #DATA.PARTY_CODE  
  
UPDATE #DATA SET PAN_GIR_NO = 'NNNNN1111N'   
WHERE LEN(PAN_GIR_NO )<>10 OR PAN_GIR_NO IS NULL  
  
UPDATE #DATA SET EMAIL_ID = 'notprovided@notprovided.com'   
WHERE EMAIL_ID NOT LIKE '%@%' OR EMAIL_ID IS NULL  
  
UPDATE #DATA SET MOBILENO = '6666666666'   
WHERE MOBILENO = '0' OR MOBILENO = '' OR MOBILENO IS NULL  
  
  
UPDATE #DATA SET TOT_ISIN = ISIN_CNT, TOT_ISIN_QTY = QTY_SUM  
FROM (SELECT PARTY_CODE, ISIN_CNT = CASE WHEN SUM(QTY) > 0 THEN COUNT(DISTINCT ISIN) ELSE 0 END, QTY_SUM = SUM(QTY), CASH_COLL = SUM(CASH_COLL), TOT_CASH_COLL = SUM(TOT_CASH_COLL) FROM #ISIN   
   GROUP BY PARTY_CODE) A  WHERE #DATA.PARTY_CODE = A.PARTY_CODE  
  
EXEC ACCOUNT.DBO.GET_LAST_SETTTLE_DATE @REPORTDATE, 'NSE'  
  
DECLARE  
 @@REC_COUNT INT  
  
SET @@REC_COUNT = 0  
  
SELECT @@REC_COUNT = COUNT(1) FROM #DATA  
  
CREATE TABLE #FILE_DATA  
(  
 FILENAME varchar(100),  
 DATA VARCHAR(2000),  
 SRNO INT IDENTITY(1, 1)  
)  
  
DECLARE  
 @BATCHNO INT    
  
SELECT @BATCHNO = 0    
  
IF @@REC_COUNT > 0  
BEGIN  
 SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0) FROM ASIANFILEBATCHNO    
 WHERE FLDFILENO = 'NE'    
 AND FLDFILEDATE = @REPORTDATE    
  
 SET @BATCHNO = @BATCHNO + 1    
  
 INSERT INTO ASIANFILEBATCHNO    
 SELECT 'NE', @BATCHNO, @REPORTDATE, GETDATE()  
  
 INSERT INTO #FILE_DATA  
 SELECT FILENAME = @NSEMEMBERCODE + '_' + REPLACE(RIGHT(CONVERT(VARCHAR(9), 
 CONVERT(DATETIME, @REPORTDATE,103), 6), 6), ' ', '_') + '_C_' + RIGHT('00' + CONVERT(VARCHAR,@BATCHNO),2) + '.csv', 
 DATA = CONVERT(VARCHAR(3), CONVERT(DATETIME, @REPORTDATE) )+
 '-'+RIGHT(CONVERT(VARCHAR(11), CONVERT(DATETIME, @REPORTDATE)),2) + ',' + CONVERT(VARCHAR, @@REC_COUNT)
   
INSERT INTO #FILE_DATA  
SELECT FILENAME =  @NSEMEMBERCODE + '_' +REPLACE(RIGHT(CONVERT(VARCHAR(9), 
CONVERT(DATETIME, @REPORTDATE,103), 6), 6), ' ', '_') + '_C_' + RIGHT('00' + CONVERT(VARCHAR,@BATCHNO),2) + '.csv',
DATA = PARTY_NAME + ',' + PARTY_CODE + ',' + PAN_GIR_NO + ',' + EMAIL_ID + ',' + MOBILENO + ',' 
+ CONVERT(VARCHAR, NSECMBAL) + ',' + CONVERT(VARCHAR, TOTAL_VDTBAL) + ',' + CONVERT(VARCHAR, TOT_ISIN) + ',' + CONVERT(VARCHAR, TOT_ISIN_QTY) + ',' + CONVERT(VARCHAR, TOT_ISIN_PLEDGE) + ',' + CONVERT(VARCHAR, QTY_PLEDGE) + ',' + CONVERT(VARCHAR, FUND_RAISE) + ',' + LAST_SETT_DATE
FROM #DATA  

SELECT FILENAME,  DATA FROM #FILE_DATA ORDER BY SRNO

END  
ELSE  
BEGIN  
 SELECT DATA = PARTY_NAME + ',' + PARTY_CODE + ',' + PAN_GIR_NO + ',' + EMAIL_ID + ',' + MOBILENO + ',' + CONVERT(VARCHAR, NSECMBAL) + ',' + CONVERT(VARCHAR, TOTAL_VDTBAL) + ',' + CONVERT(VARCHAR, TOT_ISIN) + ',' + CONVERT(VARCHAR, TOT_ISIN_QTY) + ',' + CONVERT(VARCHAR, QTY_PLEDGE) + ',' + CONVERT(VARCHAR, FUND_RAISE) + ',' + LAST_SETT_DATE  
 FROM #DATA  
END  
  
DROP TABLE #DATA   
DROP TABLE #FILE_DATA

GO
