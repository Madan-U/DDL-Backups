CREATE PROC [dbo].[CALC_GST_INVOICE] 
--Altered this SP due to IPFT issue for COMMON_CONTRACT_DATA table renamed (ORE-4052)
(
	@FORTHEDAY VARCHAR(11)
)
AS

DECLARE @EXCHANGE_NEW VARCHAR(3)
DECLARE @SEGMENT_NEW VARCHAR(10)
DECLARE @SHAREDB VARCHAR(35)
DECLARE @SHARESERVER VARCHAR(35)
DECLARE @ACCOUNTDB VARCHAR(35)
DECLARE @ACCOUNTSERVER VARCHAR(35)

DECLARE @EXCHANGEWISE_CURSOR CURSOR 
DECLARE @STRSQL VARCHAR(8000) 

CREATE TABLE [dbo].[#GST_INVOICE](
	[INVOICE_DATE] [datetime] NULL,
	[EXCHANGE] [varchar](3) NOT NULL,
	[SEGMENT] [varchar](7) NOT NULL,
	[RECORDFOR] [varchar](10) NOT NULL,
	[SUBRECORDFOR] [varchar](20) NULL,
	[INV_DESC] [varchar](500) NULL,
	[SETT_NO] [varchar](7) NULL,
	[SETT_TYPE] [varchar](3) NULL,
	[PARTY_CODE] [varchar](10) NULL,
	[CONTRACTNO] [varchar](10) NULL,
	[INT_REF_NO] [varchar](50) NULL,
	[TAXABLE_AMT] [money] NULL,
	[TAX_AMT] [money] NULL,
	[INVOICE_TYPE] [varchar](20) NULL,
	[INVOICE_SUBTYPE] [varchar](20) NULL,
	[INVOICE_CATEGORY] [varchar](20) NULL,
	[REV_INT_REF_NO] [varchar](50) NULL,
	[LOCATION_CODE] [varchar](50) NULL,
	[SOURCESTATE] [varchar](50) NULL,
	[TARGETSTATE] [varchar](50) NULL,
	[STATE_CODE] [varchar](2) NULL,
	[INV_NO] [varchar](50) NULL,
	[SACCODE] [varchar](50) NULL,
	[SAC_DESC] [varchar](500) NULL,
	[BRANCH_CODE] [varchar](10) NULL,
	[IGST_RATE] [numeric](18, 4) NULL,
	[IGST_AMOUNT] [numeric](18, 4) NULL,
	[CGST_RATE] [numeric](18, 4) NULL,
	[CGST_AMOUNT] [numeric](18, 4) NULL,
	[SGST_RATE] [numeric](18, 4) NULL,
	[SGST_AMOUNT] [numeric](18, 4) NULL,
	[TOTCHRG][numeric](18, 4) NULL
) 

SELECT DISTINCT EXCHANGE,SEGMENT INTO #CLIENT_BROK_DETAILS 
FROM MSAJAG.DBO.CLIENT_BROK_DETAILS (NOLOCK) GROUP BY EXCHANGE,SEGMENT 

SET @EXCHANGEWISE_CURSOR = CURSOR
FOR
SELECT DISTINCT 
	EXCHANGE = M.EXCHANGE,
	SEGMENT = M.SEGMENT,
	SHAREDB,
	SHARESERVER,
	ACCOUNTDB,
	ACCOUNTSERVER
FROM PRADNYA..MULTICOMPANY M(NOLOCK),
	#CLIENT_BROK_DETAILS CB(NOLOCK)
WHERE CATEGORY IN (
		'EQUITY',
		'DERIVATIVES',
		'CURRENCY','COMMODITY'
		)
--WHERE CB.EXCHANGE='NSE' AND CB.SEGMENT='FUTURES'
	AND SEGMENT_DESCRIPTION NOT LIKE '%PCM%'
	AND CB.EXCHANGE = M.EXCHANGE
	AND CB.SEGMENT = M.SEGMENT
	AND PRIMARYSERVER = 1
	--AND M.EXCHANGE NOT IN ('MFO','MDB','MSX','NCX','MCX')
	AND M.EXCHANGE NOT IN ('MFO','MDB','MSX')
	
OPEN @EXCHANGEWISE_CURSOR

FETCH NEXT
FROM @EXCHANGEWISE_CURSOR
INTO
	@EXCHANGE_NEW,
	@SEGMENT_NEW,
	@SHAREDB,
	@SHARESERVER,
	@ACCOUNTDB,
	@ACCOUNTSERVER

WHILE @@FETCH_STATUS = 0
BEGIN
	SET @STRSQL = ''
	IF @SHARESERVER = @@SERVICENAME
	BEGIN
		SET @SHARESERVER =''
	END
	ELSE
	BEGIN
		SET @SHARESERVER = @SHARESERVER + '.'
	END

		SET @STRSQL = 'INSERT INTO #GST_INVOICE EXEC ' + @SHARESERVER + @SHAREDB + '.DBO.FETCH_GST_INVOICE ''' + @FORTHEDAY + ''',''' + @EXCHANGE_NEW + ''',''' + @SEGMENT_NEW + ''' '
		print @STRSQL
		EXEC (@STRSQL)

	FETCH NEXT
	FROM @EXCHANGEWISE_CURSOR
	INTO 
		@EXCHANGE_NEW,
		@SEGMENT_NEW,
		@SHAREDB,
		@SHARESERVER,
		@ACCOUNTDB,
		@ACCOUNTSERVER
END

CLOSE @EXCHANGEWISE_CURSOR

DEALLOCATE @EXCHANGEWISE_CURSOR


UPDATE #GST_INVOICE SET 
SACCODE=CONVERT(VARCHAR(50),ACCOUNT_SERVICE_NO),
SAC_DESC=CONVERT(VARCHAR(500),SERVICE_DESCRIPTION)
FROM OWNER


CREATE INDEX #C ON #GST_INVOICE (PARTY_CODE,EXCHANGE,SEGMENT)

UPDATE #GST_INVOICE SET INT_REF_NO = 'COMMON_CONTRACT_' + COMMON_CONTRACT_DATA.CONTRACTNO_NEW
FROM COMMON_CONTRACT_DATA
WHERE COMMON_CONTRACT_DATA.SAUDA_DATE BETWEEN @FORTHEDAY AND @FORTHEDAY + ' 23:59:59'
AND #GST_INVOICE.Party_Code = COMMON_CONTRACT_DATA.PARTY_CODE
AND #GST_INVOICE.Contractno = COMMON_CONTRACT_DATA.CONTRACTNO
AND #GST_INVOICE.EXCHANGE = COMMON_CONTRACT_DATA.EXCHANGE
AND #GST_INVOICE.SEGMENT =  COMMON_CONTRACT_DATA.SEGMENT

/*
UPDATE #GST_INVOICE SET INT_REF_NO = 'COMMON_CONTRACT_' + COMMON_CONTRACT_DATA.CONTRACTNO_NEW
FROM ANGELCOMMODITY.MCDX.DBO.COMMON_CONTRACT_DATA M
WHERE M.SAUDA_DATE BETWEEN @FORTHEDAY AND @FORTHEDAY + ' 23:59:59'
AND #GST_INVOICE.Party_Code = M.PARTY_CODE
AND #GST_INVOICE.Contractno = M.CONTRACTNO
AND #GST_INVOICE.EXCHANGE = M.EXCHANGE
AND #GST_INVOICE.SEGMENT =  M.SEGMENT
*/



SET @EXCHANGEWISE_CURSOR = CURSOR
FOR
SELECT DISTINCT 
	EXCHANGE = MAX(M.EXCHANGE),
	SEGMENT = MAX(M.SEGMENT),
	SHARESERVER,
	ACCOUNTDB,
	ACCOUNTSERVER
FROM PRADNYA..MULTICOMPANY M(NOLOCK),
	#CLIENT_BROK_DETAILS CB(NOLOCK)
WHERE CATEGORY IN (
		'EQUITY' ,
		'DERIVATIVES',
		'CURRENCY','COMMODITY'
		)
--WHERE CB.EXCHANGE='NSE' AND CB.SEGMENT='FUTURES'
	AND M.SEGMENT NOT IN ('SLBS')
	AND SEGMENT_DESCRIPTION NOT LIKE '%PCM%'
	AND CB.EXCHANGE = M.EXCHANGE
	AND CB.SEGMENT = M.SEGMENT
	AND PRIMARYSERVER = 1
	AND M.EXCHANGE NOT IN ('MFO','MDB','MSX')
GROUP BY SHARESERVER, ACCOUNTDB, ACCOUNTSERVER	
OPEN @EXCHANGEWISE_CURSOR

FETCH NEXT
FROM @EXCHANGEWISE_CURSOR
INTO
	@EXCHANGE_NEW,
	@SEGMENT_NEW,
	@SHARESERVER,
	@ACCOUNTDB,
	@ACCOUNTSERVER

WHILE @@FETCH_STATUS = 0
BEGIN
	SET @STRSQL = ''
	IF @ACCOUNTSERVER = @@SERVICENAME 
	BEGIN
		SET @ACCOUNTSERVER =''
	END
	ELSE
	BEGIN
		SET @ACCOUNTSERVER = @ACCOUNTSERVER + '.'
	END

	
		SET @STRSQL = 'INSERT INTO #GST_INVOICE EXEC ' + @ACCOUNTSERVER + @ACCOUNTDB + '.DBO.FETCH_GST_INVOICE_ACCOUNT ''' + @FORTHEDAY + ''',''' + @EXCHANGE_NEW + ''',''' + @SEGMENT_NEW + ''' '
		print @STRSQL
		EXEC (@STRSQL)

	FETCH NEXT
	FROM @EXCHANGEWISE_CURSOR
	INTO 
		@EXCHANGE_NEW,
		@SEGMENT_NEW,
		@SHARESERVER,
		@ACCOUNTDB,
		@ACCOUNTSERVER
END

CLOSE @EXCHANGEWISE_CURSOR

DEALLOCATE @EXCHANGEWISE_CURSOR

DROP TABLE #CLIENT_BROK_DETAILS

UPDATE #GST_INVOICE SET LOCATION_CODE = GST_LOCATION, BRANCH_CODE = G.BRANCH_CD, TARGETSTATE = G.L_STATE
FROM TBL_CLIENT_GST_DATA G
WHERE #GST_INVOICE.Party_Code = G.PARTY_CODE
AND INVOICE_DATE BETWEEN EFF_FROM_DATE AND EFF_TO_DATE

UPDATE #GST_INVOICE SET LOCATION_CODE = GST_LOCATION, BRANCH_CODE = G.BRANCH_CD, TARGETSTATE = G.L_STATE
FROM CLIENT_DETAILS G
WHERE #GST_INVOICE.Party_Code = G.PARTY_CODE 
AND ISNULL(LOCATION_CODE,'') = ''

UPDATE #GST_INVOICE SET SOURCESTATE = G.STATE
FROM TBL_GST_LOCATION G
WHERE G.LOC_CODE = LOCATION_CODE

UPDATE #GST_INVOICE SET SOURCESTATE = G.STATE
FROM OWNER G
WHERE SOURCESTATE = ''

UPDATE #GST_INVOICE SET TARGETSTATE = SOURCESTATE
WHERE TARGETSTATE NOT IN (SELECT STATE FROM MSAJAG..State_Master WHERE STATE <> 'OTHER')

UPDATE #GST_INVOICE SET 
STATE_CODE=D.STATE_CODE,
CGST_RATE=(CASE WHEN SOURCESTATE = TARGETSTATE THEN D.CGST_PER ELSE 0 END),
IGST_RATE=(CASE WHEN SOURCESTATE <> TARGETSTATE THEN D.IGST_PER ELSE 0 END),
SGST_RATE=(CASE WHEN SOURCESTATE = TARGETSTATE THEN D.SGST_PER ELSE 0 END),
INVOICE_CATEGORY = 'IGST'
FROM TBL_STATE_GST_DATA D
WHERE D.STATE_NAME = SOURCESTATE
AND INVOICE_DATE BETWEEN EFF_FROM_DATE AND EFF_TO_DATE

UPDATE #GST_INVOICE SET 
CGST_AMOUNT = ROUND(TAX_AMT * CGST_RATE / (CGST_RATE + SGST_RATE + IGST_RATE),2),
SGST_AMOUNT = ROUND(TAX_AMT * SGST_RATE / (CGST_RATE + SGST_RATE + IGST_RATE),2),
IGST_AMOUNT = ROUND(TAX_AMT * IGST_RATE / (CGST_RATE + SGST_RATE + IGST_RATE),2)

UPDATE #GST_INVOICE SET CGST_AMOUNT = TAX_AMT - SGST_AMOUNT, INVOICE_CATEGORY = 'CGST'
WHERE CGST_AMOUNT > 0 

INSERT INTO TBL_GST_INVOICE_TMP
SELECT * FROM #GST_INVOICE

GO
