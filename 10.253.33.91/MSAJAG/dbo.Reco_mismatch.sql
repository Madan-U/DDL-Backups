--------------------------------------------------



CREATE PROC [dbo].[Reco_mismatch]
( @DATE DATETIME,@DPID VARCHAR(15),@TYPE VARCHAR(1))
AS

 


if (@TYPE ='A')
 BEGIN 

  select 'NSE' AS EXCHANGE,CLTDPID,SCRIP_CD,BO_FREEQTY,DP_FREEQTY,BO_PLDQTY,DP_PLDQTY,TODAYQTY,FREE_DIFF_QTY,PLD_DIFF_QTY,TXTREMARK AS REMARKS,SAVEBY AS USER_ID, SAVETIME  AS RECO_DATE
  from ANGELDEMAT.MSAJAG.DBO.TBL_HOLD_RECO_DATA WITH(NOLOCK)
  WHERE SaveTime>@DATE AND SAVETIME <=@DATE + ' 23:59' AND CLTDPID LIKE '%'+@DPID
  UNION ALL
   select 'BSE' AS EXCHANGE,CLTDPID,SCRIP_CD,BO_FREEQTY,DP_FREEQTY,BO_PLDQTY,DP_PLDQTY,TODAYQTY,FREE_DIFF_QTY,PLD_DIFF_QTY,TXTREMARK AS REMARKS,SAVEBY AS USER_ID, SAVETIME  AS RECO_DATE
  from ANGELDEMAT.BSEDB.DBO.TBL_HOLD_RECO_DATA WITH(NOLOCK)
  WHERE SaveTime>@DATE AND SAVETIME <=@DATE + ' 23:59' AND CLTDPID LIKE '%'+@DPID   
  
  END 
  
 if (@TYPE ='M')  

  BEGIN 

  select 'NSE' AS EXCHANGE,CLTDPID,SCRIP_CD,BO_FREEQTY,DP_FREEQTY,BO_PLDQTY,DP_PLDQTY,TODAYQTY,FREE_DIFF_QTY,PLD_DIFF_QTY,TXTREMARK AS REMARKS,SAVEBY AS USER_ID, SAVETIME  AS RECO_DATE
  from ANGELDEMAT.MSAJAG.DBO.TBL_HOLD_RECO_DATA WITH(NOLOCK)
  WHERE SaveTime>@DATE AND SAVETIME <=@DATE + ' 23:59' AND CLTDPID LIKE '%'+@DPID AND (FREE_DIFF_QTY<>0 OR PLD_DIFF_QTY<>0)
  UNION ALL
   select 'BSE' AS EXCHANGE,CLTDPID,SCRIP_CD,BO_FREEQTY,DP_FREEQTY,BO_PLDQTY,DP_PLDQTY,TODAYQTY,FREE_DIFF_QTY,PLD_DIFF_QTY,TXTREMARK AS REMARKS,SAVEBY AS USER_ID, SAVETIME  AS RECO_DATE
  from ANGELDEMAT.BSEDB.DBO.TBL_HOLD_RECO_DATA WITH(NOLOCK)
  WHERE SaveTime>@DATE AND SAVETIME <=@DATE + ' 23:59' AND CLTDPID LIKE '%'+@DPID   AND (FREE_DIFF_QTY<>0 OR PLD_DIFF_QTY<>0)
  
  END

GO
