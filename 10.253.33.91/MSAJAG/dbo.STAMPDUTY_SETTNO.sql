-- Object: PROCEDURE dbo.STAMPDUTY_SETTNO
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

  CREATE  PROC STAMPDUTY_SETTNO (@START_DATE VARCHAR(11), @END_DATE VARCHAR(11), @STATE VARCHAR(50))      
AS        
SELECT L_STATE, CMBILLVALAN.SETT_NO,         
STAMPDUTY=SUM(BROKER_CHRG)             
         
FROM CMBILLVALAN ,CLIENT2, CLIENT1, SETT_MST S2             
WHERE CMBILLVALAN.SETT_NO = S2.SETT_NO AND CMBILLVALAN.SETT_TYPE = S2.SETT_TYPE            
AND S2.END_DATE >= @START_DATE AND S2.END_DATE <= @END_DATE + ' 23:59:59'            
AND CMBILLVALAN.PARTY_CODE = CLIENT2.PARTY_CODE                 
AND CLIENT1.CL_CODE = CLIENT2.CL_CODE                 
      
AND L_STATE LIKE @STATE    
GROUP BY CMBILLVALAN.SETT_NO, L_STATE

GO
