		STRADEDQTY=SUM(SQTY),
		STRADEDAMT=CONVERT(NUMERIC(18,2),(CASE WHEN @AMOUNTWITHRATE = 1 THEN SUM(SAMT)ELSE SUM(SQTY*SMARKETRATE) END)),
		NETQTY = SUM(PQTY)-SUM(SQTY),
		NETAMT = CONVERT(NUMERIC(18,2),(CASE WHEN @AMOUNTWITHRATE = 1 THEN (SUM(SAMT)-SUM(PAMT)) ELSE (SUM(SQTY*SMARKETRATE)-SUM(PQTY*PMARKETRATE)) END)),        
		PMARKETRATE=CONVERT(NUMERIC(18,2),(CASE WHEN SUM(PQTY) > 0 THEN SUM(PQTY*PMARKETRATE)/SUM(PQTY) ELSE 0  END)), 
		PNETRATE = CONVERT(NUMERIC(18,2),(CASE WHEN SUM(PQTY) > 0  THEN SUM(PQTY*PNETRATE)/SUM(PQTY) ELSE 0 END)), 
		SMARKETRATE = CONVERT(NUMERIC(18,2),(CASE WHEN SUM(SQTY) > 0 THEN SUM(SQTY*SMARKETRATE)/SUM(SQTY)ELSE 0 END)), 
		SNETRATE = CONVERT(NUMERIC(18,2),(CASE WHEN SUM(SQTY) > 0 THEN SUM(SQTY*SNETRATE)/SUM(SQTY)ELSE 0 END)),
		CHARGES = CONVERT(NUMERIC(18,2),SUM(CHARGES)),ISIN,
		EXCHANGE_NEW,SEGMENT_NEW
	INTO #SAUDAREPORT
	FROM 
		(
			SELECT 
				SDATE = SAUDA_DATE,
				SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_NAME,SCRIP_CD,SERIES,
				PMARKETRATE = ISNULL((
							CASE WHEN SUM(PQTYTRD + PQTYDEL) > 0 THEN 
								SUM(PRATE)/SUM(PQTYTRD + PQTYDEL) END),0),
				PNETRATE =	ISNULL((
							CASE WHEN SUM(PQTYTRD + PQTYDEL) > 0 THEN 
								(CASE
									WHEN @NETRATEWITHCHRG = 1 THEN (SUM(PAMTTRD+PAMTDEL)+(SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG)))/SUM(PQTYTRD + PQTYDEL) 
									ELSE SUM(PAMTTRD+PAMTDEL)/SUM(PQTYTRD + PQTYDEL) 
								END)								
							END),0),
				SMARKETRATE = ISNULL((CASE WHEN SUM(SQTYTRD + SQTYDEL) > 0 THEN 
								SUM(SRATE)/SUM(SQTYTRD + SQTYDEL) END),0),
				SNETRATE = ISNULL((
							CASE WHEN SUM(SQTYTRD + SQTYDEL) > 0 THEN 
								(CASE
									WHEN @NETRATEWITHCHRG = 1 THEN (SUM((CASE WHEN SQTYTRD = 0 AND SAMTTRD <> 0 THEN -SAMTTRD ELSE SAMTTRD END) + (CASE WHEN SQTYDEL = 0 AND SAMTDEL <> 0 THEN -SAMTDEL ELSE SAMTDEL END))-(SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG)))/SUM(SQTYTRD + SQTYDEL)
									ELSE (SUM((CASE WHEN SQTYTRD = 0 AND SAMTTRD <> 0 THEN -SAMTTRD ELSE SAMTTRD END) + (CASE WHEN SQTYDEL = 0 AND SAMTDEL <> 0 THEN -SAMTDEL ELSE SAMTDEL END)))/SUM(SQTYTRD + SQTYDEL)
								END)	
							END),0),
				PQTY = SUM(PQTYTRD + PQTYDEL),
				PAMT = SUM(PAMTTRD + PAMTDEL)+(CASE WHEN @NETRATEWITHCHRG = 1 AND SUM(PQTYTRD + PQTYDEL) > 0 THEN (SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG)) ELSE 0 END),
				SQTY = SUM(SQTYTRD + SQTYDEL),
				SAMT = SUM(SAMTTRD + SAMTDEL)+(CASE WHEN @NETRATEWITHCHRG = 1 AND SUM(SQTYTRD + SQTYDEL) > 0 THEN -(SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG)) ELSE 0 END),
				CHARGES = (
				CASE 
					WHEN @NETRATEWITHCHRG = 1 THEN (SUM(PBROKTRD+PBROKDEL+SBROKTRD+SBROKDEL)+SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG))
					ELSE SUM(PBROKTRD+PBROKDEL+SBROKTRD+SBROKDEL)
				END),
				ISIN,
				EXCHANGE_NEW,SEGMENT_NEW
			FROM #CB_GREPORT (NOLOCK)
			WHERE 
				SAUDA_DATE BETWEEN @DATEFROM  AND @DATETO + ' 23:59:00'
				AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY        
				AND SCRIP_CD BETWEEN @FROMSCRIP AND  @TOSCRIP 
				AND SERIES = CASE WHEN @SERIES <> '' THEN @SERIES ELSE SERIES END
				AND LEFT(TRADETYPE,1) IN ( 'S','I' ) 				
			GROUP BY SETT_NO,SETT_TYPE,SAUDA_DATE,PARTY_CODE,SCRIP_NAME,SCRIP_CD,SERIES,ISIN,EXCHANGE_NEW,SEGMENT_NEW
			--HAVING SUM(PQTYTRD + PQTYDEL) - SUM(SQTYTRD + SQTYDEL) <> 0 
		) S,
		CLIENT_DETAILS C1 (NOLOCK),
		#TEMP_FILTERED_PARTY C3
	WHERE   
		S.PARTY_CODE = C1.CL_CODE
		AND C1.CL_CODE= C3.PARTY_CD
	GROUP BY 
		CONVERT(VARCHAR,CONVERT(DATETIME,SDATE,109),103),
		SETT_NO,
		SETT_TYPE,
		S.PARTY_CODE,
		LONG_NAME,
		SCRIP_NAME,
		SCRIP_CD,
		SERIES,
		ISIN,
		EXCHANGE_NEW,SEGMENT_NEW


