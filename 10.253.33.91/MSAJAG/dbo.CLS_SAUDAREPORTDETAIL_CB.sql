				ORD_DESC = LTRIM(RTRIM(SCRIP_CD))+'zzzzzzzzz'
			FROM #SAUDAREPORT
			GROUP BY
				SCRIP_CD,
				LTRIM(RTRIM(SCRIP_CD))+'zzzzzzzzz' 
			
			UNION ALL
			
			SELECT
				EXCHANGE = '',	
				SCRIP_CODE=MAX(SCRIP_CD),			
				SCRIP_NAME='GRAND TOTAL',
				SCRIP_CD='',SERIES='', 
				SAUDA_DATE = '',
				SETT_NO = '',SETT_TYPE = '',
				PARTY_CODE='GRAND TOTAL',PARTY_NAME ='',
				PMARKETRATE=0, 
				PNETRATE  =0, 
				PTRADEDQTY=SUM(PTRADEDQTY),
				PTRADEDAMT=CONVERT(NUMERIC(18,2),SUM(PTRADEDAMT)),
				SMARKETRATE = 0, 
				SNETRATE =0,
				STRADEDQTY=SUM(STRADEDQTY),
				STRADEDAMT=CONVERT(NUMERIC(18,2),SUM(STRADEDAMT)),
				NETQTY=SUM(NETQTY),
				NETAMT=CONVERT(NUMERIC(18,2),SUM(NETAMT)),
				CHARGES = SUM(CHARGES) ,     	
				ORD=4,
				ORD_DESC = 'zzzzzzzzz'		      
			FROM 
				#SAUDAREPORT		
		)t
		
		ORDER BY SCRIP_CD,PARTY_CODE,ORD
		
		IF @REPORT_FORMAT = 'C' OR @REPORT_FORMAT = 'SP' OR @REPORT_FORMAT = 'P' 
		--IF @REPORT_FORMAT = 'C' OR @REPORT_FORMAT = 'SP' OR @REPORT_FORMAT = 'P' 
		BEGIN
			SELECT * FROM #SAUDAREPORT_SCRIP_D ORDER BY ORD_DESC,ORD
		END
		ELSE
		BEGIN
					
			SELECT	EXCHANGE,
					SCRIP_NAME,
					SCRIP_CODE = SCRIP_CD,
					SERIES,
					TRADE_DATE = SAUDA_DATE,
					SETT_NO,
					SETT_TYPE,
					CLIENT_CODE = PARTY_CODE,
					CLIENT_NAME = PARTY_NAME,
					PUR_MARKET_RATE = PMARKETRATE,
					PUR_NETRATE = PNETRATE,
					PUR_QTY = PTRADEDQTY,
					PUR_AMOUNT = CONVERT(NUMERIC(18,2),PTRADEDAMT),
					SELL_MARKET_RATE = SMARKETRATE,
					SELL_NETRATE = SNETRATE,
					SELL_QTY = STRADEDQTY,
					SELL_AMOUNT= CONVERT(NUMERIC(18,2),STRADEDAMT),
					NET_QTY = NETQTY,
					NET_AMOUNT = CONVERT(NUMERIC(18,2),NETAMT),
					CHARGES
			FROM	#SAUDAREPORT_SCRIP_D 
			ORDER BY ORD_DESC,ORD
		END

		DROP TABLE #SAUDAREPORT_SCRIP_D

	END
	ELSE IF @REPORT_TYPE = 'S'
	BEGIN
		SELECT
			EXCHANGE,
			SCRIP_CODE,
			SCRIP_NAME,
			SCRIP_CD,SERIES, 
			SAUDA_DATE = '',
			SETT_NO = '',SETT_TYPE = '',
			PARTY_CODE='',PARTY_NAME ='',
			PMARKETRATE=0, 
			PNETRATE  =0, 
			PTRADEDQTY,
			PTRADEDAMT,
			SMARKETRATE = 0, 
			SNETRATE =0,
			STRADEDQTY,
			STRADEDAMT,
			NETQTY,
			NETAMT,
			CHARGES,
			ORD,
			ORD_DESC
		INTO
			#SAUDAREPORT_SCRIP_S
		FROM 
		(
		SELECT
			EXCHANGE = EXCHANGE_NEW +'-'+ SEGMENT_NEW,
			SCRIP_CODE=SCRIP_CD,
			SCRIP_NAME,
			SCRIP_CD,
			SERIES, 
			SAUDA_DATE = '',
			SETT_NO = '',SETT_TYPE = '',
			PARTY_CODE='',PARTY_NAME ='',
			PMARKETRATE=0, 
			PNETRATE  =0, 
			PTRADEDQTY=SUM(PTRADEDQTY),PTRADEDAMT=CONVERT(NUMERIC(18,2),SUM(PTRADEDAMT)),
			SMARKETRATE = 0, 
			SNETRATE =0,
			STRADEDQTY=SUM(STRADEDQTY),STRADEDAMT=CONVERT(NUMERIC(18,2),SUM(STRADEDAMT)),
			NETQTY=SUM(NETQTY),
			NETAMT=CONVERT(NUMERIC(18,2),SUM(NETAMT)),
			CHARGES = SUM(CHARGES) ,     	
			ORD=1,
			ORD_DESC = LTRIM(RTRIM(SCRIP_CD))+LTRIM(RTRIM(SERIES))
		FROM 
			#SAUDAREPORT	
		GROUP BY 
			EXCHANGE_NEW, SEGMENT_NEW,		
			SCRIP_NAME,
			SCRIP_CD, 
			SERIES,
			LTRIM(RTRIM(SCRIP_CD))+LTRIM(RTRIM(SERIES))

		UNION ALL
		
		SELECT
			EXCHANGE = '',
			SCRIP_CODE='',
			SCRIP_NAME='GRAND TOTAL',
			SCRIP_CD='',SERIES='', 
			SAUDA_DATE = '',
			SETT_NO = '',SETT_TYPE = '',
			PARTY_CODE='',PARTY_NAME ='',
			PMARKETRATE=0, 
			PNETRATE  =0, 
			PTRADEDQTY=SUM(PTRADEDQTY),
			PTRADEDAMT=CONVERT(NUMERIC(18,2),SUM(PTRADEDAMT)),
			SMARKETRATE = 0, 
			SNETRATE =0,
			STRADEDQTY=SUM(STRADEDQTY),
			STRADEDAMT=CONVERT(NUMERIC(18,2),SUM(STRADEDAMT)),
			NETQTY=SUM(NETQTY),
			NETAMT=CONVERT(NUMERIC(18,2),SUM(NETAMT)),
			CHARGES = SUM(CHARGES) ,     	
			ORD=2,
			ORD_DESC = 'zzzzzzzzzzz'	      
		FROM 
			#SAUDAREPORT	
		--GROUP BY 		
		--	SCRIP_CD, 
		--	SERIES,
		--	PARTY_CODE

		) t


		--IF @REPORT_FORMAT = 'C' OR @REPORT_FORMAT = 'SP' OR @REPORT_FORMAT = 'P' 
		IF @REPORT_FORMAT = 'SP' OR @REPORT_FORMAT = 'P' 
		BEGIN
			SELECT * FROM #SAUDAREPORT_SCRIP_S ORDER BY ORD_DESC,ORD
		END
		ELSE
		BEGIN

			SELECT	EXCHANGE,
					SCRIP_NAME,
					SCRIP_CODE = SCRIP_CD,
					SERIES,
					PUR_QTY = PTRADEDQTY,
					PUR_AMOUNT = CONVERT(NUMERIC(18,2),PTRADEDAMT),
					SELL_QTY = STRADEDQTY,
					SELL_AMOUNT= CONVERT(NUMERIC(18,2),STRADEDAMT),
					NET_QTY = NETQTY,
					NET_AMOUNT = CONVERT(NUMERIC(18,2),NETAMT),
					CHARGES
			FROM	#SAUDAREPORT_SCRIP_S 
			ORDER BY ORD_DESC,ORD
		END		

		DROP TABLE #SAUDAREPORT_SCRIP_S
	END
	
END

