CREATE PROC [dbo].[CLS_RPT_IPFT]
(
	@SAUDA_DATE VARCHAR(11),
	@FPARTY VARCHAR(20),
	@TPARTY VARCHAR(20),
	@DFLAG  VARCHAR(2)='D',
	@RTYPE  VARCHAR(2)='N'
)

/*
EXEC CLS_RPT_IPFT 'APR  3 2020','0','zzzzz','S'
EXEC CLS_RPT_IPFT 'MAR  8 2019','00','zzz','D'
EXEC CLS_RPT_IPFT 'APR  3 2020','00','zzz','D','N'
EXEC CLS_RPT_IPFT 'MAR  8 2019','00','zzz','D','I'
*/

AS
IF LEN(@SAUDA_DATE) = 10 AND CHARINDEX('/', @SAUDA_DATE) > 0
BEGIN
	SET @SAUDA_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @SAUDA_DATE, 103), 109)
END
IF @RTYPE='N'
BEGIN 
	EXEC RPT_IPFT @SAUDA_DATE,@FPARTY,@TPARTY,@DFLAG
END
ELSE
BEGIN
	EXEC RPT_IPFT_INST @SAUDA_DATE,@FPARTY,@TPARTY,@DFLAG
END

GO
