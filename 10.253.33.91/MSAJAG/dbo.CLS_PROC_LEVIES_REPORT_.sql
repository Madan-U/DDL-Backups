/*
EXEC CLS_PROC_LEVIES_REPORT  '01/01/2012','10/10/2014','','','','','BROKER','BROKER','PARTY',1
EXEC CLS_PROC_LEVIES_REPORT  '01/01/2012','10/10/2014','','','','','BROKER','BROKER','PARTY',2
EXEC CLS_PROC_LEVIES_REPORT  '01/01/2012','10/10/2014','','','','','BROKER','BROKER','PARTY',3

EXEC CLS_PROC_LEVIES_REPORT  '01/01/2012','10/10/2014','','','','','BROKER','BROKER','SCRIP',1
EXEC CLS_PROC_LEVIES_REPORT  '01/01/2012','10/10/2014','','','','','BROKER','BROKER','SCRIP',2
EXEC CLS_PROC_LEVIES_REPORT  '01/01/2012','10/10/2014','','','','','BROKER','BROKER','SCRIP',3
*/
CREATE PROC [dbo].[CLS_PROC_LEVIES_REPORT ]
(
	@DATEFROM VARCHAR(11),
	@DATETO VARCHAR(11),
	@FROMPARTY VARCHAR(20),
	@TOPARTY VARCHAR (20), 
	@FROMSCRIP VARCHAR(20),
	@TOSCRIP VARCHAR (20), 	
	@STATUSID VARCHAR(15),  
	@STATUSNAME VARCHAR(25),
	@SERIES VARCHAR(5) = '',
	@REPORT_BY VARCHAR(10)='PARTY',
	@REPORTTYPE INT=1
)  AS


/*
	@REPORTTYPE = 1 : PARTY / SCRIP WISE SUMMARY
	@REPORTTYPE = 2 : PARTY + SCRIP / SCRIP + PARTY
	@REPORTTYPE = 3 : PARTY + SCRIP + SAUDA_DATE / SCRIP + PARTY + SAUDA_DATE 
	
	@REPORT_BY : PARTY / SCRIP

*/

SET @DATEFROM = LEFT(CONVERT(DATETIME,@DATEFROM,103),11)
SET @DATETO = LEFT(CONVERT(DATETIME,@DATETO,103),11)

IF @TOSCRIP =''
SET @TOSCRIP ='ZZZZZZZZZ'

IF @TOPARTY =''
SET @TOPARTY ='ZZZZZZZ'

CREATE TABLE #LEVIES_REPORT
	(
	SRNO INT IDENTITY NOT NULL,
	SAUDA_DATE DATETIME,
	PARTY_CODE VARCHAR(20),
	PARTY_NAME VARCHAR(100),
	SCRIP_CD VARCHAR(12),
	SERIES VARCHAR(3),
	SCRIP_NAME VARCHAR(50),
	TURNOVER MONEY,
	BROKERAGE MONEY,
	SERVICE_TAX MONEY,
	TRANSACTION_CHRG MONEY,
	SEBI_FEES MONEY,
	STT_CHARGES MONEY,
	STAMP_DUTY MONEY,
	OTHER_CHRG MONEY,
	TOTAL_CHARGES MONEY
	)


	INSERT INTO #LEVIES_REPORT
	SELECT  
		SAUDA_DATE,
		PARTY_CODE, 
		PARTY_NAME,
		SCRIP_CD,
		SERIES,
		SCRIP_NAME,
		TURNOVER = (TRDAMT - DELAMT) + DELAMT,
		BROKERAGE = BUYBROKERAGE + SELBROKERAGE + BUYDELIVERYCHRG + SELLDELIVERYCHRG,
		SERVICE_TAX = SERVICE_TAX - EXSERVICE_TAX,
		TRANSACTION_CHRG = TURN_TAX,
		SEBI_FEES = SEBI_TAX,
		STT_CHARGES = INS_CHRG,
		STAMP_DUTY = BROKER_CHRG,
		OTHER_CHRG = OTHER_CHRG,
		TOTAL_CHARGES = (BUYBROKERAGE+SELBROKERAGE+BUYDELIVERYCHRG+SELLDELIVERYCHRG+SERVICE_TAX-EXSERVICE_TAX+TURN_TAX+SEBI_TAX+INS_CHRG+OTHER_CHRG+BROKER_CHRG)		
	FROM
	(
		 SELECT  
			SAUDA_DATE,
			S.PARTY_CODE, PARTY_NAME,SCRIP_CD,SERIES,SCRIP_NAME,		 
			PTRADEDAMT = SUM(PAMTTRD + PAMTDEL),
			STRADEDAMT = SUM(SAMTTRD + SAMTDEL),
			TRDAMT = SUM(TRDAMT),
			DELAMT = SUM(DELAMT),
			--STRADEDAMT = SUM((CASE WHEN SQTYTRD = 0 AND SAMTTRD <> 0 THEN -SAMTTRD ELSE SAMTTRD END) + (CASE WHEN SQTYDEL = 0 AND SAMTDEL <> 0 
			--				THEN -SAMTDEL ELSE SAMTDEL END)), 		 
			BUYBROKERAGE = SUM(PBROKTRD), SELBROKERAGE= SUM(SBROKTRD), BUYDELIVERYCHRG = SUM(PBROKDEL), SELLDELIVERYCHRG = SUM(SBROKDEL),
			SERVICE_TAX= SUM(SERVICE_TAX) ,EXSERVICE_TAX= SUM(EXSERVICE_TAX),
			TURN_TAX=SUM(TURN_TAX),SEBI_TAX=SUM(SEBI_TAX),INS_CHRG=SUM(INS_CHRG),BROKER_CHRG=SUM(BROKER_CHRG),OTHER_CHRG=SUM(S.OTHER_CHRG)
		 FROM 
			CMBILLVALAN S (NOLOCK), CLIENT1 (NOLOCK), CLIENT2 (NOLOCK)
		 WHERE  
			CLIENT1.CL_CODE = CLIENT2.CL_CODE
			AND CLIENT2.PARTY_CODE = S.PARTY_CODE
			AND S.SAUDA_DATE BETWEEN @DATEFROM AND @DATETO + ' 23:59' 
			AND S.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			AND SCRIP_CD BETWEEN @FROMSCRIP AND @TOSCRIP
			AND SERIES = CASE WHEN @SERIES <> '' THEN @SERIES ELSE SERIES END
			AND TRADETYPE NOT IN ( 'SCF','ICF','IR' ) 
			AND @STATUSNAME = (
			CASE
				WHEN @STATUSID = 'BRANCH' THEN CLIENT1.BRANCH_CD
				WHEN @STATUSID = 'SUBBROKER' THEN CLIENT1.SUB_BROKER
				WHEN @STATUSID = 'TRADER' THEN CLIENT1.TRADER
				WHEN @STATUSID = 'FAMILY' THEN CLIENT1.FAMILY
				WHEN @STATUSID = 'AREA' THEN CLIENT1.AREA
				WHEN @STATUSID = 'REGION' THEN CLIENT1.REGION
				WHEN @STATUSID = 'CLIENT' THEN CLIENT2.PARTY_CODE
				ELSE 'BROKER'
			END)
		 GROUP BY SAUDA_DATE,S.PARTY_CODE,PARTY_NAME,SCRIP_CD,SERIES,SCRIP_NAME 
	) T


	IF @REPORTTYPE = 1  -- SUMMARY
	BEGIN
		IF @REPORT_BY = 'PARTY'
		BEGIN
			SELECT PARTY_CODE AS CLIENT_CODE , 
			PARTY_NAME AS CLIENT_NAME,
			TURNOVER = CONVERT(DECIMAL(18,2),SUM(TURNOVER)),
			BROKERAGE = CONVERT(DECIMAL(18,2),SUM(BROKERAGE)),
			SERVICE_TAX = CONVERT(DECIMAL(18,2),SUM(SERVICE_TAX)),
			TRANSACTION_CHRG = CONVERT(DECIMAL(18,2),SUM(TRANSACTION_CHRG)),
			SEBI_FEES = CONVERT(DECIMAL(18,2),SUM(SEBI_FEES)),
			STT_CHARGES = CONVERT(DECIMAL(18,2),SUM(STT_CHARGES)),
			STAMP_DUTY = CONVERT(DECIMAL(18,2),SUM(STAMP_DUTY)),
			OTHER_CHRG = CONVERT(DECIMAL(18,2),SUM(OTHER_CHRG)),
			TOTAL_CHARGES= CONVERT(DECIMAL(18,2),SUM(TOTAL_CHARGES))
			FROM #LEVIES_REPORT
			GROUP BY PARTY_CODE,PARTY_NAME
			HAVING SUM(TURNOVER) <> 0
			ORDER BY 1
		END
		ELSE
		BEGIN
		
			SELECT SCRIP_CD,SERIES,SCRIP_NAME,
			TURNOVER = CONVERT(DECIMAL(18,2),SUM(TURNOVER)),
			BROKERAGE = CONVERT(DECIMAL(18,2),SUM(BROKERAGE)),
			SERVICE_TAX = CONVERT(DECIMAL(18,2),SUM(SERVICE_TAX)),
			TRANSACTION_CHRG = CONVERT(DECIMAL(18,2),SUM(TRANSACTION_CHRG)),
			SEBI_FEES = CONVERT(DECIMAL(18,2),SUM(SEBI_FEES)),
			STT_CHARGES = CONVERT(DECIMAL(18,2),SUM(STT_CHARGES)),
			STAMP_DUTY = CONVERT(DECIMAL(18,2),SUM(STAMP_DUTY)),
			OTHER_CHRG = CONVERT(DECIMAL(18,2),SUM(OTHER_CHRG)),
			TOTAL_CHARGES= CONVERT(DECIMAL(18,2),SUM(TOTAL_CHARGES))
			FROM #LEVIES_REPORT
			GROUP BY SCRIP_CD,SERIES,SCRIP_NAME
			HAVING SUM(TURNOVER) <> 0
			ORDER BY 1

		END
	END

	IF @REPORTTYPE = 2  -- DETAILS
	BEGIN
			SELECT PARTY_CODE AS CLIENT_CODE, PARTY_NAME AS CLIENT_NAME,SCRIP_CD,SERIES,SCRIP_NAME,
			TURNOVER = CONVERT(DECIMAL(18,2),SUM(TURNOVER)) ,
			BROKERAGE = CONVERT(DECIMAL(18,2),SUM(BROKERAGE)),
			SERVICE_TAX =CONVERT(DECIMAL(18,2), SUM(SERVICE_TAX)),
			TRANSACTION_CHRG = CONVERT(DECIMAL(18,2),SUM(TRANSACTION_CHRG)),
			SEBI_FEES = CONVERT(DECIMAL(18,2),SUM(SEBI_FEES)),
			STT_CHARGES = CONVERT(DECIMAL(18,2),SUM(STT_CHARGES)),
			STAMP_DUTY = CONVERT(DECIMAL(18,2),SUM(STAMP_DUTY)),
			OTHER_CHRG = CONVERT(DECIMAL(18,2),SUM(OTHER_CHRG)),
			TOTAL_CHARGES= CONVERT(DECIMAL(18,2),SUM(TOTAL_CHARGES))
			FROM #LEVIES_REPORT
			GROUP BY PARTY_CODE,PARTY_NAME,SCRIP_CD,SERIES,SCRIP_NAME
			HAVING SUM(TURNOVER) <> 0
			ORDER BY (CASE WHEN @REPORT_BY ='PARTY' THEN PARTY_CODE ELSE SCRIP_CD END), (CASE WHEN @REPORT_BY ='PARTY' THEN SCRIP_CD ELSE PARTY_CODE END)
	END

	IF @REPORTTYPE = 3  -- FURTHER DETAILS
	BEGIN
			SELECT SADUA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103),PARTY_CODE AS CLIENT_CODE, PARTY_NAME AS CLIENT_NAME,SCRIP_CD,SERIES,SCRIP_NAME,
			TURNOVER =  CONVERT(DECIMAL(18,2),SUM(TURNOVER)),
			BROKERAGE =  CONVERT(DECIMAL(18,2),SUM(BROKERAGE)),
			SERVICE_TAX =  CONVERT(DECIMAL(18,2),SUM(SERVICE_TAX)),
			TRANSACTION_CHRG =  CONVERT(DECIMAL(18,2),SUM(TRANSACTION_CHRG)),
			SEBI_FEES =  CONVERT(DECIMAL(18,2),SUM(SEBI_FEES)),
			STT_CHARGES =  CONVERT(DECIMAL(18,2),SUM(STT_CHARGES)),
			STAMP_DUTY =  CONVERT(DECIMAL(18,2),SUM(STAMP_DUTY)),
			OTHER_CHRG =  CONVERT(DECIMAL(18,2),SUM(OTHER_CHRG)),
			TOTAL_CHARGES=  CONVERT(DECIMAL(18,2),SUM(TOTAL_CHARGES))
			FROM #LEVIES_REPORT
			GROUP BY SAUDA_DATE,PARTY_CODE,PARTY_NAME,SCRIP_CD,SERIES,SCRIP_NAME
			HAVING SUM(TURNOVER) <> 0
			ORDER BY (CASE WHEN @REPORT_BY ='PARTY' THEN PARTY_CODE ELSE SCRIP_CD END), (CASE WHEN @REPORT_BY ='PARTY' THEN SCRIP_CD ELSE PARTY_CODE END), CONVERT(DATETIME,CONVERT(VARCHAR,SAUDA_DATE),103)
	END


	DROP TABLE #LEVIES_REPORT

/*----------------------------------------- END OF THE PROC ---------------------------------*/

GO
