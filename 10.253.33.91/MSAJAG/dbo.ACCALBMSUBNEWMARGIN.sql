/****** Object:  Stored Procedure dbo.ACCALBMSUBNEWMARGIN    Script Date: 12/26/2001 1:22:50 PM ******/
CREATE PROCEDURE ACCALBMSUBNEWMARGIN (@SETT_NO VARCHAR(7),@SETT_TYPE VARCHAR(2)) AS
DROP TABLE PARTYSUBNEWMARGIN 
SELECT SETT_NO ,SETT_TYPE = @SETT_TYPE,PARTIPANTCODE,SCRIP_CD,SERIES = (CASE WHEN SETT_TYPE = 'L' THEN 'EQ' ELSE 'BE' END ),
fIXMARGIN = ABS((ISNULL(( SELECT fixmargin FROM MARGIN1 WHERE SETT_NO= ( SELECT MIN(SETT_NO) FROM SETT_MST WHERE SETT_NO > S.SETT_NO AND SETT_TYPE = S.SETT_TYPE) 
AND SETT_TYPE = S.SETT_TYPE ),0)*(SUM(PQTY)-SUM(SQTY))/100 ))  , 
ADDMAR = ABS(ISNULL(( SELECT AddMargin*(SUM(PQTY)-SUM(SQTY))/100 FROM MARGIN2 
WHERE SETT_TYPE = S.SETT_TYPE AND SCRIP_CD = S.SCRIP_CD and SETT_NO= ( SELECT MIN(SETT_NO) FROM SETT_MST WHERE SETT_NO > S.SETT_NO AND SETT_TYPE = S.SETT_TYPE)  ),0))
INTO PARTYSUBNEWMARGIN FROM ALBMSUBPARTYPOS S 
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = (CASE WHEN @SETT_TYPE = 'N' THEN 'L'
					 WHEN @SETT_TYPE = 'W' THEN 'P' END )
GROUP BY SETT_NO ,SETT_TYPE,PARTIPANTCODE,SCRIP_CD

GO
