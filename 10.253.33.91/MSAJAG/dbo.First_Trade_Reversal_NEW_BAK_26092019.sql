 --FIRST_TRADE_REVERSAL '2019-06-24' ,'48',4
CREATE PROC [dbo].[First_Trade_Reversal_NEW_BAK_26092019] (@SDATE DATETIME , @TIMELIMIT INT,@NO_OF_TRADES INT,@CampaignType VARCHAR(20))
AS


DECLARE @DAY INT
SET @DAY = @TIMELIMIT/24 


SET @DAY = (CASE WHEN  @DAY=1 THEN 0 ELSE @DAY END)


DECLARE @PREVDATE DATETIME ,@CURDATE DATETIME

 
SELECT @CURDATE = MAX(START_DATE)   FROM SETT_MST WHERE START_DATE < @SDATE  AND SETT_TYPE ='N'


SELECT @PREVDATE=MAX(START_DATE)  FROM SETT_MST WHERE START_DATE < @SDATE-@DAY  AND SETT_TYPE ='N'

 

SELECT CLIENTCODE,CreationDate INTO #CLIENT FROM [196.1.115.167].KYC.DBO.tbl_Campaign_ClientInfo  WHERE CreationDate >=@PREVDATE 
AND CreationDate <=@CURDATE + ' 23:59'    --AND TimeLimitInHour =@TIMELIMIT
and CampaignType =@CampaignType
 
  
 

 SELECT * INTO #CASH FROM (
SELECT DISTINCT PARTY_CODE, ORDER_TIME   ,ROW_NUMBER() OVER(Partition by PARTY_CODE ORDER BY PARTY_CODE,ORDER_TIME ASC) AS RNO
 FROM COMMON_CONTRACT_DATA M  WHERE SAUDA_DATE >=@PREVDATE    AND SAUDA_DATE <=@CURDATE +' 23:59'
 AND EXISTS  (SELECT CLIENTCODE FROM #CLIENT WHERE CLIENTCODE =PARTY_CODE AND 
 SAUDA_DATE > CreationDate) AND SCRIPNAME <>'BROKERAGE' AND (ORDER_TIME <>'' AND ORDER_TIME <>'00:00:00')  

 ) A WHERE  RNO <= @NO_OF_TRADES

SELECT DISTINCT C.PARTY_CODE,ORDER_TIME,ORDER_NO,BROKERAGE ,EXCHANGE,SEGMENT INTO #REVERSAL FROM COMMON_CONTRACT_DATA C WHERE EXISTS ( SELECT *  FROM #CASH
   WHERE  C.PARTY_CODE=PARTY_CODE AND C.ORDER_TIME=ORDER_TIME) 
  AND SAUDA_DATE >=@PREVDATE    AND SAUDA_DATE <=@CURDATE +' 23:59'


  /* CASH END */ 

  /* COMM START*/

  SELECT * INTO #COMM FROM (
SELECT DISTINCT PARTY_CODE, ORDER_TIME   ,ROW_NUMBER() OVER(Partition by PARTY_CODE ORDER BY PARTY_CODE,ORDER_TIME ASC) AS RNO
FROM ANGELCOMMODITY.MCDX.DBO.COMMON_CONTRACT_DATA M  WHERE SAUDA_DATE >=@PREVDATE    AND SAUDA_DATE <=@CURDATE +' 23:59'
 AND  EXISTS  (SELECT CLIENTCODE FROM #CLIENT WHERE CLIENTCODE =PARTY_CODE AND 
 SAUDA_DATE > CreationDate) AND SCRIPNAME <>'BROKERAGE' AND (ORDER_TIME <>'' AND ORDER_TIME <>'00:00:00') )A
 WHERE  RNO <= @NO_OF_TRADES



 SELECT DISTINCT C.PARTY_CODE,ORDER_TIME,ORDER_NO,BROKERAGE,EXCHANGE,SEGMENT INTO  #REVERSAL_COMM FROM ANGELCOMMODITY.MCDX.DBO.COMMON_CONTRACT_DATA C 
 WHERE EXISTS ( SELECT * FROM #COMM
   WHERE  C.PARTY_CODE=PARTY_CODE AND C.ORDER_TIME=ORDER_TIME  )
AND SAUDA_DATE >=@PREVDATE    AND SAUDA_DATE <=@CURDATE +' 23:59'

 
SELECT R.PARTY_CODE,R.ORDER_TIME,R.ORDER_NO,SUM(C.BROKERAGE) AS BROKERAGE,R.EXCHANGE,R.SEGMENT INTO #FINAL
FROM #REVERSAL R, COMMON_CONTRACT_DATA C 
WHERE  C.PARTY_CODE=R.PARTY_CODE AND R.ORDER_NO=C.ORDER_NO AND SAUDA_DATE >=@PREVDATE    
AND SAUDA_DATE <=@CURDATE +' 23:59' --AND (C.ORDER_TIME ='' OR C.ORDER_TIME ='00:00:00')
GROUP BY R.PARTY_CODE,R.ORDER_TIME,R.ORDER_NO,R.EXCHANGE,R.SEGMENT


/* COMM END*/

INSERT INTO #FINAL
SELECT R.PARTY_CODE,R.ORDER_TIME,R.ORDER_NO,SUM(C.BROKERAGE) AS BROKERAGE ,R.EXCHANGE,R.SEGMENT
FROM #REVERSAL_COMM R,  ANGELCOMMODITY.MCDX.DBO.COMMON_CONTRACT_DATA C 
WHERE  C.PARTY_CODE=R.PARTY_CODE AND R.ORDER_NO=C.ORDER_NO AND SAUDA_DATE >=@PREVDATE    
AND SAUDA_DATE <=@CURDATE +' 23:59' --AND (C.ORDER_TIME ='' OR C.ORDER_TIME ='00:00:00')
GROUP BY R.PARTY_CODE,R.ORDER_TIME,R.ORDER_NO,R.EXCHANGE,R.SEGMENT



 

SELECT F.*,@CURDATE  AS SAUDA_DATE ,B2C,ROW_NUMBER() OVER(Partition by F.PARTY_CODE ORDER BY F.PARTY_CODE,F.ORDER_TIME ASC)AS RNO
FROM #FINAL F,[196.1.115.132].RISK.DBO.CLIENT_DETAILS D,
(SELECT PARTY_CODE,ORDER_TIME,ROW_NUMBER() OVER(Partition by PARTY_CODE ORDER BY PARTY_CODE,ORDER_TIME ASC) AS RNO  FROM #FINAL) M
WHERE F.PARTY_CODE =D.CL_CODE AND F.ORDER_TIME =M.ORDER_TIME AND RNO <= @NO_OF_TRADES
AND F.PARTY_CODE =M.PARTY_CODE

GO
