CREATE PROC [dbo].[BAK_PROC_CLIENT_COLL_ALLOCATE_FRESH_27042022]
(
	@PROCESS_DATE VARCHAR(11),
	@COLLAMOUNT   NUMERIC(18,4) = 0 
)
AS
DECLARE 
		@ALLOCCUR			CURSOR,
		@ADJCUR				CURSOR,
		@P_SNO				INT,
		@EXCHANGE			VARCHAR(3), 
		@SEGMENT			VARCHAR(7), 
		@SEGMENT_INDICATOR	VARCHAR(5), 
		@ALLOCATECASH		NUMERIC(18,2),
		@CLIENTCOLL			NUMERIC(18,2),
		@C_SNO				INT,
		@PARTY_CODE			VARCHAR(10),
		@PRIORITYFLAG		INT, 
		@BLOCKFUND			NuMERIC(18,2)

SET NOCOUNT ON
 

-- EXEC PROC_CLIENT_COLL_ALLOCATE_FRESH @PROCESS_DATE='MAR  5 2022',@COLLAMOUNT=10

SELECT PROCESS_DT, PARTY_CODE, CLEARING_PAN, TRADING_PAN, CP_CODE, CP_PAN,CLIENT_PAN, ACCOUNT_TYPE, 
SEGMENT_INDICATOR, EXCHANGE, SEGMENT, 
TOTALCLIENTCOLL=CONVERT(NUMERIC(18,2),EDT_LEDGER+BG_REC_TM+FD_REC_TM+APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM),
TOTALCLIENTCOLLNONCASH=CONVERT(NUMERIC(18,2),APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM
									   -APPROVED_SEC_NONCASH_WCM-APPROVED_SEC_NONCASH_WNCL-APPROVED_SEC_CASH_WCM-APPROVED_SEC_CASH_WNCL),
LIEU_EPI_LEDGER_CLTM,  
CLIENTCOLL_ORG=CONVERT(NUMERIC(18,2),EDT_LEDGER+BG_REC_TM+FD_REC_TM),
REPLD_NONCASH_ORG=CONVERT(NUMERIC(18,2),(APPROVED_SEC_NONCASH_WCM+APPROVED_SEC_NONCASH_WNCL)),
REPLD_NONCASHEQ_ORG=CONVERT(NUMERIC(18,2),APPROVED_SEC_CASH_WCM+APPROVED_SEC_CASH_WNCL),

CLIENTCOLLCASH=CONVERT(NUMERIC(18,2),EDT_LEDGER+BG_REC_TM+FD_REC_TM),
CLIENTCOLLNONCASH=CONVERT(NUMERIC(18,2),LIEU_EPI_LEDGER_CLTM+APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM
									   -APPROVED_SEC_NONCASH_WCM-APPROVED_SEC_NONCASH_WNCL-APPROVED_SEC_CASH_WCM-APPROVED_SEC_CASH_WNCL),
REPLD_NONCASH=CONVERT(NUMERIC(18,2),(APPROVED_SEC_NONCASH_WCM+APPROVED_SEC_NONCASH_WNCL)),
REPLD_NONCASHEQ=CONVERT(NUMERIC(18,2),APPROVED_SEC_CASH_WCM+APPROVED_SEC_CASH_WNCL),
MARGIN=UPFRONT_MARGIN,
MARGIN_SHORT_EXCESS=CONVERT(NUMERIC(18,2),0),
PEAK_MARGIN,UPFRONT_MARGIN,
ALLOCATECASH=CONVERT(NUMERIC(18,2),0),
ALLOCATENONCASH=CONVERT(NUMERIC(18,2),0),
BALANCECASH=CONVERT(NUMERIC(18,2),0),
FUNDEDCASH=CONVERT(NUMERIC(18,2),0),
BALANCENONCASH=CONVERT(NUMERIC(18,2),0),
PROCASH=CONVERT(NUMERIC(18,2),0), 
CC_CM_CASH=CONVERT(NUMERIC(18,2),0),
TOTALMARGIN=CONVERT(NUMERIC(18,2),0),
MARGINRATIO=CONVERT(NUMERIC(18,2),0),
EXCESSCOLL=CONVERT(NUMERIC(18,2),0),
BLOCKFUND=CONVERT(NUMERIC(18,2),0)
INTO #ALLOC FROM CLIENT_COLLATERAL_REPORTING_DETAILS
WHERE PROCESS_DT=@PROCESS_DATE
AND PARTY_CODE<>'10548'
 
CREATE NONCLUSTERED INDEX IDXEXCSEG ON #ALLOC
(
	EXCHANGE, SEGMENT
)
 
SELECT EXCHANGE, SEGMENT, PRIORITYFLAG=MAX(PRIORITYFLAG) INTO #TBL_ALLOC_PRIORITY
FROM TBL_ALLOC_PRIORITY
GROUP BY EXCHANGE, SEGMENT

INSERT INTO #TBL_ALLOC_PRIORITY
SELECT DISTINCT EXCHANGE, SEGMENT, 9 FROM #ALLOC
WHERE EXCHANGE NOT IN (SELECT EXCHANGE FROM #TBL_ALLOC_PRIORITY
					   WHERE #TBL_ALLOC_PRIORITY.EXCHANGE = #ALLOC.EXCHANGE
					   AND #TBL_ALLOC_PRIORITY.SEGMENT = #ALLOC.SEGMENT)

UPDATE #ALLOC SET CLIENTCOLLNONCASH = PRADNYA.DBO.fnMax(CLIENTCOLLCASH+CLIENTCOLLNONCASH,0),
				  CLIENTCOLLCASH = (CASE WHEN CLIENTCOLLNONCASH > ABS(CLIENTCOLLCASH) THEN 0 ELSE CLIENTCOLLCASH+CLIENTCOLLNONCASH END)
WHERE CLIENTCOLLCASH < 0 AND CLIENTCOLLNONCASH > 0 
AND ACCOUNT_TYPE <> 'P'

UPDATE #ALLOC SET REPLD_NONCASH = PRADNYA.DBO.fnMax(CLIENTCOLLCASH+REPLD_NONCASH,0),
				  CLIENTCOLLCASH = (CASE WHEN REPLD_NONCASH > ABS(CLIENTCOLLCASH) THEN 0 ELSE CLIENTCOLLCASH+REPLD_NONCASH END)
WHERE CLIENTCOLLCASH < 0 AND REPLD_NONCASH > 0  
AND ACCOUNT_TYPE <> 'P'

UPDATE #ALLOC SET REPLD_NONCASHEQ = PRADNYA.DBO.fnMax(CLIENTCOLLCASH+REPLD_NONCASHEQ,0),
				  CLIENTCOLLCASH = (CASE WHEN REPLD_NONCASHEQ > ABS(CLIENTCOLLCASH) THEN 0 ELSE CLIENTCOLLCASH+REPLD_NONCASHEQ END)
WHERE CLIENTCOLLCASH < 0 AND REPLD_NONCASHEQ > 0 
AND ACCOUNT_TYPE <> 'P'

UPDATE #ALLOC SET MARGIN = 0, PEAK_MARGIN=0, UPFRONT_MARGIN=0 WHERE ACCOUNT_TYPE='P'

SELECT PARTY_CODE, TRADING_PAN, CP_CODE, CP_PAN,CLIENT_PAN, ACCOUNT_TYPE,
CLIENTCOLLCASH = SUM(CLIENTCOLLCASH), CLIENTCOLLNONCASH = SUM(CLIENTCOLLNONCASH)
INTO #TOTAL
FROM  #ALLOC 
WHERE 1 = 2 
GROUP BY PARTY_CODE, CLEARING_PAN, TRADING_PAN, CP_CODE, CP_PAN,CLIENT_PAN, ACCOUNT_TYPE

SELECT EXCHANGE, SEGMENT, SEGMENT_INDICATOR, PARTY_CODE, TRADING_PAN, CP_CODE, CP_PAN,CLIENT_PAN, ACCOUNT_TYPE,
CLIENTCOLLCASH = SUM(CLIENTCOLLCASH), CLIENTCOLLNONCASH = SUM(CLIENTCOLLNONCASH)
INTO #CLIENTLIST	
FROM  #ALLOC WHERE ACCOUNT_TYPE <> 'P'
GROUP BY EXCHANGE, SEGMENT, SEGMENT_INDICATOR, PARTY_CODE, CLEARING_PAN, 
		 TRADING_PAN, CP_CODE, CP_PAN,CLIENT_PAN, ACCOUNT_TYPE

SET @ALLOCCUR = CURSOR FOR 
SELECT DISTINCT #ALLOC.EXCHANGE, #ALLOC.SEGMENT, #ALLOC.SEGMENT_INDICATOR, PRIORITYFLAG
FROM #ALLOC, #TBL_ALLOC_PRIORITY WHERE ACCOUNT_TYPE <> 'P'
AND #TBL_ALLOC_PRIORITY.EXCHANGE = #ALLOC.EXCHANGE
AND #TBL_ALLOC_PRIORITY.SEGMENT = #ALLOC.SEGMENT
ORDER BY PRIORITYFLAG
OPEN @ALLOCCUR
FETCH NEXT FROM @ALLOCCUR INTO @EXCHANGE, @SEGMENT, @SEGMENT_INDICATOR, @PRIORITYFLAG
WHILE @@FETCH_STATUS = 0 
BEGIN
	TRUNCATE TABLE #TOTAL

	INSERT INTO #TOTAL
	SELECT PARTY_CODE, TRADING_PAN, CP_CODE, CP_PAN,CLIENT_PAN, ACCOUNT_TYPE,
	CLIENTCOLLCASH = SUM(CLIENTCOLLCASH), CLIENTCOLLNONCASH = SUM(CLIENTCOLLNONCASH)	
	FROM  #ALLOC WHERE 1 = 2
	GROUP BY PARTY_CODE, TRADING_PAN, CP_CODE, CP_PAN,CLIENT_PAN, ACCOUNT_TYPE

	UPDATE #ALLOC SET CLIENTCOLLCASH =0, CLIENTCOLLNONCASH = 0
	WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #CLIENTLIST C
	WHERE  C.PARTY_CODE = #ALLOC.PARTY_CODE
	AND C.TRADING_PAN = #ALLOC.TRADING_PAN
	AND C.CP_CODE = #ALLOC.CP_CODE
	AND C.CP_PAN = #ALLOC.CP_PAN
	AND C.CLIENT_PAN = #ALLOC.CLIENT_PAN 
	AND C.ACCOUNT_TYPE = #ALLOC.ACCOUNT_TYPE
	AND C.EXCHANGE = @EXCHANGE AND C.SEGMENT = @SEGMENT 
	AND C.SEGMENT_INDICATOR = @SEGMENT_INDICATOR )
	AND 1 = 2 

	UPDATE #ALLOC SET CLIENTCOLLCASH = C.CLIENTCOLLCASH, CLIENTCOLLNONCASH = C.CLIENTCOLLNONCASH
	FROM #TOTAL C
	WHERE #ALLOC.EXCHANGE = @EXCHANGE AND #ALLOC.SEGMENT = @SEGMENT 
	AND C.PARTY_CODE = #ALLOC.PARTY_CODE
	AND C.TRADING_PAN = #ALLOC.TRADING_PAN
	AND C.CP_CODE = #ALLOC.CP_CODE
	AND C.CP_PAN = #ALLOC.CP_PAN
	AND C.CLIENT_PAN = #ALLOC.CLIENT_PAN 
	AND C.ACCOUNT_TYPE = #ALLOC.ACCOUNT_TYPE
	AND #ALLOC.SEGMENT_INDICATOR = @SEGMENT_INDICATOR 	 

	UPDATE #ALLOC SET ALLOCATENONCASH = PRADNYA.DBO.FNMIN(MARGIN,REPLD_NONCASHEQ) + PRADNYA.DBO.FNMIN(REPLD_NONCASHEQ,REPLD_NONCASH),
					  BALANCENONCASH  = REPLD_NONCASH - PRADNYA.DBO.FNMIN(REPLD_NONCASHEQ,REPLD_NONCASH)
	WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND SEGMENT_INDICATOR = @SEGMENT_INDICATOR 

	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '11111762' AND SEGMENT_INDICATOR = 'FO' 
	--END

	UPDATE #ALLOC SET ALLOCATENONCASH = PRADNYA.DBO.FNMIN(MARGIN,REPLD_NONCASHEQ) + 
										PRADNYA.DBO.FNMIN(PRADNYA.DBO.FNMIN(MARGIN-PRADNYA.DBO.FNMIN(MARGIN,REPLD_NONCASHEQ),0),
										PRADNYA.DBO.FNMIN(REPLD_NONCASHEQ,REPLD_NONCASH)),
					  BALANCENONCASH  = REPLD_NONCASH - PRADNYA.DBO.FNMIN(PRADNYA.DBO.FNMIN(MARGIN-PRADNYA.DBO.FNMIN(MARGIN,REPLD_NONCASHEQ),0),
										PRADNYA.DBO.FNMIN(REPLD_NONCASHEQ,REPLD_NONCASH))
	WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND SEGMENT_INDICATOR = @SEGMENT_INDICATOR 

	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '11111762' AND SEGMENT_INDICATOR = 'FO' 
	--END

	UPDATE #ALLOC SET MARGIN_SHORT_EXCESS = PRADNYA.DBO.FNMIN(MARGIN,REPLD_NONCASHEQ+PRADNYA.DBO.FNMIN(REPLD_NONCASHEQ,REPLD_NONCASH)) - MARGIN
	WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND SEGMENT_INDICATOR = @SEGMENT_INDICATOR 
	--and 1 = 2		

	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '11111762' AND SEGMENT_INDICATOR = 'FO' 
	--END

	UPDATE #ALLOC SET MARGIN_SHORT_EXCESS = MARGIN_SHORT_EXCESS + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),BALANCENONCASH),
	BALANCENONCASH = BALANCENONCASH - PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),BALANCENONCASH),
	ALLOCATENONCASH = ALLOCATENONCASH + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),BALANCENONCASH),
	BLOCKFUND = BLOCKFUND + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),BALANCENONCASH)/2
	WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND SEGMENT_INDICATOR = @SEGMENT_INDICATOR 
	AND MARGIN_SHORT_EXCESS < 0 AND BALANCENONCASH > 0 AND CLIENTCOLLCASH > 0 
	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '36210013' AND SEGMENT_INDICATOR = 'FO' 
	--END
	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '138510015' AND SEGMENT_INDICATOR = 'FO' 
	--END
	UPDATE #ALLOC SET MARGIN_SHORT_EXCESS = MARGIN_SHORT_EXCESS + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLCASH),  
	BALANCECASH = BALANCECASH + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLCASH),
	CLIENTCOLLCASH = CLIENTCOLLCASH- PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLCASH)
	WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND SEGMENT_INDICATOR = @SEGMENT_INDICATOR 
	AND MARGIN_SHORT_EXCESS < 0 AND CLIENTCOLLCASH > 0 

	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '11111762' AND SEGMENT_INDICATOR = 'FO' 
	--END
	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '36210013' AND SEGMENT_INDICATOR = 'FO' 
	--END
	UPDATE #ALLOC SET MARGIN_SHORT_EXCESS = MARGIN_SHORT_EXCESS + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),BALANCENONCASH),  
	--BALANCECASH = BALANCECASH + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLNONCASH),
	BALANCENONCASH = BALANCENONCASH- PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),BALANCENONCASH),
	ALLOCATENONCASH = ALLOCATENONCASH + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),BALANCENONCASH),
	BLOCKFUND = BLOCKFUND + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),BALANCENONCASH)/2
	WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND SEGMENT_INDICATOR = @SEGMENT_INDICATOR 
	AND MARGIN_SHORT_EXCESS < 0 AND BALANCENONCASH > 0 
	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '11111762' AND SEGMENT_INDICATOR = 'FO' 
	--END
	/*
	UPDATE #ALLOC SET CLIENTCOLLNONCASH = CLIENTCOLLNONCASH + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS)/2,BALANCENONCASH), 
	BALANCENONCASH = BALANCENONCASH - PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS)/2,BALANCENONCASH)
	WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND SEGMENT_INDICATOR = @SEGMENT_INDICATOR 
	*/ 
	UPDATE #ALLOC SET MARGIN_SHORT_EXCESS = MARGIN_SHORT_EXCESS + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLNONCASH),  
	--BALANCECASH = BALANCECASH + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLNONCASH),
	CLIENTCOLLNONCASH = CLIENTCOLLNONCASH- PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLNONCASH),
	ALLOCATENONCASH = ALLOCATENONCASH + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLNONCASH),
	FUNDEDCASH = FUNDEDCASH + PRADNYA.DBO.FNMIN(ABS(MARGIN_SHORT_EXCESS),CLIENTCOLLNONCASH)
	WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT AND SEGMENT_INDICATOR = @SEGMENT_INDICATOR 
	AND MARGIN_SHORT_EXCESS < 0 AND CLIENTCOLLNONCASH > 0 

	--IF @SEGMENT_INDICATOR = 'FO' 
	--BEGIN
	--	SELECT * FROM #ALLOC WHERE PARTY_CODE = '138510015' --AND SEGMENT_INDICATOR = 'FO' 
	--END
	FETCH NEXT FROM @ALLOCCUR INTO @EXCHANGE, @SEGMENT, @SEGMENT_INDICATOR, @PRIORITYFLAG
END 
CLOSE @ALLOCCUR
DEALLOCATE @ALLOCCUR

UPDATE #ALLOC SET TOTALMARGIN = A.TOTALMARGIN, 
MARGINRATIO = (CASE WHEN PEAK_MARGIN > UPFRONT_MARGIN THEN PEAK_MARGIN ELSE UPFRONT_MARGIN END) * 100 / A.TOTALMARGIN
FROM (SELECT PARTY_CODE, TRADING_PAN, CP_CODE, CP_PAN, CLIENT_PAN, ACCOUNT_TYPE, 
		TOTALMARGIN = SUM(CASE WHEN PEAK_MARGIN > UPFRONT_MARGIN THEN PEAK_MARGIN ELSE UPFRONT_MARGIN END)
	  FROM #ALLOC WHERE (CASE WHEN PEAK_MARGIN > UPFRONT_MARGIN THEN PEAK_MARGIN ELSE UPFRONT_MARGIN END) > 0 
	  GROUP BY PARTY_CODE, TRADING_PAN, CP_CODE, CP_PAN,CLIENT_PAN, ACCOUNT_TYPE
	  ) A
WHERE #ALLOC.TRADING_PAN = A.TRADING_PAN
AND #ALLOC.PARTY_CODE = A.PARTY_CODE
AND #ALLOC.CP_CODE = A.CP_CODE 
AND #ALLOC.CP_PAN = A.CP_PAN 
AND #ALLOC.CLIENT_PAN = A.CLIENT_PAN 
AND #ALLOC.ACCOUNT_TYPE = A.ACCOUNT_TYPE  
AND A.ACCOUNT_TYPE <> 'P'
AND (CASE WHEN PEAK_MARGIN > UPFRONT_MARGIN THEN PEAK_MARGIN ELSE UPFRONT_MARGIN END) > 0 

UPDATE #ALLOC SET EXCESSCOLL = 0 
WHERE ACCOUNT_TYPE <> 'P'

UPDATE #ALLOC SET EXCESSCOLL = CLIENTCOLLCASH, CLIENTCOLLCASH = 0 
WHERE ACCOUNT_TYPE <> 'P' AND MARGINRATIO > 0 and 1 = 2 
/*
UPDATE #ALLOC SET MARGINRATIO = 100
WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #ALLOC GROUP BY PARTY_CODE HAVING MAX(MARGINRATIO) = 0 )
AND SEGMENT = 'CAPITAL'
AND ACCOUNT_TYPE <> 'P'
 */
UPDATE #ALLOC SET CLIENTCOLLCASH = CLIENTCOLLCASH + CONVERT(NUMERIC(18,2),A.EXCESSCOLL * MARGINRATIO/100)
FROM (SELECT PARTY_CODE, TRADING_PAN, EXCESSCOLL = SUM(EXCESSCOLL) FROM #ALLOC
		GROUP BY PARTY_CODE, TRADING_PAN) A
WHERE A.PARTY_CODE=#ALLOC.PARTY_CODE
AND A.TRADING_PAN = #ALLOC.TRADING_PAN 
AND ACCOUNT_TYPE <> 'P'
AND MARGINRATIO > 0 and 1 = 2 

UPDATE #ALLOC SET EXCESSCOLL = 0 
WHERE ACCOUNT_TYPE <> 'P'

UPDATE #ALLOC SET EXCESSCOLL = CLIENTCOLLNONCASH, CLIENTCOLLNONCASH = 0 
WHERE ACCOUNT_TYPE <> 'P'
AND MARGINRATIO > 0 and 1 = 2

/*
UPDATE #ALLOC SET MARGINRATIO = 100
WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM #ALLOC GROUP BY PARTY_CODE HAVING MAX(MARGINRATIO) = 0 )
AND SEGMENT = 'CAPITAL'
AND ACCOUNT_TYPE <> 'P'
*/					 	
UPDATE #ALLOC SET CLIENTCOLLNONCASH = CLIENTCOLLNONCASH + CONVERT(NUMERIC(18,2),A.EXCESSCOLL * MARGINRATIO/100)
FROM (SELECT PARTY_CODE, TRADING_PAN, EXCESSCOLL = SUM(EXCESSCOLL) FROM #ALLOC
		GROUP BY PARTY_CODE, TRADING_PAN) A
WHERE A.PARTY_CODE=#ALLOC.PARTY_CODE
AND A.TRADING_PAN = #ALLOC.TRADING_PAN 
AND ACCOUNT_TYPE <> 'P'
AND MARGINRATIO > 0 AND 1 = 2

UPDATE #ALLOC SET Cc_Cm_CASH = C_CASH, PROCASH=P_CASH FROM (  
SELECT EXCHANGE, SEGMENT, C_CASH = SUM(CASE WHEN ACCOUNT_TYPE='C' THEN FDAMOUNT+CASHAMOUNT+BGAMOUNT ELSE 0 END),
P_CASH = SUM(CASE WHEN ACCOUNT_TYPE='P' THEN FDAMOUNT+CASHAMOUNT+BGAMOUNT ELSE 0 END) FROM TBL_CMCC_DATA 
WHERE TRANSDATE=(SELECT MAX(TRANSDATE) FROM TBL_CMCC_DATA WHERE TRANSDATE <= @PROCESS_DATE) 
AND ALLOC_LEVEL IN ('CC','CM') 
GROUP BY EXCHANGE, SEGMENT) A
WHERE #ALLOC.EXCHANGE = A.EXCHANGE AND #ALLOC.SEGMENT = A.SEGMENT AND ACCOUNT_TYPE='P'
 
ALTER TABLE #ALLOC
ADD SNO INT IDENTITY(1,1)

CREATE CLUSTERED INDEX IDXSNO ON #ALLOC
(
	SNO
)
 
UPDATE #ALLOC SET PROCASH = 0, CC_CM_CASH = PROCASH + CC_CM_CASH, EXCESSCOLL = 0
WHERE ACCOUNT_TYPE='P'
/*
SELECT * INTO #ALLOC_PRO FROM #ALLOC
WHERE ACCOUNT_TYPE='P'

UPDATE #ALLOC SET CC_CM_CASH = CC_CM_CASH + EXCESSCOLL, EXCESSCOLL = 0
WHERE  ACCOUNT_TYPE = 'P'  
*/

--SELECT * FROM #ALLOC WHERE ACCOUNT_TYPE = 'P'  

UPDATE #ALLOC SET CC_CM_CASH = CC_CM_CASH - ABS(C.MARGIN_SHORT_EXCESS), 
EXCESSCOLL = EXCESSCOLL + ABS(C.MARGIN_SHORT_EXCESS)
FROM (SELECT EXCHANGE, SEGMENT, SEGMENT_INDICATOR, MARGIN_SHORT_EXCESS = SUM(ABS(MARGIN_SHORT_EXCESS))
FROM #ALLOC WHERE ACCOUNT_TYPE <> 'P' AND MARGIN_SHORT_EXCESS < 0 
GROUP BY EXCHANGE, SEGMENT, SEGMENT_INDICATOR) C
WHERE #ALLOC.EXCHANGE = C.EXCHANGE AND #ALLOC.SEGMENT = C.SEGMENT AND #ALLOC.SEGMENT_INDICATOR = C.SEGMENT_INDICATOR
AND #ALLOC.ACCOUNT_TYPE = 'P'  

--SELECT * FROM #ALLOC WHERE ACCOUNT_TYPE = 'P'  

UPDATE #ALLOC SET CC_CM_CASH = CC_CM_CASH - ABS(C.BLOCKFUND), 
EXCESSCOLL = EXCESSCOLL + ABS(C.BLOCKFUND)
FROM (SELECT EXCHANGE, SEGMENT, SEGMENT_INDICATOR, BLOCKFUND = SUM(ABS(BLOCKFUND))
FROM #ALLOC WHERE ACCOUNT_TYPE <> 'P' AND BLOCKFUND > 0 
GROUP BY EXCHANGE, SEGMENT, SEGMENT_INDICATOR) C
WHERE #ALLOC.EXCHANGE = C.EXCHANGE AND #ALLOC.SEGMENT = C.SEGMENT AND #ALLOC.SEGMENT_INDICATOR = C.SEGMENT_INDICATOR
AND #ALLOC.ACCOUNT_TYPE = 'P'  

--SELECT * FROM #ALLOC WHERE ACCOUNT_TYPE = 'P'  


SELECT * INTO #ALLOC_PRO FROM #ALLOC
WHERE ACCOUNT_TYPE='P'

UPDATE #ALLOC_PRO SET EXCESSCOLL = 0
WHERE  ACCOUNT_TYPE = 'P'  AND CC_CM_CASH < 0 

UPDATE #ALLOC_PRO SET PROCASH = 0, CC_CM_CASH = PROCASH + CC_CM_CASH -(PROCASH + CC_CM_CASH + EXCESSCOLL )*@COLLAMOUNT/100, 
EXCESSCOLL = (PROCASH + CC_CM_CASH + EXCESSCOLL)*@COLLAMOUNT/100, BLOCKFUND=-1 
WHERE ACCOUNT_TYPE='P' AND CC_CM_CASH > 0 
AND CC_CM_CASH - (PROCASH + CC_CM_CASH + EXCESSCOLL)*@COLLAMOUNT/100  > 0 

UPDATE #ALLOC_PRO SET PROCASH = 0, CC_CM_CASH = PROCASH + CC_CM_CASH -(PROCASH + CC_CM_CASH )*@COLLAMOUNT/100, 
EXCESSCOLL = (PROCASH + CC_CM_CASH)*@COLLAMOUNT/100 
WHERE ACCOUNT_TYPE='P' AND CC_CM_CASH > 0 
AND CC_CM_CASH - (PROCASH + CC_CM_CASH)*@COLLAMOUNT/100  < 0 
AND BLOCKFUND >= 0 

UPDATE #ALLOC_PRO SET BLOCKFUND = 0 
WHERE ACCOUNT_TYPE='P'

UPDATE #ALLOC SET PROCASH = 0, 
CC_CM_CASH = PROCASH + CC_CM_CASH -(PROCASH + CC_CM_CASH + EXCESSCOLL)*@COLLAMOUNT/100,
BLOCKFUND=-1  
WHERE ACCOUNT_TYPE='P' AND CC_CM_CASH > 0  
AND CC_CM_CASH - (PROCASH + CC_CM_CASH + EXCESSCOLL)*@COLLAMOUNT/100  > 0 

UPDATE #ALLOC SET PROCASH = 0, 
CC_CM_CASH = PROCASH + CC_CM_CASH -(PROCASH + CC_CM_CASH)*@COLLAMOUNT/100,
BLOCKFUND=-1  
WHERE ACCOUNT_TYPE='P' AND CC_CM_CASH > 0  
AND CC_CM_CASH - (PROCASH + CC_CM_CASH)*@COLLAMOUNT/100  > 0 
AND BLOCKFUND >= 0 

UPDATE #ALLOC SET BLOCKFUND = 0 
WHERE ACCOUNT_TYPE='P'

SELECT EXCHANGE, SEGMENT,AMT=SUM(BLOCKFUND) INTO #BLOCK FROM #ALLOC  
GROUP BY EXCHANGE, SEGMENT

UPDATE #ALLOC SET CC_CM_CASH=CC_CM_CASH+(CASE WHEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) > 0 
				  THEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) 
				  ELSE 0 
			 END),
	BALANCECASH=BALANCECASH-(CASE WHEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) > 0 
				  THEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) 
				  ELSE 0 
			 END),
	BLOCKFUND=BLOCKFUND-(CASE WHEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) > 0 
				  THEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) 
				  ELSE 0 
			 END),
	BALANCENONCASH=BALANCENONCASH+(CASE WHEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) > 0 
