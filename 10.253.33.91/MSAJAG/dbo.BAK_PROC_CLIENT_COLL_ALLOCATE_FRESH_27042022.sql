	BALANCENONCASH=BALANCENONCASH+(CASE WHEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) > 0 
				  THEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) 
				  ELSE 0 
			 END),
	ALLOCATENONCASH=ALLOCATENONCASH-(CASE WHEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) > 0 
				  THEN PRADNYA.DBO.FNMIN(BALANCECASH,BLOCKFUND) 
				  ELSE 0 
			 END)
WHERE BLOCKFUND > 0 AND BALANCECASH > 0

DELETE FROM TBL_CLIENT_COLL_ALLOCATE
WHERE PROCESS_DT = @PROCESS_DATE

UPDATE #ALLOC SET CC_CM_CASH = #ALLOC.CC_CM_CASH + P.EXCESSCOLL
FROM #ALLOC_PRO P
WHERE P.ACCOUNT_TYPE = #ALLOC.ACCOUNT_TYPE 
AND P.EXCHANGE = #ALLOC.EXCHANGE 
AND P.SEGMENT = #ALLOC.SEGMENT

