EXCESSCOLL=(CASE WHEN ACCOUNT_TYPE = 'P' THEN 0 ELSE PRADNYA.DBO.FNMAX((CASE WHEN CLIENTCOLLCASH > 0 THEN 0 ELSE CLIENTCOLLCASH END)+CLIENTCOLLNONCASH,0) END),
RUNDATE=GETDATE(),BLOCKFUND 
