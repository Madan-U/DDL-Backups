-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

  
CREATE PROC [dbo].[PROC_PEAK_MARGIN_PENUP_NCX_ANGEL]   
(  @TDATE VARCHAR(10), @FILEFOR VARCHAR(100), @FILETYPE VARCHAR(100),   
 @PROGID VARCHAR(100), @UNAME VARCHAR(100),@FILENAME VARCHAR(100) = '' 
)  
  
AS 

DECLARE @RECCNT INT,@DELDATE DATETIME  

SELECT DISTINCT .DBO.PIECE(FILE_DATA,',',1) AS [DATE] INTO #FILDATA FROM FILEDATA  WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',1) <> 'Date' 

SELECT DISTINCT @DELDATE =  [DATE] FROM  #FILDATA

DROP TABLE #FILDATA

DELETE ACCOUNT.DBO.NCX_SHRT_PEAK_FILE WHERE [DATE] = @DELDATE

INSERT INTO ACCOUNT.DBO.NCX_SHRT_PEAK_FILE SELECT .DBO.PIECE(FILE_DATA,',',1) AS [DATE],
.DBO.PIECE(FILE_DATA,',',2) AS [Client Code],
.DBO.PIECE(FILE_DATA,',',3) AS [Portfolio based Margin],
.DBO.PIECE(FILE_DATA,',',4) AS [Net Buy Premium],
.DBO.PIECE(FILE_DATA,',',5) AS [Initial Margin],
.DBO.PIECE(FILE_DATA,',',6) AS [ELM Margin],
.DBO.PIECE(FILE_DATA,',',7) AS [Pre-Expiry Margin],
.DBO.PIECE(FILE_DATA,',',8) AS [Delivery Margin],
.DBO.PIECE(FILE_DATA,',',9) AS [Unidirectional Margin],
.DBO.PIECE(FILE_DATA,',',10) AS [Concentration Margin],
.DBO.PIECE(FILE_DATA,',',11) AS [Adhoc Margin],
.DBO.PIECE(FILE_DATA,',',12) AS [Cash Margin],
.DBO.PIECE(FILE_DATA,',',13) AS [Crystallized Loss Margin],
.DBO.PIECE(FILE_DATA,',',14) AS [Mark to Market Profit/Loss],
.DBO.PIECE(FILE_DATA,',',15) AS [Cli/Prop],
.DBO.PIECE(FILE_DATA,',',16) AS [SNAPSHOT_NUMBER],
.DBO.PIECE(FILE_DATA,',',17) AS [SNAPSHOT_TIME],
.DBO.PIECE(FILE_DATA,',',18) AS [ED],
.DBO.PIECE(FILE_DATA,',',19) AS [Collateral_Value_Snapshot],
.DBO.PIECE(FILE_DATA,',',20) AS [Short_allocation_SnapShot]
FROM FILEDATA  WHERE PROGID = @PROGID
AND .DBO.PIECE(FILE_DATA,',',1) <> 'Date'

DELETE FROM FILEDATA  WHERE PROGID = @PROGID

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM ACCOUNT.DBO.NCX_SHRT_PEAK_FILE  WHERE  [DATE] = @DELDATE

IF @RECCNT > 0   
BEGIN  
 SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)  
END  
ELSE  
BEGIN  
 SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'  
END

GO
