CREATE PROC	[dbo].[PROC_CLIENT_COLLATREAL_ALLOC_VDT_EDT_TEST]
(
	@RUNDATE VARCHAR(11)
)	
AS
DECLARE
		@PARTY_CODE	VARCHAR(10),
		@EXCHANGE	VARCHAR(3),
		@SEGMENT	VARCHAR(7),
		@NEWEXCHANGE	VARCHAR(3),
		@NEWSEGMENT	VARCHAR(7),
		@LEDBAL		NUMERIC(18,3),
		@NEWLEDBAL		NUMERIC(18,3),
		@DIFFLEDBAL		NUMERIC(18,3),
		@SNO	INT,
		@NEWSNO	INT,
		@ADJCUR		CURSOR,
		@ALLOCCURSOR	CURSOR
		 
SET NOCOUNT ON 


SELECT PARTY_CODE, CP_CODE, VDT_LEDGER, OTHER_VDT_LEDGER=VDT_LEDGER INTO #VDTLED FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE 1 = 2 

CREATE CLUSTERED INDEX IDXPARTY ON #VDTLED
(
	PARTY_CODE, CP_CODE
)
SET @ADJCUR = CURSOR FOR 
	SELECT DISTINCT EXCHANGE, SEGMENT FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY 
OPEN @ADJCUR
FETCH NEXT FROM @ADJCUR INTO @EXCHANGE, @SEGMENT
WHILE @@FETCH_STATUS = 0
BEGIN
	SET @ALLOCCURSOR = CURSOR FOR 
		SELECT DISTINCT EXCHANGE, SEGMENT FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
		WHERE 1 = (CASE WHEN EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT THEN 2 ELSE 1 END) 
	OPEN @ALLOCCURSOR
	FETCH NEXT FROM @ALLOCCURSOR INTO @NEWEXCHANGE, @NEWSEGMENT
	WHILE @@FETCH_STATUS = 0
	BEGIN
		TRUNCATE TABLE #VDTLED

		INSERT INTO #VDTLED
		SELECT PARTY_CODE,CP_CODE, VDT_LEDGER=SUM(VDT_LEDGER), OTHER_VDT_LEDGER=SUM(OTHER_VDT_LEDGER) FROM (
		SELECT PARTY_CODE,CP_CODE, VDT_LEDGER, OTHER_VDT_LEDGER=0 FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
		WHERE EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT 
		AND VDT_LEDGER < 0 
		UNION ALL
		SELECT PARTY_CODE,CP_CODE, VDT_LEDGER=0, OTHER_VDT_LEDGER=VDT_LEDGER FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
		WHERE EXCHANGE = @NEWEXCHANGE AND SEGMENT = @NEWSEGMENT
		AND VDT_LEDGER > 0 ) A
		GROUP BY PARTY_CODE,CP_CODE
		HAVING SUM(VDT_LEDGER) <> 0 AND SUM(OTHER_VDT_LEDGER) <> 0 
		AND (SUM(VDT_LEDGER) > 0 AND SUM(OTHER_VDT_LEDGER) < 0 
		OR 	SUM(VDT_LEDGER) < 0 AND SUM(OTHER_VDT_LEDGER) > 0) 

		UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY SET 
		VDT_LEDGER = (CASE WHEN OTHER_VDT_LEDGER >= ABS(V.VDT_LEDGER) 
						   THEN OTHER_VDT_LEDGER + V.VDT_LEDGER
						   ELSE 0
					  END)
		FROM #VDTLED V
		WHERE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.EXCHANGE = @NEWEXCHANGE AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.SEGMENT = @NEWSEGMENT
		AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.PARTY_CODE = V.PARTY_CODE
		AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.CP_CODE = V.CP_CODE
		AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.VDT_LEDGER > 0 
		
		UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY SET 
		VDT_LEDGER = (CASE WHEN OTHER_VDT_LEDGER >= ABS(V.VDT_LEDGER) 
						   THEN 0
						   ELSE OTHER_VDT_LEDGER + V.VDT_LEDGER
					  END)
		FROM #VDTLED V
		WHERE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.EXCHANGE = @EXCHANGE AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.SEGMENT = @SEGMENT
		AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.PARTY_CODE = V.PARTY_CODE
		AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.CP_CODE = V.CP_CODE
		AND CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.VDT_LEDGER < 0 

		FETCH NEXT FROM @ALLOCCURSOR INTO @NEWEXCHANGE, @NEWSEGMENT
	END
	CLOSE @ALLOCCURSOR
	DEALLOCATE @ALLOCCURSOR
	FETCH NEXT FROM @ADJCUR INTO @EXCHANGE, @SEGMENT
END
CLOSE @ADJCUR
DEALLOCATE @ADJCUR


