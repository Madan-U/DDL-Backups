CREATE PROC [dbo].[RPT_CLIENT_COLL_SEG_NSE_NEW]
(
	@RUNDATE	VARCHAR(11),
	@EXCHANGE	VARCHAR(3)='NSE'
)
AS

-- EXEC RPT_CLIENT_COLL_SEG_NSE_NEW 'OCT 15 2021'

SELECT Date=REPLACE(CONVERT(VARCHAR,PROCESS_DT,103),'/','-'),
CLEARING_PAN,TRADING_PAN,CP_CODE,CP_PAN,
CLIENT_PAN=CASE WHEN CLIENT_PAN LIKE '%[^0-9A-Za-z]%' OR LEN(CLIENT_PAN) <> '10' OR RIGHT(CLIENT_PAN, 1) LIKE '%[^A-Za-z]%' THEN 'NNNNN1111N' ELSE CLIENT_PAN END,
ACCOUNT_TYPE,SEGMENT_INDICATOR,MTOM=PARTY_CODE,VDT_LEDGER,
EDT_LEDGER,
PEAK_LEDGER,
BG_REC_TM,FD_REC_TM,APPROVED_SEC_CASH_REC_TM,APPROVED_SEC_NONCASH_REC_TM,
NONAPPROVED_SEC_REC_TM,CC_APPROVED_COMM_REC_TM,OTHER_COLL_REC_TM,LIEU_EPI_LEDGER_CLTM,
POOL_ACCOUNT_CLTM,CASH_RET_TM,BG_RET_TM,FD_RET_TM,APPROVED_SEC_CASH_RET_TM,
APPROVED_SEC_NONCASH_RET_TM,NONAPPROVED_SEC_RET_TM,CC_APPROVED_COMM_RET_TM,OTHER_COLL_RET_TM,
CASH_WCM,BG_WCM,FD_WCM,APPROVED_SEC_CASH_WCM,APPROVED_SEC_NONCASH_WCM,NONAPPROVED_SEC_RET_WCM,
CC_APPROVED_COMM_WCM,OTHER_COLL_WCM,CASH_RET_WCM,BG_RET_CM,FD_RET_CM,APPROVED_SEC_CASH_RET_CM,
APPROVED_SEC_NONCASH_RET_CM,NONAPPROVED_SEC_RET_RET_CM,CC_APPROVED_COMM_RET_CM,OTHER_COLL_RET_CM,
CASH_WNCL,BG_WNCL,FD_WNCL,APPROVED_SEC_CASH_WNCL,APPROVED_SEC_NONCASH_WNCL,CC_APPROVED_COMM_WNCL INTO #COLL_SEG_NSE
FROM CLIENT_COLLATERAL_REPORTING_DETAILS WITH (NOLOCK)
WHERE PROCESS_DT=@RUNDATE AND Exchange='NSE'
AND PARTY_CODE BETWEEN 'A' AND 'ZZZZZZZZZZZZZZZZZZZ999999' AND ACCOUNT_TYPE='C'

INSERT INTO #COLL_SEG_NSE
SELECT Date=REPLACE(CONVERT(VARCHAR,PROCESS_DT,103),'/','-'),
CLEARING_PAN,TRADING_PAN,CP_CODE,CP_PAN,CLIENT_PAN,
ACCOUNT_TYPE,SEGMENT_INDICATOR,MTOM=PARTY_CODE,VDT_LEDGER,EDT_LEDGER,PEAK_LEDGER,
BG_REC_TM,FD_REC_TM,APPROVED_SEC_CASH_REC_TM,APPROVED_SEC_NONCASH_REC_TM,
NONAPPROVED_SEC_REC_TM,CC_APPROVED_COMM_REC_TM,OTHER_COLL_REC_TM,LIEU_EPI_LEDGER_CLTM,
POOL_ACCOUNT_CLTM,CASH_RET_TM,BG_RET_TM,FD_RET_TM,APPROVED_SEC_CASH_RET_TM,
APPROVED_SEC_NONCASH_RET_TM,NONAPPROVED_SEC_RET_TM,CC_APPROVED_COMM_RET_TM,OTHER_COLL_RET_TM,
CASH_WCM,BG_WCM,FD_WCM,APPROVED_SEC_CASH_WCM,APPROVED_SEC_NONCASH_WCM,NONAPPROVED_SEC_RET_WCM,
CC_APPROVED_COMM_WCM,OTHER_COLL_WCM,CASH_RET_WCM,BG_RET_CM,FD_RET_CM,APPROVED_SEC_CASH_RET_CM,
APPROVED_SEC_NONCASH_RET_CM,NONAPPROVED_SEC_RET_RET_CM,CC_APPROVED_COMM_RET_CM,OTHER_COLL_RET_CM,
CASH_WNCL,BG_WNCL,FD_WNCL,APPROVED_SEC_CASH_WNCL,APPROVED_SEC_NONCASH_WNCL,CC_APPROVED_COMM_WNCL
FROM CLIENT_COLLATERAL_REPORTING_DETAILS WITH (NOLOCK)
WHERE PROCESS_DT=@RUNDATE
AND Exchange IN ('NSE','NSX') AND ACCOUNT_TYPE='P'

SELECT 'DATE','CLEARING MEMBER PAN','TRADING MEMBER PAN','CP CODE','CP PAN','CLIENT PAN','ACCOUNT TYPE','SEGMENT INDICATOR ',
'UCC CODE','FINANCIAL LEDGER BALANCE-A IN THE BOOKS OF TM FOR CLIENTS AND IN THE BOOKS OF CM FOR TM ( PRO)  AND IN THE BOOKS OF CM FOR CP',
'FINANCIAL LEDGER BALANCE (CLEAR)-B IN THE BOOKS OF TM FOR CLIENTS AND IN THE BOOKS OF CM FOR TM (PRO) AND IN THE BOOKS OF CM FOR CP',
'PEAK FINANCIAL LEDGER BALANCE (CLEAR)-C IN THE BOOKS OF TM FOR CLIENTS AND IN THE BOOKS OF CM FOR TM (PRO) AND IN THE BOOKS OF CM FOR CP',
'BANK GUARANTEE (BG) RECEIVED BY TM FROM CLIENTS AND BY CM FROM TM (PRO) AND FROM CPS',
'FIXED DEPOSIT RECEIPT (FDR) RECEIVED BY TM FROM CLIENTS AND BY CM FROM TM(PRO) AND FROM CPS',
'APPROVED SECURITIES CASH COMPONENT RECEIVED BY TM FROM CLIENTS AND BY CM FROM TM(PRO) AND FROM CPS',
'APPROVED SECURITIES NON-CASH COMPONENT RECEIVED BY TM FROM CLIENTS AND BY CM FROM TM(PRO) AND FROM CPS',
'NON-APPROVED SECURITIES RECEIVED BY TM FROM CLIENTS AND BY CM FROM TM(PRO) AND FROM CPS',
'VALUE OF NCL APPROVED COMMODITIES RECEIVED BY TM FROM CLIENTS AND BY CM FROM TM(PRO) AND FROM CPS',
'OTHER COLLATERALS RECEIVED BY TM FROM CLIENTS AND BY CM FROM TM(PRO) AND FROM CPS ',
'CREDIT ENTRY IN LEDGER IN LIEU OF EPI FOR CLIENTS / TM PRO','POOL ACCOUNT FOR CLIENTS / TM PRO',
'CASH RETAINED BY TM','BANK GUARANTEE (BG) RETAINED BY TM','FIXED DEPOSIT RECEIPT (FDR) RETAINED BY TM',
'APPROVED SECURITIES CASH COMPONENT RETAINED BY TM','APPROVED SECURITIES NON-CASH COMPONENT RETAINED BY TM',
'NON-APPROVED SECURITIES RETAINED BY TM','VALUE OF NCL APPROVED COMMODITIES RETAINED BY TM',
'OTHER COLLATERALS RETAINED BY TM','CASH PLACED WITH CM','BANK GUARANTEE (BG) PLACED WITH CM',
'FIXED DEPOSIT RECEIPT (FDR) PLACED WITH CM','APPROVED SECURITIES CASH COMPONENT PLACED WITH CM',
'APPROVED SECURITIES NON-CASH COMPONENT PLACED WITH CM','NON-APPROVED SECURITIES PLACED WITH CM',
'VALUE OF NCL APPROVED COMMODITIES PLACED WITH CM','OTHER COLLATERALS PLACED WITH CM','CASH RETAINED WITH CM',
'BANK GUARANTEE (BG) RETAINED WITH CM ','FIXED DEPOSIT RECEIPT (FDR) RETAINED WITH CM',
'APPROVED SECURITIES CASH COMPONENT RETAINED WITH CM','APPROVED SECURITIES NON-CASH COMPONENT RETAINED WITH CM',
'NON-APPROVED SECURITIES RETAINED WITH CM','VALUE OF NCL APPROVED COMMODITIES RETAINED WITH CM',
'OTHER COLLATERALS RETAINED WITH CM','CASH PLACED WITH NCL','BANK GUARANTEE (BG) PLACED WITH NCL',
'FIXED DEPOSIT RECEIPT (FDR) PLACED WITH NCL','APPROVED SECURITIES CASH COMPONENT PLACED WITH NCL',
'APPROVED SECURITIES NON-CASH COMPONENT PLACED WITH NCL','VALUE OF NCL APPROVED COMMODITIES PLACED WITH NCL'

UNION ALL

SELECT Date,CLEARING_PAN,TRADING_PAN,CP_CODE,CP_PAN,CLIENT_PAN,ACCOUNT_TYPE,SEGMENT_INDICATOR,MTOM,
CONVERT(VARCHAR,VDT_LEDGER),CONVERT(VARCHAR,EDT_LEDGER),
CONVERT(VARCHAR,PEAK_LEDGER),CONVERT(VARCHAR,BG_REC_TM),
CONVERT(VARCHAR,FD_REC_TM),CONVERT(VARCHAR,APPROVED_SEC_CASH_REC_TM),
CONVERT(VARCHAR,APPROVED_SEC_NONCASH_REC_TM),CONVERT(VARCHAR,NONAPPROVED_SEC_REC_TM),
CONVERT(VARCHAR,CC_APPROVED_COMM_REC_TM),CONVERT(VARCHAR,OTHER_COLL_REC_TM),
CONVERT(VARCHAR,LIEU_EPI_LEDGER_CLTM),CONVERT(VARCHAR,POOL_ACCOUNT_CLTM),
CONVERT(VARCHAR,CASH_RET_TM),CONVERT(VARCHAR,BG_RET_TM),
CONVERT(VARCHAR,FD_RET_TM),CONVERT(VARCHAR,APPROVED_SEC_CASH_RET_TM),
CONVERT(VARCHAR,APPROVED_SEC_NONCASH_RET_TM),CONVERT(VARCHAR,NONAPPROVED_SEC_RET_TM),
CONVERT(VARCHAR,CC_APPROVED_COMM_RET_TM),CONVERT(VARCHAR,OTHER_COLL_RET_TM),
CONVERT(VARCHAR,CASH_WCM),CONVERT(VARCHAR,BG_WCM),CONVERT(VARCHAR,FD_WCM),
CONVERT(VARCHAR,APPROVED_SEC_CASH_WCM),CONVERT(VARCHAR,APPROVED_SEC_NONCASH_WCM),
CONVERT(VARCHAR,NONAPPROVED_SEC_RET_WCM),CONVERT(VARCHAR,CC_APPROVED_COMM_WCM),
CONVERT(VARCHAR,OTHER_COLL_WCM),CONVERT(VARCHAR,CASH_RET_WCM),CONVERT(VARCHAR,BG_RET_CM),
CONVERT(VARCHAR,FD_RET_CM),CONVERT(VARCHAR,APPROVED_SEC_CASH_RET_CM),CONVERT(VARCHAR,APPROVED_SEC_NONCASH_RET_CM),
CONVERT(VARCHAR,NONAPPROVED_SEC_RET_RET_CM),CONVERT(VARCHAR,CC_APPROVED_COMM_RET_CM),CONVERT(VARCHAR,OTHER_COLL_RET_CM),
CONVERT(VARCHAR,CASH_WNCL),CONVERT(VARCHAR,BG_WNCL),CONVERT(VARCHAR,FD_WNCL),CONVERT(VARCHAR,APPROVED_SEC_CASH_WNCL),
CONVERT(VARCHAR,APPROVED_SEC_NONCASH_WNCL),CONVERT(VARCHAR,CC_APPROVED_COMM_WNCL)
FROM #COLL_SEG_NSE

GO
