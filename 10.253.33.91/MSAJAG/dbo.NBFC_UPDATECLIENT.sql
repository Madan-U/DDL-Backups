-- Object: PROCEDURE dbo.NBFC_UPDATECLIENT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[NBFC_UPDATECLIENT]
AS

UPDATE CLIENT_DETAILS SET CL_TYPE = 'NBF', MODIFIDEDON = GETDATE(), MODIFIDEDBY = 'NBFC', IMP_STATUS = 0
WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM ABVSCITRUS.NBFC.DBO.TBLCLIENTMARGIN
				      WHERE TO_DATE > LEFT(GETDATE(),11) + ' 23:59:59')
AND CL_TYPE <> 'NBF'

UPDATE CLIENT_DETAILS SET CL_TYPE = 'CLI', MODIFIDEDON = GETDATE(), MODIFIDEDBY = 'NBFC', IMP_STATUS = 0
WHERE PARTY_CODE IN (SELECT PARTY_CODE FROM ABVSCITRUS.NBFC.DBO.TBLCLIENTMARGIN
				      WHERE TO_DATE <= LEFT(GETDATE(),11) + ' 23:59:59')
AND CL_TYPE = 'NBF'

GO
