-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE GET_MTF_SPLIT_BAL
	@RUNDATE VARCHAR(11)
AS

SELECT PARTY_CODE, NETAMT = SUM(NETAMT) 
FROM MTFTRADE.DBO.TBL_MTF_DATA T
WHERE SAUDA_DATE = (SELECT MAX(SAUDA_DATE) FROM MTFTRADE.DBO.TBL_MTF_DATA WHERE SAUDA_DATE <= @RUNDATE)
GROUP BY PARTY_CODE

GO
