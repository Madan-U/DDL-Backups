-- Object: PROCEDURE dbo.PR_AUTO_FO_MTF_DATA
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[PR_AUTO_FO_MTF_DATA]
( @EXCH VARCHAR(4)
)
AS
BEGIN
IF @EXCH ='FO'
	BEGIN 
			
		
	EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'FO_MTF' 
	WAITFOR DELAY '00:00:45'; 

	SELECT 'FO' AS REPORT,CL_CODE AS PARTY_CODE,Active_Date,InActive_From,
	(CASE WHEN InActive_From > GETDATE()THEN 'ACTIVE' ELSE 'INACTIVE' END) AS CL_STATUS
	 FROM CLIENT_BROK_DETAILS WHERE Exchange ='NSE' AND Segment ='FUTURES'
	AND CL_CODE IN (SELECT * FROM AUTO_FO_MTF_DATA)

	END
	
IF @EXCH ='MTF'
	BEGIN 
			
		
	EXEC ABVSINSURANCEBO.msdb.DBO.SP_START_JOB 'FO_MTF' 
	WAITFOR DELAY '00:00:45'; 

	SELECT 'MTF' AS REPORT,PARTY_CODE,From_Date Active_Date,To_Date InActive_From,
	(CASE WHEN To_Date > GETDATE()THEN 'ACTIVE' ELSE 'INACTIVE' END) AS CL_STATUS
	 FROM MTFTRADE.DBO.TBLCLIENTMARGIN WHERE  PARTY_CODE IN (SELECT * FROM AUTO_FO_MTF_DATA)

	END

END

GO
