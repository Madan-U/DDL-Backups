		OBJ.[DIFFLEDBAL] = (CASE WHEN ABS(OBJ.[SHORT]) > ABS(T.[Balance]) THEN ABS(T.[Balance]) ELSE ABS(OBJ.[SHORT]) END),
		OBJ.[IsProcessed] = 1
FROM #ADJDATALED OBJ
INNER JOIN #ADJDATALED T ON OBJ.PARTY_CODE = T.PARTY_CODE AND ISNULL(T.[PartyCount],0) = 1
AND OBJ.[SHORT] < 0

UPDATE OBJ 
	SET OBJ.EDT_LEDGER = OBJ.[EDT_LEDGER] - T.[DIFFLEDBAL]
FROM #ADJDATALED OBJ
INNER JOIN #ADJDATALED T ON OBJ.[PARTY_CODE] = T.[PARTY_CODE] AND ISNULL(OBJ.[PartyCount],0) = 1
AND T.[SHORT] < 0 AND T.[IsProcessed] = 1

SET @ADJCUR = CURSOR FAST_FORWARD FOR 
SELECT SNO, PARTY_CODE, EXCHANGE, SEGMENT, SHORT
FROM #ADJDATALED
WHERE SHORT < 0  AND [IsProcessed] <> 1
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM #ADJDATALED
WHERE SHORT > 0 AND ISNULL([PartyCount],0) <> 1)
--AND PARTY_CODE = '10114978'
ORDER BY PARTY_CODE, EXCHANGE, SEGMENT
OPEN @ADJCUR
FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @LEDBAL
WHILE @@FETCH_STATUS = 0 
BEGIN
	SET @ALLOCCURSOR = CURSOR FAST_FORWARD FOR 
	SELECT SNO, EXCHANGE, SEGMENT, BALANCE = (CASE WHEN SHORT > EDT_LEDGER THEN EDT_LEDGER ELSE SHORT END) 
	FROM #ADJDATALED
	WHERE PARTY_CODE = @PARTY_CODE AND SHORT > 0 AND EDT_LEDGER > 0  AND ISNULL([PartyCount],0) <> 1
	ORDER BY EXCHANGE, SEGMENT
	OPEN @ALLOCCURSOR
	FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWLEDBAL
	WHILE @@FETCH_STATUS = 0 
	BEGIN
		IF @LEDBAL < 0 AND @NEWLEDBAL > 0 
		BEGIN
			SELECT @DIFFLEDBAL = (CASE WHEN ABS(@LEDBAL) > ABS(@NEWLEDBAL) THEN ABS(@NEWLEDBAL) ELSE ABS(@LEDBAL) END)
			UPDATE #ADJDATALED SET EDT_LEDGER = EDT_LEDGER - @DIFFLEDBAL
			WHERE SNO = @NEWSNO

			UPDATE #ADJDATALED SET EDT_LEDGER = EDT_LEDGER + @DIFFLEDBAL
			WHERE SNO = @SNO

			SET @LEDBAL = @LEDBAL + @DIFFLEDBAL
		END
		FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWLEDBAL
	END
	CLOSE @ALLOCCURSOR
	DEALLOCATE @ALLOCCURSOR
	FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @LEDBAL
END
CLOSE @ADJCUR
DEALLOCATE @ADJCUR

