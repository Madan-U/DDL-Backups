CREATE PROC	[dbo].[BAK_PROC_CLIENT_COLLATREAL_ALLOC_MODANGEL]
(
	@RUNDATE VARCHAR(11)
)	
AS
DECLARE
		@PARTY_CODE	VARCHAR(10),
		@EXCHANGE	VARCHAR(3),
		@SEGMENT	VARCHAR(7),
		@NEWEXCHANGE	VARCHAR(3),
		@NEWSEGMENT	VARCHAR(7),
		@LEDBAL		NUMERIC(18,3),
		@NEWLEDBAL		NUMERIC(18,3),
		@DIFFLEDBAL		NUMERIC(18,3),
		@SNO	INT,
		@NEWSNO	INT,
		@ADJCUR		CURSOR,
		@ALLOCCURSOR	CURSOR
		 
SELECT DISTINCT PARTY_CODE INTO #PARTYUPSTK_C
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE 
AND EDT_LEDGER+POOL_ACCOUNT_CLTM+LIEU_EPI_LEDGER_CLTM+
APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM-UPFRONT_MARGIN < 0 
AND UPFRONT_MARGIN > 0 
AND EXCHANGE <> 'MTF'
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE  
AND EDT_LEDGER+POOL_ACCOUNT_CLTM+LIEU_EPI_LEDGER_CLTM+
APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM-UPFRONT_MARGIN >0 
AND APPROVED_SEC_CASH_RET_TM > 0 AND EXCHANGE <> 'MTF') 

SELECT PARTY_CODE, EXCHANGE, SEGMENT, SHORT=EDT_LEDGER+POOL_ACCOUNT_CLTM+LIEU_EPI_LEDGER_CLTM+
APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM-UPFRONT_MARGIN,APPROVED_SEC_CASH_REC_TM,
APPROVED_SEC_CASH_RET_TM,NONAPPROVED_SEC_REC_TM,NONAPPROVED_SEC_RET_TM
INTO #ADJDATASTK_C FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE AND EXCHANGE <> 'MTF'
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #PARTYUPSTK_C)

ALTER TABLE #ADJDATASTK_C
ADD [SNO]			INT IDENTITY(1,1),
	[IsProcessed]	BIT ,
	[PartyCount]	INT,
	[Balance]		NUMERIC(19,6),
	[DIFFLEDBAL]	NUMERIC(19,6)

CREATE CLUSTERED INDEX IDXDT ON #ADJDATASTK_C
(
	[SNO]
)

 UPDATE OBJ
	SET [PartyCount] = T.[Count]
 FROM #ADJDATASTK_C OBJ
 INNER JOIN 
 (
	SELECT 
		[PARTY_CODE] ,
		[Count] = COUNT(*)
	FROM #ADJDATASTK_C 
	WHERE [SHORT] > 0 AND [APPROVED_SEC_CASH_RET_TM] > 0 
	GROUP BY [PARTY_CODE]
 ) T ON OBJ.[PARTY_CODE] = T.[PARTY_CODE]

UPDATE T 
	SET [Balance] =  CASE WHEN T.[SHORT] > T.[APPROVED_SEC_CASH_RET_TM] THEN T.[APPROVED_SEC_CASH_RET_TM] ELSE T.[SHORT] END
FROM #ADJDATASTK_C T 
WHERE ISNULL(T.[PartyCount],0) = 1

UPDATE OBJ 
	SET OBJ.[APPROVED_SEC_CASH_RET_TM] = OBJ.[APPROVED_SEC_CASH_RET_TM] + (CASE WHEN ABS(OBJ.[SHORT]) > ABS(T.[Balance]) THEN ABS(T.[Balance]) ELSE ABS(OBJ.[SHORT]) END),
		OBJ.[APPROVED_SEC_CASH_REC_TM] = OBJ.[APPROVED_SEC_CASH_REC_TM] + (CASE WHEN ABS(OBJ.[SHORT]) > ABS(T.[Balance]) THEN ABS(T.[Balance]) ELSE ABS(OBJ.[SHORT]) END),
		OBJ.[DIFFLEDBAL] = (CASE WHEN ABS(OBJ.[SHORT]) > ABS(T.[Balance]) THEN ABS(T.[Balance]) ELSE ABS(OBJ.[SHORT]) END),
		OBJ.[IsProcessed] = 1
FROM #ADJDATASTK_C OBJ
INNER JOIN #ADJDATASTK_C T ON OBJ.[PARTY_CODE] = T.[PARTY_CODE] AND ISNULL(T.[PartyCount],0) = 1
AND OBJ.[SHORT] < 0

UPDATE OBJ 
	SET OBJ.[APPROVED_SEC_CASH_RET_TM] = OBJ.[APPROVED_SEC_CASH_RET_TM] - T.[DIFFLEDBAL],
		OBJ.[APPROVED_SEC_CASH_REC_TM] = OBJ.[APPROVED_SEC_CASH_REC_TM] - T.[DIFFLEDBAL]
FROM #ADJDATASTK_C OBJ
INNER JOIN #ADJDATASTK_C T ON OBJ.[PARTY_CODE] = T.[PARTY_CODE] AND ISNULL(OBJ.[PartyCount],0) = 1
AND T.[SHORT] < 0 AND T.[IsProcessed] = 1

SET @ADJCUR = CURSOR FAST_FORWARD FOR 
SELECT SNO, PARTY_CODE, EXCHANGE, SEGMENT, SHORT
FROM #ADJDATASTK_C
WHERE SHORT < 0  and ISNULL([IsProcessed],0) <> 1
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM #ADJDATASTK_C
WHERE SHORT > 0 AND ISNULL([PartyCount],0) <> 1)
--AND PARTY_CODE = '10114978'
ORDER BY PARTY_CODE, EXCHANGE, SEGMENT
OPEN @ADJCUR
FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @LEDBAL
WHILE @@FETCH_STATUS = 0 
BEGIN
	SET @ALLOCCURSOR = CURSOR FAST_FORWARD FOR 
	SELECT SNO, EXCHANGE, SEGMENT, BALANCE = (CASE WHEN SHORT > APPROVED_SEC_CASH_RET_TM THEN APPROVED_SEC_CASH_RET_TM ELSE SHORT END) 
	FROM #ADJDATASTK_C
	WHERE PARTY_CODE = @PARTY_CODE AND SHORT > 0 AND APPROVED_SEC_CASH_RET_TM > 0 AND ISNULL([PartyCount],0) <> 1
	ORDER BY EXCHANGE, SEGMENT
	OPEN @ALLOCCURSOR
	FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWLEDBAL
	WHILE @@FETCH_STATUS = 0 
	BEGIN
		IF @LEDBAL < 0 AND @NEWLEDBAL > 0 
		BEGIN
			SELECT @DIFFLEDBAL = (CASE WHEN ABS(@LEDBAL) > ABS(@NEWLEDBAL) THEN ABS(@NEWLEDBAL) ELSE ABS(@LEDBAL) END)

			UPDATE #ADJDATASTK_C SET APPROVED_SEC_CASH_RET_TM = APPROVED_SEC_CASH_RET_TM - @DIFFLEDBAL,
			APPROVED_SEC_CASH_REC_TM = APPROVED_SEC_CASH_REC_TM - @DIFFLEDBAL
			WHERE SNO = @NEWSNO

			UPDATE #ADJDATASTK_C SET APPROVED_SEC_CASH_RET_TM = APPROVED_SEC_CASH_RET_TM + @DIFFLEDBAL,
			APPROVED_SEC_CASH_REC_TM = APPROVED_SEC_CASH_REC_TM + @DIFFLEDBAL
			WHERE SNO = @SNO

			SET @LEDBAL = @LEDBAL + @DIFFLEDBAL
		END
		FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWLEDBAL
	END
	CLOSE @ALLOCCURSOR
	DEALLOCATE @ALLOCCURSOR
	FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @LEDBAL
END
CLOSE @ADJCUR
DEALLOCATE @ADJCUR

UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY SET APPROVED_SEC_CASH_RET_TM = C.APPROVED_SEC_CASH_RET_TM,
APPROVED_SEC_CASH_REC_TM = C.APPROVED_SEC_CASH_REC_TM
FROM #ADJDATASTK_C C
WHERE PROCESS_DT = @RUNDATE
AND C.PARTY_CODE = CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.PARTY_CODE
AND C.EXCHANGE = CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.EXCHANGE
