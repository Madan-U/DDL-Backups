AND APPROVED_SEC_NONCASH_RET_TM > 0 AND EXCHANGE <> 'MTF') 

SELECT PARTY_CODE, EXCHANGE, SEGMENT, SHORT=EDT_LEDGER+POOL_ACCOUNT_CLTM+LIEU_EPI_LEDGER_CLTM+
APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM-UPFRONT_MARGIN,APPROVED_SEC_NONCASH_REC_TM,
