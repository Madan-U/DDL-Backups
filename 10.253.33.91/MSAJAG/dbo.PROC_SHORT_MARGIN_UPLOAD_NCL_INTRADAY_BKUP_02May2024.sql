CREATE PROCEDURE [dbo].[PROC_SHORT_MARGIN_UPLOAD_NCL_INTRADAY_BKUP_02May2024]
	@MARGINDATE	VARCHAR(11),
	@FLDFILENAME VARCHAR (300),
	@UPLOAD_BY VARCHAR(30) = ''
AS

DECLARE
	@FILENAME VARCHAR(50)
SELECT @FILENAME = RIGHT(@FLDFILENAME, CHARINDEX('\', REVERSE(@FLDFILENAME),0) - 1)

DECLARE
	@EXCHANGE VARCHAR(10),
	@SEGMENT VARCHAR(10)

	IF LEFT(@FILENAME, 1) = 'C'
	BEGIN
		SET @EXCHANGE = 'NSE'
		SET @SEGMENT = 'CAPITAL'
	END	

	IF LEFT(@FILENAME, 1) = 'F'
	BEGIN
		SET @EXCHANGE = 'NSE'
		SET @SEGMENT = 'FUTURES'
	END	

	IF LEFT(@FILENAME, 1) = 'X'
	BEGIN
		SET @EXCHANGE = 'NSX'
		SET @SEGMENT = 'FUTURES'
	END	

	DECLARE @INDICATOR VARCHAR(3)
	
	SELECT @INDICATOR = RIGHT(REPLACE(@FLDFILENAME, '.LIS', ''), 2)

	DECLARE @UPLOAD_ON DATETIME
	SET @UPLOAD_ON = GETDATE()

	DECLARE
	@STRSQL VARCHAR(1000)



	CREATE TABLE #TBL_CAPITAL_SHORT_ALLOC_INTRADAY
	(
		[MARGINDATE] [datetime] NOT NULL,
		[Client_CODE] [varchar](20) NOT NULL,
		[Margin_Req] [numeric](18, 4) NULL,
		[Total_Cash_Coll] [numeric](18, 4) NULL,
		[Total_NonCash_Coll] [numeric](18, 4) NULL,
		[Total_value_Coll] [numeric](18, 4) NULL,
		[EOD_Short_Alloc] [numeric](18, 4) NULL
	)	

	
	Set @strSql = LOWER('Bulk insert  #TBL_CAPITAL_SHORT_ALLOC_INTRADAY from ''' + @FLDFILENAME  + '''  with ( FIELDTERMINATOR = '','', ROWTERMINATOR = ''\n'' )')
	Exec(@strSql)

	
	INSERT INTO TBL_SHORT_ALLOC_INTRADAY_LOG
	SELECT *, @UPLOAD_ON FROM TBL_SHORT_ALLOC_INTRADAY WHERE MARGINDATE = @MARGINDATE AND INDICATOR = @INDICATOR AND EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT

	DELETE FROM TBL_SHORT_ALLOC_INTRADAY WHERE MARGINDATE = @MARGINDATE AND INDICATOR = @INDICATOR AND EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT

	INSERT INTO TBL_SHORT_ALLOC_INTRADAY
	SELECT MARGINDATE = @MARGINDATE,@INDICATOR,@EXCHANGE,@SEGMENT,Client_CODE,Margin_Req,Total_Cash_Coll,Total_NonCash_Coll,
	Excess_Other_Seg=0,Total_value_Coll,EOD_Short_Alloc,@FILENAME,@UPLOAD_BY,@UPLOAD_ON
	FROM #TBL_CAPITAL_SHORT_ALLOC_INTRADAY
	WHERE MARGINDATE = @MARGINDATE

GO
