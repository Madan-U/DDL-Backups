CREATE PROC [dbo].[BULK_SEC_PLD_NCCL]  
(
	@TDATE VARCHAR(10), 
	@FILEFOR VARCHAR(20), 
	@FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), 
	@UNAME VARCHAR(50),
	@FILENAME VARCHAR(500) = ''
)
AS         
      
	  --INSERT INTO tbl_class_import ([FLDFILENO],[FLDFILETYPE],[FLDFILENAME],[FLDPROCNAME],[FLDFILEFOR])VALUES('','NSCCL COLL BREAKUP','*.CSV','BULK_SEC_PLD_NCCL','COLLSEG')
DECLARE @STRSQL VARCHAR(500), 
		@FILECOUNTER INT,
		@FILEDT VARCHAR(8),
		@FILEDATE	DATETIME,
		@RECCNT	INT,
		@SAUDA_DATE	VARCHAR(11),
		@EXCHANGE VARCHAR(3),
		@SEGMENT VARCHAR(7)

		  
SET @SAUDA_DATE = CONVERT(DATETIME,@TDATE,103)

SET @FILEDT = LEFT(RIGHT(@FILENAME,12),8)

SET @FILEDATE = CONVERT(DATETIME,LEFT(@FILEDT,2)+'/'+RIGHT(LEFT(@FILEDT,4),2)+'/'+RIGHT(@FILEDT,4),103)

IF CONVERT(DATETIME,@SAUDA_DATE) <> @FILEDATE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'SELECTED DATE IS NOT MATCHING WITH THE FILE DATE.'
	RETURN
END
 
IF @FILENAME LIKE '%CollateralBreakup_FO%'  OR @FILENAME LIKE '%CollateralBreakup_CD%'   
BEGIN
	SELECT TMCODE,CLIENT_CODE,ISIN,SYMBOL,QTY,NET_EFF_QTY INTO #TBL_CCCM_SCRIP_DATA FROM TBL_CCCM_SCRIP_DATA_INTRADAY
	WHERE 1 = 2

	IF @FILETYPE = 'BLK'
	BEGIN
		SET @STRSQL = LOWER('BULK INSERT #TBL_CCCM_SCRIP_DATA FROM ''' + @FILENAME  + '''  WITH (FIELDTERMINATOR = '','',ROWTERMINATOR = ''\n'')')          
		EXEC(@STRSQL)
	END
	ELSE
	BEGIN  
		INSERT INTO #TBL_CCCM_SCRIP_DATA
		SELECT 
			TMCODE = DBO.PIECE(FILE_DATA,',',1),
			PARTY_CODE = (CASE WHEN DBO.PIECE(FILE_DATA,',',2) = 'PROPRIETARY' THEN '' ELSE DBO.PIECE(FILE_DATA,',',2) END),
			ISIN = DBO.PIECE(FILE_DATA,',',3),
			SYMBOL = DBO.PIECE(FILE_DATA,',',4), 
			QTY = DBO.PIECE(FILE_DATA,',',5),
			NET_EFF_QTY = DBO.PIECE(FILE_DATA,',',6) 
		FROM FILEDATA  (NOLOCK)
		WHERE PROGID= @PROGID
		AND ISNULL(DBO.PIECE(FILE_DATA,',',1),'') <> '' 
		AND DBO.PIECE(FILE_DATA,',',1) NOT LIKE '%TM CODE%' 
		AND DBO.PIECE(FILE_DATA,',',1) NOT LIKE '%All amounts in Rs.%' 
		AND DBO.PIECE(FILE_DATA,',',3) LIKE 'IN%' 
	END

	SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #TBL_CCCM_SCRIP_DATA
	IF @RECCNT = 0 
	BEGIN
		SELECT STRMSGCODE = -1, STRMSG = 'NO DATA FOUND IN THE FILE, PLEASE CHECK AND IMPORT CORRECT FILE'
		RETURN
	END
END
ELSE
BEGIN
	SELECT CMCODE=TMCODE,TMCODE,CLIENT_CODE,ISIN,SYMBOL,QTY,NET_EFF_QTY INTO #TBL_CCCM_SCRIP_DATA_CM FROM TBL_CCCM_SCRIP_DATA_INTRADAY
	WHERE 1 = 2

	IF @FILETYPE = 'BLK'
	BEGIN
		SET @STRSQL = LOWER('BULK INSERT #TBL_CCCM_SCRIP_DATA_CM FROM ''' + @FILENAME  + '''  WITH (FIELDTERMINATOR = '','',ROWTERMINATOR = ''\n'')')          
		EXEC(@STRSQL)
	END
	ELSE
	BEGIN  
		INSERT INTO #TBL_CCCM_SCRIP_DATA_CM
		SELECT 
			CMCODE = DBO.PIECE(FILE_DATA,',',1),
			TMCODE = DBO.PIECE(FILE_DATA,',',2),
			PARTY_CODE = (CASE WHEN DBO.PIECE(FILE_DATA,',',3) = 'PROPRIETARY' THEN '' ELSE DBO.PIECE(FILE_DATA,',',3) END),
			ISIN = DBO.PIECE(FILE_DATA,',',4),
			SYMBOL = DBO.PIECE(FILE_DATA,',',5), 
			QTY = DBO.PIECE(FILE_DATA,',',6),
			NET_EFF_QTY = DBO.PIECE(FILE_DATA,',',7) 
		FROM FILEDATA  (NOLOCK)
		WHERE PROGID= @PROGID
		AND ISNULL(DBO.PIECE(FILE_DATA,',',1),'') <> '' 
		AND DBO.PIECE(FILE_DATA,',',1) NOT LIKE '%CM CODE%' 
		AND DBO.PIECE(FILE_DATA,',',1) NOT LIKE '%All amounts in Rs.%' 
		AND DBO.PIECE(FILE_DATA,',',4) LIKE 'IN%' 
	END

	SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #TBL_CCCM_SCRIP_DATA_CM
	IF @RECCNT = 0 
	BEGIN
		SELECT STRMSGCODE = -1, STRMSG = 'NO DATA FOUND IN THE FILE, PLEASE CHECK AND IMPORT CORRECT FILE'
		RETURN
	END
END

SET @EXCHANGE = ''

IF @FILENAME LIKE '%CollateralBreakup_FO%'   
BEGIN
	SET @EXCHANGE = 'NSE'
	SET @SEGMENT = 'FUTURES'
END
	
IF @FILENAME LIKE '%CollateralBreakup_CD%'   
BEGIN
	SET @EXCHANGE = 'NSX'
	SET @SEGMENT = 'FUTURES'
END
	
IF @FILENAME LIKE '%CollateralBreakup%' AND @EXCHANGE = '' 
BEGIN
	SET @EXCHANGE = 'NSE'
	SET @SEGMENT = 'CAPITAL'
END

DELETE FROM TBL_CCCM_SCRIP_DATA_INTRADAY
WHERE TRANSDATE = @FILEDATE AND EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT

IF @FILENAME LIKE '%CollateralBreakup_FO%'  OR @FILENAME LIKE '%CollateralBreakup_CD%'   
BEGIN
	INSERT INTO TBL_CCCM_SCRIP_DATA_INTRADAY
	SELECT TRANSDATE=@FILEDATE,@EXCHANGE,@SEGMENT,TMCODE,CLIENT_CODE,ISIN,SYMBOL,QTY,NET_EFF_QTY,
	UPLOAD_BY=@UNAME,UPLOAD_ON=GETDATE()
	FROM #TBL_CCCM_SCRIP_DATA

	DROP TABLE #TBL_CCCM_SCRIP_DATA 
END
ELSE
BEGIN
	INSERT INTO TBL_CCCM_SCRIP_DATA_INTRADAY
	SELECT TRANSDATE=@FILEDATE,@EXCHANGE,@SEGMENT,TMCODE,CLIENT_CODE,ISIN,SYMBOL,QTY,NET_EFF_QTY,
	UPLOAD_BY=@UNAME,UPLOAD_ON=GETDATE()
	FROM #TBL_CCCM_SCRIP_DATA_CM

	DROP TABLE #TBL_CCCM_SCRIP_DATA_CM 
END

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END

GO
