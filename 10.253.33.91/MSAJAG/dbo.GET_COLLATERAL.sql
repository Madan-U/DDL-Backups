CREATE PROC [dbo].[GET_COLLATERAL]
(                
 @FROMDATE VARCHAR(11),      
 @TODATE   VARCHAR(11)        
)                
AS                
            
SELECT PARTY_CODE INTO #TBL_MARGINPARTY FROM ANGELFO.NSEFO.DBO.TBL_MARGINPARTY            
        
CREATE CLUSTERED INDEX party_idx on #TBL_MARGINPARTY        
(        
 party_code        
)        
  
DROP TABLE MAR_COLLATERAL_TEMP                  
DROP TABLE MAR_COLLATERALDETAILS_TEMP                  
/*                  
SELECT * INTO COLLATERAL_TEMP FROM MSAJAG.DBO.BAK_COLLATERAL C                  
WHERE TRANS_DATE =  @FROMDATE                
AND Exchange NOT IN ('MCX', 'NCX')                
AND EXISTS (SELECT PARTY_CODE FROM #TBL_MARGINPARTY TBL_MARGINPARTY               
  WHERE TBL_MARGINPARTY.PARTY_CODE = c.PARTY_CODE)              
                  
SELECT * INTO COLLATERALDETAILS_TEMP FROM MSAJAG.DBO.BAK_COLLATERALDETAILS C                   
WHERE EFFDATE =  @FROMDATE                
AND Exchange NOT IN ('MCX', 'NCX')                
AND EXISTS (SELECT PARTY_CODE FROM #TBL_MARGINPARTY TBL_MARGINPARTY               
  WHERE TBL_MARGINPARTY.PARTY_CODE = c.PARTY_CODE)          
*/        
        
SELECT * INTO MAR_COLLATERAL_TEMP FROM MSAJAG.DBO.COLLATERAL C                  
WHERE TRANS_DATE >= @FROMDATE                
AND Trans_Date <= @TODATE      
AND EXISTS (SELECT PARTY_CODE FROM #TBL_MARGINPARTY TBL_MARGINPARTY               
  WHERE TBL_MARGINPARTY.PARTY_CODE = c.PARTY_CODE)              
/*
DELETE FROM MAR_COLLATERAL_TEMP      
WHERE EXCHANGE IN ('MCX', 'NCX')    
*/        
SELECT * INTO MAR_COLLATERALDETAILS_TEMP FROM MSAJAG.DBO.COLLATERALDETAILS C                 
WHERE 1 = 2     
    
DECLARE @TDATE DATETIME,    
  @TCUR CURSOR    
      
SET @TCUR = CURSOR FOR    
SELECT DISTINCT TRANS_DATE FROM MAR_COLLATERAL_TEMP    
ORDER BY 1    
OPEN @TCUR     
FETCH NEXT FROM @TCUR INTO @TDATE     
WHILE @@FETCH_STATUS = 0     
BEGIN    
     
 INSERT INTO MAR_COLLATERALDETAILS_TEMP    
 SELECT * FROM MSAJAG.DBO.COLLATERALDETAILS C                 
 WHERE EFFDATE = @TDATE     
     
 FETCH NEXT FROM @TCUR INTO @TDATE     
END     
CLOSE @TCUR    
DEALLOCATE @TCUR    
        
DELETE FROM MAR_COLLATERALDETAILS_TEMP        
WHERE NOT EXISTS (SELECT PARTY_CODE FROM #TBL_MARGINPARTY TBL_MARGINPARTY               
  WHERE TBL_MARGINPARTY.PARTY_CODE = MAR_COLLATERALDETAILS_TEMP.PARTY_CODE)              

/*
DELETE FROM MAR_COLLATERALDETAILS_TEMP      
WHERE EXCHANGE IN ('MCX', 'NCX') 
*/

GO
