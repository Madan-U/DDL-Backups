-- Object: PROCEDURE dbo.RPT_STAMP_MISMATCH_DET
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


create PROC [dbo].[RPT_STAMP_MISMATCH_DET]
(
	@SAUDA_DATE VARCHAR(11)
)
AS
SELECT PARTY_CODE, SCRIP_CD, SERIES, TOTALSTAMP_EX=SUM(TOTALSTAMP_EX),TOTALSTAMP_BO=SUM(TOTALSTAMP_BO), DIFF = SUM(TOTALSTAMP_EX-TOTALSTAMP_BO)
FROM (
SELECT PARTY_CODE, SCRIP_CD, SERIES, TOTALSTAMP_EX=SUM(TOTALSTAMP),TOTALSTAMP_BO=CONVERT(NUMERIC(18,2),0)
FROM TBL_STAMP_EXCHG
WHERE SAUDA_DATE = @SAUDA_DATE
AND RECTYPE = 30
GROUP BY PARTY_CODE,SCRIP_CD, SERIES 
UNION ALL
SELECT PARTY_CODE, SCRIP_CD, SERIES,TOTALSTAMP_EX=CONVERT(NUMERIC(18,2),0), TOTALSTAMP_BO=SUM(BUYSQSTAMP+BUYDELSTAMP)
FROM TBL_STAMP_DATA 
WHERE SAUDA_DATE = @SAUDA_DATE
GROUP BY PARTY_CODE,SCRIP_CD, SERIES ) A 
GROUP BY PARTY_CODE,SCRIP_CD, SERIES
HAVING ABS(SUM(TOTALSTAMP_EX-TOTALSTAMP_BO)) > 0

GO
