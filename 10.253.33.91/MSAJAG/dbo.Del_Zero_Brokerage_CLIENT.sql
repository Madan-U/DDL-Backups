CREATE PROC Del_Zero_Brokerage_CLIENT
AS

SELECT DISTINCT B2C_SB INTO #B2C
FROM [MIS].REMISIOR.DBO.B2C_SB S WITH(NOLOCK)  WHERE ISNULL(B2C_SB,'')  NOT IN ('AMRVT') 

SELECT DISTINCT D.CL_CODE,SUB_BROKER,ACTIVE_DATE
FROM #B2C S WITH(NOLOCK),CLIENT_DETAILS D WITH(NOLOCK),
(SELECT CL_CODE,MIN(ACTIVE_DATE) ACTIVE_DATE FROM CLIENT_BROK_DETAILS WITH(NOLOCK)
WHERE SEGMENT ='CAPITAL' GROUP BY  CL_CODE )T 
WHERE  B2C_SB=sub_broker AND Active_Date BETWEEN CONVERT(VARCHAR(11),GETDATE(),120)
AND CONVERT(VARCHAR(11),GETDATE()+30,120)
AND D.CL_CODE = T.CL_CODE

GO
