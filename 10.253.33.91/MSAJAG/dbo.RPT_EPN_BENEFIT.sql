CREATE PROCEDURE [dbo].[RPT_EPN_BENEFIT] (@RUNDATE VARCHAR(11),@PARTY VARCHAR(11),@RPT VARCHAR(11))
AS ---- EXEC  MSAJAG.DBO.RPT_EPN_BENEFIT 'Aug 30 2021','GRMB131', ''

SELECT SETT_NO,SETT_TYPE,TRANSDATE=CONVERT(VARCHAR(11),TRANSDATE,105),PARTY_CODE,SCRIP_CD,SERIES,SELLQTY,EPNQTY_CC,NONEPNQTY,MARGIN_REQUIREMENT,SELLAMOUNT,SQOFFAMT,CASHBENEFIT,MARGIN_BENEFIT
FROM MSAJAG.DBO.TBL_EPN_BENEFIT WITH (NOLOCK)
WHERE TRANSDATE > =  @RUNDATE  AND TRANSDATE < =  @RUNDATE + ' 23:59'
AND PARTY_CODE = @PARTY
ORDER BY PARTY_CODE

GO
