CREATE PROC [dbo].[CLS_ENABLE_USER]
(
	
	@UID VARCHAR(25)='',
	@FLAG VARCHAR(7)='',
	@STATUSID VARCHAR(15) ='',
	@STATUSNAME VARCHAR(50) =''
)

AS
BEGIN


IF(@FLAG='D')--GET DATA OF USERID
 BEGIN
	IF @STATUSID = 'CLIENT'
	 begin
		SELECT LOWER(ISNULL(EMAIL,''))+'|'+LOWER(ISNULL(FLDADDRESS2,'')) AS EMAIL,A.MOBILE_PAGER+'|'+STUFF(A.MOBILE_PAGER, 2, 3, '***') AS MOBILE,UPPER(A.PAN_GIR_NO) AS PAN,CONVERT(VARCHAR,CONVERT(DATETIME,dob,109),103) AS DOB, 
			STUFF(EMAIL, 2, 5, '***')+'|'+STUFF(FLDADDRESS2, 2, 5, '***') as SECRET_EMAIL
		FROM
			CLIENT_DETAILS A, TBLPRADNYAUSERS B
		WHERE CL_CODE=@UID AND FLDUSERNAME=@UID
	  END
	 ELSE
	  BEGIN
		SELECT LOWER(ISNULL(FLDADDRESS2,'')) AS EMAIL,B.Fldphone2 as MOBILE, PAN = '' , DOB = '', 
			STUFF(FLDADDRESS2, 2, 5, '***') as SECRET_EMAIL
		FROM
			 TBLPRADNYAUSERS B
		WHERE FLDUSERNAME=@UID
	  END
 END


IF(@FLAG='U')--FOR RE-ENABLING
BEGIN
	DECLARE @USERID VARCHAR(50)

	SELECT @USERID=FLDAUTO FROM TBLPRADNYAUSERS WHERE FLDUSERNAME=@UID
	PRINT @USERID 
	UPDATE TBLUSERCONTROLMASTER 
	SET FLDSTATUS=0 
	WHERE FLDUSERID=@USERID

END



IF(@FLAG='E')--FOR SENDING OTP ON EMAIL 
BEGIN

CREATE TABLE #TEMP_OTP
(
OTP VARCHAR(20)

)

--select * from #temp_otp
DECLARE @OTP VARCHAR(100),
@EMAILFROM VARCHAR(200),  
@EMAILTO VARCHAR(200),  
@EMAILSUBJECT VARCHAR(300),  
@EMAILMESSAGE VARCHAR(MAX),  
@SMTP_SRV_NAME VARCHAR(60),  
@COMPANYNAME VARCHAR(200)

