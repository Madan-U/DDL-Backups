SET @EMAILSUBJECT = 'OTP TO ENABLE USERID FROM  ' + @COMPANYNAME  
--************************
SET @EMAILMESSAGE = 'Dear Customer,<BR><BR>As per your request, Please find your ONE TIME PASSWORD<BR><BR>'  
SET @EMAILMESSAGE = @EMAILMESSAGE +  'Your OTP IS : <FONT COLOR = RED> '  
SET @EMAILMESSAGE = @EMAILMESSAGE +   @OTP  + '<BR> </FONT><BR><BR>Please Enter the same on the OTP Screen in front of you.'  
SET @EMAILMESSAGE = @EMAILMESSAGE +  '<BR>Do not Share this OTP with any one.<BR><BR>Assuring you of our best services.<BR><BR>'  
SET @EMAILMESSAGE = @EMAILMESSAGE +  'REGARDS, <BR><BR>CUSTOMER CARE<BR>'
SET @EMAILMESSAGE = @EMAILMESSAGE +  @COMPANYNAME + '<BR>'    


SELECT EMAILFROM = @EMAILFROM, EMAILSUBJECT = REPLACE( @EMAILSUBJECT, '&','~' ),   
EMAILMESSAGE = Replace (REPLACE(REPLACE( @EMAILMESSAGE, '<', '$' ),'>','@'),'&','~') , SMTP_SRV_NAME = @SMTP_SRV_NAME ,OTP=@OTP 







END








