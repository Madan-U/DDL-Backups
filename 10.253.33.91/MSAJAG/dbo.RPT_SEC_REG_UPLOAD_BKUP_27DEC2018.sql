-- Object: PROCEDURE dbo.RPT_SEC_REG_UPLOAD_BKUP_27DEC2018
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------




Create	PROC [dbo].[RPT_SEC_REG_UPLOAD_BKUP_27DEC2018]
		(
		@FROMDATE	VARCHAR(11),
		@TODATE		VARCHAR(11)
		)
	
		AS

		/*
		EXEC	RPT_SEC_REG_UPLOAD 'FEB 20 2011', 'APR 13 2018'
		*/		
		
		DECLARE @FILENAME   VARCHAR(50)
		DECLARE @BATCHNO	INT  
		DECLARE @MEMBERCODE VARCHAR(20)
		DECLARE @MEMBERNAME	VARCHAR(500)
		DECLARE @NEWFROMDATE VARCHAR(11)

		SELECT @MEMBERCODE = MEMBERCODE, @MEMBERNAME = ISNULL(LTRIM(RTRIM(COMPANY)),'') FROM OWNER (NOLOCK)
		SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0)+1 FROM ASIANFILEBATCHNO  
		WHERE FLDFILENO = 'SR'  
		AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		
		  
		IF @BATCHNO = 1  
		BEGIN  
			INSERT INTO ASIANFILEBATCHNO VALUES ('SR', @BATCHNO, GETDATE(), GETDATE())  
		END  
		ELSE  
		BEGIN  
			UPDATE ASIANFILEBATCHNO SET FLDBATCHNO = @BATCHNO WHERE FLDFILENO = 'SR' AND LEFT(FLDFILEDATE,11) = LEFT(GETDATE(),11)
		END  
		  
		SET @FILENAME = @MEMBERCODE + '_SR_' + REPLACE(CONVERT(VARCHAR,GETDATE(),103),'/','') + '_' + RIGHT('00'+CONVERT(VARCHAR,@BATCHNO),2) 

		
		CREATE TABLE #SECREG
		(
		TRANSDATE	VARCHAR(11),
		SETT_NO		VARCHAR(10),
		SETT_TYPE	VARCHAR(5),
		PARTY_CODE	VARCHAR(20),
		LONG_NAME	VARCHAR(150),
		L_ADDRESS1	VARCHAR(150),
		L_ADDRESS2	VARCHAR(150),
		L_ADDRESS3	VARCHAR(150),
		L_CITY		VARCHAR(50),
		L_STATE		VARCHAR(50),
		L_ZIP		VARCHAR(20),
		FAMILY		VARCHAR(20),
		SCRIP_CD	VARCHAR(30),
		SCRIP_NAME	VARCHAR(500),
		CERTNO		VARCHAR(20),
		CQTY		NUMERIC(20,3),
		DQTY		NUMERIC(20,3),
		SLIPNO		VARCHAR(20),
		DPID		VARCHAR(20),
		CLTDPID		VARCHAR(20),
		TRTYPE		VARCHAR(20),
		REASON		VARCHAR(500),
		BDPID		VARCHAR(20),
		BCLTDPID	VARCHAR(20),
		TRANSDATE1	VARCHAR(11),
		FLAG		INT,
		PLEDGE_UNPLEDGE_QTY	NUMERIC(20,3),
		TRF_REF_NO			VARCHAR(20),
		POA_FLAG			VARCHAR(5),
		TRANSACTION_TYPE	VARCHAR(20),
		PURPOSE				VARCHAR(500),
		UCC_CODE			VARCHAR(20),
		EXCHANGE			VARCHAR(5),
		SEGMENT				VARCHAR(5),
		--PHONE_NO			VARCHAR(250),
		--EMAIL				VARCHAR(150),
		PANNO				VARCHAR(20),
		EXCHANGEFLAG		VARCHAR(5)
		)

		CREATE INDEX [DELHOLD_SECREG] ON [DBO].[#SECREG] (
			[TRANSDATE],
			[PARTY_CODE],
			[SCRIP_CD],
			[BDPID],
			[BCLTDPID]
			)
		
		
		INSERT INTO #SECREG
		EXEC ANGELDEMAT.MSAJAG.DBO.[RPT_TRANSSTATEMENTBEN_NEW_UPD] @FROMDATE, @TODATE, '', 'zzzzzzz', '', 'zzzzzzz', '', '', 1, 'BROKER', 'BROKER'
	

		INSERT INTO #SECREG
		EXEC ANGELDEMAT.BSEDB.DBO.[RPT_TRANSSTATEMENTBEN_NEW_UPD] @FROMDATE, @TODATE, '', 'zzzzzzz', '', 'zzzzzzz', '', '', 1, 'BROKER', 'BROKER'


		ALTER TABLE #SECREG 
		ADD	MEMBER_DEMAT_ACC  VARCHAR(20),
			COUNTERPARTY_DEMAT_ACC	VARCHAR(20)


		UPDATE	#SECREG
		SET		MEMBER_DEMAT_ACC = (
				CASE 
					WHEN ISNULL(DP.DPID,'') = #SECREG.DPID AND ISNULL(DP.DPCLTNO,'') = #SECREG.CLTDPID THEN 
					CASE 
						WHEN LEN(#SECREG.DPID+#SECREG.CLTDPID) >= 16 THEN #SECREG.CLTDPID
						ELSE #SECREG.DPID+#SECREG.CLTDPID
					END
					ELSE ''
				END)
		FROM	DELIVERYDP DP
		WHERE	DP.DPID = #SECREG.DPID AND EXCHANGEFLAG = 'NSECM'
				


		UPDATE	#SECREG
		SET		COUNTERPARTY_DEMAT_ACC = (
				CASE 
					WHEN ISNULL(DP.DPID,'') <> #SECREG.DPID OR ISNULL(DP.DPCLTNO,'') <> #SECREG.CLTDPID THEN 
					CASE 
						WHEN LEN(#SECREG.DPID+#SECREG.CLTDPID) >= 16 THEN #SECREG.CLTDPID
						ELSE #SECREG.DPID+#SECREG.CLTDPID
					END
					ELSE ''
				END)
		FROM	DELIVERYDP DP
		WHERE	DP.DPID <> #SECREG.DPID AND EXCHANGEFLAG = 'NSECM'
		


		UPDATE	#SECREG
		SET		MEMBER_DEMAT_ACC = (
				CASE 
					WHEN ISNULL(DP.DPID,'') = #SECREG.DPID AND ISNULL(DP.DPCLTNO,'') = #SECREG.CLTDPID THEN 
					CASE 
						WHEN LEN(#SECREG.DPID+#SECREG.CLTDPID) >= 16 THEN #SECREG.CLTDPID
						ELSE #SECREG.DPID+#SECREG.CLTDPID
					END
					ELSE ''
				END)
		FROM	DELIVERYDP DP
		WHERE	DP.DPID = #SECREG.DPID AND EXCHANGEFLAG = 'BSECM'
				


		UPDATE	#SECREG
		SET		COUNTERPARTY_DEMAT_ACC = (
				CASE 
					WHEN ISNULL(DP.DPID,'') <> #SECREG.DPID OR ISNULL(DP.DPCLTNO,'') <> #SECREG.CLTDPID THEN 
					CASE 
						WHEN LEN(#SECREG.DPID+#SECREG.CLTDPID) >= 16 THEN #SECREG.CLTDPID
						ELSE #SECREG.DPID+#SECREG.CLTDPID
					END
					ELSE ''
				END)
		FROM	DELIVERYDP DP
		WHERE	DP.DPID <> #SECREG.DPID AND EXCHANGEFLAG = 'BSECM'
						
		
		SELECT	TRANS_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,TRANSDATE,103),105),
				EXE_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,TRANSDATE,103),105),
				EXCHANGE,
				SEGMENT,
				UCC_CODE = PARTY_CODE,
				PARTY_CODE = UCC_CODE,
				CLIENT_NAME = LONG_NAME,
				CLIENT_PAN = ISNULL(PANNO,''),
				MEMBER_DEMAT_ACC = DPID+CLTDPID,
				COUNTERPARTY_DEMAT_ACC = BDPID+BCLTDPID,
				SETT_NO,
				ISIN = CERTNO,
				SCRIP_NAME = SCRIP_CD,
				QTY_DELIVERED = DQTY,
				QTY_RECEIVED = CQTY, 
				BALANCE_QTY = (DQTY-CQTY), 
				PLEDGE_UNPLEDGE_QTY,
				TRF_REF_NO,
				POA_FLAG,
				TRANSACTION_TYPE,
				PURPOSE = REASON,
				ORDFLAG = 0,
				ORDDATE = CONVERT(VARCHAR,CONVERT(DATETIME,TRANSDATE,103),112)
		INTO	#SECREG_NEW
		FROM	#SECREG S
		ORDER BY
				PARTY_CODE,
				EXCHANGE,
				SCRIP_CD,
				CERTNO,
				CONVERT(VARCHAR,CONVERT(DATETIME,TRANSDATE,103),112),
				FLAG,
				SETT_NO


		---SELECT	* FROM #SECREG_NEW		

		SELECT	TOP 1 FLAG = 1, DATA = @MEMBERNAME +','+ REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@FROMDATE,109),103),'/','') +' to ' + REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@TODATE,109),103),'/',''),
				EXCHANGE = '',
				SEGMENT = '',
				UCC_CODE = '',
				ORDDATE = '',
				SCRIP_NAME = '',
				ISIN = '',
				ORDFLAG = -1
		INTO	#REPORT
		FROM	#SECREG_NEW
		
		UNION		

		SELECT	FLAG = 2,
				DATA =	TRANS_DATE +','+ EXE_DATE +','+ EXCHANGE +','
						+ SEGMENT +','+ LTRIM(RTRIM(UCC_CODE)) +','+ LTRIM(RTRIM(PARTY_CODE)) +','
						+ CLIENT_NAME +','+ CLIENT_PAN +','+ MEMBER_DEMAT_ACC +','
						+ COUNTERPARTY_DEMAT_ACC +','+ SETT_NO +','+ ISIN +','
						+ SCRIP_NAME +','+ CONVERT(VARCHAR,QTY_DELIVERED) +','
						+ CONVERT(VARCHAR,QTY_RECEIVED) +','+ CONVERT(VARCHAR,BALANCE_QTY) +','
						+ CONVERT(VARCHAR,PLEDGE_UNPLEDGE_QTY) +','+ CONVERT(VARCHAR,TRF_REF_NO) +','
						+ POA_FLAG +','+ TRANSACTION_TYPE +','+ PURPOSE,						
				EXCHANGE,
				SEGMENT,
				UCC_CODE,
				ORDDATE,
				SCRIP_NAME,
				ISIN,
				ORDFLAG
		FROM	#SECREG_NEW
		ORDER BY
				UCC_CODE,
				EXCHANGE,
				SEGMENT,
				ISIN,
				ORDFLAG,
				SCRIP_NAME,
				ORDDATE

		SELECT	FILENAME = @FILENAME, DATA FROM #REPORT

		
		DROP TABLE #SECREG
		DROP TABLE #SECREG_NEW
		DROP TABLE #REPORT

GO
