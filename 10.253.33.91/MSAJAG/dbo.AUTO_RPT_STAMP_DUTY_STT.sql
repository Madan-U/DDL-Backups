--ALtering SP for SRE-20334
CREATE PROC [dbo].[AUTO_RPT_STAMP_DUTY_STT]  
--
(  @D VARCHAR(11),@F VARCHAR(4)  
)  
AS  
  
BEGIN   
IF @F ='CASH'  
 BEGIN  
SELECT PARTY_CODE,CONVERT(VARCHAR(10),SAUDA_DATE,120)SAUDA_DATE,SUM(BROKER_CHRG) AS STAMP_DUTY,SUM(INS_CHRG) AS STT,  
SETT_TYPE FROM (  
SELECT PARTY_CODE,SUM(BROKER_CHRG) BROKER_CHRG ,SUM(INS_CHRG) INS_CHRG ,SETT_TYPE,CONVERT(VARCHAR(10),SAUDA_DATE,120)SAUDA_DATE  
FROM SETTLEMENT(NOLOCK) WHERE SAUDA_DATE >=@D  
 AND SAUDA_DATE<= @D+' 23:59' --AND SETT_TYPE ='W'  
GROUP BY PARTY_CODE ,CONVERT(VARCHAR(10),SAUDA_DATE,120),SETT_TYPE  
UNION ALL  
SELECT PARTY_CODE,SUM(BROKER_CHRG) BROKER_CHRG,SUM(INS_CHRG)STT,SETT_TYPE,CONVERT(VARCHAR(10),SAUDA_DATE,120)SAUDA_DATE  
 FROM [AngelBSECM].BSEDB_AB.DBO.SETTLEMENT WITH(NOLOCK)  
WHERE SAUDA_DATE >=@D AND SAUDA_DATE<=@D+' 23:59' --AND SETT_TYPE ='C'  
GROUP BY PARTY_CODE,CONVERT(VARCHAR(10),SAUDA_DATE,120),SETT_TYPE  
  
 )A GROUP BY PARTY_CODE,CONVERT(VARCHAR(10),SAUDA_DATE,120),SETT_TYPE  
 ORDER BY PARTY_CODE  
 END  
 --FO  
   
IF @F ='FO'  
 BEGIN  
SELECT PARTY_CODE,CONVERT(VARCHAR(10),SAUDA_DATE,120)SAUDA_DATE,SUM(BROKER_CHRG) AS STAMP_DUTY,  
SUM(INS_CHRG) AS STT, SETT_TYPE  
 FROM ANGELFO.NSEFO.DBO.FOSETTLEMENT WHERE SAUDA_DATE >=@D AND SAUDA_DATE<=@D+' 23:59' --AND SETT_TYPE IN ('D','C')  
GROUP BY PARTY_CODE,CONVERT(VARCHAR(10),SAUDA_DATE,120),SETT_TYPE
UNION ALL
SELECT PARTY_CODE,CONVERT(VARCHAR(10),SAUDA_DATE,120)SAUDA_DATE,SUM(BROKER_CHRG) AS STAMP_DUTY,
SUM(INS_CHRG) AS STT, SETT_TYPE
FROM ANGELCOMMODITY.BSEFO.DBO.BFOSETTLEMENT WHERE SAUDA_DATE >=@D AND SAUDA_DATE<=@D+' 23:59' --AND SETT_TYPE IN ('D','C')
GROUP BY PARTY_CODE,CONVERT(VARCHAR(10),SAUDA_DATE,120),SETT_TYPE

  
END  
  
IF @F IS NULL  
 BEGIN  
 SELECT 1,1,1,1  
 END   
  
  
END

GO
