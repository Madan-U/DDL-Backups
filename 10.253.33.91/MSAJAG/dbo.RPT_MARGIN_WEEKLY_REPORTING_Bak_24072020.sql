Create PROCEDURE [dbo].[RPT_MARGIN_WEEKLY_REPORTING_Bak_24072020]
	@MARGIN_FROM VARCHAR(11),
	@MARGIN_TO VARCHAR(11),
	@FROM_PARTY VARCHAR(10),
	@TO_PARTY VARCHAR(10)
AS

CREATE TABLE #CAL_DATES
(
	CAL_DATE DATETIME
)

INSERT INTO #CAL_DATES
EXEC GET_CAL_DATES @MARGIN_FROM, @MARGIN_TO


CREATE TABLE #TMP_MARGIN_WEEKLY_REPORTING
(
	PARTY_CODE VARCHAR(10),
	MARGIN_DATE DATETIME,
	MARGIN_AMOUNT MONEY,
	EXCHANGE VARCHAR(10),
	SEGMENT VARCHAR(10),
	PROCESS_DT DATETIME
)

	DECLARE
		@LAST_MARGIN_DATE VARCHAR(11)

	SELECT @LAST_MARGIN_DATE = CONVERT(VARCHAR(11), MAX(MARGINDATE), 109) FROM VW_WEEKLY_MARGIN WHERE MARGINDATE <= @MARGIN_FROM

	CREATE TABLE #DBNAME
		(
			SHARESERVER	VARCHAR(50),
			ACCOUNTDB		VARCHAR(20), 
			EXCHANGE		VARCHAR(3), 
			SEGMENT			VARCHAR(20), 
			SHAREDB			VARCHAR(20) 
		)

		DECLARE @@MAINREC AS CURSOR,          
			@@ACCOUNTDB VARCHAR(20),            
			@@SHAREDB VARCHAR(20),               
			@@EXCHANGE VARCHAR(15),              
			@@SEGMENT VARCHAR(15),          
			@SQL	VARCHAR(8000),
			@@SHARESERVER VARCHAR(50)

		INSERT	INTO #DBNAME 
		SELECT	SHARESERVER, ACCOUNTDB, EXCHANGE, SEGMENT, SHAREDB
		FROM	PRADNYA.DBO.MULTICOMPANY WHERE PRIMARYSERVER = 1
		UNION ALL
		SELECT DISTINCT SHARESERVER, ACCOUNTDB='MTFTRADE', EXCHANGE='MTF', SEGMENT='MTF', SHAREDB='MTFTRADE'
		FROM  PRADNYA.DBO.MULTICOMPANY 
		WHERE EXCHANGE IN ('NSE','BSE') AND SEGMENT = 'CAPITAL' AND SHARESERVER ='ANAND1'

		SET @@MAINREC = CURSOR FOR              
		SELECT '[' + replace(replace(SHARESERVER, '[', ''), ']', '') + ']', ACCOUNTDB, EXCHANGE, SEGMENT,SHAREDB FROM #DBNAME

		               
		OPEN @@MAINREC              

		FETCH NEXT FROM @@MAINREC INTO @@SHARESERVER, @@ACCOUNTDB, @@EXCHANGE, @@SEGMENT, @@SHAREDB
		WHILE @@FETCH_STATUS = 0              
		BEGIN
			IF @MARGIN_FROM <> @MARGIN_TO
			BEGIN
				SET @SQL = "INSERT INTO #TMP_MARGIN_WEEKLY_REPORTING "
				SET @SQL = @SQL + " SELECT PARTY_CODE, MARGINDATE, MARGIN_AMOUNT = ISNULL(MARGIN_AMOUNT, -1), '" + @@EXCHANGE + "','" +  @@SEGMENT + ", CAL_DATE "
				SET @SQL = @SQL + " FROM #CAL_DATES "
				
				IF @@SERVERNAME <> @@SHARESERVER

					SET @SQL = @SQL + " 	LEFT OUTER JOIN " + @@SHARESERVER + "." + @@SHAREDB + ".DBO.VW_WEEKLY_MARGIN ON CAL_DATE = MARGINDATE "
				ELSE
					SET @SQL = @SQL + " 	LEFT OUTER JOIN " + @@SHAREDB + ".DBO.VW_WEEKLY_MARGIN ON CAL_DATE = MARGINDATE "
				SET @SQL = @SQL + " WHERE "
				SET @SQL = @SQL + " 	MARGINDATE BETWEEN '" + @LAST_MARGIN_DATE + "' AND '" + @MARGIN_TO + "' 23:59' "
				
				SET @SQL = @SQL + "UPDATE T SET PROCESS_DT = (SELECT MAX(MARGIN_DATE) FROM #TMP_MARGIN_WEEKLY_REPORTING T1 WHERE T.PARTY_CODE = T1.PARTY_CODE AND T1.MARGIN_DATE <= T.MARGIN_DATE AND MARGIN_AMOUNT <> -1) "
				SET @SQL = @SQL + "FROM #TMP_MARGIN_WEEKLY_REPORTING T  "

				SET @SQL = @SQL + "UPDATE T SET MARGIN_AMOUNT = (SELECT MARGIN_AMOUNT FROM #TMP_MARGIN_WEEKLY_REPORTING T1 WHERE T1.PARTY_CODE = T.PARTY_CODE AND T1.PROCESSDT = T.MARGIN_DATE) "
				SET @SQL = @SQL + "FROM #TMP_MARGIN_WEEKLY_REPORTING T WHERE MARGIN_AMOUNT = -1"

				SET @SQL = @SQL + "INSERT INTO TMP_MARGIN_WEEKLY_REPORTING_LOG "
				SET @SQL = @SQL + " SELECT T.*, GETDATE() FROM TMP_MARGIN_WEEKLY_REPORTING T, #TMP_MARGIN_WEEKLY_REPORTING T1 WHERE T.MARGIN_DATE = T1.MARGIN_DATE AND T.EXCHANGE = T1.EXCHANGE AND T.SEGMENT = T1.SEGMENT "

				SET @SQL = @SQL + " DELETE T FROM TMP_MARGIN_WEEKLY_REPORTING T, #TMP_MARGIN_WEEKLY_REPORTING T1 WHERE T.MARGIN_DATE = T1.MARGIN_DATE AND T.EXCHANGE = T1.EXCHANGE AND T.SEGMENT = T1.SEGMENT "

				SET @SQL = @SQL + "INSERT INTO TMP_MARGIN_WEEKLY_REPORTING "
				SET @SQL = @SQL + " SELECT PARTY_CODE, MARGIN_DATE, MARGIN_AMOUNT, EXCHANGE, SEGMENT, GETDATE() FROM #TMP_MARGIN_WEEKLY_REPORTING	"
			END
			ELSE
			BEGIN
				SET @SQL = "INSERT INTO TMP_MARGIN_WEEKLY_REPORTING_LOG "
				SET @SQL = @SQL + "SELECT *, GETDATE() FROM TMP_MARGIN_WEEKLY_REPORTING WHERE MARGIN_DATE = '" + @LAST_MARGIN_DATE + "' AND EXCHANGE = '" + @@EXCHANGE + "' AND SEGMENT = '" + @@SEGMENT + "'"
				
				SET @SQL = @SQL + "DELETE FROM TMP_MARGIN_WEEKLY_REPORTING WHERE MARGIN_DATE = '" + @LAST_MARGIN_DATE + "' AND EXCHANGE = '" + @@EXCHANGE + "' AND SEGMENT = '" + @@SEGMENT + "'"

				
				SET @SQL = @SQL + " INSERT INTO TMP_MARGIN_WEEKLY_REPORTING "
				SET @SQL = @SQL + " SELECT PARTY_CODE, MARGINDATE = '" + @MARGIN_FROM + "', MARGIN_AMOUNT, '" + @@EXCHANGE + "','" +  @@SEGMENT + "', GETDATE() "
				SET @SQL = @SQL + " FROM " + @@SHARESERVER + "." + @@SHAREDB + ".DBO.VW_WEEKLY_MARGIN "
				SET @SQL = @SQL + " WHERE "
				SET @SQL = @SQL + " 	MARGINDATE = '" + @LAST_MARGIN_DATE + "'"
				SET @SQL = @SQL + " 	AND PARTY_CODE >= CASE WHEN '" + @FROM_PARTY + "' <> '' THEN '" + @FROM_PARTY + "' ELSE '0000' END "
				SET @SQL = @SQL + " 	AND PARTY_CODE <= CASE WHEN '" + @TO_PARTY + "' <> '' THEN '" + @TO_PARTY + "' ELSE 'ZZZZZ' END "
			END
			print @SQL
			EXEC (@SQL)

		FETCH NEXT FROM @@MAINREC INTO @@SHARESERVER, @@ACCOUNTDB, @@EXCHANGE, @@SEGMENT, @@SHAREDB
		END

GO
