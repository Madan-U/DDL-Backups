 -- EXEC GET_SEBI_AUDIT_CLIENT_LIST '', 'JUN  7 2021', 1
CREATE PROCEDURE [dbo].[GET_SEBI_AUDIT_CLIENT_LIST_SEBI_SingleClient]
	@EXCHANGE VARCHAR(10),
	@FROMDATE VARCHAR(11) = 'APR  1 2020',
	@FLAG BIT = 1 ,
  @PartyCode VARCHAR(20)
AS

CREATE TABLE #CL_LIST_1
(
	CLTCODE VARCHAR(10)
)

INSERT INTO #CL_LIST_1
--EXEC CLIENTACC @FROMDATE
SELECT CLTCODE FROM ACCOUNT.DBO.LEDGER_ALL WITH(NOLOCK) 
WHERE VTYP = 15 AND VDT BETWEEN DATEADD(D, -365, @FROMDATE) AND @FROMDATE + ' 23:59'
AND CLTCODE =@PartyCode
GROUP BY CLTCODE



IF @FLAG = 0
SELECT CL_CODE FROM CLIENT_BROK_DETAILS WHERE EXCHANGE = CASE WHEN @EXCHANGE <> '' THEN @EXCHANGE ELSE EXCHANGE END

IF @FLAG = 1
BEGIN	
	INSERT INTO #CLIENT_LIST
	SELECT CL_CODE FROM CLIENT_BROK_DETAILS WHERE EXCHANGE = CASE WHEN @EXCHANGE <> '' THEN @EXCHANGE ELSE EXCHANGE END 
	AND Active_Date <= @FROMDATE + ' 23:59' AND CL_CODE =@PartyCode
	AND EXISTS(SELECT CLTCODE FROM #CL_LIST_1 WHERE CL_CODE = CLTCODE)
  group by CL_CODE
END

GO
