-- Object: PROCEDURE dbo.RPT_VANDHA_16112021
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[RPT_VANDHA_16112021] (@FDATE VARCHAR(11),@TDATE VARCHAR(11),@CLTCODE VARCHAR(MAX))

AS ---- EXEC RPT_VANDHA 'AUG  1 2021','AUG 31 2021',''

  SET @FDATE = LEFT(CONVERT(DATETIME,@FDATE,105),11)            
  SET @TDATE = LEFT(CONVERT(DATETIME,@TDATE,105),11)

  CREATE TABLE #NSECM ( VNO VARCHAR(15),VTYP SMALLINT,VANDHA VARCHAR(15))
  CREATE TABLE #BSECM ( VNO VARCHAR(15),VTYP SMALLINT,VANDHA VARCHAR(15))
  CREATE TABLE #NSEFO ( VNO VARCHAR(15),VTYP SMALLINT,VANDHA VARCHAR(15))
  CREATE TABLE #NSECD ( VNO VARCHAR(15),VTYP SMALLINT,VANDHA VARCHAR(15))
  CREATE TABLE #MCDX ( VNO VARCHAR(15),VTYP SMALLINT,VANDHA VARCHAR(15))
  CREATE TABLE #NCDX ( VNO VARCHAR(15),VTYP SMALLINT,VANDHA VARCHAR(15))

IF @CLTCODE <> ''
BEGIN

INSERT INTO #NSECM
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ACCOUNT.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE=@CLTCODE
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #BSECM
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANAND.ACCOUNT_AB.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE=@CLTCODE
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #NSEFO
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE=@CLTCODE
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #NSECD
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE=@CLTCODE
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #MCDX
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE=@CLTCODE
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #NCDX
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE=@CLTCODE
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

END
ELSE
BEGIN

INSERT INTO #NSECM
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ACCOUNT.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE IN (SELECT VANDHA FROM MSAJAG.DBO.TBL_VANDHA_CODE)
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #BSECM
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANAND.ACCOUNT_AB.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE IN (SELECT VANDHA FROM MSAJAG.DBO.TBL_VANDHA_CODE)
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #NSEFO
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANGELFO.ACCOUNTFO.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE IN (SELECT VANDHA FROM MSAJAG.DBO.TBL_VANDHA_CODE)
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #NSECD
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE IN (SELECT VANDHA FROM MSAJAG.DBO.TBL_VANDHA_CODE)
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #MCDX
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE IN (SELECT VANDHA FROM MSAJAG.DBO.TBL_VANDHA_CODE)
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

INSERT INTO #NCDX
SELECT VNO,VTYP,CLTCODE AS VANDHA FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER WITH (NOLOCK) WHERE CLTCODE IN (SELECT VANDHA FROM MSAJAG.DBO.TBL_VANDHA_CODE)
AND VDT BETWEEN @FDATE AND @TDATE + ' 23:59' AND VTYP <> '18'

END


SELECT VANDHA,A.VDT,A.VTYP,A.VNO,A.CLTCODE,A.DRCR,A.VAMT,A.NARRATION,A.ENTEREDBY,SEGMENT='NSECM' INTO #VANDHA
FROM ACCOUNT.DBO.LEDGER A WITH (NOLOCK), #NSECM B WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND CLTCODE <> VANDHA

INSERT INTO #VANDHA
SELECT VANDHA,A.VDT,A.VTYP,A.VNO,A.CLTCODE,A.DRCR,A.VAMT,A.NARRATION,A.ENTEREDBY,SEGMENT='BSECM' 
FROM ANAND.ACCOUNT_AB.DBO.LEDGER A WITH (NOLOCK), #BSECM B WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND CLTCODE <> VANDHA

INSERT INTO #VANDHA
SELECT VANDHA,A.VDT,A.VTYP,A.VNO,A.CLTCODE,A.DRCR,A.VAMT,A.NARRATION,A.ENTEREDBY,SEGMENT='NSEFO' 
FROM ANGELFO.ACCOUNTFO.DBO.LEDGER A WITH (NOLOCK), #NSEFO B WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND CLTCODE <> VANDHA

INSERT INTO #VANDHA
SELECT VANDHA,A.VDT,A.VTYP,A.VNO,A.CLTCODE,A.DRCR,A.VAMT,A.NARRATION,A.ENTEREDBY,SEGMENT='NSECD' 
FROM ANGELFO.ACCOUNTCURFO.DBO.LEDGER A WITH (NOLOCK), #NSECD B WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND CLTCODE <> VANDHA

INSERT INTO #VANDHA
SELECT VANDHA,A.VDT,A.VTYP,A.VNO,A.CLTCODE,A.DRCR,A.VAMT,A.NARRATION,A.ENTEREDBY,SEGMENT='MCDX' 
FROM ANGELCOMMODITY.ACCOUNTMCDX.DBO.LEDGER A WITH (NOLOCK), #MCDX B WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND CLTCODE <> VANDHA

INSERT INTO #VANDHA
SELECT VANDHA,A.VDT,A.VTYP,A.VNO,A.CLTCODE,A.DRCR,A.VAMT,A.NARRATION,A.ENTEREDBY,SEGMENT='NCDX' 
FROM ANGELCOMMODITY.ACCOUNTNCDX.DBO.LEDGER A WITH (NOLOCK), #NCDX B WHERE A.VNO = B.VNO AND A.VTYP = B.VTYP AND CLTCODE <> VANDHA


SELECT VANDHA,VDT=CONVERT(VARCHAR(11),VDT,105),VTYP,VNO,CLTCODE,DRCR,VAMT,NARRATION,ENTEREDBY=UPPER(ENTEREDBY),SEGMENT 
FROM #VANDHA ORDER BY VDT,SEGMENT,DRCR

GO
