CREATE PROCEDURE [dbo].[PR_COMPUTE_MARGIN_PENALTY]      
(      
 @MARGINDATE VARCHAR(11),      
 @USERID VARCHAR(20)      
)      
AS      
      
      -- exec PR_COMPUTE_MARGIN_PENALTY 'oct  7 2020',''
DECLARE @SYSTEMDATE DATETIME      
      
SET @SYSTEMDATE = GETDATE()      
DECLARE @SERVICE_TAX NUMERIC(18,4)  
SELECT @SERVICE_TAX = 0  
  
--SELECT @SERVICE_TAX = SERVICE_TAX FROM FOGLOBALS   
--WHERE @MARGINDATE BETWEEN YEAR_START_DT AND YEAR_END_DT  
  
--SET @SERVICE_TAX = (CASE WHEN CONVERT(DATETIME,@MARGINDATE)   
--BETWEEN CONVERT(DATETIME, 'JUN 22 2017') AND CONVERT(DATETIME, 'JUL  1 2017')  
-- THEN 18 ELSE @SERVICE_TAX END)  
   
CREATE TABLE #CMMARGIN_PENALTY      
(      
 PARTY_CODE VARCHAR(10),  
 MIN_MARGIN_ MONEY,     
 EOD_MARGIN_REQ MONEY,     
 EOD_MARGIN_COLLECTED MONEY,     
 PEAK_MARGIN_REQ MONEY,     
 PEAK_MARGIN_COLLECTED MONEY,     
 SHORTFALL MONEY,         
 PEAK_SHORTFALL MONEY,         
 FINAL_SHORTFALL MONEY,      
 PENALTY_PERC NUMERIC (12,6),      
 PENALTY_AMT MONEY,      
 DAYS_3_COUNT INT,      
 MONTH_5_COUNT INT,                            
 SERVICE_TAX MONEY       
)      
      
INSERT INTO #CMMARGIN_PENALTY      
SELECT PARTY_CODE,
MIN_MARGIN_ = EXTREME_LOSS_MARGIN ,
EXTREME_LOSS_MARGIN=TOTAL_MARGIN,
EOD_MARGIN_COLLECTED,
PEAKMARGIN,
PEAK_MARGIN_COLLECTED,
SHORTFALL=(CASE WHEN EOD_MARGIN_COLLECTED-TOTAL_MARGIN < 0 THEN EOD_MARGIN_COLLECTED-TOTAL_MARGIN ELSE 0 END),
PEAK_SHORTFALL=(CASE WHEN PEAK_MARGIN_COLLECTED-PEAKMARGIN < 0 THEN PEAK_MARGIN_COLLECTED-PEAKMARGIN ELSE 0 END),
ABS(SHORTFALL),0,0,0,0    ,0   
FROM CMMARGIN_REPORT_STAT (NOLOCK)      
WHERE MARGINDATE = @MARGINDATE      
AND SHORTFALL < 0       
      
IF (SELECT COUNT(1) FROM #CMMARGIN_PENALTY) = 0      
BEGIN      
 RETURN      
END      
      
--BEGIN TRAN      
       
/*IDENTIFYING THE CONSECUTIVE 3 DAYS SHORTFALL PARTY*/      
UPDATE #CMMARGIN_PENALTY SET DAYS_3_COUNT = 1      
 FROM (    
  SELECT PARTY_CODE,NCOUNT=COUNT(MARGIN_DATE) FROM CMMARGIN_PENALTY    
  WHERE     
  MARGIN_DATE IN (    
    SELECT TOP 3 MARGIN_DATE FROM CMMARGIN_PENALTY_POST_STAT     
    WHERE MARGIN_DATE < @MARGINDATE ORDER BY MARGIN_DATE DESC    
   )    
  GROUP BY PARTY_CODE HAVING COUNT(MARGIN_DATE) = 3    
 ) M      
 WHERE M.PARTY_CODE = #CMMARGIN_PENALTY.PARTY_CODE      
      
/*IDENTIFYING THE MONTHLY 5 TIMES SHORTFALL PARTY*/      
UPDATE #CMMARGIN_PENALTY SET MONTH_5_COUNT = NCOUNT + 1, DAYS_3_COUNT = (CASE WHEN NCOUNT + 1 > 5 THEN 0 ELSE   DAYS_3_COUNT END) 
FROM (      
 SELECT PARTY_CODE,NCOUNT = COUNT(1) FROM CMMARGIN_PENALTY (NOLOCK)       
 WHERE MONTH(MARGIN_DATE) = MONTH(CONVERT(DATETIME,@MARGINDATE))  
 AND YEAR(MARGIN_DATE) = YEAR(CONVERT(DATETIME,@MARGINDATE))      
 AND MARGIN_DATE < @MARGINDATE    
 GROUP BY PARTY_CODE      
 ) M      
WHERE M.PARTY_CODE = #CMMARGIN_PENALTY.PARTY_CODE      

/*APPLYING THE PENALTY PERCENTAGE*/      
UPDATE #CMMARGIN_PENALTY       
 SET PENALTY_PERC = DAYS_3_PERC      
FROM       
 CMMARGIN_PENALTY_MST (NOLOCK)      
WHERE      
 @MARGINDATE BETWEEN FROM_DATE AND TO_DATE      
 AND FINAL_SHORTFALL BETWEEN SHORTAGE_VALUE_FROM AND SHORTAGE_VALUE_TO      
 AND DAYS_3_COUNT <> 0      
       
UPDATE #CMMARGIN_PENALTY       
 SET PENALTY_PERC = MONTH_5_PERC      
FROM       
 CMMARGIN_PENALTY_MST (NOLOCK)      
WHERE      
 @MARGINDATE BETWEEN FROM_DATE AND TO_DATE      
 AND FINAL_SHORTFALL BETWEEN SHORTAGE_VALUE_FROM AND SHORTAGE_VALUE_TO      
 AND DAYS_3_COUNT = 0      
 AND MONTH_5_COUNT > 5       
        /*
select * from #CMMARGIN_PENALTY where PARTY_CODE='A294693'
UPDATE #CMMARGIN_PENALTY SET PEAK_SHORTFALL = (CASE WHEN ABS(SHORTFALL) > (CASE WHEN PEAK_SHORTFALL > 0 THEN -.01 ELSE ABS(PEAK_SHORTFALL) END) THEN SHORTFALL ELSE PEAK_SHORTFALL END)
WHERE SHORTFALL < 0 AND PEAK_MARGIN_REQ > 0 
UPDATE #CMMARGIN_PENALTY SET SHORTFALL = (CASE WHEN ABS(PEAK_SHORTFALL) > (CASE WHEN SHORTFALL > 0 THEN -.01 ELSE ABS(SHORTFALL) END) THEN PEAK_SHORTFALL ELSE SHORTFALL END)
WHERE PEAK_SHORTFALL < 0 AND EOD_MARGIN_REQ > 0 

select * from #CMMARGIN_PENALTY where PARTY_CODE='A294693'
*/

--select ((EOD_MARGIN_REQ)*10/100),((PEAK_MARGIN_REQ)*10/100) ,* from #CMMARGIN_PENALTY where PARTY_CODE='A509176'

/*
UPDATE #CMMARGIN_PENALTY     
 SET PENALTY_PERC = 1  
WHERE ABS(FINAL_SHORTFALL) = EOD_MARGIN_REQ
   AND PENALTY_PERC < 1    AND SHORTFALL < 0
   */

/*
UPDATE #CMMARGIN_PENALTY SET 
EOD_MARGIN_REQ = PEAK_MARGIN_REQ
WHERE EOD_MARGIN_REQ < PEAK_MARGIN_REQ

UPDATE #CMMARGIN_PENALTY SET 
PEAK_MARGIN_REQ = EOD_MARGIN_REQ
WHERE EOD_MARGIN_REQ > PEAK_MARGIN_REQ
 */

  
UPDATE #CMMARGIN_PENALTY     
 SET PENALTY_PERC = 1    
WHERE (ABS(SHORTFALL) > ((EOD_MARGIN_REQ)*10/100) OR ABS(SHORTFALL) > 100000)
 AND PENALTY_PERC < 1    AND SHORTFALL < 0  
AND ABS(PEAK_SHORTFALL) <= 	ABS(SHORTFALL)

UPDATE #CMMARGIN_PENALTY     
 SET PENALTY_PERC = 1    
WHERE (ABS(PEAK_SHORTFALL) > ((PEAK_MARGIN_REQ)*10/100) OR ABS(SHORTFALL) > 100000)   
 AND PENALTY_PERC < 1    AND PEAK_SHORTFALL < 0	AND ABS(PEAK_SHORTFALL) > 	ABS(SHORTFALL) 
   
UPDATE #CMMARGIN_PENALTY     
 SET PENALTY_PERC = SHORTAGE_perc    
FROM     
 CMMARGIN_PENALTY_MST (NOLOCK)    
WHERE    
 @MARGINDATE BETWEEN FROM_DATE AND TO_DATE    
 AND FINAL_SHORTFALL BETWEEN SHORTAGE_VALUE_FROM AND SHORTAGE_VALUE_TO    
 AND PENALTY_PERC = 0    
        
/*COMPUTING THE PENALTY*/      
UPDATE #CMMARGIN_PENALTY        
SET PENALTY_AMT = CONVERT(NUMERIC(18,2),FINAL_SHORTFALL * PENALTY_PERC / 100)
     
UPDATE #CMMARGIN_PENALTY                                      
--SET PENALTY_AMT = CEILING(CAST(PENALTY_AMT AS DECIMAL(10,3)) * 100.00) / 100.00              
SET PENALTY_AMT = ROUND (CAST(PENALTY_AMT AS DECIMAL(10,3)) * 100.00 / 100.00,4)
  
 UPDATE #CMMARGIN_PENALTY            
SET SERVICE_TAX = ROUND(PENALTY_AMT * F.SERVICE_TAX / 100,2)  
FROM   
 CLIENT2 C2 (NOLOCK),  
 GLOBALS F (NOLOCK)  
WHERE  
 C2.PARTY_CODE = #CMMARGIN_PENALTY.PARTY_CODE  
 AND SERVICE_CHRG = 0  
 AND LEFT(GETDATE(),11) BETWEEN YEAR_START_DT AND YEAR_END_DT  
   
      
/*POLULATING THE FINAL VALUE TO THE TABLE*/      
DELETE CMMARGIN_PENALTY WHERE MARGIN_DATE = @MARGINDATE      
      
INSERT INTO CMMARGIN_PENALTY    
SELECT       
 @MARGINDATE,PARTY_CODE,FINAL_SHORTFALL,      
 PENALTY_PERC,PENALTY_AMT,DAYS_3_COUNT,      
 MONTH_5_COUNT,0,      
 UPDATED_BY = @USERID,      
 UPDATED_ON = @SYSTEMDATE,                           
 SERVICE_TAX                    
FROM #CMMARGIN_PENALTY      
      
IF (SELECT COUNT(1) FROM CMMARGIN_PENALTY_POST_STAT (NOLOCK)      
WHERE MARGIN_DATE = @MARGINDATE) = 0      
BEGIN      
 INSERT INTO CMMARGIN_PENALTY_POST_STAT VALUES( @MARGINDATE,0,'','')      
END      
      
DROP TABLE #CMMARGIN_PENALTY      
      
--COMMIT TRAN      
      
/*--------------------------- END OF PROC ------------------------*/

GO
