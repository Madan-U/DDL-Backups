-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

  
CREATE PROCEDURE [dbo].[PAN_NO_DETAILS]  
(  
@PAN_NO VARCHAR(15)  
)  
AS   
BEGIN  
  
SELECT cl_code AS  PARTY_CODE,pan_gir_no AS PAN_NO,'BO'AS BO  FROM CLIENT_DETAILS WHERE pan_gir_no =@PAN_NO  
UNION  ALL  
SELECT PARTY_CODE,PAN_NO ,'MFSS'AS BO    FROM [AngelFO].BSEMFSS.DBO.MFSS_CLIENT WHERE PAN_NO =@PAN_NO  
  
UNION  ALL  
SELECT nise_party_code AS  PARTY_CODE,FIRST_HOLD_PAN AS PAN_NO ,'FIRST_PAN_DP'AS BO   FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER WHERE FIRST_HOLD_PAN =@PAN_NO  
UNION  ALL  
SELECT nise_party_code AS  PARTY_CODE,SECOND_HOLD_ITPAN AS PAN_NO ,'SECOND_PAN_DP'AS BO  FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER WHERE SECOND_HOLD_ITPAN =@PAN_NO  
UNION  ALL  
SELECT nise_party_code AS  PARTY_CODE,THIRD_HOLD_ITPAN  AS PAN_NO ,'THIRD_PAN_DP'AS BO  FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_CLIENT_MASTER WHERE THIRD_HOLD_ITPAN =@PAN_NO  
  
END

GO
