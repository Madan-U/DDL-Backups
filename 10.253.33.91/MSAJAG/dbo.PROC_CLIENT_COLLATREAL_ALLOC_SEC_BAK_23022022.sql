CREATE PROC	[dbo].[PROC_CLIENT_COLLATREAL_ALLOC_SEC_BAK_23022022]
(
	@RUNDATE VARCHAR(11)
)	
AS
DECLARE
		@PARTY_CODE	VARCHAR(10),
		@EXCHANGE	VARCHAR(3),
		@SEGMENT	VARCHAR(7),
		@NEWEXCHANGE	VARCHAR(3),
		@NEWSEGMENT	VARCHAR(7),
		@SECBAL		NUMERIC(18,3),
		@NEWSECBAL		NUMERIC(18,3),
		@DIFFSECBAL		NUMERIC(18,3),
		@SNO	INT,
		@NEWSNO	INT,
		@ADJCUR		CURSOR,
		@ALLOCCURSOR	CURSOR
		 
SELECT DISTINCT PARTY_CODE INTO #SEC_PARTYALLOC
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE AND APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_RET_TM-APPROVED_SEC_NONCASH_WCM < 0 
AND EXCHANGE <> 'MTF'
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE AND APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_RET_TM-APPROVED_SEC_NONCASH_WCM > 0 
AND EXCHANGE <> 'MTF') 

SELECT PARTY_CODE, EXCHANGE, SEGMENT, APPROVED_SEC_NONCASH_REC_TM,APPROVED_SEC_NONCASH_RET_TM,APPROVED_SEC_NONCASH_WCM 
INTO #SEC_ADJDATA FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE AND EXCHANGE <> 'MTF'
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #SEC_PARTYALLOC) 

ALTER TABLE #SEC_ADJDATA
ADD SNO INT IDENTITY(1,1)

CREATE CLUSTERED INDEX IDXDT ON #SEC_ADJDATA
(
	SNO
)

SET @ADJCUR = CURSOR FOR 
SELECT SNO, PARTY_CODE, EXCHANGE, SEGMENT, BALANCE=APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_RET_TM-APPROVED_SEC_NONCASH_WCM
FROM #SEC_ADJDATA
WHERE APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_RET_TM-APPROVED_SEC_NONCASH_WCM < 0
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM #SEC_ADJDATA
WHERE APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_RET_TM-APPROVED_SEC_NONCASH_WCM > 0 )
--AND PARTY_CODE = '10114978'
ORDER BY PARTY_CODE, EXCHANGE, SEGMENT
OPEN @ADJCUR
FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @SECBAL
WHILE @@FETCH_STATUS = 0 
BEGIN
	SET @ALLOCCURSOR = CURSOR FOR 
	SELECT SNO, EXCHANGE, SEGMENT, BALANCE=APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_WCM FROM #SEC_ADJDATA
	WHERE PARTY_CODE = @PARTY_CODE AND APPROVED_SEC_NONCASH_REC_TM-APPROVED_SEC_NONCASH_WCM > 0 
	ORDER BY EXCHANGE, SEGMENT
	OPEN @ALLOCCURSOR
	FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWSECBAL
	WHILE @@FETCH_STATUS = 0 
	BEGIN
		IF @SECBAL < 0 AND @NEWSECBAL > 0 
		BEGIN
			SELECT @DIFFSECBAL = (CASE WHEN ABS(@SECBAL) > ABS(@NEWSECBAL) THEN ABS(@NEWSECBAL) ELSE ABS(@SECBAL) END)
			UPDATE #SEC_ADJDATA SET APPROVED_SEC_NONCASH_REC_TM = APPROVED_SEC_NONCASH_REC_TM - @DIFFSECBAL
			WHERE SNO = @NEWSNO

			UPDATE #SEC_ADJDATA SET APPROVED_SEC_NONCASH_REC_TM = APPROVED_SEC_NONCASH_REC_TM + @DIFFSECBAL
			WHERE SNO = @SNO

			SET @SECBAL = @SECBAL + @DIFFSECBAL
		END
		FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWSECBAL
	END
	CLOSE @ALLOCCURSOR
	DEALLOCATE @ALLOCCURSOR
	FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @SECBAL
END
CLOSE @ADJCUR
DEALLOCATE @ADJCUR

UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY SET APPROVED_SEC_NONCASH_REC_TM = C.APPROVED_SEC_NONCASH_REC_TM
FROM #SEC_ADJDATA C
WHERE PROCESS_DT = @RUNDATE
AND C.PARTY_CODE = CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.PARTY_CODE
AND C.EXCHANGE = CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.EXCHANGE
AND C.SEGMENT = CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.SEGMENT

SELECT DISTINCT PARTY_CODE INTO #SEC_PARTYALLOC_C
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE AND APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_RET_TM-APPROVED_SEC_CASH_WCM < 0 
AND EXCHANGE <> 'MTF'
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE AND APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_RET_TM-APPROVED_SEC_CASH_WCM > 0 
AND EXCHANGE <> 'MTF') 

SELECT PARTY_CODE, EXCHANGE, SEGMENT, APPROVED_SEC_CASH_REC_TM,APPROVED_SEC_CASH_RET_TM,APPROVED_SEC_CASH_WCM 
INTO #SEC_ADJDATA_C FROM CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY
WHERE PROCESS_DT = @RUNDATE AND EXCHANGE <> 'MTF'
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #SEC_PARTYALLOC_C) 

ALTER TABLE #SEC_ADJDATA_C
ADD SNO INT IDENTITY(1,1)

CREATE CLUSTERED INDEX IDXDT ON #SEC_ADJDATA_C
(
	SNO
)

SET @ADJCUR = CURSOR FOR 
SELECT SNO, PARTY_CODE, EXCHANGE, SEGMENT, BALANCE=APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_RET_TM-APPROVED_SEC_CASH_WCM
FROM #SEC_ADJDATA_C
WHERE APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_RET_TM-APPROVED_SEC_CASH_WCM < 0
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM #SEC_ADJDATA_C
WHERE APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_RET_TM-APPROVED_SEC_CASH_WCM > 0 )
--AND PARTY_CODE = '10114978'
ORDER BY PARTY_CODE, EXCHANGE, SEGMENT
OPEN @ADJCUR
FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @SECBAL
WHILE @@FETCH_STATUS = 0 
BEGIN
	SET @ALLOCCURSOR = CURSOR FOR 
	SELECT SNO, EXCHANGE, SEGMENT, BALANCE=APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_WCM FROM #SEC_ADJDATA_C
	WHERE PARTY_CODE = @PARTY_CODE AND APPROVED_SEC_CASH_REC_TM-APPROVED_SEC_CASH_WCM > 0 
	ORDER BY EXCHANGE, SEGMENT
	OPEN @ALLOCCURSOR
	FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWSECBAL
	WHILE @@FETCH_STATUS = 0 
	BEGIN
		IF @SECBAL < 0 AND @NEWSECBAL > 0 
		BEGIN
			SELECT @DIFFSECBAL = (CASE WHEN ABS(@SECBAL) > ABS(@NEWSECBAL) THEN ABS(@NEWSECBAL) ELSE ABS(@SECBAL) END)
			UPDATE #SEC_ADJDATA_C SET APPROVED_SEC_CASH_REC_TM = APPROVED_SEC_CASH_REC_TM - @DIFFSECBAL
			WHERE SNO = @NEWSNO

			UPDATE #SEC_ADJDATA_C SET APPROVED_SEC_CASH_REC_TM = APPROVED_SEC_CASH_REC_TM + @DIFFSECBAL
			WHERE SNO = @SNO

			SET @SECBAL = @SECBAL + @DIFFSECBAL
		END
		FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWSECBAL
	END
	CLOSE @ALLOCCURSOR
	DEALLOCATE @ALLOCCURSOR
	FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @SECBAL
END
CLOSE @ADJCUR
DEALLOCATE @ADJCUR

UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY SET APPROVED_SEC_CASH_REC_TM = C.APPROVED_SEC_CASH_REC_TM
FROM #SEC_ADJDATA_C C
WHERE PROCESS_DT = @RUNDATE
AND C.PARTY_CODE = CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.PARTY_CODE
AND C.EXCHANGE = CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.EXCHANGE
AND C.SEGMENT = CLIENT_COLLATERAL_REPORTING_DETAILS_TODAY.SEGMENT

GO
