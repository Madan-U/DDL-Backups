-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[PROC_SHORT_MARGIN_UPLOAD_NCX]
	@MARGINDATE VARCHAR(11),
	@FLDFILENAME VARCHAR (300),
	@UPLOAD_BY VARCHAR(30) = ''
AS
 

DECLARE
	@STRSQL VARCHAR(1000)
	
	TRUNCATE TABLE TBL_NCX_SHORT_ALLOC_INTRADAY_TMP

	Set @strSql = 'Bulk insert  TBL_NCX_SHORT_ALLOC_INTRADAY_TMP from ''' + @FLDFILENAME  + '''  with ( FIELDTERMINATOR = '','', ROWTERMINATOR = ''\n'', FIRSTROW = 2 )'
	Exec(@strSql)

	DECLARE @INDICATOR VARCHAR(3)
	
	SELECT @INDICATOR = '0' + CONVERT(VARCHAR, CONVERT(INT, RIGHT(REPLACE(@FLDFILENAME, '.CSV', ''), 2)) - 1)

	DECLARE @UPLOAD_ON DATETIME
	SET @UPLOAD_ON = GETDATE()
	 
	DELETE FROM TBL_NCX_SHORT_ALLOC_INTRADAY WHERE MARGINDATE = @MARGINDATE AND INDICATOR = @INDICATOR
	
INSERT INTO TBL_NCX_SHORT_ALLOC_INTRADAY
SELECT *,@FLDFILENAME,@UPLOAD_BY,@UPLOAD_ON
FROM TBL_NCX_SHORT_ALLOC_INTRADAY_TMP WHERE MARGINDATE = @MARGINDATE

GO
