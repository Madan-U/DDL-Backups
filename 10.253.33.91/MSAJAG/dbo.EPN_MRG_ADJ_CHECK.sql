CREATE PROCEDURE [dbo].[EPN_MRG_ADJ_CHECK]

AS

DECLARE @TDAY VARCHAR(11),@MG02 INT,@MG13 INT,@EPN INT
SELECT @TDAY = LEFT(GETDATE(),11)
SELECT @MG02 = COUNT(*) FROM MSAJAG.DBO.TBL_MG02 WITH (NOLOCK) WHERE MARGIN_DATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'
SELECT @MG13 = COUNT(*) FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59'
SELECT @EPN = COUNT (*) FROM MSAJAG.DBO.TBL_PAYIN_DATA WITH (NOLOCK) WHERE TRANSDATE BETWEEN @TDAY  AND @TDAY + ' 23:59:59' AND RECORD_TYPE='EPN'

--PRINT @MG02
--PRINT @MG13
--PRINT @EPN

IF(@MG02 > 0 AND @MG13 > 0 AND @EPN > 0)
BEGIN
SELECT LEFT(GETDATE(),11)
END
ELSE
SELECT 'Margin or EPN Import Pending'

GO
