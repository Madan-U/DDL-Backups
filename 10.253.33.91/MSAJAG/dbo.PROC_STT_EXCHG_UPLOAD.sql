-- Object: PROCEDURE dbo.PROC_STT_EXCHG_UPLOAD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[PROC_STT_EXCHG_UPLOAD](@FILEPATH VARCHAR(150), @FILETYPE VARCHAR(3),  
@FLAG VARCHAR(4) = 'FILE') AS      
DECLARE @STRSQL VARCHAR(800),    
@FILEDATE VARCHAR(11)    
      
TRUNCATE TABLE STT_TMP      
SET @STRSQL = LOWER('BULK INSERT STT_TMP FROM ''' + @FILEPATH  + ''' WITH ( ROWTERMINATOR = ''\N'' )')
EXEC(@STRSQL)        
    
SELECT @FILEDATE = LEFT(CONVERT(DATETIME,RIGHT(LEFT(FILETEXT,14),11)),11)    
FROM STT_TMP    
WHERE LEFT(FILETEXT,2) = 10    
    
DECLARE @CMD VARCHAR(800)       
    
DELETE FROM STT_EXCHG WHERE STT_DATE = @FILEDATE AND ISNULL(FILETYPE,'EQT') = @FILETYPE    
      
INSERT INTO STT_EXCHG    
SELECT PRADNYA.DBO.PIECE(FILETEXT,',',1),    
PRADNYA.DBO.PIECE(FILETEXT,',',2),    
PRADNYA.DBO.PIECE(FILETEXT,',',3),    
PRADNYA.DBO.PIECE(FILETEXT,',',4),    
PRADNYA.DBO.PIECE(FILETEXT,',',5),    
PRADNYA.DBO.PIECE(FILETEXT,',',6),    
PRADNYA.DBO.PIECE(FILETEXT,',',7),    
PRADNYA.DBO.PIECE(FILETEXT,',',8),    
PRADNYA.DBO.PIECE(FILETEXT,',',9),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',10)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',10)) ELSE 0 END),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',11)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',11)) ELSE 0 END),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',12)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',12)) ELSE 0 END),    
PRADNYA.DBO.PIECE(FILETEXT,',',13),    
PRADNYA.DBO.PIECE(FILETEXT,',',14),    
PRADNYA.DBO.PIECE(FILETEXT,',',15),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',16)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',16)) ELSE 0 END),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',17)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',17)) ELSE 0 END),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',18)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',18)) ELSE 0 END),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',19)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',19)) ELSE 0 END),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',20)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',20)) ELSE 0 END),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',21)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',21)) ELSE 0 END),    
(CASE WHEN ISNUMERIC(PRADNYA.DBO.PIECE(FILETEXT,',',22)) = 1 THEN CONVERT(NUMERIC(18,4),PRADNYA.DBO.PIECE(FILETEXT,',',22)) ELSE 0 END)  
,  @FILETYPE    
FROM MSAJAG.DBO.STT_TMP WHERE LEFT(FILETEXT,2) = 30    
    
UPDATE STT_EXCHG SET AVGRATE = 0 WHERE STT_DATE = @FILEDATE AND AVGRATE IS NULL    
    
INSERT INTO STT_EXCHG    
SELECT RECTYPE=20,STT_DATE,PARTY_CODE,SETT_TYPE='',SETT_NO='',    
SCRIP_CD='',SERIES='',TPQTY=0,TSQTY=0,TPAMT=0,TSAMT=0,AVGRATE=0,TPQTYDEL=0,    
TSQTYDEL=0,TSQTYTRD=0,TPAMTDEL=0,TSAMTDEL=0,TSAMTTRD=0,TPSTTDEL=0,TSSTTDEL=0,    
TSSTTTRD=0,TOTALSTT=ROUND(SUM(TOTALSTT),0),@FILETYPE    
FROM STT_EXCHG    
WHERE STT_DATE = @FILEDATE    
AND RECTYPE = 30 AND FILETYPE = @FILETYPE    
GROUP BY STT_DATE,PARTY_CODE    
    
INSERT INTO STT_EXCHG    
SELECT RECTYPE=10,STT_DATE,PARTY_CODE='',SETT_TYPE='',SETT_NO='',    
SCRIP_CD='',SERIES='',TPQTY=0,TSQTY=0,TPAMT=0,TSAMT=0,AVGRATE=0,TPQTYDEL=0,    
TSQTYDEL=0,TSQTYTRD=0,TPAMTDEL=0,TSAMTDEL=0,TSAMTTRD=0,TPSTTDEL=0,TSSTTDEL=0,    
TSSTTTRD=0,TOTALSTT=ROUND(SUM(TOTALSTT),0),@FILETYPE    
FROM STT_EXCHG    
WHERE STT_DATE = @FILEDATE    
AND RECTYPE = 30 AND FILETYPE = @FILETYPE    
GROUP BY STT_DATE    
  
IF @FLAG = 'FILE'   
BEGIN    
 SELECT ISNULL(COUNT(1), 0) FROM STT_EXCHG    
 WHERE STT_DATE = @FILEDATE AND FILETYPE = @FILETYPE    
END

GO
