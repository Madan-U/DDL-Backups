WHILE @@FETCH_STATUS = 0 
BEGIN
	SET @ALLOCCURSOR = CURSOR FOR 
	SELECT SNO, EXCHANGE, SEGMENT, BALANCE = (CASE WHEN SHORT > EDT_LEDGER THEN EDT_LEDGER ELSE SHORT END) 
	FROM #ADJDATALED
	WHERE PARTY_CODE = @PARTY_CODE AND SHORT > 0 AND EDT_LEDGER > 0 
