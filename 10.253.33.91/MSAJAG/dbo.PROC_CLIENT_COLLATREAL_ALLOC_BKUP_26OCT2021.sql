CREATE PROC	[dbo].[PROC_CLIENT_COLLATREAL_ALLOC_BKUP_26OCT2021]
(
	@RUNDATE VARCHAR(11)
)	
AS
DECLARE
		@PARTY_CODE	VARCHAR(10),
		@EXCHANGE	VARCHAR(3),
		@SEGMENT	VARCHAR(7),
		@NEWEXCHANGE	VARCHAR(3),
		@NEWSEGMENT	VARCHAR(7),
		@LEDBAL		NUMERIC(18,4),
		@NEWLEDBAL		NUMERIC(18,4),
		@DIFFLEDBAL		NUMERIC(18,4),
		@SNO	INT,
		@NEWSNO	INT,
		@ADJCUR		CURSOR,
		@ALLOCCURSOR	CURSOR
		 
SELECT DISTINCT PARTY_CODE INTO #PARTYUPSTK_C
FROM CLIENT_COLLATERAL_REPORTING_DETAILS
WHERE PROCESS_DT = @RUNDATE 
AND EDT_LEDGER+POOL_ACCOUNT_CLTM+
APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM-UPFRONT_MARGIN < 0 
AND UPFRONT_MARGIN > 0 
AND EXCHANGE <> 'MTF'
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM CLIENT_COLLATERAL_REPORTING_DETAILS
WHERE PROCESS_DT = @RUNDATE  
AND EDT_LEDGER+POOL_ACCOUNT_CLTM+
APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM-UPFRONT_MARGIN >0 
AND APPROVED_SEC_NONCASH_RET_TM > 0 AND EXCHANGE <> 'MTF') 

SELECT PARTY_CODE, EXCHANGE, SEGMENT, SHORT=EDT_LEDGER+POOL_ACCOUNT_CLTM+
APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM+NONAPPROVED_SEC_REC_TM-UPFRONT_MARGIN,APPROVED_SEC_CASH_REC_TM,
APPROVED_SEC_CASH_RET_TM,NONAPPROVED_SEC_REC_TM,NONAPPROVED_SEC_RET_TM
INTO #ADJDATASTK_C FROM CLIENT_COLLATERAL_REPORTING_DETAILS
WHERE PROCESS_DT = @RUNDATE AND EXCHANGE <> 'MTF'
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #PARTYUPSTK_C)

ALTER TABLE #ADJDATASTK_C
ADD SNO INT IDENTITY(1,1)

CREATE CLUSTERED INDEX IDXDT ON #ADJDATASTK_C
(
	SNO
)

SET @ADJCUR = CURSOR FOR 
SELECT SNO, PARTY_CODE, EXCHANGE, SEGMENT, SHORT
FROM #ADJDATASTK_C
WHERE SHORT < 0  
AND PARTY_CODE IN ( SELECT PARTY_CODE
FROM #ADJDATASTK_C
WHERE SHORT > 0 )
--AND PARTY_CODE = '10114978'
ORDER BY PARTY_CODE, EXCHANGE, SEGMENT
OPEN @ADJCUR
FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @LEDBAL
WHILE @@FETCH_STATUS = 0 
BEGIN
	SET @ALLOCCURSOR = CURSOR FOR 
	SELECT SNO, EXCHANGE, SEGMENT, BALANCE = (CASE WHEN SHORT > APPROVED_SEC_CASH_RET_TM THEN APPROVED_SEC_CASH_RET_TM ELSE SHORT END) 
	FROM #ADJDATASTK_C
	WHERE PARTY_CODE = @PARTY_CODE AND SHORT > 0 AND APPROVED_SEC_CASH_RET_TM > 0 
	ORDER BY EXCHANGE, SEGMENT
	OPEN @ALLOCCURSOR
	FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWLEDBAL
	WHILE @@FETCH_STATUS = 0 
	BEGIN
		IF @LEDBAL < 0 AND @NEWLEDBAL > 0 
		BEGIN
			SELECT @DIFFLEDBAL = (CASE WHEN ABS(@LEDBAL) > ABS(@NEWLEDBAL) THEN ABS(@NEWLEDBAL) ELSE ABS(@LEDBAL) END)
			UPDATE #ADJDATASTK_C SET APPROVED_SEC_CASH_RET_TM = APPROVED_SEC_CASH_RET_TM - @DIFFLEDBAL,
			APPROVED_SEC_CASH_REC_TM = APPROVED_SEC_CASH_REC_TM - @DIFFLEDBAL
			WHERE SNO = @NEWSNO

			UPDATE #ADJDATASTK_C SET APPROVED_SEC_CASH_RET_TM = APPROVED_SEC_CASH_RET_TM + @DIFFLEDBAL,
			APPROVED_SEC_CASH_REC_TM = APPROVED_SEC_CASH_REC_TM + @DIFFLEDBAL
			WHERE SNO = @SNO

			SET @LEDBAL = @LEDBAL + @DIFFLEDBAL
		END
		FETCH NEXT FROM @ALLOCCURSOR INTO @NEWSNO, @NEWEXCHANGE, @NEWSEGMENT, @NEWLEDBAL
	END
	CLOSE @ALLOCCURSOR
	DEALLOCATE @ALLOCCURSOR
	FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @EXCHANGE, @SEGMENT, @LEDBAL
END
CLOSE @ADJCUR
DEALLOCATE @ADJCUR
 
UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS SET APPROVED_SEC_CASH_RET_TM = C.APPROVED_SEC_CASH_RET_TM,
APPROVED_SEC_CASH_REC_TM = C.APPROVED_SEC_CASH_REC_TM
FROM #ADJDATASTK_C C
WHERE PROCESS_DT = @RUNDATE
AND C.PARTY_CODE = CLIENT_COLLATERAL_REPORTING_DETAILS.PARTY_CODE
AND C.EXCHANGE = CLIENT_COLLATERAL_REPORTING_DETAILS.EXCHANGE
AND C.SEGMENT = CLIENT_COLLATERAL_REPORTING_DETAILS.SEGMENT

SELECT DISTINCT PARTY_CODE INTO #PARTYUPSTK
