FROM #ADJDATASTK C
WHERE PROCESS_DT = @RUNDATE
AND C.PARTY_CODE = CLIENT_COLLATERAL_REPORTING_DETAILS.PARTY_CODE
AND C.EXCHANGE = CLIENT_COLLATERAL_REPORTING_DETAILS.EXCHANGE
AND C.SEGMENT = CLIENT_COLLATERAL_REPORTING_DETAILS.SEGMENT



