			APPROVED_SEC_NONCASH_REC_TM = APPROVED_SEC_NONCASH_REC_TM - @DIFFLEDBAL
			WHERE SNO = @NEWSNO

			UPDATE #ADJDATASTK SET APPROVED_SEC_NONCASH_RET_TM = APPROVED_SEC_NONCASH_RET_TM + @DIFFLEDBAL,
			APPROVED_SEC_NONCASH_REC_TM = APPROVED_SEC_NONCASH_REC_TM + @DIFFLEDBAL
