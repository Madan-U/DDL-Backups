APPROVED_SEC_CASH_REC_TM+APPROVED_SEC_NONCASH_REC_TM-UPFRONT_MARGIN,EDT_LEDGER
INTO #ADJDATALED FROM CLIENT_COLLATERAL_REPORTING_DETAILS
