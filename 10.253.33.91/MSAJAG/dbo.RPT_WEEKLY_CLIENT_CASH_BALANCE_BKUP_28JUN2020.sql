				SET @SQL = @SQL + ' SELECT EDT=LEFT(EDT,11), CLTCODE, LEDBAL = SUM(CASE WHEN DRCR = ''C'' THEN VAMT ELSE -VAMT END), EXCHANGE = '''+@@EXCHANGE+''', SEGMENT = '''+@@SEGMENT+''''
				SET @SQL = @SQL + ' FROM  ' + @@ACCOUNTSERVER + '.' + @@ACCOUNTDB + '.DBO.LEDGER ' 
				SET @SQL = @SQL + ' WHERE EDT BETWEEN ''' + @FRDATE_NEXT + ''' AND ''' + @TODATE + ''''
				SET @SQL = @SQL + ' AND CLTCODE BETWEEN ''' + @FROMPARTY + ''' AND ''' + @TOPARTY + ''''
				SET @SQL = @SQL + ' GROUP BY LEFT(EDT,11),CLTCODE '
