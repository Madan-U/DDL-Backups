				SET @SQL = @SQL + ' SELECT EDT=''' + @FROMDATE + ''', CLTCODE, LEDBAL = SUM(CASE WHEN DRCR = ''C'' THEN VAMT ELSE -VAMT END), EXCHANGE = '''+@@EXCHANGE+''', SEGMENT = '''+@@SEGMENT+''''
				SET @SQL = @SQL + ' FROM  ' + @@ACCOUNTSERVER + '.' + @@ACCOUNTDB + '.DBO.LEDGER ' 
				SET @SQL = @SQL + ' WHERE EDT BETWEEN ''' + @FROFYMDATE + ''' AND ''' + @FROMDATE + ''' + '' 23:59'''
				IF @FROMPARTY <> '' AND @FROMPARTY = @TOPARTY AND @FROMPARTY NOT LIKE '98%'
				BEGIN
					SET @SQL = @SQL + ' AND EXISTS (SELECT PCODE FROM #CL_FILTER C WHERE PCODE = CLTCODE)'
