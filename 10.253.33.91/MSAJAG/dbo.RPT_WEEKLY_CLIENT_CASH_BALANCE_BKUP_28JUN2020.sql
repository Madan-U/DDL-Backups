				SET @SQL = @SQL + ' SELECT VDT=''' + @FROMDATE + ''', CLTCODE, LEDBAL = SUM(CASE WHEN DRCR = ''C'' THEN VAMT ELSE -VAMT END), EXCHANGE = '''+@@EXCHANGE+''', SEGMENT = '''+@@SEGMENT+''''
				SET @SQL = @SQL + ' FROM  ' + @@ACCOUNTSERVER + '.' + @@ACCOUNTDB + '.DBO.LEDGER ' 
				SET @SQL = @SQL + ' WHERE VDT BETWEEN ''' + @FROFYMDATE + ''' AND ''' + @FROMDATE + ''' + '' 23:59'''
				IF @FROMPARTY <> '' AND @FROMPARTY = @TOPARTY AND @FROMPARTY NOT LIKE '98%'
				BEGIN
					SET @SQL = @SQL + ' AND EXISTS (SELECT PCODE FROM #CL_FILTER C WHERE PCODE = CLTCODE)'
				END
				SET @SQL = @SQL + ' GROUP BY CLTCODE '
				SET @SQL = @SQL + ' UNION ALL '
				SET @SQL = @SQL + ' SELECT VDT=LEFT(VDT,11), CLTCODE, LEDBAL = SUM(CASE WHEN DRCR = ''C'' THEN VAMT ELSE -VAMT END), EXCHANGE = '''+@@EXCHANGE+''', SEGMENT = '''+@@SEGMENT+''''
				SET @SQL = @SQL + ' FROM  ' + @@ACCOUNTSERVER + '.' + @@ACCOUNTDB + '.DBO.LEDGER ' 
				SET @SQL = @SQL + ' WHERE VDT BETWEEN ''' + @FRDATE_NEXT + ''' AND ''' + @TODATE + ''''
				IF @FROMPARTY <> '' AND @FROMPARTY = @TOPARTY AND @FROMPARTY NOT LIKE '98%'
				BEGIN
					SET @SQL = @SQL + ' AND EXISTS (SELECT PCODE FROM #CL_FILTER C WHERE PCODE = CLTCODE)'
				END
				SET @SQL = @SQL + ' GROUP BY LEFT(VDT,11),CLTCODE '
				SET @SQL = @SQL + ') A '
				SET @SQL = @SQL + 'GROUP BY VDT, CLTCODE,EXCHANGE,SEGMENT '
			
