		CREATE TABLE #LEDBAL_NEW
			(
			VDT			VARCHAR(11),
			EDT 		VARCHAR(11),
			PARTY_CODE	VARCHAR(20),
			LEDBAL		NUMERIC(18,4),
			LEDBAL_EDT	NUMERIC(18,4),
			EXCHANGE	VARCHAR(3), 
			SEGMENT		VARCHAR(10)
			)
 
 
 		CREATE TABLE #COLL
			(
			TRADING_MEMBER_PAN			VARCHAR(50)		DEFAULT (''),
			HOLDING_DATE				VARCHAR(50)		DEFAULT (''),
			UNIQUE_CLIENT_CODE			VARCHAR(50)		DEFAULT (''),
			CLIENT_PAN					VARCHAR(50)		DEFAULT (''),
			CLIENT_NAME					VARCHAR(200)	DEFAULT (''),
			SEGMENT_INDICATOR			VARCHAR(10)		DEFAULT (''),
			FINANCIAL_LEDGER_BALANCEA	NUMERIC(18,3)	DEFAULT (0),
			FINANCIAL_LEDGER_BALANCEB	NUMERIC(18,3)	DEFAULT (0),
			BG							NUMERIC(18,3)	DEFAULT (0),
			FDR							NUMERIC(18,3)	DEFAULT (0),
			GSEC						NUMERIC(18,3)	DEFAULT (0),
			GILT_FUNDS					NUMERIC(18,3)	DEFAULT (0),
			LAST_SETTLEMENT_DATE		VARCHAR(20)		DEFAULT (''),
			ORDFLAG						INT				DEFAULT (0),
			EXCHANGE					VARCHAR(10)		DEFAULT (''),
			SEGMENT						
			VARCHAR(10)		DEFAULT ('')
			)
			

		SET		@FROMDT = 0
		
		SELECT	@TODT = DATEDIFF(DAY, @FROMDATE, @TODATE)
		
		WHILE	@FROMDT <= @TODT
		BEGIN
				SELECT @RPTDATE = LEFT((DATEADD(D,@FROMDT,@FROMDATE)),11)		
				
				--INSERT INTO #LEDBAL_NEW
				
				INSERT	INTO #COLL
				SELECT	TRADING_MEMBER_PAN = @PANNO,
						HOLDING_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,VDT,109),105),
						UNIQUE_CLIENT_CODE = LTRIM(RTRIM(C.PARTY_CODE)),
						CLIENT_PAN = C1.PAN_GIR_NO,
						CLIENT_NAME = C1.LONG_NAME,
						SEGMENT_INDICATOR = (
						CASE
							WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='CAPITAL' THEN 'CM'
							WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='FUTURES' THEN 'FO'
							WHEN C.EXCHANGE IN ('NSX','BSX','MCD') AND C.SEGMENT ='FUTURES' THEN 'CD'
							WHEN C.EXCHANGE IN ('MCX','NCX','BCX','NCE') AND C.SEGMENT ='FUTURES' THEN 'CO'
							WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='SLBS' THEN 'SLB'
							WHEN C.EXCHANGE = 'MTF' AND C.SEGMENT = 'MTF' THEN 'MT'
							ELSE ''
						END),
						FINANCIAL_LEDGER_BALANCEA = SUM(LEDBAL),
						FINANCIAL_LEDGER_BALANCEB = SUM(LEDBAL_EDT),
						BG = 0,
						FDR	= 0,
						GSEC = 0,
						GILT_FUNDS = 0,
						LAST_SETTLEMENT_DATE = 'NA',
						ORDFLAG = 1,
						EXCHANGE = C.EXCHANGE,
						SEGMENT = C.SEGMENT
				FROM
						(
 							SELECT VDT = @RPTDATE, EDT = @RPTDATE, PARTY_CODE, LEDBAL = SUM(LEDBAL),LEDBAL_EDT = 0,
 							EXCHANGE, SEGMENT
 							FROM #LEDBAL_VDT WHERE VDT BETWEEN @FROMDT AND @RPTDATE + ' 23:59:59'
							GROUP BY PARTY_CODE, EXCHANGE, SEGMENT
							UNION ALL
							SELECT VDT = @RPTDATE, EDT = @RPTDATE, PARTY_CODE, LEDBAL = 0,LEDBAL_EDT = SUM(LEDBAL),
 							EXCHANGE, SEGMENT
 							FROM #LEDBAL_EDT WHERE VDT BETWEEN @FROMDT AND @RPTDATE + ' 23:59:59'
							GROUP BY PARTY_CODE, EXCHANGE, SEGMENT
						) C,
						MSAJAG.DBO.CLIENT_DETAILS C1 (NOLOCK)
				WHERE	C.PARTY_CODE = C1.CL_CODE
						AND C1.cl_type <> 'INS' 
						--AND C.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
								
				GROUP BY 
						CONVERT(VARCHAR,CONVERT(DATETIME,VDT,109),105),
						LTRIM(RTRIM(C.PARTY_CODE)),
						C1.PAN_GIR_NO,
						C1.LONG_NAME,
						C.EXCHANGE,
						C.SEGMENT

				--SELECT VDT, EDT, PARTY_CODE, LEDBAL = SUM(LEDBAL),LEDBAL_EDT = SUM(LEDBAL_EDT), EXCHANGE, SEGMENT FROM

				SET @FROMDT = @FROMDT + 1
		END	
		
		


		INSERT	INTO #COLL
		SELECT	TRADING_MEMBER_PAN = @PANNO,
				HOLDING_DATE = CONVERT(VARCHAR,EFFDATE,105),
				UNIQUE_CLIENT_CODE = LTRIM(RTRIM(C.PARTY_CODE)),
				CLIENT_PAN = C1.PAN_GIR_NO,
				CLIENT_NAME = C1.LONG_NAME,
				SEGMENT_INDICATOR = (
				CASE
					WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='CAPITAL' THEN 'CM'
					WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='FUTURES' THEN 'FO'
					WHEN C.EXCHANGE IN ('NSX','BSX','MCD') AND C.SEGMENT ='FUTURES' THEN 'CD'
					WHEN C.EXCHANGE IN ('MCX','NCX','BCX','NCE') AND C.SEGMENT ='FUTURES' THEN 'CO'
					WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='SLBS' THEN 'SLB'
					WHEN C.EXCHANGE = 'MTF' AND C.SEGMENT = 'MTF' THEN 'MT'
					ELSE ''
				END),
				FINANCIAL_LEDGER_BALANCEA = 0,
				FINANCIAL_LEDGER_BALANCEB = 0,
				BG = CONVERT(NUMERIC(18,3),ROUND(SUM(CASE WHEN COLL_TYPE = 'BG' THEN FINALAMOUNT ELSE 0 END),3)),
				FDR	= CONVERT(NUMERIC(18,3),ROUND(SUM(CASE WHEN COLL_TYPE = 'FD' THEN FINALAMOUNT ELSE 0 END),3)),
				GSEC = CONVERT(NUMERIC(18,3),ROUND(SUM(CASE WHEN COLL_TYPE = 'SEC' AND SERIES = 'GS' THEN FINALAMOUNT ELSE 0 END),3)),
				GILT_FUNDS = 0,
				LAST_SETTLEMENT_DATE = 'NA',
				ORDFLAG = 1,
				EXCHANGE = C.EXCHANGE,
				SEGMENT = C.SEGMENT
		FROM	MSAJAG.DBO.COLLATERALDETAILS C (NOLOCK),
				MSAJAG.DBO.CLIENT_DETAILS C1 (NOLOCK)
		WHERE	EFFDATE BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'
				AND case when c.party_code like '98%' then right(c.party_code, len(c.party_code) - 2) else c.party_code end = C1.CL_CODE
				AND C1.cl_type <> 'INS'
				AND C.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		GROUP BY
				LTRIM(RTRIM(C.PARTY_CODE)),
				CONVERT(VARCHAR,EFFDATE,105),
				C1.PAN_GIR_NO,
				C1.LONG_NAME,
				CASE
					WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='CAPITAL' THEN 'CM'
					WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='FUTURES' THEN 'FO'
					WHEN C.EXCHANGE IN ('NSX','BSX','MCD') AND C.SEGMENT ='FUTURES' THEN 'CD'
					WHEN C.EXCHANGE IN ('MCX','NCX','BCX','NCE') AND C.SEGMENT ='FUTURES' THEN 'CO'
					WHEN C.EXCHANGE IN ('NSE','BSE') AND C.SEGMENT ='SLBS' THEN 'SLB'
					WHEN C.EXCHANGE = 'MTF' AND C.SEGMENT = 'MTF' THEN 'MT'
					ELSE ''
				END,
				C.EXCHANGE,
				C.SEGMENT
		HAVING	SUM(FINALAMOUNT) > 0


