-- Object: PROCEDURE dbo.PR_CLASSFILEUPD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE   PROC [dbo].[PR_CLASSFILEUPD]    
(    
  @USERNAME VARCHAR(20),    
  @SESSION_ID VARCHAR(30),    
  @FILE_TYPE VARCHAR(100)    
)    
AS    
  
DECLARE @FILEDATE DATETIME  
DECLARE @FILENAME VARCHAR(100)  
DECLARE @TODAY DATETIME  
DECLARE @FILEDATE1 VARCHAR(11)  
  
SET @TODAY = GETDATE()  
  
SELECT @FILENAME=MSAJAG.DBO.PIECE(FILE_PARAM,',',7)    
FROM CLASSFILEUPD_LOG WHERE SESSION_ID =@SESSION_ID  
   
  
IF @FILE_TYPE ='MRG_RETURN'  
BEGIN  
  
 SELECT @FILEDATE=LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',4))) FROM CLASSFILEUPD WHERE   
 SESSION_ID =@SESSION_ID  
 AND LEFT(FILE_DATA,2) ='01'  
  
 INSERT INTO TBL_CROSS_MARGIN_STATUS  
 SELECT @FILEDATE,REPLACE(SUBSTRING(FILE_DATA,4,LEN(FILE_DATA)),',F',''),@FILENAME,@USERNAME,@TODAY  
 FROM CLASSFILEUPD WHERE   
 SESSION_ID =@SESSION_ID  
 AND RIGHT(LTRIM(RTRIM(FILE_DATA)),2) =',F'  
  
 UPDATE TBL_CROSS_MARGIN_STATUS SET CL_CODE = C.CL_CODE  
 FROM CLIENT_BROK_DETAILS C (NOLOCK) WHERE  
 EXCHANGE ='NSE' AND SEGMENT ='FUTURES'  
 AND C.PARTICIPANT_CODE = TBL_CROSS_MARGIN_STATUS.CL_CODE  
 AND TBL_CROSS_MARGIN_STATUS.CL_CODE <>  C.CL_CODE  
 AND UPLOADED_ON =@TODAY  
  
 DELETE CLASSFILEUPD WHERE SESSION_ID =@SESSION_ID  
 DELETE CLASSFILEUPD_LOG WHERE SESSION_ID =@SESSION_ID  
   
END   
  
IF @FILE_TYPE ='MG15'  
BEGIN  
  
  
 SELECT @FILEDATE1=LEFT(RIGHT(LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_PARAM,',',7))),12),8) FROM CLASSFILEUPD_LOG  WHERE  SESSION_ID =@SESSION_ID   
 SELECT @FILEDATE=CONVERT(DATETIME,LEFT(@FILEDATE1,2) +'/'+SUBSTRING(@FILEDATE1,3,2)+'/'+RIGHT(@FILEDATE1,4),103)  
   
 DELETE TBL_CROSS_MARGIN_MG15 WHERE REPORT_DATE = @FILEDATE  
  
 INSERT INTO TBL_CROSS_MARGIN_MG15  
 SELECT @FILEDATE,LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',1))),LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',2))),  
 LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',3))),LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',4))),@USERNAME,@TODAY  
 FROM CLASSFILEUPD WHERE   
 SESSION_ID =@SESSION_ID  
  
 UPDATE TBL_CROSS_MARGIN_MG15 SET CL_CODE = C.CL_CODE  
 FROM CLIENT_BROK_DETAILS C (NOLOCK) WHERE  
 EXCHANGE ='NSE' AND SEGMENT ='FUTURES'  
 AND C.PARTICIPANT_CODE = TBL_CROSS_MARGIN_MG15.CL_CODE  
 AND TBL_CROSS_MARGIN_MG15.CL_CODE <>  C.CL_CODE  
 AND UPLOADED_ON =@TODAY  
  
 DELETE CLASSFILEUPD WHERE SESSION_ID =@SESSION_ID  
 DELETE CLASSFILEUPD_LOG WHERE SESSION_ID =@SESSION_ID  
   
END   
  
IF @FILE_TYPE ='XM01'  
BEGIN  
  
 SELECT @FILEDATE1=LEFT(RIGHT(LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_PARAM,',',7))),12),8) FROM CLASSFILEUPD_LOG  WHERE  SESSION_ID =@SESSION_ID   

 SELECT @FILEDATE=CONVERT(DATETIME,LEFT(@FILEDATE1,2) +'/'+SUBSTRING(@FILEDATE1,3,2)+'/'+RIGHT(@FILEDATE1,4),103)  
   
 DELETE TBL_CROSS_MARGIN_XM01 WHERE REPORT_DATE = @FILEDATE   
  
 INSERT INTO TBL_CROSS_MARGIN_XM01  
 SELECT @FILEDATE,LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',1))),LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',2))),  
 LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',3))),LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_DATA,',',4))),@USERNAME,@TODAY  
 FROM CLASSFILEUPD WHERE   
 SESSION_ID =@SESSION_ID  
  
 UPDATE TBL_CROSS_MARGIN_XM01 SET CL_CODE = C.CL_CODE  
 FROM CLIENT_BROK_DETAILS C (NOLOCK) WHERE  
 EXCHANGE ='NSE' AND SEGMENT ='FUTURES'  
 AND C.PARTICIPANT_CODE = TBL_CROSS_MARGIN_XM01.CL_CODE  
 AND TBL_CROSS_MARGIN_XM01.CL_CODE <>  C.CL_CODE  
 AND UPLOADED_ON =@TODAY  
  
 DELETE CLASSFILEUPD WHERE SESSION_ID =@SESSION_ID  
 DELETE CLASSFILEUPD_LOG WHERE SESSION_ID =@SESSION_ID  
   
END   
  
  
IF @FILE_TYPE ='CXM01'  
BEGIN  
  
 SELECT @FILEDATE1=LEFT(RIGHT(LTRIM(RTRIM(MSAJAG.DBO.PIECE(FILE_PARAM,',',7))),12),8) FROM CLASSFILEUPD_LOG   WHERE  SESSION_ID =@SESSION_ID   
 SELECT @FILEDATE=CONVERT(DATETIME,LEFT(@FILEDATE1,2) +'/'+SUBSTRING(@FILEDATE1,3,2)+'/'+RIGHT(@FILEDATE1,4),103)  
   
  
 DELETE TBL_CROSS_MARGIN_NSECM_XM01 WHERE REPORT_DATE = @FILEDATE   
   
 INSERT INTO TBL_CROSS_MARGIN_NSECM_XM01   
 SELECT   
 RECORDTYPE ='10',  
 FILEDATE = @FILEDATE,  
 CL_CODE = MSAJAG.DBO.PIECE(FILE_DATA,',',2),  
 SCRIP_CD = MSAJAG.DBO.PIECE(FILE_DATA,',',3),  
 SERIES = MSAJAG.DBO.PIECE(FILE_DATA,',',4),  
 SETT_TYPE = MSAJAG.DBO.PIECE(FILE_DATA,',',5),  
 SETT_NO = MSAJAG.DBO.PIECE(FILE_DATA,',',6),  
 NETOPENQTY = REPLACE(REPLACE(MSAJAG.DBO.PIECE(FILE_DATA,',',7),'.0000',''),'.00',''),  
 OFFSETTQTY = REPLACE(REPLACE(MSAJAG.DBO.PIECE(FILE_DATA,',',8),'.0000',''),'.00',''),  
 ORGMARGIN = MSAJAG.DBO.PIECE(FILE_DATA,',',9),  
 MRG_BENEFIT = MSAJAG.DBO.PIECE(FILE_DATA,',',10),  
 APPL_MARGIN = MSAJAG.DBO.PIECE(FILE_DATA,',',11),  
 UPLOADBY = @USERNAME,  
 UPLOADON = GETDATE()  
   
 FROM CLASSFILEUPD    
 WHERE   
  SESSION_ID =@SESSION_ID  
  AND LEFT(FILE_DATA,2) = '10'  
  
  
 INSERT INTO TBL_CROSS_MARGIN_NSECM_XM01   
 SELECT   
 RECORDTYPE ='20',  
 FILEDATE = @FILEDATE,  
 CL_CODE = MSAJAG.DBO.PIECE(FILE_DATA,',',2),  
 SCRIP_CD = '',  
 SERIES = '',  
 SETT_TYPE = '',  
 SETT_NO = '',  
 NETOPENQTY = 0,  
 OFFSETTQTY = 0,  
 ORGMARGIN = MSAJAG.DBO.PIECE(FILE_DATA,',',3),  
 MRG_BENEFIT = MSAJAG.DBO.PIECE(FILE_DATA,',',4),  
 APPL_MARGIN = MSAJAG.DBO.PIECE(FILE_DATA,',',5),  
 UPLOADBY = @USERNAME,  
 UPLOADON = GETDATE()  
   
 FROM CLASSFILEUPD    
 WHERE   
  SESSION_ID =@SESSION_ID  
  AND LEFT(FILE_DATA,2) = '20'  
  
  
 INSERT INTO TBL_CROSS_MARGIN_NSECM_XM01   
 SELECT   
 RECORDTYPE ='30',  
 FILEDATE = @FILEDATE,  
 CL_CODE = '',  
 SCRIP_CD = '',  
 SERIES = '',  
 SETT_TYPE = '',  
 SETT_NO = '',  
 NETOPENQTY = 0,  
 OFFSETTQTY = 0,  
 ORGMARGIN = MSAJAG.DBO.PIECE(FILE_DATA,',',2),  
 MRG_BENEFIT = MSAJAG.DBO.PIECE(FILE_DATA,',',3),  
 APPL_MARGIN = MSAJAG.DBO.PIECE(FILE_DATA,',',4),  
 UPLOADBY = @USERNAME,  
 UPLOADON = GETDATE()  
   
 FROM CLASSFILEUPD    
 WHERE   
  SESSION_ID =@SESSION_ID  
  AND LEFT(FILE_DATA,2) = '30'  
   
  
  
 UPDATE TBL_CROSS_MARGIN_NSECM_XM01 SET CL_CODE = C.CL_CODE  
 FROM CLIENT_BROK_DETAILS C (NOLOCK) WHERE  
 EXCHANGE ='NSE' AND SEGMENT ='CAPITAL'  
 AND C.PARTICIPANT_CODE = TBL_CROSS_MARGIN_NSECM_XM01.CL_CODE  
 AND TBL_CROSS_MARGIN_NSECM_XM01.CL_CODE <>  C.CL_CODE  
 AND UPLOADED_ON =@TODAY  
 AND RECORDTYPE <>'30'  
  
  
-- DELETE CLASSFILEUPD WHERE SESSION_ID =@SESSION_ID  
-- DELETE CLASSFILEUPD_LOG WHERE SESSION_ID =@SESSION_ID  
  
END

IF  @FILE_TYPE = 'IMP_NAPPR_NSE'
BEGIN
	DELETE SCRIP_UNAPPROVED WHERE EXCHANGE ='NSE' AND SEGMENT ='CAPITAL'
	INSERT INTO SCRIP_UNAPPROVED 
	SELECT  
		'NSE','CAPITAL',
		SCRIP_CD = MSAJAG.DBO.PIECE(FILE_DATA,',',2),  
		SCRIP_NAME = MSAJAG.DBO.PIECE(FILE_DATA,',',3),  
		ISIN = MSAJAG.DBO.PIECE(FILE_DATA,',',4),
		UPLOADBY = @USERNAME,  
		UPLOADON = GETDATE()     
	 FROM CLASSFILEUPD    
	WHERE   
	SESSION_ID =@SESSION_ID
END   

IF  @FILE_TYPE = 'IMP_NAPPR_BSE'
BEGIN
	DELETE SCRIP_UNAPPROVED WHERE EXCHANGE ='BSE' AND SEGMENT ='CAPITAL'
	INSERT INTO SCRIP_UNAPPROVED 
	SELECT  
		'BSE','CAPITAL',
		SCRIP_CD = MSAJAG.DBO.PIECE(FILE_DATA,',',2),  
		SCRIP_NAME = MSAJAG.DBO.PIECE(FILE_DATA,',',3),
		ISIN = MSAJAG.DBO.PIECE(FILE_DATA,',',4),
		UPLOADBY = @USERNAME,  
		UPLOADON = GETDATE()     
	 FROM CLASSFILEUPD    
	WHERE   
	SESSION_ID =@SESSION_ID
END     



SELECT 'RECORD UPDATED SUCCESSFULLY'

GO
