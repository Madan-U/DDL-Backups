CREATE PROCEDURE PAN_NO_RPT
(
@PAN_NO VARCHAR(20)
)
AS 
BEGIN



SELECT A.CL_CODE,LONG_NAME,PAN_GIR_NO,BRANCH_CD,SUB_BROKER,REGION,Exchange,SEGMENT,Active_Date,InActive_From
,(CASE WHEN InActive_From>GETDATE() THEN 'ACTIVE'ELSE 'INACTIVE'END)AS STATUS,Deactive_Remarks,Deactive_value 
FROM CLIENT_DETAILS A,CLIENT_BROK_DETAILS B WHERE PAN_GIR_NO=@PAN_NO
AND A.CL_CODE=B.Cl_Code
END

GO
