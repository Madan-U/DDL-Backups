UPDATE #CLIENTDPDATA SET DP_STATUS = #DPDETAILS.DP_STATUS FROM #DPDETAILS WHERE #DPDETAILS.DEMAT_CODE = #CLIENTDPDATA.DEMATCODE  
  
  
