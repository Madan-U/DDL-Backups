--EXEC GET_WEEKLY_COLL_DETAILS 'DEC 31 2020'
CREATE PROCEDURE [dbo].[GET_WEEKLY_COLL_DETAILS_BKUP_19JAN21]
	@REPORT_DATE VARCHAR(11)
AS

DECLARE
	@COLL_DATE VARCHAR(11)



		SELECT
		PARTY_CODE = LTRIM(RTRIM(C.PARTY_CODE)),
		BG = CONVERT(NUMERIC(18,3),ROUND(SUM(CASE WHEN COLL_TYPE = 'BG' THEN FINALAMOUNT ELSE 0 END),3)),
				FDR	= CONVERT(NUMERIC(18,3),ROUND(SUM(CASE WHEN COLL_TYPE = 'FD' THEN FINALAMOUNT ELSE 0 END),3)),
				GSEC = CONVERT(NUMERIC(18,3),ROUND(SUM(CASE WHEN COLL_TYPE = 'SEC' AND SERIES = 'GS' THEN FINALAMOUNT ELSE 0 END),3)),
				GILT_FUNDS = 0,
				EXCHANGE = C.EXCHANGE,
				SEGMENT = C.SEGMENT
		FROM	MSAJAG.DBO.COLLATERALDETAILS C (NOLOCK),
				#CL_LIST C1 (NOLOCK)--,				#client_list L
		WHERE	EFFDATE BETWEEN @REPORT_DATE AND @REPORT_DATE + ' 23:59:59'
				AND C.PARTY_CODE = C1.PARTY_CODE --AND C.Party_Code = cltcode
				AND C1.cl_type <> 'INS'
		GROUP BY
				LTRIM(RTRIM(C.PARTY_CODE)),
				C.EXCHANGE,
				C.SEGMENT
		HAVING	SUM(FINALAMOUNT) > 0
		UNION ALL
		SELECT
					PARTY_CODE = LTRIM(RTRIM(C.PARTY_CODE)),
					BG = 0,
					FDR	= 0,
					GSEC = CONVERT(NUMERIC(18,3),ROUND(SUM(QTY*CL_RATE - QTY*CL_RATE*HAIRCUT/100),3)),
					GILT_FUNDS = 0,
					EXCHANGE = 'MTF',
					SEGMENT = 'MTF'
			FROM	MTFTRADE.DBO.TBL_PRODUCT_HOLD_DATA C (NOLOCK),
					#CL_LIST C1 (NOLOCK)--,				#client_list L
			WHERE	SAUDA_DATE BETWEEN @REPORT_DATE AND @REPORT_DATE + ' 23:59:59'
					AND C.PARTY_CODE = C1.PARTY_CODE --AND C.PARTY_CODE = cltcode
					AND C.SERIES = 'GS'
					AND C.HOLDFLAG = 'MTFCOLL'
					AND C1.cl_type <> 'INS'
			GROUP BY
					LTRIM(RTRIM(C.PARTY_CODE))
			HAVING	SUM(QTY*CL_RATE - QTY*CL_RATE*HAIRCUT/100) > 0

GO
