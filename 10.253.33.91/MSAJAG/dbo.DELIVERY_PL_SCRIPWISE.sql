CREATE PROC [dbo].[DELIVERY_PL_SCRIPWISE] (@TRDATE DATETIME)
AS 
DECLARE @FROMDATE DATETIME,@PRVDATE DATETIME

SELECT @FROMDATE =MAX(Start_date)  FROM SETT_MST WHERE Start_date <= @TRDATE AND SETT_TYPE ='N'  
SET @PRVDATE = (SELECT TOP 1 Start_date  FROM SETT_MST WHERE Start_date < @FROMDATE AND SETT_TYPE ='N' ORDER BY Start_date DESC)

select ClientCode into #party from [MIS].kyc.dbo.vw_campaign_profitloss where ConsentDate >=@PRVDATE
and ConsentDate <=@FROMDATE +' 23:59'


SELECT * INTO #TEMP FROM (
SELECT PARTY_CODE,SCRIP_CD,ISIN,SUM(PQTYDEL) AS PQTY ,SUM(SQTYDEL) SQTY,SUM(PQTYTRD)AS PTRD,SUM(SQTYTRD)AS STRD,
SUM(CASE WHEN PQTYDEL >0 THEN DELAMT ELSE 0 END) AS P_AMT,
SUM(CASE WHEN SQTYDEL >0 THEN DELAMT ELSE 0 END) AS S_AMT,
SUM(CASE WHEN PQTYTRD >0 THEN TRDAMT ELSE 0 END) AS PTRD_AMT,
SUM(CASE WHEN SQTYTRD >0 THEN TRDAMT ELSE 0 END) AS STRD_AMT    
FROM CMBILLVALAN WHERE SAUDA_DATE >=@PRVDATE
AND SAUDA_DATE <=@FROMDATE + ' 23:59' AND PARTY_CODE in (select * from #party )
AND (PQTYDEL <>0 OR SQTYDEL <>0)
GROUP BY PARTY_CODE,SCRIP_CD,ISIN 
UNION ALL
SELECT PARTY_CODE,SCRIP_CD,ISIN,SUM(PQTYDEL) AS PQTY ,SUM(SQTYDEL) SQTY,SUM(PQTYTRD)AS PTRD,SUM(SQTYTRD)AS STRD,
SUM(CASE WHEN PQTYDEL >0 THEN DELAMT ELSE 0 END) AS P_AMT,
SUM(CASE WHEN SQTYDEL >0 THEN DELAMT ELSE 0 END) AS S_AMT,
SUM(CASE WHEN PQTYTRD >0 THEN TRDAMT ELSE 0 END) AS PTRD_AMT,
SUM(CASE WHEN SQTYTRD >0 THEN TRDAMT ELSE 0 END) AS STRD_AMT  
FROM AngelBSECM.BSEDB_AB.DBO.CMBILLVALAN WHERE SAUDA_DATE >=@PRVDATE
AND SAUDA_DATE <=@FROMDATE + ' 23:59' AND PARTY_CODE in (select * from #party )
AND (PQTYDEL <>0 OR SQTYDEL <>0)
GROUP BY PARTY_CODE,SCRIP_CD,ISIN )A
WHERE PQTY <>0 AND SQTY <>0
 


 SELECT PARTY_CODE,SCRIP_CD,SUM(NEW_PAMT-NEW_SAMT-NEW_PTRDAMT-NEW_STRDAMT) LOSS_AMT  FROM ( 
 SELECT  PARTY_CODE,	SCRIP_CD,	PQTY,	SQTY,	P_AMT,	S_AMT,
 (CASE WHEN PQTY =SQTY THEN PQTY  
       WHEN PQTY>SQTY THEN SQTY ELSE 0 END)  AS NEW_PQTY,
 (CASE WHEN PQTY =SQTY THEN PQTY  
       WHEN PQTY<SQTY THEN PQTY ELSE 0 END)  AS NEW_SQTY,
(CASE WHEN PQTY =SQTY THEN P_AMT  
       WHEN PQTY>SQTY THEN SQTY*(P_AMT/PQTY) ELSE 0 END)  AS NEW_PAMT,
(CASE WHEN PQTY =SQTY THEN S_AMT  
       WHEN PQTY>SQTY THEN PQTY*(S_AMT/PQTY) ELSE 0 END)  AS NEW_SAMT,
(CASE WHEN PTRD =STRD THEN PTRD  
       WHEN PTRD>STRD THEN STRD ELSE 0 END)  AS NEW_PQTYTRD,
(CASE WHEN PTRD =STRD THEN PTRD  
       WHEN PTRD<STRD THEN PTRD ELSE 0 END)  AS NEW_SQTYTRD,
(CASE WHEN PTRD =STRD THEN PTRD_AMT  
       WHEN PTRD>STRD THEN PTRD*(PTRD_AMT/PTRD) ELSE 0 END)  AS NEW_PTRDAMT,
(CASE WHEN PTRD =STRD THEN STRD_AMT  
       WHEN PTRD>STRD THEN PQTY*(STRD_AMT/PTRD) ELSE 0 END)  AS NEW_STRDAMT      
       
 FROM #TEMP
 ) A
 GROUP BY PARTY_CODE,SCRIP_CD
 
 

 --select * from [MIS].kyc.dbo.vw_campaign_profitloss

GO
