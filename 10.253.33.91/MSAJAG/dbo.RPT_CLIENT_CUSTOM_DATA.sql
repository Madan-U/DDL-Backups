CREATE PROCEDURE [dbo].[RPT_CLIENT_CUSTOM_DATA] (@RUNDATE VARCHAR(11))

AS ---- EXEC RPT_CLIENT_CUSTOM_DATA 'JUN 24 2022'

SELECT CLIENT_CODE=CL_CODE,CLIENT_NAME=LONG_NAME,BRANCH_CD,SUB_BROKER,
MOBILENO=MOBILE_PAGER,EMAIL,PANNO=PAN_GIR_NO,
BTB_BTC=(CASE WHEN B2C='Y'THEN 'B2C'ELSE 'B2B'END),
CLIENT_STATUS=CASE WHEN LAST_INACTIVE_DATE > GETDATE () THEN 'ACTIVE' ELSE 'INACTIVE' END
FROM INTRANET.RISK.DBO.CLIENT_DETAILS WITH (NOLOCK) 
WHERE PARTY_CODE IN (SELECT LTRIM(RTRIM(PARTY_CODE)) FROM MSAJAG.DBO.IMPORT_PARTY_CODE)
UNION 
SELECT CLIENT_CODE=CL_CODE,CLIENT_NAME=LONG_NAME,BRANCH_CD,SUB_BROKER,
MOBILENO=MOBILE_PAGER,EMAIL,PANNO=PAN_GIR_NO,
BTB_BTC=(CASE WHEN B2C='Y'THEN 'B2C'ELSE 'B2B'END),
CLIENT_STATUS=CASE WHEN LAST_INACTIVE_DATE > GETDATE () THEN 'ACTIVE' ELSE 'INACTIVE' END
FROM INTRANET.RISK.DBO.CLIENT_DETAILS WITH (NOLOCK) 
WHERE PAN_GIR_NO IN (SELECT LTRIM(RTRIM(PARTY_CODE)) FROM MSAJAG.DBO.IMPORT_PARTY_CODE)
ORDER BY CLIENT_CODE

GO
