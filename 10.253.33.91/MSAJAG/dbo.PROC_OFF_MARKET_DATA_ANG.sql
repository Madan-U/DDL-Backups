-- Object: PROCEDURE dbo.PROC_OFF_MARKET_DATA_ANG
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[PROC_OFF_MARKET_DATA_ANG] 
AS

DECLARE @FDATE VARCHAR(11) = GETDATE()-5
DECLARE @TDATE VARCHAR(11) =  GETDATE()

SELECT *,SCRIP_NAME=CONVERT(VARCHAR(500),''),DP_ID = LEFT(TD_BENEFICIERY,8),DP_NAME=CONVERT(VARCHAR(500),'') INTO #OFF1 
FROM AGMUBODPL3.DMAT.DBO.SYNERGY_TRXN_DETAILS 
WHERE TD_DESCRIPTION IN ('OF-DR','OF-CR') AND TD_CURDATE BETWEEN @FDATE AND  @TDATE + ' 23:59:59'

UPDATE #OFF1 SET SCRIP_NAME = ISIN_NAME FROM #OFF1 A,
(SELECT ISIN_CD,ISIN_NAME 
FROM AGMUBODPL3.DMAT.CITRUS_USR.ISIN_MSTR WITH (NOLOCK)) B
WHERE A.TD_ISIN_CODE = B.ISIN_CD

UPDATE #OFF1 SET DP_NAME = DPM_NAME FROM #OFF1 A,
(SELECT DPM_DPID,DPM_NAME 
FROM AGMUBODPL3.DMAT.CITRUS_USR.DP_MSTR WITH (NOLOCK)) B
WHERE A.DP_ID = B.DPM_DPID

SELECT A.CLIENT_CODE,A.STATUS,A.SUB_TYPE,A.FIRST_HOLD_NAME,A.FIRST_HOLD_PIN,A.FIRST_HOLD_STATE,A.FIRST_HOLD_ADD4,A.TYPE,A.BO_SEX,A.NISE_PARTY_CODE,B.SUB_BROKER,C.B2C
INTO #T1
FROM AGMUBODPL3.DMAT.CITRUS_USR.TBL_client_master A  WITH (NOLOCK), MSAJAG.DBO.Client_Details B  WITH (NOLOCK),INTRANET.RISK.DBO.CLIENT_DETAILS C WITH (NOLOCK)
WHERE NISE_PARTY_CODE = b.CL_CODE and b.CL_CODE =C.CL_CODE
AND CLIENT_CODE IN (SELECT TD_AC_CODE FROM #OFF1)

DELETE FROM OFF_MARKET_DATA WHERE TD_CURDATE BETWEEN @FDATE AND  @TDATE + ' 23:59:59'

INSERT INTO OFF_MARKET_DATA
SELECT *
FROM #OFF1 
LEFT JOIN #T1 
ON #OFF1.TD_AC_CODE = #T1.CLIENT_CODE

GO
