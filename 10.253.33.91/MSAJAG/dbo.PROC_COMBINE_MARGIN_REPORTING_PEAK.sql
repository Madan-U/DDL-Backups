CREATE PROCEDURE [dbo].[PROC_COMBINE_MARGIN_REPORTING_PEAK]   
( 
	@MARGINDATE VARCHAR(11),  
	@COMBINEFLAG INT = 1,  
	@FORREPORTING VARCHAR(1)='Y',  
	@CHECKFLAG INT = 0  ,
	@PEAK CHAR(1)='0'
)      
 WITH RECOMPILE 
AS      

IF CONVERT(DATETIME,@MARGINDATE)>=CONVERT(DATETIME,'JAN 14 2022')
BEGIN
	EXEC PROC_COMBINE_MARGIN_REPORTING_PEAK_NEW @MARGINDATE, @COMBINEFLAG, @FORREPORTING, @CHECKFLAG ,@PEAK
END
ELSE
BEGIN
	EXEC PROC_COMBINE_MARGIN_REPORTING_PEAK_OLD @MARGINDATE, @COMBINEFLAG, @FORREPORTING, @CHECKFLAG
END

GO
