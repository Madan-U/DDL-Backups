CREATE  PROCEDURE SP_HOLDING_STATUS_RESPONSE_UPDATE_NEW
(  
	@HTYPE VARCHAR(1) 
)
AS
 
BEGIN
	DELETE FROM HOLDING_FILE_UPLOAD WHERE FILE_DATA IS NULL 
	
	IF @HTYPE = 'H'
	BEGIN
		UPDATE B SET 
		CODE= A.STATUS,
		HDRSTATUS= A.HDRSTATUS,
		HDRCODE= A.CODE,
		ACDID= A.ACDID
		FROM HOLDING_UPLOAD_STATUS A , TBL_API_NSE_HOLDING B
		WHERE A.UID = B.UID 
	END
	ELSE
	BEGIN
		UPDATE B SET 
		CODE= A.STATUS,
		HDRSTATUS= A.HDRSTATUS,
		HDRCODE= A.CODE,
		ACDID= A.ACDID
		FROM HOLDING_UPLOAD_STATUS A , TBL_API_NSE_HOLDING_NIL B
		WHERE A.UID = B.UID 
	END
 
	SELECT ERRCODE='1',MESSAGETEXT='DONE'
	----DROP TABLE #PROCESSDATA
END

GO
