CREATE PROC [dbo].[CLS_ADD_CATEGORY]
(
	@CATEGORY_NAME VARCHAR(50),
	@DESC VARCHAR(80),
	@ADMINAUTO INT,
	@REPORTS VARCHAR(8000),
	@UNAME VARCHAR(50),
	@REMOTE_ADDR VARCHAR(500)

)

/*
SELECT * FROM TBLCATEGORY
SELECT * FROM TBLCATEGORY_log
EXEC CLS_ADD_CATEGORY 'ALL','', 2
SELECT * FROM TBLUSERCONTROLGLOBALS
*/
AS
DECLARE @@TODAY AS DATETIME,
		@@NEWCATCODE AS INT

SET @@TODAY = GETDATE()



IF ((SELECT COUNT(*) FROM TBLCATEGORY WHERE Fldcategoryname = @CATEGORY_NAME)  > 0 )
 BEGIN
	SELECT ErrorMessage = 'Category Name exists! Choose another Name.'
 END
ELSE
 BEGIN
	BEGIN TRY
		BEGIN TRAN
		

		SELECT  FLDCATEGORYCODE FROM TBLCATEGORY WHERE FLDCATEGORYNAME = @CATEGORY_NAME
		INSERT INTO TBLCATEGORY VALUES (@CATEGORY_NAME,@ADMINAUTO,@DESC)
		INSERT INTO TBLCATEGORY_LOG
		SELECT *,'A',@UNAME, @@TODAY FROM TBLCATEGORY WHERE FLDCATEGORYNAME = @CATEGORY_NAME

		SELECT @@NEWCATCODE = FLDCATEGORYCODE FROM TBLCATEGORY WHERE FLDCATEGORYNAME = @CATEGORY_NAME
		INSERT INTO TBLUSERCONTROLGLOBALS VALUES (@@NEWCATCODE, 30, 6, 'F', 20, 'Y', 0)

		IF LEN(@REPORTS) > 0
		 BEGIN
			INSERT INTO TBLCATMENU_LOG
			SELECT FLDREPORTCODE = CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))),
					FLDADMINAUTO = @ADMINAUTO, 
					'',FLDCATEGORYCODE = ',' + CONVERT(VARCHAR,@@NEWCATCODE) + ',','A',@UNAME,getdate(),@REMOTE_ADDR
			FROM   PRADNYA.DBO.FUN_SPLITSTRING (@REPORTS,',' ) R 
			WHERE  EXISTS (SELECT FLDREPORTCODE 
								FROM TBLCATMENU C 
								WHERE CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))) = FLDREPORTCODE AND SPLITTED_VALUE <> '') 

				UPDATE C
				SET    FLDCATEGORYCODE = FLDCATEGORYCODE + CONVERT(VARCHAR,@@NEWCATCODE) + ','
				FROM   PRADNYA.DBO.FUN_SPLITSTRING (@REPORTS,',' ) R,
					   TBLCATMENU C
				WHERE  C.FLDREPORTCODE = CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE)))
					   AND FLDADMINAUTO = @ADMINAUTO AND SPLITTED_VALUE <> ''
			
				INSERT INTO TBLCATMENU
				SELECT FLDREPORTCODE = CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))),
					   FLDADMINAUTO = @ADMINAUTO,
					   FLDCATEGORYCODE = ','+ CONVERT(VARCHAR,@@NEWCATCODE) +','
				FROM   PRADNYA.DBO.FUN_SPLITSTRING (@REPORTS,',' ) R
				WHERE  NOT EXISTS (SELECT FLDREPORTCODE
								   FROM   TBLCATMENU C
								   WHERE  CONVERT(INT,LTRIM(RTRIM(SPLITTED_VALUE))) = FLDREPORTCODE AND FLDADMINAUTO = @ADMINAUTO) AND SPLITTED_VALUE <> ''
			COMMIT
			SELECT ErrorMessage = 'added'
		 END
	END TRY  
	BEGIN CATCH
		SELECT ERROR_MESSAGE() AS ErrorMessage; 
		ROLLBACK
	END CATCH  
 END

GO
