CREATE PROCEDURE [dbo].[CLS_EPN_BENEFIT_CHECKING] (@FromDate VARCHAR(11),@ToDate VARCHAR(11))  
  
AS ---- EXEC RPT_EPN_BENEFIT_CHECKING 'DEC  1 2021','JAN 31 2022'  
  
SET @FromDate = LEFT(CONVERT(DATETIME,@FromDate,105),11)              
SET @ToDate = LEFT(CONVERT(DATETIME,@ToDate,105),11)   
  
SELECT TRANSDATE=CONVERT(VARCHAR(11),TRANSDATE,105),CASH_BENEFIT=CAST(SUM(CASHBENEFIT) AS NUMERIC(18,2)) ,   
SELL_AMOUNT=CAST(SUM(SELLAMOUNT) AS NUMERIC(18,2)),  
EPN_BENEFIT=CONVERT(VARCHAR,CAST((SUM(CASHBENEFIT) /SUM(SELLAMOUNT*-1))*100 AS INT)) + ' %'  
FROM TBL_EPN_BENEFIT WITH (NOLOCK)  
WHERE TRANSDATE >= @FromDate  AND TRANSDATE <= @ToDate + ' 23:59'  
GROUP BY TRANSDATE  
ORDER BY TRANSDATE

GO
