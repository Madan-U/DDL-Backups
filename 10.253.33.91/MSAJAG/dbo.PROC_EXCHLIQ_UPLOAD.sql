-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[PROC_EXCHLIQ_UPLOAD]    
(    
 @TDATE  VARCHAR(10),     
 @FILEFOR VARCHAR(20),     
 @FILETYPE VARCHAR(50),     
 @PROGID  VARCHAR(50),     
 @UNAME  VARCHAR(50),    
 @FILENAME VARCHAR(500) = ''    
)    
AS    

DECLARE @RECCNT NUMERIC(18,0),      
	@CNT NUMERIC(18,0),      
  @FROMDATE DATETIME      

SET @CNT = 1
      
SELECT @CNT=ISNULL(COUNT(1),1)  
FROM FILEDATA      
WHERE PROGID = @PROGID       
AND ISNUMERIC(.DBO.PIECE(FILE_DATA,',',1)) = 1
AND LEN(FILE_DATA) > 0

IF @CNT <= 0 
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'INVALID FILE.'
	RETURN
END
      
 UPDATE EXCHG_CASH_ISIN SET TODATE = CONVERT(DATETIME, @TDATE,103)-1 + ' 23:59:59'
 WHERE TODATE > CONVERT(DATETIME,@TDATE,103)

 DELETE FROM EXCHG_CASH_ISIN
 WHERE FROMDATE > TODATE

 INSERT INTO EXCHG_CASH_ISIN
 SELECT LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',1))),
		LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',2))),       
		LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',3))),
		LTRIM(RTRIM(.DBO.PIECE(FILE_DATA,',',4))),
		LTRIM(RTRIM(REPLACE(.DBO.PIECE(FILE_DATA,',',5),'*',0))),
		LTRIM(RTRIM(REPLACE(.DBO.PIECE(FILE_DATA,',',6),'%',''))),
		CONVERT(DATETIME, @TDATE,103),
		'DEC 31 2049 23:59:59',
		UPLOAD_BY=@UNAME,
		UPLOAD_ON=GETDATE() 
 FROM FILEDATA      
 WHERE PROGID = @PROGID       
 AND ISNUMERIC(.DBO.PIECE(FILE_DATA,',',1)) = 1
 AND LEN(FILE_DATA) > 0

 SELECT @CNT=ISNULL(COUNT(1),1)  
 FROM EXCHG_CASH_ISIN      
 WHERE FROMDATE = CONVERT(DATETIME, @TDATE,103)
      
 SELECT STRMSGCODE = 1, STRMSG = CONVERT(VARCHAR,@CNT) + ' RECORD(S) IMPORTED SUCCESSFULLY.'

GO
