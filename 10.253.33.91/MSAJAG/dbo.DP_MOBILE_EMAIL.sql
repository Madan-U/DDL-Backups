CREATE PROC DP_MOBILE_EMAIL

(

@MOBILENO VARCHAR(10),

@EMAIL VARCHAR(25) =''

)

AS 



BEGIN 

SELECT FIRST_HOLD_MOBILE,CLIENT_CODE,NISE_PARTY_CODE,EMAIL_ADD FROM [172.31.16.94].DMAT.CITRUS_USR.TBL_CLIENT_MASTER WHERE 

 

	 ( FIRST_HOLD_MOBILE LIKE '%'+@MOBILENO+'%'AND  EMAIL_ADD LIKE '%'+@EMAIL+'%')



END

GO
