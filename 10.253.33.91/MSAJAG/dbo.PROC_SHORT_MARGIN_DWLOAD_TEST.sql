CREATE PROC [dbo].[PROC_SHORT_MARGIN_DWLOAD_TEST]
(
	@EFFDATE	VARCHAR(11) 
)
AS

SET NOCOUNT ON 

-- EXEC PROC_SHORT_MARGIN_DWLOAD_TEST 'AUG  5 2022'
 
DECLARE @CMFILENAME VARCHAR(50)
DECLARE @FOFILENAME VARCHAR(50)
DECLARE @CDFILENAME VARCHAR(50)
DECLARE @BATCHNO INT
DECLARE @MEMBERCODE VARCHAR(10)
DECLARE @FILEDATE VARCHAR(10)

SET @FILEDATE = REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@EFFDATE),103),'/','')

SELECT @MEMBERCODE = MEMBERCODE FROM OWNER
 
SET @CMFILENAME = 'C_SA_' + @MEMBERCODE + '_' + @FILEDATE + '.CSV'
SET @FOFILENAME = 'F_SA_' + @MEMBERCODE + '_' + @FILEDATE + '.CSV'
SET @CDFILENAME = 'X_SA_' + @MEMBERCODE + '_' + @FILEDATE + '.CSV'

SELECT *, COLL_AVL=CONVERT(NUMERIC(18,2),0),REASON_CODE=CONVERT(VARCHAR(2),''),CC_CODE=CONVERT(VARCHAR(2),'')
INTO #TBL_SHORT_ALLOC FROM TBL_SHORT_ALLOC
WHERE MARGINDATE = @EFFDATE

SELECT T.EXCHANGE, T.SEGMENT, 
	   T.PARTY_CODE, COLL_AVL = OTHER_SEG_COLL, 
	   REASON_CODE = C.REASONCODE,
	   CC_CODE = C.CC_CODE INTO #SHORT_ADJ
FROM TBL_SHORT_ALLOC_ADJUST T, VW_SHORT_ALLOC_CODE C
WHERE T.MARGINDATE = @EFFDATE 
AND C.EXCHG_SEG = T.OTHER_EXCHG_SEG
AND OTHER_SEG_COLL > 0 

INSERT INTO #SHORT_ADJ
SELECT EXCHANGE, SEGMENT, CLIENT_CODE, COLL_AVL=EOD_SHORT_ALLOC, REASON_CODE='04', CC_CODE =''
FROM #TBL_SHORT_ALLOC, Client_Details C
WHERE C.party_code = #TBL_SHORT_ALLOC.Client_CODE
AND cl_type = 'NRI'
AND EOD_SHORT_ALLOC > 0 

/*
UPDATE #TBL_SHORT_ALLOC SET COLL_AVL = OTHER_SEG_COLL, 
REASON_CODE = C.REASONCODE,
CC_CODE = C.CC_CODE
FROM TBL_SHORT_ALLOC_ADJUST T, VW_SHORT_ALLOC_CODE C
WHERE T.MARGINDATE = @EFFDATE
AND T.PARTY_CODE = #TBL_SHORT_ALLOC.CLIENT_CODE 
AND T.EXCHANGE = #TBL_SHORT_ALLOC.EXCHANGE 
AND T.SEGMENT = #TBL_SHORT_ALLOC.SEGMENT 
AND C.EXCHG_SEG = T.OTHER_EXCHG_SEG
AND OTHER_SEG_COLL > 0 

UPDATE #TBL_SHORT_ALLOC SET COLL_AVL = OTHER_SEG_COLL
FROM (SELECT PARTY_CODE,EXCHANGE,SEGMENT,OTHER_SEG_COLL=SUM(OTHER_SEG_COLL) 
FROM TBL_SHORT_ALLOC_ADJUST T  
WHERE T.MARGINDATE = @EFFDATE AND OTHER_SEG_COLL > 0 AND EXCHANGE IN ('NSE','NSX') 
GROUP BY PARTY_CODE,EXCHANGE,SEGMENT) T
WHERE T.PARTY_CODE = #TBL_SHORT_ALLOC.CLIENT_CODE 
AND T.EXCHANGE = #TBL_SHORT_ALLOC.EXCHANGE 
AND T.SEGMENT = #TBL_SHORT_ALLOC.SEGMENT  
*/
UPDATE #TBL_SHORT_ALLOC SET COLL_AVL = EOD_SHORT_ALLOC, REASON_CODE = '03'
FROM Client_Details C
WHERE C.party_code = #TBL_SHORT_ALLOC.Client_CODE
AND cl_type = 'NRI'
AND EOD_SHORT_ALLOC > 0 

SELECT CLIENT_CODE=PARTY_CODE,
DATA=UPPER(@FILEDATE) 
+ ',' +  LTRIM(RTRIM(EXCHANGE)) 
+ ',' +  LTRIM(RTRIM(SEGMENT)) 
+ ',' +  LTRIM(RTRIM(PARTY_CODE)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EXCESS_COLLATERAL)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EOD_SHORT))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ADJUST_AMT))
+ ',' +  LTRIM(RTRIM(ADJUST_EXCHG_SEG)) 
+ ',' + CONVERT(VARCHAR,LEFT(RUNDATE,11)) + ' ' + CONVERT(VARCHAR,RUNDATE,108),
FILENAME_EXCHG = 'EOD_SHORT_WORKING_' + @FILEDATE + '.CSV'
FROM TBL_SHORT_EOD_WORKING
WHERE MARGINDATE = @EFFDATE 
AND ADJUST_AMT <> 0 
UNION ALL
SELECT CLIENT_CODE, 
DATA = UPPER(@FILEDATE) 
+ ',' +  LTRIM(RTRIM(INDICATOR)) 
+ ',' +  LTRIM(RTRIM(CLIENT_CODE)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),MARGIN_REQ)) 
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_CASH_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_NONCASH_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EXCESS_OTHER_SEG))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),TOTAL_VALUE_COLL))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),EOD_SHORT_ALLOC))
+ ',' + CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),A.COLL_AVL))
+ ',' + CONVERT(VARCHAR,A.REASON_CODE)
+ ',' + CONVERT(VARCHAR,A.CC_CODE),
FILENAME_EXCHG = (CASE WHEN T.EXCHANGE = 'NSE' AND T.SEGMENT = 'CAPITAL' THEN @CMFILENAME
				 WHEN T.EXCHANGE = 'NSE' AND T.SEGMENT = 'FUTURES' THEN @FOFILENAME
				 WHEN T.EXCHANGE = 'NSX' AND T.SEGMENT = 'FUTURES' THEN @CDFILENAME
				 ELSE '123'
			END)
FROM #TBL_SHORT_ALLOC T, #SHORT_ADJ A
WHERE MARGINDATE = @EFFDATE 
AND T.EXCHANGE = A.EXCHANGE
AND T.SEGMENT = A.SEGMENT
AND PARTY_CODE = CLIENT_CODE
ORDER BY 3,CLIENT_CODE

GO
