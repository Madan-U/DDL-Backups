	INSERT INTO #CLIENT_LIST
	SELECT DISTINCT CL_CODE FROM CLIENT_BROK_DETAILS C, CLIENTACCLIST CT 
	WHERE CL_CODE = CLTCODE AND EXCHANGE = CASE WHEN @EXCHANGE <> '' THEN @EXCHANGE ELSE EXCHANGE END
	--AND INACTIVE_FROM >= @REPORTDATE + ' 23:59'
	AND ACTIVE_DATE <= @REPORTDATE + ' 23:59' 
