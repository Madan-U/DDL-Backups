IF @EXCHANGE = 'NSE' OR @EXCHANGE = 'BSE' OR @EXCHANGE = ''
BEGIN
	SELECT D.PARTY_CODE, ISIN = D.CERTNO, QTY = SUM(CRQTY-DRQTY), 
	CASH_COLL = CONVERT(NUMERIC(18,3),0), TOT_CASH_COLL = CONVERT(NUMERIC(18,3),0)
	INTO #ISIN
	FROM [ANGELDEMAT].BSEDB.DBO.TBL_DEL_HOLD_SEC D
	WHERE TRANSDATE <= @REPORTDATE + ' 23:59:59'
	AND BCLTDPID IN (SELECT DPCLTNO FROM [ANGELDEMAT].BSEDB.DBO.DELIVERYDP)
	GROUP BY PARTY_CODE, CERTNO
	HAVING SUM(CRQTY-DRQTY) > 0 
	ORDER BY 1, 2 

	INSERT INTO #ISIN
	SELECT D.PARTY_CODE, ISIN = D.CERTNO, QTY = SUM(CRQTY-DRQTY), CASH_COLL = CONVERT(NUMERIC(18,3),0), TOT_CASH_COLL = CONVERT(NUMERIC(18,3),0)
	FROM [ANGELDEMAT].MSAJAG.DBO.TBL_DEL_HOLD_SEC D
	WHERE TRANSDATE <= @REPORTDATE + ' 23:59:59'
	AND BCLTDPID IN (SELECT DPCLTNO FROM [ANGELDEMAT].MSAJAG.DBO.DELIVERYDP)
	GROUP BY PARTY_CODE, CERTNO
	HAVING SUM(CRQTY-DRQTY) > 0 
	ORDER BY 1, 2 

	INSERT INTO #CLIENT_LIST
	SELECT DISTINCT PARTY_CODE FROM #ISIN
	WHERE PARTY_CODE NOT IN (SELECT CLTCODE FROM #CLIENT_LIST)
END

GO
