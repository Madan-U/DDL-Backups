AND S.Sett_Type = (CASE WHEN S1.Sett_Type = 'RD' THEN 'M'
						WHEN S1.SETT_TYPE = 'RC' THEN 'Z'
						WHEN S1.SETT_TYPE = 'TD' THEN '5'
ELSE 'A'
