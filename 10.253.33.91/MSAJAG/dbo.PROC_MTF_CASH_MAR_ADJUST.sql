SELECT M.SETT_NO, M.SETT_TYPE, PARTY_CODE, SCRIP_CD, M.SERIES, QTY = SUM(QTY),
MG02_QTY=0,
MARGIN_REQ = CONVERT(NUMERIC(18,4),0), MTM_LOSS = CONVERT(NUMERIC(18,4),0), 
ADNL_MARGIN = CONVERT(NUMERIC(18,4),0), MTFREQ = CONVERT(NUMERIC(18,4),0)
INTO #MAR FROM #POS M
GROUP BY M.SETT_NO, M.SETT_TYPE, PARTY_CODE, SCRIP_CD, M.SERIES
HAVING SUM(QTY) > 0 

CREATE CLUSTERED INDEX IDX_PARTY ON #MAR 
(
	PARTY_CODE, SCRIP_CD, SERIES
)
SELECT PARTY_CODE, MARGIN_REQ = SUM(MARGIN_REQ) + ABS(SUM(CASE WHEN MTOM < 0 THEN MTOM ELSE 0 END)) INTO #CMMARGIN FROM (
SELECT PARTY_CODE, SETT_NO, SETT_TYPE, MARGIN_REQ = SUM(MINIMUM_MARGIN), MTOM = SUM(MTOM) FROM TBL_MG02 M
WHERE MARGIN_DATE=@SAUDA_DATE
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #REQ WHERE #REQ.PARTY_CODE = M.Party_Code)
GROUP BY PARTY_CODE, SETT_NO, SETT_TYPE ) A
GROUP BY PARTY_CODE

SELECT PARTY_CODE, SCRIP_CD, SERIES, QTY = SUM(QTY), MARGIN_REQ=SUM(MARGIN_REQ)  
INTO #MTF
FROM MTFTRADE..TBL_MTF_DATA T
WHERE SAUDA_DATE = @SAUDA_DATE
AND EXISTS (SELECT PARTY_CODE FROM #MAR WHERE #MAR.PARTY_CODE = T.PARTY_CODE)
GROUP BY  PARTY_CODE, SCRIP_CD, SERIES

UPDATE #MAR SET MTFREQ = (#MTF.MARGIN_REQ/#MTF.QTY)*#MAR.QTY
FROM #MTF 
WHERE #MTF.PARTY_CODE = #MAR.PARTY_CODE 
AND #MTF.SCRIP_CD = #MAR.SCRIP_CD 
AND #MTF.SERIES = #MAR.SERIES 

UPDATE #MAR SET MARGIN_REQ = (M.MINIMUM_MARGIN/NETQTY)*QTY, MTM_LOSS = (MTOM/NETQTY)*QTY,
MG02_QTY = NETQTY
FROM TBL_MG02 M
WHERE MARGIN_DATE=@SAUDA_DATE AND M.SETT_NO = #MAR.SETT_NO
AND M.SETT_TYPE = #MAR.SETT_TYPE AND M.PARTY_CODE = #MAR.PARTY_CODE 
AND M.SCRIP_CD = #MAR.SCRIP_CD AND M.SERIES = #MAR.SERIES 
AND NETQTY > 0 
AND M.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 

UPDATE #REQ SET MARGIN_REQ  = M.MARGIN, MTF_REQ = MTFREQ
FROM (SELECT PARTY_CODE, MARGIN = SUM(MARGIN), MTFREQ=SUM(MTFREQ) FROM (
		(SELECT SETT_NO, SETT_TYPE, PARTY_CODE, MARGIN = SUM(MARGIN_REQ) + (CASE WHEN SUM(MTM_LOSS) > 0 
														   THEN 0 
														   ELSE ABS(SUM(MTM_LOSS)) 
													  END),
					MTFREQ = SUM(MTFREQ) + (CASE WHEN SUM(MTM_LOSS) > 0 
														   THEN 0 
														   ELSE ABS(SUM(MTM_LOSS)) 
													  END)
		FROM #MAR	
		GROUP BY SETT_NO, SETT_TYPE, PARTY_CODE)) M  
		GROUP BY PARTY_CODE) M 
WHERE M.PARTY_CODE = #REQ.PARTY_CODE 

UPDATE #REQ SET MARGIN_REQ = (CASE WHEN #REQ.MARGIN_REQ < #CMMARGIN.MARGIN_REQ 
								   THEN #REQ.MARGIN_REQ
								   ELSE #CMMARGIN.MARGIN_REQ 
							  END)
FROM #CMMARGIN
WHERE #REQ.PARTY_CODE = #CMMARGIN.Party_Code

UPDATE #REQ SET COLLECTED = (CASE WHEN MTF_AVL > 0 
								  THEN MARGIN_REQ
								  ELSE  
										(CASE WHEN (MTF_REQ + MTF_AVL) > 0
											  THEN (MTF_REQ + MTF_AVL)
											  ELSE 0 
										 END)
							  END)

UPDATE #REQ SET COLLECTED = MARGIN_REQ 
WHERE COLLECTED > MARGIN_REQ 
 

IF @FLAG = 0 
BEGIN
	/*
	SELECT * FROM #REQ
	WHERE MARGIN_REQ > 0 
	ORDER BY PARTY_CODE 
	*/
	SELECT *, TOTALMARGIN_REQ=MARGIN_REQ+(CASE WHEN MTM_LOSS < 0 THEN ABS(MTM_LOSS) ELSE 0 END),
	TOTALMTFREQ=MTFREQ+(CASE WHEN MTM_LOSS < 0 THEN ABS(MTM_LOSS) ELSE 0 END),
	ADJUSTEDMARGIN=CONVERT(NUMERIC(18,4),0),MTFSHORT=CONVERT(NUMERIC(18,4),0)
	INTO #ADJMAR FROM #MAR 
	WHERE Party_Code in 
			(
				SELECT Party_Code FROM  #REQ
				WHERE MARGIN_REQ > 0 
			)
	ORDER BY PARTY_CODE 
	
	UPDATE #ADJMAR SET ADJUSTEDMARGIN = COLLECTED, MTFSHORT = MTF_AVL
	FROM #REQ WHERE #ADJMAR.PARTY_CODE = #REQ.PARTY_CODE
	
	SELECT * FROM #ADJMAR
END
ELSE
BEGIN 
	SELECT PARTY_CODE, MTF_AVL, MARGIN_REQ, COLLECTED
	FROM #REQ 
	WHERE MARGIN_REQ > 0 
	ORDER BY PARTY_CODE
END
DROP TABLE #MAR 
DROP TABLE #REQ 
DROP TABLE #LED

GO
