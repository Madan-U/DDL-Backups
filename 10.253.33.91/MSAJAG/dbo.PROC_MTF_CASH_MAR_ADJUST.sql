CREATE PROC [dbo].[PROC_MTF_CASH_MAR_ADJUST]
(
	@SAUDA_DATE	VARCHAR(11),
	@FROMPARTY	VARCHAR(10) = '0',
	@TOPARTY	VARCHAR(10) = 'zzzzzzzzzz',
	@FLAG		INT = 0 
)
AS

-- EXEC PROC_MTF_CASH_MAR_ADJUST 'DEC  6 2022', 'A1005879','A1005879'

SELECT PARTY_CODE, 
MTF_AVL   = SUM(NETAMT-MTOM_LOSS-MARGIN_REQ)+(MTFLEDBAL+MTFCASHCOLLATERAL+MTFNONCASHCOLLATERAL+MTFCASHEQCOLLATERAL),
MTF_REQ  = CONVERT(NUMERIC(18,4),0),
MARGIN_REQ = CONVERT(NUMERIC(18,4),0),
COLLECTED = CONVERT(NUMERIC(18,4),0)
INTO #REQ 
FROM MTFTRADE..TBL_MTF_DATA 
WHERE SAUDA_DATE = @SAUDA_DATE
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
GROUP BY PARTY_CODE,MTFLEDBAL,MTFCASHCOLLATERAL,MTFNONCASHCOLLATERAL,MTFCASHEQCOLLATERAL

CREATE CLUSTERED INDEX IDX_PARTY ON #REQ 
(
	PARTY_CODE
)

SELECT PARTY_CODE=CLTCODE, MTF_AVL= SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END),
MARGIN_REQ = CONVERT(NUMERIC(18,4),0),
COLLECTED = CONVERT(NUMERIC(18,4),0)
INTO #LED FROM MTFTRADE..LEDGER L, MTFTRADE..TBLCLIENTMARGIN T, MTFTRADE.DBO.PARAMETER
WHERE L.CLTCODE = T.PARTY_CODE 
AND L.VDT <= @SAUDA_DATE
AND L.VDT BETWEEN SDTCUR AND LDTCUR 
AND @SAUDA_DATE BETWEEN SDTCUR AND LDTCUR 
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
AND CLTCODE NOT IN (SELECT PARTY_CODE FROM #REQ WHERE CLTCODE = #REQ.PARTY_CODE)
GROUP BY CLTCODE 

INSERT INTO #LED
SELECT PARTY_CODE, MTF_AVL = SUM(CL_RATE*QTY - CL_RATE*QTY*HAIRCUT/100),
MARGIN_REQ = CONVERT(NUMERIC(18,4),0),
COLLECTED = CONVERT(NUMERIC(18,4),0)
FROM MTFTRADE..TBL_PRODUCT_HOLD_DATA 
WHERE SAUDA_DATE = @SAUDA_DATE
AND HOLDFLAG = 'MTFCOLL'
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
AND PARTY_CODE NOT IN (SELECT PARTY_CODE FROM #REQ 
					   WHERE TBL_PRODUCT_HOLD_DATA.PARTY_CODE = #REQ.PARTY_CODE)
GROUP BY PARTY_CODE

INSERT INTO #REQ
SELECT PARTY_CODE, MTF_AVL = SUM(MTF_AVL), MTF_REQ=0, MARGIN_REQ = SUM(MARGIN_REQ), 
	   COLLECTED = SUM(COLLECTED) 
FROM #LED 
GROUP BY PARTY_CODE



SELECT SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, BSECODE,
QTY = SUM(CASE WHEN SELL_BUY = 1 THEN QTY ELSE -QTY END) 
INTO #POS
FROM MTFTRADE..TBL_PRODUCT_POSITION M 
WHERE SAUDA_DATE <= @SAUDA_DATE AND PAYIN_DATE > @SAUDA_DATE 
AND  SETT_NO <> '2000000' AND ADJUST_DATE > @SAUDA_DATE
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
GROUP BY SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, BSECODE
HAVING SUM(CASE WHEN SELL_BUY = 1 THEN QTY ELSE -QTY END) <> 0 

CREATE CLUSTERED INDEX IDX_PARTY ON #POS 
(
	 SETT_NO, SETT_TYPE,PARTY_CODE
)

