CREATE PROC NSESAUDA (@FROMDATE VARCHAR(11),@TODATE VARCHAR(11),@PARTY VARCHAR(10),@FROMSCRIP VARCHAR(10),@TOSCRIP VARCHAR(10))
AS

IF @FROMSCRIP =''  
   BEGIN 
     SET @FROMSCRIP ='0'
   END
IF @TOSCRIP =''
   BEGIN 
     SET @TOSCRIP ='ZZZZZ'
   END 
 

SELECT SAUDA_DATE,SCRIP_CD,SERIES,SCRIP_NAME,ISIN,SUM(PQTYTRD+PQTYDEL)AS PQTY,
ROUND((CASE WHEN SUM(PQTYTRD+PQTYDEL) =0 THEN 0 ELSE SUM(PAMTTRD+PAMTDEL)/SUM(PQTYTRD+PQTYDEL) END),2) PRATE ,
ROUND(SUM(PAMTTRD+PAMTDEL),2)PVALUE,   
SUM(SQTYTRD+SQTYDEL)AS SQTY,
ROUND((CASE WHEN SUM(SQTYTRD+SQTYDEL) =0 THEN 0 ELSE SUM(SAMTTRD+SAMTDEL)/SUM(SQTYTRD+SQTYDEL) END),2) SRATE ,
ROUND(SUM(SAMTTRD+SAMTDEL),2)SVALUE  , SUM(PQTYDEL-SQTYDEL)AS OPEN_QTY,
ROUND((CASE WHEN SUM(PQTYDEL-SQTYDEL) =0 THEN 0 ELSE SUM(-PAMTDEL+SAMTDEL)/SUM(PQTYDEL-SQTYDEL)*-1 END),2) AS OPEN_RATE,
ROUND(SUM (-PAMTDEL+SAMTDEL), 2)  as open_val,
ROUND((case when (sum(PQTYTRD)=sum(SQTYTRD)) AND SUM(PQTYTRD)<>0 then SUM (-PAMTTRD+SAMTTRD)ELSE '0' END),2) AS PNL,
ROUND(SUM(-PAMTTRD-PAMTDEL+SAMTTRD+SAMTDEL) ,2) AS NET_VALUE
FROM CMBILLVALAN WITH(NOLOCK) WHERE SAUDA_DATE >=@FROMDATE AND SAUDA_DATE <=@TODATE +' 23:49' AND PARTY_CODE =@PARTY 
AND SCRIP_CD >=@FROMSCRIP AND SCRIP_CD <=@TOSCRIP
GROUP BY  SAUDA_DATE,SCRIP_CD,SERIES,SCRIP_NAME,ISIN
ORDER BY SAUDA_DATE

GO
