CREATE PROC [dbo].[DIY_POA_STATUS] (@PARTY_CODE VARCHAR(10))  
AS  
  
DECLARE @CNT INT   
  
   
 /*  
 SELECT @CNT = COUNT(1) FROM (  
 SELECT PARTY_CODE FROM ANGELDEMAT.MSAJAG.DBO.MULTICLTID  WHERE   DEF ='0' AND DPID='12033200' AND @PARTY_CODE=PARTY_CODE   
 UNION   
 SELECT PARTY_CODE FROM  ANGELDEMAT.BSEDB.DBO.MULTICLTID  WHERE   DEF ='0' AND DPID='12033200' AND @PARTY_CODE=PARTY_CODE )A */  
  
   
 SELECT TOP  1 * INTO #DP_STATUS FROM (  
 SELECT PARTY_CODE ,(CASE WHEN DEF=0 THEN 'I' ELSE 'A' END) AS POA_STATUS FROM   MSAJAG.DBO.MULTICLTID  WHERE  DPID IN ('12033200','12033201') AND @PARTY_CODE=PARTY_CODE   
 UNION   
 SELECT PARTY_CODE ,(CASE WHEN DEF=0 THEN 'I' ELSE 'A'END) AS POA_STATUS FROM  AngelBSECM.BSEDB_AB.DBO.MULTICLTID  WHERE DPID IN ('12033200','12033201') AND @PARTY_CODE=PARTY_CODE )A  
    
   
  
IF (@PARTY_CODE IN ('N61558','A115390','A126749','M103737','P104922','S203000','H45178','T21077','S250357','S194943','S227494','J41740','R123896','A133377'))  
  BEGIN  
    SELECT @PARTY_CODE AS PARTY_CODE , 'I' POA_STATUS  
   RETURN    
  END  
  
 
  
  
 IF ( (SELECT COUNT(1) FROM #DP_STATUS) = 0 )  
   BEGIN   
     SELECT @PARTY_CODE AS PARTY_CODE , 'I' POA_STATUS  
   RETURN  
    END  
    ELSE   
   BEGIN   
     SELECT   PARTY_CODE , POA_STATUS   FROM #DP_STATUS (NOLOCK)
   RETURN   
   END    
  DROP TABLE #DP_STATUS

GO
