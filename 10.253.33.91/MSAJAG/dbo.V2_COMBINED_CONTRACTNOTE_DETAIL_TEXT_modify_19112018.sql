UPDATE	#CONTRACT
SET		BROKERAGE_TAXEX = ISNULL(C.BROKERAGE,0)
FROM	(SELECT 
			PARTY_CODE,SAUDA_DATE,EXCHANGE,SEGMENT,CONTRACTNO_NEW,
			BROKERAGE = SUM(BROKERAGE)
		FROM #CONTRACT
		GROUP BY 
			PARTY_CODE,SAUDA_DATE,EXCHANGE,SEGMENT,CONTRACTNO_NEW
		) C
WHERE	C.PARTY_CODE = #CONTRACT.PARTY_CODE
		AND C.SAUDA_DATE = #CONTRACT.SAUDA_DATE
		AND C.EXCHANGE = #CONTRACT.EXCHANGE
		AND C.SEGMENT = #CONTRACT.SEGMENT
		AND C.CONTRACTNO_NEW = #CONTRACT.CONTRACTNO_NEW	


