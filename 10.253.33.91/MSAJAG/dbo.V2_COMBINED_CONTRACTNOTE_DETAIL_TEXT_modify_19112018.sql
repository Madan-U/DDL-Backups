	DECLARE @EXCHANGE_NEW VARCHAR(10)
	DECLARE @SEGMENT_NEW VARCHAR(10)
	DECLARE @SHAREDB VARCHAR(35)
	DECLARE @SHARESERVER VARCHAR(35)
	DECLARE @EXCHANGEWISE_CURSOR CURSOR 
	DECLARE @STRSQL VARCHAR(8000) 
			
	ALTER	TABLE #CONTRACT
	ADD		FOREIGN_PRICE  NUMERIC(18,4)

	UPDATE	#CONTRACT 
	SET		FOREIGN_PRICE = 0

	SET @EXCHANGEWISE_CURSOR = CURSOR
	FOR
	SELECT	DISTINCT 
			EXCHANGE,
			SHAREDB,
			SHARESERVER
	FROM	#EXCHANGELIST M(NOLOCK)
	WHERE	EXCHANGE IN ('NSXFUTURES','BSXFUTURES','MCDFUTURES')
			AND EXCHANGE = (CASE WHEN @EXCHANGE <> '' THEN @EXCHANGE ELSE EXCHANGE END)
		
	OPEN @EXCHANGEWISE_CURSOR

	FETCH NEXT
	FROM @EXCHANGEWISE_CURSOR
	INTO
		@EXCHANGE_NEW,
		@SHAREDB,
		@SHARESERVER

	WHILE @@FETCH_STATUS = 0
	BEGIN
		SET @STRSQL = ''
		IF @SHARESERVER = @@SERVERNAME 
		BEGIN
			SET @SHARESERVER = ''
		END
		ELSE
		BEGIN
			SET @SHARESERVER = @SHARESERVER + '.'
		END
		
		BEGIN

			IF (@EXCHANGE_NEW = 'NSXFUTURES')
			BEGIN
				SET @STRSQL = 'IF (SELECT COUNT(1) FROM ' + @SHARESERVER + @SHAREDB + '.SYS.TABLES WHERE NAME=''TBL_RBI_RATE'') > 0 '
				SET @STRSQL = @STRSQL + 'BEGIN '
				SET @STRSQL = @STRSQL + 'UPDATE	#CONTRACT '
				SET @STRSQL = @STRSQL + 'SET	FOREIGN_PRICE = (CASE WHEN LEFT(BFCF_FLAG, 2) = ''BT'' THEN #CONTRACT.MARKETRATE/ISNULL(T.RBI_REFERENCE_RATE,1) ELSE ISNULL(T.CROSS_CURRENCY_PRICE,0) END) '
				SET @STRSQL = @STRSQL + 'FROM	' + @SHARESERVER + @SHAREDB + '.DBO.TBL_RBI_RATE T '
				SET @STRSQL = @STRSQL + 'WHERE	LEFT(T.TRADE_DATE,11) = LEFT(CONVERT(DATETIME,#CONTRACT.SAUDA_DATE,103),11) '
				SET @STRSQL = @STRSQL + '		AND REPLACE(REPLACE(#CONTRACT.SCRIPNAME,''(BF)'',''''),''(BT)'','''') = (CASE '
				SET @STRSQL = @STRSQL + '			WHEN LEFT(T.INST_TYPE,3) = ''FUT'' THEN RTRIM(T.INST_TYPE) + '' '' + RTRIM(T.SYMBOL) + '' '' + UPPER(REPLACE(CONVERT(VARCHAR,T.EXPIRYDATE,109),'' '','''')) '
				SET @STRSQL = @STRSQL + '			ELSE RTRIM(T.INST_TYPE) + '' '' + RTRIM(T.SYMBOL) + '' '' + UPPER(REPLACE(CONVERT(VARCHAR,T.EXPIRYDATE,109),'' '','''')) + '' '' + CONVERT(VARCHAR,T.STRIKE_PRICE) + '' '' + RTRIM(T.OPTION_TYPE) '
				SET @STRSQL = @STRSQL + '			END) '				
				SET @STRSQL = @STRSQL + '		AND #CONTRACT.EXCHANGE IN (''NSX'') '
				SET @STRSQL = @STRSQL + '		AND #CONTRACT.EXCHANGE+#CONTRACT.SEGMENT = ''' + @EXCHANGE_NEW +''' '
				SET @STRSQL = @STRSQL + ' END '
				PRINT @STRSQL
				EXEC (@STRSQL)
			END
			ELSE IF (@EXCHANGE_NEW = 'BSXFUTURES')
			BEGIN
				SET @STRSQL = @STRSQL + 'UPDATE	#CONTRACT '
				SET @STRSQL = @STRSQL + 'SET	FOREIGN_PRICE = (CASE WHEN LEFT(BFCF_FLAG, 2) = ''BT'' THEN ISNULL(T.CL_RATE,0) ELSE ISNULL(T.CL_RATE,0) END) '
				SET @STRSQL = @STRSQL + 'FROM	' + @SHARESERVER + @SHAREDB + '.DBO.FOCLOSING T '
				SET @STRSQL = @STRSQL + 'WHERE	LEFT(T.TRADE_DATE,11) = LEFT(CONVERT(DATETIME,#CONTRACT.SAUDA_DATE,103),11) '
				SET @STRSQL = @STRSQL + '		AND REPLACE(REPLACE(#CONTRACT.SCRIPNAME,''(BF)'',''''),''(BT)'','''') = (CASE '
				SET @STRSQL = @STRSQL + '			WHEN LEFT(T.INST_TYPE,3) = ''FUT'' THEN RTRIM(T.INST_TYPE) + '' '' + RTRIM(T.SYMBOL) + '' '' + UPPER(REPLACE(CONVERT(VARCHAR,T.EXPIRYDATE,109),'' '','''')) '
				SET @STRSQL = @STRSQL + '			ELSE RTRIM(T.INST_TYPE) + '' '' + RTRIM(T.SYMBOL) + '' '' + UPPER(REPLACE(CONVERT(VARCHAR,T.EXPIRYDATE,109),'' '','''')) + '' '' + CONVERT(VARCHAR,T.STRIKE_PRICE) + '' '' + RTRIM(T.OPTION_TYPE) '
				SET @STRSQL = @STRSQL + '			END) '				
				SET @STRSQL = @STRSQL + '		AND #CONTRACT.EXCHANGE IN (''BSX'') '
				SET @STRSQL = @STRSQL + '		AND #CONTRACT.EXCHANGE+#CONTRACT.SEGMENT = ''' + @EXCHANGE_NEW +''' '
				PRINT @STRSQL
				EXEC (@STRSQL)
			END

		END
		
		FETCH NEXT
		FROM @EXCHANGEWISE_CURSOR
		INTO 
			@EXCHANGE_NEW,
			@SHAREDB,
			@SHARESERVER
	END

	CLOSE @EXCHANGEWISE_CURSOR

	DEALLOCATE @EXCHANGEWISE_CURSOR

--### END CROSS CURRENCY CHANGES ###---
             
             
            
