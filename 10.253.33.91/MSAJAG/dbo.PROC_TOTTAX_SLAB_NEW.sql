CREATE PROC [dbo].[PROC_TOTTAX_SLAB_NEW]
(
	@SETT_TYPE VARCHAR(2),
	@SAUDA_DATE VARCHAR(11),
	@FROMPARTY  VARCHAR(10)='0',      
	@TOPARTY	VARCHAR(10)='ZZZZZZZZZZ'
)
AS

DECLARE @TOTCUR CURSOR
DECLARE @TOTAL_TOT NUMERIC(18,4)
DECLARE @FROM_TOT NUMERIC(18,4)
DECLARE @TO_TOT NUMERIC(18,6)
DECLARE @CHARGE_PER NUMERIC(18,6)
DECLARE @PARTY_CODE VARCHAR(10)
DECLARE @DIFF NUMERIC(18,4)
DECLARE @TURN_TAX NUMERIC(18,4)
DECLARE @SRNO NUMERIC(18,0)
DECLARE @SETTCUR CURSOR
DECLARE @START_DATE DATETIME

/*
UPDATE SETTLEMENT SET TURN_TAX = 0
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND TURN_TAX <> 0  
*/
SELECT @START_DATE = CONVERT(DATETIME, LEFT(@SAUDA_DATE,3) + '  1 ' + RIGHT(@SAUDA_DATE,4))

SELECT DISTINCT PARTY_CODE,TOTFLAG=1 INTO #TOTPARTY 
FROM SETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 

INSERT INTO #TOTPARTY 
SELECT DISTINCT PARTY_CODE,TOTFLAG=1 
FROM ISETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 

SELECT DISTINCT SCRIP_CD, SERIES into #SETTSCRIP FROM SETTLEMENT 
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SERIES <> 'EQ' 

INSERT INTO #SETTSCRIP
SELECT SCRIP_CD, SERIES='EQ' FROM TURNTAX_EXCEPTION
WHERE @SAUDA_DATE BETWEEN DATEFROM AND DATETO
AND TAX_PERC = -1

INSERT INTO #SETTSCRIP 
SELECT DISTINCT SCRIP_CD, SERIES FROM ISETTLEMENT 
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SERIES <> 'EQ' 

CREATE CLUSTERED INDEX IDXSCR ON #SETTSCRIP
(
	SCRIP_CD,
	SERIES
)

UPDATE #TOTPARTY SET TOTFLAG = 0 
FROM CLIENTTAXES_NEW T
WHERE @SAUDA_DATE BETWEEN FROMDATE AND TODATE
AND #TOTPARTY.PARTY_CODE = T.PARTY_CODE
AND T.TRANS_CAT = 'TRD'
AND TURNOVER_TAX = 0

UPDATE #TOTPARTY SET TOTFLAG = 0 
FROM CLIENT2 T
WHERE #TOTPARTY.PARTY_CODE = T.PARTY_CODE 
AND TURNOVER_TAX = 0

SELECT SAUDA_DATE=@SAUDA_DATE, TURNOVER=ISNULL(SUM(TRADEQTY*MARKETRATE),0),
TOTCHARG = CONVERT(NUMERIC(18,6),-1)
INTO #SETT_TOT
FROM SETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SERIES = 'EQ' AND SCRIP_CD NOT IN (SELECT SCRIP_CD FROM TURNTAX_EXCEPTION
								   WHERE SAUDA_DATE BETWEEN DATEFROM AND DATETO
								   AND SETTLEMENT.SCRIP_CD = TURNTAX_EXCEPTION.SCRIP_CD
								   AND TAX_PERC = -1) 

INSERT INTO #SETT_TOT
SELECT SAUDA_DATE=@SAUDA_DATE, TURNOVER=ISNULL(SUM(TRADEQTY*MARKETRATE),0),
TOTCHARG = CONVERT(NUMERIC(18,6),-1) 
FROM ISETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SERIES = 'EQ' AND SCRIP_CD NOT IN (SELECT SCRIP_CD FROM TURNTAX_EXCEPTION
								   WHERE SAUDA_DATE BETWEEN DATEFROM AND DATETO
								   AND ISETTLEMENT.SCRIP_CD = TURNTAX_EXCEPTION.SCRIP_CD
								   AND TAX_PERC = -1) 

DELETE FROM TBL_TOT_SUMMARY 
WHERE SAUDA_DATE = @SAUDA_DATE 

INSERT INTO TBL_TOT_SUMMARY
SELECT SAUDA_DATE, TURNOVER = ISNULL(SUM(TURNOVER),0) FROM #SETT_TOT
GROUP BY SAUDA_DATE
 
SET @TOTAL_TOT = 0 

SELECT @TOTAL_TOT = ISNULL(SUM(TURNOVER),0) FROM TBL_TOT_SUMMARY
WHERE SAUDA_DATE >= @START_DATE AND SAUDA_DATE < @SAUDA_DATE

SET @TOTCUR = CURSOR FOR
SELECT FROM_TOT, TO_TOT, CHARGE_PER FROM TBL_TURNOVER_SLAB
WHERE @SAUDA_DATE BETWEEN FROMDATE AND TODATE
AND @TOTAL_TOT >= FROM_TOT AND @TOTAL_TOT < (CASE WHEN TO_TOT = -1 THEN @TOTAL_TOT+1 ELSE TO_TOT END)
AND CHARGE_TYPE = 'EQ'
ORDER BY FROM_TOT, TO_TOT
OPEN @TOTCUR
FETCH NEXT FROM @TOTCUR INTO @FROM_TOT, @TO_TOT, @CHARGE_PER
IF @@FETCH_STATUS = 0
BEGIN
	UPDATE #SETT_TOT SET TOTCHARG = @CHARGE_PER 
END
CLOSE @TOTCUR
DEALLOCATE @TOTCUR

DELETE FROM #SETT_TOT WHERE TOTCHARG = -1

IF (SELECT ISNULL(COUNT(1),0) FROM  #SETT_TOT ) = 0 
BEGIN
	RETURN
END
UPDATE SETTLEMENT SET TURN_TAX = TRADEQTY*MARKETRATE*TOTCHARG/100
FROM #SETT_TOT 
WHERE SETTLEMENT.SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SERIES = 'EQ' AND SCRIP_CD NOT IN (SELECT SCRIP_CD FROM TURNTAX_EXCEPTION
								   WHERE SETTLEMENT.SAUDA_DATE BETWEEN DATEFROM AND DATETO
								   AND SETTLEMENT.SCRIP_CD = TURNTAX_EXCEPTION.SCRIP_CD
								   AND TAX_PERC = -1)
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
 
UPDATE ISETTLEMENT SET TURN_TAX = TRADEQTY*MARKETRATE*TOTCHARG/100
FROM #SETT_TOT 
WHERE ISETTLEMENT.SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SERIES = 'EQ' AND SCRIP_CD NOT IN (SELECT SCRIP_CD FROM TURNTAX_EXCEPTION
								   WHERE ISETTLEMENT.SAUDA_DATE BETWEEN DATEFROM AND DATETO
								   AND ISETTLEMENT.SCRIP_CD = TURNTAX_EXCEPTION.SCRIP_CD
								   AND TAX_PERC = -1) 
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
 


SELECT SAUDA_DATE=@SAUDA_DATE, TURNOVER=ISNULL(SUM(TRADEQTY*MARKETRATE),0),
TOTCHARG = CONVERT(NUMERIC(18,6),-1)
INTO #SETT_TOT_NONEQ
FROM SETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SCRIP_CD IN (SELECT SCRIP_CD FROM #SETTSCRIP
								   WHERE #SETTSCRIP.SCRIP_CD = SETTLEMENT.SCRIP_CD
								   AND #SETTSCRIP.SERIES = SETTLEMENT.SERIES) 

INSERT INTO #SETT_TOT_NONEQ
SELECT SAUDA_DATE=@SAUDA_DATE, TURNOVER=ISNULL(SUM(TRADEQTY*MARKETRATE),0),
TOTCHARG = CONVERT(NUMERIC(18,6),-1) 
FROM ISETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SCRIP_CD IN (SELECT SCRIP_CD FROM #SETTSCRIP
								   WHERE #SETTSCRIP.SCRIP_CD = ISETTLEMENT.SCRIP_CD
								   AND #SETTSCRIP.SERIES = ISETTLEMENT.SERIES) 

DELETE FROM TBL_TOT_SUMMARY_NONEQ 
WHERE SAUDA_DATE = @SAUDA_DATE 

INSERT INTO TBL_TOT_SUMMARY_NONEQ
SELECT SAUDA_DATE, TURNOVER = ISNULL(SUM(TURNOVER),0) FROM #SETT_TOT_NONEQ
GROUP BY SAUDA_DATE
 
SET @TOTAL_TOT = 0 

SELECT @TOTAL_TOT = ISNULL(SUM(TURNOVER),0) FROM TBL_TOT_SUMMARY_NONEQ
WHERE SAUDA_DATE >= @START_DATE AND SAUDA_DATE < @SAUDA_DATE

SET @TOTCUR = CURSOR FOR
SELECT FROM_TOT, TO_TOT, CHARGE_PER FROM TBL_TURNOVER_SLAB
WHERE @SAUDA_DATE BETWEEN FROMDATE AND TODATE
AND @TOTAL_TOT >= FROM_TOT AND @TOTAL_TOT < (CASE WHEN TO_TOT = -1 THEN @TOTAL_TOT+1 ELSE TO_TOT END)
AND CHARGE_TYPE = 'NONEQ'
ORDER BY FROM_TOT, TO_TOT
OPEN @TOTCUR
FETCH NEXT FROM @TOTCUR INTO @FROM_TOT, @TO_TOT, @CHARGE_PER
IF @@FETCH_STATUS = 0
BEGIN
	UPDATE #SETT_TOT_NONEQ SET TOTCHARG = @CHARGE_PER 
END
CLOSE @TOTCUR
DEALLOCATE @TOTCUR

DELETE FROM #SETT_TOT_NONEQ WHERE TOTCHARG = -1

IF (SELECT ISNULL(COUNT(1),0) FROM  #SETT_TOT_NONEQ ) = 0 
BEGIN
	RETURN
END

UPDATE SETTLEMENT SET TURN_TAX = TRADEQTY*MARKETRATE*TOTCHARG/100
FROM #SETT_TOT_NONEQ 
WHERE SETTLEMENT.SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SCRIP_CD IN (SELECT SCRIP_CD FROM #SETTSCRIP
								   WHERE #SETTSCRIP.SCRIP_CD = SETTLEMENT.SCRIP_CD
								   AND #SETTSCRIP.SERIES = SETTLEMENT.SERIES) 
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
 
UPDATE ISETTLEMENT SET TURN_TAX = TRADEQTY*MARKETRATE*TOTCHARG/100
FROM #SETT_TOT_NONEQ 
WHERE ISETTLEMENT.SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SCRIP_CD IN (SELECT SCRIP_CD FROM #SETTSCRIP
								   WHERE #SETTSCRIP.SCRIP_CD = ISETTLEMENT.SCRIP_CD
								   AND #SETTSCRIP.SERIES = ISETTLEMENT.SERIES) 
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 


DELETE FROM TBL_TOT_DETAIL 
WHERE SAUDA_DATE = @SAUDA_DATE AND SETT_TYPE= @SETT_TYPE
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 

INSERT INTO TBL_TOT_DETAIL 
SELECT SETT_NO, SETT_TYPE, SAUDA_DATE = CONVERT(DATETIME,@SAUDA_DATE), CONTRACTNO, PARTY_CODE, SCRIP_CD, SERIES, TURN_TAX = SUM(TURN_TAX),@CHARGE_PER
FROM SETTLEMENT
WHERE SETTLEMENT.SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SERIES = 'EQ' AND SCRIP_CD NOT IN (SELECT SCRIP_CD FROM TURNTAX_EXCEPTION
								   WHERE SETTLEMENT.SAUDA_DATE BETWEEN DATEFROM AND DATETO
								   AND SETTLEMENT.SCRIP_CD = TURNTAX_EXCEPTION.SCRIP_CD
								   AND TAX_PERC = -1) 
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
GROUP BY  SETT_NO, SETT_TYPE,  CONTRACTNO, PARTY_CODE, SCRIP_CD, SERIES

INSERT INTO TBL_TOT_DETAIL
SELECT SETT_NO, SETT_TYPE, SAUDA_DATE = CONVERT(DATETIME,@SAUDA_DATE), CONTRACTNO, PARTY_CODE, SCRIP_CD, SERIES, TURN_TAX = SUM(TURN_TAX),@CHARGE_PER
FROM SETTLEMENT
WHERE SETTLEMENT.SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SCRIP_CD IN (SELECT SCRIP_CD FROM #SETTSCRIP
								   WHERE #SETTSCRIP.SCRIP_CD = SETTLEMENT.SCRIP_CD
								   AND #SETTSCRIP.SERIES = SETTLEMENT.SERIES) 
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
GROUP BY  SETT_NO, SETT_TYPE,  CONTRACTNO, PARTY_CODE, SCRIP_CD, SERIES

INSERT INTO TBL_TOT_DETAIL
SELECT SETT_NO, SETT_TYPE, SAUDA_DATE = CONVERT(DATETIME,@SAUDA_DATE), CONTRACTNO, PARTY_CODE, SCRIP_CD, SERIES, TURN_TAX = SUM(TURN_TAX),@CHARGE_PER
FROM ISETTLEMENT
WHERE ISETTLEMENT.SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SERIES = 'EQ' AND SCRIP_CD NOT IN (SELECT SCRIP_CD FROM TURNTAX_EXCEPTION
								   WHERE ISETTLEMENT.SAUDA_DATE BETWEEN DATEFROM AND DATETO
								   AND ISETTLEMENT.SCRIP_CD = TURNTAX_EXCEPTION.SCRIP_CD
								   AND TAX_PERC = -1) 
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
GROUP BY  SETT_NO, SETT_TYPE,  CONTRACTNO, PARTY_CODE, SCRIP_CD, SERIES

INSERT INTO TBL_TOT_DETAIL
SELECT SETT_NO, SETT_TYPE, SAUDA_DATE = CONVERT(DATETIME,@SAUDA_DATE), CONTRACTNO, PARTY_CODE, SCRIP_CD, SERIES, TURN_TAX = SUM(TURN_TAX),@CHARGE_PER
FROM ISETTLEMENT
WHERE ISETTLEMENT.SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND SCRIP_CD IN (SELECT SCRIP_CD FROM #SETTSCRIP
								   WHERE #SETTSCRIP.SCRIP_CD = iSETTLEMENT.SCRIP_CD
								   AND #SETTSCRIP.SERIES = iSETTLEMENT.SERIES) 
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY  
GROUP BY  SETT_NO, SETT_TYPE,  CONTRACTNO, PARTY_CODE, SCRIP_CD, SERIES

UPDATE SETTLEMENT SET TURN_TAX = 0 
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #TOTPARTY WHERE SETTLEMENT.PARTY_CODE = #TOTPARTY.PARTY_CODE 
				   AND TOTFLAG = 0)

UPDATE ISETTLEMENT SET TURN_TAX = 0 
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%' 
AND SETT_TYPE = @SETT_TYPE
AND AUCTIONPART NOT LIKE 'A%'
AND AUCTIONPART NOT LIKE 'F%' AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%'
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
AND PARTY_CODE IN (SELECT PARTY_CODE FROM #TOTPARTY WHERE ISETTLEMENT.PARTY_CODE = #TOTPARTY.PARTY_CODE 
				   AND TOTFLAG = 0)

DROP TABLE #SETT_TOT

GO
