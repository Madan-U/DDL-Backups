CREATE PROC [dbo].[SEBI_AUDIT_CLIENT_MCX_DAILY]
(
	@REPORTDATE	VARCHAR(11)
)
AS

DECLARE @NSEMEMBERCODE VARCHAR(10),        
  @BSEMEMBERCODE VARCHAR(10),
  @SEGMENT VARCHAR(3)

SET @SEGMENT = ''  
--SET @SEGMENT = 'BSE'   
SET @NSEMEMBERCODE = ''

CREATE TABLE #CLIENT_LIST
(
	CLTCODE VARCHAR(10)
)


--INSERT INTO #CLIENT_LIST

EXEC GET_SEBI_AUDIT_CLIENT_LIST_COMM 'MCX', @REPORTDATE

--select * from #CLIENT_LIST where cltcode = '581619'
--SELECT @NSEMEMBERCODE = MEMBERCODE FROM MSAJAG.DBO.OWNER        
SELECT @BSEMEMBERCODE = MEMBERCODE FROM [ANGELCOMMODITY].MCDX.DBO.FOOWNER        
/*
INSERT INTO #CLIENT_LIST
SELECT DISTINCT PARTY_CODE  
FROM ACCOUNT.DBO.FINAL_AUDIT_DATA D
WHERE EFFDATE = @REPORTDATE
AND PARTY_CODE IN (SELECT CL_CODE FROM CLIENT_DETAILS)
AND NOT EXISTS(SELECT CLTCODE FROM #CLIENT_LIST L WHERE D.PARTY_CODE = CLTCODE) 
*/
SELECT PARTY_NAME, PARTY_CODE = case when party_code like '98%' then right(party_code, len(party_code) - 2) else party_code end, PAN_GIR_NO = CONVERT(VARCHAR(35), ''),  
EMAIL_ID = CONVERT(VARCHAR(100), ''), MOBILENO = CONVERT(VARCHAR(40), ''),
MCXBAL = SUM(ISNULL(MCX_VDTBAL,0)),
TOTAL_VDTBAL = SUM(ISNULL( TOTAL_VDTBAL ,0)), 
TOT_ISIN = 0, TOT_ISIN_QTY = CONVERT(NUMERIC(18,3),0),
QTY_PLEDGE = CONVERT(NUMERIC(18,3),0),
FUND_RAISE = CONVERT(NUMERIC(18,4),0),
LAST_SETT_DATE = CONVERT(VARCHAR(9), '')
INTO #DATA
FROM ACCOUNT.DBO.FINAL_AUDIT_DATA D (NOLOCK)
WHERE EFFDATE = @REPORTDATE
AND CL_TYPE <> 'PRO'        
AND PARTY_CODE NOT IN (@NSEMEMBERCODE, @BSEMEMBERCODE)        
AND NOT EXISTS (SELECT PARTY_CODE FROM ACCOUNT.DBO.SEBI_EXCEP_PARTY S     
       WHERE S.PARTY_CODE = D.PARTY_CODE) 
AND EXISTS(SELECT CLTCODE FROM #CLIENT_LIST L WHERE D.PARTY_CODE = CLTCODE)
GROUP BY PARTY_CODE, PARTY_NAME

--select * from #DATA where PARTY_CODE = '581619'
--return

SELECT D.PARTY_CODE, ISIN = D.CERTNO, QTY = SUM(CRQTY-DRQTY) 
INTO #ISIN
FROM [ANGELDEMAT].BSEDB.DBO.TBL_DEL_HOLD_SEC D WITH (NOLOCK)
WHERE TRANSDATE <= @REPORTDATE + ' 23:59:59'
AND BCLTDPID IN (SELECT DPCLTNO FROM [ANGELDEMAT].BSEDB.DBO.DELIVERYDP WITH (NOLOCK))
GROUP BY PARTY_CODE, CERTNO
HAVING SUM(CRQTY-DRQTY) > 0 
ORDER BY 1, 2 

INSERT INTO #ISIN
SELECT D.PARTY_CODE, ISIN = D.CERTNO, QTY = SUM(CRQTY-DRQTY)
FROM [ANGELDEMAT].MSAJAG.DBO.TBL_DEL_HOLD_SEC D WITH (NOLOCK)
WHERE TRANSDATE <= @REPORTDATE + ' 23:59:59'
AND BCLTDPID IN (SELECT DPCLTNO FROM [ANGELDEMAT].MSAJAG.DBO.DELIVERYDP WITH (NOLOCK))
GROUP BY PARTY_CODE, CERTNO
HAVING SUM(CRQTY-DRQTY) > 0 
ORDER BY 1, 2 

UPDATE #ISIN SET PARTY_CODE = case when party_code like '98%' then right(party_code, len(party_code) - 2) else party_code end

UPDATE #DATA SET /*PARTY_NAME = CASE WHEN ISNULL(UPPER(C1.ADDEMAILID), '') <> '' 
THEN UPPER(C1.ADDEMAILID) ELSE PARTY_NAME END, */ PAN_GIR_NO = C1.PAN_GIR_NO,
 --EMAIL_ID = C1.EMAIL, 
  EMAIL_ID =LTRIM(RTRIM(.dbo.PIECE(C1.EMAIL, ';', 1))),
 MOBILENO = C1.MOBILE_PAGER  
FROM MSAJAG.DBO.CLIENT_DETAILS C1 
WHERE C1.CL_CODE = #DATA.PARTY_CODE  
  
UPDATE #DATA SET  MOBILENO = '6666666666' 
FROM MSAJAG.DBO.CLIENT_DETAILS C (NOLOCK)
WHERE C.CL_CODE = #DATA.PARTY_CODE
and c.cl_type in('NRI','NRO','NRE')

UPDATE #DATA SET PAN_GIR_NO = 'NNNNN1111N' 
WHERE LEN(PAN_GIR_NO )<>10 OR PAN_GIR_NO IS NULL

UPDATE #DATA SET EMAIL_ID = 'notprovided@notprovided.com' 
WHERE EMAIL_ID NOT LIKE '%@%' OR EMAIL_ID IS NULL

UPDATE #DATA SET EMAIL_ID = 'notprovided@notprovided.com' 
WHERE EMAIL_ID LIKE '%,%' OR EMAIL_ID LIKE '%~%' 
   OR EMAIL_ID LIKE '%`%' OR EMAIL_ID LIKE '%!%'
   OR EMAIL_ID LIKE '%^%' OR EMAIL_ID LIKE '%:%'
   OR EMAIL_ID LIKE '%|%' 

UPDATE #DATA SET MOBILENO = '6666666666'   
WHERE MOBILENO = '0' OR MOBILENO = '' OR MOBILENO IS NULL  

UPDATE #DATA SET MOBILENO = '6666666666'   
WHERE LEFT(MOBILENO,1) NOT IN('6','7','8','9')

/*
UPDATE #DATA SET TOT_ISIN = ISIN_CNT, TOT_ISIN_QTY = QTY_SUM, MCXBAL = MCXBAL + ISNULL(CASH_COLL, 0), TOTAL_VDTBAL = TOTAL_VDTBAL + ISNULL(TOT_CASH_COLL, 0)
FROM (SELECT PARTY_CODE, ISIN_CNT = CASE WHEN SUM(QTY) > 0 THEN COUNT(DISTINCT ISIN) ELSE 0 END, QTY_SUM = SUM(QTY), CASH_COLL = SUM(CASH_COLL), TOT_CASH_COLL = SUM(TOT_CASH_COLL) FROM #ISIN 
	  GROUP BY PARTY_CODE) A
WHERE #DATA.PARTY_CODE = A.PARTY_CODE
*/

UPDATE #DATA SET TOT_ISIN = ISIN_CNT, TOT_ISIN_QTY = QTY_SUM
FROM (SELECT PARTY_CODE, ISIN_CNT = CASE WHEN SUM(QTY) > 0 THEN COUNT(DISTINCT ISIN) ELSE 0 END, QTY_SUM = SUM(QTY) FROM #ISIN 
	  GROUP BY PARTY_CODE) A
WHERE #DATA.PARTY_CODE = A.PARTY_CODE

EXEC ACCOUNT.DBO.GET_LAST_SETTTLE_DATE @REPORTDATE, 'MCX'

SELECT PARTY_NAME, PARTY_CODE , PAN_GIR_NO,    
EMAIL_ID, MOBILENO,  
MCXBAL = SUM(ISNULL(MCXBAL, 0)),  
TOTAL_VDTBAL = SUM(ISNULL(TOTAL_VDTBAL ,0)),   
TOT_ISIN = (ISNULL(TOT_ISIN, 0)), TOT_ISIN_QTY = (ISNULL(TOT_ISIN_QTY, 0)) ,TOT_ISIN_PLEDGE=0,  
QTY_PLEDGE = (ISNULL(QTY_PLEDGE, 0)),  
FUND_RAISE = SUM(ISNULL(FUND_RAISE, 0)),  
LAST_SETT_DATE = MAX(ISNULL(LAST_SETT_DATE, ''))  
INTO #DATA_PARENT
FROM #DATA D, (SELECT DISTINCT * FROM #CLIENT_LIST) C
WHERE D.PARTY_CODE = C.CLTCODE
GROUP BY PARTY_NAME, PARTY_CODE, PAN_GIR_NO, EMAIL_ID, MOBILENO, TOT_ISIN, TOT_ISIN_QTY, QTY_PLEDGE

SELECT PARTY_CODE INTO #DELDATA FROM #DATA_PARENT
WHERE ABS(MCXBAL)+ABS(TOTAL_VDTBAL) +TOT_ISIN >= 0 

DELETE FROM #DATA_PARENT
WHERE PARTY_CODE NOT IN (
SELECT PARTY_CODE FROM #DELDATA )
AND PARTY_CODE NOT IN (
SELECT CL_CODE FROM CLIENT_BROK_DETAILS 
WHERE ACTIVE_DATE <= CONVERT(DATETIME,@REPORTDATE))

DELETE FROM #DATA_PARENT
WHERE PARTY_CODE NOT IN (
SELECT PARTY_CODE FROM #DELDATA )
AND PARTY_CODE NOT IN (
SELECT CL_CODE FROM CLIENT_BROK_DETAILS 
WHERE inacTive_from < CONVERT(DATETIME,@REPORTDATE) - 365)



CREATE TABLE #CLIENT ( 
		PARTY_CODE VARCHAR(20) 
		)
		
DECLARE @EXCHANGE_NEW VARCHAR(3)
DECLARE @SEGMENT_NEW VARCHAR(10)
DECLARE @SHAREDB VARCHAR(35)
DECLARE @SHARESERVER VARCHAR(35)

DECLARE @EXCHANGEWISE_CURSOR CURSOR 
DECLARE @STRSQL VARCHAR(8000) 

SELECT EXCHANGE,SEGMENT INTO #CLIENT_BROK_DETAILS FROM MSAJAG.DBO.CLIENT_BROK_DETAILS (NOLOCK) GROUP BY EXCHANGE,SEGMENT 

SET @EXCHANGEWISE_CURSOR = CURSOR FOR
SELECT DISTINCT 
	EXCHANGE = M.EXCHANGE,
	SEGMENT = M.SEGMENT,
	SHAREDB,
	SHARESERVER
FROM PRADNYA..MULTICOMPANY M(NOLOCK),
	#CLIENT_BROK_DETAILS CB(NOLOCK)
WHERE SEGMENT_DESCRIPTION NOT LIKE '%PCM%'
    AND SHAREDB <> 'BSEFO'
	AND CB.EXCHANGE = M.EXCHANGE
	AND CB.SEGMENT = M.SEGMENT
	AND PRIMARYSERVER = 1		

OPEN @EXCHANGEWISE_CURSOR

FETCH NEXT
FROM @EXCHANGEWISE_CURSOR
INTO
	@EXCHANGE_NEW,
	@SEGMENT_NEW,
	@SHAREDB,
	@SHARESERVER

WHILE @@FETCH_STATUS = 0
BEGIN
	SET @STRSQL = ''
	IF @SHARESERVER = @@SERVERNAME 
	BEGIN
		SET @SHARESERVER =''
	END
	ELSE
	BEGIN
		SET @SHARESERVER = @SHARESERVER + '.'
	END

	IF @SEGMENT_NEW IN ('CAPITAL','SLBS')
	BEGIN
			
		SET @STRSQL = 'INSERT INTO #CLIENT '
		SET @STRSQL = @STRSQL + 'SELECT PARTY_CODE FROM ' + @SHARESERVER + @SHAREDB + '.DBO.CMBILLVALAN WITH(NOLOCK) '
		SET @STRSQL = @STRSQL + 'WHERE SAUDA_DATE > CONVERT(DATETIME,''' + @REPORTDATE +''') - 365 and SAUDA_DATE <= CONVERT(DATETIME,''' + @REPORTDATE +''') '
		SET @STRSQL = @STRSQL + 'GROUP BY PARTY_CODE '
		PRINT @STRSQL
		EXEC (@STRSQL)

	END
	ELSE
				
		IF @SEGMENT_NEW = 'FUTURES'
		BEGIN

			SET @STRSQL = 'INSERT INTO #CLIENT '
			SET @STRSQL = @STRSQL + 'SELECT PARTY_CODE FROM ' + @SHARESERVER + @SHAREDB + '.DBO.FOBILLVALAN WITH(NOLOCK) '
			SET @STRSQL = @STRSQL + 'WHERE SAUDA_DATE > CONVERT(DATETIME,''' + @REPORTDATE +''') - 365 and SAUDA_DATE <= CONVERT(DATETIME,''' + @REPORTDATE +''') '
			SET @STRSQL = @STRSQL + 'GROUP BY PARTY_CODE '
			PRINT @STRSQL
			EXEC (@STRSQL)

		END

	FETCH NEXT
	FROM @EXCHANGEWISE_CURSOR
	INTO 
		@EXCHANGE_NEW,
		@SEGMENT_NEW,
		@SHAREDB,
		@SHARESERVER
END

CLOSE @EXCHANGEWISE_CURSOR
DEALLOCATE @EXCHANGEWISE_CURSOR

SET @STRSQL = 'INSERT INTO #CLIENT '
SET @STRSQL = @STRSQL + 'SELECT PARTY_CODE FROM TBL_SEBI_ENHANCE WITH(NOLOCK) '
SET @STRSQL = @STRSQL + 'WHERE SAUDA_DATE > CONVERT(DATETIME,''' + @REPORTDATE +''') - 365 and SAUDA_DATE <= CONVERT(DATETIME,''' + @REPORTDATE +''') '
SET @STRSQL = @STRSQL + 'GROUP BY PARTY_CODE '
PRINT @STRSQL
EXEC (@STRSQL)


DELETE FROM #DATA_PARENT
WHERE PARTY_CODE NOT IN (
SELECT PARTY_CODE FROM #DELDATA )
AND PARTY_CODE NOT IN (
SELECT DISTINCT PARTY_CODE FROM #CLIENT )

DECLARE
	@@REC_COUNT INT

SET @@REC_COUNT = 0

SELECT @@REC_COUNT = COUNT(1) FROM #DATA_PARENT  

CREATE TABLE #FILE_DATA
(
	FILENAME varchar(100),
	DATA VARCHAR(2000),
	SRNO INT IDENTITY(1, 1)
)

DECLARE
	@BATCHNO INT  

SELECT @BATCHNO = 0  

IF @@REC_COUNT > 0
BEGIN
	SELECT @BATCHNO = ISNULL(MAX(FLDBATCHNO),0) FROM ASIANFILEBATCHNO  
	WHERE FLDFILENO = 'ME'  
	AND FLDFILEDATE = @REPORTDATE  

	SET @BATCHNO = @BATCHNO + 1  

	INSERT INTO ASIANFILEBATCHNO  
	SELECT 'ME', @BATCHNO, @REPORTDATE, GETDATE()
	
	--INSERT INTO #FILE_DATA
	--SELECT FILENAME =  CONVERT(VARCHAR(3), CONVERT(DATETIME, @REPORTDATE) )+'_'+RIGHT(CONVERT(VARCHAR(11), CONVERT(DATETIME, @REPORTDATE)),2) + '_' + RIGHT('00' + CONVERT(VARCHAR,@BATCHNO),2),DATA = CONVERT(VARCHAR(3), CONVERT(DATETIME, @REPORTDATE) )+'-'+RIGHT(CONVERT(VARCHAR(11), CONVERT(DATETIME, @REPORTDATE)),2) + ',' + CONVERT(VARCHAR, @@REC_COUNT)
		
	INSERT INTO #FILE_DATA  
	SELECT FILENAME = @BSEMEMBERCODE + '_' + + 'CFS' + '_' +  REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@REPORTDATE,109),103),'/','')  + '.csv',
	DATA ='Client name,Unique Client Code,Client PAN no,Fund balance (MCX) (Rs.) *,Net across Exchanges (Rs.) *,Total Number of ISINs *,Total Quantity of Securities *,Total Number of ISINs Pledged,Quantity of securities pledged *,Funds Raised-Securities (Rs.)*,Total number of Commodities *,Total number commodities Pledge *,Fund Raised-Commodities (Rs.)*,Last balance settlement date (dd/mm/yyyy),'
	 
	INSERT INTO #FILE_DATA
	SELECT FILENAME = @BSEMEMBERCODE + '_' + + 'CFS' + '_' +  REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@REPORTDATE,109),103),'/','')  + '.csv',
	--SELECT FILENAME =  CONVERT(VARCHAR(3), CONVERT(DATETIME, @REPORTDATE) )+'_'+RIGHT(CONVERT(VARCHAR(11), CONVERT(DATETIME, @REPORTDATE)),2) 
	--+ '_' + RIGHT('00' + CONVERT(VARCHAR,@BATCHNO),2),
	DATA = LTRIM(RTRIM(PARTY_NAME )) + ',' + LTRIM(RTRIM(PARTY_CODE)) + ',' + PAN_GIR_NO + ',' + CONVERT(VARCHAR, MCXBAL) + ',' + CONVERT(VARCHAR, TOTAL_VDTBAL) + ',' + CONVERT(VARCHAR, TOT_ISIN) + ',' + CONVERT(VARCHAR, TOT_ISIN_QTY) + ',' + CONVERT(VARCHAR
	, TOT_ISIN_PLEDGE) + ',' + CONVERT(VARCHAR, QTY_PLEDGE) + ',' + CONVERT(VARCHAR, FUND_RAISE)  + ',0,0,0,' + LAST_SETT_DATE
	FROM #DATA_PARENT   
	SELECT FILENAME,  DATA FROM #FILE_DATA ORDER BY SRNO
END
ELSE
BEGIN
	SELECT DATA = PARTY_NAME + ',' + LTRIM(RTRIM(PARTY_CODE)) + ',' + PAN_GIR_NO + ',' + CONVERT(VARCHAR, MCXBAL) + ',' + CONVERT(VARCHAR, TOTAL_VDTBAL) + ',' + CONVERT(VARCHAR, TOT_ISIN) + ',' + CONVERT(VARCHAR, TOT_ISIN_QTY) + ',' + CONVERT(VARCHAR, TOT_ISIN_PLEDGE) + ',' + CONVERT(VARCHAR, QTY_PLEDGE) + ',' + CONVERT(VARCHAR, FUND_RAISE)  + ',0,0,0,' + LAST_SETT_DATE
 FROM #DATA_PARENT 
WHERE CAST((ABS(MCXBAL) + ABS(TOTAL_VDTBAL) + ABS(TOT_ISIN) + ABS(TOT_ISIN)) AS NUMERIC) >= 0 
END

DROP TABLE #DATA 
DROP TABLE #DATA_PARENT
DROP TABLE #FILE_DATA
DROP TABLE #CLIENT

GO
