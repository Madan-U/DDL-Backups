CREATE PROC [dbo].[GET_COLLATERAL_NBFC]              
(              
 @TRANSDATE VARCHAR(11)      
)              
AS              

-- EXEC GET_COLLATERAL_NBFC 'SEP 28 2015'

DROP TABLE COLLATERAL_TEMP
DROP TABLE COLLATERALDETAILS_TEMP

/*                
SELECT * INTO COLLATERAL_TEMP FROM MSAJAG.DBO.BAK_COLLATERAL C                
WHERE TRANS_DATE =  @FROMDATE              
AND EXCHANGE NOT IN ('MCX', 'NCX')              
AND EXISTS (SELECT PARTY_CODE FROM #TBL_MARGINPARTY TBL_MARGINPARTY             
  WHERE TBL_MARGINPARTY.PARTY_CODE = C.PARTY_CODE)            
                
SELECT * INTO COLLATERALDETAILS_TEMP FROM MSAJAG.DBO.BAK_COLLATERALDETAILS C                 
WHERE EFFDATE =  @FROMDATE              
AND EXCHANGE NOT IN ('MCX', 'NCX')              
AND EXISTS (SELECT PARTY_CODE FROM #TBL_MARGINPARTY TBL_MARGINPARTY             
  WHERE TBL_MARGINPARTY.PARTY_CODE = C.PARTY_CODE)        
*/      

SELECT * INTO #NBFCCLT 
	     FROM ABVSCITRUS.NBFC.DBO.TBLCLIENTMARGIN 
WHERE TO_DATE > @TRANSDATE


SELECT * INTO COLLATERALDETAILS_TEMP FROM MSAJAG.DBO.COLLATERALDETAILS C               
WHERE 1 = 2   
  
DECLARE @TDATE DATETIME,  
@SEGMENT VARCHAR(7),
@EXCHANGE VARCHAR(3),
  @TCUR CURSOR  
    
    
CREATE CLUSTERED INDEX IDXCLT ON #NBFCCLT 
(
	PARTY_CODE
)

SELECT EXCHANGE, SEGMENT, TRANS_DATE=MAX(TRANS_DATE) INTO #COLL
FROM AngelNSECM.MSAJAG.DBO.COLLATERAL 
WHERE TRANS_DATE <=  @TRANSDATE + ' 23:59' 
GROUP BY EXCHANGE, SEGMENT

SELECT * INTO COLLATERAL_TEMP FROM MSAJAG.DBO.COLLATERAL C
WHERE TRANS_DATE IN (SELECT TRANS_DATE FROM #COLL 
WHERE C.TRANS_DATE = #COLL.TRANS_DATE AND C.EXCHANGE = #COLL.EXCHANGE
AND C.SEGMENT = #COLL.SEGMENT)
AND EXISTS (SELECT PARTY_CODE FROM #NBFCCLT WHERE C.PARTY_CODE = #NBFCCLT.PARTY_CODE)

SET @TCUR = CURSOR FOR  
SELECT DISTINCT TRANS_DATE, EXCHANGE, SEGMENT FROM COLLATERAL_TEMP  
ORDER BY 1  
OPEN @TCUR   
FETCH NEXT FROM @TCUR INTO @TDATE, @EXCHANGE, @SEGMENT  
WHILE @@FETCH_STATUS = 0   
BEGIN  
   
 INSERT INTO COLLATERALDETAILS_TEMP  
 SELECT * FROM MSAJAG.DBO.COLLATERALDETAILS C               
 WHERE EFFDATE = @TDATE AND EXCHANGE = @EXCHANGE AND SEGMENT = @SEGMENT   
   
 FETCH NEXT FROM @TCUR INTO @TDATE, @EXCHANGE, @SEGMENT   
END   
CLOSE @TCUR  
DEALLOCATE @TCUR  

DELETE FROM COLLATERALDETAILS_TEMP      
WHERE NOT EXISTS (SELECT PARTY_CODE FROM #NBFCCLT             
WHERE #NBFCCLT.PARTY_CODE = COLLATERALDETAILS_TEMP.PARTY_CODE)

GO
