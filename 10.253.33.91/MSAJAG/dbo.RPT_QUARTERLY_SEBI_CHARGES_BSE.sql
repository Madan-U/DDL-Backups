     END),  
     CLI_STT = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(STT_CLI_TRDFUT,0))  
      ELSE SUM(ISNULL(STT_CLI_DELOPT,0))  
     END),       
     CLI_DP_CHRG = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(DP_CHRG_CLI_TRDFUT,0))  
      ELSE SUM(ISNULL(DP_CHRG_CLI_DELOPT,0))  
     END),  
       
     CLI_TOT_TURNOVER = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(TOTTURNOVER_CLI_TRDFUT,0))  
      ELSE SUM(ISNULL(TOTTURNOVER_CLI_DELOPT,0))  
     END),  
       
     PRO_TOT_BROKERAGE = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(TOTBROKERAGE_PRO_TRDFUT,0))  
      ELSE SUM(ISNULL(TOTBROKERAGE_PRO_DELOPT,0))  
     END),  
     PRO_CLEARING_CHRG = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(CL_CHRG_PRO_TRDFUT,0))  
      ELSE SUM(ISNULL(CL_CHRG_PRO_DELOPT,0))  
     END),  
     PRO_TURN_TAX = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(TURN_TAX_PRO_TRDFUT,0))  
      ELSE SUM(ISNULL(TURN_TAX_PRO_DELOPT,0))  
     END),  
     PRO_SEBI_TAX = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(SEBI_TAX_PRO_TRDFUT,0))  
      ELSE SUM(ISNULL(SEBI_TAX_PRO_DELOPT,0))  
     END),  
     PRO_STT = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(STT_PRO_TRDFUT,0))  
      ELSE SUM(ISNULL(STT_PRO_DELOPT,0))  
     END),       
     PRO_DP_CHRG = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(DP_CHRG_PRO_TRDFUT,0))  
      ELSE SUM(ISNULL(DP_CHRG_PRO_DELOPT,0))  
     END),     
       
       
     PRO_TOT_TURNOVER = (  
     CASE  
      WHEN DETFLAG IN ('TRD','FUT') THEN SUM(ISNULL(TOTTURNOVER_PRO_TRDFUT,0))  
      ELSE SUM(ISNULL(TOTTURNOVER_PRO_DELOPT,0))  
     END),  
     ORDFLAG  
   FROM #SEBI_CHARGES_NEW  
   WHERE ORDFLAG = @INTFLAG  
   GROUP BY  
     EXCHANGE,  
     ORDFLAG,  
     DETFLAG  
   ORDER BY  
     ORDFLAG  
  END  
  ELSE  
  BEGIN  
   INSERT INTO #SEBI_CHARGES_FINAL  
   SELECT  SEGMENT    = (  
     CASE  
      WHEN @INTFLAG = 1 THEN 'EQUITY-TRD'  
      WHEN @INTFLAG = 2 THEN 'EQUITY-DEL'  
      WHEN @INTFLAG = 3 THEN 'DEBT-TRD'  
      WHEN @INTFLAG = 4 THEN 'DERIVATIVE-FUT'  
      WHEN @INTFLAG = 5 THEN 'DERIVATIVE-OPT'  
      --WHEN @INTFLAG = 6 THEN 'DERIVATIVE-DEL'  
      WHEN @INTFLAG = 6 THEN 'CURRENCY-FUT'  
      WHEN @INTFLAG = 7 THEN 'CURRENCY-OPT'  
      WHEN @INTFLAG = 8 THEN 'IRF-FUT'  
      ELSE ''  
     END),  
     CLI_TOT_BROKERAGE = 0,  
     CLI_CLEARING_CHRG = 0,  
     CLI_TURN_TAX  = 0,  
     CLI_SEBI_TAX  = 0,  
     CLI_STT    = 0,  
     CLI_DP_CHRG   = 0,  
     CLI_TOT_TURNOVER = 0,  
     PRO_TOT_BROKERAGE = 0,  
     PRO_CLEARING_CHRG = 0,  
     PRO_TURN_TAX  = 0,  
     PRO_SEBI_TAX  = 0,  
     PRO_STT    = 0,  
     PRO_DP_CHRG   = 0,       
     PRO_TOT_TURNOVER = 0,  
     ORDFLAG    = @INTFLAG  
  END  
      
  SET @INTFLAG = @INTFLAG + 1  
       
 END  
  
  
