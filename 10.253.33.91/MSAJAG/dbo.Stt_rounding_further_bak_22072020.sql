CREATE PROC [dbo].[Stt_rounding_further_bak_22072020] (@SETT_TYPE VARCHAR(2), @SAUDA_DATE VARCHAR(11), @FROMPARTY VARCHAR(10), @TOPARTY VARCHAR(10))    
AS    
    
DECLARE @STTCUR CURSOR,    
@DIFF NUMERIC(18,4),    
@PARTY_CODE VARCHAR(10),    
@CONTRACTNO VARCHAR(7)    
    
DECLARE @SETTCUR CURSOR,    
@ORDER_NO VARCHAR(20),    
@TRADE_NO VARCHAR(20),    
@SCRIP_CD VARCHAR(12),    
@INS_CHRG NUMERIC(18,4),
@SRNO NUMERIC(18,0)    
    
SELECT SETT_TYPE, PARTY_CODE, CONTRACTNO, TOTAL = SUM(INS_CHRG), ACTUAL = ROUND(SUM(INS_CHRG),0)     
INTO #SETT FROM STT_SETT_TABLE WITH(INDEX(STT_INDEX))  
WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
GROUP BY SETT_TYPE, PARTY_CODE, CONTRACTNO    
HAVING SUM(INS_CHRG) <> ROUND(SUM(INS_CHRG),0)
  
SET @STTCUR = CURSOR FOR    
SELECT PARTY_CODE, CONTRACTNO, DIFF = TOTAL - ACTUAL FROM #SETT    
ORDER BY PARTY_CODE, CONTRACTNO    
OPEN @STTCUR    
FETCH NEXT FROM @STTCUR INTO @PARTY_CODE, @CONTRACTNO, @DIFF    
WHILE @@FETCH_STATUS = 0 --1    
BEGIN    
 SET @SETTCUR = CURSOR FOR    
 SELECT SRNO, SCRIP_CD, ORDER_NO, TRADE_NO, INS_CHRG FROM STT_SETT_TABLE      
 WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
 AND PARTY_CODE = @PARTY_CODE AND CONTRACTNO = @CONTRACTNO    
 AND INS_CHRG > 0     
 ORDER BY INS_CHRG DESC    
 OPEN @SETTCUR    
 FETCH NEXT FROM @SETTCUR INTO @SRNO, @SCRIP_CD, @ORDER_NO, @TRADE_NO, @INS_CHRG    
 WHILE @@FETCH_STATUS = 0 AND @DIFF <> 0 --2    
 BEGIN     
  IF @DIFF < 0     
  BEGIN    
   UPDATE STT_SETT_TABLE SET INS_CHRG = INS_CHRG + ABS(@DIFF)    
  FROM STT_SETT_TABLE WITH(INDEX(STT_INDEX))  
   WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
   AND PARTY_CODE = @PARTY_CODE AND CONTRACTNO = @CONTRACTNO    
   AND INS_CHRG > 0 AND SCRIP_CD = @SCRIP_CD AND ORDER_NO = @ORDER_NO     
    AND TRADE_NO = @TRADE_NO AND SRNO = @SRNO   
   SELECT @DIFF = 0     
  END    
 ELSE    
   BEGIN    
   IF @DIFF >= @INS_CHRG     
   BEGIN     
    UPDATE STT_SETT_TABLE SET INS_CHRG = 0    
    FROM STT_SETT_TABLE WITH(INDEX(STT_INDEX))  
    WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
    AND PARTY_CODE = @PARTY_CODE AND CONTRACTNO = @CONTRACTNO    
     AND INS_CHRG > 0 AND SCRIP_CD = @SCRIP_CD AND ORDER_NO = @ORDER_NO     
     AND TRADE_NO = @TRADE_NO AND SRNO = @SRNO
      
    SELECT @DIFF = @DIFF - @INS_CHRG     
   END     
    ELSE    
   BEGIN    
    UPDATE STT_SETT_TABLE SET INS_CHRG = @INS_CHRG - @DIFF    
   FROM STT_SETT_TABLE WITH(INDEX(STT_INDEX))  
    WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
    AND PARTY_CODE = @PARTY_CODE AND CONTRACTNO = @CONTRACTNO    
     AND INS_CHRG > 0 AND SCRIP_CD = @SCRIP_CD AND ORDER_NO = @ORDER_NO     
     AND TRADE_NO = @TRADE_NO AND SRNO = @SRNO
    SELECT @DIFF = 0    
   END        
   END     
   FETCH NEXT FROM @SETTCUR INTO @SRNO, @SCRIP_CD, @ORDER_NO, @TRADE_NO, @INS_CHRG     
 END    
 CLOSE @SETTCUR    
 DEALLOCATE @SETTCUR    
 FETCH NEXT FROM @STTCUR INTO @PARTY_CODE, @CONTRACTNO, @DIFF    
END    
CLOSE @STTCUR    
DEALLOCATE @STTCUR    

SELECT SETT_TYPE, PARTY_CODE, CONTRACTNO, TOTAL = SUM(INS_CHRG), ACTUAL = ROUND(SUM(INS_CHRG),0)     
INTO #ISETT FROM STT_ISETT_TABLE WITH(INDEX(STT_INDEX))  
WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
GROUP BY SETT_TYPE, PARTY_CODE, CONTRACTNO    
HAVING SUM(INS_CHRG) <> ROUND(SUM(INS_CHRG),0)    
    
SET @STTCUR = CURSOR FOR    
SELECT PARTY_CODE, CONTRACTNO, DIFF = TOTAL - ACTUAL FROM #ISETT    
ORDER BY PARTY_CODE, CONTRACTNO    
OPEN @STTCUR    
FETCH NEXT FROM @STTCUR INTO @PARTY_CODE, @CONTRACTNO, @DIFF    
WHILE @@FETCH_STATUS = 0 --1    
BEGIN    
 SET @SETTCUR = CURSOR FOR    
 SELECT SRNO, SCRIP_CD, ORDER_NO, TRADE_NO, INS_CHRG FROM ISTT_SETT_TABLE      
 WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
 AND PARTY_CODE = @PARTY_CODE AND CONTRACTNO = @CONTRACTNO    
 AND INS_CHRG > 0     
 ORDER BY INS_CHRG DESC    
 OPEN @SETTCUR    
 FETCH NEXT FROM @SETTCUR INTO @SRNO, @SCRIP_CD, @ORDER_NO, @TRADE_NO, @INS_CHRG    
 WHILE @@FETCH_STATUS = 0 AND @DIFF <> 0 --2    
 BEGIN    
  IF @DIFF < 0     
  BEGIN    
   UPDATE STT_ISETT_TABLE SET INS_CHRG = INS_CHRG + ABS(@DIFF)    
   WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
   AND PARTY_CODE = @PARTY_CODE AND CONTRACTNO = @CONTRACTNO    
   AND INS_CHRG > 0 AND SCRIP_CD = @SCRIP_CD AND ORDER_NO = @ORDER_NO     
    AND TRADE_NO = @TRADE_NO AND SRNO = @SRNO   
   SELECT @DIFF = 0     
  END    
 ELSE    
   BEGIN    
   IF @DIFF >= @INS_CHRG     
   BEGIN    
    UPDATE STT_ISETT_TABLE SET INS_CHRG = 0    
    WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
    AND PARTY_CODE = @PARTY_CODE AND CONTRACTNO = @CONTRACTNO    
     AND INS_CHRG > 0 AND SCRIP_CD = @SCRIP_CD AND ORDER_NO = @ORDER_NO     
     AND TRADE_NO = @TRADE_NO AND SRNO = @SRNO
    SELECT @DIFF = @DIFF - @INS_CHRG     
   END     
    ELSE    
   BEGIN    
    UPDATE STT_ISETT_TABLE SET INS_CHRG = @INS_CHRG - @DIFF    
    WHERE SETT_TYPE = @SETT_TYPE AND SAUDA_DATE LIKE @SAUDA_DATE + '%'    
    AND PARTY_CODE = @PARTY_CODE AND CONTRACTNO = @CONTRACTNO    
     AND INS_CHRG > 0 AND SCRIP_CD = @SCRIP_CD AND ORDER_NO = @ORDER_NO     
     AND TRADE_NO = @TRADE_NO AND SRNO = @SRNO
    SELECT @DIFF = 0    
   END        
   END     
   FETCH NEXT FROM @SETTCUR INTO @SRNO, @SCRIP_CD, @ORDER_NO, @TRADE_NO, @INS_CHRG     
 END    
 CLOSE @SETTCUR    
 DEALLOCATE @SETTCUR    
 FETCH NEXT FROM @STTCUR INTO @PARTY_CODE, @CONTRACTNO, @DIFF    
END    
CLOSE @STTCUR    
DEALLOCATE @STTCUR

GO
