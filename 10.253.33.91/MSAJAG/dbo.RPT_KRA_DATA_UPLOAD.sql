-- Object: PROCEDURE dbo.RPT_KRA_DATA_UPLOAD
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[RPT_KRA_DATA_UPLOAD]
	(
	@SESSION_ID		VARCHAR(15),
	@KRA_TYPE		VARCHAR(5),
	@KRA_DATA_TYPE		VARCHAR(10),
	@UNAME			VARCHAR(15)='',
	@BATCHDATE		VARCHAR(15)=''
	)

	AS

	--- EXEC RPT_KRA_DATA_UPLOAD '658700800','CDSL','COM','SWAPNIL'
	--- EXEC RPT_KRA_DATA_UPLOAD '658700800_001','CDSL','ADD','SWAPNIL','batchno_252'

	DECLARE @BATCH_DATE VARCHAR(11)
	DECLARE @BATCH_NO	VARCHAR(10)

	IF (LEFT(@BATCHDATE,7) = 'BATCHNO')
	BEGIN
		SET @BATCH_DATE = CONVERT(VARCHAR,GETDATE(),112)
		SET @BATCH_NO	= DBO.PIECE(@BATCHDATE,'_',2)
	END
	ELSE
	BEGIN
		SET @BATCH_DATE = LEFT(@BATCHDATE,8)
		SET @BATCH_NO	= RIGHT(@BATCHDATE,(LEN(@BATCHDATE)-LEN(@BATCH_DATE)))
	END

	IF @SESSION_ID <> ''
	BEGIN
		IF @KRA_TYPE = 'NSDL'
		BEGIN
			INSERT INTO TBL_NSDL_KRA_DATA
			SELECT
				RECORD_TYPE			= LEFT(FILE_DATA,2),
				BATCH_NO			= SUBSTRING(FILE_DATA,3,8),
				LINE_NO				= SUBSTRING(FILE_DATA,11,7),
				PAN_NO				= SUBSTRING(FILE_DATA,18,10),
				INSTRUCTION_ID		= SUBSTRING(FILE_DATA,28,14),	
				TRANSACTION_FLAG	= SUBSTRING(FILE_DATA,42,1),	
				ERROR_CODE			= SUBSTRING(FILE_DATA,43,7),
				SENDER_REFERENCE1	= SUBSTRING(FILE_DATA,50,35),
				SENDER_REFERENCE2	= SUBSTRING(FILE_DATA,85,35),
				INSTRUCTION_STATUS	= (
				CASE 
					WHEN SUBSTRING(FILE_DATA,120,2) IN ('00','01') THEN SUBSTRING(FILE_DATA,120,2)
					ELSE SUBSTRING(FILE_DATA,120,56)
				END),
				ACKNOWLEDGMENT_NO	= (
				CASE 
					WHEN SUBSTRING(FILE_DATA,120,2) IN ('00','01') THEN SUBSTRING(FILE_DATA,122,16)
					ELSE ''
				END),
				FILLER				= (
				CASE 
					WHEN SUBSTRING(FILE_DATA,120,2) IN ('00','01') THEN SUBSTRING(FILE_DATA,138,38)
					ELSE ''
				END),
				UPLOADBY			= @UNAME,
				UPLOADON			= GETDATE()
			FROM
				CLASSFILEUPD
			WHERE 
				SESSION_ID = @SESSION_ID
				AND LEFT(FILE_DATA,2) IN ('02','04')
				AND SUBSTRING(FILE_DATA,3,8) NOT IN (SELECT DISTINCT BATCH_NO FROM TBL_NSDL_KRA_DATA)


			INSERT INTO TBL_KRA_LOG
			SELECT
				'NSDL',
				'',
				SUBSTRING(FILE_DATA,18,10),
				SUBSTRING(FILE_DATA,42,1),
				SUBSTRING(FILE_DATA,3,8),
				GETDATE(),
				'UPLOAD',
				'COMMON',
				SUBSTRING(FILE_DATA,120,2),
				GETDATE(),
				SUBSTRING(FILE_DATA,43,7),
				'',
				GETDATE(),
				@UNAME,
				GETDATE(),
				1
			FROM
				CLASSFILEUPD
			WHERE 
				SESSION_ID = @SESSION_ID

			DELETE FROM CLASSFILEUPD WHERE SESSION_ID = @SESSION_ID

		END

		IF @KRA_TYPE = 'CDSL' AND @KRA_DATA_TYPE = 'COM'
		BEGIN

			DELETE
			FROM
				TBL_KRA_DATA
			WHERE 
				APP_INTERNAL_REF IN 
				(
				SELECT 
					APP_INTERNAL_REF
				FROM
					TBL_KRA_DATA_TMP (NOLOCK)
				GROUP BY
					APP_INTERNAL_REF
				)

			INSERT INTO TBL_KRA_DATA
			SELECT *
			FROM
				TBL_KRA_DATA_TMP (NOLOCK)

			INSERT INTO TBL_KRA_LOG
			SELECT
				'CDSL',
				APP_INTERNAL_REF,
				APP_PAN_NO,
				APP_UPDTFLG,
				@BATCH_NO,
				CONVERT(VARCHAR,CONVERT(DATETIME,@BATCH_DATE,112),109),
				'UPLOAD',
				'COMMON',
				APP_STATUS,
				APP_STATUSDT,
				APP_ERROR_DESC,
				APP_DUMP_TYPE,
				APP_DNLDDT,
				@UNAME,
				GETDATE(),
				1
			FROM
				TBL_KRA_DATA_TMP (NOLOCK)

			TRUNCATE TABLE TBL_KRA_DATA_TMP

		END


		IF @KRA_TYPE = 'CDSL' AND @KRA_DATA_TYPE = 'ADD'
		BEGIN

			DELETE
			FROM
				TBL_KRA_DATA_ADDITIONAL
			WHERE 
				APP_ENTITY_PAN IN 
				(
				SELECT 
					APP_ENTITY_PAN
				FROM
					TBL_KRA_DATA_ADDITIONAL_TMP
				GROUP BY
					APP_ENTITY_PAN
				)

			INSERT INTO TBL_KRA_DATA_ADDITIONAL
			SELECT *
			FROM
				TBL_KRA_DATA_ADDITIONAL_TMP (NOLOCK)

			INSERT INTO TBL_KRA_LOG
			SELECT 
				'CDSL',
				'',
				APP_ENTITY_PAN,
				APP_ADDLDATA_UPDTFLG,
				@BATCH_NO,
				CONVERT(VARCHAR,CONVERT(DATETIME,@BATCH_DATE,112),109),
				'UPLOAD',
				'ADDITIONAL',
				APP_STATUS,
				APP_ADDLDATA_STATUSDT,
				APP_ERROR_DESC,
				APP_DUMP_TYPE,
				APP_DNLDDT,
				@UNAME,
				GETDATE(),
				1
			FROM
				TBL_KRA_DATA_ADDITIONAL_TMP (NOLOCK)

			TRUNCATE TABLE TBL_KRA_DATA_ADDITIONAL_TMP

		END

	END

GO
