-- Object: PROCEDURE dbo.PROC_MARGIN_DELDATA_BKUP_02SEP2020
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[PROC_MARGIN_DELDATA_BKUP_02SEP2020] (@SAUDA_DATE VARCHAR(11), @FROMPARTY VARCHAR(10), @TOPARTY VARCHAR(10))              
AS           

-- EXEC PROC_MARGIN_DELDATA 'AUG 24 2020','0','ZZZZZZZZZZ'
DELETE FROM TBL_PAYIN_DATA
WHERE TRANSDATE = @SAUDA_DATE
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
AND RECORD_TYPE = 'PAYIN'

SELECT D.* INTO #DEL FROM ANGELDEMAT.MSAJAG.DBO.DELTRANS D, ANGELDEMAT.MSAJAG.DBO.SETT_MST S
WHERE D.SETT_NO = S.SETT_NO AND D.SETT_TYPE = S.SETT_TYPE
AND @SAUDA_DATE BETWEEN START_DATE AND SEC_PAYIN

INSERT INTO TBL_PAYIN_DATA
SELECT RECORD_TYPE = 'PAYIN', D.SETT_NO, D.SETT_TYPE, TRANSDATE, PARTY_CODE, SCRIP_CD, D.SERIES, EPNQTY = SUM(QTY),
EPN_STATUS='S',NONEPNQTY=0,AVVGRATE=0,OPENQTY=0,SQOFFAMT=0,NETAMT=0,MARGIN_AMT=0,
IPLOAD_BY='',UPLOAD_ON=GETDATE()      
FROM #DEL D (NOLOCK), MSAJAG.DBO.SETT_MST S    
WHERE TRANSDATE = @SAUDA_DATE
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
AND TRANSDATE BETWEEN START_DATE AND SEC_PAYIN
AND D.SETT_NO = S.SETT_NO AND D.SETT_TYPE = S.SETT_TYPE        
AND D.DRCR = 'C' AND FILLER2 = 1 AND SHARETYPE <> 'AUCTION'     
AND PARTY_CODE <> 'BROKER' AND TRANSDATE < SEC_PAYIN      
AND TRTYPE <> 906 AND @SAUDA_DATE <> LEFT(SEC_PAYIN,11)    
AND TCODE IN (SELECT TCODE FROM #DEL D1    
				  WHERE D1.SETT_NO = D.SETT_NO AND D1.SETT_TYPE = D.SETT_TYPE    
				  AND D1.CERTNO = D.CERTNO AND D1.DRCR <> D.DRCR AND D1.TRTYPE = 906 AND D1.FILLER1 = 'EARLY PAYIN')    
GROUP BY D.SETT_NO, D.SETT_TYPE, TRANSDATE, PARTY_CODE, D.SCRIP_CD, D.SERIES, CERTNO      

INSERT INTO TBL_PAYIN_DATA
SELECT RECORD_TYPE = 'PAYIN', D.SETT_NO, D.SETT_TYPE, TRANSDATE, PARTY_CODE, SCRIP_CD, D.SERIES, EPNQTY = 0,
EPN_STATUS='S',NONEPNQTY=SUM(QTY),AVVGRATE=0,OPENQTY=0,SQOFFAMT=0,NETAMT=0,MARGIN_AMT=0,
IPLOAD_BY='',UPLOAD_ON=GETDATE()      
FROM #DEL D (NOLOCK), MSAJAG.DBO.SETT_MST S    
WHERE TRANSDATE = @SAUDA_DATE
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY 
AND TRANSDATE BETWEEN START_DATE AND SEC_PAYIN
AND D.SETT_NO = S.SETT_NO AND D.SETT_TYPE = S.SETT_TYPE        
AND D.DRCR = 'C' AND FILLER2 = 1 AND SHARETYPE <> 'AUCTION'     
AND PARTY_CODE <> 'BROKER' AND TRANSDATE < SEC_PAYIN      
AND TRTYPE <> 906 AND @SAUDA_DATE <> LEFT(SEC_PAYIN,11)    
AND TCODE NOT IN (SELECT TCODE FROM #DEL D1    
				  WHERE D1.SETT_NO = D.SETT_NO AND D1.SETT_TYPE = D.SETT_TYPE    
				  AND D1.CERTNO = D.CERTNO AND D1.DRCR <> D.DRCR AND D1.TRTYPE = 906 AND D1.FILLER1 = 'EARLY PAYIN')    
GROUP BY D.SETT_NO, D.SETT_TYPE, TRANSDATE, PARTY_CODE, D.SCRIP_CD, D.SERIES, CERTNO

GO
