CREATE PROC [dbo].[CLS_GET_FILE_DATA]
(
	@TYPE VARCHAR(20), 
	@FILEFOR VARCHAR(25),
	@DEF_FILEFOR VARCHAR(25) = ''
)

AS
/*
	CLS_GET_FILE_DATA 'FOR',''
	MSAJAG..CLS_GET_FILE_DATA 'FOR','','ONMARKET_UPLOAD'
*/
IF @TYPE = ''
	BEGIN 
		SELECT * FROM CLS_FILEUPLOAD_SETTING WHERE FILEFOR = @FILEFOR AND FILETYPE = @DEF_FILEFOR
	END
ELSE IF @TYPE = 'TYPE'
	BEGIN 
	
		SELECT UPPER(FILETYPE) AS FILETYPE ,FILENAME,PROCNAME,FILEFOR,PROCESS_NAME,PROCESS_REDIRECT,ERROR_ACTION,UPLOAD_PARAMETERS,FILE_NAMING_CONVENSION,AUTO,BULK_UPLOAD FROM CLS_FILEUPLOAD_SETTING WHERE FILEFOR = @FILEFOR
	END
ELSE
	BEGIN 
		IF LEN(@DEF_FILEFOR) > 0
			BEGIN
				SELECT DISTINCT FILEFOR, FILEDATE=CONVERT(VARCHAR,GETDATE(),103), TDATETIME = REPLACE(CONVERT(VARCHAR,GETDATE(),108),':','') FROM CLS_FILEUPLOAD_SETTING 
				WHERE REPORTCODE = @DEF_FILEFOR ORDER BY FILEFOR
			END
		ELSE
			BEGIN
				SELECT DISTINCT FILEFOR, FILEDATE=CONVERT(VARCHAR,GETDATE(),103), TDATETIME = REPLACE(CONVERT(VARCHAR,GETDATE(),108),':','') FROM CLS_FILEUPLOAD_SETTING 
				ORDER BY FILEFOR
			END
	END

GO
