----From Client	To client	From date	Todate	From Scrip	To scrip	Drop Down ( NSE / BSE/ FO)	DROP DOWN ( DETAIL / SUMMARY)


CREATE PROCEDURE [dbo].[GLOBAL_DATA_TEST_ver1]
(

@FROMPARTY VARCHAR(20),
@TOPARTY VARCHAR(20),
@FROMDATE DATETIME,
@TODATE DATETIME,
@FROMSCRIP VARCHAR(20),
@TOSCRIP VARCHAR(20),
@CONSOL VARCHAR(10),   ---10  CONSOL INDICATES THAT WHETHER IS "NSE","BSE","NSEFO"
@DATATYPE VARCHAR(10)
)

AS 
BEGIN 
IF @CONSOL = 'NSE'

BEGIN  

 IF @DATATYPE = 'D' 
                    

 BEGIN 

        SELECT 
party_code,party_name,SAUDA_DATE = LEFT(CONVERT(VARCHAR,SAUDA_DATE,109),11),(sett_no + sett_type)as SETT_DETAILS,SCRIP_CD,SCRIP_NAME,ISIN,
PTRADEDQTY = SUM(PQTYTRD + PQTYDEL),
sum(PAMTTRD+PAMTDEL/PQTYTRD+PQTYDEL)as PAvg_rate ,
sum(PAMTTRD+PAMTDEL)as purchase_value,
STRADEDQTY = SUM(SQTYTRD + SQTYDEL),
sum(SAMTTRD+SAMTDEL/SQTYTRD+SQTYDEL)as SAvg_rate ,
(PQTYTRD-SQTYTRD)as OPEN_QTY,
sum(PAMTTRD+PAMTDEL/PQTYTRD+PQTYDEL)-sum(SAMTTRD+SAMTDEL/SQTYTRD+SQTYDEL) AS AVERAGE_AMOUNT,
SUM(PAMTTRD-SAMTTRD)AS TRADING_PNL,
SUM(PAMTDEL-SAMTDEL)AS DELIVERY_PNL,
SUM(PAMTTRD-SAMTTRD)+SUM(PAMTDEL-SAMTDEL)AS TOTAL_VALUE,
TOTALBROKRAGE =SUM(PBrokTrd+SBrokTrd+PBrokDel+SBrokDel),
GST= SUM(SERVICE_TAX),
SUM(INS_CHRG) AS STT,
OTHER_TAX=SUM((TURN_TAX)+(SEBI_TAX)+(INS_CHRG)+(BROKER_CHRG))
,'NSE'AS EXCHANGE

 FROM CMBILLVALAN WITH(NOLOCK) WHERE  
 SAUDA_DATE BETWEEN @FROMDATE AND @TODATE 
  AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
  AND SCRIP_CD BETWEEN @FROMSCRIP AND @TOSCRIP
  ---AND @DATATYPE='DETAIL'
  
   
 group by party_code,party_name,sauda_date,sett_no,sett_type,SCRIP_CD,SCRIP_NAME,ISIN,PQTYTRD,SQTYTRD

END 
END 

ELSE IF @CONSOL = 'NSE'
BEGIN  

 IF @DATATYPE = 'S' 

