CREATE PROC [dbo].[DISCOUNTBROKERAGE_17102016]  
AS   
  
  
SELECT B2C_SB INTO #BTWOB FROM [196.1.115.167].REMISIOR.DBO.B2C_SB WITH(NOLOCK)  
WHERE ISNULL(B2C_SB,'') not in (select DISTINCT SUB_BROKER from TBL_ADDITIONAL_BROEKRAGE WHERE SUB_BROKER <>'')   
AND ISNULL(B2C_SB,'')  NOT IN ('AMRVT')  
  
  
INSERT INTO TBL_ADDITIONAL_BROEKRAGE  
SELECT  
'' BRANCH_CD,'' SUB_BROKER,CD.CL_CODE PARTY_CODE,20.00 TRD_BROK_VAL,20.00 DEL_BROK_VAL,CONVERT(DATETIME,(CONVERT(vARCHAR(11),GETDATE(),120) )) FROMDATE,  
'2049-12-31 23:59:59:000' TODATE,'AUTO' ADDED_BY,  
GETDATE() ADDED_ON,'' MODIFIED_BY,GETDATE() MODIFIED_ON   
FROM CLIENT1 CD WITH(NOLOCK) ,CLIENT5 AS C5,#BTWOB B  
WHERE CD.SUB_BROKER=B.B2C_SB AND CD.CL_CODE=C5.CL_CODE AND ACTIVEFROM >=getdate()-5 and ACTIVEFROM <=getdate()  
AND CD.CL_CODE NOT IN (SELECT PARTY_CODE FROM TBL_ADDITIONAL_BROEKRAGE)   
/** Added For remove discount brokerage for custom clients */   
AND CD.CL_CODE NOT IN (SELECT PARTY_CODE FROM [196.1.115.167].KYC_CI.DBO.Vi_GetCustomBrokerageDetails) AND CD.CL_CODE='S167898'

GO
