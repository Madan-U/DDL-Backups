CREATE proc ADDITIONAL_BROKERAGE  (
@FROMDATE VARCHAR (11),
@todate   VARCHAR (11),
@PARTYCODE VARCHAR (10)
)
AS BEGIN 

SELECT * FROM TBL_ADDITIONAL_BROEKRAGE WHERE PARTY_CODE=@PARTYCODE or
 SUB_BROKER='@PARTYCODE'
AND FROMDATE>=@FROMDATE +' 00:00:00.001' and TODATE <=@todate +' 23:59:59:999'
END

GO
