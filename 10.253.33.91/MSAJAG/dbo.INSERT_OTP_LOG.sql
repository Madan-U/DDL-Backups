CREATE PROC [dbo].[INSERT_OTP_LOG]
(
	@OTP_API VARCHAR(10),
	@OTP_USER VARCHAR(10),
	@LOGIN VARCHAR(50)
)
AS
	DECLARE @DATE AS DATETIME
	SET @DATE = GETDATE()

INSERT INTO tblOTP_LOG VALUES(@OTP_API, @OTP_USER,@LOGIN,@DATE,'')

IF(SELECT COUNT(*) FROM tblOTP_VERIFY WHERE LoginID = @LOGIN) = 0
 BEGIN
	INSERT INTO tblOTP_VERIFY VALUES(@OTP_API, @OTP_USER,@LOGIN,@DATE)
 END
ELSE
 BEGIN
	UPDATE tblOTP_VERIFY SET OTP_API = @OTP_API, OTP_USER = '',SendTime = @DATE WHERE LoginID = @LOGIN
 END

GO
