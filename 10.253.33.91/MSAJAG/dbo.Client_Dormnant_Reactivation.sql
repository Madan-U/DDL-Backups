-- Object: PROCEDURE dbo.Client_Dormnant_Reactivation
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

CREATE PROC [dbo].[Client_Dormnant_Reactivation] (@CL_CODE VARCHAR(11))    
AS    
    
DECLARE @CNT INT    
    
SELECT @CNT=COUNT(1)  FROM CLIENT_BROK_DETAILS C    
WHERE CL_CODE =@CL_CODE AND DEACTIVE_VALUE='D'    
    
IF (@CNT=0)    
BEGIN     
    
SELECT REMARKS ='CLIENT NOT A DORMANT'    
    
END    
ELSE    
BEGIN     
INSERT INTO DORMANT_REACTIVATION     
SELECT @CL_CODE,GETDATE()    
    
UPDATE C SET INACTIVE_FROM='2049-12-31 00:00:00.000',MODIFIEDON=GETDATE(),MODIFIEDBY='REKYC',
DEACTIVE_REMARKS='',DEACTIVE_VALUE='R',IMP_STATUS='0' FROM CLIENT_BROK_DETAILS C    
WITH(NOLOCK)
WHERE CL_CODE =@CL_CODE AND DEACTIVE_VALUE='D'    

SELECT REMARKS = 'SUCCESS' 
    
END

GO
