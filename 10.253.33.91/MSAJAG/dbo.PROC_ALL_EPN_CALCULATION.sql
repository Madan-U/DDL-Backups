CREATE PROCEDURE [dbo].[PROC_ALL_EPN_CALCULATION] (@RPT_DATE VARCHAR(11),@REPORT VARCHAR(10))

AS ---- PROC_ALL_EPN_CALCULATION 'SEP 22 2022',''

-- SELECT TOP 10  * FROM TBL_ALL_EPN_DATA

CREATE TABLE #HOLD (SCRIP_CD VARCHAR(500),SERIES VARCHAR(3) NOT NULL,PARTY_CODE VARCHAR(50),SETT_NO VARCHAR(7) NOT NULL,
SETT_TYPE VARCHAR(2) NOT NULL,QTY NUMERIC(18,3),CERTNO VARCHAR(16),BDPID VARCHAR(16),BCLTDPID VARCHAR(16),HOLDFLG VARCHAR(20))

INSERT INTO #HOLD
SELECT SCRIP_CD, SERIES ,D.PARTY_CODE,SETT_NO, SETT_TYPE, QTY=SUM(QTY),
CERTNO,BDPID,BCLTDPID,HOLDFLG=CONVERT(VARCHAR(20),'OTHER')         
FROM ANGELDEMAT.MSAJAG.dbo.DELTRANS D WITH(NOLOCK), ANGELDEMAT.MSAJAG.dbo.DELIVERYDP P WITH(NOLOCK)     
WHERE D.BDPID = P.DPID  
AND D.BCLTDPID = P.DPCLTNO                 
AND DRCR = 'D' AND FILLER2 = 1  AND DELIVERED = '0'         
AND D.PARTY_CODE <> 'BROKER' AND TRTYPE <> 906 AND CERTNO NOT LIKE 'AUCTION'          
GROUP BY SCRIP_CD,CERTNO, SERIES, D.PARTY_CODE,SETT_NO, SETT_TYPE,BDPID,BCLTDPID HAVING SUM(QTY) > 0
ORDER BY D.PARTY_CODE,SCRIP_CD,SERIES,SETT_NO, SETT_TYPE

/*  ---Free Holding from DP BO
INSERT INTO #HOLD
SELECT SECURITYNAME,SERIES='',TRADINGID,SETT_NO='',SETT_TYPE='',FREE_QTY,
HLD_ISIN_CODE,DPID='12033200',HLD_AC_CODE,TYPHOLD='FREE' 
FROM AngelDP4.DMAT.CITRUS_USR.HOLDING WHERE FREE_QTY <> 0 AND TRADINGID <> ''
*/

INSERT INTO #HOLD
SELECT SECURITYNAME='',SERIES='',B.PARTY_CODE,SETT_NO='',SETT_TYPE='',FREE_BALANCE,ISIN,DPID='12033200',BO_ID,TYPHOLD='FREE' 
FROM ANGELDEMAT.INHOUSE.DBO.DP_HOLDING_BALANCE A WITH (NOLOCK), ANGELDEMAT.MSAJAG.DBO.MULTICLTID B WITH (NOLOCK)
WHERE A.BO_ID = B.CLTDPNO AND FREE_BALANCE <> 0
AND FILE_DATE IN (SELECT MAX(FILE_DATE) FROM ANGELDEMAT.INHOUSE.DBO.DP_HOLDING_BALANCE B WHERE A.BO_ID = B.BO_ID AND A.ISIN = B.ISIN ) 


UPDATE #HOLD SET HOLDFLG='CMP' WHERE BCLTDPID='1203320030135814'
UPDATE #HOLD SET HOLDFLG='SBD' WHERE BCLTDPID='1203320000072218'
UPDATE #HOLD SET HOLDFLG='CUSA' WHERE BCLTDPID='1203320018512051'
UPDATE #HOLD SET HOLDFLG='FOCOL' WHERE BCLTDPID='1203320018512904'
UPDATE #HOLD SET HOLDFLG='MTF' WHERE BCLTDPID='1203320030135829'

SELECT PARTY_CODE,CERTNO,[FREE]=ISNULL([FREE],0),[CMP]=ISNULL([CMP],0),[MTF]=ISNULL([MTF],0), 
[CUSA]=ISNULL([CUSA],0),[SBD]=ISNULL([SBD],0),[FOCOL]=ISNULL([FOCOL],0),
[OTHER]=ISNULL([OTHER],0),TOTAL_HOLDING=ISNULL([FREE],0)+ISNULL([CMP],0)+ISNULL([MTF],0)
+ISNULL([CUSA],0)+ISNULL([SBD],0)+ISNULL([FOCOL],0)+ISNULL([OTHER],0) INTO #TOTHOLD FROM (
SELECT PARTY_CODE,CERTNO,QTY,HOLDFLG FROM #HOLD) AS SourceTable
PIVOT
(SUM(QTY) FOR HOLDFLG IN ([FREE],[CMP],[MTF],[CUSA],[SBD],[FOCOL],[OTHER])) AS PivotTable


UPDATE TBL_ALL_EPN_DATA SET ISIN = A.ISIN FROM MULTIISIN A 
WHERE TBL_ALL_EPN_DATA.SYMBOL_NSE = A.SCRIP_CD AND EPN_DATE = @RPT_DATE

SELECT SCRIP_CD,ISIN INTO #BSE FROM CLOSING_MTM B WITH (NOLOCK) 
WHERE SYSDATE IN (SELECT MAX(SYSDATE) FROM CLOSING_MTM C WITH (NOLOCK) WHERE B.ISIN = C.ISIN)

UPDATE A SET ISIN = B.ISIN FROM TBL_ALL_EPN_DATA A, #BSE B WHERE A.SYMBOL_BSE = B.SCRIP_CD AND A.ISIN=''

UPDATE TBL_ALL_EPN_DATA SET TOTAL_HOLDING = A.TOTAL_HOLDING FROM #TOTHOLD A 
WHERE TBL_ALL_EPN_DATA.CLTCODE = A.PARTY_CODE AND TBL_ALL_EPN_DATA.ISIN = A.CERTNO AND EPN_DATE = @RPT_DATE

SELECT CLTCODE,ISIN,(SELECT MIN(NSECM) FROM ( VALUES (NET_SELL_QTY),(TOTAL_HOLDING)) AS NSECM (NSECM)) AS MQTY INTO #MQTY
FROM TBL_ALL_EPN_DATA

UPDATE A SET MIN_QTY = B.MQTY FROM TBL_ALL_EPN_DATA A,
(SELECT CLT=CLTCODE,ISN=ISIN,MQTY FROM #MQTY) B WHERE A.CLTCODE = B.CLT AND A.ISIN = B.ISN

UPDATE TBL_ALL_EPN_DATA SET MIN_QTY = NET_SELL_QTY WHERE NET_SELL_QTY = TOTAL_HOLDING AND MIN_QTY=0 AND EPN_DATE = @RPT_DATE

UPDATE TBL_ALL_EPN_DATA SET EPN_VALUE = MIN_QTY * SELL_AVG_PRICE WHERE EPN_DATE = @RPT_DATE

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=2 AND REPORT_DATE = @RPT_DATE

SELECT 'EPN Calculation Process Completed Successfully'

GO
