-- Object: PROCEDURE dbo.ALLOC_RECO_ANG_CHK
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

  
        
CREATE PROC [dbo].[ALLOC_RECO_ANG_CHK]( @PROCESSDT VARCHAR(11), @PARTY_CODE VARCHAR(10) = '')                
AS                
                
DECLARE @RECODT VARCHAR(11)                 
                
SELECT DISTINCT PROCESS_DT,PARTY_CODE,EXCHANGE,SEGMENT,ALLOCATECASH 
INTO #TBL_CLIENT_COLL_ALLOCATE                
FROM TBL_CLIENT_COLL_ALLOCATE WITH (NOLOCK) WHERE  PROCESS_DT= @PROCESSDT   
AND PARTY_CODE = CASE WHEN @PARTY_CODE <> '' THEN @PARTY_CODE ELSE PARTY_CODE END    

SELECT DISTINCT PROCESS_DT,EXCHANGE,SEGMENT INTO #TBL_CLIENT_COLL_ALLOCATE_DT FROM #TBL_CLIENT_COLL_ALLOCATE                

                
SELECT DISTINCT MAX(TRANSDATE) AS TRANSDATE,B.EXCHANGE ,B.SEGMENT 
INTO #TBL_CLIENT_COLL_RECO_DT                 
FROM TBL_CLIENT_COLL_RECO B   WITH (NOLOCK), #TBL_CLIENT_COLL_ALLOCATE_DT A                
WHERE  TRANSDATE <= A.PROCESS_DT  AND A.EXCHANGE = B.EXCHANGE AND   A.SEGMENT = B.SEGMENT   
AND CLIENTCODE = CASE WHEN @PARTY_CODE <> '' THEN @PARTY_CODE ELSE CLIENTCODE END                      
GROUP BY B.EXCHANGE,B.SEGMENT     
  
select * from #TBL_CLIENT_COLL_RECO_DT

DROP TABLE #TBL_CLIENT_COLL_ALLOCATE_DT                
                
SELECT DISTINCT A.TRANSDATE,CLIENTCODE,A.EXCHANGE,A.SEGMENT,AMOUNT INTO #TBL_CLIENT_COLL_RECO                 
FROM TBL_CLIENT_COLL_RECO A  WITH (NOLOCK), #TBL_CLIENT_COLL_RECO_DT B WHERE A.TRANSDATE = B.TRANSDATE AND CLIENTCODE = CASE WHEN @PARTY_CODE <> '' THEN @PARTY_CODE ELSE CLIENTCODE END                                    
AND A.EXCHANGE = B.EXCHANGE AND   A.SEGMENT = B.SEGMENT                 
                
DROP TABLE #TBL_CLIENT_COLL_RECO_DT                
                
SELECT A.TRANSDATE AS PREVDATE,C.PROCESS_DT AS CURRDATE,C.PARTY_CODE,C.EXCHANGE,C.SEGMENT,A.AMOUNT AS PREVALLOC,ALLOCATECASH AS CURRALLOC,                
DIFF = C.ALLOCATECASH - A.AMOUNT ,                
ABS(CASE WHEN A.EXCHANGE = 'MCX' THEN C.ALLOCATECASH - A.AMOUNT ELSE C.ALLOCATECASH END) AS EXPECTED_VALUE,                
CASE WHEN A.EXCHANGE = 'MCX' THEN                
    CASE WHEN C.ALLOCATECASH - A.AMOUNT > 0 THEN 'A' ELSE 'D' END                  
    ELSE                 
    CASE WHEN C.ALLOCATECASH - A.AMOUNT > 0 THEN 'U' ELSE 'D' END END                 
    AS EXPECTED_FLAG                 
INTO #TBL_CLIENT_COLL_RECO_FINAL                
FROM #TBL_CLIENT_COLL_RECO A WITH (NOLOCK) , #TBL_CLIENT_COLL_ALLOCATE C                
WHERE  A.SEGMENT = C.SEGMENT AND  A.EXCHANGE = C.EXCHANGE AND PARTY_CODE = CLIENTCODE                  
                
                
INSERT INTO #TBL_CLIENT_COLL_RECO_FINAL  WITH (TABLOCK)              
SELECT '' AS PREVDATE,PROCESS_DT AS CURRDATE,PARTY_CODE,EXCHANGE,SEGMENT,AMOUNT = 0,ALLOCATECASH AS CURRALLOC,ALLOCATECASH AS DIFF,                
ALLOCATECASH AS EXPECTED_VALUE,                
CASE WHEN A.EXCHANGE = 'MCX' THEN 'A' ELSE 'U' END AS EXPECTED_FLAG                
FROM #TBL_CLIENT_COLL_ALLOCATE A WHERE  NOT EXISTS (                
SELECT * FROM #TBL_CLIENT_COLL_RECO_FINAL B WITH (NOLOCK) WHERE  A.EXCHANGE = B.EXCHANGE AND   A.SEGMENT = B.SEGMENT AND A.PARTY_CODE = B.PARTY_CODE)                
AND A.ALLOCATECASH <> 0 AND PARTY_CODE  <> ''                
                
                
INSERT INTO #TBL_CLIENT_COLL_RECO_FINAL   WITH (TABLOCK)              
SELECT TRANSDATE AS PREVDATE,'' AS CURRDATE,CLIENTCODE,EXCHANGE,SEGMENT,AMOUNT,0 AS CURRALLOC,-AMOUNT AS DIFF,                
ABS(CASE WHEN A.EXCHANGE = 'MCX' THEN                 
     CASE WHEN A.AMOUNT*-1 < 0 THEN A.AMOUNT ELSE 0 END                 
         ELSE 0 END) AS EXPECTED_VALUE,                
'D' AS EXPECTED_FLAG                
FROM #TBL_CLIENT_COLL_RECO A WHERE  NOT EXISTS (                
SELECT * FROM #TBL_CLIENT_COLL_RECO_FINAL B  WITH (NOLOCK) WHERE  A.EXCHANGE = B.EXCHANGE AND   A.SEGMENT = B.SEGMENT AND A.CLIENTCODE = B.PARTY_CODE)                
AND A.AMOUNT <> 0 AND CLIENTCODE  <> ''                
                
DROP TABLE #TBL_CLIENT_COLL_ALLOCATE                
DROP TABLE #TBL_CLIENT_COLL_RECO                
                
UPDATE #TBL_CLIENT_COLL_RECO_FINAL SET EXPECTED_FLAG = '' WHERE EXCHANGE = 'NCX'                
        
IF EXISTS(SELECT PREVDATE, CURRDATE, PARTY_CODE, EXCHANGE, SEGMENT,COUNT(1) AS CNT        
     FROM #TBL_CLIENT_COLL_RECO_FINAL         
     GROUP BY PREVDATE, CURRDATE, PARTY_CODE, EXCHANGE, SEGMENT        
     HAVING COUNT(1) > 1)        
BEGIN         
SELECT 'REGENERATE THE FILE AFTER REUPLOAD OF CC01'        
RETURN        
END        
        
SELECT * FROM #TBL_CLIENT_COLL_RECO_FINAL WHERE DIFF <> 0 AND PARTY_CODE <> '' AND PARTY_CODE NOT IN ('220','12798')           
          
DROP TABLE #TBL_CLIENT_COLL_RECO_FINAL

GO
