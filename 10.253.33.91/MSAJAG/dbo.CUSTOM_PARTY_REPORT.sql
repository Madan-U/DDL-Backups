CREATE PROCEDURE CUSTOM_PARTY_REPORT (@RUNDATE VARCHAR(11))

AS ---- EXEC CUSTOM_PARTY_REPORT 'MAR  7 2022'

SELECT CL_CODE,EXCHANGE,CLSTATUS=CASE WHEN INACTIVE_FROM > GETDATE() THEN 'Active' ELSE 'Inactive' END INTO #STATUS
FROM MSAJAG.DBO.CLIENT_BROK_DETAILS WITH (NOLOCK)
WHERE CL_CODE IN (SELECT LTRIM(RTRIM(PARTY_CODE)) FROM MSAJAG.DBO.IMPORT_PARTY_CODE) AND SEGMENT='CAPITAL'

SELECT SRNO=ROW_NUMBER() OVER (ORDER BY CL_CODE),TRADING_CODE=CL_CODE,DEMAT_ACCOUNT_NO=CONVERT(VARCHAR(50),''),POA_STATUS=CONVERT(VARCHAR(50),''),NSE_CASH=[NSE],BSE_CASH=[BSE] INTO #DATA
FROM (SELECT * FROM #STATUS ) AS SOURCETABLE
PIVOT
(MIN(CLSTATUS) FOR EXCHANGE IN ([NSE],[BSE])) AS PIVOTTABLE


UPDATE A SET DEMAT_ACCOUNT_NO = CASE WHEN DPTYPE='NSDL' THEN DPID+CLTDPNO ELSE CLTDPNO END ,POA_STATUS = DEF FROM #DATA A,
(SELECT M.PARTY_CODE,INTRODUCER,M.DPID,M.CLTDPNO,M.DPTYPE,DEF=(CASE WHEN DEF = 1 THEN 'RECEIVED' ELSE 'NOT RECEIVED' END),
ACTYPE='PAY_IN', DP.BANKNAME 
FROM MULTICLTID M (NOLOCK), CLIENT2 C2 (NOLOCK),
CLIENT1 C1 (NOLOCK), BANK DP (NOLOCK)
WHERE C1.CL_CODE = C2.CL_CODE AND M.PARTY_CODE = C2.PARTY_CODE AND M.DPID = DP.BANKID AND M.DPTYPE = DP.BANKTYPE) B
WHERE A.TRADING_CODE = B.PARTY_CODE

SELECT * FROM #DATA ORDER BY SRNO

GO
