		SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59'	
		AND CONTRACTNO BETWEEN @FROMCONTRACTNO AND @TOCONTRACTNO
		AND AUCTIONPART NOT IN ('AP','AR','FA','FC','FS','FL','FP')
		AND S.TRADEQTY > 0
		AND EXISTS (
			SELECT CL_CODE FROM CLIENT1 C1 (NOLOCK)
			WHERE C1.CL_CODE = S.PARTY_CODE
			AND (
			CASE
				WHEN @CONSOL = 'PARTY_CODE' THEN PARTY_CODE
				WHEN @CONSOL = 'FAMILY' THEN FAMILY
				WHEN @CONSOL = 'TRADER' THEN TRADER
				WHEN @CONSOL = 'SUB_BROKER' THEN SUB_BROKER
				WHEN @CONSOL = 'BRANCH_CD' THEN BRANCH_CD
				WHEN @CONSOL = 'REGION' THEN REGION
				WHEN @CONSOL = 'AREA' THEN AREA
				ELSE PARTY_CODE 
			END)  BETWEEN @FROMCODE AND @TOCODE
			AND @STATUSNAME = (
			CASE
				WHEN @STATUSID = 'BRANCH'	THEN C1.BRANCH_CD
				WHEN @STATUSID = 'SUBBROKER'THEN C1.SUB_BROKER
				WHEN @STATUSID = 'TRADER'	THEN C1.TRADER
				WHEN @STATUSID = 'FAMILY'	THEN C1.FAMILY
				WHEN @STATUSID = 'AREA'		THEN C1.AREA
				WHEN @STATUSID = 'REGION'	THEN C1.REGION
				WHEN @STATUSID = 'CLIENT'	THEN S.PARTY_CODE
				ELSE 'BROKER'
			END)
