CREATE PROCEDURE [dbo].[CLS_CONFIRMATION] 
	( 
	@FROMDATE VARCHAR(11),	
	@TODATE VARCHAR(11),
	@FROMCODE VARCHAR(20),
	@TOCODE VARCHAR (20),
	@CONSOL VARCHAR(10),
	@CL_TYPE VARCHAR(5),
	@FROMCONTRACTNO VARCHAR(10),
	@TOCONTRACTNO VARCHAR(10),
	@STATUSID VARCHAR(20),
	@STATUSNAME VARCHAR(25)
	)

