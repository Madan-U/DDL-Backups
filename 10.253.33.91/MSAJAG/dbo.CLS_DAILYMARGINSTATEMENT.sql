-- Object: PROCEDURE dbo.CLS_DAILYMARGINSTATEMENT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[CLS_DAILYMARGINSTATEMENT] (
	@MDATE VARCHAR(11),
	@FROMPARTY VARCHAR(15),
	@TOPARTY VARCHAR(15),
	@FROMBRANCH VARCHAR(15) = '',
	@TOBRANCH VARCHAR(15) = 'ZZZZZZZZ',
	@FROMSUBBROKER VARCHAR(15) = '',
	@TOSUBBROKER VARCHAR(15) = 'ZZZZZZZZ',
	@STATUSID VARCHAR(15),
	@STATUSNAME VARCHAR(25),
	@WITHCOLL INT = 0,
	@DCNFLAG INT = 0,
	@SEGMENT CHAR(1) = 'N',
	@SUM_DETAIL INT = 1
	)
AS

/*
EXEC CLS_DAILYMARGINSTATEMENT 'APR 13 2010', '0A141','0A141','','ZZZZZZZZZ','','ZZZZZZZZZ','BROKER','BROKER',1,1,'C',1
*/

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

IF (CHARINDEX('/', @MDATE) > 0)
BEGIN
	SELECT @MDATE = CONVERT(VARCHAR, CONVERT(DATETIME, @MDATE, 103), 109)
END

IF @TOBRANCH = ''
BEGIN
	SET @TOBRANCH = 'ZZZZZZZZ'
END

IF @TOSUBBROKER = ''
BEGIN
	SET @TOSUBBROKER = 'ZZZZZZZZ'
END

IF (@SUM_DETAIL = 1)
BEGIN
	SELECT 
		CLIENT_CODE = T.PARTY_CODE,
		CLIENT_NAME = C1.LONG_NAME
	FROM 
		TBL_MAR_REPORT T (NOLOCK),
		CLIENT_DETAILS C1 (NOLOCK)
	WHERE
		T.PARTY_CODE = C1.CL_CODE
		AND TRADE_DAY BETWEEN @MDATE AND @MDATE + ' 23:59:59'
		AND T.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
		AND C1.BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
		AND C1.SUB_BROKER BETWEEN @FROMSUBBROKER AND @TOSUBBROKER
		AND @STATUSNAME = (
		CASE   
			WHEN @STATUSID = 'BRANCH'		THEN C1.BRANCH_CD   
			WHEN @STATUSID = 'SUBBROKER'   	THEN C1.SUB_BROKER   
			WHEN @STATUSID = 'TRADER'   	THEN C1.TRADER   
			WHEN @STATUSID = 'FAMILY'   	THEN C1.FAMILY   
			WHEN @STATUSID = 'AREA'   		THEN C1.AREA   
			WHEN @STATUSID = 'REGION'   	THEN C1.REGION   
			WHEN @STATUSID = 'CLIENT'   	THEN CL_CODE   
			ELSE 'BROKER' 
		END)
		AND TOTALMARGIN <> 0
	GROUP BY
		T.PARTY_CODE,
		C1.LONG_NAME
	ORDER BY
		T.PARTY_CODE		
END
ELSE
BEGIN

	SELECT DISTINCT PARTY_CODE
	INTO #MCLIENT
	FROM TBL_MAR_REPORT(NOLOCK)
	WHERE TRADE_DAY BETWEEN @MDATE AND @MDATE + ' 23:59:59'
		AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY

	SELECT PARTY_CODE = CL_CODE,
		PARTYNAME = LONG_NAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_ZIP,
		L_STATE,
		L_NATION,
		RES_PHONE = RES_PHONE1 + RES_PHONE2,
		OFF_PHONE = OFF_PHONE1 + OFF_PHONE2,
		EMAIL,
		PAN_GIR_NO,
		BRANCH_CD,
		SUB_BROKER,
		TRADER
	INTO #CLIENTMASTER
	FROM CLIENT_DETAILS C1(NOLOCK)
	WHERE CL_CODE BETWEEN @FROMPARTY AND @TOPARTY
		AND C1.BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
		AND C1.SUB_BROKER BETWEEN @FROMSUBBROKER AND @TOSUBBROKER
		AND EXISTS (
			SELECT PARTY_CODE
			FROM #MCLIENT
			WHERE C1.CL_CODE = #MCLIENT.PARTY_CODE
			)
		AND @STATUSNAME = (
			CASE 
				WHEN @STATUSID = 'BRANCH'		THEN C1.BRANCH_CD
				WHEN @STATUSID = 'SUBBROKER'	THEN C1.SUB_BROKER
				WHEN @STATUSID = 'TRADER'		THEN C1.TRADER
				WHEN @STATUSID = 'FAMILY'		THEN C1.FAMILY
				WHEN @STATUSID = 'AREA'			THEN C1.AREA
				WHEN @STATUSID = 'REGION'		THEN C1.REGION
				WHEN @STATUSID = 'CLIENT'		THEN CL_CODE
				ELSE 'BROKER'
				END
			)

	CREATE TABLE #MARGIN_DETAIL (
		EXCHANGE VARCHAR(3),
		SEGMENT VARCHAR(7),
		PARTY_CODE VARCHAR(15),
		MARGINDATE DATETIME,
		LED_MARGIN_AMT MONEY,
		NONCASH_AMT MONEY,
		BGFD_AMT MONEY,
		OTHER_MARGIN MONEY,
		TOTAL_MARGIN_AVL MONEY,
		INITIALMARGIN MONEY,
		EXPOSURE_MARGIN MONEY,
		TOTAL_MARGIN MONEY,
		EXCESS_SHORTFALL MONEY,
		ADD_MARGIN MONEY,
		MARGIN_STATUS MONEY,
		MAR_PLEDGE_AMT NUMERIC(18,4),
		DELIVERY_MARGIN NUMERIC(18,4)
		)

	INSERT INTO #MARGIN_DETAIL
	SELECT EXCHANGE = T.EXCHANGE,
		SEGMENT = T.SEGMENT,
		PARTY_CODE = T.PARTY_CODE,
		MARGINDATE = LEFT(TRADE_DAY, 11),
		LED_MARGIN_AMT = SUM(CASH),
		NONCASH_AMT = SUM(NONCASH),
		BGFD_AMT = SUM(FD_BG),
		OTHER_MARGIN = SUM(ANY_OTHER_COLL),
		TOTAL_MARGIN_AVL = SUM(TOTALCOLL),
		INITIALMARGIN = ISNULL(SUM(INIT_MARGIN), 0),
		EXPOSURE_MARGIN = ISNULL(SUM(EXP_MARGIN), 0),
		TOTAL_MARGIN = ISNULL(SUM(TOTALMARGIN), 0),
		EXCESS_SHORTFALL = ISNULL(SUM(EXG_MAR_SHORT), 0),
		ADD_MARGIN = ISNULL(SUM(ADD_MARGIN), 0),
		MARGIN_STATUS = ISNULL(SUM(MARGIN_STATUS), 0),
		MAR_PLEDGE_AMT = ISNULL(SUM(MAR_PLEDGE_AMT), 0),
		DELIVERY_MARGIN = ISNULL(SUM(DELIVERY_MARGIN), 0)
	FROM TBL_MAR_REPORT T(NOLOCK),
		#CLIENTMASTER C(NOLOCK)
	WHERE TRADE_DAY BETWEEN @MDATE AND @MDATE + ' 23:59:59'
		AND T.PARTY_CODE = C.PARTY_CODE
	GROUP BY T.PARTY_CODE,
		LEFT(TRADE_DAY, 11),
		EXCHANGE,
		SEGMENT

	SELECT PARTY_CODE = M.PARTY_CODE,
		MARGINDATE = CONVERT(VARCHAR, MARGINDATE, 103),
		EXCHANGE,
		SEGMENT,
		LED_MARGIN_AMT = SUM(LED_MARGIN_AMT),
		NONCASH_AMT = SUM(NONCASH_AMT),
		BGFD_AMT = SUM(BGFD_AMT),
		OTHER_MARGIN = SUM(OTHER_MARGIN),
		TOTAL_MARGIN_AVL = SUM(TOTAL_MARGIN_AVL),
		INITIALMARGIN = SUM(INITIALMARGIN),
		EXPOSURE_MARGIN = SUM(EXPOSURE_MARGIN),
		TOTAL_MARGIN = SUM(TOTAL_MARGIN),
		EXCESS_SHORTFALL = SUM(EXCESS_SHORTFALL),
		ADD_MARGIN = SUM(ADD_MARGIN),
		MARGIN_STATUS = SUM(MARGIN_STATUS),
		PARTYNAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_ZIP,
		L_STATE,
		L_NATION,
		RES_PHONE,
		OFF_PHONE,
		EMAIL,
		PAN_GIR_NO,
		BRANCH_CD,
		SUB_BROKER,
		TRADER,
		RPT_ORD = '1DET',
		SCRIP_CD = CONVERT(VARCHAR(20), ''),
		SERIES = CONVERT(VARCHAR(5), ''),
		QTY = CONVERT(NUMERIC(18, 4), 0),
		SEC_CLRATE = CONVERT(NUMERIC(18, 4), 0),
		SEC_AMOUNT = CONVERT(NUMERIC(18, 4), 0),
		SEC_HAIRCUT = CONVERT(NUMERIC(18, 4), 0),
		SEC_FAMOUNT = CONVERT(NUMERIC(18, 4), 0),
		BGNO = CONVERT(VARCHAR(20), ''),
		BG_AMOUNT = CONVERT(NUMERIC(18, 4), 0),
		BG_EXPIRYDATE = CONVERT(VARCHAR(11), ''),
		FDRNO = CONVERT(VARCHAR(20), ''),
		FDR_AMOUNT = CONVERT(NUMERIC(18, 4), 0),
		FDR_EXPIRYDATE = CONVERT(VARCHAR(11), ''),
		SCRIPNAME = CONVERT(VARCHAR(100), ''),
		ISIN = CONVERT(VARCHAR(12), ''),
		MAR_PLEDGE_AMT = SUM(MAR_PLEDGE_AMT),
		DELIVERY_MARGIN = SUM(DELIVERY_MARGIN)
	INTO #FINAL_REPORT
	FROM #MARGIN_DETAIL M(NOLOCK),
		#CLIENTMASTER C(NOLOCK)
	WHERE M.PARTY_CODE = C.PARTY_CODE
	GROUP BY M.PARTY_CODE,
		CONVERT(VARCHAR, MARGINDATE, 103),
		EXCHANGE,
		SEGMENT,
		PARTYNAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_ZIP,
		L_STATE,
		L_NATION,
		RES_PHONE,
		OFF_PHONE,
		EMAIL,
		PAN_GIR_NO,
		BRANCH_CD,
		SUB_BROKER,
		TRADER
	/*
	HAVING 
		SUM(TOTAL_MARGIN) <> 0 --OR SUM(LED_MARGIN_AMT) <> 0	OR SUM(NONCASH_AMT) <> 0 OR SUM(BGFD_AMT) <> 0
		*/
	ORDER BY PARTY_CODE

	IF (@WITHCOLL = 1)
	BEGIN

		SELECT *
		INTO #COLLATERALDETAILS
		FROM MSAJAG.DBO.COLLATERALDETAILS C(NOLOCK)
		WHERE EFFDATE BETWEEN @MDATE AND @MDATE + ' 23:59:59'
			AND EXISTS (
				SELECT DISTINCT PARTY_CODE
				FROM #FINAL_REPORT
				WHERE C.PARTY_CODE = #FINAL_REPORT.PARTY_CODE
				)
	END

	IF (@WITHCOLL = 1)
	BEGIN
		SELECT PARTY_CODE,
			MARGINDATE,
			EXCHANGE,
			SEGMENT,
			SCRIP_CD,
			SERIES,
			QTY = SUM(QTY),
			SEC_CLRATE = SEC_CLRATE,
			SEC_AMOUNT = SUM(SEC_AMOUNT),
			SEC_HAIRCUT = SEC_HAIRCUT,
			SEC_FAMOUNT = SUM(SEC_FAMOUNT),
			BGNO,
			BG_AMOUNT = SUM(BG_AMOUNT),
			BG_EXPIRYDATE,
			FDRNO,
			FDR_AMOUNT = SUM(FDR_AMOUNT),
			FDR_EXPIRYDATE,
			SCRIPNAME = CONVERT(VARCHAR(100), ''),
			COLL_TYPE = '2' + UPPER(COLL_TYPE),
			ISIN
		INTO #COLL_DETAIL
		FROM (
			SELECT PARTY_CODE,
				MARGINDATE = CONVERT(VARCHAR, EFFDATE, 103),
				EXCHANGE,
				SEGMENT,
				SCRIP_CD = (
					CASE 
						WHEN COLL_TYPE = 'SEC'
							THEN SCRIP_CD
						ELSE ''
						END
					),
				SERIES,
				QTY = (
					CASE 
						WHEN COLL_TYPE = 'SEC'
							THEN QTY
						ELSE 0
						END
					),
				SEC_CLRATE = (
					CASE 
						WHEN COLL_TYPE = 'SEC'
							THEN CL_RATE
						ELSE 0
						END
					),
				SEC_AMOUNT = (
					CASE 
						WHEN COLL_TYPE = 'SEC'
							THEN AMOUNT
						ELSE 0
						END
					),
				SEC_HAIRCUT = (
					CASE 
						WHEN COLL_TYPE = 'SEC'
							THEN HAIRCUT
						ELSE 0
						END
					),
				SEC_FAMOUNT = (
					CASE 
						WHEN COLL_TYPE = 'SEC'
							THEN FINALAMOUNT
						ELSE 0
						END
					),
				BGNO = (
					CASE 
						WHEN COLL_TYPE = 'BG'
							THEN FD_BG_NO
						ELSE ''
						END
					),
				BG_AMOUNT = (
					CASE 
						WHEN COLL_TYPE = 'BG'
							THEN FINALAMOUNT
						ELSE 0
						END
					),
				BG_EXPIRYDATE = (
					CASE 
						WHEN COLL_TYPE = 'BG'
							THEN CONVERT(VARCHAR, MATURITY_DATE, 103)
						ELSE ''
						END
					),
				FDRNO = (
					CASE 
						WHEN COLL_TYPE = 'FD'
							THEN FD_BG_NO
						ELSE ''
						END
					),
				FDR_AMOUNT = (
					CASE 
						WHEN COLL_TYPE = 'FD'
							THEN FINALAMOUNT
						ELSE 0
						END
					),
				FDR_EXPIRYDATE = (
					CASE 
						WHEN COLL_TYPE = 'FD'
							THEN CONVERT(VARCHAR, MATURITY_DATE, 103)
						ELSE ''
						END
					),
				COLL_TYPE,
				ISIN
			FROM #COLLATERALDETAILS C(NOLOCK)
			WHERE PARTY_CODE IN (
					SELECT DISTINCT PARTY_CODE
					FROM #FINAL_REPORT
					)
				AND EFFDATE BETWEEN @MDATE AND @MDATE + ' 23:59:59'
				AND COLL_TYPE <> 'MARGIN'
			) COLL
		GROUP BY PARTY_CODE,
			MARGINDATE,
			SCRIP_CD,
			SERIES,
			SEC_CLRATE,
			SEC_HAIRCUT,
			BGNO,
			BG_EXPIRYDATE,
			FDRNO,
			FDR_EXPIRYDATE,
			UPPER(COLL_TYPE),
			EXCHANGE,
			SEGMENT,
			ISIN

/*
		INSERT INTO #COLL_DETAIL
		SELECT 
			PARTY_CODE,
			MARGINDATE = CONVERT(VARCHAR,TRADE_DAY,103),
			EXCHANGE,
			SEGMENT,
			SCRIP_CD,
			SERIES,
			QTY = SUM(QTY),
			SEC_CLRATE = NEWCL_RATE,
			SEC_AMOUNT = SUM(QTY*NEWCL_RATE),
			SEC_HAIRCUT = NEWHAIRCUT,
			SEC_FAMOUNT = SUM(QTY*NEWCL_RATE)-(SUM(QTY*NEWCL_RATE)*NEWHAIRCUT/100),
			BGNO = '',
			BG_AMOUNT = 0,
			BG_EXPIRYDATE = '',
			FDRNO = '',
			FDR_AMOUNT = 0,
			FDR_EXPIRYDATE = '',
			SCRIPNAME = '',
			COLL_TYPE = '2SEC',
			ISIN
		FROM
			TBL_COLL_DATA (NOLOCK)
		WHERE
			TRADE_DAY BETWEEN @MDATE AND @MDATE + ' 23:59:59'
			AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
			--AND (SEGMENT = 'POA' OR EXCHANGE = 'POA')		
			AND PARTY_CODE IN (
					SELECT DISTINCT PARTY_CODE
					FROM #FINAL_REPORT
					)
		GROUP BY
			PARTY_CODE,
			CONVERT(VARCHAR,TRADE_DAY,103),
			EXCHANGE,
			SEGMENT,
			SCRIP_CD,
			SERIES,
			NEWCL_RATE,
			NEWHAIRCUT,
			ISIN
*/
		/*
		UPDATE T
		SET T.SEC_AMOUNT = T1.AMOUNT,
			SEC_FAMOUNT = T1.FINALAMOUNT
		FROM #COLL_DETAIL T,
			NSEFO.DBO.TBL_COLLATERAL_MARGIN T1 WITH (NOLOCK)
		WHERE T.PARTY_CODE = T1.PARTY_CODE
			AND EXCHANGE = 'NSE'
			AND SEGMENT = 'FUTURES'
			AND T1.EFFDATE BETWEEN @MDATE
				AND @MDATE + ' 23:59:59'
			AND T.SCRIP_CD = T1.SCRIP_CD
			AND T1.COLL_TYPE = 'SEC'
		*/

		ALTER TABLE #FINAL_REPORT

		ALTER COLUMN RPT_ORD VARCHAR(7)

		INSERT INTO #FINAL_REPORT
		SELECT PARTY_CODE = M.PARTY_CODE,
			MARGINDATE,
			EXCHANGE,
			SEGMENT,
			LED_MARGIN_AMT = 0,
			NONCASH_AMT = 0,
			BGFD_AMT = 0,
			OTHER_MARGIN = 0,
			TOTAL_MARGIN_AVL = 0,
			INITIALMARGIN = 0,
			EXPOSURE_MARGIN = 0,
			TOTAL_MARGIN = 0,
			EXCESS_SHORTFALL = 0,
			ADD_MARGIN = 0,
			MARGIN_STATUS = 0,
			PARTYNAME,
			L_ADDRESS1,
			L_ADDRESS2,
			L_ADDRESS3,
			L_CITY,
			L_ZIP,
			L_STATE,
			L_NATION,
			RES_PHONE,
			OFF_PHONE,
			EMAIL,
			PAN_GIR_NO,
			BRANCH_CD,
			SUB_BROKER,
			TRADER,
			RPT_ORD = UPPER(COLL_TYPE),
			SCRIP_CD,
			SERIES,
			QTY,
			SEC_CLRATE,
			SEC_AMOUNT,
			SEC_HAIRCUT,
			SEC_FAMOUNT,
			BGNO,
			BG_AMOUNT,
			BG_EXPIRYDATE,
			FDRNO,
			FDR_AMOUNT,
			FDR_EXPIRYDATE,
			SCRIPNAME = SCRIP_CD,
			ISIN,
			MAR_PLEDGE_AMT = 0,
			DELIVERY_MARGIN = 0
		FROM #COLL_DETAIL M(NOLOCK),
			#CLIENTMASTER C(NOLOCK)
		WHERE M.PARTY_CODE = C.PARTY_CODE

	END

	UPDATE #FINAL_REPORT
	SET SCRIPNAME = ISNULL(S1.LONG_NAME, #FINAL_REPORT.SCRIP_CD)
	FROM SCRIP1 S1(NOLOCK),
		SCRIP2 S2(NOLOCK)
	WHERE S1.CO_CODE = S2.CO_CODE
		AND S1.SERIES = S2.SERIES
		AND #FINAL_REPORT.SCRIP_CD = S2.SCRIP_CD
		AND #FINAL_REPORT.SERIES = S2.SERIES
		AND #FINAL_REPORT.EXCHANGE = 'NSE'
		AND #FINAL_REPORT.RPT_ORD = '2SEC'

	SELECT CLIENT_CODE = UPPER(FI.PARTY_CODE),
		MARGINDATE,
		EXCHANGE = (
			CASE 
				WHEN SEGMENT = 'CAPITAL'
					THEN FI.EXCHANGE + '-CASH'
				WHEN SEGMENT = 'FUTURES'
					THEN FI.EXCHANGE + '-F&O'
				ELSE FI.EXCHANGE + '-' + SEGMENT
				END
			),
		SEGMENT,
		LED_MARGIN_AMT = SUM(LED_MARGIN_AMT),
		NONCASH_AMT = SUM(NONCASH_AMT),
		BGFD_AMT = SUM(BGFD_AMT),
		OTHER_MARGIN = SUM(OTHER_MARGIN),
		TOTAL_MARGIN_AVL = SUM(TOTAL_MARGIN_AVL),
		INITIALMARGIN = SUM(INITIALMARGIN),
		EXPOSURE_MARGIN = SUM(EXPOSURE_MARGIN),
		TOTAL_MARGIN = SUM(TOTAL_MARGIN),
		EXCESS_SHORTFALL = SUM(EXCESS_SHORTFALL),
		ADD_MARGIN = SUM(ADD_MARGIN),
		MARGIN_STATUS = SUM(MARGIN_STATUS),
		CLIENT_NAME = PARTYNAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_ZIP,
		L_STATE,
		L_NATION,
		RES_PHONE,
		OFF_PHONE,
		EMAIL,
		PAN_GIR_NO,
		BRANCH_CD,
		SUB_BROKER,
		TRADER,
		RPT_ORD,
		SCRIP_CD,
		SERIES,
		QTY = SUM(QTY),
		SEC_CLRATE,
		SEC_AMOUNT = SUM(SEC_AMOUNT),
		SEC_HAIRCUT = SUM(SEC_HAIRCUT),
		SEC_FAMOUNT = SUM(SEC_FAMOUNT),
		BGNO,
		BG_AMOUNT = SUM(BG_AMOUNT),
		BG_EXPIRYDATE,
		FDRNO,
		FDR_AMOUNT = SUM(FDR_AMOUNT),
		FDR_EXPIRYDATE,
		SCRIPNAME,
		ISIN,
		ROW_P_COUNT = CONVERT(NUMERIC(18, 0), 0),
		ROW_S_COUNT = CONVERT(NUMERIC(18, 0), 0),
		FILENAME = 'COMBINED_MARGIN_STATEMENT_'+ UPPER(LTRIM(RTRIM(FI.PARTY_CODE))) +'_'+ CONVERT(VARCHAR,CONVERT(DATETIME,MARGINDATE,103),112),
		MAR_PLEDGE_AMT = SUM(MAR_PLEDGE_AMT),
		DELIVERY_MARGIN = SUM(DELIVERY_MARGIN),
		TRADE_DATE = MARGINDATE,
		MARGIN_FLAG = 0
	INTO #FINAL_REPORTNEW
	FROM #FINAL_REPORT FI
	GROUP BY FI.PARTY_CODE,
		MARGINDATE,
		CASE 
			WHEN SEGMENT = 'CAPITAL'
				THEN FI.EXCHANGE + '-CASH'
			WHEN SEGMENT = 'FUTURES'
				THEN FI.EXCHANGE + '-F&O'
			ELSE FI.EXCHANGE + '-' + SEGMENT
			END,
		SEGMENT,
		PARTYNAME,
		L_ADDRESS1,
		L_ADDRESS2,
		L_ADDRESS3,
		L_CITY,
		L_ZIP,
		L_STATE,
		L_NATION,
		RES_PHONE,
		OFF_PHONE,
		EMAIL,
		PAN_GIR_NO,
		BRANCH_CD,
		SUB_BROKER,
		TRADER,
		RPT_ORD,
		SCRIP_CD,
		SERIES,
		SEC_CLRATE,
		BGNO,
		BG_EXPIRYDATE,
		FDRNO,
		FDR_EXPIRYDATE,
		SCRIPNAME,
		ISIN,
		'COMBINED_MARGIN_STATEMENT_'+ UPPER(LTRIM(RTRIM(FI.PARTY_CODE))) +'_'+ CONVERT(VARCHAR,CONVERT(DATETIME,MARGINDATE,103),112)
	ORDER BY FI.PARTY_CODE,
		RPT_ORD,
		EXCHANGE,
		SEGMENT

	SELECT CLIENT_CODE,
		PARTY_COUNT = COUNT(CLIENT_CODE),
		SCRIP_COUNT = COUNT(RPT_ORD)
	INTO #ROWCOUNT
	FROM #FINAL_REPORTNEW
	WHERE RPT_ORD = '2SEC'
	GROUP BY CLIENT_CODE,
		RPT_ORD
	ORDER BY 1

	UPDATE #FINAL_REPORTNEW
	SET ROW_P_COUNT = PARTY_COUNT,
		ROW_S_COUNT = SCRIP_COUNT
	FROM #ROWCOUNT
	WHERE #ROWCOUNT.CLIENT_CODE = #FINAL_REPORTNEW.CLIENT_CODE
		AND RPT_ORD = '2SEC'

	SELECT * FROM #FINAL_REPORTNEW
	WHERE (TOTAL_MARGIN <> 0 OR LED_MARGIN_AMT <> 0	OR NONCASH_AMT <> 0 OR MAR_PLEDGE_AMT <> 0 OR BGFD_AMT <> 0 OR (SEC_FAMOUNT + BG_AMOUNT + FDR_AMOUNT) <> 0 )
	ORDER BY 
		CLIENT_CODE,
		RPT_ORD,
		EXCHANGE,
		SEGMENT

	DROP TABLE #MARGIN_DETAIL

	DROP TABLE #MCLIENT

	DROP TABLE #CLIENTMASTER

	DROP TABLE #FINAL_REPORTNEW

END

GO
