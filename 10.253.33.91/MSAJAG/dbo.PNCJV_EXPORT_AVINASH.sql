CREATE PROCEDURE [dbo].[PNCJV_EXPORT_AVINASH] (@REPORTDATE VARCHAR(11))

AS --- EXEC PNCJV_EXPORT_AVINASH 'DEC  8 2021'

SELECT DISTINCT PARTY_CODE INTO #PARTY FROM COMMON_CONTRACT_DATA WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN @REPORTDATE AND @REPORTDATE + ' 23:59'
UNION
SELECT DISTINCT PARTY_CODE FROM ANGELCOMMODITY.MCDX.DBO.COMMON_CONTRACT_DATA WITH (NOLOCK)
WHERE SAUDA_DATE BETWEEN @REPORTDATE AND @REPORTDATE + ' 23:59'

INSERT INTO #PARTY
SELECT PARTY_CODE FROM (
SELECT DISTINCT PARTY_CODE FROM TBL_MAR_REPORT (NOLOCK)
WHERE TRADE_DAY BETWEEN @REPORTDATE AND @REPORTDATE + ' 23:59'
UNION
SELECT DISTINCT PARTY_CODE FROM ANGELCOMMODITY.MCDX.DBO.TBL_MAR_REPORT WITH (NOLOCK)                                         
WHERE TRADE_DAY BETWEEN @REPORTDATE AND @REPORTDATE + ' 23:59') A
WHERE A.PARTY_CODE NOT IN (SELECT DISTINCT * FROM #PARTY)


DELETE FROM #PARTY WHERE PARTY_CODE IN
(SELECT CL_CODE FROM MSAJAG.DBO.CLIENT_BROK_DETAILS  B WITH (NOLOCK) GROUP BY CL_CODE HAVING SUM(PRINT_OPTIONS) <> 0)

SELECT A.CL_CODE,EXCHANGE,SEGMENT,PRINT_OPTIONS,L_STATE,CONVERT(VARCHAR(10),ACTIVE_DATE,120)ACTIVE_DATE INTO #CL_DATA
 FROM MSAJAG.DBO.CLIENT_DETAILS A WITH (NOLOCK),MSAJAG.DBO.CLIENT_BROK_DETAILS  B WITH (NOLOCK)
WHERE A.CL_CODE=B.CL_CODE AND B.Segment <> 'SLBS'
AND A.CL_CODE IN (SELECT DISTINCT * FROM #PARTY)

SELECT SP_PARTY_CODE,SP_DATE_FROM INTO #SCHME FROM SCHEME_MAPPING WITH (NOLOCK)
WHERE SP_DATE_TO > GETDATE()
AND SP_PARTY_CODE IN (SELECT * FROM #PARTY)
AND SP_COMPUTATION_LEVEL='O'
UNION ALL
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM ANAND.BSEDB_AB.DBO.SCHEME_MAPPING WITH (NOLOCK)
WHERE SP_DATE_TO > GETDATE()
AND SP_PARTY_CODE IN (SELECT * FROM #PARTY)
AND SP_COMPUTATION_LEVEL='O'
UNION ALL
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM ANGELFO.NSEFO.DBO.SCHEME_MAPPING WITH (NOLOCK)
WHERE SP_DATE_TO > GETDATE()
AND SP_PARTY_CODE IN (SELECT * FROM #PARTY)
AND SP_COMPUTATION_LEVEL='O'
UNION ALL
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM ANGELFO.NSECURFO.DBO.SCHEME_MAPPING WITH (NOLOCK)
WHERE SP_DATE_TO > GETDATE()
AND SP_PARTY_CODE IN (SELECT * FROM #PARTY)
AND SP_COMPUTATION_LEVEL='O'
UNION ALL
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM ANGELCOMMODITY.MCDX.DBO.SCHEME_MAPPING WITH (NOLOCK)
WHERE SP_DATE_TO > GETDATE()
AND SP_PARTY_CODE IN (SELECT * FROM #PARTY)
AND SP_COMPUTATION_LEVEL='O'
UNION ALL
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM ANGELCOMMODITY.NCDX.DBO.SCHEME_MAPPING WITH (NOLOCK)
WHERE SP_DATE_TO > GETDATE()
AND SP_PARTY_CODE IN (SELECT * FROM #PARTY)
AND SP_COMPUTATION_LEVEL='O'
UNION ALL
SELECT SP_PARTY_CODE,SP_DATE_FROM FROM ANGELCOMMODITY.BSECURFO.DBO.SCHEME_MAPPING WITH (NOLOCK)
WHERE SP_DATE_TO > GETDATE()
AND SP_PARTY_CODE IN (SELECT * FROM #PARTY)
AND SP_COMPUTATION_LEVEL='O'

SELECT DISTINCT SP_PARTY_CODE AS I_TRADE_CLIENT, MAX(CONVERT(VARCHAR(10),SP_DATE_FROM,120)) AS ITRADE_ACTIVE_DATE INTO #SCHME_FIN  
FROM #SCHME GROUP BY SP_PARTY_CODE

SELECT CL_CODE,EXCHANGE,SEGMENT,PRINT_OPTIONS,L_STATE,ACTIVE_DATE=CAST(ACTIVE_DATE AS DATETIME),
I_TRADE_CLIENT,ITRADE_ACTIVE_DATE=CAST(ITRADE_ACTIVE_DATE AS DATETIME) INTO #FDATA
FROM #CL_DATA A LEFT OUTER JOIN  #SCHME_FIN B ON I_TRADE_CLIENT= CL_CODE

SELECT * INTO #JV FROM #FDATA WHERE ACTIVE_DATE > ='APR  1 2019' AND ACTIVE_DATE < = @REPORTDATE + ' 23:59'
AND ITRADE_ACTIVE_DATE > ='APR  1 2019' AND ITRADE_ACTIVE_DATE < = @REPORTDATE + ' 23:59'
AND PRINT_OPTIONS=0

SELECT CL_CODE,[NSECM],[BSECM],[NSEFO],[NSECD],[MCDX],[NCDX] INTO #PNCJV FROM (SELECT CL_CODE,
EXCHANGE=CASE WHEN EXCHANGE='NSE' AND SEGMENT='CAPITAL' THEN 'NSECM' WHEN EXCHANGE='BSE' AND SEGMENT='CAPITAL' THEN 'BSECM'
			  WHEN EXCHANGE='NSE' AND SEGMENT='FUTURES' THEN 'NSEFO' WHEN EXCHANGE='NSX' AND SEGMENT='FUTURES' THEN 'NSECD'
			  WHEN EXCHANGE='MCX' AND SEGMENT='FUTURES' THEN 'MCDX' WHEN EXCHANGE='NCX' AND SEGMENT='FUTURES' THEN 'NCDX' ELSE '' END,
PRINT_OPTIONS FROM #JV ) AS SourceTable 
PIVOT
(MIN(PRINT_OPTIONS) FOR EXCHANGE IN ([NSECM],[BSECM],[NSEFO],[NSECD],[MCDX],[NCDX])) AS PivotTable

SELECT * INTO #PNCMCDX FROM #PNCJV WHERE NSECM IS NULL AND BSECM IS NULL AND NSEFO IS NULL AND NSECD IS NULL

SELECT * INTO #PNCNSECM FROM #PNCJV WHERE CL_CODE NOT IN (SELECT CL_CODE FROM #PNCMCDX)


/* DELETE UNWANTED FILE FROM EXPORT FOLDER*/

DECLARE @N AS VARCHAR(2) = '1'
DECLARE @path AS VARCHAR(200) =  'J:\BACKOFFICE\EXPORT'
DECLARE @deleteSql AS VARCHAR(512)
  
SET @deleteSql = 'forfiles /P "' + @path + '" /S /M *.CSV /D -' + @N + ' /C "cmd /c del @PATH"'

EXEC master.dbo.xp_cmdshell @deleteSql, NO_OUTPUT


