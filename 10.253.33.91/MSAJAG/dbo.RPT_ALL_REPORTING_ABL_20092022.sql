CREATE PROCEDURE [dbo].[RPT_ALL_REPORTING_ABL_20092022] (@RPT_DATE VARCHAR(11),@REPORT VARCHAR(5))

AS ---- EXEC RPT_ALL_REPORTING_ABL 'SEP 20 2022','EQY'


DECLARE @PRV_DATE VARCHAR(11)
DECLARE @EPN_SETT_NO VARCHAR(11)

SELECT @EPN_SETT_NO = SETT_NO-1 FROM SETT_MST WHERE SETT_TYPE='N' AND START_DATE  BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59:59'
SELECT @PRV_DATE = LEFT(START_DATE,11) FROM SETT_MST WHERE SETT_TYPE='N' AND SETT_NO = @EPN_SETT_NO

---NSECM

IF OBJECT_ID(N'TEMPDB..#CMTDAY') IS NOT NULL
BEGIN
DROP TABLE #CMTDAY
END

CREATE TABLE #CMTDAY (CLIENT_CODE VARCHAR(11),CM_TDAY_MAG MONEY)

IF (SELECT COUNT(*) FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' ) <> 0
BEGIN
INSERT INTO #CMTDAY
SELECT PARTY_CODE, CM_TDAY_MAG=SUM(EXTREME_LOSS_MARGIN) 
FROM MSAJAG.DBO.TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' GROUP BY PARTY_CODE
END
ELSE
BEGIN
INSERT INTO #CMTDAY
SELECT CLIENT_CODE,
(SELECT MAX(NSECM) FROM ( VALUES (SUM(CM_TDAY_NMASS)),(SUM(CM_TDAY_PROVMRG))) AS NSECM (NSECM)) AS CM_TDAY_MAG
FROM
(SELECT CLIENT_CODE,CM_TDAY_NMASS=CONVERT(MONEY,TOTAL_MRG),CM_TDAY_PROVMRG=0
FROM TDAY_MARGIN_DATA_CM WITH (NOLOCK) WHERE CONVERT(MONEY,TOTAL_MRG) <> 0 AND CM_MARGIN_DATE = @RPT_DATE
UNION ALL
SELECT PARTY_CODE, 0,CM_TDAY_PROVMRG=SUM(TOTAL_MARGIN_VAR)
FROM TBL_CMMARGIN_PROV_INTRADAY A WHERE MARGINDATE = @RPT_DATE 
AND FILECOUNTER IN (SELECT MAX(FILECOUNTER) FROM TBL_CMMARGIN_PROV_INTRADAY B WHERE A.MARGINDATE = B.MARGINDATE)
GROUP BY PARTY_CODE) A GROUP BY CLIENT_CODE
END

IF (SELECT COUNT(*) FROM TBL_CMMARGIN WITH (NOLOCK) WHERE MARGINDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' ) = 0
BEGIN
UPDATE A SET CM_TDAY_MAG = (CM_TDAY_MAG - (EPN*0.2)) FROM #CMTDAY A,
(SELECT CLTCODE,EPN=SUM(EPN_VALUE) FROM ANGELDEMAT.MSAJAG.DBO.TDAY_NET_SELL GROUP BY CLTCODE) B
WHERE A.CLIENT_CODE = B.CLTCODE

UPDATE #CMTDAY SET CM_TDAY_MAG = 0 WHERE CM_TDAY_MAG < 0 
END


---NSEFO
IF OBJECT_ID(N'TEMPDB..#FOTDAY') IS NOT NULL
BEGIN
DROP TABLE #FOTDAY
END

CREATE TABLE #FOTDAY (CLIENT_CODE VARCHAR(11),FO_TDAY_MAG MONEY)

IF (SELECT COUNT(*) FROM ANGELFO.NSEFO.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' ) <> 0
BEGIN
INSERT INTO #FOTDAY
SELECT CLIENT_CODE=PARTY_CODE,FO_TDAY_MAG=SUM(TOTALMARGIN-DEL_MARGIN) 
FROM ANGELFO.NSEFO.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' GROUP BY PARTY_CODE
END
ELSE
BEGIN
INSERT INTO #FOTDAY
SELECT CLIENT_CODE,
(SELECT MAX(NSECM) FROM ( VALUES (SUM(FO_TDAY_NMASS)),(SUM(FO_TDAY_PROVMRG))) AS NSECM (NSECM)) AS FO_TDAY_MAG
FROM
(SELECT CLIENT_CODE,FO_TDAY_NMASS=SUM(CONVERT(MONEY,TOTAL_MRG) - CONVERT(MONEY,DELIVERY_MRG)) ,FO_TDAY_PROVMRG=0
FROM TDAY_MARGIN_DATA_FNO WITH (NOLOCK) WHERE CONVERT(MONEY,TOTAL_MRG) <> 0 AND FO_MARGIN_DATE = @RPT_DATE GROUP BY CLIENT_CODE --) A GROUP BY CLIENT_CODE END ---EXPIRY DAY

UNION ALL
SELECT CLIENT_CODE=PARTY_CODE, FO_TDAY_NMASS=0,CM_TDAY_PROVMRG=SUM(TOTALMARGIN)
FROM ANGELFO.NSEFO.DBO.FOMARGINNEW_PROV_INTRADAY A WHERE MDATE=@RPT_DATE 
AND FILECOUNTER IN (SELECT MAX(FILECOUNTER) FROM ANGELFO.NSEFO.DBO.FOMARGINNEW_PROV_INTRADAY B WHERE A.MDATE = B.MDATE)
GROUP BY PARTY_CODE) A GROUP BY CLIENT_CODE
END

---NSECD
IF OBJECT_ID(N'TEMPDB..#CDTDAY') IS NOT NULL
BEGIN
DROP TABLE #CDTDAY
END
CREATE TABLE #CDTDAY (CLIENT_CODE VARCHAR(11),CD_TDAY_MAG MONEY)

IF (SELECT COUNT(*) FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' ) <> 0
BEGIN
INSERT INTO #CDTDAY
SELECT CLIENT_CODE=PARTY_CODE,CD_TDAY_MAG=SUM(TOTALMARGIN) 
FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' GROUP BY PARTY_CODE
END
ELSE
BEGIN
INSERT INTO #CDTDAY
SELECT CLIENT_CODE,
(SELECT MAX(NSECM) FROM ( VALUES (SUM(CD_TDAY_NMASS)),(SUM(CD_TDAY_PROVMRG))) AS NSECM (NSECM)) AS CD_TDAY_MAG 
FROM
(SELECT CLIENT_CODE,CD_TDAY_NMASS=CONVERT(MONEY,TOTAL_MRG),CD_TDAY_PROVMRG=0
FROM TDAY_MARGIN_DATA_CD WITH (NOLOCK) WHERE CONVERT(MONEY,TOTAL_MRG) <> 0  AND CD_MARGIN_DATE = @RPT_DATE
UNION ALL
SELECT PARTY_CODE, 0,CD_TDAY_PROVMRG=SUM(TOTALMARGIN)
FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW_PROV_INTRADAY A WHERE MDATE=@RPT_DATE 
AND FILECOUNTER IN (SELECT MAX(FILECOUNTER) FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW_PROV_INTRADAY B WHERE A.MDATE = B.MDATE)
GROUP BY PARTY_CODE) A GROUP BY CLIENT_CODE
END


---MCDX
IF OBJECT_ID(N'TEMPDB..#MXTDAY') IS NOT NULL
BEGIN
DROP TABLE #MXTDAY
END

CREATE TABLE #MXTDAY (CLIENT_CODE VARCHAR(11),MX_TDAY_MAG MONEY)

IF @REPORT = 'COM'
BEGIN
IF (SELECT COUNT(*) FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59') <> 0
BEGIN
INSERT INTO #MXTDAY
SELECT CLIENT_CODE=PARTY_CODE,MX_TDAY_MAG=SUM(TOTALMARGIN) 
FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' GROUP BY PARTY_CODE
END
ELSE
BEGIN
INSERT INTO #MXTDAY
SELECT CLIENT_CODE,MX_TDAY_NMASS=SUM(MCX_MARGIN) FROM TDAY_MARGIN_DATA_COM WITH (NOLOCK) WHERE REPORT_DATE = @RPT_DATE GROUP BY CLIENT_CODE 
END
END

---NCDX
IF OBJECT_ID(N'TEMPDB..#NXTDAY') IS NOT NULL
BEGIN
DROP TABLE #NXTDAY
END

CREATE TABLE #NXTDAY (CLIENT_CODE VARCHAR(11),NX_TDAY_MAG MONEY)

IF @REPORT = 'COM'
BEGIN
IF (SELECT COUNT(*) FROM ANGELCOMMODITY.NCDX.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59') <> 0
BEGIN
INSERT INTO #NXTDAY
SELECT CLIENT_CODE=PARTY_CODE,NX_TDAY_MAG=SUM(TOTALMARGIN) 
FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW WITH (NOLOCK) WHERE MDATE BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59' GROUP BY PARTY_CODE
END
ELSE
BEGIN
INSERT INTO #NXTDAY
SELECT CLIENT_CODE,NX_TDAY_NMASS=SUM(NCX_MARGIN) FROM TDAY_MARGIN_DATA_COM WITH (NOLOCK) WHERE REPORT_DATE = @RPT_DATE GROUP BY CLIENT_CODE
END
END

TRUNCATE TABLE TBL_ALLOCATION_REPORTING_LOG

INSERT INTO TBL_ALLOCATION_REPORTING_LOG
SELECT *,LOG_DATE=GETDATE() FROM TBL_ALLOCATION_REPORTING ORDER BY CLIENT_CODE

TRUNCATE TABLE TBL_ALLOCATION_REPORTING

INSERT INTO TBL_ALLOCATION_REPORTING
SELECT @RPT_DATE,CLIENT_CODE,CM_TDAY_MAG=SUM(CM_TDAY_MAG),CM_PDAY_MAG=CONVERT(MONEY,0),CM_CHANGE_MAG=CONVERT(MONEY,0),
FO_TDAY_MAG=SUM(FO_TDAY_MAG),FO_PDAY_MAG=CONVERT(MONEY,0),FO_CHANGE_MAG=CONVERT(MONEY,0),
CD_TDAY_MAG=CAST(SUM(CD_TDAY_MAG) AS MONEY),CD_PDAY_MAG=CONVERT(MONEY,0),CD_CHANGE_MAG=CONVERT(MONEY,0),
MX_TDAY_MAG=CAST(SUM(MX_TDAY_MAG) AS MONEY),MX_PDAY_MAG=CONVERT(MONEY,0),MX_CHANGE_MAG=CONVERT(MONEY,0),
NX_TDAY_MAG=CAST(SUM(NX_TDAY_MAG) AS MONEY),NX_PDAY_MAG=CONVERT(MONEY,0),NX_CHANGE_MAG=CONVERT(MONEY,0),
TOTAL_TDAY_MAG=SUM(CM_TDAY_MAG)+SUM(FO_TDAY_MAG)+CAST(SUM(CD_TDAY_MAG) AS MONEY)+SUM(MX_TDAY_MAG)+SUM(NX_TDAY_MAG),
TOTAL_PDAY_MAG=CONVERT(MONEY,0),TOTAL_CHANGE_MAG=CONVERT(MONEY,0),
CM_ALLOCATED_PDAY=CONVERT(MONEY,0),FO_ALLOCATED_PDAY=CONVERT(MONEY,0),CD_ALLOCATED_PDAY=CONVERT(MONEY,0),
MX_ALLOCATED_PDAY=CONVERT(MONEY,0),NX_ALLOCATED_PDAY=CONVERT(MONEY,0),REPLEDGE_STOCK_PDAY=CONVERT(MONEY,0),
CM_ALLOCATED_TDAY=CONVERT(MONEY,0),FO_ALLOCATED_TDAY=CONVERT(MONEY,0),CD_ALLOCATED_TDAY=CONVERT(MONEY,0),
MX_ALLOCATED_TDAY=CONVERT(MONEY,0),NX_ALLOCATED_TDAY=CONVERT(MONEY,0),REPLEDGE_STOCK_TDAY=CONVERT(MONEY,0),
EDT_LEDGER_BALANCE=CONVERT(MONEY,0),TDAY_RECEIPTS=CONVERT(MONEY,0),TDAY_PAYMENTS=CONVERT(MONEY,0),
REPLEDGE_STOCK_LATEST_TDAY=CONVERT(MONEY,0),TOTAL_EOD_BALANCE=CONVERT(MONEY,0),
REAL_TIME_SHORTAGE_SURPLUS=CONVERT(MONEY,0),EPN_VALUE=CONVERT(MONEY,0),FUNDS_VERTUAL=CONVERT(MONEY,0),
TRADED_STOCK_IN_HAND=CONVERT(MONEY,0),CUSA_CMP_STOCK=CONVERT(MONEY,0),MISMATCH_IN_HAIRCUT=CONVERT(MONEY,0),
INC_FUNDS=CONVERT(MONEY,0),PREVIOUS_DAY_EPN=CONVERT(MONEY,0),EXCESS_QTY_PRO=CONVERT(MONEY,0),
INC_EXCH_RELEASE_STOCK=CONVERT(MONEY,0),TOTAL_NOTIONAL=CONVERT(MONEY,0),REAL_TIME_SHORTAGE_SURPLUS_REVISED=CONVERT(MONEY,0),
TOTAL_ALLOCATED_AMT=CONVERT(MONEY,0),TOTAL_ALLOCATED_AMT_NOTIONAL=CONVERT(MONEY,0),SHORATGE1=CONVERT(MONEY,0),
SHORTAGE_POST_NOTIONAL=CONVERT(MONEY,0),LD_SHORTAGE=CONVERT(MONEY,0),DIFF_WITH_LDSHORTAGE=CONVERT(MONEY,0),
REVISED_SHORTAGE=CONVERT(MONEY,0),NOTIONAL_2=CONVERT(MONEY,0),NCL_UPLOAD=CONVERT(MONEY,0),
MCX_UPLOAD=CONVERT(MONEY,0),GSEC_VIEW_ONLY=CONVERT(MONEY,0),REMARKS=CONVERT(VARCHAR(100),'') ---INTO TBL_ALLOCATION_REPORTING
FROM 
(SELECT CLIENT_CODE,CM_TDAY_MAG,FO_TDAY_MAG=0,CD_TDAY_MAG=0,MX_TDAY_MAG=0,NX_TDAY_MAG=0 FROM #CMTDAY
UNION ALL
SELECT CLIENT_CODE,0,FO_TDAY_MAG,0,0,0 FROM #FOTDAY
UNION ALL
SELECT CLIENT_CODE,0,0,CD_TDAY_MAG,0,0 FROM #CDTDAY
UNION ALL
SELECT CLIENT_CODE,0,0,0,MX_TDAY_MAG,0 FROM #MXTDAY
UNION ALL
SELECT CLIENT_CODE,0,0,0,0,NX_TDAY_MAG FROM #NXTDAY ) B GROUP BY CLIENT_CODE ORDER BY CLIENT_CODE


/* PREVIOUS DAY MARGIN UPDATIION */

----NSECM
UPDATE A SET CM_PDAY_MAG = TOTALMARGIN FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE, TOTALMARGIN=SUM(TOTAL_MARGIN_VAR) FROM TBL_CMMARGIN WITH (NOLOCK) 
WHERE MARGINDATE = @PRV_DATE GROUP BY PARTY_CODE) B WHERE A.CLIENT_CODE = B.PARTY_CODE

UPDATE TBL_ALLOCATION_REPORTING SET CM_CHANGE_MAG = CM_TDAY_MAG - CM_PDAY_MAG


----NSEFO
UPDATE A SET FO_PDAY_MAG = TOTALMARGIN FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,TOTALMARGIN=SUM(TOTALMARGIN) FROM ANGELFO.NSEFO.DBO.FOMARGINNEW WITH (NOLOCK) 
WHERE MDATE = @PRV_DATE GROUP BY PARTY_CODE) B WHERE A.CLIENT_CODE = B.PARTY_CODE

UPDATE TBL_ALLOCATION_REPORTING SET FO_CHANGE_MAG = FO_TDAY_MAG - FO_PDAY_MAG

----NSECD
UPDATE A SET CD_PDAY_MAG = TOTALMARGIN FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,TOTALMARGIN=SUM(TOTALMARGIN) FROM ANGELFO.NSECURFO.DBO.FOMARGINNEW WITH (NOLOCK) 
WHERE MDATE = @PRV_DATE GROUP BY PARTY_CODE) B WHERE A.CLIENT_CODE = B.PARTY_CODE

UPDATE TBL_ALLOCATION_REPORTING SET CD_CHANGE_MAG = CD_TDAY_MAG - CD_PDAY_MAG
 
----MCX
UPDATE A SET MX_PDAY_MAG = TOTALMARGIN FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,TOTALMARGIN=SUM(TOTALMARGIN) FROM ANGELCOMMODITY.MCDX.DBO.FOMARGINNEW WITH (NOLOCK)
WHERE MDATE = @PRV_DATE  GROUP BY PARTY_CODE) B WHERE A.CLIENT_CODE = B.PARTY_CODE

UPDATE TBL_ALLOCATION_REPORTING SET MX_CHANGE_MAG = MX_TDAY_MAG - MX_PDAY_MAG

----NCX
UPDATE A SET MX_PDAY_MAG = TOTALMARGIN FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,TOTALMARGIN=SUM(TOTALMARGIN) FROM ANGELCOMMODITY.NCDX.DBO.FOMARGINNEW WITH (NOLOCK)
WHERE MDATE = @PRV_DATE GROUP BY PARTY_CODE) B WHERE A.CLIENT_CODE = B.PARTY_CODE

UPDATE TBL_ALLOCATION_REPORTING SET NX_CHANGE_MAG = NX_TDAY_MAG - NX_PDAY_MAG


----CHANGE MARGIN & TOTAL MARGIN UPDATION
UPDATE TBL_ALLOCATION_REPORTING SET TOTAL_PDAY_MAG = CM_PDAY_MAG + FO_PDAY_MAG + CD_PDAY_MAG + MX_PDAY_MAG + NX_PDAY_MAG
UPDATE TBL_ALLOCATION_REPORTING SET TOTAL_CHANGE_MAG =  TOTAL_TDAY_MAG - TOTAL_PDAY_MAG



/* PREVIOUS DAY ALLOCATIONS */

SELECT PARTY_CODE,EXCHANGE,SEGMENT,ALLOCATECASH=SUM(ALLOCATECASH) INTO #PDAY_ALL
FROM TBL_CLIENT_COLL_ALLOCATE WITH (NOLOCK) 
WHERE ALLOCATECASH <> 0 AND PROCESS_DT = @PRV_DATE
GROUP BY PARTY_CODE,EXCHANGE,SEGMENT

UPDATE TBL_ALLOCATION_REPORTING SET CM_ALLOCATED_PDAY = ALLOCATECASH FROM #PDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='NSE' AND A.SEGMENT='CAPITAL'

UPDATE TBL_ALLOCATION_REPORTING SET FO_ALLOCATED_PDAY = ALLOCATECASH FROM #PDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='NSE' AND A.SEGMENT='FUTURES'

UPDATE TBL_ALLOCATION_REPORTING SET CD_ALLOCATED_PDAY = ALLOCATECASH FROM #PDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='NSX' AND A.SEGMENT='FUTURES'

UPDATE TBL_ALLOCATION_REPORTING SET MX_ALLOCATED_PDAY = ALLOCATECASH FROM #PDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='MCX' AND A.SEGMENT='FUTURES'

UPDATE TBL_ALLOCATION_REPORTING SET NX_ALLOCATED_PDAY = ALLOCATECASH FROM #PDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='NCX' AND A.SEGMENT='FUTURES'


/* T-DAY ALLOCATIONS */

SELECT PARTY_CODE,EXCHANGE,SEGMENT,ALLOCATECASH=SUM(ALLOCATECASH) INTO #TDAY_ALL
FROM TBL_CLIENT_COLL_ALLOCATE WITH (NOLOCK) 
WHERE ALLOCATECASH <> 0 AND PROCESS_DT = @RPT_DATE
GROUP BY PARTY_CODE,EXCHANGE,SEGMENT


UPDATE TBL_ALLOCATION_REPORTING SET CM_ALLOCATED_TDAY = ALLOCATECASH FROM #TDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='NSE' AND A.SEGMENT='CAPITAL'

UPDATE TBL_ALLOCATION_REPORTING SET FO_ALLOCATED_TDAY = ALLOCATECASH FROM #TDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='NSE' AND A.SEGMENT='FUTURES'

UPDATE TBL_ALLOCATION_REPORTING SET CD_ALLOCATED_TDAY = ALLOCATECASH FROM #TDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='NSX' AND A.SEGMENT='FUTURES'

UPDATE TBL_ALLOCATION_REPORTING SET MX_ALLOCATED_TDAY = ALLOCATECASH FROM #TDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='MCX' AND A.SEGMENT='FUTURES'

UPDATE TBL_ALLOCATION_REPORTING SET NX_ALLOCATED_TDAY = ALLOCATECASH FROM #TDAY_ALL A 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = A.PARTY_CODE AND A.EXCHANGE='NCX' AND A.SEGMENT='FUTURES'


/* PREVIOUS DAY RE-PLEDGE STOCK AND EPN UPDATE FROM BO */

UPDATE A SET REPLEDGE_STOCK_PDAY = EFFVALUE FROM TBL_ALLOCATION_REPORTING A,
(SELECT CLIENT_CODE,EFFVALUE=SUM(FINAL_EFFECTIVE_VALUE)
FROM TBL_CCCM_SCRIP_DATA WITH (NOLOCK) WHERE RECORDDATE = @PRV_DATE GROUP BY CLIENT_CODE) B WHERE A.CLIENT_CODE = B.CLIENT_CODE

CREATE TABLE #PDAY_EPN_DATA (PARTY_CODE VARCHAR(11),EPI_CCE MONEY, POOLACCOUNT MONEY)

INSERT INTO #PDAY_EPN_DATA
EXEC PROC_ALL_PDAY_EPN_CALCULATION @PRV_DATE

UPDATE A SET PREVIOUS_DAY_EPN = CASHBENEFIT FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,CASHBENEFIT=SUM(CASHBENEFIT) FROM TBL_EPN_BENEFIT WITH (NOLOCK) 
WHERE TRANSDATE BETWEEN @PRV_DATE AND @PRV_DATE AND SETT_NO=@EPN_SETT_NO GROUP BY PARTY_CODE) B
--(SELECT PARTY_CODE,CASHBENEFIT=SUM(EPI_CCE+POOLACCOUNT) FROM #PDAY_EPN_DATA GROUP BY PARTY_CODE) B 
--WHERE A.CLIENT_CODE = B.PARTY_CODE


/* TDAY EPN UPDATE - FROM EPN PROCESS */

UPDATE A SET EPN_VALUE = EPN*0.8 FROM TBL_ALLOCATION_REPORTING A,
(SELECT CLTCODE,EPN=SUM(EPN_VALUE) FROM TBL_ALL_EPN_DATA WHERE EPN_DATE = @RPT_DATE GROUP BY CLTCODE) B WHERE A.CLIENT_CODE = B.CLTCODE



	/* TDAY RE-PLEDGE STOCK - FROM REPLEDGE PROCESS */
UPDATE A SET REPLEDGE_STOCK_TDAY = CASE WHEN HVALUE < 0 THEN 0 ELSE B.HVALUE END, REPLEDGE_STOCK_LATEST_TDAY = 0 FROM TBL_ALLOCATION_REPORTING A,
(SELECT  PARTY_CODE,HVALUE=SUM(CCVAR_HVALUE) FROM TBL_ALL_REPLD_STOCK WHERE REPORT_DATE = @RPT_DATE GROUP BY PARTY_CODE) B
WHERE A.CLIENT_CODE = B.PARTY_CODE

UPDATE A SET MISMATCH_IN_HAIRCUT = DIFF_HVALUE FROM TBL_ALLOCATION_REPORTING A,
(SELECT  PARTY_CODE,DIFF_HVALUE=SUM(DIFF_HVALUE) FROM TBL_ALL_REPLD_STOCK WHERE REPORT_DATE = @RPT_DATE GROUP BY PARTY_CODE) B
WHERE A.CLIENT_CODE = B.PARTY_CODE


	/* EXCHANGE RELEASE STOCK -  FROM REPLEDGE PROCESS */
IF @REPORT = 'COM'
BEGIN
UPDATE TBL_ALLOCATION_REPORTING SET INC_EXCH_RELEASE_STOCK = 0
END
ELSE
BEGIN
UPDATE A SET INC_EXCH_RELEASE_STOCK = HVALUE FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,HVALUE=SUM(HVALUE) FROM TBL_ALL_RELEASE_STOCK WHERE REPORT_DATE = @RPT_DATE GROUP BY PARTY_CODE) B WHERE A.CLIENT_CODE = B.PARTY_CODE
END

UPDATE TBL_ALLOCATION_REPORTING SET TOTAL_EOD_BALANCE = (EDT_LEDGER_BALANCE +REPLEDGE_STOCK_LATEST_TDAY)

UPDATE TBL_ALLOCATION_REPORTING SET REAL_TIME_SHORTAGE_SURPLUS = 
CASE WHEN (TOTAL_EOD_BALANCE - TOTAL_TDAY_MAG) < 0 THEN  (TOTAL_EOD_BALANCE - TOTAL_TDAY_MAG) ELSE 0 END





	/* CMP & CUSTA STOCK VALUE AFTER VAR MARGIN UPDATION - FROM CMP & CUSA CALCULATION PROCESS */
UPDATE A SET CUSA_CMP_STOCK = B.HVALUE_AFTER_VAR FROM TBL_ALLOCATION_REPORTING A,
(SELECT REPORT_DATE,PARTY_CODE,HVALUE_AFTER_VAR=SUM(HVALUE_AFTER_VAR)
FROM (
SELECT REPORT_DATE,PARTY_CODE, HVALUE_AFTER_VAR FROM TBL_ALL_CMP_FREE_HOLDING WHERE REPORT_DATE = @RPT_DATE
UNION ALL 
SELECT REPORT_DATE,PARTY_CODE, HVALUE_AFTER_VAR FROM TBL_ALL_CUSA_HOLDING WHERE REPORT_DATE = @RPT_DATE) C 
GROUP BY C.REPORT_DATE,C.PARTY_CODE) B WHERE A.CLIENT_CODE = B.PARTY_CODE



	/* PENDING RECEIPTS ENTIRES UPDATION - FROM PAYIN TRANSACTION UPLOAD FILE */
UPDATE TBL_ALLOCATION_REPORTING SET INC_FUNDS = REC_PAY 
FROM TBL_ALL_PAYIN_TRAN B 
WHERE TBL_ALLOCATION_REPORTING.CLIENT_CODE = B.CLTCODE AND REPORT_DATE = @RPT_DATE

--UPDATE A SET INC_FUNDS = REC_PAY FROM TBL_ALLOCATION_REPORTING A,
--(SELECT CLTCODE,REC_PAY=SUM(VAMT) FROM ACCOUNT.DBO.LEDGER WITH (NOLOCK)
--WHERE VTYP=2 AND VDT BETWEEN @RPT_DATE AND @RPT_DATE 
--AND EDT!='2049-12-31 00:00:00.000' AND PDT BETWEEN @RPT_DATE + ' 13:00:00.000' AND @RPT_DATE + ' 23:59:59:59'
--AND CLTCODE IN (SELECT CL_CODE FROM CLIENT_DETAILS WITH (NOLOCK)) GROUP BY CLTCODE) B
--WHERE A.CLIENT_CODE = B.CLTCODE



	/* PRO ALLOCATION UPDATION - FROM EXCESS PRO UPLOAD FILE */
UPDATE A SET EXCESS_QTY_PRO = CASE WHEN (REPLEDGE_STOCK_TDAY - EXCESS_COLL_VALUE) < 0 THEN 0 ELSE (REPLEDGE_STOCK_TDAY - EXCESS_COLL_VALUE) END 
FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,EXCESS_COLL_VALUE FROM TBL_ALL_EXCESS_PRO WHERE REPORT_DATE= @RPT_DATE) B WHERE A.CLIENT_CODE = B.PARTY_CODE

UPDATE A SET EXCESS_QTY_PRO =  CASE WHEN (EXCESS_QTY_PRO - HVALUE) < 0 THEN 0 ELSE (EXCESS_QTY_PRO - HVALUE) END FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,HVALUE=SUM(CCVAR_HVALUE) FROM TBL_ALL_REPLD_STOCK 
WHERE REPORT_DATE = @RPT_DATE AND CERTNO IN (SELECT ISIN FROM TBL_GMF_DATA ) GROUP BY PARTY_CODE) B WHERE A.CLIENT_CODE = B.PARTY_CODE

UPDATE A SET GSEC_VIEW_ONLY =  HVALUE FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,HVALUE=SUM(CCVAR_HVALUE) FROM TBL_ALL_REPLD_STOCK 
WHERE REPORT_DATE = @RPT_DATE AND CERTNO IN (SELECT ISIN FROM TBL_GMF_DATA ) GROUP BY PARTY_CODE) B
WHERE A.CLIENT_CODE = B.PARTY_CODE


	/* AGGREGATOR_AMOUNT UPDATION - FROM AGGREGATOR UPLOAD FILE  */
UPDATE A SET FUNDS_VERTUAL = RECEIPT_AMT FROM TBL_ALLOCATION_REPORTING A,
(SELECT PARTY_CODE,RECEIPT_AMT=SUM(ELIGIABLE_AMT) FROM TBL_ALL_AGGREGATOR_TRANS WHERE REPORT_DATE = @RPT_DATE GROUP BY PARTY_CODE) B
--(SELECT CLTCODE,RECEIPT_AMT=SUM(VAMT) FROM ACCOUNT.DBO.LEDGER_ALL WITH (NOLOCK)
--WHERE VTYP=2 AND VDT BETWEEN @RPT_DATE AND @RPT_DATE AND EDT='2049-12-31 00:00:00.000' GROUP BY CLTCODE) B
