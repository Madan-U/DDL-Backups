						ISNULL(C.L_ADDRESS1,''),
						ISNULL(C.L_ADDRESS2,''),
						ISNULL(C.L_ADDRESS3,''),
						ISNULL(C.L_CITY,''),
						ISNULL(C.L_STATE,''),
						ISNULL(C.L_NATION,''),
						ISNULL(C.L_ZIP,'')					
					HAVING SUM(QTY) > 0           
					ORDER BY PARTY_CODE,SCRIPNAME,SCRIP_CD,SERIES,ISIN          
			END          
			ELSE          
			BEGIN          
					SELECT SETT_NO,SETT_TYPE,PARTY_CODE,SCRIPNAME,SCRIP_CD,SERIES,ISIN,QTY=SUM(QTY),BDPID,BCLTDPID,BDPTYPE,
						LONG_NAME = ISNULL(C.LONG_NAME,''),
						L_ADDRESS1 = ISNULL(C.L_ADDRESS1,''),
						L_ADDRESS2 = ISNULL(C.L_ADDRESS2,''),
						L_ADDRESS3 = ISNULL(C.L_ADDRESS3,''),
						L_CITY = ISNULL(C.L_CITY,''),
						L_STATE = ISNULL(C.L_STATE,''),
						L_NATION = ISNULL(C.L_NATION,''),
						L_ZIP = ISNULL(C.L_ZIP,'')   
					FROM (           
						SELECT SETT_NO,SETT_TYPE,PARTY_CODE,SCRIPNAME=D.SCRIP_CD,D.SCRIP_CD,D.SERIES,ISIN=CERTNO,QTY=SUM(QTY),BDPID,BCLTDPID,BDPTYPE            
						FROM DELTRANS_REPORT D (NOLOCK)       
						WHERE TCODE IN (           
						SELECT TCODE          
						FROM DELTRANS_REPORT (NOLOCK)         
						WHERE TRANSDATE <= @TRANSDATE          
						AND FILLER2 = 1 AND DRCR = 'C' AND CERTNO = D.CERTNO AND FILLER2 = D.FILLER2 AND TCODE = D.TCODE) AND FILLER2 = 1          
						AND DRCR = 'D' AND DELIVERED = '0'           
						AND BCLTDPID = @NEWCLTDPID AND BDPID = @NEWDPID
						AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY         
						AND CERTNO LIKE 'IN%'           
						--AND CERTNO LIKE @ISIN          
						AND TRTYPE <> 906 AND PARTY_CODE <> 'BROKER'          
						GROUP BY SETT_NO,SETT_TYPE,PARTY_CODE,D.SCRIP_CD,D.SCRIP_CD,D.SERIES,CERTNO,BDPID,BCLTDPID,BDPTYPE            
		  
						UNION ALL          
		  
						SELECT SETT_NO,SETT_TYPE,PARTY_CODE,SCRIPNAME=D.SCRIP_CD,D.SCRIP_CD,D.SERIES,ISIN=CERTNO,QTY=SUM(QTY),BDPID,BCLTDPID,BDPTYPE            
						FROM DELTRANS_REPORT D (NOLOCK)       
						WHERE TCODE IN (           
						SELECT TCODE          
						FROM DELTRANS_REPORT (NOLOCK)         
						WHERE TRANSDATE <= @TRANSDATE          
						AND FILLER2 = 1 AND DRCR = 'C' AND CERTNO = D.CERTNO AND FILLER2 = D.FILLER2 AND TCODE = D.TCODE) AND FILLER2 = 1          
						AND DRCR = 'D' AND DELIVERED <> '0' AND TRANSDATE > @TRANSDATE          
						AND BCLTDPID = @NEWCLTDPID AND BDPID = @NEWDPID
						AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY          
						AND CERTNO LIKE 'IN%' --AND CERTNO LIKE @ISIN          
						AND TRTYPE <> 906 AND PARTY_CODE <> 'BROKER'          
						GROUP BY SETT_NO,SETT_TYPE,PARTY_CODE,D.SCRIP_CD,D.SCRIP_CD,D.SERIES,CERTNO,BDPID,BCLTDPID,BDPTYPE   
					) A, CLIENT1 C (NOLOCK)
					WHERE
						A.PARTY_CODE = C.CL_CODE
						AND @STATUSNAME = (
						CASE 
							WHEN @STATUSID = 'BRANCH'		THEN C.BRANCH_CD
							WHEN @STATUSID = 'SUBBROKER'	THEN C.SUB_BROKER
							WHEN @STATUSID = 'TRADER'		THEN C.TRADER
							WHEN @STATUSID = 'FAMILY'		THEN C.FAMILY
							WHEN @STATUSID = 'AREA'			THEN C.AREA
							WHEN @STATUSID = 'REGION'		THEN C.REGION
							WHEN @STATUSID = 'CLIENT'		THEN C.CL_CODE
							ELSE 'BROKER'
						END)           
					GROUP BY 
						SETT_NO,SETT_TYPE,PARTY_CODE,SCRIPNAME,SCRIP_CD,SERIES,ISIN,BDPID,BCLTDPID,BDPTYPE,
						ISNULL(C.LONG_NAME,''),
						ISNULL(C.L_ADDRESS1,''),
						ISNULL(C.L_ADDRESS2,''),
						ISNULL(C.L_ADDRESS3,''),
						ISNULL(C.L_CITY,''),
						ISNULL(C.L_STATE,''),
						ISNULL(C.L_NATION,''),
						ISNULL(C.L_ZIP,'')
					HAVING SUM(QTY) > 0           
					ORDER BY SETT_NO,SETT_TYPE,PARTY_CODE,SCRIPNAME,SCRIP_CD,ISIN          
			END        
	END

	FETCH NEXT
	FROM @DELCUR
	INTO @NEWDPID,
		@NEWCLTDPID,
		@NEWDPTYPE

END	        
      
    
