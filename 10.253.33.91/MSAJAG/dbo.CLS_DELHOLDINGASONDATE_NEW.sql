	IF @STATUSID = 'BRANCH'
	BEGIN
		SELECT	@FROMPARTY = MIN(CL_CODE), @TOPARTY = MAX(CL_CODE)
		FROM	CLIENT1 C1 (NOLOCK)
		WHERE	BRANCH_CD = @STATUSNAME
	END
	IF @STATUSID = 'SUBBROKER'
	BEGIN
		SELECT	@FROMPARTY = MIN(CL_CODE), @TOPARTY = MAX(CL_CODE)
		FROM	CLIENT1 C1 (NOLOCK)
		WHERE	SUB_BROKER = @STATUSNAME
