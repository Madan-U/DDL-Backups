-- Object: PROCEDURE dbo.CLS_ADMINLOGIN
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------

create PROCEDURE [dbo].[CLS_ADMINLOGIN]  
 @USER_NAME [varchar](25),  
 @PASSWORD [varchar](100),  
 @REMOTEIP [varchar](20),  
 @DIRSEARCH [varchar](1) = 'N'  
AS  
/*  
 
 EXEC CLS_ADMINLOGIN 'Administrator', 'd', '', 'Y' 


 SELECT * FROM TBLADMIN  

*/  
 SET NOCOUNT ON  
 DECLARE  
  @SHAREDB VARCHAR(50),  
  @SHARESERVER VARCHAR(50),  
  @ACCOUNTDB VARCHAR(50),  
  @ACCOUNTSERVER VARCHAR(50),  
  @SQL VARCHAR(MAX),  
  @AFFROWS INT  
   
 CREATE TABLE #DMATLOGIN  
 (  
  FLDSTATUS VARCHAR(25),  
  USERNAME VARCHAR(51),  
  FLDSTNAME VARCHAR(50),  
  USER_LOGIN VARCHAR(50),  
  PWD_EXPIRY INT,  
  FLDCATEGORY VARCHAR(20),  
  USERAUTOID VARCHAR(25),  
  FLDPWDMINLENGTH SMALLINT,  
  FLDPWDMAXLENGTH SMALLINT,  
  FLDSPCLCHAR CHAR(1),  
  FLDPWDALPHANUMONLY INT,  
  FLDENCRYPTION CHAR(1),  
  FLDBLOCKPWD VARCHAR(255),  
  FLDAUTO BIGINT,  
  FLDUSERID INT,  
  FLDPWDEXPIRY VARCHAR(11),  
  FLDMAXATTEMPT SMALLINT,  
  FLDATTEMPTCNT SMALLINT,  
  MFLDSTATUS SMALLINT,  
  FLDLOGINFLAG SMALLINT,  
  FLDACCESSLVL CHAR(1),  
  FLDIPADD VARCHAR(2000),  
  FLDTIMEOUT SMALLINT,  
  FLDFIRSTLOGIN CHAR(1),  
  FLDFORCELOGOUT SMALLINT,  
  LOG_REASON VARCHAR(200),  
  LOGINIP VARCHAR(20)  
 )  
 
  
 INSERT INTO #DMATLOGIN (FLDSTATUS, USERNAME, FLDSTNAME, PWD_EXPIRY, USERAUTOID,  
       FLDAUTO, FLDUSERID, FLDPWDEXPIRY, FLDMAXATTEMPT, FLDATTEMPTCNT, FLDLOGINFLAG,  
       FLDACCESSLVL, FLDIPADD, FLDTIMEOUT,      LOG_REASON, LOGINIP)  
 VALUES  
  (0, @USER_NAME, '', 0, 0,  
  0, 0, 0, 0, 0, 0,  
  '', '', 0,'', @REMOTEIP)  
  
   
 UPDATE #DMATLOGIN  
 SET  
  FLDPWDMINLENGTH = P.FLDPWDMINLENGTH,  
  FLDPWDMAXLENGTH = P.FLDPWDMAXLENGTH,  
  FLDENCRYPTION = P.FLDENCRYPTION,  
  FLDSPCLCHAR = P.FLDSPCLCHAR,  
  FLDPWDALPHANUMONLY = P.FLDPWDALPHANUMONLY,  
  FLDBLOCKPWD = P.FLDBLOCKPWD  
 FROM  
 TBLGLOBALPARAMS P  


 UPDATE #DMATLOGIN  
 SET  
  USERNAME = A.FLDNAME,  
  FLDCATEGORY = A.FLDSTATUS,  
  FLDSTNAME = CASE WHEN A.FLDSTATUS = '0' THEN 'SUPER ADMINISTRATOR' ELSE A.FLDSTATUS END,  
  FLDAUTO = A.FLDAUTO_ADMIN,  
  FLDPWDEXPIRY = A.PWD_EXPIRY_DATE,  
  PWD_EXPIRY = DATEDIFF(DD, GETDATE(), A.PWD_EXPIRY_DATE),  
  FLDMAXATTEMPT = A.FLDMAXATTEMPT,  
  FLDATTEMPTCNT = A.FLDATTEMPTCNT,  
  --MFLDSTATUS = A.FLDSTATUS,  
  FLDACCESSLVL = A.FLDACCESSLVL,  
  FLDIPADD = A.FLDIPADD  
 FROM  
  TBLADMIN A  
 WHERE  
  A.FLDNAME = @USER_NAME  
  AND A.FLDPASSWORD = @PASSWORD  
  
 
 IF @@ROWCOUNT <= 0  
  BEGIN  
   UPDATE #DMATLOGIN SET LOG_REASON = 'NO SUCH USER EXISTS OR INVALID PASSWORD.'  
   SELECT * FROM #DMATLOGIN  
   RETURN  
  END  
   
 DECLARE  
  @FLDAUTO BIGINT,  
  @FLDUSERID INT,  
  @FLDPWDEXPIRY VARCHAR(11),  
  @FLDMAXATTEMPT SMALLINT,  
  @FLDATTEMPTCNT SMALLINT,  
  @FLDSTATUS SMALLINT,  
  @FLDLOGINFLAG SMALLINT,  
  @FLDACCESSLVL CHAR(1),  
  @FLDIPADD VARCHAR(2000),  
  @FLDTIMEOUT SMALLINT,  
  @LOOPI INT,  
  @SPLITIP VARCHAR(20),  
  @IPLOGIN BIT  
 SELECT  
  @FLDAUTO = ISNULL(FLDAUTO, 0),  
  @FLDUSERID = ISNULL(FLDUSERID, 0),  
  @FLDPWDEXPIRY = ISNULL(FLDPWDEXPIRY, ''),  
  @FLDMAXATTEMPT = ISNULL(FLDMAXATTEMPT, 0),  
  @FLDATTEMPTCNT = ISNULL(FLDATTEMPTCNT, 0),  
  @FLDSTATUS = ISNULL(FLDSTATUS, 0),  
  @FLDLOGINFLAG = ISNULL(FLDLOGINFLAG, 0),  
  @FLDACCESSLVL = ISNULL(FLDACCESSLVL, 'F'),  
  @FLDIPADD = ISNULL(FLDIPADD, ''),  
  @FLDTIMEOUT = ISNULL(FLDTIMEOUT, 0)  
 FROM  
  #DMATLOGIN  
    
 IF @FLDSTATUS <> 0  
  BEGIN  
   UPDATE TBLADMIN SET FLDSTATUS = CASE WHEN @FLDATTEMPTCNT >= @FLDMAXATTEMPT THEN 1 ELSE FLDSTATUS END,  
   FLDATTEMPTCNT = FLDATTEMPTCNT + 1 
   WHERE FLDAUTO_ADMIN = @FLDAUTO  
   UPDATE #DMATLOGIN SET LOG_REASON = 'THE USER ID  IS DISABLED.  KINDLY CONTACT ADMINISTRATOR.'  
   SELECT * FROM #DMATLOGIN  
   RETURN  
   --RAISERROR('THE USER ID  IS DISABLED.  KINDLY CONTACT ADMINISTRATOR.', 16, 1)  
  END  
 IF @FLDATTEMPTCNT > @FLDMAXATTEMPT AND @DIRSEARCH = 'N'  
  BEGIN  
   UPDATE TBLADMIN SET FLDSTATUS = CASE WHEN @FLDATTEMPTCNT >= @FLDMAXATTEMPT THEN 1 ELSE FLDSTATUS END,  
   FLDATTEMPTCNT = FLDATTEMPTCNT + 1 WHERE FLDAUTO_ADMIN = @FLDAUTO  
   UPDATE #DMATLOGIN SET LOG_REASON = 'YOU HAVE EXCEEDED THE MAXIMUM NUMBER OF ATTEMPTS FOR LOGIN.'  
   SELECT * FROM #DMATLOGIN  
   RETURN  
   --RAISERROR('YOU HAVE EXCEEDED THE MAXIMUM NUMBER OF ATTEMPTS FOR LOGIN.', 16, 1)  
  END  
 IF @FLDACCESSLVL <> 'F'  
  BEGIN  
   SET @IPLOGIN = 0  
   IF LEN(@FLDIPADD) > 0  
    BEGIN  
     SET @LOOPI = 1  
     WHILE 1 = 1  
      BEGIN  
       SET @SPLITIP = .dbo.CLS_Piece(@FLDIPADD, '|', @LOOPI)  
       IF @SPLITIP IS NULL OR @SPLITIP = ''  
        BEGIN  
         BREAK  
        END  
       IF LEFT(LTRIM(RTRIM(@REMOTEIP)), LEN(LTRIM(RTRIM(@SPLITIP)))) = LTRIM(RTRIM(@SPLITIP))  
        BEGIN  
         SET @IPLOGIN = 1  
         BREAK  
        END  
       SET @LOOPI = @LOOPI + 1  
      END  
    END  
   IF @IPLOGIN = 0  
    BEGIN  
     UPDATE #DMATLOGIN SET LOG_REASON = 'YOUR IP ADDRESS IS NOT REGISTERED IN THE SYSTEM. KINDLY CONTACT SYSTEM ADMINISTRATOR...'  
     SELECT * FROM #DMATLOGIN  
     RETURN  
     --RAISERROR('YOUR IP ADDRESS IS NOT REGISTERED IN THE SYSTEM. KINDLY CONTACT SYSTEM ADMINISTRATOR...', 16, 1)  
    END  
  END  
  
 IF (SELECT PWD_EXPIRY FROM #DMATLOGIN) <= 0  
  BEGIN  
   UPDATE #DMATLOGIN SET LOG_REASON = 'YOUR PASSWORD IS EXPIRED...'  
   SELECT * FROM #DMATLOGIN  
   RETURN  
   --RAISERROR('YOUR PASSWORD IS EXPIRED...', 16, 1)  
  END  
 DELETE FROM #DMATLOGIN WHERE LOG_REASON = 'NO SUCH USER EXISTS OR INVALID PASSWORD.'  
 SELECT   
  FLDSTATUS, USERNAME, FLDSTNAME, PWD_EXPIRY, USERAUTOID, FLDCATEGORY,  
  FLDAUTO, FLDUSERID, FLDPWDEXPIRY, FLDMAXATTEMPT, FLDATTEMPTCNT, MFLDSTATUS,  
  FLDLOGINFLAG, FLDACCESSLVL, FLDIPADD, FLDTIMEOUT, LOG_REASON, LOGINIP, TODAY=CONVERT(VARCHAR(11),GETDATE(),103) 
 FROM #DMATLOGIN  
 RETURN

GO
