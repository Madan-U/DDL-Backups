--------------------------------------------------

CREATE PROCEDURE [dbo].[PR_CMMARGIN_PENALTY_POST]  
(  
 @MARGIN_DATE VARCHAR(11),  
 @POSTINGFLAG INT,  
 @LEDGERVNO VARCHAR(12),  
 @LEDGERVT VARCHAR(11),  
 @UNAME VARCHAR(20),  
 @LEDGERDT VARCHAR(11)  
)  
AS  
  
DECLARE @GLCODE VARCHAR(10)  
SELECT @GLCODE = ACCODE FROM VALANACCOUNT (NOLOCK) WHERE ACNAME ='MARGIN_SHORTAGE_PENALTY'  
  
IF ISNULL(@GLCODE,'') =''  
BEGIN  
  RAISERROR('GL CODE FOR - MARGIN_SHORTAGE_PENALTY IS NOT AVAILABLE', 16, 1)  
  RETURN  
END  
  
BEGIN TRAN  
  
IF @POSTINGFLAG = 1  
BEGIN  

 DELETE ACCOUNT.DBO.LEDGER WHERE VDT BETWEEN @LEDGERVT AND @LEDGERVT + ' 23:59'  
 AND VTYP = '8' AND BOOKTYPE ='01'  
 AND VNO = @LEDGERVNO  
 
  
 DELETE ACCOUNT.DBO.LEDGER2 WHERE VTYPE = '8' AND BOOKTYPE ='01'  
 AND VNO = @LEDGERVNO  
 
 
 DELETE ACCOUNT.DBO.LEDGER3 WHERE VTYP = '8' AND BOOKTYPE ='01'  
 AND VNO = @LEDGERVNO  

 DELETE ACCOUNT.DBO.LEDGER WHERE VDT BETWEEN @LEDGERVT AND @LEDGERVT + ' 23:59'  
 AND VTYP = '8' AND BOOKTYPE ='01'  
  AND VNO IN (SELECT LEDGER_VNO FROM CMMARGIN_PENALTY_POST_STAT_DETAIL WHERE MARGIN_DATE = @MARGIN_DATE)  
  
 DELETE ACCOUNT.DBO.LEDGER2 WHERE VTYPE = '8' AND BOOKTYPE ='01'  
 AND VNO IN (SELECT LEDGER_VNO FROM CMMARGIN_PENALTY_POST_STAT_DETAIL WHERE MARGIN_DATE = @MARGIN_DATE)  
  
 DELETE ACCOUNT.DBO.LEDGER3 WHERE VTYP = '8' AND BOOKTYPE ='01'  
 AND VNO IN (SELECT LEDGER_VNO FROM CMMARGIN_PENALTY_POST_STAT_DETAIL WHERE MARGIN_DATE = @MARGIN_DATE)  
END  
  
UPDATE CMMARGIN_PENALTY_POST_STAT SET POST_FLAG =0 WHERE MARGIN_DATE = @MARGIN_DATE  

--Print @UNAME
--Print @MARGIN_DATE
--Print @GLCODE
--Print @LEDGERVNO
--Print @LEDGERDT

  
EXEC ACCOUNT.DBO.CMMARGIN_PENALTY_POST @UNAME,8,'01',@MARGIN_DATE,@GLCODE,@LEDGERVNO,@LEDGERDT  
  
COMMIT

GO
