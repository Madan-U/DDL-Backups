CREATE PROCEDURE [dbo].[GET_HOLDING_DETAILS_STATUS]
(
 @FDATE VARCHAR(11),
 @TDATE VARCHAR(11),
 @FPARTY VARCHAR(10)='0',
 @TPARTY VARCHAR(10)='zzzzzzzzz',
 @EXCHANGE VARCHAR(10),
 @FOR VARCHAR(1),
 @WSTATUS VARCHAR(1)
)
AS
--exec GET_HOLDING_DETAILS_STATUS @FDATE='09/09/2019',@TDATE='13/01/2020',@FPARTY='0',@TPARTY='ZZZZZZ',@EXCHANGE='BSE',@FOR='H',@WSTATUS='Y'
--exec GET_HOLDING_DETAILS_STATUS @FDATE='25/12/2021',@TDATE='25/12/2021',@FPARTY='0',@TPARTY='ZZZZZZZZZ',@EXCHANGE='NSE',@WSTATUS='3',@FOR='H'
BEGIN 
	
	SELECT   
	@FDATE = CASE WHEN LEN(@FDATE) = 10 AND CHARINDEX('/', @FDATE) > 0          
	THEN CONVERT(VARCHAR(11), CONVERT(DATETIME, @FDATE, 103), 109)          
	ELSE @FDATE END,
	@TDATE = CASE WHEN LEN(@TDATE) = 10 AND CHARINDEX('/', @TDATE) > 0          
	THEN CONVERT(VARCHAR(11), CONVERT(DATETIME, @TDATE, 103), 109)          
	ELSE @TDATE END   
	
	IF @WSTATUS IN('1','2') 
	BEGIN
		SELECT CDATA = MEMBERPAN+','+ CONVERT(VARCHAR,LASTWEEKDAY,103) +','+ CONVERT(VARCHAR,WEEKDAY,103)+',"'+
		DMAT+'",' + LTRIM(RTRIM(ACCOUNTTYPE))+','+LTRIM(RTRIM(UCC))+','+LTRIM(RTRIM(CLIENTNAME)) + ',' 
		+LTRIM(RTRIM(PAN))+','+LTRIM(RTRIM(ISIN))+','+LTRIM(RTRIM(SECURITYTYPE))+','+CONVERT(VARCHAR,TOTALPLDGQTY)+','
		+CONVERT(VARCHAR,FREEBALQTY)+','+CONVERT(VARCHAR,TOTALQTY) +','+ ACTION +','+ NAMEOFSECORCOMMODITY +','
		+ UNITTYPE +','+ .DBO.FN_GET_HOLDING_CODE_DETAILS(CODE) +','+ STATUS+ ','+ HDRSTATUS +','+ .DBO.FN_GET_HOLDING_CODE_DETAILS(HDRCODE) +',"'+ ACDID +'"'
		FROM TBL_API_NSE_HOLDING
		WHERE LASTWEEKDAY LIKE @FDATE  + '%'
		AND CODE = CASE WHEN @WSTATUS='1' THEN CODE ELSE '601' END
	END
	IF @WSTATUS IN('4') 
	BEGIN
		SELECT top 10 CDATA = MEMBERPAN+','+ CONVERT(VARCHAR,WEEKDAY,105)+',"'+
		DMAT+'",' + LTRIM(RTRIM(ACCOUNTTYPE))+','+LTRIM(RTRIM(UCC))+','+LTRIM(RTRIM(CLIENTNAME)) + ',' 
		+LTRIM(RTRIM(PAN))+','+LTRIM(RTRIM(ISIN))+','+LTRIM(RTRIM(SECURITYTYPE))+',NA,NA,'+CONVERT(VARCHAR,TOTALPLDGQTY)+','
		+CONVERT(VARCHAR,FREEBALQTY)+','+CONVERT(VARCHAR,TOTALQTY) 
		FROM TBL_API_NSE_HOLDING
		WHERE WEEKDAY LIKE @FDATE  + '%' 
	END
	ELSE
	BEGIN
		SELECT CDATA = MEMBERPAN+','+ CONVERT(VARCHAR,LASTWEEKDAY,103) +','+ CONVERT(VARCHAR,WEEKDAY,103)+',"'+
		DMAT+'",' + LTRIM(RTRIM(ACCOUNTTYPE))+','+LTRIM(RTRIM(UCC))+','+LTRIM(RTRIM(CLIENTNAME)) + ',' 
		+LTRIM(RTRIM(PAN))+','+LTRIM(RTRIM(ISIN))+','+LTRIM(RTRIM(SECURITYTYPE))+','+CONVERT(VARCHAR,TOTALPLDGQTY)+','
		+CONVERT(VARCHAR,FREEBALQTY)+','+CONVERT(VARCHAR,TOTALQTY) +','+ ACTION +','+ NAMEOFSECORCOMMODITY +','
		+ UNITTYPE +','+ .DBO.FN_GET_HOLDING_CODE_DETAILS(CODE) +','+ STATUS+ ','+ HDRSTATUS +','+ .DBO.FN_GET_HOLDING_CODE_DETAILS(HDRCODE) +',"' + ACDID +'"'
		FROM TBL_API_NSE_HOLDING
		WHERE LASTWEEKDAY LIKE @FDATE  + '%'
		AND CODE <> '601'
	END
  
END

GO
