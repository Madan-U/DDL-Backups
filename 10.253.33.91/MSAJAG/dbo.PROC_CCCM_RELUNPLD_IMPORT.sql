-- Object: PROCEDURE dbo.PROC_CCCM_RELUNPLD_IMPORT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[PROC_CCCM_RELUNPLD_IMPORT]  
(   
 @TDATE VARCHAR(10),   
 @FILEFOR VARCHAR(20),   
 @FILETYPE VARCHAR(50),   
 @PROGID VARCHAR(50),   
 @UNAME VARCHAR(50),  
 @FILENAME VARCHAR(100) =''   
)  
AS  
  
DECLARE @RECCNT INT,  
 @UPLOADDATE DATETIME, 
 @SNO	INT,
 @MEMBERCODE	VARCHAR(10),
 @EXC_SEG	VARCHAR(50),
 @STRFILENAME VARCHAR(50)
  
SET @RECCNT = 0  

SELECT @MEMBERCODE = MEMBERCODE FROM OWNER

SET @STRFILENAME = 'SECREL_' + @MEMBERCODE + '_' + REPLACE(@TDATE,'/','')

IF (CASE WHEN @FILENAME LIKE '%' + @STRFILENAME + '%' THEN 1 ELSE 0 END) <= 0 
BEGIN
	SELECT strMsgCode = -1, strMsg = 'Invalid File OR selected date.'  
	RETURN
END

IF (CASE WHEN @FILENAME LIKE '%C_SECREL%' OR @FILENAME LIKE '%F_SECREL%'  OR @FILENAME LIKE '%X_SECREL%'THEN 1 ELSE 0 END) > 0
BEGIN
	SET @UPLOADDATE = CONVERT(DATETIME,@TDATE,103) 
END
ELSE
BEGIN
 SELECT strMsgCode = -1, strMsg = 'Invalid File.'  
	RETURN
END

IF @FILENAME LIKE '%C_SECREL%'
	SET @EXC_SEG = 'NSE#CAPITAL'
	
IF @FILENAME LIKE '%F_SECREL%'
	SET @EXC_SEG = 'NSE#FUTURES'

IF @FILENAME LIKE '%X_SECREL%'
	SET @EXC_SEG = 'NSX#FUTURES'
   
SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA  
WHERE PROGID = @PROGID   
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',8))) LIKE 'IN%'  
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',3))) = @MEMBERCODE
 
SELECT * INTO #TBL_CMCM_PLDUNPLD FROM TBL_CMCM_PLDUNPLD
WHERE 1 = 2 

INSERT INTO #TBL_CMCM_PLDUNPLD  
SELECT  @UPLOADDATE, 
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',3))),  
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',4))),  
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',6))),  
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',8))),   
'EQ',   
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',9))),   
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',10))),
.DBO.PIECE(@EXC_SEG,'#',1),.DBO.PIECE(@EXC_SEG,'#',2),
@EXC_SEG,
'D',
@UNAME,  
@UPLOADDATE,
'REL' 
FROM FILEDATA  
WHERE PROGID = @PROGID   
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',8))) LIKE 'IN%'  
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',3))) = @MEMBERCODE
  
DELETE FROM TBL_CMCM_PLDUNPLD  
WHERE TRANSDATE = @UPLOADDATE
AND SEG_DESC = @EXC_SEG
AND FILETYPE = 'REL'

SELECT CLIENTCODE, ISIN, PSNNO, QTY = SUM(CASE WHEN DRCR = 'C' THEN QTY ELSE -QTY END)
INTO #PSN
FROM TBL_CMCM_PLDUNPLD
WHERE TRANSDATE <= @UPLOADDATE
AND SEG_DESC = @EXC_SEG
AND PSNNO IN (SELECT PSNNO FROM #TBL_CMCM_PLDUNPLD)
GROUP BY CLIENTCODE, ISIN, PSNNO
HAVING SUM(CASE WHEN DRCR = 'C' THEN QTY ELSE -QTY END) > 0 

INSERT INTO TBL_CMCM_PLDUNPLD  
SELECT * FROM #TBL_CMCM_PLDUNPLD
WHERE PSNNO IN (SELECT PSNNO FROM #PSN)

IF @RECCNT > 0  
BEGIN  
 SELECT strMsgCode = 1, strMsg = 'File Uploaded Successfully with no of records ' + convert(varchar,@RECCNT)  
END  
ELSE  
BEGIN  
 SELECT strMsgCode = -1, strMsg = 'File not uploaded properly Or data already available in the system.'  
END

GO
