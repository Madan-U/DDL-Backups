CREATE PROC [dbo].[PROC_ALL_EXCHANGE_RELEASE_UPLOAD_ABL] 
(
	@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), 
	@PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(100) = ''
)
AS

DECLARE @RECCNT	INT

DELETE FROM TBL_ALL_EXCH_RELEASE WHERE REPORT_DATE = CONVERT(DATETIME,@TDATE,103)

SELECT CONVERT(DATETIME,@TDATE,103) REPORT_DATE,
DBO.PIECE(FILE_DATA,',',1) PARTY_CODE,
DBO.PIECE(FILE_DATA,',',2) ISIN,
CAST(DBO.PIECE(FILE_DATA,',',3) AS INT) QTY INTO #EXCHREL
FROM FILEDATA WHERE PROGID = @PROGID AND .DBO.PIECE(FILE_DATA,',',2) <> ' Client Code'

INSERT INTO TBL_ALL_EXCH_RELEASE
SELECT REPORT_DATE,PARTY_CODE,ISIN,QTY=SUM(QTY) FROM #EXCHREL WHERE ISIN!='ISIN' GROUP BY REPORT_DATE,PARTY_CODE,ISIN

UPDATE TBL_ALL_REPORT_STATUS SET STATUS ='Completed' WHERE SRNO=3 AND REPORT_DATE = CONVERT(DATETIME,@TDATE,103)

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA WHERE PROGID = @PROGID

DELETE FROM FILEDATA WHERE PROGID = @PROGID

IF @RECCNT > 0 
BEGIN
	SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
	SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

GO
