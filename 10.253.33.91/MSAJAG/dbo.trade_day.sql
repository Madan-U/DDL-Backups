CREATE proc trade_day
(@date datetime)
as

SELECT DISTINCT PARTY_cODE ,SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,120) FROM CMBILLVALAN  with(nolock) WHERE SAUDA_DATE >=@DATE AND SAUDA_DATE <=@DATE+ ' 23:59'
UNION  
SELECT  DISTINCT PARTY_cODE ,SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,120) FROM CMBILLVALAN with(nolock) WHERE SAUDA_DATE >=@DATE AND SAUDA_DATE <=@DATE+ ' 23:59'
UNION  
SELECT  DISTINCT PARTY_cODE,SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,120)  FROM angelfo.nsefo.dbo.fosettlement with(nolock) WHERE SAUDA_DATE >=@DATE AND SAUDA_DATE <=@DATE+ ' 23:59'
UNION  
SELECT  DISTINCT PARTY_cODE,SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,120)  FROM angelfo.nsecurfo.dbo.fosettlement with(nolock) WHERE SAUDA_DATE >=@DATE AND SAUDA_DATE <=@DATE+ ' 23:59'
UNION  
SELECT  DISTINCT PARTY_cODE,SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,120)  FROM angelcommodity.mcDx.dbo.fosettlement with(nolock) WHERE SAUDA_DATE >=@DATE AND SAUDA_DATE <=@DATE+ ' 23:59'
UNION  
SELECT  DISTINCT PARTY_cODE,SAUDA_DATE=CONVERT(VARCHAR(11),SAUDA_DATE,120)  FROM angelcommodity.ncdx.dbo.fosettlement with(nolock) WHERE SAUDA_DATE >=@DATE AND SAUDA_DATE <=@DATE+ ' 23:59'

GO
