CREATE PROC	[dbo].[PROC_CLIENT_COLLATREAL_ALLOC_CMCC_BKUP_01OCT2021]
(
	@RUNDATE VARCHAR(11)
)	
AS

DECLARE
		@PARTY_CODE	VARCHAR(10),
		@EXCHANGE	VARCHAR(3),
		@SEGMENT	VARCHAR(7),
		@NEWEXCHANGE	VARCHAR(3),
		@NEWSEGMENT	VARCHAR(7),
		@LEDBAL		NUMERIC(18,4),
		@NEWLEDBAL		NUMERIC(18,4),
		@DIFFLEDBAL		NUMERIC(18,4),
		@FDAMOUNT		NUMERIC(18,4),
		@SNO	INT,
		@NEWSNO	INT,
		@ACCOUNT_TYPE	VARCHAR(1),
		@ADJCUR		CURSOR,
		@ALLOCCURSOR	CURSOR,
		@SEGCUR		CURSOR
		
SELECT PARTY_CODE, EXCHANGE, SEGMENT, CASH_RET_TM, FD_RET_TM, BG_REC_TM, CASH_WCM, FD_WCM, BG_WCM, CASH_RET_WCM, FD_RET_CM, BG_RET_CM, CASH_WNCL, FD_WNCL, BG_WNCL,BG_RET_TM
INTO #SEGLED
FROM CLIENT_COLLATERAL_REPORTING_DETAILS
WHERE PROCESS_DT = @RUNDATE  
AND EDT_LEDGER > 0 
AND EXCHANGE <> 'MTF' 
AND 1 = 2 

ALTER TABLE #SEGLED
ADD SNO INT IDENTITY(1,1)

CREATE CLUSTERED INDEX IDXDT ON #SEGLED
(
	SNO
)

UPDATE CLIENT_COLLATERAL_REPORTING_DETAILS SET FD_RET_TM = FD_REC_TM, FD_WCM = 0, FD_WNCL = 0, FD_RET_CM = 0,
											   BG_RET_TM = BG_REC_TM, BG_WCM = 0, BG_WNCL = 0, BG_RET_CM = 0,  
CASH_RET_TM = (CASE WHEN EDT_LEDGER < 0 THEN 0 ELSE EDT_LEDGER END),
CASH_RET_WCM = 0,
CASH_WCM = 0,
CASH_WNCL = 0
WHERE PROCESS_DT = @RUNDATE

SET @SEGCUR = CURSOR FOR
SELECT EXCHANGE, SEGMENT, FDAMOUNT, ACCOUNT_TYPE FROM TBL_CMCC_DATA
WHERE TRANSDATE= @RUNDATE AND FDAMOUNT > 0 AND ACCOUNT_TYPE='C' AND ALLOC_LEVEL = 'TM'
ORDER BY FDAMOUNT DESC
OPEN @SEGCUR
FETCH NEXT FROM @SEGCUR INTO @EXCHANGE, @SEGMENT, @FDAMOUNT, @ACCOUNT_TYPE
WHILE @@FETCH_STATUS = 0 
BEGIN   
	TRUNCATE TABLE #SEGLED
