	SELECT SNO, PARTY_CODE, CASH_RET_TM
	FROM #SEGLED 
	ORDER BY CASH_RET_TM DESC
	OPEN @ADJCUR
	FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @LEDBAL
	WHILE @@FETCH_STATUS = 0 
	BEGIN
		IF @LEDBAL >= @FDAMOUNT
		BEGIN 
			UPDATE #SEGLED SET 
			FD_RET_TM = FD_RET_TM + @FDAMOUNT,
			CASH_RET_TM = CASH_RET_TM - @FDAMOUNT
			WHERE SNO = @SNO

			SET @FDAMOUNT = 0 
		END
		ELSE
		BEGIN	
			UPDATE #SEGLED SET  
			FD_RET_TM = FD_RET_TM + @LEDBAL,
			CASH_RET_TM = CASH_RET_TM - @LEDBAL
			WHERE SNO = @SNO

			SET @FDAMOUNT = @FDAMOUNT - @LEDBAL
		END	
		IF @FDAMOUNT <= 0 
		BEGIN
			BREAK;
		END
		FETCH NEXT FROM @ADJCUR INTO @SNO, @PARTY_CODE, @LEDBAL	 
	END
