	SELECT PARTY_CODE, EXCHANGE, SEGMENT, CASH_RET_TM, FD_RET_TM, BG_REC_TM, CASH_WCM, FD_WCM, BG_WCM, CASH_RET_WCM, FD_RET_CM, BG_RET_CM, CASH_WNCL, FD_WNCL, BG_WNCL,BG_RET_TM
