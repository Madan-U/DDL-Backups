SELECT REPORT_DATE,CLTCODE,CLASS_LEDGER=TB_EDT,SCFM_BALANCE=CONVERT(MONEY,0),CASH_EDT_BAL=CONVERT(MONEY,0) FROM #TBEDT
) A
GROUP BY REPORT_DATE,UCC

