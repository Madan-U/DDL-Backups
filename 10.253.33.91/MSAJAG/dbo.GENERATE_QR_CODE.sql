CREATE PROCEDURE [dbo].[GENERATE_QR_CODE]
	@FROMDATE VARCHAR(11),
	@TODATE VARCHAR(11)
AS

IF LEN(@FROMDATE) = 10
BEGIN
	SELECT @FROMDATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @FROMDATE, 103), 109)
	SELECT @TODATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @TODATE, 103), 109)
END


SELECT PARTY_CODE = A.PARTY_CODE, GST_NO = B.GST_NO, INV_NO = A.INV_NO, BROKER_GSTN = '08AAACM6094R1ZP', 
TAXABLE_AMT = SUM(TAXABLE_AMT), IGST = SUM(IGST_AMOUNT), CGST = SUM(CGST_AMOUNT), SGST = SUM(SGST_AMOUNT), 
IRN = isnull(IRN, 'Angel Broking Ltd client A/C / ICICI BANK / 000405024925 / ICIC0000004 / NARIMAN POINT')
INTO #INVOICE_DATA
FROM TBL_GST_INVOICE A LEFT OUTER JOIN INVOICE_IRN_DETAILS I 
ON A.PARTY_CODE = I.PARTY_CODE AND A.INV_NO = I.INV_NO, 
CLIENT_DETAILS  B (NOLOCK)
WHERE A.INVOICE_DATE BETWEEN @FROMDATE AND @TODATE
AND A.PARTY_CODE = B.PARTY_CODE
GROUP BY A.PARTY_CODE, B.GST_NO, A.INV_NO, 
isnull(IRN, 'Angel Broking Ltd client A/C / ICICI BANK / 000405024925 / ICIC0000004 / NARIMAN POINT')

TRUNCATE TABLE GST_QRCODE_DETAILS

INSERT INTO GST_QRCODE_DETAILS
SELECT PARTY_CODE, INV_NO, QR_CODE = 'Trade.angelbroking.com/login?p1=' + GST_NO + '&p2=' + BROKER_GSTN + '&p3=' + INV_NO + '&p4=' + CONVERT(VARCHAR(10), CONVERT(DATETIME, @TODATE, 109), 103)  + '&p5=' + CONVERT(VARCHAR, TAXABLE_AMT)  + '&p6=' + CONVERT(VARCHAR, TAXABLE_AMT) + '&p7=' + CONVERT(VARCHAR, IGST)  + '&p8=' + CONVERT(VARCHAR, CGST)  + '&p9=' + CONVERT(VARCHAR, SGST)  + '&p10=' + CONVERT(VARCHAR, TAXABLE_AMT + IGST + CGST + SGST)  + '&p11=' + IRN, Getdate(), IRN
FROM #INVOICE_DATA

GO
