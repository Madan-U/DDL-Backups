		DBO.PIECE(FILE_DATA,',',31),
		DBO.PIECE(FILE_DATA,',',32),
		DBO.PIECE(FILE_DATA,',',33)
	FROM STAMPDUTY_FILEDATA  
	-- WHERE  
   --SESSION_ID= @SESSIONID   
   --AND LEFT(FILE_DATA,1) <> 'Sgmt'

    UPDATE A SET STTLMTP = PROCESS_VALUE
	FROM TBL_OBLIGATION_UDIFF A,MSAJAG..TBL_STANDARD_VALUES B
		WHERE A.STTLMTP = B.FIELD_VALUE
		AND FIELD_DESC='SETTLEMENTTYPE'
		AND EXCHANGE='NSE'
		AND SEGMENT='CAPITAL'




  INSERT INTO TBL_NSE_OBL  
  SELECT   
		SETT_NO = SCTIESSTTLMTXID,
		SETT_TYPE = STTLMTP,
		TM_CODE = BRKRORCTDNPTCPTID,
