BEGIN

 DECLARE @RECCNT              INT
 

  TRUNCATE TABLE TBL_OBLIGATION_UDIFF
  DELETE FROM TBL_NSE_OBL WHERE SETT_NO = @SETTNO AND SETT_TYPE = @SETTTYPE 
  
  
