  FROM TBL_OBLIGATION_UDIFF
 WHERE SCTIESSTTLMTXID = @SETTNO AND STTLMTP = @SETTTYPE




SELECT @RECCNT = ISNULL(COUNT(1),0) FROM TBL_NSE_OBL WHERE SETT_NO = @SETTNO AND SETT_TYPE = @SETTTYPE 
IF @RECCNT > 0
BEGIN
       SELECT STRMSGCODE = 1, STRMSG = 'FILE UPLOADED SUCCESSFULLY WITH NO OF RECORD(S) ' + CONVERT(VARCHAR,@RECCNT)
END
ELSE
BEGIN
       SELECT STRMSGCODE = -1, STRMSG = 'PLEASE CHECK AND IMPORT CORRECT FILE'
END

END

GO
