CREATE proc [dbo].[to_chk_peak] 
(
	@margindate varchar(11)
)
as
-- exec to_chk_peak 'dec  8 2020'
DECLARE @MRGCUR CURSOR , 
@MARGINDATENEW VARCHAR(11),
@STRSQL VARCHAR(2000),
@FORREPORTING	VARCHAR(1)
 
SET @MARGINDATENEW = @MARGINDATE
SET @FORREPORTING = 'Y' 

SELECT MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, TDAY_MARGIN, FILECOUNTER=0, PEAK_EOD = CONVERT(NUMERIC(18,4),0),
DEBITBILL= CONVERT(NUMERIC(18,4),0), LedBal=CONVERT(NUMERIC(18,4),0) 
INTO #PEAKSNAPSHOT FROM TBL_COMBINE_REPORTING_PEAK_DETAIL
WHERE 1 =2 

SELECT MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, TDAY_MARGIN, FILECOUNTER=0 INTO #PEAKEOD FROM TBL_COMBINE_REPORTING_PEAK_DETAIL
WHERE 1 =2 

SET @MRGCUR = CURSOR FOR             
    SELECT             
    MARGSQL = ' INSERT INTO #PEAKSNAPSHOT (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, TDAY_MARGIN, FILECOUNTER )'    
    +  ' SELECT ''' + @MARGINDATE + ''',''' + T.EXCHANGE + ''', ''' + T.SEGMENT + ''',PARTY_CODE,MARGIN_CALL=SUM(ISNULL(' + (CASE WHEN @FORREPORTING = 'Y' THEN REPLACE(MARGINCOLUMNSNONCASH,'ADDMARGIN',0) ELSE MARGINCOLUMNSNONCASH END) + ',0)),FILECOUNTER FROM ' +              
    SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE +              
   ' WHERE ' + MARGINDATEFIELD + ' = (SELECT MAX(' + MARGINDATEFIELD + ') FROM ' + SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE + ' WHERE ' + MARGINDATEFIELD + ' <= ''' +  @MARGINDATENEW +' 23:59'') GROUP BY PARTY_CODE, FILECOUNTER'            
    FROM V2_TBL_EXCHANGE_MARGIN_REPORT_PEAK_INTRADAY T, PRADNYA..MULTICOMPANY M            
    WHERE T.EXCHANGE = M.EXCHANGE            
    AND T.SEGMENT = M.SEGMENT     
    AND M.SHARESERVER <> 'ANGELDEMAT'          
    AND T.SEGMENT <> 'POA'          
    and @margindate between FROM_DATE and TO_DATE
   OPEN @MRGCUR            
  
   FETCH NEXT FROM @MRGCUR INTO @STRSQL            
   WHILE @@FETCH_STATUS = 0                   
   BEGIN                
  
    EXEC (@STRSQL)        
	--PRINT @STRSQL
    FETCH NEXT FROM @MRGCUR INTO @STRSQL            
  
   END             
  
   CLOSE @MRGCUR            
  
   DEALLOCATE @MRGCUR    
  
  
SET @MRGCUR = CURSOR FOR             
    SELECT             
    MARGSQL = ' INSERT INTO #PEAKEOD (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, TDAY_MARGIN, FILECOUNTER )'    
    +  ' SELECT ''' + @MARGINDATE + ''',''' + T.EXCHANGE + ''', ''' + T.SEGMENT + ''',PARTY_CODE,MARGIN_CALL=SUM(ISNULL(' + (CASE WHEN @FORREPORTING = 'Y' THEN REPLACE(MARGINCOLUMNSNONCASH,'ADDMARGIN',0) ELSE MARGINCOLUMNSNONCASH END) + ',0)),FILECOUNTER=0 FROM ' +              
    SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE +              
   ' WHERE ' + MARGINDATEFIELD + ' = (SELECT MAX(' + MARGINDATEFIELD + ') FROM ' + SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE + ' WHERE ' + MARGINDATEFIELD + ' <= ''' +  @MARGINDATE +' 23:59'') GROUP BY PARTY_CODE'            
    FROM V2_TBL_EXCHANGE_MARGIN_REPORT_PEAK T, PRADNYA..MULTICOMPANY M            
    WHERE T.EXCHANGE = M.EXCHANGE            
    AND T.SEGMENT = M.SEGMENT     
    AND M.SHARESERVER <> 'ANGELDEMAT'          
    AND T.SEGMENT <> 'POA'           
	and MARGINCOLUMNSNONCASH = 'peakmargin'
	and @margindate between FROM_DATE and TO_DATE
   OPEN @MRGCUR            
  
   FETCH NEXT FROM @MRGCUR INTO @STRSQL            
   WHILE @@FETCH_STATUS = 0                   
   BEGIN                
  
    EXEC (@STRSQL)        
	--PRINT @STRSQL
    FETCH NEXT FROM @MRGCUR INTO @STRSQL            
  
   END             
  
   CLOSE @MRGCUR            
  
   DEALLOCATE @MRGCUR    
  
 
SET @MRGCUR = CURSOR FOR             
    SELECT             
    MARGSQL = ' INSERT INTO #PEAKSNAPSHOT (MARGINDATE, EXCHANGE, SEGMENT, PARTY_CODE, TDAY_MARGIN, FILECOUNTER )'    
    +  ' SELECT ''' + @MARGINDATE + ''',''' + T.EXCHANGE + ''', ''' + T.SEGMENT + ''',PARTY_CODE,MARGIN_CALL=SUM(ISNULL(' + (CASE WHEN @FORREPORTING = 'Y' THEN REPLACE(MARGINCOLUMNSNONCASH,'ADDMARGIN',0) ELSE MARGINCOLUMNSNONCASH END) + ',0)),FILECOUNTER=5 FROM ' +              
    SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE +              
   ' WHERE ' + MARGINDATEFIELD + ' = (SELECT MAX(' + MARGINDATEFIELD + ') FROM ' + SHARESERVER + '.'+ SHAREDB + '.DBO.' + MARGINTABLE + ' WHERE ' + MARGINDATEFIELD + ' <= ''' +  @MARGINDATE +' 23:59'') GROUP BY PARTY_CODE'            
    FROM V2_TBL_EXCHANGE_MARGIN_REPORT T, PRADNYA..MULTICOMPANY M            
    WHERE T.EXCHANGE = M.EXCHANGE            
    AND T.SEGMENT = M.SEGMENT     
    AND M.SHARESERVER <> 'ANGELDEMAT'          
    AND T.SEGMENT <> 'POA'           
	--and MARGINCOLUMNSNONCASH = 'peakmargin'
	and t.Exchange not in('mcx','ncx')
   OPEN @MRGCUR            
  
   FETCH NEXT FROM @MRGCUR INTO @STRSQL            
   WHILE @@FETCH_STATUS = 0                   
   BEGIN                
  
    EXEC (@STRSQL)        
	--PRINT @STRSQL
    FETCH NEXT FROM @MRGCUR INTO @STRSQL            
  
   END             
  
   CLOSE @MRGCUR            
  
   DEALLOCATE @MRGCUR  
      
  UPDATE #PEAKSNAPSHOT SET TDAY_MARGIN = TDAY_MARGIN * PEAK_PER / 100
  FROM  V2_TBL_EXCHANGE_MARGIN_REPORT_PEAK_INTRADAY P
  WHERE #PEAKSNAPSHOT.EXCHANGE = P.EXCHANGE 
  AND #PEAKSNAPSHOT.SEGMENT = P.SEGMENT
  and @MARGINDATE BETWEEN FROM_DATE AND TO_DATE 
  
  UPDATE #PEAKEOD SET TDAY_MARGIN = TDAY_MARGIN * PEAK_PER / 100
  FROM  V2_TBL_EXCHANGE_MARGIN_REPORT_PEAK P
  WHERE #PEAKEOD.EXCHANGE = P.EXCHANGE 
  AND #PEAKEOD.SEGMENT = P.SEGMENT
  
  UPDATE #PEAKSNAPSHOT SET PEAK_EOD = 0

  UPDATE #PEAKSNAPSHOT SET PEAK_EOD = P.TDAY_MARGIN
  FROM #PEAKEOD P
  WHERE P.PARTY_CODE = #PEAKSNAPSHOT.PARTY_CODE
  AND #PEAKSNAPSHOT.FILECOUNTER = 1
  AND P.EXCHANGE = 'NSE' AND P.SEGMENT = 'CAPITAL'
  AND P.EXCHANGE = #PEAKSNAPSHOT.EXCHANGE AND P.SEGMENT = #PEAKSNAPSHOT.SEGMENT 

  SELECT DISTINCT EXCHANGE, SEGMENT, FILECOUNTER FROM #PEAKSNAPSHOT
  ORDER BY EXCHANGE, SEGMENT, FILECOUNTER

  SELECT MARGINDATE, PARTY_CODE, TDAY_MARGIN=SUM(TDAY_MARGIN), FILECOUNTER 
  INTO #MAXPEAK
  FROM #PEAKSNAPSHOT
  WHERE FILECOUNTER <= 4
  GROUP BY MARGINDATE, PARTY_CODE,FILECOUNTER 

  SELECT EXCHANGE, SEGMENT, FILECOUNTER = MAX(FILECOUNTER) INTO #PEAKSEGMENT FROM #PEAKSNAPSHOT 
  WHERE EXCHANGE NOT IN ('MCX', 'NCX') 
  GROUP BY EXCHANGE, SEGMENT 

  DECLARE @FILECNT INT,
		  @FILECUR	CURSOR

  SET @FILECUR = CURSOR FOR 
  SELECT DISTINCT FILECOUNTER FROM #PEAKSNAPSHOT
  WHERE FILECOUNTER > 4 AND EXCHANGE IN ('MCX', 'NCX') 
  ORDER BY 1
  OPEN @FILECUR
  FETCH NEXT FROM @FILECUR INTO @FILECNT
  WHILE @@FETCH_STATUS = 0 
  BEGIN
	INSERT INTO #PEAKSEGMENT
	SELECT 'MCX', 'FUTURES', @FILECNT 
	INSERT INTO #PEAKSEGMENT
	SELECT 'NCX', 'FUTURES', @FILECNT 

	INSERT INTO #MAXPEAK
	SELECT MARGINDATE, PARTY_CODE, TDAY_MARGIN=SUM(TDAY_MARGIN),FILECOUNTER=@FILECNT
	FROM #PEAKSNAPSHOT P, #PEAKSEGMENT P1
	WHERE P.EXCHANGE = P1.EXCHANGE 
	AND P.SEGMENT = P1.SEGMENT 
	AND P.FILECOUNTER = P1.FILECOUNTER 
	GROUP BY MARGINDATE, PARTY_CODE

	FETCH NEXT FROM @FILECUR INTO @FILECNT
  END
  CLOSE @FILECUR
  DEALLOCATE @FILECUR 

  SELECT MARGINDATE, PARTY_CODE, TDAY_MARGIN = MAX(TDAY_MARGIN), EOD_PEAK=CONVERT(NUMERIC(18,4),0) 
  INTO #PEAK FROM (
  SELECT MARGINDATE, PARTY_CODE, TDAY_MARGIN=SUM(TDAY_MARGIN),FILECOUNTER FROM #MAXPEAK 
  GROUP BY MARGINDATE, PARTY_CODE,FILECOUNTER ) A 
  GROUP BY MARGINDATE, PARTY_CODE

  /*
  SELECT MARGINDATE, EXCHANGE, SEGMENT, FILECOUNTER = MAX(FILECOUNTER)
  INTO #PEAKCNTR FROM #PEAKSNAPSHOT
  GROUP BY MARGINDATE, EXCHANGE, SEGMENT

  SELECT P.MARGINDATE, P.PARTY_CODE, TDAY_MARGIN=SUM(P.TDAY_MARGIN)
  INTO #PEAKFILE
  FROM #PEAKSNAPSHOT P, #PEAKCNTR C
  WHERE P.MARGINDATE = C.MARGINDATE 
  AND P.EXCHANGE = P.EXCHANGE 
  AND P.SEGMENT = P.SEGMENT 
  AND P.FILECOUNTER = C.FILECOUNTER 

  UPDATE #PEAK SET TDAY_MARGIN = (CASE WHEN #PEAK.TDAY_MARGIN < P.TDAY_MARGIN THEN #PEAK.TDAY_MARGIN ELSE P.TDAY_MARGIN END)
  FROM #PEAKFILE P
  WHERE P.PARTY_CODE = #PEAK.PARTY_CODE 
  */
  
  SELECT cltcode, vamt=sum(vamt) into #dbill from account.dbo.ledger l,  #PEAKSNAPSHOT p
  where edt like @margindate + '%' and vtyp =15 and drcr = 'D'
  and p.party_code = l.cltcode and P.EXCHANGE = 'NSE' AND P.SEGMENT = 'CAPITAL'
  and PEAK_EOD = P.TDAY_MARGIN and TDAY_MARGIN > 0
  group by cltcode

  UPDATE #PEAKSNAPSHOT sET DEBItBILL = vAMT 
  fROM #dbill
  where #dbill.cltcode = party_code
   
  UPDATE #PEAKSNAPSHOT SET LEDBAL = TDAY_LEDGER FROM TBL_COMBINE_REPORTING_PEAK_DETAIL T
  WHERE T.MARGINDATE = @MARGINDATE AND #PEAKSNAPSHOT.PARTY_CODE = T.PARTY_CODE AND T.EXCHANGE = #PEAKSNAPSHOT.EXCHANGE 
  AND T.SEGMENT = #PEAKSNAPSHOT.SEGMENT  AND T.EXCHANGE='NSE' AND T.SEGMENT ='CAPITAL'
  AND TDAY_LEDGER is NOT NULL 

  sELECT * fROM #PEAKSNAPSHOT
  wHERE PARTY_CODE='N41633'
  sELECT * fROM #PEAK 
  wHERE PARTY_CODE='N41633'
   
  DROP TABLE #PEAK 
  DROP TABLE #PEAKSNAPSHOT

GO
