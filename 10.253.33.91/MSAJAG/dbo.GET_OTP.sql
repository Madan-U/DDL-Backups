CREATE PROC [dbo].[GET_OTP]
(
	@OPT_LEN INT
)
AS 
/*
GET_OTP  2
SELECT OTP = CONVERT(VARCHAR(8),'05683620748785')

*/
DECLARE @SQL VARCHAR(100)
IF @OPT_LEN <= 1
BEGIN
	SELECT OTP  = 'OTP CAN BE LESS THAN OR EQUAL TO 1'
	RETURN 
END
ELSE IF @OPT_LEN > 10
 BEGIN
	SELECT OTP  = 'OTP CAN BE LESS THAN 10'
	RETURN 
 END	


DECLARE @RAND VARCHAR(100)
DECLARE @RAND1 VARCHAR(100)
SELECT @RAND = RAND() 
SELECT @RAND1 = RAND() 
SELECT @RAND = REPLACE(@RAND,'.','')
SELECT @RAND1 = REPLACE(@RAND1,'.','')


SET @SQL = "SELECT OTP = CONVERT(VARCHAR(" + CONVERT(VARCHAR,@OPT_LEN) + "),'" + @RAND + @RAND1 + "')"
EXEC(@SQL)

GO
