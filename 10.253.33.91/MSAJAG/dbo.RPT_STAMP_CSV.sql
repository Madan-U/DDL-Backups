-- Object: PROCEDURE dbo.RPT_STAMP_CSV
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------



CREATE PROC [dbo].[RPT_STAMP_CSV] 
(
	@SAUDA_DATE VARCHAR(11)
)
AS

-- EXEC RPT_STAMP_CSV @SAUDA_DATE ='JAN  6 2020'

SELECT * INTO #TBL_STAMP_EXCHG FROM TBL_STAMP_EXCHG
WHERE SAUDA_DATE = @SAUDA_DATE 

SELECT * INTO #TBL_STAMP_DATA FROM TBL_STAMP_DATA
WHERE SAUDA_DATE = @SAUDA_DATE 

SELECT * INTO #NSE_CMBILLVALAN FROM MSAJAG.DBO.CMBILLVALAN
WHERE SAUDA_DATE >= @SAUDA_DATE AND SAUDA_DATE <= @SAUDA_DATE + ' 23:59'

SELECT * INTO #BSE_CMBILLVALAN FROM BSEDB.DBO.CMBILLVALAN
WHERE SAUDA_DATE >= @SAUDA_DATE AND SAUDA_DATE <= @SAUDA_DATE + ' 23:59'
AND SETT_TYPE IN (SELECT BSE_SETT_TYPE FROM TBL_INTEROP_SETT_TYPE
WHERE @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE)

ALTER TABLE #NSE_CMBILLVALAN 
ALTER COLUMN SERIES VARCHAR(3)

UPDATE #NSE_CMBILLVALAN SET COMPANYNAME = L_STATE FROM CLIENT1 
WHERE CL_CODE = PARTY_CODE

UPDATE #BSE_CMBILLVALAN SET COMPANYNAME = L_STATE FROM CLIENT1 
WHERE CL_CODE = PARTY_CODE

UPDATE #BSE_CMBILLVALAN SET SETT_NO = S.SETT_NO, SETT_TYPE = S.SETT_TYPE
FROM SETT_MST S, TBL_INTEROP_SETT_TYPE O
WHERE @SAUDA_DATE BETWEEN S.START_DATE AND S.END_DATE
AND S.SETT_TYPE = O.NSE_SETT_TYPE
AND #BSE_CMBILLVALAN.SETT_TYPE = BSE_SETT_TYPE
AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE

UPDATE #BSE_CMBILLVALAN SET ISIN = M.ISIN FROM TBL_EXCHG_SCRIP_MAP M
WHERE M.BSECODE = SCRIP_CD AND SERIES = 'BSE' AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE 

UPDATE #BSE_CMBILLVALAN SET SCRIP_CD = NSE_SCRIP_CD, SERIES = NSE_SERIES FROM TBL_EXCHG_SCRIP_MAP M
WHERE M.ISIN = #BSE_CMBILLVALAN.ISIN AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE 
AND #BSE_CMBILLVALAN.SCRIP_CD = BSECODE

SELECT SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = SUM(TOTALSTAMP), BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = CONVERT(NUMERIC(18,4),0)
INTO #SUMMARY
FROM #TBL_STAMP_EXCHG
WHERE RECTYPE = 10
GROUP BY SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

INSERT INTO #SUMMARY
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME='', SETT_NO='', SETT_TYPE='', PARTY_CODE='', SCRIP_CD='', SERIES='', 
EXSTAMP = 0, BOSTAMP_CALC = SUM(TOTALSTAMP), BOSTAMP_MIS = 0
FROM #TBL_STAMP_DATA
GROUP BY LEFT(SAUDA_DATE,11)

INSERT INTO #SUMMARY
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME='', SETT_NO='', SETT_TYPE='', PARTY_CODE='', SCRIP_CD='', SERIES='', 
EXSTAMP = 0, BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = SUM(BROKER_CHRG)
FROM #NSE_CMBILLVALAN
GROUP BY LEFT(SAUDA_DATE,11)

INSERT INTO #SUMMARY
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME='', SETT_NO='', SETT_TYPE='', PARTY_CODE='', SCRIP_CD='', SERIES='', 
EXSTAMP = 0, BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = SUM(BROKER_CHRG)
FROM #BSE_CMBILLVALAN
GROUP BY LEFT(SAUDA_DATE,11)

SELECT FILENAME = 'CS_SUM_' + CONVERT(VARCHAR,SAUDA_DATE,112) + '.CSV' , SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = SUM(EXSTAMP), BOSTAMP_CALC = SUM(BOSTAMP_CALC), BOSTAMP_MIS = SUM(BOSTAMP_MIS),
MISMATCH = (CASE WHEN SUM(EXSTAMP) <> SUM(BOSTAMP_CALC) 
			  OR SUM(EXSTAMP)<> SUM(BOSTAMP_MIS) THEN 'YES' ELSE 'NO' END)
FROM #SUMMARY
GROUP BY SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

DROP TABLE #SUMMARY

SELECT SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE='', SCRIP_CD, SERIES, 
EXSTAMP = SUM(TOTALSTAMP), BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = CONVERT(NUMERIC(18,4),0)
INTO #SUMMARY1
FROM #TBL_STAMP_EXCHG
WHERE RECTYPE = 20
GROUP BY SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, SCRIP_CD, SERIES

INSERT INTO #SUMMARY1
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=L_STATE, SETT_NO='', SETT_TYPE='', PARTY_CODE='', SCRIP_CD='', SERIES='', 
EXSTAMP = 0, BOSTAMP_CALC = SUM(TOTALSTAMP), BOSTAMP_MIS = 0
FROM #TBL_STAMP_DATA
GROUP BY LEFT(SAUDA_DATE,11),L_STATE

INSERT INTO #SUMMARY1
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=COMPANYNAME, SETT_NO='', SETT_TYPE='', PARTY_CODE='', SCRIP_CD='', SERIES='', 
EXSTAMP = 0, BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = SUM(BROKER_CHRG)
FROM #NSE_CMBILLVALAN
GROUP BY LEFT(SAUDA_DATE,11), COMPANYNAME 

INSERT INTO #SUMMARY1
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=COMPANYNAME, SETT_NO='', SETT_TYPE='', PARTY_CODE='', SCRIP_CD='', SERIES='', 
EXSTAMP = 0, BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = SUM(BROKER_CHRG)
FROM #BSE_CMBILLVALAN
GROUP BY LEFT(SAUDA_DATE,11), COMPANYNAME 

SELECT FILENAME = 'CS_SUM_STATE_' + CONVERT(VARCHAR,SAUDA_DATE,112) + '.CSV' , SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103), STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = SUM(EXSTAMP), BOSTAMP_CALC = SUM(BOSTAMP_CALC), BOSTAMP_MIS = SUM(BOSTAMP_MIS),
MISMATCH = (CASE WHEN SUM(EXSTAMP) <> SUM(BOSTAMP_CALC) 
			  OR SUM(EXSTAMP)<> SUM(BOSTAMP_MIS) THEN 'YES' ELSE 'NO' END)
FROM #SUMMARY1
GROUP BY SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

DROP TABLE #SUMMARY1

SELECT SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = SUM(TOTALSTAMP), BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = CONVERT(NUMERIC(18,4),0)
INTO #SUMMARY2
FROM #TBL_STAMP_EXCHG
WHERE RECTYPE = 20
GROUP BY SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

INSERT INTO #SUMMARY2
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=L_STATE, SETT_NO='', SETT_TYPE='', PARTY_CODE, SCRIP_CD='', SERIES='', 
EXSTAMP = 0, BOSTAMP_CALC = SUM(TOTALSTAMP), BOSTAMP_MIS = 0
FROM #TBL_STAMP_DATA
GROUP BY LEFT(SAUDA_DATE,11), L_STATE, PARTY_CODE 

INSERT INTO #SUMMARY2
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=COMPANYNAME, SETT_NO='', SETT_TYPE='', PARTY_CODE, SCRIP_CD='', SERIES='',  
EXSTAMP = 0, BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = SUM(BROKER_CHRG)
FROM #NSE_CMBILLVALAN
GROUP BY LEFT(SAUDA_DATE,11), COMPANYNAME, PARTY_CODE 

INSERT INTO #SUMMARY2
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=COMPANYNAME, SETT_NO='', SETT_TYPE='', PARTY_CODE, SCRIP_CD='', SERIES='',  
EXSTAMP = 0, BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = SUM(BROKER_CHRG)
FROM #BSE_CMBILLVALAN
GROUP BY LEFT(SAUDA_DATE,11), COMPANYNAME, PARTY_CODE 

SELECT FILENAME = 'CS_SUM_PARTY_' + CONVERT(VARCHAR,SAUDA_DATE,112) + '.CSV' , SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103), STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = SUM(EXSTAMP), BOSTAMP_CALC = SUM(BOSTAMP_CALC), BOSTAMP_MIS = SUM(BOSTAMP_MIS),
MISMATCH = (CASE WHEN SUM(EXSTAMP) <> SUM(BOSTAMP_CALC) 
			  OR SUM(EXSTAMP)<> SUM(BOSTAMP_MIS) THEN 'YES' ELSE 'NO' END)
FROM #SUMMARY2
GROUP BY SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

DROP TABLE #SUMMARY2

SELECT SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = SUM(TOTALSTAMP), BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = CONVERT(NUMERIC(18,4),0)
INTO #SUMMARY3
FROM #TBL_STAMP_EXCHG
WHERE RECTYPE = 30
GROUP BY SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

INSERT INTO #SUMMARY3
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=L_STATE, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = 0, BOSTAMP_CALC = SUM(TOTALSTAMP), BOSTAMP_MIS = 0
FROM #TBL_STAMP_DATA
GROUP BY LEFT(SAUDA_DATE,11), L_STATE, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

INSERT INTO #SUMMARY3
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=COMPANYNAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = 0, BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = SUM(BROKER_CHRG)
FROM #NSE_CMBILLVALAN
GROUP BY LEFT(SAUDA_DATE,11), COMPANYNAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

INSERT INTO #SUMMARY3
SELECT SAUDA_DATE=LEFT(SAUDA_DATE,11), STATE_NAME=COMPANYNAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = 0, BOSTAMP_CALC = CONVERT(NUMERIC(18,4),0), BOSTAMP_MIS = SUM(BROKER_CHRG)
FROM #BSE_CMBILLVALAN
GROUP BY LEFT(SAUDA_DATE,11), COMPANYNAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

SELECT FILENAME = 'CS_DET_' + CONVERT(VARCHAR,SAUDA_DATE,112) + '.CSV' , SAUDA_DATE=CONVERT(VARCHAR,SAUDA_DATE,103), STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, 
EXSTAMP = SUM(EXSTAMP), BOSTAMP_CALC = SUM(BOSTAMP_CALC), BOSTAMP_MIS = SUM(BOSTAMP_MIS),
MISMATCH = (CASE WHEN SUM(EXSTAMP) <> SUM(BOSTAMP_CALC) 
			  OR SUM(EXSTAMP)<> SUM(BOSTAMP_MIS) THEN 'YES' ELSE 'NO' END)
FROM #SUMMARY3
GROUP BY SAUDA_DATE, STATE_NAME, SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES

DROP TABLE #SUMMARY3

DROP TABLE #TBL_STAMP_EXCHG
DROP TABLE #NSE_CMBILLVALAN
DROP TABLE #BSE_CMBILLVALAN
DROP TABLE #TBL_STAMP_DATA

GO
