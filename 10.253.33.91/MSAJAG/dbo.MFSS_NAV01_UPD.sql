CREATE  PROC [dbo].[MFSS_NAV01_UPD] (@TRADEDATE VARCHAR(11), @CMFLAG INT = 0) AS

IF @TRADEDATE =''
BEGIN
	 RETURN 
END

IF (SELECT COUNT(1) FROM MFSS_NAV01_TMP) =0
BEGIN
	 RETURN 
END


CREATE TABLE #MFSS_NAV01
(
	ISIN_GROWTH VARCHAR(16),
	ISIN_REINVESTMENT VARCHAR(16),
	NET_ASSET_VALUE NUMERIC(18,4),
	SCHEME_NAME	VARCHAR(200),
	SCHEME_CODE VARCHAR(12)
)

INSERT INTO #MFSS_NAV01
SELECT 
	ISIN_GROWTH			= ISNULL(DBO.PIECE(FILEDATA,';',2),'') ,
	ISIN_REINVESTMENT	= ISNULL(DBO.PIECE(FILEDATA,';',3),'') ,
	NET_ASSET_VALUE		= CONVERT(NUMERIC(18,4),ISNULL(DBO.PIECE(FILEDATA,';',5),0)),
	SCHEME_NAME			= ISNULL(DBO.PIECE(FILEDATA,';',4),''), 
	SCHEME_CODE			= ISNULL(DBO.PIECE(FILEDATA,';',1),'')
FROM MFSS_NAV01_TMP 
WHERE CHARINDEX('INF',FILEDATA) > 0
AND CHARINDEX('N.A.',FILEDATA) = 0

DELETE MFSS_NAV01 WHERE NAV_DATE = @TRADEDATE

INSERT INTO MFSS_NAV01
SELECT @TRADEDATE,ISIN_GROWTH,NET_ASSET_VALUE,SCHEME_NAME,SCHEME_CODE+'_1'
FROM #MFSS_NAV01 WHERE NET_ASSET_VALUE <> 0
AND LTRIM(RTRIM(ISIN_GROWTH)) <> ''
AND LTRIM(RTRIM(ISIN_GROWTH)) <> '-'
AND LTRIM(RTRIM(ISIN_GROWTH)) <> '.'

INSERT INTO MFSS_NAV01
SELECT @TRADEDATE,ISIN_REINVESTMENT,NET_ASSET_VALUE,SCHEME_NAME,SCHEME_CODE+'_2'
FROM #MFSS_NAV01 WHERE NET_ASSET_VALUE <> 0
AND LTRIM(RTRIM(ISIN_REINVESTMENT)) <> ''
AND LTRIM(RTRIM(ISIN_REINVESTMENT)) <> '-'
AND LTRIM(RTRIM(ISIN_REINVESTMENT)) <> '.'



IF @CMFLAG = 1 
BEGIN
	INSERT INTO MULTIISIN
	SELECT DISTINCT SCHEME_CODE, 'MF', ISIN, VALID = 1
	FROM MFSS_NAV01  WHERE NAV_DATE = @TRADEDATE
	AND ISIN NOT IN (SELECT ISIN FROM MULTIISIN WHERE VALID = 1)

	INSERT INTO CLOSING (MARKET, SCRIP_CD, SERIES, CL_RATE, SYSDATE)
	SELECT MARKET='NORMAL', SCRIP_CD = SCHEME_CODE, 
	SERIES = 'MF', NAV_VALUE,  @TRADEDATE
	FROM MFSS_NAV01 C
	WHERE NAV_DATE = @TRADEDATE
	AND SCHEME_CODE NOT IN (SELECT SCRIP_CD FROM CLOSING WHERE SYSDATE = @TRADEDATE)

	CREATE TABLE #SCRIP
	(
		SNO	INT IDENTITY(1,1),
		SCRIP_CD	VARCHAR(12),
		SERIES	VARCHAR(3),
		SCRIP_NAME	VARCHAR(50)
	)

	INSERT INTO #SCRIP 
	SELECT SCHEME_CODE, 'MF', RIGHT(SCHEME_NAME, 50)
	FROM MFSS_NAV01 C
	WHERE NAV_DATE = @TRADEDATE
	AND SCHEME_CODE NOT IN (SELECT SCRIP_CD FROM SCRIP2)

	DECLARE @CO_CODE INT

	SELECT @CO_CODE = ISNULL(MAX(CONVERT(INT, CO_CODE)),0) FROM SCRIP2

	INSERT INTO SCRIP1
	SELECT @CO_CODE + SNO,SERIES='MF',SHORT_NAME=SCRIP_NAME,LONG_NAME=SCRIP_NAME,MARKET_LOT=0,FACE_VAL=0,BOOK_CL_DT='',EX_DIV_DT='',EX_BON_DT='',EX_RIT_DT='',EQT_TYPE='MF',
	SUB_TYPE='MF',AGENT_CD='',DEMAT_FLAG=0,DEMAT_DATE='',RES1='',RES2='',RES3='',RES4=''
	FROM #SCRIP

	INSERT INTO SCRIP2
	SELECT @CO_CODE + SNO,SERIES='MF',EXCHANGE='NSE',SCRIP_CD,SCRIP_CAT='HIG',NO_DEL_FR='',NO_DEL_TO='',CL_RATE=0,CLOS_RATE_DT='',MIN_TRD_QTY=0,BSECODE='',ISIN,
	DELSC_CAT='NRM',SECTOR='',TRACK='N',CDOL_NO='',RES1='',RES2='',RES3='',RES4='',GLOBALCUSTODIAN='',COMMON_CODE='',INDEXNAME='',INDUSTRY='',BLOOMBERG='',RICCODE='',
	REUTERS='',IES='',NOOFISSUEDSHARES=0,STATUS='',ADRGDRRATIO=0,GEMULTIPLE=0,GROUPFORGE=0,RBICEILINGINDICATORFLAG='',RBICEILINGINDICATORVALUE=0
	FROM #SCRIP

	DROP TABLE #SCRIP
END

GO
