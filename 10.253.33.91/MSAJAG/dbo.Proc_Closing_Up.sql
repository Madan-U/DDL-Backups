create PROC Proc_Closing_Up 
(@FILENAME VARCHAR(100))
AS

-- EXEC Proc_Closing_Up 'C:\11080000.MD'

DECLARE @SYSDATE VARCHAR(10),
	    @STRSQL  VARCHAR(500)

TRUNCATE TABLE CLOSINGTEMP

SELECT @SYSDATE = CONVERT(VARCHAR,YEAR(GETDATE())) + '-' + CONVERT(VARCHAR,RIGHT(LEFT(RIGHT(@FILENAME,11),4),2)) + '-' + CONVERT(VARCHAR,LEFT(RIGHT(@FILENAME,11),2))

SELECT MARKET,SCRIP_CD,SERIES,PRE_CL_RATE,OPEN_RATE,HI_RATE,LOW_RATE,
	   CL_RATE,FILLER=CONVERT(VARCHAR(20),''),TOTTRDQTY,TOTTURNOVER,YEARHIRATE,YEARLOWRATE
INTO #CLOSING FROM CLOSINGTEMP
WHERE 1 = 2 

SET @STRSQL = 'BULK INSERT #CLOSING FROM ''' + @FILENAME  + '''  WITH ( FIELDTERMINATOR = '','', ROWTERMINATOR = ''\n'' )'      
EXEC(@STRSQL)      

DELETE FROM CLOSING
WHERE SYSDATE LIKE LEFT(CONVERT(DATETIME,@SYSDATE),11) + '%' 

INSERT INTO CLOSINGTEMP 
SELECT MARKET,SCRIP_CD,SERIES,CL_RATE,SYSDATE=@SYSDATE,PRE_CL_RATE,OPEN_RATE,HI_RATE,
LOW_RATE,TOTTRDQTY,TOTTURNOVER,YEARHIRATE,YEARLOWRATE 
FROM #CLOSING

INSERT INTO CLOSING
SELECT MARKET,SCRIP_CD,SERIES,CL_RATE,SYSDATE=@SYSDATE,PRE_CL_RATE,OPEN_RATE,HI_RATE,
LOW_RATE,TOTTRDQTY,TOTTURNOVER,YEARHIRATE,YEARLOWRATE 
FROM #CLOSING

GO
