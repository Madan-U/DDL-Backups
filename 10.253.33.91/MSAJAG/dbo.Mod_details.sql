CREATE Proc  [dbo].[Mod_details] (@FROMdate varchar(11),@TOdate  VARCHAR(11),@TYPE Varchar(1))
  
as 


DECLARE @DTCNT INT 

SELECT @DTCNT =DATEDIFF (DD,@FROMdate,@TOdate) 

IF @DTCNT >7
 begin 
  Select 'Date Should be one week'
  return 
end 

SELECT * INTO #TEMP1 FROM (
SELECT CREATED_DT,CRN_NO, Members.Member.value('.','VARCHAR(8000)') Mod_type
FROM
(--Convert delimited string to XML
 SELECT CRN_NO,CREATED_DT, CAST('<Players><Player>'
        + REPLACE((CASE WHEN ISNULL(MOD3,'') = '' THEN MOD_RMKS ELSE MOD3 END), ',' , '</Player><Player>') 
    + '</Player></Players>' AS XML) AS tempMOD 
 FROM ABVSCITRUS.CRMDB_A.DBO.CLIENT_MOD_DETAILS WHERE CREATED_DT >=@FROMdate  AND CREATED_DT <=@TOdate +' 23:59') AS tempMOD
 CROSS APPLY tempMOD.nodes('/Players/Player') Members(Member)
 )A 
 WHERE Mod_type <>''

 SELECT T.*,BO_PARTYCODE INTO #TEMP FROM #TEMP1 T,ABVSCITRUS.CRMDB_A.DBO.CLIENT_MASTER C 
 
 WHERE T.CRN_NO=C.CRN_NO 
  
 
 
 ALTER TABLE #TEMP
 ADD MODIFICATION_REQUEST VARCHAR(100)
 
 ALTER TABLE #TEMP
 ALTER COLUMN MOD_TYPE VARCHAR(100)


 UPDATE #TEMP SET MODIFICATION_REQUEST ='ONLY DP' FROM ABVSCITRUS.CRMDB_A.DBO.CLIENT_EXCHANGE  E
 WHERE #TEMP.CRN_NO=E.CLEA_CRN_NO AND CLEA_EXCHANGE_ID =1 

  UPDATE #TEMP SET MODIFICATION_REQUEST = 'BROKING  AND DP'   FROM ABVSCITRUS.CRMDB_A.DBO.CLIENT_EXCHANGE  E
 WHERE #TEMP.CRN_NO=E.CLEA_CRN_NO AND ISNULL(CLEA_EXCHANGE_ID,'') <>'1'
 AND  MODIFICATION_REQUEST  ='ONLY DP'
 
  UPDATE #TEMP SET MODIFICATION_REQUEST = 'BROKING'  WHERE  MODIFICATION_REQUEST IS NULL 
 
	UPDATE T SET MOD_TYPE = ( CASE WHEN isnull(M.APP_MOB_NO,'')<>ISNULL(Y.APP_MOB_NO,'') AND ISNULL(M.APP_EMAIL,'')<>ISNULL(Y.APP_EMAIL,'')  THEN 'MOBILE AND EMAIL' 
					  WHEN isnull(M.APP_MOB_NO,'')<>ISNULL(Y.APP_MOB_NO,'') THEN 'MOBILE'
					  WHEN ISNULL(M.APP_EMAIL,'')<>ISNULL(Y.APP_EMAIL,'') THEN 'EMAIL'
					  ELSE 'OTHER' END )
	FROM #TEMP  T , ABVSCITRUS.CRMDB_A.DBO.CLIENT_KYC_DATA_MAK_MOD M,ABVSCITRUS.CRMDB_A.DBO.CLIENT_KYC_DATA_MAK Y WHERE 
	T.CRN_NO =M.APP_CRN_NO AND M.APP_CRN_NO =Y.APP_CRN_NO --AND CRN_NO IN ('662975','659705')
	AND MOD_TYPE ='CONTACT'

 
 IF @TYPE ='A'
 BEGIN 

 
 SELECT CREATED_DT, CRN_NO,BO_PARTYCODE,MOD_TYPE='ADDRESS',
 CITRUS_COR_ADD =(ISNULL(APP_COR_ADD1,'')+ISNULL(APP_COR_ADD2,'')+ISNULL(APP_COR_ADD3,'')+ISNULL(APP_COR_CITY,'')+ISNULL(APP_COR_PINCD,'')) 
 ,BO_COR_ADD =(ISNULL(L_ADDRESS1,'')+ISNULL(L_ADDRESS2,'')+ISNULL(L_ADDRESS3,'')+ISNULL(L_CITY,'')+ISNULL(L_ZIP,'')) ,
 CITRUS_PER_ADD =(ISNULL(APP_PER_ADD1,'')+ISNULL(APP_PER_ADD2,'')+ISNULL(APP_PER_ADD3,'')+ISNULL(APP_PER_CITY,'')+ISNULL(APP_PER_PINCD,'')) ,
 BO_PER_ADD =(ISNULL(P_ADDRESS1,'')+ISNULL(P_ADDRESS2,'')+ISNULL(P_ADDRESS3,'')+ISNULL(P_CITY,'')+ISNULL(P_ZIP,'')) ,MODIFICATION_REQUEST  INTO #ADDREESS
  FROM  
 ( SELECT DISTINCT  CRN_NO,BO_PARTYCODE,MOD_TYPE='ADDRESS',APP_COR_ADD1,APP_COR_ADD2,APP_COR_ADD3,APP_COR_CITY,APP_COR_PINCD,APP_PER_ADD1,
 APP_PER_ADD2,APP_PER_ADD3,APP_PER_CITY,APP_PER_PINCD ,MODIFICATION_REQUEST,CREATED_DT
  FROM #TEMP T  
  LEFT OUTER JOIN
 ABVSCITRUS.CRMDB_A.DBO.CLIENT_KYC_DATA K  ON T.CRN_NO=K.APP_CRN_NO  
 WHERE  Mod_type LIKE '%ADDRESS%' )A
 LEFT OUTER JOIN 
  AngelNSECM.MSAJAG.DBO.CLIENT_DETAILS D  WITH(NOLOCK)
 ON  BO_PARTYCODE =CL_CODE  
 
  -- APP_COR_ADD1 =L_ADDRESS1
 --AND APP_COR_ADD2=L_address2 AND APP_COR_ADD3=l_address3 AND  APP_COR_CITY=l_city 
 --and APP_COR_PINCD =L_ZIP AND APP_PER_ADD1=p_address1 
 --AND APP_PER_ADD2 =p_address2 AND APP_PER_ADD3=p_address3 AND APP_PER_CITY =p_city AND APP_PER_PINCD =p_zip

 
 SELECT * FROM (
 SELECT DISTINCT A.*,
 T.FIRST_HOLD_ADD1+T.FIRST_HOLD_ADD2+T.FIRST_HOLD_ADD3 AS DP_ADDRESS
 FROM #ADDREESS A
 LEFT OUTER JOIN
 AGMUBODPL3.DMAT.DBO.TBL_CLIENT_MASTER	T WITH(NOLOCK)
 ON NISE_PARTY_CODE =A.BO_PARTYCODE  )A 
 WHERE CITRUS_COR_ADD <> BO_COR_ADD OR CITRUS_PER_ADD <> BO_PER_ADD OR DP_ADDRESS <> CITRUS_COR_ADD


 RETURN 
 END 

 IF @TYPE ='E'
  BEGIN 

 SELECT A.*,D.EMAIL,D.repatriat_bank_ac_no  INTO #EMAIL1
 FROM ( 
  SELECT DISTINCT  CREATED_DT,CRN_NO,BO_PARTYCODE,MOD_TYPE ,APP_EMAIL ,MODIFICATION_REQUEST
  FROM #TEMP T  
  LEFT OUTER JOIN
 ABVSCITRUS.CRMDB_A.DBO.CLIENT_KYC_DATA K  ON T.CRN_NO=K.APP_CRN_NO  
 WHERE  Mod_type IN ('ECN','EMAIL','MOBILE AND EMAIL' ))A
 LEFT OUTER JOIN 
 AngelNSECM.MSAJAG.DBO.CLIENT_DETAILS D  WITH(NOLOCK)
 ON  BO_PARTYCODE =CL_CODE 

  SELECT DISTINCT A.*,
 EMAIL_ADD AS DP_ADDRESS
 FROM #EMAIL1 A
 LEFT OUTER JOIN
 AGMUBODPL3.DMAT.DBO.TBL_CLIENT_MASTER	T WITH(NOLOCK)
 ON NISE_PARTY_CODE =BO_PARTYCODE 
 WHERE  EMAIL <> EMAIL_ADD OR APP_EMAIL <> EMAIL

 RETURN 
 END

 IF @TYPE ='M'

 BEGIN 
 
 SELECT A.*,D.MOBILE_PAGER AS ASPER_BO INTO #MOBILE
 FROM ( 
  SELECT DISTINCT CREATED_DT, CRN_NO,BO_PARTYCODE,MOD_TYPE ,APP_MOB_NO ,MODIFICATION_REQUEST
  FROM #TEMP T  
  LEFT OUTER JOIN
 ABVSCITRUS.CRMDB_A.DBO.CLIENT_KYC_DATA K  ON T.CRN_NO=K.APP_CRN_NO  
 WHERE  Mod_type IN ('MOBILE','MOBILE AND EMAIL') )A
 LEFT OUTER JOIN 
 AngelNSECM.MSAJAG.DBO.CLIENT_DETAILS D  WITH(NOLOCK)
 ON  BO_PARTYCODE =CL_CODE  AND MOBILE_PAGER =APP_MOB_NO


  SELECT DISTINCT A.*,
 FIRST_HOLD_MOBILE AS DP_MOBILE,FIRST_HOLD_PHONE 
 FROM #MOBILE A
 LEFT OUTER JOIN
 AGMUBODPL3.DMAT.DBO.TBL_CLIENT_MASTER	T WITH(NOLOCK)
 ON NISE_PARTY_CODE =BO_PARTYCODE 
 WHERE FIRST_HOLD_MOBILE <> ASPER_BO OR ASPER_BO <>APP_MOB_NO

 RETURN

 END 

 IF @TYPE ='S'

  BEGIN 

 SELECT A.*,(CASE WHEN CL_CODE IS NULL THEN 'N' ELSE 'Y' END) Activation_Status FROM 
  ( SELECT DISTINCT T.CREATED_DT,T.CRN_NO,T.BO_PARTYCODE,EXCHANGE,SEGMENT ,MODIFICATION_REQUEST
  FROM #TEMP T  
  LEFT OUTER JOIN
  (SELECT * FROM ABVSCITRUS.CRMDB_A.DBO.CLIENT_MASTER C,ABVSCITRUS.CRMDB_A.DBO.AP_ADDSEG S WHERE C.BO_PARTYCODE = S.BOPARTYCODE )  K  ON T.CRN_NO=K.CRN_NO  
  WHERE  Mod_type LIKE '%ADDSEG%')A
  LEFT OUTER JOIN 
  AngelNSECM.MSAJAG.DBO.CLIENT_BROK_DETAILS  D WITH(NOLOCK)
  ON BO_PARTYCODE =CL_CODE AND A.EXCHANGE=D.EXCHANGE AND A.SEGMENT =D.SEGMENT 
  WHERE CL_CODE IS NULL



  RETURN 
 END 


 IF @TYPE ='B'
 BEGIN 
 SELECT BO_PARTYCODE,BANK_NAME AS PER_CITRUS,ACNUM AS ACNUM_PER_CITRUS ,BANKNAME AS PER_BO,ACNUM AS PER_BOACNUM ,MODIFICATION_REQUEST,Mod_type INTO #BANK FROM 
  ( SELECT BO_PARTYCODE,CRN_NO,BANK_NAME ,CLIBA_AC_NO ,MODIFICATION_REQUEST,Mod_type  FROM 
  ( SELECT DISTINCT T.CREATED_DT,T.CRN_NO,T.BO_PARTYCODE,CLIBA_BANK_ID,CLIBA_AC_NO ,MODIFICATION_REQUEST,Mod_type
  FROM #TEMP T  
  LEFT OUTER JOIN
  (SELECT * FROM ABVSCITRUS.CRMDB_A.DBO.CLIENT_MASTER C,ABVSCITRUS.CRMDB_A.DBO.CLIENT_BANK_ACCTS  S WHERE C.CRN_NO = S.CLIBA_CRN_NO AND CLIBA_EXCSM_ID <>1 AND CLIBA_DELETED_IND =0)  K 
   ON T.CRN_NO=K.CRN_NO  
  WHERE  Mod_type LIKE '%BANK%') A
  LEFT OUTER JOIN  ABVSCITRUS.CRMDB_A.DBO.RBIBANKMASTER R 
  ON A.CLIBA_BANK_ID =R.RBI_BANKID )A
  LEFT OUTER JOIN 
  (SELECT DISTINCT PARTY_CODE,BANKNAME,ACNUM  FROM AngelNSECM.MSAJAG.DBO.PARTY_BANK_DETAILS  WITH(NOLOCK) ) D
  ON PARTY_CODE=BO_PARTYCODE AND CLIBA_AC_NO =ACNUM AND A.BANK_NAME=D.BANKNAME 
  WHERE BO_PARTYCODE IS NOT NULL
 
   SELECT DISTINCT A.*,
 BANK_ACCNO 
  AS DP_BANK
 FROM #BANK A
 LEFT OUTER JOIN
 AGMUBODPL3.DMAT.DBO.TBL_CLIENT_MASTER	T WITH(NOLOCK)
 ON NISE_PARTY_CODE =BO_PARTYCODE 
 WHERE ACNUM_PER_CITRUS <> PER_BOACNUM 


 RETURN 
 END 

  --select * FROM   AGMUBODPL3.DMAT.DBO.TBL_CLIENT_MASTER	 WHERE NISE_PARTY_CODE='ZR816R'

GO
