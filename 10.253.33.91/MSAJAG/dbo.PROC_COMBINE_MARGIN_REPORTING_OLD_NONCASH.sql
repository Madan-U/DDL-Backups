					UPDATE TBL_COMBINE_REPORTING_DETAIL SET T_MTMAVL = T_MTMAVL-@MARG_AMT

					WHERE MARGINDATE = @MARGINDATE

					AND PARTY_CODE = @PARTY_CODE

