BEGIN  	

	UPDATE TBL_COMBINE_REPORTING_DETAIL 

	SET TDAY_MARGIN_SHORT = T1.TDAY_MARGIN_SHORT,

		TDAY_MTM_SHORT = T1.TDAY_MTM_SHORT,

		T1DAY_MARGIN_SHORT = T1.T1DAY_MARGIN_SHORT,

