	WHERE MARGINDATE = @MARGINDATE 

	AND EXISTS (SELECT PARTY_CODE FROM TBL_COMBINE_REPORTING

				WHERE MARGINDATE = @MARGINDATE 

				AND TBL_COMBINE_REPORTING_DETAIL.PARTY_CODE = TBL_COMBINE_REPORTING.PARTY_CODE

				AND TDAY_MTM_SHORT >= 0)



	SELECT PARTY_CODE INTO #PARTYCNT FROM TBL_COMBINE_REPORTING_DETAIL

	WHERE MARGINDATE = @MARGINDATE

	GROUP BY PARTY_CODE 

	HAVING COUNT(1) = 1



	UPDATE TBL_COMBINE_REPORTING_DETAIL

	SET T_MARGINAVL = T1.TDAY_MARGIN_SHORT, T_MTMAVL = T1.TDAY_MTM_SHORT

	FROM TBL_COMBINE_REPORTING T1

	WHERE TBL_COMBINE_REPORTING_DETAIL.MARGINDATE = @MARGINDATE 

	AND T1.MARGINDATE = @MARGINDATE 

	AND TBL_COMBINE_REPORTING_DETAIL.PARTY_CODE = T1.PARTY_CODE

	AND EXISTS (SELECT PARTY_CODE FROM #PARTYCNT

				WHERE #PARTYCNT.PARTY_CODE = TBL_COMBINE_REPORTING_DETAIL.PARTY_CODE)

				 

	SET @MRGCUR = CURSOR FOR

	SELECT PARTY_CODE, AVAL_MARGIN=TDAY_MARGIN_SHORT FROM TBL_COMBINE_REPORTING

	WHERE MARGINDATE = @MARGINDATE 

	AND NOT EXISTS (SELECT PARTY_CODE FROM #PARTYCNT

				WHERE #PARTYCNT.PARTY_CODE = TBL_COMBINE_REPORTING.PARTY_CODE)

	AND TDAY_MARGIN_SHORT < 0 

	ORDER BY PARTY_CODE 

	OPEN @MRGCUR

	FETCH NEXT FROM @MRGCUR INTO @PARTY_CODE, @SHRT_AMT

	WHILE @@FETCH_STATUS = 0

	BEGIN

		SET @EXCHANGEWISE_CURSOR = CURSOR FOR

		SELECT PARTY_CODE, T.EXCHANGE, T.SEGMENT, TDAY_MARGIN, EXCHANGE_PRIORITY 

		FROM TBL_COMBINE_REPORTING_DETAIL T, V2_TBL_EXCHANGE_MARGIN_REPORT T1

		WHERE MARGINDATE = @MARGINDATE

		AND PARTY_CODE = @PARTY_CODE

		AND T.EXCHANGE = T1.EXCHANGE 

		AND T.SEGMENT = T1.SEGMENT 

		AND TDAY_MARGIN > 0

		ORDER BY EXCHANGE_PRIORITY DESC

		OPEN @EXCHANGEWISE_CURSOR

		FETCH NEXT FROM @EXCHANGEWISE_CURSOR INTO @PARTY_CODE, @EXCHANGE_NEW, @SEGMENT_NEW, @MARG_AMT, @EXCHANGE_PRIORITY

		WHILE @@FETCH_STATUS = 0

		BEGIN

			IF @SHRT_AMT < 0 

			BEGIN

				IF @MARG_AMT >= ABS(@SHRT_AMT)

				BEGIN

					UPDATE TBL_COMBINE_REPORTING_DETAIL SET T_MARGINAVL = @SHRT_AMT

					WHERE MARGINDATE = @MARGINDATE

					AND PARTY_CODE = @PARTY_CODE

					AND EXCHANGE = @EXCHANGE_NEW 

					AND SEGMENT = @SEGMENT_NEW 

					SET @SHRT_AMT = 0

				END

				ELSE

				BEGIN

					UPDATE TBL_COMBINE_REPORTING_DETAIL SET T_MARGINAVL = -@MARG_AMT

					WHERE MARGINDATE = @MARGINDATE

					AND PARTY_CODE = @PARTY_CODE

					AND EXCHANGE = @EXCHANGE_NEW 

					AND SEGMENT = @SEGMENT_NEW 

					SET @SHRT_AMT = @SHRT_AMT + @MARG_AMT

				END

			END

			ELSE

			BEGIN

				UPDATE TBL_COMBINE_REPORTING_DETAIL SET T_MARGINAVL = @MARG_AMT

				WHERE MARGINDATE = @MARGINDATE

				AND PARTY_CODE = @PARTY_CODE

				AND EXCHANGE = @EXCHANGE_NEW 

				AND SEGMENT = @SEGMENT_NEW 

			END

			FETCH NEXT FROM @EXCHANGEWISE_CURSOR INTO @PARTY_CODE, @EXCHANGE_NEW, @SEGMENT_NEW, @MARG_AMT, @EXCHANGE_PRIORITY

		END

		CLOSE @EXCHANGEWISE_CURSOR

		DEALLOCATE @EXCHANGEWISE_CURSOR

		FETCH NEXT FROM @MRGCUR INTO @PARTY_CODE, @SHRT_AMT

	END

	CLOSE @MRGCUR

	DEALLOCATE @MRGCUR



	SET @MRGCUR = CURSOR FOR

	SELECT PARTY_CODE, AVAL_MARGIN=TDAY_MTM_SHORT FROM TBL_COMBINE_REPORTING

	WHERE MARGINDATE = @MARGINDATE 

	AND NOT EXISTS (SELECT PARTY_CODE FROM #PARTYCNT

				WHERE #PARTYCNT.PARTY_CODE = TBL_COMBINE_REPORTING.PARTY_CODE)

	AND TDAY_MTM_SHORT < 0 

	ORDER BY PARTY_CODE 

	OPEN @MRGCUR

	FETCH NEXT FROM @MRGCUR INTO @PARTY_CODE, @SHRT_AMT

	WHILE @@FETCH_STATUS = 0

	BEGIN

		SET @EXCHANGEWISE_CURSOR = CURSOR FOR

		SELECT PARTY_CODE, T.EXCHANGE, T.SEGMENT, TDAY_MTM, EXCHANGE_PRIORITY 

		FROM TBL_COMBINE_REPORTING_DETAIL T, V2_TBL_EXCHANGE_MARGIN_REPORT T1

		WHERE MARGINDATE = @MARGINDATE

		AND PARTY_CODE = @PARTY_CODE

		AND T.EXCHANGE = T1.EXCHANGE 

		AND T.SEGMENT = T1.SEGMENT 

		AND TDAY_MTM > 0

		ORDER BY EXCHANGE_PRIORITY DESC

		OPEN @EXCHANGEWISE_CURSOR

		FETCH NEXT FROM @EXCHANGEWISE_CURSOR INTO @PARTY_CODE, @EXCHANGE_NEW, @SEGMENT_NEW, @MARG_AMT, @EXCHANGE_PRIORITY

		WHILE @@FETCH_STATUS = 0

		BEGIN

			IF @SHRT_AMT < 0 

			BEGIN

				IF @MARG_AMT >= ABS(@SHRT_AMT)

				BEGIN

					UPDATE TBL_COMBINE_REPORTING_DETAIL SET T_MTMAVL = @SHRT_AMT

					WHERE MARGINDATE = @MARGINDATE

					AND PARTY_CODE = @PARTY_CODE

					AND EXCHANGE = @EXCHANGE_NEW 

					AND SEGMENT = @SEGMENT_NEW 

					SET @SHRT_AMT = 0

				END

				ELSE

				BEGIN

					UPDATE TBL_COMBINE_REPORTING_DETAIL SET T_MTMAVL = -@MARG_AMT

					WHERE MARGINDATE = @MARGINDATE

					AND PARTY_CODE = @PARTY_CODE

					AND EXCHANGE = @EXCHANGE_NEW 

					AND SEGMENT = @SEGMENT_NEW 

					SET @SHRT_AMT = @SHRT_AMT + @MARG_AMT

