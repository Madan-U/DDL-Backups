IF (SELECT ISNULL(SUM(T1DAY_MARGIN+T1DAY_MTM),0) FROM  TBL_COMBINE_REPORTING WHERE MARGINDATE = @MARGINDATE) > 0 

BEGIN

/* old Changes 
	UPDATE TBL_COMBINE_REPORTING   
	SET   TDAY_MTM_SHORT = (CASE WHEN TDAY_MARGIN_SHORT > 0 THEN TDAY_MARGIN_SHORT ELSE 0 END) 
					+ PRADNYA.DBO.FNMAX((T1DAY_CASH+T1DAY_NONCASH+TDAY_MTM)-(TDAY_CASH + TDAY_NONCASH),0)
						 + PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN,0)- TDAY_MTM    
	WHERE MARGINDATE = @MARGINDATE
	AND (TDAY_MARGIN + TDAY_MTM) > 0 
	--AND TDAY_MARGIN <> T1DAY_MARGIN 
	AND TDAY_MTM_SHORT < 0  */

	/* Revised Changes as per Shashi Team 22102019 */ 
	UPDATE TBL_COMBINE_REPORTING   
	SET   TDAY_MTM_SHORT = (CASE WHEN TDAY_MARGIN_SHORT > 0 THEN TDAY_MARGIN_SHORT ELSE 0 END) 
					+ PRADNYA.DBO.FNMAX(((T1DAY_CASH+T1DAY_NONCASH+TDAY_MTM)-(TDAY_CASH + TDAY_NONCASH))
					+(CASE WHEN (TDAY_CASH + TDAY_NONCASH)<0 THEN (TDAY_CASH + TDAY_NONCASH) ELSE 0 END)
					,0)+ PRADNYA.DBO.FNMIN((CASE WHEN (TDAY_CASH + TDAY_NONCASH)>0 THEN 
					  (TDAY_MARGIN+TDAY_MARGIN_SHORT) ELSE 0 END ), PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN,0))- TDAY_MTM    
	WHERE MARGINDATE = @MARGINDATE
	AND (TDAY_MARGIN + TDAY_MTM) > 0 
	AND TDAY_MTM_SHORT < 0 



	UPDATE TBL_COMBINE_REPORTING   
	SET   TDAY_MTM_SHORT = (CASE WHEN TDAY_MARGIN_SHORT > 0 THEN TDAY_MARGIN_SHORT ELSE 0 END) 
					+ PRADNYA.DBO.FNMAX((T2DAY_CASH+T2DAY_NONCASH+T1DAY_MTM+TDAY_MTM)-(TDAY_CASH + TDAY_NONCASH),0)
						 + PRADNYA.DBO.FNMAX(TDAY_MARGIN-T1DAY_MARGIN-T2DAY_MARGIN,0)- TDAY_MTM    
	WHERE MARGINDATE = @MARGINDATE	
	AND (TDAY_MARGIN + TDAY_MTM) > 0 
	AND EXCHANGE IN ('MCX','NCX','MCM','NCM')  
	AND TDAY_MTM_SHORT < 0 

END



