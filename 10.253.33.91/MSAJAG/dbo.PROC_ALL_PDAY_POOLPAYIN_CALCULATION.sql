CREATE PROCEDURE [dbo].[PROC_ALL_PDAY_POOLPAYIN_CALCULATION] (@RPT_DATE VARCHAR(11))

AS ---- EXEC PROC_ALL_PDAY_POOLPAYIN_CALCULATION 'SEP 20 2022'

DECLARE @PRV_DATE VARCHAR(11)
DECLARE @EPN_SETT_NO VARCHAR(11)

SELECT @EPN_SETT_NO = SETT_NO-1 FROM SETT_MST WHERE SETT_TYPE='N' AND START_DATE  BETWEEN @RPT_DATE AND @RPT_DATE + ' 23:59:59'
SELECT @PRV_DATE = LEFT(START_DATE,11) FROM SETT_MST WHERE SETT_TYPE='N' AND SETT_NO = @EPN_SETT_NO

SELECT TRANSDATE,Party_Code,SCRIP_CD,CertNo,SERIES,QTY=SUM(QTY),REASON,DRCR,DELIVERED,BRANCHCD,SETT_NO,SETT_TYPE,BCLTDPID,
CLRATE=CONVERT(MONEY,0),HVALUE=CONVERT(MONEY,0) INTO #POOLPAYIN
FROM ANGELDEMAT.MSAJAG.DBO.DELTRANS_REPORT WITH (NOLOCK) 
WHERE Sett_No=@EPN_SETT_NO AND BCLTDPID='1203320006951435' 
AND DELIVERED=0 AND DRCR='C' AND SETT_TYPE IN ('N','W') 
AND TRANSDATE = @RPT_DATE 
GROUP BY TRANSDATE,Party_Code,SCRIP_CD,CertNo,SERIES,REASON,DRCR,DELIVERED,BRANCHCD,SETT_NO,SETT_TYPE,BCLTDPID

UPDATE A SET CLRATE = CL_RATE FROM #POOLPAYIN A,
 (SELECT SCRIP_CD,SERIES,CL_RATE  FROM CLOSING WITH (NOLOCK)  WHERE SysDate BETWEEN @PRV_DATE AND @PRV_DATE + ' 23:59:59') B
 WHERE A.SCRIP_CD = B.SCRIP_CD AND A.SERIES = B.SERIES

UPDATE A SET CLRATE = CL_RATE FROM #POOLPAYIN A,
(SELECT SCRIP_CD,SERIES,CL_RATE FROM AngelBSECM.BSEDB_AB.DBO.CLOSING WITH (NOLOCK) WHERE SysDate BETWEEN @PRV_DATE AND @PRV_DATE + ' 23:59:59') B
WHERE A.SCRIP_CD = B.SCRIP_CD AND A.SERIES = B.SERIES AND CLRATE=0

UPDATE A SET CLRATE = NAV_VALUE FROM #POOLPAYIN A,
(SELECT ISIN,NAV_VALUE FROM ANGELFO.BSEMFSS.DBO.MFSS_NAV WITH (NOLOCK) WHERE NAV_DATE BETWEEN @PRV_DATE AND @PRV_DATE + ' 23:59:59') B
WHERE A.CERTNO = B.ISIN AND CLRATE=0

 
UPDATE #POOLPAYIN SET CLRATE = CLOPM_CDSL_RT FROM #POOLPAYIN A1,
(SELECT CLOPM_ISIN_CD,CLOPM_CDSL_RT,CLOPM_DT FROM AngelDP5.DMAT.CITRUS_USR.CLOSING_LAST_CDSL A 
WHERE CLOPM_DT IN (SELECT MAX(CLOPM_DT) FROM AngelDP5.DMAT.CITRUS_USR.CLOSING_LAST_CDSL B WHERE A.CLOPM_ISIN_CD = B.CLOPM_ISIN_CD)) A2
WHERE A1.CERTNO = A2.CLOPM_ISIN_CD AND CLRATE=0  

UPDATE #POOLPAYIN SET HVALUE = QTY * CLRATE

SELECT * FROM #POOLPAYIN

GO
