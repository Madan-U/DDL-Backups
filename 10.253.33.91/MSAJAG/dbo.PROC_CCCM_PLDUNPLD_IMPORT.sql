-- Object: PROCEDURE dbo.PROC_CCCM_PLDUNPLD_IMPORT
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROC [dbo].[PROC_CCCM_PLDUNPLD_IMPORT]  
(   
 @TDATE VARCHAR(10),   
 @FILEFOR VARCHAR(20),   
 @FILETYPE VARCHAR(50),   
 @PROGID VARCHAR(50),   
 @UNAME VARCHAR(50),  
 @FILENAME VARCHAR(100) =''   
)  
AS  
  
DECLARE @RECCNT INT,  
 @UPLOADDATE DATETIME,
 @INTFILENAME	VARCHAR(50),
 @SNO	INT,
 @MEMBERCODE	VARCHAR(10),
 @EXC_SEG	VARCHAR(50)  
SET @RECCNT = 0  
 
SELECT @INTFILENAME = MemberCode +'_SEC_TRF_' + REPLACE(@TDATE,'/',''), @MEMBERCODE = MemberCode  
FROM Owner

IF (CASE WHEN @FILENAME LIKE '%C_' +  @INTFILENAME + '%' OR @FILENAME LIKE '%F_' +  @INTFILENAME + '%' OR @FILENAME LIKE '%X_' +  @INTFILENAME + '%' THEN 1 ELSE 0 END) > 0
BEGIN
	SET @UPLOADDATE = CONVERT(datetime,@TDATE,103) 
END
ELSE
BEGIN
 SELECT strMsgCode = -1, strMsg = 'Invalid File.'  
	RETURN
END

IF @FILENAME LIKE '%C_' +  @INTFILENAME + '%'
	SET @EXC_SEG = 'NSE#CAPITAL'
	
IF @FILENAME LIKE '%F_' +  @INTFILENAME + '%'
	SET @EXC_SEG = 'NSE#FUTURES'
   
IF @FILENAME LIKE '%X_' +  @INTFILENAME + '%'
	SET @EXC_SEG = 'NSX#FUTURES'

SELECT @RECCNT = ISNULL(COUNT(1),0) FROM FILEDATA  
WHERE PROGID = @PROGID   
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',5))) LIKE 'IN%'  
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',1))) = @MEMBERCODE

SELECT @SNO = ISNULL(MIN(SRNO),0) FROM FILEDATA  
WHERE PROGID = @PROGID   
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',1))) LIKE '%Release due to Transfer Request%'   

SELECT * INTO #TBL_CMCM_PLDUNPLD FROM TBL_CMCM_PLDUNPLD
WHERE 1 = 2 

INSERT INTO #TBL_CMCM_PLDUNPLD  
SELECT  @UPLOADDATE, 
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',1))),  
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',2))),  
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',4))),  
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',5))),   
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',6))),   
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',9))),   
RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',10))),
.DBO.PIECE(@EXC_SEG,'#',1),.DBO.PIECE(@EXC_SEG,'#',2),
@EXC_SEG,
(CASE WHEN SRNO < @SNO THEN 'C' ELSE 'D' END),
@UNAME,  
@UPLOADDATE,
'TRNF'  
FROM FILEDATA  
WHERE PROGID = @PROGID   
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',5))) LIKE 'IN%'  
AND RTRIM(LTRIM(.DBO.PIECE(FILE_DATA,',',1))) = @MEMBERCODE
  
DELETE FROM TBL_CMCM_PLDUNPLD  
WHERE TRANSDATE = @UPLOADDATE
AND SEG_DESC = @EXC_SEG
AND FILETYPE = 'TRNF'

INSERT INTO TBL_CMCM_PLDUNPLD  
SELECT * FROM #TBL_CMCM_PLDUNPLD

IF @RECCNT > 0  
BEGIN  
 SELECT strMsgCode = 1, strMsg = 'File Uploaded Successfully with no of records ' + convert(varchar,@RECCNT)  
END  
ELSE  
BEGIN  
 SELECT strMsgCode = -1, strMsg = 'File not uploaded properly Or data already available in the system.'  
END

GO
