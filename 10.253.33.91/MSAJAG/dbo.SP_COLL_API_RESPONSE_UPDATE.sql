-- Object: PROCEDURE dbo.SP_COLL_API_RESPONSE_UPDATE
-- Server: 10.253.33.91 | DB: MSAJAG
--------------------------------------------------


CREATE PROCEDURE [dbo].[SP_COLL_API_RESPONSE_UPDATE]
(
	@PROCESSDATE VARCHAR(11),
	@P_FILENAME VARCHAR(100),
	@SESSIONID VARCHAR(200),
	@USERNAME VARCHAR(50),
	@UNIQUEREFID VARCHAR(20),
	@EXCHANGE VARCHAR(10)
)AS

BEGIN
	DECLARE @V_DATA VARCHAR(MAX)
	
	SELECT @V_DATA=P_DATA FROM TBL_COLL_API_RESPONSE 
	WHERE PROCESSDATE = @PROCESSDATE
	AND SESSIONID = @SESSIONID
	AND UNIQUEREFID = @UNIQUEREFID
	AND P_FILENAME = @P_FILENAME
 
	CREATE TABLE #PDATA(RESPONSE VARCHAR(MAX))
	
	INSERT INTO #PDATA 
	SELECT * FROM DBO.FNSPLITSTRING2TBL(@V_DATA,'^')  

	UPDATE #PDATA SET RESPONSE = @UNIQUEREFID + ',' + RESPONSE WHERE @EXCHANGE='NSE'

	CREATE TABLE #TBL_CLIENT_COLL_FILE
	(
		UPD_FILENAME VARCHAR(100) ,
		SRNO VARCHAR(30), 
		FILEDATE VARCHAR(12) , 
		SEGMENT VARCHAR(7) , 
		CM_CODE VARCHAR(20) ,
		TM_CODE VARCHAR(20) ,
		CP_CODE VARCHAR(20) ,
		CLI_CODE VARCHAR(20) ,
		ACCOUNT_TYPE VARCHAR(12) ,
		AMOUNT NUMERIC(18, 2) ,
		FILLER1 VARCHAR(7) ,
		FILLER2 VARCHAR(7) ,
		FILLER3 VARCHAR(7) ,
		FILLER4 VARCHAR(7) ,
		FILLER5 VARCHAR(7) ,
		FILLER6 VARCHAR(7) ,
		ACTION_TYPE VARCHAR(12), 
		RECSTATUS VARCHAR(100),
		ERRCODE VARCHAR(100),
		ERRMSG VARCHAR(100)	 
	)  
	
	INSERT INTO #TBL_CLIENT_COLL_FILE
	SELECT  @P_FILENAME,
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 1))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 2))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 3))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 4))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 5))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 6))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 7))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 8))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 9))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 10))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 11))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 12))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 13))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 14))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 15))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 16))), 
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 18))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 17))),
		LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 20)))
	FROM #PDATA WHERE ISNULL(RESPONSE,'')<>'' 
	AND ISNULL(LTRIM(RTRIM(.DBO.PIECE(RESPONSE, ',', 9))),'')<>''
	 
	UPDATE A SET A.ERRCODE = B.ERRDESC
	FROM #TBL_CLIENT_COLL_FILE A,TBL_ERR_CODE_COLL B
	WHERE RIGHT(A.ERRCODE,4) = B.ERRCODE

	INSERT INTO TBL_CLIENT_COLL_FILE
	SELECT UPD_FILENAME,FILEDATE=CONVERT(VARCHAR(11),CONVERT(DATETIME,FILEDATE,105),109),EXCHANGE=@EXCHANGE,SEGMENT,SEGMENT_INDICATOR='',
	CM_CODE,TM_CODE,CP_CODE,CLI_CODE,ACCOUNT_TYPE,AMOUNT,FILLER1,FILLER2,FILLER3,FILLER4,FILLER5,FILLER6,
	ACTION_TYPE,0,'API',@USERNAME,GETDATE(),BATCH_STATUS=CASE WHEN UPPER(A.RECSTATUS)='SUCCESS' THEN 'D' ELSE 'F' END,ERRCODE,ERRMSG,GETDATE() 
	FROM #TBL_CLIENT_COLL_FILE A 
	WHERE NOT EXISTS(SELECT CLI_CODE FROM TBL_CLIENT_COLL_FILE B WHERE A.CLI_CODE=B.CLI_CODE AND UPD_FILENAME=@P_FILENAME
	AND FILEDATE LIKE CONVERT(VARCHAR(11), CONVERT(DATETIME, @PROCESSDATE, 103), 109) + '%') 

	UPDATE B SET BATCH_STATUS=CASE WHEN UPPER(A.RECSTATUS)='SUCCESS' THEN 'D' ELSE 'F' END,RESPONSE_CODE=ERRCODE,RESPONSE_BY=ERRMSG
	FROM #TBL_CLIENT_COLL_FILE A,TBL_CLIENT_COLL_FILE B
	WHERE A.CLI_CODE=B.CLI_CODE 
	AND A.UPD_FILENAME=B.UPD_FILENAME
	AND B.FILEDATE LIKE CONVERT(VARCHAR(11), CONVERT(DATETIME, @PROCESSDATE, 103), 109) + '%'

	UPDATE TBL_COLL_API_RESPONSE SET UPLOAD_STATUS='SUCCESS' WHERE 
	PROCESSDATE = @PROCESSDATE
	AND SESSIONID = @SESSIONID
	AND UNIQUEREFID = @UNIQUEREFID
	AND P_FILENAME = @P_FILENAME

	DROP TABLE #TBL_CLIENT_COLL_FILE
	DROP TABLE #PDATA

	SELECT MSG='PROCESS COMPLETED SUCCESSFULLY'
 END

GO
