CREATE PROC [dbo].[PROC_NSEFINOBL_UPLOAD](@FILEDATE VARCHAR(11), @FILEPATH VARCHAR(150)) AS    
--EXEC PROC_NSEFINOBL_UPLOAD 'DEC  1 2014', 'C:\TEMP\20141127\C_12348_FOBG_W2014219_27112014.CSV'
DECLARE @CMD	VARCHAR(1500),
@SETT_NO	VARCHAR(7),
@SETT_TYPE	VARCHAR(2)

SET @SETT_NO = LEFT(RIGHT(@FILEPATH,20),7)
SET @SETT_TYPE = LEFT(RIGHT(@FILEPATH,21),1)

SET @CMD = 'DELETE FROM TBL_NSE_OBL WHERE SETT_NO = ''' + @SETT_NO + ''' AND SETT_TYPE = ''' + @SETT_TYPE + ''' '
SET @CMD = @CMD + ' SELECT SETT_TYPE,SETT_NO,TM_CODE,TM_BR_CODE,PARTICIPANTCODE,CUST_CODE,SCRIP_CD,SERIES,SCRIP_CODE,OLB_DATE,ND_FLAG,D_PQTY,D_SQTY,D_PAMT,D_SAMT,C_PQTY,C_SQTY,C_PAMT,C_SAMT,FINAL_OBL_FLAG INTO #TBL_NSE_OBL FROM TBL_NSE_OBL WHERE 1 = 2 '
SET @CMD = @CMD + ' BULK INSERT #TBL_NSE_OBL FROM ''' + @FILEPATH + ''' WITH  ( '
SET @CMD = @CMD + ' FIELDTERMINATOR = '','', '
SET @CMD = @CMD + ' ROWTERMINATOR = ''' + CHAR(10) + ''', FIRSTROW = 2 ) '
SET @CMD = @CMD + ' INSERT INTO TBL_NSE_OBL '
SET @CMD = @CMD + ' SELECT SETT_NO,SETT_TYPE,TM_CODE,TM_BR_CODE,PARTICIPANTCODE,CUST_CODE,SCRIP_CD,SERIES,SCRIP_CODE,OLB_DATE,ND_FLAG,D_PQTY,D_SQTY,D_PAMT,D_SAMT,C_PQTY,C_SQTY,C_PAMT,C_SAMT,FINAL_OBL_FLAG FROM #TBL_NSE_OBL '

EXEC (@CMD)

DELETE FROM TBL_FINOBL_STATUS WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE 

--INSERT INTO TBL_FINOBL_STATUS 
EXEC RPT_OBLMATCH_NEW @SETT_NO, @SETT_TYPE, @FILEDATE, 'Y'

GO
