--EXEC CLS_SAUDAREPORT '01/01/2010','10/10/2014','','','','', 'BROKER', 'BROKER','SCRIP','SP','S',0,0

CREATE PROC [dbo].[CLS_SAUDAREPORT]
(         
	@DATEFROM VARCHAR(11),
	@DATETO VARCHAR(11),
	@FROMPARTY VARCHAR(20),
	@TOPARTY VARCHAR (20), 
	@FROMSCRIP VARCHAR(20),
	@TOSCRIP VARCHAR (20), 	
	@STATUSID VARCHAR(15),  
	@STATUSNAME VARCHAR(25),
	@REPORT_BY VARCHAR(10)='SCRIP',
	@REPORT_FORMAT VARCHAR(20)='D',
	@REPORT_TYPE VARCHAR(20)='S',
	@SERIES VARCHAR(5) = '',
	@USERNAME VARCHAR(20) = '',
	@NETRATEWITHCHRG INT = 0,
	@AMOUNTWITHRATE INT = 0
)  
AS         
        
SET @DATEFROM = LEFT(CONVERT(DATETIME,@DATEFROM,103),11)
SET @DATETO = LEFT(CONVERT(DATETIME,@DATETO,103),11)

IF @TOSCRIP =''
SET @TOSCRIP ='ZZZZZZZZZ'

IF @TOPARTY =''
SET @TOPARTY ='ZZZZZZZ'

CREATE TABLE #TEMP_FILTERED_PARTY
(
	PARTY_CD VARCHAR(10)
)

INSERT INTO #TEMP_FILTERED_PARTY
EXEC MSAJAG..CLS_GET_CLIENTDETAILS @FROMPARTY,@TOPARTY,@STATUSID,@STATUSNAME,@USERNAME,'CL_CODE',''
	SELECT 
		SAUDA_DATE = CONVERT(VARCHAR,CONVERT(DATETIME,SDATE,109),103),
		SETT_NO,SETT_TYPE,S.PARTY_CODE,
		LONG_NAME AS PARTY_NAME,SCRIP_CD,SERIES,PTRADEDQTY=SUM(PQTY),
		PTRADEDAMT=CONVERT(NUMERIC(18,2),(
		CASE
			WHEN @AMOUNTWITHRATE = 1 THEN SUM(PAMT)
			ELSE SUM(PQTY*PMARKETRATE)
		END)),        
		STRADEDQTY=CONVERT(NUMERIC(18,2),SUM(SQTY)),
		STRADEDAMT=CONVERT(NUMERIC(18,2),(
		CASE
			WHEN @AMOUNTWITHRATE = 1 THEN SUM(SAMT)
			ELSE SUM(SQTY*SMARKETRATE)
		END)),
		NQTY = SUM(PQTY)-SUM(SQTY),
		NAMT = CONVERT(NUMERIC(18,2),(
		CASE
			WHEN @AMOUNTWITHRATE = 1 THEN (SUM(SAMT)-SUM(PAMT))
			ELSE (SUM(SQTY*SMARKETRATE)-SUM(PQTY*PMARKETRATE))
		END)),        
		PMARKETRATE=CONVERT(NUMERIC(18,2),(CASE WHEN SUM(PQTY) > 0 
		   THEN SUM(PQTY*PMARKETRATE)/SUM(PQTY)
		   ELSE 0 
		END)), 
		PNETRATE = CONVERT(NUMERIC(18,2),(CASE WHEN SUM(PQTY) > 0 
		   THEN SUM(PQTY*PNETRATE)/SUM(PQTY)
		   ELSE 0 
		END)), 
		SMARKETRATE = CONVERT(NUMERIC(18,2),(CASE WHEN SUM(SQTY) > 0 
		   THEN SUM(SQTY*SMARKETRATE)/SUM(SQTY)
		   ELSE 0 
		END)), 
		SNETRATE = CONVERT(NUMERIC(18,2),(CASE WHEN SUM(SQTY) > 0 
		   THEN SUM(SQTY*SNETRATE)/SUM(SQTY)
		   ELSE 0 
		END)),
		CHARGES = CONVERT(NUMERIC(18,2),SUM(CHARGES))
	INTO #SAUDAREPORT
	FROM 
		(
			SELECT 
				SDATE = SAUDA_DATE,
				SETT_NO,SETT_TYPE,PARTY_CODE,SCRIP_CD,SERIES,
				PMARKETRATE = ISNULL((
							CASE WHEN SUM(PQTYTRD + PQTYDEL) > 0 THEN 
								SUM(PRATE)/SUM(PQTYTRD + PQTYDEL) END),0),
				PNETRATE =	ISNULL((
							CASE WHEN SUM(PQTYTRD + PQTYDEL) > 0 THEN 
								(CASE
									WHEN @NETRATEWITHCHRG = 1 THEN (SUM(PAMTTRD+PAMTDEL)+(SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG)))/SUM(PQTYTRD + PQTYDEL) 
									ELSE SUM(PAMTTRD+PAMTDEL)/SUM(PQTYTRD + PQTYDEL) 
								END)								
							END),0),
				SMARKETRATE = ISNULL((CASE WHEN SUM(SQTYTRD + SQTYDEL) > 0 THEN 
								SUM(SRATE)/SUM(SQTYTRD + SQTYDEL) END),0),
				SNETRATE = ISNULL((
							CASE WHEN SUM(SQTYTRD + SQTYDEL) > 0 THEN 
								(CASE
									WHEN @NETRATEWITHCHRG = 1 THEN (SUM(SAMTTRD+SAMTDEL)-(SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG)))/SUM(SQTYTRD + SQTYDEL)
									ELSE SUM(SAMTTRD+SAMTDEL)/SUM(SQTYTRD + SQTYDEL)
								END)	
							END),0),
				PQTY = SUM(PQTYTRD + PQTYDEL),
				PAMT = SUM(PAMTTRD + PAMTDEL)+(CASE WHEN @NETRATEWITHCHRG = 1 AND SUM(PQTYTRD + PQTYDEL) > 0 THEN (SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG)) ELSE 0 END),
				SQTY = SUM(SQTYTRD + SQTYDEL),
				SAMT = SUM(SAMTTRD + SAMTDEL)+(CASE WHEN @NETRATEWITHCHRG = 1 AND SUM(SQTYTRD + SQTYDEL) > 0 THEN -(SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG)) ELSE 0 END),
				CHARGES = (
				CASE 
					WHEN @NETRATEWITHCHRG = 1 THEN (SUM(PBROKTRD+PBROKDEL+SBROKTRD+SBROKDEL)+SUM(SERVICE_TAX)+SUM(TURN_TAX)+SUM(SEBI_TAX)+SUM(INS_CHRG)+SUM(BROKER_CHRG)+SUM(OTHER_CHRG))
					ELSE SUM(PBROKTRD+PBROKDEL+SBROKTRD+SBROKDEL)
				END)
			FROM CMBILLVALAN (NOLOCK)
			WHERE 
				SAUDA_DATE BETWEEN @DATEFROM  AND @DATETO + ' 23:59:00'
				AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY        
				AND SCRIP_CD BETWEEN @FROMSCRIP AND  @TOSCRIP 
				AND SERIES = CASE WHEN @SERIES <> '' THEN @SERIES ELSE SERIES END
				AND LEFT(TRADETYPE,1) IN ( 'S','I') 				
			GROUP BY SETT_NO,SETT_TYPE,SAUDA_DATE,PARTY_CODE,SCRIP_CD,SERIES 
			--HAVING SUM(PQTYTRD + PQTYDEL) - SUM(SQTYTRD + SQTYDEL) <> 0 
		) S ,
		CLIENT1 C1 (NOLOCK),
		CLIENT2 C2 (NOLOCK) ,#TEMP_FILTERED_PARTY C3
	WHERE   
		C1.CL_CODE= C3.PARTY_CD and     
		C1.CL_CODE = C2.CL_CODE AND C2.PARTY_CODE = S.PARTY_CODE        
	GROUP BY 
		CONVERT(VARCHAR,CONVERT(DATETIME,SDATE,109),103),
		SETT_NO,
		SETT_TYPE,
		S.PARTY_CODE,
		LONG_NAME,
		SCRIP_CD,
		SERIES
	

IF @REPORT_BY = 'PARTY'
BEGIN	
IF @REPORT_FORMAT = 'C' OR @REPORT_FORMAT = 'SP' OR @REPORT_FORMAT = 'P' 
BEGIN
	IF @REPORT_TYPE = 'D'
	BEGIN
		SELECT SAUDA_DATE,SETT_NO,SETT_TYPE,PARTY_CODE,PARTY_NAME,SCRIP_CD,SERIES,PTRADEDQTY,
		PTRADEDAMT = CONVERT(NUMERIC(18,2),PTRADEDAMT),
		STRADEDQTY,
		STRADEDAMT= CONVERT(NUMERIC(18,2),STRADEDAMT),
		NQTY,NAMT = CONVERT(NUMERIC(18,2),NAMT),
		PMARKETRATE,
		PNETRATE,
		SMARKETRATE,
		SNETRATE,
		CHARGES
		FROM #SAUDAREPORT  
		ORDER BY PARTY_CODE,SCRIP_CD
	END
	ELSE IF @REPORT_TYPE = 'S'
	BEGIN
		SELECT 
			SAUDA_DATE = '',
			SETT_NO = '',SETT_TYPE = '',PARTY_CODE,PARTY_NAME,SCRIP_CD = '',SERIES = '',
			PTRADEDQTY=SUM(PTRADEDQTY),PTRADEDAMT=CONVERT(NUMERIC(18,2),SUM(PTRADEDAMT)),
			STRADEDQTY=SUM(STRADEDQTY),STRADEDAMT=CONVERT(NUMERIC(18,2),SUM(STRADEDAMT)),
			NQTY=SUM(NQTY),NAMT=CONVERT(NUMERIC(18,2),SUM(NAMT)),
			PMARKETRATE=0, 
			PNETRATE  =0, 
			SMARKETRATE = 0, 
			SNETRATE =0,
			CHARGES = SUM(CHARGES)
		FROM 
			#SAUDAREPORT
		GROUP BY 
			PARTY_CODE,
			PARTY_NAME	
	END
	RETURN
END		

	SELECT SAUDA_DATE,SETT_NO,SETT_TYPE,PARTY_CODE,PARTY_NAME,SCRIP_CD,SERIES,PTRADEDQTY,
	PTRADEDAMT=CONVERT(NUMERIC(18,2),PTRADEDAMT),STRADEDQTY,STRADEDAMT=CONVERT(NUMERIC(18,2),
	STRADEDAMT),NQTY,NAMT = CONVERT(NUMERIC(18,2),NAMT),PMARKETRATE,PNETRATE,SMARKETRATE,SNETRATE,
	CHARGES = CHARGES
	FROM #SAUDAREPORT  ORDER BY PARTY_CODE,SCRIP_CD
	
	SELECT 
		SAUDA_DATE = '',
		SETT_NO = '',SETT_TYPE = '',PARTY_CODE,PARTY_NAME,SCRIP_CD = '',SERIES = '',
		PTRADEDQTY=SUM(PTRADEDQTY),PTRADEDAMT=CONVERT(NUMERIC(18,2),SUM(PTRADEDAMT)),
		STRADEDQTY=SUM(STRADEDQTY),STRADEDAMT=CONVERT(NUMERIC(18,2),SUM(STRADEDAMT)),
		NQTY=SUM(NQTY),NAMT=CONVERT(NUMERIC(18,2),SUM(NAMT)),
		PMARKETRATE=0, 
		PNETRATE  =0, 
		SMARKETRATE = 0, 
		SNETRATE =0,
		CHARGES = SUM(CHARGES)      
	FROM 
		#SAUDAREPORT	
	GROUP BY 
		PARTY_CODE,
		PARTY_NAME
	ORDER BY PARTY_CODE
	   
	
END	
ELSE IF @REPORT_BY ='SCRIP'
BEGIN
IF @REPORT_FORMAT = 'C' OR @REPORT_FORMAT = 'SP' OR @REPORT_FORMAT = 'P' 
BEGIN
	IF @REPORT_TYPE = 'D'
	BEGIN
	SELECT * FROM #SAUDAREPORT  
	 ORDER BY SCRIP_CD,PARTY_CODE
	END
	ELSE IF @REPORT_TYPE = 'S'
	BEGIN
		SELECT 
		SAUDA_DATE = '',
		SETT_NO = '',SETT_TYPE = '',PARTY_CODE='',PARTY_NAME ='',SCRIP_CD,SERIES,
		PTRADEDQTY=SUM(PTRADEDQTY),PTRADEDAMT=CONVERT(NUMERIC(18,2),SUM(PTRADEDAMT)),
		STRADEDQTY=SUM(STRADEDQTY),STRADEDAMT=CONVERT(NUMERIC(18,2),SUM(STRADEDAMT)),
		NQTY=SUM(NQTY),NAMT=CONVERT(NUMERIC(18,2),SUM(NAMT)),
		PMARKETRATE=0, 
		PNETRATE  =0, 
		SMARKETRATE = 0, 
		SNETRATE =0,
		CHARGES = SUM(CHARGES)      
	FROM 
		#SAUDAREPORT	
	GROUP BY 		
		SCRIP_CD, 
		SERIES
	END
	RETURN
END		
	SELECT * FROM #SAUDAREPORT  ORDER BY PARTY_CODE,SCRIP_CD
	
	SELECT 
		SAUDA_DATE = '',
		SETT_NO = '',SETT_TYPE = '',PARTY_CODE='',PARTY_NAME ='',SCRIP_CD,SERIES,
		PTRADEDQTY=SUM(PTRADEDQTY),PTRADEDAMT=CONVERT(NUMERIC(18,2),SUM(PTRADEDAMT)),
		STRADEDQTY=SUM(STRADEDQTY),STRADEDAMT=CONVERT(NUMERIC(18,2),SUM(STRADEDAMT)),
		NQTY=SUM(NQTY),NAMT=CONVERT(NUMERIC(18,2),SUM(NAMT)),
		PMARKETRATE=0, 
		PNETRATE  =0, 
		SMARKETRATE = 0, 
		SNETRATE =0,
		CHARGES = SUM(CHARGES)      
	FROM 
		#SAUDAREPORT	
	GROUP BY 		
		SCRIP_CD, 
		SERIES
	ORDER BY PARTY_CODE,SCRIP_CD
	
END

DROP TABLE #SAUDAREPORT


/*-------------------------------------------- END OF THE PROC ------------------------------------*/

GO
