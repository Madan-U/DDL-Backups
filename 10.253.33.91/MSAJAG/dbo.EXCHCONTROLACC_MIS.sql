--AND #EXCHCONTROLACC.CLTCODE = #CASH_FINAL.CLTCODE 

UPDATE #EXCHCONTROLACC SET [Ledger Bal] = VAMT FROM #LEDGERGL_BAL  
WHERE #EXCHCONTROLACC.EXCHANGE = #LEDGERGL_BAL.EXCHANGE AND #EXCHCONTROLACC.CLTCODE = #LEDGERGL_BAL.CLTCODE


UPDATE #EXCHCONTROLACC SET [Dif( Vallan vs Exch File)] = [Valan Posting]  + [Exchange Obligation]
UPDATE #EXCHCONTROLACC SET [ Dif(Led vs Vallan Posting)] = [Valan Posting]  - [Ledger Bal]
UPDATE #EXCHCONTROLACC SET Remark = 'Tally' WHERE [Dif( Vallan vs Exch File)] = 0 AND [ Dif(Led vs Vallan Posting)] = 0 


SELECT * FROM #EXCHCONTROLACC ORDER BY 10,1


DROP TABLE #EXCHCONTROLACC
DROP TABLE #LEDGERGL
DROP TABLE #LEDGERGL_NSEBSE
DROP TABLE #FNO
--DROP TABLE #CASH
