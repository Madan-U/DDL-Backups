CREATE PROC [dbo].[MARGIN_ANG_MCX] (@DELDATE DATETIME)  
AS  
SELECT * INTO #FOEXERCISEALLOCATION FROM ANGELCOMMODITY.MCDX.DBO.FOEXERCISEALLOCATION WITH (NOLOCK)   
WHERE FOEA_POSITION_DATE = @DELDATE    
  
  
SELECT FOEA_PARTY_CODE,SUM(FOEA_NETPREMIUM) AS NETBUYP,SUM(FOEA_DAILY_MTM_SETTLEMENT_VALUE) AS MTMV INTO #FOEXERCISEALLOCATION1  
FROM #FOEXERCISEALLOCATION 
--WHERE  CONVERT(NUMERIC(18,2),FOEA_NETPREMIUM) < CONVERT(NUMERIC(18,2),0)   OR CONVERT(NUMERIC(18,2),FOEA_DAILY_MTM_SETTLEMENT_VALUE) < CONVERT(NUMERIC(18,2),0)   
GROUP BY FOEA_PARTY_CODE  
  
UPDATE MCX_MRG_ANG_FINAL SET MTMM = ABS(MTMV) FROM #FOEXERCISEALLOCATION1 WHERE MDATE = @DELDATE AND FOEA_PARTY_CODE = PARTY_CODE  AND MTMV < 0 
UPDATE MCX_MRG_ANG_FINAL SET IM = IM + ABS(NETBUYP) FROM #FOEXERCISEALLOCATION1 WHERE MDATE = @DELDATE AND FOEA_PARTY_CODE = PARTY_CODE  AND NETBUYP < 0 
  
  
DROP TABLE #FOEXERCISEALLOCATION  
DROP TABLE #FOEXERCISEALLOCATION1  
  
SELECT MDATE=CONVERT(VARCHAR(20),FORMAT(MDATE,'dd-MMM-yyyy')),CMID,PARTY_CODE,IM,OTHERM,MTMM,RESERVED1,RESERVED2,MTMMC,IMC,OMC,PMT,PIM,PMC,PMS,
ASINTRAALLOC1,ASINTRAALLOC2,ASINTRAALLOC3,ASINTRAALLOC4,ASINTRAALLOC5  
FROM MCX_MRG_ANG_FINAL  WHERE MDATE = @DELDATE  ORDER BY PARTY_CODE

   
  
