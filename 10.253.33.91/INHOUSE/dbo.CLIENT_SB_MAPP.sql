CREATE PROC [dbo].[CLIENT_SB_MAPP]
AS 

BEGIN 
SELECT CL_CODE,BRANCH_CD,SUB_BROKER INTO #NEW_CLIENT FROM MSAJAG.DBO.CLIENT_DETAILS C (NOLOCK) WHERE MODIFIDEDON >=GETDATE()-10
AND NOT EXISTS ( SELECT CL_CODE FROM CLIENT_BRANCH_SB_FLAG S (NOLOCK)  WHERE C.CL_CODE=S.CL_CODE  )
AND CL_CODE >='A00' AND CL_CODE <='ZZZZZZZZZ'

INSERT INTO #NEW_CLIENT 
SELECT PARTY_CODE,BRANCH_CD,SUB_BROKER 
 FROM ANGELFO.BSEMFSS.DBO.MFSS_CLIENT C (NOLOCK) WHERE ADDEDON >=GETDATE()-10
AND NOT EXISTS ( SELECT CL_CODE FROM CLIENT_BRANCH_SB_FLAG S (NOLOCK)  WHERE C.PARTY_CODE=S.CL_CODE  )
AND PARTY_CODE >='A00' AND PARTY_CODE <='ZZZZZZZZZ'
AND NOT EXISTS (SELECT * FROM #NEW_CLIENT N WHERE  N.CL_CODE=PARTY_CODE)


INSERT INTO #NEW_CLIENT 
SELECT CL_CODE,BRANCH_CD,SUB_BROKER  FROM MSAJAG.DBO.CLIENT_DETAILS C (NOLOCK) WHERE  MODIFIDEDON >=GETDATE()-10
AND EXISTS ( SELECT CL_CODE FROM CLIENT_BRANCH_SB_FLAG S (NOLOCK)  WHERE C.CL_CODE=S.CL_CODE 
AND (S.BRANCH_CD <> C.BRANCH_CD  OR S.SUB_BROKER <>C.SUB_BROKER ) AND TAG_STATUS =1)
AND CL_CODE >='A00' AND CL_CODE <='ZZZZZZZZZ'

 

ALTER TABLE #NEW_CLIENT
ADD CLI_TYPE VARCHAR(10)

SELECT * INTO #B2CSB FROM MIS.REMISIOR.DBO.B2C_SB WITH(NOLOCK)
 
UPDATE A
 SET CLI_TYPE ='B2B'
 FROM #NEW_CLIENT A WITH(NOLOCK)
 LEFT OUTER JOIN #B2CSB B
 ON A.SUB_BROKER=B.B2C_SB
 WHERE B.B2C_SB IS NULL

UPDATE CLIENT_BRANCH_SB_FLAG SET TAG_STATUS='0' ,MODIFY_DATE =GETDATE()
WHERE  CL_CODE IN (
  SELECT CL_CODE FROM #NEW_CLIENT WHERE BRANCH_CD NOT LIKE 'CALNT')

INSERT INTO CLIENT_BRANCH_SB_FLAG
SELECT *,1,GETDATE(),'' FROM #NEW_CLIENT WHERE BRANCH_CD NOT LIKE 'CALNT'

drop table #NEW_CLIENT

END

GO
