DECLARE @SAUDA_DATE VARCHAR(11)

SELECT @SAUDA_DATE = MAX(SAUDA_DATE) FROM  MTFTRADE.DBO.TBL_MTF_DATA   WITH (NOLOCK) 

DECLARE @MTFCOLLVALUE VARCHAR(100)

SELECT  PARTY_CODE,CASHCOLL=(CASE WHEN SUM(NETAMT) + MTFLEDBAL > 0  
          THEN SUM(NETAMT) + MTFLEDBAL  
          ELSE 0  
           END) 
INTO #COLLCASH  
FROM  MTFTRADE.DBO.TBL_MTF_DATA   WITH (NOLOCK) 
WHERE SAUDA_DATE = @SAUDA_DATE  
GROUP BY  PARTY_CODE,MTFLEDBAL  


