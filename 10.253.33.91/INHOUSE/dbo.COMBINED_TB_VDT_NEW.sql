CREATE PROC [dbo].[COMBINED_TB_VDT_NEW] (@FROMDATE VARCHAR(20) )      
AS       
--SET @FROMDATE = 'AUG 31 2021'      
          
      
      
DECLARE @FINSTDT DATETIME       
      
SELECT @FINSTDT = SDTCUR FROM ACCOUNT.DBO.PARAMETER WHERE @FROMDATE BETWEEN SDTCUR AND LDTCUR      
       
      
          
----NSEBLBS          
SELECT CLTCODE,SUM(VAMT)AS VAMT ,'NSE' EXCHANGE,'SLBS'  SEGMENT INTO #SLBS FROM (          
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   ACCOUNTSLBS..LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT  AND          
 VDT<=@FROMDATE  + ' 23:59' GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
 HAVING SUM(VAMT) <>0      
          
------------MTF          
          
          
          
SELECT CLTCODE,SUM(VAMT)AS VAMT ,'MTF' EXCHANGE,'MTF'  SEGMENT  INTO #MTF FROM (          
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   MTFTRADE..LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT  AND          
 VDT<=@FROMDATE  + ' 23:59' GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
  HAVING SUM(VAMT) <>0         
------------NSE          
          
SELECT CLTCODE,SUM(VAMT)AS VAMT ,'NSE' EXCHANGE,'CAPITAL'  SEGMENT INTO #NSE FROM (          
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM  ACCOUNT.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE + ' 23:59'  GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
  HAVING SUM(VAMT) <>0         
          
 ------------BSE          
 SELECT CLTCODE,SUM(VAMT)AS VAMT  ,'BSE' EXCHANGE,'CAPITAL'  SEGMENT INTO #BSE FROM (          
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   [AngelBSECM].ACCOUNT_AB.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE  + ' 23:59' GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
  HAVING SUM(VAMT) <>0         
          
 ------------NSEFO          
 SELECT CLTCODE,SUM(VAMT)AS VAMT  ,'NSE' EXCHANGE,'FUTURES'  SEGMENT INTO #NSEFO FROM (          
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   [AngelFO].ACCOUNTFO.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE  + ' 23:59'  GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
  HAVING SUM(VAMT) <>0         
          
  ------------NSX          
 SELECT CLTCODE,SUM(VAMT)AS VAMT ,'NSX' EXCHANGE,'FUTURES'  SEGMENT INTO #NSX FROM (          
      
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   [AngelFO].ACCOUNTCURFO.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE  + ' 23:59'  GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
   HAVING SUM(VAMT) <>0        
          
          
  ------------MCD           
 SELECT CLTCODE,SUM(VAMT)AS VAMT  ,'MCD' EXCHANGE,'FUTURES'  SEGMENT INTO #MCD FROM (          
      
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   [AngelCommodity].ACCOUNTMCDXCDS.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE  + ' 23:59'  GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
  HAVING SUM(VAMT) <>0         
  ------------MCX           
 SELECT CLTCODE,SUM(VAMT)AS VAMT ,'MCX' EXCHANGE,'FUTURES'  SEGMENT INTO #MCX FROM (          
       
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   [AngelCommodity].ACCOUNTMCDX.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE  + ' 23:59'  GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
   HAVING SUM(VAMT) <>0        
   ------------NCX           
 SELECT CLTCODE,SUM(VAMT)AS VAMT ,'NCX' EXCHANGE,'FUTURES'  SEGMENT  INTO #NCX FROM (          
      
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   [AngelCommodity].ACCOUNTNCDX.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE  + ' 23:59'  GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
   HAVING SUM(VAMT) <>0        
    ------------BSEFO           
 SELECT CLTCODE,SUM(VAMT)AS VAMT ,'BSE' EXCHANGE,'FUTURES'  SEGMENT INTO #BSEFO FROM (          
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   [AngelCommodity].ACCOUNTBFO.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND      
 VDT<=@FROMDATE  + ' 23:59'  GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
  HAVING SUM(VAMT) <>0         
          
          
     ------------BSX           
 SELECT CLTCODE,SUM(VAMT)AS VAMT ,'BSX' EXCHANGE,'FUTURES'  SEGMENT INTO #BSX FROM (          
SELECT CLTCODE,SUM(CASE WHEN DRCR='D'THEN VAMT ELSE -VAMT END)VAMT FROM   [AngelCommodity].ACCOUNTCURBFO.DBO.LEDGER WITH(NOLOCK)          
WHERE          
VDT>=@FINSTDT AND          
 VDT<=@FROMDATE  + ' 23:59' GROUP BY CLTCODE)A          
 GROUP BY CLTCODE          
   HAVING SUM(VAMT) <>0        
          
 CREATE TABLE #FINAL          
(CLTCODE VARCHAR(10),   EXCHANGE VARCHAR(10),SEGMENT     VARCHAR(10),  
VAMT MONEY,          
)          
          
       
          
 INSERT INTO #FINAL          
SELECT DISTINCT CLTCODE,EXCHANGE,SEGMENT,Sum(VAMT) FROM (          
SELECT *  FROM #NSE         
UNION ALL          
SELECT * FROM #BSE         
UNION ALL          
SELECT * FROM #NSEFO          
UNION ALL          
SELECT * FROM #NSX          
UNION ALL          
SELECT * FROM #MCD          
UNION ALL          
SELECT * FROM #MCX          
UNION ALL          
SELECT * FROM #NCX          
UNION ALL          
SELECT * FROM #BSEFO          
UNION ALL          
SELECT * FROM #BSX          
UNION ALL          
SELECT * FROM #MTF          
UNION ALL          
SELECT * FROM #SLBS          
 )A           Group by CLTCODE,EXCHANGE,SEGMENT
          
        
          
UPDATE #FINAL SET CLTCODE =FAMILY FROM MSAJAG.DBO.CLIENT_DETAILS WHERE CL_CODE =CLTCODE           
     AND CLTCODE LIKE '98%'          
 
        
          
 SELECT CLTCODE,LONG_NAME,PAN_GIR_NO=REPLACE(PAN_GIR_NO,',','')    ,  
 EXCHANGE,SEGMENT,VAMT AS TOTAL          
  INTO #TEMP          
    FROM #FINAL  A          
  INNER JOIN        
   MSAJAG..CLIENT_DETAILS B          
  ON A.CLTCODE=B.CL_CODE        
  WHERE CLTCODE <>'BBBB'   AND CL_CODE >='A0' AND CL_CODE <='ZZZZZZZZ'      
--  GROUP BY CLTCODE,LONG_NAME,PAN_GIR_NO, EXCHANGE,SEGMENT
  
	SELECT * FROM #TEMP
      
  --SELECT * FROM #TEMP

GO
