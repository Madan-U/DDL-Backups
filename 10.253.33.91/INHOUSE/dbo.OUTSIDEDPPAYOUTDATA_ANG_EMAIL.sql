CREATE PROC [dbo].[OUTSIDEDPPAYOUTDATA_ANG_EMAIL]

AS

IF ( CONVERT(VARCHAR(11),ACCOUNT.DBO.FN_NEXTWEEKDAY(GETDATE(), 'SAT'),109) = CONVERT(VARCHAR(11),GETDATE()))    
BEGIN     
RETURN    
END    
    
IF ( CONVERT(VARCHAR(11),ACCOUNT.DBO.FN_NEXTWEEKDAY(GETDATE(), 'SUN'),109) = CONVERT(VARCHAR(11),GETDATE()))    
BEGIN     
RETURN
END   


IF ( SELECT COUNT(1) FROM MSAJAG.DBO.SETT_MST WHERE CONVERT(VARCHAR(11),START_DATE) = CONVERT(VARCHAR(11),GETDATE()) AND SETT_TYPE = 'M') = ''  
BEGIN     
RETURN
END    

DECLARE @SETT_NO VARCHAR(7) , @SAUDA_DATE DATETIME, @SAUDA_DATE1 DATETIME

SET @SAUDA_DATE = CONVERT(VARCHAR(11),GETDATE())

SELECT @SETT_NO = SETT_NO FROM MSAJAG.DBO.SETT_MST  WITH (NOLOCK) WHERE SETT_TYPE = 'M' AND SEC_PAYIN = @SAUDA_DATE

SELECT @SAUDA_DATE1 = START_DATE FROM MSAJAG.DBO.SETT_MST  WITH (NOLOCK) WHERE SETT_TYPE = 'M' AND SETT_NO = @SETT_NO


SELECT DISTINCT D.SETT_NO,D.SETT_TYPE,D.SCRIP_CD,D.PARTY_CODE,D.QTY,D.I_ISIN,DPID = CONVERT(VARCHAR(20),''),
CLTDPID = CONVERT(VARCHAR(20),''),CLOSING = CONVERT(NUMERIC(18,2),0),TOTAL = CONVERT(NUMERIC(18,2),0)
INTO #CLIENTDPDATAOUTSIDE
FROM ANGELDEMAT.MSAJAG.DBO.DELIVERYCLT D WITH (NOLOCK),ANGELDEMAT.MSAJAG.DBO.CLIENT4 C WITH (NOLOCK), MSAJAG.DBO.CLIENT_DETAILS CD WITH (NOLOCK)
WHERE C.PARTY_CODE = D.PARTY_CODE AND D.SETT_NO = @SETT_NO AND D.INOUT = 'O'
--AND C.DEPOSITORY IN ('NSDL','CDSL') AND C.BANKID NOT IN ('12033200','12033201','12033202') --- // COMMENTED ON 15 JUL 2024 SRE-27948
AND C.DEPOSITORY IN ('NSDL','CDSL') AND C.BANKID NOT IN ('12033200','12033201','12033202','12033203')  --- // ADDED ON 15 JUL 2024 SRE-27948
AND D.PARTY_CODE =  CD.CL_CODE 
AND D.PARTY_CODE NOT IN (SELECT PARTY_CODE FROM ANGELDEMAT.MSAJAG.DBO.CLIENT4 C WITH (NOLOCK)
--WHERE BANKID  IN ('12033200','12033201','12033202') AND DEFDP = 1) --- // COMMENTED ON 15 JUL 2024 SRE-27948
WHERE BANKID  IN ('12033200','12033201','12033202','12033203') AND DEFDP = 1) --- // ADDED ON 15 JUL 2024 SRE-27948
ORDER BY D.PARTY_CODE

UPDATE #CLIENTDPDATAOUTSIDE SET DPID = BANKID,CLTDPID = C.CLTDPID  FROM ANGELDEMAT.MSAJAG.DBO.CLIENT4 C WITH (NOLOCK) WHERE DEFDP = 1
AND #CLIENTDPDATAOUTSIDE.PARTY_CODE = C.PARTY_CODE

UPDATE #CLIENTDPDATAOUTSIDE SET CLOSING = CL_RATE FROM MSAJAG.DBO.CLOSING_MTM  WITH (NOLOCK) WHERE SYSDATE = @SAUDA_DATE1 AND ISIN = I_ISIN

UPDATE #CLIENTDPDATAOUTSIDE SET TOTAL = CLOSING * QTY


DECLARE @EMAILS VARCHAR(4000),@BODYCONTENT VARCHAR(MAX),@TBLE VARCHAR(MAX),@SUB VARCHAR(4000),@SUB1 VARCHAR(4000),@XML VARCHAR(MAX)       

SET @SUB = 'Payout For Outside Dpid on : ' + CONVERT(VARCHAR(11),@SAUDA_DATE,105)  
  
SET @SUB1 = 'Payout For Outside Dpid on : ' + CONVERT(VARCHAR(11),@SAUDA_DATE,105)  
           
    
SET @XML =  CAST((SELECT [td/@align]='center',[td/@width]='10%',td = SETT_NO,'',          
    [td/@align]='center',[td/@width]='10%',td = SETT_TYPE ,'',          
    [td/@align]='center',[td/@width]='10%',td =SCRIP_CD,'', 
	[td/@align]='center',[td/@width]='10%',td =PARTY_CODE,'', 
	[td/@align]='center',[td/@width]='10%',td =QTY,'',
	[td/@align]='center',[td/@width]='10%',td =I_ISIN,'',
	[td/@align]='center',[td/@width]='10%',td =DPID,'',
	[td/@align]='center',[td/@width]='10%',td =CLTDPID,'',
	[td/@align]='center',[td/@width]='10%',td =CLOSING,'',
	[td/@align]='center',[td/@width]='10%',td =TOTAL,''
FROM #CLIENTDPDATAOUTSIDE 
ORDER BY PARTY_CODE ASC FOR XML PATH('TR'), ELEMENTS ) AS VARCHAR(MAX))          

    
SET @TBLE ='<HEAD><STYLE>BODY>TABLE>TBODY>TR>TD:NTH-CHILD(1)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(2)BODY>TABLE>TBODY > TR > TD:NTH-CHILD(3)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(4)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(5)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(6)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(7)BODY>TABLE>TBODY>TR>TD:NTH-CHILD(10)</STYLE> </HEAD> <TABLE BORDER = 1;> <THEAD> <TR STYLE="BACKGROUND-COLOR: #4F6E9C; COLOR: WHITE; FONT-SIZE: 13PX"><TH> SETT_NO </TH> <TH> SETT_TYPE </TH><TH> SCRIP_CD </TH><TH> PARTY_CODE </TH><TH> QTY </TH><TH> I_ISIN </TH> <TH> DPID </TH><TH> CLTDPID </TH> <TH> CLOSING </TH> <TH> TOTAL </TH> </TR> </THEAD><TBODY STYLE="FONT-SIZE: 13PX;">'              
          
SET @TBLE = @TBLE + @XML +'</TBODY></TABLE></BODY></HTML>'          
          
