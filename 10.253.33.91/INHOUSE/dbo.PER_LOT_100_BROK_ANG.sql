CREATE PROC [dbo].[PER_LOT_100_BROK_ANG]
AS
SELECT DISTINCT SCHEME_MAPPING.SP_PARTY_CODE,SP_SCHEME_ID,SP_SCRIP,SP_MIN_BROKAMT,SP_MAX_BROKAMT,'NSEFO' AS SEGMENT INTO #SCHEME_MAPPING
FROM ANGELFO.NSEFO.DBO.SCHEME_MAPPING WITH (NOLOCK)  WHERE GETDATE() BETWEEN SP_DATE_FROM AND SP_DATE_TO
AND (SP_MIN_BROKAMT > 100 OR SP_MAX_BROKAMT > 100) 
AND SP_INST_TYPE = 'OPT'

 INSERT INTO #SCHEME_MAPPING  
SELECT DISTINCT SCHEME_MAPPING.SP_PARTY_CODE,SP_SCHEME_ID,SP_PRODUCT_CODE,SP_MIN_BROKAMT,SP_MAX_BROKAMT,'BSEFO'  AS SEGMENT  
FROM ANGELCOMMODITY.BSEFO.DBO.SCHEME_MAPPING WITH (NOLOCK) WHERE GETDATE() BETWEEN SP_DATE_FROM AND SP_DATE_TO  
AND (SP_MIN_BROKAMT > 100 OR SP_MAX_BROKAMT > 100)   
AND SP_PRODUCT_TYPE = 'OPT'

 INSERT INTO #SCHEME_MAPPING  
SELECT DISTINCT SCHEME_MAPPING.SP_PARTY_CODE,SP_SCHEME_ID,SP_SCRIP,SP_MIN_BROKAMT,SP_MAX_BROKAMT,'NSECD' AS  SEGMENT 
FROM ANGELFO.NSECURFO.DBO.SCHEME_MAPPING WITH (NOLOCK) WHERE GETDATE() BETWEEN SP_DATE_FROM AND SP_DATE_TO  
AND (SP_MIN_BROKAMT > 100 OR SP_MAX_BROKAMT > 100)   
AND SP_INST_TYPE = 'OPT'  

INSERT INTO #SCHEME_MAPPING  
SELECT DISTINCT SCHEME_MAPPING.SP_PARTY_CODE,SP_SCHEME_ID,SP_SCRIP,SP_MIN_BROKAMT,SP_MAX_BROKAMT,'MCDX'  AS SEGMENT  
FROM ANGELCOMMODITY.MCDX.DBO.SCHEME_MAPPING WITH (NOLOCK) WHERE GETDATE() BETWEEN SP_DATE_FROM AND SP_DATE_TO  
AND (SP_MIN_BROKAMT > 100 OR SP_MAX_BROKAMT > 100)   
AND SP_INST_TYPE = 'OPT' 
  
INSERT INTO #SCHEME_MAPPING  
SELECT DISTINCT SCHEME_MAPPING.SP_PARTY_CODE,SP_SCHEME_ID,SP_SCRIP,SP_MIN_BROKAMT,SP_MAX_BROKAMT,'NCDX'  AS SEGMENT  
FROM ANGELCOMMODITY.NCDX.DBO.SCHEME_MAPPING WITH (NOLOCK) WHERE GETDATE() BETWEEN SP_DATE_FROM AND SP_DATE_TO  
AND (SP_MIN_BROKAMT > 100 OR SP_MAX_BROKAMT > 100)   
AND SP_INST_TYPE = 'OPT' 

INSERT INTO #SCHEME_MAPPING  
SELECT DISTINCT SCHEME_MAPPING.SP_PARTY_CODE,SP_SCHEME_ID,SP_SCRIP,SP_MIN_BROKAMT,SP_MAX_BROKAMT,'NCE' AS SEGMENT
FROM ANGELCOMMODITY.NCE.DBO.SCHEME_MAPPING WITH (NOLOCK) WHERE GETDATE() BETWEEN SP_DATE_FROM AND SP_DATE_TO  
AND (SP_MIN_BROKAMT > 100 OR SP_MAX_BROKAMT > 100)   
AND SP_INST_TYPE = 'OPT' 


SELECT A.SP_PARTY_CODE,A.SP_SCHEME_ID,A.SEGMENT,[ALL_MIN],[BANKNIFTY_MIN],[NIFTY_MIN],[MINIFTY_MIN],[FINNIFTY_MIN],[BKXOPT_MIN],[BSXOPT_MIN],[SX50OPT_MIN],[GOLD_MIN],[COPPER_MIN],[NATURALGAS_MIN],[Zinc_MIN],[CRUDEOIL_MIN],[SILVER_MIN],[ALL_MAX],[BANKNIFTY_MAX],[NIFTY_MAX],[MINIFTY_MAX],[FINNIFTY_MAX],[BKXOPT_MAX],[BSXOPT_MAX],[SX50OPT_MAX],[GOLD_MAX],[COPPER_MAX],[NATURALGAS_MAX],[Zinc_MAX],[CRUDEOIL_MAX],[SILVER_MAX] INTO #CHECK  
FROM   
(  
SELECT SP_PARTY_CODE,SP_SCHEME_ID,SEGMENT,ISNULL([ALL],0) AS [ALL_MIN],ISNULL([BANKNIFTY],0) AS [BANKNIFTY_MIN],ISNULL([NIFTY],0) AS [NIFTY_MIN],ISNULL([MINIFTY],0) AS [MINIFTY_MIN],ISNULL([FINNIFTY],0) AS [FINNIFTY_MIN],ISNULL([BKXOPT],0) AS [BKXOPT_MIN],ISNULL([BSXOPT],0) AS [BSXOPT_MIN],ISNULL([SX50OPT],0) AS [SX50OPT_MIN],ISNULL([GOLD],0) AS [GOLD_MIN],
ISNULL([COPPER],0) AS [COPPER_MIN],ISNULL([NATURALGAS],0) AS [NATURALGAS_MIN],ISNULL([ZINC],0) AS [ZINC_MIN],ISNULL([CRUDEOIL],0) AS [CRUDEOIL_MIN],
 ISNULL([SILVER],0) AS [SILVER_MIN]   
FROM    
(SELECT DISTINCT SP_PARTY_CODE,SP_SCHEME_ID,SEGMENT,SP_SCRIP,SP_MIN_BROKAMT FROM #SCHEME_MAPPING) AS SOURCETABLE  
PIVOT ( SUM(SP_MIN_BROKAMT)  FOR SP_SCRIP IN ([ALL],[BANKNIFTY],[NIFTY],[MINIFTY],[FINNIFTY],[BKXOPT],[BSXOPT],[SX50OPT],[GOLD],[COPPER],[NATURALGAS],[ZINC],[CRUDEOIL],[SILVER])) AS PIVOUTABLE1  
)A  
FULL OUTER JOIN  
(SELECT SP_PARTY_CODE,SP_SCHEME_ID,SEGMENT,ISNULL([ALL],0) AS [ALL_MAX],ISNULL([BANKNIFTY],0) AS [BANKNIFTY_MAX],ISNULL([NIFTY],0) AS [NIFTY_MAX],ISNULL([MINIFTY],0) AS [MINIFTY_MAX],ISNULL([FINNIFTY],0) AS [FINNIFTY_MAX],ISNULL([BKXOPT],0) AS [BKXOPT_MAX],ISNULL([BSXOPT],0) AS [BSXOPT_MAX],ISNULL([SX50OPT],0) AS [SX50OPT_MAX],ISNULL([GOLD],0) AS [GOLD_MAX],ISNULL([COPPER],0) AS [COPPER_MAX],
ISNULL([NATURALGAS],0) AS [NATURALGAS_MAX],ISNULL([ZINC],0) AS [ZINC_MAX],ISNULL([CRUDEOIL],0) AS [CRUDEOIL_MAX],ISNULL([SILVER],0) AS [SILVER_MAX]           
FROM    
(SELECT DISTINCT SP_PARTY_CODE,SP_SCHEME_ID,SEGMENT,SP_SCRIP,SP_MAX_BROKAMT FROM #SCHEME_MAPPING) AS SOURCETABLE  
PIVOT ( SUM(SP_MAX_BROKAMT)  FOR SP_SCRIP IN ([ALL],[BANKNIFTY],[NIFTY],[MINIFTY],[FINNIFTY],[BKXOPT],[BSXOPT],[SX50OPT],[GOLD],[COPPER],[NATURALGAS],[ZINC],[CRUDEOIL],[SILVER])) AS PIVOUTABLE1  
) B  ON (A.SP_PARTY_CODE = B.SP_PARTY_CODE AND A.SP_SCHEME_ID = B.SP_SCHEME_ID  AND A.SEGMENT =B.SEGMENT)  
ORDER BY A.SP_PARTY_CODE 

SELECT DISTINCT SP_PARTY_CODE INTO #CHECK1 FROM #SCHEME_MAPPING 


SELECT SP_PARTY_CODE,BRANCH_CD,SUB_BROKER,CONVERT(VARCHAR(10),MAX(INACTIVE_FROM),105) AS INACTIVE_FROM,
CONVERT(VARCHAR(10),MIN(ACTIVE_DATE),105) AS ACTIVE_DATE
,B2B_B2C,MAX(DEACTIVE_VALUE)
AS DEACTIVE_VALUE
INTO #CHECK2
FROM #CHECK1 , MSAJAG.DBO.CLIENT_BROK_DETAILS CD WITH (NOLOCK) , MSAJAG.DBO.VW_BTB_BTC B , MSAJAG.DBO.CLIENT_DETAILS C WITH (NOLOCK)
WHERE SP_PARTY_CODE = CD.CL_CODE AND B.CL_CODE = CD.CL_CODE AND C.CL_CODE = CD.CL_CODE
GROUP BY SP_PARTY_CODE,B2B_B2C,BRANCH_CD,SUB_BROKER

SELECT 'SP_PARTY_CODE','SP_SCHEME_ID','SEGMENT','ALL_MIN','BANKNIFTY_MIN','NIFTY_MIN','MINIFTY_MIN','FINNIFTY_MIN','BKXOPT_MIN','BSXOPT_MIN','SX50OPT_MIN','GOLD_MIN','COPPER_MIN','NATURALGAS_MIN','ZINC_MIN','CRUDEOIL_MIN','SILVER_MIN','ALL_MAX','BANKNIFTY_MAX','NIFTY_MAX','MINIFTY_MAX','FINNIFTY_MAX','BKXOPT_MAX','BSXOPT_MAX','SX50OPT_MAX','GOLD_MAX','COPPER_MAX','NATURALGAS_MAX','ZINC_MAX','CRUDEOIL_MAX','SILVER_MAX','INACTIVE_FROM','ACTIVE_DATE','DEACTIVE_VALUE','B2B_B2C','BRANCH_CD','SUB_BROKER',SRNO= 1
UNION ALL
SELECT CONVERT(VARCHAR(100),#CHECK.SP_PARTY_CODE) AS SP_PARTY_CODE,
CONVERT(VARCHAR(100),SP_SCHEME_ID) AS SP_SCHEME_ID,
CONVERT(VARCHAR(100),SEGMENT) AS SEGMENT,
CONVERT(VARCHAR(100),ALL_MIN),
CONVERT(VARCHAR(100),BANKNIFTY_MIN),
CONVERT(VARCHAR(100),NIFTY_MIN),
CONVERT(VARCHAR(100),MINIFTY_MIN),
CONVERT(VARCHAR(100),FINNIFTY_MIN),
CONVERT(VARCHAR(100),BKXOPT_MIN),
CONVERT(VARCHAR(100),BSXOPT_MIN),
CONVERT(VARCHAR(100),SX50OPT_MIN),
CONVERT(VARCHAR(100),GOLD_MIN),
CONVERT(VARCHAR(100),COPPER_MIN),
CONVERT(VARCHAR(100),NATURALGAS_MIN),
CONVERT(VARCHAR(100),ZINC_MIN),
CONVERT(VARCHAR(100),CRUDEOIL_MIN),
CONVERT(VARCHAR(100),SILVER_MIN),
CONVERT(VARCHAR(100),ALL_MAX),
CONVERT(VARCHAR(100),BANKNIFTY_MAX),CONVERT(VARCHAR(100),NIFTY_MAX),CONVERT(VARCHAR(100),MINIFTY_MAX),
CONVERT(VARCHAR(100),FINNIFTY_MAX),
CONVERT(VARCHAR(100),BKXOPT_MAX),
CONVERT(VARCHAR(100),BSXOPT_MAX),
CONVERT(VARCHAR(100),SX50OPT_MAX),
CONVERT(VARCHAR(100),GOLD_MAX),
CONVERT(VARCHAR(100),COPPER_MAX),
CONVERT(VARCHAR(100),NATURALGAS_MAX),
CONVERT(VARCHAR(100),ZINC_MAX),
CONVERT(VARCHAR(100),CRUDEOIL_MAX),
CONVERT(VARCHAR(100),SILVER_MAX),
CONVERT(VARCHAR(100),INACTIVE_FROM),CONVERT(VARCHAR(100),ACTIVE_DATE),CONVERT(VARCHAR(100),DEACTIVE_VALUE),
CONVERT(VARCHAR(100),B2B_B2C),
CONVERT(VARCHAR(100),BRANCH_CD),CONVERT(VARCHAR(100),SUB_BROKER),SRNO= 2  FROM #CHECK , #CHECK2 
WHERE #CHECK.SP_PARTY_CODE = #CHECK2.SP_PARTY_CODE AND DEACTIVE_VALUE NOT IN ('C','B','I','T')
ORDER BY SRNO 




DROP TABLE #SCHEME_MAPPING
DROP TABLE #CHECK1
DROP TABLE #CHECK2

GO
