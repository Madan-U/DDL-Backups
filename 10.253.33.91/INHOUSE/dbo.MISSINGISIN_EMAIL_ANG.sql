CREATE PROC MISSINGISIN_EMAIL_ANG

AS


SELECT DISTINCT SAUDA_DATE,SCRIPNAME,SETT_NO,SETT_TYPE INTO #MISSINGISIN
FROM MSAJAG.DBO.COMMON_CONTRACT_DATA WITH (NOLOCK)
WHERE SAUDA_DATE > GETDATE () -7 AND SEGMENT = 'CAPITAL'
AND SCRIPNAME NOT LIKE 'BROKERAGE%'
AND ISIN = ''
ORDER BY SAUDA_DATE,SCRIPNAME

DECLARE @EMAILS VARCHAR(4000),@BODYCONTENT VARCHAR(MAX),@TBLE VARCHAR(MAX),@PEOPLE VARCHAR(4000),@SNAME VARCHAR(4000),@SUB VARCHAR(4000),@SUB1 VARCHAR(4000),@XML VARCHAR(MAX)           

SET @XML =  ISNULL(CAST((SELECT [td/@align]='center',[td/@width]='25%',td = SAUDA_DATE,'',                    
    [td/@align]='center',[td/@width]='25%',td = SCRIPNAME ,'',             
    [td/@align]='center',[td/@width]='25%',td = SETT_NO ,'',             
    [td/@align]='center',[td/@width]='25%',td = SETT_TYPE,''     
FROM #MISSINGISIN  FOR XML PATH('TR'), ELEMENTS ) AS VARCHAR(MAX)) ,'') 

SET @SUB = 'Missing Isin in Contract Note '
  
SET @SUB1 = 'Please find Missing Isin in Contract Note '
     
