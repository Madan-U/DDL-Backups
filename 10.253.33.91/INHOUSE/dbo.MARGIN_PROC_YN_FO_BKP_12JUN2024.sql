CREATE PROC [dbo].[MARGIN_PROC_YN_FO_BKP_11JUN2024] (@MDATE VARCHAR(11),@MDATE_END VARCHAR(11) ,@CTYPE VARCHAR(1) = 'N')       
AS      
CREATE TABLE #MARGIN_PROV(EXCHANGE VARCHAR(10),SEGMENT VARCHAR(10),      
P_SAUDADATE VARCHAR(11),PARTY_CODE VARCHAR(10),PROV VARCHAR(1),SHORTAGE NUMERIC(18,2),      
PENALTY_AMT NUMERIC(18,2),POSITION_TYPE VARCHAR(5),LAST_SAUDA_DATE VARCHAR(11),PENALTY_PERC NUMERIC(18,2))      
      
DECLARE @MDATE_DT DATETIME = @MDATE ,@MDATE_DT_END DATETIME = @MDATE_END      
      
      
WHILE @MDATE_DT_END > = @MDATE_DT      
BEGIN      
      
SELECT MARGIN_DATE,PARTY_CODE,SHORTAGE,PENALTY_PERC,PENALTY_AMT,EXCHANGE = 'NSE',SEGMENT= 'FUTURES' INTO #MARGIN       
FROM  ANGELFO.NSEFO.DBO.FOMARGIN_PENALTY WITH (NOLOCK) WHERE MARGIN_DATE = @MDATE_DT      
      
      
SELECT FOEXERCISEALLOCATION.* INTO #FOEXERCISEALLOCATION FROM ANGELFO.NSEFO.DBO.FOEXERCISEALLOCATION WITH (NOLOCK) ,#MARGIN       
WHERE  FOEA_POSITION_DATE = @MDATE_DT AND PARTY_CODE = FOEA_PARTY_CODE      
      
      
ALTER TABLE #MARGIN ADD POSITION_TYPE VARCHAR(5)       
      
ALTER TABLE #MARGIN ADD PROV VARCHAR(1)       
      
UPDATE #MARGIN SET POSITION_TYPE  = '',PROV = ''      
      
;WITH CTE AS      
(      
SELECT DISTINCT FOEA_PARTY_CODE FROM #FOEXERCISEALLOCATION WITH (NOLOCK)  , #MARGIN WHERE FOEA_POSITION_DATE = MARGIN_DATE      
AND  FOEA_BROUGHT_FORWARD_LONG_QUANTITY = 0 AND FOEA_BROUGHT_FORWARD_SHORT_QUANTITY = 0      
AND (FOEA_DAY_BUY_OPEN_QUANTITY <> 0 OR FOEA_DAY_SELL_OPEN_QUANTITY <> 0) AND PARTY_CODE = FOEA_PARTY_CODE AND FOEA_PARTY_CODE NOT IN      
(SELECT DISTINCT FOEA_PARTY_CODE FROM #FOEXERCISEALLOCATION WITH (NOLOCK)  , #MARGIN WHERE FOEA_POSITION_DATE = MARGIN_DATE      
AND (FOEA_BROUGHT_FORWARD_LONG_QUANTITY <> 0 OR FOEA_BROUGHT_FORWARD_SHORT_QUANTITY <> 0)      
AND (FOEA_DAY_BUY_OPEN_QUANTITY <> 0 OR FOEA_DAY_SELL_OPEN_QUANTITY <> 0) AND PARTY_CODE = FOEA_PARTY_CODE) )      
      
UPDATE #MARGIN SET POSITION_TYPE = 'T-DAY' ,PROV = 'Y'  FROM CTE WHERE FOEA_PARTY_CODE = PARTY_CODE      
      
SELECT FOEA_PARTY_CODE,MAX(REMRKS) REMRKS INTO #POS      
  FROM (      
SELECT *,      
REMRKS=      
(CASE WHEN OPEN_QTY >0 AND CLOSE_QTY <0 THEN 'Y' ELSE (CASE WHEN OPEN_QTY<0 AND CLOSE_QTY >0 THEN 'Y' ELSE      
(CASE WHEN (CASE WHEN OPEN_QTY<0 THEN OPEN_QTY*-1 ELSE OPEN_QTY END )>=      
(CASE WHEN CLOSE_QTY<0 THEN CLOSE_QTY*-1 ELSE CLOSE_QTY END ) THEN 'N' ELSE 'Y' END )  END)END)      
 FROM (      
SELECT FOEA_PARTY_CODE,FOEA_SYMBOL,FOEA_EXPIRYDATE,FOEA_STRIKE_PRICE,FOEA_INST_TYPE,FOEA_OPTION_TYPE,      
OPEN_QTY=FOEA_BROUGHT_FORWARD_LONG_QUANTITY-FOEA_BROUGHT_FORWARD_SHORT_QUANTITY,TDAY_QTY=FOEA_DAY_BUY_OPEN_QUANTITY-FOEA_DAY_SELL_OPEN_QUANTITY,      
  CLOSE_QTY=(FOEA_BROUGHT_FORWARD_LONG_QUANTITY-FOEA_BROUGHT_FORWARD_SHORT_QUANTITY+FOEA_DAY_BUY_OPEN_QUANTITY-FOEA_DAY_SELL_OPEN_QUANTITY)      
  FROM #FOEXERCISEALLOCATION WITH (NOLOCK)  , #MARGIN WHERE FOEA_POSITION_DATE = MARGIN_DATE AND PARTY_CODE = FOEA_PARTY_CODE        
  AND PROV=''      
  AND (FOEA_BROUGHT_FORWARD_LONG_QUANTITY+FOEA_BROUGHT_FORWARD_SHORT_QUANTITY+FOEA_DAY_BUY_OPEN_QUANTITY+FOEA_DAY_SELL_OPEN_QUANTITY)<>0 )A )A      
  GROUP BY FOEA_PARTY_CODE      
       
UPDATE #MARGIN SET POSITION_TYPE = 'INCDC' ,PROV = REMRKS  FROM #POS WHERE FOEA_PARTY_CODE = PARTY_CODE AND POSITION_TYPE  = ''      
      
       
SELECT DISTINCT CONVERT(VARCHAR(11),SAUDA_DATE,109) AS P_SAUDA_DATE,      
A.PARTY_CODE--,SCRIP_MST = INST_TYPE+SYMBOL+CONVERT(VARCHAR(50),EXPIRYDATE)+OPTION_TYPE+CONVERT(VARCHAR(50),STRIKE_PRICE)      
INTO #TEST FROM ANGELFO.NSEFO.DBO.FOBILLVALAN A WITH (NOLOCK)  , #MARGIN B      
WHERE SAUDA_DATE BETWEEN  CONVERT(VARCHAR(11),@MDATE_DT) AND CONVERT(VARCHAR(11),@MDATE_DT) + ' 23:59:59'   AND A.PARTY_CODE  = B.PARTY_CODE  --AND POSITION_TYPE  = ''      
AND TRADETYPE = 'BF' AND AUCTIONPART <> 'CA'      
      
SELECT DISTINCT P_SAUDA_DATE,A.PARTY_CODE,--,SCRIP_MST = A.INST_TYPE+A.SYMBOL+CONVERT(VARCHAR(50),A.EXPIRYDATE)+A.OPTION_TYPE+CONVERT(VARCHAR(50),A.STRIKE_PRICE),      
CONVERT(VARCHAR(11),MAX(SAUDA_DATE),109) AS LAST_SAUDA_DATE INTO #TEST2      
FROM ANGELFO.NSEFO.DBO.FOBILLVALAN  A WITH (NOLOCK)  , #TEST B      
WHERE A.PARTY_CODE = B.PARTY_CODE-- AND (A.INST_TYPE+A.SYMBOL+CONVERT(VARCHAR(50),A.EXPIRYDATE)+A.OPTION_TYPE+CONVERT(VARCHAR(50),A.STRIKE_PRICE)) = B.SCRIP_MST      
AND SAUDA_DATE BETWEEN CONVERT(DATETIME,@MDATE_DT) - 30 AND CONVERT(DATETIME,@MDATE_DT) --+ ' 23:59:59'      
AND TRADETYPE = 'BT' AND AUCTIONPART <> 'CA'      
GROUP BY P_SAUDA_DATE,A.PARTY_CODE      
      
;WITH CTE AS (      
SELECT #TEST2.PARTY_CODE FROM ANGELFO.NSEFO.DBO.FOMARGIN_PENALTY A WITH (NOLOCK) ,#TEST2      
WHERE A.MARGIN_DATE = #TEST2.LAST_SAUDA_DATE AND #TEST2.PARTY_CODE = A.PARTY_CODE )      
      
UPDATE #MARGIN SET POSITION_TYPE = 'BFPSH' ,PROV = 'Y'  FROM CTE WHERE CTE.PARTY_CODE = #MARGIN.PARTY_CODE AND POSITION_TYPE  = ''      
      
UPDATE #MARGIN SET POSITION_TYPE = 'BFPSN' ,PROV = 'N'  WHERE POSITION_TYPE  = ''      
  
;WITH CTE AS (SELECT FOEA_POSITION_DATE,FOEA_PARTY_CODE,  
FOEA_INST_TYPE,  
FOEA_SYMBOL,  
FOEA_EXPIRYDATE,  
FOEA_STRIKE_PRICE,  
FOEA_OPTION_TYPE,  
FOEA_BROUGHT_FORWARD_LONG_QUANTITY,  
FOEA_BROUGHT_FORWARD_SHORT_QUANTITY,  
FOEA_DAY_BUY_OPEN_QUANTITY,  
FOEA_DAY_SELL_OPEN_QUANTITY,  
FOEA_POSTEXALL_LONGQTY,  
FOEA_POSTEXALL_SHORTQTY   
FROM #FOEXERCISEALLOCATION WITH (NOLOCK)  
WHERE  FOEA_POSITION_DATE = @MDATE_DT )  
  
  
, CTE1 AS (SELECT FOEA_POSITION_DATE,FOEA_PARTY_CODE,FOEA_SYMBOL,FOEA_EXPIRYDATE,  
SUM(FOEA_BROUGHT_FORWARD_LONG_QUANTITY) AS FOEA_BROUGHT_FORWARD_LONG_QUANTITY,  
SUM(FOEA_BROUGHT_FORWARD_SHORT_QUANTITY) AS FOEA_BROUGHT_FORWARD_SHORT_QUANTITY,  
SUM(FOEA_DAY_BUY_OPEN_QUANTITY) AS FOEA_DAY_BUY_OPEN_QUANTITY,  
SUM(FOEA_DAY_SELL_OPEN_QUANTITY) AS FOEA_DAY_SELL_OPEN_QUANTITY,  
SUM(FOEA_POSTEXALL_LONGQTY) AS FOEA_POSTEXALL_LONGQTY,  
SUM(FOEA_POSTEXALL_SHORTQTY) AS FOEA_POSTEXALL_SHORTQTY    
FROM CTE GROUP BY FOEA_POSITION_DATE,FOEA_PARTY_CODE,FOEA_SYMBOL,FOEA_EXPIRYDATE )  
  
  
SELECT CTE1.* INTO #CHECK FROM CTE1  ,  #MARGIN  
WHERE FOEA_PARTY_CODE = PARTY_CODE  
AND FOEA_POSITION_DATE = MARGIN_DATE  
AND FOEA_POSITION_DATE =  @MDATE_DT  
AND FOEA_BROUGHT_FORWARD_LONG_QUANTITY<>0 AND FOEA_BROUGHT_FORWARD_SHORT_QUANTITY <> 0  
  
;WITH CTE1 AS (SELECT DISTINCT FOEA_PARTY_CODE FROM (  
SELECT FOEA_POSITION_DATE,FOEA_PARTY_CODE,  
FOEA_INST_TYPE,  
FOEA_SYMBOL,  
FOEA_EXPIRYDATE,  
FOEA_STRIKE_PRICE,  
FOEA_OPTION_TYPE,  
FOEA_BROUGHT_FORWARD_LONG_QUANTITY-FOEA_BROUGHT_FORWARD_SHORT_QUANTITY AS CF_B_SELL,  
FOEA_DAY_BUY_OPEN_QUANTITY -FOEA_DAY_SELL_OPEN_QUANTITY AS BUY_SELL,  
FOEA_POSTEXALL_LONGQTY-FOEA_POSTEXALL_SHORTQTY AS CLOSE_POS  
FROM #FOEXERCISEALLOCATION WITH (NOLOCK)  
WHERE  FOEA_POSITION_DATE = @MDATE_DT --AND FOEA_PARTY_CODE = 'PTM7597'    
AND (FOEA_BROUGHT_FORWARD_LONG_QUANTITY-FOEA_BROUGHT_FORWARD_SHORT_QUANTITY)<>0 ) AS A  
WHERE CLOSE_POS <> CF_B_SELL)  
  
SELECT CTE1.* INTO #CHECK2 FROM CTE1  ,  #CHECK WHERE CTE1.FOEA_PARTY_CODE = #CHECK.FOEA_PARTY_CODE  
  
--SELECT FOEA_POSITION_DATE,FOEA_PARTY_CODE,  
--FOEA_INST_TYPE,  
--FOEA_SYMBOL,  
--FOEA_EXPIRYDATE,  
--FOEA_STRIKE_PRICE,  
--FOEA_OPTION_TYPE,  
--FOEA_BROUGHT_FORWARD_LONG_QUANTITY,  
--FOEA_BROUGHT_FORWARD_SHORT_QUANTITY,  
--FOEA_DAY_BUY_OPEN_QUANTITY,  
--FOEA_DAY_SELL_OPEN_QUANTITY,  
--FOEA_POSTEXALL_LONGQTY,  
--FOEA_POSTEXALL_SHORTQTY   
--FROM #FOEXERCISEALLOCATION WITH (NOLOCK)  
--WHERE FOEA_POSITION_DATE = @MDATE_DT AND FOEA_PARTY_CODE IN (SELECT FOEA_PARTY_CODE FROM #CHECK2)  
--ORDER BY 2,3,4,5,6,7  
  
SELECT DISTINCT FOEA_PARTY_CODE INTO #CHECK3  
FROM #FOEXERCISEALLOCATION WITH (NOLOCK)  
WHERE FOEA_POSITION_DATE = @MDATE_DT AND FOEA_PARTY_CODE IN (SELECT FOEA_PARTY_CODE FROM #CHECK2)  
  
  
IF @MDATE_DT >= 'JAN 30 2023'  OR @CTYPE = 'O'  
BEGIN  
UPDATE #MARGIN SET POSITION_TYPE = 'HEDGE' ,PROV = 'Y' FROM #CHECK3 WHERE FOEA_PARTY_CODE = PARTY_CODE  
END  
  
DROP TABLE #CHECK3  
DROP TABLE #CHECK2  
DROP TABLE #CHECK  
  
      
ALTER TABLE #MARGIN ADD LAST_SAUDA_DATE VARCHAR(11)      
      
UPDATE #MARGIN SET LAST_SAUDA_DATE = ''      
      
UPDATE #MARGIN SET #MARGIN.LAST_SAUDA_DATE = #TEST2.LAST_SAUDA_DATE FROM #TEST2      
WHERE   #MARGIN.PARTY_CODE = #TEST2.PARTY_CODE      
      
SET @MDATE_DT = @MDATE_DT + 1      
      
INSERT INTO #MARGIN_PROV      
SELECT EXCHANGE , SEGMENT,MARGIN_DATE,PARTY_CODE,PROV,SHORTAGE,PENALTY_AMT,POSITION_TYPE,LAST_SAUDA_DATE,PENALTY_PERC       
FROM #MARGIN      
      
      
DROP TABLE #MARGIN      
DROP TABLE #TEST      
DROP TABLE #TEST2      
DROP TABLE #POS      
DROP TABLE #FOEXERCISEALLOCATION      
      
END      
      
SELECT * FROM #MARGIN_PROV      
      
DROP TABLE #MARGIN_PROV

GO
