-- Object: PROCEDURE dbo.MARGIN_PROC_YN_CASH
-- Server: 10.253.33.91 | DB: INHOUSE
--------------------------------------------------

  
CREATE PROC MARGIN_PROC_YN_CASH (@MDATE VARCHAR(11),@MDATE_END VARCHAR(11))  
AS  
CREATE TABLE #MARGIN_PROV(EXCHANGE VARCHAR(10),SEGMENT VARCHAR(10),  
P_SAUDADATE VARCHAR(11),PARTY_CODE VARCHAR(10),PROV VARCHAR(1),SHORTAGE NUMERIC(18,2),  
PENALTY_AMT NUMERIC(18,2),POSITION_TYPE VARCHAR(5),LAST_SAUDA_DATE VARCHAR(11),PENALTY_PERC NUMERIC(18,2))  
  
DECLARE @MDATE_DT DATETIME = @MDATE ,@MDATE_DT_END DATETIME = @MDATE_END  
  
WHILE @MDATE_DT_END > = @MDATE_DT  
BEGIN  
  
SELECT  MARGIN_DATE,PARTY_CODE,SHORTAGE,PENALTY_PERC,PENALTY_AMT,EXCHANGE = 'NSE',SEGMENT = 'CAPITAL' INTO #MARGIN   
FROM  MSAJAG.DBO.CMMARGIN_PENALTY WITH (NOLOCK) WHERE MARGIN_DATE  = @MDATE_DT  
  
ALTER TABLE #MARGIN ADD LAST_SAUDA_DATE VARCHAR(11)  
  
;WITH CTE AS (SELECT DISTINCT PARTY_CODE  FROM MSAJAG.DBO.CMBILLVALAN WITH (NOLOCK) WHERE SAUDA_DATE BETWEEN CONVERT(VARCHAR(11),@MDATE_DT)   
AND  CONVERT(VARCHAR(11),@MDATE_DT) + ' 23:59:59')  
  
UPDATE #MARGIN SET LAST_SAUDA_DATE = 'Y' FROM CTE WITH (NOLOCK) WHERE   CTE.PARTY_CODE = #MARGIN.PARTY_CODE  
  
INSERT INTO #MARGIN_PROV  
SELECT EXCHANGE ,SEGMENT,MARGIN_DATE,PARTY_CODE,PROV='',SHORTAGE,PENALTY_AMT,POSITION_TYPE='',LAST_SAUDA_DATE,PENALTY_PERC FROM #MARGIN  
  
DROP TABLE #MARGIN  
  
SET  @MDATE_DT = @MDATE_DT + 1  
  
END   
  
SELECT * FROM #MARGIN_PROV  
  
DROP TABLE #MARGIN_PROV

GO
