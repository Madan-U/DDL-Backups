DECLARE @SAUDA_DATE VARCHAR(11)

SELECT @SAUDA_DATE = MAX(SAUDA_DATE) FROM  MTFTRADE.DBO.TBL_MTF_DATA   WITH (NOLOCK) 


DECLARE  @MTFBOOKCLASS NUMERIC(18,2), @MTFBOOKINHOUSE NUMERIC(18,2),@MTFDIFF NUMERIC(18,2)


SELECT SUM(NETAMOUNT) AS   NETAMOUNT INTO #NSERPT  
FROM MTFTRADE.DBO.TBL_NSE_REPORTING P  WITH (NOLOCK) , MSAJAG.DBO.CLIENT_DETAILS C (NOLOCK)    
WHERE SAUDA_DATE = @SAUDA_DATE  AND C.CL_CODE = P.PARTY_CODE            
AND (BODQTY + FRESHBQTY + FRESHSQTY) > 0   
    

SELECT * INTO #MTFEXCHANGEFILEDATA FROM mimansa.CLIENTSERVICE.DBO.MTFEXCHANGEFILEDATA WHERE REPORTINGDATE = @SAUDA_DATE


SELECT @MTFBOOKCLASS =  SUM(NETAMOUNT)  FROM   #NSERPT   

SELECT @MTFBOOKINHOUSE = SUM(EODVALUE) FROM #MTFEXCHANGEFILEDATA


