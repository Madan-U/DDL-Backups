      SET @sql=@sql+' AND VDT <'''+@FROMDATE+''''
      SET @sql=@sql+' GROUP BY CLTCODE'
    
      INSERT INTO #TEMP_OPEN_CLOSING_BLNC
      EXEC sp_executesql @sql; 

      set @Min = @Min+1
