-- Object: PROCEDURE dbo.Dupreccheck
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC [dbo].[Dupreccheck] AS                              
DECLARE                              
@SAUDA_DATE VARCHAR(11)                                
                              
DELETE FROM FTTRADE WHERE PARTY_CODE = 'NSE'                              
    
DELETE FROM FTTRADE WHERE STATUS = '13'    
                             
UPDATE FTTRADE SET BRANCH_ID = PARTY_CODE, DUMMY1 = MARKETRATE                            

SELECT TOP 1 @SAUDA_DATE = LEFT(SAUDA_DATE,11) FROM FTTRADE                      
          
INSERT INTO TRD_SETT_POS_ROLL          
SELECT SAUDA_DATE, ORDER_NO, TRADE_NO, SCRIP_CD, SERIES, EXPIRYDATE=LEFT(CONVERT(DATETIME,EXPIRYDATE),11), TRADEQTY, SELL_BUY FROM FTTRADE          
WHERE NOT EXISTS (SELECT SAUDA_DATE FROM TRD_SETT_POS_ROLL          
      WHERE TRD_SETT_POS_ROLL.SAUDA_DATE LIKE @SAUDA_DATE + '%'         
      AND LEFT(FTTRADE.SAUDA_DATE,11) = LEFT(TRD_SETT_POS_ROLL.SAUDA_DATE,11)
      AND FTTRADE.ORDER_NO = TRD_SETT_POS_ROLL.ORDER_NO          
      AND FTTRADE.TRADE_NO = TRD_SETT_POS_ROLL.TRADE_NO          
      AND FTTRADE.SCRIP_CD = TRD_SETT_POS_ROLL.SCRIP_CD          
      AND FTTRADE.SERIES = TRD_SETT_POS_ROLL.SERIES          
      AND LEFT(CONVERT(DATETIME,FTTRADE.EXPIRYDATE),11) = LEFT(CONVERT(DATETIME,TRD_SETT_POS_ROLL.EXPIRYDATE),11))           
AND SERIES IN (SELECT ROLL_SERIES FROM SLBS_SERIES_MAPPING)

UPDATE TRD_SETT_POS_ROLL SET SELL_BUY = FTTRADE.SELL_BUY
FROM FTTRADE 
WHERE TRD_SETT_POS_ROLL.SAUDA_DATE LIKE @SAUDA_DATE  + '%'    
      AND LEFT(FTTRADE.SAUDA_DATE,11) = LEFT(TRD_SETT_POS_ROLL.SAUDA_DATE,11)      
      AND FTTRADE.ORDER_NO = TRD_SETT_POS_ROLL.ORDER_NO      
      AND FTTRADE.TRADE_NO = TRD_SETT_POS_ROLL.TRADE_NO      
      AND FTTRADE.SCRIP_CD = TRD_SETT_POS_ROLL.SCRIP_CD      
      AND FTTRADE.SERIES = TRD_SETT_POS_ROLL.SERIES      
      AND LEFT(CONVERT(DATETIME,FTTRADE.EXPIRYDATE),11) = TRD_SETT_POS_ROLL.EXPIRYDATE      
      AND FTTRADE.SELL_BUY > TRD_SETT_POS_ROLL.SELL_BUY
AND TRD_SETT_POS_ROLL.SERIES IN (SELECT ROLL_SERIES FROM SLBS_SERIES_MAPPING)

INSERT INTO FTTRADE
SELECT TRADE_NO,ORDER_NO,STATUS,SCRIP_CD,SERIES=SRC_SERIES,SCRIPNAME,INSTRUMENT,BOOKTYPE,MARKETTYPE,USER_ID,PARTIPANTCODE,BRANCH_ID,
	   SELL_BUY=(CASE WHEN SELL_BUY = 2 THEN 3 WHEN SELL_BUY = 1 THEN 4 ELSE SELL_BUY END),
	   TRADEQTY,MARKETRATE=0,PRO_CLI,PARTY_CODE,AUCTIONPART,AUCTIONNO,SETTNO,SAUDA_DATE,TRADEMODIFIED,CPID,SETTFLAG,
	   TMARK,SCHEME,DUMMY1=0,DUMMY2,EXPIRYDATE 
FROM FTTRADE, SLBS_SERIES_MAPPING 
WHERE SERIES = ROLL_SERIES 

INSERT INTO FTTRADE
SELECT TRADE_NO,ORDER_NO,STATUS,SCRIP_CD,SERIES=TGT_SERIES,SCRIPNAME,INSTRUMENT,BOOKTYPE,MARKETTYPE,USER_ID,PARTIPANTCODE,BRANCH_ID,
	   SELL_BUY=(CASE WHEN SELL_BUY = 2 
					  THEN 2 
					  WHEN SELL_BUY = 1 
					  THEN 1 
					  ELSE SELL_BUY+1 
				 END),
	   TRADEQTY,MARKETRATE,PRO_CLI,PARTY_CODE,AUCTIONPART,AUCTIONNO,
	   SETTNO = (CASE WHEN CONVERT(INT,TGT_EXPSERIES) >= MONTH(SAUDA_DATE) 
					  THEN CONVERT(VARCHAR,YEAR(SAUDA_DATE)) + '5' + RIGHT('00'+CONVERT(VARCHAR,TGT_EXPSERIES),2)
					  ELSE CONVERT(VARCHAR,YEAR(SAUDA_DATE)+1) + '5' + RIGHT('00'+CONVERT(VARCHAR,TGT_EXPSERIES),2)
				 END),
	   SAUDA_DATE,TRADEMODIFIED,CPID,SETTFLAG,
	   TMARK,SCHEME,DUMMY1,DUMMY2,EXPIRYDATE='1900-01-01' 
FROM FTTRADE, SLBS_SERIES_MAPPING 
WHERE SERIES = ROLL_SERIES 

DELETE FROM FTTRADE
WHERE SERIES IN (SELECT ROLL_SERIES FROM SLBS_SERIES_MAPPING)

UPDATE FTTRADE SET EXPIRYDATE = REPLACE(CONVERT(VARCHAR,SEC_PAYIN,106),' ',''), SETTNO = '7' 
FROM SETT_MST S
WHERE SETTNO = SETT_NO
AND SETT_TYPE = 'P'
AND EXPIRYDATE = '1900-01-01' 

IF(SELECT ISNULL(COUNT(1),0) FROM FTTRADE
   WHERE EXPIRYDATE = '1900-01-01') > 0
BEGIN
	RETURN
END

SELECT TOP 1 @SAUDA_DATE = LEFT(SAUDA_DATE,11) FROM FTTRADE                      
          
INSERT INTO TRD_SETT_POS          
SELECT SAUDA_DATE, ORDER_NO, TRADE_NO, SCRIP_CD, SERIES, EXPIRYDATE=LEFT(CONVERT(DATETIME,EXPIRYDATE),11), TRADEQTY, SELL_BUY FROM FTTRADE          
WHERE NOT EXISTS (SELECT SAUDA_DATE FROM TRD_SETT_POS          
      WHERE TRD_SETT_POS.SAUDA_DATE LIKE @SAUDA_DATE + '%'         
      AND LEFT(FTTRADE.SAUDA_DATE,11) = LEFT(TRD_SETT_POS.SAUDA_DATE,11)
      AND FTTRADE.ORDER_NO = TRD_SETT_POS.ORDER_NO          
      AND FTTRADE.TRADE_NO = TRD_SETT_POS.TRADE_NO          
      AND FTTRADE.SCRIP_CD = TRD_SETT_POS.SCRIP_CD          
      AND FTTRADE.SERIES = TRD_SETT_POS.SERIES          
      AND LEFT(CONVERT(DATETIME,FTTRADE.EXPIRYDATE),11) = LEFT(CONVERT(DATETIME,TRD_SETT_POS.EXPIRYDATE),11))           

UPDATE TRD_SETT_POS SET SELL_BUY = FTTRADE.SELL_BUY
FROM FTTRADE 
WHERE TRD_SETT_POS.SAUDA_DATE LIKE @SAUDA_DATE  + '%'    
      AND LEFT(FTTRADE.SAUDA_DATE,11) = LEFT(TRD_SETT_POS.SAUDA_DATE,11)      
      AND FTTRADE.ORDER_NO = TRD_SETT_POS.ORDER_NO      
      AND FTTRADE.TRADE_NO = TRD_SETT_POS.TRADE_NO      
      AND FTTRADE.SCRIP_CD = TRD_SETT_POS.SCRIP_CD      
      AND FTTRADE.SERIES = TRD_SETT_POS.SERIES      
      AND LEFT(CONVERT(DATETIME,FTTRADE.EXPIRYDATE),11) = TRD_SETT_POS.EXPIRYDATE      
      AND FTTRADE.SELL_BUY > TRD_SETT_POS.SELL_BUY
                
UPDATE FTTRADE SET AUCTIONNO = SELL_BUY,           
SELL_BUY = (CASE WHEN SELL_BUY = 3 THEN 1 WHEN SELL_BUY = 4 THEN 2 ELSE SELL_BUY END)            
                
UPDATE FTTRADE SET PARTY_CODE = C.PARTY_CODE                 
FROM MSAJAG.DBO.CLIENT_DETAILS C, MSAJAG.DBO.PRODUCT_MASTER P, MSAJAG.DBO.CLIENT_BROK_DETAILS D                
WHERE C.CL_CODE = D.CL_CODE AND D.EXCHANGE = 'NSE' AND D.SEGMENT = 'SLBS'                
AND C.PAN_GIR_NO = FTTRADE.PARTY_CODE                 
AND C.CL_TYPE <> 'INS'                
                
UPDATE FTTRADE SET PARTY_CODE = C.PARTY_CODE                 
FROM MSAJAG.DBO.CLIENT_DETAILS C, MSAJAG.DBO.PRODUCT_MASTER P, MSAJAG.DBO.CLIENT_BROK_DETAILS D                
WHERE C.CL_CODE = D.CL_CODE AND D.EXCHANGE = 'NSE' AND D.SEGMENT = 'SLBS'                
AND D.PARTICIPANT_CODE = FTTRADE.PARTIPANTCODE                
AND C.CL_TYPE = 'INS'                
                
INSERT INTO TRADE       
SELECT DISTINCT TRADE_NO,ORDER_NO,STATUS,SCRIP_CD,SERIES,SCRIPNAME,INSTRUMENT,BOOKTYPE,      
MARKETTYPE,USER_ID,PARTIPANTCODE,BRANCH_ID,SELL_BUY,TRADEQTY,MARKETRATE,PRO_CLI,PARTY_CODE,      
AUCTIONPART,AUCTIONNO,SETTNO,SAUDA_DATE,TRADEMODIFIED,CPID,                
SETTFLAG,TMARK,SCHEME,DUMMY1,DUMMY2 FROM FTTRADE WHERE TRADE_NO NOT IN                               
      ( SELECT TM.TRADE_NO FROM TRADE TM                              
   WHERE CONVERT(VARCHAR,FTTRADE.SAUDA_DATE,106) = CONVERT(VARCHAR,TM.SAUDA_DATE,106)                              
  AND FTTRADE.TRADE_NO = TM.TRADE_NO                              
         AND FTTRADE.ORDER_NO = TM.ORDER_NO                              
 AND FTTRADE.SCRIP_CD = TM.SCRIP_CD                              
 AND FTTRADE.MARKETRATE = TM.MARKETRATE                              
 AND FTTRADE.SELL_BUY = TM.SELL_BUY                              
     )                              
                              
TRUNCATE TABLE FTTRADE

GO
