-- Object: PROCEDURE dbo.CBO_GETREGIONLIST
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE PROC CBO_GETREGIONLIST
	@REGION_CODE VARCHAR(20) = '',
	@STATUSID VARCHAR(25) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'

AS
/*
	CBO_GETREGIONLIST '%', 'CLIENT', '001A000001'
*/
SET @REGION_CODE = LTRIM(RTRIM(@REGION_CODE))
IF @STATUSID = 'BROKER'
	BEGIN
		SELECT DISTINCT
				R.REGIONCODE,
				R.DESCRIPTION
			FROM
				REGION R
			WHERE
				R.REGIONCODE = CASE WHEN LEN(@REGION_CODE) > 0 AND @REGION_CODE <> '%' THEN @REGION_CODE ELSE R.REGIONCODE END
			ORDER BY
				R.REGIONCODE
	END
ELSE IF @STATUSID = 'BRANCH'
	BEGIN
		SELECT DISTINCT
				R.REGIONCODE,
				R.DESCRIPTION
			FROM
				REGION R,
				BRANCH B
			WHERE
				R.BRANCH_CODE = B.BRANCH_CODE
				AND R.REGIONCODE = CASE WHEN LEN(@REGION_CODE) > 0 AND @REGION_CODE <> '%' THEN @REGION_CODE ELSE R.REGIONCODE END
				AND B.BRANCH_CODE = @STATUSNAME
			ORDER BY
				R.REGIONCODE
	END
ELSE IF @STATUSID = 'AREA'
	BEGIN
		SELECT DISTINCT
				R.REGIONCODE,
				R.DESCRIPTION
			FROM
				REGION R,
				BRANCH B,
				AREA A
			WHERE
				R.BRANCH_CODE = B.BRANCH_CODE
				AND A.BRANCH_CODE = B.BRANCH_CODE
				AND R.REGIONCODE = CASE WHEN LEN(@REGION_CODE) > 0 AND @REGION_CODE <> '%' THEN @REGION_CODE ELSE R.REGIONCODE END
				AND A.AREACODE = @STATUSNAME
			ORDER BY
				R.REGIONCODE
	END
ELSE IF @STATUSID = 'REGION'
	BEGIN
		SELECT DISTINCT
				R.REGIONCODE,
				R.DESCRIPTION
			FROM
				REGION R
			WHERE
				R.REGIONCODE = CASE WHEN LEN(@REGION_CODE) > 0 AND @REGION_CODE <> '%' THEN @REGION_CODE ELSE R.REGIONCODE END
				AND R.REGIONCODE = @STATUSNAME
			ORDER BY
				R.REGIONCODE
	END
ELSE IF @STATUSID = 'SUBBROKER'
	BEGIN
		SELECT DISTINCT
				R.REGIONCODE,
				R.DESCRIPTION
			FROM
				REGION R,
				SUBBROKERS S
			WHERE
				R.BRANCH_CODE = S.BRANCH_CODE
				AND R.REGIONCODE = CASE WHEN LEN(@REGION_CODE) > 0 AND @REGION_CODE <> '%' THEN @REGION_CODE ELSE R.REGIONCODE END
				AND S.SUB_BROKER = @STATUSNAME
			ORDER BY
				R.REGIONCODE
	END
ELSE IF @STATUSID = 'TRADER'
	BEGIN
		SELECT DISTINCT
				R.REGIONCODE,
				R.DESCRIPTION
			FROM
				REGION R,
				BRANCHES B
			WHERE
				R.BRANCH_CODE = B.BRANCH_CD
				AND R.REGIONCODE = CASE WHEN LEN(@REGION_CODE) > 0 AND @REGION_CODE <> '%' THEN @REGION_CODE ELSE R.REGIONCODE END
				AND B.SHORT_NAME = @STATUSNAME
			ORDER BY
				R.REGIONCODE
	END
ELSE IF @STATUSID = 'FAMILY'
	BEGIN
		SELECT DISTINCT
				R.REGIONCODE,
				R.DESCRIPTION
			FROM
				REGION R,
				CLIENT1 C1
			WHERE
				R.REGIONCODE = C1.REGION
				AND R.REGIONCODE = CASE WHEN LEN(@REGION_CODE) > 0 AND @REGION_CODE <> '%' THEN @REGION_CODE ELSE R.REGIONCODE END
				AND C1.FAMILY = @STATUSNAME
			ORDER BY
				R.REGIONCODE
	END
ELSE IF @STATUSID = 'CLIENT'
	BEGIN
		SELECT DISTINCT
			C1.REGION
		INTO
			#CLIENT2
		FROM
			CLIENT1 C1,
			CLIENT2 C2
		WHERE
			C1.CL_CODE = C2.CL_CODE
			AND C2.PARTY_CODE = @STATUSNAME
		SELECT DISTINCT
			R.REGIONCODE,
			R.DESCRIPTION
		FROM
			REGION R,
			#CLIENT2 C2
		WHERE
			R.REGIONCODE = C2.REGION
			AND R.REGIONCODE = CASE WHEN LEN(@REGION_CODE) > 0 AND @REGION_CODE <> '%' THEN @REGION_CODE ELSE R.REGIONCODE END
		ORDER BY
			R.REGIONCODE
	END
ELSE
	BEGIN
		SELECT
			R.REGIONCODE,
			R.DESCRIPTION
		FROM
			REGION R
		WHERE
			1 = 0
	END

GO
