-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE  Proc Asian_Proc_FoOpenPosNCDX (@Sauda_Date Varchar(11))    
As    
SELECT HEADER = '07', PARTY_CODE, TRADINGAC = 'MARGIN', INST_TYPE, SYMBOL,     
EXPIRYDATE=REPLACE(CONVERT(VARCHAR,EXPIRYDATE,111),'/', '-'),     
STRIKE_PRICE, OPTION_TYPE,    
BUYQTY = (CASE WHEN SUM(PQTY-SQTY) > 0 THEN SUM(PQTY-SQTY) ELSE 0 END),    
SELLQTY = (CASE WHEN SUM(SQTY-PQTY) > 0 THEN SUM(SQTY-PQTY) ELSE 0 END),
BUYVALUE = (CASE WHEN SUM(PQTY) > 0 THEN SUM(PQTY*PRATE)/SUM(PQTY) ELSE 0 END),    
SELLVALUE = (CASE WHEN SUM(SQTY) > 0 THEN SUM(SQTY*SRATE)/SUM(SQTY) ELSE 0 END)    
INTO #NFO_POS FROM NCDX.DBO.FOBILLVALAN    
WHERE SAUDA_DATE LIKE @Sauda_Date + '%'    
AND EXPIRYDATE > @Sauda_Date + ' 23:59'    
GROUP BY PARTY_CODE, INST_TYPE, SYMBOL, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE    
HAVING SUM(PQTY-SQTY) <> 0    
    

    
UPDATE #NFO_POS SET 
	BUYVALUE = CL_RATE*BUYQTY*Numerator/Denominator, 
	SELLVALUE = CL_RATE*SELLQTY*Numerator/Denominator    
FROM NCDX.DBO.FOCLOSING F,    NCDX.DBO.FOSCRIP2 FOSCRIP2
WHERE F.TRADE_DATE LIKE @Sauda_Date + '%'    
AND F.INST_TYPE = #NFO_POS.INST_TYPE    
AND F.SYMBOL = #NFO_POS.SYMBOL    
AND REPLACE(CONVERT(VARCHAR,F.EXPIRYDATE,111),'/', '-') = #NFO_POS.EXPIRYDATE    
AND F.STRIKE_PRICE = #NFO_POS.STRIKE_PRICE    
AND F.OPTION_TYPE = #NFO_POS.OPTION_TYPE 
and f.inst_type like 'FUT%'  
AND FOSCRIP2.INST_TYPE = F.INST_TYPE
AND FOSCRIP2.SYMBOL = F.SYMBOL
AND LEFT(FOSCRIP2.EXPIRYDATE,11) = LEFT(F.EXPIRYDATE,11)
AND FOSCRIP2.STRIKE_PRICE = F.STRIKE_PRICE
AND FOSCRIP2.OPTION_TYPE = F.OPTION_TYPE


SELECT *    
FROM #NFO_POS    
ORDER BY PARTY_CODE, INST_TYPE, SYMBOL, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE

GO
