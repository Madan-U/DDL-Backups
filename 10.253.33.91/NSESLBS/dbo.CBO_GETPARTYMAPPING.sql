CREATE PROC CBO_GETPARTYMAPPING
	@OLDPARTY_CODE VARCHAR(12) = '',
	@STATUSID VARCHAR(25) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'
AS
	IF @STATUSID <> 'BROKER'
		BEGIN
			RAISERROR ('This Procedure is accessible to Broker', 16, 1)
			RETURN
		END
	SELECT 
		OLDPARTY_CODE, NEWPARTY_CODE
  		FROM  PARTYMAPPING
	WHERE
		OLDPARTY_CODE = CASE WHEN @OLDPARTY_CODE > 0 THEN @OLDPARTY_CODE ELSE OLDPARTY_CODE END
	ORDER BY
		OLDPARTY_CODE

GO
