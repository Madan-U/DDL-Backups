-- Object: PROCEDURE dbo.RPT_STT_MISMATCH
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE  PROC [dbo].[RPT_STT_MISMATCH] 
(
	@SAUDA_DATE VARCHAR(11),
	@FROMPARTY VARCHAR(10),
	@TOPARTY   VARCHAR(10),
	@FROMSCRIP VARCHAR(12),
	@TOSCRIP   VARCHAR(12),
	@DISFLAG VARCHAR(1), --'A - ALL, M -- MATCHED, U -- UNMATCHED
	@CHARGEFLAG VARCHAR(1), -- 'A -- ACTUAL, C -- CALCULATED,
	@REPBY VARCHAR(2) -- P - PARTY SUMMARY, S - SCRIP SUMMARY, PS -- PARTY SCRIP
)
AS

DECLARE @RECTYPE INT,
@COUNTER INT
SELECT @RECTYPE = (CASE WHEN @REPBY = 'P' THEN 20 ELSE 30 END),
@FROMSCRIP = (CASE WHEN @REPBY = 'P' THEN '' ELSE @FROMSCRIP END)

IF LEN(RTRIM(LTRIM(@SAUDA_DATE))) = 10
	BEGIN
		SET @SAUDA_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @SAUDA_DATE, 103), 109)
	END


-- EXEC RPT_STT_MISMATCH '01/06/2007', '0', 'ZZZZZ', '0', 'ZZZZZ', 'N', 'A', 'P' 

SELECT * INTO #SETT_MST FROM SETT_MST 
WHERE START_DATE LIKE @SAUDA_DATE + '%'


SELECT
	SAUDA_DATE,
	BOPARTYCODE=PARTY_CODE,
	SCRIP_CD,
	SERIES,
	BOSTT=SUM(TOTALSTT),
	EXPARTYCODE=CONVERT(VARCHAR(10),''),
	EXSTT=CONVERT(NUMERIC(18,4),0)
INTO
	#STT_CLT
FROM
	STT_CLIENTDETAIL
WHERE
	RECTYPE = @RECTYPE
	AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
	AND SCRIP_CD BETWEEN @FROMSCRIP AND @TOSCRIP
	AND 1 = (CASE WHEN @DISFLAG = 'N' 
		      THEN (CASE WHEN STT_CLIENTDETAIL.SETT_NO NOT IN (SELECT SETT_NO FROM #SETT_MST SETT_MST 
						  WHERE STT_CLIENTDETAIL.SETT_NO = SETT_MST.SETT_NO
						  AND STT_CLIENTDETAIL.SETT_TYPE = SETT_MST.SETT_TYPE ) 
				 THEN 1 
				 ELSE 0 
			    END)
		      ELSE 1 
		 END) 
GROUP BY
	SAUDA_DATE,
	PARTY_CODE,
	SCRIP_CD,
	SERIES

-- SELECT @DISFLAG, @RECTYPE, * FROM #STT_CLT

IF @CHARGEFLAG = 'A' 
	BEGIN
		UPDATE
			#STT_CLT
		SET
			BOSTT = 0
		FROM
			CLIENTTAXES_NEW C
		WHERE
			C.PARTY_CODE = #STT_CLT.BOPARTYCODE
			AND SAUDA_DATE BETWEEN FROMDATE AND TODATE
			AND TRANS_CAT = 'TRD'
			AND INSURANCE_CHRG = 0
			AND C.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
	END

SELECT
	STT_DATE,
	EXPARTYCODE=PARTY_CODE,
	SCRIP_CD,
	SERIES,
	EXSTT=SUM(TOTALSTT)
INTO
	#STT_EXG
FROM
	STT_EXCHG
WHERE
	RECTYPE = @RECTYPE
	AND STT_DATE LIKE @SAUDA_DATE + '%'
	AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
	AND SCRIP_CD BETWEEN @FROMSCRIP AND @TOSCRIP
	AND 1 = (CASE WHEN @DISFLAG = 'N' 
		      THEN (CASE WHEN SETT_NO NOT IN (SELECT SETT_NO FROM #SETT_MST SETT_MST 
						  WHERE STT_EXCHG.SETT_NO = SETT_MST.SETT_NO
						  AND STT_EXCHG.SETT_TYPE = SETT_MST.SETT_TYPE ) 
				 THEN 1 
				 ELSE 0 
			    END)
		      ELSE 1 
		 END)
GROUP BY
	STT_DATE,
	PARTY_CODE,
	SCRIP_CD,
	SERIES

SET @DISFLAG = (CASE WHEN @DISFLAG = 'N' THEN 'A' ELSE @DISFLAG END)

UPDATE
	#STT_CLT
SET
	EXPARTYCODE = #STT_EXG.EXPARTYCODE,
	EXSTT = #STT_EXG.EXSTT
FROM
	#STT_EXG 
WHERE
	SAUDA_DATE = STT_DATE
	AND BOPARTYCODE = #STT_EXG.EXPARTYCODE 
	AND #STT_CLT.SCRIP_CD = #STT_EXG.SCRIP_CD
	AND #STT_CLT.SERIES = #STT_EXG.SERIES

INSERT INTO
	#STT_CLT
SELECT
	STT_DATE,
	BOPARTYCODE = '',
	SCRIP_CD,
	SERIES,
	BOSTT = 0,
	EXPARTYCODE,
	EXSTT
FROM
	#STT_EXG
WHERE
	NOT EXISTS
		(
			SELECT
				BOPARTYCODE
			FROM
				#STT_CLT
			WHERE
				SAUDA_DATE = STT_DATE
				AND BOPARTYCODE = #STT_EXG.EXPARTYCODE
				AND #STT_CLT.SCRIP_CD = #STT_EXG.SCRIP_CD
				AND #STT_CLT.SERIES = #STT_EXG.SERIES
		)

IF @REPBY = 'P' OR @REPBY = 'PS' 
	BEGIN
		IF @DISFLAG = 'A'
			BEGIN
				IF (SELECT COUNT(1) FROM #STT_CLT) > 0
					BEGIN
						SELECT
							SAUDA_DATE,
							BOPARTYCODE,
							SCRIP_CD,
							SERIES,
							BOSTT,
							EXPARTYCODE,
							EXSTT,
							DIFF = (BOSTT - EXSTT)
						FROM
							#STT_CLT
						ORDER BY
							BOPARTYCODE,
							EXPARTYCODE,
							SCRIP_CD,
							SERIES
						COMPUTE
							SUM(BOSTT),
							SUM(EXSTT),
							SUM(BOSTT - EXSTT)
					END
				ELSE
					BEGIN
						SELECT
							SAUDA_DATE,
							BOPARTYCODE,
							SCRIP_CD,
							SERIES,
							BOSTT,
							EXPARTYCODE,
							EXSTT,
							DIFF = 0
						FROM
							#STT_CLT
						ORDER BY
							BOPARTYCODE,
							EXPARTYCODE,
							SCRIP_CD,
							SERIES
					END
			END
		IF @DISFLAG = 'M'
			BEGIN
				IF (SELECT COUNT(1) FROM #STT_CLT WHERE BOSTT = EXSTT) > 0
					BEGIN
						SELECT
							SAUDA_DATE,
							BOPARTYCODE,
							SCRIP_CD,
							SERIES,
							BOSTT,
							EXPARTYCODE,
							EXSTT,
							DIFF = (BOSTT - EXSTT)
						FROM
							#STT_CLT
						WHERE
							BOSTT = EXSTT
						ORDER BY
							BOPARTYCODE,
							EXPARTYCODE,
							SCRIP_CD,
							SERIES
						COMPUTE
							SUM(BOSTT),
							SUM(EXSTT),
							SUM(BOSTT - EXSTT)
					END
				ELSE
					BEGIN
						SELECT
							SAUDA_DATE,
							BOPARTYCODE,
							SCRIP_CD,
							SERIES,
							BOSTT,
							EXPARTYCODE,
							EXSTT,
							DIFF = 0
						FROM
							#STT_CLT
						WHERE
							BOSTT = EXSTT
						ORDER BY
							BOPARTYCODE,
							EXPARTYCODE,
							SCRIP_CD,
							SERIES
					END
			END
		IF @DISFLAG = 'U'
			BEGIN
				IF (SELECT COUNT(1) FROM #STT_CLT WHERE BOSTT <> EXSTT) > 0
					BEGIN
						SELECT
							SAUDA_DATE,
							BOPARTYCODE,
							SCRIP_CD,
							SERIES,
							BOSTT,
							EXPARTYCODE,
							EXSTT,
							DIFF = (BOSTT - EXSTT)
						FROM
							#STT_CLT
						WHERE
							BOSTT <> EXSTT
						ORDER BY
							BOPARTYCODE,
							EXPARTYCODE,
							SCRIP_CD,
							SERIES
						COMPUTE
							SUM(BOSTT),
							SUM(EXSTT),
							SUM(BOSTT - EXSTT)
					END
				ELSE
					BEGIN
						SELECT
							SAUDA_DATE,
							BOPARTYCODE,
							SCRIP_CD,
							SERIES,
							BOSTT,
							EXPARTYCODE,
							EXSTT,
							DIFF = 0
						FROM
							#STT_CLT
						WHERE
							BOSTT <> EXSTT
						ORDER BY
							BOPARTYCODE,
							EXPARTYCODE,
							SCRIP_CD,
							SERIES
					END
			END
	END
ELSE
	BEGIN
		IF @DISFLAG = 'A'
			BEGIN
				IF (SELECT COUNT(1) FROM (SELECT 1 AS A FROM #STT_CLT GROUP BY SAUDA_DATE, SCRIP_CD, SERIES) A) > 0
					BEGIN
						SELECT
							SAUDA_DATE,
							SCRIP_CD,
							SERIES,
							BOSTT=SUM(BOSTT),
							EXSTT=SUM(EXSTT),
							DIFF = SUM(BOSTT) - SUM(EXSTT)
						FROM
							#STT_CLT
						GROUP BY
							SAUDA_DATE,
							SCRIP_CD,
							SERIES
						ORDER BY
							SCRIP_CD,
							SERIES
						COMPUTE
							SUM(SUM(BOSTT)),
							SUM(SUM(EXSTT)),
							SUM(SUM(BOSTT) - SUM(EXSTT))
					END
				ELSE
					BEGIN
						SELECT
							SAUDA_DATE,
							SCRIP_CD,
							SERIES,
							BOSTT=SUM(BOSTT),
							EXSTT=SUM(EXSTT),
							DIFF = SUM(BOSTT) - SUM(EXSTT)
						FROM
							#STT_CLT
						GROUP BY
							SAUDA_DATE,
							SCRIP_CD,
							SERIES
						ORDER BY
							SCRIP_CD,
							SERIES
					END
			END
		IF @DISFLAG = 'M'
			BEGIN
				IF (SELECT COUNT(1) FROM (SELECT 1 AS A FROM #STT_CLT GROUP BY SAUDA_DATE, SCRIP_CD, SERIES HAVING SUM(BOSTT) = SUM(EXSTT)) A) > 0
					BEGIN
						SELECT
							SAUDA_DATE,
							SCRIP_CD,
							SERIES,
							BOSTT=SUM(BOSTT),
							EXSTT=SUM(EXSTT),
							DIFF = SUM(BOSTT) - SUM(EXSTT) 
						FROM
							#STT_CLT
						GROUP BY
							SAUDA_DATE,
							SCRIP_CD,
							SERIES
						HAVING
							SUM(BOSTT) = SUM(EXSTT)
						ORDER BY
							SCRIP_CD,
							SERIES
						COMPUTE
							SUM(SUM(BOSTT)),
							SUM(SUM(EXSTT)),
							SUM(SUM(BOSTT) - SUM(EXSTT))
					END
				ELSE
					BEGIN
						SELECT
							SAUDA_DATE,
							SCRIP_CD,
							SERIES,
							BOSTT=SUM(BOSTT),
							EXSTT=SUM(EXSTT),
							DIFF = SUM(BOSTT) - SUM(EXSTT)
						FROM
							#STT_CLT
						GROUP BY
							SAUDA_DATE,
							SCRIP_CD,
							SERIES
						HAVING
							SUM(BOSTT) = SUM(EXSTT)
						ORDER BY
							SCRIP_CD,
							SERIES
					END
			END
		IF @DISFLAG = 'U'
			BEGIN
				IF (SELECT COUNT(1) FROM (SELECT 1 AS A FROM #STT_CLT GROUP BY SAUDA_DATE, SCRIP_CD, SERIES HAVING SUM(BOSTT) <> SUM(EXSTT)) A) > 0
					BEGIN
						SELECT
							SAUDA_DATE,
							SCRIP_CD,
							SERIES,
							BOSTT=SUM(BOSTT),
							EXSTT=SUM(EXSTT),
							DIFF = SUM(BOSTT) - SUM(EXSTT)
						FROM
							#STT_CLT
						GROUP BY
							SAUDA_DATE,
							SCRIP_CD,
							SERIES
						HAVING
							SUM(BOSTT) <> SUM(EXSTT)
						ORDER BY
							SCRIP_CD,
							SERIES
						COMPUTE
							SUM(SUM(BOSTT)),
							SUM(SUM(EXSTT)),
							SUM(SUM(BOSTT) - SUM(EXSTT))
					END
				ELSE
					BEGIN
						SELECT
							SAUDA_DATE,
							SCRIP_CD,
							SERIES,
							BOSTT=SUM(BOSTT),
							EXSTT=SUM(EXSTT),
							DIFF = SUM(BOSTT) - SUM(EXSTT)
						FROM
							#STT_CLT
						GROUP BY
							SAUDA_DATE,
							SCRIP_CD,
							SERIES
						HAVING
							SUM(BOSTT) <> SUM(EXSTT)
						ORDER BY
							SCRIP_CD,
							SERIES
					END
			END
	END

DROP TABLE #STT_CLT
DROP TABLE #STT_EXG

GO
