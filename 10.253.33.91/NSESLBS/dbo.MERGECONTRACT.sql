CREATE PROC MERGECONTRACT
	@PARTY_CODE VARCHAR(10),
	@SCRIP_CD VARCHAR(10),
	@SERIES VARCHAR(3),
	@SELLBUY INT,
	@SAUDA_DATE VARCHAR(11),
	@SETT_NO VARCHAR(7),
	@SETT_TYPE VARCHAR(3),
	@MKTRATE NUMERIC(18, 4),
	@NETRATE NUMERIC(18, 4),
	@BROKRGE NUMERIC(18, 4),
	@EDITNET BIT,
	@ROUNDING VARCHAR(5),
	@CONTRACTNOS VARCHAR(8000),
	@UNAME VARCHAR(50),
	@CALLEDFROM VARCHAR(100)

AS
/*
	BEGIN TRAN
	EXEC MERGECONTRACT 'Z09145','JSTAINLESS','EQ','23/07/2007','0283991,0284004,','AshokS','/InsApp/innerReport.aspx'
	EXEC MERGECONTRACT 'UTIBK','SBIN','EQ','01/06/2007','0000272,0000273,0000274,','AshokS','/InsApp/InnerReport.aspx'
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	SELECT DISTINCT CONTRACTNO FROM ISETTLEMENT WHERE PARTY_CODE = 'UTIBK' AND SAUDA_DATE LIKE 'JUN  1 2007%' AND SCRIP_CD = 'SBIN'
	SELECT * FROM INST_LOG ORDER BY TIMESTAMP DESC
	ROLLBACK
*/
	DECLARE
		@INNERCONTRACT VARCHAR(7),
		@CONTPOS INT,
		@CONTRACTNO VARCHAR(7)
IF LEN(RTRIM(LTRIM(@SAUDA_DATE))) = 10
	BEGIN
		SET @SAUDA_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @SAUDA_DATE, 103), 109)
	END
SET @CONTPOS = 1
CREATE TABLE #CONTRACTS
(
	CONTRACTNO VARCHAR(7)
)
WHILE 1 = 1
	BEGIN
		SET @INNERCONTRACT = .dbo.PIECE(@CONTRACTNOS, ',', @CONTPOS)
		IF @INNERCONTRACT IS NOT NULL AND @INNERCONTRACT <> ''
			BEGIN
				INSERT INTO #CONTRACTS (CONTRACTNO) VALUES (@INNERCONTRACT)
			END
		ELSE
			BEGIN
				BREAK
			END
		SET @CONTPOS = @CONTPOS + 1
	END
SELECT @CONTRACTNO = MIN(CONTRACTNO) FROM #CONTRACTS
DELETE FROM #CONTRACTS WHERE CONTRACTNO = @CONTRACTNO
INSERT INTO INST_LOG
	(
		PARTY_CODE,
		NEW_PARTY_CODE,
		SAUDA_DATE,
		SETT_NO,
		SETT_TYPE,
		SCRIP_CD,
		SERIES,
		ORDER_NO,
		TRADE_NO,
		SELL_BUY,
		CONTRACT_NO,
		NEW_CONTRACT_NO,
		BROKERAGE,
		NEW_BROKERAGE,
		MARKET_RATE,
		NEW_MARKET_RATE,
		NET_RATE,
		NEW_NET_RATE,
		QTY,
		NEW_QTY,
		PARTICIPANT_CODE,
		NEW_PARTICIPANT_CODE,
		USERNAME,
		MODULE,
		CALLED_FROM,
		TIMESTAMP,
		EXTRAFIELD3,
		EXTRAFIELD4,
		EXTRAFIELD5
	)
	SELECT
		I.PARTY_CODE,
		I.PARTY_CODE,
		SAUDA_DATE = @SAUDA_DATE,
		I.SETT_NO,
		I.SETT_TYPE,
		I.SCRIP_CD,
		I.SERIES,
		ORDER_NO = '',
		TRADE_NO = '',
		SELL_BUY,
		I.CONTRACTNO,
		NEW_CONTRACTNO = @CONTRACTNO,
		BROKERAGE = 0,
		NEW_BROKERAGE = 0,
		MARKET_RATE = 0,
		NEW_MARKET_RATE = 0,
		NET_RATE = 0,
		NEW_NET_RATE = 0,
		QTY = 0,
		NEW_QTY = 0,
		PARTICIPANT_CODE = '',
		NEW_PARTICIPANT_CODE = '',
		USERNAME = @UNAME,
		MODULE_NAME = @CALLEDFROM,
		CALLED_FROM = 'CONTRACT MERGING',
		GETDATE(),
		EXTRAFIELD3 = '',
		EXTRAFIELD4 = '',
		EXTRAFIELD5 = ''
	FROM
		ISETTLEMENT I,
		#CONTRACTS C
	WHERE
		I.CONTRACTNO = C.CONTRACTNO
		--AND I.CONTRACTNO = @INNERCONTRACT
		AND I.PARTY_CODE = @PARTY_CODE
		AND I.SCRIP_CD = @SCRIP_CD
		AND I.SERIES = @SERIES
		AND I.SELL_BUY = @SELLBUY
		AND I.SAUDA_DATE LIKE @SAUDA_DATE + '%'
	GROUP BY
		I.PARTY_CODE,
		I.SETT_NO,
		I.SETT_TYPE,
		I.SCRIP_CD,
		I.SERIES,
		I.CONTRACTNO,
		I.SELL_BUY
				
UPDATE
	I
SET
	CONTRACTNO = @CONTRACTNO
FROM
	ISETTLEMENT I,
	#CONTRACTS C
WHERE
	I.CONTRACTNO = C.CONTRACTNO
	AND I.PARTY_CODE = @PARTY_CODE
	AND I.SCRIP_CD = @SCRIP_CD
	AND I.SERIES = @SERIES
	AND I.SELL_BUY = @SELLBUY
	AND I.SAUDA_DATE LIKE @SAUDA_DATE + '%'
IF @EDITNET = 0
	BEGIN
		UPDATE
			ISETTLEMENT
		SET
			DUMMY2 = 1,
			MARKETRATE = @MKTRATE
		WHERE
			PARTY_CODE = @PARTY_CODE
			AND SCRIP_CD = @SCRIP_CD
			AND SERIES = @SERIES
			AND SELL_BUY = @SELLBUY
			AND CONTRACTNO = @CONTRACTNO
			AND CONVERT(VARCHAR(11), SAUDA_DATE, 109) = @SAUDA_DATE
		EXEC Nse_Inst_Consolidation_brokReCal @PARTY_CODE, @SCRIP_CD, @SAUDA_DATE, @SETT_TYPE, '%', '%', @SERIES, @CONTRACTNO, @UNAME, @CALLEDFROM
	END
ELSE
	BEGIN
		DECLARE
			@STMETHOD VARCHAR(10),
			@STVAL NUMERIC(18, 4),
			@GROSSVAL NUMERIC(18, 4),
			@STAX NUMERIC(18, 4)
		SELECT TOP 1
			@STMETHOD = LTRIM(RTRIM(CONVERT(VARCHAR, SERVICE_CHRG))) + '-' + LTRIM(RTRIM(CONVERT(VARCHAR, SERTAXMETHOD)))
		FROM
			CLIENT2
		WHERE
			CL_CODE = @PARTY_CODE
		SELECT
			@STVAL = SERVICE_TAX
		FROM
			GLOBALS
		WHERE
			@SAUDA_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT
		IF @STVAL IS NULL
			BEGIN
				SET @STVAL = 0
			END
		IF @STMETHOD = '1-1'
			BEGIN
				UPDATE
					ISETTLEMENT
				SET
					DUMMY2 = 1,
					MARKETRATE = @MKTRATE,
					NETRATE = @NETRATE,
					N_NETRATE = @NETRATE,
					BROKAPPLIED = @BROKRGE,
					NBROKAPP = @BROKRGE,
					SERVICE_TAX = 0,
					NSERTAX = 0,
					STATUS = 'E'
				WHERE
					PARTY_CODE = @PARTY_CODE
					AND SCRIP_CD = @SCRIP_CD
					AND SERIES = @SERIES
					AND SELL_BUY = @SELLBUY
					AND CONTRACTNO = @CONTRACTNO
					AND CONVERT(VARCHAR(11), SAUDA_DATE, 109) = @SAUDA_DATE
			END
		ELSE IF @STMETHOD = '1-0'
			BEGIN
				SET @GROSSVAL = 100 + @STVAL
				SET @BROKRGE = ((@BROKRGE * 100) / @GROSSVAL)
				SET @NETRATE = @MKTRATE + CASE WHEN @SELLBUY = 1 THEN (@BROKRGE) ELSE (-@BROKRGE) END
				SET @NETRATE = ROUND(@NETRATE, CONVERT(INT, .dbo.PIECE(@ROUNDING, '|', 2)))
				UPDATE
					ISETTLEMENT
				SET
					DUMMY2 = 1,
					MARKETRATE = @MKTRATE,
					NETRATE = @NETRATE,
					N_NETRATE = @NETRATE,
					BROKAPPLIED = @BROKRGE,
					NBROKAPP = @BROKRGE,
					SERVICE_TAX = (TRADEQTY * ((@BROKRGE * @STVAL)/100)),
					NSERTAX = (TRADEQTY * ((@BROKRGE * @STVAL)/100)),
					STATUS = 'E'
				WHERE
					PARTY_CODE = @PARTY_CODE
					AND SCRIP_CD = @SCRIP_CD
					AND SERIES = @SERIES
					AND SELL_BUY = @SELLBUY
					AND CONTRACTNO = @CONTRACTNO
					AND CONVERT(VARCHAR(11), SAUDA_DATE, 109) = @SAUDA_DATE
			END
		ELSE
			BEGIN
				SET @STAX = ((@BROKRGE * @STVAL) / 100)
				UPDATE
					ISETTLEMENT
				SET
					DUMMY2 = 1,
					MARKETRATE = @MKTRATE,
					NETRATE = @NETRATE,
					N_NETRATE = @NETRATE,
					BROKAPPLIED = @BROKRGE,
					NBROKAPP = @BROKRGE,
					SERVICE_TAX = (TRADEQTY * ((@BROKRGE * @STVAL)/100)),
					NSERTAX = (TRADEQTY * ((@BROKRGE * @STVAL)/100)),
					STATUS = 'E'
				WHERE
					PARTY_CODE = @PARTY_CODE
					AND SCRIP_CD = @SCRIP_CD
					AND SERIES = @SERIES
					AND SELL_BUY = @SELLBUY
					AND CONTRACTNO = @CONTRACTNO
					AND CONVERT(VARCHAR(11), SAUDA_DATE, 109) = @SAUDA_DATE
			END
	END
EXEC STT_CHARGES_FINAL @SETT_TYPE, @SAUDA_DATE, @PARTY_CODE, @PARTY_CODE

GO
