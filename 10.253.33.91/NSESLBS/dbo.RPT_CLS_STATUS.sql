CREATE PROC [dbo].[RPT_CLS_STATUS]
	(
	@EXCHANGE	VARCHAR(5),
	@SEGMENT	VARCHAR(8),
	@FROMPARTY	VARCHAR(15),  
	@TOPARTY	VARCHAR(15),  
	@FROMBRANCH 	VARCHAR(15),  
	@TOBRANCH	VARCHAR(15)  
	)

	AS
	
	---EXEC RPT_CLS_STATUS 'NSE','CAPITAL','','ZZZZZZZZZZ','','ZZZZZZZZ'
	/*
	SELECT 
		SEGMENT = 'C',
		PARTY_CODE = C1.CL_CODE,
		CLIENT_STATUS = (
		CASE
			WHEN ACTIVEFROM <= GETDATE() AND INACTIVEFROM+1 >= GETDATE() THEN 'A'
			ELSE 'I'
		END),
		ACTIVEFROM,
		INACTIVEFROM,
		REASON = ''
	FROM 
		CLIENT2 C1 (NOLOCK), 
		CLIENT5 C5 (NOLOCK)
	WHERE
		C1.CL_CODE = C5.CL_CODE
		AND C1.CL_CODE BETWEEN @FROMPARTY AND @TOPARTY
		AND C1.BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
		AND 
		( 
			CONVERT(VARCHAR,ACTIVEFROM,112) = CONVERT(VARCHAR,GETDATE(),112) 
			OR CONVERT(VARCHAR,INACTIVEFROM+1,112) = CONVERT(VARCHAR,GETDATE(),112) 
		)
	ORDER BY
		C1.CL_CODE, 3	
	*/

	SELECT 
		SEGMENT = (
		CASE
			WHEN EXCHANGE = 'NSE' AND SEGMENT = 'CAPITAL' THEN 'C'
			WHEN EXCHANGE = 'NSE' AND SEGMENT = 'FUTURES' THEN 'F'
			WHEN EXCHANGE = 'NSX' AND SEGMENT = 'FUTURES' THEN 'X'
			WHEN EXCHANGE = 'NSE' AND SEGMENT = 'SLBS' THEN 'S'
			ELSE ''
		END),
		PARTY_CODE = CD.CL_CODE,
		CLIENT_STATUS = (
		CASE
			WHEN ACTIVE_DATE <= GETDATE() AND INACTIVE_FROM >= GETDATE() THEN 'A'
			ELSE 'I'
		END),
		ACTIVE_DATE,
		INACTIVE_FROM,
		REASON = ''
	FROM 
		MSAJAG.DBO.CLIENT_DETAILS CD (NOLOCK),
		MSAJAG.DBO.CLIENT_BROK_DETAILS CB (NOLOCK)
	WHERE
		CD.CL_CODE = CB.CL_CODE
		AND CD.CL_CODE BETWEEN @FROMPARTY AND @TOPARTY
		AND CD.BRANCH_CD BETWEEN @FROMBRANCH AND @TOBRANCH
		AND EXCHANGE = @EXCHANGE
		AND SEGMENT = @SEGMENT
		AND 
		( 
			CONVERT(VARCHAR,ACTIVE_DATE,112) = CONVERT(VARCHAR,GETDATE(),112) 
			OR CONVERT(VARCHAR,INACTIVE_FROM,112) = CONVERT(VARCHAR,GETDATE(),112) 
		)
		AND CB.IMP_STATUS <> 0
	ORDER BY
		CD.CL_CODE, 3

GO
