-- EXEC RPT_TRADE_MATCH 'MAY 16 2007', '', '', 0
-- EXEC RPT_TRADE_MATCH 'MAY 16 2007', '', '', 1
-- EXEC RPT_TRADE_MATCH 'MAY 16 2007', '3IINFOTECH', 'EQ', 2

CREATE PROC RPT_TRADE_MATCH (@SAUDA_DATE VARCHAR(11), @SCRIP_CD VARCHAR(12), @SERIES VARCHAR(3), @FLAG INT)
AS
IF @FLAG = 0 
BEGIN 
	SELECT TDATE=LEFT(SAUDA_DATE,11),
	TPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	TPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END),
	TSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	TSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)
	INTO #TRADE FROM TRADE_MATCH
	WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
	GROUP BY LEFT(SAUDA_DATE,11)
	
	SELECT TDATE=LEFT(SAUDA_DATE,11),
	SPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	SPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*DUMMY1 ELSE 0 END),
	SSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	SSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*DUMMY1 ELSE 0 END)
	INTO #SETT_1 FROM SETTLEMENT WITH(INDEX(STT_INDEX), NOLOCK)
	WHERE SETT_TYPE IN ('N', 'W')
	AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND TRADE_NO NOT LIKE '%C%'
	GROUP BY LEFT(SAUDA_DATE,11)
	
	INSERT INTO #SETT_1
	SELECT TDATE=LEFT(SAUDA_DATE,11),
	SPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	SPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*DUMMY1 ELSE 0 END),
	SSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	SSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*DUMMY1 ELSE 0 END)
	FROM ISETTLEMENT WITH(INDEX(STT_INDEX), NOLOCK)
	WHERE SETT_TYPE IN ('N', 'W')
	AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND TRADE_NO NOT LIKE '%C%'
	GROUP BY LEFT(SAUDA_DATE,11)
	
	SELECT TDATE, SPQTY = SUM(SPQTY), SPAMT = SUM(SPAMT), SSQTY = SUM(SSQTY), SSAMT = SUM(SSAMT)
	INTO #SETT FROM #SETT_1
	GROUP BY TDATE
	
	SELECT TDATE=ISNULL(T.TDATE,S.TDATE),
	TPQTY = ISNULL(TPQTY,0), 
	SPQTY = ISNULL(SPQTY,0), 
	TPAMT = ISNULL(TPAMT,0),
	SPAMT = ISNULL(SPAMT,0),
	TSQTY = ISNULL(TSQTY,0),
	SSQTY = ISNULL(SSQTY,0),
	TSAMT = ISNULL(TSAMT,0),
	SSAMT = ISNULL(SSAMT,0) 
	FROM #TRADE T FULL OUTER JOIN #SETT S
	ON (T.TDATE = S.TDATE)
END

IF @FLAG = 1 
BEGIN 
	SELECT TDATE=LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES, 
	TPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	TPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END),
	TSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	TSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)
	INTO #TRADE_SCRIP FROM TRADE_MATCH
	WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
	GROUP BY LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES
	
	SELECT TDATE=LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES,
	SPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	SPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*DUMMY1 ELSE 0 END),
	SSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	SSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*DUMMY1 ELSE 0 END)
	INTO #SETT_SCRIP_1 FROM SETTLEMENT WITH(INDEX(STT_INDEX), NOLOCK)
	WHERE SETT_TYPE IN ('N', 'W')
	AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND TRADE_NO NOT LIKE '%C%'
	GROUP BY LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES
	
	INSERT INTO #SETT_SCRIP_1
	SELECT TDATE=LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES,
	SPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	SPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*DUMMY1 ELSE 0 END),
	SSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	SSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*DUMMY1 ELSE 0 END)
	FROM ISETTLEMENT WITH(INDEX(STT_INDEX), NOLOCK)
	WHERE SETT_TYPE IN ('N', 'W')
	AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND TRADE_NO NOT LIKE '%C%'
	GROUP BY LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES
	
	SELECT TDATE, SCRIP_CD, SERIES, SPQTY = SUM(SPQTY), SPAMT = SUM(SPAMT), SSQTY = SUM(SSQTY), SSAMT = SUM(SSAMT)
	INTO #SETT_SCRIP FROM #SETT_SCRIP_1
	GROUP BY TDATE, SCRIP_CD, SERIES
	
	SELECT TDATE=ISNULL(T.TDATE,S.TDATE),
	SCRIP_CD = ISNULL(T.SCRIP_CD, S.SCRIP_CD),
	SERIES = ISNULL(T.SERIES, S.SERIES),
	TPQTY = ISNULL(TPQTY,0), 
	SPQTY = ISNULL(SPQTY,0), 
	TPAMT = ISNULL(TPAMT,0),
	SPAMT = ISNULL(SPAMT,0),
	TSQTY = ISNULL(TSQTY,0),
	SSQTY = ISNULL(SSQTY,0),
	TSAMT = ISNULL(TSAMT,0),
	SSAMT = ISNULL(SSAMT,0) 
	FROM #TRADE_SCRIP T FULL OUTER JOIN #SETT_SCRIP S
	ON (T.TDATE = S.TDATE 
	    AND T.SCRIP_CD = S.SCRIP_CD
	    AND T.SERIES = S.SERIES)
END

IF @FLAG = 2 
BEGIN 
	SELECT TDATE=LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES, 
	TRADE_NO = PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO, 
	TPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	TPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END),
	TSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	TSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)
	INTO #TRADE_SCRIP_TRADE FROM TRADE_MATCH
	WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES
	GROUP BY LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES,
	PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO 
	
	SELECT TDATE=LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES,
	TRADE_NO = PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO, 
	SPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	SPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*DUMMY1 ELSE 0 END),
	SSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	SSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*DUMMY1 ELSE 0 END)
	INTO #SETT_SCRIP_TRADE_1 FROM SETTLEMENT WITH(INDEX(STT_INDEX), NOLOCK)
	WHERE SETT_TYPE IN ('N', 'W')
	AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES
	AND TRADE_NO NOT LIKE '%C%'
	GROUP BY LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES,
	PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO 
	
	INSERT INTO #SETT_SCRIP_TRADE_1
	SELECT TDATE=LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES,
	TRADE_NO = PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO, 
	SPQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
	SPAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*DUMMY1 ELSE 0 END),
	SSQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
	SSAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*DUMMY1 ELSE 0 END)
	FROM ISETTLEMENT WITH(INDEX(STT_INDEX), NOLOCK)
	WHERE SETT_TYPE IN ('N', 'W')
	AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES
	AND TRADE_NO NOT LIKE '%C%'
	GROUP BY LEFT(SAUDA_DATE,11), SCRIP_CD, SERIES,
	PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO
	
	SELECT TDATE, SCRIP_CD, SERIES, TRADE_NO, ORDER_NO, SPQTY = SUM(SPQTY), SPAMT = SUM(SPAMT), SSQTY = SUM(SSQTY), SSAMT = SUM(SSAMT)
	INTO #SETT_SCRIP_TRADE FROM #SETT_SCRIP_TRADE_1
	GROUP BY TDATE, SCRIP_CD, SERIES, TRADE_NO, ORDER_NO
	
	SELECT TDATE=ISNULL(T.TDATE,S.TDATE),
	SCRIP_CD = ISNULL(T.SCRIP_CD, S.SCRIP_CD),
	SERIES = ISNULL(T.SERIES, S.SERIES),
	TRADE_NO = ISNULL(T.TRADE_NO, S.TRADE_NO), 
	ORDER_NO = ISNULL(T.ORDER_NO, S.ORDER_NO),
	TPQTY = ISNULL(TPQTY,0), 
	SPQTY = ISNULL(SPQTY,0), 
	TPAMT = ISNULL(TPAMT,0),
	SPAMT = ISNULL(SPAMT,0),
	TSQTY = ISNULL(TSQTY,0),
	SSQTY = ISNULL(SSQTY,0),
	TSAMT = ISNULL(TSAMT,0),
	SSAMT = ISNULL(SSAMT,0) 
	FROM #TRADE_SCRIP_TRADE T FULL OUTER JOIN #SETT_SCRIP_TRADE S
	ON (T.TDATE = S.TDATE 
	    AND T.SCRIP_CD = S.SCRIP_CD
	    AND T.SERIES   = S.SERIES
	    AND T.TRADE_NO = S.TRADE_NO 
	    AND T.ORDER_NO = S.ORDER_NO)
END

GO
