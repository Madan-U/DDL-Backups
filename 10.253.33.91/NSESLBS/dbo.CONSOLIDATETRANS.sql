CREATE PROC CONSOLIDATETRANS
	@FORMDATA VARCHAR(8000),
	@AFTERBEFORE VARCHAR(1),
	@STATUSNAME VARCHAR(50),
	@MODULE VARCHAR(100)

AS

/*
