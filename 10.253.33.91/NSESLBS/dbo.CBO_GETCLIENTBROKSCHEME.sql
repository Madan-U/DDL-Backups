-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------





CREATE  PROCEDURE [DBO].[CBO_GETCLIENTBROKSCHEME]
(
	@STRTABLE VARCHAR(15) = '',
	@STRPARTY VARCHAR(25)='',
	@STRSCRIP VARCHAR(25)='',
	@STATUSID VARCHAR(25) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'
)
AS
	IF @STATUSID <> 'BROKER'
		BEGIN
			RAISERROR ('THIS PROCEDURE IS ACCESSIBLE TO BROKER', 16, 1)
			RETURN
		END
	SELECT
		C.SCHEME_ID SRNO,
		C.PARTY_CODE,
		C.TABLE_NO,
		SCHEMETYPE = CASE SCHEME_TYPE WHEN 'TRD' THEN 'TRADING' ELSE 'DELIVERY' END,
		SCHEME_TYPE = LTRIM(RTRIM(SCHEME_TYPE)),
		SCRIP_CD,
		TRADETYPE = CASE TRADE_TYPE WHEN 'NRM' THEN 'NORMAL' ELSE 'INSTITUTIONAL' END,
		TRADE_TYPE = LTRIM(RTRIM(TRADE_TYPE)),
		FROM_DATE = CONVERT(VARCHAR,FROM_DATE,103),
		TO_DATE = CONVERT(VARCHAR,TO_DATE,103),
		TABLE_NAME,
		S.* 
	FROM 
		CLIENTBROK_SCHEME C,
		SCHEMES S,
		BROKTABLE B
	WHERE
		C.BROKSCHEME = S.SCHEMEID
		AND C.TO_DATE > GETDATE()
		AND B.TABLE_NO = C.TABLE_NO
		AND B.LINE_NO = 1
		AND C.TABLE_NO = (CASE WHEN @STRTABLE <> '' AND @STRTABLE <> '%' THEN @STRTABLE ELSE C.TABLE_NO END)
		AND C.PARTY_CODE = (CASE WHEN @STRPARTY <> '' AND @STRPARTY <> '%' THEN @STRPARTY ELSE C.PARTY_CODE END)
		AND C.SCRIP_CD = (CASE WHEN @STRSCRIP <> '' AND @STRSCRIP <> '%' THEN @STRSCRIP ELSE C.SCRIP_CD END)
	ORDER BY
		TO_DATE,
		SRNO DESC

GO
