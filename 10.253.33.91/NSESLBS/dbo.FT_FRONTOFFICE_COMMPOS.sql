-- Object: PROCEDURE dbo.FT_FRONTOFFICE_COMMPOS
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC FT_FRONTOFFICE_COMMPOS
(@FILEDATE VARCHAR(11)) AS

SELECT FILLER = ' ', MARKET_SEGMENT = 64, PRODUCT_TYPE = 'A',
PARTY_CODE, GROUP_ID = CONVERT(VARCHAR(6),''), TERMINAL_ID=CONVERT(VARCHAR(6),''), 
INSTRU_NAME = INST_TYPE, SYMBOL, 
SERIES = CONVERT(VARCHAR(2), ''), EXPIRYDATE=REPLACE(CONVERT(VARCHAR,EXPIRYDATE,106),' ', '-'), 
STRIKE_PRICE, OPTION_TYPE,
BUYQTY = (CASE WHEN SUM(PQTY-SQTY) > 0 THEN SUM(PQTY-SQTY) ELSE 0 END),
BUYVALUE = CONVERT(NUMERIC(18,4),0),
SELLQTY = (CASE WHEN SUM(SQTY-PQTY) > 0 THEN SUM(SQTY-PQTY) ELSE 0 END), 
SELLVALUE = CONVERT(NUMERIC(18,4),0),
NUMERATOR, DENOMINATOR
INTO #NCDX_POS FROM NCDX.DBO.FOBILLVALAN
WHERE SAUDA_DATE LIKE @FILEDATE + '%'
AND EXPIRYDATE > @FILEDATE + ' 23:59'
GROUP BY PARTY_CODE, INST_TYPE, SYMBOL, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE, NUMERATOR, DENOMINATOR
HAVING SUM(PQTY-SQTY) <> 0

UPDATE #NCDX_POS SET BUYVALUE = CL_RATE*BUYQTY*NUMERATOR/DENOMINATOR, SELLVALUE = CL_RATE*SELLQTY*NUMERATOR/DENOMINATOR
FROM NCDX.DBO.FOCLOSING F
WHERE F.TRADE_DATE LIKE @FILEDATE + '%'
AND F.INST_TYPE = #NCDX_POS.INSTRU_NAME
AND F.SYMBOL = #NCDX_POS.SYMBOL
AND REPLACE(CONVERT(VARCHAR,F.EXPIRYDATE,106),' ', '-') = #NCDX_POS.EXPIRYDATE
AND F.STRIKE_PRICE = #NCDX_POS.STRIKE_PRICE
AND F.OPTION_TYPE = #NCDX_POS.OPTION_TYPE

UPDATE #NCDX_POS SET GROUP_ID = T.GROUPID, TERMINAL_ID = T.USERID
FROM NCDX.DBO.TBL_FTUSERLST T
WHERE #NCDX_POS.PARTY_CODE = T.PARTY_CODE

SELECT FILLER = ' ', MARKET_SEGMENT = 16, PRODUCT_TYPE = 'A',
PARTY_CODE, GROUP_ID = CONVERT(VARCHAR(6),''), TERMINAL_ID=CONVERT(VARCHAR(6),''), 
INSTRU_NAME = INST_TYPE, SYMBOL, 
SERIES = CONVERT(VARCHAR(2), ''), EXPIRYDATE=REPLACE(CONVERT(VARCHAR,EXPIRYDATE,106),' ', '-'), 
STRIKE_PRICE, OPTION_TYPE,
BUYQTY = (CASE WHEN SUM(PQTY-SQTY) > 0 THEN SUM(PQTY-SQTY) ELSE 0 END),
BUYVALUE = CONVERT(NUMERIC(18,4),0),
SELLQTY = (CASE WHEN SUM(SQTY-PQTY) > 0 THEN SUM(SQTY-PQTY) ELSE 0 END), 
SELLVALUE = CONVERT(NUMERIC(18,4),0)
INTO #MCDX_POS FROM MCDX.DBO.FOBILLVALAN
WHERE SAUDA_DATE LIKE @FILEDATE + '%'
AND EXPIRYDATE > @FILEDATE + ' 23:59'
GROUP BY PARTY_CODE, INST_TYPE, SYMBOL, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE
HAVING SUM(PQTY-SQTY) <> 0

UPDATE #MCDX_POS SET BUYVALUE = CL_RATE*BUYQTY, SELLVALUE = CL_RATE*SELLQTY
FROM MCDX.DBO.FOCLOSING F
WHERE F.TRADE_DATE LIKE @FILEDATE + '%'
AND F.INST_TYPE = #MCDX_POS.INSTRU_NAME
AND F.SYMBOL = #MCDX_POS.SYMBOL
AND REPLACE(CONVERT(VARCHAR,F.EXPIRYDATE,106),' ', '-') = #MCDX_POS.EXPIRYDATE
AND F.STRIKE_PRICE = #MCDX_POS.STRIKE_PRICE
AND F.OPTION_TYPE = #MCDX_POS.OPTION_TYPE

UPDATE #MCDX_POS SET GROUP_ID = T.GROUPID, TERMINAL_ID = T.USERID
FROM NCDX.DBO.TBL_FTUSERLST T
WHERE #MCDX_POS.PARTY_CODE = T.PARTY_CODE

SELECT FILLER,MARKET_SEGMENT,PRODUCT_TYPE,PARTY_CODE,GROUP_ID,TERMINAL_ID,INSTRU_NAME,
SYMBOL,SERIES,EXPIRYDATE,STRIKE_PRICE,OPTION_TYPE,BUYQTY,BUYVALUE,SELLQTY,SELLVALUE,REMARK='',''
FROM #NCDX_POS
UNION ALL
SELECT FILLER,MARKET_SEGMENT,PRODUCT_TYPE,PARTY_CODE,GROUP_ID,TERMINAL_ID,INSTRU_NAME,
SYMBOL,SERIES,EXPIRYDATE,STRIKE_PRICE,OPTION_TYPE,BUYQTY,BUYVALUE,SELLQTY,SELLVALUE,REMARK='','' 
FROM #MCDX_POS
ORDER BY PARTY_CODE,INSTRU_NAME,SYMBOL,SERIES,EXPIRYDATE,STRIKE_PRICE,OPTION_TYPE

DROP TABLE #NCDX_POS
DROP TABLE #MCDX_POS

GO
