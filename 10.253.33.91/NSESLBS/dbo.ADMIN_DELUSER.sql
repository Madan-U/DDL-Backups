CREATE PROC [dbo].[ADMIN_DELUSER]
(
	@FLDAUTO VARCHAR(500),
	@ADMIN_NAME VARCHAR(50),
	@IPADD VARCHAR(20)
)

AS
/*
ADMIN_DELUSER '19,10,11,13','BROKER_CHECKER','10.11.12.74' 
SELECT * FROM TBLUSERCONTROLMASTER_JRNL
SELECT * FROM TBLPRADNYAUSERS_LOG WHERE FLDUSERNAME  ='testBhrt'

ROLLBACK
*/
CREATE TABLE #TEMPID
(
	ID INT
)

DECLARE 
	@LOOPID INT,
	@LOOPDATA VARCHAR(300),
	@GLOBALDATE DATETIME,
	@ADMINID INT

SET @GLOBALDATE = GETDATE()
SELECT  @ADMINID = FLDAUTO_ADMIN FROM TBLADMIN WHERE FLDNAME = @ADMIN_NAME

BEGIN TRAN
	SET @LOOPID = 1
	WHILE 1 = 1
	 BEGIN
		SET @LOOPDATA = .DBO.PIECE(@FLDAUTO,',',@LOOPID)

		IF @LOOPDATA = '' OR @LOOPDATA IS NULL
		 BEGIN
			BREAK
		 END
			
		INSERT INTO #TEMPID(ID) VALUES(@LOOPDATA)
		SET @LOOPID = @LOOPID + 1 
	 END	
select * from #TEMPID
	INSERT INTO TBLPRADNYAUSERS_LOG 
		(FLDAUTO,
		FLDUSERNAME,
		FLDPASSWORD,
		FLDFIRSTNAME,
		FLDMIDDLENAME,
		FLDLASTNAME,
		FLDSEX,
		FLDADDRESS1,
		FLDADDRESS2,
		FLDPHONE1,
		FLDPHONE2,
		FLDCATEGORY,
		FLDADMINAUTO,
		FLDSTNAME,
		PWD_EXPIRY_DATE,
		EDIT_FLAG,
		CREATED_BY,
		CREATED_ON,
		MACHINEIP,
		FLDLOG_DATA)
	SELECT 
		FLDAUTO,
		FLDUSERNAME,
		FLDPASSWORD,
		FLDFIRSTNAME,
		FLDMIDDLENAME,
		FLDLASTNAME,
		FLDSEX,
		FLDADDRESS1,
		FLDADDRESS2,
		FLDPHONE1,
		FLDPHONE2,
		FLDCATEGORY,
		FLDADMINAUTO,
		FLDSTNAME,
		PWD_EXPIRY_DATE,
		'D',
		@ADMIN_NAME,
		@GLOBALDATE,
		@IPADD,
		'DELETEENTRY' 
	FROM 
		TBLPRADNYAUSERS, #TEMPID
	WHERE 
		FLDAUTO = ID

	INSERT INTO TBLUSERCONTROLMASTER_JRNL
		(FLDAUTO,
		FLDUSERID,
		FLDPWDEXPIRY,
		FLDMAXATTEMPT,
		FLDATTEMPTCNT,
		FLDSTATUS,
		FLDLOGINFLAG,
		FLDACCESSLVL,
		FLDIPADD,
		FLDTIMEOUT,
		FLDFIRSTLOGIN,
		FLDFORCELOGOUT,
		FLDUPDTBY,
		FLDUPDTDT)
	SELECT 
		FLDAUTO,
		FLDUSERID,
		FLDPWDEXPIRY,
		FLDMAXATTEMPT,
		FLDATTEMPTCNT,
		FLDSTATUS,
		FLDLOGINFLAG,
		FLDACCESSLVL,
		FLDIPADD,
		FLDTIMEOUT,
		FLDFIRSTLOGIN,
		FLDFORCELOGOUT,
		@ADMINID,
		@GLOBALDATE 
	FROM 
		TBLUSERCONTROLMASTER TBC, #TEMPID
	WHERE 
		FLDUSERID = ID
	
	DELETE 
		TB
	FROM 
		TBLPRADNYAUSERS TB,  #TEMPID 
	WHERE 
		FLDAUTO = ID
COMMIT

DROP TABLE #TEMPID

GO
