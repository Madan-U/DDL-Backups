-- Object: PROCEDURE dbo.FT_FRONTOFFICE_CASHPOS
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE  PROC FT_FRONTOFFICE_CASHPOS
(@FILEDATE VARCHAR(11)) AS
SELECT FILLER = ' ', MARKET_SEGMENT = 1, PRODUCT_TYPE = 'B',
PARTY_CODE, GROUP_ID = CONVERT(VARCHAR(6),''), TERMINAL_ID=CONVERT(VARCHAR(6),''), 
INSTRU_NAME = CONVERT(VARCHAR(6),''), SYMBOL=CONVERT(VARCHAR(12),SCRIP_CD), 
SERIES = S.SERIES, EXPIRYDATE=CONVERT(VARCHAR(12),''), 
STRIKE_PRICE = 0, OPTION_TYPE = ' ',
BUYQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END),
BUYVALUE = CONVERT(NUMERIC(18,4),0),
SELLQTY=0, SELLVALUE = CONVERT(NUMERIC(18,4),0)
INTO #NSE_POS FROM MSAJAG.DBO.SETTLEMENT S, MSAJAG.DBO.SETT_MST SM
WHERE S.SETT_NO = SM.SETT_NO
AND S.SETT_TYPE = SM.SETT_TYPE 
AND SEC_PAYOUT > @FILEDATE
AND AUCTIONPART NOT IN ('AP', 'AR', 'FP', 'FS', 'FL', 'FA', 'FC')
GROUP BY PARTY_CODE, SCRIP_CD, S.SERIES
HAVING SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END) > 0 

INSERT INTO #NSE_POS
SELECT FILLER = ' ', MARKET_SEGMENT = 1, PRODUCT_TYPE = 'C',
PARTY_CODE, GROUP_ID = CONVERT(VARCHAR(6),''), TERMINAL_ID=CONVERT(VARCHAR(6),''), 
INSTRU_NAME = '', SYMBOL=LEFT(SCRIP_CD,12), SERIES=LEFT(SERIES,2), EXPIRYDATE='', 
STRIKE_PRICE = 0, OPTION_TYPE = '',
BUYQTY = SUM(QTY),
BUYVALUE = CONVERT(NUMERIC(18,4),0),
SELLQTY=0, SELLVALUE = CONVERT(NUMERIC(18,4),0)
FROM MSAJAG.DBO.DELTRANS D, MSAJAG.DBO.DELIVERYDP DP
WHERE D.BDPTYPE = DP.DPTYPE
AND D.BDPID = DP.DPID
AND D.BCLTDPID = DPCLTNO
AND D.DRCR = 'D'
AND D.FILLER2 = 1 
AND DELIVERED = '0'
AND TRTYPE = 904
AND SHARETYPE = 'DEMAT'
AND DESCRIPTION NOT LIKE '%POOL%'
AND PARTY_CODE NOT IN ('PARTY', 'BROKER', 'EXE', 'BSE', 'NSE')
GROUP BY PARTY_CODE, SCRIP_CD, SERIES

UPDATE #NSE_POS SET BUYVALUE = CL_RATE*BUYQTY
FROM MSAJAG.DBO.CLOSING C
WHERE C.SYSDATE LIKE (SELECT LEFT(MAX(SYSDATE),11) + '%'
FROM MSAJAG.DBO.CLOSING C1 WHERE C.SCRIP_CD = C1.SCRIP_CD
AND C.SERIES IN ('EQ', 'BE')
AND C1.SERIES IN ('EQ', 'BE')
AND C1.SYSDATE <= @FILEDATE + ' 23:59:59')
AND C.SCRIP_CD = #NSE_POS.SYMBOL
AND C.SERIES IN ('EQ', 'BE')
AND #NSE_POS.SERIES IN ('EQ', 'BE')
AND BUYVALUE = 0 

UPDATE #NSE_POS SET BUYVALUE = CL_RATE*BUYQTY
FROM MSAJAG.DBO.CLOSING C
WHERE C.SYSDATE LIKE (SELECT LEFT(MAX(SYSDATE),11) + '%'
FROM MSAJAG.DBO.CLOSING C1 WHERE C.SCRIP_CD = C1.SCRIP_CD
AND C.SERIES = C1.SERIES AND C1.SYSDATE <= @FILEDATE + ' 23:59:59')
AND C.SCRIP_CD = #NSE_POS.SYMBOL
AND C.SERIES = #NSE_POS.SERIES
AND BUYVALUE = 0 

UPDATE #NSE_POS SET GROUP_ID = T.GROUPID, TERMINAL_ID = T.USERID
FROM MSAJAG.DBO.TBL_FTUSERLST T
WHERE #NSE_POS.PARTY_CODE = T.PARTY_CODE

SELECT FILLER = ' ', MARKET_SEGMENT = 8, PRODUCT_TYPE = 'B',
PARTY_CODE, GROUP_ID = CONVERT(VARCHAR(6),''), TERMINAL_ID=CONVERT(VARCHAR(6),''), 
INSTRU_NAME = CONVERT(VARCHAR(6),''), SYMBOL=CONVERT(VARCHAR(12),SCRIP_CD), 
SERIES = CONVERT(VARCHAR(2), ''), EXPIRYDATE=CONVERT(VARCHAR(12),''), 
STRIKE_PRICE = 0, OPTION_TYPE = ' ',
BUYQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END),
BUYVALUE = CONVERT(NUMERIC(18,4),0),
SELLQTY=0, SELLVALUE = CONVERT(NUMERIC(18,4),0)
INTO #BSE_POS FROM BSEDB.DBO.SETTLEMENT S, BSEDB.DBO.SETT_MST SM
WHERE S.SETT_NO = SM.SETT_NO
AND S.SETT_TYPE = SM.SETT_TYPE 
AND SEC_PAYOUT > @FILEDATE
AND AUCTIONPART NOT IN ('AP', 'AR', 'FP', 'FS', 'FL', 'FA', 'FC')
GROUP BY PARTY_CODE, SCRIP_CD, S.SERIES
HAVING SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END) > 0 

INSERT INTO #BSE_POS
SELECT FILLER = ' ', MARKET_SEGMENT = 8, PRODUCT_TYPE = 'C',
PARTY_CODE, GROUP_ID = CONVERT(VARCHAR(6),''), TERMINAL_ID=CONVERT(VARCHAR(6),''), 
INSTRU_NAME = '', SYMBOL=LEFT(SCRIP_CD,12), SERIES='', EXPIRYDATE='', 
STRIKE_PRICE = 0, OPTION_TYPE = '',
BUYQTY = SUM(QTY),
BUYVALUE = CONVERT(NUMERIC(18,4),0),
SELLQTY=0, SELLVALUE = CONVERT(NUMERIC(18,4),0)
FROM BSEDB.DBO.DELTRANS D, BSEDB.DBO.DELIVERYDP DP
WHERE D.BDPTYPE = DP.DPTYPE
AND D.BDPID = DP.DPID
AND D.BCLTDPID = DPCLTNO
AND D.DRCR = 'D'
AND D.FILLER2 = 1 
AND DELIVERED = '0'
AND TRTYPE = 904
AND SHARETYPE = 'DEMAT'
AND DESCRIPTION NOT LIKE '%POOL%'
AND PARTY_CODE NOT IN ('PARTY', 'BROKER', 'EXE', 'BSE', 'NSE')
GROUP BY PARTY_CODE, SCRIP_CD, SERIES

UPDATE #BSE_POS SET BUYVALUE = CL_RATE*BUYQTY
FROM BSEDB.DBO.CLOSING C
WHERE C.SYSDATE LIKE (SELECT LEFT(MAX(SYSDATE),11) + '%'
FROM BSEDB.DBO.CLOSING C1 WHERE C.SCRIP_CD = C1.SCRIP_CD
AND C1.SYSDATE <= @FILEDATE + ' 23:59:59')
AND C.SCRIP_CD = #BSE_POS.SYMBOL
AND BUYVALUE = 0 

UPDATE #BSE_POS SET GROUP_ID = T.GROUPID, TERMINAL_ID = T.USERID
FROM MSAJAG.DBO.TBL_FTUSERLST T
WHERE #BSE_POS.PARTY_CODE = T.PARTY_CODE

SELECT FILLER,MARKET_SEGMENT,PRODUCT_TYPE,PARTY_CODE,GROUP_ID,TERMINAL_ID,INSTRU_NAME,
SYMBOL,SERIES,EXPIRYDATE,STRIKE_PRICE,OPTION_TYPE,BUYQTY,BUYVALUE,SELLQTY,SELLVALUE,REMARK='','' 
FROM #BSE_POS
UNION ALL
SELECT FILLER,MARKET_SEGMENT,PRODUCT_TYPE,PARTY_CODE,GROUP_ID,TERMINAL_ID,INSTRU_NAME,
SYMBOL,SERIES,EXPIRYDATE,STRIKE_PRICE,OPTION_TYPE,BUYQTY,BUYVALUE,SELLQTY,SELLVALUE,REMARK='','' 
FROM #NSE_POS
ORDER BY PARTY_CODE, SYMBOL, SERIES

DROP TABLE #BSE_POS
DROP TABLE #NSE_POS

GO
