CREATE PROCEDURE GENCONTRACT

	@PARTY_CODE VARCHAR(10),
	@SCRIP_CD VARCHAR(12),
	@SERIES VARCHAR(3),
	@SAUDADATE VARCHAR(11)


AS
	DECLARE
		@CL_TYPE VARCHAR(3),
		@NEWCONTNO VARCHAR(7)
	SELECT @CL_TYPE = CL_TYPE FROM CLIENT1 C1, CLIENT2 C2 WHERE C1.CL_CODE = C2.CL_CODE AND C2.PARTY_CODE = @PARTY_CODE
	IF @CL_TYPE = 'PRO'
		BEGIN
			SELECT '0000000'
		END
	ELSE
		BEGIN
			BEGIN TRAN
			SELECT
				@NEWCONTNO = CONVERT(VARCHAR(7), CONVERT(NUMERIC, CONTRACTNO) + 1)
			FROM
				CONTGEN WITH (TABLOCKX, HOLDLOCK)
			WHERE
				@SAUDADATE BETWEEN START_DATE AND END_DATE
	
			UPDATE
				CONTGEN
			SET
				CONTRACTNO = @NEWCONTNO
			WHERE
				@SAUDADATE BETWEEN START_DATE AND END_DATE
			COMMIT TRAN
			SELECT
				@NEWCONTNO = STUFF('0000000', 8 - LEN(@NEWCONTNO), LEN(@NEWCONTNO), @NEWCONTNO)
		END

GO
