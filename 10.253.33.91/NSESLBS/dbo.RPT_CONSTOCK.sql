CREATE PROC [dbo].[RPT_CONSTOCK]
(
	@STATUSID VARCHAR(20),
	@STATUSNAME VARCHAR(25),
	@TDATE VARCHAR(11),
	@BRANCH_CD VARCHAR(10),
	@SUB_BROKER VARCHAR(10),
	@FROMPARTY VARCHAR(10),
	@TOPARTY VARCHAR(10)
)

AS
/*
	EXEC RPT_CONSTOCK 'BROKER', 'BROKER', 'JUL  3 2007', '%', '%', '0', 'ZZZZ'
*/
IF LEN(LTRIM(RTRIM(@TDATE))) = 10
	BEGIN
		SET @TDATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @TDATE, 103), 109)
	END
SELECT
	PARTY_CODE,
	LONG_NAME,
	NSESCRIP_CD,
	BSESCRIP_CD,
	SCRIPNAME,
	NSEPOOL,
	BSEPOOL,
	MPAY,
	NPAY,
	MSHRT,
	NSHRT,
	NSEHOLD,
	BSEHOLD,
	NSEMARG,
	BSEMARG,
	NFOMARG,
	MTFHOLD,
	POAHOLD,
	TOTALHOLD,
	CL_RATE,
	HOLDAMT,
	BRANCH_CD,
	SUB_BROKER,
	TRADER,
	AREA,
	REGION,
	FAMILY,
	CLIENT_CTGRY,
	DEBITFLAG,
	CERTNO,
	RUNDATE
FROM
	TBL_CONSTOCKREPORT
WHERE
	RUNDATE LIKE @TDATE + '%'
	AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
	AND BRANCH_CD = (CASE WHEN @BRANCH_CD = '%' OR @BRANCH_CD = 'ALL' THEN BRANCH_CD ELSE @BRANCH_CD END)
	AND SUB_BROKER = (CASE WHEN @SUB_BROKER = '%' OR @SUB_BROKER = 'ALL' THEN SUB_BROKER ELSE @SUB_BROKER END)
	AND @StatusName =
		(
			case
				when @StatusId = 'BRANCH' then branch_cd
				when @StatusId = 'SUBBROKER' then sub_broker
				when @StatusId = 'Trader' then Trader
				when @StatusId = 'Family' then Family
				when @StatusId = 'Area' then Area
				when @StatusId = 'Region' then Region
				when @StatusId = 'Client' then party_code
			else
				'BROKER'
			End
		)
ORDER BY
	BRANCH_CD,
	SUB_BROKER,
	TRADER,
	PARTY_CODE,
	LONG_NAME,
	SCRIPNAME,
	NSESCRIP_CD,
	BSESCRIP_CD,
	CERTNO
COMPUTE
	SUM(NSEPOOL),
	SUM(BSEPOOL),
	SUM(MPAY),
	SUM(NPAY),
	SUM(MSHRT),
	SUM(NSHRT),
	SUM(NSEHOLD),
	SUM(BSEHOLD),
	SUM(NSEMARG),
	SUM(BSEMARG),
	SUM(NFOMARG),
	SUM(MTFHOLD),
	SUM(POAHOLD),
	SUM(TOTALHOLD),
	SUM(CL_RATE),
	SUM(HOLDAMT)

GO
