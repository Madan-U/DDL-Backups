-- Object: PROCEDURE dbo.CBO_GETBRANCHLIST
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE PROC CBO_GETBRANCHLIST
	@BRANCH_CODE VARCHAR(20) = '',
	@STATUSID VARCHAR(25) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'

AS
/*
	CBO_GETBRANCHLIST '', 'PARTY', 'C000000002'
*/
SET @BRANCH_CODE = LTRIM(RTRIM(@BRANCH_CODE))
IF @STATUSID = 'BROKER'
	BEGIN
		SELECT DISTINCT
				B.BRANCH_CODE,
				B.BRANCH
			FROM
				BRANCH B
			WHERE
				B.BRANCH_CODE = CASE WHEN LEN(@BRANCH_CODE) > 0 AND @BRANCH_CODE <> '%' THEN @BRANCH_CODE ELSE B.BRANCH_CODE END
			ORDER BY
				B.BRANCH_CODE
	END
ELSE IF @STATUSID = 'BRANCH'
	BEGIN
		SELECT DISTINCT
				B.BRANCH_CODE,
				B.BRANCH
			FROM
				BRANCH B
			WHERE
				B.BRANCH_CODE = CASE WHEN LEN(@BRANCH_CODE) > 0 AND @BRANCH_CODE <> '%' THEN @BRANCH_CODE ELSE B.BRANCH_CODE END
				AND B.BRANCH_CODE = @STATUSNAME
			ORDER BY
				B.BRANCH_CODE
	END
ELSE IF @STATUSID = 'AREA'
	BEGIN
		SELECT DISTINCT
				B.BRANCH_CODE,
				B.BRANCH
			FROM
				BRANCH B,
				AREA A
			WHERE
				B.BRANCH_CODE = A.BRANCH_CODE
				AND B.BRANCH_CODE = CASE WHEN LEN(@BRANCH_CODE) > 0 AND @BRANCH_CODE <> '%' THEN @BRANCH_CODE ELSE B.BRANCH_CODE END
				AND A.AREACODE = @STATUSNAME
			ORDER BY
				B.BRANCH_CODE
	END
ELSE IF @STATUSID = 'REGION'
	BEGIN
		SELECT DISTINCT
				B.BRANCH_CODE,
				B.BRANCH
			FROM
				BRANCH B,
				REGION R
			WHERE
				B.BRANCH_CODE = R.BRANCH_CODE
				AND B.BRANCH_CODE = CASE WHEN LEN(@BRANCH_CODE) > 0 AND @BRANCH_CODE <> '%' THEN @BRANCH_CODE ELSE B.BRANCH_CODE END
				AND R.REGIONCODE = @STATUSNAME
			ORDER BY
				B.BRANCH_CODE
	END
ELSE IF @STATUSID = 'SUBBROKER'
	BEGIN
		SELECT DISTINCT
				B.BRANCH_CODE,
				B.BRANCH
			FROM
				BRANCH B,
				SUBBROKERS S
			WHERE
				B.BRANCH_CODE = S.BRANCH_CODE
				AND B.BRANCH_CODE = CASE WHEN LEN(@BRANCH_CODE) > 0 AND @BRANCH_CODE <> '%' THEN @BRANCH_CODE ELSE B.BRANCH_CODE END
				AND S.SUB_BROKER = @STATUSNAME
			ORDER BY
				B.BRANCH_CODE
	END
ELSE IF @STATUSID = 'TRADER'
	BEGIN
		SELECT DISTINCT
				B.BRANCH_CODE,
				B.BRANCH
			FROM
				BRANCH B,
				BRANCHES T
			WHERE
				B.BRANCH_CODE = T.BRANCH_CD
				AND B.BRANCH_CODE = CASE WHEN LEN(@BRANCH_CODE) > 0 AND @BRANCH_CODE <> '%' THEN @BRANCH_CODE ELSE B.BRANCH_CODE END
				AND T.SHORT_NAME = @STATUSNAME
			ORDER BY
				B.BRANCH_CODE
	END
ELSE IF @STATUSID = 'FAMILY'
	BEGIN
		SELECT DISTINCT
				B.BRANCH_CODE,
				B.BRANCH
			FROM
				BRANCH B,
				CLIENT1 C1
			WHERE
				B.BRANCH_CODE = C1.BRANCH_CD
				AND B.BRANCH_CODE = CASE WHEN LEN(@BRANCH_CODE) > 0 AND @BRANCH_CODE <> '%' THEN @BRANCH_CODE ELSE B.BRANCH_CODE END
				AND C1.FAMILY = @STATUSNAME
			ORDER BY
				B.BRANCH_CODE
	END
ELSE IF @STATUSID = 'CLIENT'
	BEGIN
		SELECT
			C2.PARTY_CODE,
			C1.BRANCH_CD
		INTO
			#CLIENT2
		FROM
			CLIENT1 C1,
			CLIENT2 C2
		WHERE
			C1.CL_CODE = C2.CL_CODE
			AND C2.PARTY_CODE = @STATUSNAME
		SELECT DISTINCT
				B.BRANCH_CODE,
				B.BRANCH
			FROM
				BRANCH B,
				#CLIENT2 C2
			WHERE
				B.BRANCH_CODE = C2.BRANCH_CD
				AND B.BRANCH_CODE = CASE WHEN LEN(@BRANCH_CODE) > 0 AND @BRANCH_CODE <> '%' THEN @BRANCH_CODE ELSE B.BRANCH_CODE END
			ORDER BY
				B.BRANCH_CODE
	END
ELSE
	BEGIN
		SELECT
			BRANCH_CODE = '',
			BRANCH = ''
		FROM
			BRANCH
		WHERE
			1 = 0
	END

GO
