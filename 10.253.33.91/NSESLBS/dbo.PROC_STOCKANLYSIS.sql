-- Object: PROCEDURE dbo.PROC_STOCKANLYSIS
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC PROC_STOCKANLYSIS (@BILLDATE VARCHAR(11))
AS
DECLARE @DELCUR CURSOR,
@INTCUR CURSOR,
@PARTY_CODE VARCHAR(10), 
@OUTLEDBAL NUMERIC(18,4), 
@HOLDAMT NUMERIC(18,4),
@SETT_NO VARCHAR(7), 
@SETT_TYPE VARCHAR(2), 		     
@SCRIP_NAME VARCHAR(50), 
@SCRIP_CD VARCHAR(12), 
@SERIES VARCHAR(3), 
@QTY INT,
@CL_RATE NUMERIC(18,4)

TRUNCATE TABLE TBL_STOCK_HOLD

SELECT CLTCODE, LEDBAL = SUM(LEDBAL) INTO #LEDGER FROM (
SELECT L.CLTCODE, LEDBAL = SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END)
FROM ACCOUNT.DBO.LEDGER L, ACCOUNT.DBO.PARAMETER, ACCOUNT.DBO.ACMAST A
WHERE VDT >= SDTCUR AND VDT <= LDTCUR
AND CURYEAR = 1 
AND L.CLTCODE = A.CLTCODE
AND ACCAT = 4
GROUP BY L.CLTCODE
UNION ALL
SELECT L.CLTCODE, LEDBAL = SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END)
FROM ACCOUNTBSE.DBO.LEDGER L, ACCOUNTBSE.DBO.PARAMETER, ACCOUNTBSE.DBO.ACMAST A
WHERE VDT >= SDTCUR AND VDT <= LDTCUR
AND CURYEAR = 1 
AND L.CLTCODE = A.CLTCODE
AND ACCAT = 4
GROUP BY L.CLTCODE) A
GROUP BY CLTCODE
HAVING SUM(LEDBAL) < 0

SELECT CLTCODE, LEDBAL = SUM(LEDBAL) INTO #LEDGER_OUT FROM (
SELECT L.CLTCODE, LEDBAL = SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END)
FROM ACCOUNT.DBO.LEDGER L, ACCOUNT.DBO.PARAMETER, ACCOUNT.DBO.ACMAST A
WHERE VDT >= SDTCUR AND VDT <= LDTCUR
AND CURYEAR = 1 
AND L.CLTCODE = A.CLTCODE
AND ACCAT = 4 AND VDT <= @BILLDATE + ' 23:59:59'
GROUP BY L.CLTCODE
UNION ALL
SELECT L.CLTCODE, LEDBAL = SUM(CASE WHEN DRCR = 'C' THEN VAMT ELSE -VAMT END)
FROM ACCOUNTBSE.DBO.LEDGER L, ACCOUNTBSE.DBO.PARAMETER, ACCOUNTBSE.DBO.ACMAST A
WHERE VDT >= SDTCUR AND VDT <= LDTCUR
AND CURYEAR = 1 
AND L.CLTCODE = A.CLTCODE
AND ACCAT = 4 AND VDT <= @BILLDATE + ' 23:59:59'
GROUP BY L.CLTCODE) A
GROUP BY CLTCODE
HAVING SUM(LEDBAL) < 0

INSERT INTO TBL_STOCK_HOLD
SELECT EXCHANGE = 'NSE', SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, CERTNO, 
QTY = SUM(QTY), CL_RATE = CONVERT(NUMERIC(18,4),0), SOLDQTY = 0,
LEDBAL = CONVERT(NUMERIC(18,4),0), OUTLEDBAL = CONVERT(NUMERIC(18,4),0),
HOLDAMT = CONVERT(NUMERIC(18,4),0), PAYFLAG = 0, START_DATE = CONVERT(DATETIME, 'DEC 31 2049'),
SCRIP_NAME = CONVERT(VARCHAR(50),''), Flag = 0
FROM MSAJAG.DBO.DELTRANS D, MSAJAG.DBO.DELIVERYDP DP, #LEDGER L
WHERE D.PARTY_CODE = L.CLTCODE
AND DRCR = 'D' AND D.BDPID = DP.DPID
AND D.BCLTDPID = DP.DPCLTNO
AND D.FILLER2 = 1 AND D.DELIVERED = '0'
AND TRTYPE = 904
AND SHARETYPE = 'DEMAT'
AND DESCRIPTION NOT LIKE '%BEN%'
GROUP BY SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, CERTNO

INSERT INTO TBL_STOCK_HOLD
SELECT EXCHANGE = 'BSE', SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, CERTNO, 
QTY = SUM(QTY), CL_RATE = CONVERT(NUMERIC(18,4),0), SOLDQTY = 0,
LEDBAL = CONVERT(NUMERIC(18,4),0), OUTLEDBAL = CONVERT(NUMERIC(18,4),0),
HOLDAMT = CONVERT(NUMERIC(18,4),0), PAYFLAG = 0, START_DATE = CONVERT(DATETIME, 'DEC 31 2049'),
SCRIP_NAME = CONVERT(VARCHAR(50),''), Flag = 0 
FROM BSEDB.DBO.DELTRANS D, BSEDB.DBO.DELIVERYDP DP, #LEDGER L
WHERE D.PARTY_CODE = L.CLTCODE
AND DRCR = 'D' AND D.BDPID = DP.DPID
AND D.BCLTDPID = DP.DPCLTNO
AND D.FILLER2 = 1 AND D.DELIVERED = '0'
AND TRTYPE = 904
AND SHARETYPE = 'DEMAT'
AND DESCRIPTION NOT LIKE '%BEN%'
GROUP BY SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, CERTNO

      SELECT       
            Scrip_Cd,       
            Series='EQ',       
            Cl_Rate,       
            SysDate       
      INTO #NSE_LatestClosing       
      FROM MSAJAG.DBO.Closing C WITH(NOLOCK)      
      WHERE SYSDATE =       
            (      
                  SELECT       
                        MAX(SYSDATE)       
                  FROM MSAJAG.DBO.Closing WITH(NOLOCK)       
                  WHERE SCRIP_CD = C.SCRIP_CD        
                        And C.SERIES In ('BE', 'EQ')      
            )      
      And SERIES In ('BE', 'EQ')      
      
      INSERT INTO #NSE_LatestClosing       
      SELECT       
            Scrip_Cd,       
            Series,       
            Cl_Rate,       
            SysDate       
      FROM MSAJAG.DBO.Closing C WITH(NOLOCK)      
      WHERE SYSDATE =       
            (      
                  SELECT       
                        MAX(SYSDATE)       
                  FROM MSAJAG.DBO.Closing WITH(NOLOCK)       
                  WHERE SCRIP_CD = C.SCRIP_CD        
                        And SERIES = C.SERIES       
            )      
      And SERIES Not In ('BE', 'EQ')   
  
UPDATE TBL_STOCK_HOLD SET CL_RATE = C.CL_RATE
FROM #NSE_LatestClosing C  
WHERE C.Scrip_Cd = TBL_STOCK_HOLD.Scrip_Cd                  
And C.Series = (Case When TBL_STOCK_HOLD.Series In ('EQ', 'BE')   
       Then 'EQ'   
       Else TBL_STOCK_HOLD.Series   
  End)
AND EXCHANGE = 'NSE'

      SELECT             
            Scrip_Cd,             
            Series,             
            Cl_Rate,             
            SysDate             
      INTO #BSE_LatestClosing             
      FROM BSEDB.DBO.Closing C WITH(NOLOCK)            
      WHERE SYSDATE =             
            (            
                  SELECT             
                        MAX(SYSDATE)             
                  FROM BSEDB.DBO.Closing WITH(NOLOCK)             
                  WHERE SCRIP_CD = C.SCRIP_CD              
                        And SERIES = C.SERIES       
            )            
        
UPDATE TBL_STOCK_HOLD SET CL_RATE = C.CL_RATE  
FROM #BSE_LatestClosing C        
WHERE C.Scrip_Cd = TBL_STOCK_HOLD.Scrip_Cd                        
And C.Series = TBL_STOCK_HOLD.Series
AND EXCHANGE = 'BSE'

UPDATE TBL_STOCK_HOLD SET START_DATE = S.START_DATE 
FROM MSAJAG.DBO.SETT_MST S
WHERE S.SETT_NO = TBL_STOCK_HOLD.SETT_NO
AND S.SETT_TYPE = TBL_STOCK_HOLD.SETT_TYPE
AND TBL_STOCK_HOLD.EXCHANGE = 'NSE'

UPDATE TBL_STOCK_HOLD SET START_DATE = S.START_DATE 
FROM BSEDB.DBO.SETT_MST S
WHERE S.SETT_NO = TBL_STOCK_HOLD.SETT_NO
AND S.SETT_TYPE = TBL_STOCK_HOLD.SETT_TYPE
AND TBL_STOCK_HOLD.EXCHANGE = 'BSE'

INSERT INTO TBL_STOCK_HOLD
SELECT EXCAHNGE = 'NSE', SETT_NO = '', SETT_TYPE = '', CLTCODE, SCRIP_CD = '', SERIES = '', CERTNO = '', 
QTY = 0, CL_RATE = 0, SOLDQTY = 0,
LEDBAL = CONVERT(NUMERIC(18,4),0), OUTLEDBAL = CONVERT(NUMERIC(18,4),0),
HOLDAMT = CONVERT(NUMERIC(18,4),0), PAYFLAG = 0, START_DATE = CONVERT(DATETIME, @BILLDATE),
SCRIP_NAME = CONVERT(VARCHAR(50),''), Flag = 0
FROM #LEDGER WHERE CLTCODE NOT IN (SELECT DISTINCT PARTY_CODE FROM TBL_STOCK_HOLD WHERE START_DATE LIKE @BILLDATE + '%')

UPDATE TBL_STOCK_HOLD SET LEDBAL = ABS(L.LEDBAL)
FROM #LEDGER L
WHERE L.CLTCODE = PARTY_CODE

UPDATE TBL_STOCK_HOLD SET OUTLEDBAL = ABS(L.LEDBAL)
FROM #LEDGER_OUT L
WHERE L.CLTCODE = PARTY_CODE

UPDATE TBL_STOCK_HOLD SET HOLDAMT = A.HOLDAMT
FROM (SELECT PARTY_CODE, HOLDAMT = SUM(QTY*CL_RATE) 
FROM TBL_STOCK_HOLD
GROUP BY PARTY_CODE ) A
WHERE TBL_STOCK_HOLD.PARTY_CODE = A.PARTY_CODE

UPDATE TBL_STOCK_HOLD SET SCRIP_NAME = S1.LONG_NAME
FROM MSAJAG.DBO.SCRIP1 S1, MSAJAG.DBO.SCRIP2 S2
WHERE S1.CO_CODE = S2.CO_CODE
AND S1.SERIES = S2.SERIES
AND S2.SCRIP_CD = TBL_STOCK_HOLD.SCRIP_CD
AND S2.SERIES = TBL_STOCK_HOLD.SERIES
AND TBL_STOCK_HOLD.EXCHANGE = 'NSE'

UPDATE TBL_STOCK_HOLD SET SCRIP_NAME = S1.LONG_NAME
FROM BSEDB.DBO.SCRIP1 S1, BSEDB.DBO.SCRIP2 S2
WHERE S1.CO_CODE = S2.CO_CODE
AND S1.SERIES = S2.SERIES
AND S2.SCRIP_CD = TBL_STOCK_HOLD.SCRIP_CD
AND TBL_STOCK_HOLD.EXCHANGE = 'BSE'

UPDATE TBL_STOCK_HOLD SET PAYFLAG = DEBITFLAG
FROM MSAJAG.DBO.DELPARTYFLAG 
WHERE TBL_STOCK_HOLD.PARTY_CODE = DELPARTYFLAG.PARTY_CODE

UPDATE TBL_STOCK_HOLD SET FLAG = 1 
WHERE START_DATE LIKE @BILLDATE + '%'

SET @DELCUR = CURSOR FOR
SELECT PARTY_CODE, OUTLEDBAL, HOLDAMT = SUM(QTY*CL_RATE) FROM TBL_STOCK_HOLD
WHERE START_DATE LIKE @BILLDATE + '%' AND OUTLEDBAL > 0 AND CL_RATE > 0 
GROUP BY PARTY_CODE, OUTLEDBAL
ORDER BY PARTY_CODE
OPEN @DELCUR
FETCH NEXT FROM @DELCUR INTO @PARTY_CODE, @OUTLEDBAL, @HOLDAMT
WHILE @@FETCH_STATUS = 0 
BEGIN
	IF @OUTLEDBAL >= @HOLDAMT
	BEGIN
		UPDATE TBL_STOCK_HOLD SET SOLDQTY = QTY
		WHERE  START_DATE LIKE @BILLDATE + '%' AND OUTLEDBAL > 0 AND CL_RATE > 0 
		AND PARTY_CODE = @PARTY_CODE
	END
	ELSE
	BEGIN
		SET @INTCUR = CURSOR FOR
		SELECT SETT_NO, SETT_TYPE, SCRIP_NAME, SCRIP_CD, SERIES, QTY, CL_RATE FROM TBL_STOCK_HOLD
		WHERE  START_DATE LIKE @BILLDATE + '%' AND OUTLEDBAL > 0 AND CL_RATE > 0 
		AND PARTY_CODE = @PARTY_CODE
		OPEN @INTCUR
		FETCH NEXT FROM @INTCUR INTO @SETT_NO, @SETT_TYPE,
			     @SCRIP_NAME, @SCRIP_CD, @SERIES, @QTY, @CL_RATE
		WHILE @@FETCH_STATUS = 0 AND @OUTLEDBAL > 0 
		BEGIN 
			IF @OUTLEDBAL >= @QTY*@CL_RATE
			BEGIN
				UPDATE TBL_STOCK_HOLD SET SOLDQTY = QTY
				WHERE  START_DATE LIKE @BILLDATE + '%' AND OUTLEDBAL > 0 AND CL_RATE > 0 
				AND PARTY_CODE = @PARTY_CODE AND SETT_NO = @SETT_NO
				AND SETT_TYPE = @SETT_TYPE AND SCRIP_NAME = @SCRIP_NAME
				AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES

				SET @OUTLEDBAL = @OUTLEDBAL - @QTY*@CL_RATE
			END
			ELSE
			BEGIN
				UPDATE TBL_STOCK_HOLD SET SOLDQTY = CEILING(@OUTLEDBAL/@CL_RATE)
				WHERE  START_DATE LIKE @BILLDATE + '%' AND OUTLEDBAL > 0 AND CL_RATE > 0 
				AND PARTY_CODE = @PARTY_CODE AND SETT_NO = @SETT_NO
				AND SETT_TYPE = @SETT_TYPE AND SCRIP_NAME = @SCRIP_NAME
				AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES 
				SET @OUTLEDBAL = 0
			END
			FETCH NEXT FROM @INTCUR INTO @SETT_NO, @SETT_TYPE,
			     @SCRIP_NAME, @SCRIP_CD, @SERIES, @QTY, @CL_RATE
		END 
	END
	FETCH NEXT FROM @DELCUR INTO @PARTY_CODE, @OUTLEDBAL, @HOLDAMT
END
CLOSE @DELCUR
DEALLOCATE @DELCUR

GO
