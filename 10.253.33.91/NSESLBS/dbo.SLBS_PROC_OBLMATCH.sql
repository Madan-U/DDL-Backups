-- Object: PROCEDURE dbo.SLBS_PROC_OBLMATCH
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC SLBS_PROC_OBLMATCH        
(@SETT_NO VARCHAR(7), @SETT_TYPE VARCHAR(2))        
AS        
        
UPDATE SLBS_OLBMATCH SET PARTY_CODE = NEWPARTY_CODE        
FROM PARTYMAPPING        
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE         
AND UCC_CODE = OLDPARTY_CODE        
      
SELECT SETT_NO, SETT_TYPE, TRADE_NO=PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO, SCRIP_CD, SERIES, SELL_BUY,         
AMOUNT = SUM(CASE WHEN SELL_BUY = 1 AND SETT_TYPE = 'L'         
      THEN TRADEQTY*(SCHEME+DUMMY1)        
      ELSE TRADEQTY*MARKETRATE        
    END)        
INTO #OBL FROM SETTLEMENT        
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE         
GROUP BY SETT_NO, SETT_TYPE, PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO, SCRIP_CD, SERIES, SELL_BUY        
        
INSERT INTO #OBL        
SELECT SETT_NO, SETT_TYPE, TRADE_NO=PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO, SCRIP_CD, SERIES, SELL_BUY,         
AMOUNT = SUM(CASE WHEN SELL_BUY = 1 AND SETT_TYPE = 'L'         
      THEN TRADEQTY*(SCHEME+DUMMY1)        
      ELSE TRADEQTY*MARKETRATE        
    END)        
FROM ISETTLEMENT        
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE         
GROUP BY SETT_NO, SETT_TYPE, PRADNYA.DBO.REPLACETRADENO(TRADE_NO), ORDER_NO, SCRIP_CD, SERIES, SELL_BUY        
        
SELECT S.TRADE_NO, S.ORDER_NO, S.SCRIP_CD, S.SERIES, S.SELL_BUY,         
BOAMOUNT=S.AMOUNT, EXAMOUNT = E.AMOUNT        
FROM #OBL S, SLBS_OLBMATCH E        
WHERE S.SETT_NO = E.SETT_NO        
AND S.SETT_TYPE = E.SETT_TYPE        
AND S.SCRIP_CD = E.SCRIP_CD        
AND S.SERIES = E.SERIES        
AND S.ORDER_NO = E.ORDER_NO        
AND S.TRADE_NO = CONVERT(INT,RIGHT(E.TRADE_NO,8))        
AND S.SELL_BUY = (CASE WHEN E.SELL_BUY = 'B' THEN 1 ELSE 2 END)        
AND S.AMOUNT <> E.AMOUNT
AND E.SETT_NO = @SETT_NO
AND E.SETT_TYPE = @SETT_TYPE        
UNION ALL        
SELECT S.TRADE_NO, S.ORDER_NO, S.SCRIP_CD, S.SERIES, S.SELL_BUY,         
BOAMOUNT=S.AMOUNT, EXAMOUNT = 0        
FROM #OBL S        
WHERE TRADE_NO NOT IN (SELECT E.TRADE_NO FROM SLBS_OLBMATCH E        
WHERE S.SETT_NO = E.SETT_NO        
AND S.SETT_TYPE = E.SETT_TYPE        
AND S.SCRIP_CD = E.SCRIP_CD        
AND S.SERIES = E.SERIES        
AND S.ORDER_NO = E.ORDER_NO        
AND S.TRADE_NO = CONVERT(INT,RIGHT(E.TRADE_NO,8))         
AND S.SELL_BUY = (CASE WHEN E.SELL_BUY = 'B' THEN 1 ELSE 2 END)
AND E.SETT_NO = @SETT_NO
AND E.SETT_TYPE = @SETT_TYPE)        
UNION ALL        
SELECT S.TRADE_NO, S.ORDER_NO, S.SCRIP_CD, S.SERIES,         
SELL_BUY = (CASE WHEN S.SELL_BUY = 'B' THEN 1 ELSE 2 END),         
BOAMOUNT=0, EXAMOUNT = S.AMOUNT        
FROM SLBS_OLBMATCH S        
WHERE S.SETT_NO = @SETT_NO
AND S.SETT_TYPE = @SETT_TYPE
AND TRADE_NO NOT IN (SELECT CONVERT(INT,RIGHT(E.TRADE_NO,8))  FROM #OBL E        
WHERE S.SETT_NO = E.SETT_NO        
AND S.SETT_TYPE = E.SETT_TYPE        
AND S.SCRIP_CD = E.SCRIP_CD        
AND S.SERIES = E.SERIES        
AND S.ORDER_NO = E.ORDER_NO        
AND S.TRADE_NO = CONVERT(INT,RIGHT(E.TRADE_NO,8))       
AND E.SELL_BUY = (CASE WHEN S.SELL_BUY = 'B' THEN 1 ELSE 2 END))

GO
