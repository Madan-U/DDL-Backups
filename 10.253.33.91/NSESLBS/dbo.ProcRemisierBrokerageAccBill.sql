	SELECT 
		B.EXCHANGE,
		SUBBROKER=A.REMCODE,
		A.BRANCH_CD,
		A.REMPARTYCD, 
		A.SETT_NO,
		A.SETT_TYPE,
		START_DATE=A.FROMDATE,
		END_DATE=A.TODATE,
		PAYIN_DATE=A.TODATE,
		PAYOUT_DATE=A.TODATE,
		REMBROK=A.REM_NSEFOBROK,
		REMBROKTDS=A.REM_NSEFOTDS,
		TDSPERCENTAGE=A.TDSPERCENTAGE
	FROM REM_BROK_TRANS A, #SubBroker B	                      
		WHERE A.REMCODE = B.SubBroker 
		AND B.Exchange = 'NSEFO'
		AND ISNULL(A.NSEFORemBrokSharedSettNo, '') = ''
		AND A.REM_NSEFOBROK > 0 

	SELECT * FROM #REM_ACCBILL

	---- REMOVE THE EXISTING RECORDS IN THE REM_ACCBILL TABLE
	DELETE FROM REM_ACCBILL WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE   

--- ENTRY FOR REMISIER_BROKERAGE FOR "REMISIER BROKERAGE CONTROL AC (SUB)" (FOR SELECTED SETTLEMENT NO)
---  BUY_SELL = 1 (DR)	
	INSERT INTO REM_ACCBILL
		(PARTY_CODE,
		BILL_NO,
		SELL_BUY,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		AMOUNT,
		BRANCHCD,
		NARRATION,
		EXCHANGE,
		SEGMENT,
		BROK_SHARED_SETTNO, BILL_FOR)
	SELECT
		B.ACCTCODE,
		BILLNO = 0,
		SELL_BUY = 1,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		REMBROK=SUM(REMBROK),
		BRANCH_CD='ZZZ',
		NARRATION=@REMBROKNARR_SUB,
		A.EXCHANGE,
		SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
		BROK_SHARED_SETTNO = @SETT_NO,
		BILL_FOR = 'SUB'
	FROM #REM_ACCBILL A, #ValanAccount B
		WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE CONTROL AC (SUB)'
		AND SETT_NO = @SETT_NO
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		BRANCH_CD,
		A.EXCHANGE,
		CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR REMISIER_BROKERAGE FOR "REMISIER BROKERAGE SHARING CONTROL AC (SUB)" (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 2 (CR)
	INSERT INTO REM_ACCBILL
		(PARTY_CODE,
		BILL_NO,
		SELL_BUY,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		AMOUNT,
		BRANCHCD,
		NARRATION,
		EXCHANGE,
		SEGMENT,
		BROK_SHARED_SETTNO, BILL_FOR)
	SELECT
		B.ACCTCODE,
		BILLNO = 0,
		SELL_BUY = 2,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		REMBROK=SUM(REMBROK),
		BRANCH_CD='ZZZ',
		NARRATION=@REMBROKNARR_SUB,
		A.EXCHANGE,
		SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
		BROK_SHARED_SETTNO = @SETT_NO,
		BILL_FOR = 'SUB'
	FROM #REM_ACCBILL A, #ValanAccount B
		WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING CONTROL AC (SUB)'
		AND SETT_NO = @SETT_NO
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		BRANCH_CD,
		A.EXCHANGE,
		CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR REMISIER_BROKERAGE TDS FOR "REMISIER BROKERAGE SHARING CONTROL AC (SUB)" (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 2 (CR)
	INSERT INTO REM_ACCBILL
		(PARTY_CODE,
		BILL_NO,
		SELL_BUY,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		AMOUNT,
		BRANCHCD,
		NARRATION,
		EXCHANGE,
		SEGMENT,
		BROK_SHARED_SETTNO, BILL_FOR)
	SELECT
		B.ACCTCODE,
		BILLNO = 0,
		SELL_BUY = 2,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		REMBROK=SUM(REMBROKTDS),
		BRANCH_CD ='ZZZ',
		NARRATION=@REMTDSNARR_SUB,
		A.EXCHANGE,
		SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
		BROK_SHARED_SETTNO = @SETT_NO,
		BILL_FOR = 'SUB'
	FROM #REM_ACCBILL A, #ValanAccount B
		WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING CONTROL AC (SUB)'
		AND SETT_NO = @SETT_NO
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		BRANCH_CD,
		A.EXCHANGE,
		CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR REMISIER_BROKERAGE TDS FOR REMISIER BROKERAGE SHARING TDS AC (SUB) (FOR OPEN SETTLEMENT NOS)
	---  BUY_SELL = 1 (CR)
	INSERT INTO REM_ACCBILL
		(PARTY_CODE,
		BILL_NO,
		SELL_BUY,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		AMOUNT,
		BRANCHCD,
		NARRATION,
		EXCHANGE,
		SEGMENT,
		BROK_SHARED_SETTNO, BILL_FOR)
	SELECT
		B.ACCTCODE,
		BILLNO = 0,
		SELL_BUY = 1,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		REMBROK=SUM(REMBROKTDS),
		BRANCH_CD='ZZZ',
		NARRATION=@REMTDSNARR_SUB,
		A.EXCHANGE,
		SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
		BROK_SHARED_SETTNO = @SETT_NO,
		BILL_FOR = 'SUB'
	FROM #REM_ACCBILL A, #ValanAccount B
		WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING TDS AC (SUB)'
		AND SETT_NO = @SETT_NO
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		BRANCH_CD,
		A.EXCHANGE,
		CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR REMISIER_BROKERAGE ST FOR "REMISIER BROKERAGE SHARING CONTROL AC (SUB)" (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 2 (CR)
	IF @STPERCENTAGE <> 0 
		INSERT INTO REM_ACCBILL
			(PARTY_CODE,
			BILL_NO,
			SELL_BUY,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			AMOUNT,
			BRANCHCD,
			NARRATION,
			EXCHANGE,
			SEGMENT,
			BROK_SHARED_SETTNO, BILL_FOR)
		SELECT
			B.ACCTCODE,
			BILLNO = 0,
			SELL_BUY = 2,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			REMBROK=SUM(REMBROKTDS)*@STPERCENTAGE/100,
			BRANCH_CD='ZZZ',
			NARRATION=@REMSTARR_SUB,
			A.EXCHANGE,
			SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
			BROK_SHARED_SETTNO = @SETT_NO,
			BILL_FOR = 'SUB'
		FROM #REM_ACCBILL A, #ValanAccount B
			WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING CONTROL AC (SUB)'
			AND SETT_NO = @SETT_NO
		GROUP BY 
			B.ACCTCODE,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			BRANCH_CD,
			A.EXCHANGE,
			CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR ST FOR REMISIER BROKERAGE SHARING SERVICE TAX AC (SUB) (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 1 (DR)
	IF @STPERCENTAGE <> 0
		INSERT INTO REM_ACCBILL
			(PARTY_CODE,
			BILL_NO,
			SELL_BUY,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			AMOUNT,
			BRANCHCD,
			NARRATION,
			EXCHANGE,
			SEGMENT,
			BROK_SHARED_SETTNO, BILL_FOR)
		SELECT
			B.ACCTCODE,
			BILLNO = 0,
			SELL_BUY = 1,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			REMBROK=SUM(REMBROKTDS)*@STPERCENTAGE/100,
			BRANCH_CD='ZZZ',
			NARRATION=@REMSTARR_SUB,
			A.EXCHANGE,
			SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
			BROK_SHARED_SETTNO = @SETT_NO,
			BILL_FOR = 'SUB'
		FROM #REM_ACCBILL A, #ValanAccount B
			WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING SERVICE TAX AC (SUB)'
			AND SETT_NO = @SETT_NO
		GROUP BY 
			B.ACCTCODE,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			BRANCH_CD,
			A.EXCHANGE,
			CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR REMISIER_BROKERAGE CESS FOR "REMISIER BROKERAGE SHARING CONTROL AC (SUB)" (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 2 (CR)
	IF @CESSPERCENTAGE <> 0
		INSERT INTO REM_ACCBILL
			(PARTY_CODE,
			BILL_NO,
			SELL_BUY,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			AMOUNT,
			BRANCHCD,
			NARRATION,
			EXCHANGE,
			SEGMENT,
			BROK_SHARED_SETTNO, BILL_FOR)
		SELECT
			B.ACCTCODE,
			BILLNO = 0,
			SELL_BUY = 2,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			REMBROK=(SUM(REMBROKTDS)*@STPERCENTAGE/100)*(@CESSPERCENTAGE/100),
			BRANCH_CD='ZZZ',
			NARRATION=@REMCESSARR_SUB,
			A.EXCHANGE,
			SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
			BROK_SHARED_SETTNO = @SETT_NO,
			BILL_FOR = 'SUB'
		FROM #REM_ACCBILL A, #ValanAccount B
			WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING CONTROL AC (SUB)'
			AND SETT_NO = @SETT_NO
		GROUP BY 
			B.ACCTCODE,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			BRANCH_CD,
			A.EXCHANGE,
			CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

 --- ENTRY FOR CESS FOR REMISIER BROKERAGE SHARING CESS AC (SUB) (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 1 (DR)
	IF @CESSPERCENTAGE <> 0
		INSERT INTO REM_ACCBILL
			(PARTY_CODE,
			BILL_NO,
			SELL_BUY,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			AMOUNT,
			BRANCHCD,
			NARRATION,
			EXCHANGE,
			SEGMENT,
			BROK_SHARED_SETTNO, BILL_FOR)
		SELECT
			B.ACCTCODE,
			BILLNO = 0,
			SELL_BUY = 1,
			SETT_NO,
			SETT_TYPE,
			START_DATE,
			END_DATE,
			PAYIN_DATE,
			PAYOUT_DATE,
			REMBROK=(SUM(REMBROKTDS)*@STPERCENTAGE/100)*(@CESSPERCENTAGE/100),
			BRANCH_CD='ZZZ',
			NARRATION=@REMCESSARR_SUB,
			A.EXCHANGE,
			SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
			BROK_SHARED_SETTNO = @SETT_NO,
			BILL_FOR = 'SUB'
		FROM #REM_ACCBILL A, #ValanAccount B
			WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING CESS AC (SUB)'
