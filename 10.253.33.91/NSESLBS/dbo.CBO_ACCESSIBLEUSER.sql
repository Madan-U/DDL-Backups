CREATE PROC CBO_ACCESSIBLEUSER
	@REPORTCODE INT,
	@USERCATEGORY VARCHAR(2000)
AS
/*
	CBO_ACCESSIBLEUSER 1056, 388
*/

IF (SELECT COUNT(1) FROM TBLCATMENU WHERE FLDREPORTCODE = @REPORTCODE AND FLDCATEGORYCODE LIKE '%' + @USERCATEGORY + '%') > 0
	SELECT ALLOWED = 1
ELSE
	SELECT ALLOWED = 0

GO
