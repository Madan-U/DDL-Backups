-- Object: PROCEDURE dbo.FT_PARTYMAPPING_UPLOAD
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE PROC FT_PARTYMAPPING_UPLOAD
	@TRADEDATE VARCHAR(11)

	AS

	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	SELECT 
		ACTIONCODE	= 'A',
		DEALERGROUP	= 'HO',
		DEALERID	= BRANCH_CD,
		CLIENTCATEGORY	= '2',
		CLIENTGROUP	= 'HO',
		CLIENTID	= PARTY_CODE
	FROM
		(
		SELECT 
			DISTINCT
			PARTY_CODE,
			BRANCH_CD
		FROM 
			CLIENT1 C1 (NOLOCK),
			CLIENT2 C2 (NOLOCK),
			CLIENT5 C5 (NOLOCK)
		WHERE
			C1.CL_CODE = C2.CL_CODE
			AND C5.CL_CODE = C1.CL_CODE
			AND C5.Systumdate >= @TRADEDATE 
		
		-----
		UNION
		-----
		
		SELECT 
			DISTINCT
			PARTY_CODE,
			BRANCH_CD
		FROM 
			BSEDB..CLIENT1 C1 (NOLOCK),
			BSEDB..CLIENT2 C2 (NOLOCK),
			BSEDB..CLIENT5 C5 (NOLOCK)
		WHERE
			C1.CL_CODE = C2.CL_CODE
			AND C5.CL_CODE = C1.CL_CODE
			AND C5.Systumdate >= @TRADEDATE 
		
		-----
		UNION
		-----
		
		SELECT 
			DISTINCT
			PARTY_CODE,
			BRANCH_CD
		FROM 
			NSEFO..CLIENT1 C1 (NOLOCK),
			NSEFO..CLIENT2 C2 (NOLOCK),
			NSEFO..CLIENT5 C5 (NOLOCK)
		WHERE
			C1.CL_CODE = C2.CL_CODE
			AND C5.CL_CODE = C1.CL_CODE
			AND C5.Systumdate >= @TRADEDATE 
		
		-----
		UNION
		-----
		
		SELECT 
			DISTINCT
			PARTY_CODE,
			BRANCH_CD
		FROM 
			MCDX..CLIENT1 C1 (NOLOCK),
			MCDX..CLIENT2 C2 (NOLOCK),
			MCDX..CLIENT5 C5 (NOLOCK)
		WHERE
			C1.CL_CODE = C2.CL_CODE
			AND C5.CL_CODE = C1.CL_CODE
			AND C5.Systumdate >= @TRADEDATE 
		
		-----
		UNION
		-----
		
		SELECT 
			DISTINCT
			PARTY_CODE,
			BRANCH_CD
		FROM 
			NCDX..CLIENT1 C1 (NOLOCK),
			NCDX..CLIENT2 C2 (NOLOCK),
			NCDX..CLIENT5 C5 (NOLOCK)
		WHERE
			C1.CL_CODE = C2.CL_CODE
			AND C5.CL_CODE = C1.CL_CODE
			AND C5.Systumdate >= @TRADEDATE 
		/*
		-----
		UNION
		-----
		
		SELECT 
			DISTINCT
			PARTY_CODE,
			BRANCH_CD
		FROM 
			BSEFO..CLIENT1 C1 (NOLOCK),
			BSEFO..CLIENT2 C2 (NOLOCK)
			BSEFO..CLIENT5 C5 (NOLOCK)
		WHERE
			C1.CL_CODE = C2.CL_CODE 
			AND C5.CL_CODE = C1.CL_CODE
			AND C5.Systumdate >= @TRADEDATE */
	
		)  CLIENTMASTER
	
	ORDER BY
		BRANCH_CD,
		PARTY_CODE

GO
