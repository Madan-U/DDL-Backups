-- Object: PROCEDURE dbo.RPT_PODPBANK_DETAILS
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC RPT_PODPBANK_DETAILS
	(
	@BANKTYPE VARCHAR(5)
	)

	AS

	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

	IF (@BANKTYPE = 'PO')
	BEGIN
		SELECT 
			DISTINCT 
			BANKID, 
			BANK_NAME, 
			BRANCH_NAME 
		FROM 
			POBANK (NOLOCK)
	END
	ELSE
	BEGIN
		SELECT 
			DISTINCT 
			BANKID, 
			BANKNAME, 
			BANKTYPE 
		FROM 
			BANK (NOLOCK)
	END

GO
