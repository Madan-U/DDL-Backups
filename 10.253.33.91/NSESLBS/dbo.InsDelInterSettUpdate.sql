-- Object: PROCEDURE dbo.InsDelInterSettUpdate
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC [DBO].[INSDELINTERSETTUPDATE](@REFNO INT) AS        
DECLARE         
@@SNO INT,        
@@DELSNO INT,        
@@PARTY_CODE VARCHAR(10),        
@@SCRIP_CD VARCHAR(12),        
@@SERIES VARCHAR(3),        
@@QTY INT,        
@@TCODE INT,        
@@TRDATE VARCHAR(11),        
@@SETT_NO VARCHAR(7),        
@@ISETT_NO VARCHAR(7),        
@@SETT_TYPE VARCHAR(2),        
@@SETTDELQTY INT,        
@@SETTDELTRANSQTY INT,        
@@DEMATCUR CURSOR,        
@@SETTDEL CURSOR,        
@@SETTDELTRANS CURSOR        
      
      
/*=======================================================================================================      
 GET DATA FROM DEMATTRANS       
 GET MATCHING RECORDS FROM DELTRANS FOR THE SETTLEMENT / SCRIP       
=======================================================================================================*/      
      
TRUNCATE TABLE DELTRANS_PROCESS      
      
INSERT INTO DELTRANS_PROCESS       
 SELECT       
  *,      
  UPDFLAG = 0      
 FROM       
  DELTRANS       
 WHERE       
  FILLER2 = 1       
  AND DRCR = 'D'       
  AND DELIVERED = 'G'        
  AND TRTYPE = 907       
  AND BDPTYPE = 'NSDL'        
      
      
SELECT @@SETTDELQTY = 0         
SELECT @@SETTDELTRANSQTY = 0        
/* GETTING ALL THE RECORDS FROM DEMATTRANS FOR INTERSETTLEMENT */        
SET @@DEMATCUR = CURSOR FOR        
      
 SELECT       
  SNO,SCRIP_CD,D.SERIES,QTY,TCODE,TRDATE=LEFT(CONVERT(VARCHAR,TRDATE,109),11),      
  D.SETT_NO,D.SETT_TYPE,D.CLTACCNO        
 FROM       
  DEMATTRANS D,       
  SETT_MST S       
 WHERE       
  D.CLTACCNO = S.SETT_NO       
  AND D.SETT_TYPE = S.SETT_TYPE       
  AND PARTY_CODE = 'PARTY'        
  AND BDPTYPE = 'NSDL'        
  AND REFNO = @REFNO        
 ORDER BY SCRIP_CD,D.SERIES,QTY DESC,D.SETT_NO,D.SETT_TYPE,D.CLTACCNO        
      
OPEN @@DEMATCUR         
FETCH NEXT FROM @@DEMATCUR INTO @@SNO,@@SCRIP_CD,@@SERIES,@@QTY,@@TCODE,@@TRDATE,@@SETT_NO,@@SETT_TYPE,@@ISETT_NO         
WHILE @@FETCH_STATUS = 0        
BEGIN         
 /* TAKING PARTY'S SELL POSITION FOR THAT SCRIP FROM DELIVERYCLT TABLE. */        
 SET @@SETTDEL = CURSOR FOR        
 SELECT       
  SNO,QTY,PARTY_CODE       
 FROM       
  DELTRANS_PROCESS       
 WHERE       
  SETT_NO = @@ISETT_NO        
  AND ISETT_TYPE = @@SETT_TYPE       
  AND SCRIP_CD = @@SCRIP_CD       
  AND SERIES = @@SERIES          
  AND ISETT_NO = @@SETT_NO       
  AND FILLER2 = 1       
  AND DRCR = 'D'       
  AND REFNO = @REFNO       
  AND DELIVERED = 'G'        
  AND TRTYPE = 907       
  AND BDPTYPE = 'NSDL'        
 ORDER BY QTY DESC,PARTY_CODE        
 OPEN @@SETTDEL        
 FETCH NEXT FROM @@SETTDEL INTO @@DELSNO,@@SETTDELQTY,@@PARTY_CODE        
 WHILE @@FETCH_STATUS = 0 AND @@QTY > 0          
 BEGIN        
          
  IF @@SETTDELQTY > 0 AND @@QTY > 0         
  BEGIN        
   IF @@SETTDELQTY >= @@QTY         
   BEGIN        
    /* UPDATE THE RECORD AND EXIT FROM CURRENT LOOP */        
        
    UPDATE DEMATTRANS SET PARTY_CODE = @@PARTY_CODE WHERE SNO = @@SNO       
    IF @@SETTDELQTY > @@QTY         
    BEGIN        
 INSERT INTO DELTRANS_PROCESS         
 SELECT       
  0,SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,@@QTY,FROMNO,TONO,      
  CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,'D',ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,      
  PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,      
  FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5,UPDFLAG = 1        
 FROM DELTRANS_PROCESS WHERE SNO = @@DELSNO        
      
 UPDATE DELTRANS_PROCESS SET QTY=@@SETTDELQTY-@@QTY,UPDFLAG = 1 WHERE SNO = @@DELSNO      
     SELECT @@QTY = 0         
    END        
    ELSE         
    BEGIN         
     UPDATE DELTRANS_PROCESS SET DELIVERED = 'D',UPDFLAG = 1 WHERE SNO = @@DELSNO        
     SELECT @@QTY = 0        
    END 
    SELECT @@SETTDELQTY = 0          
   END        
   ELSE        
   BEGIN        
    /* UPDATE THE RECORD WITH @@QTY - @@SETTDELQTY AND FETCH THE NEXT         
       RECORD FROM CURRENT LOOP */            
 INSERT INTO DEMATTRANS       
 SELECT       
  SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,@@PARTY_CODE,SCRIP_CD,        
  SERIES,@@SETTDELQTY,TRDATE,CLTACCNO,DPID,DPNAME,ISIN,BRANCH_CD,PARTIPANTCODE,DPTYPE,TRANSNO,        
  DRCR,BDPTYPE,BDPID,BCLTACCNO,FILLER1,FILLER2,FILLER3,FILLER4,FILLER5       
 FROM DEMATTRANS         
 WHERE SNO = @@SNO       
         
 UPDATE DEMATTRANS SET QTY = @@QTY-@@SETTDELQTY        
 WHERE SNO = @@SNO       
      
 UPDATE DELTRANS_PROCESS SET DELIVERED = 'D', UPDFLAG = 1 WHERE SNO = @@DELSNO       
    SELECT @@QTY = @@QTY-@@SETTDELQTY        
    SELECT @@SETTDELQTY = 0          
   END        
  END        
  FETCH NEXT FROM @@SETTDEL INTO @@DELSNO,@@SETTDELQTY,@@PARTY_CODE         
 END        
 CLOSE @@SETTDEL         
 DEALLOCATE @@SETTDEL        
 FETCH NEXT FROM @@DEMATCUR INTO @@SNO,@@SCRIP_CD,@@SERIES,@@QTY,@@TCODE,@@TRDATE,@@SETT_NO,@@SETT_TYPE,@@ISETT_NO         
END        
CLOSE @@DEMATCUR        
DEALLOCATE @@DEMATCUR        
      
UPDATE DELTRANS SET DELIVERED = DP.DELIVERED, QTY = DP.QTY FROM DELTRANS_PROCESS DP       
WHERE DELTRANS.SNO = DT_SNO      
AND DT_SNO <> 0      
AND UPDFLAG = 1      
      
INSERT INTO DELTRANS       
SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO,      
HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,      
ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5       
FROM DELTRANS_PROCESS WHERE DT_SNO = 0

GO
