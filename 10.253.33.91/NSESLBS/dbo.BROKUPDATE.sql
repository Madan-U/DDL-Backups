CREATE PROC BROKUPDATE (
@SETT_NO VARCHAR(7),
@SETT_TYPE VARCHAR(2),
@PARTY_CODE VARCHAR(10),
@BROKPER NUMERIC(18,4),
@UName Varchar(50)
)
AS

Insert into Brok_Log
(Sett_No,Sett_Type,Party_Code,BrokPer,UserName,ChangeDate)
Values (@Sett_No, @Sett_Type, @Party_Code, @BrokPer, @UName, GetDate())

UPDATE SETTLEMENT SET BROKAPPLIED = ROUND(MARKETRATE*@BROKPER/100,4),
NBROKAPP    = ROUND(MARKETRATE*@BROKPER/100,4),
NETRATE     = MARKETRATE + (CASE WHEN SELL_BUY = 1 
			         THEN ROUND(MARKETRATE*@BROKPER/100,4)
			         ELSE -ROUND(MARKETRATE*@BROKPER/100,4)
			    END),
N_NETRATE   = MARKETRATE + (CASE WHEN SELL_BUY = 1 
			         THEN ROUND(MARKETRATE*@BROKPER/100,4)
			         ELSE -ROUND(MARKETRATE*@BROKPER/100,4)
			    END),
SERVICE_TAX = TRADEQTY*ROUND(MARKETRATE*@BROKPER/100,4)*G.SERVICE_TAX/100,
NSERTAX     = TRADEQTY*ROUND(MARKETRATE*@BROKPER/100,4)*G.SERVICE_TAX/100
FROM GLOBALS G
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = @SETT_TYPE
AND PARTY_CODE = @PARTY_CODE
AND SAUDA_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT
AND TRADE_NO NOT LIKE '%C%'

GO
