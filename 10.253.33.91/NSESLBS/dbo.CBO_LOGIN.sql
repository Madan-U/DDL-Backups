SET NOCOUNT ON

SELECT
	@SHAREDB = ISNULL(SHAREDB, ''),
	@SHARESERVER = ISNULL(SHARESERVER, ''),
	@SHAREIP = ISNULL(SHAREIP, ''),
	@ACCOUNTDB = ISNULL(ACCOUNTDB, ''),
	@ACCOUNTSERVER = ISNULL(ACCOUNTSERVER, ''),
	@ACCOUNTIP = ISNULL(ACCOUNTIP, ''),
	@LOGINID = ISNULL(DBUSERNAME, ''),
	@LOGINPASSWORD = ISNULL(DBPASSWORD, '')
FROM
	PRADNYA.dbo.MULTICOMPANY
WHERE
	EXCHANGE = @EXCHANGE
	AND SEGMENT = @SEGMENT
	AND PRIMARYSERVER = 1

SELECT
	@PWDMINLENGTH = FLDPWDMINLENGTH,
	@PWDMAXLENGTH = FLDPWDMAXLENGTH,
	@PWDENCRYPTION = FLDENCRYPTION,
	@PWDSPLCHAR = FLDSPCLCHAR,
	@FLDBLOCKPWD = FLDBLOCKPWD
FROM
	TBLGLOBALPARAMS

SELECT
	@FLDAUTO = ISNULL(B.FLDAUTO, 0),
	@FLDUSERID = ISNULL(B.FLDUSERID, 0),
	@FLDPWDEXPIRY = ISNULL(B.FLDPWDEXPIRY, ''),
	@FLDMAXATTEMPT = ISNULL(B.FLDMAXATTEMPT, 0),
	@FLDATTEMPTCNT = ISNULL(B.FLDATTEMPTCNT, 0),
	@FLDSTATUS = ISNULL(B.FLDSTATUS, 0),
	@FLDLOGINFLAG = ISNULL(B.FLDLOGINFLAG, 0),
	@FLDACCESSLVL = ISNULL(B.FLDACCESSLVL, 'F'),
	@FLDIPADD = ISNULL(B.FLDIPADD, ''),
	@FLDTIMEOUT = ISNULL(B.FLDTIMEOUT, 0),
	@FLDFIRSTLOGIN = ISNULL(B.FLDFIRSTLOGIN, 'N'),
	@FLDFORCELOGOUT = ISNULL(B.FLDFORCELOGOUT, 0)
FROM
	TBLPRADNYAUSERS A
	LEFT OUTER JOIN TBLUSERCONTROLMASTER B ON (A.FLDAUTO = B.FLDUSERID)
WHERE
	A.FLDUSERNAME = @USER_NAME

IF @@ROWCOUNT <= 0
	BEGIN
		SELECT RETCODE = '1,No Such User Exists...'
		RETURN
	END

IF @FLDFORCELOGOUT <> 0
	BEGIN
		UPDATE
			TBLUSERCONTROLMASTER
		SET
			FLDSTATUS = CASE WHEN ISNULL(@FLDATTEMPTCNT, 0) + 1 >= ISNULL(@FLDMAXATTEMPT, 0) THEN 1 ELSE FLDSTATUS END,
			FLDATTEMPTCNT = FLDATTEMPTCNT + 1
		WHERE
			FLDAUTO = @FLDAUTO
		SELECT RETCODE = '2,Access has been TEMPORARILY RESTRICTED due to Database Maintainance at CSO Kindly bear with us for some time Sorry for the inconvenience.'
		RETURN
	END

IF @FLDSTATUS <> 0
	BEGIN
		UPDATE
			TBLUSERCONTROLMASTER
		SET
			FLDSTATUS = CASE WHEN ISNULL(@FLDATTEMPTCNT, 0) + 1 >= ISNULL(@FLDMAXATTEMPT, 0) THEN 1 ELSE FLDSTATUS END,
			FLDATTEMPTCNT = FLDATTEMPTCNT + 1
		WHERE
			FLDAUTO = @FLDAUTO
		SELECT RETCODE = '3,The User id  is DISABLED.  Kindly contact Administrator.'
		RETURN
	END

IF @FLDATTEMPTCNT > @FLDMAXATTEMPT
	BEGIN
		UPDATE
			TBLUSERCONTROLMASTER
		SET
			FLDSTATUS = CASE WHEN ISNULL(@FLDATTEMPTCNT, 0) + 1 >= ISNULL(@FLDMAXATTEMPT, 0) THEN 1 ELSE FLDSTATUS END,
			FLDATTEMPTCNT = FLDATTEMPTCNT + 1
		WHERE
			FLDAUTO = @FLDAUTO
		SELECT RETCODE = '4,You have exceeded the Maximum Number of Attempts for Login.'
		RETURN
	END
IF @FLDACCESSLVL <> 'F'
	BEGIN
		SET @IPLOGIN = 0
		IF LEN(@FLDIPADD) > 0
			BEGIN
				SET @LOOPI = 1
				WHILE 1 = 1
					BEGIN
						SET @SPLITIP = .dbo.PIECE(@FLDIPADD, '|', @LOOPI)
						IF @SPLITIP IS NULL OR @SPLITIP = ''
							BEGIN
								BREAK
							END
						IF LEFT(LTRIM(RTRIM(@REMOTEIP)), LEN(LTRIM(RTRIM(@SPLITIP)))) = LTRIM(RTRIM(@SPLITIP))
							BEGIN
								SET @IPLOGIN = 1
								BREAK
							END
						SET @LOOPI = @LOOPI + 1
					END
			END
		IF @IPLOGIN = 0
			BEGIN
				SELECT RETCODE = '5,Your IP Address is not registered in the System. Kindly contact System Administrator...'
				RETURN
			END
	END

	SELECT
		@USER_STATUS = A.FLDSTATUS,
		@USERNAME = T.FLDFIRSTNAME + ' ' + T.FLDLASTNAME,
		@FLDSTNAME = T.FLDSTNAME,
		@USER_LOGIN = T.FLDSTNAME,
		@PWDEXPIRY = CAST(T.PWD_EXPIRY_DATE - GETDATE() AS INT),
		@USER_CATEGORY = T.FLDCATEGORY,
		@USERAUTOID = @USER_NAME
	FROM
		TBLPRADNYAUSERS T,
		TBLADMIN A,
		TBLUSERCONTROLMASTER C
	WHERE
		T.FLDADMINAUTO = A.FLDAUTO_ADMIN
		AND C.FLDUSERID = T.FLDAUTO
		AND T.FLDUSERNAME = @USER_NAME
		AND T.FLDPASSWORD = PRADNYA.dbo.CLASS_PWDENCRY(@PASSWORD)

IF @@ROWCOUNT <= 0
	BEGIN
		UPDATE
			TBLUSERCONTROLMASTER
		SET
			FLDSTATUS = CASE WHEN ISNULL(@FLDATTEMPTCNT, 0) + 1 >= ISNULL(@FLDMAXATTEMPT, 0) THEN 1 ELSE FLDSTATUS END,
			FLDATTEMPTCNT = FLDATTEMPTCNT + 1
		WHERE
			FLDAUTO = @FLDAUTO
		SELECT RETCODE = '6,Incorrect Password...'
		RETURN
	END

IF @PWDEXPIRY <= 0
	BEGIN
		SELECT RETCODE = '7,Your Password is been expired...'
		RETURN
	END

IF @FLDFIRSTLOGIN = 'Y'
	BEGIN
		SELECT RETCODE = '8,First Login'
		RETURN
	END

INSERT INTO V2_LOGIN_LOG
	(USERID, USERNAME, CATEGORY, STATUSNAME, STATUSID, IPADD, ACTION, ADDDT)
VALUES
	(@FLDAUTO, @USER_NAME, @USER_CATEGORY, @FLDSTNAME, @USER_STATUS, @REMOTEIP, 'LOGIN', GETDATE())

UPDATE TBLUSERCONTROLMASTER SET FLDLOGINFLAG = 1, FLDATTEMPTCNT = 0 WHERE FLDAUTO = @FLDAUTO


SELECT RETCODE = '0,Login Successful.'

