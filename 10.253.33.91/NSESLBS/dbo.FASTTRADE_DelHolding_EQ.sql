-- Object: PROCEDURE dbo.FASTTRADE_DelHolding_EQ
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE Procedure FASTTRADE_DelHolding_EQ ( @TradeDate Varchar(11)) AS
SELECT 
PARTY_CODE, 
SCRIP_CD, 
SERIES, 
QUANTITY = SUM(QTY), 
QUANTITYUSED = 0, 
BSESCRIPCODE = CONVERT(VARCHAR(12),''),
CERTNO
INTO #HOLDING
FROM DELTRANS D, DELIVERYDP DP
WHERE D.BDPTYPE = DP.DPTYPE
AND D.BDPID = DP.DPID
AND D.BCLTDPID = DPCLTNO
AND D.DRCR = 'D'
AND D.FILLER2 = 1 
AND DELIVERED = '0'
AND TRTYPE = 904
AND SHARETYPE = 'DEMAT'
AND DESCRIPTION NOT LIKE '%POOL%'
GROUP BY PARTY_CODE, 
SCRIP_CD, 
SERIES,
CERTNO

INSERT INTO #HOLDING
SELECT 
PARTY_CODE, 
SCRIP_CD = '', 
SERIES = '', 
QUANTITY = SUM(QTY), 
QUANTITYUSED = 0, 
BSESCRIPCODE = SCRIP_CD,
CERTNO
FROM BSEDB.DBO.DELTRANS D, BSEDB.DBO.DELIVERYDP DP
WHERE D.BDPTYPE = DP.DPTYPE
AND D.BDPID = DP.DPID
AND D.BCLTDPID = DPCLTNO
AND D.DRCR = 'D'
AND D.FILLER2 = 1 
AND DELIVERED = '0'
AND TRTYPE = 904
AND SHARETYPE = 'DEMAT'
AND DESCRIPTION NOT LIKE '%POOL%'
GROUP BY PARTY_CODE, 
SCRIP_CD, 
SERIES,
CERTNO

UPDATE #HOLDING SET BSESCRIPCODE = M.SCRIP_CD
FROM BSEDB.DBO.MULTIISIN M WHERE M.ISIN = CERTNO 
AND VALID = 1 AND BSESCRIPCODE = ''

UPDATE #HOLDING SET SCRIP_CD = M.SCRIP_CD, SERIES = M.SERIES
FROM MULTIISIN M WHERE M.ISIN = CERTNO 
AND VALID = 1 AND #HOLDING.SCRIP_CD = ''

SELECT H.PARTY_CODE, 
SCRIP_CD, 
SERIES, 
QUANTITY = SUM(QUANTITY), 
QUANTITYUSED = SUM(QUANTITYUSED),
BSESCRIPCODE
FROM #HOLDING H, Client2 C2
WHERE C2.PARTY_CODE = H.PARTY_CODE
GROUP BY H.PARTY_CODE, 
SCRIP_CD, 
SERIES,
BSESCRIPCODE
ORDER BY H.PARTY_CODE, 
SCRIP_CD, 
SERIES

DROP TABLE #HOLDING

GO
