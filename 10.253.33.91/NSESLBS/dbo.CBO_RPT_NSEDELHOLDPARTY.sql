CREATE PROC CBO_RPT_NSEDELHOLDPARTY
(
	@STATUSID VARCHAR(15),
	@STATUSNAME VARCHAR(25),
	@FROMPARTY VARCHAR(10),
	@TOPARTY VARCHAR(10),
	@FROMSCRIP VARCHAR(12),
	@TOSCRIP VARCHAR(12),
	@BDPID VARCHAR(8),
	@BCLTDPID VARCHAR(16),
	@BRANCH VARCHAR(10)
)
AS
/*
	Exec CBO_RPT_NSEDELHOLDPARTY 'BROKER','BROKER','','','','','','','%'
*/
SET NOCOUNT ON

IF @BDPID = ''
	SELECT @BDPID = '%'
IF @BCLTDPID = ''
	SELECT @BCLTDPID = '%'

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT
	SCRIP_CD = LTRIM(RTRIM(SCRIP_CD)),
	SERIES = LTRIM(RTRIM(SERIES)),
	PARTY_CODE = LTRIM(RTRIM(D.PARTY_CODE)),
	SETT_NO = LTRIM(RTRIM(SETT_NO)),
	SETT_TYPE = LTRIM(RTRIM(SETT_TYPE)),
	QTY=CONVERT(VARCHAR, SUM(QTY)),
	CERTNO = LTRIM(RTRIM(CERTNO)),
	BDPID = LTRIM(RTRIM(BDPID)),
	BCLTDPID = LTRIM(RTRIM(BCLTDPID)),
	HOLDQTY= (CASE WHEN @STATUSID = 'BROKER' THEN SUM(CASE WHEN TRTYPE <> 909 THEN QTY ELSE 0 END) ELSE SUM(QTY) END),
	PLEDGEQTY= (CASE WHEN @STATUSID = 'BROKER' THEN SUM(CASE WHEN TRTYPE = 909 THEN QTY ELSE 0 END) ELSE 0 END),
	PARTY_NAME = LTRIM(RTRIM(C1.LONG_NAME)),
	LONG_NAME = LTRIM(RTRIM(C1.LONG_NAME))
FROM
	DELTRANS D,
	dbo.CBO_FN_LOGINCHECK_BASIC(@STATUSID, @STATUSNAME, 'PARTY') C1
WHERE
	C1.PARTY_CODE = D.PARTY_CODE
	AND D.PARTY_CODE <> 'BROKER'
	AND D.PARTY_CODE BETWEEN (CASE WHEN @FROMPARTY = '' OR @FROMPARTY = '%' THEN '0' ELSE @FROMPARTY END) AND (CASE WHEN @TOPARTY = '' OR @TOPARTY = '%' THEN 'ZZZZZZZZZZ' ELSE @TOPARTY END)
	AND SCRIP_CD BETWEEN (CASE WHEN @FROMSCRIP = '' OR @FROMSCRIP = '%' THEN '0' ELSE @FROMSCRIP END) AND (CASE WHEN @TOSCRIP = '' OR @TOSCRIP = '%' THEN 'ZZZZZZZZZZ' ELSE @TOSCRIP END)
	AND CERTNO <> 'AUCTION'
	AND TRTYPE <> 906
	AND FILLER2 = 1
	AND DRCR = 'D'
	AND BDPID LIKE @BDPID
	AND BCLTDPID LIKE @BCLTDPID
	AND DELIVERED = '0'
	AND C1.BRANCH_CD = (CASE WHEN @BRANCH = '' OR @BRANCH = '%' THEN C1.BRANCH_CD ELSE @BRANCH END)
GROUP BY
	LTRIM(RTRIM(SCRIP_CD)),
	LTRIM(RTRIM(CERTNO)),
	LTRIM(RTRIM(SERIES)),
	LTRIM(RTRIM(D.PARTY_CODE)),
	LTRIM(RTRIM(C1.LONG_NAME)),
	LTRIM(RTRIM(SETT_NO)),
	LTRIM(RTRIM(SETT_TYPE)),
	LTRIM(RTRIM(BDPID)),
	LTRIM(RTRIM(BCLTDPID))
HAVING
	SUM(QTY) > 0
ORDER BY
	D.PARTY_CODE,
	SCRIP_CD,
	SERIES,
	SETT_NO,
	SETT_TYPE

GO
