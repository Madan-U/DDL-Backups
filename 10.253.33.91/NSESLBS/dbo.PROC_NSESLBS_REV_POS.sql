CREATE  PROC [dbo].[PROC_NSESLBS_REV_POS]        
AS        
        
DECLARE @TDATE VARCHAR(11),        
  @SYSDATE VARCHAR(11),        
  @EXPDATE VARCHAR(11),        
  @FLAG INT,  
  @EXPCUR CURSOR        
        
SET @FLAG = 0        
SELECT @SYSDATE =LEFT(GETDATE(),11)        

SELECT * INTO #DELIVERYCLT 
FROM  MSAJAG.DBO.DELIVERYCLT
WHERE 1 = 2 
SELECT * INTO #DELNET
FROM  MSAJAG.DBO.DELNET
WHERE 1 = 2 
 
SET @EXPCUR = CURSOR FOR    
SELECT DISTINCT EXPDATE = LEFT(CONVERT(DATETIME,EXPIRYDATE),11) FROM TRD_SETT_POS        
WHERE CONVERT(DATETIME,EXPIRYDATE) >= @SYSDATE         
ORDER BY 1  
OPEN @EXPCUR  
FETCH NEXT FROM @EXPCUR INTO @EXPDATE  
WHILE @@FETCH_STATUS = 0  
BEGIN  
        
 SELECT TOP 1 @TDATE = LEFT(SEC_PAYIN,11) FROM (        
 SELECT DISTINCT TOP 3 SEC_PAYIN FROM SETT_MST        
 WHERE SEC_PAYIN <= @EXPDATE        
 AND SETT_TYPE = 'N'        
 ORDER BY SEC_PAYIN DESC )  A        
 ORDER BY SEC_PAYIN         
    
 IF CONVERT(DATETIME,@TDATE) <= CONVERT(DATETIME,@SYSDATE)  and CONVERT(DATETIME,@tdate) <= CONVERT(DATETIME,@expdate)       
 BEGIN        
 /*       
  DELETE FROM MSAJAG.DBO.DELIVERYCLT        
  WHERE SETT_NO IN (SELECT M.SETT_NO FROM SETTLEMENT S, TRD_SETT_POS T, SETT_MST M, OWNER        
  WHERE S.SAUDA_DATE = T.SAUDA_DATE        
  AND S.ORDER_NO = T.ORDER_NO        
  AND S.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(T.TRADE_NO)        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SERIES = T.SERIES        
  AND M.SETT_TYPE = 'P'        
  AND RIGHT(M.SETT_NO,3) >= '500'  
  AND LEFT(M.SEC_PAYIN, 11) = LEFT(CONVERT(DATETIME,EXPIRYDATE),11)         
  AND LEFT(CONVERT(DATETIME,EXPIRYDATE),11) = @EXPDATE )         
        
  DELETE FROM MSAJAG.DBO.DELNET        
  WHERE SETT_NO IN (SELECT M.SETT_NO FROM SETTLEMENT S, TRD_SETT_POS T, SETT_MST M, OWNER        
  WHERE S.SAUDA_DATE = T.SAUDA_DATE        
  AND S.ORDER_NO = T.ORDER_NO        
  AND S.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(T.TRADE_NO)        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SERIES = T.SERIES        
  AND M.SETT_TYPE = 'P'        
  AND RIGHT(M.SETT_NO,3) >= '500'  
  AND LEFT(M.SEC_PAYIN, 11) = LEFT(CONVERT(DATETIME,EXPIRYDATE),11)         
  AND LEFT(CONVERT(DATETIME,EXPIRYDATE),11) = @EXPDATE )         
*/        
  INSERT INTO #DELIVERYCLT        
  SELECT M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, SERIES='EQ', PARTY_CODE,         
  TRADEQTY=ABS(SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END)),         
  INOUT = (CASE WHEN SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) < 0 THEN 'O' ELSE 'I' END),         
  BRANCH_CD = 'HO', PARTIPANTCODE = MEMBERCODE ,''  AS A       
  FROM SETTLEMENT S, TRD_SETT_POS T, SETT_MST M, OWNER        
  WHERE S.SAUDA_DATE = T.SAUDA_DATE        
  AND S.ORDER_NO = T.ORDER_NO        
  AND S.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(T.TRADE_NO)        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SERIES = T.SERIES        
  AND M.SETT_TYPE = 'P'        
  AND LEFT(M.SEC_PAYIN, 11) = LEFT(CONVERT(DATETIME,EXPIRYDATE),11)        
  AND LEFT(CONVERT(DATETIME,EXPIRYDATE),11) = @EXPDATE AND T.SELL_BUY IN (1,4)      
  AND RIGHT(M.SETT_NO,3) >= '500'      
  GROUP BY M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, S.SERIES, PARTY_CODE, MEMBERCODE        
  HAVING SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) <> 0        
      
  INSERT INTO #DELIVERYCLT       
  SELECT M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, SERIES='EQ', PARTY_CODE,         
  TRADEQTY=ABS(SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END)),         
  INOUT = (CASE WHEN SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) < 0 THEN 'O' ELSE 'I' END),         
  BRANCH_CD = 'HO', PARTIPANTCODE = MEMBERCODE  ,''  AS A       
  FROM SETTLEMENT S, TRD_SETT_POS T, SETT_MST M, OWNER        
  WHERE S.SAUDA_DATE = T.SAUDA_DATE        
  AND S.ORDER_NO = T.ORDER_NO        
  AND S.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(T.TRADE_NO)        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SERIES = T.SERIES        
  AND M.SETT_TYPE = 'P'        
  AND LEFT(M.SEC_PAYIN, 11) = LEFT(CONVERT(DATETIME,EXPIRYDATE),11)        
  AND LEFT(CONVERT(DATETIME,EXPIRYDATE),11) = @EXPDATE AND T.SELL_BUY IN (2,3)      
  AND RIGHT(M.SETT_NO,3) >= '500'      
  GROUP BY M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, S.SERIES, PARTY_CODE, MEMBERCODE        
  HAVING SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) <> 0        
      
  INSERT INTO #DELNET        
  SELECT M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, SERIES='EQ',          
  TRADEQTY=ABS(SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END)),         
  INOUT = (CASE WHEN SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) < 0 THEN 'O' ELSE 'I' END)        
  FROM SETTLEMENT S, TRD_SETT_POS T, SETT_MST M, OWNER        
  WHERE S.SAUDA_DATE = T.SAUDA_DATE        
  AND S.ORDER_NO = T.ORDER_NO        
  AND S.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(T.TRADE_NO)        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SERIES = T.SERIES        
  AND M.SETT_TYPE = 'P'        
  AND LEFT(M.SEC_PAYIN, 11) = LEFT(CONVERT(DATETIME,EXPIRYDATE),11)        
  AND LEFT(CONVERT(DATETIME,EXPIRYDATE),11) = @EXPDATE AND T.SELL_BUY IN (1,4)      
  AND RIGHT(M.SETT_NO,3) >= '500'      
  GROUP BY M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, S.SERIES        
  HAVING SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) <> 0      
        
  INSERT INTO #DELNET        
  SELECT M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, SERIES='EQ',          
  TRADEQTY=ABS(SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END)),         
  INOUT = (CASE WHEN SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) < 0 THEN 'O' ELSE 'I' END)        
  FROM SETTLEMENT S, TRD_SETT_POS T, SETT_MST M, OWNER        
  WHERE S.SAUDA_DATE = T.SAUDA_DATE        
  AND S.ORDER_NO = T.ORDER_NO        
  AND S.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(T.TRADE_NO)        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SCRIP_CD = T.SCRIP_CD        
  AND S.SERIES = T.SERIES        
  AND M.SETT_TYPE = 'P'        
  AND LEFT(M.SEC_PAYIN, 11) = LEFT(CONVERT(DATETIME,EXPIRYDATE),11)        
  AND LEFT(CONVERT(DATETIME,EXPIRYDATE),11) = @EXPDATE AND T.SELL_BUY IN (2,3)      
  AND RIGHT(M.SETT_NO,3) >= '500'      
  GROUP BY M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, S.SERIES        
  HAVING SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) <> 0        
        
 END     
 FETCH NEXT FROM @EXPCUR INTO @EXPDATE  
END 

DELETE FROM MSAJAG.DBO.DELIVERYCLT
WHERE SETT_NO IN (SELECT SETT_NO FROM #DELIVERYCLT )
AND SETT_TYPE = 'P'

INSERT INTO MSAJAG.DBO.DELIVERYCLT
SELECT * FROM #DELIVERYCLT

DELETE FROM MSAJAG.DBO.DelNet
WHERE SETT_NO IN (SELECT SETT_NO FROM #DELNET )
AND SETT_TYPE = 'P'

INSERT INTO MSAJAG.DBO.DELNET
SELECT * FROM #DELNET

DELETE FROM ANGELDEMAT.MSAJAG.DBO.DELIVERYCLT
WHERE SETT_NO IN (SELECT SETT_NO FROM #DELIVERYCLT )
AND SETT_TYPE = 'P'

INSERT INTO ANGELDEMAT.MSAJAG.DBO.DELIVERYCLT
SELECT * FROM #DELIVERYCLT

DELETE FROM ANGELDEMAT.MSAJAG.DBO.DelNet
WHERE SETT_NO IN (SELECT SETT_NO FROM #DELNET )
AND SETT_TYPE = 'P'

INSERT INTO ANGELDEMAT.MSAJAG.DBO.DELNET
SELECT * FROM #DELNET

GO
