-- Object: PROCEDURE dbo.CBO_APPMENU
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC CBO_APPMENU
(
	@USER_CATEGORY VARCHAR(2000)
)

AS
/*
	EXEC CBO_APPMENU '388'
*/
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
SET NOCOUNT ON
SELECT
	R.FLDREPORTCODE,
	R.FLDREPORTNAME,
	R.FLDPATH,
	R.FLDTARGET,
	G.FLDGRPNAME,
	C.FLDCATEGORYCODE,
	M.FLDMENUNAME,
	R.FLDMENUGRP,
	R.FLDDESC,
	FLDMANUAL = CASE WHEN
					R.FLDMENUGRP BETWEEN 0 AND 3
				THEN
					'SHARE ACCOUTING'
				ELSE
					CASE WHEN
						R.FLDMENUGRP BETWEEN 4 AND 6
					THEN
						'FINANCE ACCOUNTING'
					ELSE
						'UTILITIES'
					END
				END
FROM
	TBLREPORTS R (NOLOCK),
	TBLREPORTGRP G (NOLOCK),
	TBLMENUHEAD M (NOLOCK),
	TBLCATMENU C (NOLOCK)
WHERE
	R.FLDREPORTGRP = G.FLDREPORTGRP
	AND R.FLDMENUGRP = M.FLDMENUCODE
	AND R.FLDREPORTCODE = C.FLDREPORTCODE
	AND C.FLDCATEGORYCODE LIKE '%,' +  @USER_CATEGORY + ',%'
ORDER BY
	R.FLDMENUGRP,
	G.FLDGRPNAME,
	R.FLDORDER,
	R.FLDREPORTNAME

GO
