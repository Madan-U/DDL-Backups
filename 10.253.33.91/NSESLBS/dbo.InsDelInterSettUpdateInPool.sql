-- Object: PROCEDURE dbo.InsDelInterSettUpdateInPool
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC [DBO].[INSDELINTERSETTUPDATEINPOOL] AS          
DECLARE           
@@SNO INT,          
@@DELSNO INT,          
@@PARTY_CODE VARCHAR(10),          
@@ISIN VARCHAR(12),          
@@QTY INT,          
@@TCODE INT,          
@@EXCHG VARCHAR(3),          
@@TRDATE VARCHAR(11),          
@@SETT_NO VARCHAR(7),          
@@BCLTDPID VARCHAR(16),          
@@BDPID VARCHAR(8),          
@@SETT_TYPE VARCHAR(2),          
@@SETTDELQTY INT,          
@@SETTDELTRANSQTY INT,          
@@DEMATCUR CURSOR,          
@@SETTDEL CURSOR,          
@@SETTDELTRANS CURSOR          
      
TRUNCATE TABLE BSEDB..DELTRANS_PROCESS       
      
INSERT INTO BSEDB..DELTRANS_PROCESS       
SELECT       
 *, UPDFLAG = 0       
FROM       
 BSEDB.DBO.DELTRANS       
WHERE       
 FILLER2 = 1       
 AND DRCR = 'D'       
 AND DELIVERED = 'G'        
 AND TRTYPE <> 908           
          
SELECT @@SETTDELQTY = 0           
SELECT @@SETTDELTRANSQTY = 0          
/* GETTING ALL THE RECORDS FROM DEMATTRANS FOR INTERSETTLEMENT */          
SET @@DEMATCUR = CURSOR FOR          
SELECT D.SNO,ISIN,QTY,TCODE,TRDATE=LEFT(CONVERT(VARCHAR,TRDATE,109),11),D.SETT_NO,D.SETT_TYPE,DP.DPCLTNO,DP.DPID,EXCHG='NSE'          
FROM DEMATTRANS D, DELSEGMENT DS, BSEDB.DBO.DELIVERYDP DP WHERE D.CLTACCNO = DS.BSECMBPID AND PARTY_CODE = 'PARTY'            
AND DP.DPTYPE = 'NSDL' AND DP.DESCRIPTION LIKE '%POOL%'          
ORDER BY ISIN,QTY DESC,D.SETT_NO,D.SETT_TYPE,DP.DPCLTNO,DP.DPID          
OPEN @@DEMATCUR           
FETCH NEXT FROM @@DEMATCUR INTO @@SNO,@@ISIN,@@QTY,@@TCODE,@@TRDATE,@@SETT_NO,@@SETT_TYPE,@@BCLTDPID,@@BDPID,@@EXCHG          
WHILE @@FETCH_STATUS = 0          
BEGIN           
 /* TAKING PARTY'S SELL POSITION FOR THAT SCRIP FROM DELIVERYCLT TABLE. */          
 SET @@SETTDEL = CURSOR FOR          
 SELECT SNO,QTY,PARTY_CODE FROM BSEDB.DBO.DELTRANS_PROCESS WHERE BCLTDPID = @@BCLTDPID AND BDPID = @@BDPID          
 AND CERTNO = @@ISIN AND ISETT_NO = @@SETT_NO AND FILLER2 = 1 AND DRCR = 'D' AND DELIVERED = 'G'  AND TRTYPE <> 908           
 ORDER BY QTY DESC,PARTY_CODE          
 OPEN @@SETTDEL          
 FETCH NEXT FROM @@SETTDEL INTO @@DELSNO,@@SETTDELQTY,@@PARTY_CODE          
 WHILE @@FETCH_STATUS = 0 AND @@QTY > 0            
 BEGIN          
  SELECT @@DELSNO,@@SETTDELQTY,@@PARTY_CODE,@@QTY          
            
  IF @@SETTDELQTY > 0 AND @@QTY > 0           
  BEGIN          
   IF @@SETTDELQTY >= @@QTY           
   BEGIN          
    /* UPDATE THE RECORD AND EXIT FROM CURRENT LOOP */          
    UPDATE DEMATTRANS SET PARTY_CODE = @@PARTY_CODE WHERE SNO = @@SNO AND SETT_NO = @@SETT_NO          
    AND ISIN = @@ISIN AND SNO = @@SNO AND DRCR = 'C' AND TRTYPE = 904          
          
    IF @@SETTDELQTY > @@QTY           
    BEGIN          
     INSERT INTO BSEDB.DBO.DELTRANS_PROCESS           
     SELECT 0,SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,@@QTY,FROMNO,TONO,CERTNO,FOLIONO,      
     HOLDERNAME,REASON,DRCR,'D',ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,      
     ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5, UPDFLAG = 1          
     FROM BSEDB.DBO.DELTRANS_PROCESS WHERE SNO = @@DELSNO AND BCLTDPID = @@BCLTDPID AND BDPID = @@BDPID AND CERTNO = @@ISIN           
     AND PARTY_CODE = @@PARTY_CODE AND ISETT_NO = @@SETT_NO AND FILLER2 = 1 AND DRCR = 'D'           
          
     UPDATE BSEDB.DBO.DELTRANS_PROCESS SET QTY=@@SETTDELQTY-@@QTY, UPDFLAG = 1 WHERE SNO = @@DELSNO AND BCLTDPID = @@BCLTDPID AND BDPID = @@BDPID          
     AND CERTNO = @@ISIN AND PARTY_CODE = @@PARTY_CODE AND ISETT_NO = @@SETT_NO AND           
     FILLER2 = 1 AND DRCR = 'D'           
     SELECT @@QTY = 0          
    END          
    ELSE           
    BEGIN           
     UPDATE BSEDB.DBO.DELTRANS_PROCESS SET DELIVERED = 'D', UPDFLAG = 1 WHERE SNO = @@DELSNO AND BCLTDPID = @@BCLTDPID AND BDPID = @@BDPID          
     AND CERTNO = @@ISIN AND PARTY_CODE = @@PARTY_CODE AND ISETT_NO = @@SETT_NO AND FILLER2 = 1           
     AND DRCR = 'D'           
     SELECT @@QTY = 0          
    END               
    SELECT @@SETTDELQTY = 0            
END          
   ELSE          
   BEGIN          
    /* UPDATE THE RECORD WITH @@QTY - @@SETTDELQTY AND FETCH THE NEXT           
       RECORD FROM CURRENT LOOP */          
    INSERT INTO DEMATTRANS SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,@@PARTY_CODE,SCRIP_CD,          
    SERIES,@@SETTDELQTY,TRDATE,CLTACCNO,DPID,DPNAME,ISIN,BRANCH_CD,PARTIPANTCODE,DPTYPE,TRANSNO,          
    DRCR,BDPTYPE,BDPID,BCLTACCNO,FILLER1,FILLER2,FILLER3,FILLER4,FILLER5 FROM DEMATTRANS           
    WHERE SNO = @@SNO AND SETT_NO = @@SETT_NO AND ISIN = @@ISIN AND SNO = @@SNO AND DRCR = 'C'               
              
    UPDATE DEMATTRANS SET QTY = @@QTY-@@SETTDELQTY          
    WHERE SNO = @@SNO AND SETT_NO = @@SETT_NO AND ISIN = @@ISIN AND SNO = @@SNO AND DRCR = 'C'           
          
    UPDATE BSEDB.DBO.DELTRANS_PROCESS SET DELIVERED = 'D', UPDFLAG = 1 WHERE SNO = @@DELSNO AND BCLTDPID = @@BCLTDPID AND BDPID = @@BDPID          
    AND CERTNO = @@ISIN           
    AND PARTY_CODE = @@PARTY_CODE AND ISETT_NO = @@SETT_NO AND FILLER2 = 1 AND DRCR = 'D'              
          
    SELECT @@QTY = @@QTY-@@SETTDELQTY          
    SELECT @@SETTDELQTY = 0            
   END          
  END          
  FETCH NEXT FROM @@SETTDEL INTO @@DELSNO,@@SETTDELQTY,@@PARTY_CODE           
 END          
 CLOSE @@SETTDEL           
 DEALLOCATE @@SETTDEL          
 FETCH NEXT FROM @@DEMATCUR INTO @@SNO,@@ISIN,@@QTY,@@TCODE,@@TRDATE,@@SETT_NO,@@SETT_TYPE,@@BCLTDPID,@@BDPID,@@EXCHG           
END          
CLOSE @@DEMATCUR          
DEALLOCATE @@DEMATCUR          
          
UPDATE BSEDB..DELTRANS SET DELIVERED = DP.DELIVERED, QTY = DP.QTY FROM BSEDB..DELTRANS_PROCESS DP       
WHERE BSEDB..DELTRANS.SNO = DT_SNO      
AND DT_SNO <> 0      
AND UPDFLAG = 1      
--AND (BSEDB..DELTRANS.DELIVERED <> DP.DELIVERED OR BSEDB..DELTRANS.QTY <> DP.QTY)      
      
INSERT INTO BSEDB..DELTRANS       
SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO,
HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,
ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5       
FROM BSEDB..DELTRANS_PROCESS WHERE DT_SNO = 0

GO
