CREATE   PROC V2_CASHINSTREPORT
	(
	@FROMDATE VARCHAR(11),
	@TODATE VARCHAR(11),
	@FROMPARTY VARCHAR(15),
	@TOPARTY VARCHAR(15),
	@REPORT_TYPE INT,
	@SUMMARIES INT,
	@DATEFORMAT INT,
	@SETTDATE VARCHAR(11),
	@STATUSID VARCHAR(15),
	@STATUSNAME VARCHAR(25)
	)

	AS
	

SELECT 
	DISTINCT
	PARTY_CODE,
	LONG_NAME,
	SHORT_NAME,
	BRANCH_CD,
	FAMILY,
	SUB_BROKER,
	PAN_GIR_NO,
	EXCHANGE,
	INSURANCE_CHRG,
	SERVICE_CHRG,
	TURNOVER_TAX,
	C2.OTHER_CHRG,
	SEBI_TURN_TAX,
	BROKERNOTE,
	FAX,
	OFF_PHONE1,
	BANKID	
INTO
	#CLIENTMASTER	
FROM
	CLIENT1 C1 (NOLOCK),
	CLIENT2 C2 (NOLOCK)
WHERE
	C1.CL_CODE = C2.CL_CODE
	AND C2.PARTY_CODE >= @FROMPARTY
	AND C2.PARTY_CODE <= @TOPARTY
	AND @STATUSNAME = (
	CASE
		WHEN @STATUSID = 'BRANCH'
		THEN C1.BRANCH_CD
		WHEN @STATUSID = 'SUBBROKER'
		THEN C1.SUB_BROKER
		WHEN @STATUSID = 'TRADER'
		THEN C1.TRADER
		WHEN @STATUSID = 'FAMILY'
		THEN C1.FAMILY
		WHEN @STATUSID = 'AREA'
		THEN C1.AREA
		WHEN @STATUSID = 'REGION'
		THEN C1.REGION
		WHEN @STATUSID = 'CLIENT'
		THEN C2.PARTY_CODE
		ELSE 'BROKER' 
	END)


	IF @REPORT_TYPE >= 3
	BEGIN
	SELECT 
		DISTINCT
		PARTY_CODE,
		LONG_NAME,
		SHORT_NAME,
		BRANCH_CD,
		FAMILY,
		SUB_BROKER,
		PAN_GIR_NO,
		EXCHANGE,
		INSURANCE_CHRG,
		SERVICE_CHRG,
		TURNOVER_TAX,
		C2.OTHER_CHRG,
		SEBI_TURN_TAX,
		BROKERNOTE,
		FAX,
		OFF_PHONE1,
		BANKID	
	INTO
		#CLIENTMASTERBSE	
	FROM
		BSEDB.DBO.CLIENT1 C1 (NOLOCK),
		BSEDB.DBO.CLIENT2 C2 (NOLOCK)
	WHERE
		C1.CL_CODE = C2.CL_CODE
		AND C2.PARTY_CODE >= @FROMPARTY
		AND C2.PARTY_CODE <= @TOPARTY
		AND @STATUSNAME = (
		CASE
			WHEN @STATUSID = 'BRANCH'
			THEN C1.BRANCH_CD
			WHEN @STATUSID = 'SUBBROKER'
			THEN C1.SUB_BROKER
			WHEN @STATUSID = 'TRADER'
			THEN C1.TRADER
			WHEN @STATUSID = 'FAMILY'
			THEN C1.FAMILY
			WHEN @STATUSID = 'AREA'
			THEN C1.AREA
			WHEN @STATUSID = 'REGION'
			THEN C1.REGION
			WHEN @STATUSID = 'CLIENT'
			THEN C2.PARTY_CODE
			ELSE 'BROKER' 
		END)
	END


	IF @REPORT_TYPE = 4
	BEGIN
	SELECT 
		DISTINCT
		PARTY_CODE,
		LONG_NAME,
		SHORT_NAME,
		BRANCH_CD,
		FAMILY,
		SUB_BROKER,
		PAN_GIR_NO,
		EXCHANGE,
		INSURANCE_CHRG,
		SERVICE_CHRG,
		TURNOVER_TAX,
		C2.OTHER_CHRG,
		SEBI_TURN_TAX,
		BROKERNOTE,
		FAX,
		OFF_PHONE1,
		BANKID	
	INTO
		#CLIENTMASTERNFO	
	FROM
		NSEFO.DBO.CLIENT1 C1 (NOLOCK),
		NSEFO.DBO.CLIENT2 C2 (NOLOCK)
	WHERE
		C1.CL_CODE = C2.CL_CODE
		AND C2.PARTY_CODE >= @FROMPARTY
		AND C2.PARTY_CODE <= @TOPARTY
		AND @STATUSNAME = (
		CASE
			WHEN @STATUSID = 'BRANCH'
			THEN C1.BRANCH_CD
			WHEN @STATUSID = 'SUBBROKER'
			THEN C1.SUB_BROKER
			WHEN @STATUSID = 'TRADER'
			THEN C1.TRADER
			WHEN @STATUSID = 'FAMILY'
			THEN C1.FAMILY
			WHEN @STATUSID = 'AREA'
			THEN C1.AREA
			WHEN @STATUSID = 'REGION'
			THEN C1.REGION
			WHEN @STATUSID = 'CLIENT'
			THEN C2.PARTY_CODE
			ELSE 'BROKER' 
		END)
	END


IF @REPORT_TYPE = 2
   BEGIN

	IF @SUMMARIES = 1 
	   BEGIN

		SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
		SELECT
			I.PARTY_CODE,
			PARTYNAME = C.LONG_NAME,
			PARTYSHORTNAME = C.SHORT_NAME,
			CONTRACTNO,
			TRADE_NO = Pradnya.DBO.ReplaceTradeNo(MIN(TRADE_NO)),
			ORDER_NO = MIN(ORDER_NO),
			I.SETT_NO,
			I.SETT_TYPE,
			TRADEDATE = CONVERT(VARCHAR,SAUDA_DATE,@DATEFORMAT),
			TRADETIME = CONVERT(VARCHAR,MIN(SAUDA_DATE),108),
			MAXTRADETIME = CONVERT(VARCHAR,MAX(SAUDA_DATE),108),
			SETTDATE  = CONVERT(VARCHAR,FUNDS_PAYIN,@DATEFORMAT),	
			I.SCRIP_CD,
			I.SERIES,	
			SCRIPNAME = S1.LONG_NAME,
			QTY = SUM(TRADEQTY),
			PQTY = SUM(
			CASE WHEN SELL_BUY = 1 
				THEN TRADEQTY 
				ELSE 0 END),
			SQTY = SUM(
			CASE WHEN SELL_BUY = 2 
				THEN TRADEQTY 
				ELSE 0 END),
			SELL_BUY = (
			CASE WHEN SELL_BUY = 1 
				THEN 'BUY'
				ELSE 'SELL' 
				END),
			SELL_BUYBS = (
			CASE WHEN SELL_BUY = 1 
				THEN 'B'
				ELSE 'S' END),
			SELL_BUY_O = SELL_BUY,
			SETTTYPE = (
			CASE WHEN I.SETT_TYPE = 'W' THEN 'TFT' 
			ELSE 'RSD' END),
			SETTTYPE1 = (
			CASE WHEN I.SETT_TYPE = 'W' THEN 'Trade For Trade' 
			ELSE 'Normal' END),
			MARKETRATE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(SUM(MARKETRATE*TRADEQTY)/SUM(TRADEQTY),4))),
			AMOUNT = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(SUM(MARKETRATE*TRADEQTY),2))),
			NETRATE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND((SUM(TRADEQTY*N_NETRATE)/SUM(TRADEQTY)),4))),
			NETAMOUNT = (
			CASE 
				WHEN SELL_BUY = 1 
				THEN CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND((SUM(N_NETRATE*TRADEQTY)),2)))
				ELSE CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND((SUM(N_NETRATE*TRADEQTY)),2))) END),
			NETAMOUNTSTT = (
			CASE 
				WHEN SELL_BUY = 1 
				THEN CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND((SUM(N_NETRATE*TRADEQTY)+SUM(INS_CHRG)),2)))
				ELSE CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND((SUM(N_NETRATE*TRADEQTY)-SUM(INS_CHRG)),2))) END),

			BROKRAGE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND((SUM(NBROKAPP*TRADEQTY)/SUM(TRADEQTY)),4))),
			TOTBROKRAGE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(SUM(NBROKAPP*TRADEQTY),2))),
			BROKRAGECOMMISSION = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(
					((SUM(NBROKAPP*TRADEQTY) 
					+ SUM(CASE 
					WHEN INSURANCE_CHRG = 1 
					THEN INS_CHRG 
					ELSE 0 END))*100) / SUM(MARKETRATE*TRADEQTY),4))),
			COMMPERS = (SUM(TRADEQTY*NBROKAPP)/SUM(TRADEQTY))*100/(SUM(TRADEQTY*MARKETRATE)/SUM(TRADEQTY)),

			STT = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				SUM(CASE 
				WHEN INSURANCE_CHRG = 1 
				THEN INS_CHRG 
				ELSE 0 END),2))),
			TURN_TAX = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				SUM(CASE 
			        WHEN C.TURNOVER_TAX = 1
			        THEN TURN_TAX 
			        ELSE 0 END),2))), 
			SEBI_TAX = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				SUM(CASE 
			        WHEN SEBI_TURN_TAX = 1 
			        THEN SEBI_TAX 
			        ELSE 0 END),2))), 
			OTHER_CHRG = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				SUM(CASE 
			        WHEN C.OTHER_CHRG = 1 
			        THEN I.OTHER_CHRG 
			        ELSE 0 END),2))), 
			SERVICE_TAX = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				SUM(CASE 
			        WHEN SERVICE_CHRG = 0 
			        THEN SERVICE_TAX 
			        ELSE 0 END),2))), 
			BROKER_CHRG = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				SUM(CASE 
			        WHEN BROKERNOTE = 1 
			        THEN BROKER_CHRG
			        ELSE 0 END),2))),
			TOTSERSTTCHRG = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				SUM(CASE 
			        WHEN SERVICE_CHRG = 0 
			        THEN SERVICE_TAX 
			        ELSE 0 END)+
				SUM(CASE 
				WHEN INSURANCE_CHRG = 1 
				THEN INS_CHRG 
				ELSE 0 END),2))), 
			BROKERCODE = ISNULL(O.MEMBERCODE,''),
			COMPANYNAME = ISNULL(O.COMPANY,''),
			COMPANYADD1 = ISNULL(O.ADDR1,''),
			COMPANYADD2 = ISNULL(O.ADDR2,''),
			COMPANYCITY = ISNULL(O.CITY,''),
			COMPANYZIP = ISNULL(O.ZIP,''),
			COMPANYPHONE = ISNULL(O.PHONE,''),
			COMPANYPSEBINO = ISNULL(O.BROKERSEBIREGNO,''),
			COMPANYADDRESS = ISNULL(O.ADDR1,'') +' '+ ISNULL(O.ADDR2,'') +' '+ ISNULL(O.CITY,'') +' '+ ISNULL(O.ZIP,''),
			RICCODE = ISNULL(RIC,''),
			RICBASIC= ISNULL(RICBASIC,''),
			ISIN = ISNULL(M.ISIN,''),
			PARTIPANTCODE = PARTIPANTCODE,
			ORD_DATE = CONVERT(VARCHAR,SAUDA_DATE,112),
			EXCHANGE = 'NSE',
			BLOOMBERGCODE = ISNULL(BM.BLOOMBERGCODE,''),
			BLOOMBERG_TICKER = ISNULL(BM.BLOOMBERG_TICKER,'')
		FROM
			ISETTLEMENT I (NOLOCK)
				LEFT OUTER JOIN CMRICCODEMASTER CR 
					ON ( I.SCRIP_CD = CR.SCRIP_CD )
				LEFT OUTER JOIN RICBASICCODE R 
					ON ( I.SCRIP_CD = R.Scrip_CD )
				LEFT OUTER JOIN MULTIISIN M 
					ON ( I.SCRIP_CD = M.SCRIP_CD 
					AND I.SERIES = M.SERIES 
					AND VALID = 1)
				LEFT OUTER JOIN BLOOMBERGMASTER BM
					ON (I.SCRIP_CD = BM.SCRIP_CD),
			#CLIENTMASTER C (NOLOCK),
			SETT_MST SM (NOLOCK),
			SCRIP1 S1 (NOLOCK),
			SCRIP2 S2 (NOLOCK),
			OWNER O (NOLOCK)
		WHERE
			I.PARTY_CODE = C.PARTY_CODE
			AND I.SETT_NO = SM.SETT_NO
			AND I.SETT_TYPE = SM.SETT_TYPE
			AND S1.CO_CODE = S2.CO_CODE
			AND S1.SERIES = S2.SERIES
			AND I.SCRIP_CD = S2.SCRIP_CD
			AND I.SERIES = S2.SERIES
			AND I.SAUDA_DATE >= @FROMDATE
			AND I.SAUDA_DATE <= @TODATE + ' 23:59:59'
			AND I.PARTY_CODE >= @FROMPARTY
			AND I.PARTY_CODE <= @TOPARTY
			AND TRADEQTY > 0

		GROUP BY
			I.PARTY_CODE,
			C.LONG_NAME,
			C.SHORT_NAME,
			CONTRACTNO,
			I.SETT_NO,
			I.SETT_TYPE,
			CONVERT(VARCHAR,SAUDA_DATE,@DATEFORMAT),
			CONVERT(VARCHAR,FUNDS_PAYIN,@DATEFORMAT),	
			I.SCRIP_CD,
			I.SERIES,	
			S1.LONG_NAME,
			SELL_BUY,
			ISNULL(O.MEMBERCODE,''),
			ISNULL(O.COMPANY,''),
			ISNULL(O.ADDR1,''),
			ISNULL(O.ADDR2,''),
			ISNULL(O.CITY,''),
			ISNULL(O.ZIP,''),
			ISNULL(O.PHONE,''),
			ISNULL(O.ADDR1,'') +' '+ ISNULL(O.ADDR2,'') +' '+ ISNULL(O.CITY,'') +' '+ ISNULL(O.ZIP,''),
			ISNULL(BROKERSEBIREGNO,''),
			ISNULL(RIC,''),
			ISNULL(RICBASIC,''),
			ISNULL(M.ISIN,''),
			CONVERT(VARCHAR,SAUDA_DATE,112),
			PARTIPANTCODE,
			ISNULL(BM.BLOOMBERGCODE,''),
			ISNULL(BM.BLOOMBERG_TICKER,'')

		ORDER BY
			CONVERT(VARCHAR,SAUDA_DATE,112),
			I.PARTY_CODE,
			I.SCRIP_CD
	   END

	ELSE

	   BEGIN

		SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
		SELECT
			I.PARTY_CODE,
			PARTYNAME = C.LONG_NAME,
			PARTYSHORTNAME = C.SHORT_NAME,
			CONTRACTNO,
			TRADE_NO = Pradnya.DBO.ReplaceTradeNo(TRADE_NO),
			ORDER_NO,
			I.SETT_NO,
			I.SETT_TYPE,
			TRADEDATE = CONVERT(VARCHAR,SAUDA_DATE,@DATEFORMAT),
			TRADETIME = CONVERT(VARCHAR,SAUDA_DATE,108),
			SETTDATE  = CONVERT(VARCHAR,FUNDS_PAYIN,@DATEFORMAT),	
			I.SCRIP_CD,
			I.SERIES,	
			SCRIPNAME = S1.LONG_NAME,
			QTY = TRADEQTY,
			PQTY = (
			CASE WHEN SELL_BUY = 1 
				THEN TRADEQTY 
				ELSE 0 END),
			SQTY = (
			CASE WHEN SELL_BUY = 2 
				THEN TRADEQTY 
				ELSE 0 END),
			SELL_BUY = (
			CASE WHEN SELL_BUY = 1 
				THEN 'BUY'
				ELSE 'SELL' 
				END),
			SELL_BUYBS = (
			CASE WHEN SELL_BUY = 1 
				THEN 'B'
				ELSE 'S' END),
			SELL_BUY_O = SELL_BUY,
			SETTTYPE = (
			CASE WHEN I.SETT_TYPE = 'W' THEN 'TFT' 
			ELSE 'RSD' END),
			SETTTYPE1 = (
			CASE WHEN I.SETT_TYPE = 'W' THEN 'Trade For Trade' 
			ELSE 'Normal' END),

			MARKETRATE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(MARKETRATE,4))),
			AMOUNT = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND((MARKETRATE*TRADEQTY),2))),
			NETRATE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(N_NETRATE,4))),
			NETAMOUNT = (
			CASE 
				WHEN SELL_BUY = 1 
				THEN CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(((N_NETRATE*TRADEQTY)),2)))
				ELSE CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(((N_NETRATE*TRADEQTY)),2))) END),
			NETAMOUNTSTT = (
			CASE 
				WHEN SELL_BUY = 1 
				THEN CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(((N_NETRATE*TRADEQTY)+(INS_CHRG)),2)))
				ELSE CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(((N_NETRATE*TRADEQTY)-(INS_CHRG)),2))) END),

			BROKRAGE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(NBROKAPP,4))),
			TOTBROKRAGE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND((NBROKAPP*TRADEQTY),2))),
			BROKRAGECOMMISSION = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(
					(((NBROKAPP*TRADEQTY) 
					+ (CASE 
					WHEN INSURANCE_CHRG = 1 
					THEN INS_CHRG 
					ELSE 0 END))*100) / (MARKETRATE*TRADEQTY),4))),
			COMMPERS = ((NBROKAPP*100)/MARKETRATE),

			STT = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				(CASE 
				WHEN INSURANCE_CHRG = 1 
				THEN INS_CHRG 
				ELSE 0 END),2))),
			TURN_TAX = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				(CASE 
			        WHEN C.TURNOVER_TAX = 1
			        THEN TURN_TAX 
			        ELSE 0 END),2))), 
			SEBI_TAX = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				(CASE 
			        WHEN SEBI_TURN_TAX = 1 
			        THEN SEBI_TAX 
			        ELSE 0 END),2))), 
			OTHER_CHRG = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				(CASE 
			        WHEN C.OTHER_CHRG = 1 
			        THEN I.OTHER_CHRG 
			        ELSE 0 END),2))), 
			SERVICE_TAX = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				(CASE 
			        WHEN SERVICE_CHRG = 0 
			        THEN SERVICE_TAX 
			        ELSE 0 END),2))), 
			BROKER_CHRG = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				(CASE 
			        WHEN BROKERNOTE = 1 
			        THEN BROKER_CHRG
			        ELSE 0 END),2))), 
			TOTSERSTTCHRG = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(
				(CASE 
			        WHEN SERVICE_CHRG = 0 
			        THEN SERVICE_TAX 
			        ELSE 0 END)+
				(CASE 
				WHEN INSURANCE_CHRG = 1 
				THEN INS_CHRG 
				ELSE 0 END),2))), 
			BROKERCODE = ISNULL(O.MEMBERCODE,''),
			COMPANYNAME = ISNULL(O.COMPANY,''),
			COMPANYADD1 = ISNULL(O.ADDR1,''),
			COMPANYADD2 = ISNULL(O.ADDR2,''),
			COMPANYCITY = ISNULL(O.CITY,''),
			COMPANYZIP = ISNULL(O.ZIP,''),
			COMPANYPHONE = ISNULL(O.PHONE,''),
			COMPANYPSEBINO = ISNULL(O.BROKERSEBIREGNO,''),
			COMPANYADDRESS = ISNULL(O.ADDR1,'') +' '+ ISNULL(O.ADDR2,'') +' '+ ISNULL(O.CITY,'') +' '+ ISNULL(O.ZIP,''),
			RICCODE = ISNULL(RIC,''),
			RICBASIC= ISNULL(RICBASIC,''),
			ISIN = ISNULL(M.ISIN,''),
			ORD_DATE = CONVERT(VARCHAR,SAUDA_DATE,112),
			PARTIPANTCODE = PARTIPANTCODE,
			EXCHANGE = 'NSE',
			BLOOMBERGCODE = ISNULL(BM.BLOOMBERGCODE,''),
			BLOOMBERG_TICKER = ISNULL(BM.BLOOMBERG_TICKER,'')
		FROM
			ISETTLEMENT I (NOLOCK)
				LEFT OUTER JOIN CMRICCODEMASTER CR 
					ON ( I.SCRIP_CD = CR.SCRIP_CD )
				LEFT OUTER JOIN RICBASICCODE R 
					ON ( I.SCRIP_CD = R.Scrip_CD )
				LEFT OUTER JOIN MULTIISIN M 
					ON ( I.SCRIP_CD = M.SCRIP_CD 
					AND I.SERIES = M.SERIES 
					AND VALID = 1)
				LEFT OUTER JOIN BLOOMBERGMASTER BM
					ON (I.SCRIP_CD = BM.SCRIP_CD),
			#CLIENTMASTER C (NOLOCK),
			SETT_MST SM (NOLOCK),
			SCRIP1 S1 (NOLOCK),
			SCRIP2 S2 (NOLOCK),
			OWNER O (NOLOCK)
		WHERE
			I.PARTY_CODE = C.PARTY_CODE
			AND I.SETT_NO = SM.SETT_NO
			AND I.SETT_TYPE = SM.SETT_TYPE
			AND S1.CO_CODE = S2.CO_CODE
			AND S1.SERIES = S2.SERIES
			AND I.SCRIP_CD = S2.SCRIP_CD
			AND I.SERIES = S2.SERIES
			AND I.SAUDA_DATE >= @FROMDATE
			AND I.SAUDA_DATE <= @TODATE + ' 23:59:59'
			AND I.PARTY_CODE >= @FROMPARTY
			AND I.PARTY_CODE <= @TOPARTY
			AND TRADEQTY > 0

		ORDER BY
			CONVERT(VARCHAR,SAUDA_DATE,112),
			I.PARTY_CODE,
			I.SCRIP_CD
	
	   END	
   END

   ELSE IF @REPORT_TYPE = 3

   BEGIN

	IF @SUMMARIES = 1 
	   BEGIN

		SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
		SELECT
			I.PARTY_CODE,
			PARTYNAME = C.LONG_NAME,
			PARTYSHORTNAME = C.SHORT_NAME,
			CONTRACTNO,
			TRADE_NO = Pradnya.DBO.ReplaceTradeNo(MIN(TRADE_NO)),
			ORDER_NO = MIN(ORDER_NO),
			I.SETT_NO,
			I.SETT_TYPE,
			TRADEDATE = CONVERT(VARCHAR,SAUDA_DATE,@DATEFORMAT),
			TRADETIME = CONVERT(VARCHAR,MIN(SAUDA_DATE),108),
			MAXTRADETIME = CONVERT(VARCHAR,MAX(SAUDA_DATE),108),
			SETTDATE  = CONVERT(VARCHAR,FUNDS_PAYIN,@DATEFORMAT),	
			I.SCRIP_CD,
			I.SERIES,	
			SCRIPNAME = S1.LONG_NAME,
			QTY = SUM(TRADEQTY),
			QTYPLUS = SUM(
			CASE WHEN SELL_BUY = 1 
				THEN TRADEQTY 
				ELSE -TRADEQTY END),
			PQTY = SUM(
			CASE WHEN SELL_BUY = 1 
				THEN TRADEQTY 
				ELSE 0 END),
			SQTY = SUM(
			CASE WHEN SELL_BUY = 2 
				THEN TRADEQTY 
				ELSE 0 END),
			SELL_BUY = (
			CASE WHEN SELL_BUY = 1 
				THEN 'BUY'
				ELSE 'SELL' 
				END),
			SELL_BUYBS = (
			CASE WHEN SELL_BUY = 1 
				THEN 'B'
				ELSE 'S' END),
			SELL_BUY_O = SELL_BUY,
			SETTTYPE = (
			CASE WHEN I.SETT_TYPE = 'W' THEN 'TFT' 
			ELSE 'RSD' END),
			SETTTYPE1 = (
			CASE WHEN I.SETT_TYPE = 'W' THEN 'Trade For Trade' 
			ELSE 'Normal' END),

			MARKETRATE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND(SUM(MARKETRATE*TRADEQTY)/SUM(TRADEQTY),4))),
			AMOUNT = CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(SUM(MARKETRATE*TRADEQTY),2))),
			NETRATE = CONVERT(VARCHAR,CONVERT(NUMERIC(18,4),ROUND((SUM(TRADEQTY*N_NETRATE)/SUM(TRADEQTY)),4))),
			NETAMOUNT = (
