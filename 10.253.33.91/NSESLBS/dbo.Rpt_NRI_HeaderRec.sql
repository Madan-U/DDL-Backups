CREATE PROC Rpt_NRI_HeaderRec (
@SAUDA_DATE VARCHAR(11), 
@PARTY_CODE VARCHAR(10),
@CONTRACTNO VARCHAR(7),
@SELL_BUY   VARCHAR(1) )
AS

SELECT RECTYPE='H', PARTY_CODE, SAUDA_DATE=REPLACE(CONVERT(VARCHAR(10),SAUDA_DATE,20),'-',''), CONTRACTNO, 
SERVICE_TAX = CONVERT(NUMERIC(18,4),SUM(NSERTAX)*100/(100+CESS_TAX+EDUCESSTAX)), 
EDUCESS = CONVERT(NUMERIC(18,4),SUM(NSERTAX) - CONVERT(NUMERIC(18,4),SUM(NSERTAX)*100/(100+CESS_TAX+EDUCESSTAX))), 
TURNTAX = CONVERT(NUMERIC(18,4),SUM(TURN_TAX)), STT = CONVERT(NUMERIC(18,4),SUM(INS_CHRG)),
STAMP_DUTY = CONVERT(NUMERIC(18,4),SUM(BROKER_CHRG)), OTHER_CHRG = CONVERT(NUMERIC(18,4),SUM(OTHER_CHRG)),
NOOFTRANS=COUNT(1), 
TOTALVAL = SUM(TRADEQTY*MARKETRATE)+
	  (CASE WHEN SELL_BUY = 1 
		THEN  CONVERT(NUMERIC(18,4),SUM(TRADEQTY*NBROKAPP))
		     + CONVERT(NUMERIC(18,4),SUM(NSERTAX)*100/(100+CESS_TAX+EDUCESSTAX))
		     + CONVERT(NUMERIC(18,4),SUM(NSERTAX) - CONVERT(NUMERIC(18,4),SUM(NSERTAX)*100/(100+CESS_TAX+EDUCESSTAX)))
		     + CONVERT(NUMERIC(18,4),SUM(TURN_TAX))
		     + CONVERT(NUMERIC(18,4),SUM(INS_CHRG))
		     + CONVERT(NUMERIC(18,4),SUM(BROKER_CHRG))
		     + CONVERT(NUMERIC(18,4),SUM(OTHER_CHRG))
		ELSE
		     - CONVERT(NUMERIC(18,4),SUM(TRADEQTY*NBROKAPP))
		     - CONVERT(NUMERIC(18,4),SUM(NSERTAX)*100/(100+CESS_TAX+EDUCESSTAX))
		     - CONVERT(NUMERIC(18,4),SUM(NSERTAX) - CONVERT(NUMERIC(18,4),SUM(NSERTAX)*100/(100+CESS_TAX+EDUCESSTAX)))
		     - CONVERT(NUMERIC(18,4),SUM(TURN_TAX))
		     - CONVERT(NUMERIC(18,4),SUM(INS_CHRG))
		     - CONVERT(NUMERIC(18,4),SUM(BROKER_CHRG))
		     - CONVERT(NUMERIC(18,4),SUM(OTHER_CHRG))		
	   END)
FROM SETTLEMENT S, GLOBALS G
WHERE S.SAUDA_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT
AND AUCTIONPART NOT LIKE 'A%' AND AUCTIONPART NOT LIKE 'F%' 
AND TRADEQTY > 0 AND TRADE_NO NOT LIKE '%C%' 
AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND PARTY_CODE = @PARTY_CODE
AND CONTRACTNO = @CONTRACTNO
AND SELL_BUY = @SELL_BUY
GROUP BY PARTY_CODE, REPLACE(CONVERT(VARCHAR(10),SAUDA_DATE,20),'-',''), CONTRACTNO, CESS_TAX, EDUCESSTAX, SELL_BUY
ORDER BY REPLACE(CONVERT(VARCHAR(10),SAUDA_DATE,20),'-',''),PARTY_CODE, CONTRACTNO

GO
