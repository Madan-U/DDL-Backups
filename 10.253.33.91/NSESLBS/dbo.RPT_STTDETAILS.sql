-- Object: PROCEDURE dbo.RPT_STTDETAILS
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC RPT_STTDETAILS
	(
	@FROMDATE VARCHAR(11),
	@TODATE VARCHAR(11),
	@FROMPARTY VARCHAR(15),
	@TOPARTY VARCHAR(15)
	)
	AS

	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	SELECT
		SAUDA_DATE = LEFT(SAUDA_DATE,11),
		SETT_NO = SETT_NO,
		SCRIPTNAME = SCRIP_CD,
		BROKERAGE = 0,
		STT_BUY = SUM(PSTTDEL),
		STT_SELL = SUM(SSTTTRD)+SUM(SSTTDEL),
		TOTALSTT = ROUND(SUM(TOTALSTT),0)
	FROM 
		STT_CLIENTDETAIL (NOLOCK)
	WHERE
		SAUDA_DATE >= @FROMDATE
		AND SAUDA_DATE <= @TODATE + ' 23:59:59'
		AND PARTY_CODE >= @FROMPARTY
		AND PARTY_CODE <= @TOPARTY
		AND RECTYPE = 30
	GROUP BY
		LEFT(SAUDA_DATE,11),
		SETT_NO,
		SCRIP_CD
	ORDER BY
		SETT_NO,
		SCRIP_CD

GO
