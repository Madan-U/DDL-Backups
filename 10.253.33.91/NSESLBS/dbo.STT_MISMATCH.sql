CREATE PROC STT_MISMATCH (@SAUDA_DATE VARCHAR(11))
AS
SELECT SDATE=LEFT(SAUDA_DATE,11), PARTY_CODE, BOSTT=SUM(INS_CHRG),EXSTT=CONVERT(NUMERIC(18,4),0)
INTO #STTSETT FROM SETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND AUCTIONPART NOT IN ('AP', 'AR', 'FP', 'FS', 'FL', 'FA', 'FC')
AND TRADEQTY > 0
GROUP BY LEFT(SAUDA_DATE,11),PARTY_CODE
HAVING SUM(INS_CHRG) = 0

INSERT INTO #STTSETT 
SELECT SDATE=LEFT(SAUDA_DATE,11),PARTY_CODE, BOSTT=SUM(INS_CHRG),EXSTT=CONVERT(NUMERIC(18,4),0)
FROM iSETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND AUCTIONPART NOT IN ('AP', 'AR', 'FP', 'FS', 'FL', 'FA', 'FC')
AND TRADEQTY > 0
GROUP BY LEFT(SAUDA_DATE,11),PARTY_CODE
HAVING SUM(INS_CHRG) = 0

UPDATE #STTSETT SET EXSTT = TOTALSTT
FROM (SELECT PARTY_CODE, TOTALSTT=SUM(TOTALSTT)
FROM STT_CLIENTDETAIL
WHERE RECTYPE = 20
AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
GROUP BY PARTY_CODE ) A
WHERE #STTSETT.PARTY_CODE = A.PARTY_CODE

SELECT *, DIFF = EXSTT-BOSTT FROM #STTSETT
WHERE BOSTT <> EXSTT
ORDER BY PARTY_CODE

DROP TABLE #STTSETT

GO
