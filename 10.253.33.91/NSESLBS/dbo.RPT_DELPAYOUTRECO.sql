CREATE PROC RPT_DELPAYOUTRECO
	(
	@STATUSID VARCHAR(20),  
	@STATUSNAME VARCHAR(25),  
	@SDATE VARCHAR(11),  
	@FROMPARTY VARCHAR(15),  
	@TOPARTY VARCHAR(15),  
	@FROMSCRIP VARCHAR(15),  
	@TOSCRIP VARCHAR(15),  
	@ACCTYPE VARCHAR(5),
	@DATATYPE INT,
	@EXCHANGE VARCHAR(5) 
	)

	AS

	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

	SELECT
		EXCHANGE = DEL.EXCHANGE,
		SETT_NO,
		SETT_TYPE,
		PARTY_CODE = DEL.PARTY_CODE,
		SCRIP_CD,
		SERIES,
		CERTNO,
		ACTYPE,
		HOLDQTY = SUM(HOLDQTY),
		RMSPAYQTY = SUM(RMSPAYQTY),
		PAYQTY = SUM(PAYQTY),
		RUNDATE = LEFT(CONVERT(VARCHAR,RUNDATE,103),11)
	
	INTO 	#DELPAYOUTRECO
	FROM
		DELPAYOUT_RECO DEL WITH(NOLOCK),
		CLIENT1 C1 WITH(NOLOCK),
		CLIENT2 C2 WITH(NOLOCK)

	WHERE
		RUNDATE LIKE @SDATE + '%'
		AND C1.CL_CODE = C2.CL_CODE
		AND C2.PARTY_CODE = DEL.PARTY_CODE
		AND DEL.PARTY_CODE >= @FROMPARTY 
		AND DEL.PARTY_CODE <= @TOPARTY
		AND SCRIP_CD >= @FROMSCRIP 
		AND SCRIP_CD <= @TOSCRIP
		AND ACTYPE = @ACCTYPE
		AND DEL.EXCHANGE LIKE @EXCHANGE + '%'
		AND @STATUSNAME = 
			(CASE 
				WHEN @STATUSID = 'BRANCH' THEN BRANCH_CD
				WHEN @STATUSID = 'SUBBROKER' THEN SUB_BROKER
				WHEN @STATUSID = 'TRADER' THEN TRADER
				WHEN @STATUSID = 'FAMILY' THEN FAMILY
				WHEN @STATUSID = 'AREA' THEN AREA
				WHEN @STATUSID = 'REGION' THEN REGION
				WHEN @STATUSID = 'CLIENT' THEN DEL.PARTY_CODE
			ELSE
				'BROKER'
			END)
	
	GROUP BY
		LEFT(CONVERT(VARCHAR,RUNDATE,103),11),
		SETT_NO,
		SETT_TYPE,
		DEL.EXCHANGE,
		DEL.PARTY_CODE,
		SCRIP_CD,
		SERIES,
		CERTNO,
		ACTYPE
		
	ORDER BY 
		DEL.PARTY_CODE,
		DEL.EXCHANGE,
		SCRIP_CD, 
		SERIES, 
		CERTNO



--------------------------------------------------------
	IF (@DATATYPE = 1)
	   BEGIN	
		SELECT *
		FROM #DELPAYOUTRECO
		WHERE RMSPAYQTY = PAYQTY
	   END 
	ELSE IF (@DATATYPE = 2)
	   BEGIN	
		SELECT *
		FROM #DELPAYOUTRECO
		WHERE RMSPAYQTY <> PAYQTY
	   END 
	ELSE
	   SELECT * FROM #DELPAYOUTRECO
	
--------------------------------------------------------

GO
