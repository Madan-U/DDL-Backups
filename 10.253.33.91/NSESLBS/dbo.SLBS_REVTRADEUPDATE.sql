--------------------------------------------------

    
CREATE PROC [dbo].[SLBS_REVTRADEUPDATE]   
AS        
DECLARE @PROCESSDATE VARCHAR(11)    

DELETE FROM SLBS_REVTRADE
WHERE SETT_NO IN (SELECT SETT_NO FROM SLBS_REVTRADE_TMP T WHERE SLBS_REVTRADE.SETT_NO = T.SETT_NO AND SLBS_REVTRADE.SETT_TYPE = T.SETT_TYPE)

UPDATE SLBS_REVTRADE_TMP SET PARTY_CODE = C1.CL_CODE    
FROM CLIENT1 C1, CLIENT2 C2  
WHERE C1.CL_CODE = C2.CL_CODE  
AND PAN_GIR_NO = SLBS_REVTRADE_TMP.PARTY_CODE        
AND C2.PARTY_CODE = PARENTCODE  

INSERT INTO SLBS_REVTRADE
SELECT * FROM SLBS_REVTRADE_TMP

TRUNCATE TABLE SLBS_REVTRADE_TMP

GO
