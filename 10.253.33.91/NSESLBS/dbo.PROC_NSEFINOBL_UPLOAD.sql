CREATE PROC [dbo].[PROC_NSEFINOBL_UPLOAD](@FILEDATE VARCHAR(11), @FILEPATH VARCHAR(150)) AS        
--EXEC PROC_NSEFINOBL_UPLOAD 'DEC  1 2014', 'C:\TEMP\20141127\C_12348_FOBG_W2014219_27112014.CSV'    
DECLARE @CMD VARCHAR(1500)     
    
SET @CMD = 'SELECT SRNO,REC_DEL_FLAG,DELIVERY_DATE,TRADE_NO=CONVERT(VARCHAR(50),''''),SETT_TYPE,SETT_NO, '    
SET @CMD = @CMD + 'SCRIP_CD,SERIES,EXPIRYDATE,TRADEQTY=CONVERT(VARCHAR(50),''''),'    
SET @CMD = @CMD + ' UCC_CODE,CP_CODE,MARKETRATE=CONVERT(VARCHAR(50),''''),AMOUNT=CONVERT(VARCHAR(50),''''),ROLL_FLAG=CONVERT(VARCHAR(50),'''') '    
SET @CMD = @CMD + 'INTO #SLBS_REVTRADE_TMP FROM SLBS_REVTRADE_TMP WHERE 1 = 2 '    
    
SET @CMD = @CMD + ' TRUNCATE TABLE SLBS_REVTRADE_TMP '    
SET @CMD = @CMD + ' BULK INSERT #SLBS_REVTRADE_TMP FROM ''' + @FILEPATH + ''' WITH  ( '    
SET @CMD = @CMD + ' FIELDTERMINATOR = '','', '    
SET @CMD = @CMD + ' ROWTERMINATOR = ''' + CHAR(10) + ''', FIRSTROW = 2 ) '    
SET @CMD = @CMD + ' INSERT INTO SLBS_REVTRADE_TMP '    
SET @CMD = @CMD + ' SELECT SRNO,REC_DEL_FLAG,DELIVERY_DATE,TRADE_NO=CONVERT(BIGINT,RIGHT(TRADE_NO,LEN(TRADE_NO)-8)),SETT_TYPE,SETT_NO,PARTY_CODE=UCC_CODE,SCRIP_CD,SERIES,'    
SET @CMD = @CMD + ' TRADEQTY=CONVERT(NUMERIC(18,0),TRADEQTY),UCC_CODE,CP_CODE=ISNULL(CP_CODE,''''),MARKETRATE=CONVERT(NUMERIC(18,4),MARKETRATE),AMOUNT,EXPIRYDATE,ROLL_FLAG=isnull(ROLL_FLAG,''0'') FROM #SLBS_REVTRADE_TMP '    
    
EXEC (@CMD)    
  
EXEC SLBS_REVTRADEUPDATE    
    
/*    
DELETE FROM TBL_FINOBL_STATUS WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE     
    
INSERT INTO TBL_FINOBL_STATUS     
EXEC RPT_OBLMATCH @SETT_NO, @SETT_TYPE, @FILEDATE, 'Y'     
*/

GO
