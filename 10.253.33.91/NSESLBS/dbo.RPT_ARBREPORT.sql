--EXEC RPT_ARBREPORT 'FEB  4 2008'      
      
CREATE PROC RPT_ARBREPORT       
(      
 @SDATE VARCHAR(11),       
 @FROMPARTY VARCHAR(10)='0',       
 @TOPARTY VARCHAR(10)='ZZZZZZZZZZ'      
)      
      
AS      
      
DECLARE @LASTDATE VARCHAR(11)      
      
SELECT @LASTDATE = LEFT(MAX(TRADE_DATE),11) FROM NSEFO.DBO.FOCLOSING      
WHERE TRADE_DATE < @SDATE       
    
SELECT EXCHANGE = 'NSE', PARTY_CODE, SCRIP_CD = CONVERT(VARCHAR(12), SCRIP_CD),       
SERIES = CONVERT(VARCHAR(3), SERIES),      
EXPIRYDATE = CONVERT(DATETIME,''), SEGMENT = CONVERT(VARCHAR(7),'CASH'),      
STRIKE_PRICE = CONVERT(NUMERIC(18,4),0),      
OPTION_TYPE = CONVERT(VARCHAR(2), ''),      
OPENQTY = SUM(CASE WHEN SAUDA_DATE < @SDATE       
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END)      
     ELSE 0       
       END),      
OPENVALPRICE = SUM(CASE WHEN SAUDA_DATE < @SDATE       
          THEN (CASE WHEN SELL_BUY = 1     
       THEN TRADEQTY*N_NETRATE + NSERTAX + INS_CHRG + BROKER_CHRG + SEBI_TAX + OTHER_CHRG + TURN_TAX    
       ELSE -(TRADEQTY*N_NETRATE - NSERTAX - INS_CHRG - BROKER_CHRG - SEBI_TAX - OTHER_CHRG - TURN_TAX)    
         END)      
          ELSE 0       
                   END),      
TBUYQTY = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END)      
     ELSE 0       
       END),      
TBUYPRICE = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END)      
     ELSE 0       
       END),      
TSELLQTY = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END)      
     ELSE 0       
       END),      
TSELLPRICE = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)      
     ELSE 0       
       END),      
NETQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END),      
MTMPRICE = CONVERT(NUMERIC(18,4),0),      
MTMPNL = CONVERT(NUMERIC(18,4),0),      
MTMSQOFF = CONVERT(NUMERIC(18,4),0),      
TOTALMTM = CONVERT(NUMERIC(18,4),0),      
TAXES = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
   THEN INS_CHRG + SEBI_TAX + TURN_TAX + OTHER_CHRG + BROKER_CHRG      
   ELSE 0       
     END),      
BROKSER = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN NBROKAPP*TRADEQTY + NSERTAX      
     ELSE 0       
       END),      
NETMTM = CONVERT(NUMERIC(18,4),0),      
GROSSMARGIN = CONVERT(NUMERIC(18,4),0),      
NETMARGIN = CONVERT(NUMERIC(18,4),0)      
INTO #ARBREPORT FROM MSAJAG.DBO.SETTLEMENT      
WHERE SAUDA_DATE <= @SDATE + ' 23:59:59'      
AND TRADE_NO NOT LIKE '%C%'      
AND AUCTIONPART NOT LIKE 'A%' AND AUCTIONPART NOT LIKE 'F%'      
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY      
GROUP BY PARTY_CODE, SCRIP_CD, SERIES      
      
INSERT INTO #ARBREPORT       
SELECT EXCHANGE = 'BSE', PARTY_CODE, SCRIP_CD, SERIES = CONVERT(VARCHAR(3), SERIES),      
EXPIRYDATE = CONVERT(DATETIME,''), SEGMENT = 'CASH',       
STRIKE_PRICE = CONVERT(NUMERIC(18,4),0),      
OPTION_TYPE = CONVERT(VARCHAR(2), ''),      
OPENQTY = SUM(CASE WHEN SAUDA_DATE < @SDATE       
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END)
     ELSE 0       
       END),      
OPENVALPRICE = SUM(CASE WHEN SAUDA_DATE < @SDATE       
          THEN (CASE WHEN SELL_BUY = 1     
       THEN TRADEQTY*N_NETRATE + NSERTAX + INS_CHRG + BROKER_CHRG + SEBI_TAX + OTHER_CHRG + TURN_TAX    
       ELSE -(TRADEQTY*N_NETRATE - NSERTAX - INS_CHRG - BROKER_CHRG - SEBI_TAX - OTHER_CHRG - TURN_TAX)    
         END)      
          ELSE 0       
                   END),    
TBUYQTY = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END)      
     ELSE 0
       END),      
TBUYPRICE = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END)      
     ELSE 0       
       END),      
TSELLQTY = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END)      
     ELSE 0       
       END),      
TSELLPRICE = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)      
     ELSE 0       
       END),      
NETQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END),      
MTMPRICE = CONVERT(NUMERIC(18,4),0),      
MTMPNL = CONVERT(NUMERIC(18,4),0),      
MTMSQOFF = CONVERT(NUMERIC(18,4),0),      
TOTALMTM = CONVERT(NUMERIC(18,4),0),      
TAXES = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
   THEN INS_CHRG + SEBI_TAX + TURN_TAX + OTHER_CHRG + BROKER_CHRG      
   ELSE 0       
     END),      
BROKSER = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN NBROKAPP*TRADEQTY + NSERTAX      
     ELSE 0       
       END),      
NETMTM = CONVERT(NUMERIC(18,4),0),      
GROSSMARGIN = CONVERT(NUMERIC(18,4),0),      
NETMARGIN = CONVERT(NUMERIC(18,4),0)      
FROM BSEDB.DBO.SETTLEMENT      
WHERE SAUDA_DATE <= @SDATE + ' 23:59:59'      
AND TRADE_NO NOT LIKE '%C%'      
AND AUCTIONPART NOT LIKE 'A%' AND AUCTIONPART NOT LIKE 'F%'      
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY      
GROUP BY PARTY_CODE, SCRIP_CD, SERIES      
      
INSERT INTO #ARBREPORT       
SELECT EXCHANGE = 'NSE', PARTY_CODE, SCRIP_CD=SYMBOL, SERIES = '',      
EXPIRYDATE = EXPIRYDATE, SEGMENT = (CASE WHEN LEFT(INST_TYPE,3) = 'FUT' THEN 'FUTURES' ELSE 'OPTION' END),       
STRIKE_PRICE, OPTION_TYPE,      
OPENQTY = SUM(CASE WHEN SAUDA_DATE < @SDATE       
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END)      
     ELSE 0       
       END),      
OPENVALPRICE = SUM(CASE WHEN SAUDA_DATE < @SDATE       
          THEN (CASE WHEN SELL_BUY = 1     
       THEN TRADEQTY*NETRATE + SERVICE_TAX + INS_CHRG + BROKER_CHRG + SEBI_TAX + OTHER_CHRG + TURN_TAX    
       ELSE -(TRADEQTY*NETRATE - SERVICE_TAX - INS_CHRG - BROKER_CHRG - SEBI_TAX - OTHER_CHRG - TURN_TAX)    
         END)      
          ELSE 0       
                   END),      
TBUYQTY = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END)      
     ELSE 0       
       END),      
TBUYPRICE = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 1 THEN TRADEQTY*PRICE ELSE 0 END)      
     ELSE 0       
       END),      
TSELLQTY = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END)      
     ELSE 0       
       END),      
TSELLPRICE = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN (CASE WHEN SELL_BUY = 2 THEN TRADEQTY*PRICE ELSE 0 END)      
     ELSE 0       
       END),      
NETQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE -TRADEQTY END),      
MTMPRICE = CONVERT(NUMERIC(18,4),0),      
MTMPNL = CONVERT(NUMERIC(18,4),0),      
MTMSQOFF = CONVERT(NUMERIC(18,4),0),      
TOTALMTM = CONVERT(NUMERIC(18,4),0),      
TAXES = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
   THEN INS_CHRG + SEBI_TAX + TURN_TAX + OTHER_CHRG + BROKER_CHRG      
   ELSE 0       
     END),      
BROKSER = SUM(CASE WHEN SAUDA_DATE LIKE @SDATE + '%'      
     THEN NBROKAPP*TRADEQTY + NSERTAX      
     ELSE 0       
       END),      
NETMTM = CONVERT(NUMERIC(18,4),0),      
GROSSMARGIN = CONVERT(NUMERIC(18,4),0),      
NETMARGIN = CONVERT(NUMERIC(18,4),0)      
FROM NSEFO.DBO.FOSETTLEMENT      
WHERE SAUDA_DATE <= @SDATE + ' 23:59:59'      
AND EXPIRYDATE >= GETDATE()      
AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY      
GROUP BY PARTY_CODE, LEFT(INST_TYPE,3), SYMBOL, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE      
      
DELETE FROM #ARBREPORT       
WHERE OPENQTY = 0      
AND TBUYQTY = 0 AND TSELLQTY = 0       
    
/*    
UPDATE #ARBREPORT SET OPENVALPRICE = CL_RATE      
FROM CLOSING C      
WHERE #ARBREPORT.SCRIP_CD = C.SCRIP_CD      
AND #ARBREPORT.SERIES = C.SERIES      
AND SYSDATE = (SELECT MAX(SYSDATE) FROM CLOSING C1      
WHERE C1.SCRIP_CD = C.SCRIP_CD      
AND C1.SERIES = C.SERIES      
AND SYSDATE < @SDATE)    
AND EXCHANGE = 'NSE' AND SEGMENT = 'CASH'      
AND OPENQTY <> 0       
*/    
    
UPDATE #ARBREPORT SET MTMPRICE = CL_RATE      
FROM CLOSING C      
WHERE #ARBREPORT.SCRIP_CD = C.SCRIP_CD      
AND #ARBREPORT.SERIES = C.SERIES      
AND SYSDATE = (SELECT MAX(SYSDATE) FROM CLOSING C1      
WHERE C1.SCRIP_CD = C.SCRIP_CD      
AND C1.SERIES = C.SERIES      
AND SYSDATE <= @SDATE + ' 23:59:59')      
AND EXCHANGE = 'NSE' AND SEGMENT = 'CASH'      
      
/*    
UPDATE #ARBREPORT SET OPENVALPRICE = CL_RATE      
FROM BSEDB.DBO.CLOSING C      
WHERE #ARBREPORT.SCRIP_CD = C.SCRIP_CD      
AND #ARBREPORT.SERIES = C.SERIES      
AND SYSDATE = (SELECT MAX(SYSDATE) FROM BSEDB.DBO.CLOSING C1      
WHERE C1.SCRIP_CD = C.SCRIP_CD      
AND C1.SERIES = C.SERIES      
AND SYSDATE < @SDATE)      
AND EXCHANGE = 'BSE' AND SEGMENT = 'CASH'      
AND OPENQTY <> 0       
*/    
    
UPDATE #ARBREPORT SET MTMPRICE = CL_RATE      
FROM BSEDB.DBO.CLOSING C      
WHERE #ARBREPORT.SCRIP_CD = C.SCRIP_CD      
AND #ARBREPORT.SERIES = C.SERIES      
AND SYSDATE = (SELECT MAX(SYSDATE) FROM BSEDB.DBO.CLOSING C1      
WHERE C1.SCRIP_CD = C.SCRIP_CD      
AND C1.SERIES = C.SERIES      
AND SYSDATE <= @SDATE + ' 23:59:59')      
AND EXCHANGE = 'BSE' AND SEGMENT = 'CASH'      
      
UPDATE #ARBREPORT SET SCRIP_CD = M.SCRIP_CD, SERIES = M.SERIES      
FROM MULTIISIN M, BSEDB.DBO.MULTIISIN BM      
WHERE M.ISIN = BM.ISIN      
AND M.VALID = 1 AND BM.VALID = 1      
AND BM.SCRIP_CD = #ARBREPORT.SCRIP_CD      
AND BM.SERIES = #ARBREPORT.SERIES      
AND EXCHANGE = 'BSE' AND SEGMENT = 'CASH'      
      
UPDATE #ARBREPORT SET SCRIP_CD = LEFT(S2.SCRIP_CD,12)      
FROM BSEDB.DBO.SCRIP2 S2      
WHERE S2.BSECODE = #ARBREPORT.SCRIP_CD      
AND #ARBREPORT.EXCHANGE = 'BSE' AND SEGMENT = 'CASH'      
      
/*    
UPDATE #ARBREPORT SET OPENVALPRICE = CL_RATE      
FROM NSEFO.DBO.FOCLOSING C      
WHERE TRADE_DATE LIKE @LASTDATE + '%'      
AND LEFT(SEGMENT,3) = LEFT(INST_TYPE,3)      
AND #ARBREPORT.SCRIP_CD = C.SYMBOL      
AND #ARBREPORT.EXPIRYDATE = C.EXPIRYDATE      
AND #ARBREPORT.STRIKE_PRICE = C.STRIKE_PRICE      
AND #ARBREPORT.OPTION_TYPE = C.OPTION_TYPE      
AND EXCHANGE = 'NSE' AND SEGMENT <> 'CASH'      
AND OPENQTY <> 0     
*/      
      
UPDATE #ARBREPORT SET MTMPRICE = CL_RATE      
FROM NSEFO.DBO.FOCLOSING C      
WHERE TRADE_DATE LIKE @SDATE + '%'      
AND LEFT(SEGMENT,3) = LEFT(INST_TYPE,3)      
AND #ARBREPORT.SCRIP_CD = C.SYMBOL      
AND #ARBREPORT.EXPIRYDATE = C.EXPIRYDATE      
AND #ARBREPORT.STRIKE_PRICE = C.STRIKE_PRICE      
AND #ARBREPORT.OPTION_TYPE = C.OPTION_TYPE      
AND EXCHANGE = 'NSE' AND SEGMENT <> 'CASH'      
      
UPDATE #ARBREPORT SET TBUYPRICE = (CASE WHEN TBUYQTY > 0 THEN TBUYPRICE / TBUYQTY ELSE 0 END),      
TSELLPRICE = (CASE WHEN TSELLQTY > 0 THEN TSELLPRICE / TSELLQTY ELSE 0 END),    
OPENVALPRICE = ABS(round((CASE WHEN OPENQTY <> 0 THEN OPENVALPRICE / ABS(OPENQTY) ELSE 0 END),4))   
      
UPDATE #ARBREPORT SET MTMPNL = (Case When OpenQty <> 0 And NetQty <> 0 
         Then (OPENQTY * OPENVALPRICE) - (NETQTY * MTMPRICE)
		 When OpenQty <> 0 And NetQty = 0      
	     then (OPENQTY * OPENVALPRICE) + (TBUYQTY * TBUYPRICE) - (TSELLQTY * TSELLPRICE)
         Else (TBUYQTY * TBUYPRICE) - (TSELLQTY * TSELLPRICE) - (NETQTY * MTMPRICE)    
    End),    
MTMSQOFF = PRADNYA.DBO.fnMin(TBUYQTY, TSELLQTY) * TSELLPRICE - PRADNYA.DBO.FNMIN(TBUYQTY, TSELLQTY) * TBUYPRICE      
      
UPDATE #ARBREPORT SET TOTALMTM = MTMPNL - MTMSQOFF,      
NETMTM = MTMPNL - MTMSQOFF + TAXES + BROKSER      
      
UPDATE #ARBREPORT SET GROSSMARGIN = TOTALMARGIN,      
NETMARGIN = MTOM FROM       
(      
 SELECT PARTY_CODE, TOTALMARGIN = SUM(TOTALMARGIN), MTOM = SUM(MTOM)      
 FROM NSEFO.DBO.FOMARGINNEW      
 WHERE MDATE LIKE @SDATE + '%'      
 AND PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY      
 GROUP BY PARTY_CODE      
) A      
WHERE #ARBREPORT.PARTY_CODE = A.PARTY_CODE      
      
SELECT A.PARTY_CODE,LONG_NAME,EXCHANGE,      
SYMBOL = LTRIM(RTRIM(SCRIP_CD)) + ' ' + LTRIM(RTRIM(SERIES)) + ' ' +       
 (CASE WHEN EXPIRYDATE NOT LIKE 'JAN  1 1900%' THEN LEFT(EXPIRYDATE, 11) ELSE '' END)      
 + ' ' + (CASE WHEN STRIKE_PRICE <> 0 THEN CONVERT(VARCHAR,STRIKE_PRICE) ELSE '' END)      
 + ' ' + (CASE WHEN OPTION_TYPE <> '' THEN OPTION_TYPE ELSE '' END),      
EXPIRYDATE, segment,      
OPENQTY,OPENVALPRICE,TBUYQTY,TBUYPRICE,TSELLQTY,TSELLPRICE,NETQTY,MTMPRICE,REALZMTM=MTMPNL,      
MTMSQOFF,TOTALMTM,TAXES,BROKSER,totalcost=taxes+brokser,NETMTM,GROSSMARGIN=-GROSSMARGIN,NETMARGIN=-NETMARGIN,SCRIP_CD       
FROM #ARBREPORT A, CLIENT_DETAILS C      
WHERE A.PARTY_CODE = C.CL_CODE      
ORDER BY A.PARTY_CODE, SCRIP_CD, SERIES, EXCHANGE, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE

GO
