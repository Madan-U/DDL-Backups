-- Object: PROCEDURE dbo.CBO_ADDEDITPARTYMAPPING
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE   PROC CBO_ADDEDITPARTYMAPPING
	@OLDPARTY_CODE VARCHAR(12),
	@NEWPARTY_CODE VARCHAR(12),
	@FLAG VARCHAR(1),
	@STATUSID VARCHAR(25),
	@STATUSNAME VARCHAR(25)
AS
	IF @STATUSID <> 'BROKER'
		BEGIN
			RAISERROR ('This Procedure is accessible to Broker', 16, 1)
			RETURN
		END
	IF @FLAG <> 'A' AND @FLAG <> 'E' AND @FLAG <> 'D'
		BEGIN
			RAISERROR ('Add/Edit/Delete Flag Not Set Properly', 16, 1)
			RETURN
		END
	IF @OLDPARTY_CODE = '' 
		BEGIN
			RAISERROR ('Old Party Code Cannot be Blank...', 16, 1)
			RETURN
		END
	IF @FLAG = 'A'
		BEGIN
			INSERT INTO
				PARTYMAPPING
				(
					OLDPARTY_CODE,NEWPARTY_CODE
				)
			VALUES
				(
					@OLDPARTY_CODE,@NEWPARTY_CODE
				)
		END
	ELSE IF @FLAG = 'E'
		BEGIN
			UPDATE
				PARTYMAPPING
			SET
				NEWPARTY_CODE = @NEWPARTY_CODE
			WHERE
				OLDPARTY_CODE = @OLDPARTY_CODE
		END
	ELSE IF @FLAG = 'D'
		BEGIN
			DELETE FROM
				PARTYMAPPING
			WHERE
				OLDPARTY_CODE = @OLDPARTY_CODE
		END

GO
