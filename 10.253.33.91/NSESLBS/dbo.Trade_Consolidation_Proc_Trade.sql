				SELECT I.ORDER_NO, SUM(I.TRADEQTY), SUM(I.TRADEQTY) FROM TRADE I, #SELECTED S
				WHERE
					I.ORDER_NO = S.F1
					AND I.PARTY_CODE = @FROMPARTY
					AND I.SCRIP_CD = @SCRIP_CD
					AND I.SERIES = @SERIES
					AND I.SELL_BUY = @SELL_BUY
