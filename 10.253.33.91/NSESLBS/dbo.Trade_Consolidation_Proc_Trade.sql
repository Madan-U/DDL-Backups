						IF @LEVEL = 2
							INSERT INTO #DISTRIBUTED (FROMPARTY, TOPARTY, ORGQTY, TRFQTY, MKTRATE, ORDER_NO, TRADE_NO) SELECT @FPARTY, @TPARTY, @ORGQTY, @TRFQTY, @MKTRATE, '%', '%'
