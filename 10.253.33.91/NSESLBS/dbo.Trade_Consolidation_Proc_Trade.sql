				IF @TQTY = @@ORGQTY
					BEGIN*/
						SET @SQL = "EXEC NSE_TradeCursor '" + CONVERT(VARCHAR(11), CONVERT(DATETIME, @SAUDA_DATE, 103), 109) + "', "
