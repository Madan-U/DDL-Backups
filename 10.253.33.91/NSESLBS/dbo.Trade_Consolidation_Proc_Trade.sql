						SET @SQL = @SQL + CONVERT(VARCHAR, @SELL_BUY) + ", "
						SET @SQL = @SQL + CONVERT(VARCHAR, @@MKTRATE) + ", '%', '" + @TERMINALID + "', '%', "
						SET @SQL = @SQL + "'" + @@ORDER_NO + "', '" + @@TRADE_NO + "', " + CONVERT(VARCHAR, @@TRFQTY) + ", "
						SET @SQL = @SQL + "'" + @STATUSNAME + "', '" + @FROMWHERE + "', "
