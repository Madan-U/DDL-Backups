CREATE  PROC GETCLIENTBROKSCHEME
	@PARTYCODE VARCHAR(10),
	@TABLENO INT,
	@SAUDA_DATE VARCHAR(11)

AS
/*
	Exec GETCLIENTBROKSCHEME '0A141', 222, '28/05/2007' 
	EXEC GETCLIENTBROKSCHEME '0A141', 101, 'MAY 28 2007'
*/

IF LEN(@SAUDA_DATE) = 10 AND CHARINDEX('/', @SAUDA_DATE) > 0
	BEGIN
		SET @SAUDA_DATE = CONVERT(VARCHAR(11), CONVERT(DATETIME, @SAUDA_DATE, 103), 109)
	END
/*
DECLARE @TABLE_NO INT, @SCHEME INT, @CL_TYPE VARCHAR(3)
SELECT @CL_TYPE = CL_TYPE FROM CLIENT1 C1, CLIENT2 C2 WHERE C1.CL_CODE = C2.CL_CODE AND C2.PARTY_CODE = @PARTYCODE
SELECT @SCHEME = BROKSCHEME FROM CLIENTBROK_SCHEME WHERE PARTY_CODE = @PARTYCODE AND SCHEME_TYPE = 'TRD' AND TRADE_TYPE = (CASE WHEN @CL_TYPE = 'INS' THEN 'INS' ELSE 'NRM' END) AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE

IF (SELECT COUNT(1) FROM BROKTABLE WHERE TABLE_NO = @TABLENO AND TRD_DEL = (CASE WHEN @SCHEME = 0 THEN 'T' ELSE 'F' END)) <= 0
	BEGIN
		SELECT '0'
	END
ELSE
	BEGIN
		SELECT '1'
	END
	SELECT CHARINDEX('10/12/2007', '/')
*/
SELECT CNT = ISNULL(COUNT(1),0) FROM CLIENT1 C1, CLIENT2 C2, CLIENTBROK_SCHEME C, BROKTABLE B
WHERE C1.CL_CODE = C2.CL_CODE AND C2.PARTY_CODE = @PARTYCODE
AND SCHEME_TYPE = 'TRD' 
AND TRADE_TYPE = (CASE WHEN CL_TYPE = 'INS' 
		       THEN 'INS' 
		       ELSE 'NRM' 
	          END) 
AND @SAUDA_DATE BETWEEN FROM_DATE AND TO_DATE
AND B.TABLE_NO = @TABLENO
AND B.LINE_NO = 1
AND BROKSCHEME = (CASE WHEN TRD_DEL = 'T' THEN 0 ELSE 1 END)

GO
