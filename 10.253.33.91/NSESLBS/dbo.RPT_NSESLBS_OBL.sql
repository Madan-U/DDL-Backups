-- Object: PROCEDURE dbo.RPT_NSESLBS_OBL
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


create PROC [dbo].[RPT_NSESLBS_OBL]      
(      
 @SETT_NO VARCHAR(7),      
 @SETT_TYPE VARCHAR(2)      
)      
      
AS      
    
DECLARE @EXPDATE VARCHAR(11)    
    
SELECT @EXPDATE = LEFT(SEC_PAYIN,11) FROM SETT_MST WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE    
    
SELECT SETT_NO, SETT_TYPE, PARTY_CODE, PARTY_NAME=CONVERT(VARCHAR(50),''), UCC_CODE, SCRIP_CD,       
EXCHGBUYQTY=SUM(CASE WHEN REC_DEL_FLAG = 'R' THEN TRADEQTY ELSE 0 END),      
OURBUYQTY=0,      
EXCHGSELLQTY=SUM(CASE WHEN REC_DEL_FLAG = 'D' THEN TRADEQTY ELSE 0 END),      
OURSELLQTY=0      
INTO #OBL FROM SLBS_REVTRADE      
WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE      
GROUP BY SETT_NO, SETT_TYPE, PARTY_CODE, UCC_CODE, SCRIP_CD      
    
SELECT M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, SERIES='EQ', PARTY_CODE=(CASE WHEN C1.PAN_GIR_NO = OWNER.PANNO THEN MEMBERCODE ELSE S.PARTY_CODE END),             
 TRADEQTY=ABS(SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END)),             
 INOUT = (CASE WHEN SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) < 0 THEN 'O' ELSE 'I' END),             
 BRANCH_CD = 'HO', PARTIPANTCODE = MEMBERCODE            
 INTO #SETT     
 FROM SETTLEMENT S, TRD_SETT_POS T, SETT_MST M, OWNER, CLIENT1 C1, CLIENT2 C2            
 WHERE S.SAUDA_DATE = T.SAUDA_DATE            
 AND S.ORDER_NO = T.ORDER_NO            
 AND S.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(T.TRADE_NO)            
 AND S.SCRIP_CD = T.SCRIP_CD            
 AND S.SCRIP_CD = T.SCRIP_CD            
 AND S.SERIES = T.SERIES            
 AND M.SETT_TYPE = 'P'            
 AND LEFT(M.SEC_PAYIN, 11) = LEFT(CONVERT(DATETIME,EXPIRYDATE),11)            
 AND LEFT(CONVERT(DATETIME,EXPIRYDATE),11) = @EXPDATE AND T.SELL_BUY IN (1,4)          
 AND RIGHT(M.SETT_NO,3) >= '500'
 AND C1.CL_CODE = C2.CL_CODE 
 AND C2.PARTY_CODE = S.PARTY_CODE          
 GROUP BY M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, S.SERIES, (CASE WHEN C1.PAN_GIR_NO = OWNER.PANNO THEN MEMBERCODE ELSE S.PARTY_CODE END), MEMBERCODE,
 PAN_GIR_NO, PANNO            
 HAVING SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) <> 0     
    
 INSERT INTO #SETT    
 SELECT M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, SERIES='EQ', PARTY_CODE=(CASE WHEN C1.PAN_GIR_NO = OWNER.PANNO THEN MEMBERCODE ELSE S.PARTY_CODE END),             
 TRADEQTY=ABS(SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END)),             
 INOUT = (CASE WHEN SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) < 0 THEN 'O' ELSE 'I' END),             
 BRANCH_CD = 'HO', PARTIPANTCODE = MEMBERCODE            
 FROM SETTLEMENT S, TRD_SETT_POS T, SETT_MST M, OWNER, CLIENT1 C1, CLIENT2 C2            
 WHERE S.SAUDA_DATE = T.SAUDA_DATE            
 AND S.ORDER_NO = T.ORDER_NO            
 AND S.TRADE_NO = PRADNYA.DBO.REPLACETRADENO(T.TRADE_NO)            
 AND S.SCRIP_CD = T.SCRIP_CD            
 AND S.SCRIP_CD = T.SCRIP_CD            
 AND S.SERIES = T.SERIES            
 AND M.SETT_TYPE = 'P'            
 AND LEFT(M.SEC_PAYIN, 11) = LEFT(CONVERT(DATETIME,EXPIRYDATE),11)            
 AND LEFT(CONVERT(DATETIME,EXPIRYDATE),11) = @EXPDATE AND T.SELL_BUY IN (2,3)          
 AND RIGHT(M.SETT_NO,3) >= '500'          
  AND C1.CL_CODE = C2.CL_CODE 
 AND C2.PARTY_CODE = S.PARTY_CODE 
 GROUP BY M.SETT_NO, M.SETT_TYPE, S.SCRIP_CD, S.SERIES, (CASE WHEN C1.PAN_GIR_NO = OWNER.PANNO THEN MEMBERCODE ELSE S.PARTY_CODE END), MEMBERCODE,
 PAN_GIR_NO, PANNO            
 HAVING SUM(CASE WHEN S.SELL_BUY IN (1,3) THEN S.TRADEQTY ELSE -S.TRADEQTY END) <> 0    
      
UPDATE #OBL SET       
OURBUYQTY = (CASE WHEN INOUT = 'O' THEN QTY ELSE 0 END),      
OURSELLQTY = (CASE WHEN INOUT = 'I' THEN QTY ELSE 0 END)      
FROM (SELECT SETT_NO,SETT_TYPE,SCRIP_CD,SERIES,PARTY_CODE,QTY=TRADEQTY,INOUT,BRANCH_CD,PARTIPANTCODE FROM #SETT) D      
WHERE D.SETT_NO = @SETT_NO AND D.SETT_TYPE = @SETT_TYPE      
AND D.SCRIP_CD = #OBL.SCRIP_CD      
AND D.PARTY_CODE = #OBL.PARTY_CODE      
      
INSERT INTO #OBL      
SELECT SETT_NO, SETT_TYPE, PARTY_CODE, PARTY_NAME='', UCC_CODE=PARTY_CODE, SCRIP_CD,       
EXCHGBUYQTY=0,      
OURBUYQTY=(CASE WHEN INOUT = 'O' THEN QTY ELSE 0 END),      
EXCHGSELLQTY=0,      
OURSELLQTY = (CASE WHEN INOUT = 'I' THEN QTY ELSE 0 END)      
FROM (SELECT SETT_NO,SETT_TYPE,SCRIP_CD,SERIES,PARTY_CODE,QTY=TRADEQTY,INOUT,BRANCH_CD,PARTIPANTCODE FROM #SETT) D      
WHERE D.SETT_NO = @SETT_NO AND D.SETT_TYPE = @SETT_TYPE      
AND D.SCRIP_CD NOT IN (SELECT SCRIP_CD FROM #OBL      
    WHERE  #OBL.SETT_NO = @SETT_NO AND #OBL.SETT_TYPE = @SETT_TYPE      
AND D.SCRIP_CD = #OBL.SCRIP_CD      
AND D.PARTY_CODE = #OBL.PARTY_CODE)      
      
UPDATE #OBL SET UCC_CODE = PAN_GIR_NO      
FROM CLIENT1 WHERE CL_CODE = PARTY_CODE      
AND UCC_CODE = ''      
      
UPDATE #OBL SET PARTY_NAME = LEFT(LONG_NAME,50)      
FROM CLIENT1 WHERE CL_CODE = PARTY_CODE      

UPDATE #OBL SET PARTY_NAME = COMPANY
FROM OWNER WHERE MEMBERCODE = PARTY_CODE 
      
SELECT * FROM #OBL      
ORDER BY SCRIP_CD, PARTY_CODE

GO
