-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------



CREATE  PROC CBO_GETPOBANK
	@BANKID NUMERIC(18) = 0,
	@STATUSID VARCHAR(25) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'
AS
	IF @STATUSID <> 'BROKER'
		BEGIN
			RAISERROR ('This Procedure is accessible to Broker', 16, 1)
			RETURN
		END
	SELECT
		BANKID,
		BANK_NAME,
		BRANCH_NAME,
		ADDRESS1 = ISNULL(ADDRESS1, ''),
		ADDRESS2 = ISNULL(ADDRESS2, ''),
		CITY = ISNULL(CITY, ''),
		STATE = ISNULL(STATE, ''),
		NATION = ISNULL(NATION, ''),
		ZIP = ISNULL(ZIP, ''),
		PHONE1 = ISNULL(PHONE1, ''),
		PHONE2 = ISNULL(PHONE2, ''),
		FAX = ISNULL(FAX, ''),
		EMAIL = ISNULL(EMAIL, '')
	FROM
		POBANK
	WHERE
		BANKID = CASE WHEN @BANKID > 0 THEN @BANKID ELSE BANKID END
	ORDER BY
		BANKID

GO
