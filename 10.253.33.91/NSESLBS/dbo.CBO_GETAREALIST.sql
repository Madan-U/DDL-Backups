-- Object: PROCEDURE dbo.CBO_GETAREALIST
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE PROC CBO_GETAREALIST
	@AREA_CODE VARCHAR(20) = '',
	@STATUSID VARCHAR(25) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'

AS	
/*
	CBO_GETAREALIST '', 'BRANCH', '1234563'
*/
	IF @STATUSID = 'BROKER'
		BEGIN
			SELECT DISTINCT
				A.AREACODE,
				DESCRIPTION = ISNULL(A.DESCRIPTION, '')/*,
				A.BRANCH_CODE,
				DUMMY1 = ISNULL(A.DUMMY1, ''),
				DUMMY2 = ISNULL(A.DUMMY2, '')*/
			FROM
				AREA A
			WHERE
				A.AREACODE = CASE WHEN LEN(LTRIM(RTRIM(@AREA_CODE))) > 0 AND @AREA_CODE <> '%' THEN @AREA_CODE ELSE A.AREACODE END
			ORDER BY
				A.AREACODE/*,
				A.BRANCH_CODE*/
		END
	ELSE IF @STATUSID = 'BRANCH'
		BEGIN
			SELECT DISTINCT
				A.AREACODE,
				DESCRIPTION = ISNULL(A.DESCRIPTION, '')/*,
				A.BRANCH_CODE,
				DUMMY1 = ISNULL(A.DUMMY1, ''),
				DUMMY2 = ISNULL(A.DUMMY2, '')*/
			FROM
				AREA A
			WHERE
				A.AREACODE = CASE WHEN LEN(LTRIM(RTRIM(@AREA_CODE))) > 0 AND @AREA_CODE <> '%' THEN @AREA_CODE ELSE A.AREACODE END
				AND A.BRANCH_CODE = @STATUSNAME
			ORDER BY
				A.AREACODE/*,
				A.BRANCH_CODE*/
		END
	ELSE IF @STATUSID = 'REGION'
		BEGIN
			SELECT DISTINCT
				A.AREACODE,
				DESCRIPTION = ISNULL(A.DESCRIPTION, '')/*,
				A.BRANCH_CODE,
				DUMMY1 = ISNULL(A.DUMMY1, ''),
				DUMMY2 = ISNULL(A.DUMMY2, '')*/
			FROM
				AREA A,
				REGION R
			WHERE
				A.BRANCH_CODE = R.BRANCH_CODE
				AND A.AREACODE = CASE WHEN LEN(LTRIM(RTRIM(@AREA_CODE))) > 0 AND @AREA_CODE <> '%' THEN @AREA_CODE ELSE A.AREACODE END
				AND R.REGIONCODE = @STATUSNAME
			ORDER BY
				A.AREACODE/*,
				A.BRANCH_CODE*/
		END
	ELSE IF @STATUSID = 'AREA'
		BEGIN
			SELECT DISTINCT
				A.AREACODE,
				DESCRIPTION = ISNULL(A.DESCRIPTION, '')/*,
				A.BRANCH_CODE,
				DUMMY1 = ISNULL(A.DUMMY1, ''),
				DUMMY2 = ISNULL(A.DUMMY2, '')*/
			FROM
				AREA A
			WHERE
				A.AREACODE = CASE WHEN LEN(LTRIM(RTRIM(@AREA_CODE))) > 0 AND @AREA_CODE <> '%' THEN @AREA_CODE ELSE A.AREACODE END
				AND A.AREACODE = @STATUSNAME
			ORDER BY
				A.AREACODE/*,
				A.BRANCH_CODE*/
		END
	ELSE IF @STATUSID = 'SUBBROKER'
		BEGIN
			SELECT DISTINCT
				A.AREACODE,
				DESCRIPTION = ISNULL(A.DESCRIPTION, '')/*,
				A.BRANCH_CODE,
				DUMMY1 = ISNULL(A.DUMMY1, ''),
				DUMMY2 = ISNULL(A.DUMMY2, '')*/
			FROM
				AREA A,
				SUBBROKERS S
			WHERE
				A.BRANCH_CODE = S.BRANCH_CODE
				AND A.AREACODE = CASE WHEN LEN(LTRIM(RTRIM(@AREA_CODE))) > 0 AND @AREA_CODE <> '%' THEN @AREA_CODE ELSE A.AREACODE END
				AND S.SUB_BROKER = @STATUSNAME
			ORDER BY
				A.AREACODE/*,
				A.BRANCH_CODE*/
		END
	ELSE IF @STATUSID = 'TRADER'
		BEGIN
			SELECT DISTINCT
				A.AREACODE,
				DESCRIPTION = ISNULL(A.DESCRIPTION, '')/*,
				A.BRANCH_CODE,
				DUMMY1 = ISNULL(A.DUMMY1, ''),
				DUMMY2 = ISNULL(A.DUMMY2, '')*/
			FROM
				AREA A,
				BRANCHES B
			WHERE
				A.BRANCH_CODE = B.BRANCH_CD
				AND A.AREACODE = CASE WHEN LEN(LTRIM(RTRIM(@AREA_CODE))) > 0 AND @AREA_CODE <> '%' THEN @AREA_CODE ELSE A.AREACODE END
				AND B.SHORT_NAME = @STATUSNAME
			ORDER BY
				A.AREACODE/*,
				A.BRANCH_CODE*/
		END
	ELSE IF @STATUSID = 'FAMILY'
		BEGIN
			SELECT DISTINCT
				A.AREACODE,
				DESCRIPTION = ISNULL(A.DESCRIPTION, '')/*,
				A.BRANCH_CODE,
				DUMMY1 = ISNULL(A.DUMMY1, ''),
				DUMMY2 = ISNULL(A.DUMMY2, '')*/
			FROM
				AREA A,
				CLIENT1 C1
			WHERE
				A.AREACODE = C1.AREA
				AND A.AREACODE = CASE WHEN LEN(LTRIM(RTRIM(@AREA_CODE))) > 0 AND @AREA_CODE <> '%' THEN @AREA_CODE ELSE A.AREACODE END
				AND C1.FAMILY = @STATUSNAME
			ORDER BY
				A.AREACODE/*,
				A.BRANCH_CODE*/
		END
	ELSE IF @STATUSID = 'CLIENT'
		BEGIN
			SELECT
				C2.PARTY_CODE,
				C1.AREA
			INTO
				#CLIENT2
			FROM
				CLIENT1 C1,
				CLIENT2 C2
			WHERE
				C1.CL_CODE = C2.CL_CODE
				AND C2.PARTY_CODE = @STATUSNAME
			SELECT DISTINCT
				A.AREACODE,
				DESCRIPTION = ISNULL(A.DESCRIPTION, '')/*,
				A.BRANCH_CODE,
				DUMMY1 = ISNULL(A.DUMMY1, ''),
				DUMMY2 = ISNULL(A.DUMMY2, '')*/
			FROM
				AREA A,
				#CLIENT2 C2
			WHERE
				A.AREACODE = C2.AREA
				AND A.AREACODE = CASE WHEN LEN(LTRIM(RTRIM(@AREA_CODE))) > 0 AND @AREA_CODE <> '%' THEN @AREA_CODE ELSE A.AREACODE END
			ORDER BY
				A.AREACODE/*,
				A.BRANCH_CODE*/
		END
	ELSE
		SELECT
			AREACODE = '',
			DESCRIPTION = '',
			BRANCH_CODE = '',
			DUMMY1 = '',
			DUMMY2 = ''
		FROM
			AREA
		WHERE
			1 = 0

GO
