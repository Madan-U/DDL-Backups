CREATE PROC RPT_SSJFILEFORMAT
	(
	@SETT_NO VARCHAR(10),
	@SETT_TYPE VARCHAR(3),
	@FROMPARTY VARCHAR(15),
	@TOPARTY VARCHAR(15),
	@SETTFLAG VARCHAR(1),
	@REPORTTYPE VARCHAR(15)
	)

	AS

	--EXEC RPT_NETPOSITIONFORMAT ''
	--EXEC RPT_SSJFILEFORMAT 'OCT 16 2007','D','','ZZZZZZZZZ','D','TRADEFILE' 
	--EXEC RPT_SSJFILEFORMAT '2007004','D','','ZZZZZZZZZ','S','TRADEFILE' 

	IF @SETTFLAG = 'D'
	BEGIN
		SELECT @SETT_NO = SETT_NO 
		FROM SETT_MST 
		WHERE START_DATE LIKE @SETT_NO + '%' 
		AND SETT_TYPE = @SETT_TYPE
	END
	
	PRINT @SETT_NO
	PRINT @REPORTTYPE

	IF (@REPORTTYPE = 'NETPOSITION')
	BEGIN
		PRINT @REPORTTYPE
		SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
		SET NOCOUNT ON
		SELECT 
			SETT_NO 	= SETT_NO,
			SETT_TYPE 	= SETT_TYPE,
			CLIENT_CODE 	= PARTY_CODE,
			CLIENT_NAME 	= LONG_NAME,
			SCRIP_CODE 	= S.SCRIP_CD,
			SCRIPT_CODE 	= S.SCRIP_CD,
			ISINNO 		= ISNULL(ISIN,''),
			BUYQTY 		= SUM(CASE WHEN SELL_BUY=1 THEN TRADEQTY ELSE 0 END),
			SELLQTY		= SUM(CASE WHEN SELL_BUY=2 THEN TRADEQTY ELSE 0 END)
		FROM
			SETTLEMENT S
				LEFT OUTER JOIN MULTIISIN M 
				ON (M.SCRIP_CD = S.SCRIP_CD AND VALID = 1),
			CLIENT1 C1
		WHERE
			S.PARTY_CODE 	= C1.CL_CODE
			AND SETT_NO 	= @SETT_NO
			AND SETT_TYPE 	= @SETT_TYPE
			AND PARTY_CODE >= @FROMPARTY
			AND PARTY_CODE <= @TOPARTY
		
		GROUP BY
			SETT_NO,
			SETT_TYPE,
			PARTY_CODE,
			LONG_NAME,
			S.SCRIP_CD,
			ISIN
	
		------
		UNION
		------
	
		SELECT 
			SETT_NO 	= SETT_NO,
			SETT_TYPE 	= SETT_TYPE,
			CLIENT_CODE 	= PARTY_CODE,
			CLIENT_NAME 	= LONG_NAME,
			SCRIP_CODE 	= S.SCRIP_CD,
			SCRIPT_CODE 	= S.SCRIP_CD,
			ISINNO 		= ISNULL(ISIN,''),
			BUYQTY 		= SUM(CASE WHEN SELL_BUY=1 THEN TRADEQTY ELSE 0 END),
			SELLQTY		= SUM(CASE WHEN SELL_BUY=2 THEN TRADEQTY ELSE 0 END)
		FROM
			ISETTLEMENT S
				LEFT OUTER JOIN MULTIISIN M 
				ON (M.SCRIP_CD = S.SCRIP_CD AND VALID = 1),
			CLIENT1 C1
		WHERE
			S.PARTY_CODE 	= C1.CL_CODE
			AND SETT_NO 	= @SETT_NO
			AND SETT_TYPE 	= @SETT_TYPE
			AND PARTY_CODE >= @FROMPARTY
			AND PARTY_CODE <= @TOPARTY
		
		GROUP BY
			SETT_NO,
			SETT_TYPE,
			PARTY_CODE,
			LONG_NAME,
			S.SCRIP_CD,
			ISIN
	
		ORDER BY
			SETT_NO,
			PARTY_CODE
	END

	IF @REPORTTYPE = 'TRADEFILE'
	BEGIN
		SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
		SET NOCOUNT ON
		SELECT 
			PARTY_CODE,
			PARTY_CODE,
			TRADE_DATE = LEFT(SAUDA_DATE,11),
			SETT_NO,
			SETT_TYPE,
			SCRIP_CD,
			SCRIP_CD,
			MARKETRATE,
			N_NETRATE,
			BUYQTY = (CASE WHEN SELL_BUY=1 THEN TRADEQTY ELSE 0 END),
			BUYAMT = (CASE WHEN SELL_BUY=1 THEN (MARKETRATE*TRADEQTY) ELSE 0 END),
			SELQTY = (CASE WHEN SELL_BUY=2 THEN TRADEQTY ELSE 0 END),
			SELAMT = (CASE WHEN SELL_BUY=2 THEN (N_NETRATE*TRADEQTY) ELSE 0 END),
			USER_ID,
			SERTAX = NSERTAX,
			BROKERAGE = NBROKAPP
		FROM
			SETTLEMENT S (NOLOCK),
			CLIENT1 C1 (NOLOCK)
		WHERE
			S.PARTY_CODE 	= C1.CL_CODE
			AND SETT_NO 	= @SETT_NO
			AND SETT_TYPE 	= @SETT_TYPE
			AND PARTY_CODE >= @FROMPARTY
			AND PARTY_CODE <= @TOPARTY

		------
		UNION
		------

		SELECT 
			PARTY_CODE,
			PARTY_CODE,
			TRADE_DATE = LEFT(SAUDA_DATE,11),
			SETT_NO,
			SETT_TYPE,
			SCRIP_CD,
			SCRIP_CD,
			MARKETRATE,
			N_NETRATE,
			BUYQTY = (CASE WHEN SELL_BUY=1 THEN TRADEQTY ELSE 0 END),
			BUYAMT = (CASE WHEN SELL_BUY=1 THEN (MARKETRATE*TRADEQTY) ELSE 0 END),
			SELQTY = (CASE WHEN SELL_BUY=2 THEN TRADEQTY ELSE 0 END),
			SELAMT = (CASE WHEN SELL_BUY=2 THEN (N_NETRATE*TRADEQTY) ELSE 0 END),
			USER_ID,
			SERTAX = NSERTAX,
			BROKERAGE = NBROKAPP
		FROM
			ISETTLEMENT S (NOLOCK),
			CLIENT1 C1 (NOLOCK)
		WHERE
			S.PARTY_CODE 	= C1.CL_CODE
			AND SETT_NO 	= @SETT_NO
			AND SETT_TYPE 	= @SETT_TYPE
			AND PARTY_CODE >= @FROMPARTY
			AND PARTY_CODE <= @TOPARTY
		ORDER BY
			SETT_NO,
			PARTY_CODE

	END

GO
