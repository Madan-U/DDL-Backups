-- Object: PROCEDURE dbo.InsDelBTSTAdjustment
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE Proc InsDelBTSTAdjustment (@N_Sett_No Varchar(7), @Sett_Type Varchar(2))
AS

DECLARE @SETT_NO VARCHAR(7)

SELECT TOP 1 @SETT_NO = SETT_NO FROM SETT_MST
WHERE START_DATE = 
(SELECT MAX(START_DATE) FROM SETT_MST
WHERE START_DATE < (SELECT START_DATE FROM SETT_MST
WHERE SETT_NO = @N_Sett_No AND SETT_TYPE = @SETT_TYPE)
AND SETT_TYPE = @SETT_TYPE) AND SETT_TYPE = @SETT_TYPE

DELETE FROM DELTRANS
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = @SETT_TYPE
AND FILLER2 = 1 
AND FILLER1 = 'BTST'+@N_Sett_No

DELETE FROM DELTRANS
WHERE SETT_NO = @N_SETT_NO
AND SETT_TYPE = @SETT_TYPE
AND FILLER2 = 1 
AND FILLER1 = 'BTST'+@N_Sett_No

SELECT *, PAYQTY=0, ISIN = CONVERT(VARCHAR(12), ''), ADJUSTQTY = 0 INTO #DELPAYOUT
FROM DELIVERYCLT D
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = @SETT_TYPE
AND EXISTS (SELECT SCRIP_CD FROM TBLBTST T WHERE T.SCRIP_CD = D.SCRIP_CD)
AND INOUT = 'O' AND SERIES = 'EQ'

SELECT D.*, RECQTY=0 INTO #DELPAYIN FROM DELIVERYCLT D
WHERE SETT_NO = @N_SETT_NO
AND SETT_TYPE = @SETT_TYPE
AND EXISTS (SELECT SCRIP_CD FROM TBLBTST T WHERE T.SCRIP_CD = D.SCRIP_CD)
AND EXISTS (SELECT DISTINCT PARTY_CODE FROM #DELPAYOUT C 
            WHERE C.PARTY_CODE = D.PARTY_CODE AND C.SCRIP_CD = D.SCRIP_CD AND C.SERIES = D.SERIES )
AND INOUT = 'I' 

DELETE FROM #DELPAYOUT WHERE NOT EXISTS 
(SELECT PARTY_CODE FROM #DELPAYIN WHERE #DELPAYIN.PARTY_CODE = #DELPAYOUT.PARTY_CODE
AND #DELPAYIN.SCRIP_CD = #DELPAYOUT.SCRIP_CD
AND #DELPAYIN.SERIES = #DELPAYOUT.SERIES)

UPDATE #DELPAYOUT SET PAYQTY = D.QTY 
FROM (SELECT SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES, QTY=SUM(QTY)
FROM DELTRANS WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = @SETT_TYPE AND FILLER2 = 1 
AND DRCR = 'D' AND TRTYPE <> 906 AND SHARETYPE <> 'AUCTION'
GROUP BY SETT_NO, SETT_TYPE, PARTY_CODE, SCRIP_CD, SERIES) D
WHERE D.SETT_NO = #DELPAYOUT.SETT_NO
AND D.SETT_TYPE = #DELPAYOUT.SETT_TYPE
AND D.PARTY_CODE = #DELPAYOUT.PARTY_CODE
AND D.SCRIP_CD = #DELPAYOUT.SCRIP_CD
AND D.SERIES = #DELPAYOUT.SERIES

DELETE FROM #DELPAYOUT
WHERE QTY = PAYQTY

UPDATE #DELPAYIN SET RECQTY = D.QTY 
FROM (SELECT T.SETT_NO, T.SETT_TYPE, PARTY_CODE, SCRIP_CD, T.SERIES, QTY=SUM(QTY)
FROM DELTRANS T WHERE T.SETT_NO = @N_SETT_NO
AND T.SETT_TYPE = @SETT_TYPE AND FILLER2 = 1 
AND DRCR = 'C' AND TRTYPE <> 906 AND SHARETYPE <> 'AUCTION'
GROUP BY T.SETT_NO, T.SETT_TYPE, PARTY_CODE, SCRIP_CD, T.SERIES) D
WHERE D.SETT_NO = #DELPAYIN.SETT_NO
AND D.SETT_TYPE = #DELPAYIN.SETT_TYPE
AND D.PARTY_CODE = #DELPAYIN.PARTY_CODE
AND D.SCRIP_CD = #DELPAYIN.SCRIP_CD
AND D.SERIES = #DELPAYIN.SERIES

DELETE FROM #DELPAYIN
WHERE QTY = RECQTY

UPDATE #DELPAYOUT SET ISIN = M.ISIN
FROM MULTIISIN M
WHERE M.SCRIP_CD = #DELPAYOUT.SCRIP_CD
AND M.SERIES = #DELPAYOUT.SERIES
AND VALID = 1 

UPDATE #DELPAYOUT SET ADJUSTQTY = (CASE WHEN (#DELPAYOUT.QTY - #DELPAYOUT.PAYQTY) >= (R.QTY - R.RECQTY)
					THEN (R.QTY - R.RECQTY)
					ELSE (#DELPAYOUT.QTY - #DELPAYOUT.PAYQTY)
				   END)
FROM #DELPAYIN R
WHERE #DELPAYOUT.PARTY_CODE = R.PARTY_CODE
AND #DELPAYOUT.SCRIP_CD = R.SCRIP_CD
AND #DELPAYOUT.SERIES = R.SERIES

INSERT INTO DELTRANS
SELECT D.Sett_No,D.Sett_Type,110,0,904,Party_Code,
Scrip_Cd,D.Series,ADJUSTQTY,'0','0',ISIN,'0','ADJUSTMENT TO ' + @N_SETT_NO,
'ADJUSTMENT TO ' + @N_SETT_NO,'D','D',ADJUSTQTY,'','','','HO',
MEMBERCODE,'0','0','','','DEMAT',
left(sec_payin,11),'BTST'+@N_Sett_No,1,'0',DP.dptype,DP.dpid,DPCLTNO,
'',''
FROM #DELPAYOUT D, SETT_MST S, OWNER, DELIVERYDP DP
WHERE D.SETT_NO = S.SETT_NO
AND D.SETT_TYPE = S.SETT_TYPE
AND DP.DESCRIPTION LIKE '%POOL%'
AND DP.DPTYPE = 'NSDL'
AND ADJUSTQTY > 0 

INSERT INTO DELTRANS
SELECT D.Sett_No,D.Sett_Type,110,0,904,AUCTIONPARTY='Y04041',
Scrip_Cd,D.Series,ADJUSTQTY,'0','0',ISIN,'0','ADJUSTMENT TO ' + @N_SETT_NO,
'ADJUSTMENT TO ' + @N_SETT_NO,'C','0',ADJUSTQTY,'','','','HO',
MEMBERCODE,'0','0','','','DEMAT',
left(sec_payin,11),'BTST'+@N_Sett_No,1,'0',DP.dptype,DP.dpid,DPCLTNO,
'',''
FROM #DELPAYOUT D, SETT_MST S, OWNER, DELIVERYDP DP, DELSEGMENT 
WHERE D.SETT_NO = S.SETT_NO
AND D.SETT_TYPE = S.SETT_TYPE
AND DP.DESCRIPTION LIKE '%POOL%'
AND DP.DPTYPE = 'NSDL'
AND ADJUSTQTY > 0 

INSERT INTO DELTRANS
SELECT S.Sett_No,S.Sett_Type,110,0,904,Party_Code,
Scrip_Cd,D.Series,ADJUSTQTY,'0','0',ISIN,'0','ADJUSTMENT FROM ' + @SETT_NO,
'ADJUSTMENT FROM ' + @SETT_NO,'C','C',ADJUSTQTY,'','','','HO',
MEMBERCODE,'0','0','','','DEMAT',
left(sec_payin,11),'BTST'+@N_Sett_No,1,'0',DP.dptype,DP.dpid,DPCLTNO,
'',''
FROM #DELPAYOUT D, SETT_MST S, OWNER, DELIVERYDP DP
WHERE S.SETT_NO = @N_SETT_NO
AND S.SETT_TYPE = @SETT_TYPE
AND DP.DESCRIPTION LIKE '%POOL%'
AND DP.DPTYPE = 'NSDL'
AND ADJUSTQTY > 0 

INSERT INTO DELTRANS
SELECT S.Sett_No,S.Sett_Type,110,0,904,AUCTIONPARTY='Y04041',
Scrip_Cd,D.Series,ADJUSTQTY,'0','0',ISIN,'0','ADJUSTMENT FROM ' + @SETT_NO,
'ADJUSTMENT FROM ' + @SETT_NO,'D','D',ADJUSTQTY,'','','','HO',
MEMBERCODE,'0','0','','','DEMAT',
left(sec_payin,11),'BTST'+@N_Sett_No,1,'0',DP.dptype,DP.dpid,DPCLTNO,
'',''
FROM #DELPAYOUT D, SETT_MST S, OWNER, DELIVERYDP DP, DELSEGMENT 
WHERE S.SETT_NO = @N_SETT_NO
AND S.SETT_TYPE = @SETT_TYPE
AND DP.DESCRIPTION LIKE '%POOL%'
AND DP.DPTYPE = 'NSDL'
AND ADJUSTQTY > 0

GO
