 FETCH NEXT FROM @@SETTDEL INTO @@DELSNO,@@SETTDELQTY,@@PARTY_CODE,@@XSETT_NO,@@XSETT_TYPE          
 WHILE @@FETCH_STATUS = 0 AND @@QTY > 0            
 BEGIN          
            
  IF @@SETTDELQTY > 0 AND @@QTY > 0           
  BEGIN          
   IF @@SETTDELQTY >= @@QTY           
   BEGIN          
    /* UPDATE THE RECORD AND EXIT FROM CURRENT LOOP */          
    UPDATE DEMATTRANS SET PARTY_CODE = @@PARTY_CODE,DPNAME=@@XSETT_NO+@@XSETT_TYPE WHERE SNO = @@SNO       
          
    IF @@SETTDELQTY > @@QTY           
    BEGIN          
     IF @@EXCHG = 'BSE'           
     BEGIN          
 INSERT INTO BSEDB.DBO.DELTRANS_PROCESS           
 SELECT       
  0,SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,@@QTY,FROMNO,TONO,      
  CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,'D',ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,      
  PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,        
        FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5, UPDFLAG = 1          
 FROM       
  BSEDB.DBO.DELTRANS_PROCESS       
 WHERE       
  SNO = @@DELSNO      
           
 UPDATE BSEDB.DBO.DELTRANS_PROCESS SET QTY=@@SETTDELQTY-@@QTY, UPDFLAG = 1 WHERE SNO = @@DELSNO       
     END          
     ELSE          
     BEGIN          
 INSERT INTO MSAJAG.DBO.DELTRANS_PROCESS           
 SELECT       
  0,SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,@@QTY,FROMNO,TONO,      
  CERTNO,FOLIONO,HOLDERNAME,REASON,DRCR,'D',ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,      
  PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,        
  FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5, UPDFLAG = 1          
 FROM       
  MSAJAG.DBO.DELTRANS_PROCESS       
 WHERE       
  SNO = @@DELSNO      
      
      UPDATE MSAJAG.DBO.DELTRANS_PROCESS SET QTY=@@SETTDELQTY-@@QTY, UPDFLAG = 1 WHERE SNO = @@DELSNO       
     END          
     SELECT @@QTY = 0          
    END          
    ELSE           
    BEGIN           
     IF @@EXCHG = 'BSE'           
     BEGIN          
      UPDATE BSEDB.DBO.DELTRANS_PROCESS SET DELIVERED = 'D', UPDFLAG = 1 WHERE SNO = @@DELSNO      
     END          
     ELSE          
     BEGIN          
      UPDATE MSAJAG.DBO.DELTRANS_PROCESS SET DELIVERED = 'D', UPDFLAG = 1 WHERE SNO = @@DELSNO      
     END          
     SELECT @@QTY = 0          
    END               
    SELECT @@SETTDELQTY = 0            
   END          
   ELSE          
   BEGIN          
    /* UPDATE THE RECORD WITH @@QTY - @@SETTDELQTY AND FETCH THE NEXT           
       RECORD FROM CURRENT LOOP */          
    INSERT INTO DEMATTRANS       
 SELECT       
  SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,@@PARTY_CODE,SCRIP_CD,          
      SERIES,@@SETTDELQTY,TRDATE,CLTACCNO,DPID,DPNAME=@@XSETT_NO+@@XSETT_TYPE,ISIN,BRANCH_CD,PARTIPANTCODE,DPTYPE,TRANSNO,          
      DRCR,BDPTYPE,BDPID,BCLTACCNO,FILLER1,FILLER2,FILLER3,FILLER4,FILLER5       
 FROM       
  DEMATTRANS           
     WHERE SNO = @@SNO      
              
    UPDATE DEMATTRANS SET QTY = @@QTY-@@SETTDELQTY          
    WHERE SNO = @@SNO         
              
    IF @@EXCHG = 'BSE'           
    BEGIN             
     UPDATE BSEDB.DBO.DELTRANS_PROCESS SET DELIVERED = 'D', UPDFLAG = 1 WHERE SNO = @@DELSNO      
    END           
    ELSE              
    BEGIN             
     UPDATE MSAJAG.DBO.DELTRANS_PROCESS SET DELIVERED = 'D', UPDFLAG = 1 WHERE SNO = @@DELSNO       
    END           
    SELECT @@QTY = @@QTY-@@SETTDELQTY          
    SELECT @@SETTDELQTY = 0            
   END          
  END          
  FETCH NEXT FROM @@SETTDEL INTO @@DELSNO,@@SETTDELQTY,@@PARTY_CODE,@@XSETT_NO,@@XSETT_TYPE           
 END          
 CLOSE @@SETTDEL           
 DEALLOCATE @@SETTDEL          
 FETCH NEXT FROM @@DEMATCUR INTO @@SNO,@@ISIN,@@QTY,@@TCODE,@@TRDATE,@@SETT_NO,@@SETT_TYPE,@@BCLTDPID,@@EXCHG           
END          
CLOSE @@DEMATCUR          
DEALLOCATE @@DEMATCUR          
  
