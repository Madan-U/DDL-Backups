CREATE PROC [DBO].[INSDELINTERSETTUPDATEINDPEXBEN]       
      
AS          
      
DECLARE           
 @@SNO INT,          
 @@DELSNO INT,          
 @@PARTY_CODE VARCHAR(10),          
 @@ISIN VARCHAR(12),          
 @@QTY INT,          
 @@TCODE INT,          
 @@EXCHG VARCHAR(3),          
 @@TRDATE VARCHAR(11),          
 @@SETT_NO VARCHAR(7),          
 @@BCLTDPID VARCHAR(16),          
 @@SETT_TYPE VARCHAR(2),          
 @@SETTDELQTY INT,          
 @@SETTDELTRANSQTY INT,          
 @@XSETT_NO VARCHAR(7),          
 @@XSETT_TYPE VARCHAR(2),          
 @@DEMATCUR CURSOR,          
 @@SETTDEL CURSOR,          
 @@SETTDELTRANS CURSOR          
          
TRUNCATE TABLE BSEDB..DELTRANS_PROCESS      
TRUNCATE TABLE MSAJAG..DELTRANS_PROCESS      
      
INSERT INTO BSEDB..DELTRANS_PROCESS       
SELECT *, UPDFLAG = 0 FROM BSEDB..DELTRANS       
 WHERE FILLER2 = 1       
 AND TRTYPE IN (907,1000)      
 AND DRCR = 'D' AND DELIVERED = 'G'        
      
INSERT INTO MSAJAG..DELTRANS_PROCESS       
SELECT *, UPDFLAG = 0 FROM MSAJAG..DELTRANS       
 WHERE FILLER2 = 1       
 AND TRTYPE IN (907,1000)      
 AND DRCR = 'D' AND DELIVERED = 'G'        
      
SELECT @@SETTDELQTY = 0           
SELECT @@SETTDELTRANSQTY = 0          
/* GETTING ALL THE RECORDS FROM DEMATTRANS FOR INTERSETTLEMENT */        
      
SET @@DEMATCUR = CURSOR FOR          
     
 SELECT       
  D.SNO,      
  ISIN,      
  QTY,      
  TCODE,      
  TRDATE=LEFT(CONVERT(VARCHAR,TRDATE,109),11),      
  D.SETT_NO,      
  D.SETT_TYPE,      
  D.CLTACCNO,      
  EXCHG='BSE'          
 FROM       
  DEMATTRANS D,       
  BSEDB.DBO.DELIVERYDP DP       
 WHERE       
  D.DPID = DP.DPID       
  AND D.CLTACCNO = DP.DPCLTNO       
  AND PARTY_CODE = 'PARTY'            
 ORDER BY       
  ISIN,QTY DESC,D.SETT_NO,D.SETT_TYPE,D.CLTACCNO          
      
OPEN @@DEMATCUR           
FETCH NEXT FROM @@DEMATCUR INTO @@SNO,@@ISIN,@@QTY,@@TCODE,@@TRDATE,@@SETT_NO,@@SETT_TYPE,@@BCLTDPID,@@EXCHG          
WHILE @@FETCH_STATUS = 0          
BEGIN           
 /* TAKING PARTY'S SELL POSITION FOR THAT SCRIP FROM DELIVERYCLT TABLE. */          
 IF @@EXCHG = 'BSE'           
 BEGIN           
  SET @@SETTDEL = CURSOR FOR          
 SELECT       
  SNO,QTY,PARTY_CODE,SETT_NO,SETT_TYPE       
 FROM       
  BSEDB.DBO.DELTRANS_PROCESS      
 WHERE       
  ISETT_NO = @@SETT_NO       
  AND ISETT_TYPE = @@SETT_TYPE       
  AND FILLER2 = 1       
  AND 1 = (CASE WHEN BDPTYPE = 'CDSL' AND TRTYPE IN (907, 1000) 
		THEN 1 
		WHEN BDPTYPE = 'NSDL' AND TRTYPE = 1000
		THEN 1
		ELSE 2
	   END)     
  AND DRCR = 'D'       
  AND DELIVERED = 'G'        
  AND BCLTDPID = @@BCLTDPID          
  AND CERTNO = @@ISIN       
 ORDER BY       
  SETT_NO,SETT_TYPE,PARTY_CODE          
 END          
