---EXEC RPT_DELPAYOUTREPORT 'broker','broker','','0','zzz','0','zzzzzzzz','BEN',1  
  
CREATE  PROC RPT_DELPAYOUTREPORT  
 (  
 @STATUSID VARCHAR(20),  
 @STATUSNAME VARCHAR(25),  
 @SDATE VARCHAR(11),  
 @FROMPARTY VARCHAR(15),  
 @TOPARTY VARCHAR(15),  
 @FROMSCRIP VARCHAR(15),  
 @TOSCRIP VARCHAR(15),  
 @ACCTYPE VARCHAR(5),  
 @REPOTTYPE INT  
 )  
  
AS  
  
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED  
  
SELECT   
 EXCHANGE = DEL.EXCHANGE,   
 SETT_NO,   
 SETT_TYPE,   
 PARTY_CODE = DEL.PARTY_CODE,  
 SCRIP_CD,   
 SERIES,   
 CERTNO,  
 LEDBAL,  
 HOLDQTY = (CASE WHEN @ACCTYPE = 'BEN' THEN DEBITQTY ELSE POOLQTY END),  
 CL_RATE,  
 HOLDINGAMT = ((CASE WHEN @ACCTYPE = 'BEN' THEN DEBITQTY ELSE POOLQTY END)*CL_RATE),  
 RMSPAYQTY = ACTPAYOUT,  
 PAYOUTAMT = (ACTPAYOUT*CL_RATE),  
 PAYOUTDATE = CONVERT(VARCHAR,PAYOUTDATE)  
  
INTO  #DELPAYOUTREPORT  
FROM   
 DELPAYOUT DEL WITH(NOLOCK),  
 CLIENT1 C1 WITH(NOLOCK),  
 CLIENT2 C2 WITH(NOLOCK)   
  
WHERE   
 C1.CL_CODE = C2.CL_CODE  
 AND C2.PARTY_CODE = DEL.PARTY_CODE  
 AND DEL.PARTY_CODE >= @FROMPARTY   
 AND DEL.PARTY_CODE <= @TOPARTY  
 AND SCRIP_CD >= @FROMSCRIP   
 AND SCRIP_CD <= @TOSCRIP  
 AND ACTYPE = @ACCTYPE  
 AND @STATUSNAME =   
  (CASE   
   WHEN @STATUSID = 'BRANCH' THEN BRANCH_CD  
   WHEN @STATUSID = 'SUBBROKER' THEN SUB_BROKER  
   WHEN @STATUSID = 'TRADER' THEN TRADER  
   WHEN @STATUSID = 'FAMILY' THEN FAMILY  
   WHEN @STATUSID = 'AREA' THEN AREA  
   WHEN @STATUSID = 'REGION' THEN REGION  
   WHEN @STATUSID = 'CLIENT' THEN DEL.PARTY_CODE  
  ELSE  
   'BROKER'  
  END)      
  
ORDER BY   
 DEL.PARTY_CODE,   
 SCRIP_CD,   
 SERIES,   
 CERTNO  
  
  
  
IF (@REPOTTYPE = 1)  
 BEGIN  
  SELECT *   
  FROM #DELPAYOUTREPORT 
  WHERE HOLDQTY > 0  
 END   
ELSE IF (@REPOTTYPE = 2)  
 BEGIN  
  SELECT   
   PARTY_CODE,  
   LEDBAL,  
   HOLDQTY = SUM(HOLDQTY),  
   HOLDINGAMT = SUM(HOLDINGAMT),  
   RMSPAYQTY = SUM(RMSPAYQTY),  
   PAYOUTAMT = SUM(PAYOUTAMT),  
   PAYOUTDATE  
  
   FROM  #DELPAYOUTREPORT  
  
  GROUP BY  
   PARTY_CODE,  
   LEDBAL,  
   PAYOUTDATE  
  HAVING SUM(HOLDQTY) > 0 
 END

GO
