CREATE PROC CALCBROK_ONTHEFLY
(
	@PARTYCODE VARCHAR(10),
	@SELLBUY INT,
	@MKTRATE NUMERIC(18, 4)
)
/*
	EXEC CALCBROK_ONTHEFLY 'Z08818', 2, 189.0989
	SELECT * FROM BROKTABLE WHERE
*/
AS
	DECLARE
		@CNT INT,
		@BRKTABLENO INT,
		@VALPER VARCHAR(1),
		@BRKRATE NUMERIC(18, 4),
		@BROKERAGE NUMERIC(18, 9),
		@NETRATE NUMERIC(18, 4),
		@ACTBRK CHAR(18),
		@ACTNET CHAR(18),
		@BRKRND INT,
		@NETRND INT,
		@Round_To Int,
		@RoFig Int,
		@ErrNum Numeric(18, 9),
		@NoZero Int
SET @CNT = 0
SET @BRKRND = 0
SET @NETRND = 0
Select
	@CNT = ISNULL(Count(1), 0),
	@BRKTABLENO = ISNULL(C2.TABLE_NO, 0)
From
	Client1 C1,
	Client2 C2
Where
	C1.Cl_Code = C2.Cl_Code
	And C2.Party_Code = @PARTYCODE
GROUP BY
	ISNULL(C2.TABLE_NO, 0)
IF @CNT > 0
	BEGIN
		SELECT
			@VALPER = VAL_PERC,
			@BRKRATE = (CASE WHEN @SELLBUY = 1 THEN DAY_PUC ELSE DAY_SALES END),
			@Round_To = Round_To,
			@RoFig = RoFig,
			@ErrNum = ErrNum,
			@NoZero = NoZero
		FROM
			BROKTABLE
		WHERE
			TABLE_NO = @BRKTABLENO
			AND @MKTRATE >= LOWER_LIM AND @MKTRATE < UPPER_LIM
		IF @VALPER = 'V'
			BEGIN
				SELECT
					@BROKERAGE = @MKTRATE * @BRKRATE
			END
		ELSE
			BEGIN
				SELECT @BROKERAGE = (@MKTRATE * @BRKRATE) / 100
			END
		SET @BROKERAGE = ((floor((@BROKERAGE * power(10, @Round_To)+ @RoFig + @ErrNum)/(@RoFig + @NoZero )) * (@RoFig + @NoZero))/power(10,@Round_To))
		SELECT
			@BRKRND = BROKERAGE,
			@NETRND = NETRATE
		FROM
			INSTCLIENT_TBL
		WHERE
			PARTYCODE = @PARTYCODE
		IF @BRKRND = 0
			SET @BRKRND = 4
		IF @NETRND = 0
			SET @NETRND = 4
		--SET @BROKERAGE = ROUND(@BROKERAGE, @BRKRND, 1)
		SET @ACTBRK = CONVERT(CHAR(18), @BROKERAGE)
		SET @NETRATE = CASE WHEN @SELLBUY = 1 THEN (@MKTRATE + @BROKERAGE) ELSE (@MKTRATE - @BROKERAGE) END
		SET @NETRATE = ROUND(@NETRATE, @NETRND, 1)
		SET @ACTNET = CONVERT(CHAR(18), @NETRATE)
		SET 
	END
ELSE
	BEGIN
		SET @CNT = 0
		SET @ACTBRK = CONVERT(CHAR(18), 0)
		SET @ACTNET = CONVERT(CHAR(18), @MKTRATE)
		SET @NETRND = 4
	END

SELECT
	CNT = ISNULL(@CNT, 0),
	BROKERAGE = ISNULL(@ACTBRK, CONVERT(CHAR(18), 0)),
	NETROUND = CONVERT(VARCHAR(1), @NETRND),
	NETRATE = ISNULL(@ACTNET, CONVERT(CHAR(18), @MKTRATE))

GO
