-- Object: PROCEDURE dbo.PROC_NSEAUCRATE_UPDATE
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE PROC PROC_NSEAUCRATE_UPDATE 
(@SETT_NO VARCHAR(7), @SETT_TYPE VARCHAR(2))
AS

UPDATE DELAUCSCRIP SET EXRATE = RATE, FINRATE = RATE
FROM (SELECT SCRIP_CD, SERIES, RATE = SUM(BUYAMT) / SUM(BUYQTY)
      FROM DELAUCNSERATE
	  WHERE SETT_NO = @SETT_NO
	  AND SETT_TYPE = @SETT_TYPE
	  AND BUYQTY > 0
	  GROUP BY SCRIP_CD, SERIES ) AUCRATE
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = @SETT_TYPE
AND DELAUCSCRIP.SCRIP_CD = AUCRATE.SCRIP_CD
AND DELAUCSCRIP.SERIES = AUCRATE.SERIES
AND QTY > 0

GO
