-- Object: PROCEDURE dbo.Proc_SLBS_Auction
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE Proc Proc_SLBS_Auction   
(  
 @Sett_No  Varchar(7),  
 @Sett_Type  Varchar(2)  
)  
  
As  

DECLARE 
@SETTCUR	CURSOR,
@DELCUR		CURSOR,
@SCRIP_CD	VARCHAR(12),
@SERIES		VARCHAR(2),
@QTY		INT,
@RATE		NUMERIC(18,4),
@INOUT		VARCHAR(1),
@PARTY_CODE	VARCHAR(10),
@SHRTQTY	INT,
@AUCTIONCHRG		NUMERIC(18,4)

DELETE FROM TRD_SETT_POS
WHERE ORDER_NO IN (SELECT ORDER_NO FROM SETTLEMENT
WHERE SETT_NO = @SETT_NO 
AND SETT_TYPE = 'Q')

DELETE FROM SETTLEMENT
WHERE SETT_NO = @SETT_NO 
AND SETT_TYPE = 'Q'

SELECT @AUCTIONCHRG = AUCTIONCHRG FROM ANGELDEMAT.MSAJAG.DBO.DELSEGMENT
SELECT @AUCTIONCHRG = 0

SELECT *, RECQTY = 0, RATE=CONVERT(NUMERIC(18,4),0) INTO #DELCLT FROM MSAJAG..DELIVERYCLT
WHERE SETT_NO = @SETT_NO 
AND SETT_TYPE = @SETT_TYPE

UPDATE #DELCLT SET RECQTY = DELQTY
FROM 
(
	SELECT PARTY_CODE, SCRIP_CD, SERIES,DELQTY = SUM(QTY), DRCR
	FROM /*SAN2.*/MSAJAG.DBO.DELTRANS
	WHERE SETT_NO = @SETT_NO 
	AND SETT_TYPE = @SETT_TYPE
	AND FILLER2 = 1 
	AND SHARETYPE <> 'AUCTION'
	AND TRTYPE <> 906
	AND PARTY_CODE <> 'BROKER'
	GROUP BY PARTY_CODE, SCRIP_CD, SERIES, DRCR 
) A
WHERE #DELCLT.PARTY_CODE = A.PARTY_CODE
AND #DELCLT.SCRIP_CD = A.SCRIP_CD
AND #DELCLT.SERIES = A.SERIES
AND INOUT = (CASE WHEN DRCR = 'C' THEN 'I' ELSE 'O' END)

SELECT SCRIP_CD, SERIES, PARTY_CODE, QTY, RATE, INOUT, AUCTIONCHRG = @AUCTIONCHRG INTO #DELCLT_AUC FROM #DELCLT
WHERE 1 = 2 

SET @SETTCUR = CURSOR FOR
SELECT SCRIP_CD, SERIES, QTY, RATE=SQ_OFF_PRICE, INOUT = (CASE WHEN DR_CR_FLAG = 'D' THEN 'I' ELSE 'O' END) 
FROM TBL_SLBS_AUCTION 
WHERE SETT_NO = @SETT_NO 
AND SETT_TYPE = @SETT_TYPE
ORDER BY 1, 2
OPEN @SETTCUR
FETCH NEXT FROM @SETTCUR INTO @SCRIP_CD, @SERIES, @QTY, @RATE, @INOUT 
WHILE @@FETCH_STATUS = 0
BEGIN
	SET @DELCUR = CURSOR FOR
	SELECT PARTY_CODE, SHRTQTY = QTY - RECQTY
	FROM #DELCLT 
	WHERE SCRIP_CD = @SCRIP_CD
	AND INOUT = @INOUT
	AND QTY <> RECQTY
	OPEN @DELCUR
	FETCH NEXT FROM @DELCUR INTO @PARTY_CODE, @SHRTQTY
	WHILE @@FETCH_STATUS = 0 AND @QTY > 0
	BEGIN
		IF @SHRTQTY <= @QTY
		BEGIN
			INSERT INTO #DELCLT_AUC VALUES (@SCRIP_CD, @SERIES, @PARTY_CODE, @SHRTQTY, @RATE, @INOUT, @AUCTIONCHRG)

			UPDATE #DELCLT SET RECQTY = RECQTY + @SHRTQTY
			WHERE PARTY_CODE = @PARTY_CODE
			AND SCRIP_CD = @SCRIP_CD
			AND INOUT = @INOUT
			AND QTY <> RECQTY

			SET @QTY = @QTY - @SHRTQTY
		END
		ELSE 
		BEGIN
			INSERT INTO #DELCLT_AUC VALUES (@SCRIP_CD, @SERIES, @PARTY_CODE, @QTY, @RATE, @INOUT, @AUCTIONCHRG)

			UPDATE #DELCLT SET RECQTY = RECQTY + @QTY
			WHERE PARTY_CODE = @PARTY_CODE
			AND SCRIP_CD = @SCRIP_CD
			AND INOUT = @INOUT
			AND QTY <> RECQTY

			SET @QTY = 0 
		END
		FETCH NEXT FROM @DELCUR INTO @PARTY_CODE, @SHRTQTY
	END
	CLOSE @DELCUR
	DEALLOCATE @DELCUR
	FETCH NEXT FROM @SETTCUR INTO @SCRIP_CD, @SERIES, @QTY, @RATE, @INOUT 
END
CLOSE @SETTCUR
DEALLOCATE @SETTCUR


INSERT INTO SETTLEMENT
SELECT --TOP 1 
	   Contractno='0',Billno='0',Trade_No='0',Party_Code,Scrip_Cd,User_Id='0',Tradeqty=QTY,
	   Auctionpart='FS',
	   Markettype='Q',D.Series,Order_No='0',Marketrate=RATE,Sauda_Date=START_DATE,
       Table_No='0',Line_No=1,Val_Perc='P',
       Normal=0,Day_Puc=0,Day_Sales=0,Sett_Purch=0,Sett_Sales=0,
       Sell_Buy=(CASE WHEN INOUT = 'O' THEN 2 ELSE 1 END),
       Settflag=(CASE WHEN INOUT = 'O' THEN 5 ELSE 4 END),Brokapplied=0,
       Netrate=RATE,Amount=QTY*RATE,Ins_Chrg=0,Turn_Tax=0,Other_Chrg=QTY*RATE*AUCTIONCHRG/100,Sebi_Tax=0,Broker_Chrg=0,
       Service_Tax=0,Trade_Amount=QTY*RATE,
	   Billflag=(CASE WHEN INOUT = 'O' THEN 5 ELSE 4 END),
	   Sett_No=@SETT_NO,Nbrokapp=0,Nsertax=0,N_Netrate=rate,Sett_Type='Q',Partipantcode=MEMBERCODE,
       Status='E',Pro_Cli='1',Cpid='',Instrument='0',Booktype='0',Branch_Id=PARTY_CODE,
	   Tmark='D',Scheme=0,Dummy1=RATE,Dummy2=0 
FROM #DELCLT_AUC D, OWNER, SETT_MST
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = 'Q'

UPDATE SETTLEMENT SET TRADE_NO = SRNO, ORDER_NO = SRNO
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = 'Q'
AND TRADE_NO = '0'

INSERT INTO TRD_SETT_POS
SELECT 
SAUDA_DATE,ORDER_NO,TRADE_NO,S.SCRIP_CD,S.SERIES,
EXPIRYDATE,TRADEQTY,
SELL_BUY=(CASE WHEN SELL_BUY = 1 THEN 3 ELSE 4 END)
FROM SETTLEMENT S, (SELECT DISTINCT SCRIP_CD, SERIES, EXPIRYDATE
					FROM TRD_SETT_POS
					WHERE CONVERT(DATETIME,EXPIRYDATE) > LEFT(GETDATE(),11)
					) A
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = 'Q'
AND S.SCRIP_CD = A.SCRIP_CD
AND S.SERIES = A.SERIES

GO
