		AND SETT_NO = @SETT_NO AND REM_ST > 0
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		BRANCH_CD,
		A.EXCHANGE,
		CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR ST FOR REMISIER BROKERAGE SHARING SERVICE TAX AC (SUB) (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 2 (CR)
		INSERT INTO Rem_AccBill
			(PARTY_CODE,
			BILL_NO,
			SELL_BUY,
			SETT_NO,
