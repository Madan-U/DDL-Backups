CREATE PROCEDURE ProcRemisierBrokeragePostAccBill 
(                     
	@Sett_No Varchar(7), 
	@Sett_Type Varchar(2),                      
	@FromDate Varchar(11),                      
	@ToDate  Varchar(11)                      
)
AS                      
BEGIN
	---*---*---*---*---*---*---*			Remisier Voucher Posting Logic  	--*---*---*---*---*---*---*---*---*---*---
	--- Brokerage , Service Tax, Cess, Edn-Cess	Amounts to be credited to Remisier Party
	--- TDS amount to be debited to Remisier Party	
	---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*---*

	DECLARE 
		@REMBROKNARR_SUB  VARCHAR(50),  
		@REMTDSNARR_SUB   VARCHAR(50),  
		@REMSTARR_SUB     VARCHAR(50),  
		@REMCESSARR_SUB     VARCHAR(50),  
		@REMEDNCESSARR_SUB  VARCHAR(50),  
		@REMBROKNARR_BR  VARCHAR(50),  
		@REMTDSNARR_BR   VARCHAR(50),  
		@REMSTARR_BR     VARCHAR(50),  
		@REMCESSARR_BR     VARCHAR(50),  
		@REMEDNCESSARR_BR    VARCHAR(50),  
		@STPERCENTAGE NUMERIC(10,4),
		@CESSPERCENTAGE NUMERIC(10,4),
		@EDNCESSPERCENTAGE NUMERIC(10,4),
		@FUNDDATE DATETIME

	CREATE TABLE #ValanAccount
		(
			Exchange VARCHAR(5) DEFAULT(''),
			AcctCode VARCHAR(10) DEFAULT(''),
			AcctName VARCHAR(100) DEFAULT('')
		)

	CREATE TABLE #SubBroker
		(
			Exchange VARCHAR(5) DEFAULT(''),
			SubBroker VARCHAR(10) DEFAULT(''),
			SubBrokerParty VARCHAR(10) DEFAULT(''),
			AccoutingStartSettNo VARCHAR(12) DEFAULT(''),
		)

	--- Update shared settlement, where the accounting entries is passed on the selected settlement. 
	UPDATE REM_BROK_TRANS SET 
		BSERemBrokSharedSettNo = CASE WHEN BSERemBrokSharedSettNo = @SETT_NO THEN '' ELSE BSERemBrokSharedSettNo END,
		NSERemBrokSharedSettNo = CASE WHEN NSERemBrokSharedSettNo = @SETT_NO THEN '' ELSE NSERemBrokSharedSettNo END,
		NSEFORemBrokSharedSettNo = CASE WHEN NSEFORemBrokSharedSettNo = @SETT_NO THEN '' ELSE NSEFORemBrokSharedSettNo END

	--- ASSIGN NARRATION 
	SELECT @REMBROKNARR_SUB = 'REM BROK SHARE FOR SUB BROKER', 
			 @REMTDSNARR_SUB = 'REM TDS AMOUNT FOR SUB BROKER', 
			 @REMSTARR_SUB = 'REM ST AMOUNT FOR SUB BROKER', 
			 @REMCESSARR_SUB = 'REM CESS AMOUNT FOR SUB BROKER',
			 @REMEDNCESSARR_SUB= 'REM EDN CESS AMOUNT FOR SUB BROKER',
		    @REMBROKNARR_BR = 'REM BROK SHARE FOR BRANCH', 
			 @REMTDSNARR_BR = 'REM TDS AMOUNT FOR BRANCH', 
			 @REMSTARR_BR = 'REM ST AMOUNT FOR BRANCH', 
			 @REMCESSARR_BR = 'REM CESS AMOUNT FOR BRANCH',
			 @REMEDNCESSARR_BR= 'REM EDN CESS AMOUNT FOR BRANCH'

	-- SELECT SERVICE TAX & EDUCATION CESS
	SELECT @STPERCENTAGE = SERVICE_TAX, @CESSPERCENTAGE = CESS_TAX, @EDNCESSPERCENTAGE = EDUCESSTAX 
		FROM GLOBALS WHERE @FromDate BETWEEN YEAR_START_DT AND YEAR_END_DT

	IF @STPERCENTAGE <> 0 AND (@CESSPERCENTAGE+@EDNCESSPERCENTAGE) <> 0 
		SELECT @STPERCENTAGE = CONVERT(NUMERIC(18,2), (@STPERCENTAGE*100)/(100+@CESSPERCENTAGE+@EDNCESSPERCENTAGE))

	--- Collect Control A/c entries from ValanAccount table in "NSE" database.
	INSERT INTO #ValanAccount(Exchange, AcctCode, AcctName) 
		SELECT 'NSECM', AcCode, AcName 
		FROM MSAJAG.DBO.ValanAccount WHERE AcName
		 IN(
				'REMISIER BROKERAGE CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING TDS AC (SUB)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (SUB)', 
				'REMISIER BROKERAGE SHARING CESS AC (SUB)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (SUB)',
				'REMISIER BROKERAGE CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING TDS AC (BR)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (BR)', 
				'REMISIER BROKERAGE SHARING CESS AC (BR)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (BR)'
			)

	--- Collect the Control A/c entries from ValanAccount table in "BSE" database.
	INSERT INTO #ValanAccount(Exchange, AcctCode, AcctName) 
		SELECT 'BSECM', AcCode, AcName 
		FROM BSEDB.DBO.ValanAccount WHERE AcName
		 IN(
				'REMISIER BROKERAGE CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING TDS AC (SUB)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (SUB)',
				'REMISIER BROKERAGE SHARING CESS AC (SUB)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (SUB)',
				'REMISIER BROKERAGE CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING TDS AC (BR)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (BR)', 
				'REMISIER BROKERAGE SHARING CESS AC (BR)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (BR)' 
			)

	--- Collect the Control A/c entries from ValanAccount table in "NSEFO" database.
	INSERT INTO #ValanAccount(Exchange, AcctCode, AcctName) 
		SELECT 'NSEFO', AcCode, AcName 
		FROM NSEFO.DBO.ValanAccount WHERE AcName
		 IN(
				'REMISIER BROKERAGE CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING TDS AC (SUB)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (SUB)',
				'REMISIER BROKERAGE SHARING CESS AC (SUB)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (SUB)',
				'REMISIER BROKERAGE CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING TDS AC (BR)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (BR)', 
				'REMISIER BROKERAGE SHARING CESS AC (BR)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (BR)' 
			)
	
	INSERT INTO #ValanAccount(Exchange, AcctCode, AcctName) 
		SELECT 'NCDX', AcCode, AcName 
		FROM NCDX.DBO.ValanAccount WHERE AcName
		 IN(
				'REMISIER BROKERAGE CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING TDS AC (SUB)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (SUB)',
				'REMISIER BROKERAGE SHARING CESS AC (SUB)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (SUB)',
				'REMISIER BROKERAGE CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING TDS AC (BR)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (BR)', 
				'REMISIER BROKERAGE SHARING CESS AC (BR)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (BR)' 
			)


	INSERT INTO #ValanAccount(Exchange, AcctCode, AcctName) 
		SELECT 'MCDX', AcCode, AcName 
		FROM MCDX.DBO.ValanAccount WHERE AcName
		 IN(
				'REMISIER BROKERAGE CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING CONTROL AC (SUB)',
				'REMISIER BROKERAGE SHARING TDS AC (SUB)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (SUB)',
				'REMISIER BROKERAGE SHARING CESS AC (SUB)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (SUB)',
				'REMISIER BROKERAGE CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING CONTROL AC (BR)',
				'REMISIER BROKERAGE SHARING TDS AC (BR)',
				'REMISIER BROKERAGE SHARING SERVICE TAX AC (BR)', 
				'REMISIER BROKERAGE SHARING CESS AC (BR)',
				'REMISIER BROKERAGE SHARING EDN CESS AC (BR)' 
			)


