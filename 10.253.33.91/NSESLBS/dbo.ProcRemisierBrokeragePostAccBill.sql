		WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING TDS AC (SUB)'
		AND SETT_NO = @SETT_NO
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
