	INSERT INTO #SubBroker(Exchange, SubBroker, SubBrokerParty, AccoutingStartSettNo)
		SELECT Exchange, SubBroker, SubBrokerParty, AccoutingStartSettNo FROM SubBrokerRegDetails
		WHERE RegFromDate BETWEEN @FromDate AND @ToDate

--*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*
--			POSTING OF REMISIER BROERAGE SHARING FOR SUB BROKER (BROKERAGE, ST, CESS, EDNCESS FOR OPEN SETTLEMENT)
--*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*

	--- Update Service Tax, Cess, Edn Cess
	UPDATE RemisierBrokerageTrans SET 
		Rem_ST = CASE WHEN @STPERCENTAGE > 0 THEN (RemBrokeragePayable*@STPERCENTAGE/100) ELSE 0 END,
		Rem_Cess = CASE WHEN @CESSPERCENTAGE > 0 THEN ( (RemBrokeragePayable*@STPERCENTAGE/100)*(@CESSPERCENTAGE/100) ) ELSE 0 END,
		Rem_EdnCess = CASE WHEN @EDNCESSPERCENTAGE > 0 THEN ( (RemBrokeragePayable*@STPERCENTAGE/100)*(@EDNCESSPERCENTAGE/100) ) ELSE 0 END
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE

	--- Update TDS
	UPDATE RemisierBrokerageTrans SET Rem_Tds = (RemBrokeragePayable + ISNULL(Rem_ST, 0) + ISNULL(Rem_Cess, 0) + ISNULL(Rem_EdnCess, 0)) * (TDSPercentage/100)
		WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE AND TDSPercentage > 0 
	
	--- Pick Funds_PayIn Date From Rem_Sett_Mst table for the given settlement.
	SELECT @FUNDDATE = FUNDS_PAYIN FROM REM_SETT_MST WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE

	--- PICK REMISIER BROKERAGE ENTRIES FOR BSE 
	SELECT 
		A.EXCHANGE,
		SUBBROKER=A.REMCODE,
		A.BRANCH_CD,
		A.REMPARTYCD, 
		A.SETT_NO,
		A.SETT_TYPE,
		START_DATE=A.FROMDATE,
		END_DATE=A.TODATE,
		PAYIN_DATE=@FUNDDATE,
		PAYOUT_DATE=@FUNDDATE,
		REMBROK=A.REMBROKERAGEPAYABLE,
		REMBROKTDS=A.REM_TDS,
		TDSPERCENTAGE=A.TDSPERCENTAGE,
		REM_ST = A.REM_ST,
		REM_CESS = A.REM_CESS,
		REM_EDNCESS = A.REM_EDNCESS
	INTO #Rem_AccBill
	FROM RemisierBrokerageTrans A, #SubBroker B	                      
		WHERE A.REMCODE = B.SubBroker 
		AND B.Exchange = A.Exchange
		AND A.Sett_Type = @Sett_Type
		AND (A.Sett_No = B.AccoutingStartSettNo OR A.Sett_No = @Sett_No)
		AND ISNULL(A.RemBrokSharedSettNo, '') = ''
		AND A.REMBROKERAGEPAYABLE > 0 
		AND A.RemType = 'SUB'

-- -- 	FROM RemisierBrokerageTrans A
-- -- 		WHERE ISNULL(A.RemBrokSharedSettNo, '') = ''
-- -- 		AND A.REMBROKERAGEPAYABLE > 0 
		
	--SELECT * FROM #Rem_AccBill

	---- REMOVE THE EXISTING RECORDS IN THE Rem_AccBill TABLE
	DELETE FROM Rem_AccBill WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE   

--- ENTRY FOR REMISIER_BROKERAGE FOR "REMISIER BROKERAGE CONTROL AC (SUB)" (FOR SELECTED SETTLEMENT NO)
---  BUY_SELL = 1 (DR)	
	INSERT INTO Rem_AccBill
		(PARTY_CODE,
		BILL_NO,
		SELL_BUY,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		AMOUNT,
		BRANCHCD,
		NARRATION,
		EXCHANGE,
		SEGMENT,
		BROK_SHARED_SETTNO, BILL_FOR)
	SELECT
		B.ACCTCODE,
		BILLNO = 0,
		SELL_BUY = 1,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		REMBROK=SUM(REMBROK),
		BRANCH_CD='ZZZ',
		NARRATION=@REMBROKNARR_SUB,
		A.EXCHANGE,
		SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
		BROK_SHARED_SETTNO = @SETT_NO,
		BILL_FOR = 'SUB'
	FROM #Rem_AccBill A, #ValanAccount B
		WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE CONTROL AC (SUB)'
		AND SETT_NO = @SETT_NO
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		BRANCH_CD,
		A.EXCHANGE,
		CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR REMISIER_BROKERAGE FOR "REMISIER BROKERAGE SHARING CONTROL AC (SUB)" (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 2 (CR)
	INSERT INTO Rem_AccBill
		(PARTY_CODE,
		BILL_NO,
		SELL_BUY,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		AMOUNT,
		BRANCHCD,
		NARRATION,
		EXCHANGE,
		SEGMENT,
		BROK_SHARED_SETTNO, BILL_FOR)
	SELECT
		B.ACCTCODE,
		BILLNO = 0,
		SELL_BUY = 2,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		REMBROK=SUM(REMBROK),
		BRANCH_CD='ZZZ',
		NARRATION=@REMBROKNARR_SUB,
		A.EXCHANGE,
		SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
		BROK_SHARED_SETTNO = @SETT_NO,
		BILL_FOR = 'SUB'
	FROM #Rem_AccBill A, #ValanAccount B
		WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING CONTROL AC (SUB)'
		AND SETT_NO = @SETT_NO
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		BRANCH_CD,
		A.EXCHANGE,
		CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR REMISIER_BROKERAGE TDS FOR "REMISIER BROKERAGE SHARING CONTROL AC (SUB)" (FOR SELECTED SETTLEMENT NO)
	---  BUY_SELL = 2 (CR)
	INSERT INTO Rem_AccBill
		(PARTY_CODE,
		BILL_NO,
		SELL_BUY,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		AMOUNT,
		BRANCHCD,
		NARRATION,
		EXCHANGE,
		SEGMENT,
		BROK_SHARED_SETTNO, BILL_FOR)
	SELECT
		B.ACCTCODE,
		BILLNO = 0,
		SELL_BUY = 2,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		REMBROK=SUM(REMBROKTDS),
		BRANCH_CD ='ZZZ',
		NARRATION=@REMTDSNARR_SUB,
		A.EXCHANGE,
		SEGMENT=CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END,
		BROK_SHARED_SETTNO = @SETT_NO,
		BILL_FOR = 'SUB'
	FROM #Rem_AccBill A, #ValanAccount B
		WHERE A.EXCHANGE = B.EXCHANGE AND B.ACCTNAME='REMISIER BROKERAGE SHARING CONTROL AC (SUB)'
		AND SETT_NO = @SETT_NO
	GROUP BY 
		B.ACCTCODE,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		BRANCH_CD,
		A.EXCHANGE,
		CASE A.EXCHANGE WHEN 'NSEFO' THEN 'FUTURES' ELSE 'CAPITAL' END

	--- ENTRY FOR REMISIER_BROKERAGE TDS FOR REMISIER BROKERAGE SHARING TDS AC (SUB) (FOR OPEN SETTLEMENT NOS)
	---  BUY_SELL = 1 (DR)
	INSERT INTO Rem_AccBill
		(PARTY_CODE,
		BILL_NO,
		SELL_BUY,
		SETT_NO,
		SETT_TYPE,
		START_DATE,
		END_DATE,
		PAYIN_DATE,
		PAYOUT_DATE,
		AMOUNT,
		BRANCHCD,
		NARRATION,
		EXCHANGE,
