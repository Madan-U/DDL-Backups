-- Object: PROCEDURE dbo.FT_FRONTOFFICE_FOPOS
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC FT_FRONTOFFICE_FOPOS  
(@FILEDATE VARCHAR(11)) AS      
      
SELECT FILLER = ' ', MARKET_SEGMENT = 2, PRODUCT_TYPE = 'A',      
PARTY_CODE, GROUP_ID = CONVERT(VARCHAR(6),''), TERMINAL_ID=CONVERT(VARCHAR(6),''),       
INSTRU_NAME = INST_TYPE, SYMBOL,       
SERIES = CONVERT(VARCHAR(2), ''), EXPIRYDATE=REPLACE(CONVERT(VARCHAR,EXPIRYDATE,106),' ', '-'),       
STRIKE_PRICE, OPTION_TYPE,      
BUYQTY = (CASE WHEN SUM(PQTY-SQTY) > 0 THEN SUM(PQTY-SQTY) ELSE 0 END),      
BUYVALUE = CONVERT(NUMERIC(18,4),0),      
SELLQTY = (CASE WHEN SUM(SQTY-PQTY) > 0 THEN SUM(SQTY-PQTY) ELSE 0 END),       
SELLVALUE = CONVERT(NUMERIC(18,4),0)      
INTO #NFO_POS FROM NSEFO.DBO.FOBILLVALAN      
WHERE SAUDA_DATE LIKE @FILEDATE + '%'      
AND EXPIRYDATE > @FILEDATE + ' 23:59'      
GROUP BY PARTY_CODE, INST_TYPE, SYMBOL, EXPIRYDATE, STRIKE_PRICE, OPTION_TYPE      
HAVING SUM(PQTY-SQTY) <> 0      
      
UPDATE #NFO_POS SET BUYVALUE = CL_RATE*BUYQTY, SELLVALUE = CL_RATE*SELLQTY      
FROM NSEFO.DBO.FOCLOSING F      
WHERE       
--F.TRADE_DATE LIKE @FILEDATE + '%'      
F.TRADE_DATE = (       
  SELECT MAX(TRADE_DATE) FROM NSEFO.DBO.FOCLOSING      
  WHERE NSEFO.DBO.FOCLOSING.TRADE_DATE <= @FILEDATE + ' 23:59'      
   AND NSEFO.DBO.FOCLOSING.INST_TYPE = #NFO_POS.INSTRU_NAME      
   AND NSEFO.DBO.FOCLOSING.SYMBOL = #NFO_POS.SYMBOL      
   AND REPLACE(CONVERT(VARCHAR,NSEFO.DBO.FOCLOSING.EXPIRYDATE,106),' ', '-') = #NFO_POS.EXPIRYDATE      
   AND NSEFO.DBO.FOCLOSING.STRIKE_PRICE = #NFO_POS.STRIKE_PRICE      
    AND NSEFO.DBO.FOCLOSING.OPTION_TYPE = #NFO_POS.OPTION_TYPE   
AND NSEFO.DBO.FOCLOSING.CL_RATE > 0  
  )       
AND F.INST_TYPE = #NFO_POS.INSTRU_NAME      
AND F.SYMBOL = #NFO_POS.SYMBOL      
AND REPLACE(CONVERT(VARCHAR,F.EXPIRYDATE,106),' ', '-') = #NFO_POS.EXPIRYDATE      
AND F.STRIKE_PRICE = #NFO_POS.STRIKE_PRICE      
AND F.OPTION_TYPE = #NFO_POS.OPTION_TYPE 
      
UPDATE #NFO_POS SET GROUP_ID = T.GROUPID, TERMINAL_ID = T.USERID      
FROM MSAJAG.DBO.TBL_FTUSERLST T      
WHERE #NFO_POS.PARTY_CODE = T.PARTY_CODE      
      
