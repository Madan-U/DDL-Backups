-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------



CREATE PROC CALCULATE_BROKERAGE
	@DATA VARCHAR(2000)
AS
/*
	BEGIN TRAN
	EXEC CALCULATE_BROKERAGE 'ISettleMent,0000479,Z02153,,SYNDIBANK,EQ,04/08/2006,,,0,2,2,65.0787,64.9201,64.9310'
	EXEC CALCULATE_BROKERAGE 'ISettleMent,0560840,Z02153,,SYNDIBANK,EQ,04/08/2006,,,0,2,2,65.4807,64.9200,64.9100,INST           ,N|'
	ROLLBACK
*/
	DECLARE
		@TABLENAME VARCHAR(30),
		@CONTRACTNO VARCHAR(7),
		@PARTYCODE VARCHAR(10),
		@PARTICIPANT VARCHAR(30),
		@TERMID VARCHAR(30),
		@SCRIPCD VARCHAR(10),
		@SERIES VARCHAR(2),
		@SAUDADATE VARCHAR(10),
		@ORDERNO VARCHAR(16),
		@TRADENO VARCHAR(14),
		@SELLBUY VARCHAR(1),
		@MKTRATE VARCHAR(20),
		@NEWNETRATE NUMERIC(18, 4),
		@OLDNETRATE NUMERIC(18, 4),
		@OPTION VARCHAR(1),
		@LEVEL INT,
		@SQL VARCHAR(2000),
		@STMETHOD VARCHAR(10),
		@STVAL NUMERIC(18, 4),
		@BROKERAGE NUMERIC(18, 4),
		@NEWST NUMERIC(18, 4),
		@GROSSVAL NUMERIC(18, 4),
		@MKTROUND TINYINT,
		@NETROUND TINYINT,
		@BRKROUND TINYINT


		SET @MKTROUND = 2; SET @NETROUND = 2; SET @BRKROUND = 2

		SET @LEVEL = CONVERT(INT, LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 11))))

		SET @TABLENAME = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 1)))
		SET @CONTRACTNO = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 2)))
		SET @PARTYCODE = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 3)))
		SET @TERMID = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 4)))
		SET @SCRIPCD = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 5)))
		SET @SERIES = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 6)))
		SET @SAUDADATE = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 7)))
		IF @LEVEL = 3 OR @LEVEL = 4
			BEGIN
				SET @ORDERNO = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 8)))
			END
		IF @LEVEL = 4
			BEGIN
				SET @TRADENO = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 9)))
			END
		SET @SELLBUY = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 12)))
		SET @OPTION = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 10)))
		SET @MKTRATE = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 13)))
		SET @NEWNETRATE = CONVERT(NUMERIC(18, 4), LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 14))))
		SET @OLDNETRATE = CONVERT(NUMERIC(18, 4), LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 15))))
		SET @PARTICIPANT = LTRIM(RTRIM(.dbo.PIECE(@DATA, ',', 16)))
		SET @BROKERAGE = ABS(CONVERT(NUMERIC(18, 4), @MKTRATE) - @NEWNETRATE)
		SELECT @STVAL = SERVICE_TAX FROM GLOBALS WHERE CONVERT(DATETIME, @SAUDADATE, 103) BETWEEN YEAR_START_DT AND YEAR_END_DT
		IF @STVAL IS NULL
			BEGIN
				SET @STVAL = 0
			END
		SELECT TOP 1 @STMETHOD = LTRIM(RTRIM(CONVERT(VARCHAR, SERVICE_CHRG))) + '-' + LTRIM(RTRIM(CONVERT(VARCHAR, SERTAXMETHOD))) FROM CLIENT2 WHERE CL_CODE = @PARTYCODE
		SELECT @MKTROUND = ISNULL(MARKETRATE, 2), @NETROUND = ISNULL(NETRATE, 2), @BRKROUND = ISNULL(BROKERAGE, 2)
		FROM INSTCLIENT_TBL (NOLOCK) WHERE PARTYCODE = @PARTYCODE
		IF @STMETHOD = '1-1'	-- SERVIEC TAX IS 0
			BEGIN
				SET @SQL = "UPDATE " + @TABLENAME + " SET DUMMY2 = 1, MARKETRATE = " + @MKTRATE + ", "
				SET @SQL = @SQL + "NETRATE = " + CONVERT(VARCHAR(23), @NEWNETRATE) + ", N_NETRATE = " + CONVERT(VARCHAR(23), @NEWNETRATE) + ", "
				SET @SQL = @SQL + "BROKAPPLIED = " + CONVERT(VARCHAR(23), @BROKERAGE) + ", NBROKAPP = " + CONVERT(VARCHAR(23), @BROKERAGE) + ", "
				SET @SQL = @SQL + "SERVICE_TAX = 0, NSERTAX = 0, STATUS = 'E' "
				SET @SQL = @SQL + "WHERE PARTY_CODE = '" + @PARTYCODE + "' "
				SET @SQL = @SQL + "AND SCRIP_CD = '" + @SCRIPCD + "' AND SERIES = '" + @SERIES + "' "
				SET @SQL = @SQL + "AND SELL_BUY = " + CONVERT(VARCHAR(1), @SELLBUY) + " AND CONTRACTNO = '" + @CONTRACTNO + "' "
				SET @SQL = @SQL + "AND PARTIPANTCODE = '" + @PARTICIPANT + "' "
				SET @SQL = @SQL + "AND CONVERT(VARCHAR(10), SAUDA_DATE, 103) = '" + @SAUDADATE + "' "
				IF @TERMID <> ''
					BEGIN
						SET @SQL = @SQL + "AND USER_ID = '" + @TERMID + "' "
					END
				IF @LEVEL = 3
					BEGIN
						SET @SQL = @SQL + "AND ORDER_NO = '" + @ORDERNO + "' "
					END
				IF @LEVEL = 4
					BEGIN
						SET @SQL = @SQL + "AND ORDER_NO = '" + @ORDERNO + "' "
						SET @SQL = @SQL + "AND TRADE_NO = '" + @TRADENO + "' "
					END
			END
		ELSE IF @STMETHOD = '1-0'	-- SERVIEC TAX IS INCLUDED IN BROKERAGE, NEED TO SEPARATE
			BEGIN
				SET @GROSSVAL = 100 + @STVAL
				SET @BROKERAGE = ((@BROKERAGE * 100) / @GROSSVAL)
				SET @NEWNETRATE = CONVERT(NUMERIC(18, 4), @MKTRATE) + CASE @SELLBUY WHEN 1 THEN (@BROKERAGE) ELSE (-@BROKERAGE) END
				SET @NEWNETRATE = ROUND(@NEWNETRATE, @NETROUND)
				SET @SQL = "UPDATE " + @TABLENAME + " SET DUMMY2 = 1, MARKETRATE = " + @MKTRATE + ", "
				SET @SQL = @SQL + "NETRATE = " + CONVERT(VARCHAR(23), @NEWNETRATE) + ", N_NETRATE = " + CONVERT(VARCHAR(23), @NEWNETRATE) + ", "
				SET @SQL = @SQL + "BROKAPPLIED = " + CONVERT(VARCHAR(23), @BROKERAGE) + ", NBROKAPP = " + CONVERT(VARCHAR(23), @BROKERAGE) + ", "
				SET @SQL = @SQL + "SERVICE_TAX = (TRADEQTY * " + CONVERT(VARCHAR(23), (@BROKERAGE * @STVAL/100)) + "), NSERTAX = (TRADEQTY * " + CONVERT(VARCHAR(23), (@BROKERAGE * @STVAL/100)) + "), STATUS = 'E' "
				SET @SQL = @SQL + "WHERE PARTY_CODE = '" + @PARTYCODE + "' "
				SET @SQL = @SQL + "AND SCRIP_CD = '" + @SCRIPCD + "' AND SERIES = '" + @SERIES + "' "
				SET @SQL = @SQL + "AND SELL_BUY = " + CONVERT(VARCHAR(1), @SELLBUY) + " AND CONTRACTNO = '" + @CONTRACTNO + "' "
				SET @SQL = @SQL + "AND PARTIPANTCODE = '" + @PARTICIPANT + "' "
				SET @SQL = @SQL + "AND CONVERT(VARCHAR(10), SAUDA_DATE, 103) = '" + @SAUDADATE + "' "
				IF @TERMID <> ''
					BEGIN
						SET @SQL = @SQL + "AND USER_ID = '" + @TERMID + "' "
					END
				IF @LEVEL = 3
					BEGIN
						SET @SQL = @SQL + "AND ORDER_NO = '" + @ORDERNO + "' "
					END
				IF @LEVEL = 4
					BEGIN
						SET @SQL = @SQL + "AND ORDER_NO = '" + @ORDERNO + "' "
						SET @SQL = @SQL + "AND TRADE_NO = '" + @TRADENO + "' "
					END
			END
		ELSE	-- CALULATE SERVIEC TAX NORMALLY
			BEGIN
				SET @NEWST = ((@BROKERAGE * @STVAL) / 100)
				SET @SQL = "UPDATE " + @TABLENAME + " SET DUMMY2 = 1, MARKETRATE = " + @MKTRATE + ", "
				SET @SQL = @SQL + "NETRATE = " + CONVERT(VARCHAR(23), @NEWNETRATE) + ", N_NETRATE = " + CONVERT(VARCHAR(23), @NEWNETRATE) + ", "
				SET @SQL = @SQL + "BROKAPPLIED = " + CONVERT(VARCHAR(23), @BROKERAGE) + ", NBROKAPP = " + CONVERT(VARCHAR(23), @BROKERAGE) + ", "
				SET @SQL = @SQL + "SERVICE_TAX = (TRADEQTY * " + CONVERT(VARCHAR(23), ((@BROKERAGE * @STVAL) / 100)) + "), NSERTAX = (TRADEQTY * " + CONVERT(VARCHAR(23), ((@BROKERAGE * @STVAL) / 100)) + "), STATUS = 'E' "
				SET @SQL = @SQL + "WHERE PARTY_CODE = '" + @PARTYCODE + "' "
				SET @SQL = @SQL + "AND SCRIP_CD = '" + @SCRIPCD + "' AND SERIES = '" + @SERIES + "' "
				SET @SQL = @SQL + "AND SELL_BUY = " + @SELLBUY + " AND CONTRACTNO = '" + @CONTRACTNO + "' "
				SET @SQL = @SQL + "AND PARTIPANTCODE = '" + @PARTICIPANT + "' "
				SET @SQL = @SQL + "AND CONVERT(VARCHAR(10), SAUDA_DATE, 103) = '" + @SAUDADATE + "' "
				IF @TERMID <> ''
					BEGIN
						SET @SQL = @SQL + "AND USER_ID = '" + @TERMID + "' "
					END
				IF @LEVEL = 3
					BEGIN
						SET @SQL = @SQL + "AND ORDER_NO = '" + @ORDERNO + "' "
					END
				IF @LEVEL = 4
					BEGIN
						SET @SQL = @SQL + "AND ORDER_NO = '" + @ORDERNO + "' "
						SET @SQL = @SQL + "AND TRADE_NO = '" + @TRADENO + "' "
					END
			END
		EXEC(@SQL)

GO
