-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC RPT_NSENET (@SETT_NO VARCHAR(7), @SETT_TYPE VARCHAR(2))
AS
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
SELECT SCRIP_CD, SERIES,
BUYQTY = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY ELSE 0 END),
BUYAMT = SUM(CASE WHEN SELL_BUY = 1 THEN TRADEQTY*MARKETRATE ELSE 0 END),
SELLQTY = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY ELSE 0 END),
SELLAMT = SUM(CASE WHEN SELL_BUY = 2 THEN TRADEQTY*MARKETRATE ELSE 0 END)
FROM SETTLEMENT
WHERE SETT_NO = @SETT_NO
AND SETT_TYPE = @SETT_TYPE
AND TRADEQTY > 0 
GROUP BY SCRIP_CD, SERIES
ORDER BY SCRIP_CD, SERIES

GO
