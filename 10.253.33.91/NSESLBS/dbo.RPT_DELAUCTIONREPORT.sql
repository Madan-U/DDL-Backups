-- Object: PROCEDURE dbo.RPT_DELAUCTIONREPORT
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC RPT_DELAUCTIONREPORT 
(@STATUSID VARCHAR(20),
 @STATUSNAME VARCHAR(25),
 @FROMDATE VARCHAR(11),
 @TODATE VARCHAR(11),
 @BRANCH_CD VARCHAR(10),
 @SUB_BROKER VARCHAR(10),
 @FROMPARTY VARCHAR(10),
 @TOPARTY VARCHAR(10)
)
AS

DECLARE @EXCHG VARCHAR(3)

SELECT @EXCHG = EXCHANGECODE FROM OWNER

SELECT C1.BRANCH_CD, SUB_BROKER, TRADER, S.PARTY_CODE, C1.LONG_NAME, 
S.SCRIP_CD, S.SERIES, SCRIP_NAME =  CONVERT(VARCHAR(50),''),
SELL_BUY = (CASE WHEN S.SELL_BUY = 1 THEN 'B' ELSE 'S' END), POSQTY=D.QTY,
LPQTY   = SUM(CASE WHEN S.AUCTIONPART = 'FL' THEN TRADEQTY ELSE 0 END),
MSQTY   = SUM(CASE WHEN S.AUCTIONPART = 'FS' THEN TRADEQTY ELSE 0 END),
CLQTY   = SUM(CASE WHEN S.AUCTIONPART = 'FC' THEN TRADEQTY ELSE 0 END),
AUCRATE = (CASE WHEN SUM(CASE WHEN S.AUCTIONPART IN ('FC', 'FS') AND MARKETRATE > 0 
			      THEN TRADEQTY 
			      ELSE 0 
	   	         END) > 0 
		THEN 
		     SUM(CASE WHEN S.AUCTIONPART IN ('FC', 'FS') AND MARKETRATE > 0 
		  	      THEN TRADEQTY*MARKETRATE 
		  	      ELSE 0 
	     	         END)/
		     SUM(CASE WHEN S.AUCTIONPART IN ('FC', 'FS') AND MARKETRATE > 0 
		  	      THEN TRADEQTY
		  	      ELSE 0 
	     	         END)
		ELSE 0
		END),
AUCAMT  = SUM(CASE WHEN S.AUCTIONPART IN ('FC', 'FS') AND MARKETRATE > 0 
		  THEN TRADEQTY*MARKETRATE 
		  ELSE 0 
	     END),
D.SETT_NO, D.SETT_TYPE, POASTATUS=CONVERT(VARCHAR(4),''),
CL_CTGR = DUMMY10, CL_STATUS,
DEBITFLAG = CONVERT(VARCHAR(20),''),
PAYINDATE=GETDATE()
INTO #AUCREPORT
FROM SETTLEMENT S, DELIVERYCLT D, CLIENT1 C1, CLIENT2 C2
WHERE S.SAUDA_DATE BETWEEN @FROMDATE AND @TODATE + ' 23:59:59'
AND S.SETT_TYPE IN ('A', 'X', 'AD', 'AC')
AND D.SETT_NO = S.SETT_NO
AND D.SETT_TYPE = S.SETT_TYPE
AND D.PARTY_CODE = S.PARTY_CODE
AND D.SCRIP_CD = S.SCRIP_CD
AND D.SERIES = S.SERIES
AND S.AUCTIONPART LIKE 'F%'
AND C1.CL_CODE = C2.CL_CODE
AND S.PARTY_CODE = C2.PARTY_CODE
AND S.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
AND C1.BRANCH_CD = (CASE WHEN @BRANCH_CD = '%' OR @BRANCH_CD = 'ALL' THEN C1.BRANCH_CD ELSE @BRANCH_CD END)  
AND SUB_BROKER = (CASE WHEN @SUB_BROKER = '%' OR @SUB_BROKER = 'ALL' THEN C1.SUB_BROKER ELSE @SUB_BROKER END)  
AND @StatusName =   
                  (case   
                        when @StatusId = 'BRANCH' then C1.branch_cd  
                        when @StatusId = 'SUBBROKER' then sub_broker  
                        when @StatusId = 'Trader' then Trader  
                        when @StatusId = 'Family' then Family  
                        when @StatusId = 'Area' then Area  
                        when @StatusId = 'Region' then Region  
                        when @StatusId = 'Client' then S.party_code  
                  else   
                        'BROKER'  
                  End)  
GROUP BY C1.BRANCH_CD, SUB_BROKER, TRADER, 
D.SETT_NO, D.SETT_TYPE, S.PARTY_CODE, C1.LONG_NAME, 
S.SCRIP_CD, S.SERIES, D.QTY, SELL_BUY, DUMMY10, CL_STATUS

IF @EXCHG = 'NSE'
BEGIN
	UPDATE #AUCREPORT SET SCRIP_NAME = LEFT(S1.LONG_NAME,50)
	FROM SCRIP1 S1, SCRIP2 S2
	WHERE S1.CO_CODE = S2.CO_CODE
	AND S1.SERIES = S2.SERIES
	AND S2.SCRIP_CD = #AUCREPORT.SCRIP_CD
	AND S2.SERIES = #AUCREPORT.SERIES
END
ELSE
BEGIN
	UPDATE #AUCREPORT SET SCRIP_NAME = LEFT(S1.LONG_NAME,50)
	FROM SCRIP1 S1, SCRIP2 S2
	WHERE S1.CO_CODE = S2.CO_CODE
	AND S1.SERIES = S2.SERIES
	AND S2.BSECODE = #AUCREPORT.SCRIP_CD
END

UPDATE #AUCREPORT SET PAYINDATE = SEC_PAYIN
FROM SETT_MST
WHERE #AUCREPORT.SETT_NO = SETT_MST.SETT_NO
AND #AUCREPORT.SETT_TYPE = SETT_MST.SETT_TYPE

UPDATE #AUCREPORT SET DEBITFLAG = (CASE WHEN P.DEBITFLAG = 0 THEN 'CHECK DEBIT'
				  WHEN P.DEBITFLAG = 1 THEN 'ALWAYS PAYOUT'	
				  ELSE 'TRANSFER TO BEN' 
			     END)
FROM DELPARTYFLAG P
WHERE P.PARTY_CODE = #AUCREPORT.PARTY_CODE

UPDATE #AUCREPORT SET POASTATUS = 'POA' --(CASE WHEN POATYPE = 'MTF' THEN 'PLAT' ELSE 'POA' END)
FROM MULTICLTID 
WHERE MULTICLTID.PARTY_CODE = #AUCREPORT.PARTY_CODE
AND DEF = 1

SELECT * FROM #AUCREPORT
ORDER BY BRANCH_CD, SUB_BROKER, TRADER, PARTY_CODE, SETT_NO, SETT_TYPE, SCRIP_NAME

DROP TABLE #AUCREPORT

GO
