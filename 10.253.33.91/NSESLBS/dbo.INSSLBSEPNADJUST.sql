-- Object: PROCEDURE dbo.INSSLBSEPNADJUST
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

---------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
CREATE  PROC [dbo].[INSSLBSEPNADJUST] 
AS

DECLARE 
@SETT_NO		VARCHAR(7),
@SETT_TYPE		VARCHAR(2),
@PARTY_CODE		VARCHAR(10),
@SCRIP_CD		VARCHAR(12),
@SERIES			VARCHAR(3),
@POSQTY			INT,
@HOLDSETTNO		VARCHAR(7),
@HOLDSETTTYPE	VARCHAR(2),
@AVLQTY			INT,
@DIFFQTY		INT,
@SNO			NUMERIC(18,0),
@ISIN			VARCHAR(12),
@MEMBERCODE		VARCHAR(15),
@BDPID			VARCHAR(8),
@BCLTDPID		VARCHAR(16),
@EXPIRYDATE		VARCHAR(11),
@DELCUR			CURSOR

SELECT @MEMBERCODE = MEMBERCODE FROM OWNER

SELECT @BDPID = DPID, @BCLTDPID = DPCLTNO FROM MSAJAG.DBO.DELIVERYDP WHERE DPTYPE = 'NSDL' AND DESCRIPTION LIKE '%POOL%'

SET @DELCUR = CURSOR FOR
	SELECT S.SETT_NO, S.SETT_TYPE, PARTY_CODE, S.SCRIP_CD, SERIES='EQ', QTY=SUM(S.TRADEQTY), 
	HOLDSETTNO=E.SETT_NO, HOLDSETTTYPE=E.SETT_TYPE, EXPIRYDATE 
	FROM TRD_SETT_POS P, SETT_MST M, SETTLEMENT S, SETT_MST E 
	WHERE S.SAUDA_DATE BETWEEN M.START_DATE AND M.END_DATE 
	AND S.SETT_NO = M.SETT_NO AND S.SETT_TYPE = M.SETT_TYPE
	AND S.SAUDA_DATE = P.SAUDA_DATE
	AND S.SCRIP_CD = P.SCRIP_CD
	AND S.SERIES = P.SERIES
	AND S.ORDER_NO = P.ORDER_NO
	AND S.TRADE_NO = P.TRADE_NO
	AND CONVERT(DATETIME,P.EXPIRYDATE) BETWEEN E.START_DATE AND E.END_DATE
	AND E.SETT_TYPE = 'P'
	--AND SEC_PAYIN >= GETDATE()
	AND P.SELL_BUY = 4
	GROUP BY S.SETT_NO, S.SETT_TYPE, PARTY_CODE, S.SCRIP_CD, E.SETT_NO, E.SETT_TYPE, EXPIRYDATE
	ORDER BY S.SETT_NO, S.SETT_TYPE, PARTY_CODE, S.SCRIP_CD, E.SETT_NO, E.SETT_TYPE
OPEN @DELCUR
FETCH NEXT FROM @DELCUR INTO @SETT_NO, @SETT_TYPE, @PARTY_CODE, @SCRIP_CD, @SERIES, @POSQTY, @HOLDSETTNO, @HOLDSETTTYPE, @EXPIRYDATE
WHILE @@FETCH_STATUS = 0
BEGIN
	SELECT @AVLQTY = 0

	SELECT @AVLQTY = ISNULL(SUM(CASE WHEN DRCR = 'C' THEN QTY ELSE -QTY END),0)
	FROM MSAJAG.DBO.DELTRANS D
	WHERE SETT_NO = @SETT_NO AND SETT_TYPE = @SETT_TYPE
	AND PARTY_CODE = @PARTY_CODE AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES
	AND FILLER2 = 1 AND DRCR = 'C' 

	SELECT @POSQTY = @POSQTY - @AVLQTY
	
	SELECT @AVLQTY = 0
			
	SELECT @AVLQTY = ISNULL(SUM(CASE WHEN DRCR = 'C' THEN QTY ELSE -QTY END),0)
	FROM MSAJAG.DBO.DELTRANS D
	WHERE SETT_NO = @HOLDSETTNO AND SETT_TYPE = @HOLDSETTTYPE
	AND PARTY_CODE = @PARTY_CODE AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES
	AND FILLER2 = 1 
	
	SET @ISIN = ''
	
	SELECT @ISIN = ISIN FROM MSAJAG.DBO.MULTIISIN WHERE SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES AND VALID = 1
	 
	IF @AVLQTY > 0 AND @POSQTY > 0
	BEGIN
		IF @AVLQTY <= @POSQTY
		BEGIN
			INSERT INTO MSAJAG.DBO.DELTRANS VALUES (@HOLDSETTNO, @HOLDSETTTYPE, 110, 1234, 907, @PARTY_CODE, @SCRIP_CD, @SERIES, @AVLQTY, 
			'1234', '1234', @ISIN, '1234', 'TRANSFER TO ' + @SETT_NO + '-' + @SETT_TYPE, 'PAYIN', 'D', 'D', @AVLQTY, '', '', '', 'HO', @MEMBERCODE,
			'0', '0', @SETT_NO, @SETT_TYPE, 'DEMAT', LEFT(GETDATE(),11), '0', 1, '0', 'NSDL', @BDPID, @BCLTDPID, '', '')
			
			INSERT INTO MSAJAG.DBO.DELTRANS VALUES (@HOLDSETTNO, @HOLDSETTTYPE, 110, 1234, 906, 'NSE', @SCRIP_CD, @SERIES, @AVLQTY, 
			'1234', '1234', @ISIN, '1234', 'TRANSFER TO ' + @SETT_NO + '-' + @SETT_TYPE, 'PAYIN', 'C', '0', @AVLQTY, '', '', '', 'HO', @MEMBERCODE,
			'0', '0', @SETT_NO, @SETT_TYPE, 'DEMAT', LEFT(GETDATE(),11), '0', 1, '0', 'NSDL', @BDPID, @BCLTDPID, '', '')			
			
			INSERT INTO MSAJAG.DBO.DELTRANS VALUES (@SETT_NO, @SETT_TYPE, 110, 1234, 906, 'NSE', @SCRIP_CD, @SERIES, @AVLQTY, 
			'1234', '1234', @ISIN, '1234', 'REC FROM ' + @HOLDSETTNO + '-' + @HOLDSETTTYPE, 'PAYIN', 'D', 'D', @AVLQTY, '', '', '', 'HO', @MEMBERCODE,
			'0', '0', '', '', 'DEMAT', LEFT(GETDATE(),11), '0', 1, '0', 'NSDL', @BDPID, @BCLTDPID, '', '')	
			
			INSERT INTO MSAJAG.DBO.DELTRANS VALUES (@SETT_NO, @SETT_TYPE, 110, 1234, 907, @PARTY_CODE, @SCRIP_CD, @SERIES, @AVLQTY, 
			'1234', '1234', @ISIN, '1234', 'REC FROM ' + @HOLDSETTNO + '-' + @HOLDSETTTYPE, 'PAYIN', 'C', '0', @AVLQTY, '', '', '', 'HO', @MEMBERCODE,
			'0', '0', '', '', 'DEMAT', LEFT(GETDATE(),11), '0', 1, '0', 'NSDL', @BDPID, @BCLTDPID, '', '')
			
			UPDATE TBL_SLBS_EPNPOS SET MARKED_QTY = MARKED_QTY - @AVLQTY 
			WHERE SETT_NO = @HOLDSETTNO AND SETT_TYPE = @HOLDSETTTYPE
			AND PARTY_CODE = @PARTY_CODE AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES
			
			EXEC PROC_NSESLBS_EPN_POS @EXPIRYDATE			
		END
		ELSE
		BEGIN
			INSERT INTO MSAJAG.DBO.DELTRANS VALUES (@HOLDSETTNO, @HOLDSETTTYPE, 110, 1234, 907, @PARTY_CODE, @SCRIP_CD, @SERIES, @POSQTY, 
			'1234', '1234', @ISIN, '1234', 'TRANSFER TO ' + @SETT_NO + '-' + @SETT_TYPE, 'PAYIN', 'D', 'D', @POSQTY, '', '', '', 'HO', @MEMBERCODE,
			'0', '0', @SETT_NO, @SETT_TYPE, 'DEMAT', LEFT(GETDATE(),11), '0', 1, '0', 'NSDL', @BDPID, @BCLTDPID, '', '')
			
			INSERT INTO MSAJAG.DBO.DELTRANS VALUES (@HOLDSETTNO, @HOLDSETTTYPE, 110, 1234, 906, 'NSE', @SCRIP_CD, @SERIES, @POSQTY, 
			'1234', '1234', @ISIN, '1234', 'TRANSFER TO ' + @SETT_NO + '-' + @SETT_TYPE, 'PAYIN', 'C', '0', @POSQTY, '', '', '', 'HO', @MEMBERCODE,
			'0', '0', @SETT_NO, @SETT_TYPE, 'DEMAT', LEFT(GETDATE(),11), '0', 1, '0', 'NSDL', @BDPID, @BCLTDPID, '', '')

			INSERT INTO MSAJAG.DBO.DELTRANS VALUES (@SETT_NO, @SETT_TYPE, 110, 1234, 906, 'NSE', @SCRIP_CD, @SERIES, @POSQTY, 
			'1234', '1234', @ISIN, '1234', 'REC FROM ' + @HOLDSETTNO + '-' + @HOLDSETTTYPE, 'PAYIN', 'D', 'D', @POSQTY, '', '', '', 'HO', @MEMBERCODE,
			'0', '0', '', '', 'DEMAT', LEFT(GETDATE(),11), '0', 1, '0', 'NSDL', @BDPID, @BCLTDPID, '', '')	
			
			INSERT INTO MSAJAG.DBO.DELTRANS VALUES (@SETT_NO, @SETT_TYPE, 110, 1234, 907, @PARTY_CODE, @SCRIP_CD, @SERIES, @POSQTY, 
			'1234', '1234', @ISIN, '1234', 'REC FROM ' + @HOLDSETTNO + '-' + @HOLDSETTTYPE, 'PAYIN', 'C', '0', @POSQTY, '', '', '', 'HO', @MEMBERCODE,
			'0', '0', '', '', 'DEMAT', LEFT(GETDATE(),11), '0', 1, '0', 'NSDL', @BDPID, @BCLTDPID, '', '')									
			
			UPDATE TBL_SLBS_EPNPOS SET MARKED_QTY = MARKED_QTY - @POSQTY 
			WHERE SETT_NO = @HOLDSETTNO AND SETT_TYPE = @HOLDSETTTYPE
			AND PARTY_CODE = @PARTY_CODE AND SCRIP_CD = @SCRIP_CD AND SERIES = @SERIES
			
			EXEC PROC_NSESLBS_EPN_POS @EXPIRYDATE
		END
	END
	FETCH NEXT FROM @DELCUR INTO @SETT_NO, @SETT_TYPE, @PARTY_CODE, @SCRIP_CD, @SERIES, @POSQTY, @HOLDSETTNO, @HOLDSETTTYPE, @EXPIRYDATE
END
CLOSE @DELCUR
DEALLOCATE @DELCUR

GO
