-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC CBO_NEWGETPARTYMAPPING
	@OLDPARTY_CODE VARCHAR(12) = '',
  @NEWPARTY_CODE VARCHAR(12)='',
	@STATUSID VARCHAR(25) = 'BROKER',
	@STATUSNAME VARCHAR(25) = 'BROKER'
AS
	IF @STATUSID <> 'BROKER'
		BEGIN
			RAISERROR ('This Procedure is accessible to Broker', 16, 1)
			RETURN
		END
    IF (@OLDPARTY_CODE = '' OR @OLDPARTY_CODE = '%')and(@NEWPARTY_CODE = '' OR @NEWPARTY_CODE = '%')
	   BEGIN
			SELECT
				OLDPARTY_CODE,
	      NEWPARTY_CODE	
			FROM
				PARTYMAPPING
			ORDER BY
				OLDPARTY_CODE
		END
   ELSE IF @OLDPARTY_CODE <> ''
		BEGIN
			SELECT
			 OLDPARTY_CODE,
       NEWPARTY_CODE		
			FROM
				PARTYMAPPING
			WHERE
				OLDPARTY_CODE LIKE @OLDPARTY_CODE + '%'
			ORDER BY
				OLDPARTY_CODE
		END
   Else
      BEGIN
			SELECT
			 OLDPARTY_CODE,
       NEWPARTY_CODE 
			FROM
				PARTYMAPPING
			WHERE
				NEWPARTY_CODE LIKE @NEWPARTY_CODE + '%'
			ORDER BY
				NEWPARTY_CODE
		END

GO
