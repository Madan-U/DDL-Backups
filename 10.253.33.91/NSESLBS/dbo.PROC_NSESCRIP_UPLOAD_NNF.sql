CREATE PROC [dbo].[PROC_NSESCRIP_UPLOAD_NNF](@FILEDATE VARCHAR(11), @FILEPATH VARCHAR(150)) AS          
      
-- EXEC PROC_NSESCRIP_UPLOAD_NNF 'JAN 13 2015', 'C:\TEMP\nnf_security.DAT'      
DECLARE @CMD VARCHAR(500)      
      
SELECT FILE_DATA INTO #FILEDATA FROM FILEDATA WHERE 1 = 2       
      
SET @CMD = 'BULK INSERT #FILEDATA FROM ''' + @FilePath + ''' WITH  (FIELDTERMINATOR = '','', ROWTERMINATOR = ''' + Char(10) + ''', FirstRow = 2 )'      
EXEC (@CMD)      
      
IF (SELECT ISNULL(COUNT(1),0) FROM SYSOBJECTS WHERE NAME = 'SCRIP_IMP_TMP') > 0       
 DROP TABLE SCRIP_IMP_TMP       
      
SELECT SCRIP_CD=CONVERT(VARCHAR(12),''), SERIES=CONVERT(VARCHAR(3),''),       
    SCRIP_NAME=CONVERT(VARCHAR(50),''), FILLER1=CONVERT(VARCHAR(12),''),       
    ISIN=CONVERT(VARCHAR(12),'')       
INTO SCRIP_IMP_TMP WHERE 1 = 2       
      
INSERT INTO SCRIP_IMP_TMP      
SELECT SCRIP_CD=.DBO.PIECE(FILE_DATA,'|',2),      
    SERIES=.DBO.PIECE(FILE_DATA,'|',3),      
    SCRIP_NAME=LEFT(REPLACE(.DBO.PIECE(FILE_DATA,'|',18),'''',''),50),      
       ACTIVE_STATUS =  .DBO.PIECE(FILE_DATA,'|',8),      
    ISIN=.DBO.PIECE(FILE_DATA,'|',54)      
FROM #FILEDATA      
      
DROP TABLE #FILEDATA      
      
DELETE FROM SCRIP_IMP_TMP      
WHERE SERIES = 'GC'    
      
Exec NSE_SCRIPUPLOAD '', ''

GO
