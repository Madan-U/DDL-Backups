CREATE PROC Rpt_NRI_DetailRec (
@SAUDA_DATE VARCHAR(11), 
@PARTY_CODE VARCHAR(10),
@CONTRACTNO VARCHAR(7),
@SELL_BUY   VARCHAR(1) )
AS

SELECT RECTYPE='T', PARTY_CODE, CONTRACTNO, TRTYPE = (CASE WHEN SELL_BUY = 1 THEN 'P' ELSE 'S' END),
ISIN = ISNULL(ISIN,''), QTY = TRADEQTY, MARKETRATE = CONVERT(NUMERIC(18,4), MARKETRATE), 
BROKERAGE = CONVERT(NUMERIC(18,4),TRADEQTY*NBROKAPP), 
AMOUNT = TRADEQTY * (CONVERT(NUMERIC(18,4), MARKETRATE) + 
		    (CASE WHEN SELL_BUY = 1 
			  THEN CONVERT(NUMERIC(18,4),TRADEQTY*NBROKAPP)
			  ELSE -CONVERT(NUMERIC(18,4),TRADEQTY*NBROKAPP)
		     END))
FROM  GLOBALS G, SETTLEMENT S LEFT OUTER JOIN MULTIISIN M 
ON (M.SCRIP_CD = S.SCRIP_CD AND M.SERIES = S.SERIES AND VALID = 1) 
WHERE S.SAUDA_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT AND TRADEQTY > 0 
AND AUCTIONPART NOT LIKE 'A%' AND AUCTIONPART NOT LIKE 'F%' 
AND TRADE_NO NOT LIKE '%C%'
AND SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND PARTY_CODE = @PARTY_CODE
AND CONTRACTNO = @CONTRACTNO
AND SELL_BUY = @SELL_BUY
ORDER BY REPLACE(CONVERT(VARCHAR(10),SAUDA_DATE,20),'-',''),PARTY_CODE, CONTRACTNO

GO
