-- Object: PROCEDURE dbo.VBB_BROKRECAL
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------

CREATE PROC VBB_BROKRECAL (
@SAUDA_DATE VARCHAR(11), @FORPARTY VARCHAR(10))
AS

DECLARE @BUYBROK NUMERIC(18,4),
@SELLBROK NUMERIC(18,4),
@BROKCUR CURSOR,
@CONTRACTNO VARCHAR(7), 
@SETT_TYPE VARCHAR(2), 
@SELL_BUY VARCHAR(1), 
@DIFF NUMERIC(18,4),
@TRADE_NO VARCHAR(20),
@TRADEQTY INT,
@SERVICE_TAX NUMERIC(18,4),
@TCUR CURSOR

SELECT @SERVICE_TAX = SERVICE_TAX FROM GLOBALS
WHERE @SAUDA_DATE BETWEEN YEAR_START_DT AND YEAR_END_DT

SELECT @BUYBROK = SETT_PURCH, @SELLBROK = SETT_SALES
FROM BROKTABLE B, CLIENTBROK_SCHEME C 
WHERE C.PARTY_CODE = @FORPARTY AND C.TABLE_NO = B.TABLE_NO
AND @SAUDA_DATE BETWEEN C.FROM_DATE AND C.TO_DATE
AND TRADE_TYPE = 'INS' AND SCHEME_TYPE = 'TRD'

SELECT CONTRACTNO, SETT_TYPE, SELL_BUY,
TOTALBROKERAGE=SUM(TRADEQTY*NBROKAPP), 
TOCHARGE = ROUND(SUM(TRADEQTY*(CASE WHEN SELL_BUY = 1 
				    THEN MARKETRATE*@BUYBROK 
				    ELSE MARKETRATE*@SELLBROK 
			       END))/100,2),
QTY = SUM(TRADEQTY)
INTO #BROK FROM ISETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND SETT_TYPE IN ('W', 'N', 'C', 'D')
AND PARTY_CODE = @FORPARTY
AND STATUS <> 'E'
AND TRADE_NO NOT LIKE '%C%'
AND TRADEQTY > 0 
GROUP BY CONTRACTNO, SETT_TYPE, SELL_BUY

UPDATE ISETTLEMENT SET BROKAPPLIED = TOCHARGE/QTY, NBROKAPP = TOCHARGE/QTY,
SERVICE_TAX = (TOCHARGE/QTY)*TRADEQTY*@SERVICE_TAX/100,
NSERTAX = (TOCHARGE/QTY)*TRADEQTY*@SERVICE_TAX/100,
NETRATE = (CASE WHEN ISETTLEMENT.SELL_BUY = 1 
		THEN MARKETRATE+(TOCHARGE/QTY)
		ELSE MARKETRATE-(TOCHARGE/QTY)
	   END),
N_NETRATE = (CASE WHEN ISETTLEMENT.SELL_BUY = 1 
		THEN MARKETRATE+(TOCHARGE/QTY)
		ELSE MARKETRATE-(TOCHARGE/QTY)
	   END)
FROM #BROK B
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND ISETTLEMENT.SETT_TYPE IN ('W', 'N', 'C', 'D')
AND PARTY_CODE = @FORPARTY
AND STATUS <> 'E'
AND TRADE_NO NOT LIKE '%C%'
AND ISETTLEMENT.SETT_TYPE = B.SETT_TYPE
AND ISETTLEMENT.CONTRACTNO = B.CONTRACTNO
AND ISETTLEMENT.SELL_BUY = B.SELL_BUY
AND TOTALBROKERAGE > TOCHARGE
AND TRADEQTY > 0 

SELECT CONTRACTNO, SETT_TYPE, SELL_BUY,
TOTALBROKERAGE=SUM(TRADEQTY*NBROKAPP), 
TOCHARGE = ROUND(SUM(TRADEQTY*(CASE WHEN SELL_BUY = 1 
				    THEN MARKETRATE*@BUYBROK 
				    ELSE MARKETRATE*@SELLBROK 
			       END))/100,2),
QTY = SUM(TRADEQTY)
INTO #BROK_1 FROM ISETTLEMENT
WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
AND SETT_TYPE IN ('W', 'N', 'C', 'D')
AND PARTY_CODE = @FORPARTY
AND STATUS <> 'E'
AND TRADE_NO NOT LIKE '%C%'
AND TRADEQTY > 0 
GROUP BY CONTRACTNO, SETT_TYPE, SELL_BUY

SELECT * FROM #BROK_1

SET @BROKCUR = CURSOR FOR
SELECT CONTRACTNO, SETT_TYPE, SELL_BUY, DIFF=TOCHARGE-TOTALBROKERAGE FROM #BROK_1
WHERE TOTALBROKERAGE < TOCHARGE
ORDER BY CONTRACTNO
OPEN @BROKCUR
FETCH NEXT FROM @BROKCUR INTO @CONTRACTNO, @SETT_TYPE, @SELL_BUY, @DIFF
WHILE @@FETCH_STATUS = 0
BEGIN 
	SET @TCUR = CURSOR FOR
	SELECT TOP 1 TRADE_NO, TRADEQTY FROM ISETTLEMENT
	WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
	AND SETT_TYPE = @SETT_TYPE
	AND PARTY_CODE = @FORPARTY
	AND STATUS <> 'E'
	AND TRADE_NO NOT LIKE '%C%'
	AND CONTRACTNO = @CONTRACTNO
	AND SELL_BUY = @SELL_BUY
	AND TRADEQTY > 0 
	ORDER BY TRADEQTY ASC
	OPEN @TCUR
	FETCH NEXT FROM @TCUR INTO @TRADE_NO, @TRADEQTY
	IF @TRADEQTY = 1 
	BEGIN 
		UPDATE ISETTLEMENT SET BROKAPPLIED = BROKAPPLIED+@DIFF, NBROKAPP = NBROKAPP+@DIFF, 
		NETRATE =   (CASE WHEN SELL_BUY = 1 
				  THEN MARKETRATE+BROKAPPLIED+@DIFF
				  ELSE MARKETRATE-BROKAPPLIED-@DIFF
			     END),
		N_NETRATE = (CASE WHEN SELL_BUY = 1 
				  THEN MARKETRATE+NBROKAPP+@DIFF
				  ELSE MARKETRATE-NBROKAPP-@DIFF
			     END),
		SERVICE_TAX = TRADEQTY*(BROKAPPLIED+@DIFF)*@SERVICE_TAX/100,
		NSERTAX = TRADEQTY*(NBROKAPP+@DIFF)*@SERVICE_TAX/100
		WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
		AND SETT_TYPE = @SETT_TYPE
		AND PARTY_CODE = @FORPARTY
		AND STATUS <> 'E'
		AND TRADE_NO NOT LIKE '%C%'
		AND CONTRACTNO = @CONTRACTNO
		AND SELL_BUY = @SELL_BUY
		AND TRADE_NO = @TRADE_NO
		AND TRADEQTY = @TRADEQTY
	END
	ELSE
	BEGIN
		Insert into iSettlement
		SELECT Contractno,Billno,'S'+Trade_No,Party_Code,Scrip_Cd,User_Id,Tradeqty=1,Auctionpart,
		Markettype,Series,Order_No,Marketrate,Sauda_Date,Table_No,Line_No,Val_Perc,Normal,
		Day_Puc,Day_Sales,Sett_Purch,Sett_Sales,Sell_Buy,Settflag,
		Brokapplied=Brokapplied+@Diff,
		NETRATE =   (CASE WHEN SELL_BUY = 1 
				  THEN MARKETRATE+BROKAPPLIED+@DIFF
				  ELSE MARKETRATE-BROKAPPLIED-@DIFF
			     END),
		Amount,Ins_Chrg,Turn_Tax=Turn_Tax/Tradeqty,Other_Chrg=Other_Chrg/Tradeqty,
		Sebi_Tax=Sebi_Tax/Tradeqty,Broker_Chrg=Broker_Chrg/Tradeqty,
		Service_Tax=(BROKAPPLIED+@DIFF)*@SERVICE_TAX/100,
		Trade_Amount,Billflag,Sett_No,Nbrokapp=Nbrokapp+@Diff,
		Nsertax=(Nbrokapp+@DIFF)*@SERVICE_TAX/100,
		N_Netrate=(CASE WHEN SELL_BUY = 1 
				  THEN MARKETRATE+NBROKAPP+@DIFF
				  ELSE MARKETRATE-NBROKAPP-@DIFF
			     END),
		Sett_Type,Partipantcode,Status,Pro_Cli,Cpid,Instrument,
		Booktype,Branch_Id,Tmark,Scheme,Dummy1,Dummy2 FROM ISETTLEMENT
		WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
		AND SETT_TYPE = @SETT_TYPE
		AND PARTY_CODE = @FORPARTY
		AND STATUS <> 'E'
		AND TRADE_NO NOT LIKE '%C%'
		AND CONTRACTNO = @CONTRACTNO
		AND SELL_BUY = @SELL_BUY
		AND TRADE_NO = @TRADE_NO
		AND TRADEQTY = @TRADEQTY

		UPDATE ISETTLEMENT SET TRADEQTY = TRADEQTY - 1,
		SERVICE_TAX = (TRADEQTY-1)*BROKAPPLIED*@SERVICE_TAX/100,
		NSERTAX = (TRADEQTY-1)*NBROKAPP*@SERVICE_TAX/100,
		Turn_Tax=(TRADEQTY-1)*(Turn_Tax/Tradeqty),Other_Chrg=(TRADEQTY-1)*(Other_Chrg/Tradeqty),
		Sebi_Tax=(TRADEQTY-1)*(Sebi_Tax/Tradeqty),Broker_Chrg=(TRADEQTY-1)*(Broker_Chrg/Tradeqty)
		WHERE SAUDA_DATE LIKE @SAUDA_DATE + '%'
		AND SETT_TYPE = @SETT_TYPE
		AND PARTY_CODE = @FORPARTY
		AND STATUS <> 'E'
		AND TRADE_NO NOT LIKE '%C%'
		AND CONTRACTNO = @CONTRACTNO
		AND SELL_BUY = @SELL_BUY
		AND TRADE_NO = @TRADE_NO
		AND TRADEQTY = @TRADEQTY		
	END
	CLOSE @TCUR
	DEALLOCATE @TCUR
	FETCH NEXT FROM @BROKCUR INTO @CONTRACTNO, @SETT_TYPE, @SELL_BUY, @DIFF
END
CLOSE @BROKCUR
DEALLOCATE @BROKCUR

DROP TABLE #BROK
DROP TABLE #BROK_1

GO
