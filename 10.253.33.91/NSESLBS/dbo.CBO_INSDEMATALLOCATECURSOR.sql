-- Object: PROCEDURE dbo.CBO_INSDEMATALLOCATECURSOR
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE PROC CBO_INSDEMATALLOCATECURSOR (@REFNO INT) AS      
DECLARE      
 @@SERIALNO NUMERIC(18,0),       
 @@SNO VARCHAR(7),      
 @@STYPE VARCHAR(2),      
 @@PARTYCD VARCHAR(10),      
 @@SCRIPCD VARCHAR(12),      
 @@SERIES VARCHAR(3),      
 @@BANKCODE VARCHAR(30),      
 @@ISIN VARCHAR(12),      
 @@CLTACCNO VARCHAR(16),      
 @@TRANSNO VARCHAR(15),      
 @@INOUT VARCHAR(1),      
 @@DEMATQTY INT,      
 @@EXECDATE VARCHAR(20),      
 @@CERTQTY INT,      
 @@DELIVERQTY INT,      
 @@REMQTY INT,      
 @@EXCESSQTY INT,      
 @@BRANCHCD VARCHAR(10),               
 @@PARTIPANTCODE VARCHAR(10),        
 @@TCODE NUMERIC(18,0),      
 @@TRTYPE NUMERIC(18,0),      
 @@DPTYPE VARCHAR(10),      
 @@DPNAME VARCHAR(25),      
 @@DEMATCUR CURSOR,       
 @@DEMATMOV CURSOR,      
 @@BDPTYPE VARCHAR(5),      
 @@BDPID VARCHAR(16),      
 @@BCLTDPID VARCHAR(16),      
 @@FILLER1 VARCHAR(100)      
      
SET NOCOUNT ON       
      
DELETE FROM DEMATTRANS WHERE DRCR = 'D' AND DPID <> 'PAY-OUT' AND REFNO = @REFNO      
      
UPDATE DEMATTRANS SET DPID = LEFT(CLTACCNO,8) FROM DEMATTRANS WHERE LEN(CLTACCNO) = 16      
AND DPID <> LEFT(CLTACCNO,8)      
      
UPDATE DEMATTRANS SET DPNAME = '' WHERE DPNAME IS NULL      
      
UPDATE DEMATTRANS SET SCRIP_CD = S2.SCRIP_CD,SERIES=S2.SERIES        
FROM MULTIISIN S2, DEMATTRANS D WHERE S2.ISIN = D.ISIN AND REFNO = @REFNO   
AND VALID = 1 
      
EXEC INSDELSETTNOUPDATE @REFNO               
TRUNCATE TABLE DELTRANS_PROCESS
TRUNCATE TABLE BSEDB.DBO.DELTRANS_PROCESS

EXEC INSDELINTERSETTUPDATE @REFNO      
TRUNCATE TABLE DELTRANS_PROCESS
TRUNCATE TABLE BSEDB.DBO.DELTRANS_PROCESS

EXEC INSDELINTERSETTUPDATEINDPEXBEN      
TRUNCATE TABLE DELTRANS_PROCESS
TRUNCATE TABLE BSEDB.DBO.DELTRANS_PROCESS

EXEC INSDELINTERSETTUPDATEINEX       
TRUNCATE TABLE DELTRANS_PROCESS
TRUNCATE TABLE BSEDB.DBO.DELTRANS_PROCESS

EXEC INSDELINTERSETTUPDATEINPOOL      
TRUNCATE TABLE DELTRANS_PROCESS
TRUNCATE TABLE BSEDB.DBO.DELTRANS_PROCESS
  
      
TRUNCATE TABLE DELTRANS_PROCESS      
      
 INSERT INTO DELTRANS_PROCESS       
 SELECT DT.*, UPDFLAG = 0       
 FROM       
  DELTRANS DT,       
  (      
    SELECT DISTINCT D.SETT_NO ,D.SETT_TYPE,D.PARTY_CODE,D.SCRIP_CD,D.SERIES      
          FROM DEMATTRANS D        
   WHERE D.PARTY_CODE NOT IN ('PARTY','BSE', 'NSE','')       
   AND ISIN IN ( SELECT ISIN FROM MULTIISIN M       
     WHERE D.ISIN = M.ISIN AND D.SCRIP_CD = M.SCRIP_CD       
     AND D.SERIES = M.SERIES AND M.VALID = 1 )       
  ) N       
  WHERE        
  DT.PARTY_CODE  = N.PARTY_CODE       
  AND DT.SCRIP_CD = N.SCRIP_CD      
    AND DT.SERIES = N.SERIES       
  AND DT.SETT_NO = N.SETT_NO       
    AND DT.SETT_TYPE = N.SETT_TYPE      
  AND DT.DRCR = 'C'       
  AND DT.FILLER2 = 1      
      
SET @@DEMATCUR = CURSOR FOR      
  SELECT DISTINCT SNO,D.SETT_NO ,D.SETT_TYPE,D.PARTY_CODE , D.SCRIP_CD , D.SERIES ,D.QTY ,      
 D.DPID ,D.ISIN,D.DRCR ,D.CLTACCNO, D.TRANSNO , D.TRDATE,D.BRANCH_CD,D.PARTIPANTCODE,TCODE,TRTYPE,DPTYPE,BDPTYPE,BDPID,BCLTACCNO,FILLER1,DPNAME       
             FROM DEMATTRANS D        
 WHERE D.PARTY_CODE NOT IN ('PARTY','BSE', 'NSE','') AND REFNO = @REFNO       
 AND ISIN IN ( SELECT ISIN FROM MULTIISIN M WHERE D.ISIN = M.ISIN AND D.SCRIP_CD = M.SCRIP_CD AND D.SERIES = M.SERIES AND M.VALID = 1 )       
 ORDER BY D.SETT_NO , D.SETT_TYPE ,D.SCRIP_CD       
OPEN @@DEMATCUR      
FETCH NEXT FROM @@DEMATCUR INTO @@SERIALNO,@@SNO,@@STYPE,@@PARTYCD,@@SCRIPCD,@@SERIES,@@DEMATQTY,@@BANKCODE,@@ISIN,@@INOUT,@@CLTACCNO,@@TRANSNO,@@EXECDATE,@@BRANCHCD,@@PARTIPANTCODE,@@TCODE,@@TRTYPE,@@DPTYPE,@@BDPTYPE,@@BDPID,@@BCLTDPID,@@FILLER1,@@DPNAME
  
      
WHILE @@FETCH_STATUS = 0      
BEGIN      
      
        SELECT @@CERTQTY = ISNULL(SUM(QTY),0) FROM DELTRANS_PROCESS        
   WHERE  PARTY_CODE  = @@PARTYCD AND SCRIP_CD = @@SCRIPCD  
   AND SERIES = @@SERIES AND SETT_NO = @@SNO       
   AND SETT_TYPE = @@STYPE AND REFNO = @REFNO AND DRCR = 'C'      
   AND PARTIPANTCODE = @@PARTIPANTCODE AND FILLER2 = 1      
      
     SELECT  @@DELIVERQTY = ISNULL(SUM(QTY),0) FROM DELIVERYCLT       
     WHERE  PARTY_CODE  = @@PARTYCD AND SCRIP_CD = @@SCRIPCD       
     AND SERIES = @@SERIES AND SETT_NO = @@SNO      
     AND SETT_TYPE = @@STYPE AND INOUT = 'I'      
     AND PARTIPANTCODE = @@PARTIPANTCODE      
      
 SELECT @@REMQTY = @@DELIVERQTY - @@CERTQTY      
      
      
 IF @@REMQTY >= @@DEMATQTY      
 BEGIN       
  
 INSERT INTO DELTRANS_PROCESS(DT_SNO,SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO      
 ,HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5, UPDFLAG)      
 SELECT 0,@@SNO,@@STYPE,@REFNO,@@TCODE,@@TRTYPE,@@PARTYCD,@@SCRIPCD,@@SERIES,@@DEMATQTY,@@TRANSNO,'',@@ISIN,@@TRANSNO      
                   ,@@DPNAME,'DEMAT','C','0',@@DEMATQTY,@@DPTYPE,@@BANKCODE,@@CLTACCNO,@@BRANCHCD,@@PARTIPANTCODE,0,'','','','DEMAT',@@EXECDATE,@@FILLER1,1,0,@@BDPTYPE,@@BDPID,@@BCLTDPID,0,0,1      
      
 INSERT INTO DELTRANS_PROCESS(DT_SNO,SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO      
 ,HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5, UPDFLAG)      
 SELECT 0,@@SNO,@@STYPE,@REFNO,@@TCODE,904,'BROKER',@@SCRIPCD,@@SERIES,@@DEMATQTY,@@TRANSNO,'',@@ISIN,@@TRANSNO      
 ,'','DEMAT','D','0',@@DEMATQTY,@@DPTYPE,@@BANKCODE,@@CLTACCNO,@@BRANCHCD,@@PARTIPANTCODE,0,'','','','DEMAT',@@EXECDATE,@@FILLER1,1,0,@@BDPTYPE,@@BDPID,@@BCLTDPID,0,0, 1      
       
  DELETE FROM DEMATTRANS WHERE SNO = @@SERIALNO AND PARTY_CODE = @@PARTYCD AND SCRIP_CD = @@SCRIPCD      
                AND SERIES = @@SERIES AND SETT_NO = @@SNO AND TRANSNO = @@TRANSNO AND ISIN = @@ISIN AND REFNO = @REFNO      
 AND TCODE = @@TCODE AND TRTYPE = @@TRTYPE AND BRANCH_CD = @@BRANCHCD AND PARTIPANTCODE = @@PARTIPANTCODE      
 END      
      
 ELSE IF (@@REMQTY < @@DEMATQTY AND @@REMQTY > 0)       
 BEGIN      
      
 SELECT @@EXCESSQTY = @@DEMATQTY - @@REMQTY      
 INSERT INTO DELTRANS_PROCESS(DT_SNO,SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO,      
 HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5, UPDFLAG)      
 SELECT 0,@@SNO,@@STYPE,@REFNO,@@TCODE,@@TRTYPE,@@PARTYCD,@@SCRIPCD,@@SERIES,@@REMQTY,@@TRANSNO,'',@@ISIN,@@TRANSNO,      
 @@DPNAME,'DEMAT','C','0',@@DEMATQTY,@@DPTYPE,@@BANKCODE,@@CLTACCNO,@@BRANCHCD,@@PARTIPANTCODE,0,'','','','DEMAT',@@EXECDATE,@@FILLER1,1,0,@@BDPTYPE,@@BDPID,@@BCLTDPID,0,0, 1      
      
 INSERT INTO DELTRANS_PROCESS(DT_SNO,SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO,      
 HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5, UPDFLAG)      
 SELECT 0,@@SNO,@@STYPE,@REFNO,@@TCODE,904,'BROKER',@@SCRIPCD,@@SERIES,@@REMQTY,@@TRANSNO,'',@@ISIN,@@TRANSNO,      
 '','DEMAT','D','0',@@DEMATQTY,@@DPTYPE,@@BANKCODE,@@CLTACCNO,@@BRANCHCD,@@PARTIPANTCODE,0,'','','','DEMAT',@@EXECDATE,@@FILLER1,1,0,@@BDPTYPE,@@BDPID,@@BCLTDPID,0,0,1      
       
  UPDATE DEMATTRANS SET QTY = @@EXCESSQTY WHERE SNO = @@SERIALNO AND PARTY_CODE = @@PARTYCD AND SCRIP_CD = @@SCRIPCD      
        AND SERIES = @@SERIES AND SETT_NO = @@SNO AND TRANSNO = @@TRANSNO AND ISIN = @@ISIN AND REFNO = @REFNO      
 AND TCODE = @@TCODE AND TRTYPE = @@TRTYPE   AND PARTIPANTCODE = @@PARTIPANTCODE      
 END      
 FETCH NEXT FROM @@DEMATCUR INTO @@SERIALNO,@@SNO,@@STYPE,@@PARTYCD,@@SCRIPCD,@@SERIES,@@DEMATQTY,@@BANKCODE,@@ISIN,@@INOUT,@@CLTACCNO,@@TRANSNO,@@EXECDATE,@@BRANCHCD,@@PARTIPANTCODE,@@TCODE,@@TRTYPE,@@DPTYPE,@@BDPTYPE,@@BDPID,@@BCLTDPID,@@FILLER1,@@DPNAME      
      
END      
CLOSE @@DEMATCUR      
DEALLOCATE @@DEMATCUR      
      
INSERT INTO DELTRANS       
SELECT SETT_NO,SETT_TYPE,REFNO,TCODE,TRTYPE,PARTY_CODE,SCRIP_CD,SERIES,QTY,FROMNO,TONO,CERTNO,FOLIONO,      
 HOLDERNAME,REASON,DRCR,DELIVERED,ORGQTY,DPTYPE,DPID,CLTDPID,BRANCHCD,PARTIPANTCODE,SLIPNO,      
 BATCHNO,ISETT_NO,ISETT_TYPE,SHARETYPE,TRANSDATE,FILLER1,FILLER2,FILLER3,      
 BDPTYPE,BDPID,BCLTDPID,FILLER4,FILLER5        
FROM DELTRANS_PROCESS WHERE DT_SNO = 0       

Update DelTrans Set   
DpType = IsNull(C.Depository,''),  
DpId = IsNull(C.BankID,''),   
CltDpId = IsNull(C.Cltdpid,'')   
From DelTrans D Left Outer Join Client4 C   
On (C.Party_Code = D.Party_Code And DefDp = 1),   
DeliveryDp DP    
Where Filler2 = 1 and DrCr = 'D' And Delivered = '0'     
And TrType In (904,905,909) And DP.DpId = D.BDpId And DP.DpCltNo = D.BCltDpId   
And DP.DpType = D.BDpType     
And DP.Description Not Like '%POOL%'  
And Not ( D.DpType = IsNull(C.Depository,'')   
          And D.DpId = IsNull(C.BankID,'')   
          And D.CltDpId = IsNull(C.Cltdpid,'') )  
And Filler1 not Like 'Third Party'

GO
