CREATE Proc Asian_Proc_BenHolding (@Sauda_Date Varchar(11))
As

SELECT 
PARTY_CODE, DPID = (CASE WHEN BDPTYPE = 'NSDL' THEN BDPID+BCLTDPID ELSE BCLTDPID END),
CERTNO, QUANTITY = SUM(QTY)
INTO #HOLDING
FROM DELTRANS D, DELIVERYDP DP
WHERE D.BDPTYPE = DP.DPTYPE
AND D.BDPID = DP.DPID
AND D.BCLTDPID = DPCLTNO
AND D.DRCR = 'D'
AND D.FILLER2 = 1 
AND DELIVERED = '0'
AND TRTYPE = 904
AND SHARETYPE = 'DEMAT'
AND DESCRIPTION NOT LIKE '%POOL%'
GROUP BY PARTY_CODE,CERTNO,BDPTYPE,BDPID,BCLTDPID

INSERT INTO #HOLDING
SELECT 
PARTY_CODE, DPID = (CASE WHEN BDPTYPE = 'NSDL' THEN BDPID+BCLTDPID ELSE BCLTDPID END),
CERTNO, QUANTITY = SUM(QTY)
FROM BSEDB.DBO.DELTRANS D, BSEDB.DBO.DELIVERYDP DP
WHERE D.BDPTYPE = DP.DPTYPE
AND D.BDPID = DP.DPID
AND D.BCLTDPID = DPCLTNO
AND D.DRCR = 'D'
AND D.FILLER2 = 1 
AND DELIVERED = '0'
AND TRTYPE = 904
AND SHARETYPE = 'DEMAT'
AND DESCRIPTION NOT LIKE '%POOL%'
GROUP BY PARTY_CODE,CERTNO,BDPTYPE,BDPID,BCLTDPID 

SELECT HEADER = '04', PARTY_CODE, DPID, CERTNO, QUANTITY = SUM(QUANTITY)
FROM #HOLDING GROUP BY PARTY_CODE,CERTNO,DPID
ORDER BY PARTY_CODE,CERTNO,DPID

GO
