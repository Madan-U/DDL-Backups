-- Object: PROCEDURE dbo.V2_ARCHIVAL_PROCESS
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


CREATE PROC V2_ARCHIVAL_PROCESS 
(
	@ARCHIVAL_DATE VARCHAR(11), 
	@STEP INT
)

AS

/*=====================================================================================================
	CASH SEGMENT DATA ARCHIVAL PROCESS
		--DATA IS ARCHIVED AS PER ARCHIVAL DATE PROVIDED BY USER. 
		--THIS PROCEDURE IS EXECUTED 5 TIMES BY PROVIDING SAME ARCHIVAL DATE
		--FOLLOWING 5 QUERIES NEED TO BE EXECUTED IN THE GIVEN ORDER TO COMPLETE THE PROCESS
			--EXEC V2_ARCHIVAL_PROCESS @ARCHIVAL_DATE = 'MAR 31 2000', @STEP = 1
			--EXEC V2_ARCHIVAL_PROCESS @ARCHIVAL_DATE = 'MAR 31 2000', @STEP = 2
			--EXEC V2_ARCHIVAL_PROCESS @ARCHIVAL_DATE = 'MAR 31 2000', @STEP = 3
			--EXEC V2_ARCHIVAL_PROCESS @ARCHIVAL_DATE = 'MAR 31 2000', @STEP = 4
			--EXEC V2_ARCHIVAL_PROCESS @ARCHIVAL_DATE = 'MAR 31 2000', @STEP = 5
		--DETAILS ARE DESCRIBED BELOW AT THE START OF EACH STEP 
		--NONE OF THE STEPS EXCEPT FOR STEP 3 CAN BE EXECUTED MORE THAN ONES
		--THIS HAS BEEN CONTROLLED BY CREATING TABLE V2_STEP_FLAG IN STEP 1
		--AS EACH STEP IS COMPLETE THE NEXT ONE IS INITIALIZED. 
=====================================================================================================*/

SET NOCOUNT ON

DECLARE 
	@TABLE_NAME SYSNAME,
	@TABLE_CUR CURSOR,
	@WHERE_CLAUSE VARCHAR(2000), 
	@QUERY VARCHAR(8000), 
	@ROWCNT BIGINT  

/*=====================================================================================================
	STEP 1 :: CASH SEGMENT DATA ARCHIVAL PROCESS
		--STEP WILL BE EXECUTED IF AND ONLY IF TABLE V2_STEP_FLAG DOES NOT EXIST IN THE DATABASE. 
		--CREATE TABLE V2_STEP_FLAG
		--QUERY TO UPDATE V2_ARCHIVAL_TABLE_LIST TO FILTER EXISTING TABLES TO BE ARCHIVED
		--CREATE V2_ARCHIVAL_PROCESS_LOG DATA FOR TABLES WHICH ARE TO BE ARCHIVED
		--CREATE V2_ARCHIVAL_SETTLEMENT_LIST FROM SETT_MST FOR LIST OF SETTLEMENTS TO BE ARCHIVED. 
			--ARCHIVAL SETTLEMENTS ARE FILTERED BASED ON THE PAYIN DATE.
		--INITIALIZE NEXT STEP IN TABLE V2_STEP_FLAG
=====================================================================================================*/
IF @STEP = 1 AND (SELECT COUNT(1) FROM SYSOBJECTS WHERE NAME = "V2_STEP_FLAG" AND XTYPE = "U") = 0 
BEGIN
		SELECT STEP_FLAG = ATL_FLAG, SF_CREATEON = GETDATE() INTO V2_STEP_FLAG FROM V2_ARCHIVAL_TABLE_LIST WHERE 1=0

		UPDATE V2_ARCHIVAL_TABLE_LIST 
		SET ATL_FLAG = 1 
		WHERE ATL_NAME IN (SELECT NAME FROM SYSOBJECTS WHERE XTYPE = "U") 

		INSERT INTO V2_ARCHIVAL_PROCESS_LOG (APL_DATABASENAME,APL_ARCHIVALDATE,APL_PROCESSDATE,APL_TABLENAME,APL_SPACEONHD,APL_ROWCOUNT,APL_ROWSTODELETE,APL_ROWSDELETED,APL_SUCCESSFLAG,APL_CREATEDBY,APL_CREATEDON)
		SELECT 
			DATABASENAME = UPPER(DB_NAME()),
			ARCHIVALDATE = @ARCHIVAL_DATE, 
			PROCESSDATE = LEFT(GETDATE(),11), 
			TABLENAME =UPPER(B.NAME), 
			SPACEONHD = SUM(C.USED), 
			ROW_COUNT = A.ROWCNT, 
			ROWSTODELETE = 0,
			ROWSDELETED = 0,
			SUCCESSFLAG = "N",
			CREATEDBY = UPPER(SYSTEM_USER),
			CREATEDON = GETDATE() 
		FROM 
			SYSINDEXES A,
			SYSOBJECTS B,
			SYSINDEXES C,
			(SELECT ATL_NAME FROM V2_ARCHIVAL_TABLE_LIST WHERE ATL_FLAG = 1) V 
		WHERE 
			A.ID = B.ID 
			AND A.INDID  IN (0,1,255)
			AND A.ID = C.ID
			AND C.INDID IN (0,1,255)
			AND B.XTYPE = 'U' 
			AND B.NAME = V.ATL_NAME 
		GROUP BY
			B.NAME,
			A.ROWCNT

		TRUNCATE TABLE V2_ARCHIVAL_SETTLEMENT_LIST

		INSERT INTO V2_ARCHIVAL_SETTLEMENT_LIST 
		SELECT 
			SETT_NO, 
			SETT_TYPE,  
			ARCHIVAL_DATE = @ARCHIVAL_DATE, 
			PROCESS_DATE = LEFT(GETDATE(),11) 
		FROM SETT_MST 
		WHERE FUNDS_PAYIN <= @ARCHIVAL_DATE 
		ORDER BY 1,2 

		INSERT INTO V2_STEP_FLAG VALUES (@STEP, GETDATE())
	RETURN
END

/*=====================================================================================================
	STEP 2 :: CASH SEGMENT DATA ARCHIVAL PROCESS
		--CURSOR TO GET THE COUNT OF DATA TO BE DELETED FROM EACH TABLE IN V2_ARCHIVAL_PROCESS_LOG.
			--THE COUNT QUERY USES WHERE CLAUSE AS MENTIONED IN THE V2_ARCHIVAL_TABLE_LIST. 
			--V2_ARCHIVAL_SETTLEMENT_LIST IS ADDED IN THE JOIN IF ARCHIVAL IS BASED ON SETTLEMENT NUM.
		--INITIALIZE NEXT STEP IN TABLE V2_STEP_FLAG
=====================================================================================================*/
IF @STEP = 2 AND @STEP = (SELECT ISNULL(MAX(STEP_FLAG),0) + 1 FROM V2_STEP_FLAG)
BEGIN
	SET @TABLE_CUR = CURSOR FOR
	SELECT APL_TABLENAME FROM V2_ARCHIVAL_PROCESS_LOG
	OPEN @TABLE_CUR
	FETCH NEXT FROM @TABLE_CUR INTO @TABLE_NAME 
	WHILE @@FETCH_STATUS = 0 
	BEGIN 
		SELECT @WHERE_CLAUSE = ISNULL(ATL_ARCHIVEWHERE," ") FROM V2_ARCHIVAL_TABLE_LIST WHERE ATL_NAME = @TABLE_NAME 
		
		SET @QUERY = "DECLARE @@ARCHIVAL_DATE VARCHAR(11) SET @@ARCHIVAL_DATE = '" + @ARCHIVAL_DATE + "' " 
		SET @QUERY = @QUERY + "UPDATE V2_ARCHIVAL_PROCESS_LOG SET APL_ROWSTODELETE = (SELECT COUNT(1) FROM " 
		SET @QUERY = @QUERY + @TABLE_NAME 
		SET @QUERY = @QUERY + " WITH(NOLOCK) " 
		IF CHARINDEX(" B.",@WHERE_CLAUSE) > 0 
		BEGIN 
			SET @QUERY = @QUERY + ", V2_ARCHIVAL_SETTLEMENT_LIST B WITH(NOLOCK) "  
		END 
		SET @QUERY = @QUERY + @WHERE_CLAUSE 
		SET @QUERY = @QUERY + ") WHERE APL_TABLENAME = '" 
		SET @QUERY = @QUERY + @TABLE_NAME + "' " 

--		PRINT (@QUERY) 
		EXEC (@QUERY) 

		FETCH NEXT FROM @TABLE_CUR INTO @TABLE_NAME
	END
	CLOSE @TABLE_CUR
	DEALLOCATE @TABLE_CUR

	INSERT INTO V2_STEP_FLAG VALUES (@STEP, GETDATE())
	RETURN
END

/*=====================================================================================================
	STEP 3 :: CASH SEGMENT DATA ARCHIVAL PROCESS
		--SELECT QUERY FOR ARCHIVAL PROCESS STATISTICS GIVING DETAILS OF EXPECTED ARCHIVAL COUNTS. 
			--IT IS EXPECTED THAT THE ARCHIVAL COUNTS ARE VERIFIED BEFORE EXECUTING FUTURE STEPS. 
			--SIMILAR OUTPUT CAN BE EXECUTED FROM ARCHIVAL SERVER TO COMPLETE THE VALIDATION. 
		--INITIALIZE NEXT STEP IN TABLE V2_STEP_FLAG
=====================================================================================================*/
IF @STEP = 3 --AND @STEP = (SELECT ISNULL(MAX(STEP_FLAG),0) + 1 FROM V2_STEP_FLAG)
BEGIN
	SELECT 
		SR_NO = ATL_SNO,
		TABLE_NAME = ATL_NAME,
		ARCHIVE_FLAG = (CASE WHEN ATL_FLAG = 1 THEN 'Y' ELSE 'N' END), 
		DBNAME = ISNULL(APL_DATABASENAME,""),
		ARCHIVAL_DATE = ISNULL(LEFT(APL_ARCHIVALDATE,11),""),
		PROCESS_DATE = ISNULL(LEFT(APL_PROCESSDATE,11),""),
		HD_SPACE = ISNULL(APL_SPACEONHD,0),
		ROW_COUNT = ISNULL(APL_ROWCOUNT,0),
		ROWS_TO_DELETE = ISNULL(APL_ROWSTODELETE,0),
		ARCHIVED_SPACE = ROUND(CASE WHEN ISNULL(APL_ROWCOUNT,0) = 0 THEN 0 ELSE ISNULL(APL_ROWSTODELETE,0) * ISNULL(APL_SPACEONHD,0) / ISNULL(APL_ROWCOUNT,0) END,0), 
		ROWS_DELETED = ISNULL(APL_ROWSDELETED,0),
		SUCCESS_FLAG = ISNULL(APL_SUCCESSFLAG,"") 
	FROM 
		V2_ARCHIVAL_TABLE_LIST 
		LEFT OUTER JOIN V2_ARCHIVAL_PROCESS_LOG 
	ON (ATL_NAME = APL_TABLENAME) 

	IF (SELECT COUNT(1) FROM V2_STEP_FLAG WHERE STEP_FLAG = @STEP) = 0 
	BEGIN 
		INSERT INTO V2_STEP_FLAG VALUES (@STEP, GETDATE())
	END 
	RETURN
END

/*=====================================================================================================
	STEP 4 :: CASH SEGMENT DATA ARCHIVAL PROCESS
		--DUMMY STEP CREATED TO INITIALIZE THE FINAL ARCHIVAL STEP. 
			--IT IS EXPECTED THAT THE ARCHIVAL COUNTS ARE VERIFIED BEFORE EXECUTING THIS STEP. 
		--INITIALIZE FINAL STEP IN TABLE V2_STEP_FLAG
=====================================================================================================*/
IF @STEP = 4 AND @STEP = (SELECT ISNULL(MAX(STEP_FLAG),0) + 1 FROM V2_STEP_FLAG)
BEGIN
	INSERT INTO V2_STEP_FLAG VALUES (@STEP, GETDATE())
	RETURN
END

/*=====================================================================================================
	STEP 5 :: CASH SEGMENT DATA ARCHIVAL PROCESS
		--CURSOR TO ARCHIVE DATA FROM EACH TABLE IN V2_ARCHIVAL_PROCESS_LOG.
			--THE DELETE QUERY USES WHERE CLAUSE AS MENTIONED IN THE V2_ARCHIVAL_TABLE_LIST. 
			--V2_ARCHIVAL_SETTLEMENT_LIST IS ADDED IN THE JOIN IF DELETION IS BASED ON SETTLEMENT NUM.
			--THE ROWS DELETED ARE VERIFIED WITH THE ROWS TO BE DELETED BEFORE COMMIT. 
			--V2_ARCHIVAL_PROCESS_LOG IS UPDATED AFTER EACH DELETION. 
		--SET FINAL STEP STATUS AS COMPLETED IN TABLE V2_STEP_FLAG
=====================================================================================================*/
IF @STEP = 5 AND @STEP = (SELECT ISNULL(MAX(STEP_FLAG),0) + 1 FROM V2_STEP_FLAG) 
BEGIN
	SET @TABLE_CUR = CURSOR FOR
	SELECT APL_TABLENAME FROM V2_ARCHIVAL_PROCESS_LOG
	OPEN @TABLE_CUR
	FETCH NEXT FROM @TABLE_CUR INTO @TABLE_NAME 
	WHILE @@FETCH_STATUS = 0 
	BEGIN 
		SELECT @WHERE_CLAUSE = ISNULL(ATL_ARCHIVEWHERE," ") FROM V2_ARCHIVAL_TABLE_LIST WHERE ATL_NAME = @TABLE_NAME 
		
		SET @QUERY = "DECLARE @@ARCHIVAL_DATE VARCHAR(11) SET @@ARCHIVAL_DATE = '" + @ARCHIVAL_DATE + "' " 
		SET @QUERY = @QUERY + "DELETE " 
		SET @QUERY = @QUERY + @TABLE_NAME + " " 
		IF CHARINDEX(" B.",@WHERE_CLAUSE) > 0 
		BEGIN 
			SET @QUERY = @QUERY + " FROM V2_ARCHIVAL_SETTLEMENT_LIST B "  
		END 
		SET @QUERY = @QUERY + @WHERE_CLAUSE 

		BEGIN TRAN 
--			PRINT (@QUERY) 
			EXEC (@QUERY) 
			SET @ROWCNT = @@ROWCOUNT 	
			IF @ROWCNT = (SELECT TOP 1 APL_ROWSTODELETE FROM V2_ARCHIVAL_PROCESS_LOG WHERE APL_TABLENAME = @TABLE_NAME) 
			BEGIN
				UPDATE V2_ARCHIVAL_PROCESS_LOG 
				SET 
					APL_ROWSDELETED = @ROWCNT, 
					APL_SUCCESSFLAG = 'Y',
					APL_UPDATEDBY = SYSTEM_USER, 
					APL_UPDATEDON = GETDATE() 
				WHERE 
					APL_TABLENAME = @TABLE_NAME
				COMMIT TRAN
			END
			ELSE 
			BEGIN
				ROLLBACK TRAN
			END
		FETCH NEXT FROM @TABLE_CUR INTO @TABLE_NAME
	END
	CLOSE @TABLE_CUR
	DEALLOCATE @TABLE_CUR

	INSERT INTO V2_STEP_FLAG VALUES (@STEP, GETDATE())
	RETURN
END

PRINT "ERROR!! INCORRECT STEP PROCESSED..."
RETURN

GO
