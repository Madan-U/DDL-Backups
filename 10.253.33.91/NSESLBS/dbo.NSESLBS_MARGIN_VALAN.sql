-- Object: PROCEDURE dbo.NSESLBS_MARGIN_VALAN
-- Server: 10.253.33.91 | DB: NSESLBS
--------------------------------------------------


  
CREATE PROC NSESLBS_MARGIN_VALAN    
(    
 @MARGINDATE  VARCHAR(11)    
)    
AS    
    
DECLARE @PREMARGINDATE VARCHAR(11)    
    
SELECT @PREMARGINDATE = 'JAN  1 1900'    
    
IF (SELECT ISNULL(COUNT(1),0) FROM VALANACCOUNT WHERE ACNAME = 'NSE SLBS MARGIN' ) > 0    
BEGIN    
 DELETE FROM ACCBILL WHERE START_DATE LIKE @MARGINDATE + '%'    
 AND SETT_TYPE = 'L'    
    AND NARRATION IN ('NSE SLBS REV MARGIN', 'NSE SLBS MARGIN')    
    
 SELECT @PREMARGINDATE = LEFT(MAX(START_DATE),11) FROM SETT_MST    
 WHERE START_DATE < @MARGINDATE    
 AND SETT_TYPE = 'L'    
    
 SELECT PARTY_CODE, MARGIN_DATE, TOTAL_MARGIN = SUM(VARAMT+ABS(MTOM)), SELL_BUY = 1, BRANCH_CD = CONVERT(VARCHAR(10), '') INTO #MARGIN FROM TBL_MG02    
 WHERE MARGIN_DATE LIKE @MARGINDATE + '%'    
 AND 1 = (CASE WHEN BUYQTY > 0  AND SETT_TYPE = 'L' THEN 1  
      WHEN SELLQTY > 0  AND SETT_TYPE = 'P' THEN 1   
      ELSE 0   
    END)  
 GROUP BY PARTY_CODE, MARGIN_DATE    
 HAVING SUM(VARAMT+ABS(MTOM)) > 0     
    
 INSERT  INTO #MARGIN     
 SELECT PARTY_CODE, MARGIN_DATE=@MARGINDATE, TOTAL_MARGIN = SUM(VARAMT+ABS(MTOM)), SELL_BUY = 2, BRANCH_CD = CONVERT(VARCHAR(10), '') FROM TBL_MG02    
 WHERE MARGIN_DATE LIKE @PREMARGINDATE + '%'    
 AND MARGIN_DATE >= 'SEP 18 2012'    
 AND 1 = (CASE WHEN BUYQTY > 0  AND SETT_TYPE = 'L' THEN 1  
      WHEN SELLQTY > 0  AND SETT_TYPE = 'P' THEN 1   
      ELSE 0   
    END)  
 GROUP BY PARTY_CODE, MARGIN_DATE    
 HAVING SUM(VARAMT+ABS(MTOM)) > 0     
    
 UPDATE #MARGIN SET BRANCH_CD = C1.BRANCH_CD    
 FROM CLIENT1 C1    
 WHERE C1.CL_CODE = PARTY_CODE    
    
 DELETE FROM #MARGIN    
 WHERE BRANCH_CD = ''    
    
 INSERT INTO  ACCBILL    
 SELECT PARTY_CODE,BILL_NO=0,SELL_BUY,SETT_NO,SETT_TYPE,START_DATE,END_DATE,PAYIN_DATE=FUNDS_PAYIN,PAYOUT_DATE=FUNDS_PAYOUT,    
 AMOUNT=TOTAL_MARGIN,BRANCH_CD,NARRATION=(CASE WHEN SELL_BUY = 2 THEN 'NSE SLBS REV MARGIN' ELSE 'NSE SLBS MARGIN' END)    
 FROM #MARGIN M, SETT_MST S    
 WHERE S.SETT_TYPE = 'L'    
 AND @MARGINDATE BETWEEN S.START_DATE AND S.END_DATE    
    
 INSERT INTO ACCBILL    
 SELECT PARTY_CODE=ACCODE,BILL_NO=0,SELL_BUY=(CASE WHEN SELL_BUY = 1 THEN 2 ELSE 1 END),    
 SETT_NO,SETT_TYPE,START_DATE,END_DATE,PAYIN_DATE=FUNDS_PAYIN,PAYOUT_DATE=FUNDS_PAYOUT,    
 AMOUNT=SUM(TOTAL_MARGIN),BRANCH_CD,NARRATION=(CASE WHEN SELL_BUY = 2 THEN 'NSE SLBS REV MARGIN' ELSE 'NSE SLBS MARGIN' END)    
 FROM #MARGIN M, SETT_MST S, VALANACCOUNT V    
 WHERE S.SETT_TYPE = 'L'    
 AND @MARGINDATE BETWEEN S.START_DATE AND S.END_DATE    
 AND V.ACNAME = 'NSE SLBS MARGIN'    
 GROUP BY ACCODE, SELL_BUY,SETT_NO,SETT_TYPE,START_DATE,END_DATE,FUNDS_PAYIN,FUNDS_PAYOUT,BRANCH_CD    
    
 INSERT INTO ACCBILL    
 SELECT PARTY_CODE=ACCODE,BILL_NO=0,SELL_BUY=(CASE WHEN SELL_BUY = 1 THEN 2 ELSE 1 END),    
 SETT_NO,SETT_TYPE,START_DATE,END_DATE,PAYIN_DATE=FUNDS_PAYIN,PAYOUT_DATE=FUNDS_PAYOUT,    
 AMOUNT=SUM(TOTAL_MARGIN),BRANCH_CD='ZZZ',NARRATION=(CASE WHEN SELL_BUY = 2 THEN 'NSE SLBS REV MARGIN' ELSE 'NSE SLBS MARGIN' END)    
 FROM #MARGIN M, SETT_MST S, VALANACCOUNT V    
 WHERE S.SETT_TYPE = 'L'    
 AND @MARGINDATE BETWEEN S.START_DATE AND S.END_DATE    
 AND V.ACNAME = 'NSE SLBS MARGIN'    
 GROUP BY ACCODE, SELL_BUY,SETT_NO,SETT_TYPE,START_DATE,END_DATE,FUNDS_PAYIN,FUNDS_PAYOUT    
END

GO
