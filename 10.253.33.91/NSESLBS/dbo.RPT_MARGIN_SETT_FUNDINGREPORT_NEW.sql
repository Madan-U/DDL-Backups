--Exec BseDb.Dbo.RPT_MARGIN_SETT_FUNDINGREPORT_NEW 'Dec 17 2004', 'Dec 31 2004', 'T'
--EXEC RPT_MARGIN_SETT_FUNDINGREPORT_NEW 'DEC 17 2004', 'DEC 17 2005', 'T'
CREATE PROC RPT_MARGIN_SETT_FUNDINGREPORT_NEW
--EXEC RPT_FUNDINGREPORT_NEW 'OCT  1 2005', 'OCT 31 2005'
@FDATE VARCHAR(11),
@TDATE VARCHAR(11),
@REPOPTION VARCHAR(1)
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
--SELECT CLTCODE, 0 AS EXTFUND, SUM(MARG_FUND_AMT) AS MARG_FUND_AMT, SUM(INSTAMT) AS INSTAMT, SUM(NONINSTAMT) AS NONINSTAMT INTO #RPT_FUNDING_TABLE FROM

IF @REPOPTION <> 'M' AND @REPOPTION <> 'B' AND @REPOPTION <> 'T'
	BEGIN
		SELECT 
			MARGINFUNDING = 0,
			INSTAMT = 0, 
			NONISTAMT = 0, 
			TOTALAMOUNT = 0, 
			EXTFUND = 0, 
			PARTY_CODE_CNT = 0
	END

IF @REPOPTION = 'M'
	BEGIN
		SELECT 
			MARGINFUNDING = CONVERT(VARCHAR(15), MAX(MARGINFUNDING)), 
			INSTAMT = 0, 
			NONISTAMT = CONVERT(VARCHAR(15), MAX(MARGINFUNDING)), 
			TOTALAMOUNT = CONVERT(VARCHAR(15), MAX(MARGINFUNDING)), 
			EXTFUND = 0, 
			PARTY_CODE_CNT = MAX(PARTY_CODE_CNT) 
		FROM 
			( 
					SELECT 
						RUNDATE = CONVERT(VARCHAR(11), RUNDATE, 109), 
						MARGINFUNDING = ABS(SUM(MARGINFUNDING)), 
						PARTY_CODE_CNT = 0 
					FROM V2_ClientFundingData V 
					WHERE RUNDATE BETWEEN @FDATE AND @TDATE + ' 23:59:59' 
						AND ABS(VARMARGIN) > 100000 
					GROUP BY CONVERT(VARCHAR(11), RUNDATE, 109) 
				UNION ALL 
					SELECT 
						RUNDATE = '', 
						MARGINFUNDING = 0, 
						PARTY_CODE_CNT = COUNT(DISTINCT PARTY_CODE) 
					FROM V2_ClientFundingData V 
					WHERE RUNDATE BETWEEN @FDATE AND @TDATE + ' 23:59:59' 
						AND ABS(VARMARGIN) > 100000 
			) 
			A 
	END

IF @REPOPTION = 'B'
	BEGIN
		SELECT 
			BILLFUNDING = CONVERT(VARCHAR(15), MAX(BILLFUNDING)), 
			INSTAMT = 0, 
			NONINSTAMT = CONVERT(VARCHAR(15), MAX(BILLFUNDING)), 
			TOTALAMOUNT = CONVERT(VARCHAR(15), MAX(BILLFUNDING)), 
			EXTFUND = 0, 
			PARTY_CODE_CNT = MAX(PARTY_CODE_CNT) 
		FROM 
			( 
					SELECT 
						RUNDATE = CONVERT(VARCHAR(11), RUNDATE, 109), 
						BILLFUNDING = ABS(SUM(BILLFUNDING)), 
						PARTY_CODE_CNT = 0 
					FROM V2_ClientFundingData V 
					WHERE RUNDATE BETWEEN @FDATE AND @TDATE + ' 23:59:59' 
						AND ABS(BILLAMOUNT) > 100000 
					GROUP BY CONVERT(VARCHAR(11), RUNDATE, 109) 
				UNION ALL 
					SELECT 
						RUNDATE = '', 
						BILLFUNDING = 0, 
						PARTY_CODE_CNT = COUNT(DISTINCT PARTY_CODE) 
					FROM V2_ClientFundingData V 
					WHERE RUNDATE BETWEEN @FDATE AND @TDATE + ' 23:59:59' 
						AND BILLAMOUNT < 100000 
			) 
			A 
	END

IF @REPOPTION = 'T'
	BEGIN
		SELECT 
			TOTALFUNDING = CONVERT(VARCHAR(15), MAX(TOTALFUNDING)), 
			INSTAMT = 0, 
			NONINSTAMT = CONVERT(VARCHAR(15), MAX(TOTALFUNDING)), 
			TOTALAMOUNT = CONVERT(VARCHAR(15), MAX(TOTALFUNDING)), 
			EXTFUND = 0, 
			PARTY_CODE_CNT = MAX(PARTY_CODE_CNT) 
		FROM 
			( 
					SELECT 
						RUNDATE = CONVERT(VARCHAR(11), RUNDATE, 109), 
						TOTALFUNDING = ABS(SUM(MARGINFUNDING + BILLFUNDING)), 
						PARTY_CODE_CNT = 0 
					FROM V2_ClientFundingData V 
					WHERE RUNDATE BETWEEN @FDATE AND @TDATE + ' 23:59:59' 
						AND (ABS(VARMARGIN) > 100000 OR ABS(BILLAMOUNT) > 100000)
					GROUP BY CONVERT(VARCHAR(11), RUNDATE, 109) 
/*
					SELECT 
						RUNDATE = CONVERT(VARCHAR(11), RUNDATE, 109), 
						TOTALFUNDING = ABS(SUM(TOTALFUNDING)), 
						PARTY_CODE_CNT = 0 
					FROM V2_ClientFundingData V 
					WHERE RUNDATE BETWEEN @FDATE AND @TDATE + ' 23:59:59' 
						AND ABS(TOTALFUNDING) > 100000 
					GROUP BY CONVERT(VARCHAR(11), RUNDATE, 109) */
				UNION ALL 
					SELECT 
						RUNDATE = '', 
						TOTALFUNDING = 0, 
						PARTY_CODE_CNT = COUNT(DISTINCT PARTY_CODE) 
					FROM V2_ClientFundingData V 
					WHERE RUNDATE BETWEEN @FDATE AND @TDATE + ' 23:59:59' 
						AND ABS(TOTALFUNDING) > 100000 
			) 
			A
	END

GO
