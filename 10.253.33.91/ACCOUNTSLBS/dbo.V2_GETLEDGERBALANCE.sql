    #LEDGERBILLTMP  
   SET   
    CLIENTTYPE = C1.CL_TYPE,       
    FAMILYCODE = C1.FAMILY   
   FROM   
    MSAJAG..CLIENT1 C1,  
    MSAJAG..CLIENT2 C2,  
    MSAJAG..CLIENTTYPE C3  
   WHERE   
    CLTCODE = C2.PARTY_CODE  
    AND C1.CL_CODE = C2.CL_CODE  
    AND C1.CL_TYPE = C3.CL_TYPE  
  
   DELETE   
    #LEDGERBILLTMP  
   WHERE   
    CLIENTTYPE IN (SELECT CL_TYPE FROM MSAJAG..CLIENTTYPE WHERE FUNDS_ADHOC_PAYOUT <> '1')  
  END   
  ELSE   
  IF @GENFLAG = 'S' --SETTLEMENT PAYOUT CAPITAL  
  BEGIN  
   IF @SEGMENT <> 'FUTURES'   
   BEGIN  
    DECLARE @SQL VARCHAR(1000)  
  
    SET @SQL = ''  
  
    IF @EXCHANGE = 'NSE'   
    BEGIN  
     SET @SQL = 'SELECT PARTY_CODE, SELL_BUY, AMOUNT, SETT_NO, SETT_TYPE INTO #ACCBILL FROM MSAJAG..ACCBILL WHERE SETT_NO = ' + @STLNO   
    END  
  
    IF @EXCHANGE = 'BSE'   
    BEGIN  
     SET @SQL = 'SELECT PARTY_CODE, SELL_BUY, AMOUNT, SETT_NO, SETT_TYPE INTO #ACCBILL FROM BSEDB..ACCBILL WHERE SETT_NO = ' + @STLNO   
    END  
  
    EXEC (@SQL)  
      
    INSERT INTO  
     #LEDGERBILLTMP  
    SELECT  
     A.CLTCODE,  
     A.BRANCHCODE,  
     LONGNAME,  
     ACCAT,  
     BTOBPAYMENT,  
     PAYMODE,  
     POBANKCODE,  
     SETT_NO = @STLNO,  
     NORMALBILLAMT = SUM(  
         CASE  
         WHEN SETT_TYPE = (CASE WHEN @EXCHANGE = 'NSE' THEN 'N' ELSE 'D' END)  
         THEN (  
          CASE  
          WHEN SELL_BUY = '1'  
          THEN -AMOUNT  
          ELSE AMOUNT  
          END  
         )  
         ELSE 0  
         END  
         ),  
     T2TBILLAMT = SUM(  
         CASE  
         WHEN SETT_TYPE = (CASE WHEN @EXCHANGE = 'NSE' THEN 'W' ELSE 'C' END)  
         THEN (  
          CASE  
          WHEN SELL_BUY = '1'  
          THEN -AMOUNT  
          ELSE AMOUNT  
          END  
         )  
         ELSE 0  
         END  
         ),  
     CLIENTTYPE = '',       
     FAMILYCODE = '',       
     RUNDATE = GETDATE()  
    FROM   
     #ACCBILL L WITH(NOLOCK),  
     ACMAST A WITH(NOLOCK)  
    WHERE   
     L.PARTY_CODE = A.CLTCODE  
     AND A.CLTCODE BETWEEN @FPARTY AND @TPARTY  
     AND A.ACCAT IN (4, 104)  
     AND L.SETT_NO = @STLNO  
     AND A.POBANKCODE IN  
     (  
      SELECT  
      CLTCODE  
      FROM ACMAST  
      WITH(NOLOCK)  
      WHERE ACCAT = 2  
     )  
    GROUP BY   
     A.CLTCODE,  
     A.BRANCHCODE,  
     LONGNAME,  
     ACCAT,  
     BTOBPAYMENT,  
     PAYMODE,  
     POBANKCODE  
    HAVING  
     SUM(  
     CASE  
     WHEN SELL_BUY = '1'  
     THEN -AMOUNT  
     ELSE AMOUNT  
     END  
     ) > 0  
  
    UPDATE  
     #LEDGERBILLTMP  
    SET   
     CLIENTTYPE = C1.CL_TYPE,       
     FAMILYCODE = C1.FAMILY   
    FROM   
     MSAJAG..CLIENT1 C1,  
     MSAJAG..CLIENT2 C2,  
     MSAJAG..CLIENTTYPE C3  
    WHERE   
     CLTCODE = C2.PARTY_CODE  
     AND C1.CL_CODE = C2.CL_CODE  
     AND C1.CL_TYPE = C3.CL_TYPE  
  
    DELETE   
     #LEDGERBILLTMP  
    WHERE   
     CLIENTTYPE IN (SELECT CL_TYPE FROM MSAJAG..CLIENTTYPE WHERE FUNDS_SETT_PAYOUT <> '1')  
   END  
  END   
 END   
  
