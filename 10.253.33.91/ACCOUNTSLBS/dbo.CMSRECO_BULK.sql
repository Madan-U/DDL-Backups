	@BANKCODE VARCHAR(10),
	@FNAME VARCHAR(500),
	@UNAME VARCHAR(100)

AS

SET NOCOUNT ON 
/*--------------------------VALIDATION FOR SINGLE VOUCHER TYPE BASED ON DRCR ------------------------*/

DECLARE  
	@@ERROR_COUNT INT, 
	@@CMSRECOCOUNT AS INT, 
	@@MICRNO AS VARCHAR(10),
	@@FROMDATE AS DATETIME,
	@@SQL VARCHAR(8000)


