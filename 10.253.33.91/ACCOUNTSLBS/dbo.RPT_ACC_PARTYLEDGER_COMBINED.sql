BEGIN       
	SELECT 
		CLTCODE, SEGMENT, ORDERSEG, OPBAL = MAX(OPBAL) 
	INTO 
		#OPBAL 
	FROM 
		#TMPLEDGERNEW         
	GROUP BY 
		CLTCODE, SEGMENT, ORDERSEG        	

	SELECT 
		CLTCODE, ORDERSEG, OPBAL = SUM(OPBAL) 
	INTO 
		#FINOPBAL 
	FROM 
		#OPBAL         
	GROUP BY 
		CLTCODE, ORDERSEG        


	UPDATE                 
		#TMPLEDGERNEW               
	SET OPBAL = 0                 

	UPDATE                 
		#TMPLEDGERNEW                
	SET 
		OPBAL = T.OPBAL                 
	FROM 
		#TMPLEDGERNEW L WITH(NOLOCK),                 
		#FINOPBAL T WITH(NOLOCK)                 
	WHERE 
		L.CLTCODE = T.CLTCODE  AND L.ORDERSEG = T.ORDERSEG             

END      
        
              
