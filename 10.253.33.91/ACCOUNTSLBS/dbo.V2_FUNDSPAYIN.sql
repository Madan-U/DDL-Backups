-- Object: PROCEDURE dbo.V2_FUNDSPAYIN
-- Server: 10.253.33.91 | DB: ACCOUNTSLBS
--------------------------------------------------

  
CREATE PROCEDURE [dbo].[V2_FUNDSPAYIN]  
(  
 @SETTNO VARCHAR(12),  
 @RPTTYPE CHAR(4),  
 @BANK_CODE VARCHAR(12)  
)  
  
AS  
 /*  
  EXEC [DBO].[V2_FUNDSPAYIN]  
   '2005059',  
   'FILE',  
   '326090'  
  
  EXEC [DBO].[V2_FUNDSPAYIN]  
   '2005059',  
   'HTML',  
   '326090'  
 */  
  
 DECLARE  
  @@FDATE DATETIME,  
  @@DBNAME VARCHAR(25),  
  @@EXCHANGE VARCHAR(3),  
  @@SEGMENT VARCHAR(20)  
   
 SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED  
 SET NOCOUNT ON  
   
 DECLARE  
  @@TDATE DATETIME  
   
 SET @@TDATE = CONVERT(VARCHAR(11), GETDATE())  
  
 EXEC MSAJAG.DBO.PROC_RMS_CALCULATION   
  @SAUDA_DATE=@@TDATE,  
  @FROMPARTY='0',  
  @TOPARTY='ZZZZ',  
  @INSERTFINAL=0  
  
 SELECT  
  @@FDATE = SDTCUR  
 FROM  
  PARAMETER  
 WHERE  
  GETDATE() BETWEEN SDTCUR AND LDTCUR  
  
 SELECT   
  @@DBNAME = DB_NAME()  
  
 SELECT  
  @@EXCHANGE = EXCHANGE,  
  @@SEGMENT = SEGMENT  
 FROM  
  PRADNYA.DBO.MULTICOMPANY  
 WHERE  
  ACCOUNTDB = @@DBNAME  
  
 CREATE TABLE [DBO].[#FUNDSPAYIN_DATA](  
  [SETT_NO] [VARCHAR](12) NOT NULL,  
  [SETT_TYPE] [VARCHAR](3) NOT NULL,  
  [PARTY_CODE] [VARCHAR](10) NOT NULL,  
  [BILLDATE] [DATETIME] NOT NULL,  
  [AMOUNT] [MONEY] NOT NULL,  
  [DRCR] [CHAR](1) NOT NULL,  
  [VNO] [VARCHAR](12) NOT NULL,  
  [BRANCH] [VARCHAR](10) NOT NULL,  
  [EXCHANGE] [VARCHAR](3) NOT NULL,  
  [SEGMENT] [VARCHAR](7) NOT NULL,  
  [BANK_CODE] [VARCHAR](10) NOT NULL,  
  [SRNO] [INT] IDENTITY (1, 1)  
 ) ON [PRIMARY]  
  
 INSERT INTO  
  #FUNDSPAYIN_DATA  
 SELECT  
  SETT_NO,  
  SETT_TYPE,  
  PARTY_CODE,  
  DATE = CONVERT(VARCHAR(11), DATE),  
  AMOUNT,  
  DRCR = CASE DRCR WHEN 'D' THEN 'C' END,  
  VNO,  
  BRANCH,  
  EXCHANGE,  
  SEGMENT,  
  BANK_CODE = @BANK_CODE  
 FROM  
  BFBILLMATCH B,  
  ACMAST A  
 WHERE  
  VTYPE = 15  
  AND SETT_NO = @SETTNO  
  AND DRCR = 'D'  
  AND B.PARTY_CODE = A.CLTCODE  
  AND BTOBPAYMENT = 1  
   
 UNION ALL  
  
 SELECT  
  SETT_NO = '',  
  SETT_TYPE = '',  
  PARTY_CODE = A.CLTCODE,  
  DATE = GETDATE(),  
  AMOUNT = ABS(SUM(VDTLEDBAL)),  
  DRCR = 'C',  
  VNO = '',  
  BRANCH = BRANCHCODE,  
  EXCHANGE = @@EXCHANGE,  
  SEGMENT = @@SEGMENT,  
  BANK_CODE = @BANK_CODE  
 FROM  
  MSAJAG.DBO.TBL_RMS_DATA_TODAY L,  
  ACMAST A  
 WHERE    
  BTOBPAYMENT = 0  
  AND L.PARTY_CODE = A.CLTCODE  
  AND RMS_DATE = CONVERT(VARCHAR(11), GETDATE())  
  AND EXCHANGE = @@EXCHANGE  
  AND SEGMENT = @@SEGMENT  
 GROUP BY  
  A.CLTCODE,  
  BRANCHCODE  
 HAVING  
  SUM(VDTLEDBAL) < 0  
 ORDER BY  
  PARTY_CODE,  
  BRANCH  
  
 IF @RPTTYPE = 'HTML'  
  SELECT  
   SRNO,  
   SETT_NO,  
   SETT_TYPE,  
   PARTY_CODE,  
   BILLDATE = CONVERT(VARCHAR(11), BILLDATE),  
   AMOUNT,  
   DRCR,  
   VNO,  
   BRANCH,  
   EXCHANGE,  
   SEGMENT  
  FROM  
   #FUNDSPAYIN_DATA  
  ORDER BY  
   SRNO  
  
 ELSE --FILE  
  SELECT  
   PROCESSDT = CONVERT(VARCHAR(11), BILLDATE, 103),  
   TXN_TYPE = '1',  
   ACCNO,  
   BRANCH = '1',  
   TXN_CODE = '1408',  
   TXN_DT = CONVERT(VARCHAR(11), BILLDATE, 103),  
   DRCR,  
   VALUEDT = CONVERT(VARCHAR(11), BILLDATE, 103),  
   TXN_CCY = '104',  
   AMT_LCY = AMOUNT,  
   AMT_TCY = AMOUNT,  
   RATE_CON = '1.00',  
   REFNO = ACCNO,  
   TRN_DESC = 'FUNDSPAYIN ' + @SETTNO + ' - ' + PARTY_CODE  
  FROM  
   #FUNDSPAYIN_DATA R,  
   MULTIBANKID M  
  WHERE  
   PARTY_CODE = CLTCODE  
   AND DEFAULTBANK = 1  
    
  UNION ALL  
    
  SELECT --BARCLAYS CLIENT DESIGNATED A/C DETAILS  
   PROCESSDT = CONVERT(VARCHAR(11), BILLDATE, 103),  
   TXN_TYPE = '3',  
   ACCDTLS,  
   BRANCH = '1',  
   TXN_CODE = '1008',  
   TXN_DT = CONVERT(VARCHAR(11), BILLDATE, 103),  
   DRCR = 'D',  
   VALUEDT = CONVERT(VARCHAR(11), BILLDATE, 103),  
   TXN_CCY = '104',  
   AMT_LCY = 0,  
   AMT_TCY = 0,  
   RATE_CON = '1.00',  
   REFNO = ACCDTLS,  
   TRN_DESC = 'FUNDSPAYIN ' + @SETTNO  
  FROM  
   #FUNDSPAYIN_DATA R,  
   ACMAST A  
  WHERE  
   BANK_CODE = CLTCODE  
   AND ACCAT = 2  
  GROUP BY  
   CONVERT(VARCHAR(11), BILLDATE, 103),  
   ACCDTLS  
  ORDER BY  
   TXN_TYPE

GO
