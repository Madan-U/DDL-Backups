 SET @STRSQL = @STRSQL + " A.CLTCODE <= '" + @COMPARETOWHAT + "' AND "    
 SET @STRSQL = @STRSQL + " (ACCAT LIKE '" + @ACCAT  + "%' OR ACCAT LIKE '" + @@INACCAT + "%')"      
 SET @STRSQL = @STRSQL + " AND A.CLTCODE = C.CLTCODE "  
