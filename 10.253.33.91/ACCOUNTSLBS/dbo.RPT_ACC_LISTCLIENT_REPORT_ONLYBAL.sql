 SET @STRSQL = @STRSQL + "WHERE "    
 SET @STRSQL = @STRSQL + " C1.CL_CODE = C2.CL_CODE AND "    
 SET @STRSQL = @STRSQL + " A.CLTCODE = C2.PARTY_CODE AND "    
