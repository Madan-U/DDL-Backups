 SET @STRSQL = @STRSQL + " CLTCODE <= '" + @COMPARETOWHAT + "' "    
 SET @STRSQL = @STRSQL + " AND C2.PARTY_CODE = C.CLTCODE "  
 SET @STRSQL = @STRSQL + "ORDER BY "    
 SET @STRSQL = @STRSQL + " A.CLTCODE, "    
 SET @STRSQL = @STRSQL + " C1.SUB_BROKER "    
END    
    
--REGION     
    
