CREATE PROCEDURE MAP_MULTIPLE_RECO
	@CLTCODE VARCHAR(10),
	@REFERENCENO VARCHAR(25),
	@VDT VARCHAR(11),
	@VALUEDATE VARCHAR(11),
	@ACTION BIT
AS


CREATE TABLE #RECO_MAPPING
(
	L1_VNO VARCHAR(12),
	L_VDT DATETIME,
	L1_DDNO VARCHAR(25),
	BR_REFERENCENO VARCHAR(25),
	L1_RELAMT MONEY,
	BR_AMOUNT MONEY,
	BR_CROSSREFERENCENO BIGINT,
	BR_VALUEDATE DATETIME,
	L1_SNO BIGINT,
	BR_SNO BIGINT
)

INSERT INTO
	#RECO_MAPPING
SELECT 
	L1.VNO,
	L.VDT,
	L1.DDNO, 
	BR.REFERENCENO,
	L1.RELAMT,
	BR.AMOUNT,
	MAX(CROSSREFERENCENO),
	VALUEDATE,
	MAX(L1_SNO),
	MAX(BR_SNO)
FROM 
	LEDGER1 L1, 
	(
		SELECT 
			VNO, 
			VTYP, 
			BOOKTYPE, 
			VDT 
		FROM
			LEDGER 
		WHERE 
			CLTCODE = @CLTCODE
			AND VDT LIKE @VDT + '%'
			AND VTYP = 3
	) L,
	(
		SELECT 
			REFERENCENO, 
			AMOUNT, 
			CLTCODE,
			CROSSREFERENCENO,
			VALUEDATE
			BR_SNO 
		FROM 
			BANKRECO 
		WHERE 
			CLTCODE = @CLTCODE 
			AND REFERENCENO = @REFERENCENO 
			AND STATUS = 'UNMATCHED' 
			AND VALUEDATE LIKE @VALUEDATE + '%'
	)BR
WHERE 
	L.VNO = L1.VNO
	AND L.VTYP = L1.VTYP
	AND L.BOOKTYPE = L1.BOOKTYPE
	AND BR.REFERENCENO = L1.DDNO 
	AND BR.AMOUNT = L1.RELAMT 
	AND BR.CLTCODE = @CLTCODE
	AND L1.RELDT = ''
GROUP BY 
	L1.VNO,
	L.VDT,
	L1.DDNO, 
	BR.REFERENCENO,
	L1.RELAMT,
	BR.AMOUNT,
	VALUEDATE
HAVING COUNT(1) = 1

IF @ACTION = 0
BEGIN
	SELECT 	
		L1_VNO,
		CONVERT(VARCHAR(11), L_VDT, 103),
		L1_DDNO,
		BR_REFERENCENO,
		L1_RELAMT,
		BR_AMOUNT,
		BR_CROSSREFERENCENO,
		CONVERT(VARCHAR(11), BR_VALUEDATE, 103)
	FROM 
		#RECO_MAPPING
END
ELSE
BEGIN
	UPDATE 
		L1
	SET
		RELDT = BR_VALUEDATE,
		REFNO = BR_CROSSREFERENCENO
	FROM
		LEDGER1 L1,
		#RECO_MAPPING R
	WHERE
		L1.L1_SNO = R.L1_SNO

	UPDATE
		BR
	SET
		STATUS = 'MATCHED'
	FROM
		BANKRECO BR,
		#RECO_MAPPING R		
	WHERE
		BR.BR_SNO = R.BR_SNO
		
END

GO
