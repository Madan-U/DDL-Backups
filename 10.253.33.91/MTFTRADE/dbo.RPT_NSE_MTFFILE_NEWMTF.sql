	where SELL_BUY = 1 
		and SAUDA_DATE < = @PREVDATE 
		and ADJUST_DATE > @PREVDATE
		AND PARTY_CODE = (CASE WHEN @PARTY_CODE='' THEN PARTY_CODE ELSE @PARTY_CODE END)
	Group by SAUDA_DATE,PARTY_CODE,SCRIP_CD,SERIES,
				ISIN,TRANSTYPE--,PAYIN_DATE
		HAVING	SUM(QTY) > 0 AND SUM(NETAMT) > 0
	Order By SAUDA_DATE

	Update  A set nBrokerNetQty = B.NF_QTY,
