				  nCliNetQty = B.M_QTY,
				  nCliNetAmt = B.M_AMT,
				  nCliBuyQty = B.M_QTY,
				  nCliBuyAmt = B.M_AMT,
				  nOrgHairCut = B.HairCut
	From #MTR_MaintMarginQty  A 
	Inner Join TBL_MTR_OPEN_BALANCE B ON A.ORG_SAUDA_DATE = B.ORG_SAUDA_DATE
