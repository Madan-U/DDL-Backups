		EXEC	ACC_GENVNO @SAUDA_DATE, 6, '01', @SDTCUR, @LDTCUR, @RECCNT

		SELECT	@VNO = VNO FROM #VNO

		DROP TABLE #VNO

		UPDATE	#DATAFINAL_JV SET VNO = CONVERT(VARCHAR,CONVERT(BIGINT,@VNO) + SNO - 1)
	
		UPDATE	#DATAFINAL_JV SET ACNAME = A.LONGNAME 
		FROM	ACMAST A
		WHERE	#DATAFINAL_JV.PARTY_CODE = A.CLTCODE

		DELETE FROM LEDGER WHERE VDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59:59' AND NARRATION LIKE '%MTF SELL PAYIN' AND VTYP = '6'
		AND EXISTS (SELECT DISTINCT PARTY_CODE FROM #DATAFINAL_JV WHERE #DATAFINAL_JV.PARTY_CODE = LEDGER.CLTCODE AND AMOUNT <> 0)
		DELETE FROM LEDGER WHERE VDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59:59' AND NARRATION LIKE '%MTF SELL PAYIN' AND VTYP = '66'
		AND EXISTS (SELECT DISTINCT PARTY_CODE FROM #DATAFINAL_JV WHERE #DATAFINAL_JV.PARTY_CODE = LEDGER.CLTCODE AND AMOUNT <> 0)
		
		DELETE FROM LEDGER WHERE VDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59:59' AND NARRATION LIKE '%MTF PAYIN' AND VTYP = '6'
		AND EXISTS (SELECT DISTINCT PARTY_CODE FROM #DATAFINAL_JV WHERE #DATAFINAL_JV.PARTY_CODE = LEDGER.CLTCODE AND MARGIN_REQ <> 0)
		DELETE FROM LEDGER WHERE VDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59:59' AND NARRATION LIKE '%MTF PAYIN' AND VTYP = '66'
		AND EXISTS (SELECT DISTINCT PARTY_CODE FROM #DATAFINAL_JV WHERE #DATAFINAL_JV.PARTY_CODE = LEDGER.CLTCODE AND MARGIN_REQ <> 0)

		/*---- MTF_CASH_COLL_LEDGER -----------*/

		INSERT INTO LEDGER
		SELECT '6', VNO, @EDT, LNO=1, ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END), AMOUNT=ABS(AMOUNT-ConvertedQtyAmt), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '17.MTF SELL PAYIN'
		FROM #DATAFINAL_JV
		WHERE AMOUNT-ConvertedQtyAmt <> 0

		INSERT INTO LEDGER
		SELECT '6', VNO, @EDT, LNO=2, A.ACNAME, DRCR = (CASE WHEN DRCR = 'D' THEN 'D' ELSE 'C' END), AMOUNT=ABS(AMOUNT-ConvertedQtyAmt), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		GLCODE, '02', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '17.MTF SELL PAYIN'
		FROM #DATAFINAL_JV J, ACMAST A
		WHERE J.GLCODE = A.CLTCODE
		AND AMOUNT-ConvertedQtyAmt <> 0
		-- For Sold Qty  END --

		-- For Converted Qty START--
		INSERT INTO LEDGER
		SELECT '6', VNO, @SAUDA_DATE, LNO=3, ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END), AMOUNT=ABS(ConvertedQtyAmt), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '17.MTF SELL PAYIN'
		FROM #DATAFINAL_JV
		WHERE ConvertedQtyAmt <> 0

		INSERT INTO LEDGER
		SELECT '6', VNO, @SAUDA_DATE, LNO=4, A.ACNAME, DRCR = (CASE WHEN DRCR = 'D' THEN 'D' ELSE 'C' END), AMOUNT=ABS(ConvertedQtyAmt), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		GLCODE, '02', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '17.MTF SELL PAYIN'
		FROM #DATAFINAL_JV J, ACMAST A
		WHERE J.GLCODE = A.CLTCODE
		AND ConvertedQtyAmt <> 0
		-- For Converted Qty END--

		---- PAYIN JV (MARGIN_REQ) ----
		INSERT INTO LEDGER
		SELECT '6', VNO, @EDT, LNO=5, ACNAME, DRCR, AMOUNT=ABS(MARGIN_REQ), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '18.MTF PAYIN'
		FROM #DATAFINAL_JV
		WHERE MARGIN_REQ <> 0

		INSERT INTO LEDGER
		SELECT '6', VNO, @EDT, LNO=6, A.ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END), AMOUNT=ABS(MARGIN_REQ), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		GLCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '18.MTF PAYIN'
		FROM #DATAFINAL_JV J, ACMAST A
		WHERE J.GLCODE = A.CLTCODE
		AND MARGIN_REQ <> 0
		
		INSERT INTO LEDGER
		SELECT '66', VNO, @EDT, LNO=1, ACNAME, DRCR, AMOUNT=ABS(AMOUNT-ConvertedQtyAmt), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '21.MTF SELL PAYIN'
		FROM #DATAFINAL_JV
		WHERE AMOUNT-ConvertedQtyAmt <> 0

		INSERT INTO LEDGER
		SELECT '66', VNO, @EDT, LNO=2, A.ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END), AMOUNT=ABS(AMOUNT-ConvertedQtyAmt), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		GLCODE, '02', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '21.MTF SELL PAYIN'
		FROM #DATAFINAL_JV J, ACMAST A
		WHERE J.GLCODE = A.CLTCODE
		AND AMOUNT-ConvertedQtyAmt <> 0
		-- For Sold Qty  END --

		-- For Converted Qty START --
		INSERT INTO LEDGER
		SELECT '66', VNO, @SAUDA_DATE, LNO=3, ACNAME, DRCR, AMOUNT=ABS(ConvertedQtyAmt), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '21.MTF SELL PAYIN'
		FROM #DATAFINAL_JV
		WHERE ConvertedQtyAmt <> 0

		INSERT INTO LEDGER
		SELECT '66', VNO, @SAUDA_DATE, LNO=4, A.ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END), AMOUNT=ABS(ConvertedQtyAmt), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		GLCODE, '02', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '21.MTF SELL PAYIN'
		FROM #DATAFINAL_JV J, ACMAST A
		WHERE J.GLCODE = A.CLTCODE
		AND ConvertedQtyAmt <> 0
		-- For Converted Qty End --

		---- PAYIN JV (MARGIN_REQ) ----
		INSERT INTO LEDGER
		SELECT '66', VNO, @EDT, LNO=5, ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END), AMOUNT=ABS(MARGIN_REQ), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '22.MTF PAYIN'
		FROM #DATAFINAL_JV
		WHERE MARGIN_REQ <> 0

		INSERT INTO LEDGER
		SELECT '66', VNO, @EDT, LNO=6, A.ACNAME, DRCR, AMOUNT=ABS(MARGIN_REQ), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		GLCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = '22.MTF PAYIN'
		FROM #DATAFINAL_JV J, ACMAST A
		WHERE J.GLCODE = A.CLTCODE
		AND MARGIN_REQ <> 0

	END

		/*---  SELL PAYIN CLOSING ADJ AMOUNT DIFF OF FIFO VS AVG RATE ---*/

	SELECT	PARTY_CODE,
			AMOUNT = SUM(M_FRESHBUY) - ROUND(SUM(BODVALUE + FRESHBUY - FRESHSELL),2),
			VNO = CONVERT(VARCHAR(12),''),
			ACNAME = CONVERT(VARCHAR(100),''),
			DRCR = (CASE WHEN (SUM(M_FRESHBUY) - ROUND(SUM(BODVALUE + FRESHBUY - FRESHSELL),2)) > 0 THEN 'D' ELSE 'C' END),
			GLCODE = @GLCODE
	INTO	#JV_SELLPAYIN_ADJ
	FROM	TBL_MTR_REPORTING
	WHERE	SAUDA_DATE = @SAUDA_DATE
			AND NETQTY = M_FRESHBQTY
	GROUP BY
			PARTY_CODE


	ALTER TABLE #JV_SELLPAYIN_ADJ
	ADD SNO	INT	IDENTITY(1,1)

	SET @RECCNT = 0

	SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #JV_SELLPAYIN_ADJ 
	IF @RECCNT > 0 AND @MTFFLAG = 0
	BEGIN

		CREATE TABLE #VNO5
		(
			VNO	VARCHAR(12)
		)


		INSERT INTO #VNO5 
		EXEC ACC_GENVNO @SAUDA_DATE, 6, '01', @SDTCUR, @LDTCUR, @RECCNT

		SELECT @VNO = VNO FROM #VNO5
		
		DROP TABLE #VNO5

		UPDATE	#JV_SELLPAYIN_ADJ SET VNO = CONVERT(VARCHAR,CONVERT(BIGINT,@VNO) + SNO - 1)
	
		UPDATE	#JV_SELLPAYIN_ADJ SET ACNAME = A.LONGNAME 
		FROM	ACMAST A
		WHERE	#JV_SELLPAYIN_ADJ.PARTY_CODE = A.CLTCODE

		DELETE FROM LEDGER WHERE VDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59:59' AND NARRATION LIKE '%BEING THE SELL PAYIN CLOSING ADJ AMT DIFF OF FIFO/PROPORTION VS AVG RATE%' AND CHECKEDBY = 'MTF PROCESS' AND VTYP='6'
		AND EXISTS (SELECT DISTINCT PARTY_CODE FROM #JV_SELLPAYIN_ADJ WHERE #JV_SELLPAYIN_ADJ.PARTY_CODE = LEDGER.CLTCODE AND AMOUNT <> 0)

		DELETE FROM LEDGER WHERE VDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59:59' AND NARRATION LIKE '%BEING THE SELL PAYIN CLOSING ADJ AMT DIFF OF FIFO/PROPORTION VS AVG RATE%' AND CHECKEDBY = 'MTF PROCESS' AND VTYP='66'
		AND EXISTS (SELECT DISTINCT PARTY_CODE FROM #JV_SELLPAYIN_ADJ WHERE #JV_SELLPAYIN_ADJ.PARTY_CODE = LEDGER.CLTCODE AND AMOUNT <> 0)


		INSERT INTO LEDGER
		SELECT VTYP='66', VNO, @SAUDA_DATE, LNO=1, ACNAME, DRCR, AMOUNT=ABS(AMOUNT), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'BEING THE SELL PAYIN CLOSING ADJ AMT DIFF OF FIFO/PROPORTION VS AVG RATE'
		FROM #JV_SELLPAYIN_ADJ
		WHERE AMOUNT <> 0 AND DRCR = 'D'

		INSERT INTO LEDGER
		SELECT VTYP='66', VNO, @SAUDA_DATE, LNO=2, A.ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END), AMOUNT=ABS(AMOUNT), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		GLCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'BEING THE SELL PAYIN CLOSING ADJ AMT DIFF OF FIFO/PROPORTION VS AVG RATE'
		FROM #JV_SELLPAYIN_ADJ J, ACMAST A
		WHERE J.GLCODE = A.CLTCODE
		AND AMOUNT <> 0 AND DRCR = 'D'
	

		INSERT INTO LEDGER
		SELECT VTYP='6', VNO, @SAUDA_DATE, LNO=1, ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END), AMOUNT=ABS(AMOUNT), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'BEING THE SELL PAYIN CLOSING ADJ AMT DIFF OF FIFO/PROPORTION VS AVG RATE'
		FROM #JV_SELLPAYIN_ADJ
		WHERE AMOUNT <> 0 AND DRCR = 'D'

		INSERT INTO LEDGER
		SELECT VTYP='6', VNO, @SAUDA_DATE, LNO=2, A.ACNAME, DRCR, AMOUNT=ABS(AMOUNT), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		GLCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'BEING THE SELL PAYIN CLOSING ADJ AMT DIFF OF FIFO/PROPORTION VS AVG RATE'
		FROM #JV_SELLPAYIN_ADJ J, ACMAST A
		WHERE J.GLCODE = A.CLTCODE
		AND AMOUNT <> 0 AND DRCR = 'D'




		INSERT INTO LEDGER
		SELECT VTYP='6', VNO, @SAUDA_DATE, LNO=3, ACNAME, DRCR, AMOUNT=ABS(AMOUNT), @SAUDA_DATE, '', '', 0, 0, GETDATE(), 
		PARTY_CODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'BEING THE SELL PAYIN CLOSING ADJ AMT DIFF OF FIFO/PROPORTION VS AVG RATE'
		FROM #JV_SELLPAYIN_ADJ
		WHERE AMOUNT <> 0 AND DRCR = 'C'

		INSERT INTO LEDGER
