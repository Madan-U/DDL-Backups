SELECT	PARTY_CODE,
		AMOUNT = CONVERT(NUMERIC(18,2),ROUND(SUM(AMOUNT),2)),
		VNO = CONVERT(VARCHAR(12),''),
		ACNAME = CONVERT(VARCHAR(100),''),
		DRCR = 'D',
		GLCODE = @GLCODE,
		AMOUNT_ORG = CONVERT(NUMERIC(18,2),0),
		MARGIN_REQ = CONVERT(NUMERIC(18,2),0),
		MTOM_LOSS = CONVERT(NUMERIC(18,2),0),
		ConvertedQtyAmt = isnull(CONVERT(NUMERIC(18,2),ROUND(SUM(ConvertedQtyAmt),2)),0)
INTO	#DATAFINAL_JV 
FROM	(
		SELECT	PARTY_CODE, 
				AMOUNT = SUM(nCliSellAmt),
				ConvertedQtyAmt = sum(nClientConvertedAmt)
		FROM	#SellPosition
		WHERE	nCliSellAmt > 0
		GROUP BY
				PARTY_CODE
		) A
GROUP BY
	PARTY_CODE


	ALTER TABLE #DATAFINAL_JV
	ADD SNO	INT	IDENTITY(1,1)

	SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #DATAFINAL_JV 
	IF @RECCNT > 0 AND @MTFFLAG = 0
	BEGIN
		
		CREATE TABLE #VNO
		(
			VNO	VARCHAR(12)
		)

