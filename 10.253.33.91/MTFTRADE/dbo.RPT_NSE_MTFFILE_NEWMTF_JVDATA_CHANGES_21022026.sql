CREATE PROC [dbo].[RPT_NSE_MTFFILE_NEWMTF_JVDATA_CHANGES_21022026]      
(      
@SAUDA_DATE		VARCHAR(11),
@FLAG			INT = 0,
@PARTY_CODE		VARCHAR(10) = ''      
)      
AS      

--[RPT_NSE_MTFFILE_NEWMTF_JVDATA] 'feb 20 2026', 1, 'A51193713'    

DECLARE @BATCHNO	INT,
		@SDTCUR		DATETIME,
		@LDTCUR		DATETIME,
		@MTFFLAG	INT,
		@PROPOR_ALLOC_FLAG	INT
      
SELECT	@SDTCUR = SDTCUR, @LDTCUR = LDTCUR FROM ACCOUNT.DBO.PARAMETER(NOLOCK) 
WHERE	@SAUDA_DATE BETWEEN SDTCUR AND LDTCUR      

SELECT	@BATCHNO = 0    
SELECT	@PROPOR_ALLOC_FLAG = 1
    
SELECT	@BATCHNO = ISNULL(MAX(FLDBATCHNO),0) FROM ASIANFILEBATCHNO    
WHERE	FLDFILENO = 'MT'    
		AND FLDFILEDATE = @SAUDA_DATE    
  
DECLARE	@VNO		VARCHAR(12),
		@RECCNT		INT,
		@EDT		VARCHAR(11)

SELECT	@EDT = LEFT(FUNDS_PAYIN,11) FROM MSAJAG.DBO.SETT_MST SM(NOLOCK)
WHERE	START_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59' AND SETT_TYPE = 'M'


SELECT	@MTFFLAG = COUNT(1) FROM TBL_MTF_LOG WHERE FUNDING_DATE BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59'

SELECT	PARTY_CODE,
		ORG_SAUDA_DATE as SAUDA_DATE,
		PAYIN_AMT=CONVERT(NUMERIC(18,4),0),
		M_AMT = CONVERT(NUMERIC(18,4),sum(nCliNetAmt))
INTO	#TBL_MTF_DATA_BUY 
FROM	TBL_MTR_MAINTMARGINQTY(NOLOCK) 
WHERE	PAYIN_DATE = @SAUDA_DATE
		and SAUDA_DATE = @SAUDA_DATE
		AND PARTY_CODE = (CASE WHEN @PARTY_CODE = '' THEN PARTY_CODE ELSE @PARTY_CODE END)
		AND nCliNetAmt > 0
Group by PARTY_CODE,ORG_SAUDA_DATE

CREATE INDEX #VDT ON  #TBL_MTF_DATA_BUY(PARTY_CODE,SAUDA_DATE)



---- MTF_CASH_PAYIN_LEDGER ---------

UPDATE	#TBL_MTF_DATA_BUY 
SET		PAYIN_AMT = ISNULL(D.VAMT,0)
FROM	(
		SELECT	P.CLTCODE,
				VAMT = SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),
				VDT
		FROM	LEDGER P(NOLOCK)  --,Acmast a
		WHERE	P.VDT BETWEEN @SDTCUR AND @LDTCUR
				AND P.VDT <= @SAUDA_DATE + ' 23:59:59' 
				AND P.VTYP = '66'
				--AND P.EDT BETWEEN @SDTCUR AND @SAUDA_DATE + ' 23:59:59' --VDT = @PREVDATE 
				AND P.EDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59:59' --VDT = @PREVDATE
				AND P.CLTCODE = (CASE WHEN @PARTY_CODE = '' THEN P.CLTCODE ELSE @PARTY_CODE END)
				AND P.NARRATION LIKE '%MTF PAYIN'  --and p.cltcode =a.cltcode and a.accat =4 
		GROUP BY
				P.CLTCODE,
				VDT
		HAVING	SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END) > 0
		) D
WHERE	D.CLTCODE = #TBL_MTF_DATA_BUY.PARTY_CODE
		AND D.VDT = #TBL_MTF_DATA_BUY.SAUDA_DATE
 
DECLARE @GLCODE	VARCHAR(10)

SELECT	@GLCODE = ACCODE FROM VALANACCOUNT WHERE ACNAME = 'MTF JV CONTROL'

---- MTF_CASH_PAYIN_LEDGER --------------

SELECT	PARTY_CODE = CLTCODE, 
		PAYIN_AMT = ISNULL(SUM(CASE WHEN DRCR='D' THEN -VAMT ELSE VAMT END),0),
		PAYIN_ALLOCATED_AMT = CONVERT(NUMERIC(18,2),0),
		PAYIN_EXCESS_AMT = CONVERT(NUMERIC(18,2),0),
		ACNAME = P.ACNAME, 
		DRCR = 'D',
		VNO = CONVERT(VARCHAR(12),''),
		GLCODE = @GLCODE,
		VDT
INTO	#EXCESS_PAYIN_JV
FROM	LEDGER P(NOLOCK)
WHERE	---VDT = @PREVDATE 
		P.VDT BETWEEN @SDTCUR AND @LDTCUR
		AND P.VDT <= @SAUDA_DATE + ' 23:59:59' 
		AND P.VTYP = '66'
		--AND P.EDT BETWEEN @SDTCUR AND @SAUDA_DATE + ' 23:59:59' --VDT = @PREVDATE 
		AND P.EDT BETWEEN @SAUDA_DATE AND @SAUDA_DATE + ' 23:59:59' --VDT = @PREVDATE 
		AND CLTCODE = (CASE WHEN @PARTY_CODE = '' THEN CLTCODE ELSE @PARTY_CODE END)
		AND P.NARRATION LIKE '%MTF PAYIN'
		AND 1 = 2 --- STOP JV FROM DATE : 11/02/2026 --
GROUP BY
		CLTCODE, P.ACNAME, VDT

ALTER TABLE #EXCESS_PAYIN_JV
ADD SNO	INT	IDENTITY(1,1)
		
UPDATE	#EXCESS_PAYIN_JV
SET		PAYIN_ALLOCATED_AMT = ISNULL(A.M_FRESHBUY,0)
FROM	(
		SELECT	PARTY_CODE,
				M_FRESHBUY = ROUND(SUM(M_AMT),2),
				SAUDA_DATE
		FROM	#TBL_MTF_DATA_BUY D
		GROUP BY
				PARTY_CODE,
				SAUDA_DATE
		) A
WHERE	A.PARTY_CODE = #EXCESS_PAYIN_JV.PARTY_CODE
		AND A.SAUDA_DATE = #EXCESS_PAYIN_JV.VDT
					
UPDATE	#EXCESS_PAYIN_JV SET PAYIN_EXCESS_AMT = CONVERT(NUMERIC(18,2),ROUND((PAYIN_AMT-PAYIN_ALLOCATED_AMT),2))
WHERE	(PAYIN_AMT-PAYIN_ALLOCATED_AMT) <> 0

-- ********** --
--   START    --
-- ********** --
-- Sagun S - 17 DEC 2025
-- The below code is written to bifurcate sold quantity and quantity converted to delivery.
-- If the client sells the quantity, the EDT of "MTF SELL PAYIN" in the ledger will be the next working day.
-- If the client converts the quantity to delivery, the EDT of "MTF SELL PAYIN" in the ledger will be the same day.

select @SAUDA_DATE as SAUDA_DATE,PARTY_CODE,SCRIP_CD,SERIES, ISIN,
	   sum(qty) as nTotalConvertedQty,
	   CONVERT(NUMERIC(18,6),0) as nCliSellQty,
	   CONVERT(NUMERIC(18,2),0) as nCliSellAmt,
	   CONVERT(NUMERIC(18,6),0) as nClientConvertedQty,
	   CONVERT(NUMERIC(18,2),0) as nClientConvertedAmt
Into #tbl_MTFSellPayin
FROM TBL_PRODUCT_POSITION P1
WHERE P1.SAUDA_DATE = @SAUDA_DATE
AND P1.SELL_BUY = 2 AND P1.SETT_NO = '2000000'
GROUP BY PARTY_CODE,SCRIP_CD,SERIES, ISIN

select PARTY_CODE,SCRIP_CD,SERIES, sISIN,
		sum(nFreshSellQty) as nFreshSellQty,
		sum(nFreshSellAmt) as nFreshSellAmt,
		sum(nCliSellQty) as nCliSellQty,
		sum(nCliSellAmt) as nCliSellAmt, 
		sum(nFreshSellAmt)/sum(nFreshSellQty) as Price,
		CONVERT(NUMERIC(18,6),0) as nClientConvertedQty,
	    CONVERT(NUMERIC(18,2),0) as nClientConvertedAmt
into #SellPosition
From tbl_MTR_MaintMarginQty
WHERE	SAUDA_DATE = @SAUDA_DATE
	and nCliSellAmt > 0
Group by PARTY_CODE,SCRIP_CD,SERIES, sISIN


/** -- CHANGES FOR SELLPAYIN INCASE OF PARTIAL QUANTITY ALLOCATED IN MAINTENANCE QTY ON DATE : 01/02/2026 --- **/

--Update A SET nClientConvertedQty = B.nTotalConvertedQty * A.nCliSellQty / A.nFreshSellQty,
--			 nClientConvertedAmt = (B.nTotalConvertedQty * A.nCliSellQty / A.nFreshSellQty) * A.Price
--From #SellPosition A
--Left Join #tbl_MTFSellPayin B ON A.PARTY_CODE = B.PARTY_CODE
--					And A.SCRIP_CD = B.SCRIP_CD
--					And A.SERIES = B.SERIES
--					AND A.sISIN = B.ISIN

Update A SET nClientConvertedQty = (CASE WHEN B.nTotalConvertedQty > A.nFreshSellQty THEN A.nFreshSellQty ELSE B.nTotalConvertedQty END) * A.nCliSellQty / A.nFreshSellQty,
			 nClientConvertedAmt = ((CASE WHEN B.nTotalConvertedQty > A.nFreshSellQty THEN A.nFreshSellQty ELSE B.nTotalConvertedQty END) * A.nCliSellQty / A.nFreshSellQty) * A.Price
From #SellPosition A
Left Join #tbl_MTFSellPayin B ON A.PARTY_CODE = B.PARTY_CODE
					And A.SCRIP_CD = B.SCRIP_CD
					And A.SERIES = B.SERIES
					AND A.sISIN = B.ISIN

/** -- CHANGES FOR SELLPAYIN INCASE OF PARTIAL QUANTITY ALLOCATED IN MAINTENANCE QTY ON DATE : 01/02/2026 --- **/


Update #SellPosition 
	set nClientConvertedAmt = case when nClientConvertedQty = nCliSellQty 
								   then nCliSellAmt Else nClientConvertedAmt End
