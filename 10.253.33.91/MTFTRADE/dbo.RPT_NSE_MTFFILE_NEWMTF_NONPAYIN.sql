	INSERT INTO #REPORT     
	SELECT DATA = '10,MTR,' + LTRIM(RTRIM(MEMBERCODE)) + ',' +     
	REPLACE(CONVERT(VARCHAR,CONVERT(DATETIME,@SAUDA_DATE),103),'/','') + ',' +    
