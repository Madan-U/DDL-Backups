+ ',' + 'N,'+ RIGHT(EXCHANGE,3)
FROM #NSERPT    
GROUP BY MEMBERCODE,LONG_NAME, PARTY_CODE, SCRIP_CD, SERIES, PAN_GIR_NO, RIGHT(EXCHANGE,3), LTRIM(RTRIM(UPPER(ISIN))) 

