CREATE PROC [dbo].[RPT_MTR_MTFFILE_NEW_BSE_BKP_05FEB2026_SRE_43657]      
(      
	@SAUDA_DATE VARCHAR(11),
	@FLAG		INT = 0,
	@PARTY_CODE	VARCHAR(10) = ''      
)      
AS      

/*
