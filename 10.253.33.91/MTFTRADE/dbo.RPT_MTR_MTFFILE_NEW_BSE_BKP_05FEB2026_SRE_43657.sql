--CONVERT(VARCHAR,CONVERT(NUMERIC(18,2),ROUND(SUM(CASE WHEN M_FRESHBQTY > 0 THEN (BODVALUE + FRESHBUY - FRESHSELL)-(M_FRESHBUY) ELSE 0 END),2))) + ',' +     
