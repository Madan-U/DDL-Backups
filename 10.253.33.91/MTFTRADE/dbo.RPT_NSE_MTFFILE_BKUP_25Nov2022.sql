FRESHBQTY = SUM(QTY),     
FRESHBUY = CONVERT(NUMERIC(18,2),SUM(NETAMT) ),      
FRESHSQTY = 0,    
FRESHSELL = CONVERT(NUMERIC(18,2),0),      
NETAMOUNT = CONVERT(NUMERIC(18,2),0)       
