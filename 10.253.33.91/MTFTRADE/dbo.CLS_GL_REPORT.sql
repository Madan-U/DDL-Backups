CREATE PROC [dbo].[CLS_GL_REPORT]
      @FDATE VARCHAR(11),            /* AS MMM DD YYYY */                          
      @TDATE VARCHAR(11),            /* AS MMM DD YYYY */                          
      @SDATE VARCHAR(11),            /* AS MMM DD YYYY */                          
      @FCODE VARCHAR(10),                          
      @TCODE VARCHAR(10),                          
      @STATUSID VARCHAR(30),                          
      @STATUSNAME VARCHAR(30),                          
      @BRANCH VARCHAR(10),      
      @SELECTIONBY VARCHAR(3) = 'VDT',
      @REPORT_TYPE VARCHAR(5) = 'GL'                           
                          
AS                          
                          
DECLARE                          
 @@OPENDATE AS VARCHAR(11),                          
 @@REPDATE AS VARCHAR(8),                          
 @@STARTDATE AS VARCHAR(11),       
 @@COSTCODE AS SMALLINT
      


CREATE TABLE #GL_OPBAL
(
	CLTCODE VARCHAR(10),
	OPBAL MONEY
)


SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

CREATE TABLE [DBO].[#TBL_GLREPORT]([SRNO] [INT] IDENTITY (1, 1) NOT NULL,
[BOOKTYPE] [CHAR](2) NULL,
[VOUDT] [VARCHAR](10) NULL,
[EFFDT] [VARCHAR](10) NULL,
[SHORTDESC] [CHAR](6) NOT NULL DEFAULT (' '),
[DRAMT] [MONEY] NULL,
[CRAMT] [MONEY] NULL,
[VNO] [VARCHAR](15) NULL,
[NARRATION] [VARCHAR](500) NULL,
[DDNO] [VARCHAR](32) NULL,
[CLTCODE] [VARCHAR](10) NOT NULL,
[LONGNAME] [VARCHAR](100) NULL,
[VDT] [DATETIME] NULL,
[VTYP] [SMALLINT] NOT NULL,
[ACCAT] [CHAR](2) NULL,
[OPBAL] [MONEY] NOT NULL,
[CROSAC] [VARCHAR](10) NOT NULL,
[ACNAME] [VARCHAR](100) NULL,
[BRANCHCODE] [VARCHAR](35) NULL,
[LNO] [INT] NULL,
[REPDATE] [VARCHAR](8) NULL,
[MAINCODE] [VARCHAR](10) NULL,
[VCHDT] [DATETIME] NULL,
[EDIFF] INT NULL)


--DELETE #TBL_GLREPORT WHERE PROCID = @@SPID                                        

SELECT
	@@COSTCODE = -1

IF @STATUSID <> 'BROKER' BEGIN
SELECT TOP 1
	@@COSTCODE = COSTCODE
FROM COSTMAST
WHERE COSTNAME = @STATUSNAME
END ELSE IF @STATUSID = 'BROKER' BEGIN
IF @BRANCH <> '' OR @BRANCH <> '%' BEGIN
SELECT TOP 1
	@@COSTCODE = COSTCODE
FROM COSTMAST
WHERE COSTNAME = @BRANCH
END
END


SELECT
	@@STARTDATE = LEFT(CONVERT(VARCHAR, CONVERT(DATETIME, @SDATE, 103), 109), 11)


CREATE TABLE #FORMULA_CLIENT_MASTER(CLTCODE VARCHAR(10))


IF @REPORT_TYPE <> 'GL'
BEGIN
	INSERT INTO #FORMULA_CLIENT_MASTER
		SELECT
			CLTCODE
		FROM ACMAST
		WHERE CLTCODE BETWEEN @FCODE AND @TCODE
		AND ACCAT =
					CASE
						WHEN @REPORT_TYPE = 'BANK' THEN 2
						ELSE CASE
								WHEN @REPORT_TYPE = 'CASH' THEN 1
								ELSE 3
							END
					END
		--AND (BRANCHCODE =
		--					CASE
		--						WHEN @BRANCH <> '' OR
		--							@BRANCH <> '%' THEN @BRANCH
		--						ELSE BRANCHCODE
		--					END
		--OR BRANCHCODE = 'ALL')
END
ELSE
BEGIN
	INSERT INTO #FORMULA_CLIENT_MASTER
		SELECT
			CLTCODE
		FROM ACMAST
		WHERE CLTCODE BETWEEN @FCODE AND @TCODE
		AND ACCAT IN(3, 14)
END


	
/* GET OPNING BALANCE */
IF @SELECTIONBY = 'VDT' BEGIN
IF @STATUSID = 'BROKER' AND @@COSTCODE = -1 BEGIN
--INSERT INTO #GL_OPBAL
EXEC CLS_GET_ACC_LEDGER_FORMULA_BALANCES	@FDATE
											,@TDATE
											,''
											,'VDTBAL_OPBAL'
											,'NSE'
											,'CAPITAL'
											,'M.CLTCODE'
											,''
											,'#GL_OPBAL'

END ELSE BEGIN
--		INSERT INTO #GL_OPBAL
EXEC CLS_GET_ACC_LEDGER2_FORMULA_BALANCES	@FDATE
											,@TDATE
											,''
											,'VDTBAL_OPBAL'
											,'NSE'
											,'CAPITAL'
											,'M.CLTCODE'
											,''
											,@@COSTCODE
											,'#GL_OPBAL'
END
END ELSE BEGIN
IF @STATUSID = 'BROKER' AND @@COSTCODE = -1 BEGIN
--INSERT INTO #GL_OPBAL
EXEC CLS_GET_ACC_LEDGER_FORMULA_BALANCES	@FDATE
											,@TDATE
											,''
											,'EDTBAL_OPBAL'
											,'NSE'
											,'CAPITAL'
											,'M.CLTCODE'
											,''
											,'#GL_OPBAL'
END ELSE BEGIN
--INSERT INTO #GL_OPBAL
EXEC CLS_GET_ACC_LEDGER2_FORMULA_BALANCES	@FDATE
											,@TDATE
											,''
											,'EDTBAL_OPBAL'
											,'NSE'
											,'CAPITAL'
											,'M.CLTCODE'
											,''
											,@@COSTCODE
											,'#GL_OPBAL'
END
END
/* GET OPNING BALANCE */



/* GET THE RECORDS FROM LEDGER TABLE */
IF @REPORT_TYPE <> 'GL' BEGIN
INSERT INTO #TBL_GLREPORT
	SELECT
		L.BOOKTYPE
		,VOUDT = CONVERT(VARCHAR, L.VDT, 103)
		,EFFDT = CONVERT(VARCHAR, L.EDT, 103)
		,ISNULL(SHORTDESC, '') SHORTDESC
		,DRAMT = (CASE
			WHEN UPPER(L.DRCR) = 'D' THEN VAMT
			ELSE 0
		END)
		,CRAMT = (CASE
			WHEN UPPER(L.DRCR) = 'C' THEN VAMT
			ELSE 0
		END)
		,L.VNO
		,REPLACE(L.NARRATION, '"', '') NARRATION
		,DDNO = (CASE
			WHEN ISNULL(L1.DDNO, '') = '0' THEN ''
			ELSE ISNULL(L1.DDNO, '')
		END)
		,L.CLTCODE
		,A.LONGNAME
		,VDT
		,L.VTYP
		,ACCAT
		,OPBAL = 0
		,CROSAC = ''
		,A.ACNAME
		,A.BRANCHCODE
		,L.LNO
		,CONVERT(VARCHAR, GETDATE(), 112)
		,''
		,L.VDT
		,EDIFF = DATEDIFF(D, L.EDT, GETDATE())
	FROM	LEDGER L
			LEFT JOIN (SELECT
					VTYP
					,BOOKTYPE
					,VNO
					,DDNO = MAX(DDNO)
				FROM LEDGER1
				GROUP BY	VTYP
							,BOOKTYPE
							,VNO) L1
				ON (L1.VTYP = L.VTYP
				AND L1.BOOKTYPE = L.BOOKTYPE
				AND L1.VNO = L.VNO)
			,
			--(SELECT VNO, VTYP, BOOKTYPE FROM LEDGER WHERE VDT BETWEEN @FDATE AND @TDATE + ' 23:59:59' AND CLTCODE = @FCODE) LL,                        
			(SELECT
					VNO
					,VTYP
					,BOOKTYPE
				FROM LEDGER
				WHERE @FDATE <=
								CASE
									WHEN @SELECTIONBY = 'VDT' THEN VDT
									ELSE EDT
								END
				AND @TDATE + ' 23:59:59' >=
											CASE
												WHEN @SELECTIONBY = 'VDT' THEN VDT
												ELSE EDT
											END
				AND CLTCODE BETWEEN @FCODE AND @TCODE) LL
			,VMAST V
			,ACMAST A
	WHERE L.VNO = LL.VNO
	AND L.VTYP = LL.VTYP
	AND L.BOOKTYPE = LL.BOOKTYPE
	AND L.CLTCODE = A.CLTCODE
	AND L.VTYP = V.VTYPE
	AND NOT EXISTS (SELECT
			CLTCODE
		FROM #FORMULA_CLIENT_MASTER F
		WHERE L.CLTCODE = F.CLTCODE)
	AND L.VTYP <> 18
	
END ELSE BEGIN
IF @@COSTCODE = -1 BEGIN
INSERT INTO #TBL_GLREPORT
	SELECT
		L.BOOKTYPE
		,VOUDT = CONVERT(VARCHAR, L.VDT, 103)
		,EFFDT = CONVERT(VARCHAR, L.EDT, 103)
		,ISNULL(SHORTDESC, '') SHORTDESC
		,DRAMT = (CASE
			WHEN UPPER(L.DRCR) = 'D' THEN VAMT
			ELSE 0
		END)
		,CRAMT = (CASE
			WHEN UPPER(L.DRCR) = 'C' THEN VAMT
			ELSE 0
		END)
		,L.VNO
		,REPLACE(L.NARRATION, '"', '') NARRATION
		,DDNO = ISNULL((SELECT TOP 1
				DDNO
			FROM LEDGER1
			WHERE VTYP = L.VTYP
			AND VNO = L.VNO
			AND BOOKTYPE = L.BOOKTYPE
			AND LNO = L.LNO)
		, '')
		,L.CLTCODE
		,A.LONGNAME
		,VDT
		,L.VTYP
		,ACCAT
		,OPBAL = 0
		,CROSAC = ''
		,A.ACNAME
		,A.BRANCHCODE
		,L.LNO
		,CONVERT(VARCHAR, GETDATE(), 112)
		,''
		,L.VDT
		,EDIFF = DATEDIFF(D, L.EDT, GETDATE())
	FROM	LEDGER L
			,(SELECT
					CLTCODE
					,LONGNAME
					,ACCAT
					,BRANCHCODE
					,ACNAME
				FROM ACMAST
				WHERE CLTCODE BETWEEN @FCODE AND @TCODE
				AND (ACCAT LIKE '3%'
				OR ACCAT LIKE '14%'
				OR ACCAT LIKE '103%')) A
			,VMAST V
	WHERE L.VDT BETWEEN @FDATE AND @TDATE + ' 23:59:59'
	AND VTYP = VTYPE
	AND L.CLTCODE = A.CLTCODE
	AND L.VTYP <> 18
/*ORDER BY                    
   L.CLTCODE,                    
   VOUDT1,                    
   L.VTYP DESC,                    
   L.VNO  */
END ELSE BEGIN
INSERT INTO #TBL_GLREPORT
	SELECT
		L.BOOKTYPE
		,VOUDT = CONVERT(VARCHAR, L.VDT, 103)
		,EFFDT = CONVERT(VARCHAR, L.EDT, 103)
		,ISNULL(SHORTDESC, '') SHORTDESC
		,DRAMT = (CASE
			WHEN UPPER(L.DRCR) = 'D' THEN VAMT
			ELSE 0
		END)
		,CRAMT = (CASE
			WHEN UPPER(L.DRCR) = 'C' THEN VAMT
			ELSE 0
		END)
		,L.VNO
		,REPLACE(L.NARRATION, '"', '') NARRATION
		,DDNO = ISNULL((SELECT TOP 1
				DDNO
			FROM LEDGER1
			WHERE VTYP = L.VTYP
			AND VNO = L.VNO
			AND BOOKTYPE = L.BOOKTYPE
			AND LNO = L.LNO)
		, '')
		,L.CLTCODE
		,A.LONGNAME
		,VDT
		,L.VTYP
		,ACCAT
		,OPBAL = 0
		,CROSAC = ''
		,A.ACNAME
		,A.BRANCHCODE
		,L.LNO
		,CONVERT(VARCHAR, GETDATE(), 112)
		,''
		,L.VDT
		,EDIFF = DATEDIFF(D, L.EDT, GETDATE())
	FROM	LEDGER L
			,LEDGER2 L2
			,(SELECT
					CLTCODE
					,LONGNAME
					,ACCAT
					,BRANCHCODE
					,ACNAME
				FROM ACMAST
				WHERE CLTCODE BETWEEN @FCODE AND @TCODE
				AND (ACCAT LIKE '3%'
				OR ACCAT LIKE '14%'
				OR ACCAT LIKE '103%')) A
			,VMAST V
	WHERE L.VDT BETWEEN @FDATE AND @TDATE + ' 23:59:59'
	AND VTYP = V.VTYPE
	AND L.CLTCODE = A.CLTCODE
	AND L.VTYP <> 18
	AND L.VNO = L2.VNO
	AND L.VTYP = L2.VTYPE
	AND L.BOOKTYPE = L2.BOOKTYPE
	AND L.LNO = L2.LNO
	AND L.CLTCODE = L2.CLTCODE
	AND COSTCODE = @@COSTCODE
/*ORDER BY                    
   L.CLTCODE,                    
   VOUDT1,                    
   L.VTYP DESC,                    
   L.VNO  */
END
END
