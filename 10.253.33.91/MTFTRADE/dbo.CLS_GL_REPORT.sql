	,DRAMT DEBIT
	,CRAMT CREDIT
	,VNO
	,NARRATION
	,DDNO
	,CLTCODE
	,LONGNAME
	,VTYP
	,ACCAT
	,OPBAL
	,CROSAC
	,ACNAME
	,BRANCHCODE
	,VCHDT
	,EDIFF
FROM #TBL_GLREPORT

ORDER BY CONVERT(DATETIME, LEFT(CONVERT(VARCHAR, VCHDT, 109), 11)), CONVERT(NUMERIC, VTYP), VNO

