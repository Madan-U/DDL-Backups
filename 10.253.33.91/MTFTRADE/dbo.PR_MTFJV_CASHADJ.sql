CREATE PROC [dbo].[PR_MTFJV_CASHADJ]    
(    
 @SAUDA_DATE VARCHAR(11)    
)    
AS    


    
SELECT *   INTO #C_JVDATA    
 FROM FUND_MOVEMENTS_LEDGER_STG   
WHERE VDT >= @SAUDA_DATE   AND VDT <= @SAUDA_DATE   +' 23:59' and Valid ='0'
--AND FLAG = 'CASH'    

ALTER TABLE #C_JVDATA
ADD GLCODE VARCHAR(20),ACNAME VARCHAR(100),VNO VARCHAR(15), FLAG VARCHAR(10)

UPDATE #C_JVDATA SET ACNAME ='',VNO='',FLAG='CASH',GLCODE ='46000062'
    
 ALTER TABLE #C_JVDATA    
 ADD SNO INT IDENTITY(1,1)    
     
 DECLARE @VNO  VARCHAR(12), @VNO1    VARCHAR(12),
   @RECCNT  INT,    
   @SDTCUR  VARCHAR(11),    
   @LDTCUR  VARCHAR(11)    
    
 SELECT @SDTCUR = SDTCUR, @LDTCUR = LDTCUR    
 FROM PARAMETER WHERE @SAUDA_DATE BETWEEN @SDTCUR AND LDTCUR    
    
 SELECT @RECCNT = ISNULL(COUNT(1),0) FROM #C_JVDATA    
     
 IF @RECCNT > 0     
 BEGIN    
  CREATE TABLE #C_VNO    
  (    
   VNO VARCHAR(12)    
  )    
    
  INSERT INTO #C_VNO     
  EXEC ACCOUNT.DBO.ACC_GENVNO_new @SAUDA_DATE, 6, '01', @SDTCUR, @LDTCUR, @RECCNT    
    
  SELECT @VNO = VNO FROM #C_VNO    
    
  UPDATE #C_JVDATA SET VNO = CONVERT(VARCHAR,CONVERT(BIGINT,@VNO) + SNO - 1)    
     
  UPDATE #C_JVDATA SET ACNAME = A.LONGNAME     
  FROM ACCOUNT.DBO.ACMAST A    
  WHERE #C_JVDATA.CLTCODE = A.CLTCODE    
    
  INSERT INTO ACCOUNT.DBO.LEDGER    
  SELECT '6', VNO, @SAUDA_DATE, LNO=1, ACNAME, DRCR = (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END) , AMOUNT=vamt, @SAUDA_DATE, '', '', 0, 0, GETDATE(),     
  CLTCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'MTF JV FROM ' + FLAG + ' SEGMENT'  
  FROM #C_JVDATA    
    
  INSERT INTO ACCOUNT.DBO.LEDGER    
  SELECT '6', VNO, @SAUDA_DATE, LNO=2, A.ACNAME, DRCR, AMOUNT=vamt, @SAUDA_DATE, '', '', 0, 0, GETDATE(),     
  GLCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'MTF JV FROM ' + FLAG + ' SEGMENT'  
  FROM #C_JVDATA J, ACMAST A    
  WHERE J.GLCODE = A.CLTCODE    


    CREATE TABLE #C_VNO1    
  (    
   VNO VARCHAR(12)    
  )    

  SELECT * INTO #C_JVDATA1    
 FROM FUND_MOVEMENTS_LEDGER_STG   
WHERE VDT >= @SAUDA_DATE   AND VDT <= @SAUDA_DATE   +' 23:59' and Valid ='0'
--AND FLAG = 'CASH'    

ALTER TABLE #C_JVDATA1
ADD GLCODE VARCHAR(20),ACNAME VARCHAR(100),VNO VARCHAR(15),FLAG VARCHAR(10)

UPDATE #C_JVDATA1 SET ACNAME ='',VNO=''  ,FLAG='CASH',GLCODE ='46000062'
    
 ALTER TABLE #C_JVDATA1    
 ADD SNO INT IDENTITY(1,1)   

    INSERT INTO #C_VNO1     
  EXEC ACC_GENVNO_new @SAUDA_DATE, 6, '01', @SDTCUR, @LDTCUR, @RECCNT    
    
 SELECT @VNO1 = VNO FROM #C_VNO1    
    
  UPDATE #C_JVDATA1 SET VNO = CONVERT(VARCHAR,CONVERT(BIGINT,@VNO1) + SNO - 1)    
     
  UPDATE #C_JVDATA1 SET ACNAME = A.LONGNAME     
  FROM ACMAST A    
  WHERE #C_JVDATA1.CLTCODE = A.CLTCODE    
    
  INSERT INTO LEDGER   
  SELECT '6', VNO, @SAUDA_DATE, LNO=1, ACNAME, DRCR  , AMOUNT=vamt, @SAUDA_DATE, '', '', 0, 0, GETDATE(),     
  CLTCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'MTF JV FROM ' + FLAG + ' SEGMENT'  
  FROM #C_JVDATA1    
    
  INSERT INTO LEDGER    
  SELECT '6', VNO, @SAUDA_DATE, LNO=2, A.ACNAME, DRCR= (CASE WHEN DRCR = 'C' THEN 'D' ELSE 'C' END) , AMOUNT=vamt, @SAUDA_DATE, '', '', 0, 0, GETDATE(),     
  GLCODE, '01', 'MTF PROCESS', GETDATE(), 'MTF PROCESS', 0, NARRATION = 'MTF JV FROM ' + FLAG + ' SEGMENT'  
  FROM #C_JVDATA1 J, ACMAST A    
  WHERE J.GLCODE = A.CLTCODE    


 UPDATE FUND_MOVEMENTS_LEDGER_STG   Set Valid=1
WHERE VDT >= @SAUDA_DATE   AND VDT <= @SAUDA_DATE   +' 23:59' and Valid ='0'



 END

GO
