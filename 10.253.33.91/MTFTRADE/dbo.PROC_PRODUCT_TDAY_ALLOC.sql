AND SELL_BUY = 1       
AND C.CL_CODE = P.PARTY_CODE      
--AND TRANSTYPE='TRNSE'
AND P.PARTY_CODE BETWEEN @FROMPARTY AND @TOPARTY
GROUP BY MEMBERCODE, P.PARTY_CODE, LONG_NAME,SCRIP_CD, SERIES, TRANSTYPE, BSECODE, ISNULL(ISIN,'')
HAVING SUM(QTY) > 0 AND SUM(MKTAMT) > 0 


