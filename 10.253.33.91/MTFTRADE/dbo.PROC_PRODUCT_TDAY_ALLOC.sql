		@FROMPARTY	VARCHAR(20),
		@TOPARTY	VARCHAR(20)

SET		@SAUDA_DATE = 'JAN 31 2025'
SET		@FROMPARTY	= 'AF7259'
SET		@TOPARTY	= 'AF7259'
---IN('AA8143','AA8559')		'AF7259'
