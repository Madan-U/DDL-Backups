CREATE PROC [dbo].[PROC_PRODUCT_TDAY_ALLOC]
		(
		@SAUDA_DATE	VARCHAR(11),
		@FROMPARTY	VARCHAR(20) = '',
		@TOPARTY	VARCHAR(20) = 'ZZZZZZ'
		)

		AS

DECLARE @SDTCUR		DATETIME,
		@LDTCUR		DATETIME,
		@EDT		VARCHAR(11),
		@PREVDATE	VARCHAR(11),
		@PROPOR_ALLOC_FLAG	INT

SET		@PROPOR_ALLOC_FLAG = 1

/*
