 --WHERE PROGID = @PROGID AND ISNULL(.DBO.PIECE(FILE_DATA,',',2),'0') NOT LIKE 'CM Code'           
 
 
 UPDATE #DATA SET TMCODE = CMCODE WHERE TMCODE = ''        
END        
        
