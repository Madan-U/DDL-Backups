CREATE PROC [dbo].[PROC_COLL_RECO_UPLOAD_BKP_03MAY2024] (@TDATE VARCHAR(10), @FILEFOR VARCHAR(20), @FILETYPE VARCHAR(50), @PROGID VARCHAR(50), @UNAME VARCHAR(50),@FILENAME VARCHAR(500) = '')        
        
