BEGIN        
 INSERT INTO #DATA (TRANSDATE,EXCHANGE,SEGMENT,CMCODE,TMCODE,CLIENTCODE,AMOUNT,RECO_FILENAME,UPLOAD_ON,UPLOAD_BY,Total_Collateral_EOD,Eligible_Collateral_EOD)        
