SELECT TRANSDATE,EXCHANGE,SEGMENT,CMCODE,TMCODE,CLIENTCODE,AMOUNT,RECO_FILENAME,UPLOAD_ON,UPLOAD_BY,Total_Collateral_EOD,Eligible_Collateral_EOD        
INTO #DATA        
FROM TBL_CLIENT_COLL_RECO WITH (NOLOCK)              
WHERE 1 = 2         
       
  
