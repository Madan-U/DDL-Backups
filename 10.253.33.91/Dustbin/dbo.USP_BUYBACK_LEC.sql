 SCRIP_NAME VARCHAR (100),        
 ISIN VARCHAR (100),        
 CONTRACTNO VARCHAR (100),        
 SETT_NO VARCHAR (100),        
 SETT_TYPE VARCHAR (100),        
 EXCHANGE VARCHAR (100),        
 SEGMENT VARCHAR (100),        
 LONG_NAME VARCHAR (100),        
 PTRADEDQTY INT,        
 PTRADEDAMT MONEY,        
 STRADEDQTY INT,        
 STRADEDAMT MONEY,        
 BUYBROKERAGE MONEY,        
 SELBROKERAGE MONEY,        
