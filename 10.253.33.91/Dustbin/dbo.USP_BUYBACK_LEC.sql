CONVERT(VARCHAR,[RBI APPROVAL NO]) AS [RBI APPROVAL NO], CONVERT(VARCHAR,[PIS ACCOUNT NO]) AS [PIS ACCOUNT NO], CONVERT(VARCHAR,[SAVING BANK ACCOUNT NO]) AS [SAVING BANK ACCOUNT NO],
